<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same on the other half as well. </strong>It would be impossible to make even stitches without pinning the seams and basting the topstitching guidelines. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I usually use 8mm spiral steel bones in corset garments but I make 11-12mm tunnels to account for the thickness of the bone and the cap at the end.</strong> The tunnel should be neither too loose nor too tight. When you insert the bone in the tunnel, you sort of squeeze it like a spring and it might tear the lining when it returns to its original length in wear. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>If you can adjust the needle position on your sewing machine, </strong>you can simply align the edge of the presser foot with the topstitched line and position the needle at the required distance from it. If you cannot adjust the needle position, you can mark the edge of the tunnel for the sake of convenience. Just remember that you cannot re-stitch anything on leather: pierced holes from the previously made stitches will be too noticeable.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I will sew a tunnel from either side of each seam.</strong> The distance between the bones should be 3-3.5cm at the waistline level. The intermediate front piece (piece 2) is rather wide and I will see whether it makes sense to add another bone in the middle of it. Since the side front piece (piece 3) is narrow, I will make a single tunnel from the side of the side seam which faces the back of the corset. I use a Teflon presser foot because I am stitching over the face of the garment. You can also use a roller presser foot for this purpose. </p>
<p><strong>I sew a tunnel from either side of the seam which joins the central front piece with the intermediate front piece (pieces 1 and 2).</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image046.jpg" class="img-responsive center-block"></p>
<p>When sewing these tunnels, you can either turn <strong>the concealed busk bar</strong> toward the back of the corset to avoid catching it or leave it as is and stitch right through. It's up to you. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a tunnel from the other side of the seam that joins pieces 1 and 2.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>And I make a tunnel from either side of the seam which joins the intermediate front piece and the side front piece of the corset (pieces 2 and 3).</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I make a tunnel from either side of the seam which joins the side back piece with the central back piece of the corset (pieces 4 and 5).</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image062.jpg" class="img-responsive center-block"></p>
<p>
