<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The first topstitched seam looks really nice.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<p>
