<h1>Tutorial 12. Topstitching the Seams. Sewing Tunnels and Lacing Bars.</h1>
<p></p>
<p><strong>I will topstitch both sides of each seam 1.5-2mm from the guideline.</strong> I switch to a Teflon presser foot and set the stitch length to 4mm. Remember to try it on a small piece of leather first. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p>
