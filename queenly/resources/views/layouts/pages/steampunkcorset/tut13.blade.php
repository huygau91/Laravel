<p><strong>I topstitch the remaining three seams on the first half of the corset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p>
