<h1>Tutorial 20. Cutting Flat Steels and Spirals. Inserting Bones in the Corset.</h1>
<p><strong>I will use flat bones for the lacing bars.</strong> They are made of steel and coated in plastic. The flat bone is supposed to be 0.5cm shorter than the finished tunnel. You can measure it by putting it over the corset or by simply using a ruler.</p>
<p><strong>I need four flat bones, 20.7cm long each. </strong>It takes rather sharp clippers and strong hands to cut the boning. I round off the ends of the bones trimming away sharp corners. You can also file them smooth on a bench-grinder.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image002.jpg" alt=""></p>
<p><strong>I put protective caps on the ends of each bone</strong> to prevent it from tearing the garment. You can secure the caps with the help of a hand press machine and specialty dies or with regular flat-nose pliers. If you don't have such caps at hand, you can tape the ends of all bones with pieces of masking tape or seal them with thermal glue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image004.jpg" alt=""></p>
<p><strong>I insert the bones in the tunnels made in the lacing bars of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image006.jpg" alt=""></p>
<p><strong>Spiral bones are cut just the same way as flat bones.</strong> The bone is supposed to be 0.5cm shorter than the finished tunnel. I cut bones in pairs because all tunnels are symmetric on both halves of the corset. You can also number the bones and the corresponding tunnels for the sake of convenience. I put protective caps on the ends of all bones and secure them by pressing.</p>
<p><strong>Then I insert the bones in the tunnels between the lining and the base.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image008.jpg" alt=""></strong></p>
<p><strong>Use flat-nose pliers to push the bone down to the very bottom of the tunnel. </strong>If it does not want to go in, you can insert a thinner flat bone in the tunnel, use it as a guide for sliding the spiral bone inside, and then remove the flat bone when you�re done.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image012.jpg" alt=""></p>
<p><strong>There are bones in all tunnels of the corset now.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2020_files/image014.jpg" alt=""></strong></p>

