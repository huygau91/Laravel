<h1>Tutorial 14. Sewing Decorative Boning Tunnels.</h1>
<h2>Finishing the Lay-On Elements with Bias Tape.</h2>
<p><strong>I need to sew a decorative leather bias tape parallel to the bone on the intermediate front piece of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image002.jpg" alt=""></p>
<p><strong>I need to cut this bias tape, fold the seam allowances, and prepare it for getting sewn on the corset.</strong> The bias tape should be a little wider than the tunnel and you also need to account for some space for stitching. The tunnel is 1.2cm and I will make 1.6cm wide bias tape. Its length should be about 26cm.</p>
<p><strong>In order to make a bias tape like this, I need a strap of leather that is 26cm long and 3cm wide. </strong>I will need two such bias tapes.</p>
<p><strong>I mark it on the thinnest side of the hide and cut it out with a knife. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image006.jpg" alt=""></p>
<p><strong>I also need two bias tapes for finishing the lay-on side elements.</strong> They will show from underneath those elements by about 1cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image008.jpg" alt=""></p>
<p><strong>I cut two straps for these bias tapes: each 4cm wide and 26cm long. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image010.jpg" alt=""></p>
<p><strong>First I will prepare the decorative bias tapes for the corset itself.</strong></p>
<p><strong>The bias tape is supposed to be 16cm wide and have 0.7cm seam allowances. I mark it on the prepared straps of leather.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><strong>I smear the flesh side of the strap with rubber-based adhesive and fold the seam allowances as indicated. </strong>The marked line is supposed to be on the edge of the folded bias tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image020.jpg" alt=""></p>
<p><strong>I press the bias tape with a roller from both sides.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image022.jpg" alt=""></p>
<p><strong>The decorative bias tape is ready.</strong> It has an even enough width of about 1.5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image024.jpg" alt=""></p>
<p><strong>Let us finish the lay-on side elements with bias tape.</strong></p>
<p><strong>I smear the top edge of the piece with rubber-based adhesive, fold it, and press it with a roller.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image030.jpg" alt=""></p>
<p><strong>Then I smear some adhesive over the strap of leather (bias tape) and fold it in half.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image032.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image034.jpg" alt=""></strong></p>
<p><strong>I press the bias tape with a roller. I keep slightly pulling it to give it a somewhat arched shape. </strong>The adhesive will secure it safely.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image036.jpg" alt=""></p>
<p><strong>Now I just need to make relevant marks on the bias tape and glue it to the lay-on side element.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2014_files/image038.jpg" alt=""></p>

