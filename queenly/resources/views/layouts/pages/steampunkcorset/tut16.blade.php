<h3>Tutorial 15. Applying Appliqu� Motifs and Decorative Plackets.</h3>
<h3>Sewing Bias Tape on the Corset.</h3>
<p><strong>I check how well the appliqu� composition fits on the lay-on element with the folded top edge.</strong> I mark the centres of the gears.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image002.jpg" alt=""></p>
<p><strong>And I do the same on the other lay-on element, only this time in a horizontally mirrored fashion.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image004.jpg" alt=""></p>
<p><strong>I want to add decorative flat piping along the top edge of the lay-on element before adding the appliqu�. </strong>This piping will look good if the width of its visible part is 0.8cm.</p>
<p><strong>I make dashed lines on the prepared bias tapes 1cm from the outer edge.</strong> The visible part of the piping will be exactly 0.8cm if the dashed line is overlaid by the lay-on piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image006.jpg" alt=""></p>
<p><strong>I smear the folded edge of the lay-on piece and the marked area of the bias tape with rubber-based adhesive. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image008.jpg" alt=""></p>
<p><strong>I put the lay-on piece over the bias tape trying to cover up the dashed line and press it with a roller.</strong> Try to avoid piercing your leather through. For the same reason, I prefer to replace basting stitches with glue wherever possible. I slightly ease in the edge of the bias tape when gluing it to the piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image019.jpg" alt=""></p>
<p><strong>The first lay-on side element has a piping at the top now.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image021.jpg" alt=""></p>
<p><strong>I do the same thing with the second lay-on element.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image023.jpg" alt=""></p>
<p><strong>Now it's time to apply the appliqu�.</strong></p>
<p><strong>I smear the motifs with rubber-based adhesive, glue them onto both pieces according to the composition, and press them with a roller.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image025.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image029.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image031.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image033.jpg" alt=""></p>
<p><strong>Now the second lay-on piece:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image035.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image037.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image039.jpg" alt=""></strong></p>
<p><strong>I have decorated both lay-on pieces with appliqu�.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image041.jpg" alt=""></p>
<p><strong>Next I will decorate the corset with the prepared bias tapes.</strong> They will reinforce the wide intermediate front piece. I will glue them on first and then sew them later.</p>
<p><strong>I smear the first bias tape with some adhesive, put it in place, and press it with a roller.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image043.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image045.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image047.jpg" alt=""></strong></p>
<p><strong>I add a symmetric placket on the other half of the corset.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image049.jpg" alt=""></strong></p>
<p><strong>And I secure the piping of the lay-on elements by stitching</strong> 2mm from the edge. (Set your stitch to at least 3.5mm).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2015_files/image051.jpg" alt=""></p>
