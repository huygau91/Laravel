<p><strong>The first half of the corset is ready.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I assemble the other half in just the same manner.</strong></p>
<p><strong>Now I need to open the seam allowances of the corset.</strong> You cannot press them open because they simply won't stay that way. </p>
<p><strong>Instead, you usually "glue them open" in this case.</strong> You do it by applying some rubber-based adhesive with a brush on the seam allowances and on the areas they need to be glued to. My leather is rather thin so I leave the iron-on fabric on the seam allowances to reinforce the seams. If your leather is very thick, you should not duplicate the seam allowances. You are also supposed to pound the seam allowances with a hammer in advance when working with thick leather.</p>
<p><strong>I smear both sides of the seam allowances with a rubber-based adhesive.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I stretch out the seam, spread the seam allowance by hand, and flatten it with a seam roller.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>The seam allowance is secured in place and looks as if it had been pressed open.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Make 0.5-0.7cm notches in the seam allowances of curved seams before opening them. It will help you spread them properly.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I continue opening the seam allowances.</strong></p>
