<h1>Tutorial 7. Assembling the Face of the Corset. Opening the Seam Allowances of the Corset.</h1>
<p></p>
<p><strong>I join the pieces of the front half of the corset one by one.</strong> I put them down in the required sequence to make things easier. I sew with a 1.5cm seam allowance and make bar tacks in the beginning and at the end of the row. I try to stitch very evenly because you are not supposed to rip seams up and re-stitch them on leather.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
