<h1>Tutorial 1. Types of Leather. Choosing the Right Leather and Calculating its Consumption.</h1>
<p></p>
<p><strong>I am about to start sewing a real-leather steampunk underbust corset.</strong> Steampunk is a style characterized by metal accessories and appliqué in the form of various mechanisms. All accessories are usually given an aged, vintage look.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Below is a waist cincher with brass accents and lay-on elements at the sides. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I will use lambskin for this garment.</strong> Judging from my personal experience, this type of genuine leather is the best for sewing corsets because it is flexible enough and hugs the curves nicely. <strong>Normally, you use 7-8mm lambskin in clothing. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>You can achieve a very nice decorative effect by combining plain dyed lambskin and lambskin with different coating effects (metallic, for example).</strong> Needless to say that you should be very careful when working with such leather because metallic coatings may go dull under high temperature: for instance, when you apply iron-on duplicating fabric. Silver or lamé-covered leather is also broadly used. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>You can also find sheepskin with coatings</strong> which allow you to combine the face with the underside, i.e. make the underside visible. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The wide spectrum of available colours will really let your creative flows run free.</strong> I will decorate my garment using all these types of leather in the forthcoming tutorials.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>You can also sew corsets from pigskin.</strong> Pigskin, however, is not robust enough unless you apply some special treatment to its surface. Otherwise it may easily start coming apart when wet. In order to make pigskin more noble and robust, you need to cover it with a layer of polish. </p>
<p><strong>The best type of pigskin for sewing corsets is pigskin with a high-gloss wrinkled finish.&nbsp; </strong>The wrinkles make it more flexible.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Pigskin with a high-gloss smooth finish is stiffer and tends to form folds.</strong> This kind of leather should only be used if you want to achieve a particular effect.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Bovine leather is also suitable for corset-making.</strong> It is thicker and more durable and it does not get scratched as easily. Although this leather is still rather flexible, it does not stretch as well. It is thicker than sheepskin and pigskin. </p>
<p><strong>Garment bovine leather is usually 0.9-1.2mm thick.</strong> You will need an industrial sewing machine to work with such leather after duplicating it with iron-on fabric. Garments made with such leather are very heavy and they also look quite brutal!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Calfskin </strong>is thinner than regular bovine leather but its elasticity properties are not good enough for sewing corsets. </p>
<p><strong>Bovine leather used in haberdashery goods is also suitable</strong> although even thicker (1.2-1.4mm). You will definitely need an industrial sewing machine to work with it. The best choice is a triple transport (triple feed) machine. Corsets are assembled by sewing lapped seams and by riveting. It is best to duplicate leather before punching holes for rivets.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Finally, you can also use very thick bovine leather − double bend</strong>. This is very thick and hard leather (2.0-6.0mm) and you can only join separate pieces by riveting. Corsets made with such leather have a particular look and style.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I have chosen sheepskin of two colours for my corset: chocolate brown and tan.</strong> To be precise, I will use two chocolate brown hides for the garment itself and one tan hide for decoration. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Make sure to take the pattern of your garment with you when shopping for leather: </strong>all hides are different in size and they may also have certain defects like holes, etc. It's why&nbsp; you need to lay your patterns out on the hides to double-check everything and make sure you get the right amount of leather. Don't forget to account for seam allowances either. Moreover, you should bear in mind that even hides from the same batch might differ in shade. </p>
<p><strong>The size of a hide is usually measured in square feet or square decimeters (dm²).</strong> It takes about 8-12 sq.ft. of leather to sew an underbust corset (depending on the size). </p>
<p><strong>Just like regular fabric, a hide tends to stretch more in a certain direction and less in other directions. It stretches least lengthwise (parallel to the backbone).</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%201_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>When calculating leather consumption, remember that only the central part of a hide can be used for high-quality garments.</strong> All other parts (shanks and belly) can be used for welting, appliqué and other decoration purposes. Pay attention to defects! For example, I bought a tan hide with a hole because I am only going to use it for decoration. My choice was determined by its colour.</p>
<p><strong>Always examine hides from both sides and not just the face!</strong> The underside of a hide is known as its flesh side. One of the major defects of the flesh side is when its top layer starts peeling off. I recommend you should avoid such areas when cutting the garment because it is hardly possible to duplicate them with iron-on fabric properly.</p>
<p><strong>It is also a good idea to hold the hide up against the light before making your choice:</strong> there may be some really small unnoticeable holes in it. I suggest you should circle stretch marks or any other defects with a pencil as soon as you notice them, to avoid these areas during the cutting. </p>
<p><strong>I need to press the hides before I can start working with them.</strong> I set the iron to "wool", turn off the steam, and press them from the flesh side. You can overlay the hide with paper if your iron sticks to the surface. Try to press a small area first to make sure your leather won't chance its colour and structure under high temperature. </p>
<p><strong>Careful! It is very easy to over-dry leather:</strong> it will lose all moisture, shrink, and become absolutely unsuitable for sewing! You should also be very careful when pressing metallic leather because it might not only change its colour but also go dull.</p>

