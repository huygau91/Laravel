<p><strong>The corset is laced up.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image060.jpg" alt=""></p>
<p><strong>What's the point of lacing it up this way?</strong></p>
<p><strong>First of all,</strong> the laces will be crossed in a neat and beautiful way after you tighten them.</p>
<p><strong>Moreover,</strong> your client will be able to tighten the laces herself after putting the corset on. She will put it on, fasten the busk at the front, and then take those loops at the back (also known as 'rabbit ears') and tighten the laces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image062.jpg" alt=""></p>
<p><strong>If you pull both loops simultaneously, the laces will be tightened evenly at the top and down the bottom. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image064.jpg" alt=""></p>
<p><strong>The top half and the bottom half of the loop are responsible for tightening the top and the bottom of the corset correspondingly. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image068.jpg" alt=""></p>
<p><strong>The corset can now be tightened with full regard to individual body proportions.</strong> A lacing ribbon like this usually lasts for up to six months even if the garment is worn frequently. And there is nothing difficult in changing it when the need comes. If you are confident in lacing corsets, you can change the ribbon whenever you want and choose one of a contrasting colour for more diversity.</p>
