<p><strong>I have reached the bottom grommet on the left half of the corset.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image056.jpg" alt=""></strong></p>
<p><strong>And I tie the ends of the ribbon in a tight knot. </strong></p>
