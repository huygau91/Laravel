<p><strong>I continue lacing the corset as per usual.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image048.jpg" alt=""></p>
