<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image042.jpg" alt=""></p>
<p><strong>I reach the ceinture grommet on the right half of the corset and make a loop.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image044.jpg" alt=""></p>
