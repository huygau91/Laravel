<h1>Tutorial 24. Sewing the Modesty Panel in Place. Lacing the Corset.</h1>
<p><strong>I usually sew the modesty panel to the right half of the corset: </strong>it is easier to spread it out that way. I align the edge of the leather piece with the row of stitches made along the second bone of the lacing bar. This way you will see the leather and not the lining through the eyelets.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image002.jpg" alt=""></p>
<p><strong>I pin the modesty panel to the lining and sew it on with blind stitches.</strong> I highly recommend using a thimble for this type of work. I make a back stitch every 5cm to secure the seam. I am using very robust reinforced polyester thread. You should by no means draw it too tight or else the seam will be visible on the face.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image006.jpg" alt=""></p>
<p><strong>Here is what it looks like with the modesty panel sewn in place: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image008.jpg" alt=""></p>
<p><strong>Now I can lace the corset and examine it on a dress-form with proportions similar to my client's.</strong></p>
<p>I usually lace it with a 6mm satin ribbon or a silk cord.</p>
<p><strong>I will teach you how to lace a corset</strong> to help it hold its shape for as long as possible and to make it easy for the wearer to lace it herself.</p>
<p>I have installed a pair of closely-spaced grommets near the ceinture. You need to make a loop between them when lacing the corset. In order to form the loop, the ribbon needs to pass through the top ceinture grommet from the bottom and through the bottom ceinture grommet from the top down.</p>
<p><strong>Knowing this, you can figure out how to bring the ribbon through the top grommet of the lacing bar. Arrows in the picture below indicate the direction in which the ribbon is brought through each grommet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image010.jpg" alt=""></p>
<p><strong>I put the end of the ribbon through the top grommet from the top down.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image012.jpg" alt=""></p>
<p><strong>I divide the ribbon in half and start lacing the corset in the round</strong> alternating the direction in which the ribbon goes through the grommets: down to up and up to down, etc. I step a grommet lower each time I move from one half of the corset to the other.</p>
<p><strong>The step-by-step lacing process is shown in detail in the pictures below.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image022.jpg" alt=""></p>
<p><strong>If all is well, the ribbon is supposed to pass from the bottom up through the top ceinture grommet on the left half of the corset. Now I need to make a loop in the ceinture grommets.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image024.jpg" alt=""></p>
<p><strong>I skip the ceinture grommets on the right half of the corset and bring the ribbon through the next grommet pulling it upwards from underneath the lacing bar. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image026.jpg" alt=""></p>
<p><strong>I continue lacing the corset with the first half of the ribbon. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2024_files/image030.jpg" alt=""></p>
