<h1>Tutorial 22. Finishing the Bottom Edge of the Corset.</h1>
<p><strong>I need to finish the bottom edge of the corset with bias tape </strong>− just the same way as the top edge. I have already finished the hem of the first half of the corset and now I will do the same on the second half. We will then compare the widths of bias tapes and the level they are sewn at on both halves.</p>
<p><strong>I put the bias tape to the corset and start sewing it on. I stitch at a slow pace 1cm from the bottom edge.</strong> I use a special presser foot accessory because there are more bulky areas along the bottom edge of the corset. You just place it underneath the presser foot when stitching over the bulky areas to level out the thickness.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image006.jpg" alt=""></p>
<p><strong>If you noticed any skipped stitches in the row, you can re-stitch it from the other side.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image008.jpg" alt=""></strong></p>
<p><strong>The bias tapes are placed at the same level on both halves of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image010.jpg" alt=""></p>
<p><strong>I finish sewing the bias tape along the bottom edge of the corset. </strong></p>
<p><strong>I smear the seam allowance of the bias tape and the area 1cm below it with rubber-based adhesive, turn it upwards, and press it with a roller. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image014.jpg" alt=""></strong></p>
<p><strong>I need to form neat corners at the ends of the bias tape. </strong>I trim the excess of seam allowances in the corners.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image022.jpg" alt=""></p>
<p><strong>The bias tape should be placed at the same level and have the same width on both halves. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image024.jpg" alt=""></p>
<p><strong>I finish working on the hem: smear the bias tape with the adhesive, fold it over the edge, and press it with a roller. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image030.jpg" alt=""></p>
<p><strong>I recommend that you pound particularly thick areas with a mallet before sewing.</strong> I use a silicone mallet to avoid damaging the leather.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image032.jpg" alt=""></p>
<p><strong>Now I can secure the bias tape by stitching.</strong> As I was securing the bias tape on the first half of the corset, I figured that a regular presser foot was better-suited for this purpose than a Teflon presser foot because the latter did not seem to press the leather down as well and kept skipping stitches. I use a specialty presser foot for stitching through thicker areas.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image040.jpg" alt=""></p>
<p><strong>The bias tape is sewn in place.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image042.jpg" alt=""></p>
<p><strong>I trim the seam allowance on the inside and tie knots at the beginning and the end of the seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image044.jpg" alt=""></p>
<p><strong>All I need to do now is glue the lay-on elements to the corset, secure them with rivets, add the decorative swing-arm latches, and sew a modesty panel.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2022_files/image046.jpg" alt=""></p>

