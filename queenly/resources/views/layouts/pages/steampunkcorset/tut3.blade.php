<h1>Tutorial 2. Additional Tools and Materials.</h1>
<p><strong>I would like to talk about certain materials which I always use in corset-making. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image002.jpg" alt=""></p>
<p><strong>Iron-on fabric is a must</strong> because a corset is supposed to shape the body and, therefore, not supposed to stretch out. Leather should be duplicated with special low-temperature iron-on fabric which can be fused on with the iron set to "silk" or "wool" (no steam).</p>
<p><strong>I use woven interfacing as duplication fabric.&nbsp; </strong>Since it is rather thin, I duplicate each piece of leather twice and in the opposite directions.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>Sometimes I also use low-temperature iron-on muslin,</strong> which I either apply with a household iron or with a warm press-iron. I put the muslin down with its least stretchy grain horizontally to prevent the corset from stretching widthwise.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>As for the lining, I prefer to sew it with natural cotton materials.</strong> I set the iron to strong steam and decatize the fabric in advance to prevent the lining from shrinking later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>Let's move on to accessories.</strong></p>
<p><strong>Rivets.</strong> They are made from two parts: the cap and the post. I use double cap rivets for thick-leather corsets to protect the wearer's skin from sharp edges. If you need to carry out a fitting, simply snap the caps of rivets on the posts without actually pressing them closed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image010.jpg" alt=""></p>
<p><strong>I will also use brass grommets and washers in the lacing bars.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image012.jpg" alt=""></p>
<p><strong>I will add a double fastener to the corset to make sure it can handle strong tightening</strong>: there will be busks on the inside and decorative swing-arm latches on the outside (I will install the latter at the very last moment). I will use stainless steel uncoated busks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>All these elements are set with the help of a hand-press tool and special accessories. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image016.jpg" alt=""></p>
<p><strong>I will also use revolving punch pliers to punch holes for setting rivets and grommets.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image018.jpg" alt=""></p>
<p><strong>It is best to make smaller holes and then broaden them with an awl to make sure your rivets and grommets will sit safely in the leather. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image020.jpg" alt=""></p>
<p><strong>I use tin snips for cutting busks.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image022.jpg" alt=""></p>
<p><strong>Please look at these two closely-spaced loops at the bottom of the busk.</strong> Make sure to cut the busk from the opposite end to preserve this pair of loops and posts (they will be placed at the level of the stomach). When cutting the busk, remember that it is not supposed to get into the seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image024.jpg" alt=""></p>
<p><strong>I only use steel bones for hard corsets. Preferably those made in Germany. </strong>My favourite choice is 8mm steel bones. Sometimes I also use 4-5mm bones: when sewing for a really thin girl or with very thin leather, for example. As for whalebone and plastic bones, I use them very seldom and only in those cases when minor tightening is enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image026.jpg" alt=""></p>
<p><strong>I cut steel boning with side cutters and put special steel caps on their ends.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image028.jpg" alt=""></p>
<p><strong>I use flat steels with plastic casing on lacing bars. </strong>With these bones, your lacing bars won't bend and buckle.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image030.jpg" alt=""></p>
<p><strong>I round off the ends of flat bones and file them smooth on a bench-grinder.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image032.jpg" alt=""></p>
<p><strong>Working with leather is technically different from working with fabric:</strong> you can neither pin pieces together, nor baste them, nor even press your seams. You need to use other methods instead.</p>
<p><strong>I use a rubber-based adhesive to temporarily secure leather pieces together and prevent their seam allowances from shifting against each other.</strong> In most cases, you can easily separate the pieces without any damage after the adhesive dries out.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image034.jpg" alt=""></p>
<p><strong>And I use rapid glue to secure the seam allowances together for good.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image036.jpg" alt=""></p>
<p><strong>I pound seam allowances </strong>using either a shoe mallet or a silicone mallet that doesn't leave marks on leather.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image038.jpg" alt=""></p>
<p><strong>Working with leather also requires extra robust sewing thread.</strong> I use Synton polyester thread No. 60 by Amann. It is very durable and I don't have to worry about any seams coming undone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image040.jpg" alt=""></p>
<p><strong>Sheepskin corsets are sewn with needles No.90/14 and 100/16.</strong> I recommend using special needles with sharp triangular points for thicker types of leather.</p>
<p><strong>A silver leather marking pen is another necessary tool.</strong> Markings made with a pen like this can be easily wiped off with petroleum jelly or silicone-based machine oil. But, of course, it does not mean you should make markings all over your leather.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%202_files/image042.jpg" alt=""></p>

