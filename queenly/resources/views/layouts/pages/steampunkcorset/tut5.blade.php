<h1>Tutorial 4. Duplicating Leather with Iron-On Batiste.</h1>
<p><strong>I need to examine the hides before getting started with the cutting.</strong> I have prepared two chocolate brown hides for the main part of the corset. Let me remind you that the backbone is the most robust area and there are belly halves from either side of it. Of course, high-quality garments are made of the central part of the hide (the back) without the belly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image002.jpg" alt=""></p>
<p><strong>Remember to examine the flesh side of the hide. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>There are small holes on the second hide but only on the belly parts which I am not going to use anyway.</strong> I haven't found any scars or scratches on the hides. I check the flesh side. If you notice any scars that you don't want to see on the face of your garment, make sure to circle them with a pencil straight away so you can account for them during the cutting.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image006.jpg" alt=""></p>
<p><strong>It's time to arrange the pieces on the hides.</strong> If your leather is too wrinkled (particularly if it was simply folded several times and not rolled up for storage), I recommend you should carefully press it from the flesh side at the �wool" setting and without steam.</p>
<p><strong>I trace the central front piece (piece 1).</strong> I use a couple of weights to press the paper pattern down because you must never pin things to leather. I transfer all pieces onto the face of the hide to make sure I will notice any defects. It's OK if some defects get on the seam allowances but they should by no means get on the main part of the piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image008.jpg" alt=""></p>
<p><strong>I draw the second half of the central front piece in a mirrored fashion. Make sure to transfer the notches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image012.jpg" alt=""></p>
<p><strong>Next I arrange and trace the two symmetric intermediate pieces of the front (piece 2).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image018.jpg" alt=""></p>
<p><strong>And then both halves of the central back piece (piece 5):</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image022.jpg" alt=""></p>
<p><strong>I trace the side parts of the back (piece 4).</strong> Part of the seam allowance falls upon the belly of the hide but it won't be noticeable on the side seam. Leather is a rather expensive material and I try to arrange the pieces wisely.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image028.jpg" alt=""></p>
<p><strong>I trace the side parts of the front (piece 3).</strong> Unfortunately, the seam allowance falls upon the belly again.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image032.jpg" alt=""></p>
<p><strong>Now I need to transfer the lay-on decorative side elements.</strong> They fit onto the same hide.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image036.jpg" alt=""></p>
<p><strong>I have transferred all major pieces and they have all fit onto one hide.</strong> The second hide will also come in handy though.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image038.jpg" alt=""></p>
<p><strong>Now I need to duplicate the pieces.&nbsp; </strong></p>
<p><strong>I have removed the excess of leather so I don't have to duplicate the entire hide.</strong> I have also separated the lay-on elements because they don't require duplication.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image040.jpg" alt=""></p>
<p><strong>And now I will duplicate the leather with iron-on fabric.</strong> I will use low-temperature iron-on batiste. I set the press-iron to "wool" and turn off steam because steam might make leather too dry and then it won't look as good or feel as soft anymore.</p>
<p><strong>I turn the leather flesh side up</strong> and put the batiste down with its lengthwise grain parallel to the backbone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image042.jpg" alt=""></p>
<p><strong>And I start fusing it on.</strong> I simply stamp it with the iron moving it from place to place. I hold it down for at least 10 seconds in each place and try my best not to miss any areas. You could also use a heat press machine for this purpose.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%204_files/image044.jpg" alt=""></p>

