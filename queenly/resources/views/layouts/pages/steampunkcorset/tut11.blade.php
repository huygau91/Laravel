<h1>Tutorial 10. Stitching Around the Busk. Adding a Ceinture to the Lining. </h1>
<h3>Attaching the Concealed Busk Bars.</h3>
<p></p>
<p><strong>I stitch along the edge of either busk half following the lines marked.</strong> I use a zipper presser-foot and set the stitch to 3.5mm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Done.</strong> I can remove the basting stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can assemble the lining. </strong>I have sorted out the lining pieces and arranged them in the joining sequence. I start joining them from the intermediate front piece (piece 2). I switch back to the regular presser foot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I assemble the other half of the lining.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>The lining is ready and now I need to press open the seam allowances.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I have pressed the seam allowances open and now I can sew on a ceinture.</strong> A ceinture can be made of any tape that is thick enough. I am using rather robust reflective fabric tape. </p>
<p><strong>I sew the ceinture following the marks I made during the cutting.</strong> I stitch 1.5mm from the edge of the tape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a ceinture onto the other half of the lining.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Now the lining of the corset has a ceinture on it.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the first concealed busk bar in place, cover it up with the central front piece of the lining from the third set of cut pieces, and secure it with pins.</strong> I align the notches of the pieces.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same with the other concealed busk bar. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I join the pinned together pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>The lining of the corset is nearly finished!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2010_files/image062.jpg" class="img-responsive center-block"></p>
