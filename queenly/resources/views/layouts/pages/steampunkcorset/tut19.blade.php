<h1>Tutorial 18. Marking the Placement of Garment Accessories.</h1>
<p><strong>I have finished the top edge of the other half of the corset. </strong>Make sure the corners are placed at the same level and the width of the bias tape is the same on both halves.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image002.jpg" alt=""></p>
<p><strong>It's time to mark the placement of grommets and rivets.</strong></p>
<p><strong>I will start from grommets.</strong> They are usually placed with 2-3cm intervals on hard corsets. The first and the last grommet are placed about 1.5cm from the edge.</p>
<p><strong>I mark 1.4cm from the bottom edge for the last grommet and 1.5cm from the top edge for the first one.</strong> The distance is different because I haven't finished the hem yet.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image004.jpg" alt=""></p>
<p>Grommets won�t be spaced evenly <strong>because there is a ceinture on the inside </strong>and there will be rabbit ears loops for lacing the corset at its level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image006.jpg" alt=""></p>
<p><strong>I will install a grommet right above the top edge and another right below the bottom edge of the ceinture.</strong> These grommets will bite into the ceinture and hold firmly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image008.jpg" alt=""></p>
<p><strong>The parts of the lacing bar above and below the ceinture grommets will be divided into equal sections.</strong> Grommets will be placed with 2.4cm intervals at the bottom and 2.3cm at the top. <strong>&nbsp;Symmetric markings should be made on the other half of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image010.jpg" alt=""></p>
<p><strong>I also need to mark several holes for installing decorative rivets.</strong></p>
<p><strong>First I mark their placement along the decorative placket.</strong> I step 0.7cm from the edge of the placket to account for the width of caps and prevent them from getting on the bias tape. I step 1.4cm down from the top edge and 1.5cm up from the bottom edge, measure the distance between the outermost caps (22.8cm), and divide it into even sections. I will need to place rivet caps with 2.05cm intervals. I mark corresponding markings and transfer them symmetrically onto the other half of the corset.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image012.jpg" alt=""></p>
<p><strong>Next, I need to mark the placement of rivets on the lay-on side elements.</strong> They will be installed in the middle of each gear and along the rounded edge of the main element. I step 1.5cm from either end of the rounded edge. The distance between the outermost rivets is 20cm, therefore, I will place them with 2cm intervals along the rest of the line. The middles of their caps should be placed 0.3cm from the edge of the piece. I make corresponding markings and transfer them onto the other lay-on side element. I will use rapid glue to attach the lay-on side elements to the corset and then secure them with rivets.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image014.jpg" alt=""></p>
<p><strong>I punch holes for the rivets using a hand press </strong>and a die of a corresponding diameter. It is best to make the holes a little smaller than needed.</p>
<p><strong>The holes are punched as marked on the piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image018.jpg" alt=""></p>
<p><strong>I also make a hole in the centre of each gear.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image022.jpg" alt=""></strong></p>
<p><strong>Then I punch holes along the decorative placket at the front of the corset.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image026.jpg" alt=""></strong></p>
<p><strong>I will use revolving punch pliers to make holes for grommets on the lacing bars.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2018_files/image028.jpg" alt=""></strong></p>

