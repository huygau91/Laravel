    <h1>Tutorial 13. Cutting&nbsp;<span class="GramE">Appliqu�</span> Motifs.</h1>
    <p><strong>I prepared two lay-on elements when I was cutting the corset. </strong>They will be glued on at the sides and secured by rivets. <strong>I have also prepared paper patterns of decorative motifs</strong> which I will cut from the tan leather and secure on the lay-on side elements. You will find these patterns in the appendix at the end of the manual.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image002.jpg" alt=""></p>
    <p>Before cutting these motifs from leather, I suggest we should check whether they will fit on the lay-on side elements well enough.</p>
    <p><strong>I mark the appliqu� area on both lay-on pieces:</strong> I step 1cm from the bottom edge to leave space for bias tape and 0.7cm from the top edge for folding it and finishing it with a bias tape.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image004.jpg" alt=""></p>
    <p><strong>I arrange the motifs on the background pieces</strong>. Why these particular motifs? Well, we are making a steampunk corset. This style is characterized by decoration in the form of gear wheels and various mechanisms. It's why my composition is represented by a large gear wheel with smaller gear wheels at the sides. It resembles a working mechanism which seems to be rolling toward the centre of the corset. Please keep in mind that the central gear wheel is not symmetric. Make sure to cut it out horizontally mirrored for the second lay-on piece.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image005.jpg" alt=""></p>
    <p><strong>The gear wheels will be attached to the lay-on elements by rivets in the middle. I mark the corresponding points.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image007.jpg" alt=""></p>
    <p><strong>And I start cutting the appliqu� motifs from the decorative tan leather.</strong> My leather has a rather smooth and even flesh side.</p>
    <p><strong>I secure the motifs on the leather with a glue stick and outline them. </strong>I also mark the centres of the pieces. I don't want to waste any leather so I try my best to arrange the motifs wisely.</p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image009.jpg" alt=""></strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image011.jpg" alt=""></strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image013.jpg" alt=""></strong></p>
    <p><strong>I cut the motifs following the outer contours and remove the paper patterns.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image015.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image017.jpg" alt=""></p>
    <p><strong>I will use a knife, scissors, and revolving punch pliers for cutting the motifs properly.</strong> Make sure not to leave any silver lines inside the motifs if you don�t want them to turn out larger than needed. <strong>I use a self-healing cutting mat for this kind of work.</strong> It is a true must-have because your knife doesn�t go blunt (unlike with wooden mats) and you can cut things out more accurately.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image019.jpg" alt=""></p>
    <p><strong>I cut the motifs following the contours.</strong> You need very sharp scissors for working with leather.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image021.jpg" alt=""></p>
    <p><strong>I cut out the teeth of the large gear wheel.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image023.jpg" alt=""></p>
    <p><strong>I notch the sharp corners of the inner contours with a knife and then cut them with scissors.</strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image025.jpg" alt=""></strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image027.jpg" alt=""></strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image029.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image031.jpg" alt=""></p>
    <p><strong>I use revolving punch pliers for the holes inside the motifs.</strong>&nbsp; First I make a small hole in the middle with the smallest punch: I will insert a rivet in there. Then I make the rest of the holes on the motif choosing punches of matching diameters. The central appliqu� element is ready!</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image033.jpg" alt=""></p>
    <p><strong>I cut out the second appliqu� motif.</strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image035.jpg" alt=""></strong></p>
    <p><strong>First I will punch all holes.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image037.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image039.jpg" alt=""></p>
    <p><strong>Since the teeth of this gear have rounded edges, I can simply cut them out with the same punching pliers.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image041.jpg" alt=""></p>
    <p><strong>I remove the excess of leather along the perimeter.</strong></p>
    <p><strong><br>
    <img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image043.jpg" alt=""></strong></p>
    <p><strong>I punch one hole after another to shape the teeth of the gear and even them out with a knife.</strong></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image045.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image047.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image049.jpg" alt=""></p>
    <p><strong>Now I just need to cut out the inner part of the motif.</strong> First I notch the middle with a knife and then cut it out with scissors.</p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image051.jpg" alt=""></p>
    <p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2013_files/image053.jpg" alt=""></p>
    <p><strong>The second appliqu� element is ready as well.&nbsp; </strong></p>
    <p><strong><br>
