<h1>Tutorial 8. Cutting the Busk. Marking Sewing Lines for the Busk.</h1>
<p></p>
<p><strong>Now, before assembling the lining, I need to prepare special bars for installing a concealed busk fastener. It involves cutting the busk to the required length of course.</strong> The fastener is supposed be 1.5cm shorter than the corset from either end. I will keep this in mind when marking the placement of the busk.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I take the two central pieces of the lining duplicated with batiste along the edges.</strong> I put them wrong side up and take the busk. Busks are made of two long, flat pieces of steel, about 1.5mm thick, with posts or buttons on one and matching loops on the other. I am using a tapered busk because it helps flatten the stomach. The tapered busk is wider at the bottom (in the stomach area) and features two closely-spaced clasps. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the placement of the loop half of the busk. </strong>I mark 1.5 from the top edge and the bottom edge of the lining piece and mark a 1.5cm seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>If I align the bottom of the busk with the mark at the bottom of the lining,</strong> the top loop of the busk will be too far from the top edge of the corset. In other words, it's impossible to put the busk in place without cutting it from both ends and not just from one end.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I put the busk down so as to maintain the same distance between the outermost loops and the marks at the top and at the bottom.</strong> As I have said, I will have to cut it from both ends. The two loops at the bottom will be preserved. I mark the cutting lines on the busk.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fasten the busk and transfer the cutting lines onto the post half. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I cut both halves of the busk with tin snips as marked.</strong> Cutting the busk takes quite a lot of effort. You do need some really good tin snips. I round off the ends.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I put the adjusted busk halves on the lining and compare their lengths.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I file the ends on a bench-grinder.</strong> I try my best to make them perfectly smooth and round.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong><em>Unfortunately, my busk turned out too long and I had to start from scratch. I took another busk and cut it anew removing another loop from one end. You will know what I mean from further pictures and explanations.</em></strong></p>
<p><strong>I align the prepared busk halves with the marks on the lining, trace them, and mark the placement of loops.</strong> And I file their ends smooth again.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>

