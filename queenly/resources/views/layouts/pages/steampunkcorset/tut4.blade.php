<h1>Tutorial 3. Calculating Leather Consumption and Preparing the Patterns.</h1>
<p><strong>Let us look at the garment we are going to sew.</strong> It is a waist cincher or an underbust corset with a waist restriction effect. The top edge is basically straight: only slightly raised in the middle of the front. The bottom edge is somewhat convex at the front and more or less straight at the sides and at the back. There are decorative lay-on elements at the sides of the corset with some appliqu� on them. The corset performs a body control function, which means it is supposed to restrict the wearer's waist. I will account for this during the cutting.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image002.jpg" alt=""></p>
<p><strong>Let us calculate necessary alterations of the standard pattern.</strong></p>
<p><strong>If you are sewing a corset for a distant client,</strong> it is enough to know three key measurements: under-bust circumferences, waist circumference, and stomach circumference taken 12cm below the waistline.</p>
<p>You can naturally use additional measurements <strong>if you can meet with your client and carry out fittings .</strong> I would not ask a distant client to take those additional measurements off herself because there is a great chance she will do it wrong! It makes sense to ask your client about her height in addition to the three key measurements. Any other measurements can be taken from a sizing chart.</p>
<p><strong>The table below features my client's measurements and measurements taken off the pattern (columns 3 and 2 correspondingly).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image004.jpg" alt=""></strong></p>
<p><strong>There will be a gap between the lacing bars of my body-control corset.</strong>&nbsp; I purposely leave this gap because of the waist restriction effect. The corset is likely to stretch out with time and I must provide for an opportunity of lacing it tighter when the need comes. I usually make a gap of about 6cm. But since a leather corset is rather thick, I will make it 5cm. Personal experience tells me it should be enough.</p>
<p><strong>I subtract 5cm from all client's measurements and write the resulting values in column 4. </strong>In other words, these are new client's measurements which account for the gap between the lacing bars. These new values will be used in all further calculations.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image006.jpg" alt=""></strong></p>
<p><strong>Now let us calculate necessary alterations. I will write the values in column 5.</strong></p>
<p><strong>Under-bust circumference:</strong> 73cm on the client and 70cm on the pattern.</p>
<p><strong>73cm - 70cm = 3cm</strong></p>
<p>Therefore, I need to add 3cm to the under-bust circumference.</p>
<p><strong>Waist circumference:</strong> 63cm on the client and 59cm on the pattern.</p>
<p><strong>63cm - 59cm = 4cm</strong></p>
<p>I need to add 4cm to the waist circumference.</p>
<p><strong>Stomach circumference:</strong> 85cm on the client and 80cm on the pattern.</p>
<p><strong>85cm - 80cm = 5cm</strong></p>
<p>I need to add 5cm to the stomach circumference or the bottom circumference of the corset.</p>
<p><strong>But my corset is supposed to restrict the client's waist, which means I need to make her waist circumference smaller!</strong> It is enough to remove 4cm from the waist circumference for a mild restriction effect. In this particular case, it means I can simply use the initial client's measurement of 59cm. I am not supposed to change the under-bust and stomach circumferences.</p>
<p><strong>The final values for customizing the pattern are written in columns 5 and 6 and account for the gap between the lacing bars and the desired degree of waist restriction.</strong></p>
<p>I leave the waist circumference as is, add 3cm to the under-bust circumference, and add 5cm to the stomach circumference.</p>
<p><strong>I have calculated all alterations which need to be applied to the standard corset pattern.</strong></p>
<p><strong>Next I print out the pattern.</strong> It is made of five pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image008.jpg" alt=""></p>
<p><strong>I need to adjust them in correspondence with client's measurements.</strong></p>
<p><strong>Under-bust circumference.</strong> According to the calculations, I need to add 3cm to the under-bust circumference - 1.5cm on each half of the corset. It is best to distribute this value between the side seams and the princess seams at the back. This means I need to shift each corresponding cutting line 0.35cm outwards at the level of the under-bust line. I shift it 0.4cm outwards in the side seams and 0.3cm outwards in the princess seams at the back. I carefully bring the new side seams and princess seams to the waistline using the pattern as a template.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image012.jpg" alt=""></p>
<p><strong>What concerns the stomach circumference, I need to add 5cm - 2.5cm on each half of the corset.</strong> Again, I will distribute it between the side seams and the princess seams at the back. I would not recommend using the front princess seams for this purpose. My client's proportions are rather balanced so I just add 1cm in each side seam and 0.2cm in each back princess seam. And I re-draw the lines gracefully with the help of the pattern. Please note that I start not from the ceinture and not from the waistline. <strong>Ceinture</strong> is dense non-stretch bias tape that serves to prevent the corset from stretching out in the waist and help it hold its shape. Since regular ceinture is 2cm wide, I step 2cm down from the line that marks its top edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image016.jpg" alt=""></p>
<p><strong>Now I will adjust the central piece.</strong> Let me remind you that the top edge of the corset is raised in the middle of the front while the rest of it is almost straight. The original pattern was made for a corset undergarment which was supposed to add support to the wearer's bra. I move the centre of the front of the corset up by literally 1cm and re-draw the top edge of the central front piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image018.jpg" alt=""></p>
<p><strong>I have adjusted the width of the corset. Now I need to cut out the altered pattern and adjust the length of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image020.jpg" alt=""></p>
<p><strong>Let me adjust the top edge of the corset. </strong></p>
<p><strong>I pin the pieces together aligning their sewing lines from the top to the waistline.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image028.jpg" alt=""></p>
<p><strong>And I re-draw the top edge of the corset. </strong>&nbsp;There was a noticeable rise at the back of the corset on the standard pattern. I straighten the top edge and bring it to the edge of the lacing area at a straight angle while keeping it slightly raised in the middle at the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image030.jpg" alt=""></p>
<p><strong>I trim the top edge of the corset following the newly marked cutting line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%203_files/image034.jpg" alt=""></p>
<p><strong>Next, I unpin the pieces and adjust the bottom edge of the corset the same way.</strong> I pin the pieces together from the waistline to the bottom.</p>
