<h1>Tutorial 17. Finishing the Top Edge with Bias Tape.</h1>
<p><strong>I need to finish the top edge and the hem of the garment with bias tape before installing grommets.</strong></p>
<p><strong>I will start from the top edge. </strong>I have prepared 4.5cm wide straps of tan leather. They are a couple centimeters longer than the top edge of the corset.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image002.jpg" alt=""></p>
<p><strong>I apply the first bias tape sewing 1cm from the edge.</strong> I will make it wide enough to match the decorative plackets on the corset. I stitch with a Telfon presser foot. The stitch should be at least 3.5mm. I haven’t glued it in place in advance but you can if you want to. Pull on it as you sew − not too hard though.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image010.jpg" alt=""></p>
<p><strong>I turn the bias tape up and fold it over the edge of the corset. </strong>Unlike with fabric bias tape, I won't fold the other edge of this leather bias tape but simply sew it down and trim the excess.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image016.jpg" alt=""></p>
<p><strong>Always examine the seams from the inside to make sure there are no skipped stitches.</strong> Sewing machines often skip stitches in thick areas, so you might have to use a thicker needle or a special leather needle. I try to avoid it as best I can because you are not supposed to pierce leather too much.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image018.jpg" alt=""></p>
<p><strong>I take my rubber-based adhesive and smear the bias tape lengthwise 2cm from the edge.</strong> This will help me secure it in the folded position and then I will secure its opposite edge and fold the corners.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image020.jpg" alt=""></p>
<p><strong>I fold the tape over the top edge of the corset, secure it in place, and press it with a roller.</strong> You can also pound it with a mallet.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image024.jpg" alt=""></p>
<p><strong>Now I need to finish the corners of the bias tape.</strong> They will be too thick if I do it the same way you usually do it with fabric bias tape.</p>
<p><strong>Therefore, I need to notch out the excess.</strong> I could do it with a knife if my leather were thick but it is in fact very soft.</p>
<p><strong>And so I just cut the end of the bias tape down to 1.2cm so I can still fold it.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image026.jpg" alt=""></strong></p>
<p><strong>I trim the excess of the seam allowance.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image028.jpg" alt=""></p>
<p><strong>The corner of the bias tape is now thinner.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image029.jpg" alt=""></p>
<p><strong>After smearing the end of the bias tape with glue and folding it, I trim the part of the seam allowance that does not perform any function.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image031.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image033.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image035.jpg" alt=""></p>
<p><strong>I have basically trimmed away two layers of leather in the corner of the bias tape and it has become a lot thinner.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image037.jpg" alt=""></p>
<p><strong>I do the same with the other end of the tape.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image041.jpg" alt=""></p>
<p><strong>Look how neat it looks in the end.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image043.jpg" alt=""></p>
<p><strong>I finish framing the top edge of the corset.</strong></p>
<p><strong>I smear the bias tape lengthwise 1cm below the fold line, fold it over the edge of the corset pulling it rather tightly, and press it with a roller. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image045.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image047.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image049.jpg" alt=""></p>
<p><strong>The top edge of the corset is finished.</strong> I do the same with the top edge of the other half of the corset.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/steampunk%20corset/Tutorial%2017_files/image051.jpg" alt=""></p>

