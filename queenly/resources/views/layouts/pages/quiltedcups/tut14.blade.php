<h3>Tutorial 14. Finishing the Corset. Decoration. </h3>
<p></p>
<p>I have almost finished sewing this corset. Now I only need to put some finishing touches and decorate it.</p>
<h4>Sewing a Lacing Flap. </h4>
<p></p>
<p><strong>First of all I need to sew a flap that is sewn-on underneath the lacing.</strong> </p>
<p>Its length should equal the length of the back of the corset.</p>
<p>You can choose its width yourself depending on how wide the open gap on the back is going to be.</p>
<p><strong>I take 2 rectangular pieces of face fabric fused with iron-on batiste from the wrong side and sew them together along 3 sides:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I bend the seam allowances towards the lining side and make a clean-finish-edge seam</strong> in order to turn the flap out easier and achieve a neat piped-like edge:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p>I trim corners and seam allowances. </p>
<p>I make a stitch along the fourth side leaving a small hole in the middle for turning the flap out. </p>
<p><strong>Then I turn the flap out and stitch the hole up by machine:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>Then I press this flap and hand-stitch it onto the lining of the corset at the lacing area.</p>
<h4>Punching Grommets.</h4>
<p></p>
<p><strong>So I have sewn a flap and now I start marking holes for grommets: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p>I measure the length of the back leaving 0.4-0.6 inches from top and bottom and then divide this value into sections to make the distance between the holes about 1.4-1.6 inches. </p>
<p>In my case the distance between the grommets is 1.5 inches. I make markings.</p>
<p><strong>I take my punch machine and punch holes through using a special accessory:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I bend the corset and make markings on its second side through the existing holes in order to make sure these lacing holes are in exactly the same spots as those on the first side, i.e. the grommets are symmetric:</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I punch all holes through, insert grommets and expand them with the help of the machine. </p>
<p><strong>Now the grommets are inserted:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image013.jpg" class="img-responsive center-block"></p>
<h4>Decoration.</h4>
<p></p>
<p>I am going to decorate my corset in a very simple way. </p>
<p><strong>I take a 1 inch wide satin ribbon and make gathers on it using a household sewing machine with a special presser foot: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image015.jpg" class="img-responsive center-block"></p>
<p>You could gather the ribbon by hand as well then secure it with pins and make a running hand-stitch. </p>
<p><strong>I put the corset onto the mannequin and arrange this gathered ribbon into an improvised ornament securing it with pins: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image017.jpg" class="img-responsive center-block"></strong></p>
<p>You can use any ornament depending on the shape of the neckline. You could also make an ornament along the bottom of the corset. </p>
<p>Afterwards I stitch this decorative element onto the corset by hand. </p>
<p></p>
<p><strong>And thereupon my quilted cup corset is completed! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2014_files/image019.jpg" class="img-responsive center-block"></p>

