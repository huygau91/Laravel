<h3>Tutorial 9. Joining the Face Pieces of the Corset. </h3>
<p></p>
<p>During this tutorial I am going to sew and press the face pieces of the corset. </p>
<p><strong>I start by sewing together the central and side pieces of the front, i.e. joining the bust curves:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p>I match the pieces, even out their cut edges and sew them together using a 0.5 inch seam allowance.</p>
<p>Please note that I haven�t pinned the pieces together and moreover - I am not using any bar tacks. I urge you to learn sewing exactly like this. Then you will also learn to get the feel of the fabric, its tension and fullness. You will be able to control the tension degree of two layers with your hands and make more professional-level garments. </p>
<p>After the fabric layers have been pinned together the sewing machine starts bouncing on them disturbing the smoothness of the seam. However it is absolutely impossible to control the matching of fabric layers when you hand-tack them. </p>
<p><strong>Now I sew the second medium front piece onto the central piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I trim corners and then trim seam allowances by 0.2-0.3 inches:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>As opposed to the lining you should trim absolutely all seam allowances on the face part of the corset!</strong></p>
<p><strong>I am frequently asked: why do I need to sew things together first and then trim seam allowances?</strong>&nbsp; Why not make a 0.2-0.3 allowance from the very beginning? Please trust my experience - it is way easier to join pieces with a 0.4-0.5 inch allowance. Your seam in this case has higher quality and the layers are joined together much better which has to do with the way the needle plate of your sewing machine is adjusted.</p>
<p><strong>I sew-on the first side piece of the front:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p>Unlike for the lining a traditional sequence of sewing pieces together is used here: first I sew together all front pieces and then - vertical curves of the back.</p>
<p><strong>I sew-on the second side piece of the front:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p>And again I trim corners and seam allowances.</p>
<p><strong>I sew together the vertical curves of the back (joining the side and the central pieces of the back):</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p>And again I trim corners and seam allowances. </p>
<p>&nbsp;<strong>Afterwards I press open seam allowances on all pieces.</strong></p>
<p><strong>Back piece:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p>I use an additional sleeve pressing board for more convenience. The garment hangs down the edges of the board and doesn�t get deformed. </p>
<p>I always press seams from both sides: first from the inner side and then from the outer side!</p>
<p>Having pressed open the back pieces I go on to the front half of the corset.</p>
<p>I press open straight curves on the additional pressboard as well and <strong>use cup pressing moulds for pressing open bust curves: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I press open from one side and then from the other: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>After pressing I sew together side seams using a 0.5 inch allowance:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p>I trim corners and seam allowances as before. </p>
<p><strong>I use a pear-shaped pressing mould when pressing open side seams because its bend repeats the shape of a side seam:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Again, I press open from both sides. </p>
<p><strong>I would like to reveal a little trick for you! </strong></p>
<p>If you are aware that you sometimes fail to hold a seam allowance precisely enough, then you must have sewn the lining with sometimes somewhat bigger and sometimes smaller seam allowances. Face fabric should overlay the lining with some tension. In a case like this I recommend you sewing together the side seams of the face piece using a slightly bigger seam allowance than usual (0.5-0.6 inches). </p>
<p>This way you will make the face part smaller and it will be pulled up very well. </p>
<p>All this of course comes with practice and experience. So if you are a beginner working in wedding fashion - start from the basics and then proceed to sophisticated know-how�s. Learn to get a feel for fabric, for bones. </p>
<p>And believe me, very soon you will have a steady hand at it and you will be able to decide on your own when you should use the methods I offer and when not. </p>
<p>In this tutorial I have sewn together the pieces of the face part. </p>
<p><strong>The lengths of all my corset pieces match perfectly! </strong></p>
<p><strong>This means the corset pattern is tried-and-true! </strong></p>

