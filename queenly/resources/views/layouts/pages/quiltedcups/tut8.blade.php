<h3>Tutorial 8. Sewing-on Horizontal Bones. Fusing with Batiste. </h3>
<h4>Sewing-on Horizontal Bones.</h4>
<p></p>
<p>So the vertical bones have all been stitched-on.</p>
<p><strong>And now I need to sew a bone onto the top of the corset.</strong></p>
<p>I take a narrow Rigilene bone and tape its end with some masking tape. </p>
<p><strong>This bone is placed 1mm aside from the stay-stitch and sewn-on beginning at the cup. </strong></p>
<p>This way I overlap the ends of the vertical bones and secure the top of the corset.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Now the top of the corset won�t stretch and I will therewith achieve a perfect line of the top.</p>
<p>Then I make the second parallel stitch along the bone.</p>
<p></p>
<p><strong>I repeat the procedure performing it from the other side of the corset:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And then I sew another bone onto the bottom cut edge. </strong></p>
<p>I gather this bone in advance to make it lie smoother, and tape its end with some masking tape. The matter is that the lower cut edge of the corset is not really straight - there is a bend radius (although not a large one) that depends on the size of the corset. And the smaller the corset is - the more you need to gather this lower bone in order to help it spread evenly and lie as flat as possible over the bottom cut edge of the corset. </p>
<p><strong>I stitch-on this bone exactly the same way leaving 1mm from the stay-stitch:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>The bottom of the corset has been slightly deformed by the sewn-on vertical bones but the horizontal bone that I stitch-on makes up for these defects and restores the bottom of the corset after the original patterns. </p>
<p>There is one more important trick: I try to stretch the fabric a little bit underneath this bone. The point is that the lining should slightly exceed the face in order to keep the face fabric as tight as required.&nbsp; </p>
<p><strong>That is why you should never ease-in any fabric underneath this bone! </strong>Otherwise the fabric on the face part of the corset will be bumpy and uneven because of a lack of tension. </p>
<p>Upon attaching the bone with one seam I always check the length of the back. In case of a slightest mismatch it is better to rip this bone off, even out the length of the back and stitch it back on.</p>
<p>As a next step I make the second parallel stitch.</p>
<p></p>
<p>This is what the corset lining looks like with its bones stitched-on:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Fusing Bones with Batiste. </h4>
<p></p>
<p>I have covered the cups with padding polyester to prevent the bones from sticking through the face fabric. However if I used fine face fabric knowing for sure that the corset would be smooth and have no appliqu�s or draperies - then I would want to ensure that no vertical bones would stick through the face fabric. This is particularly essential for corsets with a tummy-control effect. </p>
<p><strong>I am using a very simple trick - all the bones of the front piece of the corset should be covered with straps of iron-on batiste. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p>I use leftover cuttings for these straps. </p>
<p>When fusing batiste on, you should make sure the seam allowances are not creased underneath it. </p>
<p><strong>The surface should be perfectly smooth. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p>And so this way I have completely finished working on the lining now and fully shaped my corset. </p>

