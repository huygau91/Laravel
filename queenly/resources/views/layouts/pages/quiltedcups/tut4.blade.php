<h3>Tutorial 4.&nbsp; Joining the Lining along Its Curves. </h3>
<h4>Bust Curves.</h4>
<p></p>
<p><strong>I start joining together the lining pieces from the pieces of the front. </strong></p>
<p><strong>I take the central and medium pieces of the lining front. </strong></p>
<p><strong>I start sewing them together, </strong>i.e. start from the bust curves. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>I don�t use any pins but if you find it difficult to work without them - you can pin pieces together. </p>
<p>I keep the upper piece in my left hand and the bottom piece - in my right hand. I link the pieces together and direct them underneath the machine. <strong>The seam allowance is the standard 0.5 inch one. </strong>I match all cut edges, notches, and top and bottom of the garment very carefully. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the corners </strong>of the seam allowances on top and down the bottom: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p>I take the second medium piece of the front and <strong>sew together the second bust curve: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>So, I have sewn 2 bust curves: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p>In order to make opening these curve seams easier I <strong>topstitch them by 1 mm</strong> from both sides of the seam. I spread the seams carefully by hand when sewing: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image012.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image448.jpg"></p>
<p>You should be particularly careful working on the cup area! There should be no creases or tucks!</p>
<p><strong>I check the quality of the seam: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I repeat the same steps for the second bust curve. </p>
<p><strong>You�re supposed to get this result at the end: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trim seam allowances: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p>It is enough to trim seam allowances only at the cup level because hard iron-on material makes further work much more complicated. But the seam allowances going from the bottom of the cups to the corset bottom are soft and do not necessarily have to be trimmed. </p>
<p>There might be some mismatch of the lines that mark the bones between the front pieces due to some inaccuracy of patterns or cutting. This is excusable enough. These marking lines should simply be corrected. </p>
<p>The sewn pieces should be properly pressed afterwards. </p>
<p>I always use a cup pressing mould. </p>
<p><strong>I shape the cup making it look nice and embossed: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p>I press it in a circular motion from the centre of the bust to the edges. I divide the curve and form the desired rounded shape. </p>
<p></p>
<p><strong>After the pressing the cups should look like this: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And then I press open the bottom parts of the curves: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I press from the face side</strong> using the pressing mould again:</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And now I make a stay-stitch along the top and bottom of the sewn piece: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p>Let me remind you something! </p>
<p>Stay-stitch is a machine stitch made at a distance of a seam allowance. </p>
<p>It is important that the stay-stitch is sewn very accurately along the top of the garment. </p>
<p>Hard iron-on fabric often happens to be trimmed insufficiently and goes beyond the boundaries of the stay-stitch, which means it gets into the seam allowance and makes it difficult to work further. </p>
<p>That is why the areas of hard iron-on fabric going beyond the stay-stitch must certainly be trimmed. </p>
<p>I can show you a case like this in the picture: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Back Curves of the Back. </h4>
<p></p>
<p>I put the side piece of the front aside for now. </p>
<p><strong>And now I am going to sew together the back curves of the back. </strong></p>
<p><strong>I sew together the first couple of the side and central pieces of the back: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I put the second pair of pieces under the machine without tearing the thread off: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></p>
<p>I trim the corners of seam allowances. </p>
<p>And just the same way as was done for the bust curves I <strong>topstitch</strong> the seams of the back curves of the corset back <strong>by 1mm</strong> from both sides of the seam to make them easier to open. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Side Cut Edges. </h4>
<p></p>
<p><strong>I take the side pieces of the front and sew them onto the side pieces of the back, i.e. join together the side cut edges of the lining. </strong>Soon you are going to see why exactly I join pieces in this very sequence. </p>
<p><strong>I sew together the side cut edges: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></p>
<p>Looking ahead I should tell you that it is much easier to quilt a cup and work on the front of the corset before attaching it to the side piece. </p>
<p>However it is better to work on the back part after the side seams have already been joined. </p>
<p>I trim the corners of seam allowances. </p>
<p>And again - the same way as was done for the bust and back curves - I <strong>topstitch</strong> the seams of the side cut edges <strong>by 1 mm</strong> from both sides of the seam to make them easier to open. </p>
<p><strong>I have got 2 pieces like this: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a stay-stitch along the back.</strong></p>
<p>I go across 3 sides: top, bottom and the middle of the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p>Let me note one more time that <strong>a stay-stitch should go right from one edge of the garment to the other! You should by no means turn the garment around on the needle! </strong></p>
<p>Stay-stitches should intersect and I am going to be guided by these intersection points in further work. </p>
<p>Then I thoroughly <strong>press all sewn pieces</strong> all over again: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p>I would like you to pay attention to the fact that I do not flatten out the pieces on the pressboard when pressing them but try to preserve their volume. Besides I use some additional pressing moulds, for example a pear-shaped pressing mould. The curve of the pear-shaped pressing mould should be the same as the desired curve of the side seam. </p>
<p>So, during this tutorial I have sewn the lining along its curves. </p>

