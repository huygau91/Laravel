<h3>Tutorial 7. Covering Cups with Padding Polyester. Joining the Lining Along Its Last Curve.</h3>
<p></p>
<h4>Pressing of Corset Pieces.</h4>
<p></p>
<p>First of all you need to press all pieces with bones attached. The thing is that when I take Rigilene bones out of the package they are bow-shaped. I shape them in the desired way with the help of an iron. I use pressing moulds for pressing side seams and corset cups. </p>
<p><strong>In this picture I am pressing the back and the side seam of the corset: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It is necessary to press the fullness areas on the front part of the corset very thoroughly:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the front piece inside out and shape the cups:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p>During this stage the cups should be particularly thoroughly pressed because they are going to acquire their final appearance. </p>
<p><strong>This is what a ready pressed front piece of our corset looks like: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<h4>Covering Cups with Padding Polyester. </h4>
<p></p>
<p>After pressing I need to cover the cups with padding polyester. This is done in order to prevent the numerous bones stitched onto the cups from sticking through the face fabric. Moreover - padding polyester supplements the roundness of the bust. </p>
<p><strong>I pin a strap of polyester onto the cups: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I start sewing padding polyester on along the top part of the corset, along the inner seam attaching the bone. </strong>I also place the ribbons (loops) for hanging the corset there. I sew them on inside the cups instead of doing it at the side seams. After my experience, it makes a corset look better and more advantageous when put on a clothes hanger. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image014.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image579.jpg"></p>
<p>Having stitched-on padding polyester along the top I go on to the side part of the piece. <strong>I sew padding polyester on along the sides and the bottom, along the outer seam attaching the bones of the cup curves. </strong>I try to get exactly into the bone-attaching seam when sewing-on this polyester. Having gone all around the perimeter I return to the point where I started sewing. </p>
<p>I remove pins and trim extra fabric along the whole perimeter close to the seam. </p>
<p><strong>Cups covered with padding polyester look like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Making the Lining Whole. </h4>
<p></p>
<p>Now it�s time to join all the pieces of the lining into a whole piece. </p>
<p><strong>I sew the front piece together with the back piece using a standard 0.5 inch seam: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p>I trim the corners of the seam allowance. And I repeat all the steps of joining the curves. </p>
<p><strong>I topstitch the joining seam from both sides: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I put a Rigilene bone along the curve leaving a 2-3 mm gap from the edges and stitch it on with two seams: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p>The very same procedure is repeated with the second curve.</p>

