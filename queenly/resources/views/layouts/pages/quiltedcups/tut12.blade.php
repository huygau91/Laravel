<h3>Tutorial 12. Pulling Face Fabric onto Lining. Pinning Along the Bottom. Finishing Lacing.</h3>
<p></p>
<p>After careful pressing I should pull up the face part of the corset and secure it along the bottom line. </p>
<p>As you can remember I have made an extra notch under the bust. So now I should pull up as much fabric along the centre of the front as I have used for this extra notch. Had I not made this notch - there would have been a fold formed under the bust (you can see this area in the pictures of corsets). </p>
<p><strong>First of all I make a notch along the middle of the front both on the lining and the face side:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I pull out some face fabric below the lining level by the size of a notch making sure the notches are on the same vertical line: </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the spot with pins: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image005.jpg" class="img-responsive center-block"></p>
<p>Afterwards I add pins all along the bottom of the corset. I keep pulling up the face fabric by hand and matching the curves. The pins here have to pierce right through, going around the horizontal bone sewn along the bottom of the lining. </p>
<p><strong>This is what the corset bottom looks like from its face side after getting pinned together:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image007.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And from the lining side: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image009.jpg" class="img-responsive center-block"></strong></p>
<h4>Finishing Lacing.</h4>
<p></p>
<p>There are 3 wide Rigilene bones attached to the central piece of the back of the lining. </p>
<p><strong>I turn the corset out and make 3 machine seams on each central back piece going along the inner seams that attach bones:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image011.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image686.jpg"></p>
<p>These seams should be very even. </p>
<p>The tunnels formed as a result will be guiding lines for plastic bones that I am going to insert. These plastic bones will protect the back plackets of the lacing from getting deformed. The thing is that lacing plackets go through some serious pressure if a corset is supposed to have some tummy-control effect, and therefore start losing their shape. </p>
<p>I am going to punch grommets onto the central one of the three bones - this way they will be well secured and won�t pop out. I always place an additional Rigilene bone under the grommets. </p>
<p><strong>Having quilted the tunnels I make a machine stitch along the bottom of the corset as well:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image013.jpg" class="img-responsive center-block"></p>
<p>I step away from the edge of the garment by a lacing width and start sewing. I leave the tunnels open to insert plastic bones there.</p>
<p><strong>The presser foot should slide along the lower horizontal bone of the corset when I�m sewing, i.e. at a presser foot distance from the bone: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2012_files/image015.jpg" class="img-responsive center-block"></p>
<p>I finish this stitch the same way without reaching the lacing.</p>
<p>During this tutorial I have pulled the face fabric onto the lining and finished working on the lacing area. </p>

