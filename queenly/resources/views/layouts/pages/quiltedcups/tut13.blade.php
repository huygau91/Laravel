<h3>Tutorial 13.&nbsp; Finishing the Bottom of the Corset.</h3>
<p></p>
<p><strong>I remove pins and trim the bottom of the corset:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p>Then I even out the bottom line of the corset. </p>
<p>Afterwards I insert plastic bones into the tunnels of the lacing. </p>
<p><strong>First of all I need to measure the length of the bone which should be about 0.8 inches shorter than the length of the finished back:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p>I trim the other 3 bones on both sides of the corset down to the same length. </p>
<p>I slightly round off the ends of the bones and insert them into the tunnels.</p>
<p></p>
<p><strong>Now the tunnels look nice and dimensional:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></p>
<p>Besides the lacing placket has become very firm and doesn�t bend even after some heavy pressing of the corset. </p>
<p>I insert the bones into the tunnels on the other side of the corset.</p>
<p><strong>With the help of chalk I mark the length of the back in its final appearance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p>This length should be identical from both sides. I will sew-on a ribbon (welt) here to fold over the bottom cut edge. </p>
<p>The bottom cut edge of the corset can be finished with a bias tape the same way as was done on the previous corset. However if you haven�t managed to find a bias tape of required color or size, you can do the following: </p>
<p>I have torn off a 2.8 inch satin strap crosswise, bent it in half and pressed it. </p>
<p><strong>Now I should sew this strap or ribbon onto the bottom of the corset at a presser foot distance from the edge:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></p>
<p>I slightly pull up the ribbon when sewing. Then I trim it leaving a 0.5 inch end. </p>
<p><strong>I turn the ribbon out:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I make sure there is a nice piped-like edge along the bottom:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image014.jpg" class="img-responsive center-block"></p>
<p>I secure the ribbon with pins, press it and sew it on along the bottom with an invisible stitch. </p>
<p><strong>This is what the bottom of the corset looks like:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2013_files/image016.jpg" class="img-responsive center-block"></p>
<p>As a result of this tutorial I have finished the corset bottom.</p>

