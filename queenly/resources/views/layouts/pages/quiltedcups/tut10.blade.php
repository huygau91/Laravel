<h3>Tutorial 10. Joining the Face and the Lining.</h3>
<p></p>
<p><strong>I start joining the lining and face of my corset. </strong></p>
<p>I put the main fabric face down onto the face side of the lining.</p>
<p><strong>I start joining the bust curves of the lining and the face:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>However I don�t join the curves seam-to-seam but shift the bust curve by around 2mm towards the side: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>I have eased-in along the top of the corset on the lining. </p>
<p><strong>It is important for me to prevent the resulted fullness from passing onto the central part of the face fabric. </strong></p>
<p>I am going to re-distribute this fullness on the rounded areas of the top (along the sides of the neckline and the underarms). </p>
<p>&nbsp;</p>
<p><strong>I pin the pieces together atop: first I pin at the curve points, then divide the in-between distance in half and pin again, then divide in half once more - and so on. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>This way I re-distribute the face fabric evenly along the centre. I add pins in a gliding motion catching the lining fabric underneath the bone. </p>
<p><strong>I join the pieces at the level of the back evening out their cut edges:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I pin together the side seams and carefully arrange the face fabric going from the side seam to the back the same way as was done for the central piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p>As you can see in the picture I have left some extra face fabric on the rounded area on top hoping that the cut edges have been cut bias and it will allow me to re-distribute this fabric. </p>
<p><strong>I re-distribute the fabric in the last "problem area" the same way: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p>I use quite many pins when joining the top and the lining.</p>
<p>The same steps are repeated on the second side of the corset. <strong>Both sides should be pinned together absolutely symmetrically! </strong></p>
<p><strong>Then I join the lining and the face along the back in the lacing area, matching notches. </strong>I pin them together the same way as the corset top: first I pin along the edges and the waistline notch, then divide each in-between distance in half, then again in half, etc. Pins should not pierce through the lining! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p>Then I do the same with the second side of the corset.</p>
<p><strong>And afterwards I start joining the face and lining with the sewing machine. </strong></p>
<p><strong>I start from the cut edge of the back:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p>For more convenience I have taken a one-sided presser foot with the ski-shaped part on the right. The foot moves along the bone as if it were a guiding line and the needle gets right into the stay-stitch. <strong>It is for this purpose that I stepped 1mm away from the stay-stitch when attaching the bone.</strong></p>
<p><strong>And then I join the corset pieces along the top:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Note: </strong>never turn a corset around on the needle! Every seam should be made from beginning to end and there should be bar tacks at the points of intercrossing with the stay-stitch! Bar tacks should remain where they are after seam allowances have been trimmed!</p>
<p>The pace of the sewing machine can be slowed down<strong> when working on fancy cut edges. </strong>You should always be guided by the location of the stay-stitch.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p>I made this stay-stitch at the very beginning of the working process when the corset pattern was not yet deformed by further treatment. </p>
<p>Therefore the stay-stitch marks the original shape and size of the pattern. </p>
<p>I make a bar tack at the intersection of the seam and the stay-stitch.</p>
<p><strong>I sew the last seam along the second cut edge of the back:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p>As a result of this tutorial I have joined the face with the lining along the top and back of the corset.</p>

