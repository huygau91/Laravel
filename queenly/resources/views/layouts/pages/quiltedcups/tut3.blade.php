<h3>Tutorial 3.&nbsp; Fusing the Pieces of the Lining. </h3>
<p></p>
<p><strong>In this tutorial I am going to fuse the lining pieces. </strong></p>
<p>I put <strong>the medium piece of the corset front</strong> onto the pressboard and unpin it: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p>I take off <strong>the lining pieces</strong> cut out of batiste and main lining fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The fused pieces of the front crepe part</strong> remain on the pressboard: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I place <strong>the batiste</strong> carefully over the crepe pieces <strong>its adhesive layer up:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p>I put<strong> 2 crepe-satin pieces of the lining </strong>on top<strong> and even out cut edges very carefully</strong>: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>And finally I put <strong>the second batiste piece </strong>on top of it<strong> with the adhesive layer down</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I <strong>fasten the iron-on material</strong> by stamping it carefully with the iron: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p>Then I remove the face pieces and <strong>finish pressing the lining pieces: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p>No matter how accurately I arrange the fabric,<strong> it still goes beyond the iron-on material by some 1-2 mm</strong> because the fabric does get deformed during the pressing process and the fusing of batiste: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p>So I still <strong>need to shape these areas:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>This procedure demonstrates how hard it is to fuse pieces that have been individually cut out of iron-on material. That is why I highly recommend you fusing the fabric beforehand and only afterwards going on to the cutting. </p>
<p>However there are situations when fusing all fabric in advance is impossible which is why I have shown you what to do in a case like this. </p>
<p>Then and there I have explained and demonstrated you <strong>the principle of fusing a corset.</strong> </p>
<p>It is a �sandwich" principle. I place the pieces that need fusing onto the pieces that match the pattern best. </p>
<p>I repeat the same with the rest of the cut-out corset pieces.</p>
<p><strong>The side pieces of the front: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I reach <strong>the central piece of the front. </strong></p>
<p>At first I go through the same steps that were made for other corset pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>But I have cut out a separate piece of hard iron-on fabric for some extra supporting effect in the cup area. </p>
<p>And I do the following: </p>
<p>I put <strong>a cup piece made of the hard iron-on material</strong> over the lining piece with its adhesive layer down: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I press it a little:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image028.jpg" class="img-responsive center-block"></p>
<p>Next I <strong>shape the fused pieces: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the hard iron-on material along the seam allowance line: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And I <strong>press</strong> the hard iron-on material one more time through: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image034.jpg" class="img-responsive center-block"></p>
<p>This is what the central piece of the lining of the front looks like when <strong>the cup piece of the iron-on material is fused onto it</strong>: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p>And again I take 2 medium front pieces of the lining and place <strong>the cup supporting pieces</strong> of hard iron-on material on top of them with their adhesive layer down: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I press the hard iron-on material through: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I shape the pieces:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I restore the notches</strong> that have been covered with the hard iron-on material: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And I repeat the same steps for <strong>the pieces of the side part of the back: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>I fuse <strong>the lining pieces of the central part of the back: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Marking the Location of Bones.</h4>
<p></p>
<p>I have finished fusing all lining pieces and fused some hard iron-on fabric onto the cups. </p>
<p><strong>Now I start marking the location of bones. </strong></p>
<h5>Central Piece of the Front. </h5>
<p></p>
<p>I start with <strong>the lining piece of the central part of the front.</strong> </p>
<p>I make <strong>preparatory markings on the fabric</strong> (blue pecked lines): </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image050.jpg" class="img-responsive center-block"></p>
<p>Here is the sequence of the working process: </p>
<p>1. I join the notches of the bust line using a ruler (<strong>line A-B</strong>). </p>
<p>2. I find the middle of the A-B section (<strong>point A1</strong>). </p>
<p>3. I join the notches of the waistline using a ruler (<strong>line C-D</strong>). </p>
<p>4. I find the middle of the C-D section (<strong>point C1</strong>).</p>
<p>5. I draw a line connecting <strong>points A1 and C1</strong>. </p>
<p>6. I make <strong>2 parallel lines G-H and J-K</strong> at a distance of <strong>0.8 inches</strong> from each side of the A1-C1 line.</p>
<p>7. I mark <strong>points E1 and E2</strong> at <strong>a 0.8 gap</strong> from the edge of the piece. </p>
<p>8. I draw <strong>2 parallel lines N-P and L-M</strong> stepping <strong>1.4-1.6 inches down </strong>and<strong> 1.2 inches up</strong> the A-B line. </p>
<p>9. The A1-C1 line divides the neckline curve in half. I divide each half of the curve one more time marking<strong> points F1 and F2</strong>. </p>
<p>10. I join <strong>points E1 and E2</strong> along the curve repeating the bottom edge of the hard iron-on fabric. </p>
<p>The marking is finished.</p>
<p><strong>Please take a look at how I�m going to sew-on bones according to my markings: </strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image052.jpg" class="img-responsive center-block"></p>
<p>There are going to be <strong>10 bones in total</strong> sewn onto the front piece (their numbers are written in red). </p>
<p><strong>Bone 1:</strong> it goes along the A1-C1 line. This is a central vertical bone. </p>
<p><strong>Bone 2:</strong> it goes along the A-B bust-line. </p>
<p><strong>This is a central horizontal bone or a chest bone. It goes along the bust-line of the left medium piece of the front, continues along the bust-line of the central piece of the front and then along the bust-line of the right medium piece of the front. </strong></p>
<p><strong>Bones 3 and 4: </strong>they go along the G-H and J-K lines.</p>
<p>These are additional vertical bones for supporting the central piece of the front. However the central piece of the front of my corset is rather narrow. And that is why <strong>I have not decided yet whether or not some additional bones, parallel to the central bone, are required. </strong>I mark the location of these bones just in case and will make the final decision later during the sewing process. </p>
<p><strong>Bones 5 and 6:</strong> they are sewn between the points E1-F1 and E2-F2. These are inner bones of the cup. </p>
<p><strong>Bone 7:</strong> it goes along the N-P line parallel to the central chest bone. </p>
<p>Same as the central chest bone <strong>this bone goes along the bust-line of the left medium piece of the front, along the bust-line of the central piece of the front and along the bust-line of the right medium piece of the front.</strong></p>
<p><strong>Bone 8: </strong>it goes along the L-M line. </p>
<p><strong>It starts at the central piece of the front and goes over to the left medium piece of the front. </strong></p>
<p><strong>Bone 9: </strong>it goes along the L-M line. </p>
<p><strong>It starts at the central piece of the front and goes over to the right medium piece of the front. </strong></p>
<p><strong>Bone 10: it goes along the bottom (base line) of the cup that I�m going to quilt and goes over to the left and right medium pieces of the front. </strong></p>
<p>The inner bones of the cup create and maintain its shape making it look round and embossed. </p>
<h5>Medium Pieces of the Front.</h5>
<p></p>
<p><strong>I go through the same steps for the medium pieces of the front. </strong></p>
<p>I make some <strong>preparatory marking on the fabric</strong> (blue pecked lines):<br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image054.jpg" class="img-responsive center-block"></p>
<p>Here is the sequence of the working process:</p>
<p>1. I join the notches of the bust-line using a ruler (<strong>line A-A2</strong>). </p>
<p>2. <strong>The A-A2 Line </strong>is an extension of <strong>the A-B line</strong> on the central piece of the front. </p>
<p>3. <strong>Point A</strong> is the general point of all these pieces. </p>
<p>4. I mark <strong>point E1</strong> leaving <strong>a 0.8 inch gap</strong> from the edge of the piece. </p>
<p>5. <strong>This point</strong> should lie at the same level with <strong>the E1 point</strong> on the central piece of the front.</p>
<p>6. Now I need to decide where will be the border or the base line of the cup I�m going to quilt. <strong>I need to determine the intersection of the cup base line and the bust-line, i.e. determine point R. </strong></p>
<p><strong>The base line of the round cup begins about 2-2.4 inches <em>off the side of the corset</em>. </strong></p>
<p>I place a corresponding side piece to this medium piece of the front: </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image056.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image420.jpg"></p>
<p>If you don�t take the seam allowances of the side piece of the front into account, you are going to have about 0.8-1.2 inches left at the bust-line level of this piece. </p>
<p><strong>Therefore I need to mark the remaining 0.8-1.2 inches off the seam allowance of the medium piece of the front in order to determine the intersection of the cup base line and the bust-line (point R).</strong></p>
<p>Let me draw the location of the base line of the cup. It is going to be <strong>a curve going through points R and E1.</strong></p>
<p>It is very easy to mark the base line of the cup (the curve): I just place my fist onto the bust-line, mark the radius with a pencil and draw a curve connecting <strong>points R and E1</strong> by moving my hand:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>The curve connecting points R and E1 is an extension of the curve connecting points E1 and E2 on the central piece of the front. </strong></p>
<p>7. Now I find the culmination point of the hard iron-on material (<strong>point F3</strong>).</p>
<p>8. I draw a line connecting <strong>points E1 and F3.</strong> </p>
<p>9. I mark <strong>points N and L</strong> stepping <strong>1.4-1.6 inches down </strong>and<strong> 1.2 inches up the A-A2 line.</strong> I used exactly the same values for the A-B line on the central piece of the front. </p>
<p>10. I draw a line N-N1 - not parallel to line A-A2 but at a slight downward angle. </p>
<p><strong>Line N-N1 </strong>is an extension of <strong>line N-P</strong> on the central piece of the front.</p>
<p><strong>Point N </strong>is the general point of these pieces. </p>
<p>11. I draw a line L-L1 - also not parallel to the A-A2 line but at a slight upward angle. </p>
<p><strong>Line L-L1</strong> is an extension of<strong> line L-M </strong>on the central piece of the front. </p>
<p><strong>Point L </strong>is a general point of these pieces. </p>
<p>12. I draw a <strong>line C2-C3</strong> dividing the bottom part of the medium front piece in about a half. </p>
<p>The marking is finished.</p>
<p><strong>Please take a look at how I am going to sew bones onto the left medium piece of the front: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Bone 2:</strong> it goes along the A-A2 bust-line. </p>
<p><strong>This is a central horizontal or a chest bone. It goes along the bust-line of the left medium piece of the front, continues along the bust-line of the central piece of the front and then along the bust-line of the right medium piece of the front.</strong></p>
<p><strong>Bone 7:</strong> it goes along line N-N1 at a slight angle downwards to the central chest bone. </p>
<p>Same as the central chest bone this bone goes <strong>this bone goes along the left medium piece of the front, along the central piece of the front and along the right medium piece of the front.</strong></p>
<p><strong>Bone 8: </strong>it goes along line L-L1. </p>
<p><strong>It begins at the central piece of the front and goes over to the left medium piece of the front. </strong></p>
<p><strong>Bone 10:</strong>&nbsp; <strong>it goes along the bottom (base line) of the cup which I am going to quilt and then goes over to the central part of the front and the right medium piece of the front. </strong></p>
<p><strong>Bone 11: </strong>it is an inner bone going along lines E1 and F3. </p>
<p><strong>Bone 12: </strong>it is a vertical bone going along line C2-C3.</p>
<p>This is what the marking for quilting rounded cups with bones looks like.</p>
<p><strong>Please note the way quilted bones will pass from the central part of the front onto the left side part of the front: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image062.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I repeat the same steps for the right half of the medium piece of the corset front. </strong></p>
<p>I think there is no need for detailed explanations this time. </p>
<p>All the marking is done in a mirrored way: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>Bones 2, 7, 9 and 10 </strong>pass onto the central piece of the front. </p>
<p><strong>Bones 13 and 14 </strong>remain only on this piece. </p>
<p></p>
<h5>Side Pieces of the Front. </h5>
<p></p>
<p><strong>I mark the location of bones on the side pieces of the front.</strong></p>
<p>I draw <strong>a line on both pieces going along the middle of the waistline lengthwise to the whole piece</strong> - almost parallel to the cut edge line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image066.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image430.jpg"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image068.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image432.jpg"></p>
<p></p>
<h5>Side Pieces of the Back. </h5>
<p></p>
<p><strong>I mark the location of bones on the side pieces of the back.</strong></p>
<p>My corset is rather small-sized which is why I am going to put one vertical bone onto each piece about in the middle of it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image070.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%202/Tutorial%203_files/image072.jpg" class="img-responsive center-block"></p>
<p>If the corset were large-sized I would have divided the width of the piece into 3 sections and put 2 vertical bones onto each section. </p>
<p></p>
<h5>Central Pieces of the Back. </h5>
<p></p>
<p>There is <strong>no need for additional bones</strong> on the central pieces of the back because there will be some bones for the lacing. </p>

