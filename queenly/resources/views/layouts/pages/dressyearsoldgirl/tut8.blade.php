<h1>Tutorial 8. Finishing the Lacing Area. Doing a Fitting. </h1>
<p></p>
<p><strong>And now it's time to join the bodice with the skirt. </strong>&nbsp;</p>
<p>I start by <strong>making a row of joining stitches along the bottom edge of the bodice </strong>to secure the waistband and simultaneously join all layers.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I measure a piece of narrow Rigilene boning for the lacing area. </strong>It should stop about 2cm above the bottom edge.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I need two bones of the same length: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>I tape their ends over with masking tape. </p>
<p><strong>And I start sewing them on. </strong>&nbsp;I place the edge of the bone right at the fold line of the seam allowance.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I secure the bone with two rows of parallel stitches so I can neatly fold the seam allowance over it.&nbsp; </p>
<p><strong>And I add a symmetric bone along the opposite lacing edge. </strong>I remove all excess threads straight away to make further work easier.&nbsp; </p>
<p><strong>Then, I take the skirt and align the zipper area with the fold line of the seam allowance of the bodice.</strong> The fold line of the lacing edge and the zipper must be aligned perfectly! I secure this area with pins...&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>...and carefully distribute the fabric of the skirt and the bodice.&nbsp; </p>
<p>I align the centre points of the skirt and the bodice and pin them together. </p>
<p>And I continue adding pins moving in the opposite directions from the centre. </p>
<p><strong>I make a row of joining stitches. </strong>I sew over the lining of the skirt. I simply follow the row of stay-stitches on the lining.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I fold the seam allowance of the bodice to the inside and make a row of strengthening stitches from the top down to the bottom of the lacing edge. </strong>I stitch at a presser-foot width it. I pass onto the skirt just a little to secure the seam allowance and the end of the zipper tape. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The second row of parallel stitches is made about 1cm from the first one. </strong>This time I don't pass onto the skirt but stop right at the end of the bodice. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And then I do the same with the opposite lacing edge.&nbsp; </p>
<p></p>
<p><strong>Here is the result of this work: </strong>the bodice is joined with the skirt and there is a smooth transition from the lacing area to the zipper. All pieces are aligned perfectly.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I mark the location of grommets.</strong> I step 1cm down from the top edge and up from the bottom edge and divide the in-between area in equal sections. There will be 2.5cm intervals between my grommets. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I use a special hand press tool to punch holes and set grommets. I punch holes along the first lacing edge: &nbsp;&nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>And then I put one lacing edge over the other </strong>and mark points on the second one through the holes made in the first one. This way the holes will be perfectly symmetric. I punch holes along the other lacing edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p>I swap to a grommet setting die. I use two-piece grommets with washers. </p>
<p></p>
<p><strong>I install grommets in the holes punched along the lacing edges.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>The boning in the lacing area will prevent the dress from wrinkling when I put it on the child and lace it.&nbsp; At the same time, it is soft and will not irritate the child. </p>
<p>My advice to all professional corset-makers: do invest some money in a professional hand press tool. Such tools often come with special dies for making fabric-covered buttons or setting snaps. They are truly multi-functional and very handy.</p>
<p></p>
<p><strong>The lacing area at the back of the bodice looks like this. Time for a fitting! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p>Sophia-Lielle was in a bad mood when she came to the fitting. We couldn't cheer her up no matter how we tried: she always was on the verge of bursting into tears. I laced the dress as quickly as I could. <strong>The main task was to check whether it fit the girl properly. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%208_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>And it does fit perfectly as you can see! </strong>Everything is correct: the width of the waistband, the armscyes, the neck hole, the length of the skirt, etc. These are the most important things! I didn't want to torture the child so the fitting did not take long. </p>

