<h1>Tutorial 3. Cutting the Main Dress, the Skirt, and the Ruffles.</h1>
<p><strong>I start cutting a dress for Sophia-Lielle.&nbsp; </strong></p>
<p>I cut out the paper patterns which were drafted in correspondence with the child's measurements.&nbsp; <strong>Seam allowances are not included in these patterns. </strong></p>
<p>First of all, let me determine how much iron-on fabric I need. I measure the width and the length of the lay, add seam allowances, and thereby find the amount of iron-on fabric required for duplicating the bodice.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image004.jpg" alt=""></p>
<p><strong>The dress will be sewn from natural silk-like fabric and crystal organza.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image006.jpg" alt=""></p>
<p><strong>I have prepared about 30cm of iron-on batiste: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image008.jpg" alt=""></p>
<p>My batiste has a width of 90cm and all bodice pieces fit onto it perfectly.</p>
<p><strong>I start by preparing the main fabric:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image010.jpg" alt=""></p>
<p><strong>I need to press it and duplicate it with iron-on batiste: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image012.jpg" alt=""></p>
<p>Although there will be no bones sewn in the dress because it is made for a little girl, I still highly recommend you duplicate the entire bodice with thin iron-on fabric to help the dress hold its shape and protect it from wrinkling. It will also help you secure all decorative elements more properly. Please note that <strong>I duplicate the fabric <em>before</em> cutting it.</strong> It is a lot easier and faster and provides more accurate results.</p>
<p><strong>I mark a width equal to the total width of all pieces and cut the required amount of fabric: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image014.jpg" alt=""></p>
<p><strong>The face fabric has been duplicated and the lining stays as it was: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image016.jpg" alt=""></p>
<p><strong>I fold the fabrics in half, align their fold lines, and pin them together:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image018.jpg" alt=""></p>
<p><strong>Then, I arrange the paper patterns on the lay: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image020.jpg" alt=""></p>
<p>I prefer to do this on the ironing board because its soft surface allows me to secure the patterns in place by sticking pins in vertically rather than sliding them in horizontally.</p>
<p><strong>I mark all seam allowances: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image022.jpg" alt=""></p>
<p><strong>I simply add another 1.2cm along the perimeter of the piece:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image024.jpg" alt=""></p>
<p>You can add as much as you like: if you prefer working with 1cm seam allowances, then add 1cm.</p>
<p><strong>Now I can take out the pins and remove the paper: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image026.jpg" alt=""></p>
<p><strong>I transfer the back pieces on fabric just the same way, adding 1.2cm seam allowances along the perimeter and 2cm along the central open edges.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image028.jpg" alt=""></strong></p>
<p><strong>Now I can pin all the layers together: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image030.jpg" alt=""></p>
<p><strong>I raise the waistline </strong>in accordance with the girl's calculated proportions, add a seam allowance, and <strong>cut out the pieces.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image032.jpg" alt=""></p>
<p><strong>All parts of the bodice are now cut out:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image034.jpg" alt=""></p>
<p>Next, I take the main fabric again (it serves both as the face and the lining) and<strong> prepare to cut the skirt pieces by pressing it lightly.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image036.jpg" alt=""></p>
<p>I cut a single skirt layer, i.e. the lining. I take a piece of fabric with the required length and fold it in half.</p>
<p><strong>I double-check the total width of the pieces with a seam allowance included...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image038.jpg" alt=""></p>
<p><strong>...and mark it along the fold line of the fabric: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image042.jpg" alt=""></p>
<p><strong>I secure the fabric on the ironing board and lay down the paper patterns: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image044.jpg" alt=""></strong></p>
<p><strong>I cut them out adding all seam allowances. </strong>I do it by eye but you can always mark your seam allowances if you don't feel confident enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image048.jpg" alt=""></p>
<p>Then I take my organza fabric and <strong>cut the skirt pieces from it </strong>using the already cut out lining pieces as templates. The organza needs to be prepared the same way as the main fabric: press it a little bit, find the fold line, and lay down the paper patterns. <strong>I make the organza skirt 1cm longer.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image050.jpg" alt=""></p>
<p><strong>Next, I need to cut ruffles from the same organza fabric.</strong></p>
<p>Let us do all necessary calculations first. The skirt is about 40cm wide down the bottom. I want to add five ruffles at the front and five at the back, i.e. a total of ten ruffles.</p>
<p>In this case, <strong>each ruffle is supposed to have the following width: 40cm / 5 = 8cm.</strong></p>
<p><strong>The length of the ruffle equals the length of the skirt - 30 cm. </strong></p>
<p><strong>The ruffles will be cut as swirls with an inner radius of 5cm</strong> calculated based on the formula (2πR = 30cm).</p>
<p>Let us find the <strong>outer radius</strong> of the swirl: <strong>5cm + 8cm = 13cm.</strong></p>
<p><strong>Based on these calculations, I find the fabric in half lengthwise. The resulting piece has a width of 28cm (I have added a couple of centimeters just in case) and a length of 150cm.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image052.jpg" alt=""></p>
<p>I carefully press the fold line.</p>
<p><strong>And then I fold the piece of fabric four times so as to form five rectangles (almost squares) with a side length of 30cm:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image054.jpg" alt=""></p>
<p><strong>As the result, there are 10 layers of fabric, 28cm by 30cm each.</strong></p>
<p>I have decided to cut two more additional ruffles just in case. In other words, I have prepared a total of twelve. The ruffles will be sewn on the skirt at an angle and it is hard to predict how they will settle upon it so I prefer to have some in stock.</p>
<p><strong>I find the middle of the first rectangle:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image058.jpg" alt=""></p>
<p><strong>The inner radius should be 5cm. I draw a circle with a 10cm diameter in the middle of the piece:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image060.jpg" alt=""></p>
<p>I also know that the width of the ready ruffle is supposed to be 8cm. I add another centimeter for a serging seam. <strong>I pin the layers together in the middle and mark 9cm from the inner radius all along its perimeter.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image062.jpg" alt=""></p>
<p><strong>Here is the resulting circle:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image064.jpg" alt=""></p>
<p>Last but not least, <strong>I draw a line along the diagonal stretch grain to turn the circle into a swirl.</strong> This way the ruffle will be a little wavy at the end and look very pretty.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image066.jpg" alt=""></p>
<p><strong>I cut out all twelve ruffles: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%203_files/image068.jpg" alt=""></strong></p>
<p><strong>The ruffles are a little longer than the skirt: </strong>first of all, because they will be sewn on diagonally and also because I will need to gather them a little to make them "curlier."</p>
