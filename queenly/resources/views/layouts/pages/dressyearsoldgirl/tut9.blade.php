<h1>Tutorial 9. Sewing the Bow in Place.&nbsp; Adding a Decorative Flower. </h1>
<h3>Pictures of the Finished Garment.</h3>
<p><strong>All I need to do after the fitting is decorate the dress.&nbsp; </strong></p>
<p><strong>The bow will be placed right in the middle of the waistband. I form a small fold in the middle of the waistband </strong>to make it look neater and secure it with a couple of hand stitches.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put the bow right in the middle and sew it on by hand: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p>I make a generous amount of stitches to make sure the bow will hold because it will definitely attract the child's attention more than any other element of the dress. I am sure Sophia-Lielle will keep touching the bow so it needs to be secured as well as possible.&nbsp; </p>
<p><strong>And now I will decorate the dress with a flower. </strong></p>
<p>I bought a ready fabric flower and tinted it a little with a regular powder blush. You can do the same with wax pastels. Then I tied ribbons of mixed colours to the clasp at the back of the flower and tinted the leaves with fabric paint.&nbsp; </p>
<p><strong>I put the flower just above the bow: </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>If you find a clasp or a safety pin unsafe for a child, you can remove it and sew the flower on by hand.&nbsp; As for me, I believe the clasp at the back of the flower is perfectly safe. I discussed it with Sophia-Lielle's mum and she was okay with it.&nbsp; </p>
<p>By the way, it was the flower that impressed the girl the most in the end.&nbsp; She loved the bow and the flower so much that she said it was a true fairy's dress now.&nbsp; She thanked me sincerely! </p>
<p></p>
<p><strong>Here is our lovely delicate dress for 3.5 year old Sophia-Lielle. </strong>&nbsp;She wore it at home for the final pictures and was really content. What could be more rewarding than a child's happy smile!&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%209_files/image007.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Drafting the Base of a Dress for a 3 Year Old Girl (Based on Calculations).</p>
<p>Key Measurements. </p>
<p></p>
<p> </p>
<p>Height - 100cm</p>
<p>Bust Width Front - 29cm</p>
<p>BC - 53cm</p>
<p>WC - 51cm</p>
<p>SC (6) - 53cm</p>
<p>HC (13) - 62cm</p>
<p>Waist to Floor - 61cm</p>
<p>Side Length - 10.5cm</p>
<p>Bust Apex to Bust Apex - 11cm</p>
<p>Clip - 6-5-4cm</p>
<p>Armpit to Armpit Front - 21cm</p>
<p>Armpit to Armpit Back - 21cm</p>
<p>Shoulder to Shoulder Across Front - 25cm</p>
<p>Shoulder to Shoulder Across Back - 27cm</p>
<p>Full Length Front - 24cm</p>
<p>Full Length Back - 24cm</p>
<p>Bust Line to Waistline - 11cm </p>
<p>Centre Length Back - 22cm</p>
<p>Centre Length Front (from sternal notch) - 21.5cm</p>
<p>Armscye Circumference - 28cm</p>
<p>Upper Arm Circumference - 18cm </p>
<p>Stomach Width Front - 28cm</p>
<p>Waist Width Front - 28cm</p>
<p>Shoulder Length - 7cm (5cm on the dress)</p>
<p>Shoulder Slope Front - 25.5cm</p>
<p>Shoulder Slope Back - 24cm</p>
<p> </p>
<p>Drawing a Grid.</p>
<p></p>
<p>Draw a straight angle with a vertex A. </p>
<p>AW = Full Length Back = 24cm. </p>
<p>WB = Side Length = 10.5cm.</p>
<p>WH = 13cm (the hip circumference was measured at this distance from the waistline). </p>
<p>Draw horizontal lines to the right. </p>
<p>When drawing line AA1, remember to add 4-5cm for the flare of the skirt. Here is how I calculate the length of this line: HC/2 + 1 (allowance for freedom of movement) + 5 (flare) = 62/2+1+5=37cm. </p>
<p>Draw a perpendicular line down from A1.&nbsp; </p>
<p>The points of intersection with the horizontal lines are B1, W1, and H1 correspondingly. </p>
<p></p>
<p>Drafting the Front of the Dress (From Top to Hip Line). </p>
<p></p>
<p>B1K1 = Bust Width Front/2 + 0.5 (half allowance for freedom of movement) = 29/2+0.5=15cm. </p>
<p>Draw a perpendicular line down from K1. </p>
<p>K1P = 1/2 Armscye Width. </p>
<p>Armscye Width = 1/4 Half Bust Circumference + 1/6 allowance for freedom of movement = (53/8) + (1/6*1) = 6.6 + 0.2 = 6.8cm.&nbsp; </p>
<p>K1P = 7/2 = 3.5cm. </p>
<p>Draw a 1-1.2cm bisector line up from point K1. This is the area where the armscye starts to curve. Examine the curve − it should be smooth and even. </p>
<p>W1N = Waist to Sternal Notch = 21.5cm. </p>
<p>A1A2 = Shoulder to Shoulder Across Front/2 + 0.5 = 25/2 + 0.5 = 13cm. Since Sophia Liel's shoulder to shoulder measurement is the same at the back as it is at the front, I mark an identical line from point A - AA4. </p>
<p>A2A3 = Shoulder Length = 7cm. You can make it 5cm straight away because that is the length according to the dress style. </p>
<p>Draw a smooth neckline. </p>
<p>Now let us check the Shoulder Slope Front measurement and double-check the position of point A2. Check if it corresponds with the Armpit to Armpit Front measurement. Draw the armscye. </p>
<p>Calculate the depth of waistline darts (if relevant). </p>
<p>This is how I calculate it: BC - WC = 53 - 51 = 2cm. </p>
<p>(BC - WC) / 4 = 0.5cm - this is the depth of the side dart. There will be no darts in the middle of the front or the back.</p>
<p>Draw a perpendicular line down from K1. Mark point W3 0.5cm right of the intersection with the waistline (this is the side dart). </p>
<p>Calculate the difference between BC and WC: </p>
<p>62 - 53 = 9cm. </p>
<p>9 / 4 = 2.25cm − this is the position of the side seam line along the hip line. </p>
<p>Measure 2.25cm left of the intersection of the perpendicular line and line HH1. Mark point H3. </p>
<p>Double-check the position of the side seam line against the stomach line (marked 6cm below the waistline in our case). </p>
<p>Sophia-Lielle's BC = SC (6). </p>
<p>We have drafted the front of the dress down to the hip level. </p>
<p></p>
<p>Drafting the Back of the Dress (From Top to Hip Line).</p>
<p></p>
<p>BK = [(BC + 0.5 (allowance for freedom of movement)) - 2*B1K1]/2 = (53 + 0.5 - 2*15)/2 = 11.8cm. </p>
<p>Measure the length of line K1P (3.5cm) left of point K. (This is a half of the armscye width).&nbsp; Mark point C. </p>
<p>Draw a bisector of the angle and mark 1cm along it. </p>
<p>Measure 1/2 Shoulder to Shoulder Across Back right of point A = 13.5cm. Mark point A4. </p>
<p>A4A5 = Shoulder Length = 5cm (according to the style). </p>
<p>Mark the Centre Length Back measurement (22cm) up from point W. Mark point D1. Draw a smooth neckline. </p>
<p>Draw a perpendicular line down from point K. Measure 0.5cm left of the intersection with the waistline (the side dart). Mark point W2.</p>
<p>Measure 2.25cm right of the intersection with the hip line and mark point H2. </p>
<p>Check the Shoulder Slope Back = 24cm. Mark point A6. Mark the Shoulder Length (5cm) from point A5. Connect the armscye points. </p>
<p>We have drafted the back of the dress down to the hip level.</p>
<p></p>
<p>Drafting the Skirt (After Dividing the Dress in Well-Proportioned Parts </p>
<p>With Regard to the Child's Body Proportions). </p>
<p></p>
<p>Waist to Floor = 32.5cm from the double-checked waistline level. </p>
<p>Calculate Bottom Skirt Width following the golden ratio rule. </p>
<p>Suppose a = Skirt Width and b = Shoulder to Shoulder Across Front = 25cm. </p>
<p>A = 1.62, b = 1.62*25 = 40.5cm. </p>
<p>Bottom Skirt Width = 40.5cm. </p>
<p>WD1 (waist to bottom) = 30cm (2.5cm added for the hanging ruffles).&nbsp; </p>
<p>WD = 31cm (1cm for the prominence of the buttocks). </p>
<p>BK = 11.8cm; B1K1 = 15cm. </p>
<p>15 - 11.8 = 3.2cm.</p>
<p>bb2 = x </p>
<p>b1b3 = y</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%209_files/image009.png" class="img-responsive center-block"></p>
<p>y = 3.2 + x </p>
<p>x + 3.2 + x = 40.5</p>
