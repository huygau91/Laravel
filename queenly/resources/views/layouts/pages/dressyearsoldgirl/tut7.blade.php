<h1>Tutorial 7. Sewing a Waistband and a Bow. </h1>
<p></p>
<p><strong>I need to cut a waistband, a bow, and a bridge for the bow. </strong></p>
<p><strong>I measure the assembled bodice of the dress: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The bottom edge is 53cm long</strong>, which fully corresponds with Sophia-Lielle's under-bust measurement.&nbsp; But it is the bottom edge of the finished bodice, which means I need to add a 2cm seam allowance from either end to determine the length of the waistband. <strong>In other words, I need to cut a waistband with a length of 57cm. </strong></p>
<p><strong>Let us calculate the width of the piece of fabric for cutting the waistband.</strong> According to all calculations, the finished waistband is supposed to have a width of 7.5cm. I multiply it by two, because the fabric will be folded in half, and add 2.5cm for the seam allowances (1.2cm each). <strong>The result is 17.5cm.</strong> </p>
<p><strong>I prepare a piece of fabric with a length of 57cm and a width of 18cm.</strong></p>
<p><strong>And I make a bow from another piece of fabric of the same size and position it on the dress:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p>This is just the right size! <strong>So it takes the same amount of fabric to make the bow (57cm x 18cm). </strong></p>
<p>I will use part of the remaining fabric to make a bridge for the bow.</p>
<p>I press the pieces prepared for the waistband and the bow. </p>
<p><strong>I fold the waistband in half lengthwise and press the fold line:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The two layers will be fused together with fusible webbing.</strong> In theory, I could join them by sewing but the fabric is too delicate and might get drawn together. I always prefer using fusible webbing in such cases. It reinforces the fabric and holds the layers together very well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I wait for it to cool down and then remove the paper backing:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the fabric in half and press it (you can do it without steam)</strong> fusing together the two layers.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Let's see how the waistband looks on the dress.</strong> It must not go beyond the armscyes. It's all good! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I will use the same fusible webbing to secure the waistband on the dress. </strong>I apply two parallel strips of it on the fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I wait for it to cool down, remove the paper backing, and put the waistband on the bodice.</strong> I find the middle first so I can position the waistband correctly.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I start securing the waistband on the bodice from the middle.</strong> Make sure the ends of the waistband are positioned at the same level and meet properly.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I trim away excess fabric:</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I fold and press the 2cm seam allowance along the central edge of the back:&nbsp; </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Here is the result:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to make the bow.</strong> I will use fusible webbing again. It is rather wide so <strong>I cut it in half lengthwise </strong>to make thinner strips. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p>This method of joining fabric is known as fusing. I remove the paper backing and fold the piece in half. <strong>Please note that fusible webbing should be applied on the face side of the fabric.</strong> I align the edges and press it. Make sure to let the fabric cool down properly afterwards. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to make a bridge.</strong> Again, I fuse the layers with a piece of fusible web cut in half lengthwise.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the desired width of the ready bridge</strong>, press it, fold it, and trim excess fabric.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p>I remove the paper backing and fold the piece in half. <strong>That is all, the bridge is ready! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I take the main bow piece. The layers hold together very well. </strong>&nbsp;I turn it, roll the joined edge to the underside as neatly as possible, and secure by pressing.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold it in a ring with the help of the same fusible webbing: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>The second layer of fabric is secured the same way: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>And I start forming a bow. </strong>First I make a neat fold in the centre.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I stitch it down by machine by about 2-3cm: </strong>&nbsp;</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I add the bridge. </strong>First I secure it with a pin and then with hand stitches. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%207_files/image048.jpg" class="img-responsive center-block"></p>

