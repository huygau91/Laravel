<h1>Tutorial 4. Assembling the Bodice and the Skirt.</h1>
<p><strong>I need to assemble the bodice. </strong></p>
<p>I start at the top. I unpin the lay and start joining the duplicated face pieces with the lining pieces.</p>
<p><strong>I sew the shoulder seams of the face with a 1.2cm seam allowance... </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image002.jpg" alt=""></p>
<p><strong>...and I do the same with the lining:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>I press the seams open:&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image006.jpg" alt=""></strong></p>
<p><strong>I trim the ends of the seam allowances at an angle </strong>to reduce the thickness in the areas where the face gets joined with the lining. <strong>&nbsp;</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image008.jpg" alt=""></strong></p>
<p><strong>I put the face pieces and the lining pieces together, face to face, and pin first the shoulder seams and then the armscyes: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image010.jpg" alt=""></p>
<p><strong>I join the pieces along the armscyes with a 1.2cm seam allowance: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image012.jpg" alt=""></p>
<p><strong>Never join it along the armscyes AND along the neckline in one go! You won't be able to turn the bodice out! </strong></p>
<p>I will finish the neckline with a bias tape later.</p>
<p><strong>I notch the rounded parts of the seam allowances so I can turn out the armscyes properly:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image014.jpg" alt=""></p>
<p>You could simply trim the seam allowances lengthwise but I recommend <strong>topstitching them first.</strong> I stitch over the side of the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image016.jpg" alt=""></p>
<p>Topstitching will help you roll the seams toward the underside neatly and turn the garment out properly. I carefully spread out the seam trying to avoid tucks and creases. Sewing for children has something in common with sewing for dolls. Small pieces demand particular scrupulosity.</p>
<p><strong>After turning out the joined pieces, I put them underside to underside and pin together along the neckline:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image018.jpg" alt=""></p>
<p><strong>I stitch at about a presser foot width from the edge: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image022.jpg" alt=""></p>
<p><strong>Then I take a 1.2cm bias tape, leave a small tail, and start sewing it on along the neckline:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image024.jpg" alt=""></p>
<p>Never pull the bias tape on tight! As you can see in the picture, <strong>I keep leaving a little slack as I sew. </strong>The bias tape settles over the rounded edge much better this way. The width of the bias tape coincides with the width of the seam allowance, which makes things a lot easier. I always leave some slack there, especially in the particularly curved areas.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image026.jpg" alt=""></p>
<p><strong>Make sure the ends of your bias tape are sewn on at the same level and meet properly:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image028.jpg" alt=""></p>
<p><strong>I cut off the tail and trim down the seam allowance,</strong> fold the bias tape back, and hold it in place with my fingers to avoid cutting it by accident. It is highly recommended to notch the seam allowance in the rounded areas.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image030.jpg" alt=""></p>
<p><strong>I fold the bias tape over the edge of the garment and check </strong>how well it settles and whether I can finish sewing it on neatly enough in the curved areas.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image032.jpg" alt=""></p>
<p><strong>I press the armscye areas both from the face and from the underside:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image034.jpg" alt=""></p>
<p><strong>I fold the bias tape over the neckline edge and carefully secure it in place by pressing.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image036.jpg" alt=""></p>
<p>It looks very neat, doesn't it? This is why you must never pull the bias tape on tight when sewing it on. Let it settle down naturally.</p>
<p><strong>I carefully press it from the face making sure not to wrinkle the bias tape on the inside:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image040.jpg" alt=""></p>
<p><strong>I sew along the edge of the bias tape to secure it.</strong> You can also secure it with blind stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image042.jpg" alt=""></p>
<p><strong>I align the assembled back with the face of the front, start sewing, stop at the armscye, and make a bar tack:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image044.jpg" alt=""></p>
<p><strong>And then I fold the lining over the seam:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image046.jpg" alt=""></p>
<p><strong>And I make another row of stitches over the previous one:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image048.jpg" alt=""></strong></p>
<p><strong>I notch out the corner so I can turn it out better:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image050.jpg" alt=""></p>
<p><strong>And I turn it out: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image052.jpg" alt=""></p>
<p>As the result, the entire side seam is hidden underneath the lining and won't rub against the skin and disturb the child.</p>
<p><strong>I make a row of joining stitches along the central edge at the back of the bodice. </strong>I stitch at a presser foot width from the edge. There will be a lacing here.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%204_files/image054.jpg" alt=""></p>
<p><strong>I assemble the lining of the skirt. </strong>I join the front with the back along the sides.</p>
