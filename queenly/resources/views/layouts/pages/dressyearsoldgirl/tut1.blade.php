<h1>Tutorial 1. Choosing a Suitable Style for the Child's Body Proportions.</h1>
<p>Dividing the Dress in Well-Proportioned Parts.</p>
<p>I need to choose a dress for Sophia-Lielle, a 3.5 year old girl. There is a fixed algorithm. First of all, I put a slim-fit singlet and shorts (or skirt) on the child so I can see her figure properly and take measurements correctly. Additional clothes will affect the accuracy of measurements!</p>
<p>Tie cords around the girl's body just the way you would on a woman: one around the waist, another around the supposed stomach line, and yet another along the bust line. I recommend tying the last cord along the under-bust line rather than the bust line on elder girls.</p>
<p>You can start taking measurements after tying the cords. I will explain more about it in the next tutorial. Once all measurements are taken, I try to take a picture of the girl against the wall with a height chart or a measuring tape hanging next to her.</p>
<p><strong>I take three pictures: front, back, and side view. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image002.jpg" alt="" class="img-responsive center-block"></p>
<p>As you can see, my young top-model was not very happy about taking pictures and absolutely refused to pose. As the result, the picture of the side view does not look that great. Nevertheless, you can clearly see the girl's height and the position of the cords.</p>
<p>Why do I recommend tying cords before taking measurements? The cords indicate at what levels the measurements need to be taken. And when I start laying additional grids over the pictures in Photoshop to figure out the proportions, determine all key lengths, and divide the dress in parts, these cords remind me where each measurement was taken. I can clearly see in the picture all circumferences, levels at which all measurements were taken, distances between circumference lines, etc. In other words, a picture which shows all cord positions provides you with a coordinate system to rely upon in your calculations.</p>
<p>I apply ready grid templates to the pictures. Here is how these templates are made: I open a new file in Photoshop and<strong> draw grid lines by dragging them in place from the horizontal and vertical rulers. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image004.jpg" alt="" class="img-responsive center-block"></p>
<p>I position the first grid line 1cm from the top edge and then add another grid line 4cm below it. This is 1/4. It will be 1/5 if the second grid line is drawn 5cm below the first one, etc. I simply <strong>draw all necessary grids on separate layers.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image006.jpg" alt="" class="img-responsive center-block"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image008.jpg" alt="" class="img-responsive center-block"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image010.jpg" alt="" class="img-responsive center-block"></p>
<p><strong>I also have a golden ratio grid at hand:</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image012.jpg" alt="" class="img-responsive center-block"></p>
<p>So I have a Photoshop file with various grid templates on separate layers which I can easily transfer and lay over pictures of my little clients. If you don't know how to use Photoshop, you will find simpler graphic editors with rulers to determine dimensions and proportions on a picture. It is impossible to do without computer software these days. It speeds up the process of choosing and customizing the right dress or calculating all necessary dimensions. Needless to say, that this applies not only to children's wear but to adults' clothes as well. Simply take several pictures of your client, develop a custom dress model on your computer, and then sew it with full confidence that it will fit well and suit your client.</p>
<p><strong>I lay the golden ratio grid over the pictures of Sophia-Lielle </strong>and find the position of the waistline. The three blue guides mark shoulder line, the waistline, and the bottom line of the girl's height.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image014.jpg" alt="" class="img-responsive center-block"></p>
<p>And now I will do something really fun − dress Sophia-Lielle in a dress chosen by her and her mum. The dress is just an example.</p>
<p>I have removed the background of the original picture and now I can "put" the dress on the child.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image016.jpg" alt="" class="img-responsive center-block"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image018.jpg" alt="" class="img-responsive center-block"></p>
<p>This way I can see how the chosen dress will look on the girl. Once the choice is made, I need to decide how long the dress should be, how to harmoniously divide it into a bodice and a skirt, how wide the waistband and how large the bow should be, etc. In other words, I need to know the length of each separate element of the dress.</p>
<p><strong>And that is when the golden ratio grid comes in handy: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image020.jpg" alt="" class="img-responsive center-block"></p>
<p>Look what a strange thing! There are truly many riddles about the Golden ratio! Look at the girls’ head-to-height ratio and the supposed golden-ratio position of the waistline. I copy the same golden ratio grid, make it smaller without disturbing the proportions, and lay it over the picture aligning the top line with the child's shoulder line. Can you see that the waistline positions basically coincide? Unbelievable! I get amazed by this kind of things every time I work with the Golden ratio!</p>
<p><strong>I have figured out the lengths of the bodice and the skirt.&nbsp; </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image022.jpg" alt="" class="img-responsive center-block"></p>
<p><strong>Now I want to decide on the width of the waistband. </strong>I duplicate the golden ratio grid layer and split the bodice into a waistband and a top part.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image024.jpg" alt="" class="img-responsive center-block"></p>
<p>This grid shows how wide the waistband will be.</p>
<p><strong>I also need to calculate the width of the skirt according to the golden ratio. </strong>I do this by rotating the golden ratio grid 90 degrees clockwise. The total width of the girl’s shoulders should be to the width of the skirt as 3/8 is to 5/8.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image026.jpg" alt="" class="img-responsive center-block"></p>
<p><strong>Therefore, the skirt needs to be that wide: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image028.jpg" alt="" class="img-responsive center-block"></p>
<p>I remove the grid and mark the skirt width with blue guide lines.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image030.jpg" alt="" class="img-responsive center-block"></p>
<p>Now there are exact coordinates of all elements in the picture. Moreover, I know the length of each element in centimeters. There is a vertical ruler in Photoshop and you can easily figure out the value of its divisions knowing the girl's height. The horizontal ruler at the top of the window will help you calculate the width of the skirt.</p>
<p><strong>I do these very simple calculations and get the following results:</strong> bodice length − 20cm, waistband width − about 7cm, skirt length − 31cm, etc. Now I know how to mark it on fabric. Even if something has slipped my attention, I will be able to make necessary adjustments using these rulers.</p>
<p><strong>All preliminary work is done. </strong></p>
<p>I take a pretty dress and transform it in Photoshop with respect to the guides. This is not compulsory! You should do it if you want to show the expected result to the girl and her mum in advance though.</p>
<p>I have adjusted everything save for the bow.</p>
<p><strong>This is roughly what the dress will look like on Sophia-Lielle: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image032.jpg" alt="" class="img-responsive center-block"></p>
<p><strong>Time to start working on the actual dress.</strong> All preliminary work has been done. The dress below was also found on the Internet and the girl and I both like it a lot.<strong> This is how it looks on her: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image034.jpg" alt="" class="img-responsive center-block"></p>
<p>Guided by the rulers on the screen, I record the width of the skirt, the length of the bodice, the length of the skirt, the width of the waistband, the size of the bow, etc.</p>
<p><strong>The picture below on the right shows Sophia-Lielle's dress during the first fitting: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image036.jpg" alt="" class="img-responsive center-block"></strong></p>
<p>This picture is a clear proof that Photoshop rulers are a perfect tool for calculating all dimensions!</p>
<p>Photoshop is very handy for every seamstress but it is somewhat complicated. It is up to you whether you want to use it or find some simpler software.</p>
<p><strong>And, of course, you can always do the same calculations on paper without a computer.</strong> This involves solving basic systems of equations though.</p>
<p><strong>What is Golden ratio from the mathematics point of view? </strong></p>
<p>Fractions 3/8 and 5/8 are the closest numeric expressions of the Golden ratio.</p>
<p>There is a special Golden ratio Formula, too: if A is the larger part of a section and B is the smaller part of the same section, then A = B*1.62.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%201_files/image038.jpg" alt="" class="img-responsive center-block"></p>
<p>I have taken A as 5/8 and B as 3/8 and double-checked it. As you can see, the formula is accurate.</p>
<p><strong>Let us solve the task mathematically. </strong></p>
<p>I need to measure the distance from my client's shoulder line to the floor. It was for this reason that I took a picture of her next to a height chart.</p>
<p><strong>The distance from the shoulder line to the floor is 85cm. </strong></p>
<p>Let us take <strong>the length of the dress </strong>as "<strong>a" </strong>and <strong>the visible part of the legs</strong> as <strong>"b"</strong>.</p>
<p>I make a system of equations knowing that A and B must be in a golden ratio and their sum must be 85cm.</p>
<p><strong>a = 1.62 × b (1)</strong></p>
<p><strong>a + b = 85 (2)</strong></p>
<p>Let us solve the system of equations.</p>
<p>I put the numeric expression of "<strong>a" </strong>from equation 1 into equation 2:</p>
<p><strong>1.62 × b + b = 85cm</strong></p>
<p><strong>b = 32.5cm</strong></p>
<p>And I put the found numeric expression of <strong>"b" </strong>into equation 1:</p>
<p><strong>a = -52.5cm</strong></p>
<p>Therefore, <strong>the length of the dress is 52.5cm and the visible part of the girl's legs is 32.5cm. </strong></p>
<p><strong>Now I need to divide the dress into a bodice and a skirt. </strong></p>
<p>Let us take <strong>the length of the skirt </strong>as <strong>"a" </strong>and <strong>the length of the bodice</strong> as <strong>"b"</strong>.</p>
<p>I make a system of equations knowing that “a" and “b" must be in a golden ratio and their sum must be 52.5cm.</p>
<p><strong>a = 1.62 × b (3)</strong></p>
<p><strong>a + b = 52.5 (4)</strong></p>
<p>Let us solve the system of equations.</p>
<p>I put the numeric expression of <strong>"a" </strong>from equation 3 into equation 4:</p>
<p><strong>1.62 × b + b = 52.5cm</strong></p>
<p><strong>b = 20cm</strong></p>
<p>And I put the found numeric expression of <strong>"b" </strong>into equation 3:</p>
<p><strong>a = 32.5cm</strong></p>
<p>Therefore, <strong>the length of the skirt is 32.5cm while the length of the bodice is 20cm. </strong></p>
<p>Please note how the length of the skirt coincides with the length of the visible part of the girl's legs.&nbsp; Everything is very well-proportioned.</p>
<p><strong>Last but not least, I need to divide the bodice into a waistband and a top part. </strong></p>
<p>Let us take <strong>the length of the top part of the bodice </strong>as <strong>"a" </strong>and <strong>the width of the waistband</strong> as <strong>"b"</strong>.</p>
<p>I make a system of equations knowing that “a" and “b" must be in a golden ratio and their sum must be 20cm.</p>
<p><strong>a = 1.62 × b (5)</strong></p>
<p><strong>a + b = 20 (6)</strong></p>
<p>Let us solve the system of equations.</p>
<p>I put the numeric expression of <strong>"a" </strong>from equation 5 into equation 6:</p>
<p><strong>1.62 × b + b = 20cm</strong></p>
<p><strong>b = -7.6cm</strong></p>
<p>And I put the found numeric expression of <strong>"b" </strong>into equation 5:</p>
<p><strong>a = 12.4cm</strong></p>
<p>Therefore,<strong> the length of the top part of the bodice is 12.4cm and the width of the waistband is 7.6cm.</strong></p>
<p>The width of the skirt is calculated in absolutely the same manner considering that I know the girl's shoulder to shoulder measurement. I will do this calculation during the cutting process.</p>
<p>So, now I know Sophia-Lielle's proportions and, you can trust me, they fully coincide with the results I got in Photoshop. I have calculated all required values using two different methods. Now I can take all measurements off the girl and start constructing the pattern.</p>

