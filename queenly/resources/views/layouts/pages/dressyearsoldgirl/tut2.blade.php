<h1>Tutorial 2. Taking Measurements.</h1>
<p><strong>In this tutorial, I will explain how to correctly take measurements off a little girl. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image002.jpg" alt=""></p>
<p>As I said before, Sophia-Lielle was feeling a little peevish, which made it impossible to document the process of taking measurements properly. I should say that working with children always requires finding the right approach and means of communication. And, of course, parents should try to bring their child to the fitting in a good mood.</p>
<p>Now I will list all required measurements and show them to you schematically. There is nothing particularly hard about taking measurements off children! It is done just the same way as with adults, only in this case you need to work faster and take a lot more measurements because fixed standards don't work in children's fashion.</p>
<p><strong>Here is a list of required measurements in the order you normally take them:</strong></p>
<p><strong>Height. </strong></p>
<p><strong>Bust Width Front:</strong> measure from side to side along the bust line (the way you would on a woman).</p>
<p><strong>Bust Circumference.</strong></p>
<p><strong>Waist Circumference.</strong></p>
<p><strong>Stomach Circumference. </strong></p>
<p><strong>Hip Circumference. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image004.jpg" alt=""></strong></p>
<p><strong>There are also vertical measurements:</strong></p>
<p>- Bust Line to Waistline;</p>
<p>- Waistline to Stomach Line;</p>
<p>- Waistline to Hip Line;</p>
<p><strong>Waist to Floor: </strong>measure the distance from the waistline down to the floor.</p>
<p><strong>Side Length:</strong> measure the distance from the waistline to the underarm. This measurement must be taken in the actual size. Ask the girl to lift her arm so you can see where the armscye should stop. It shouldn't rub against the skin and yet it shouldn't be too low either.</p>
<p><strong>Bust Apex to Bust Apex. &nbsp;&nbsp;</strong></p>
<p><strong>Clip </strong>(only relevant if you are making an open front neckline). This measurement is taken as the actual distance to the supposed edge of the bodice: the armscye, the top corner, and the front neckline if it features a sharp corner.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>Armpit to Armpit Front: </strong>with the girl's arms hanging down freely, measure the distance between the supposed armscyes at the underarms level across the front.</p>
<p><strong>Armpit to Armpit Back: </strong>with the girl's arms hanging down freely, measure the distance between the supposed armscyes at the underarms level across the back.</p>
<p>These measurements will help you construct the armscyes correctly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>Shoulder to Shoulder Across Front:</strong> measure the distance between the outermost shoulder points of the dress across the front.</p>
<p><strong>Shoulder to Shoulder Across Back:</strong> same as the previous measurement but taken across the back.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image010.jpg" alt=""></p>
<p><strong>Full Length Front:</strong> measure the distance from the side neck point to the waistline.</p>
<p><strong>Full Length Back:</strong> measure the distance from the same side neck point to the waistline at the back.</p>
<p>These two measurements can be taken simultaneously: just mark the middle of the shoulder at the base of the neck and measure from waistline to waistline through this point. The measurements will be much more precise this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image012.jpg" alt=""></p>
<p><strong>Central Length Back:</strong> measure the distance from the seventh cervical vertebra to the waistline.</p>
<p><strong>A similar measurement is taken from the front.</strong> If the dress has a high neck, then measure from the sternal notch to the waistline. But if the dress features a low neck, then measure from the supposed middle of the neckline to the waistline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>Armscye circumference:</strong> wrap the measuring tape around the girl's arm, think over how loose the sleeve should be, and measure it in the actual size. This measurement indicates the actual length of the armscye on the finished dress.</p>
<p><strong>Upper Arm Circumference </strong>(taken if you plan on sewing sleeves): wrap the measuring tape around the girl�s upper arm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image016.jpg" alt=""></p>
<p><strong>There are some other key measurements for very young children: </strong></p>
<p><strong>Waist Width Front: </strong>measure from side to side along the waistline across the front.</p>
<p><strong>&nbsp;Stomach Width Front: </strong>measure from side to side along the stomach line across the front.</p>
<p>Little children always have somewhat protruding tummies and it is important to calculate the exact position of the side seam at the stomach level. The two measurements above are not so important if you are sewing a loose-fit dress with a skirt flared from the under-bust line (like in my case). They are, however, crucial if you are making a dress with an accentuated waist and a skirt flared from the waist.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image018.jpg" alt=""></p>
<p><strong>Shoulder Length:</strong> measure from the base of the neck to the shoulder tip. If you have already decided on the style, then measure the distance between the supposed innermost and outermost shoulder points on the dress. In my case, Sophia-Lielle's natural shoulder length is 7cm. According to the dress style though, it will be 5cm. So I step 1cm from the shoulder tip and measure 5cm from that point. I believe it will look beautiful with the chosen neckline style.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image020.jpg" alt=""></p>
<p><strong>Shoulder Slope Front: </strong>measure diagonally from the shoulder tip point to the point where the Full Length Front line arrives at the waistline. It will help you draw the incline of the shoulder correctly.</p>
<p><strong>Shoulder Slope Back:</strong> measure diagonally from the shoulder tip point to the centre waist point. It will help you draw a correct incline of the shoulder at the back of the bodice.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image022.jpg" alt=""></p>
<p><strong>These are all measurements you need to take to draft a sewing pattern for a child. </strong></p>
<p><strong>Now I can start drafting. </strong></p>
<p>There is a separate chapter in this book: <strong><em>Drafting the Base of a Dress for a 3.5 Year Old Girl (Based on Calculations). </em></strong>It contains all key measurements, as well as step-by-step guidelines to drafting the pattern of the entire dress.</p>
<p><strong>I would like to comment on it in advance. </strong></p>
<p>You begin by constructing the base of the dress down to the hip line in correspondence with the child's measurements.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image024.jpg" alt=""></p>
<p>Then, you adjust the position of the waistline and the lengths of the bodice and the skirt with regard to the defined body proportions (the red line below marks the new length of the bodice for Sophia-Lielle).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image026.jpg" alt=""></p>
<p>Only after doing all this, you finish constructing the skirt:</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Tutorial%202_files/image028.jpg" alt=""></p>
<p>The pattern drafting process is described in all detail in the above-mentioned chapter. It is very simple. I want you to know that all calculation methods explained in my books are the methods I use personally on a regular basis. I cannot refer to their original authors because I have been polishing these methods myself for many years. I write down an algorithm and then construct the pattern accordingly on paper or directly on fabric.</p>
<p>Once the pattern is ready, the cutting process begins.</p>

