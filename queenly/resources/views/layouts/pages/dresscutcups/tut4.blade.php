<h3>Tutorial 4.&nbsp; Dress Cutting: Main Fabric and Lace. </h3>
<p></p>
<p><strong>During this tutorial I am going to cut the bottom or the base of my dress out of the main fabric (stretch satin) and out of the lace. </strong></p>
<h4>Cutting Main Fabric. </h4>
<p></p>
<p>I fold the fabric piece in half and secure the crease with pins. I am going to cut it crosswise. The stretching (stretch thread) goes along the shoot. That�s why the whole stretching effect will go from top downwards. </p>
<p>I lay out the front pattern on the fabric and outline it with a tailor�s chalk. I mark notches.</p>
<p>I alter the central piece of the front. As I have changed the bust middle, the central piece of the front also has to be reduced by 0.4 inches. </p>
