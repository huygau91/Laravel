<h3>Tutorial 14. Joining the Side Seams. Hemming the Bottom of the Dress.</h3>
<h4>Joining Side Seams.</h4>
<p></p>
<p>I start joining the side seams. I am going to join them step by step: first I am going to join the side seam by a common seam at the level of the cups, i.e. going from the top to the under-bust line, and then I am going to divide the dress into 2 layers - backing and lace. This is a familiar procedure that requires no detailed explanations.</p>
<p>It is necessary to place some pins at the top and bottom of the future side seam at the cup level because this spot is rather thick.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p>For the sake of convenience and a neater look I have finished the side seam 1.2-1.6 inches below the level of the yoke and cups. This makes spreading the layers of the dress easier. Bar tacks are also necessary.</p>
<p>I make the same kind of a seam on the other side as well.</p>
<p>I turn up the satin pieces and find the point where I started sewing together the lace fabric. I sew together the layers of lace from both sides of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p>Having sewn together the lace I return to the upper point and cut the lace basically up to the seaming. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p>I fold the seam allowances of the lace inwards and sew together the side seams of the satin front and back dress pieces starting from the same point as with the lace.</p>
<p></p>
