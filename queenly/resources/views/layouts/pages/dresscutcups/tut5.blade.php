<h3>Tutorial 5. Marking the Location of Cup Bones. Removing Beads off Seam Allowances on the Lace. </h3>
<h4>Marking the Location of Cup Bones.</h4>
<p></p>
<p><strong>I start working on the cups.</strong></p>
<p>I fuse the cups with hard iron-on fabric. </p>
<p><strong>And I start marking the location of their bones.</strong></p>
<p><strong>Central cup piece:</strong></p>
<p>&nbsp;- I draw a bust line connecting notches along the bust (line 1);</p>
<p>&nbsp;- I draw a vertical line along the centre of the cup (line 2);</p>
<p>&nbsp;- I step 0.8 inches back from the edge of the curve on each side and draw lines going upwards and slightly inclined, basically going through the middle of each section of the bust-line (lines 3 and 4); </p>
<p>&nbsp;- I draw a line parallel to the bust-line, 1.6 inches below it (line 5);</p>
<p>&nbsp;- I draw 2 parallel lines (lines 6 and 7) 1.2 inches above the bust-line. These lines of the upper bone location are going to be adjusted during the sewing process.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%205_files/image002.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image890.jpg"></p>
<p><strong>Left side piece of the cup:</strong></p>
<p>- I draw a bust line connecting notches along the bust (line 1);</p>
<p>- I draw a line parallel to the bust-line, 1.6&nbsp; inches below it (line 5);</p>
<p>- I draw a slightly upwards-inclined line 1.2 inches above the bust-line (line 7);</p>
<p>- I draw a slightly inclined upward line stepping 0.8 inches from the edge of the curve (line 8).&nbsp; The location of the bone following this line is also going to be adjusted during the sewing process.</p>
