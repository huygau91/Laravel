<p>First, I pin the pieces together along the furthest points, then divide each distance in half, re-arrange the fullness, pin it and then divide in half again, and so on. </p>
<p><strong>Please take a look at the way I�ve pinned these pieces together and by how much I have let off the lining from under the face:</strong></p>
<p>
