<p>As a next step I remove pins and overlay the seam with the face part of the back of the cups. I have pressed and prepared this piece in advance. </p>
<p><strong>I even out their cut edges and - without pinning them together - stitch them on along the top, 1mm from the edge:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
