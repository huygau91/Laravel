<p>I would like to remind you once more that I add pins in a gliding motion without piercing the bone that is attached on top of the lining.</p>
<p>I switch to a one-sided presser foot that is handier for sewing face and lining together. I stitch over the lining and the pieces have been pinned together from the face side. I start sewing them together from the bar tack. </p>
<p><strong>The presser foot glides along the bone (the stay-stitch serves as a guiding line):</strong></p>
<p>
