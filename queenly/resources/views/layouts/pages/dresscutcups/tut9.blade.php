						<p><strong>Features: </strong></p>
<ul>
<li>One of the most popular technologies of constructing soft, flowing dresses with hard made cups.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>Cutting and sewing quilted cups and connecting it to a soft dress;</li>
<li>Altering standard pattern to any size using a unique formula for calculating the side seam (Tatiana Kozorovitsky�s formula);</li>
<li>Constructing hard lacing on floating loops;</li>
<li>Decorating the garment with lace, beads and sequins.</li>
</ul>
<p><strong>Where to use:</strong></p>
<p>This technology is widely used:</p>
<ul>
<li>Sewing youth dresses for every occasion: from light sundress to evening or wedding dress;</li>
<li>Instead of lace you can use chiffon, organza, net, natural or artificial silk;</li>
<li>Prefect for dresses for expectant mothers.</li>
