<p>Approaching the triangle of the neckline I slow down the pace of the sewing machine and this is one of the rare cases when I do turn the garment around on the needle. But my neckline cut is so precisely formed that turning the garment in such a manner is not going to cause any obstruction or deformation. The neckline is symmetrical.</p>
<p><strong>The seam joining the pieces from the face side looks like this:</strong></p>
<p>
