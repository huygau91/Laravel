<p>I remove pins and trim extra polyester along the perimeter.</p>
<p><strong>Cups covered with padding polyester look like this:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And now I am going to join the dress with the hard quilted cups using an overseam.</strong></p>
<p><strong>I put the front piece of the dress over the seam allowance of the cup,</strong> match the center and the sides along the points of reference, then carefully arrange all fabric according to the curve and secure it with pins:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
