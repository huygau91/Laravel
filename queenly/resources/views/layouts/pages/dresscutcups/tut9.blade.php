<h3>Tutorial 9.&nbsp; Joining the Cups and the Dress.</h3>
<p></p>
<p><strong>I start joining the cups with the dress.</strong></p>
<p><strong>First of all, I need to stitch up the darts on both back pieces of the dress:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>A stitched-up dart looks like this:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I cover the lining of the quilted cups with padding polyester and secure it with a couple of pins.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I stitch this padding polyester on with the sewing machine: first along the outer side of the bone that marks the border of the cups:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch-on the polyester along the top of the cup, along the inner side of the upper perimeter bone:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p>It is important to prevent the padding polyester from getting into the seam allowances and creating extra thickness. I also place two ribbon loops under the seam for hanging the dress.</p>
<p><strong>I stitch-on padding polyester along the other side and the bottom of the cup, along the inner side of the bones (thereby overlapping them):</strong></p>
<p><strong><br>
