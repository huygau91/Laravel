<h3>Tutorial 7.&nbsp; Sewing-on Inner Cup Bones.</h3>
<p></p>
<p><strong>Now I start sewing-on the inner bones of the cups. </strong></p>
<p>I use narrow Rigilene bones. </p>
<p>I have attached the perimeter bones with one outer seam. <strong>I push the ends of all inner bones underneath the perimeter bones. </strong>I make bar tacks at the start and end points of sewing. I stitch-on the bones along the marked lines (the numbers of the bones coincide with the numbers of the marking lines).</p>
<p><strong>I stitch-on bone 9 along the border of the left side piece of the cups on its outer side:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I stitch-on bone 11 along the border of the right side piece of the cups on its outer side:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch-on bone 1 along the bust-line.</strong></p>
<p>This bone passes through all cup pieces. It begins at the left side piece. </p>
<p><strong>I push the end of the narrow Rigilene bone underneath bone 9 sewn-on only from the outer side:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%207_files/image005.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I reach the intersection of the upper perimeter bones of the cups.</strong> I try not to overlap the bones so I trim bone 1, push its end underneath the perimeter bones sewn-on from one side, and make a bar tack.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%207_files/image007.jpg" class="img-responsive center-block"></p>
<p><strong>I push the end of the bone underneath the perimeter bone and keep stitching it on as a prolongation of bone 1: </strong></p>
<p>
