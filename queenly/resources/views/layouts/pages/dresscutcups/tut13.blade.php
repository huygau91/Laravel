<h3>Tutorial 13. Hanging Loops for the Lacing.</h3>
<p></p>
<p><strong>During this tutorial I am going to show you how to mark and make hanging loops for the lacing.</strong></p>
<p>Since the lace on my dress is black and it is difficult to make distinct bright marking on it, <strong>I am going to show you this marking on a regular piece of paper first.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image002.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image1183.jpg"></p>
<p>I begin by drawing a straight line and marking an assumed length of the lacing on it. </p>
<p>The cleavage at the back, i.e. the triangle I have pressed, is about 1.6 inches wide. This means that loops can go beyond the edge of the garment by 0.5-0.6 inches to allow for beautiful lacing. I have drawn an imaginary borderline for the loops.&nbsp; </p>
<p>You should step 0.8 inches inwards from the edge of the garment in order to make the spot where the ends or �tails" of the loops are going to end. I draw this line. </p>
<p>I mark an assumed location of the loops on the edge of the garment, i.e. I mark their width and the distance between them. The stitch attaching these loops is going to go along this line.</p>
<p>I mark middle and upper points of the loops on this imaginary borderline.</p>
<p>I mark the location of the ends of these loops on the inner line.</p>
<p><strong>I place pins at the points of reference.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p>Such markings are best of all done on a pressboard as it makes adding pins easier. </p>
<p><strong>I take a cord or a ribbon - whatever you want to use for the loops - and start arranging it between the pins:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></p>
<p>I secure the end of the ribbon and arrange it in a �serpent"-like shape between the pins.</p>
<p><strong>Now I only need to secure this ribbon at those spots where the stitch attaching the loops is going to pass:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p>The ribbon passes exactly through those spots of the loop width that I have marked on the line of the pressed flat crease.</p>
<p></p>
<p>I remove the pins that served as the pegs I went around with the ribbon and finish securing the loops on the fabric or - as you can see now on paper - at the spots where they should be attached.</p>
<p><strong>It looks like this:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></p>
<p>This way I have made the marking for the loops very easily, precisely and quickly!</p>
<p><strong>Now I mark the loops on the actual garment based on the same principle: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></p>
<p>I have pressed out the edge of the garment and now I mark the width of the loops and the distance between them on it.</p>
<p><strong>Stepping away 0.6 and 0.8 inches correspondingly I draw borderlines for the loops and the end of the loop �tails":</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image014.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image1195.jpg"></strong></p>
<p>I mark middle and upper points of the loops on their borderline and the location of loop �tails" - on their own borderline.</p>
<p>The line attaching the loops is going to go right along the crease or edge of the garment while the �tails" are going to go inside the seam allowance. </p>
<p>The loop width is determined by the thickness of the rope, cord or ribbon - i.e. any material you are using for it.&nbsp; The distance between the loops is determined by the same parameter.</p>
<p>Make it whichever way you like it most!</p>
<p><strong>I place pins to mark where future loops are going to be formed:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I take a ribbon and form hanging loops:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image018.jpg" class="img-responsive center-block"></p>
<p>You don�t really need to arrange the ribbon along its whole length from the very beginning. It is enough to form 2-3 loops and then secure them and re-locate the pins. I move to the very end, gradually re-locating the pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same thing with the other side of the lacing:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I stitch-on hanging loops along the pressed flat crease from both sides of the lacing:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove pins:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the fastener along the pressed flat crease:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I have measured and cut 2 pieces of a wide Rigilene bone corresponding to the length of the lacing:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image030.jpg" class="img-responsive center-block"></p>
<p>The ends of the bones are taped over with masking tape.</p>
<p>And I have torn off a strap of the main fabric about 1.2-1.5 inches wide and 1.6-2 inches longer than the prepared bone. </p>
<p><strong>I place the Rigilene bone in the middle of the strap leaving 0.8 inches from the edge: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I wrap one side of the strap over the bone and stitch it on leaving 1mm from the edge:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image033.jpg" class="img-responsive center-block"></strong></p>
<p>I reach the end of the strap.</p>
<p><strong>I wrap the strap from the other side and stitch it, leaving 1mm from the edge like before:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image035.jpg" class="img-responsive center-block"></p>
<p>I trim the ends of the strap leaving 0.6 inches. </p>
<p><strong>A bone wrapped in a fabric strap looks like this when finished:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image037.jpg" class="img-responsive center-block"></p>
<p>I do the same with the second bone.</p>
<p><strong>I overlay the �tails" of the loops with the bone additionally securing the lacing:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the spot where the bone is going to be sewn-on:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch up the end of the strap:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image043.jpg" class="img-responsive center-block"></p>
<p>I bend the wrapped bone towards the face side, overlap&nbsp; loop �tails" and make two parallel stitches along the existing ones. The other end of the strap should be tucked and pushed underneath the bone.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>The result is the following:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image047.jpg" class="img-responsive center-block"></p>
<p>It is simple, neat, quite fast and what�s more important - very safe! Loops like this won�t break!</p>
<p>I cover the loops on the other side of the lacing in exactly the same manner.</p>
<p><strong>The lacing loops are identical, symmetrical and located at the same level:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image049.jpg" class="img-responsive center-block"></p>
<p><strong>Then I secure the fly-off allowance at the level of the upper yoke:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image051.jpg" class="img-responsive center-block"></p>
<p>You can decide for yourself whether you want to leave such a wide allowance or not. It is quite possible to trim and overlock it now. But I�ve decided to leave everything as it is!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2013_files/image053.jpg" class="img-responsive center-block"></p>
<p>I prefer making a lacing with hanging loops only when the corset has no tightening effect. I always make hanging loops �serpent"-shaped and secure them with Rigilene bones. It guarantees their safety and they won�t ever break. </p>
<p>As a matter of fact, you should always be very careful with corset garment fasteners. Any garment that requires tightening should be securely fastened. People often ask me why I don�t use zippers. And quite often my clients persuade me into sewing a zip fastener. But I always say that I cannot guarantee good quality of a plastic zipper!&nbsp; I don�t think you are going to be happy if your zipper suddenly breaks while you are embracing your guests or relatives during a ceremony. A couple times I have even asked my clients, who insisted on having a zip fastener, for a note of hand confirming that they have been warned and won�t hold any demands against me in this respect.</p>
