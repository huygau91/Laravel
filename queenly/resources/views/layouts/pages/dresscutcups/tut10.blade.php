<h3>Tutorial 10. Pressing the Top of the Cups. Sewing-on the Face.</h3>
<h4>Pressing Cups.</h4>
<p></p>
<p>Upon being turned out the cups should be thoroughly pressed. I take the largest cup pressing mould I have. I place the cups on this form and spread them in such a way that the allowances of the upper seam lie very smooth and don�t ridge.</p>
<p><strong>I �stamp" these seam allowances with the iron and then press them thoroughly:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p>I believe you have noticed that I haven�t trimmed any seam allowances on the hard iron-on fabric. I haven�t done this in order to prevent the upper edge from stretching too much. I assumed that the seam allowances would be slightly seen through the face as you can see in the picture. But I also knew in advance that there would be some appliqu� d�cor over the cup that�s why it didn�t bother me at all. On the plus side, the upper edge remains hard and its shape is well set. </p>
<p>As a matter of fact one should pay great attention to the pressing of the upper edge of the upper cut. I press it without a hurry, very accurately in a stroking motion - a small area at a time. I try to preserve the embossed shape of the cups and their slight gather to ensure good fitting to the bust. I keep making sure that the piped-like edge is even. </p>
<p><strong>I pay special attention to the neckline corner in the middle of the bust:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
