<h3>Tutorial 6.&nbsp; Joining the Lining of the Cups. Pressing. Sewing-on Perimeter Bones.</h3>
<h4>Joining the Lining of the Cups.</h4>
<p></p>
<p><strong>I take the pieces of the cup lining and start joining the curves matching and evening out cut edges:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I sew them together using a 0.5 inch seam allowance: </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p>I never tack or pin pieces together. </p>
<p></p>
<p><strong>And I sew together the second curve of the cup lining in exactly the same manner: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The curves are ready:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I turn the curve seam around and topstitch it by 1mm from both sides of the seam using the sewing machine:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image010.jpg" class="img-responsive center-block"></p>
<p>And I topstitch the second curve seam the same way.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></p>
<p>I trim corners.</p>
<p><strong>I cut seam allowances off completely leaving 2mm from each side.</strong> This will make it easier to press the seam and to sew bones onto it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>This is what a trimmed and topstitched curve seam looks like:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image016.jpg" class="img-responsive center-block"></p>
<p>And then I make a machine stay-stitch at a distance of the seam allowance along the bottom of the cup. </p>
<p><strong>I make a stay-stitch going along the inner side of one half of the cup in order to achieve symmetry of the parts:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image018.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image918.jpg"></strong></p>
<p><strong>And I make a stay-stitch going along the outer side on the second half of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image020.jpg" class="img-responsive center-block"></p>
<p>In this case the intersection of these stay-stitches in the corner of the cup is going to be perfect. </p>
<p>If a V-neck is intended, then I start making the stay-stitch along the top of the cup <strong>from the inner side:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the corner of the cup, make a couple of stitches and stop:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the garment around and make a stay-stitch along the face side of the second half of the cup, again slightly below the corner:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image026.jpg" class="img-responsive center-block"></p>
<p>The resulting intersection of these stay-stitches is the most precise marking of the centre of the front. Later I am going to use this point as a reference. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image030.jpg" class="img-responsive center-block"></p>
<h4>Pressing.</h4>
<p>Now it�s time to press the stitched curves. </p>
<p><strong>I use my cup pressing mould for pressing the curves:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p>I carefully shape the cup by a circular motion of the iron making it look beautiful and rounded. I press both from the inner and outer side. </p>
<h4>Sewing-on Perimeter Bones.</h4>
<p></p>
<p><strong>I sew-on bones along the bottom of the cup.</strong></p>
<p>I take a narrow Rigilene bone and round it a little the same way I did when sewing corsets. Then I tape over the end of the bone with some masking tape. </p>
<p><strong>I place the bone at a 1.2 inch distance from the edge stepping 1mm from the existing stay-stitch:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I sew this bone onto the bottom of the cup along the outer side:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image036.jpg" class="img-responsive center-block"></p>
<p>I don�t ease in here! I try to go right along the stay-stitch very carefully. </p>
<p><strong>I trim the bone leaving 1mm to the intersection with the second stay-stitch:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image037.jpg" class="img-responsive center-block"></p>
<p>I tape the end of the bone with masking tape and finish stitching it on. </p>
<p>This way I have sewn-on a narrow Rigilene bone along its outer side to the first half of the cup bottom.</p>
<p>Now I take another narrow Rigilene bone and make it rounded. Instead of taping the end of this bone with masking tape I push it underneath the sewn-on bone stepping 1mm away from the stay-stitch that goes along the bottom of the second half of the cup.</p>
<p><strong>I sew-on this bone along the outer side as well, starting from the very edge and making bar tacks:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the bone leaving about a 1.2 inch gap from the edge:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image041.jpg" class="img-responsive center-block"></p>
<p>I tape over the edge of the bone and finish sewing it on. </p>
<p></p>
<p><strong>And then I start sewing-on bones along the top of the cup.</strong></p>
<p>I take a narrow Rigilene bone and make it rounded the same way I did when sewing corsets. The end of the bone should be taped with masking tape. </p>
<p>I place the bone at about a 1.2 inch distance from the side cut edge the same way it was done along the bottom of the cup and start stitching it on along the outer side, 1mm from the stay-stitch. Bar tacks are necessary here.</p>
<p><strong>I start easing-in the fabric underneath the bone:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>The main rule of easing-in goes: the smaller the bust - the more fullness on the cups; and vice versa, the larger the bust - the less fullness on the cups! </strong></p>
<p>That�s rather logical. Full breasts don�t need to be squeezed into the cups - they will naturally fill up their space in a beautiful way. But small-size breasts will require additional push-up pads and very good fitting along the bust area. </p>
<p><strong>I touch upon the intersection of the stay-stitches with this bone and prolong it further, up to the lower horizontal bone:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image045.jpg" class="img-responsive center-block"></p>
<p>I haven�t marked the location of this bone after the intersection of the stay-stitches; I just lay it out the way it naturally goes. However, atop of the cup this bone should go along the stay-stitch. </p>
<p>&nbsp;I trim the end of this bone in such a way to make it possible to push it underneath the horizontal bone sewn-on from the outer side.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>Now I get on to sewing a bone along the top of the cup from its other side. </strong></p>
<p>And again I take a narrow Rigilene bone and round it out. I try to place this bone symmetrically to the first sewn-on bone, that�s why I start sewing it on from the lower bone. I don�t tape its end over with masking tape but just push it underneath the lower bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image049.jpg" class="img-responsive center-block"></p>
<p>I try to lay the bone very symmetrically at the intersection of the stay-stitches because it determines how good the corner of the V-neck cut will look. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image050.jpg" class="img-responsive center-block"></p>
<p>Then I put another bone going along the stay-stitch at a 1mm distance from it and start easing-in the fabric underneath this bone. I trim the bone as far as 1.2 inches from the side cut edge and finish sewing it on, making necessary bar tacks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image052.jpg" class="img-responsive center-block"></p>
<p>Many times have I tried to make a stay-stitch go along just one side of a piece and each time I got a really askew V-neck. Then I had to suffer, adjust it using a ruler or a pattern and yet the V-neck would still stay askew! Only after I had first made the stay-stitch go from edge to centre and then turned the garment around and made the second part of the stay-stitch go along the face side, was the V-neck finally beyond reproach - a perfect triangle! </p>
<p><strong>Afterwards, I re-distribute the fullness by hand, spreading it more evenly along the whole d�collet�: </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%206_files/image054.jpg" class="img-responsive center-block"></p>
