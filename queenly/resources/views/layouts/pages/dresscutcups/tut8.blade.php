<h3>Tutorial 8.&nbsp; Sewing Together the Face of the Cups.</h3>
<h4>Face Cup Pieces.</h4>
<p></p>
<p><strong>I sew together the face pieces along the curves with all cut edges evened out:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p>There are no tacks and no pins used!</p>
<p><strong>Having joined the curves I trim the seam allowances leaving about 7mm:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stay-stitch going along the face part of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>I spread the seam allowance (that hasn�t been pressed open yet) with my fingers and reach the corner of the bottom.</p>
<p><strong>I turn the garment face up and make a stay-stitch along the bottom of the second half of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p>Then I turn the seam allowance around to get an open seam, and finish at the corner.</p>
<p><strong>The resulting stay-stitches along the bottom of the cup look like this (the corner is exactly in the middle): </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%208_files/image010.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image997.jpg"></p>
<h4>Back Pieces.</h4>
<p></p>
