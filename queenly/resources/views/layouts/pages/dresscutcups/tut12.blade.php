<h3>Tutorial 12. Joining the Lace Pieces. Sewing a Zipper onto the Back.</h3>
<h4>Working with Lace.</h4>
<p></p>
<p><strong>First of all, I need to stitch up the darts on both back pieces.</strong> </p>
<p>I have previously marked the location of these darts with a thread and removed all sequins and beads hindering the sewing process.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I have already cut out the front lace fabric and there are no darts on it which means I can immediately lay it out over the front pieces. I lay it out very carefully, watching the bottom line. I even out the sides and let some lace off onto the cups by the length of a seam allowance. </p>
<p><strong>I secure the front lace fabric with pins and trim the side cut edges:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The question is: how come I�ve decided to stitch the lace onto the cups that have already been handled?</strong> It seems like the lace could be inserted into the cups, i.e. the face of the cups could be overlaid with lace fabric already stitched-on. And this is also correct, first of all, when you deal with chiffon or other drapery fabric. But in this case I would like the lace appliqu� on the cups to gradually blend with the main lace of the whole dress. I don�t like it when the joining line between the cup and the body can be seen through the appliqu� and there is no smooth flow from the appliqu� to the lace. Besides, the thickness of the cups would then form a kind of a ledge at the transition from the appliqu� to the lace. In theory it is technically possible to sew the lace fabric together with the main satin. </p>
<p><strong>I do exactly the same thing with the back pieces:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p>I arrange the lace pieces on the pieces of the main fabric, then secure them with pins and even out their cut edges. Due to the alterations made after the fit test the lace needs to be slightly trimmed. I transfer all notches onto the lace fabric, viz. the notches at the beginning and end of the zipper and the waistline notch. </p>
<p><strong>I sew-on the lace fabric of the front with a machine stitch going across the whole cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the lace on the back pieces:</strong></p>
<p>
