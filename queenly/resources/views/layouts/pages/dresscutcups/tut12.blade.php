<p><strong>I make an additional connecting stitch 3-4mm below the notch at the end of the zipper:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<h4>Sewing-on a Concealed Zipper.</h4>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></p>
<p>First of all I look for the waistline notch. The notch at the beginning of the zipper is 1.6 inches below it. I undo the zipper and place its corresponding side at the notch where it should begin. The seam allowance at the centre of the back is 0.8 inches. I fold the upper end of the zipper into a �hut-shape" to get it out of the way, and turn it down. I use a standard presser foot here. </p>
<p><strong>I sew the zipper into place with just a basic catch seam for now:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p>Thanks to the fact I secure the zipper in its place with a regular machine stitch - it will never go askew. Moreover, this will reduce the fullness on the fabric when I switch from a regular presser foot to a special foot for attaching concealed zippers.</p>
<p>I find the point where the zipper should begin on the second piece of the back. I turn the stitchwork around and <strong>sew-on the other side of the concealed zipper</strong> using the notches as guiding lines and folding its end into a �hut" like before.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></p>
<p>At this stage the hardest part of the process of attaching a concealed zipper is over for me. Now it is fixed in the right place. Upon zipping it up I check how well the pieces match.</p>
<p>Afterwards, I switch to a special presser foot for attaching concealed zippers.</p>
<p><strong>I stitch-on the zipper from both sides, close to its teeth:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></p>
<h4>Joining the Central Seam of the Back.</h4>
<p></p>
<p>Since there is lace, I am going to join the central seams step by step: first I am going to sew the central seam of the lace fabric, make cuts and bend seam allowances inwards, and then go on to the central seam of the main fabric. But first of all I need to stitch through a small area of the central seam (1.2-1.6 inches) catching both the main fabric and the lace. I start at the point where I finished attaching the zipper. <strong>I stitch over a small area using a one-sided presser foot with the left ski-shaped part and make bar tacks:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Having switched back to the regular foot I sew together the lace:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the seam allowance of the lace and bend it inwards:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%203/transcript/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I sew together the main fabric:</strong></p>
<p><strong><br>
