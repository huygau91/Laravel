<h3>Tutorial 9. Cutting a Lace Overlay for the Back of the Dress.</h3>
<p></p>
<p><strong>I fold the lace in half, put down the left half of the back made of the main fabric and the mesh, pin it down, cut a lace overlay after its contour and make all key notches on it.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
