<h3>Tutorial 7. Cutting the Dress.</h3>
<p></p>
<p>I would like to touch upon the cutting method we are going to use. There are certain things you need to be aware of. </p>
<p><strong>The front pieces of the dress:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I will cut the front pieces from the main and the lining fabric. </strong></p>
<p>I will cut them from <strong>batiste</strong> full-sized (down to the very bottom) since the fabric I've chosen for the dress is white and very thin and the shade of duplicated parts would otherwise differ noticeably from that of non-duplicated areas. </p>
<p><strong>Since the dress is made of separate pieces</strong>, it follows to duplicate each piece with batiste entirely. </p>
<p><strong>If you want to sew a long one-piece dress</strong>, keep in mind that you don't want to have a visible border between its duplicated and non-duplicated parts. Everything depends on the face fabric. Choose the colours accordingly. </p>
<p><strong>After sewing down the princess seam between the face pieces I will cut a single seamless lace overlay after their contour.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The bottom parts of the cup will be overlaid with lace while the top will stay smooth. &nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image005.jpg" class="img-responsive center-block"></p>
<p><strong>This lace piece will also be cut separately: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image007.jpg" class="img-responsive center-block"></p>
<p>I will cut <strong>the back pieces of the dress</strong> from the main and the lining fabric and duplicate them with batiste the same way (down to the very bottom). </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image009.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I will also sew down the princess seams between the back pieces</strong>, press them open, put the piece over the paper pattern of the top part of the dress, and cut two lace pieces after its contour. The back is supposed to be a single piece.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image011.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>All dress pieces are fully prepared for the cutting process. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>I lay them out on the fabric.</strong> I highly recommend duplicating the entire fabric with iron-on batiste before cutting out the pieces. If you can't do that for some reason, you will have to cut each piece from the main fabric and from batiste and then duplicate them one by one.&nbsp;&nbsp; </p>
<p><strong>I pin all pieces down onto my duplicated fabric.</strong> I've duplicated all of it straight away. The skirt will be 40cm long. As usual, I cut crosswise to achieve even vertical seams. I place the top parts of the cups parallel to the crosswise grain. Next I cut all pieces out.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image017.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>I have cut all pieces out</strong>, arranged them on the main and the lining fabric, made all notches and numbered the cup pieces. Now the key thing is not to get confused or forget anything! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to cut the pieces from the lace and from mesh fabric.</strong> </p>
<p>My <strong>lace</strong> is rather delicate and stretches quite well. On the one hand this kind of lace will cling to the body very well - on the other hand it is rather difficult to deal with. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>A great way out is to duplicate the lace pieces with additional mesh fabric.</strong> I have chosen mesh fabric that differs from traditional skirt bobbinet and from bridal veiling. It is special mesh fabric used in see-through corsets and in bodysuits. It is very transparent and stretches really well crosswise but a lot less well lengthwise. In other words it is very resilient. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>Such mesh makes a perfect backing for lace.</strong> It is usually available in black, white, and nude. </p>
<p><strong>Here's what the lace looks like when duplicated with a layer of white or nude mesh fabric:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image027.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image029.jpg" class="img-responsive center-block"></p>
<p>You should choose the colour of your mesh fabric depending on the effect you want to achieve and on your personal preferences. A white mesh backing will make the lace look chic and snow white while a nude mesh backing will add more contrast to the garment. The nude version can also help create the bare skin effect. After careful consideration I've decided to duplicate my lace with white mesh. </p>
<p><strong>Here is my working plan:</strong></p>
<p><strong>First I will sew down the princess seams between the face pieces of the back.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I will align a half of the back of the dress with the back pieces of its top part and cut a single lace overlay after their contours.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>Next I will cut the back halves of the lace inset from the mesh fabric.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>This mesh piece will be sewn to a half of the back of the dress and then I will overlay it with lace and sew in a zipper. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%207_files/image037.jpg" class="img-responsive center-block"></strong></p>

