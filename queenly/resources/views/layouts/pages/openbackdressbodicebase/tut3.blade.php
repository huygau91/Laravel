<h3>Tutorial 3. Altering the Standard Pattern.</h3>
<p></p>
<p><strong>I have printed out and glued together the sheets with the standard pattern.</strong> I don't trim the blank borders left by the printer. There is a thumbnail on every sheet that tells you which part of the pattern you're dealing with.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let us start applying the adjustments to the standard pattern. &nbsp;</strong></p>
<p><strong>The central front piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It needs to be narrowed down by 1cm at the bust middle level.</strong> </p>
<p>If not for the separately cut cups, we could have simply placed the fold line of the central front piece to the fold line of the fabric and narrowed the pattern by shifting it 1cm beyond the border of fabric and tracing the contour. That would make the bust middle measurement smaller. </p>
<p><strong>However our garment does feature separately cut sew-in cups.</strong> </p>
<p>If I shift the fold line of the central piece 1cm outwards, I will thereby move the cups closer to one another and reduce the distance between them to nearly zero. This is totally unacceptable here!</p>
<p><strong>Taking that into account let me suggest a non-conventional way of removing that extra centimeter at the bust middle level - with the help of the princess seam at the front. </strong></p>
<p><strong>Please note that you need to adjust the princess seam of the cup correspondingly whenever you alter this princess seam! </strong></p>
<p>Keep it in mind that you can only use a single fixed value, from top to bottom, for altering the princess seam of the cup. </p>
<p><strong>I remove 1cm at the bust middle level by shifting the princess seam of the front piece. And I re-draw the princess seam. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I shift the princess seam of the central cup piece 1cm inwards too. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Next, the side front piece:</strong></p>
<p><strong>I shift the entire side seam of this piece inwards by a fixed value - 0.5cm.</strong> I re-draw the side seam and transfer the notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The side length is now 2cm smaller than it was.</strong> I re-draw the top of the side front piece. </p>
<p><strong>I apply corresponding alterations to the side cup piece. </strong>I shift its top edge down by the same 2cm. Then the distance between the middle of the breast and the corner of the neckline equals 9cm (seam allowances excluded), which fully corresponds with the clip measurement.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Let's go back to the central piece. </strong></p>
<p><strong>I remove 2cm along the top and 1cm along the neckline on the central cup piece in correspondence with the clip towards neckline measurement. </strong>I re-draw the pattern.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>What we get instead of the typical arched cup shape is a straight inclined cut.</strong> The cup is straight. You can leave it that way if it agrees with the style of the garment. </p>
<p><strong>If you want the cups to be round</strong>, you should adjust the shape. There's an easy way to do it. I align the seam allowances of the side cup piece and the central cup piece and draw a line of the desired shape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I apply final adjustments to the central cup piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The side back piece:</strong></p>
<p><strong>I shift the entire side seam 0.5cm inwards. </strong></p>
<p><strong>I shorten the side by 2cm </strong>and re-draw the top even though it's not relevant in this particular dress.</p>
<p><strong>What concerns the princess seam, I shift it 1cm inwards at the bust-line level, 0.5cm inwards at the under-bust level, and 0.7cm at the hip level. </strong></p>
<p><strong>I re-draw the side back piece </strong>and make it a little more rounded towards the bottom.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The side back piece:</strong></p>
<p><strong>I shift the princess seam 1cm inwards at the bust-line level, 0.5cm inwards at the under-bust level, and 0.7cm at the hip level. I re-draw its contour </strong>and make the piece a little more rounded towards the bottom.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%203_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>We have adjusted the patterns of the front and the back of the dress. </strong></p>

