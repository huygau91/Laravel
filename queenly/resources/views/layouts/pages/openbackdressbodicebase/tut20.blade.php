<h3>Tutorial 20. Sewing the Cups into the Dress.</h3>
<p></p>
<p><strong>I start sewing in the cups. </strong>First I make a strengthening stitch.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I secure the ends of the bone in the central part.</strong> I simply sew it down with a single central seam.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and secure the cups with another raw of parallel stitches. </strong>&nbsp;These stitches catch the seam allowance inside the cup and fix the cup in the right place.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Both cups are sewn in now. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to pad them with polyester wadding.</strong> I pin the polyester down and stitch along the inner side of the bones along the entire perimeter. Then I remove the pins and trim the polyester. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>Look how neat the cups are. </strong>I hadn't padded them before sewing them into the dress because I wanted to overlay the perimeter seam allowances with soft padding polyester. It looks much neater.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Now let's work on the face of the cups. </strong>&nbsp;</p>
<p><strong>I start by making a stay-stitch along the entire perimeter of the cup using a seam allowance of 7mm and not the regular 1.2cm. </strong>The thing is the lining of the cups sewn into the dress grew larger when I covered it by polyester. So I narrow down the seam allowance of the face to make sure it will overlay the cup properly.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Besides the stay-stitch is also used to gather the cup a little and give it more volume. </strong>We did some easing in when assembling the lining of the cups and we need to do the same kind of easing in on their face. I pull the top and the bottom edge of the cup on the stay-stitch giving it a more embossed look and fitting it to the required size.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image062.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>There are two ways you can sew the face parts onto the cups. </strong>&nbsp;</p>
<p><strong>The first way:</strong> you can bend the bottom edge of the cup along the stay-stitch, press it, secure it with pins and stitch it down. But this means there will be a visible seam at the bottom of the cup.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>The second way: </strong>you can also align the stay-stitch on the face of the cup with the joining seam on the lining, make a blind seam and turn it out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I prefer the second option. </strong>&nbsp;</p>
<p><strong>I place a pin at the point where the joining seam between the lace part and the smooth part of the cup is supposed to arrive. </strong>&nbsp;I'll mark the same point on the other cup to achieve perfect symmetry. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I make a blind seam along the stay-stitch on the face of the cup aligned with the joining seam on its lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image074.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>And I fold the top edge of the cup inwards. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>That's it - the cup is sewn in place.</strong> I will finish the top edge with a narrow lace ribbon.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2020_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>Now you should do the same with the face of the other cup.&nbsp; </strong></p>

