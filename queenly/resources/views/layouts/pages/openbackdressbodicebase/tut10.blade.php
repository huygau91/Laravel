<h3>Tutorial 10. Joining the Lace with the Base.&nbsp; Sewing In the Zipper.</h3>
<p></p>
<p><strong>We need to sew a blind zipper into the back pieces pinned together with their lace overlays.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the lace off the seam allowance of the central back seam</strong>: this will help sew in the zipper and make the lace look neater in the area where it joins the opaque fabric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I join the lace and the base from top to bottom stitching at a presser-foot distance from the edge.</strong> The bottom edge doesn't require any special handling since we'll attach the skirt to it. </p>
<p><strong>Along the central cut:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Along the side cut</strong>: I also go along the lace duplicated with mesh fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Along the bottom edge: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And I join the lace with its mesh backing along the top edge.</strong> That minor fullness formed during the sewing process will ensure better fitting against the back. I remove the pins.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I have finished the first half of the back and now I can secure the zipper in its place. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I finish with the other half of the back following the same steps.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the other side of the zipper tape onto the other half of the back.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I want to place it exactly the same way as on the first half of the back. There is a notch made in the area where I stopped sewing down the first side of the zipper tape. I make a mark at the same level on the other side of the tape.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the other side of the tape making sure the mark I've made is aligned with the notch on this half of the back. Both sides of the zipper tape will be placed absolutely symmetrically then.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I switch to a specialty invisible zipper presser foot.</strong> </p>
<p><strong>And I start sewing in the zipper.</strong> Leave the lace detached at the top of the seam allowance.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Please note that the lace is not sewn onto the seam allowance.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>The transition from the main fabric to the lace will look like this after you bend all seam allowances back: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the other side of the zipper tape.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image064.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I close the zipper. It looks good! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I switch to a one-sided presser foot with the ski-shaped part on the left. </strong>&nbsp;</p>
<p><strong>I put together the seam allowances of the zipper, align the notches at the end of sewing, and pin them together.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I lift the extension of the zipper and pin down the seam allowances of the central back seam below the zipper.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image071.jpg" class="img-responsive center-block"></p>
<p><strong>I find the beginning of the seam that attaches the zipper and place the needle right at that point. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image073.jpg" class="img-responsive center-block"></p>
<p><strong>And I stitch down to the very bottom.</strong> If you feel that the stitch made by your one-sided presser foot is not good enough, stitch just a couple of millimeters and then switch back to the regular foot and continue. I always change feet when working with delicate fabrics.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image075.jpg" class="img-responsive center-block"></p>
<p><strong>I test the quality of the joining seam and the seam below the zipper.&nbsp; It's all good! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2010_files/image077.jpg" class="img-responsive center-block"></strong></p>

