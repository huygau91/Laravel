<h3>Tutorial 22. Applying the Final Touches to the Dress. </h3>
<h3>Pictures of the Finished Garment.</h3>
<p></p>
<p><strong>I have already done some finishing work that shouldn't raise any questions.&nbsp; </strong></p>
<p><strong>I have cut out a full sweep skirt with the length of 45cm and a semi-circle skirt for a small train. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The common radius (R) of both skirts is calculated the following way: </strong>&nbsp;</p>
<p>The length of the total skirt circumference (i.e. the full sweep skirt plus the semi-circle) should equal the hip circumference or the length of the bottom edge of the dress - 96cm.&nbsp; </p>
<p><strong>Therefore: </strong></p>
<p><strong>2π R + π R = 96cm</strong></p>
<p><strong>I find the radius (R): </strong></p>
<p><strong>R = 10cm</strong></p>
<p><strong>You can see the full lay for the skirts in the picture below. </strong>&nbsp;</p>
<p>The <strong>full sweep skirt </strong>is made of a whole piece of fabric that is 150cm wide and 150cm long. I will make a cut in the full sweep skirt (red line) to sew the semi-circle train into it. &nbsp;</p>
<p>The semi-circle piece is made of the same 150cm wide fabric. <strong>The length of the train</strong> depends on your client's preferences or on the dress model. I want mine to be 90cm long. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Total fabric consumption:</strong> 2.5m, where 1.5m goes to the full sweep skirt and 1m to the semi-circle skirt. I have joined the skirts into a single piece and added a lace hem at the bottom.&nbsp; </p>
<p><strong>Underskirt: </strong>the underskirt also serves as a lining. I have cut it out as a single full sweep skirt. </p>
<p><strong>You can see the lay for the underskirt in the picture below.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Its length is 60cm. </strong></p>
<p><strong>The radius (R) is calculated after the formula: </strong>&nbsp;</p>
<p><strong>2π R = 96cm</strong></p>
<p><strong>Therefore:</strong></p>
<p><strong>R = 10cm</strong></p>
<p><strong>The underskirt requires 1.5m of fabric. </strong></p>
<p><strong>Total fabric consumption: </strong>&nbsp;</p>
<p>- 2.5m for the skirt;</p>
<p>- 1.5m for the underskirt;</p>
<p>- 1.5m for the dress (face plus lining). </p>
<p><strong>This makes a total of 5.5.m.</strong> I recommend that you buy a little more just in case. Sometimes a dress like this requires a total of 6m or fabric or more. </p>
<p><strong>Other materials required for our dress: </strong></p>
<p>- 1m lace fabric;</p>
<p>- 0.5m see-through mesh fabric;</p>
<p>- 2m mesh fabric of medium stiffness (with the width of 3m). </p>
<p><strong>I have sewn three rows of ruffles onto the lining.</strong> The width of fabric straps for the ruffles was about 15cm each. I used mesh fabric of medium stiffness to make the skirt fuller. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I cut a rectangle out of the same medium-stiff mesh fabric. Its size is 3m by 60cm. I have gathered its longer side down to 96cm.</strong> It serves as an additional layer of the underskirt. This additional layer stops the ruffles of the underskirt from protruding through the face and gives it a smooth bell-like shape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I assembled the skirt in the following order: </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image012.jpg" class="img-responsive center-block"></p>
<p>First I assembled the full sweep ruffled underskirt, then I overlaid it with the mesh and topped it with one and a half of the full sweep face skirt. I joined the layers along the top edge, serged it, and attached the skirt to the bottom of the dress by machine. Then I decorated the joining seam with a lace border.&nbsp; </p>
<p><strong>I did no particular handling on the raw edges of the neck hole of the lace inset</strong> - I simply glued lace motifs along the perimeter. In my case they are flowers. I sewed hooks and eyes onto the closure at the back. I highly recommend that you should sew the hooks in the following order to prevent them from coming undone in wear: first place a hook on the left and an eye on the right, then place the next eye on the left and the next hook on the right, etc. This helps the hooks stay in the eyes and not come undone when the wearer moves her shoulders.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>There is some handwork embellishment left to do after the final fitting. </strong>&nbsp;</p>
<p><strong>I pin lace motifs onto the back right on the client. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image016.jpg" class="img-responsive center-block"></p>
<p>They look somewhat wrinkled at the moment. <strong>But they will get straightened out when the client puts the dress on.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I'm highlighting the fact that you should pin those lace motifs on when the garment is on the person. </strong>Otherwise you can never guess where exactly and how much you should stretch the garment to make them settle in a beautiful way later. Use a generous amount of pins - you need to secure the lace right at the very spot where it is situated.&nbsp; </p>
<p><strong>There are many ways you can attach your lace motifs: </strong>hand-stitching, gluing, etc. I attach mine with the help of Gutermann fabric glue. I don't glue all motifs at once but work on separate areas: I stretch a small area of the garment, put some packaging film underneath it so I can easily tear it off later, smear the specified area with glue, and stick the lace on. I add small flowers at the ends of the lace motifs for a more finished look.&nbsp; </p>
<p><strong>You should fold the area of the closure and secure it with hand stitches.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>You can also finish it with some flower appliqué.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I'll use hooks for the closure.</strong> I don't think snap fasteners would be robust enough because the dress has a really tight fit. </p>
<p></p>
<p><strong>I marked the desired skirt length during the fitting and added a lace hem down the bottom.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I had also serged the hemline of the skirt before adding the lace. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I recommend you additionally secure the top parts of the lace motifs on the skirt. </strong>&nbsp;You can either do it with the help of fusible web, or with Gutermann fabric glue, or even by hand-stitching - whichever way you prefer.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The process of framing the edges of the back with lace motifs is demonstrated in the pictures below. </strong></p>
<p><strong>I put some packaging film onto the sleeve-ironing board and stretch the desired area of the back over it with the help of pins.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins one by one, smear the revealed areas of lace with some Gutermann glue and press them down. </strong>Make sure the elements overlay one another correctly. I move along the entire pinned area of the back removing the pins one by one and gluing on the lace. You don't have to wait long because this glue sets really fast. Fabric glue like this really helps you spare a lot of time. Besides it doesn't affect the resilience of your fabric. If you stitched the appliqué on, the thread would likely strain the fabric and it wouldn't stretch as well in certain areas.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I leave it out to dry for a couple of minutes.&nbsp; </strong>It's really easy to take glued areas off the packaging film.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I pin down the next area of the dress </strong>and continue embellishing it.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>We have finished a half of the back! The second half should be embellished symmetrically.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%2022_files/image056.jpg" class="img-responsive center-block"></strong></p>
