<h3>Tutorial 4. Shaping the Top Part of the Front.</h3>
<p></p>
<p><strong>It's time to work on the pattern of the lace inset at the top of the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let me say a couple of works about this pattern.</strong> You can use it as is or adjust it as per usual. <strong>In case you want to alter the pattern radically,</strong> i.e. make it 4-5 sizes bigger or smaller, let me explain how to do that. There are transversal lines marked along the perimeter of the pattern and you can use them for reference to alter the size of the pattern. </p>
<p><strong>Bust circumference is the key criterion for increasing or decreasing the size. </strong>I provide you with two standard patterns that differ by 2 sizes: one has a bust circumference of 88cm (black line) and another - 92cm (red line). The distance between the red and the black line at the intersection with the transversal lines is 6mm, which means you need to mark 3mm towards one or another line on the transversal lines to make the pattern smaller or bigger (change the bust circumference by 2cm).&nbsp; </p>
<p><strong>Say, if you want to make a pattern with a bust circumference of 96cm, </strong>you need to mark 6mm outwards from the red line on each transversal line and then connect the points (blue dashed line). You will get an adjusted pattern with a 96cm bust circumference. </p>
<p>That is of course a very fast yet a not so precise method of adjusting the size of the pattern. You should make further adjustments using the traditional measurements taken for garments with shoulder seams: neck base to bust middle, shoulder tip to bust middle, nape to waist, shoulder to waist, shoulder slope, shoulder length, upper-arm circumference, armscye circumference, etc. In other words, you should compare and double check the pattern against your client's measurements. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I will use the pattern with the 88cm bust circumference for my dress (black line).</strong> My pattern is rather precise so I don�t need to double check it against my client's measurements. But I do need to improve the shape of the top piece to maintain overall harmony. </p>
<p><strong>I go through the patterns of the lace inset in the following sequence: &nbsp;</strong></p>
<p><strong>First I cut out the <em>adjusted</em> patterns of the central cup piece and the central front piece of the dress.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Then I align the seam allowances of the central cup piece and the central front piece and pin them together.</strong> By doing so you can see if you've adjusted the standard pattern correctly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I do the same with the adjusted patterns of the side cup piece and the side front piece of the dress. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Please note that the correct order plays a great role here.</strong> After altering the pattern of the lace inset I will also need to alter the shape of the cup. Right now our cup pattern is very basic and features a vertical princess seam. What we want is more fashionable 3-part cups. <strong>However we can only shape the cups after applying all adjustments to all other parts of the dress!</strong></p>
<p><strong>I align the cup with the cradle on the side front piece.</strong> NB: the cup is supposed to be larger than the front piece (0.7cm larger in my case) since we will ease it in by the joining seam. Besides, a small dart at the under-bust level of the side front piece will make it impossible to achieve perfect matching of the seam allowances on the cup and on the cradle. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the pieces together.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I take the front piece of the lace inset.</strong> It provides for a V-neck but we don't need it in our case. </p>
<p><strong>I prolong the central line upwards </strong>because we want to make a regular round neck hole. Moreover, the neckline should be slightly lowered unlike, for example, a plain crew neck. <strong>I re-draw the neckline (blue dashed line).</strong> Alterations like this depend on the overall style and individual preferences. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the side front piece of the lace inset following the black outline and glue it in place. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I align the dress pieces and the lace inset pieces along the side seam.</strong> I trace the top edge of the side front piece (blue dashed line) and draw the top edge without including the seam allowance (blue solid line). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I do the same with the central parts of the dress and the lace inset. </strong>I add a seam allowance at the bottom edge of the central piece of the lace inset (black dashed line).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the front top piece with seam allowances included.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>This piece will look like this after we assemble the garment:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></p>

