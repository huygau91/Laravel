<h3>Tutorial 5. Shaping the Back of the Dress.</h3>
<p></p>
<p><strong>I begin working on the back of the dress.</strong> I need to cut a mock-up to design a beautiful low neckline and determine adjustments to give a graceful shape to the entire back. </p>
<p><strong>I lay the adjusted back pieces onto mock-up fabric. </strong>I will only cut a half of the back or one copy per each piece - it's more than enough for working on the shape of the neckline. The seam allowances are included. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the back pieces from mock-up fabric</strong> and make notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I join them along the princess seam to make a mock-up half of the back of our dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the mock-up back onto the dress-form.</strong> Pay attention to the waistline and the area above it.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a beautifully shaped neckline with a seam allowance included.</strong> It's a rather low neckline: I step 5-6cm down from the middle of the waistline, nearly to the notch at the beginning of the zipper. The neckline can be triangular, oval or egg-shaped. Let me point out that such things are best done on a 3D dress-form. Sometimes you can't achieve as good results on the table surface.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I take the back piece of the lace inset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the side back piece and cover up the dart.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I take the mock-up back piece off the dress-form and pin it onto the patterns along the contour of the neckline that shows through the fabric. I account for the seam allowances, too. </strong>The length of the side is exactly as needed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the fabric along the neckline preserving the seam allowance</strong> and remove the bottom part. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Here are the back pieces of our dress:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I continue working on the back of the lace inset at the top of the dress.</strong></p>
<p><strong>I transfer the contour of the mock-up back onto the paper pattern of the top part of the dress, add a seam allowance, and draw the actual border of the top part of the dress (green line).</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the mock-up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And add a seam allowance for the joining seam.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a beautiful contour for the lace inset at the back.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>You can design the shape of the top neckline based on your own taste.</strong> I have decided to make it somewhat lowered and not a regular crew neck. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>A half of the lace inset at the back of the dress will look like this when sewn together:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I add seam allowances:</strong> 0.5cm for the armscye and the neckline, and 1.5cm for the shoulder seam. The seam allowance of the side seam is included in the pattern and I've already marked the seam allowance for attaching the piece to the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the back piece of the lace inset with seam allowances included and cover up the dart.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>The pattern of the back of the lace inset is ready! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image040.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let's return to the back pieces of the main part of the dress.</strong></p>
<p><strong>I take the mock-up to pieces, put them over the corresponding pieces of the back of the dress, match their notches and trace the outline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut the pieces out.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The pattern of the back of the main dress is ready! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image049.jpg" class="img-responsive center-block"></strong></p>
<p><strong>We should check if the back pieces of the dress match those of the lace inset.</strong> I align them along the seam allowances of the princess seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>Looks like a perfect match!</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%204/transcripts/Tutorial%205_files/image053.jpg" class="img-responsive center-block"></strong></p>

