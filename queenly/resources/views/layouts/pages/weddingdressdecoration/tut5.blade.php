<h1>Tutorial 5. Sewing Lace Motifs onto the Garment.</h1>
<p></p>
<p><strong>I pin the dried lace motifs on the corset.</strong> I had re-pinned them several times before I was fully satisfied with the layout. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p>I start by pinning large fragments and then fill in the gaps with smaller ones. <strong>I look for empty areas and add separate small fragments of a corresponding size.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>They are only pinned down for now so I can still rearrange or redistribute them at any moment. It's just a test.</p>
<p><strong>I have prepared long narrow lace motifs for the bottom edge of the corset:</strong></p>
<p><strong>&nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The side looks like this:</strong> the closer to the back, the smaller the motifs.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p>Even on this test version <strong>you can see the difference in the shine </strong>and the effects produced by pearl beads, short and long bugle beads. You can also see how the iridescent seed beads look on the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p>For comparison, here is how plain lace would look against the same white background. See? It makes a huge difference! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I will demonstrate how to sew on a motif by working on the bottom edge of the corset.</strong> </p>
<p>I sew it on with small stitches making sure the thread does not get tangled. The stitches are made at about 1cm intervals. </p>
<p>Never try to pierce the garment through! It is very hard and there is absolutely no need in it. <strong>I only catch the top layer of fabric, i.e. the face.</strong>&nbsp; </p>
<p><strong>Basically, the key task is to secure the contour of the lace motif</strong> but you can also secure the inner areas if you want to. I try to go through the entire element. I sew the rest of the motifs on the corset just the same way.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>

