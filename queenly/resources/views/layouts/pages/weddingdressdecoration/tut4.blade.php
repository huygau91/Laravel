<h1>Tutorial 4.&nbsp; Dissolving the Fusible Interfacing.</h1>
<p></p>
<p><strong>The embroidery is finished:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>I prepare some very warm if not hot water and remove the excess of fusible interfacing. I will also need a towel. It should be white or at least made of fabric that won't leave stains on white lace. </p>
<p><strong>I dissolve the fusible interfacing in the hot water. </strong>It takes just five minutes.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I put the motifs on the towel and leave them out to dry for about half an hour: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Once the towel has absorbed the main moisture, <strong>I put the motifs on a white plate and leave them there overnight </strong>so they can dry out completely. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Now all knots tied at the back are secured by the interfacing. After drying out, the motifs will be stiffer. </p>
<p>And then I can secure them on the corset.</p>

