<h1>Tutorial 3. Embroidering Lace Motifs.</h1>
<p></p>
<p>I arrange lace motifs on a piece of fusible interfacing and secure them with pins at the sides and, if necessary, in the middle. Two pins are usually enough to secure a motif. I will use Tex 45 polyester-linen thread. </p>
<p><strong>I look at the motifs and look for a good way to embroider them.</strong> There are flowers on my lace motifs. I will put a pearl bead into each round petal in the middle of the flower and decorate it with short bugle beads and seed beads along the decorative thread lines. All other lines will be embroidered as rows of stem stitches with seed beads, short bugle beads, and long bugle beads. I will use a single long bugle bead for the smallest leaves, etc. </p>
<p><strong>I take a two-ply thread, tie a knot, and make the first stitch:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image001.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a large pearl bead in the middle of the petal: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image003.jpg" class="img-responsive center-block"></p>
<p>I have large and smaller pearl beads at hand. <strong>I sew a smaller pearl bead in the middle of the next petal.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I will alternate between large and smaller beads for some diversity.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p>My right hand is responsible for holding the needle, bringing it down in the right place, and making neat stitches. I hold the embroidery hoop in my left hand and secure the thread with it whenever needed: I hold the thread with my left hand if I notice that it's likely to get caught on a pin or on the motif itself when I'm drawing up. It's the fastest way! If you are left-handed, you will naturally do it the other way round. </p>
<p>I alternate between larger and smaller pearl beads. <strong>The middle of the flower is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>Using the same thread, I start embroidering the first petal with seed beads: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image013.jpg" class="img-responsive center-block"></p>
<p>I dip the needle into the container and pick up the required amount of seed beads (4-6 is enough). </p>
<p><strong>I fill the petals of the flower one after another. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image015.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image017.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image019.jpg" class="img-responsive center-block"></p>
<p>Hold the thread with your left hand if you notice that it is likely to get tangled. I lay the stitches along the decorative thread lines on the petals. If the outer edge of the motif starts breaking up, which can happen when you accidentally damage it when cutting out the motif or when the appliquï¿½ was originally made of two parts, then you should use some fabric glue to put it back together. </p>
<p><strong>I finish filling in the first petal, leave a small gap, pass onto the next one, and continue embroidering with satin stitches:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image021.jpg" class="img-responsive center-block"></p>
<p>I remove the pin because itï¿½s only standing in my way now. The stitches hold the appliquï¿½ in place and I don't need to secure it anymore. </p>
<p><strong>My thread is running out.</strong> </p>
<p><strong>I tie a good knot at the back.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>And then I insert the needle in the lace by about one centimeter:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>And I hide the thread tail in the lace: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>NB:</strong> I hide it in the lace and not in the fusible interfacing because the latter is going to dissolve! In this case, nothing will ever happen to the knot and the embroidery won't come undone - it will be neat and you won't be able to see any thread tails. </p>
<p><strong>I continue embroidering the flower following the same technique:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Let's go ahead. <strong>Now I will show you how to embroider a leaf of a rather complicated shape.</strong> There are several ways you can do it and I will use them all in turns. </p>
<p>I pick up a long bugle bead. Long bugle beads are probably the only kind of beads you cannot pick up very fast. <strong>I make the first stitch in the direction of the thread and then continue embroidering by bringing the needle out next to the middle of the previous stitch with a shift so as to make a kind of stairs.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I continue making long bugle bead stitches along the decorative threads with a stair-step shift against each other.</strong> All you need to do is shift the bead against the previous one. You don't even have to bring the needle up next to its middle: you can bring it up closer to its end. Just make sure it doesn't look like a messy bunch of bugle beads piled up in one spot. Long bugle beads sparkle particularly nicely when spaced properly. I will embroider the same kind of leaves with seed beads and short bugle beads and you will see that they produce different effects. They will look different, shine differently, and make the garment look more sophisticated. </p>
<p><strong>I have finished decorating the leaf with long bugle beads: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I move on to a pair of tiny leaves.</strong> I will decorate them with long bugle beads as well. <strong>Please note: I bring the needle down and up right through the thread of the motif</strong> because doing it through the interfacing is risky. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image033.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I bring the needle down and back up right through the thread. </strong>It's the only way to secure the beads safely.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>I return to the initial position.</strong> The long bugle bead is secured very well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a long bugle bead onto the second leaf just the same way.</strong> Again, I bring the needle up right through the thread at the bottom of the leaf (it takes a really sharp needle), pick up a long bugle bead, and bring it down through the thread. Then I return to the initial position. </p>
<p><strong>I have embroidered the pair of tiny leaves.</strong> Thanks to this technique, there is a chance my embroidery won't come apart after the fusible interfacing dissolves in water. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image038.jpg" class="img-responsive center-block"></p>
<p>Next I return to the flower. I bring the needle over the embroidered area from the back of the motif to make sure the thread won't show through after the interfacing dissolves. </p>
<p><strong>I have finished the flower and now I will decorate the paisley detail:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I bring the needle out at the corner, pick up the beads, and bring it down at about 2mm from the edge to leave room for the next stitch.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image041.jpg" class="img-responsive center-block"></p>
<p>I continue making the same kind of stitches until I reach the turn. After reaching the turn, I continue stepping 2mm from the edge for each next stitch. You can use fewer beads in each stitch if you need to make a difficult sharp turn - it will be easier this way. I keep stepping 2mm from the edge. You can fix the beads in place with your fingers if they shift around. There is little room at the sharp turn, so I pick up fewer beads and finish the paisley with a small stitch. </p>
<p><strong>Here is the paisley I have embroidered: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>The next element is a leaf of a complicated shape:</strong> </p>
<p>I will follow the convex line and fill the leaf with <strong>a row of stem stitches with short bugle beads.</strong> I pick up five bugle beads at a time. <strong>I make the first stitch:</strong> I bring the needle out through the thread of the motif and then bring it down through the same thread. To make the second stitch, I bring the needle out next to the middle of the previous stitch, right below it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image045.jpg" class="img-responsive center-block"></p>
<p>I <strong>embroider a row of stem stitches</strong> following the tradition technique. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I move to the next element, which I want to decorate with long bugle beads.</strong></p>
<p><strong>I arrange the first bead diagonally:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image048.jpg" class="img-responsive center-block"></p>
<p>And I make the next stitches with a stair-step shift following the motif. You can make larger or smaller intervals between the stitches depending on the amount of beads at hand and on the budget of the project. Actually, there are never too many beads in this case as long as they are well-matched and there are no large pearl beads or uneven seed beads among them. If all components are really small, everything will look lovely and elegant! <strong>I finish my row of stem stitches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I embroider the adjoining smaller leaf with seed beads:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image051.jpg" class="img-responsive center-block"></p>
<p>Most of the piece will be decorated with stem stitches. They will create different effects from a distance. You can use small pearl beads instead of long bugle beads to embroider the remaining elements of the lace motif. It's all up to you. </p>
<p><strong>I have approached the second flower. I will fill the petals with short bugle beads </strong>instead of seed beads like on the first flower. I will do it the same way: moving from the centre to the edges with 4-5 bugle beads in each stitch. I believe you know what to do if your thread runs out. The petals should be filled in very densely. I remove the pin to prevent the thread from getting caught on it. I want to make sure the thread wonï¿½t get tangled. If it does happen, I will tie a knot, cut the thread, and secure the tail. You could even glue the tail down to make it safer. It's the same algorithm as always, nothing difficult. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image053.jpg" class="img-responsive center-block"></p>
<p><strong>Here is the next element I am going to embroider.</strong> I make the first stitch with five short bugle beads following the contour. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image055.jpg" class="img-responsive center-block"></p>
<p>Next I fill in the leaf. I pick up the beads by dipping the needle into the container, remove the excess, and follow the outer contour of the motif. When finished, I tie a knot at the back. I leave a tail of about 1cm and tuck it in the lace. <strong>Here is the finished fragment:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image057.jpg" class="img-responsive center-block"></p>
<p><strong>Now I will decorate the middle of the flower.</strong> I alternate between larger and smaller pearl beads sewing them on in the round. <strong>The middle of the flower is ready!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>I move to the petals. </strong>&nbsp;These petals will be decorated with seed beads: about 5-6 per stitch. I make seed bead stitches following the decorative threads in the lace. <strong>The first petal is finished.</strong> The rest are embroidered following the same principle.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>The next element will be embroidered with seed bead stitches in the round.</strong> It's really nice to work with small seed beads: they are easy to pick up and they settle down evenly on the surface. I will embroider another paisley element following the stem stitch technique. If necessary, I will reduce the number of beads in each new stitch when I reach the turn. And I will make two stitches to cover up the middle. </p>
<p><strong>I make the first stitch:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>The second stitch is made next to the middle of the previous one in keeping with the classic stem stitch technique. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image064.jpg" class="img-responsive center-block"></p>
<p>And again, I bring the needle out from the outer side of the previous stitch and start gradually making a turn. As for now, I keep picking up six beads for each stitch. </p>
<p><strong>Here comes the beginning of the challenging area.</strong> I pick up five seed beads and bring the needle down really close to the previous stitch to make a neat turn. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image065.jpg" class="img-responsive center-block"></p>
<p>I bring it back up next to the middle of the previous stitch, pick up four seed beads, <strong>bring it down really, really close to the previous stitch, and bring it back up next to the middle of the stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image067.jpg" class="img-responsive center-block"></p>
<p><strong>I finish the row. </strong>This element was embroidered in the round.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%203_files/image069.jpg" class="img-responsive center-block"></p>

