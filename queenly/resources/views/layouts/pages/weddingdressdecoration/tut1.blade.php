<h1>Tutorial 1. Decoration Materials.</h1>
<p></p>
<p><strong>You can see the decoration materials I plan to use in the picture below:</strong></p>
<p>1. Embroidery hoop, </p>
<p>2. Sewing needles, </p>
<p>3. Pins, </p>
<p>4. Handy scissors, </p>
<p>5. Long Japanese bugle beads, </p>
<p>6. Seed beads, </p>
<p>7. Short bugle beads, </p>
<p>8. Glass pearl beads, </p>
<p>9. 50LB nylon thread, </p>
<p>10. Tex 45 polyester-linen thread, </p>
<p>11. Fusible interfacing, </p>
<p>12. Lace fabric. </p>
<p>First of all, I would like to show you the seed beads and pearls I have prepared and explain what made me choose these very types. All of them are the most expensive kind made in Japan. </p>
<p><strong>These, here, are Japanese bugle beads</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image002.jpg" class="img-responsive center-block" alt="1.1.00_01_24_22.Still002"></p>
<p><strong>You can see how iridescent and sparkling they are:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image004.jpg" class="img-responsive center-block" alt="col-005 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_5""=""></p>
<p>Long bugle beads make particularly pronounced embroidery on fabric and are known for their distinct shine. </p>
<p><strong>Traditional seed beads</strong>.</p>
<p>These seed beads are white with a light iridescent finish. Their shine is somewhat milder as compared to that of long bugle beads, which also looks very elegant.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image006.jpg" class="img-responsive center-block" alt="col-007 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_7""=""></p>
<p></p>
<p><strong>Short bugle beads.</strong></p>
<p><strong>What are short bugle beads?</strong> </p>
<p>They are basically long bugle beads cut into smaller pieces. Short bugle beads tend to sparkle a little more than long ones since they have more planes. It does look different on the garment which is why I use both short and long bugle beads. They create different effects. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image008.jpg" class="img-responsive center-block" alt="col-009 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_9""=""></p>
<p><strong>I will also use high-quality small glass pearl beads of two sizes</strong></p>
<p><strong>There is one thing you should remember when choosing pearl beads:</strong> always take a sample of your lace fabric with you when shopping for accessories for your dress or corset. There are countless shades of white: lighter or darker, a little creamier, a little more ivory, etc. It's very easy to choose pearl beads of a wrong shade: they may look white in the store but when you bring them home they may turn out to have a light hue and make your corset look as if it were dirty. It's why you should always make sure the beads match the lace perfectly before actually buying them. </p>
<p>It is basically impossible to find matching pearl beads for that white fabric with a very delicate blue hue which is often used in wedding dresses. I simply use plain white pearl beads in this case and it works quite well. </p>
<p>If the dress happens to have a distinct hue (warm creamy white, for example), <strong>then I look for pearl beads of a perfectly matching shade. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image010.jpg" class="img-responsive center-block" alt="col-011 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_11"=""></p>
<p><strong>I have mentioned all major types of decoration materials: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image012.jpg" class="img-responsive center-block" alt="col-012 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_12"=""></p>
<p><strong>Let's talk about embroidery thread now. </strong></p>
<p>I work with two types of thread. Let me tell you about the pros and cons of each one. </p>
<p><strong>You are looking at 50LB nylon thread (left) and Tex 45 polyester-linen thread (right): </strong></p>
<p>Both types of thread are tear-proof and each has its own pros and cons. </p>
<p><strong>The nylon thread has the following advantages:</strong> it usually looks very good on fabric when used in embroidery. It is great for sewing on rhinestones because it doesn't tear even if you make multiple stitches from each end of the rhinestone. Nylon thread is a perfect choice for working with rhinestones!&nbsp; Besides, it stays robust for years, which makes it a great embroidery thread. <strong>The key disadvantage of using nylon thread</strong> is the fact that knots made in it may eventually come undone. Sometimes, it happens right during the sewing process which is why I highly recommend you should tie two or three knots and draw them very tight. It will be a great pity if you complete some significant embroidery work on the garment and it just starts coming undone at some point. That is probably the only disadvantage of working with this type of thread: apart from that, it's just perfect! </p>
<p><strong>The polyester-linen thread has the following advantage: </strong>it doesn’t seem to get tangled as easily. Usually, I use a not so long piece of thread and try to prevent it from tangling with my left hand in which I am simultaneously holding the embroidery hoop. It is a lot easier with polyester-linen thread − it's really comfortable to work with. Slippery nylon thread tends to get tangled faster and it takes more skill for comfortable embroidering. <strong>What are the disadvantages of the polyester-linen thread? </strong>&nbsp;It is as tear-proof as the nylon thread but after some time you might notice that the knots are somewhat darker than the lace even though you had chosen a perfectly matching shade. It seems like the thread goes darker after being in contact with your hands no matter how squeaky clean they are. <strong>And it's why with the polyester-linen thread you must only tie knots on the inside of the garment!</strong> I try my best to avoid tying any knots on the face. This way the thread won't show anywhere and won't spoil the look of the garment. </p>
<p>I suggest you should try out the two types and make your own choice. </p>
<p><strong>Fusible interfacing is incredibly helpful in solving decoration tasks.</strong></p>
<p>It has an amazing property - it dissolves in water. If you embroider something on a piece of fusible interfacing and then put it in warm water, the interfacing will dissolve and vanish completely. I know it from personal experience that it takes a lot of time to cut out lace motifs after you embroider them. But what I do is secure a piece of fusible interfacing in the embroidery hoop, embroider the desired lace motifs on it, and then simply put it in water to dissolve the interfacing instead of cutting them out. It is about 20% more time-efficient. All you need to do next is wait for the motifs to dry out. Such sparing of time allows you to take on more embroidery projects and increase your earnings. </p>
<p>Fusible interfacing is sold in numerous DIY-shops and online stores. </p>
<p>And now I would like to talk about the lace fabric and its individual fragments with which I will work. </p>
<p>I want to explain my algorithm for cutting out lace motifs. You will see how important it is to do it with great accuracy and what fine points must be kept in mind. </p>
<p>I start by determining roughly how many large and small elements I will need to decorate my corset. I cut out some more just in case. Then I pin them onto the corset to get the overall design concept. </p>
<p>With manicure or any small handy scissors, I carefully cut the fragments following the contours on the face side <strong>and leaving no mesh along the outer edges:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image014.jpg" class="img-responsive center-block" alt="col-017 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_18"=""></p>
<p><strong>There are areas with mesh fabric inside the fragments and I leave them as is. Because </strong>if I remove it, the motif will start coming apart in the areas where mesh fabric used to hold it together and it will be incredibly difficult to sew it on the corset. You will have to secure it in many spots. In other words, the less mesh I remove, the fewer spots I will have to stitch through. </p>
<p><strong>You should therefore stick with the happy medium:</strong> I cut a motif out following the outer contours and preserving the mesh in those areas where it obviously holds the motif together. It won't stand out against white background: all attention will be drawn to the embroidery. And it will really help you spare some time on sewing these motifs on the garment. </p>
<p><strong>Please take a look at the picture. The fragment is cut out along the outer perimeter</strong> and I have carefully removed the mesh around the small leaves. Yet <strong>I have also preserved rather large areas of mesh.</strong> They will hold the fragment together and help me sew it onto the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/1/Tutorial%201_files/image016.jpg" class="img-responsive center-block" alt="col-018 v:shapes=" _x0420__x0438__x0441__x0443__x043d__x043e__x043a__x0020_19"=""></p>
<p><strong>I cut out a set of motifs</strong> according to my design concept, as well as several additional motifs so I can play around with the layout. </p>
