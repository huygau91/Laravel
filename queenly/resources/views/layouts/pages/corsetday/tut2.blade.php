<h2>Tutorial 2. Sewing Face Pieces Together. Stitching-on Bones. Pressing.</h2>
<p><strong>Now I start working on the pieces of the face side of the corset. </strong></p>
<p><strong>I am sewing together the vertical curve lines of the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image002.jpg" alt=""></p>
<p>I am matching the pieces accurately together and joining them with a 0.5 inch seam. I don�t cut off the thread but use one single seam line to make the sewing process go faster. I cut a small corner off the seam allowances at the beginning and the end of each curve line.</p>
<p><strong>I am joining together the bust curves: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image004.jpg" alt=""></strong></p>
<p>I would like to draw your attention to the fact that the seam allowances made when joining the lining pieces should fully coincide with the seam allowances I am now using to join the face pieces. Besides I should explain why exactly I am using a 0.5 inch allowance in this case. A wide Rigilene bone is 0.5 inch wide or maybe a little narrower. And when I am stitching on such a wide Rigilene bone to a seam allowance the outer seam will be going over the edge of the seam allowance thereby stitching this seam allowance on. This way the seam allowance won�t crease when supporting plastic bones are being pushed under these wide Rigilene ones. This is a very important aspect that should be kept in mind.</p>
<p><strong>I am stitching together all the front pieces one by one. Then I trim the corners of the seam allowance on a slant. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image006.jpg" alt=""></strong></p>
<p><strong>And now it�s time for some pressing. </strong></p>
<p>I press open the back curves placing them on the edge of an additional sleeve pressboard. It is very handy when you can place the curve on the pressboard edge like this and spread the seam allowances.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image008.jpg" alt=""></p>
<p>Please note that I am using a regular household iron but equipped with a steam generator.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image010.jpg" alt=""></p>
<p>There is no need in buying a super powerful professional press iron for making a corset like this. For this purpose it is enough to use a semi-professional or a household iron with a steam generator.</p>
<p><strong>The bottom bone of the right side of the lining and the spot where the bones are placed for the lacing need to be pressed open: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image012.jpg" alt=""></p>
<p>As I was attaching this Rigilene bone it was bent but it gets really well spread with the help of the iron and acquires the desirable shape. I am also pressing the upper bone as well as the boning in the underarm area and give them a finished look. Rigilene bones are very flexible when you use an iron.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image014.jpg" alt=""></p>
<p><strong>Now I am taking a cushion and a cup pressing mould, and I am placing the curve onto the mould: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image016.jpg" alt=""></p>
<p>I draw and straighten it slightly and form a pretty rounded cup by working with the iron in a circular motion. I am using the steam generator all along. This is necessary: every touch of the iron is accompanied with steaming. I let the pieces cool down so that the shape could set.</p>
<p><strong>I press open all the seams of the face side of the front corset piece: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image018.jpg" alt=""></strong></p>
<p><strong>I always use the cup pressing mould for pressing the bust curves: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image020.jpg" alt=""></strong></p>
<p>I put the cushion underneath so that the garment is lifted above the pressboard and press open the seam allowance in a circular motion while shaping the cup itself. Please note that I am sewing the whole corset without any additional fusing. This technique is obviously suitable only for dense fabric with a close weave - such as dense linen, blended fabric or denim.</p>
<p><strong>It is best to press open the curve line along the face side as well: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image022.jpg" alt=""></strong></p>
<p><strong>This is an important step: I am starting attaching the Rigilene boning to the face side of the corset. </strong></p>
<p>I have originally made a marking on every seam 0.6 inch from the edge of the garment. This is going to be the start and end point of the stitching-on of the bone. In other words I stitch at a 0.6 inch distance from the top and the bottom. Every bone should be necessarily taped over by masking tape and sewn with two stitches. I have put a decorative contrasting thread onto the spindle as normally done for stitching jeans.</p>
<p><strong>Back pieces:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image026.jpg" alt=""></strong></p>
<p>I am stitching-on the bone in such a manner that its edge touches the seam that joins the pieces together.</p>
<p><strong>Front pieces:</strong></p>
<p>And exactly the same way I mark the start and end points of the stitching-on of the bones leaving some 0.6 inch space at the edge. The edges of the bones are always taped over by masking tape and sewn with two stitches. I arrange the bones on the piece the way I want. They can be placed on the right or on the left from the curve line, or you could stitch-on two Rigilene bones - both on the right and on the left.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image028.jpg" alt=""></strong></p>
<p><strong>NB! :</strong> I am not using any bar tacks. I start stitching-on a bone at one edge of the corset and finish at the other edge without bar tacking.</p>
<p>These Rigilene bones are going to carry out both a securing function and the function of a tunnel or a guiding line for the supporting plastic bones. Corsets like this sewn in a simplified technique need some additional support; otherwise they will bend, crease and break.</p>
<p>Each time I cut a Rigilene bone right on spot. I wouldn�t recommend you doing it in advance. The fabric underneath the bone could stretch or gather and you could make a false guess concerning the length of a bone cut in advance.</p>
<p>You could also sew a corset made of thinner fabric using this technique, but in that case you would need to fuse the face and lining pieces with some iron-on fabric for extra strengthening.</p>
<p><strong>I have attached vertical wide Rigilene bones to all the curves: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image030.jpg" alt=""></p>
<p>Now I am checking how well the bones have been sewn-on at the bust area. The fabric underneath the bone should be neither eased in nor stretched too much or else the curve will protrude.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image032.jpg" alt=""></p>
<p><strong>After stitching-on all the bones of the face side I am joining the side seams using a 0.5 inch seam allowance: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image034.jpg" alt=""></p>
<p><strong>It is necessary to press the side seams - I am using a pear-shaped mould for this purpose: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image036.jpg" alt=""></p>
<p>A pear-shaped mould is a simple tool for pressing side seams, curves with a bend, necklines and other cuts. It can be handmade using available materials: a skittle, a toy or maracas. It is enough to wrap some padding polyester around it and fuse over with some iron-on material and - voila - the side seam pressing tool is ready!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image038.jpg" alt=""></p>
<p>However tools like this are of no great importance for the pressing of this particular garment. You could also press on the surface of a pressboard or on the edge of a sleeve pressboard. But you can�t do without additional pressing tools when creating a more complicated garment. And I would say even for this garment it is rather hard to manage without a cup pressing mould: the bust shape won�t be as beautiful.</p>
<p><strong>I am pressing the cup from the face side the same way I did it from the lining side: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image040.jpg" alt=""></p>
<p><strong>I let the garment cool down from time to time so that the shape of the bone could set: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image042.jpg" alt=""></p>
<p>An iron with a steam generator helps give Rigilene bones the required rounded shape. I am pressing in a circular motion, always using the steam. I adjust the temperature of the iron on spot. It shouldn�t be too hot but steam should be easily generated. It is desirable to use an iron with a Teflon bottom or an additional Teflon cap. I remove extra moisture with a small pad.</p>
<p><strong>This is what the face side of the corset looks like after it�s been pressed: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image044.jpg" alt=""></p>
<p><strong>Now I am attaching Rigilene bones to the side seams: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image046.jpg" alt=""></p>
<p>Let me remind you that I start stitching-on the bones leaving at least a 0.6 inch gap from the edge. Despite there being a 0.5 inch seam allowance from the top and bottom I still leave some additional millimeters for the thickness of the fabric when it is bent or folded. I tape the edges of the bones over with masking tape. I place the bone as close as possible to the joining seam and sew it on with two stitches. The side seam has a bend that is why I need to check how well the bone lies. If I notice a fault I have to immediately rip and re-stitch it.</p>
<p><strong>I am stitching-on another parallel Rigilene bone to the second seam allowance: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image048.jpg" alt=""></p>
<p><strong>This way the side seam holds its shape better and looks more decorative from the face side: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image050.jpg" alt=""></p>
<p>Now I am checking how well the bones have been sewn-on again. Remember that I am not making any bar tacks because they don�t look good from the face side.</p>
<p><strong>I am repeating the whole procedure with the second side seam. </strong></p>
<p><strong>I make special bar tacks along the front under-bust curve line: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image052.jpg" alt=""></p>
<p>When making bar tacks I look to the notches made on the pattern at the under-bust line level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image054.jpg" alt=""></p>
<p>These bar tacks or horizontal seams will put bounds to the tunnel and won�t let the supporting plastic bone go up along the cup. According to my design I want the embossed curves to reach only the under-bust level while the cup should stay smooth.</p>
<p>I trim the threads accurately and draw the decorative seam threads from the face side to the inside. You could tie these threads in a knot or - the way I do it - glue them with some Guterman fabric glue. You could also both tie and glue to prevent the stitch from going undone. After the glue has set you can trim the edges neatly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image056.jpg" alt=""><strong>Now after I have stitched-on 2 parallel wide Rigilene bones to the side seams they need to be thoroughly pressed and nicely shaped. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image058.jpg" alt=""></strong></p>
<p>I am using the pear-shaped pressing mould.</p>
<p>I am pressing in a circular motion always with some steam until the Rigilene bone is bent in a required way and the side takes the desirable rounded shape. I remove extra moisture with a small pad. It helps the garment dry and the shape set faster.</p>
<p><strong>The side starts looking like a proper corset side on the pressing mould: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/2_files/image060.jpg" alt=""></strong></p>

