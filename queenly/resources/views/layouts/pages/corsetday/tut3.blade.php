<h2>Tutorial 3. Joining the Face and Lining of the Corset.</h2>
<p>So the face and lining pieces have been fully sewn and are ready to be joined together. I am starting by joining the lining with the face along the top cut edge of the corset. I haven�t put any supporting Rigilene bones along the lining of the corset top at the level of the neckline and that is why the neckline won�t be perfect. The Rigilene bones that have been stitched-on along the curves are going to slightly distort the neckline. For this reason I would recommend choosing designs with straight neckline cuts (for example strapless or crew neck designs) when working in this simplified technique.</p>
<p><strong>I join the pieces of the face and the lining along the cut, even out the cut edges and pin them together:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image002.jpg" alt=""></p>
<p>I am joining together the curve line seams avoiding mismatching of the pieces. I recommend using as many pins as possible. If you feel like making the stitch could be a trouble - add some more pins. Do not spare the pins otherwise when you start sewing the corset yourself you will feel how much the bones are in the way.</p>
<p><strong>I am placing the pinned together pieces under the machine and joining them along the top cut with a 0.5 inch seam allowance: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image004.jpg" alt=""></p>
<p>I am stitching over the lining where a strengthening stitch has been made, which can be used as a perfect guiding line. I am trying to stitch exactly onto this strengthening stitch thereby keeping the original shape of the pattern used for the cutting. This way I get a straight, even and symmetrical cut despite the bones being in the way and distorting the seam. I make a bar tack for a seam like that at the intersection point of the strengthening stitches.</p>
<p>I am turning the garment face upwards to check how the stitch lies against the vertical Rigilene bones attached to the curves.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image006.jpg" alt=""></p>
<p>This stitch should be symmetrical from the right and the left side of the curve, and the seam allowance should be maintained precisely. Upon noticing any fault it is better to rip off the corresponding spot of the stitch and re-stitch it. <strong>If the stitch goes over the surface of the Rigilene bone you won�t be able to turn the corset inside out! </strong></p>
<p>I take an organza ribbon that is around 0.8-1.2 inch wide. I place the ribbon at the edge of the seam allowance and stitch-on from the lining side along the same stitch I have made along the cut. You can use the previous stitch as a guiding line because organza is see-through.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image008.jpg" alt=""></p>
<p>I am bending the organza upwards to the seam allowance side and making a strengthening stitch leaving a 1-2 mm gap from the crease line in order to secure it in this position.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image009.jpg" alt=""></p>
<p>The function of the organza is to prevent the cut from stretching and give it a rounded shape for a better fitting to the body. It is recommended to ease in the fabric underneath the organza very slightly.</p>
<p><strong>Then I unfold the pieces and make a clean-finish-edge seam leaving 1-1.5mm from the seam that joins the pieces together:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image011.jpg" alt=""></p>
<p>I am going to stitch over the lining while catching up the seam allowance. This seam is made for the sake of achieving a pretty even piped-like edge along the neckline cut of the garment. Now I am thoroughly spreading the seam allowances guiding them to the lining side and catching up by the stitch.</p>
<p><strong>I am trimming all extra seam allowances leaving about 0.2 inches: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image013.jpg" alt=""></p>
<p><strong>Now I turn the garment inside out to check what the bones look like, how well they lie and what the cutting line itself looks like: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image015.jpg" alt=""></p>
<p>Having checked the quality of the cutting line I unfold the garment again and start joining together the face and lining pieces along the top part in both directions from the cut.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/corset_in_1_day/transcript/3_files/image017.jpg" alt=""></p>
<p>I pin over the face side; the lining is underneath. I am trying to only catch the fabric underneath the horizontal bone going along the corset top without catching the bone itself. I am pinning it together in a gliding motion. At first I put some pins at the controlling points and then re-spread the fabric between these pins and add new pins - this time more densely. The same thing is done from the second side of the cut.</p>
<p>I also pin the face and the lining along the middle of the back, the vertical cut where the lacing is going to be made. I even out the cut edges on the middle of the back, then put a controlling pin and start adding more pins in a gliding horizontal motion.</p>

