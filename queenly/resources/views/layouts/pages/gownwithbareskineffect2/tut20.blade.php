<h3>Tutorial 14. Finishing Off the Dress Top.</h3>
<p></p>
<p><strong>Let's run another fit test on the dress-form. </strong>I have already mentioned that the decorative flap tends to stick up when you put the skirt on a person or on the dress-form. There are two ways you can cope with this problem. </p>
<p><strong>The first way</strong> is to make another seam parallel to the one that attaches the flap. This additional seam will secure the seam allowance and press the flap down to the body.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p>But I find <strong>the second way</strong> easier and more efficient. When you're sewing on decorative buttons along the centre of the back, make sure the needle comes out as close as possible to the zipper teeth.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>This was the last fit test for the skirt.</strong> You can see from the pictures that the skirt has a rather tight fit without obstructing my client's movements. The size is perfect. </p>
<p><strong>Now let me make up my mind about the final decorative touches to the dress top.</strong> First of all I mark the location of the top edge of the skirt on the bodysuit. It will serve as a guideline when I continue decorating the bodysuit with lace motifs. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p>I take the skirt off the dress-form to finish it and prepare it for getting joined with the bodysuit. </p>
<p><strong>But before doing that let's finish decorating the top of the dress. </strong></p>
<p>I put a piece of lace over it, trim it along the line marked for attaching the skirt, and secure it on the mesh with the help of Gutermann textile glue. I basically weave one lace motif into another. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
