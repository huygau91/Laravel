<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>After securing the lace at the side of the dress-form I turn it over and wrap it in the same piece of lace so that it reaches the beginning of the skirt.</strong> I carefully spread the lace and secure it with pins. </p>
