<h3>Drafting the Dress-Form. Part 1.</h3>
<p></p>
<p><strong>So I have equipped myself with rulers, patterns, scissors and markers of two different colours, and it’s time to start drawing a grid for building my dress-form. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image002.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-022.jpg"></strong></p>
<p><strong>I draw a vertical line (the middle of the dress-form) and draw a horizontal line (the shoulder line) in the upper part of the sheet. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image004.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-023.jpg"></strong></p>
<p>I will use a red marker for all values of the front of the dress-form and a black one for those of the back.</p>
<p><strong>I start from the front and the back shoulder widths.</strong> </p>
<p><strong>The front shoulder width measurement is 33cm</strong>. I divide it in half and get 16.5cm. I mark it on the shoulder line left and right of the vertical line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image006.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-024.jpg"></p>
<p></p>
<p><strong>The back shoulder width measurement is 31cm.</strong> I divide it in half and get 15.5cm. I mark it on the shoulder line left and right of the vertical line.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image008.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-025.jpg"></p>
<p><strong>I mark the shoulder length (10cm) inwards from the ends of the front shoulder width.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image010.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-026.jpg"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image012.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-027.jpg"></p>
<p></p>
<p><strong>I mark the neck base to bust middle measurement (23cm) down from the end of the shoulder length (point A) and draw the bust line. &nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image014.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-028.jpg"></p>
<p><strong>The bust middle measurement is 18cm. I mark 9cm on the bust line left and right of the vertical line (points B and C).</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image016.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-029.jpg"></strong></p>
<p><strong>Then I draw a perpendicular line through the end point of the front shoulder width (point D).</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image018.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-030.jpg"></p>
<p><strong>And I mark the front shoulder slope measurement starting from the bust point (point B) and up to the shoulder tip. It is 21cm in my case. I mark point E on the perpendicular line. </strong>We can see the shoulder slope now.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image020.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-031.jpg"></p>
<p>This way we have maintained both the width and the height of the shoulder. <strong>I join the points.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image022.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-032.jpg"></p>
<p><strong>And I transfer the construction onto the other side of the dress-form. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image024.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-033.jpg"></p>
<p><strong>I mark the bust front measurement left and right of the centre.</strong> My client's bust front measurement is 46cm, so I measure 23cm in each direction and mark the notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image026.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-034.jpg"></p>
<p><strong>Let's see how much falls upon the back.</strong> My client's bust circumference is 86cm. I subtract the bust front measurement (46cm) and get 40cm. This means <strong>20cm from each side of the dress-form will fall upon the back. </strong>I mark the notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image028.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-035.jpg"></p>
<p><strong>Next I step 8cm below the bust line and draw the under-bust line.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image030.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-036.jpg"></strong></p>
<p><strong>Let's make calculations.</strong> The under-bust front measurement is 40cm and the under-bust circumference is 78cm. Therefore 38cm will fall upon the back (nearly as much as at the front). So I'll do the same thing I did with the waistline, stomach line and hip line: mark the entire circumference in quarters.</p>
<p><strong>I mark 19.5cm on the under-bust line left and right of the central vertical line.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image032.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-037.jpg"></strong></p>
<p><strong>It's important to check the location of cups at this stage.</strong> I have always told you there should be 5-6cm between the cups and the side seam. I want to mark the location of the cups first. Let’s assume the side seams cross the bust line through the notches of the bust front measurement 23cm from the centre. <strong>I mark 6cm inwards from those notches and get the end points of the cups.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image034.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-038.jpg"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image036.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-039.jpg"></p>
<p><strong>I draw the waistline in order to check if the cups are placed correctly.</strong> The waistline is usually 10-11cm below the under-bust line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image038.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-040.jpg"></p>
<p><strong>By the way the golden mean principle tells us that the distance between the under-bust line and the waistline should equal 1.5 of your palm width.</strong> My body is more or less close to my client's body so I can easily check if I've chosen the right location for the waistline.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image040.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-041.jpg"></p>
<p><strong>I draw a low neckline that will leave the entire centre of our dress front open. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image042.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-042.jpg"></p>
<p><strong>I put the first cup in place with regard to the low neckline.</strong> I will shape it with the help of an arched underwire before setting it in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image044.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-043.jpg"></p>
<p><strong>I mark the location of the underwire and outline it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image046.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-044.jpg"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image048.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-045.jpg"></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image050.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-046.jpg"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image052.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-047.jpg"></strong></p>
<p>Of course these are only preparatory markings. Later we will determine the location of the cups more precisely.</p>
<p><strong>I refer to my client's nape to waist measurement to double-check the location of the waistline.</strong> In her case this measurement taken from the waist up to the seventh cervical vertebra is 38cm. I step 1cm below the shoulder line and mark it. <strong>Now I can see that my client's waistline should be 2cm higher than it is.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image054.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-048.jpg"></strong></p>
<p><strong>I re-draw it and move the under-bust line up to the level of the cup underwires.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image056.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-049.jpg"></p>
<p><strong>Determining the location of waistline is a very important step in dress-form construction.</strong> The thing is measurements often lack perfect precision. I have already brought the waistline in correspondence with the nape to waist measurement and now <strong>I'd like to check its location with regard to the back shoulder slope. </strong>This measurement equals 43cm; it is taken from the point where the waistline intersects with the spine and up to the shoulder tip (i.e. the outer end of the shoulder seam). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image058.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-050.jpg"></p>
<p><strong>I can tell I need to adjust the location of the waistline again: and namely, shift it down by 1.5cm. I draw the final location of the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image060.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-051.jpg"></p>
<p><strong>I mark a half of the waist circumference on the waistline.</strong>&nbsp; My client's waist circumference is 68cm so a half of it makes 34cm. Therefore I need to mark 17cm left and right of the centre of the dress-form.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%202.files/image062.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-052.jpg"></p>

