<h3>Tutorial 15. Sewing the Skirt with a Train. Joining the Skirt with the Dress.</h3>
<p></p>
<p><strong>We need to sew a large flounce with a lace overlay (the semi-circle part of the skirt) to the bottom edge of our upper skirt piece. </strong>I find the middle of each piece by folding it in half and make a notch in the centre. Then I put the pieces together face to face, match their central notches and secure them with a pin. </p>
<p><strong>I start joining the pieces from the centre:</strong> moving first in one direction and then in the other. You must by no means join them from edge to edge!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I do the overlock finish on the joining seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can start sewing in the train.</strong> </p>
<p><strong>First I will sew it down along one side of the central seam and then along the other.</strong> Working with this piece is really easy and doesn't take much time because we've glued the lace overlay onto the train.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I find the end of the central seam, pin it, pin the train onto the same pin, align the raw edges of the central seam and the train and secure it all safely with another pin.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I place the needle of the sewing machine at the point marked and stitch along the first side of the train.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>And I find a starting point for sewing in the other side of the train and repeat the same procedure.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>There's a slight mismatch along the joining seam between the length of the semi-circle skirt and the train because the fabric got eased in.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I press the ready skirt. All appliqu� elements on the bodysuit have set properly.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I put the skirt on the dress-form. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I align its central seam with the central vertical line of the dress-form.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I close the zipper on the skirt and align it with the zipper on the bodysuit. I pin down the back of the skirt according to the marked location of its top edge.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the dress-form over, adjust the sides and pin down the front half of the skirt in accordance with the marking. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2015_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the skirt to the bodysuit with a backstitch</strong> following the machine stitch I made to finish the top edge of the skirt. The seam will be covered up by lace appliqu�. </p>
<p><strong>The dress is ready for a fit test.</strong></p>

