<h2>Introduction. Review of the Dress Model and Materials Used.</h2>
<p></p>
<p>I am glad to take you further into my course dedicated to sewing open-back and open-shoulders wedding and evening dresses. We are going to work on another dress model with the bare-skin effect that makes it seem as though lace motifs magically held on skin without any support. </p>
<p>The base of our dress will be made of that very see-through resilient mesh fabric that 'disappears' in wear and creates the illusion of bare skin. This type of mesh fabric is known as 'illusion.' We have already used it for the previous dress in the course and we will do it again. I have previously mentioned that this fabric can be either two-way stretch or all-way stretch. It is normally available in three colours: nude, white, and black. </p>
<p><strong>Let me introduce the dress we are going to sew. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image002.jpg" class="img-responsive center-block"> </p>
<p><strong>The front of the dress:</strong> it features a kind of off-shoulder sleeves and a boat neck the contour of which is defined by lace appliqu�. If you decide against using those appliqu� motifs, there will be a barely noticeable edge of mesh fabric around the neck hole. It looks as if the dress weren't secured any way and stayed on just by itself. </p>
<p><strong>I'd like to say a couple of words about off-shoulder straps</strong> that are as popular among brides as they are disapproved of among us, seamstresses. The thing with these straps is that they are bound to obstruct the wearer's movements if her dress is sewn the traditional way. She can't lift her arms, the straps slide way too far off her shoulders and refuse to stay in place. Besides it is really hard to build a perfect pattern of such straps and then sew them in place in a neat way with no seams seen from any angle. But the sewing technique I offer will allow you to fulfill your client's desire by explaining that you need to use see-through mesh fabric in order to make the dress comfortable and secure it on the body. </p>
<p><strong>I will purposely make it a very low-cut dress</strong> to demonstrate the most of the opportunities of using see-through mesh fabric. However you are free to choose from a variety of models with different neckline depths depending on your preferences.</p>
<p><strong>The skirt</strong> will feature a semi-circle inset with a lace overlay. </p>
<p><strong>The most piquant part of our dress is its back.</strong> The entire back is open, with a perfect illusion of bare skin. And it features another trendy element: a zip fastener running along the middle of the back. I will explain how they sew in such fasteners in VIP-ateliers, how to sew it into resilient mesh fabric and how to conceal it. It�s best to decorate such fasteners with small pearl buttons or beads. A zip fastener embellished that way that goes along the spine of a 'bare' back looks incredibly glamorous! </p>
<p>The lace from the front of the dress passes onto the back and frames the 'low-cut neckline.' The skirt features a gathered area secured by a zipper. A triangular train starts right below the bottom end of the zipper. The flounce of the skirt passes from the front onto the back. </p>
<p>The construction of our dress might seem complicated at first but you will see that it doesn't require any strenuous efforts provided you keep to the technique and working order explained. It is all simple and logical. You will know it's nothing to wrestle with once you have familiarized yourself with all steps.</p>
<p><strong>Now let me name the materials and tools we are going to use. </strong></p>
<p><strong>The base of our dress - its bodysuit part - will be made of 'illusion' mesh. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image004.jpg" class="img-responsive center-block"> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image006.jpg" class="img-responsive center-block"> </p>
<p><strong>I have chosen dense stretch satin for the top part of the skirt </strong>so we don't have to use a lining. It's a single-layer skirt but since the fabric is opaque you don�t have to worry about your client's lingerie showing through. This fabric is also perfect for draping. Moreover, there are no side seams on the skirt - it is made as a single piece of fabric that sort of hugs the body. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image008.jpg" class="img-responsive center-block"> </p>
<p><strong>The dress features two zip fasteners:</strong> one on the skirt and another on the bodysuit piece. The zipper on the bodysuit will be nude. It is equally acceptable to use a regular zipper or a blind one. You will see it for yourself in the next tutorials.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image010.jpg" class="img-responsive center-block"> </p>
<p><strong>The rest of the skirt will be made of regular fine stretch satin.</strong> I most likely won�t use any additional lining to avoid making the skirt too heavy.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image012.jpg" class="img-responsive center-block"> </p>
<p><strong>That part of the skirt will also feature a very lightweight and delicate lace overlay.</strong> Its printing is represented by rather closely spaced elements, which creates an illusion of 'luxe' lace fabric. There are no beads or sequins on my lace but you could use embellished lace if you wanted to.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image014.jpg" class="img-responsive center-block"> </p>
<p><strong>We will need foam bra cups.</strong> Mine are nude-coloured. But of course you could also use white cups depending on the design and the model. Their size should match your client's bra size. Sometimes I use actual cups from my client's bra for this purpose. You should see for yourself: feel free to use ready bra cups if they correspond with all your requirements and match the chosen dress model. Still, I prefer working with semi-finished cups so I can fully adjust them after my needs.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image016.jpg" class="img-responsive center-block"> </p>
<p><strong>Semi-circular metal underwires should agree with the size of the cups.</strong> Remember that each underwire must be 1.5-2cm shorter than the bottom arch of the cup so you can insert it and secure it without trouble. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image018.jpg" class="img-responsive center-block"> </p>
<p><strong>In order to secure the underwires inside the cups we will need bone casing - i.e. special bias tape with a tunnel.</strong> This bias tape is sewn along the bottom of each cup as per usual and then you insert a semi-circular underwire into the tunnel.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image020.jpg" class="img-responsive center-block"> </p>
<p><strong>And of course we will use the flat dress-form template that we made after client's measurements in the first part of this course.</strong> I will upgrade it a little: imitate upper arms so we can work on the sleeves. Besides I will add �thighs� at the bottom of the dress-form so we can use it for working on the skirt, too.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Introduction_files/image022.jpg" class="img-responsive center-block"> </p>
<p>Basically, you could use a standard 3D dress-form for working on the skirt (it would even be easier) but that only works if your client's hip circumference does not exceed that of the dress-form. It's quite a disaster when you suddenly realize the skirt has turned out too small for the client!</p>
<p><strong>I have described the model of our future dress.</strong> You must have already guessed that it�s sewn without a pattern but based on a flat template customized after your client's measurements. You don't need any ready-made patterns for sewing such dresses! There is also no need in any particular styling process! And although the order of assembly and the work stages are somewhat unconventional, you will get the grasp of it towards the end of the course - and then you'll be amazed at how simple and yet trendy and glamorous this dress model really is.</p>

