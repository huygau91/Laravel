<h3>Drafting the Dress-Form. Part 2.</h3>
<p></p>
<p><strong>I draw the stomach line 12cm below the waistline.</strong> My client's stomach circumference is 90cm. I divide it by four and get 22.5cm. I measure this distance on the stomach line left and right of the centre of the dress-form and mark the notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image002.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-053.jpg"></p>
<p><strong>Then I draw the hip line 19cm below the waistline or 7cm below the stomach line.</strong> My client's hip circumference is 98cm. I divide it by four, measure 24.5cm on the hip line left and right of the centre of the dress-form and mark the notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image004.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-054.jpg"></p>
<p><strong>I mark a half of the crotch length on the central vertical line down from the waistline.</strong> My client's crotch length is 63cm, therefore I mark 31.5cm - it's the length of the panties from up to down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image006.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-055.jpg"></p>
<p><strong>I mark 5cm left and right of the central vertical line at the bottom of the panties and join the points with the hip-line notches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image008.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-056.jpg"></p>
<p></p>
<p><strong>Now we can join the notches of the hip line, the stomach line, the waistline, the bust line and the under-bust line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image010.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-057.jpg"></p>
<p><strong>Do the same from the opposite side.</strong> The sides should be symmetric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image012.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-058.jpg"></p>
<p></p>
<p><strong>Looking at the draft I noticed that the cups stand way too far from the neckline.</strong> So I've decided to mark 6cm inwards from the bust-line notches of the back and not of the front to move the cups closer to the edges of the neckline.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image014.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-059.jpg"></p>
<p><strong>I re-draw the location of the underwires.</strong> Please note their configuration: they are placed with their lower end to the centre. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image016.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-060.jpg"></p>
<p><strong>So I have marked a new location of the cups.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image018.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-061.jpg"></strong></p>
<p><strong>Next I re-draw the front neckline starting at the shoulders.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image020.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-062.jpg"></p>
<p></p>
<p><strong>I draw the shoulders of the front half. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image022.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-063.jpg"></strong></p>
<p><strong>And I check it one more time.</strong> </p>
<p><strong>The front shoulder to waist measurement I took is 44cm. Let's check it.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image024.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-064.jpg"></p>
<p><strong>Now I can tell the old location of the waistline was actually right!</strong> I purposely demonstrate you the entire process of constructing a dress-form with all adjustment moments and not just the final results so you know it's a creative process and you shouldn't be afraid of applying changes.</p>
<p><strong>I restore the initial waistline, re-draw the stomach line, the hip line and the crotch length, transfer all notches onto the new lines and adjust the shape of the dress-form.</strong> The new lines of the front are marked green.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image026.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-065.jpg"></p>
<p><strong>Now let's work on the armscyes. </strong>My client's armscye circumference is 45cm and I need a half of it - 22.5cm. </p>
<p><strong>First I mark the side length in accordance with the measurement (22cm).</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image028.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-066.jpg"></p>
<p>The outer end of the front shoulder line I've drawn corresponds with the front shoulder slope measurement and is located 42cm from the waist. And the outer end of the back shoulder line corresponds with the back shoulder slope measurement and is located 43cm from the waist. <strong>I find the outer end of the shoulder line on the back piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image030.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-067.jpg"></strong></p>
<p><strong>In order to build the armscye I will use the average of the front and the back shoulder slopes</strong>: i.e. lift the armscye up by 0.5cm at the front and lower it by the same value at the back to even things out. That totally works for knitted fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image032.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-068.jpg"></p>
<p>We have measured the front width (33cm in my case). It's taken horizontally right above the breasts between implied vertical lines going upwards from the front corners of the underarms. The line of the armscye is supposed to pass through it. <strong>I mark a half of the measurement (16.5cm) left and right of the central vertical line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image034.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-069.jpg"></p>
<p></p>
<p><strong>And I use a French curve to draw the armscyes on the front of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image036.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-070.jpg"></p>
<p></p>
<p><strong>The armscye line of the back piece should pass through the points of the back width measurement that equals 31cm in my case. I mark a half of it (15.5cm) left and right of the centre.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image038.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-071.jpg"></p>
<p><strong>I draw the armscyes on the back of the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image040.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-072.jpg"></p>
<p></p>
<p><strong>Our 2-D development drawing of a custom dress-form is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image042.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-073.jpg"></p>
<p></p>
<p>I fold it in half along the central line and<strong> cut it out</strong> following the outer lines. I can always apply minor alterations later if it comes to that.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%203.files/image044.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-074.jpg"></p>
