<h3>Tutorial 8. Sewing Down the Side Seams of the Dress.</h3>
<p></p>
<p><strong>Now we need to sew down the side seams of the bodysuit.</strong> The bodysuit is made of fine highly resilient mesh fabric so it's best to use the invisible seam stitch for the side seams (it is available on a regular household sewing machine). You could also sew the side seams with the help of an overlocker but it makes them thicker. </p>
<p><strong>I sew mine with the regular elastic machine stitch - the invisible stitch.</strong> Just make sure the seam is slim and elastic, there are no other requirements.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I check the quality of the seam: it shouldn't tear when you stretch it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew down the other side seam exactly the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Then I carefully flatten and even out the seam allowance on the table.</strong> And I turn out the bodysuit. The seam will look good on the garment in wear. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>At this point we can work on the panties: sew down the bottom seam and finish the raw edges.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>In fact in my case I won't finish those raw edges</strong> because my skirt is made of delicate fabric and I don't want any seams showing through. It�s enough to just even them out. </p>
<p><strong>Instead of making a snap closure on the bodysuit I simply sew it down.</strong> Trust me it is convenient enough whereas using a snap closure would be rather complicated with a narrow skirt like ours.</p>
<p>To make it less complicated let�s sew down the bottom seam of the panties later: we will need to put the garment on the dress-form again and that would be quite challenging with that seam sewn down. </p>

