<h2>Advance Preparation of the Dress-Form, Cups, and Zipper.</h2>
<h3>Tutorial 1. Upgrading the Dress-Form.</h3>
<p></p>
<p><strong>I start upgrading our dress-form template.</strong> </p>
<p><strong>First of all we need to add sleeves to imitate off-shoulder straps on the dress. </strong>You need to measure your client's arm fullness in order to draw the sleeves. </p>
<p>This measurement is taken from the front corner of the armscye to its back corner over the arm that either hangs freely or is slightly moved out to the side. You can measure it with the arm hanging freely if your sleeves will be made of highly resilient fabric (they can stretch during movement). But if you plan to have some rather stiff appliqu� elements on the sleeves that are likely to obstruct arm movements, then you should measure the arm fullness with the arm slightly moved out to the side. </p>
<p><strong>My client's arm fullness measurement is 21cm.</strong> I place a piece of cardboard to the edge of the dress-form and mark a half of it - 10.5cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a sleeve.</strong> </p>
<p><strong>Next I slightly prolong the shoulder line preserving the same incline and start rounding it off.</strong> I basically draw the arm configuration from the shoulder down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image006.jpg" class="img-responsive center-block"></p>
<p>Where the sleeve ends depends on the model and the width of appliqu� elements on the shoulder strap. It's highly recommended that these elements should pass onto the dress towards the chest.</p>
<p>You are surely aware that such dresses only suit women of a moderate breast size and very good body proportions. This means the cups should not be too tall. We simply want them to provide support for the breasts and improve their shape. I usually trim the top of the cups to avoid unnecessary volume and remove any extra height. Our goal is to imitate bare skin so we don't want any foam to show through the mesh fabric. It�s why I don't use large deep cups even when they match my client's size.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>So, I have made a sleeve template.</strong> Now I need to cut it out and make several copies. My dress-form is made of four layers of cardboard so I need four sleeve templates for each side of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image010.jpg" class="img-responsive center-block"></p>
<p>Of course we could have made a universal dress-form with off-shoulder sleeves straight away but I decided to start with a simplified version. Next time you�ll know to prepare a more multi-purpose dress-form right away. </p>
<p><strong>You can join the layers with either white craft glue or sticky tape.</strong> I tear off a strap of sticky tape, fold it in a circle sticky side up, stick it onto the first layer, put the second layer over it, etc. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>All that's left to do now is attach the sleeves to the dress-form.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I cover the sleeves with sticky tape so lace motifs don�t stick to them later and attach them from both sides of the dress-form</strong> using bits of sticky tape and rectangular cardboard pieces for better bonding. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Key%20Measurements_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I extend the dress-form at the bottom.</strong> I want to have 40cm between the waistline and the bottom edge. I believe it is more than enough. The bottom edge of the panties remains unchanged. </p>
<p><strong>Next I draw templates for the thighs, cut them out, stick together four layers per each, cover them with sticky tape and attach them to the dress-form the same way I did with the sleeves.</strong></p>
