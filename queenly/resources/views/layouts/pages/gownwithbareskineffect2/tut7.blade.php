<h2>Key Measurements. </h2>
<p></p>
<p>Tie a cord or a rubber band around the waistline. Relax and stand the way you normally do. You should neither let the measuring tape hang loose nor pull it too tight. </p>
<p><strong>Bust front. </strong>It is measured nearly the same way as bust circumference but between the side seams and from the front. </p>
<p><strong>Under-bust front.</strong> It is measured nearly the same way as under-bust circumference but between the side seams and from the front. </p>
<p><strong>Bust circumference</strong> (defines the body size). The measuring tape should pass through the protruding parts of the shoulder-blades and the apex points of the breasts at the underarms level. When you are taking this measurement, the measuring tape should stay strictly horizontal at the front even if your client has low set breasts. Later you can add some extra centimeters for the fullness of the bust. </p>
<p><strong>Under-bust circumference.</strong> The measuring tape passes right below the bust and below the shoulder-blades. </p>
<p><strong>Waist circumference.</strong> It is measured around the thinnest part of the waist. </p>
<p><strong>Stomach circumference.</strong> It is measured 10-14cm below the waistline. The measuring tape should be parallel to the waistline and the hip line. </p>
<p><strong>Hip circumference. </strong>It is measured around the most prominent part of the hips with some allowance for the stomach. </p>
<p><strong>Bust middle</strong> (distance between the apex points of the breasts). It is measured horizontally. </p>
<p><strong>Neck base to waist front - 1.</strong> It is measured from the inner end of the shoulder seam and through the apex point of the bust down to the cord tied around the waist. </p>
<p><strong>Neck base to waist front - 2.</strong> It is measured from the sternal notch to the waistline. </p>
<p><strong>Neck base to bust middle.</strong> It is measured simultaneously with the neck base to waist front - 1. The measuring tape goes from the inner end of the shoulder seam down to the apex point of the bust. </p>
<p><strong>Shoulder tip to bust middle. </strong>It is taken from the apex of the bust to the shoulder tip. </p>
<p><strong>Nape to waist.</strong> It is measured from the sevenths cervical vertebra to the waistline. </p>
<p><strong>Back width.</strong> You measure it by placing the measuring tape horizontally between the bottom corners of the underarms at the level of the prominent shoulder-blade areas. </p>
<p><strong>Front width. </strong>Imagine vertical lines going up from the front corners of the underarms and place the measuring tape between them horizontally right above the breasts. </p>
<p><strong>Shoulder width at the front. </strong>This measurement is taken between the shoulder joints from the front. </p>
<p><strong>Shoulder width at the back. </strong>This measurement is taken between the shoulder joints from the back. </p>
<p><strong>Side length.</strong> It is measured along the side from the waistline up to the bottom point of the armscye. </p>
<p><strong>Back neck base to waist: </strong>This measurement is taken from the point where the shoulder meets the neck and down to the waistline parallel to the spine. </p>
<p><strong>Back shoulder slope. </strong>It is taken from the point where the waistline intersects with the spine and up to the shoulder tip (i.e. outer end of the shoulder seam). Make sure your measuring tape is pulled on tight. </p>
<p><strong>Waist to hip length.</strong> Measure the distance between the waistline and the hip line at a straight angle. This measurement is normally about 16-20cm. </p>
<p><strong>Armscye circumference.</strong> Measure around the arm in the underarm area. </p>
<p><strong>Elbow circumference. </strong>Measure around the elbow. </p>
<p><strong>Wrist circumference.</strong> Measure around the wrist joint. </p>
<p><strong>Shoulder length. </strong>It is taken from the inner end to the outer end of the shoulder seam or sometimes from the middle of the shoulder slope to the shoulder tip. I recommend you should measure the <strong>sleeve length</strong> at the same time. </p>
<p><strong>Shoulder to elbow. </strong>This measurement is taken from the shoulder joint down to the elbow. </p>
<p><strong>Shoulder to wrist. </strong>This measurement is taken from the shoulder joint down to the wrist. </p>
<p><strong>Shoulder circumference (upper arm circumference). </strong>It is measured horizontally around the fullest part of the shoulder with the arm hanging down freely. The ends of the measuring tape should meet on the outer part of the arm. </p>
<p><strong>Crotch length.</strong> Measure between the legs from the front waistline to the back waistline. </p>
<p><strong>Garment length. </strong>Measure vertically along the middle of the back from the seventh cervical vertebra down to the desired length.</p>
