<h3>Tutorial 9. Calculating Fabric Consumption for the Skirt.</h3>
<p></p>
<p><strong>Let me freshen up your memory about the model of our skirt.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The front: </strong>&nbsp;</p>
<p>The upper part of the skirt has a slim fit. The rest is a flowing semi-circle with a lace overlay. The length of the entire skirt is 115cm. Let us make the upper part 35cm long in the middle and 45cm at the sides. <strong>In this case the length of the lower part will be: </strong></p>
<p><strong>115cm - 35cm = 80cm</strong></p>
<p><strong>The back:</strong> </p>
<p>The upper part of the skirt is cut as a single piece with no side seams. According to our concept the top edge of the skirt is concave at the back: there�s about 7cm between the waistline and its middle. A 20cm long zipper is sewn in the skirt and the train starts about 3cm below the zipper. The train is sewn into the lower part of the skirt with vertical seams. Both the train and the skirt feature a lace overlay. The upper part of the skirt is gathered at the zipper level. Its front half is 35cm long in the middle and 45cm long at the sides; the back half should be about 55cm long in the middle so we can visually achieve the same incline from both sides. </p>
<p><strong>Let's calculate the length of the part of the train that merges with the upper part of the skirt through the central seam.</strong> </p>
<p><strong>55cm- 7cm - 20cm&nbsp; - 3cm&nbsp; = 25cm</strong></p>
<p><strong>Now let's decide upon the length of the train.</strong> I suggest there should be 65cm of it lying on the floor. In that case <strong>the length of the entire skirt with the train included is: </strong>&nbsp;</p>
<p><strong>115cm+ 65cm= 180cm</strong></p>
<p><strong>Then the length of the train from the starting point to the bottom is:</strong></p>
<p><strong>180cm- 7cm - 20cm&nbsp; - 3cm&nbsp; = 150cm</strong></p>
<p><strong>Let us work with development drawings. </strong></p>
<p><strong>The lower part of the skirt is a semi-circle.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s calculate its radius.</strong> </p>
<p>We need to add about 5cm to the hip circumference measurement (98cm in my case) to account for the semi-circle shape and the lowering. <strong>According to the semi-circle calculation formula the radius is 33cm.</strong></p>
<p>In this case I need 113cm of fabric for the skirt that is 80cm long in the middle.</p>
<p><strong>80cm+ 33cm= 113cm</strong></p>
<p>This way we can safely start cutting the skirt with a piece of fabric that's at least 120cm wide. </p>
<p><strong>Next let's calculate the length of the seams for sewing in the train (i.e. the length of the longer sides of the skirt).</strong></p>
<p>The entire length of each seam is 150cm - however 25cm of the train falls upon the upper part of the skirt. <strong>Therefore the length of the central seam of the semi-circle piece is:</strong></p>
<p><strong>150cm+ 25cm= 125cm</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p>We need to account for the radius of 33cm to know the full fabric consumption.</p>
<p><strong>125cm+ 33cm= 158cm</strong></p>
<p><strong>This means the semi-circle part of the skirt requires 3.2m of main fabric and as much lace.</strong></p>
<p><strong>Let's see how things are with the train then.</strong> The train is a quarter of a semi-circle, square, or diamond with a side length of 150cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p>It can have any kind of a configuration. You can use a diamond the way we did for the previous dress. You can start rounding this diamond off and bring it right to a quarter of a semi-circle. In other words you should decide on the length of the 'tail' of the train in accordance with the model. And of course you need a piece of lace of a matching size to overlay the train, <strong>i.e. I need another 1.5m of main fabric and lace.</strong></p>
<p><strong>3.2m+ 1.5m = 4.7m</strong></p>
<p><strong>And I need about 0.5m of fabric for the upper part of the skirt.</strong></p>
<p><strong>4.7m+ 0.5m = 5.2m</strong></p>
<p><strong>I recommend that we take 5.5m of main fabric and at least 4.7m of lace for sewing a skirt like this.</strong></p>
<p>I will work on the skirt moving in the reverse direction, i.e. <strong>start by preparing the upper part of the skirt following the draping method (after cutting it out). </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p>Then I will take a piece of fabric that is 150cm wide and 50cm long and fold it in half (the fold line is on the left). Next I will determine the location of the side seam in accordance with the measurements. I will mark 35cm down along the centre of the front and move further down to 45cm at the side seam level. And I will trim the skirt by 1-1.5cm at the top to achieve a beautiful contour at the front.</p>
<p>Next I will calculate the location of the back seam. The model provides that the back seam of the main skirt involves some gathering, which means transferring all darts of the skirt into the back seam. </p>
<p>Once I know the configuration and the full size of the upper part of the skirt for certain, I can measure the bottom circumference of the skirt and use that value for calculating the radius of its lower semi-circle part. </p>
<p><strong>The most important thing at this stage is knowing how much fabric we need for the skirt: 5.5m of main fabric and about 4.7m of lace. </strong></p>

