<h3>Tutorial 3. Preparing the Zipper and the Zip-Up Area.</h3>
<p></p>
<p>Somewhat surprisingly we are going to sew in the zipper right after finishing the dress-form. But first let�s prepare the zipper itself and mark its location. </p>
<p>No matter how narrow your zipper is, <strong>its tape is still too wide </strong>to be invisible on see-through mesh. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Our task is to make the visible part of the zipper as narrow as possible.</strong> So I fold the tape of the zipper inwards from both sides and stitch it down as close as possible to the teeth. I use a blind zipper since the protruding part of its chain is on the inner side while a regular zipper has it on the face side which makes it stand out a lot more. I will sew it in following the traditional technique though.</p>
<p><strong>I put the zipper underside up, open it, fold one side of the tape in half and stitch it down</strong> thereby making it half as wide as it was. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And I make the tape narrower from the opposite side of the zipper, too. </strong>You don't have to go all the way down to the bottom of the zipper because a part of it will be hidden in the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The tape of the zipper is now a lot narrower.</strong> Folding it does not affect the performance of the zipper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I fold a piece of see-through mesh fabric in half and put it over the dress-form aligning the fold line with the shoulder line.</strong> I want the dress to have seamsless shoulders for a neater look. The dress will only have side seams and a zipper in the middle of the back. The mesh will get pulled on so I place it 1.5cm-2cm below the top line and 1cm above the bottom of the panties. I draw the central vertical line with a disappearing ink pen and mark the left and right ends of the neck base from the back.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I cut up one layer of mesh fabric (back piece) along the central vertical line and cut the fold line horizontally between the neck base points. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p>I will start sewing in the zipper from the very edge. </p>
<p><strong>I have also prepared</strong> a 7mm wide white satin ribbon the length of which equals the distance between the top stop and the bottom stop of the zipper, as well as two straps of paper-backed fusible web of the same width and length.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I take the mesh off the dress-form and lay it on the table.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I iron fusible web onto both sides of the satin ribbon.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I stick the ribbon onto mesh about 1.5cm inwards from the raw edge of the cut.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the raw edge back and stick it onto the other side of the ribbon. </strong>These are very important steps that require particular accuracy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>From this moment on we can already differentiate between the underside and the face of the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>When I start sewing in the zipper, I will make sure the groove between the teeth passes right along the middle of the ribbon. </strong>I will attach the zipper by stitching along the very edge of the ribbon. Only some 1-2mm of the zipper tape will show from behind the ribbon. And then I will embellish it with beads (they won't affect opening or closing the zipper). Fusible web has made the ribbon stiffer and nicer to the touch and it holds the shape really well now. It won�t be hard to sew the zipper onto it.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%203_files/image034.jpg" class="img-responsive center-block"></p>

