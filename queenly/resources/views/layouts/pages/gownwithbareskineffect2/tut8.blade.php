<h3>Tutorial 2. Preparing the Cups.</h3>
<p></p>
<p><strong>I move on to the cups. </strong>As I've already said, they are a little too big. My client's bust circumference is not that large - 86cm. </p>
<p><strong>I suggest trimming the top of each cup down by about 2cm. </strong>Their material allows us to do it without worrying about fraying. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image001.jpg" class="img-responsive center-block"></p>
<p><strong>As the result I get slightly shallower cups. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image003.jpg" class="img-responsive center-block"></p>
<p><strong>Now let's sew bone casing onto the cups and insert underwires.</strong> I sew the bias tape on with two rows of parallel stitches leaving its ends beyond the borders of the cup (I will trim them more accurately later).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image005.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image007.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image009.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image011.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image013.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image015.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>I make a bar tack in the outer corner of the cup that faces the underarms.</strong> For the sake of speeding up the process my bar tacks are just series of regular closely spaced machine stitches but I really don't advise you to do the same! Bar tacks made as close zigzag stitches are a lot more robust.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the ends of the bias tape. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>I take one semi-circular underwire</strong> and determine which end faces the centre of the bust and which one - the underarm. I insert the right end of the underwire into the bias tape and push it in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>There is some room between the end of the underwire and the edge of the cup</strong> so I can make another bar tack there. The underwire won't be in the way. It is for that very reason that the underwire should be a little shorter than the bottom arch of the cup.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>I remove all unnecessary thread and trim the end of the bias tape. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>Next I perform the same procedure on the second cup.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image031.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image033.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>The resulting cups are dome-shaped, stiff and fully prepared for getting sewn into the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image037.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The cups are waiting to be put in place.</strong> The under-bust line is marked on the dress-form for your reference. When I am putting the cups down, I make sure they can perform their function to the maximum without disturbing the harmony of the model. According to the design the top of the strap will merge with the contour of the neckline. I secure the cups with bits of sticky tape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>Next I insert our spray-foam moulds.</strong> They are too big for these cups so I trim the excess. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%202_files/image041.jpg" class="img-responsive center-block"></p>

