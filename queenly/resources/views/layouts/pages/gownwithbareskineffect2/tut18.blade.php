<h3>Tutorial 12. Preparing to Sew the Zipper into the Skirt.</h3>
<p></p>
<p><strong>I make two rows of parallel running stitches to gather the specified area of the central seam at the back of the skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>It's best to gather fabric from two ends of the stitches:</strong> gather it from one end and then turn it around and gather it from the other end. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>You should get 14cm between the notches as the result of such gathering.</strong> Now simply spread the gathers carefully and secure them by machine stitching. As for me I will do that a little later and show you where exactly you should stitch. <strong>I do the same from the other side as well.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I need to sew down a small area of the central seam and start inserting the zipper.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>A number of issues arise when you start sewing it in:</strong> firstly, the zipper should be concealed and, secondly, there is some thickening in the gathered area, which makes the entire task rather troublesome and makes it look somewhat messy in the end.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<p>You can't make it a <strong>blind zipper</strong> as you know it because it's simply impossible to stitch that close to the teeth of the zipper through such thickly gathered fabric. Besides, increased tension on the zipper caused by this thickness is likely to make it break in no time. </p>
<p><strong>Which is why cases like ours require a special technique for sewing in zippers.</strong> And it doesn't even matter whethere you choose a blind or a regular one. I personally prefer a blind zipper because it's flatter and has a more graceful slider.</p>
<p><strong>I need an additional strap of fabric in order to sew in the zipper following the special technique.</strong> So my zipper is a blind one. It's longer than needed but I will simply trim it in the process.</p>
<p><strong>I tear off a strap of white fabric</strong> that is 1cm wider than the zipper tape when the zipper is closed. I match its length with that of the zipper tape (20cm plus all technical allowances). It is best to use fine thin fabric for this purpose. Mine is the same stretch satin I used for the lower part of the skirt. Needless to say that its structure should match that of the main skirt fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I turn up the top edge of the strap and secure it with fusible web horizontally. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take a piece of web half as wide as the strap and fuse it on vertically. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the strap in half lengthwise and press to secure it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Next I put the strap over the left half of the zipper tape so that its top edge reaches the teeth and covers up the slider.</strong> It overlays the teeth by about 3mm: in other words the teeth should be in the middle of this strap when you fuse it on. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse a thin strap of fusible web along the very edge of the left half of the zipper tape.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>After the web cools down I remove the protective layer, place the zipper onto the prepared strap of fabric, and fuse it on. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p>This way I secure the required location of the strap on the zipper tape. Now I can safely stitch it on. </p>
<p><strong>I guess you know what it's all about.</strong> When you close the zipper, there will be a flap covering up its teeth. The slider will also be concealed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I will trim the length of the zipper at the very last moment. I leave it as is for now.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Each side of the zipper tape will be sewn to the gathered parts of the skirt.</strong> The additional strap of fabric we've made will be located between those gathered areas. And then we can sew decorative buttons onto the flap that will cover up the teeth of the zipper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding6/Tutorial%2012_files/image040.jpg" class="img-responsive center-block"></p>

