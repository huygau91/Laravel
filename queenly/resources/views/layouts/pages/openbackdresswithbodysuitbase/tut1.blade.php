<h3>Tutorial 1. Getting Acquainted with the Model.</h3>
<p></p>
<p><strong>Let me introduce you to this tutorial dedicated to sewing the dress sketched on the writing board below.</strong> What's peculiar about this dress? Its base and its lining are represented by a bodysuit. It also features ready-made sew-in foam cups. The dress has a really low neckline at the back so we will add straps that resemble a tiny short-sleeve bolero for better support around the shoulders. </p>
<p></p>
<p></p><p><strong>Why have I chosen this particular dress model?</strong> It is the most basic one from the range of models that involve a straight cut, a low back, sew-in cups, and �sleeves.� This dress can be overlaid by lace, chiffon, resilient mesh or any other similar fabric. You can also make it a mermaid dress. You can use a mermaid skirt or a full skirt likewise. Feel free to go creative with various decorative elements, such as drapes, appliqu�, rhinestones, ribbons, bows or ruffles. </p>
<p>In other words, this dress will help me explain and illustrate that very basic sewing technique that you can later employ to create dresses of countless styles and your own designs! </p>
<p><strong>Your main goal is to understand the method of assembling this garment - as soon as you do, you will know how to amaze your clients! </strong></p>
<p><strong>I would like to tell you about the materials we are going to use.</strong> </p>
<p><strong>The most important and uncommon one - something we have never used before - is the reReady-Something that we�ve never used before is ready-made foam cups.</strong> You can use any kind of them. </p>
<p><strong>As for me I will use foam cups that have a semi-circle top edge and no push-up pads. </strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image004.jpg" class="img-responsive center-block"> </p>
<p>Such cups are normally sold in different shapes and in three basic colours: white, black, and nude. </p>
<p><strong>I have worked with different kinds of cups:</strong> from cups with push-up pads and corners for attaching shoulder straps to round push-up cups, etc. They differ in size just like regular bra cups. Your choice depends solely on the kind of a dress you want to sew. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image006.jpg" class="img-responsive center-block"> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image008.jpg" class="img-responsive center-block"> </p>
<p><strong>You will need some specific materials apart from fabric. Elastic bands, for example. </strong></p>
<p><strong>Shoulder strap elastic:</strong> it is more robust and less stretchy than the regular lingerie elastic. It is available in very many versions: narrow, wide, ruffle, scallop, printed, multi-colour, etc. <strong>The key requirement - it should be springy enough.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image010.jpg" class="img-responsive center-block"> </p>
<p><strong>Elastic band for finishing the top edge and the low neckline at the back of the dress:</strong> </p>
<p><strong>It�s a regular elastic band </strong>with a smoother surface from one side and a velvety surface from the other.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image012.jpg" class="img-responsive center-block"> </p>
<p><strong>I will use a silicone-backed elastic band.</strong> It has a more decorative look; it's quite narrow (about 7mm) and has a sticky layer on the reverse side. Let me warn you in advance: don't place any high hopes on this elastic expecting it to help the back of your dress cling to the body. It won't. But it will make the back of the dress less slippery, which will indeed help preserve its shape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image014.jpg" class="img-responsive center-block"> </p>
<p><strong>We also need semi-circle metal bra underwires that we'll insert in the cups.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image016.jpg" class="img-responsive center-block"> </p>
<p><strong>How to choose such underwires correctly?</strong> First of all, just take the very same kind of a cup you are going to use and place an underwire at its bottom edge where it�s supposed to be sewn in. The underwire should be 1.5-2cm shorter than the bottom arch of the cup - otherwise it will stand in your way when you try to make bar tacks after sewing it in.</p>
<p><strong>Another thing we need is bone casing for our semi-circle metal underwires.</strong> It is non-elastic tape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image018.jpg" class="img-responsive center-block"> </p>
<p><strong>You are likely to find two basic types.</strong> </p>
<p><strong>The first type</strong> is bone casing tape sold as a strap of fabric folded and pressed in half and unstitched from the opposite edge. You close that edge when sewing the casing onto the garment and then insert the bone into the tunnel. </p>
<p><strong>The second type </strong>is tubular bone casing with both edges closed and a slight depression that you can stitch through. In other words, you can insert a bone in there straight away. <strong>It�s what I am going to use.</strong> This bone casing is not as straight as the first one so it's easier to give it a rounded shape in the sewing process. </p>
<p><strong>I will use the same bone casing tape for plastic bones.</strong> 7-8cm long fabric-encased bones will be sewn along the side seams of the dress at the same level with similar plastic bones at the sides of a bra. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image020.jpg" class="img-responsive center-block"> </p>
<p><strong>We also need some hook-and-eye tape to make a closure on the bodysuit part of our dress.</strong> This kind of tape with 2, 3 or 4 hooks is sold fully ready for getting sewn onto a garment. I prefer buying it per meter: it spares your money and provides for more design and size options. You can then make the closure as wide or as narrow as your client likes. There is basically no difference between finishing this kind of tape and finishing a ready-made closure. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image022.jpg" class="img-responsive center-block"> </p>
<p><strong>The next thing we need is elastic band for finishing the panties of the bodysuit part.</strong> It is thin and soft, and it stretches really well. It can be either absolutely smooth or have decorative ruffled edges. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image024.jpg" class="img-responsive center-block"> </p>
<p><strong>Another thing I�ve prepared is some lovely elastic band with light chiffon ruffles. I will use it for finishing the edges of the shoulder straps. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image026.jpg" class="img-responsive center-block"> </p>
<p><strong>There's another reason why I'm going to use this elastic band.</strong> I have chosen very fine and delicate lace fabric for this dress. Lace fabric like this tends to have very thin and uneven scallop edges, which makes it impossible to use for finishing the edges of the straps. But my elastic band is perfect for that purpose!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image028.jpg" class="img-responsive center-block"> </p>
<p><strong>Make sure to use resilient lace fabric! </strong>Since our garment has a very slim fit, we want all fabrics to be resilient. I wouldn't recommend sewing dresses like this with non-resilient fabrics! </p>
<p><strong>Another important question: what material is the bodysuit made of?</strong> It is made of rather dense and not very see-through mesh fabric that is resilient both horizontally and vertically. This is exactly the type of mesh fabric used in bodysuits. The bodysuit is supposed to hold the entire dress in place and create the desired slimming effect. I am not trying to say that a bodysuit can change body proportions radically but it should still fit very tightly if you want the wearer to feel confident in the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image030.jpg" class="img-responsive center-block"> </p>
<p><strong>The dress itself will be made of stretch satin.</strong> It is rather dense two-way stretch fabric that stretches crosswise and not lengthwise. One side of it is sheen and the other is matte. I will use the matte side as the face and add a delicate lace overlay to make the design more up-to-date and suitable for young women.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image032.jpg" class="img-responsive center-block"> </p>
<p><strong>Let me say a couple of words about something that I use quite frequently - mock-up fabric.</strong> No living fashion designer or dressmaker could possibly imagine their work without this fabric. You should definitely add it to your toolkit! This fabric is above all essential for creating various patterns in the mock-up technique (from basic ones to complicated corset and dress patterns). </p>
<p><strong>What is mock-up fabric then?</strong> It is non-stretch fabric or thick resilient fabric normally made from natural fibers. It can be cotton or linen or contain viscose as well. This kind of fabric usually comes in the widths of 2m, 2.5m or even 3m and it's really handy for creating garments with the draping method. This fabric is also unbleached and its natural ecru colour does not wash off. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%201_files/image034.jpg" class="img-responsive center-block"> </p>
<p><strong>Now you know what main materials are required for our dress.</strong> </p>
<p><strong>In conclusion I would like to bring your attention back to the model of our dress.</strong> Certain challenges often arise when you are sewing a garment with sew-in cups (sewn or made of foam alike) and a low neckline at the back! More often than not the wearer tends to feel uneasy in a garment sewn in the wrong technique and assembled in the wrong way. She is likely to feel as though the dress were sliding off so she�s constantly trying to pull it up. I'm sure you know those awkward moves your client makes when she doesn�t feel comfortable in her dress. <strong>And for that very reason the lining of a low-back dress is often represented by a bodysuit - to dispel any doubts and give confidence to the wearer!</strong> The bodysuit should be dense and resilient, with a perfect fit. </p>

