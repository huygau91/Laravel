						<p><strong>Features:</strong></p>
<ul>
<li>Detailed analysis of the open-back dress styles.</li>
<li><span draggable="false">Recommendations and rules of choosing a right type of open-back garment.</span></li>
<li><span draggable="false">Choosing the right base: corset, bodice or bodysuit.&nbsp;</span></li>
<li><span draggable="false">Choosing the right depth of the cut on the back.</span></li>
<li>Creating a garment with a bodysuit base.</li>
<li><span draggable="false">Choosing the right shape of the straps.</span></li>
<li>Creating a garments with ready made foam cup.</li>
<li>Cutting and sewing of original removable light skirt.</li>
<li>For making this dress were used thin lace material and elastic transparent net.</li>
<li>Connecting bodysuit and the dress into one whole garment.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>How to connect any ready made cup to the dress.</li>
<li>How to draft a pattern for the cover of the ready made foam cup.</li>
<li>How to make a cover for a foam cup.</li>
<li>Modeling of the separately cut cup.</li>
<li>Modeling of the open backs.</li>
<li>Modeling of the straps with complex forms.</li>
<li>How to alter &nbsp;the pattern with separately cut cups for different body types.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>The main agenda of this course is to deliver the technique of sewing open-back dresses with bodysuit base. This knowledge can be used in different combinations and gives you a&nbsp;wide range of opportunities for creating cocktail, evening, prom and wedding gowns.</li>
</ul>
