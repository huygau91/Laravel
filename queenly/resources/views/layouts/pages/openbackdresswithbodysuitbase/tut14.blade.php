<h3>Tutorial 14. Joining the Backs of the Dress and the Bodysuit.</h3>
<h3>Sewing Down the Side Seams of the Dress.</h3>
<p></p>
<p><strong>I have decided to replace the crotch on the back piece of the bodysuit before joining it with the back of the dress. I cut out a whole crotch piece</strong>, rip off the old one and replace it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut out another whole crotch piece and sew it onto the front of the bodysuit. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we can join the back of the bodysuit with the back of the dress.</strong> </p>
<p><strong>It ought to be remembered that the zippers of the dress and the bodysuit are placed one underneath the other and in the same direction.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>When you unzip the dress, you see the zipper of the bodysuit - and when you unzip the bodysuit, you see bare skin. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I align the top edges of the dress and the bodysuit and secure them with pins.</strong> I align the notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I make sure the stops of both zippers are placed at the same level before pinning together the top edges of the second halves. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I join the back pieces along the top edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Next I sew down the side seams in the area where the lace got joined with the base of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I align the front of the dress with a half of its back and join them in the same area of the side seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image048.jpg" class="img-responsive center-block"></p>
<p>I highly recommend that you finish the side seams with a serger before sewing them down completely. <strong>It's also important to notch the raw side edges of the bodysuit and the lace at the end of the joining seam.</strong> There is no need in notching the raw side edges of the main fabric of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image050.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>First I sew together the lace pieces starting from the notch I made.</strong> If your lace is elastic enough, you can do it with the serger right away. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Next I join the dress pieces made of the main fabric.</strong> I pull the bodysuit up and push the lace overlay inside the dress for the sake of convenience. I start sewing from the end point of the joining seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>After pulling the bodysuit pieces up I sew them together making sure the seam allowance of the side seam stays between the dress and the bodysuit (with no contact to the skin). </strong>It's best to use a serger or a flat lock machine to make the side seams elastic. I stop stitching about 1cm from the joined main fabric pieces.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>The side seam looks like this when sewn down: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same procedures with the other half of the dress: notch the seam allowances of the bodysuit and the lace overlay, sew down the side seam of the overlay, join the dress pieces and sew down the side seam of the bodysuit. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image070.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the open areas of the side seams leaving about 6-7mm of the seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image074.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>Next I take some bone casing.</strong> The one I am using makes a perfect tunnel for a plastic bone. </p>
<p><strong>I direct the seam allowance of the side seam towards the front of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>I place the casing over the side seam overlaying the seam allowance and sew it down from both sides stitching along the very edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image080.jpg" class="img-responsive center-block"></strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image082.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>This is what it looks like from the face: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>I will insert a plastic bone into this tunnel. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the same kind of bone casing onto the seam allowance of the other side seam.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image090.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image092.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image094.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I trim the ends of the tunnels. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image096.jpg" class="img-responsive center-block"></p>
<p><strong>I need to prepare loops for the straps before finishing the top edge. </strong>&nbsp;</p>
<p><strong>I take my shoulder strap elastic and cut two 4cm-long pieces.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image098.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The strap should be attached next to the side seam on a low-back garment. </strong>Sewing it into the side seam is awkward because the bone casing makes it rather thick. You can choose where you want to place the loops: either right behind the side seam at the back or just before the side seam at the front. <strong>I prefer sewing them onto the front </strong>since the straps will then press the cups and the entire front piece tighter to the chest.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image100.jpg" class="img-responsive center-block"></p>
<p><strong>I prepare two loops and sew them on just above the side seam, perpendicular to the top edge of the dress. The loops are facing down. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image102.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image104.jpg" class="img-responsive center-block"></p>
<p><strong>Bit by bit, our dress is acquiring a more distinct look.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2014_files/image106.jpg" class="img-responsive center-block"></p>
