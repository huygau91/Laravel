<h3>Tutorial 9. Making a Pattern of a Fabric Cover for the Cups.</h3>
<p></p>
<p><strong>Let�s make a cup pattern following the same mock-up method.</strong> I mark a model line on the foam cup.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Then I overlay it with a piece of mock-up fabric and secure it along the top edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>We need to decide where to make a dart.</strong> </p>
<p><strong>You can make darts from both sides or from just one. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p>The placement of the dart is important because it will later help you locate the lengthwise grain correctly against the bottom edge of the cup. It can be crucial in some cases! Sometimes you need the lengthwise grain to go strictly horizontally. This can be due to the peculiarities of the print that needs careful matching (checks, stripes, etc.) or to account for the direction in which the fabric stretches better or worse, etc. Sometimes it has to do with the style as well. There are lots of factors and each individual case requires that you find the best way to place the dart. </p>
<p><strong>The location of the dart is not that important in our particular dress so I've decided to place it on the outward-facing side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I start by pinning the model line on the cup</strong> that shows through the fabric to secure the cutting line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I push the fabric towards the dart. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I pin along the bottom edge of the cup trying to spread the fabric as carefully as possible.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the dart to make it start at the model line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I shape the dart and secure it with pins from both sides. It follows the model line precisely. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the fabric with additional pins.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trace the model line, outline the dart from both sides, and mark its top point.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image023.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I outline the cup, too.</strong> It doesn't matter whether you outline it from the inner side or from the face. Do as you wish. First I trace the contour of the entire cup and then add the seam allowances. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>We got a precise pattern of the cup. </strong>The mock-up method allows you to make a pattern of any kind of a cup regardless of its size and shape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I add seam allowances.</strong> The seam allowance along the top edge of the cup should be 1.5cm because of the specific way I want to attach the fabric cover to the top of the cup. There is a 0.7cm seam allowance along the bottom edge of the cup and a 1.2cm allowance for the princess seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I transfer the patterns of the top and bottom of the cup onto paper</strong> adding seam allowances in the joining areas.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put the cup patterns onto fabric and cut them out.</strong> I cut perpendicular to the lengthwise grain and make notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image039.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>The bottom part of the cup has a lace overlay.</strong> It's best to place flower motifs in the middle of it. I fold the lace in half aligning the motifs, put the bottom cup piece over it and cut it out.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>The cup pieces are ready.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%209_files/image044.jpg" class="img-responsive center-block"></p>

