<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to sew in the zippers.</strong> Please note that there are two different zippers: a regular zipper for the bodysuit and a blind one for the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>It's important to join the back halves with their lace overlays before sewing a zipper into the dress.</strong></p>
<p><strong>We need to join the lace with the main fabric in the same area of the side seam as on the front piece. </strong>I stitch at a presser-foot distance from the edge.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I join the lace with the main fabric along the central back seam from the top down to the level 6cm below the zipper notch. </strong>I use a 1.2cm seam allowance since the allowance of the central seam is 2cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Once you have joined the lace with the main fabric along their vertical edges, you can also join them along the neckline of the back.</strong> I stitch at a presser-foot distance like before. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I join the other half of the back with its lace overlay exactly the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I will sew the zippers in one after another since it is done with different presser feet.</strong> Let me remind you that it makes a lot of sense to duplicate the zip-up area with an iron-on material if you are working with high-stretch fabric (knitted fabric, for example). You finish sewing as you reach the notch.</p>
<p><strong>I start sewing the zipper into the dress </strong>using a regular presser foot.</p>
<p><strong>I stitch through the tape of the zipper. </strong>There is 2cm between the edge and the teeth (the width of a seam allowance). Make sure to align the notches when sewing down the other side of the zipper tape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I sew the other zipper onto the bodysuit the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Let us return to the zipper of the dress.</strong></p>
<p><strong>I switch to a specialty invisible zipper presser foot and stitch next to the teeth of the zipper.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I start joining the back halves of the dress. I stop at the end of the area where the lace is sewn to the main fabric </strong>and switch to the one-sided presser foot. I place the needle right at the point where I stopped sewing in the zipper, make a bar tack, and make a neat seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image064.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>The zipper is safely secured on the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>Now let's finish working on the zipper of the bodysuit.</strong> I stitch next to the teeth of the zipper using the one-sided presser foot - i.e. use the blind zipper method for a regular zipper. Try to stitch as close as possible to the teeth. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image072.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>I zip the bodysuit up and check the quality of my work.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>Then I align the seam allowances of the central seam, pin them together right where I stopped sewing in the zipper, place the needle at the same spot and join the back halves of the bodysuit down to the very bottom.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image078.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image080.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image082.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image084.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>Let's finishing working on the central seam of the dress.</strong> </p>
<p><strong>I notch the lace down to the seam that attaches it to the main fabric.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>I align the scallop edges and sew the lace pieces together.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image090.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance of the lace down to 0.8cm. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image092.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I get the lace out of my way and sew down the seam on the main part of the dress. </strong>I basically continue the seam that attaches the zipper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image094.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image096.jpg" class="img-responsive center-block"></p>
<p><strong>All we need to do now is simply press those seams. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2013_files/image098.jpg" class="img-responsive center-block"></p>

