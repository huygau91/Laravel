<h3>Tutorial 13. Working on the Backs of the Dress and the Bodysuit.</h3>
<p></p>
<p><strong>The first step is to cut a crotch for the back of the bodysuit and sew it in place.</strong> I cut it out of the same mesh fabric as the bodysuit using the latter as a template. Since it's really easy, let me simply illustrate the process with step-by-step pictures without additional comments.</p>
<p>
