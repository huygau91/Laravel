<h3>Tutorial 18. Finishing the Tunnel for the Semi-Circle Cup Underwire.</h3>
<p></p>
<p><strong>Finally, we need to finish the inside of the cups and insert the semi-circle underwires. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>After that the dress will be basically ready to wear!</strong> The inside of the dress is already finished: we have supplied the bodysuit with a hook-and-eye fastener and finished the edges with elastic bands. We still need to finish the hem but since it's just a training sample I will simply secure it with some fusible web without serging it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Well, let us work on the cups now.</strong> </p>
<p><strong>Try your best to make the bone casing overlay the seam allowance of the cup and settle in a semi-circle.</strong>&nbsp; In the end you need to insert a semi-circle underwire into the tunnel.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p>This is one of the most challenging steps! I believe the most appropriate solution is to secure the bone casing with a generous amount of pins and stitch over the face side. </p>
<p><strong>I pin the casing down very carefully, fold the seam allowance and hide it underneath.</strong> I leave small tips from both ends of the casing. I will trim them after making horizontal bar tacks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Feel free to use a specialty edge-stitch presser foot if you're not sure you can manage to stitch along the very edge of the cup. </strong>As for me I hardly ever use it.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the bone casing on really close to the seam that attaches the cup.</strong> Make the stitches really neat and even, as close as possible to the edge, making sure to catch the casing at the same time. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch along the inside of the cup to secure the other side of the casing.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image021.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image023.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>The casing is sewn in place.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I do the exact same thing with the other cup.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image033.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I trim the ends of the bone casing.</strong> I do it carefully to avoid cutting into the cup.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image037.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>Next I insert the semi-circle underwires and secure each tunnel with close zigzag stitches from both ends.</strong> Don't forget that it makes a difference which end of the underwire faces the centre of the front and which one faces the underarm. Make sure to insert them correctly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image041.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>I insert an underwire into the tunnel on the other cup exactly the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>I distribute the underwires inside the tunnels to have them placed at the same level.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>Here are our cups:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image049.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2018_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>And, finally, we have reached the last work stage - the straps. </strong></p>
<p>You can sew them yourself but it is also fine to use ready-made shoulder straps from a shop. You could use clear silicone straps as well. </p>

