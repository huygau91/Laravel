<h3>Tutorial 4. Finishing the Dress Pattern. Working on the Bodysuit Pattern.</h3>
<p></p>
<p><strong>The side seam contour of the back piece:</strong></p>
<p><strong>I shift it 2.5cm inwards at the bust-line level, 3cm at the under-bust level, 2cm at the waistline level, 1.5cm at the stomach level 12cm below the waistline, and 1.5cm at the hip level.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I cut along the adjusted side seam line of the front piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I simply use it as a template to transfer the side seam contour onto the back piece.</strong> The side seam should pass through all reference points. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I align the top edges of the front and the back of our dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Below are the front and the back with their new side seam contours:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The final configuration of the top edge looks like this on the front piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next I glue together the sheets of the bodysuit pattern. </strong></p>
<p><strong>The back of the bodysuit:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The front of the bodysuit:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The top edges of the front and back of the bodysuit are fully identical to those of the dress.</strong> </p>
<p><strong>When you start drawing the cradle on the front of the bodysuit, simply cut along the top edge of the dress front pattern, put it down, align the edges, and trace the contour. </strong>The only difference between the dress and the bodysuit front patterns is that the latter is narrower in the waistline area.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Their back pieces differ respectively. Besides, there is a dart at the back of the dress while on the bodysuit it is transferred into the central back seam.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we need to apply all alterations to the side seams. </strong></p>
<p><strong>The side seam of the bodysuit front piece:</strong></p>
<p><strong>I shift it 1.5cm inwards at the bust-line level, 2cm at the under-bust level, 1cm at the waistline level, 0.5cm at the stomach level 12cm below the waistline.</strong> The hip line is not relevant here. <strong>I re-draw the contour.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I use the dress front piece as a template to re-draw the top edge of the bodysuit piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>We have adjusted the pattern of the front of the bodysuit!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The side seam contour of the back of the bodysuit:</strong> </p>
<p><strong>I need to shift it 2.5cm inwards at the bust-line level, 3cm at the under-bust level, 2cm at the waistline level, and 1.5cm at the stomach level 12cm below the waistline.</strong> To make it easier, I put the back of the dress over the back of the bodysuit and align their waistlines and the reference points on the bust line (minus 2.5cm). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the side seam contour of the back of the bodysuit.</strong> Remember to transfer all notches onto the new line.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you that the bodysuit pattern includes the following seam allowances:</strong> 2cm for the central back seam (same as on the dress) to insert a zipper or any other fastener, and 1.2cm for all other seams. </p>
<p><strong>Make sure to measure the area of closure at the bottom of the bodysuit.</strong> Don't forget that the front of the bodysuit has a fold line and there is a 1.2cm seam allowance in the closure area from the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>We need to decide how we are going to distribute our hooks and eyes.</strong> The width of the closure area is 5cm on my pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I check my hook-and-eye tape.</strong> It's quite possible to cut 5cm from it without hitting a hook or an eye.&nbsp; But first I will finish the area of closure itself.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>There is an additional measurement required for bodysuits - crotch length.</strong> It is taken with the measuring tape between the legs from the middle of the waistline at the front to the middle of the waistline at the back. Please be sure to compare your pattern against your client's measurements or else the panties might come out too long or too short. And remember to account for the width of the hook-and-eye tape when closed.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the dress and the bodysuit pieces.</strong></p>
<p><strong>Then I put the front of the bodysuit onto the front of the dress.</strong> The cradles of the face (dress) and the lining (bodysuit) match perfectly. The bodysuit is narrower than the dress in the waistline but their sizes are fully identical again at the stomach level. There is a reason for it: we will ease in the area of the bodysuit around the legs onto an elastic band yet at the same time we don�t want to make it too tight in the stomach - otherwise it is likely to form a noticeable 'bubble'. The size of the bodysuit coincides with that of the dress from the stomach level and down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I put the back of the bodysuit onto the back of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>The back of the bodysuit is also slightly narrowed <strong>at the waistline</strong> (by about 1cm) but then again the same size as the dress at the stomach level. <strong>The dart at the back of the dress is compensated for in the central back seam of the bodysuit. The dress is 1cm wider than the bodysuit at the top.</strong> Technically, you could extend that upper part of the bodysuit to the very edge of the dress pattern if you wanted to use fabric that doesn't stretch well, or for whatever other reason. However this extra centimeter of the dress will get re-distributed anyway and ensure better fitting at the back since you normally ease the back of the dress onto the lining, i.e. onto the back of the bodysuit in our case. The dress will have a really slim fit at the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%204_files/image046.jpg" class="img-responsive center-block"></p>

