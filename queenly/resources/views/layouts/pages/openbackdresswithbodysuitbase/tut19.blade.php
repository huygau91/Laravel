<h3>Tutorial 19. Cutting the Skirt.</h3>
<p></p>
<p><strong>It�s time to sew the skirt of our dress.</strong> You can either sew it in for good or make it removable. </p>
<p><strong>I have used bobbinet fabric with the width of 3m for cutting my skirt.</strong> I'll explain the cutting process schematically on paper.</p>
<p><strong>I�ve cut out five rectangular pieces of the same width (3m) but of different lengths.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The first one is a rectangular piece of bobbinet that is 3m wide and 130cm long.</strong> I used it for cutting two right-angle trapezium pieces with the sides of 30cm and 100cm. </p>
<p><strong>The second rectangular piece of bobbinet is 3m wide and 100cm long.</strong> It was used for two right-angle trapezium pieces with the sides of 30cm and 70cm. </p>
<p><strong>Finally, the third rectangular piece of bobbinet is 3m wide and 70cm long.</strong> I used it for two right-angle trapezium pieces with the sides of 20cm and 50cm. </p>
<p>These pieces will form beautiful lambrequin-like drapes. Since the skirt has a left side and a right side, all pieces should be cut out in pairs. </p>
<p><strong>The fourth and the fifth piece make the front of the skirt. They are two rectangular pieces of bobbinet, each 3m wide and 25cm long.</strong></p>
<p><strong>It's easy to calculate how much bobbinet fabric you need </strong>by summing up the lengths of all rectangular pieces. <strong>I need as little as 3.5m of 3m-wide bobbinet for the entire skirt,</strong> which is a lot less than what you'd need for a full sweep skirt, let alone a multi-layer full sweep skirt. The skirt will be lightweight and really full despite the thrifty fabric consumption. </p>
<p><strong>Let me show you an easy way of cutting trapezium skirt pieces on a small rectangular piece of bobbinet. </strong></p>
<p><strong>I cut a piece of bobbinet that is 300cm wide and 130cm long for the first piece and fold it in half widthwise. </strong>Bobbinet is normally sold that way - folded in half. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I mark 30cm inwards from the top and the bottom of the raw edge and mark the very middle of the fold line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I connect the points into a triangle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Next I cut the top layer of bobbinet along the first side (1), from the edge to the midpoint. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the fabric around and cut the top layer along the other side of the triangle (2) from the edge to the midpoint. </strong>The cutting line shows through rather clearly. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The result is two trapezium skirt pieces with the width of 30cm and 100cm and the length of 3m. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Next I sew a ribbon along the straight side of each piece. </strong>I have simply pressed a bias tape open for that purpose. You could use a 2-3mm wide straight satin ribbon or a lace ribbon depending on the design. </p>
<p><strong>Then I gather the inclined edges of the pieces down to the length of about 90cm </strong>using a special-purpose ruffler foot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I cut all skirt pieces this way. </strong></p>

