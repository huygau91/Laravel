<h3>Tutorial 5. Working on the Strap Pattern.</h3>
<p></p>
<p><strong>We need to work on the pattern of the upper part of the dress - the straps that resemble a short-sleeve bolero.</strong>&nbsp; This half-bolero will be incorporated into the dress. </p>
<p><strong>I glue together the pattern sheets and get two pieces of our half-bolero: the front and the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The pattern I offer you accounts for two versions of bust circumferences: 88cm (the black line) and 92cm (the red line).</strong> </p>
<p><strong>You are probably asking, 'But what if my client has smaller or larger breasts? How am I supposed to adjust the pattern then?'</strong> </p>
<p><strong>First of all</strong>, you can adjust it after your client's key measurements: neck base to bust middle, shoulder to waist, shoulder slope, front width, armscye circumference, neck hole circumference, front neck base to waist, side length, bust middle, etc. In other words, you can adjust the pattern the way you normally do it for a garment with shoulder seams. I explain it in great detail in another course: �My Mum's Dress.�&nbsp; </p>
<p><strong>And then</strong>, you can also use an easier though less precise method. There are thin transversal lines marked along the entire perimeter of the pattern. The distance between the black and the red contour (i.e. 82cm and 92cm bust circumference patterns) at the intersection with transversal lines is always 6mm. Thus if the difference in bust circumferences comprises 4cm, then the distance between their points of intersection with transversal lines equals 6mm. </p>
<p><strong>I begin adjusting the half-bolero pattern after my client whose bust circumference is 96cm.</strong> It differs from the standard pattern with the bust circumference of 92cm (red line) by 4cm. This means I need to mark 6mm outwards from the red line at each intersection point with a transversal line. I will get a larger pattern by connecting the resulting points (blue line). </p>
<p><strong>Alterations at the back of the half-bolero:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I adjust the pattern of the front of the half-bolero exactly the same way.</strong></p>
<p>Frankly speaking, I am not a big fan of using such methods since it makes rather rough patterns and you always have to adjust them further. But it does make sense to use it as a guide. </p>
<p><strong>Please note that there are no seam allowances on these patterns</strong> as indicated. </p>
<p><strong>If your cups are not the same size as mine</strong>, find the centre of the breast on the bust line right on your cup (in my case 6cm down from the top edge). </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then carefully put the cup in its place on the half-bolero</strong> and make sure the shoulder piece reaches the top edge of the cup. In my particular case I need to prolong the shoulder piece by one centimeter.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the side piece and align it with the shoulder piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Right now the task is to define the armscye and the side seam of the bolero.</strong> </p>
<p><strong>I want the bolero to start below the bust line at the front.</strong> I know it for sure that the top edge of the dress will inevitably shift down when I start working on the low neckline at the back. And I don't really like leaving bare skin uncovered. So I want the bolero piece that performs the function of straps to begin somewhere at the under-bust level, follow the edge of the cup, and go up towards the neck diagonally. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I align the patterns of the dress and the bolero along the bust line</strong> and draw the under-bust line on the side piece of the bolero. This means we need to start cutting the bolero from this very line.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p>I also hate it when the strap arrives right at the corner of the side seam. It never looks good! <strong>It makes sense to step about 3cm inwards from the side seam at the under-bust level</strong> and attach the strap there. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I adjust the bolero pattern. It's best to make the edge of the bolero convex to maintain the overall harmony.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p>My client's side length measurement is 18cm as compared to 16cm on the pattern. I should lift the sleeve of the bolero up by 2cm or else the armscye will be too low. <strong>I prolong the side seam upwards by 2cm and re-draw the armscye. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now let�s add seam allowances to the front of the bolero and cut it out. The pattern will be as good as finished. </strong>The only thing is I'm still hoping I can sew the bolero without shoulders seams so I haven�t accounted for them. <strong>I add 0.5cm seam allowances</strong>. Since the bolero is made of very thin knitted fabric and finished with a very thing elastic band, I will assemble and handle it with the overlock machine.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>The upper part of the front looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s alter the back of the bolero.</strong></p>
<p><strong>I prolong the side seam upwards by 2cm and re-draw the armscye, same as on the front piece. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image025.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I align the front with the back and mark the length of the latter. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I leave 3cm for the joining seam at the bottom of the back piece and 1.5cm for a hook or a button fastener. I make the open edge of the back piece concave</strong> to help the bolero settle upon the wearer's back in a graceful way. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>Now you have seen the shaping process on paper.</strong> You could also do it using the mock-up method (sometimes it�s even more convenient). You just cut all standard patterns out of mock-up fabric, put them on the dress-form, and find all relevant reference lines. Actually, the mock-up method allows you to define the side length of the bolero a lot more precisely since the dress is supposed to have been nearly finished by the time and you can see the actual way the bolero looks on the dress-form. There is only one challenge: mock-up fabric is non-elastic and you'll have to account for it since the garment will be made of knitted fabric. </p>
<p><strong>I add 0.5cm seam allowances and cut out the back piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I glue together the patterns of the front and the back to cut them out as a single piece without shoulder seams.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>Voila! The mock-up of the lace bolero piece is ready! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>This is how you join it with the dress later:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%205_files/image037.jpg" class="img-responsive center-block"></strong></p>

