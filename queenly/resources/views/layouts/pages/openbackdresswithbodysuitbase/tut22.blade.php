<h3>Tutorial 22. Applying the Final Touches to the Dress. </h3>
<h3>Pictures of the Finished Garment.</h3>
<p></p>
<p><strong>I have carried out final fitting.</strong> The garment showed no flaws except for some minor technical moments. I have decided to re-stitch the straps a little closer to the centre because you could see the loops from the side view. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let me say a couple of words about the skirt.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></p>
<p>As I have already mentioned, <strong>you can make the skirt removable</strong> by adding a waistband or a wide ribbon at the top. You could use a lace bias tape, too. What you need to do to make the skirt removable is put it on the client, mark its precise location and then sew snap fasteners or hooks onto it. The skirt is really lightweight and it'll hold well around the body. </p>
<p><strong>I don't want to make my skirt removable.</strong> During the fitting I studied the dress on my client, determined necessary adjustments, and then marked the location of the skirt, pinned it onto the dress and overlaid it with lace. I sewed the skirt and the lace on by hand strictly after the markings I made during the fitting. I let the fabric lie naturally as I was sewing. It will get pulled on when in wear and the dress will fit like a glove. </p>
<p><strong>Go creative with handwork: feel free to use beads, rhinestones or decorative flowers.</strong> You can overlay the seams that don't look even enough (if there are any). My dress features minimum handwork embellishments. </p>
<p>The only thing I find appropriate is to <strong>cover the very bottom of the back neckline with small lace motifs</strong> to disguise the elastic band even though it's a pretty scallop-edge one. I just want to create a more glamorous look. I sew those bits of lace on by hand as well.</p>
