<h3>Tutorial 20. Assembling the Skirt.</h3>
<p></p>
<p><strong>All skirt pieces have been prepared for assemblage.</strong> </p>
<p><strong>I pair up the lambrequin pieces: the gathered part is at the top and the ribbon at the bottom. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>There should be lambrequin-like drapes on the skirt, i.e. the ribbon should cascade down in a spiral. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I have also prepared two pieces for the front of the skirt. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The base for assembling the skirt is a 5-6cm wide strap of organza.</strong> You should mark the placement of each piece of the skirt on it and mark the length of the top edge of the skirt as well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I put the dress side seam up in front of me and find the waistline.</strong> The top edge of the skirt will be located about 10cm below the waistline of the dress. I will also make a shaped transition in height from the front to the back of the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the organza strap in half, place it at the line that marks skirt placement on the dress, and mark the location of the fold line, the side seam, and the back.</strong> I transfer the marks onto the other side of the strap.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I've decided that the central part of the skirt should be sewn on starting about 12cm from the side seam.</strong> There is 10cm between the starting point and the fold line, which means the entire front of the skirt has to fit into an area of about 20cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The remaining area left for attaching three lambrequin pieces goes from the end of the front piece to the centre of the back and equals 33-34cm.</strong> We need to divide it in three 11cm long sections. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>The straight part of the skirt will look like this on the dress:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next we will take a short piece folded into lambrequin drapes and sew it with the shorter side to the centre and the longer side to the back.</strong> And then we will sew on the remaining two lambrequin pieces: the medium one and the longest one. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I can tell that the skirt will be quite full if we use a single layer for the front; I cut the piece in half.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I put together both halves of the front piece and pin them down onto the corresponding area of the organza strap.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>There's another area marked on the organza strap onto which the next piece of the skirt should be pinned - the short lambrequin piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I put it shorter side to the centre</strong>, fold the raw side edge, and mark the length of the top edge in accordance with the mark on the organza strap. Keep in mind that the front skirt piece will overlay it by about 2cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the piece in large pleats preserving the required length of the top edge (11cm).</strong> It looks like lambrequin drapery now. Although the piece is not that closely pleated, all layers of fabric together make the skirt really full.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I place the lambrequin piece underneath the front piece by 2cm and pin it onto the corresponding area of the organza strap.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the next piece down based on the same principle:</strong> I put it shorter side to the centre, fold the side edge, mark the length of the top edge after the mark on the organza strap keeping in mind it's going to be overlaid by the front piece by about 2cm, fold the piece into large pleats and pin it in place. Since all pieces are equally closely pleated, the number of folded layers is also the same on all of them.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I pin down the last longest piece of the skirt following the same steps.</strong> Let me remind you that the bobbinet and the ribbons should be pressed thoroughly by the time you start assembling the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>We can assemble the first part of the skirt now.</strong> The ribbons form beautiful waves when you let the piece hang freely, making it look as though you had to do some really sophisticated cutting and sewing tricks to achieve that. But it was in fact really easy, as you know now!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I assemble the other half of the skirt the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Later I will bend the organza strap upwards from the inside, sew it down, and serge the top edge.</strong> It will be thin and flat. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>You can attach the skirt to the dress any way you like:</strong> you can use a lapping seam or some kind of a decorative lay-on element or do the blind stitch to make the skirt even fuller in the joining area.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2020_files/image064.jpg" class="img-responsive center-block"></p>

