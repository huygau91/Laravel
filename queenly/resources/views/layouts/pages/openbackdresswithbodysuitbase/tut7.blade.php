<h3>Tutorial 7. Cutting a Lace Overlay and a Mock-up for the Dress.</h3>
<p></p>
<p><strong>Let us cut out a lace overlay for the back of the dress.</strong> I fold the lace crosswise with the scallop edge at the bottom and align the scallops symmetrically.<strong> I sew down the darts on both halves of the back and press the seams flat towards the centre.</strong> Then I put the back piece onto the lace.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the entire piece out </strong>and make notches. We will work on the shape of the back later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I pin together all pieces of the back of the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I will cut the front of the dress on the open.</strong> I put the front piece made of the main fabric on the table and carefully spread it out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Why have I decided to cut it on the open?</strong> Although the exact placement of the motifs or the scallops of the lace fabric on the piece is not important in my case, it might be crucial in yours. Cutting on the open allows you to place the lace motifs more precisely. Moreover, cutting lace with a fold line often results in distortion, especially if it's really fine and delicate. </p>
<p><strong>I overlay the piece with lace and cut it out.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Let's move to the next stage.</strong>&nbsp; The dress pattern is ready but we still need to make fabric covers for the cups and embellish them with lace insets. <strong>The easiest way to make a fabric cover for a cup like ours </strong>is to make a pattern of the cup after the mock-up method and then cut and sew the cover. <strong>We also need to cut a mock-up of the dress itself</strong> to double-check the configuration of the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>But first let's cut another lace piece - the short-sleeve bolero.</strong> </p>
<p>I fold the lace in half trying to align the scallops and the motifs on both halves. My lace fabric stretches better along the crosswise grain. <strong>I put the bolero piece onto it with the shoulder perpendicular to the crosswise grain.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I cut the piece out.</strong> All seams allowances have already been accounted for. I make notches at both ends of the shoulder line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now let's cut a mock-up of the dress.</strong> It's enough to cut just one half per front and back and then sew down the side seam. You could even shorten the pieces down to the hip line to spare your mock-up fabric. I outline the dart. I highly recommend that you sew it down and not just pin it down. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out my mock-up half of the dress and notch the edges.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>

