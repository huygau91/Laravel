<h3>Tutorial 21. Working on the Waist of the Skirt.</h3>
<p></p>
<p><strong>I make a row of regular straight stitches through all skirt pieces pinned onto the organza strap.</strong> Keep adjusting the organza to prevent it from curling up, gathering or getting eased in underneath the layers of bobbinet.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Once you have attached all layers, carefully feel the seam with your fingers and remove all pins. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I turn the skirt over, bend the organza strap upwards, and stitch along at a presser-foot distance from the joining seam. </strong>This is done in order to flatten the joining seam. I carefully spread the layers of the skirt to make sure they don't get caught in.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>This seam requires an overlock finish.</strong> I trim the tips of the organza strap. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2021_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>You are free to choose what you want to do next:</strong> you could either set the skirt onto a waistband and attach it to the dress with a lapping seam (it will be less full then) or simply sew it to the dress with a blind seam (it will make the skirt fuller). </p>
<p>If you wish to use this skirt as a separate garment, I highly recommend that you add a couple more layers of bobbinet and a lining.</p>

