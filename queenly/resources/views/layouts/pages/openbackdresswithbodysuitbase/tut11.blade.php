<h3>Tutorial 11. Encasing the Cups in Fabric. Sewing the Cups into the Dress.</h3>
<p></p>
<p><strong>I assemble the fabric cover of the cup </strong>aligning the raw edges and the notches of the pieces. The seam allowance is 1.2cm. Don�t forget to make bar tacks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I sew on the lace overlay at the bottom of the cup.</strong> I stitch at a presser-foot distance from the edge.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I assemble the other cup the same way and sew the lace overlay onto its bottom part. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I press the seam allowance of the joining seam flat towards the top.</strong> I am using a large cup pressing mould. I am not pressing the entire cup the way you usually do to shape corset cups. Our cups are soft and it's enough to just slightly press the seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image013.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowances down to 4-5mm and trim their corners.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image017.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image019.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>Our dress features �straps� and we need to mark their location on the cups before encasing them in fabric. </strong></p>
<p><strong>I mark 4cm inwards along the top edge of each cup. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image023.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut two pieces of shoulder strap elastic, each 3cm long, and pin them at the marked spots. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>You can stitch through the middle of the elastic where it's going to bend. It'll make it more robust and more comfortable.</strong> But please note that by doing so you will flatten the cup in that area, which might show through the fabric cover. Our cups feature a lace overlay at the bottom that will cover up the joining seam and disguise everything. But if you wanted to make perfectly smooth cups, you would have to refuse the idea of stitching through the middle of the elastic and simply secure the loop in the joining seam. </p>
<p><strong>I make relevant markings and stitch through the middle of the elastic.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image033.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I fold the elastic over the edge and secure the loop in place.</strong> This loop allows you to hook a shoulder strap onto the cup. You can make removable straps depending on your client's preferences.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the same kind of shoulder strap elastic onto the other cup.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image041.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Next I start encasing the cup into its fabric cover.</strong></p>
<p><strong>I put the fabric cover face side inside the cup, let the seam allowances out, and align the top edges of the cup and the cover.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Simply turn the cup out for convenience's sake and pin the cover down along the top edge.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I start sewing the cover down along the top edge. </strong>I use a 0.7mm seam allowance to stitch through the slight depression on the cup. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image050.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the pins and turn the cup right side out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I turn out the fabric cover, too. </strong>The top of the cup looks perfectly neat and the loop is in place. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully spread the fabric cover over the cup and secure it with pins along the bottom edge.</strong> I use quite many pins to avoid creases and help the fabric settle smoothly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to sew the cover onto the cup as close as possible to the bottom edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>The cup is now encased in fabric. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I even out the seam allowance.</strong> It should be the same on the main fabric and on the lace.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>We need to sew the cup into the dress now.</strong> </p>
<p><strong>There are two ways you can do it.</strong></p>
<p><strong>Option 1: </strong>You can sew the cup in by aligning its seam allowance with that of the cradle.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>Option 2:</strong> Or you can do it the way it is done on see-through corsets: fold the seam allowance of the cup inwards, baste it, and sew the cup in with a lapping seam strictly following the seam that attaches the fabric cover to the bottom edge of the cup. You will need to get into the same row of stitches when sewing on the bone casing.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>The second option with a lapping seam is a lot easier, faster and more precise.</strong> You won't be able to see the joining seam once the cup is overlaid by lace and fully decorated. </p>
<p><strong>But you would prefer the first method if the cups were perfectly smooth.</strong> </p>
<p>Although we could use the easier option number two since our cups feature lace overlays, <strong>I will demonstrate you how to sew a cup in using the first, more complicated method</strong> of aligning the seam allowances of the cup and the cradle.</p>
<p><strong>I align the cup with the cradle.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>The cup is supposed to look like this when sewn in the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the cup in using a 1.2cm seam allowance.</strong> I�ve notched the seam allowance of the cradle to make it easier. Remember to make bar tacks at the beginning and at the end of the stitch.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image078.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image080.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image082.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image084.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>The cup is in place.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image088.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The cradle on the dress fits the cup perfectly.</strong> As a result there is a smooth transition from the dress to the cup in the middle and a seam allowance left at the side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image090.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%203/transcript/Tutorial%2011_files/image092.jpg" class="img-responsive center-block"></p>

