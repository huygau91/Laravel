<p>You need to find the sum of the unknown variables<strong> X + Y</strong>. It is supposed to equal a half of the difference between the values of client's measurements <strong>(c)</strong> and the values of pattern dimensions <strong>(d)</strong>. If there is any alteration to the bust middle <strong>(e)</strong>, you should account for its value: <strong>d + 2e.</strong></p>
<p>The difference between the unknown variables <strong>X - Y</strong> is supposed to equal the difference between the values added at the front <strong>(a)</strong> and at the back <strong>(b)</strong> from the previous line of the table, i.e. <strong>a - b</strong>.</p>
<p>It's the rare case when things are a lot easier in practice than in word.</p>
