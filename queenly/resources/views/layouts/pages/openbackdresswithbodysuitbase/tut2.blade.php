<p><strong>The part of the table involved in these calculations starts from the bust circumference.</strong> I copy the values of front and back side seam alterations at the bust-line level (-1.5cm and -2.5cm) into columns 5 and 6. These will be our basic values used in shaping the side seam contours at the front and at the back of the garment. </p>
<p>So I have narrowed the front down by 1.5cm and the back by 2.5cm from both sides <strong>at the bust-line level</strong>.</p>
