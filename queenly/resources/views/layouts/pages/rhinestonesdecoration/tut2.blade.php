<h1>Tutorial 2. Sewing On Rhinestones.</h1>
<p><strong>I arrange large rhinestones on the corset</strong> in accordance with the desired layout.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image002.jpg" alt=""></p>
<p>I thread the needle with nylon thread and start sewing.</p>
<p><strong>The technique is as follows:</strong></p>
<p><strong>I tie a knot and catch a little face fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>Then I hide the knot underneath the rhinestone:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image006.jpg" alt=""></p>
<p>And <strong>I carefully sew the rhinestone in place </strong>without piercing the corset through: only catching the top layer of fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image010.jpg" alt=""></p>
<p>The thread won't look white - it will blend with the shade of the rhinestone. <strong>I make about four stitches to secure it.</strong></p>
<p><strong>And I sew it on from the other end as well.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image015.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image016.jpg" alt=""></p>
<p><strong>I tie a knot and then another one:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image018.jpg" alt=""></p>
<p><strong>And I tuck the thread in the fabric to prevent it from going undone: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image019.jpg" alt=""></p>
<p><strong>I cut the thread. The rhinestone is secured in place!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image020.jpg" alt=""></p>
<p><strong>I sew the rest of rhinestones on the garment following the same technique.</strong> The empty areas between them will be decorated with glue-on rhinestones.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%202_files/image022.jpg" alt=""></p>

