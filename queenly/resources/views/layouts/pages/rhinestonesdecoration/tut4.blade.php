<h1>Tutorial 4. Applying Rhinestones.&nbsp; Part 1.</h1>
<p><strong>Now I will embellish the vertical panels.</strong></p>
<p>I sharpen the pencil and prepare a paper tissue. <strong>I apply the first drops of glue on the surface of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image002.jpg" alt=""></p>
<p>My task here is to arrange large and small rhinestones as if in a splash, i.e. create some "organized chaos." <strong>It's why I use both large and small rhinestones at the same time.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>I continue applying drops of glue and gluing rhinestones on.</strong> Apply a small number of drops at a time. You can start applying 10 at a time later when your working pace improves. I applied my first thousand of rhinestones in a rather short time and, believe me, your skills upgrade very much after that milestone! Your hand just "knows" how much glue to squeeze out and how hard to press the rhinestone down. And it doesn't take long to acquire those skills.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image006.jpg" alt=""></p>
<p>I shake the containers up every now and again to turn the rhinestones decorative side up.</p>
<p>Basically, <strong>you should have no problems decorating flat surfaces (prominent areas like cups are much more challenging).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image008.jpg" alt=""></p>
<p><strong>When you're decorating dome-shaped cups, the rhinestones may start sliding down.</strong> It's very important not to use more glue than needed. It's not so bad if you apply a little less than needed, but if you apply more - your rhinestones will start sliding off leaving glue stains behind. You need to find the happy medium. And then all rhinestones will stay in their places even in the most prominent areas of the corset.</p>
<p><strong>Whenever I reach the cups, I do the following:</strong> I decorate a small part of the convex area with rhinestones and place something underneath it to<strong> let it dry while lying horizontally.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image010.jpg" alt=""></p>
<p><strong>Then I turn the corset and decorate another part of the cup.</strong> Again, I place something suitable underneath the newly embellished area to let the rhinestones set with the garment lying horizontally. In this case the rhinestones cannot just slide down and you don't have to deal with those nasty glue stains.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image012.jpg" alt=""></p>
<p>I have added more rhinestones between the existing ones to make it richer. It's very easy to apply new rhinestones between the ones you have already glued on because they basically have nowhere to run, i.e. they cannot slide down. And it's exactly what I did to make the embellishment richer.</p>
<p><strong>The picture below shows the last unfinished area of the cup</strong> which I am about to complete with the help of large and small rhinestones.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%204_files/image014.jpg" alt=""></p>

