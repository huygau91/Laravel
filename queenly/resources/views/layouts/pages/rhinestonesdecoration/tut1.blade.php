<h1>Tutorial 1. Decoration Materials.</h1>
<p></p>
<p><strong>This chapter is dedicated to working with rhinestones. </strong></p>
<p><strong>I will decorate this corset </strong>using large and small glue-on rhinestones<strong>:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let's look at the materials I plan to work with. </strong></p>
<p>You can see <strong>a list of required materials</strong> in the picture below, as well as <strong>packs of ss16 and ss20 crystal-color rhinestones and large sew-on cut-glass rhinestones</strong> which will be the main highlight of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p>1. Ss16 and ss20 crystal-color rhinestones, </p>
<p>2. Sew-on cut-glass rhinestones, </p>
<p>3. 50LB nylon thread, </p>
<p>4. E6000 adhesive, </p>
<p>5. Syringe (5ml), </p>
<p>6. Pencil sharpener, </p>
<p>7. Rhinestone-picker pencil, </p>
<p>8. Small scissors, </p>
<p>9. Wet wipes, </p>
<p>10. Bead containers</p>
<p>I will sew the large rhinestones with <strong>50LB nylon thread.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p>I will also use some <strong>Ð•6000 adhesive.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>You need a syringe for applying the adhesive.</strong> Please note that it should be a 5ml syringe. Experience shows that smaller syringes are difficult to work with. And with larger syringes glue tends to leak out and you might spoil the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>A pencil sharpener</strong> is another must-have. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I use a special-purpose white rhinestone-picker pencil</strong> which needs to be sharpened every now and again. It is used for picking up rhinestones and putting them on the corset. I can say with full confidence that it's the most convenient tool for working with rhinestones of all sizes, including large ones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image014.jpg" class="img-responsive center-block"></p>
<p>Along with that, I always use <strong>wet wipes</strong> (any kind). Wet wipes are good for cleaning the nose of the syringe and the nose of the glue bottle to prevent the cap from sticking to it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Scissors or garden clippers:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>Clear round storage containers.</strong> I recommend this very kind. When you shake such a container up, rhinestones tend to turn decorative side up, which really spares you a lot of time.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/3/Tutorial%201_files/image019.jpg" class="img-responsive center-block"></p>

