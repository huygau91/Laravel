<h1>Tutorial 28. Joining the Sleeves with the Necklace. Finished Garment.</h1>
<p><strong>I gather the bottom of the sleeve so as to match the circumference of the cuff.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image002.jpg" alt=""></strong></p>
<p><strong>I pin the cuff to the sleeve, face to face, and align their edges.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image006.jpg" alt=""></p>
<p><strong>And I sew the cuff to the sleeve. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image010.jpg" alt=""></p>
<p><strong>I remove the pins, pull out the gathering thread, and secure the gathers with a row of stitches. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image014.jpg" alt=""></strong></p>
<p><strong>I trim the seam allowance...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image016.jpg" alt=""></p>
<p><strong>...and finish it with a bias tape to make it pretty on the inside.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image022.jpg" alt=""></p>
<p><strong>I have sewn the cuff to the sleeve.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image024.jpg" alt=""></p>
<p><strong>Next, I secure the sleeve with pins and sew from the beginning of the cuff up to about the armscye level (approximately 50cm).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image026.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2028_files/image030.jpg" alt=""></strong></p>
