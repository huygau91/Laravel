<h1>Tutorial 6. Testing the New Pattern on a Mock-Up.</h1>
<p><strong>The result of my work is a pattern comprised of three pieces. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image002.jpg" alt=""></p>
<p><strong>I cut them out from mock-up fabric</strong> and mark control notches and major circumference lines from either side of each piece. They will be my guidelines when I start joining the pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image004.jpg" alt=""></p>
<p><strong>It is necessary to notch the curved edges of all pieces rather deep before joining them. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image012.jpg" alt=""></p>
<p><strong>I pin the pieces together using the control points as guidelines.</strong> Please note that I first align the pieces along the major circumference lines and then distribute the fabric in-between in agreement with the control notches.</p>
<p><strong>I pin together the two halves of the central front piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image014.jpg" alt=""></p>
<p><strong>Next, I pin the side pieces to the central front piece along the princess seam line. </strong>I make more closely-spaced notches in the waist area of the side pieces and stretch the fabric when pinning.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image022.jpg" alt=""></p>
<p><strong>And I pin the central back pieces to the side pieces along the princess seam lines. </strong>Again, I notch the side pieces in the waist area and stretch them when pinning.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image028.jpg" alt=""></p>
<p><strong>The mock-up is ready and sitting on the dress-form.</strong></p>
