<p><strong>Now I need to measure all major circumferences</strong> to make sure the pattern is true. All measurements basically coincide with my client's.</p>
<p>I carefully study the shape of the princess seams, the depth of the neckline, and the length of the back.</p>
<p><strong>I feel like making the back a little lower.</strong></p>
<p>I will draw a beautiful back neckline, cut away all excess fabric, and then adjust the pattern by putting the cut-off part of the back over it, tracing the neckline, and cutting along the new line.</p>
<p><strong>When putting the mock-up on the dress-form, make sure the side seam line is strictly vertical.</strong> And pay attention to the armscyes, too.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image034.jpg" alt=""></p>
<p><strong>What concerns the back neckline,</strong> I want to make it a little lower but I prefer to do it right in the sewing process, i.e. leaving the pattern itself unchanged. I will re-draw the neckline <em>before</em> and notch out excess fabric <em>after</em> sewing bones on the dress.</p>
<p><strong>The central seam is supposed to be a vertical line on the mock-up.</strong> The actual central piece will be cut with a fold line, with no seam there.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%206_files/image036.jpg" alt=""></p>
<p>My main task was to help the side piece settle down evenly, without wrinkles. And I have achieved this!</p>
<p>When I was assembling the mock-up, I realized it was easier to sew princess seams by stitching over the side piece and not over the central front or central back piece.</p>
<p><strong>I re-draw the back neckline with a seam allowance already included and cut along the new line. </strong></p>
