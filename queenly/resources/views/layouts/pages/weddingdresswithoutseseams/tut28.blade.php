<h1>Tutorial 27. Cutting and Sewing Removable Chiffon Sleeves.</h1>
<p><strong>It's time to sew the removable multi-chiffon sleeves.</strong> These sleeves will be sewn to a clear necklace and their cuffs will be made of the same lace used on the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image006.jpg" alt=""></p>
<p><strong>The cutting process is very simple:</strong> I tear off a strap of chiffon that is 70cm wide and 150cm long (the full length of the fabric). Then I tear it in half to get two straps for the two sleeves: 70cm by 75cm each.</p>
<p><strong>I fold the first strap in half, notch the fold line, and mark the distance from the base of the neck to the elbow (about 40cm) to make a slit in the sleeve. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image008.jpg" alt=""></strong></p>
<p><strong>I tear the 40cm section along the fold line (right up to the marked point).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image012.jpg" alt=""></strong></p>
<p><strong>And I prepare the second sleeve the same way.</strong></p>
<p><strong>How to assemble the sleeve?</strong> I will finish the edges of the slit and the vertical edges the same way I finished the chiffon halter-neck straps, gather the two halves at the top to a width of 2-3cm and attach them to the necklace, sew together the two halves of the sleeve starting from the top and leaving 30-40cm open at the bottom, and gather the bottom of the sleeve to match the circumference of the cuff.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image016.jpg" alt=""></p>
<p><strong>I also need to cut the cuffs.</strong> There will be no fasteners on them. The cuffs will be 20cm wide at the bottom, 23cm wide at the top, and 14cm tall. I fold the lace in half trying to maintain symmetry between the motifs. I cut two pieces of lace (each 25cm by 14cm with seam allowances included), fold each one in half, mark 11cm along the bottom edge, and cut out the cuff.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image022.jpg" alt=""></p>
<p><strong>The cuff will look like this </strong>and I will sew it onto the chiffon sleeve.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2027_files/image024.jpg" alt=""></p>
