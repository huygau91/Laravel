<h1>Tutorial 17. Sewing the Front Princess Seams. Installing the Zipper.</h1>
<p><strong>I sew the front princess seams with a 1.2cm seam allowance. </strong>Be very careful when sewing princess seams for they are the key element on the face of your dress. Moreover, you should make sure not to ease in the central piece made of a single non-duplicated layer of fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image010.jpg" alt=""></p>
<p><strong>I don�t make a bar tack as I stop at the bottom of the skirt</strong> because I want to be able to rip up the seam when evening out the hem of the dress and working with individual layers of fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image012.jpg" alt=""></p>
<p><strong>I have noticed a flaw when examining the princess seam:</strong> I did not pull on the fabric of the side piece hard enough in the curved area of the princess seam, which has resulted in a tuck. I rip up the corresponding area of the princess seam, make additional notches on the side piece, secure the pieces with pins, and re-stitch it. Everything looks great now!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image018.jpg" alt=""></p>
<p><strong>I sew the other bust princess seam.</strong> I sew over the side piece moving from the end of the iron-on fabric up to the top. Then I turn the garment around and continue sewing the princess seam from the bottom of the skirt up to the point where I started.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image022.jpg" alt=""></p>
<p><strong>The princess seam looks very neat and pretty.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2017_files/image026.jpg" alt=""></p>
