<p><strong>I have finished sewing the front princess seams.</strong> And now I only need to press them.</p>
<p><strong>I will install a blind zipper in the back of the dress before sewing down its princess seams.</strong> I have already duplicated the zip-up area of the central back pieces down to the hip line level. My zipper is quite long but I will shorten it later.</p>
<p><strong>I always sew a blind zipper into the back of the dress before sewing down the central seam itself.</strong></p>
<p><strong>The blind zipper will be installed the traditional way:</strong></p>
