<h1>Tutorial 15. Sewing Ready Foam Cups into the Dress.</h1>
<p><strong>Now I will demonstrate how to sew foam cups into a mixed-technique dress </strong>which is supposed to be supported by bones and, at the same time, have soft cups. I have chosen beige cups solely because they will be seen more clearly in the pictures. You should naturally use white cups on this dress. If you cannot find ready cups of a matching colour, you can always encase them in fabric yourself.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image002.jpg" alt=""></p>
<p><strong>The cups will be sewn to the face of the lining (have contact with the body) and not in-between the layers of the dress.</strong> Cups sewn in this way imitate a bra secured inside the garment.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image004.jpg" alt=""></p>
<p><strong>I have already marked the position of the cups</strong> and I know the precise distance from the cup to the side seam, as well as the configuration of the bottom border of the cups. They are the key parameters.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image006.jpg" alt=""></p>
<p><strong>The correct position of the cups depends on the dress model.</strong> I position the cup so as to make the strap tab lie on the strap of the dress. If the size of your cups has been chosen correctly, their underarm edges will settle perfectly in the armscyes on the dress. If the cups are somewhat bigger than needed, you can always trim them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image008.jpg" alt=""></p>
<p><strong>I highly recommend making a row of stay-stitches along the V-neckline at the front of the dress before sewing in the cups. </strong>I forgot to do it and it cost me a lot of trouble later.</p>
<p><strong>I stretch the lining and pin the cup in place.</strong> My guidelines are those marked lines which you can see against the light.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image009.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image011.jpg" alt=""></p>
<p><strong>I fold back the neckline edge of the piece, smear the foam cup with Gutermann fabric glue, and stick it to the lining of the dress carefully pulling on and distributing the fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image013.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image015.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image017.jpg" alt=""></p>
<p><strong>The cup is now secured in place.</strong> Make sure to leave some space for finishing the edge of the neckline. I have left about 0.7cm for this purpose. If it is not enough, I will trim the cup in the process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image019.jpg" alt=""></p>
<p><strong>I install the second cup just the same way and perfectly symmetrically.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image021.jpg" alt=""></p>
<p><strong>There are special metal underwires that help cups hold their shape.</strong> One end of such underwires (the CF end which should be placed closer to the center and the front of the bust) is lower than the other end (the underarm end).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image023.jpg" alt=""></p>
<p><strong>The underwire is supposed to be 1-1.5cm shorter than the edge of the cup. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image025.jpg" alt=""></strong></p>
<p><strong>It is installed in the cup with the help of special bone casing.</strong> I would by no means recommend replacing it with a ribbon or a bias tape because they are simply not robust enough and may easily get torn by the tip of the underwire.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image027.jpg" alt=""></p>
<p><strong>You sew the bone casing to the bottom edge of the cup</strong> just like on a regular bra. When you insert the underwire in the tunnel, it will give proper volume to the cup.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image029.jpg" alt=""></p>
<p><strong>You can make the edge of the cup prettier by decorating it with lace.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image031.jpg" alt=""></p>
<p><strong>I start sewing the cups in the dress.</strong> First, I will stitch along the bottom edge of the cups to secure them in place and remove the pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image033.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image035.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image037.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image041.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image043.jpg" alt=""></p>
<p><strong>I lay bone casing along the bottom edge of the first cup and sew it on with two rows of parallel stitches.</strong> I leave a tip of about 1.5cm from either side. I will tack it with close zigzag stitches and trim it after installing the bone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image045.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image047.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image049.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image051.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image053.jpg" alt=""></p>
<p><strong>I sew bone casing onto the other cup.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image055.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image057.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image059.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image061.jpg" alt=""></p>
<p><strong>Both cups are installed in the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image063.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image065.jpg" alt=""></p>
<p><strong>I insert a metal underwire in the tunnel with the CF end facing the center of the cup.</strong> The underwire is 1cm shorter than the bottom edge of the cup from either end.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image067.jpg" alt=""></p>
<p><strong>The cup now has proper volume.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2015_files/image069.jpg" alt=""></p>
<p><strong>After installing underwires in the cups, I make bar tacks with close zigzag stitches at the ends of the tunnels and trim the tips.</strong></p>
