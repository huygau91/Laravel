<h1>Tutorial 24. Finishing the Slit and the Hem of the Dress with the Lining.</h1>
<p><strong>The slit in the middle of the front is not made in a seam </strong>so I need to cut up the fabric. I have already duplicated the slit area with iron-on batiste - both on the face and on the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image002.jpg" alt=""></p>
<p><strong>The fabric needs to be cut up to the marked end of the slit. Remember that the lining is supposed to be pulled 0.5cm higher up as compared to the face.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image004.jpg" alt=""></strong></p>
<p><strong>I turn out the dress, move away all other layers of fabric, lay the face fabric over a sleeve-ironing board, and cut it up right to the marked end of the slit. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image006.jpg" alt=""></strong></p>
<p><strong>And then I cut the lining up to the marked end of the slit. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image008.jpg" alt=""></strong></p>
<p><strong>Next, I turn out the dress and put together the edges of the slit on the face and on the lining (face to face).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image010.jpg" alt=""></p>
<p><strong>I align the points that mark the end of the slit and secure the layers with a pin. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image014.jpg" alt=""></p>
<p><strong>And I add pins along the full length of the slit. The lining is 0.5cm shorter than the face just like it was supposed to be.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image016.jpg" alt=""></p>
<p><strong>I pin the layers together along the other side of the slit just the same way.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image018.jpg" alt=""></p>
<p><strong>Again, the lining is 0.5cm shorter than the face.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image020.jpg" alt=""></p>
<p><strong>I have prepared the slit and approached the task of finishing its bottom edges and hemming the dress. </strong>I won't make the bottom edge of the slit round although it does look very nice on certain dresses. In this case, I will just finish it with the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2024_files/image022.jpg" alt=""></p>
<p>Before pinning the face with the lining along the bottom edge of the dress, <strong>I align and pin together all layers of the face </strong>(namely: the main fabric, the chiffon, and the lace).</p>
