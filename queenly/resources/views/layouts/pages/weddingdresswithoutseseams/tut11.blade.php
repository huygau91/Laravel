<h1>Tutorial 10. Marking the Placement of Bones. Cutting the Lacing Panels.</h1>
<p><strong>The only bones will be sewn onto the side pieces duplicated with iron-on batiste. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2010_files/image002.jpg" alt=""></p>
<p><strong>I start by marking the side seam line along which the first bone will be placed (1).</strong> The length of this bone depends on the length of the duplicated area of the piece. In my case, it will stop just below the stomach line.</p>
<p><strong>If you want to make this bone longer,</strong> you should fuse an additional strap of batiste along the side seam line below the duplicated area. A longer side bone will prevent the dress from crawling up when the wearer stands up without making her feel uncomfortable when she is sitting down. It will also reduce the probability of wrinkles forming in the stomach and hips area.</p>
<p><strong>I mark 5.5cm inwards from the intersection of the side seam line and the bust line (point C) </strong>to determine the border of the cup (point B).</p>
<p><strong>I mark the cup in advance</strong> by drawing an arc (B-D) from point A with a radius equal to the distance from point A to point B.</p>
<p><strong>I step 3cm from the side seam line toward the side of the back and mark another line (2). </strong>This bone will stop 12cm below the waistline.</p>
<p><strong>I mark two more vertical bones: bone 3 divides the C-B section in half and bone 4 does the same with the B-D section.</strong> Both bones will stop 11cm below the waistline.</p>
<p><strong>In addition, I will place bones along the front and back princess seams. </strong></p>
<p><strong>I mark symmetric bones on the second side piece.</strong> The top border of the bones will be indicated by stay-stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2010_files/image004.jpg" alt=""></p>
<p><strong>Now that the bones have all been marked, I can start cutting the lacing panels.</strong></p>
<p><strong>Let me draw a pattern first.</strong> It will be based on the pattern of the central back piece. Before cutting the lacing panels, you need to make up your mind about their size and configuration which depend on how open the back of your dress will be and what degree of tightening you want to achieve. I will certainly sew bones onto the lacing panels because there are none sewn onto the back itself.</p>
<p><strong>I take the paper pattern of the central back piece.</strong> The seam allowance of the central seam (2cm) is included in the pattern. I add a seam allowance of 1.2cm for attaching the lacing panel. In this case, the lacing panel will be 1.2cm narrower than the central back piece when finished. Therefore, there will be a gap of about 2-2.5cm between the lacing panels. It is enough to lace the dress comfortably. Besides, the zipper on the central back piece won't have any contact with the edge of the lacing panel.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2010_files/image006.jpg" alt=""></p>
<p><strong>The opposite edge of the lacing panel is supposed to repeat the configuration of the princess seam line of the back piece.</strong></p>
<p><strong>The lacing panel will start 1.5cm below the top edge of the back and have the same configuration at the top.</strong> There is a seam allowance along the top edge on the pattern of the back piece, which means the marked top edge of the lacing panel also includes a seam allowance.</p>
<p><strong>The lacing panel will stop just above the hip line. </strong>I step 3cm above the hip line and draw the bottom edge of the lacing panel with the same configuration as the bottom edge of the back piece. Although there is no seam allowance along the hip line on the pattern of the back piece, I add 1.8cm to the bottom edge of the lacing panel to make sure I can give it a neat finish. In this case, the finished lacing panel will be 3cm shorter than the back piece.</p>
<p><strong>I cut out the prepared pattern of a lacing panel. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2010_files/image008.jpg" alt=""></strong></p>
<p><strong>Next, I prepare a lay of four layers of the same stretch satin</strong> used for the lining. Two of these layers are duplicated with iron-on batiste.</p>
