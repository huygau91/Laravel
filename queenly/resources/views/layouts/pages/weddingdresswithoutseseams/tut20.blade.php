<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image034.jpg" alt=""></p>
<p><strong>I even out the bottom edge of the lining the same way.</strong> I will later trim the lining and make it about 2cm shorter than the face.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image040.jpg" alt=""></p>
<p><strong>Before trimming the lining, I need to align the edges of the slit on the face and the lining of the dress. </strong>There is a slit at the front, and since there is no seam in the middle of the central front piece, it needs to be made in the fold line. I have already duplicated the area of the slit with iron-on batiste. I will finish it with the lining after joining the layers of the dress along the neckline. As for now, I may as well mark the slit while the dress is still not assembled.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image042.jpg" alt=""></p>
<p><strong>There is always a risk that the lining will pull on the slit at the top, in which case there will be no evening it out. But I will show you a good way to avoid this trouble.</strong></p>
<p><strong>I align the first edge of the slit on the face with the corresponding edge of the slit on the lining. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image044.jpg" alt=""></p>
<p><strong>Then I mark the end of the slit on the face and transfer the same point onto the lining with a pin.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image048.jpg" alt=""></p>
<p>It is enough to pull the lining 0.5cm higher up to make it a little looser <strong>and prevent it from pulling on the end of the slit</strong>.</p>
<p><strong>In other words, I mark the end of the slit 0.5cm <em>below</em> the pin on the lining. </strong>When I align the two marked points, there will be a little more lining fabric and the slit will settle perfectly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image052.jpg" alt=""></p>
<p>I always use this trick when making slits or vents − regardless of whether in a seam or in a fold line. I always pull the lining just a little higher up. It is a good idea to put it on a hanger for this purpose.</p>
<p>I put the dress on a hanger, pull the lining up, and secure it with a pin.</p>
<p><strong>Now I need to press the princess seams. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image054.jpg" alt=""></p>
