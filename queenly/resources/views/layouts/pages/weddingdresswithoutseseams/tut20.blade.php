<p><strong>And I sew down the other princess seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image016.jpg" alt=""></p>
