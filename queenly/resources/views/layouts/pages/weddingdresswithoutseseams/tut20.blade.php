<p><strong>The back princess seams are now sewn.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2019_files/image030.jpg" alt=""></strong></p>
<p><strong>I even out the bottom edge of the face of the dress.</strong> I fold the dress precisely in half and adjust it with a pair of scissors. You can draw a beautiful hemline in advance. I will make final adjustments when finishing the hem but for now I simply need to even it out.</p>
