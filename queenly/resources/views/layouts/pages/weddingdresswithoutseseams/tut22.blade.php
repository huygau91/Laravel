<h1>Tutorial 21. Joining the Face and the Lining along the Neckline.</h1>
<p><strong>It is important to ensure a good fit of the cups before joining the face and the lining along the neckline.</strong> I need to mark areas that require easing in. I slide my finger along the cup to feel for excess fabric and mark those areas. The degree of ease is determined individually right on the cup. I form a dart to press the lining fabric closer to the cup: as I can tell, the ease amounts to about 0.5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image002.jpg" alt=""></p>
<p><strong>I transfer the marked ease-in area onto the other cup. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image006.jpg" alt=""></p>
<p><strong>I have determined the areas of the lining that needs to be eased in around the cups.</strong> I will gather the fabric on a strap of organza.</p>
<p><strong>I turn out the face of the dress, put the lining inside, and start pinning them together along the neckline.</strong> This is very meticulous work because you need to align the edges perfectly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image008.jpg" alt=""></p>
<p><strong>When pinning the lining to the face, I keep in mind that I will need to roll the seam when I turn the dress out. For this purpose, I make the lining go 1.0-1.5mm beyond the neckline. </strong>I also watch out for the cup not to get caught in the joining seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image014.jpg" alt=""></p>
<p><strong>Pinning the lining to the face in the middle of the neckline is very challenging.</strong> I place the first pin in the very corner of the neckline and then add pins along the row of stay-stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image019.jpg" alt=""></p>
<p><strong>Next, I sew a joining seam along the row of stay-stitches.</strong> I set a smaller stitch length and start from the end point of the strap marked by a pin.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2021_files/image021.jpg" alt=""></p>
