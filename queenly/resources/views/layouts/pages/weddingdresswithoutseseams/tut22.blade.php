<p><strong>I reinforce the corner of the neckline with a drop of Gutermann glue applied from the side of the lining.</strong> Use the glue to smear the apex of the corner and the seam allowances of the joining seams but not the garment itself. <strong>The goal is</strong> to protect the thread from tearing under tension and at the same time reserve an opportunity to notch out excess fabric down to the very seam so the corner looks sharp when I turn the dress right side out.</p>
