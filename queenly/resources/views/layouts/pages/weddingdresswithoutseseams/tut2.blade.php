<h1>Tutorial 1. Getting Started.</h1>
<p></p>
<p><strong>I am about to start working on the pattern of our to-be dress. </strong>First of all, I would like to warn you that this particular dress model definitely requires sewing a mock-up. There are no side seams but there are very complicated princess seams, and so working with a mock-up is a must, no matter how good the initial pattern is! It is impossible to adjust the customized pattern included in the book after individual measurements using the traditional method (i.e. by applying shifting the side seams and princess seams of the front and the back half of the dress). Such adjustments would distort the shape of the front princess seams and the dress would lose its charm. </p>
<p><strong>I have sewn a mock-up following the initial pattern and put it on the dress-form. </strong>All hollows are stuffed with padding polyester and bits of fabric. A dress-form is an absolute must-have for sewing dresses of this level of complexity. It is just as necessary as your sewing machine and press iron!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>After putting the mock-up on the dress-form, I measure it </strong>as if it were a real person. I tie cords and mark the level of the bust line, the under-bust line, and the waistline. I mark the stomach line at the level where I measure the stomach circumference on the client: 12 cm below the waistline. </p>
<p><strong>I will share the values of all measurements taken off my mock-up in the process.</strong> Yours may be different because every seamstress takes measurements in her own manner. </p>
<p><strong>After taking all measurements off the mock-up,</strong> I record them as initial measurements in my traditional table for recording pattern adjustment values. Then I apply all adjustments to the standard pattern, sew a new mock-up after the adjusted pattern, and finalize its shape.</p>

