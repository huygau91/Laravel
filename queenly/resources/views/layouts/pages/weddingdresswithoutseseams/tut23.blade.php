<h1>Tutorial 22. Finishing the Zipper with the Lining.</h1>
<p><strong>Let us press the neckline of the dress.</strong> I put it on a cup pressing mould, spread it out, and secure it with a pin. After pressing it, I lift the edge and secure the cup with a thin layer of Gutermann glue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image014.jpg" alt=""></p>
<p><strong>I press the sharp corner of the neckline on a hard surface with a dry iron (no steam).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image018.jpg" alt=""></p>
<p><strong>And I press the top parts of the shoulder straps the same way.</strong> Later, I will fold the seam allowances of the straps and of the lining to the inside and notch out excess fabric to reduce the thickness. The back of the straps will look clean and neat.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image024.jpg" alt=""></p>
<p><strong>And now I would like to talk about finishing the top edge of the dress.</strong> There are two possible options.</p>
<p><strong>&nbsp;The first option</strong> is to turn out the dress and stitch along the top edge.</p>
<p><strong>The second option</strong> is to simply fold the top edge back to the inside and finish it with a lace ribbon or a bias tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image026.jpg" alt=""></p>
<p>Most people prefer the second option because it is easy, fast, and simple and still allows for changes to be made after a fitting if necessary.</p>
<p><strong>Both options are applicable in the case of our dress.</strong> In the end, the choice depends on the decor. If you plan to add appliqu� elements along the neckline, then you should definitely choose the first option or else the edge will be too thick. If you don't plan to decorate the neckline of your dress, it makes sense to just fold the edge back, which is exactly what I will do. It will produce the opposite effect in this particular case: slightly increased thickness will make the edge smoother.</p>
<p><strong>NB:</strong> regardless of how you decide to finish the top edge of your dress, you must reinforce the non-duplicated areas at the back. I will use organza for this purpose.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2022_files/image028.jpg" alt=""></p>
