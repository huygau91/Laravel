<h1>Tutorial 7. Review of Materials Used.</h1>
<p><strong>The picture below shows all materials I am going to use in this dress:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image002.jpg" alt=""></p>
<p><strong>White Armani stretch fabric.</strong> This fabric is sheen from one side and matte from the other side. It only stretches crosswise.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image004.jpg" alt=""></p>
<p><strong>Beige Armani stretch fabric (the same kind).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image006.jpg" alt=""></p>
<p><strong>White multi-chiffon.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image008.jpg" alt=""></strong></p>
<p><strong>White stretch satin for the lining.</strong> It also has a sheen side and a matte side and stretches crosswise. This fabric won't make the dress heavier since it is very light.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image010.jpg" alt=""></p>
<p><strong>Ornate lace with a mesh base.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image012.jpg" alt=""></p>
<p><strong>Decorative trimmings</strong>. I will use them to overlay joining seams between differently coloured pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image014.jpg" alt=""></p>
<p><strong>One of the key questions is which side of the fabric to use as the face: sheen or matte?</strong></p>
<p>Since I want to complement this dress with a chiffon accessory, I believe <strong>it is better to use the matte side of the white Armani fabric as the face.</strong></p>
<p><strong>The matte side of the beige Armani fabric also looks more glamorous.</strong> But it does look a lot darker than the sheen side, too. I will make it lighter by <strong>adding a white multi-chiffon overlay.</strong> It will create a more delicate shade of beige.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image016.jpg" alt=""></p>
<p><strong>When I cover the chiffon with lace, it will stand out really well against this background.</strong> If I decided to sew the dress entirely from white fabric, the lace would blend in and the effect would be totally different. The beautiful motifs of this white lace will be more emphasized on the beige background.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image018.jpg" alt=""></p>
<p><strong>I will overlay the joining seams with decorative trimmings to create a smoother transition from white to beige.</strong> It will look lovely!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image020.jpg" alt=""></p>
<p><strong>And last but not least:</strong> the colour of the wearer’s arms in the chiffon sleeves will match the background of the lace insets perfectly, creating an illusion of bare skin.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image022.jpg" alt=""></p>
<p><strong>Before I start cutting the dress, </strong>I would like to draw your attention to certain challenges that may arise when you are duplicating the fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image024.jpg" alt=""></p>
<p>Anyone with experience in sewing corset garments knows that <strong>you are supposed to duplicate the lining </strong>before cutting the face fabric. This is done to prevent the bones from showing through and conceal the inner corset construction of the dress.</p>
<p><strong>But there is a common problem with dresses (particularly white ones) with minimum seams and no decor in the waist and hips area: you can see the border of the duplication fabric through the face fabric.</strong> Sometimes it is hardly noticeable and doesn't really catch your eye, but other times it is so obvious that you simply cannot leave it that way!</p>
<p><strong>I suggest you should carry out a test to check how noticeable this border will be on your dress.</strong> I fold the white Armani fabric in half and put the iron-on batiste (also folded in half) between the two layers.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image026.jpg" alt=""></p>
<p><strong>The border is not so noticeable and yet it is somewhat risky!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%207_files/image028.jpg" alt=""></p>
<p><strong>There are three ways you can solve this problem.</strong></p>
<p><strong>The first and the easiest way is to use the second layer of fabric.</strong> You cut and duplicate two layers of lining for the plain white parts of the dress (in our case, they are the central front piece and the central back pieces) and then cut separate pieces from the face fabric and sew them on. This additional layer of face fabric guarantees that the border between the duplicated and the non-duplicated areas won't show through.</p>
<p><strong>The second option is to duplicate the entire piece, i.e. not down to the hip line but from the top down to the very bottom.</strong> It may be enough to duplicate it down to the knee level because the difference in shades does not tend to catch your eye that low.</p>
<p><strong>The third option involves a particular sewing technique.</strong> You should know what sewing technique you are going to employ and where you are going to place bones when you start cutting your garment. All that concerns the placement of bones depends on your client's figure and the chosen style. Working on this dress, I would like to demonstrate one more technique thereby answering another frequently asked question: how to combine a corset with soft foam cups? I normally use this technique on clients of average body proportions, i.e. when the waist needs a little highlighting but the bust looks good with plain foam cups and there is no need in a corset base. In this case, I sew bones at the waistline level at the sides of the dress to prevent the non-duplicated fabric from shrinking during the pressing.</p>
<p>In other words, there will be no bones on the central front piece but only on the side piece. There is no need in sewing bones onto the back of the dress either since it will feature concealed lacing panels sewn into the princess seams. For this reason, I won't duplicate the central front piece and the central back pieces but cut their face and lining from the Armani fabric. The only piece that will be duplicated is the side piece which features bones.</p>

