<h1>Tutorial 26. Decorating the Dress with Beaded Trimmings.</h1>
<p><strong>I have carried out a fitting</strong> which showed that the dress fits my client like a glove! The only adjustment I made was to make it just some 2mm narrower in the hips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image002.jpg" alt=""></p>
<p><strong>Now I need to decorate the dress. </strong></p>
<p><strong>I will use beaded braid trimmings with sequins for this purpose.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image003.jpg" alt=""></p>
<p><strong>I start from the neckline. </strong>Frankly speaking, I could leave it as is because it looks very neat!</p>
<p><strong>I trim one end of the trimming at an angle</strong> and leave the other end straight.</p>
<p>The straight end will be placed underneath the inclined end.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image005.jpg" alt=""></p>
<p><strong>I use Gutermann fabric glue to secure the trimming on the dress.</strong> Even if you prefer the old good sew-it-on method, I still recommend you should apply several drops of glue on the trimming to simply prevent it from shifting around. Trust me, it will spare you a lot of time.</p>
<p><strong>I pin the trimming in place: it is supposed to go beyond the edge of the dress by a couple of millimeters. I form a sharp corner in the middle of the neckline. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image007.jpg" alt=""></p>
<p><strong>I start by gluing it on in the corner. I lift the end of the trimming, smear it with a rather thick layer of glue, put it back in place, and secure it with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image009.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image011.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image013.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image015.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image017.jpg" alt=""></p>
<p><strong>After securing the trimming from one side of the neckline, I do the same from the opposite side. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image019.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2026_files/image021.jpg" alt=""></p>
