<p><strong>Particular attention should be paid to the area of the princess seam where I had to pull on the fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image014.jpg" alt=""></p>
