<p><strong>I press open the princess seams of the lining just the same way.</strong> I will trim the seam allowances later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image020.jpg" alt=""></p>
<p><strong>I finish by pressing the garment from the face side.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image022.jpg" alt=""></p>
