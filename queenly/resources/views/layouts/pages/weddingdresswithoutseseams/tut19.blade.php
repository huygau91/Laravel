<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2018_files/image030.jpg" alt=""></p>
<p><strong>Now, before joining the face with the lining, I need to perform some additional manipulations.</strong></p>
<p><strong>Firstly, I need to reinforce the sharp corner of the neckline on the lining.</strong> This is done by fusing a small piece of iron-on batiste over the corner of the neckline to prevent it from stretching out. It will also help me turn out the dress.</p>
