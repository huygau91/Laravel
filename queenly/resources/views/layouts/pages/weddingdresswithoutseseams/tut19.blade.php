<p><strong>Secondly, I need to trim the seam allowances of the princess seams.</strong> I trim them down to the waistline on the lace-overlay parts of the dress and down the very bottom on the plain white parts (otherwise they will show through). Try to trim the seam allowances on the white fabric as neatly as possible, especially if you are not planning to decorate the seams later. It is not so important in my case because my seams will be decorated.</p>
