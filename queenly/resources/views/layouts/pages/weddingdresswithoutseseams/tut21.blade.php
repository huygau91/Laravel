<h1>Tutorial 20. Cutting and Sewing Chiffon Halter-Neck Straps.</h1>
<p><strong>The face of the dress is nearly finished and I could join it with the lining right now. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image002.jpg" alt=""></p>
<p><strong>But I haven't finished the shoulder straps yet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image004.jpg" alt=""></p>
<p><strong>There are many possible ways you can add shoulder straps to a dress depending on the overall style and personal preferences.</strong></p>
<p>You can take the pattern included in the book, cut the remaining part of the strap that did not fit into the main lay, and sew it to the face and the lining of the dress - <strong>it is the first option. The second possible option</strong> is to sew lace shoulder straps with scalloped edges. The strap should have a width of 2.5cm and include a 1.2cm seam allowance from either side. You can choose beautiful lace with motifs matching those on the lace overlay of your dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image006.jpg" alt=""></p>
<p><strong>Finally, the third option</strong> is to sew chiffon halter-neck straps that will be tied in a bow at the back <strong>(which is exactly what I am going to do)</strong>. As you remember, the dress will be complemented with removable chiffon sleeves − and this makes chiffon straps the most harmonious choice. Now I just need to prepare them and sew them in place and then join the face of the dress with the lining.</p>
<p><strong>Considering the required width of the halter-neck strap (2.5cm), I need two straps of chiffon with a width of 18cm which includes seam allowances for finishing the edges.</strong> You can tear off the two straps or cut them with a soldering iron. It does not matter what method you choose as long as their edges are protected from fraying.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image008.jpg" alt=""></p>
<p><strong>Let me show you my way of finishing the edges of a chiffon strap.</strong> Although many of you must be familiar with this method, it involves certain fine points.</p>
<p><strong>Please note</strong> that I will trim the selvage after finishing the longer sides of the strap because the ends of finishing seams never look neat enough and I prefer to trim them away as well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image010.jpg" alt=""></p>
<p><strong>I start by make a row of strengthening stitches very close to the edge</strong> (literally 3-4mm from it) to achieve a very thin and very even edge when I fold it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image018.jpg" alt=""></p>
<p><strong>And then I easily fold the edge over the seam and secure it with another row of stitches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2020_files/image022.jpg" alt=""></p>
