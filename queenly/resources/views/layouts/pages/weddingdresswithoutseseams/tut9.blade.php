<h1>Tutorial 8. Cutting the Dress.</h1>
<p><strong>I start from cutting the side piece from iron-on batiste.</strong> I fold a piece of iron-on batiste two times and trace the side piece. I will tear the bottom edge of the batiste instead of cutting it to achieve a smoother transition from the duplicated to the non-duplicated part of the dress. My batiste is not wide enough to duplicate the piece down to the hip line but it's okay because the bones won't reach that far anyway.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image002.jpg" alt=""></p>
<p><strong>I am cutting on a soft surface: the lay and the pattern are pinned to it.</strong> I transfer the notches, the side seam line, and the control notches for aligning and joining the pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>So I have cut four side pieces from batiste,</strong> pinned them together, and placed pins in all control points (notches) from one side of the piece. Next I lift two layers of batiste and transfer the control points on the second and the third layer by marking above and below the tip of the needle.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image006.jpg" alt=""></p>
<p><strong>Now, without removing the pins, I slide all layers of batiste up them, turn the lay over together with the pins, and mark the control points on the last (fourth) side piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image010.jpg" alt=""></p>
<p><strong>Then I remove the pins, put them along the other side of the lay, and transfer the control points onto the four pieces just the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image014.jpg" alt=""></p>
<p><strong>Now the control points for notches are marked on all four side pieces.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image016.jpg" alt=""></strong></p>
<p>It is physically impossible to make a full-scale detailed demonstration of the cutting process but <strong>it is enough to show it on a paper mock-up.</strong></p>
<p><strong>I need to cut the central front piece and the central back pieces from the white Armani fabric (both the face and the lining).</strong> I will cut them on the cross grain. I take a piece of fabric that is 150cm long and 140cm wide and divide it in two halves: 75cm by 140cm each. Then I fold each piece in half, put one over the other, and align their edges and fold lines. I got a lay made of four layers of fabric with two fold lines, the width of 37cm, and the length of 140cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image018.jpg" alt=""></p>
<p><strong>I put the central front piece on top of the lay. </strong>But a center front with a long integrated collar or shoulder strap requires a great amount of fabric if you cut it as one piece. Therefore, you normally cut the collar or the strap separately (or cut part of it separately from lace/chiffon) and sew it to the main part of the dress.</p>
<p><strong>Knowing this, I lay down the central front piece keeping in mind that I will cut part of the strap later</strong>: namely, replace it with a chiffon scarf. On the pattern, I mark the part of the shoulder strap which needs to be cut from chiffon (seam allowance included).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>I trace the pattern of the central front piece and mark all notches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image022.jpg" alt=""></p>
<p><strong>Next, I take the skirt piece.</strong> The skirt needs to be cut down to the hip line. I lay the piece parallel to the fold line, align its hip line with the hip line of the central front piece, and trace it with a dashed line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image024.jpg" alt=""></p>
<p><strong>I have decided to slightly narrow the skirt 45cm below the waistline:</strong> it will look more elegant this way. I have figured that by shifting each princess seam 1cm inwards I will reduce the circumference of the skirt by 8cm in the area just above the knee (as compared to the hip circumference).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image026.jpg" alt=""></p>
<p><strong>I re-draw the shape of the skirt piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image028.jpg" alt=""></p>
<p><strong>The central front piece is ready to be cut.</strong> My skirt is now 105cm long in the middle of the front (from waist to hem) and 108cm long at the sides.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image030.jpg" alt=""></p>
<p><strong>The back piece.</strong> I flip the pattern horizontally for the sake of sparing the fabric, lay it parallel to the selvage, trace it, mark the hip line, and transfer all notches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image032.jpg" alt=""></p>
<p>Next I prolong the middle of the back of the skirt down to the very end of the lay. The back of the skirt is now about 122cm long in the middle. <strong>I have also narrowed the skirt 45cm below the waistline by removing 1cm in each princess seam,</strong> finalized the shape with the help of the pattern, and gradually brought the length to 118cm to provide for a smooth transition to the train.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image034.jpg" alt=""></p>
<p><strong>Two central front pieces with fold lines and four back pieces are ready to be cut.</strong> If you put all layers together, you will get one central front piece and one central back piece with a seam in the middle.</p>
<p><strong>Let us cut the side piece. </strong>I need to cut two face pieces from the beige Armani fabric and two lining pieces from the stretch satin. I take 1.2m of each fabric and fold it in half.</p>
<p><strong>I draw a horizontal line on the lay and put down the pattern of the side piece aligning the side seam with it. </strong>Transferring the notches is a must.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image038.jpg" alt=""></p>
<p><strong>Next, I draw the skirt the same way I did on the central pieces.</strong> I shift either princess seam of the skirt 1cm inwards at the level 45cm below the waistline and re-draw it using the pattern as a template.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image042.jpg" alt=""></p>
<p><strong>Now I can cut the side piece.</strong> The skirt is 108cm long at the front, 118cm long at the back, and 110cm long along the assumed side seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image044.jpg" alt=""></p>
<p><strong>I cut all pieces from fabric similarly to the way I cut the mock-up earlier.</strong></p>
<p>First of all, <strong>I cut a piece of iron-on batiste to duplicate the side piece</strong> and transfer the control points for aligning and joining the pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image046.jpg" alt=""></p>
<p><strong>The central back piece is also cut from white Armani fabric and I transfer all control points onto it.</strong> Please remember that the control points should be transferred onto all layers: the face and the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image050.jpg" alt=""></p>
<p><strong>The central front piece is cut with a fold line.</strong> As always, I transfer all control points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image052.jpg" alt=""></p>
<p><strong>The side piece is cut from the beige Armani fabric and the lining fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image054.jpg" alt=""></p>
<p><strong>I don�t transfer the control points onto it because they are already marked on the side piece cut from the iron-on batiste.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%208_files/image056.jpg" alt=""></p>
<p><strong>Now I need to duplicate the fabric and then cut the chiffon and the lace.</strong> I will demonstrate a special technique for cutting chiffon that prevents it from getting distorted underneath the lace.</p>

