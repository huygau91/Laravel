<h1>Tutorial 23. Joining the Face and the Lining (Final Steps).</h1>
<p><strong>I pin the lining to the face along the top edge aligning the notches on the side seam and on the princess seams and leaving seam allowances for the halter-neck straps.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image008.jpg" alt=""></p>
<p><strong>I turn the garment around and examine it to see if the face and the lining are aligned properly and the fabric is pulled on well.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image010.jpg" alt=""></p>
<p><strong>Then I pin together the other half of the dress and the other strap just the same way.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image014.jpg" alt=""></p>
<p><strong>I have prepared a bias tape for finishing the top edge but you could also use a thin lace ribbon.</strong> As I mentioned earlier, you could also add some lace around the perimeter of the cups.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image018.jpg" alt=""></p>
<p><strong>I make a row of joining stitches along the top edge of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image026.jpg" alt=""></p>
<p><strong>I sew a strap of organza along the top edge of the back piece 0.5mm above the row of stay-stitches. I don�t pull on the strap as I sew.</strong> It is only supposed to reinforce the back without easing in the fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2023_files/image030.jpg" alt=""></p>
