<h1>Tutorial 14. Sewing Bones onto the Lining.</h1>
<p><strong>First of all, I make a row of stay-stitches along the entire top edge of the lining.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image012.jpg" alt=""></strong></p>
<p><strong>And then I sew a narrow Rigilene bone along the slanted edge.</strong> I gather the bone and give it an arched shape by pulling on the fishing lines. The ends of all bones are taped with masking tape.</p>
<p><strong>The bones should be placed 1mm below the row of stay-stitches and only in the duplicated areas.</strong> I ease in the armscyes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image022.jpg" alt=""></p>
<p><strong>I sew a symmetric gathered Rigilene bone along the other slanted edge.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image024.jpg" alt=""></strong></p>
<p><strong>Since these slanted parts of the armscyes must be perfectly symmetric, the two bones must also have the same length. </strong>If the degree of ease on the right is not the same as on the left, it means the fabric stretched unevenly and I need to even it out.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%2014_files/image030.jpg" alt=""></p>
<p><strong>I lay vertical bones in correspondence with the markings. </strong>The two central bones which are supposed to protect the dress from wrinkles in the waistline will be wide, and the bones placed along the princess seams (responsible for holding the shape of the dress) - narrow.</p>
<p><strong>I sew a wide Rigilene bone along the side seam line. </strong>Remember to cover its ends with pieces of masking tape. The previously marked line runs along the middle of the bone. I trim the vertical bone at an angle as I reach the bone sewn on at the top and push its end underneath it. When I trim the vertical bone, I make sure its end does not push into the row of stitches in the upper bone to avoid deformation.</p>
