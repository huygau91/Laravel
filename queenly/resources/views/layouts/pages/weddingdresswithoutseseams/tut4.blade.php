<p><strong>The central back piece is ready.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image076.jpg" alt=""></p>
