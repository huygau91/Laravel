<h1>Tutorial 3. Customizing the Size of the Pattern.</h1>
<p><strong>I print out another set of standard pieces to work on their shape.</strong> For the sake of your convenience, there is a thumbnail on every sheet. I take a glue stick and assemble the central front piece and the side front piece and the same parts of the back half of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image002.jpg" alt=""></p>
<p><strong>I start customizing the pattern by altering the side front piece.</strong> I draw the major circumference lines and apply adjustments based on the calculated values from the Adjusted Side Seam Front column: I remove 1cm at the bust line level, leave it as is at the under-bust level, and remove 1cm in the waist, in the stomach, and in the hips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image004.jpg" alt=""></p>
<p><strong>I re-draw the side seam line using the side front piece of the pattern after which I made the first mock-up:</strong> first I draw the section from the bust line to the under-bust line, then from the under-bust line to the waistline, and finally from the waistline to the hip line. You can also transfer the pieces onto cardboard or plastic if you want.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image010.jpg" alt=""></p>
<p><strong>According to the calculations, the side seam needs to be made 1cm shorter.</strong> First I bring the newly drawn side seam line to its initial length and then shorten it by 1cm (point C).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image014.jpg" alt=""></p>
<p><strong>I move to the clip-toward-armscye measurement.</strong> I need to shorten the clip by 0.5cm or mark the adjusted clip toward the armscye (10cm). I mark 1.2cm (seam allowance) and find the apex of the breast (point A). I measure 10 cm (clip) and another 1.2cm (seam allowance) from the apex of the breast and mark point B.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image016.jpg" alt=""></strong></p>
<p><strong>I re-draw the top edge</strong> by connecting points C and B using the standard piece as a template.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image018.jpg" alt=""></p>
<p><strong>Next I need to alter the princess seam of the front.</strong> There are no changes in the area above the waistline. According to the calculations, I need to shift it 0.6cm inwards in the waist, 0.5cm inwards in the stomach, and 0.1cm inwards in the hips. You can safely leave that 1mm in the hips because a slightly looser fit will only ensure better freedom of movement. As for me, I will account for it though. There will be no side seam on the final version of the pattern and so the newly made pattern needs to be very precise.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image020.jpg" alt=""></p>
<p><strong>I re-draw the front princess seam.</strong> I connect the points on the hip line, the stomach line, and the waistline, and carefully bring it to the under-bust line. You can use the standard pattern as a template.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image022.jpg" alt=""></p>
<p><strong>I have applied all alterations to the side front piece.</strong> Make sure to transfer the notches onto the new side seam and princess seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image024.jpg" alt=""></p>
<p><strong>Now I need to customize the central front piece.</strong></p>
<p>For a start, <strong>I shift the entire central fold line 1cm inwards in keeping with the bust middle measurement.</strong> And then <strong>I apply adjustments to the princess seam: </strong>shift it 0.6cm inwards in the waist, 0.5cm inwards in the stomach, and 0.1cm inwards in the hips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image026.jpg" alt=""></p>
<p><strong>I re-draw the front princess seam </strong>and transfer all notches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image028.jpg" alt=""></p>
<p><strong>The length of the princess seam is supposed to be the same on the side front piece as on the central front piece.</strong> I check how much shorter the princess seam has become on the side front piece (I got 2cm) and shorten it by the same value on the central front piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image032.jpg" alt=""></p>
<p>I mark a seam allowance of 1.2cm and <strong>mark the adjusted clip toward the center of the neckline (6cm, point D). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image034.jpg" alt=""></p>
<p><strong>I draw a smooth transition to the shoulder strap from the princess seam and the neckline. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image038.jpg" alt=""></p>
<p><strong>And I work on the shape of the neckline to make it more graceful.</strong> Remember: the smaller the breasts are, the more the neckline should reveal. I try to make it agree with my client's clip measurement. Sometimes though, it may make sense to slightly prolong the clip just to achieve a beautiful neckline.</p>
<p>I can always adjust the shape or the depth of the neckline after sewing a mock-up based on the customized pattern and examining it on the dress-form.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image040.jpg" alt=""></p>
<p><strong>I adjust the width of the shoulder strap:</strong> it should be even along its full length. My shoulder strap has ended up 4.5cm wide.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image042.jpg" alt=""></p>
<p><strong>The central front piece adjusted after my client's measurements looks like this:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image044.jpg" alt=""></strong></p>
<p><strong>I cut the adjusted side front piece along the side seam line.</strong> I will use it to restore the same length and configuration of the side seam line on the back half of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image048.jpg" alt=""></p>
<p><strong>I take the side back piece. </strong>I need to change the configuration and the length of the side seam and apply alterations to the princess seam.</p>
<p>I shift <strong>the side seam</strong> in correspondence with the calculations: 2.5cm inwards at the bust line level, in the stomach, and in the ships, and 1.5cm inwards at the under-bust level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image050.jpg" alt=""></p>
<p><strong>I put the side front piece over the side back piece, align the notches, and re-draw the side seam line of the back piece using the front piece as a template. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image054.jpg" alt=""></p>
<p>I shift <strong>the princess seam of the side back piece</strong> 0.3cm outwards at the under-bust level, 0.6cm inwards in the waist, 0.5cm inwards in the stomach, and 0.1cm inwards in the hips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image056.jpg" alt=""></p>
<p><strong>I re-draw the princess seam </strong>using the side back piece from the standard set of patterns.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image060.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image062.jpg" alt=""></p>
<p><strong>I draw the top edge.</strong> You can lower the back depending on the style preferred.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image064.jpg" alt=""></p>
<p><strong>I have applied all alterations to the side back piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image066.jpg" alt=""></p>
<p><strong>I need to customize the central back piece after my client's measurements </strong>by applying changes to the princess seams at the back.</p>
<p><strong>To make things easier, I cut the side back piece along the princess seam line to use it as a template.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image068.jpg" alt=""></p>
<p><strong>I take the central back piece and shift the princess seam line</strong> 0.6cm inwards in the waist, 0.5cm in the stomach, and 0.1cm in the hips. I put the side back piece over it, align the princess seam line with the control points on the central piece, and re-draw it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%203_files/image070.jpg" alt=""></p>
