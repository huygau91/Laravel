<h1>Foreword.</h1>
<p><strong>I am opening a series of tutorials dedicated to a new type of wedding dresses: dresses with sleeves like the one you can see in the picture below.</strong> I always choose dress models for my courses based not only on the beautiful look but also on how clever the involved sewing techniques are. I try to focus upon those technical fine points that were not mentioned in the previous wedding fashion tutorials. <strong>What new things have I prepared this time? </strong></p>
<p><strong>Let's look at the first dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Foreword_files/image002.jpg" alt=""></p>
<p><strong>This dress has a peculiar construction</strong>: it features halter-neck straps integrated into the bodice and it has no side seams. There are only two princess seams at the front and another two at the back. You can complement a dress like this with all kinds of removable accessories: for example, a long chiffon cloak with fancy epaulettes embroidered with seed beads, sequins, rhinestones or pearls, or decorated with lace appliqué. You could also form puffs. A lightweight cloak like this can replace traditional bridal veiling. It can also solve the problem of a long removable train. Another thing you can do is sew loose lightweight sleeves attached to a matching necklace. Their cuffs can be wide or narrow. Such sleeves can be made from thick fabric and fastened with pearl buttons or made from the same lace used for the dress itself.</p>
<p><strong>What is the point of sewing a dress without side seams? &nbsp;</strong>First of all, it is very elegant! And then what if you feel like adding lace insets at the sides of the dress without having to cut them in halves? Or maybe you want to use a kerchief with a pattern or a hand-printed batik image at the sides of your dress? You can probably think of many other creative ideas for insets at the sides of the dress which cannot be brought to life with the side seams standing in your way.</p>
<p><strong>Another new feature of the dress is a slit in the middle of the front. </strong>The slit and the hem of the dress will be neatly finished with the lining fabric. I will share a special trick to protect the dress from getting deformed by the lining after you finish the hem.</p>
<p>Using ready foam cups is <strong>another important peculiarity of the described sewing technique</strong>. I will share my fast and easy way of installing soft foam cups into a corset- or bodice-based dress.</p>
<p><strong>Low neck and nude fabric insets will make the dress particularly elegant!</strong></p>
<p><strong>As I always do in my courses, I will calculate all necessary alterations and customize the standard pattern with regard to a particular client's body proportions. </strong>I don’t want you to depend on the basic pattern included in the book. Instead, you will develop the skills required to create your own patterns. The customized pattern I used for sewing the described dress is also included in the book.</p>
<p>The shape of the <strong>princess seams at the front and the back of the dress</strong> makes the central pieces somewhat guitar-shaped. This forms a very graceful silhouette!</p>
<p><strong>The dress is fastened by a blind zipper. </strong>There is also a concealed lace-up area inside, which ensures good fitting and helps the dress hold well on the body.</p>
<p><strong>The dress is made from mixed materials:</strong> thick fabric of two colours, chiffon, and dense lace. I will explain how to cut all these fabrics and sew them into a single side piece and then join this three-layer side piece with the single-layer central piece. You will learn a great professional trick for joining pieces with differently shaped edges.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>My goal</strong> is to teach you to create your own things and not just copy the described dresses by repeating after me! By carefully studying my tutorials, you will get hold of those seven notes to create your own music, your one-of-a-kind masterpiece!</p>

