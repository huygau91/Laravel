<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image048.jpg" alt=""></strong></p>
<p><strong>The paper pattern of the side piece is ready. All I need to do is add seam allowances.</strong></p>
