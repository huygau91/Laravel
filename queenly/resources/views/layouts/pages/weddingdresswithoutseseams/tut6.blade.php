<h1>Tutorial 5. Building a Paper Pattern without Side Seams.</h1>
<p><strong>I need to build a paper pattern of the central front piece.</strong></p>
<p><strong>I start by pressing the mock-up piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image002.jpg" alt=""></p>
<p><strong>Then I put a sheet of squared paper under it, pin the piece onto the ironing board together with the paper, and trace the contour.</strong> I trace the shoulder strap with a dashed line because I will restore its incline and width based on the initial pattern. I transfer the control notches from the reference line on the paper and mark the major circumference lines with arrows.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image006.jpg" alt=""></p>
<p><strong>I remove the mock-up piece, put the initial piece over the drawn pattern, put the mock-up piece back atop, pin it all together, and trace the area of the shoulder strap.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>I remove the initial pattern piece without shifting the mock-up piece and make final adjustments to the new paper pattern of the central front piece.</strong> I transfer all necessary notches and circumference lines and cross out the lines that are no longer relevant.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image012.jpg" alt=""></p>
<p><strong>The new central front piece looks like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image014.jpg" alt=""></p>
<p><strong>I add a seam allowance of 1.2cm- that's it, now the central front piece is ready. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>And I do the same with the central back piece.</strong></p>
<p><strong>I press the piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>I transfer the initial central back piece on paper...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image022.jpg" alt=""></p>
<p><strong>...put the mock-up piece over it, and pin them together. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image024.jpg" alt=""></p>
<p><strong>Then I trace the mock-up piece, transfer all notches and circumference lines...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image028.jpg" alt=""></p>
<p><strong>...add a seam allowance of 1.2cm, and adjust the shape of the back at the top. The pattern is ready.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image032.jpg" alt=""></p>
<p><strong>And here begins the most challenging stage: building a paper pattern of the side piece.</strong></p>
<p><strong>The key task </strong>is to make the side seam settle in a perfectly straight line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image034.jpg" alt=""></p>
<p><strong>I draw a straight line on a paper sheet, pin the side seam right along it, and start pressing the top half of the piece.</strong> I carefully smooth out all air bubbles in the waist and hips by pushing the air out through the princess seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image036.jpg" alt=""></p>
<p><strong>I secure the pressed edge of the piece with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image038.jpg" alt=""></p>
<p><strong>And I start working on the bottom half of the piece.</strong> You are simply supposed to press it on a flat surface. Do not unpin the side seam at this point.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image040.jpg" alt=""></p>
<p><strong>Then I free the side seam by moving the pins onto the ready half of the piece and continue pressing.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image044.jpg" alt=""></p>
<p><strong>I secure the other edge of the piece with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%207/transcript/Tutorial%205_files/image046.jpg" alt=""></p>
<p><strong>As you can see, I have managed to turn an oddly-shaped 3D piece into a flat piece using just a press iron and my own hands. </strong>Although the edges of the piece differ in shape from the edges of the central front piece and the central back piece, the notches will help me get them aligned.</p>
<p><strong>I transfer the side piece on paper </strong>together with all control notches and major circumference lines. When sewing, I will stretch the piece in the waist area to avoid air bubbles and creases.</p>
