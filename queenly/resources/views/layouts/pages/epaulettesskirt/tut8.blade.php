<h1>Tutorial 8. Adding Decorative Chains.</h1>
<p><strong>I have already made the second epaulette.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image002.jpg" alt=""></strong></p>
<p><strong>I have also sewn chains onto the first one:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image004.jpg" alt=""></p>
<p>Now I will sew symmetric chains of three lengths onto the second epaulette. There is a closed link at one end of the chain and it's easy to put a thread through it. I apply a drop of glue to the opposite end.</p>
<p><strong>I have marked the spots for sewing the chains. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image006.jpg" alt=""></p>
<p><strong>I start from the shortest one.</strong> I don't try to sew it on too tight because I want the chains to hang down freely and not stick up. You can see the step-by-step of the process in the pictures below.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image016.jpg" alt=""></p>
<p><strong>The epaulettes look like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image018.jpg" alt=""></p>
<p>Thanks to the fresh combination of colors, these epaulettes will match a dark blue, a black, or a brown outfit equally well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/7/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>There are different ways to fasten the epaulettes to the garment. </strong>First of all, you can carefully sew them on with neat hand stitches and rip those up when you need to wash or dry clean the garment. And then you can also sew two safety pins onto the back of each epaulette and fasten them to the garment whenever you want.</p>

