<h1>Tutorial 5. Sewing On Rhinestones. Applying Final Touches to the Garment.</h1>
<p>The glue has set and now I can <strong>sew the rhinestones on.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%205_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%205_files/image004.jpg" alt=""></p>
<p>Needless to say, it is a lot easier when they are already secured in place. It's enough to make a couple of stitches from each end. I am using 2-ply thread of a matching blue shade. I hold the fabric with my left hand and sew with the right hand. If I hadn't glued the rhinestones on, I would have had to simultaneously hold the fabric and the rhinestone in place, which is particularly difficult with stretch fabric.</p>
<p>Now you see that these small tricks really come in handy when sewing on rhinestones.</p>
