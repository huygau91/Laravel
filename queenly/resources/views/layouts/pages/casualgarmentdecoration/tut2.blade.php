<h1>Tutorial 2. Various Decoration Options.</h1>
<p><strong>I have sewn on the first row of rhinestones.</strong>&nbsp; Now I can clearly see the border of the area I need to embellish. I will use a different kind of rhinestones now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image002.jpg" alt=""></p>
<p><strong>I will carefully study several possible decoration options and choose the one that looks the best. </strong></p>
<p><strong>Version 1.</strong> I could fill the entire area with plain horizontal rows of rhinestones.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>Version 2.</strong> These are vertical rows of rhinestones placed broad side to broad side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>Version 3.</strong> These are vertical rows of rhinestones placed tip to tip.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>Version 4.</strong> Here rhinestones are arranged in horizontal rows, tip to tip.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image010.jpg" alt=""></p>
<p><strong>You could also add some teardrop rhinestones. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image012.jpg" alt=""></strong></p>
<p><strong>I prefer version 1 with horizontal rows</strong>. <strong>I need to decide whether I want to place them tip to tip (as diamonds) or broad side to broad side (as squares).</strong> It's easier to make turns in a row made of diamonds. On the other hand, a row of squares looks thicker.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>Wait, I have another idea! Indeed, I can make vertical rows of squares! I believe it's exactly what I need!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%202_files/image016.jpg" alt=""></p>

