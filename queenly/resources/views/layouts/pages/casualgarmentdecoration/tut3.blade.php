						<h1>Tutorial 3. Applying the Rhinestones. Part 1.</h1>
						<p>
							<strong>I have already sewn the large rhinestones on and now I
								fill all empty areas with matching small rhinestones.</strong>
							Please note that I arrange them in the same direction as the
							square rhinestones. It will look lovely when finished.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%203_files/image002.jpg"
								alt="">
						</p>
						<p>
							What other highlights can I add? <strong>I put some AB and clear
								crystal rhinestones along the neckline and compare them.</strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%203_files/image004.jpg"
								alt="">
						</p>
						<p>AB rhinestones don't look convincing to me in this particular
							case. But silvery clear rhinestones create a nice contrast with
							the somewhat golden square rhinestones.</p>
						<p>
							<strong>I add crystal rhinestones along the neckline</strong> to
							spice up the composition, throw in a new color, and intensify the
							shine. In addition, <strong>I mark another line for gluing on
								more rhinestones</strong> below the border of the decoration
							area.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%203_files/image006.jpg"
								alt="">
						</p>
						<p>Please note that you should treat tank tops with sew-on and
							glue-on embellishments with due care. Wash it by hand in warm
							water and don't use any strong cleaning products. Otherwise, the
							coating of your rhinestones might start flaking or they will
							simply fall off.</p>
						<p>
							<strong>Below is a close-up of the neckline of the tank top. You
								can see the rhinestones very well.</strong> Look how well they
							match and how rich this kind of contrast looks!
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/decoration/5/Tutorial%203_files/image008.jpg"
								alt="">
						</p>
						<p>
							I continue embellishing the garment. <strong>I place the same
								crystal rhinestones </strong>along the newly marked line. I want
							to use the contrast between gold and silver again. Please note
							how I keep small intervals between the rhinestones to protect the
							garment from deformation. These intervals will allow the tank top
							to stretch properly. Any decor should not only look good but
							preserve the resilience of the garment. And that is impossible
							when you don't leave any gaps between the rhinestones.
						</p>
