<h2>Tutorial 12. Making Hanging Loops for the Lacing.</h2>
<p></p>
<p>Before I can start working with the hanging loops, I need to prepare a cord they will actually be made of. </p>
<p>I take a strap of fabric that is 3cm wide and 1.5m long and stitch it up in half lengthwise leaving a rather long thread at the end of the sewing. Then I make another stitch at a 1mm distance from the first one and going towards the allowance. This is done in order to prevent the thread from breaking if the cord happens to get too strained. Next I take a sack needle with a thick eye, insert the thread that I left at the end of the sewing into it, tie a knot and insert the needle into the hollow cord (blunt end first). And then I turn the strap out by pulling it down on the thread. </p>
<p><strong>There it is - our cord is ready! I�ll cut it in two halves. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to mark the location of loops on the central back piece. </strong></p>
<p>I do it on the surface of my pressboard. I pin the corset down in such a way that the cut edge is straight, mark a 1.2cm seam allowance and draw a line. </p>
<p>Then I determine what size the loops should be. I check it with the help of a ruler and get about 3-3.5cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I cannot give any precise recommendations here because the loop size depends on the width and thickness of the cord and your personal preferences. In my opinion loops of this size will look pretty. The distance between the loops will be about 1cm. </p>
<p>Then I measure the length of the back or, in other words, the distance that I need to divide and use for marking the location of loops and the distance between them. I distribute loops carefully along the length of the back and I end up having 5 of them. </p>
<p></p>
<p><strong>You can see the layout in the picture below:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image005.jpg" class="img-responsive center-block" alt="Description: Description: E:\Corset Academy\FINISHED\English\Party Dress\mp4\kn party ENG corrected_files\image151.jpg"></strong></p>
<p>I have decided that the height of each loop should be 1.5cm. I draw an upper borderline for the loops and mark their peak or central points on it. In other words, I mark the centre of each loop. Then I draw a line for loop �tails" and mark their bottom points on it. </p>
<p></p>
<p><strong>Next I need to add pins vertically at all points of reference:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image007.jpg" class="img-responsive center-block"></p>
<p><strong>I arrange the loop cord between these pins:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image009.jpg" class="img-responsive center-block"></p>
<p>I distribute the cord in a �serpent"-like way making uncut hanging loops. This is very important - the loops should be really durable because the cord that will go through them later has to support the whole dress. </p>
<p><strong>Next I remove the vertical pins and secure the loops in place according to the markings:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image011.jpg" class="img-responsive center-block"></strong></p>
<p>This technique allows you to make very neat and even loops. And these loops will remain hanging after I turn back the seam allowance and sew them on.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>I make a strengthening stitch along the line of the seam allowance:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2012_files/image015.jpg" class="img-responsive center-block"></p>
