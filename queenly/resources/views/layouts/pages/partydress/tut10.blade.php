<h2>Tutorial 10. Joining the Face and the Lining. Adjusting the Face of the Corset. </h2>
<p></p>
<p><strong>I join the face with the lining by machine-stitching. </strong></p>
<p>I have put on a one-sided presser foot with the ski-shaped part on the right and now I move it along using the bone as a guiding line. The needle gets right into the stay-stitch because I have sewn on this bone 1mm below the stay-stitch. This allows me to pass precisely along the edge of the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I use a smaller stitch width when approaching the corner of the neckline. The corner should be quilted very carefully! I make one perpendicular stitch at the bottom of the corner. In other words I don�t just turn the garment around on the needle but make a small stitch so as to make it easier to notch the seam allowance later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>It is necessary to make a bar tack at the final sewing point. </p>
<p>Then I remove all pins and check the quality of the seam! </p>
<p><strong>Next I take a tube of Gutermann fabric glue </strong>and spread a little bit of it along the seam line at the very corner slightly passing onto the seam allowance. This enables me to leave as little seam allowance fabric as possible when notching the corner and additionally secures the thread of the seam. </p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p>I spread some glue over the same corner from the side of the lining. </p>
<p>Then I wait for the glue to set and trim all seam allowances leaving about 0.5cm along the whole length of the top cut edge. </p>
<p><strong>I notch the corner of the neckline as accurately as possible:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p>You should notch it down to the very seam line but be careful not to cut the thread! I cut as close to the seam as I can. The look of the neckline depends on how neatly this corner is notched. </p>
<p><strong>Now I turn out the garment and press it thoroughly:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p>I press the whole top of the corset. Please note that you need some heavy steaming and rather high pressure here. You can�t press the top of the corset with quilted cups using just a regular household iron. You really need a semi-industrial or even an industrial press-iron with a powerful steam-generator.</p>
<p><strong>And now I put the corset onto the dress-form:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
