<h2>Tutorial 15. Assembling the Layers of the Detachable Skirt.</h2>
<p></p>
<p><strong>Now I start sewing together the upper detachable skirt made of our beautiful chiffon. </strong></p>
<p><strong>I have put on a specialized presser foot and now I gather the fabric along the whole circle:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>A short back-story:</strong> the idea of making a transformer dress didn�t strike me straight away. The client chose the model that you have seen in the picture. Only after buying the fabrics I realized that the upper skirt was going to be made of a rather tricky material (the structure of the embroidered flowers differs greatly from the structure of the chiffon: they are very thick while the chiffon is thin). However I would have to gather it very accurately. And then the main technical problem popped out: how to combine a beautiful draped belt, a chiffon skirt with ruffles and a lacing? I could imagine it clearly how bad it would look from the back: the pretty belt would have to be interrupted by the lacing and then I�d have to join the lacing and the skirt too. </p>
<p>So the first thought I got was �this chiffon should stay uncut!" You can�t just insert a zipper into these thick flowers and finish the edges. Of course you could make a split seam at the back, but I really didn�t want to cut this gorgeous fabric. </p>
<p>So this is how the idea of a detachable skirt was born. Then it gradually transformed into a dress and, finally, it was clear that this dress was going to be multifunctional. </p>
<p>A detachable skirt can be replaced by any other skirt! </p>
<p>A detachable skirt can be worn as a separate garment! </p>
<p>And since the full tulle skirt is attached to the corset by only one seam, it will be easy to rip it off! </p>
<p></p>
<p>After I have finished gathering the embroidered chiffon, I make the same kind of gathering on the regular chiffon (multi chiffon that will serve as the lining of the detachable skirt). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2015_files/image004.jpg" class="img-responsive center-block"></p>
<p>The multi chiffon and the embroidered chiffon should have the same volume of gathering. Their length should also be identical because you will need to join them together along the waistline. </p>
<p>Now I hem these two skirts. I believe the prettiest way is to fold the hemline twice, each time by 0.5cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2015_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2015_files/image008.jpg" class="img-responsive center-block"></p>
<p>Then I measure the lengths of the resulting skirts made of multi chiffon and embroidered chiffon, pin them together along the upper cut edge and machine-stitch them together. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2015_files/image010.jpg" class="img-responsive center-block"></p>
<p>Next I make the second parallel joining stitch at a presser foot distance below the first one to flatten the seam allowances, just like I did when sewing together the tulle skirt. </p>
<p>And finally, I overlock the upper cut edge of the skirt. </p>

