<h2>Tutorial 13. Calculating and Cutting the Skirts. Sewing a Zippered Lining.</h2>
<p><strong>I start preparing patterns for the skirts.</strong></p>
<p><strong>First I need to calculate the pattern for the lining of the main black crepe-satin skirt.</strong></p>
<p>I am going to cut a full sweep skirt.</p>
<p>The stomach measurement at the spot where the corset ends is 69cm.</p>
<p><strong>I use a regular formula for calculating the perimeter of a circle:</strong></p>
<p><strong>2πR =69cm, </strong>where</p>
<p>- <strong>π</strong> = 3.14 is a constant;</p>
<p>- <strong>R</strong> is the radius I’m going to mark during the cutting process;</p>
<p>- <strong>69cm</strong> is the measurement taken off my client.</p>
<p>The result of the calculation is:</p>
<p><strong>R = 11cm</strong>.</p>
<p>The length of the lining skirt will be 40cm.</p>
<p>My piece of black crepe-satin is 150cm long.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image001.jpg" alt="Description: Description: E:\Corset Academy\FINISHED\English\Party Dress\mp4\kn party ENG corrected_files\image163.jpg"></p>
<p>If I fold it in half, the bottom line of the rectangle is 75cm.</p>
<p>The diagonal line of the rectangle is a sum of the radius and the length of the skirt multiplied by two. In this case it is 102cm.</p>
<p>Now I need to find out how much 150cm wide fabric I need in order to cut out a skirt that is 40cm long and has a radius of 11cm.</p>
<p>I use the famous <strong>right triangle formula (Pythagorean equation) </strong>for this calculation. The formula states that the square of the hypotenuse (the side opposite the right angle) equals the sum of the squares of the other two sides of the triangle.</p>
<p>In my case it looks like this:</p>
<p>72² + x² = 102²</p>
<p>Therefore,</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image009.png" alt="" height="20">x² = 102² - 75²&nbsp;= (102 х 102) - (75 х 75) = 4779 square centimeters</p>
<p>And the sought quantity is then:</p>
<p><strong>Х</strong>=&nbsp;√4779&nbsp;=&nbsp;<strong>69.1cm</strong> ≈ 70cm</p>
<p>This means I need a piece of fabric that is 70cm long and 150cm wide.</p>
<p><strong>This is what the cutting pattern of my full sweep skirt looks like:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image016.jpg" alt="Description: Description: E:\Corset Academy\FINISHED\English\Party Dress\mp4\kn party ENG corrected_files\image171.jpg"></p>
<p><strong>I can start cutting it out. </strong></p>
<p>I fold the piece of fabric into a flat cone. I mark 11cm (the radius) from the peak of the cone and then another 40cm (the skirt length) down from this point. And I cut all extra fabric off.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image018.jpg" alt=""></p>
<p>I fold the resulting piece of fabric diagonally, secure the corner with a pin, place the two quadrants cut out as a whole piece over it and cut the semi-circle of the front after this piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><strong>Then I cut off the top following the 11cm radius line:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><strong>The result is a semi-circle for the front piece and two quadrants for the back piece with a central seam:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image024.jpg" alt=""></strong></p>
<p><strong>And now I start cutting the beautiful embroidered fabric.</strong></p>
<p>My piece is 1.5m long and the width of the embroidered part is about 90cm.</p>
<p>I fold the piece in half lengthwise, even it out to make sure the embroidery is folded in half too, and cut the fold line open. Now I got two 1.5m long straps - one for the back and one for the front - and the width of their embroidered parts is 45cm.</p>
<p>There is also some black chiffon for the hem and I even it out across the width as well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image026.jpg" alt=""></p>
<p>I want to put a layer of chiffon over the embroidered fabric to prevent the skirt from being too see-through. For this purpose I have prepared two straps of chiffon (45cm wide and 1.5m long) that will be used for the front and the back correspondingly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image028.jpg" alt=""></p>
<p><strong>And now I need a bunch of tulle straps. </strong></p>
<p>Each strap is 45cm long and 3m wide. I have cut out 12 of them.</p>
<p>The amount and the kind of straps you need depend on how full you want your skirt to be and how soft or hard the tulle is. Needless to say, it is rather subjective. I normally choose the amount of layers by looking at the finished garment. I just look at the result and decide how many layers are needed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image030.jpg" alt=""></p>
<p>Then I cut out an additional ruffle detail out of a 20cm wide piece of hard mesh fabric. It will make the skirt fuller and at the same time spare me some soft tulle because I won’t need as many layers of it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image032.jpg" alt=""></p>
<p><strong>Now I sew together the lining. </strong></p>
<p><strong>I start with the central back seam:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image034.jpg" alt=""></p>
<p>I join the lining using a 2cm seam allowance leaving these 2cm for sewing in the zipper.</p>
<p><strong>I press the seam open and sew in a regular, non-blind zipper:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image036.jpg" alt=""></strong></p>
<p><strong>Here is what the result looks like:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image038.jpg" alt=""></strong></p>
<p><strong>Then I join the side seams of the lining using a 1.2cm seam allowance: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image040.jpg" alt=""></p>
<p><strong>And I join together the side seams of the chiffon:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image042.jpg" alt=""></strong></p>
<p>In other words I join 2 chiffon straps - each 45cm wide and 150cm long - into one whole circle.</p>
<p><strong>Then I do basically the same thing with the embroidered chiffon:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image044.jpg" alt=""></p>
<p>I join two 150cm long straps into one whole circular piece, i.e. make two side seams.</p>
<p><strong>Then I finish the small areas going from the zipper inwards the lining skirt with a bias tape:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image046.jpg" alt=""></strong></p>
<p>I know it for certain that the corset is going to slightly part from the skirt, so I want the visible area of its upper edge to have a pretty pencil-edge look. That’s why I finish it off with 8cm of bias tape from each side.</p>
<p><strong>That’s what it looks like:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2013_files/image048.jpg" alt=""></p>
<p>I have overlocked all joining seams.</p>
<p>And I have also whipped the top and bottom edges of the lining skirt.</p>

