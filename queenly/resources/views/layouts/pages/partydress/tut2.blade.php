<h2>Tutorial 2. Printing Out Patterns. Pattern Calculation.</h2>
<p></p>
<p><strong>Now I can start working on our new dress:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image001.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>The photo above demonstrates the dress we are going to make during this course. I will sew, provide necessary comments and illustrate the whole process with pictures and all you have to do is repeat after me.</p>
<p><strong>The dress consists of a corset, a full tulle skirt and a detachable chiffon skirt with stumpwork and a wide draped belt.</strong></p>
<p>Choosing the right model was a serious and responsible task for me because bright fancy dresses often get stowed away into the wardrobe after being worn just once and it is hard to find another suitable festive occasion for putting these fabulous garments on. </p>
<p><strong>And so I came up with the idea of making this dress transformable! </strong></p>
<p><strong>Firstly,</strong> you could wear its pretty chiffon skirt with top tanks of matching colors: black, blue, red, white, etc... </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Secondly,</strong> you could swap the fancy multi-colored skirt for any other skirt of a different style and colour or with a different belt. And the dress would look absolutely new. You could also use some other type of corset decoration or an elegant accessory: belt buckle, brooch, fabric flower, etc… </p>
<p><strong>And finally,</strong> if you rip off the tulle skirt from the corset, the corset will turn into an individual garment that can be worn with jeans or trousers. </p>
<p><strong>In short, this dress can live a long and happy life!</strong></p>
<h3> </h3>
<h3>Printing Out Patterns.</h3>
<p>So, you have extracted the enclosed archive into a separate folder. It contains the file with the book and a folder with patterns that you can open with a double click.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image004.jpg" class="img-responsive center-block"></p>
<p>You need to open the files one-at-a-time in order to print out the patterns. All files open with a double click on the icon.</p>
<p>I double click the icon ‘1-2а’ and open the first pattern:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image006.jpg" class="img-responsive center-block"></p>
<p>To print out this pattern, you should click (just once) on the ‘Print’ icon (marked with the red arrow).</p>
<p><strong>When I click this icon, the printing menu comes up:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>You should select the printer that is currently connected to your computer<strong> in the Printer drop-down list marked with the red arrow</strong>:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image010.jpg" class="img-responsive center-block"></p>
<p>Now you need to select ‘Document’<strong> in the Comments &amp; Forms drop-down list marked with the purple arrow</strong>.</p>
<p><strong>And next you need to specify all necessary printing properties. </strong></p>
<p>The menu with your printer’s properties opens<strong> when you click the Properties button marked with the blue arrow (see above)</strong>:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%202_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>You should tick Borderless Printing, select the A4 printer paper size and tick the Portrait orientation (green arrows).</strong></p>
<p><strong>It is necessary to press ‘OK’ afterwards!</strong></p>
<p>This will return you to the printing menu. You should check all settings carefully one more time and then press the Print button. </p>
<p><strong>Now your printer should start printing:</strong></p>
<p>
