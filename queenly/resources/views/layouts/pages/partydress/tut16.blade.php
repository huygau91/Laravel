<h2>Tutorial 16. Sewing the Belt. Finishing Touches for the Skirt.</h2>
<p></p>
<p><strong>First of all I need to determine how wide the belt should be when finished:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p>I place a measuring tape on the dress and decide that the best possible width of the belt would be 14cm. The length of the belt should be 1m when fully stretched so that the clients� shoulders can go through. The actual non-stretched length of the belt will be 65cm to make sure it fits snugly against the waist. </p>
<p>I tear off a 31cm wide strap of multi chiffon inclusive of seam allowances, fold it in half and press it flat. </p>
<p>Then I stitch through the belt along the edge, overlock it and sew it together into a circle. </p>
<p><strong>I take a satin elastic ribbon, fold it in half and secure it:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p>I place the ribbon against the belt at a 1.2cm distance (the distance of a seam allowance) from the edge. I match the stitch on the ribbon with the side seam of the belt. The length of the ribbon should be slightly smaller than the waistline circumference to ensure the belt will sit tightly around the garment. I stretch the ribbon out to match the length of the belt and secure it with pins. When I let go of the ribbon, it will gather the chiffon evenly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I sew on the ribbon in a circle along the edge of the belt, first along one side and then along the other: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></p>
<p>You should know that the chiffon is not just gathered underneath the ribbon but slightly eased in, too. This causes the belt to shrink a little and changes its planned length, and that is why you should always add 5cm extra bearing this effect in mind. </p>
<p>I sew on the second parallel ribbon. This time the task is somewhat harder. You need to pull on not the chiffon alone but together with the previous ribbon. I pull both ribbons and secure the second ribbon with pins to ensure the pleats are even. I make sure the ribbons stay parallel to each other as I sew. </p>
<p>Now after I have sewn on two parallel elastic ribbons from the face side, I need to sew on the same kind of ribbon from the backside below the previous two. I sew it on with two parallel stitches. This ribbon provides for beautiful gathering of the face. The sewing technique is exactly the same as before. I use the seam lines that attach the previous ribbons as my guiding lines. </p>
<p></p>
<p><strong>Here is what it looks like from the face side:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p>Then I sew on another two parallel ribbons from the face side, exactly the same way I did it before. </p>
<p>I also sew on a ribbon 2.5cm below the other edge of the belt that is going to be its top line. This ribbon creates a gathering effect at the top. </p>
<p></p>
<p><strong>And here is what I got. Two rows of parallel elastic ribbons and one elastic ribbon at the top. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I pin together the skirts and the assembled belt: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2016_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I stretch the belt to check if the skirt works well with it in this state. </p>
<p>I stitch it on from the side of the belt to see where the ribbon is located and follow the seam allowance carefully. </p>
