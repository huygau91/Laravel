<h2>Tutorial 14. Assembling the Tulle Skirt and Joining it with the Lining and the Corset.</h2>
<p></p>
<p><strong>Now I start gathering tulle layers:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p>I fold a 3m long strap in half which makes 1.5m. I highly recommend assembling these layers not one by one but three or four in a row. It is much faster and easier that way. I sew together and gather four layers of tulle simultaneously. I can control the level of gathering better by slowing down the motion of the presser foot (by placing my finger behind the presser foot) which makes the pleats much thinner. If I don�t place my finger there, the pleats are thicker. After I�ve sewn and gathered one strap, I check its actual length and decide how many straps in total I need to gather and how puff they will be. </p>
<p><strong>Next I gather a 25cm wide strap of hard mesh fabric in the same manner.</strong></p>
<p>After gathering, the length of this hard mesh strap became 69cm which is two times larger than the stomach circumference in the area where the corset ends. So I have folded it in half and secured. Now the hard mesh actually wraps around the stomach area four times. As a result I have a mini-skirt that helps fluff up the straps of tulle. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p>Measuring the length of the gathered tulle has shown that I need to join 2 straps with an 8cm overlap. I always try to overlap tulle skirts instead of just joining them along their vertical cut edges. </p>
<p><strong>Then I sew four layers of hard mesh onto the first four layers of tulle:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>And then I place another four layers of gathered tulle onto these eight layers and sew them on. I place the next four layers of tulle with an overlap stepping about 8cm from the edge of the previous four tulle straps, and continue sewing them onto the joined eight layers. </p>
<p>Now I have 12 joined layers: 8 made of soft tulle and 4 made of hard mesh fabric. </p>
<p>Then I place the last overlay of four straps of gathered tulle over it, secure it with pins and sew everything together.</p>
<p>I make the second parallel joining stitch at a presser foot distance below the first one. This second stitch presses down the seam allowances, flattens them and reduces their volume. </p>
<p>Then I overlock the upper edge of the tulle skirts. Now that the seam allowances are flat and even it�s going to be much easier to join the tulle layers with the lining. </p>
<p></p>
<p><strong>I pin together the tulle and the lining skirts matching their midpoints and re-distribute the lining along the whole length: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I haven�t stitched up the back seam of the tulle skirts as there is absolutely no need for it. These skirts are so voluminous that they overlap perfectly and the joint spots won�t catch the eye. </p>
<p><strong>I sew together the lining and the tulle skirts: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And now I pin all these skirts to the corset:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I sew them on carefully using the machine: </strong></p>
