<h2>Tutorial 7. Sewing Bones onto the Central Front Piece. Pressing.</h2>
<p></p>
<p>The cup is now fully formed. </p>
<p><strong>And I start sewing on vertical bones. </strong></p>
<p><strong>Question: should vertical bones be wide or narrow? </strong></p>
<p>This is up to you as there are no strict rules here. It is wise to use wide Rigilene bones when you want a firm and strong corset. Wide Rigilene bones are also better when your client is a full-figured woman with a plus size and you need to achieve a good tightening effect.&nbsp; </p>
<p>If your client�s size is rather small and the dress is going to be light, you can use narrow Rigilene bones. You could actually stitch on 2-3 parallel narrow bones in a row if you don�t have any wide bones at hand and they will perform the same function. </p>
<p>I stitch on narrow Rigilene bones following the already marked lines. </p>
<p></p>
<p><strong>I stitch on bone 12:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p>I step 1-2mm from the edge of the cup to start and trim the bone at a 1-2mm distance from the stay-stitch when I finish. I tape over its edges using some masking tape. </p>
<p></p>
<p><strong>Next I stitch on bone 15.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p>I have started by sewing on the vertical bones to secure the shape of the piece. This will make things easier when I continue working on the inside of the cup. </p>
<p>I sew these bones on with two parallel stitches, but as you can remember I have made only one stitch along the perimeter when attaching the bones onto the cup. </p>
<p></p>
<p><strong>I stitch on bone 8:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p>I trim bone 8 leaving 1-2mm from bone 13 underneath the bust. There is no need of overlapping this bone with the rounded bone under the bust. </p>
<p></p>
<p><strong>I sew a Rigilene bone onto the left curve line.</strong> </p>
<p>I straighten the bottom cut edge with my hands as I start sewing on this bone because I know that the fabric has been slightly deformed and distorted due to the tension created by the sewn-on bones. So I restore the original look of the patterns and sew on this bone in such a way so as to recreate and secure the initial shape of the corset. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to sew on bone 9:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I start sewing a Rigilene bone onto the right curve line:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p>It is important to trim all threads straight away or else they will get secured by other seams and will be really hard to remove afterwards.</p>
<p></p>
<p><strong>Now I start sewing on the inner bones of the cup. </strong></p>
<p></p>
<p>I turn the cup inside out very carefully. It still holds its shape well. </p>
<p></p>
<p><strong>I start by attaching a bone that goes along the bust line (bone 1).</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>I am going to hide all bone ends by pushing them underneath the perimeter bones. I push the bone away from me carefully while pulling the fabric on me to preserve the dome-shape of the cup. I trim the bone when I reach the end of the cup and push its end underneath the perimeter bone. Then I make the second stitch line. </p>
<p></p>
<p><strong>The same procedure should be repeated with the same kind of a bone going along the bust line on the second cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>There is a debatable question to it: do I need to gather any bones that are to be sewn in inside the cup?</strong> </p>
<p>In theory, you can do it. You could gather all bones that you stitch onto the inner side of the cup. This would make your cup somewhat more elastic. When you perform the final pressing of the cup with the bones already attached, such gathered bones might get slightly distorted. In this case you can adjust the shape with the help of your press-iron.</p>
<p><strong>Now I stitch on the bone going along the right curve line of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p>And I repeat the same thing again: push the bone away from me while pulling the cup slightly towards me to avoid unnecessary gathering underneath the presser foot. Then I push the ends of the bone underneath the perimeter bones.</p>
<p><strong>Now I go on to the corresponding bone along the left curve of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I need to stitch on vertical bones that go along the side of the cup and along its central part. </strong></p>
<p><strong>I sew on bone 14:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p>I hide the ends of all bones that I am now sewing on underneath the perimeter bones. For this reason I have attached them with only one outer stitch for now. The more bones you stitch on, the stiffer the garment becomes and the harder it is to do machine-stitching. It gets especially difficult when you work on small-sized corsets. </p>
<p><strong>Now I stitch on bone 5:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I attach bones 11 and 6 onto the right cup in exactly the same manner. </strong></p>
<p><strong>And then I start sewing on our half-round bone 2 (I have gathered it in advance) that goes along the under-bust line on the lower half of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p>I trim the bone when I reach the edge of the right cup and push its end underneath the perimeter bone. Then I make the second parallel stitch. And then I repeat the same procedure with the left cup. I try to spread any tucks or creases that got formed inside the cup during the easing-in so as to secure the cup in the well-spread state with the bone. </p>
<p>What concerns upper bones 3 and 4: the size of my cup is so small that I don�t even need to sew them on. </p>
<p>I decide it on the spot each time because it really depends. If I can tell that the cup won�t hold its shape without this bone, I stitch it on. For example if the cup were higher, its size would be bigger and in that case this bone would be a prerequisite. </p>
<p><strong>I am not going to use this bone for my cup with its rather small clip measurement. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now I press it again, this time with the bones attached. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Please notice how snugly the area in the middle of the bust fits now, you can already see this:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p>I have achieved the desired result! </p>
<p>Now I turn the cups inside out and continue pressing. I use my cup pressing mould again. I form the final shape of the cup with the help of some heavy steaming. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p>A regular household iron won�t be enough for pressing the cups. This is the moment when you really need a semi-industrial or an industrial press-iron. </p>
<p><strong>A frequently asked question: is it possible to sew a corset using just a regular household iron?</strong> </p>
<p>In theory you could do it but the effect of dome-shaped cups can be achieved only with the help of a semi-industrial or an industrial press-iron. </p>
<p><strong>Now take a look at what happens next. </strong></p>
<p>I take the initial pattern and compare it with the shape of the piece that was originally cut after this pattern and then sewn and eased in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p>Look at the difference between the initial shape and the result. The pattern has been noticeably deformed during the sewing and pressing work. How can you foresee a deformation like this if you have never sewn a corset before and are just about to prepare your pattern? Making a correct pattern with no previous sewing experience is nearly impossible!</p>
<p>Guided by my own experience I have come to the conclusion that the central piece will match the side pieces much better if I straighten the cut edge that you can see in the picture below:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></p>
<p>So I cut off this extra fabric from both sides and the pieces match quite well afterwards. </p>

