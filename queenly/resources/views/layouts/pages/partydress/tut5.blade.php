<h2>Tutorial 5. Joining the Bust Curves of the Lining.</h2>
<p></p>
<p><strong>Now I need to join the bust curves of the lining.</strong></p>
<p>I match the pieces together and start machine-stitching them with a 1.2cm allowance. </p>
<p>I would like to point it out again that I don�t use any pins along the curves. But if you find it difficult, you can secure it with pins along the bust line: along the line itself and not just at the notches. This way you will secure the pattern safely with basically just one pin. </p>
<p>I try to hold the upper and bottom pieces together with either my right or left hand to ensure perfect matching. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I join it along the second curve line exactly the same way. </strong></p>
<p>I unfold the seam allowances to check if the seam is good enough, and then I trim corners of these seam allowances from both sides of the seams. </p>
<p><strong>I make a strengthening stitch at a 1mm distance from the seam:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>This stitch spreads the seam allowance, secures the seam itself and delivers me from the necessity of pressing it flat at this stage. </p>
<p>I go back along the other side. </p>
<p>You don�t need to make this stitch at a wider distance, 1mm from the joining seam is sufficient. </p>
<p>Then I repeat the same procedure with the second curve line. </p>
<p>Try to unfold and spread the seam allowance very thoroughly. There should be no tucks or creases. </p>
<p></p>
<p><strong>I trim the seam allowances.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p>I leave just 0.5cm for inlay. </p>
<p>Next I take a narrow Rigilene bone and start gathering it. I pull its fishing lines out of it, and then I take one fishing line and keep pulling until the bone acquires a rounded shape. Then I trim the ends of the fishing lines and tape over the bone with some masking tape. </p>
<p><strong>And now I need to make a stay stitch. </strong></p>
<p>I start by stitching at the top along the neckline. This stay-stitch not only secures the rounded cut edges but marks the location of bones as well. I stitch at a 1.2cm distance and when I reach the corner, I let go of the machine and let it stitch automatically. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Next I turn the garment around and <strong>make a stay-stitch from the other side (this time going over the facing side) </strong>to make it perfectly symmetrical. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p>I go all along the top and let go of the machine at the corner. </p>
<p><strong>The corner is now precisely marked in the very middle:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image011.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I make another stay-stitch along the bottom line of the garment </strong>in order to know where the bones should reach their ends at the bottom:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>I sew on that rounded narrow Rigilene bone along the under-bust line (lines 10 and 13 after the markings).</strong> </p>
<p></p>
<p><strong>Please note the way I am going to do it for this very corset model!</strong> </p>
<p>I begin by doing it the usual way - starting from the stay-stitch at the top. I ease in in the rounding area to create some fullness for the cups.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image015.jpg" class="img-responsive center-block"></p>
<p></p>
<p>After reaching the curve line I continue arranging the bone in a circle (on the central cup piece) and <strong>easing in the fabric underneath this bone!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>This is that very trick that ensures perfect fitting in the centre of the bust that people often ask me about!</strong> </p>
<p>However the same trick causes some difficulties with the front because the central cup piece gets slightly smaller. I will have to customize some things a little bit. </p>
<p>But this kind of fullness provides for a nice dome-shaped look of the cups and the small dart in the middle of the bust ensures perfect fitting. Since there is a dent in the centre of the bust, it is going to fit snugly against the body.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image019.jpg" class="img-responsive center-block"></p>
<p>Besides, this trick works wonders for a small bust. </p>
<p>Thanks to the perfect dome-shape of the cups, the bust won�t be shifted to the centre when you insert push-up pads and the rounded bone will keep it in the right place. The bust will be shaped in a beautiful way and appear more attractive. </p>
<p></p>
<p><strong>Now I need to prepare another half-round bone for stitching it onto the other side (bone 13).</strong> </p>
<p>I start working from the middle of the bust and ease in like before. This process is quite tricky! You need to have a good feel of the amount of fullness not to overdo it. On the other hand, there will be hardly any effect at all if you create too little fullness! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%205_files/image021.jpg" class="img-responsive center-block"></p>
<p>And then I ease in along the outer side of the cup as usual in order to help it acquire a dome-like shape. I cut off the rest of the bone leaving a 1mm gap above the stay-stitch and tape its edge over with some masking tape. </p>

