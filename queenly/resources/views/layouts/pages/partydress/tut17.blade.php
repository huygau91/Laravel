<h2>Tutorial 17. Making Push-up Pads.</h2>
<p></p>
<p><strong>In order to make push-up pads I will need: </strong>2 pieces of double-sided sticky tape; 2 square pieces of the main fabric (about 15-16cm in side length); pins, and 2 flocks of padding polyester.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take a square piece of the main fabric and put a flock of padding polyester over it:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold it:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I secure it with pins:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I make some markings with a piece of chalk to make sure the pads are going to have the same size: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I join the layers of fabric together by machine-stitching:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I compare the push-up pads:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And I whip their edges with a zigzag or an overlock seam: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image016.jpg" class="img-responsive center-block"></p>
<p>The result is two pads that you can see above. </p>
<p><strong>I attach the prepared pieces of sticky tape onto the push-up pads using Gutermann fabric glue: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I spread a little Gutermann fabric glue onto the other side of the sticky tape on the pads:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image020.jpg" class="img-responsive center-block"></p>
<p>I place the push-up pads symmetrically onto the spots where they are supposed to be located on the dress. I press them down to make sure they�re fixed properly. </p>
<p><strong>The push-up effect is ready:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I have finished working on the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%2017_files/image026.jpg" class="img-responsive center-block"></strong></p>
