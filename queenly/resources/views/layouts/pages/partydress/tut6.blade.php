<h2>Tutorial 6. Quilting the Cups with Perimeter Bones.</h2>
<p></p>
<p><strong>I continue sewing on our bones. </strong></p>
<p>The centre went somewhat bubbly after I eased in some additional fullness. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I need to remove this bubble with the help of a dart. </strong>I make this dart intuitively - it�s just a matter of experience. It is almost impossible to calculate it because all depends on the amount of fullness underneath the bone. I spread it all out properly and stitch over the extra fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>So now I�ve got a horizontal dart under the bust. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p>You can apply this ease-in trick on the central part of the rounded bone regardless of the type of neckline cut. It can be a straight neckline, a boat neckline or any kind of a low neckline. It makes absolutely no difference. You will simply need to account for the difference between the central lining piece and the central facing piece of the corset as you continue working. But we will talk about this a little later.</p>
<p><strong>Now I need to sew on another slightly gathered narrow Rigilene bone along the top of the cup:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p>I push the end of the bone underneath the half-round bone (bone 13) that�s been stitched on along one side and sew it on at a 1mm distance from the stay-stitch. I ease in slightly along the neckline to prevent it from stretching and ensure good fitting against the bust. The amount of fullness is inversely proportional to the bust size. The smaller the bust is, the more fullness is needed and vice versa: the larger the bust is, the less fullness it requires. </p>
<p><strong>I reach the middle of the neckline and continue stitching through the bone along the whole central piece down to the very bottom (following line 7):</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image010.jpg" class="img-responsive center-block"></p>
<p>These two bones (the one I am sewing on right now and the one I am going to sew on from the other side) will provide for good fitting against the body in the central area between the breasts. </p>
<p>I trim the bone at a 1-2mm distance from the stay-stitch and tape over its edge with some masking tape.</p>
<p></p>
<p><strong>And now I need to sew on the second parallel bone in exactly the same manner:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p>I try my best to preserve the shape of the corner in the middle of the neckline cut. The intersection of stay-stitches serves me as a guideline. I try to recreate the same amount of fullness along the top line as I�ve made on the previous cup. Then I trim the bone and push its end underneath the half-round bone that is stitched on from one side (bone 10).&nbsp; </p>
<p><strong>This very stage requires some pressing action. </strong></p>
<p>We need some serious pressing to help the cup acquire its proper shape! I place the cup onto my special cup pressing mould and start shaping it and making it nice and round with the help of steaming and light pressing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%206_files/image014.jpg" class="img-responsive center-block"></p>
<p>I spread it carefully and stretch it when needed. If I feel that there is some space between the cup and the pressing mould, I try to minimize this space by pressing the cup down onto the mould. The shape of the cup should be practically the same as the shape of the pressing mould! I let the cup cool down from time to time without taking it off the mould. </p>
<p><strong>The press-iron truly works wonders! And our cup has a lovely rounded shape now!</strong></p>
