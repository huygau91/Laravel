<h2>Tutorial 9. Finishing the Lining. Joining the Face Pieces of the Corset. Pressing.</h2>
<p></p>
<p><strong>Now it�s time to join it along the last curve line. </strong></p>
<p></p>
<p>It is advisable to secure this curve line with pins because you will have to slightly re-distribute the fabric. I start stitching using a regular 1.2cm seam allowance.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And then I topstitch this curve line seam by 1mm and trim corners. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I place a narrow Rigilene bone onto the curve line and sew it on:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Now I repeat the same procedure with the second curve line.</strong></p>
<p><strong>And I trim all extra seam allowances that create unnecessary thickness and get in my way:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I sew on a Rigilene bone that goes along the upper cut edge:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p>I place it 1mm below the upper stay-stitch and continue sewing it on until it reaches the cup. I start stitching it on from the middle of the back piece, both from the face and backside of the corset.</p>
<p><strong>Next I need to sew on a Rigilene bone along the bottom cut edge. </strong></p>
<p>Since the bottom cut edge is not straight, I need to gather the bone just a little bit until it acquires a slightly arched shape. This bone is rather long (about 1m) as it goes along the whole bottom of the corset and such long bones are sometimes hard to gather. That�s why I simply gather this bone section-by-section: first I gather one section of the bone and sew it on. And when the gathered section of the bone is over, I trim the bone at the required length (with some extra fabric in reserve), gather it again and continue stitching the rest of it on. </p>
<p><strong>Please notice where I�m sewing on this bone!</strong> </p>
<p>I sew this bone onto the seam allowance because the bottom of this corset is going to have a pencil-edge finish (the same way it is done on see-through corsets), i.e. the edge won�t be folded. Remember that a full tulle skirt is going to be attached to the corset bottom. I start sewing on from the middle of the back.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p>I try to slightly stretch the bottom of the corset, this way its face part will snug tighter to the lining and the corset will fit better against the body. </p>
<p></p>
<p><strong>And now it�s time to assemble the face of the corset. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>It is necessary to trim corners and trim all seam allowances down to about 0.5-0.6cm:</strong> </p>
