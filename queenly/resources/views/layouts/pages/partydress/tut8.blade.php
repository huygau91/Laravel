<h2>Tutorial 8. Covering Cups with Padding Polyester. Joining the Back and the Side of the Lining. Sewing on Bones.</h2>
<p></p>
<p><strong>So I continue working on the corset. I make an inner stitch along all perimeter bones. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And now I need to cover the cups with some padding polyester:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch a flock of polyester onto the cup. </strong>&nbsp;</p>
<p>I stitch it on along the inner stitch that attaches the bone along the top of the cup trying to prevent it from getting into the seam allowances. I also place two ribbon loops underneath the seam for putting the corset on a clothes hanger in the future.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>However at the bottom of the cup I stitch it on along the outer stitch that attaches the bone and make sure it overlaps the bone properly. </p>
<p>Then I remove all pins and trim all extra polyester. </p>
<p>I have used two small flocks instead of taking a whole piece of polyester as it works better for covering cups. </p>
<p><strong>I cover the second cup with padding polyester in exactly the same manner.</strong> I secure it with a couple of pins and stitch it on along the outer seam down the bottom and along the inner seam at the top. </p>
<p><strong>I trim all extra polyester along the perimeter:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now I start joining together the back pieces of the lining using a 1.2cm seam allowance:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I trim corners to get rid of unnecessary thickness in the area of future seam allowances at the top and down the bottom of the corset and topstitch the seam allowance by 1mm:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And then I attach the side piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Again, I topstitch the seam allowance by 1mm:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trim the seam allowance in the narrow area so that it doesn�t get in the way when I stitch the pieces together:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/party_dress/transcript/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make stay-stitches along the top and bottom of each piece and along the centre of the back: </strong></p>
<p><strong><br>
