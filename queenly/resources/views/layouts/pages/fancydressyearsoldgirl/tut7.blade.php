<h1>Tutorial 7. Installing Grommets.&nbsp; Sewing Modesty Flaps.</h1>
<p><strong>Now I need to mark positions of grommet holes along the lacing edges.</strong> If I step 1cm from either end of the lacing edge at the back of the bodice, then the distance in-between will be 18cm. It then makes sense to <strong>punch holes with 3cm intervals</strong> because 18cm is a perfect multiple of 3.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image002.jpg" alt=""></p>
<p><strong>I mount a hole-punching die onto my hand press tool and punch holes as marked. </strong>I punch them next to the seam parallel to the bone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image004.jpg" alt=""></p>
<p>Then, I pin the two lacing edges of the back together to <strong>mark symmetric points along the second edge through the holes made along the first one. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image006.jpg" alt=""></p>
<p><strong>I punch holes along the other lacing edge as marked: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image008.jpg" alt=""></p>
<p><strong>Then, I switch to a grommet setting die and install grommets in the punched holes: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image010.jpg" alt=""></p>
<p><strong>Grommets at the front of the bodice are installed just the same way. </strong></p>
<p><strong>Now there are lacing holes at the front and at the back of the bodice: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image012.jpg" alt=""></strong></p>
<p><strong>Next, I need to prepare two modesty flaps: </strong>one for the front and another for the back of the bodice.</p>
<p><strong>Let us start from a modesty flap for the front. </strong></p>
<p>The front piece has a bit of an incline at the top and down the bottom. <strong>I cut a piece of iron-on fabric of a matching length:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image014.jpg" alt=""></p>
<p><strong>I fold it two times: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image016.jpg" alt=""></p>
<p>And I draw a modesty flap in its actual size with regard to how much the two halves of the front are supposed to part. I trim the fabric leaving enough space for seam allowances and cut it in half lengthwise to get a piece for each half of the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image018.jpg" alt=""></p>
<p><strong>And I cut a modesty flap for the back of the bodice. </strong>I make it a little shorter to prevent it from poking out at the top when the bodice is laced.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image020.jpg" alt=""></p>
<p><strong>I cut a matching piece of iron-on fabric. </strong>I do it all by eye: just look at the bodice and estimate how wide the modesty flap needs to be.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image022.jpg" alt=""></p>
<p><strong>I fuse the batiste pieces onto the main fabric</strong> and cut out the modesty flaps following the contours of the iron-on batiste.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image024.jpg" alt=""></p>
<p><strong>I put the first modesty flap to the bodice and examine it: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image026.jpg" alt=""></strong></p>
<p><strong>Modesty flaps for the back of the bodice are prepared the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%207_files/image030.jpg" alt=""></p>

