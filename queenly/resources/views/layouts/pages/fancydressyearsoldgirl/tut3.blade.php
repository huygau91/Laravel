						<h1>Tutorial 3. Cutting the Bodice.</h1>
						<p>
							<strong>I draw all parts of the bodice on paper and cut them out.</strong>
							Then <strong>I arrange them </strong>on the ironing board and
							measure their total width and length.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image002.jpg"
								alt="">
						</p>
						<p>
							After doing so, <strong>I tear off a corresponding piece of
								iron-on batiste </strong>to duplicate the main fabric. I will
							duplicate the face and the lining simultaneously.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image004.jpg"
								alt="">
						</p>
						<p>The bodice will be made of genuine linen and the skirt - of
							cotton fabric with a little lycra in it.</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image006.jpg"
								alt="">
						</p>
						<p>I prepare the fabric by pressing it lightly.</p>
						<p>
							<strong>Then, I overlay it with the iron-on batiste and start
								fusing it on. </strong>Make sure no threads get under the
							iron-on batiste. If they do, it will show through on the face of
							the dress.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image008.jpg"
								alt="">
						</p>
						<p>
							Area by area, <strong>I duplicate the entire piece</strong> which
							will serve both as the face and the lining of the bodice.
							Duplicating cotton fabric is a real pleasure.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image010.jpg"
								alt="">
						</p>
						<p>
							<strong>I cut the required amount of fabric to make things
								easier:</strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image012.jpg"
								alt="">
						</p>
						<p>
							<strong>I fold the duplicated fabric two times:</strong>
						</p>
						<p>
							<strong><br> <img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image014.jpg"
								alt=""></strong>
						</p>
						<p>
							<strong>And I start laying down the paper patterns.</strong>
						</p>
						<p>I draw the waistline perpendicular to the fold line. It will
							help me position all pieces correctly.</p>
						<p>None of the pieces are cut with a fold line because the dress
							will be laced at the front and at the back.</p>
						<p>I secure the first paper piece on the fabric with just two pins
							and mark 1.2cm seam allowances along the perimeter.</p>
						<p>
							<strong>I outline the piece together with the seam allowances </strong>and
							mark notches.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image016.jpg"
								alt="">
						</p>
						<p>Then I remove the paper piece.</p>
						<p>
							<strong>The same thing needs to be done with all remaining paper
								pieces: </strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image018.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image020.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image022.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image024.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image026.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image028.jpg"
								alt="">
						</p>
						<p>
							<strong>Remember to add 1.2cm seam allowances along the full
								perimeter of each piece.</strong>
						</p>
						<p>
							<strong>I secure the fabric layers with pins for the sake of
								convenience:</strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image030.jpg"
								alt="">
						</p>
						<p>
							<strong>And I cut the pieces out and notch them as marked. </strong>
						</p>
						<p>
							<strong><br> <img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image032.jpg"
								alt=""></strong>
						</p>
						<p>
							<strong><br> <img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image034.jpg"
								alt=""></strong>
						</p>
						<p>
							<strong>Always remember to check if you can align the pieces
								properly. </strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%203_files/image036.jpg"
								alt="">
						</p>
