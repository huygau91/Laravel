<h1>Tutorial 4. Assembling the Lining. Sewing Bones onto the Lining.</h1>
<p><strong>I join all lining pieces with a 1.2cm seam allowance:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image002.jpg" alt="det2-055"></p>
<p>I try to sew <strong>without interrupting the seam </strong>to spare time and effort.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image004.jpg" alt="det2-056"></p>
<p>Again, for the sake of sparing time, <strong>I topstitch all seam allowances</strong> 1mm from either side of the joining seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image006.jpg" alt="det2-057"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image008.jpg" alt="det2-058"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image010.jpg" alt="det2-059"></p>
<p><strong>I deepen the waistline notches on the side seams of the joined pieces.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image012.jpg" alt="det2-060"></p>
<p><strong>You don't have to trim the seam allowances on the lining lengthwise but you do need to trim their ends at an angle. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image014.jpg" alt="det2-061"></strong></p>
<p><strong>I make a row of stay-stitches </strong>1.2cm from the central edge of the back, the bottom edge, the central edge of the front, and the top edge on both halves of the bodice. I follow the guides on the needle plate.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image016.jpg" alt="det2-062"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image018.jpg" alt=""></p>
<p><strong>The corner at the top looks very sharp: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image020.jpg" alt="det2-064"></p>
<p><strong>Now I need to sew narrow Rigilene bones on the bodice. </strong></p>
<p>I step about 0.5cm from the top edge, <strong>place the first bone right next to the row of stay-stitches along the central edge of the front, and secure it with two rows of parallel stitches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image022.jpg" alt=""></p>
<p><strong>I trim the bottom end of the bone 2-3mm from the bottom horizontal row of stay-stitches:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image024.jpg" alt=""></strong></p>
<p><strong>Bones are supposed to be placed along all vertical princess seams.</strong> The joining seam is supposed to run right along the middle of the bone. The ends of all bones are taped over with masking tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image026.jpg" alt="det2-067"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image028.jpg" alt="det2-068"></p>
<p><strong>I approach the central edge of the back.</strong> I lay a bone along the row of stay-stitches slightly pulling on the fabric. Stay-stitches tend to slightly draw the fabric together so I try to pull it on as I sew to bring it back to its initial state.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image030.jpg" alt="det2-069"></p>
<p><strong>I lay another bone next to the first one</strong> with literally just one millimeter gap in-between.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image032.jpg" alt="det2-070"></p>
<p>The outermost bone reinforces the edge and the other bone will have grommet holes punched in it and protect the grommets from popping out.</p>
<p><strong>I do the same thing with the central edges of the front. </strong></p>
<p><strong>The first half of the lining is ready!</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%204_files/image034.jpg" alt="det2-077"></strong></p>
<p><strong>I sew symmetric Rigilene bones on the other half of the lining of the bodice.</strong></p>
