<h1>Tutorial 16. Decorating the Dress with Ribbons.</h1>
<h1>Pictures of the Finished Garment.</h1>
<p><strong>I have approached the most fun part of the process - decoration! </strong></p>
<p><strong>The armlets and the skirt will be decorated with flower motifs: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image002.jpg" alt=""></p>
<p><strong>How do I make these motifs? </strong></p>
<p><strong>I cut ribbons of different colors </strong>matching the colors of the dress but a lot brighter. They are fun, saturated colors which will add perfect highlights to the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image004.jpg" alt=""></p>
<p><strong>I apply a drop of fabric glue in the middle of the ribbon...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image006.jpg" alt=""></p>
<p><strong>...add another ribbon... </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image008.jpg" alt=""></p>
<p><strong>...apply another drop of glue...</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image010.jpg" alt=""></p>
<p><strong>...glue a rose in place...&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image012.jpg" alt=""></p>
<p><strong>...and heat-seal the edges of the ribbons with a lighter or a match.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image014.jpg" alt=""></p>
<p><strong>Then I make a pair of loops from the yellow ribbon. </strong>Try to form a proper loop and not just fold the ribbon in half.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image018.jpg" alt=""></p>
<p><strong>I glue the pair of loops together and glue the crossed ribbons with the rose on top of them. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image019.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image024.jpg" alt=""></p>
<p><strong>Here is the first decoration! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image026.jpg" alt=""></p>
<p><strong>It takes quite many motifs to decorate the garment:</strong> three per each armlet and many more for the skirt. I have already secured the motifs on the armlets. You can sew them on by hand or use fabric glue. As for me, I completely trust my Gutermann fabric glue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image028.jpg" alt=""></p>
<p><strong>I glue the motifs on the skirt.</strong> Before doing this, I measure the length of the ruffle and divide it into equal sections based on the number of prepared decorations. The intervals between the decorations are 16-17cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image030.jpg" alt=""></p>
<p><strong>Here is the decorated skirt: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2016_files/image034.jpg" alt=""></p>
<p><strong>It is now finished! </strong></p>
