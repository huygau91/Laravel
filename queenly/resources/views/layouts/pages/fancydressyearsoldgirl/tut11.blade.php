<h1>Tutorial 11. Cutting the Puffed Armlets.&nbsp; Adding the First Decor.</h1>
<p><strong>I start cutting armlets for Sonja. </strong></p>
<p><strong>First of all, let me see how much fabric I need for this purpose. </strong>According to the measurements, the length of Sonja's forearm is 26cm, the ruffle at the top is 4cm, and the ruffle down the bottom is 8cm out of which 4cm is supposed to be lace.</p>
<p><strong>My lace trimming has a width of 7cm. </strong>I need 4cm plus another 1.5cm for a seam allowance, i.e. a total of 5.5cm. I will cut the lace accordingly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image002.jpg" alt=""></p>
<p><strong>Let us continue. </strong>The length of the forearm (26cm) minus the lace trimming (4cm) makes 22cm of fabric.&nbsp; The ruffle at the top will be made of two layers of fabric though. I need 4cm for the ruffle plus a seam allowance for forming a tunnel for an elastic band, which makes another 2.5cm. To put it simply, I need a total of 28.5cm now. And I also need to account for a 1.2cm seam allowance down the bottom. <strong>In other words, the fabric needs to have a total width of about 30cm.</strong> The second tunnel for inserting an elastic band along the bottom of the armlet will be made from bias tape.</p>
<p><strong>What about the length? </strong>Sonja's upper arm circumference is 18cm. I multiply it by two so I can gather it in pleats - 36cm. I believe it should be enough. Then, I also need to add seam allowances for sewing it into a ring. This makes 39cm. <strong>Therefore, I need a piece of fabric with a length of about 40cm. </strong></p>
<p><strong>To sum it up, I need two rectangular pieces of fabric with a size of 30cm x 40cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image004.jpg" alt=""></p>
<p>The sewing process will be easier if you <strong>fold and press the ruffle area in advance:</strong> 4cm for the ruffle and 2.5cm for the seam allowance, i.e. a total of 6.5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image006.jpg" alt=""></p>
<p>And, of course, I recommend you <strong>mark the level of the tunnel stitch straight away</strong> - 4cm from the fold line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image008.jpg" alt=""></p>
<p>There will be a 1.2cm seam allowance at the bottom.</p>
<p><strong>I do the same for the other armlet. This is all the cutting process. </strong></p>
<p>I still haven't decorated the assembled bodice. I am sure <strong>it will look very beautiful embellished by lace.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image010.jpg" alt=""></p>
<p><strong>How to secure the lace on the bodice? </strong>I recommend doing it with the help of fusible webbing - the fastest and easiest way possible. Besides, you should remember that you are sewing for a little girl, which does not imply any particular demands to the decor. If I start sewing the lace on or securing it with the help of some other complicated technique, the bodice might simply stretch out. I don't think it is worth the risk. <strong>I put a strip of webbing over the part of the bodice which I want to decorate with lace.</strong> Trust me you won't notice the webbing when everything is done.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image018.jpg" alt=""></p>
<p><strong>I try to position the flower motifs in a harmonious way.</strong> The ends of the trimming will be folded over the top and the bottom edge of the bodice and sewn in place by hand or glued on.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image020.jpg" alt=""></p>
<p><strong>The most challenging task is to position the flowers perfectly symmetrically.</strong> I had to choose whether I wanted to keep the scallops perfectly aligned from both sides or keep the flowers symmetric. I thought that keeping the scallops aligned was more important in the end. It is always hard to maintain perfect symmetry.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image022.jpg" alt=""></p>
<p><strong>This is how lovely the bodice looks now! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image024.jpg" alt=""></p>
<p><strong>You could also add a pretty frill showing from under the bodice: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2011_files/image026.jpg" alt=""></p>

