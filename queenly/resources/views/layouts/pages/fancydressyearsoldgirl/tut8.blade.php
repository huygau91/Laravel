<h1>Tutorial 8. Sewing the Modesty Flaps in Place.</h1>
<p><strong>I assemble the cut out modesty flaps. Here is one for the front. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image002.jpg" alt=""></p>
<p><strong>I topstitch the seam. It will be easier to turn the piece out this way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>I trim the seam allowances: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image006.jpg" alt=""></strong></p>
<p><strong>Please look at the way I join pieces along even very blunt corners: </strong>I try my best to avoid turning the piece on the needle. Instead, I prefer to make two separate rows of stitches that meet in the corner.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image010.jpg" alt=""></p>
<p><strong>I topstitch the seam: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image012.jpg" alt=""></strong></p>
<p><strong>I trim the seam allowances:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image014.jpg" alt=""></strong></p>
<p><strong>And I make a row of joining stitches along the vertical raw edge: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image016.jpg" alt=""></strong></p>
<p><strong>I topstitch it again: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image018.jpg" alt=""></strong></p>
<p><strong>I trim the seam allowance lengthwise and trim the ends at an angle: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>Then I turn out the modesty flap and fold the raw edges inwards: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image022.jpg" alt=""></strong></p>
<p><strong>I stitch down the last side of the modesty flap: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image024.jpg" alt=""></strong></p>
<p><strong>And I sew the second modesty flap in the same technique. </strong></p>
<p>Only this time, I use an even easier way of sewing down the last side of the modesty flap: <strong>I simply leave a small hole for turning it out. </strong>It is a little harder to turn out the piece in this case but a lot easier to stitch down the hole.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image026.jpg" alt=""></p>
<p><strong>I use a thick sewing needle for turning out the corners. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image028.jpg" alt=""></p>
<p><strong>Then I stitch down the hole: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image030.jpg" alt=""></p>
<p><strong>And I press the finished modesty flaps:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image032.jpg" alt=""></strong></p>
<p><strong>I put the first modesty flap in its place at the back of the bodice, secure it with pins, and sew it on by hand:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image034.jpg" alt=""></p>
<p>It is done just the same with women�s clothes. People often ask me how to sew a modesty flap to the garment correctly. Do not make it too complicated! There is no need in sewing it in-between the lining layers. A modesty flap is nothing more than a purely decorative element which covers up the bare skin behind the laces. That is why hand stitches are enough. Just be careful not to pierce the bodice right through because you don't want to see these seams on the face.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image036.jpg" alt=""></p>
<p><strong>I sew the other modesty flap to the front of the bodice: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image038.jpg" alt=""></strong></p>
<p><strong>It is necessary to secure the opposite edge of the modesty flap as well </strong>to make the bodice whole. I check whether I have met the bust line and waistline measurements. The bodice is about 1cm smaller along these lines, which is perfect.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image042.jpg" alt=""></p>
<p><strong>I mark the area for pinning the modesty flap to the other half of the bodice </strong>to make sure the lacing will look neat at the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image044.jpg" alt=""></p>
<p><strong>And then I secure the modesty flap in place with hand stitches: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image046.jpg" alt=""></strong></p>
<p><strong>The bodice is finished! </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%208_files/image048.jpg" alt=""></strong></p>

