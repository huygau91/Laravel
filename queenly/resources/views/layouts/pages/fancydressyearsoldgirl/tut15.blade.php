<h1>Tutorial 15. Sewing a Petticoat.</h1>
<p><strong>In this tutorial, I will show you how to sew a petticoat for a little girl.</strong></p>
<p>I am holding <strong>a steel hoop </strong>in my hands. I took it out of a petticoat made for a grown woman and it is therefore a little longer than needed. Well, this is not a big deal. Although part of the hoop has to be double then, it will only hold the shape better. The hoop will still be soft and resilient enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image002.jpg" alt=""></p>
<p>I measure the diameter of the hoop to see how much fabric I need for the petticoat. It is 50cm − about the same value I figured out based on the girl’s body proportions. In this case, the hoop or <strong>the bottom edge of the petticoat should have the following circumference: 50cm х π = 157cm.</strong></p>
<p>Underskirts or petticoats are normally sewn with thin fabric. <strong>I will use nylon mesh fabric </strong>which stretches widthwise a lot more than it does lengthwise. I will cut it correspondingly so as to have the lesser stretch in the waist and along the hem.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image004.jpg" alt=""></p>
<p><strong>What concerns the length of the petticoat, </strong>it should first of all be comfortable for walking around. As for me, I take the distance from the waistband to the beginning of the hem ruffle on the skirt, which is 56-57cm. I add another 3cm for a waistband and get <strong>a total length of 60cm. This is the length of the petticoat with a waistband included.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image006.jpg" alt=""></p>
<p><strong>I need to leave some space for folding the edges of the petticoat </strong>to form a waistband with tunnels for elastic bands at the top and another tunnel for the hoop at the bottom. There will be a 2cm hem allowance at the bottom and another 3cm plus 1cm seam allowance at the top. And this means <strong>I need a piece of fabric with a total length of 66cm. </strong></p>
<p>I recommend making the petticoat as simple as possible in what concerns the sewing process. Do not make things too complicated. Sonja's petticoat will be sewn from a plain rectangle with a width of 160cm (length of hem) and a length of 66cm.</p>
<p>I start sewing the petticoat. <strong>All I need to do is sew the only vertical seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image008.jpg" alt=""></p>
<p><strong>I sew a 1cm tunnel for the hoop:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image010.jpg" alt=""></p>
<p><strong>Then, I fold the other edge 1cm</strong>:</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image012.jpg" alt=""></p>
<p><strong>And I fold it another 3cm (width of waistband). </strong>I leave a small hope for inserting an elastic band.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image014.jpg" alt=""></p>
<p><strong>I make a row of parallel stitches in the middle of this strip </strong>to form two tunnels for thin lingerie elastic bands.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image016.jpg" alt=""></p>
<p>Next, I insert two elastic bands with the same length as the girl's waist circumference into the two tunnels at the top <strong>and insert the hoop in the tunnel at the bottom. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image018.jpg" alt=""></p>
<p><strong>It hardly took me a few minutes to sew this very comfortable petticoat of just the right size! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2015_files/image020.jpg" alt=""></p>

