<h1>Tutorial 14. Carrying Out a Fitting.&nbsp; Shortening the Skirt.</h1>
<h1>Cutting Another Lace Ruffle for Finishing the Hem of the Skirt.</h1>
<p><strong>We are doing the first fitting, without a petticoat for now. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image004.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image006.jpg" alt=""></p>
<p><strong>I mark the required length of the skirt along the perimeter </strong>using a hem marker. I have set it to 1cm above the floor.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image012.jpg" alt=""></p>
<p>The fitting is over and I need to mark the actual hem line. The hem will be finished with a 6cm wide thickly gathered ruffle. The skirt is supposed to stop 1cm above the floor. I want the ruffle to reach down to the very bottom because there will be a petticoat underneath the skirt adding some volume to it. And, therefore, this centimeter is no longer relevant. I need to mark 6cm (ruffle width) minus 1cm (distance from floor) minus 1cm (seam allowance) from the marked line. As the result, I need to mark 4cm from the line. <strong>I mark it all along the perimeter.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><strong>And now I can safely cut it: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image016.jpg" alt=""></p>
<p>Next, I fold the full sweep skirt two times to measure the length of the hem. I need to figure out how long the ruffle should be. I will use double the length of the hem to account for gathers. <strong>A quarter of the hem is 104cm long. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image018.jpg" alt=""></p>
<p>If I multiply it by four (because the skirt is folded two times) and then by two (to account for gathers), I will get about 8.5m of fabric.</p>
<p><strong>Let us calculate the required width. </strong>The finished ruffle is supposed to have a width of 6cm, I add 1cm for a seam allowance, which makes 7cm total, and multiply it by two. The result is 14cm.</p>
<p><strong>Therefore, I need to prepare strips of fabric with a width of 14cm and a total length of 8.5m to finish the hem of the skirt. </strong></p>
<p><strong>I make corresponding marks </strong>on the remaining fabric and tear it into strips of the required width.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image020.jpg" alt=""></p>
<p><strong>I have prepared a pile of 14cm wide strips of fabric: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image022.jpg" alt=""></p>
<p><strong>And now I need to prepare the lace trimming </strong>for sewing it on together with the ruffle. I cut it in half lengthwise, very precisely. The half with a scalloped edge will be sewn onto the front and the half with a plain edge - onto the back of the skirt. The difference will hardly be noticeable on a full sweep skirt decorated with a ruffle, especially one made for a little girl.</p>
<p><strong>I sew the lace trimming on along the hem: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image024.jpg" alt=""></p>
<p>And then <strong>I sew the ruffle strips together in one long strip.</strong> I sew without interrupting the thread, fold the top strip conveniently, and sew the next one to it. The resulting single strip is about 9m long.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image026.jpg" alt=""></p>
<p><strong>I press open every seam:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><strong>Then, I fold the strip in half lengthwise and secure it by sewing.</strong> It is very important to stitch the strip down along the full length of the open edge before gathering it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image030.jpg" alt=""></p>
<p><strong>I switch to a ruffler presser foot and start gathering the first strip of fabric.</strong> You can sew it into a ring straight away or do it later when sewing the ruffle on the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image032.jpg" alt=""></p>
<p>After measuring the ruffle, I realized it was still way too long: the presser foot did not gather it into half the original length. <strong>I want to achieve the right length though, so I gather it again.</strong> The ruffle is supposed to have a length of 4.2m.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image034.jpg" alt=""></p>
<p>I have achieved the desired result and now I can <strong>sew the ruffle on along the hem of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image036.jpg" alt=""></p>
<p>I have notched the lace trimming slightly to help it settle evenly and <strong>serged the edge on the inside.</strong> You could also make a row of strengthening stitches to secure the seam allowance in the upward position. I haven't though.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2014_files/image038.jpg" alt=""></p>
<p><strong>You can see the result below: </strong></p>
