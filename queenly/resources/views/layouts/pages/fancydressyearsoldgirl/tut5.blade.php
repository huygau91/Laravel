<h1>Tutorial 5. Assembling the Face. Joining the Face of the Bodice with the Lining.</h1>
<p><strong>I assemble the face of the bodice. </strong></p>
<p>I arrange the pieces in the same order in which they are supposed to be joined and<strong> sew them together with a 1.2cm seam allowance.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image004.jpg" alt=""></p>
<p>You can number the pieces with a disappearing ink pen to avoid confusion.</p>
<p><strong>I trim the seam allowance down to about 0.5-0.7cm, trim the ends at an angle, and continue sewing.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image008.jpg" alt=""></p>
<p><strong>Here is a finished half of the bodice: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>And now I need to press the lining and the face. </strong></p>
<p><strong>I start from the lining. </strong>I even out the bones and help them acquire the right shape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image012.jpg" alt=""></p>
<p><strong>Next, I press the face pieces. </strong>I do it on a sleeve ironing board placed edgeways. It is really easy to press seams open this way. Then I put the garment on the regular flat ironing board and press it from the other side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image016.jpg" alt=""></p>
<p><strong>Now I need to join the face with the lining. </strong></p>
<p><strong>I pin the pieces together along the top edge:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>And I make a row of joining stitches following the stay-stitches on the lining.</strong> Remember to make bar tacks at the intersections of stay-stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>I take an organza ribbon with a width of 2-3cm and slightly ease in the top edge of the bodice underneath it.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image022.jpg" alt=""></p>
<p>This ribbon will reinforce the top edge of the bodice, prevent the rounded areas from stretching out, and enhance the fit of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image024.jpg" alt=""></p>
<p><strong>Next I topstitch the seam folding the seam allowances to the side of the lining:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image026.jpg" alt=""></p>
<p><strong>And I trim the seam allowances lengthwise: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image028.jpg" alt=""></p>
<p><strong>The seam looks like this now: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image030.jpg" alt=""></p>
<p><strong>I do the same with the second pair of pieces. </strong></p>
<p><strong>Again, I pin them together and then join them along the top edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image032.jpg" alt=""></p>
<p><strong>And I make a row of joining stitches following the stay-stitches on the lining. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image034.jpg" alt=""></p>
<p><strong>I take another organza ribbon and sew it on slightly easing in the fabric underneath it. </strong>I keep the seam allowance at 1.2cm. I simply follow the previously made seam seen through the organza.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image036.jpg" alt=""></p>
<p><strong>I fold the organza up and secure it by stitching to prevent it from getting in the way in further work: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image038.jpg" alt=""></p>
<p><strong>I notch the seam allowances in the rounded armscye area and trim the ends at an angle:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image040.jpg" alt=""></p>
<p><strong>And I topstitch the seam.</strong> I fold the seam allowance toward the side of the lining, carefully even out the seam, and topstitch it 1mm from the edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image042.jpg" alt=""></p>
<p><strong>I trim the seam allowances down to about 0.5cm: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image044.jpg" alt=""></p>
<p>Make sure to notch out any excess fabric in the corner so you can turn them out neatly.</p>
<p><strong>I sew along the central edge of the back and the central edge of the front </strong>trying to get right into the row of stay-stitches next to the bone. Beware not to sew over the bone itself!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image046.jpg" alt=""></p>
<p><strong>I trim the seam allowances lengthwise and trim their ends at an angle: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image048.jpg" alt=""></p>
<p><strong>I have finished working on the first half of the bodice! </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image050.jpg" alt=""></strong></p>
<p><strong>And I simply do all the same things with the other half. </strong></p>
<p><strong>I turn out the two halves</strong>. The smaller the size, the harder it is to turn it out. I use a thick sewing needle for turning out the corners.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%205_files/image052.jpg" alt=""></p>

