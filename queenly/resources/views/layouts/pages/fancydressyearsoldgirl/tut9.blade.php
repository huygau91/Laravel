<h1>Tutorial 9. Cutting a Full Sweep Skirt and a Waistband.</h1>
<p>The fabric which I plan to use for the skirt has a width of 240cm. It is perfect for cutting a flared skirt.</p>
<p><strong>Let me calculate fabric consumption for a full sweep skirt</strong> for a girl with a 52cm Waist Circumference and a 68cm Waist to Floor measurement. <strong>I will try to cut it as a seamless circle. </strong>If <strong>2πR=52cm,</strong> then<strong> R=8cm.</strong> This is the inner radius of the circle (the waist opening). <strong>And the outer radius is 68+8=76cm.</strong> I multiply it by two and get <strong>152cm.</strong> Therefore, I need a square piece of fabric with a side of 152cm. My fabric is wide enough.</p>
<p><strong>It must be folded correctly to avoid mistakes.</strong> First of all, I need to find the center of the circle. <strong>I mark 80cm instead of 76cm from the edge to make sure there is enough fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image002.jpg" alt=""></p>
<p><strong>And I mark another 80cm from the resulting point down along the stripe. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image004.jpg" alt=""></p>
<p><strong>This is the center of the circle. </strong></p>
<p><strong>I fold the fabric along the found center so as to form a square with a side length of 80cm. </strong>My piece of fabric is about 160cm long so I fold it in half lengthwise and 80cm widthwise. The remaining fabric will be used for the armlets, the waistband, and other decoration purposes.</p>
<p><strong>I fold the fabric in a triangle or cone as shown in the pictures below:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image010.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image016.jpg" alt=""></p>
<p><strong>I mark the inner radius of 8cm from the corner and mark another 70cm from it. </strong>The skirt is supposed to be 68cm long but I add some extra centimeters because children grow really fast.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image018.jpg" alt=""></p>
<p><strong>Here is all about the cutting process. </strong>All I need is cut along the lines. I leave a seam allowance at the top for sewing on the waistband. The rest of the fabric will be used for the waistband and the armlets.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image020.jpg" alt=""></p>
<p>The full sweep skirt is ready. <strong>There will be a zipper at the back so I make an 18cm long slit: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image022.jpg" alt=""></p>
<p><strong>I notch the front and the side seams in the very middle. </strong>This will help the waistband settle neatly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image024.jpg" alt=""></p>
<p><strong>I measure the waist opening just in case. </strong>It is 60cm whereas the Waist Circumference measurement is 52cm. It means I need to gather the skirt a little to fit the waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image026.jpg" alt=""></p>
<p><strong>I start cutting the waistband.</strong></p>
<p>It should not be too wide because the girl is small. <strong>I think 3cm is good. </strong></p>
<p>I need to tear off a strip of fabric with seam allowances included. Therefore, it should have a <strong>width of 3 + 3 + 2.5 = 8.5cm.</strong> Now let's calculate the length: the Waist Circumference is 52cm, plus another 4cm for the modesty flap, and another 3cm for the seam allowances. The result is 59cm. <strong>I will make it 60cm just to be on the safe side.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image028.jpg" alt=""></p>
<p><strong>I duplicate the waistband with a strip of iron-on batiste from the underside. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image030.jpg" alt=""></p>
<p><strong>And I fold it in half: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%209_files/image032.jpg" alt=""></p>

