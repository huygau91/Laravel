<h1>Tutorial 13. Finishing the Bottom Edge of the Bodice</h1>
<h1>And the Skirt with Lace Trimmings.</h1>
<p><strong>I need to add a lace ruffle along the bottom edge of the bodice. </strong></p>
<p><strong>I make a row of stitches at a seam allowance width from the edge of the lace trimming.</strong> The trimming is two times longer than the bottom edge of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>I make another row of parallel stitches </strong>to gather it evenly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><strong>And I gather the lace on the stitching.</strong> It is always best to gather fabric from both ends to achieve even folds. After reaching the desired length, I secure it by sticking in a pin and wrapping the thread tails around it. This way the gathers won't be able to slide further along the thread.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image006.jpg" alt=""></p>
<p>I can safely distribute the gathers on the thread knowing that its length is now fixed. Next, I take the bodice and <strong>distribute the gathered lace along its bottom edge.</strong> The end of the ruffle should be positioned in about the middle of the modesty flap at the back of the bodice so as to leave no gap between the two ends when it is laced. <strong>The ruffle below is secured in place with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><strong>I sew it on by hand.</strong> I could have done it by machine because the fabric is not so thick, but I don't feel like having another row of stitches on the face of the bodice. I could have also finished it with a bias tape on the inside but I prefer to leave it as is for the time being.</p>
<p><strong>Here is the bodice decorated with a lace ruffle: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image010.jpg" alt=""></p>
<p><strong>I move on to the skirt and mark the position of the ruffle on it. </strong></p>
<p>When I was calculating the girl's body proportions, I estimated that the ruffle should be placed 40cm below the waistline.</p>
<p>Therefore, <strong>I mark 40cm down from the waistband </strong>and <strong>mark the position of the ruffle </strong>along the full perimeter of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image012.jpg" alt=""></p>
<p>I make sure <strong>all marks are clear enough</strong> so I don't have to make guesses in the sewing process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image014.jpg" alt=""></p>
<p>I start sewing the lace trimming on from the center of the back<strong> forming folds by hand. </strong>I didn't want to have to calculate the required length so I didn't gather it in advance. I will simply put down another lace trimming if this one ends too soon. I continue forming occasional folds and sewing the lace trimming on along the full perimeter of the skirt. I carefully secure the last fold at the end.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2013_files/image016.jpg" alt=""></p>
