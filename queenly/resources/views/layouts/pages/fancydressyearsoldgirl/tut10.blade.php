<h1>Tutorial 10. Serging the Edges of the Skirt.&nbsp; Installing the Zipper.</h1>
<h1>Sewing the Waistband in Place.</h1>
<p><strong>I serge one edge of the waistband:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image002.jpg" alt=""></p>
<p><strong>And I serge both edges of the zipper slit. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image004.jpg" alt=""></p>
<p>I spread the slit out straight and serge the edges forming a teardrop shape at the bottom. It is <strong>a single, uninterrupted serging seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image006.jpg" alt=""></p>
<p><strong>The skirt will feature a regular zipper and not a blind one. </strong>It will look good on the striped fabric. <strong>I make a small dart</strong> to achieve a smooth transition from the zipper to the rest of the skirt. This way I will also be able to open the seam allowances neatly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image008.jpg" alt=""></p>
<p><strong>I make a notch at the end of the zipper: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image012.jpg" alt=""></strong></p>
<p>And I open the seam allowances. <strong>I highly recommend pressing the dart and the seam allowances in advance:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image014.jpg" alt=""></p>
<p>I undo the zipper and <strong>sew along one side of the zipper tape</strong> with the teeth showing just a little.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image016.jpg" alt=""></p>
<p><strong>I close the zipper and finish framing it:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image018.jpg" alt=""></p>
<p><strong>I return back to the top </strong>and undo the zipper at the end to prevent the slider from getting in the way as I sew.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image020.jpg" alt=""></p>
<p><strong>That's it! The zipper is where it should be!</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image022.jpg" alt=""></strong></p>
<p><strong>I mark 3-4cm from the edge of the waistband for the modesty flap </strong>and then mark the waist circumference from that point. These are the starting and finishing point for sewing in the waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image024.jpg" alt=""></p>
<p><strong>I make a row of stay-stitches along the top edge of the skirt </strong>and measure the resulting length. You can gather the fabric on the thread a little if the top edge is a little longer than the waist circumference.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image026.jpg" alt=""></p>
<p><strong>I position the waistband in accordance with the control points</strong>.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image028.jpg" alt=""></p>
<p><strong>I find the middle of the waistband</strong> and fold it in half between the control points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image030.jpg" alt=""></p>
<p><strong>I mark the middle of the waistband and pin it to the middle of the skirt: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image032.jpg" alt=""></strong></p>
<p><strong>I re-distribute the entire skirt along the waistband:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image034.jpg" alt=""></p>
<p><strong>And I sew the waistband to the skirt with a 1.2cm seam allowance:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image036.jpg" alt=""></p>
<p><strong>I form hanging loops, pin them at the side seam notches</strong>, and sew them on from the inside.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image038.jpg" alt=""></p>
<p><strong>I trim all excess seam allowance fabric from one side of the waistband, fold it in half, and sew it down: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image040.jpg" alt=""></p>
<p><strong>I turn out the end of the waistband: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image044.jpg" alt=""></p>
<p><strong>It looks very clean if you fold the seam allowance upwards.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image046.jpg" alt=""></p>
<p><strong>I find the other end of the waistband with a closure flap</strong> and sew along the vertical raw edge. Then I find the starting point of the waistband, make a neat bar tack, and finish sewing the closure flap.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image048.jpg" alt=""></p>
<p><strong>I trim the seam allowance lengthwise and trim the ends at an angle:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image050.jpg" alt=""></p>
<p><strong>I carefully turn the piece out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image052.jpg" alt=""></p>
<p><strong>Then I fold the seam allowances upwards, hide them underneath the waistband, and secure them with pins. </strong>All seam allowances must be pushed inside the waistband. Seam allowances tend to be somewhat unruly so I secure them with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image054.jpg" alt=""></p>
<p>Finally, <strong>I make the last row of joining stitches </strong>over the face side. It can be made right through the groove of the seam or 1mm above it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image056.jpg" alt=""></p>
<p><strong>Here is the finished waistband.</strong> All I need to do is add a hook and a thread eye.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/6%20yo/Tutorial%2010_files/image058.jpg" alt=""></p>
