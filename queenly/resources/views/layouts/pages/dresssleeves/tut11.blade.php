<h1>Tutorial 10. Sewing a Mock-Up after the New Pattern.</h1>
<p><strong>I have cut mock-up pieces based on the newly adjusted patterns</strong> and transferred the major circumference lines and the control points for aligning the curved seams.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image002.jpg" alt=""></p>
<p><strong>Instead of cutting the central pieces with a fold line, I cut them as two halves for the mock-up. It is easier to align the edges of the pieces this way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image004.jpg" alt=""></p>
<p><strong>Next, I assemble the mock-up, put it on the dress-form, </strong>and check whether the pieces cut after the newly adjusted patterns fit together well enough. This stage is important because it allows you to double-check everything and develop a proper plan of actions before cutting expensive quality fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image006.jpg" alt=""></p>
<p><strong>I have only assembled a half of the mock-up</strong> because my dress-form fully imitates my client's body thanks to the first mock-up. If the proportions of your dress-form are different from your client's body proportions, then you should sew a full mock-up, put it on, and stuff all hollows with padding polyester.</p>
<p><strong>I am quite happy with the result!</strong> It is exactly what I wanted to achieve: the princess seams look graceful, the modelling lines are exactly the way I drew them, the control points for aligning the curved edges meet correctly and so do the horizontal circumference lines, and the separate side element passes from the back onto the front of the dress very smoothly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image012.jpg" alt=""></p>
<p>The experience of sewing a mock-up really helps when you start working with the actual expensive fabric because you already know how to join the pieces and have a good understanding of the process.</p>
<p><strong>The flare on the skirt looks really beautiful and the train sewn into the central seam at the back of the skirt will only emphasize the effect.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2010_files/image014.jpg" alt=""></p>
<p>The pattern has been trued and now I can print it out, cut the pieces, and begin with the actual sewing process!</p>

