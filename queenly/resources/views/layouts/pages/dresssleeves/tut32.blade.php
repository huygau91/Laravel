<h1>Tutorial 31. Decorating the Garment. Part 1.</h1>
<p><strong>I will decorate this dress with bits of lace left from the previous dress.</strong> I cut out symmetric motifs and stack them up in piles. Apart from this, I need to cut out scalloped straps of lace for finishing the sleeves and the neck hole.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image002.jpg" alt=""></p>
<p><strong>I have prepared a set of decoration tools.</strong></p>
<p><strong>I have wrapped a small dress-form in packaging film and secured it with sticky tape.</strong> It is very convenient and protects the dress-form from getting damaged.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image004.jpg" alt=""></p>
<p>But my dress is rather small and I can perform all decoration operations right on it. That is why <strong>instead of a dress-form I have wrapped in packaging film and secured with sticky tape a small ironing board.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image006.jpg" alt=""></p>
<p><strong>I will add lace cuffs to the sleeves.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image010.jpg" alt=""></p>
<p><strong>Here is what I do to determine how wide exactly the cuff needs to be to allow my client's hand to pass through and at the same time hug the wrist.</strong></p>
<p><strong>I take an A4 sheet of thin plastic</strong> and draw stripes with 1cm intervals along the longer side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image012.jpg" alt=""></p>
<p><strong>During a fitting, I wrap the plastic sheet around my client's arm, very tightly, </strong>thereby determining the smallest possible size of the cuff, even out the edges of the sheet using the marked guidelines, take it off, and secure the resulting plastic tube with sticky tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image016.jpg" alt=""></p>
<p><strong>Then I put the sleeve on the plastic tube and decorate it.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image020.jpg" alt=""></p>
<p><strong>It is a clever idea to put the sleeves from either end of the tube simultaneously and align their seams - it will allow you to create perfectly symmetrically decorated cuffs.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image022.jpg" alt=""></strong></p>
<p>Unfortunately, my dress does not fit onto the dress-form so <strong>I put it on the previously prepared small ironing board and secure it with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image024.jpg" alt=""></p>
<p><strong>There will be a large lace motif in the middle of the dress. I position it exactly in the middle and secure it with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image026.jpg" alt=""></p>
<p><strong>I secure the bottom part of the motif in place with Gutermann fabric glue. </strong>Even if you prefer to sew lace motifs on the dress, I highly recommend you should still first secure them in place with a little glue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image028.jpg" alt=""></p>
<p><strong>I have arranged scalloped straps of lace along the neckline.</strong> I have notched them to help them settle in the desired way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image030.jpg" alt=""></p>
<p><strong>I glue each strap of lace on, a small area at a time.</strong> It doesn't take much glue at all.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image036.jpg" alt=""></p>
<p><strong>The first strap is in place.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image038.jpg" alt=""></p>
<p><strong>Now I need to position the second strap along the other side of the neckline in a perfectly symmetric way.</strong> The second lace strap has the same length as the first one and its motifs are symmetric. I glue it in place.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image042.jpg" alt=""></p>
<p><strong>While waiting for the glue to set, I decorate a small area of the front with lace motifs and glue them on.</strong> I try my best to position the motifs symmetrically on the right and the left half of the front. You can buy ready-to-use lace trims in advance if your budget allows for it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image050.jpg" alt=""></p>
<p><strong>I "tear" the dress off the packaging film as soon as the glue at the top sets properly.</strong> Then I simply move it along the ironing board and continue decorating it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2031_files/image052.jpg" alt=""></p>

