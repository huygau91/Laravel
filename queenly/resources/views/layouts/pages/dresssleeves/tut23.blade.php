<h1>Tutorial 22. Assembling the Face of the Back Half of the Dress.</h1>
<h3></h3>
<p><strong>I start working on the back of the dress. First I will sew a blind zipper in the middle of it.</strong> There are two notches on the central back piece: one marks the end of the zipper and the other the beginning of the train. I will cut the train after assembling the back so I can figure out its length by measuring the distance along the central seam from the notch at the beginning of the train down to the hem of the skirt. I will sew the central back seam only after sewing in the train.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image002.jpg" alt=""></p>
<p><strong>I start sewing in the zipper.</strong> I undo it, place it over the seam allowance of the central back seam (2cm), and sew it on down to the notch which marks the level where it is supposed to stop.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image012.jpg" alt=""></p>
<p><strong>I use a specialty zipper presser foot and stitch close to the teeth.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image018.jpg" alt=""></p>
<p><strong>The zipper is secured in place now. Make sure the two tails of the zipper tape are levelled. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image020.jpg" alt=""></p>
<p><strong>The zipper will look like this when installed properly:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image022.jpg" alt=""></p>
<p><strong>To shorten the zipper you just make small closely spaced horizontal stitches at the required level and then safely trim it.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image024.jpg" alt=""></p>
<p><strong>The zipper is now in place and I continue working on the back pieces in the same order I worked on the lining of the dress. </strong></p>
<p><strong>First of all, I pin the princess seams in the control points trying to do it as accurately as I can. </strong>There are moments when it feels like it�s impossible to get certain control points aligned. It's why I notch the princess seam almost full length both on the side piece and on the central piece with 1cm intervals between the notches. Each notch is about 1cm deep.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image030.jpg" alt=""></p>
<p><strong>I will only sew the princess seams down to the intersection with the seam allowance of the top edge so I can fold the lace piece back when joining it with the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image034.jpg" alt=""></p>
<p><strong>I sew the princess seams on the back half of the dress.</strong> I stitch over the side piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image044.jpg" alt=""></p>
<p><strong>I check the quality of the seams. There was a small tuck in a particular spot so I ripped it up and re-stitched it even though it was not compulsory.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image050.jpg" alt=""></p>
<p><strong>Both princess seams at the back are nice and even! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image054.jpg" alt=""></p>
<p><strong>As I have mentioned before, I did not sew the back princess seams up to the very top because of the lace piece.</strong> When joining the lining and the face of the back, I will notch the lace so I can fold it back and join the pieces along the top edge without catching it. The top edge will then be finished with the lining while the lace piece will continue upwards without any seams on it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image058.jpg" alt=""></p>
<p><strong>The back of the dress will look like this:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image060.jpg" alt=""></strong></p>
<p><strong>I will press open and trim the seam allowances.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image062.jpg" alt=""></p>
<p><strong>Then I will sew the skirt to the bottom of the bodice after pinning them together in the control joining points.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2022_files/image066.jpg" alt=""></p>
