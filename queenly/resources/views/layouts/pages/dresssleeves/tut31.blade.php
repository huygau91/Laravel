<h1>Tutorial 30. Setting the Sleeves into the Armscyes.</h1>
<p><strong>I have carefully pressed the side seams.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image002.jpg" alt=""></p>
<p><strong>And now I notch the side seam 5cm below the armscye.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image004.jpg" alt=""></p>
<p><strong>I fold the seam allowance toward the front of the dress. </strong>One side of the seam allowance will be secured by the set-in sleeve and the other side can be sewn to the lining or just secured by glue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image006.jpg" alt=""></p>
<p><strong>I have prepared the area for sewing in the sleeve. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image010.jpg" alt=""></p>
<p><strong>I fold the armscye in half and mark the apex with a pin to align it with the apex of the sleeve cap.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image014.jpg" alt=""></p>
<p><strong>I have sewn the sleeves.</strong> Be very careful when doing it: the fabric is very delicate. You should pull the fabric on as you sew, trying to make the garment somewhat elastic so the thread does not burst when the wearer moves her arm. You can make another seam 1mm below the first one just in case.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image016.jpg" alt=""></p>
<p><strong>I turn the sleeves right side out. Make sure to tell the front of the sleeve from its back before sewing it in. I made a notch at the front of the sleeve during the cutting.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image018.jpg" alt=""></p>
<p><strong>I pin the first sleeve into the armscye.</strong> I align the side seam of the dress with the seam of the sleeve and the notch at the top of the armscye with the notch on the sleeve cap. First I distribute the armscye fabric along the front part of the sleeve and secure it with pins. To prevent the pins from falling out at the back of the sleeve, I let pierce the lace several times. Do not ease in the sleeves!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image036.jpg" alt=""></p>
<p><strong>I pin the other sleeve into the other armscye and sew them both in by stitching along the armscyes.</strong> The sleeves fit in the armscyes perfectly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image038.jpg" alt=""></p>
<p><strong>There is an area that requires particular attention when you are sewing in the sleeves:</strong> it is the area where the lace at the back of the dress joins the lace of the sleeve. Be very careful not to draw the fabric together as you sew. Keep checking the quality of the seam and if you have drawn the lace together, then simply notch the thread, rip the seam up,&nbsp; carefully spread the lace, and re-stitch it. Pull the piece lightly to yourself as you sew.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image042.jpg" alt=""></p>
<p><strong>I still need to hem the dress before I can decorate it.</strong> You can choose any technique you like here. Heat-sealing will work very well, as well as the traditional method when you just serge the edge and then fold it and sew it. You should either slightly shorten the lining so it doesn't show from underneath the skirt or hem it even with the face fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image044.jpg" alt=""></p>
<p><strong>I really like using lace ribbons on the hem of a dress.</strong></p>
<p><strong>Part of the bottom edge of the dress, where the train was sewn in, forms a kind of a stair step </strong>that is actually a seam allowance that needs to be folded up to the inside.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image046.jpg" alt=""></p>
<p><strong>I put a lace ribbon over the edge of the seam allowance, scalloped edge facing up. </strong>I will fold the seam allowance to the inside but first I sew the ribbon onto it positioning the scallops 1mm above the assumed fold line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image048.jpg" alt=""></p>
<p><strong>When I fold the seam allowance, the scalloped edge will stay on the face and the rest of the ribbon will overlay the seam allowance on the inside.</strong> You can secure the seam allowance on the inside by hand, by machine, or with the help of fusible webbing.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image050.jpg" alt=""></p>
<p><strong>The hem of the dress looks really clean.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image052.jpg" alt=""></strong></p>
<p>If you want to fold the hem of the dress, then you should<strong> notch the corners of the lace insets</strong> by the width of the turn up, fold the main fabric to the inside, and continue finishing the hem according to the chosen method. The lace stays detached and you trim it even with the hem. You can also add appliqu� along the bottom edge of the lace.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2030_files/image056.jpg" alt=""></p>

