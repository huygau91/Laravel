<h1>Tutorial 3. Customizing the Size of the Pattern.</h1>
<p>It turns out that this pattern requires exactly the same adjustments as the pattern of the halter-neck dress. As the result, the mock-up of the dress with shoulder straps will also be exactly the same as the mock-up of the previous dress.</p>
<p>Please not that if you did not sew the first dress, you absolutely must sew a mock-up after the initial unadjusted pattern like I did in the first chapter and then put it on the dress-form, measure it as if it were a person, and write all resulting values under "Pattern" (table 1).</p>
<p>Only then can you start calculating adjustments to the standard pattern based on your client's measurements.</p>
<p>I believe there is no need in another step-by-step demonstration because you can find it in the previous chapter.</p>
<p>I have already calculated all adjustments values and recorded them in the table and now I can start applying them to the standard paper pattern of our dress with shoulder straps.</p>
<p><strong>I start from the side front piece.</strong> I draw the major circumference lines and apply adjustments based on the calculated values written in table 1 under �Adjusted Side Seam Front": I remove 1cm at the bust line level, leave it as is at the under-bust level, and remove 1cm in the waist, in the stomach (12cm below the waist), and in the hips.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image002.jpg" alt=""></p>
<p><strong>I re-draw the side seam line using as a template the side front piece of the pattern after which I made the mock-up:</strong> first I draw the section from the waistline to the hip line, then I turn the pattern about the point on the waistline, prolong the side seam line up to the under-bust line, turn the pattern about the point on the under-bust line, and prolong the side seam line up to the top edge of the piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image010.jpg" alt=""></p>
<p><strong>I transfer the notches made at the level of the under-bust line, the waistline, the stomach line, and the hip line onto the new side seam line and mark its length.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image012.jpg" alt=""></p>
<p><strong>And then I shorten it by 1cm as follows from the calculations.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image014.jpg" alt=""></strong></p>
<p><strong>And I move to the clip-toward-armscye measurement.</strong>&nbsp; I will use the initial measurement of the pattern instead of applying adjustments to it. My client's clip measurement is 10cm but I will prolong it up to 11cm for this particular dress. I mark point B 12.2cm from the apex of the bust (point A): 11cm plus a seam allowance of 1.2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image016.jpg" alt=""></p>
<p><strong>Next I need to alter the front princess seam.</strong> There are no changes in the area above the waistline. As follows from the calculations, I shift it 0.6cm inwards in the waist, 0.5cm inwards in the stomach, and 0.1cm inwards in the hips.</p>
<p><strong>I re-draw the front princess seam.</strong> I connect the points on the hip line, the stomach line, and the waistline, and carefully bring it to the under-bust line. You can use the standard pattern as a template.</p>
<p><strong>I re-draw the top edge</strong> using the standard pattern piece as a template.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image018.jpg" alt=""></p>
<p><strong>I have applied all alterations to the side front piece.</strong> Make sure to transfer the notches onto the new side seam and princess seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image020.jpg" alt=""></p>
<p><strong>Now I need to alter the central front piece.</strong></p>
<p>First of all, <strong>I shift the entire central fold line 1cm inwards in keeping with the bust middle measurement.</strong> Next <strong>I apply alterations to the princess seam:</strong> shift it 0.6cm inwards in the waist, 0.5cm inwards in the stomach, and 0.1cm inwards in the hips. I re-draw the front princess seam and transfer the notches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image022.jpg" alt=""></p>
<p><strong>You can predetermine and mark the length of the shoulder strap based on the neck base to bust point measurement.</strong> My client's measurement is 23cm and I add another 2cm for a seam allowance. The shoulder strap will then stop 25cm above the apex of the bust. It looks quite short at the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image024.jpg" alt=""></p>
<p><strong>The length of the princess seam is supposed to be the same on both front pieces.</strong> I check how much shorter the princess seam has become on the side front piece (in my case, the difference is 2.3cm) and shorten it by the same value on the central front piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image028.jpg" alt=""></p>
<p><strong>I mark the adjusted clip-toward-neckline</strong> which makes 6cm plus a 1.2cm seam allowance, i.e. a total of about 7.5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image030.jpg" alt=""></p>
<p><strong>I draw a smooth transition from the princess seam and the neckline to the shoulder strap using the initial pattern.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image032.jpg" alt=""></strong></p>
<p><strong>I give a small area of the neckline the same width as the shoulder strap (4.3cm).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image034.jpg" alt=""></p>
<p><strong>And I work on the shape of the neckline to make it more graceful.</strong> Remember: the smaller the breasts are, the more open the neckline should be. I try to shape it based on my client's clip measurement. I prolong the clip-toward-neckline by just 0.5cm to enhance the shape of the neckline. I will definitely examine it later, after sewing a mock-up after the adjusted pattern and putting it on the dress-form, and adjust its shape or depth if needed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image036.jpg" alt=""></p>
<p><strong>The customized central front piece customized looks like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image038.jpg" alt=""></p>
<p><strong>I move on to the side back piece. </strong>I need to change the configuration and the length of the side seam line and apply alterations to the princess seam.</p>
<p><strong>You should be able to align the side seam lines of the front and the back pieces perfectly.</strong> I have noticed a minor difference in their configuration and adjusted it before applying alterations to the side back piece. It is a very small adjustment: I simply made it a little narrower in the waist. You can do the same if you wish.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image040.jpg" alt=""></p>
<p>I shift <strong>the side seam line </strong>as indicated by the calculated values recorded under �Adjusted Side Seam Back": 2.5cm inwards at the bust line level, in the stomach and in the ships, and 1.5cm inwards at the under-bust level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image042.jpg" alt=""></p>
<p><strong>I cut the adjusted side front piece along the side seam line.</strong> It will help me repeat the same length and configuration for the side seam line of the back piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%203_files/image044.jpg" alt=""></p>
<p><strong>I put the side front piece over the side back piece, align the notches, and re-draw the side seam line of the back piece using the front piece as a template. </strong></p>
