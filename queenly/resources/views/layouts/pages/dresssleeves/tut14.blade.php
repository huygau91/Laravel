<h1>Tutorial 13. Cutting the Dress.</h1>
<p><strong>I lay the pieces that require duplication on the iron-on batiste.</strong> I try to arrange them in the most efficient way to spare the fabric. I pin the pieces in place, carefully trace their contours, and transfer all control joining points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><strong>I will show you how to arrange the pieces on fabric schematically</strong> because they are long. Always try to arrange them in a rational way because you don�t want to waste expensive fabric in vain.</p>
<p><strong>I have cut out the pieces from the file titled "How to Join Pattern Sheets."</strong> They are all depicted at the same scale. I cut the pieces from three types of fabric: white and beige Armani fabric and lining fabric. We will talk about cutting the lace and the chiffon a little later. <strong>I have prepared three sheets of paper at the required scale. Two of them imitate the Armani fabric with a width of 140cm and the third one imitates the lining fabric with a width of 150cm.</strong> I will cut on the cross grain, which means the selvages will be at the top and down the bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image006.jpg" alt=""></p>
<p><strong>I pick out the pieces that need to be cut from the beige fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><strong>I lay the pieces in the most efficient way possible and measure the required amount of the beige fabric. </strong>I need 70cm of the beige fabric considering it will be folded in half.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image010.jpg" alt=""></p>
<p><strong>I cut the required amount of beige fabric and as much lining fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><strong>I fold both pieces of fabric in half, align their edges and fold lines, pin them together, lay and pin down the pieces, and then trace their contours and transfer all control joining points. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image016.jpg" alt=""></p>
<p><strong>During the cutting, </strong>I put the lining fabric on top of the beige fabric. All pieces cut from the beige fabric need to be duplicated with iron-on batiste. For this reason, instead of putting paper patterns on the lining fabric, I cover it up with the batiste which already has the contours of all pieces on it. I secure the lay with pins and cut the pieces out together simultaneously with their duplication pieces.</p>
<p><strong>I prolong the side front piece by 3cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image018.jpg" alt=""></strong></p>
<p><strong>The required amount of the white Armani fabric and the lining fabric is determined just the same way.</strong> I need 1.3m of either to cut the dress of the required size. I will fold each fabric in half during the cutting process. I will also prolong the central skirt piece by 3cm. The side front piece of the skirt has been prolonged by 3cm from the side adjoining to the front and by 4.5cm from the side adjoining to the back of the dress. I have prolonged the sides of the back skirt piece by 4.5cm and 15cm correspondingly. During the cutting, I will cover the fabric with the iron-on batiste and follow the contour of the central back piece transferred onto it instead of using the paper pattern.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><strong>When cutting the lace insets, I will cut the back piece and the top front piece as one whole.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><strong>The cutting process is over.</strong> I have all pieces at hand now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2013_files/image026.jpg" alt=""></p>

