<h1>Tutorial 4. Building the Lace Top Part of the Back.</h1>
<p><strong>I have prepared the back piece from the pattern of the dress with shoulder seams and cut the adjusted side back piece of the main dress.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image002.jpg" alt=""></strong></p>
<p><strong>It makes sense to draw the final configuration of the back at this stage (i.e. the way it is supposed to be on the finished dress) and build the lace part based on it. </strong>The best way to do it is to cut and join the adjusted back pieces, put them on the dress-form, and draw the desired shape of the back.</p>
<p><strong>You can save some time by doing the following:</strong> cut the adjusted side back piece and central back piece, put them together aligning their side seam lines, mark a seam allowance of precisely 1.2cm (particularly at the top), and pin the pieces together along the seam allowance line as if they were joined.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image011.jpg" alt=""></p>
<p><strong>I open the seam allowance and turn out the piece.</strong> It is a little distorted but it will go away on fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image013.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image015.jpg" alt=""></p>
<p><strong>Now let's decide on the length of the back.</strong> My client is a slender woman and there is no need in a strong tummy control effect. The back neckline can stop as low as 6cm above the waistline including the seam allowance. <strong>I mark the bottom point of the back neckline and a 2cm seam allowance.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image017.jpg" alt=""></strong></p>
<p><strong>I re-draw the back neckline of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image019.jpg" alt=""></p>
<p><strong>And I cut the back along the top edge. I have new back pieces for my dress now.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image021.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image023.jpg" alt=""></strong></p>
<p><strong>I apply adjustments to the central back piece of the pattern with shoulder seams in correspondence with the calculated values from table 2.</strong> I draw a waistline for the sake of convenience because all length measurements usually stop at the waistline. I also mark the length of the princess seam and the seam allowances.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image025.jpg" alt=""></p>
<p><strong>According to the calculations, I need to remove 3.5cm from the width of the back (point A).</strong></p>
<p><strong>My client's nape to waist measurement coincides with that of the initial pattern.</strong></p>
<p><strong>My client's shoulder to waist (back) measurement is larger </strong>than on the pattern - 44cm. I add a seam allowance of 1.2cm and draw an arc with a radius of 45.2cm.</p>
<p><strong>The back shoulder width measurement stays the same and so does the position of point B. </strong></p>
<p><strong>My client�s back shoulder slope measurement is larger </strong>than on the pattern - 43cm. I add a seam allowance of 1.2cm and draw an arc with a radius of 44.2cm.</p>
<p><strong>In order to preserve the back shoulder width measurement, I draw a vertical line through point B to the intersection with the arc marked based on the back shoulder slope measurement (point C).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image027.jpg" alt=""></p>
<p><strong>The actual shoulder measurement is 10cm but I add a 1.2cm seam allowance from either end, which makes a total of 12.5cm. </strong></p>
<p><strong>I draw an arc with a radius of 12.5cm from point C to the intersection with the arc built based on the shoulder to waist measurement of the back (point D).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image029.jpg" alt=""></p>
<p><strong>I connect points C and D and get a new shoulder line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image031.jpg" alt=""></strong></p>
<p><strong>I re-draw the back neck and the armscye.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image033.jpg" alt=""></strong></p>
<p><strong>To prolong the armscye line, </strong>I pin back together the adjusted back pieces, lay them over the back piece with a shoulder seam, align their waistlines, and trace the contour. The part of the back below the waistline is not relevant at the moment. Make sure to mark the level of the princess seam - it is an important part of the shaping process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image035.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image037.jpg" alt=""></p>
<p><strong>And remember the seam allowance!</strong> Both the top back piece and the bottom back piece have a seam allowance of 1.2cm. I mark a seam allowance of 2.4cm from the traced top edge of the back (line 1) and draw line 2 using the pattern.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image041.jpg" alt=""></p>
<p><strong>Line 2 is the bottom edge of the seam allowance of the top back piece. </strong>Anything below line 2 is not part of this piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image043.jpg" alt=""></p>
<p><strong>I draw line 3 in the middle of the seam allowance.</strong> The area from the bottom edge to line 3 is the back piece in its actual size.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image045.jpg" alt=""></p>
<p><strong>I take the back half of the sleeve and cut it along the armscye to prolong the armscye line.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image047.jpg" alt=""></p>
<p>The armscye of the sleeve which I have just cut out needs to sewn into the bottom part of the armscye at the back of the dress, which means they must have the same configuration. <strong>I align the back piece with the sleeve piece and draw the bottom part of the armscye.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image049.jpg" alt=""></p>
<p><strong>The shoulder seam part of the back looks like this: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%204_files/image051.jpg" alt=""></p>
