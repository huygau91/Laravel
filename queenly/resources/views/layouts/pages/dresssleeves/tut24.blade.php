<h1>Tutorial 23. Overview of Various Types of Trains and Related Cutting Techniques.</h1>
<p><strong>Before cutting the train, you need to make up your mind about its shape and volume. </strong>I will show you how to calculate and cut different types of trains.</p>
<p><strong>The back of the dress is ready:</strong> I have pressed everything, trimmed the seam allowances, and left the central back seam open for now. I measure the distance along the central seam from the notch at the beginning of the train to the bottom of the skirt. It is 101cm or <strong>a total of 103cm</strong> including the seam allowances and some space for turning the hem.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image002.jpg" alt=""></p>
<p><strong>Such trains are usually cut as a diamond, a circle, or a semi-circle</strong> depending on your client's and your own preferences. The shape of the train's "tail" extending along the floor is also up to you: it can be pointy or have a smooth half-circle edge.</p>
<p><strong>Here is how you cut a diamond-shaped train.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image004.jpg" alt=""></strong></p>
<p><strong>I take a square piece of fabric, 150cm by 150cm.</strong> I mark the length of the train (103cm) along two sides of it and draw two diagonal lines at a 45 degrees angle. Here is it, a diamond-shaped train.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image006.jpg" alt=""></p>
<p>If you want to <strong>make the train fuller</strong>, make it look richer, you can decorate it with lace appliqué or ruffles made of mesh fabric or chiffon.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image008.jpg" alt=""></p>
<p><strong>You can cut your train as a rectangle if you don't want the tail to be so pointy.</strong> You just take a piece of fabric with a width of 150cm and double the length of the train (206cm) and draw a triangular train on it. The length of the train is naturally limited to the width of your fabric. Then you fold the triangle in half along the base and sew it into the central seam at the back of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image010.jpg" alt=""></p>
<p><strong>You can sew ruffles along the sides of the triangle to make the train fuller or cut it as a half-circle.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image012.jpg" alt=""></p>
<p><strong>A half-circle or a full-circle train will have a lot more volume.</strong></p>
<p><strong>Here is how you cut a half-circle train. </strong>You sew together two pieces of fabric, each 150cm by 300cm, and cut a rectangle with a length of 300cm and a width of 253cm (150cm + 103cm). Then you make a 103cm long slit the sides of which will be sewn into the seam at the back of the skirt. And you draw a beautiful half-circle. You can adjust the shape of the train after cutting it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image014.jpg" alt=""></p>
<p><strong>There is one disadvantage about this kind of a train:</strong> its maximum length is limited to the width of your fabric (in my case, it is 150cm). If 103cm of it is supposed to be sewn into the skirt, then only some 50cm will extend along the floor.</p>
<p><strong>If your task is to cut a long full train, then you should cut it as a full circle.</strong> You sew together two rectangular pieces of fabric, each with a width of 150cm. Here is how you calculate the length of the rectangle: 103cm for sewing the train into the skirt + the length of the train based on your client's preferences or your own liking. When joining the two pieces, you leave that 103cm open so you can sew the train into the seam at the back of the skirt. I recommend shaping the bottom edge of the train as if it were a circle with a radius of 150cm (the width of your fabric). Experience shows that it creates the most beautiful shape. Do not try to spare the fabric by making its corners round!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image016.jpg" alt=""></p>
<p><strong>There is no way you can avoid a seam in the middle if you want to make your train as long as possible. </strong>Those trains do look gorgeous though!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image018.jpg" alt=""></p>
<p><strong>There are many possible ways to modify the above-mentioned techniques.</strong></p>
<p><strong>Suppose you want to make your train as long as possible but the central seam really spoils it all.</strong> In this case, you cut a full-circle train but instead of joining the two pieces of fabric you set them apart and insert a gore in-between leaving 103cm for sewing the train into the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image020.jpg" alt=""></p>
<p><strong>The train of your dress is really worth a great deal of attention and careful thought! </strong>It is an element that really stands out and you can't just neglect it.</p>
<p>Moreover, <strong>it takes a fair amount of fabric to make a train </strong>and it would be very sad to realize you made the wrong choice or made a mistake in the cutting process.</p>
<p><strong>The number of layers depends on the shape and the volume of the train.</strong></p>
<p>It is not a good idea to use two layers of fabric for a full-circle train for example. Besides, it should be made from lightweight fabric, such as veiling, organza, or chiffon.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2023_files/image022.jpg" alt=""></p>
<p><strong>You can also cut a double-circle train from mesh fabric or chiffon − it will look even more luxurious!</strong></p>
<p><strong>Never cut a half-circle train from bobbinet</strong> - you will end up with a curtailed and absolutely unattractive train!</p>
<p><strong>There is a rule:</strong> the thinner the fabric, the more volume the train needs, and vice versa.</p>

