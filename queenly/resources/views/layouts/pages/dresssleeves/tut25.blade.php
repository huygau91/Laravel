<h1>Tutorial 24. Cutting and Sewing a Train for the Dress.</h1>
<p><strong>A full-circle train is the best choice for my dress with regard both to the volume and the design.</strong> Besides, it is easy to cut and you can finish the hem in any way you like. You can heat-seal the bottom edge (you will find out how to do it in my course "Premium Skirts") or you can sew a baby hem like I did on the sleeves of the dress from the first chapter of this course.</p>
<p>Since this dress is just a training sample, I have decided to focus upon the technique to the detriment of beauty and simplicity. There are no particular technical fine points about cutting a single-layer train. And therefore I have decided to show you <strong>how to cut and finish a half-circle train made from two layers of chiffon.</strong></p>
<p><strong>I need </strong>two pieces of fabric, each with a width of 150cm and a length double the length of the central seam at the back of the skirt (103 x 2 = 206cm). In other words, I need a total of about <strong>4.5m of chiffon</strong> for my train.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image002.jpg" alt=""></p>
<p><strong>I will demonstrate the cutting process on paper.</strong></p>
<p>It is very hard to work with large pieces of chiffon and you will never get two identical pieces if you cut each one individually. <strong>Let me share a clever trick</strong> that will make the cutting process a lot easier and help you get two absolutely identical pieces of chiffon.</p>
<p><strong>I take a piece of chiffon with a width of 150cm and a length of 412cm</strong> and with selvages at the top and down the bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image004.jpg" alt=""></p>
<p><strong>I fold the piece in half.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image006.jpg" alt=""></p>
<p><strong>And then I stitch along the four sides of the folded piece at a presser-foot width from the edge in the following order:</strong></p>
<p><strong>First, I make a row of stitches (1) along the selvage of the longer side of the lay (206cm).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image008.jpg" alt=""></strong></p>
<p><strong>Then I turn the lay around and make a row of stitches (2) along the selvage of the opposite side (206cm - raw selvages).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image010.jpg" alt=""></strong></p>
<p><strong>Then I return the lay in the initial position and make a row of stitches (3) along the shorter side (150cm - raw edges opposite the fold line). </strong>I have stitched the lay from three sides.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image012.jpg" alt=""></strong></p>
<p><strong>Then I cut up the fold line and sew the cut edges together (4). The two pieces of chiffon are one whole now.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image014.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image016.jpg" alt=""></strong></p>
<p><strong>I fold the joined pieces of chiffon in half along the longer side (the fold line is on the right).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image020.jpg" alt=""></p>
<p><strong>I take the bottom left corner of the lay and fold it up and to the right so as to align the edge with the fold line.</strong> I mark point A 103cm up from the bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image022.jpg" alt=""></p>
<p><strong>My next task is to draw a half-circle line for the bottom edge of the train.</strong></p>
<p><strong>Using a measuring tape, I mark radii of 103cm from point A along the assumed half-circle line and draw the bottom edge of the train. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image026.jpg" alt=""></strong></p>
<p><strong>I trim the lay along the marked line.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image028.jpg" alt=""></p>
<p><strong>Without unfolding the lay, I lift the chiffon layers with my hand and pin them together along the half-circle bottom edge on either half of the lay.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image030.jpg" alt=""></p>
<p><strong>Then I open the lay and sew the layers together along the pinned bottom edge.</strong> The layers of chiffon are now sewn together along the entire perimeter.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image032.jpg" alt=""></p>
<p><strong>I trim away the selvage at the top together with the joining stitches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image034.jpg" alt=""></p>
<p><strong><em>I turn the piece out </em>and topstitch the half-circle edge.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image036.jpg" alt=""></p>
<p><strong>I stitch at a presser-foot width from the raw edge at the top </strong>thereby joining the two layers of chiffon along the full perimeter. <strong>And then I press it very carefully.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image038.jpg" alt=""></p>
<p><strong>I fold the train in half and press the part that will be sewn into the central seam at the back of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image040.jpg" alt=""></p>
<p><strong>The train will look like this.</strong> About 40cm of it will extend along the floor.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image042.jpg" alt=""></p>
<p><strong>This was the fastest and neatest way of finishing a train made from two layers of fabric. </strong>You can find more methods of cutting and sewing in trains in my other course - "Premium Wedding Skirts."</p>
<p><strong>I have sewn a soft cascading two-layer chiffon train using the very same technique.</strong></p>
<p><strong>I fold the train in half.</strong> The raw edge (which is 103cm long) needs to be sewn into the central seam at the back of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image044.jpg" alt=""></p>
<p><strong>I step about 1.5cm up from the bottom edge of the skirt to leave some space for finishing the hem and pin the end of the folded train to one side of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image046.jpg" alt=""></p>
<p><strong>I spread the train out along the skirt and find the point where it is actually supposed to start.</strong> This point basically coincides with the notch that marks the beginning of the train. The reality has met my expectations.</p>
<p><strong>The seam allowance of the central back seam is 2cm.</strong> I put the fold line of the train to the notch and secure part of the central back seam with pins: a section of about 15cm into which the train will be sewn in a single line. It will split in half below that level. I leave this merged area for the sake of preserving the tight fit of the skirt 20-25cm below the hip line. The flare should start lower.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image048.jpg" alt=""></p>
<p><strong>I cover the train with the other half of the skirt</strong>, align the edges and the slanted princess seams, and re-pin it so as to catch all layers. Next, I will <strong>stitch from the end of the zipper to the end of the merged part of the train.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2024_files/image050.jpg" alt=""></p>
<p><strong>And then I will open the skirt, sew either side of the train to the corresponding side of the skirt, </strong>and press the seams open.</p>
