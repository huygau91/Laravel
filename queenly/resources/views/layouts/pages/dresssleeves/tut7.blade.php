<h1>Tutorial 6. Testing the Adjusted Pattern on a Mock-Up.</h1>
<p><strong>I cut the pieces full length and mark the major lines from both sides.</strong> I simply align the hip line of the side skirt piece with the hip line of the side front piece to cut it full length.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image002.jpg" alt=""></p>
<p><strong>Before joining the pieces, I draw a separate side inset on the side front piece starting from the stomach line.</strong> I will adjust this line on the assembled mock-up. Besides, I have cut the central front piece with a central seam and corresponding seam allowances. It will make the shaping process easier.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image004.jpg" alt=""></p>
<p><strong>I join the pieces and put the mock-up on the dress-form.</strong></p>
<p><strong>I pin the top "lace" parts in place.</strong> The armscye looks way too wide and I need to form a dart at the back (I will take this into account when building the next paper pattern).</p>
<p><strong>The most important task right now is to draw beautiful modelling lines on the mock-up.</strong> When doing this, I take a few steps back every now and again to examine those lines from a distance. I will make them smoother later, on a flat surface.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image010.jpg" alt=""></p>
<p><strong>The modelling lines at the bottom of the dress are all parallel but I feel like adding a small flare there.</strong> I will set the pieces apart on the paper pattern to create a flare on the bottom lace piece according to all rules and make the skirt a little wider. It will look gorgeous!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image012.jpg" alt=""></p>
<p><strong>The train will start at the intersection of the modelling line and the central seam. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image014.jpg" alt=""></strong></p>
<p><strong>Once you are fully happy with the fit of the mock-up, </strong>simply cut it along the marked modelling lines, transfer the pieces on paper, and apply all final adjustments. Afterwards, you will need to cut the new pieces from mock-up fabric, assemble another mock-up, put it on the dress-form, and analyze the results. Do not cut the pieces from the actual fabric before you have done all this.</p>
<p><strong>I take the mock-up off the dress-form and even out the cutting lines.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image016.jpg" alt=""></p>
<p><strong>I also rip up the central seam at the front of the mock-up to separate the two halves.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image018.jpg" alt=""></p>
<p><strong>I start working on the top part of the bodice.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image020.jpg" alt=""></p>
<p><strong>I cut the dart as marked, put the piece on the corresponding paper pattern, and transfer the dart onto it.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image026.jpg" alt=""></p>
<p><strong>Then I cut up the transferred dart and glue the edges back together.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image032.jpg" alt=""></strong></p>
<p><strong>This top piece can be cut without shoulder seams.</strong> It is particularly handy when you want to preserve beautiful motifs on your fabric or when you simply don't want any additional seams on the shoulders. However, you can only cut it without shoulder seams if you know that your pattern is 100% true.</p>
<p><strong>I mark 2.4cm seam allowance on the top front piece, glue it together with the top back piece, and even out the edge of the front piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image038.jpg" alt=""></p>
<p><strong>Here is a ready pattern of a single top piece for the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%206_files/image040.jpg" alt=""></p>

