<h1>Tutorial 18. Sewing Bones onto the Cups. Pressing the Lining.</h1>
<p><strong>It is time to sew bones onto the cups.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image002.jpg" alt=""></p>
<p><strong>It is important to make a row of stay-stitches going along the other side of the shoulder strap and onto the armscye to mark a border for the bones. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image010.jpg" alt=""></strong></p>
<p><strong>The cups will be reinforced with narrow Rigilene bones. </strong></p>
<p><strong>First I lay bones along the perimeter to define the boundaries of the cup.</strong></p>
<p><strong>I gather the bones in advance giving them a semi-circle shape.</strong> This is done by pulling the fishing lines out of the bone and pulling on one of them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image012.jpg" alt=""></p>
<p><strong>The top perimeter bone goes along the princess seam.</strong> I tape the end of the gathered bone with a piece of masking tape and sew it on with a single row of stitches along the outer side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image020.jpg" alt=""></p>
<p><strong>I sew the top perimeter bone onto the other cup. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image024.jpg" alt=""></p>
<p><strong>And I add the bottom perimeter bone which passes under the breast.</strong> I ease in the fabric just like you always do on cups with bones. I push the end of the bone underneath the top perimeter bone which is attached from only one side. The stitches are made along the outer side of the bone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image028.jpg" alt=""></p>
<p><strong>Next, I sew the inner bones on the cups. Each bone is secured with two rows of parallel stitches and its ends are pushed underneath the perimeter bones.</strong> I gather the bones in advance so I can always stretch them out if the garment shrinks during pressing.</p>
<p><strong>I sew a bone along the bust line (marked as bone 1).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image032.jpg" alt=""></strong></p>
<p><strong>I make another row of parallel stitches.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image034.jpg" alt=""></strong></p>
<p><strong>I sew a bone below the bust line (marked as bone 2).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image038.jpg" alt=""></p>
<p><strong>I sew a vertical bone (bone 3).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image042.jpg" alt=""></strong></p>
<p><strong>And I secure the perimeter bones of the cup with the second row of parallel stitches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image050.jpg" alt=""></p>
<p><strong>Sewing bones over curved seams is not the best idea:</strong> the bone settles edgeways and makes the cup look square. I will have to put the cup on a cup pressing mould and work on its shape to make it round. I will also cover the cup with a layer of padding polyester, which will help even out this area. The cups will still push the breasts up and in very well and you can always put push-up pads inside them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image054.jpg" alt=""></p>
<p><strong>I add the bottom perimeter bone which passes under the breast.</strong> I ease in the fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image060.jpg" alt=""></p>
<p><strong>I sew a bone below the bust line (marked as bone 2).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2018_files/image068.jpg" alt=""></p>
