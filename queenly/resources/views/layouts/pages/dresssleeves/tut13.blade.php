<h1>Tutorial 12. Review of Materials Used. Analyzing the Dress Model before Cutting.</h1>
<p><strong>Before cutting the dress, I would like to talk about the fabrics I am going to use for it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image002.jpg" alt=""></p>
<p><strong>The white face parts of the dress will be made of Armani fabric.</strong> It hardly stretches crosswise and is absolutely non-stretch lengthwise.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image006.jpg" alt=""></p>
<p>This fabric has a sheen face side and a matte reverse side which can also be easily used as the face.</p>
<p><strong>I will use the matte side of the main Armani fabric as the face because I believe it will look more harmonious combined with the chiffon</strong> overlay on the train and on the cups.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image010.jpg" alt=""></p>
<p><strong>Our dress features lace insets set against a nude background, which creates an illusion of bare skin, as well as lace sleeves and lace insets on the shoulders and at the back which will be in direct contact with the skin. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image016.jpg" alt=""></p>
<p><strong>I will use thin lace that lightly stretches crosswise. </strong>This kind of lace looks very good in wear.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image018.jpg" alt=""></p>
<p>I will use <strong>beige Armani fabric</strong> as a background for the lace in those areas where I want to create an illusion of bare skin. It possesses the same quality as the white fabric: hardly stretches crosswise and does not stretch lengthwise.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image020.jpg" alt=""></p>
<p><strong>Again, I will use the matte side as the face.</strong> It will match the sleeves perfectly when covered by lace. The insets on the dress will create an illusion of bare skin, which is exactly what I want.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image024.jpg" alt=""></p>
<p><strong>The lining will be made from white stretch satin</strong> which stretches only crosswise. It doesn't matter whether you choose the sheen or the matte side as the face of the lining. It all depends on your own and your client's preferences.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image026.jpg" alt=""></p>
<p>I will use individual matching motifs from the remaining lace fabric to decorate the joining seams.</p>
<p><strong>Another material is nude mesh fabric</strong> which is absolutely unnoticeable in wear. Even its edges blend in with the color of the skin, i.e. you can't see where the mesh fabric ends or begins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image028.jpg" alt=""></p>
<p><strong>But let me explain why I will use this mesh fabric in the first place.</strong> The dress features a very low front neckline, as well as a rather low back neckline. The shoulder straps hold only thanks to the hook-and-eye fastener at the top. And this means there is no proper support to the cups. The dress also features sleeves and the cups may start shifting around when the wearer starts moving her arms. I have decided to prevent it by cutting one layer of mesh fabric and putting it underneath the entire chest area to secure the low front neckline. Of course, you could solve the task without using mesh fabric: for example, by sewing bridges between the cups or making the neckline not so low. But all this would affect the style of the dress and I want to make it the same as on the sketch.</p>
<p><strong>Now I need to decide on the placement of bones and see what pieces need to be duplicated with iron-on batiste. </strong></p>
<p><strong>If I duplicate the central and the side front piece of the skirt</strong> made from the white Armani fabric down to the hip line, there will be an obvious white border showing through the face in the area where the batiste stops. I can safely duplicate <strong>the beige parts of the front and the cups</strong> though. First of all, you won't be able to see where the batiste ends because of the nude color. And besides, the pieces will feature a lace overlay. I will also duplicate the entire <strong>central back piece made from the white Armani fabric.</strong> In this case, there will be no visible border because the color of the duplicated and the non-duplicated back pieces will stay the same.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image030.jpg" alt=""></p>
<p><strong>I have picked out all face and lining pieces that need to be duplicated and prepared corresponding pieces of iron-on batiste.</strong></p>
<p><strong>The central back piece.</strong> It will be cut from the white Armani fabric and fully duplicated with batiste.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image032.jpg" alt=""></p>
<p><strong>The side back piece.</strong> It will be cut from the beige Armani fabric and fully duplicated.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image034.jpg" alt=""></p>
<p><strong>The cup pieces.</strong> They will be cut from the beige Armani fabric, overlaid with white chiffon, and fully duplicated with iron-on batiste.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image036.jpg" alt=""></p>
<p><strong>The central front piece.</strong> It will be cut from the beige Armani fabric, overlaid with lace, and fully duplicated.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image038.jpg" alt=""></p>
<p><strong>The side front piece.</strong> It will be cut from the beige Armani fabric and overlaid with lace. It is enough to duplicated it down to the hip line. Please note that I will first duplicate the central front piece and the side front piece, then join them, and only then overlay them with one seamless piece of lace.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image040.jpg" alt=""></p>
<p><strong>I will sew bones on the cups just the way I do it on any regular corset </strong>save for certain nuances related to the configuration of the cup pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2012_files/image042.jpg" alt=""></p>

