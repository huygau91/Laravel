<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image122.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image124.jpg" alt=""></strong></p>
<p><strong>I join the seam allowances of the lining and the train on the other half of the back. </strong>Again, remember to check how the lining hangs down.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image126.jpg" alt=""></p>
