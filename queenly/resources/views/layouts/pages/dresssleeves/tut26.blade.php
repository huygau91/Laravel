<p><strong>I fold the lining over the top edge of the back to check whether the two corners are symmetric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image080.jpg" alt=""></p>
<p><strong>It's time to finish the seam allowances of the zipper with the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image082.jpg" alt=""></p>
<p><strong>As usual, I turn the back out through the open side edge and put the seam allowances of the lining over the seam allowances of the zipper aligning the notches </strong>to ensure their correct position against one another.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image084.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image086.jpg" alt=""></p>
<p><strong>I pin the seam allowances together from the beginning to the end of the zipper and sew along the seam which holds the zipper tape.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image088.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image090.jpg" alt=""></p>
<p><strong>I trim down the remaining seam allowance of the joined face and lining and trim its end at an angle.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image092.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image094.jpg" alt=""></strong></p>
<p><strong>And I turn out the first finished half of the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image096.jpg" alt=""></p>
<p><strong>And I use the seam allowances of the lining to finish the zipper area on the other half of the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image098.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image100.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image102.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image104.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image106.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image108.jpg" alt=""></p>
<p><strong>It looks neat and pretty.</strong> I usually add a hook-and-eye fastener at the top to protect the zipper from coming undone and to make the zipping up easier.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image110.jpg" alt=""></p>
<p><strong>The zipper looks very clean from the side of the lining, too. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image112.jpg" alt=""></p>
<p><strong>Now I just need to sew the central seam on the lining of the back. </strong>I will leave two sections undone: 5-6cm below the end of the zipper and 2cm above the very beginning of the train. This will allow for some freedom of movement for the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image114.jpg" alt=""></p>
<p><strong>I will finish the seam allowances of the train with the lining just like I did in the zipper area.</strong> I turn out the back, put the seam allowances of the lining and the train face to face, align the edges, pin them together, and join them with a 2cm seam allowance starting at the bottom and stopping 4-5cm below the point where the train splits in two halves.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image116.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image118.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2025_files/image120.jpg" alt=""></p>
<p><strong>I turn out the back and hold it hanging down to make sure the lining hangs freely along the full length without pulling on the main fabric anywhere. </strong>It is very bad if the lining does start pulling on the dress somewhere (it means there is not enough lining fabric) because it will distort the entire back of the dress. You can direct the seam allowance to the desired side and topstitch the seam that joins the lining with the train.</p>
