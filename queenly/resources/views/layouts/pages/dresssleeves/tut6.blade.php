<h1>Tutorial 5. Building the Lace Top Part of the Front.</h1>
<p><strong>I will build the lace top part of the front half of the dress using the central front piece of the shoulder seam pattern.</strong> It will look like a triangle resting upon the shoulder and one side of it will be part of the armscye.</p>
<p><strong>Before building this lace piece, I would like to share some important know-how that concerns the lace top part of the back which I have built just before.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image002.jpg" alt=""></p>
<p><strong>Suppose you don't want any lace insets on your dress but prefer having two shoulder straps and a plain straight edge at the back (like on a singlet).</strong> In this case, you should calculate all relevant adjustments for the shoulder seam part of the back, like I did before, and then adjust its shape to your liking. I have seen some very beautiful dresses with a low front neckline, thin shoulder straps, and nearly fully covered back.</p>
<p><strong>Let us return to the lace part of the front. </strong>I mark the waistline, the bust line, and the seam allowances.</p>
<p><strong>According to the calculations, I need to remove 0.5cm from the front shoulder width measurement (point A).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image004.jpg" alt=""></strong></p>
<p><strong>My client's neck base to bust point measurement is </strong>23cm. I add a seam allowance of 1.2cm and draw an arc with a radius of 24.2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image006.jpg" alt=""></p>
<p><strong>My client's shoulder tip to bust point measurement is </strong>21cm. I add a seam allowance of 1.2cm and draw an arc with a radius of 22.2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image008.jpg" alt=""></p>
<p><strong>In order to preserve the front shoulder width measurement, I draw a vertical line through point A to the intersection with the arc made based on the shoulder tip to bust point measurement (point B).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>I draw an arc with a radius equal to the shoulder measurement (12.5cm) from point B to the intersection with the arc built based on the neck base to bust point measurement (point C)</strong>. This is the end of the shoulder.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image012.jpg" alt=""></p>
<p><strong>I connect points B and C and get a new shoulder line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image014.jpg" alt=""></strong></p>
<p><strong>I need to prolong the armscye line before finalizing the shape of the piece.</strong> I take the central front piece, align it with the new shoulder line, and trace the armscye bringing it to the princess seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>And then I draw a pretty lace element for the front of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>Here is how it will join the side front piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image022.jpg" alt=""></p>
<p><strong>Next I need to cut out all pieces and sew a mock-up.</strong> Moreover, I insist that you should also cut the top lace pieces. Why not test them on your mock-up? You can always pin or sew them to it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image024.jpg" alt=""></p>
<p><strong>If the top parts of the mock-up will fit in well with the main part, then you can safely cut the top lace pieces without shoulder seams. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%205_files/image026.jpg" alt=""></strong></p>

