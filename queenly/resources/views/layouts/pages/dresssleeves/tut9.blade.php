<h1>Tutorial 8. Creating a Re-styled Paper Pattern. Part 1.</h1>
<p><strong>What you need to transfer the pieces on paper</strong> is blank squared paper, paper patterns customized after your client's measurements, paper pattern of the side skirt piece, and the fabric patterns you got after cutting the mock-up into separate parts. It doesn't matter what piece to start from.</p>
<p><strong>As for me, I will start from the back of the skirt.</strong> I take the back skirt piece cut from the mock-up fabric and lay it down on paper. The seam allowances should face up so they don't get in the way. The central edge of the skirt has remained straight and I arrange it in a strictly straight line. I spread the piece out and secure it with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image002.jpg" alt=""></p>
<p><strong>Using the paper pattern as a template, I draw the side seam line and the bottom edge of the piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>I transfer all notches for aligning the major circumference lines and the curved areas of the princess seams.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image006.jpg" alt=""></p>
<p><strong>I remove the fabric piece, finish drawing the central back seam and the bottom edge, and add a seam allowance of 1.2cm.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image010.jpg" alt=""></strong></p>
<p><strong>The central back piece of the bodice.</strong> You can tell that it was deformed in the sewing process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image012.jpg" alt=""></p>
<p><strong>I take the initial central back piece of the bodice and secure it on paper with pins and sticky tape.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image014.jpg" alt=""></p>
<p><strong>I lay the mock-up piece over it and align their major lines. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image016.jpg" alt=""></p>
<p><strong>I trace the new line of the back princess seam and the new line for joining it with the bottom piece.</strong> I transfer all notches and control joining points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image018.jpg" alt=""></p>
<p><strong>I remove the fabric piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>And I cut the initial pattern along the new princess seam line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image024.jpg" alt=""></strong></p>
<p><strong>I transfer the princess seam line on paper. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image026.jpg" alt=""></p>
<p><strong>The new central back piece is ready.</strong> I have transferred the notches of all major circumference lines, the end of the zipper (1), and the beginning of the train (2), as well as all control joining points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image028.jpg" alt=""></p>
<p><strong>I mark a seam allowance of 1.2cm. </strong>Let me remind you that my patterns always include seam allowances. I don't like adding them on the fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image030.jpg" alt=""></p>
<p><strong>The side back piece of the bodice.</strong></p>
<p><strong>I trace the paper piece which was used for cutting the mock-up.</strong> Remember to transfer all notches and draw the major circumference lines.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image034.jpg" alt=""></p>
<p><strong>I put the mock-up fabric piece over the outline on the paper and align their top edges and side seam lines as indicated by the notches.</strong> I secure the piece in place with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image036.jpg" alt=""></p>
<p><strong>And I even out the princess seam and the bottom edge of the piece by pressing them without steam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image040.jpg" alt=""></p>
<p><strong>I trace the contour of the mock-up piece</strong> and transfer all control notches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image042.jpg" alt=""></p>
<p><strong>The new side back piece of the bodice is ready.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image044.jpg" alt=""></p>
<p><strong>I add seam allowances.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%208_files/image046.jpg" alt=""></p>

