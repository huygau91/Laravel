<h1>Tutorial 14. Duplicating the Patterns. Marking the Bones.</h1>
<p><strong>I need to make all notches and transfer the control joining points onto all layers before duplicating the pieces.</strong> Moreover, the control points must also be transferred onto all layers of those pieces which are not going to be duplicated.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image002.jpg" alt=""></p>
<p><strong>I will show you how to transfer the control points for aligning princess seams using the side back piece as an example.</strong></p>
<p><strong>I move the pins from the perimeter of the piece closer to its center trying my best not to shift the layers against one another and place pins vertically in the control points marked on the batiste.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image004.jpg" alt=""></p>
<p><strong>And then I carefully slide the piece up on the pins and turn it around.</strong> The vertical pins have pierced all layers in the marked points.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image010.jpg" alt=""></p>
<p><strong>I carefully lift the layers one by one and mark control joining points as indicated by the pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image016.jpg" alt=""></p>
<p><strong>After transferring the control points, I need to prepare the fabric for duplication.</strong> I remove the pins from the piece and arrange the layers of fabric the following way: I put a layer of batiste adhesive side up, put two layers of beige fabric on top of it, put down another layer of batiste adhesive side down, then another layer of batiste adhesive side up, then two layers of lining fabric, and the last layer of batiste adhesive side down.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image030.jpg" alt=""></p>
<p><strong>I align the edges of all layers, secure the lay with pins, and fuse the batiste on with the iron.</strong> I press the piece with steam: first from one side and then from the other. Then I take the lay to pieces and press them with steam individually to secure the batiste.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image044.jpg" alt=""></p>
<p><strong>I transfer the control points onto the remaining pieces and duplicate them in the same manner.</strong></p>
<p><strong>Now I need to mark the placement of bones.</strong></p>
<p><strong>There will be no bones on the top half of the cup.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image046.jpg" alt=""></p>
<p><strong>The bust will only be supported in the bottom half.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image048.jpg" alt=""></p>
<p><strong>The bottom border of the round cup should start 5-6cm from the side seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image050.jpg" alt=""></p>
<p><strong>I draw an arc to mark the bottom border of the cup.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image054.jpg" alt=""></p>
<p><strong>I need to reinforce this area of the lining with iron-on fabric before sewing bones on it. </strong>You can use hard iron-on fabric normally used in corsets with bones in the cups but in this case it must repeat the shape of the arc precisely. That is why I prefer to <strong>duplicate the area of the cup limited by the arc with two layers of iron-on batiste</strong>. I try to stay within the borders of the cup but 100% accuracy is not compulsory here.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image060.jpg" alt=""></p>
<p><strong>I trim away all excess iron-on fabric and press the cup from the other side.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image064.jpg" alt=""></p>
<p><strong>The cup pieces are now elastic but rather soft and at the same time dense enough to hold the bones.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image066.jpg" alt=""></strong></p>
<p><strong>I always mark the placement of bones first on the cups and then on the rest of the garment.</strong></p>
<p><strong>As I have said before, there will be no bones on the top half of the cup.</strong> It doesn�t need any additional support since there is a shoulder strap integrated into it. Bones would only make it too stiff and uncomfortable.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image068.jpg" alt=""></p>
<p><strong>As for the bottom half of the cup, it does make sense to duplicate it and sew bones onto it.</strong> It is the part that supports the bust. <strong>For clients with small breasts</strong> you simply insert push-up pads in the bottom halves of the cups to push the breasts up from the bottom and in from the sides and create an appealing look. There is no need in the push-up effect <strong>if your client has large full breasts</strong>, but it is still the bottom haves of the cups with bones that will support the bust<strong>. If your client has large but sagging breasts</strong> that have lost their shape and cannot fill the cups in a natural way, then it also makes sense to use push-up pads to push them up and in and create an illusion of full firm breasts. At the same time, breasts like this need round cups lined with bones for proper support.</p>
<p><strong>I mark the placement of bones on the bottom half of the cup.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image070.jpg" alt=""></strong></p>
<p>I draw <strong>line 1</strong> for laying a bone along the bust line.</p>
<p>Then I go along the princess seam line and stop 3.5cm below the bust line (point E). I divide the A-D arc in about a half (point B) and mark <strong>line 2</strong> along the arc for placing another bone.</p>
<p>I divide the B-D arc and the F-H arc in about a half (points C and G) and draw <strong>line 3</strong> for the next bone.</p>
<p><strong>And then I mark symmetric bones on the other cup.</strong></p>
<p><strong>I will lay vertical bones in the middle of the side back piece and the central back piece (lines 5 and 6). </strong>Their length is limited to the length of the pieces<strong>.</strong></p>
<p>Since the princess seams of the side back piece and the central back piece have absolutely different configurations, I won't place any bones there. There is also no need in a concealed lacing because the straps will provide the necessary support for the back of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image072.jpg" alt=""></p>
<p><strong>I mark symmetric bones on the other half of the back (lines 7 and 8).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image074.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image076.jpg" alt=""></p>
<p>I will place two vertical bones <strong>(9 and 10) on the central front piece: </strong>3.5cm left and right from the centre. There will also be bones <strong>11 and 12 </strong>along the princess seams that join the central front piece and the side front pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image078.jpg" alt=""></p>
<p><strong>Two vertical bones on the side front piece will run perpendicular to the waistline (13 and 14).</strong></p>
<p><strong>Bone 13</strong> will be placed about 3cm from the seam allowance of the princess seam and stop 14cm below the waistline.</p>
<p><strong>Bone 14 </strong>will be placed 4cm from bone 13 and stop 13cm below the waistline. Please remember that there should be at least 2cm between this bone and the seam allowance of the side seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image080.jpg" alt=""></p>
<p><strong>I mark symmetric bones on the other side front piece (15 and 16).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2014_files/image082.jpg" alt=""></p>
<p><strong>All bones are marked now.</strong> I might make some adjustments in the sewing process.</p>

