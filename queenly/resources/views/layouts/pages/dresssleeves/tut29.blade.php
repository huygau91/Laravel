<h1>Tutorial 28. Sewing the Side Seams.</h1>
<p><strong>I carefully press the front half of the dress </strong>starting from the corner of the neckline. I dab it with a cushion to cool it down and secure it in the right position.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image004.jpg" alt=""></p>
<p><strong>Then I press the shoulder straps. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image008.jpg" alt=""></p>
<p><strong>And I press the cups on a cup pressing mould.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image016.jpg" alt=""></p>
<p><strong>Next I will sew the face and the lining together along the sides.</strong></p>
<p><strong>The first thing you need to do is carefully pin them together along the sides. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image018.jpg" alt=""></p>
<p><strong>If the side seams are open, I usually sew them down to the hip line.</strong> The face of the dress / skirt is usually separated from the lining below the hip line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image020.jpg" alt=""></p>
<p>But on our dress, both the face and the lining of the front are white. And the fabric is rather thin, too. If you separate the face from the lining on a curve-hugging dress like this, their seam allowances will shift against one another <strong>and there will be two seams showing through the fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image022.jpg" alt=""></p>
<p><strong>Therefore, I will separate the side seams of the face and the lining starting from the flare on the skirt.</strong> I can safely do it because the skirt flows down freely from that area and the seam allowances won't stand out.</p>
<p><strong>Before sewing the face to the lining, I pin them together along the sides from the top point to the point 55cm below the beginning of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image024.jpg" alt=""></p>
<p><strong>And I do the same along the opposite side of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image026.jpg" alt=""></p>
<p><strong>Now I can make a row of joining stitches and serge or heat-seal the edges.</strong></p>
<p>The face and the lining of the back are already joined along the sides but only to the level of the hip line, which is not enough. <strong>I will prolong the seams at the sides of the back down to the point 55cm below the beginning of the skirt. I secure these areas with pins in advance.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image032.jpg" alt=""></strong></p>
<p><strong>I have stitched along the pinned areas at the sides of the front and the back half of the dress and serged the edges.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image036.jpg" alt=""></strong></p>
<p><strong>Now I carefully put the back and the front together and secure them with pins.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image038.jpg" alt=""></p>
<p><strong>Needless to say, the side seams must be aligned perfectly in the areas where the lace stops and the white fabric begins.</strong> In order to achieve this, I step 2cm from the edge (the width of the seam allowance) and stick a pin right into the border between the white and the nude and then do the same on the other piece. I try to stick the pin in vertically.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image040.jpg" alt=""></p>
<p><strong>I pin the pieces together.</strong> Don't forget that I have already finished the top edge of the back so it is 2cm shorter than the front half of the dress (the width of the seam allowance). The top edge still needs to be finished on the latter.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image046.jpg" alt=""></p>
<p><strong>I pin the pieces together along the other side seam line.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image052.jpg" alt=""></p>
<p><strong>And I sew the first side seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image058.jpg" alt=""></p>
<p><strong>I remove the pins and check the quality of the seam.</strong> I examine the top of the dress and the border between the nude and the white fabric at the beginning of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image060.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image062.jpg" alt=""></p>
<p><strong>I need to join the layers at the bottom of the dress individually.</strong> They are four layers of fabric which have not been sewn together: two face layers and two lining layers.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2028_files/image064.jpg" alt=""></p>
<p><strong>I fold the two lining layers upwards to move them out of the way. Left in my hands are the two face layers, put together face to face. </strong></p>
