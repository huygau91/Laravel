<p><strong>Now I need to press the garment and carry out a fitting. </strong>The fitting will help me determine the final length of the shoulder strap and of the part of the lace piece that passes from the back onto the front of the dress. I need to decide at what level these pieces should be joined.</p>
<p><strong>Ideally, this should be the first fitting and the first time your client sees the dress.</strong> Of course, you can carry out earlier fittings if you don�t have enough experience. It makes sense, for example, to do it at the stage when you work on the lining so you can determine the final back length, the depth of the neckline, adjust the fit, etc. As for me, I am not a fan of such early fittings. Even when I was a newbie in corset-making, I would invite my clients for a fitting when the dress was as much as finished. It is not an easy task to reason with a client when she is dressed in a lining with a whole stockade of bones and other elements of the inner corset construction on it. Many women simply panic and it is very hard to convince them that the finished garment will look amazing and none of those things will be visible. The client is not supposed to have the slightest clue about the tricks we use to make her figure graceful and attractive!</p>
<p><strong>And that is why I prefer to offer her a nearly finished garment at the first fitting.</strong> Moreover, I try to prepare all required decoration materials (lace motifs, appliqu� elements, etc.) in advance and pin them on the dress right during the fitting so she can see what beauty is waiting ahead!</p>

