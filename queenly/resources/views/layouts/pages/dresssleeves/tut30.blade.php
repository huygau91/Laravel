<h1>Tutorial 29. Cutting the Sleeves.</h1>
<p><strong>The fitting has shown that the dress fits my client like a glove!</strong> I don't need to make any adjustments! Not a single millimeter needs to be changed!</p>
<p><strong>I have double-checked the position of the lace inset right on the client, pinned the shoulder strap in its correct place, and marked the level of the closure at the back.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image004.jpg" alt=""></strong></p>
<p><strong>Before cutting the sleeves, let me draw your attention to a certain problem related to the side seam of the dress.</strong> The technique for sewing the side seam was an either-or choice for me.</p>
<p><strong>I chose the traditional method of sewing open side seams, which has happened to have a significant disadvantage on this particular dress model:</strong> when the dress has a low back neckline with a noticeable incline from the front half of the dress, the top corner of the seam allowance of the side seam shows through the thin lace at the top of the dress, which really does not look good! It would have made no difference if the top part of the dress was opaque but I have added see-through lace insets and you can see the corner of the seam allowance "in all its beauty."</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image006.jpg" alt=""></p>
<p><strong>What can be done in this case?</strong></p>
<p><strong>I could have chosen a different method of sewing the side seam: </strong>namely, sew the front pieces in-between the lining and the face of the back. Or the other way round: sew the back pieces in-between the lining and the face of the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image008.jpg" alt=""></p>
<p>This method works very well on low-back dresses. <strong>Its major disadvantage</strong> is the thickness of the joined layers of fabric, which is what stopped me from choosing it in the first place. Side seams sewn in this technique look great on dresses made from non-duplicated delicate fabric. My dress, however, is made from rather dense duplicated fabric and lining fabric. Moreover, parts of it feature a lace overlay so the side seams would have been really coarse and thick.</p>
<p><strong>After careful consideration, I have found a simple solution:</strong> I will notch the top of the side seam and fold it onto the opaque front part of the dress. In other words, I will simply hide this corner. And I will press the rest of the side seam open as usual. Of course, there will be a raw notch on the side seam in this case but it won't affect the overall look of the inside of the dress. I will carefully mark and press it and show you the final result.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image010.jpg" alt=""></p>
<p><strong>Now let's work on the armscyes.&nbsp; </strong></p>
<p><strong>I have measured the armscye circumference of the dress including thin seam allowances of about 8mm. The result is 44cm total.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image012.jpg" alt=""></p>
<p>My client's armscye circumference (without seam allowances) is 40cm for non-elastic and 38cm for elastic fabrics. <strong>I will use the measurement of 40cm.</strong></p>
<p><strong>The finished armscye of the dress has a circumference of 44cm while it needs to be 40cm for the sleeve to hug the arm tightly. In other words, it is 4cm larger than needed.</strong> I know it for certain that the sleeve will be loose in the armscye if I cut it with a cap circumference of 44cm. And it is no good because if the wearer lifts her arm and the dress has a tight fit and cannot "catch up" with the sleeve, then the sleeve will simply tear off. In the case of knit-fabric blouses with narrow sleeves, the entire blouse stretches and follows the armscye at an arm movement. And in the case of curve-hugging corset dresses, the sleeve just tears off. In short, I need to find a compromise.</p>
<p><strong>Here are the two most popular out of many possible ways of solving this issue.</strong></p>
<p><strong>The first option is to leave a hole in the armscye area.</strong> You can see a schematic drawing of an armscye and a sleeve cap in the picture below. The red areas are supposed to be left unsewn. This option is often used on big size garments.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image014.jpg" alt=""></p>
<p><strong>And the second option (the one I will use) is to narrow the upper arm part of the sleeve.</strong></p>
<p><strong>This method has the following point.</strong> When you are building a sleeve, the key measurements are armscye circumference and upper arm circumference. These two measurements are interrelated and you can build a narrow sleeve based on this correlation if you step aside from the traditional rules of sleeve construction.</p>
<p><strong>Let us return to our particular case.</strong> The armscye circumference of the dress is larger than needed. But you can cut a sleeve with a cap circumference equal to the circumference of the resulting armscye and narrow it down straight away by bringing the upper arm circumference down to your client's measurement. You can achieve the same effect by prolonging the side seam upwards thereby artificially adjusting the armscye down to the required size.</p>
<p><strong>In other words, I will start narrowing the sleeve down to the right size literally 1-1.5cm from the armscye.</strong> It won�t be noticeable on the garment.</p>
<p><strong>The method is explained schematically in the picture below (the side seam is marked red).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image016.jpg" alt=""></p>
<p><strong>I start building the actual sleeve pattern. </strong>I use the sleeve patterns I printed out together with the patterns of the shoulder-seam dress enclosed with the book.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image018.jpg" alt=""></p>
<p><strong>I mark my client's upper arm circumference on the pattern (28cm),</strong> divide the value in half, and add 1cm seam allowance. In other words, I mark 15cm left and right of the center of the sleeve cap.</p>
<p><strong>Then I measure the sleeve cap circumference on the pattern.</strong> I remove the seam allowance and get 47cm. As we know, it is supposed to be 44 cm - same as the armscye circumference on the dress.</p>
<p><strong>I keep lowering the sleeve cap until it meets the desired length of 44cm. </strong>And I re-draw the sleeve cap and the seam allowance (red line).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image024.jpg" alt=""></p>
<p><strong>My client's arm fullness measurement (20cm) fits the measurement on the pattern quite well.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image026.jpg" alt=""></p>
<p><strong>I smoothly narrow the upper arm circumference down to my client's measurement.</strong> As I said before, it is as if the side seam of the dress gradually transformed into the sleeve and went a little further up into the armscye. I add a seam allowance.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image028.jpg" alt=""></p>
<p><strong>I cut out the new paper pattern of the sleeve cap with the seam allowance included. </strong>I fold it in half to achieve perfect symmetry.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image032.jpg" alt=""></p>
<p><strong>Now I have a new sleeve pattern with a sleeve cap that fully corresponds with the armscye of the dress and my client's measurement.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%2029_files/image034.jpg" alt=""></p>
<p><strong>Time to cut the sleeves from lace.</strong> I will cut along the selvage to make them stretch widthwise more than lengthwise.</p>
<p>I pin the paper pattern to the fabric and draw a vertical line to mark the center of the sleeve. I mark all relevant measurements taken off the client:</p>
<p>- Shoulder to wrist - 57cm.</p>
<p>- Wrist circumference - 18cm plus another 2cm for the seam allowances, which makes 10cm from either side.</p>
<p>- Shoulder to elbow - 34cm.</p>
<p>- Elbow circumference - 24cm plus another 2cm for the seam allowances, which makes 13cm from either side.</p>
<p><strong>I draw the outline of the sleeve by connecting the marked points.</strong></p>
