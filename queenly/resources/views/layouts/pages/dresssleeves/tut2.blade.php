<h1>Tutorial 1. Getting Started.</h1>
<p><strong>I start working on the pattern.</strong></p>
<p><strong>I have printed out three sets of patterns included in the book:</strong></p>
<p><strong>- Main pattern of a dress with shoulder straps.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image002.jpg" alt=""></p>
<p><strong>- Pattern of a corset dress with shoulder seams</strong> required for the closed top part of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image004.jpg" alt=""></p>
<p><strong>- Pattern of a bell-shaped skirt.</strong></p>
<p>I have only printed out the part that features the side seam (namely, pages 2, 4, 6, 8, and 9) because I only need to know the configuration of the side seam and the rest can be added in the process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image006.jpg" alt=""></p>
<p><strong>I assemble the side piece of the skirt by gluing the sheets together.</strong> Please remember that you are not supposed to trim the blank borders: simply glue the sheets together as they are.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image008.jpg" alt=""></p>
<p><strong>If you don't want to change the degree of flare and the level where it starts</strong> but prefer to leave the side seam as it is on the pattern (which is fine), then you can cut it along the side seam line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image010.jpg" alt=""></p>
<p><strong>I glue the patterns together.</strong></p>
<p><strong>The central front piece:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image012.jpg" alt=""></p>
<p><strong>The side front piece.</strong> Please pay attention to the dashed line and the words "For setting in the sleeve." It is your guideline for sewing sleeves into the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image014.jpg" alt=""></p>
<p><strong>The side back piece: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image016.jpg" alt=""></p>
<p><strong>The central back piece: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image018.jpg" alt=""></p>
<p><strong>Let us take another look at the sketch of the dress.</strong></p>
<p>The dress has fancy princess seams that pass onto the skirt (which means they will require some adjustments below the hip line), as well as lace elements going from the shoulder to the neckline both at the front and at the back. It also features set-in sleeves.</p>
<p><strong>You can use the enclosed pattern of a corset dress with shoulder seams for cutting the lace parts of the dress. Moreover, it will be enough to glue together only the top parts of the pattern.</strong></p>
<p>You can also <strong>employ the draping method or the mock-up method.</strong> For example, if you are working on a dress with see-through insets or nude mesh, then I recommend that you use a specialty dress-form described in my teaching course titled "Wedding Dresses. The Bare Skin Effect."</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image020.jpg" alt=""></p>
<p><strong>As for me, I will use the top part of the pattern of a corset dress with shoulder seams:</strong> the part that stops at the bust line or at the waistline depending on the dress model.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image022.jpg" alt=""></p>
<p><strong>Please remember </strong>that you are only supposed to use the pattern with shoulder seams for cutting the lace top part of the dress! The main part of the dress is cut after the pattern of the dress with shoulder straps!</p>
<p>If you try to align these patterns along the notches marked on the major reference lines, you will see <strong>how different they are from one another </strong>despite the fact they were based on the same measurements taken off the same client. Each pattern was designed for a particular type of a dress to ensure a perfect fit - particularly in the chest.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image024.jpg" alt=""></p>
<p><strong>So I will only use the top part of the pattern with shoulder seams for cutting the small lace insets on the shoulders of our dress.</strong></p>
<p><strong>If you feel like covering the front of the dress with lace up to the very top, </strong>you will also need to cut the lace part after the pattern with shoulder seams.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image026.jpg" alt=""></p>
<p><strong>I will also use the top parts of the central back piece and side back piece of the shoulder seam dress pattern for building a pattern of the lace part of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image030.jpg" alt=""></p>
<p><strong>In addition, the course includes a sleeve pattern made of two halves: front and back. </strong>You can easily adjust the size and the style of a sleeve made of two halves.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image032.jpg" alt=""></p>
<p>After applying changes to the central front piece, I will glue blank sheets of paper to it so I can <strong>draw the part of the skirt below the hip line </strong>in its actual size: 110cm or 120cm down from the waistline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image034.jpg" alt=""></p>
<p><strong>After customizing the side back piece, </strong>I will align the outermost point of its hip line with the outermost point of the hip line of the skirt piece to form a single side seam line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%208/transcripts/Tutorial%201_files/image036.jpg" alt=""></p>
<p><strong>I would also like to add that you can shape the skirt following the mock-up method if you like.</strong> You will need to sew a regular mock-up after customizing the pattern with regard to your client's measurements and then draw all modelling lines on the mock-up. It is not only faster and easier but also a lot more precise.</p>
<p>I will calculate pattern adjustments, analyze the changes, and then begin the shaping process. I will do on paper the part of the shaping process which is supposed to be done on paper and I will use the mock-up method for the part of the shaping process that is relevant to it.</p>

