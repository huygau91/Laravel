<h1>Thick Decoration Element.</h1>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image002.jpg" alt=""></p>
<h3>Tutorial 1. Decoration Materials. Creating the Base of the Element.</h3>
<p><strong>All required materials are shown in the picture below:</strong></p>
<p>1. Woven interfacing,</p>
<p>2. White biflex fabric,</p>
<p>3. Thin felt fabric,</p>
<p>4. Silver short bugle beads,</p>
<p>5. Ivory seed beads,</p>
<p>6. Faceted beads,</p>
<p>7. Golden long bugle beads,</p>
<p>8. Golden seed beads silver lined inside,</p>
<p>9. Rhinestones (color: crystal),</p>
<p>10. Mounted rhinestones (color: champagne),</p>
<p>11. Tex 45 polyester-linen thread,</p>
<p>12. Fusible web</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image006.jpg" alt=""></strong></p>
<p><strong>I need to make a base for the decoration element.</strong></p>
<p><strong>The first layer is 1.4mm thick felt fabric.</strong> I have cut a sheet of felt fabric in half lengthwise and sewn the halves together with zigzag stitches so as to fit a long appliqu� element.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image008.jpg" alt=""></p>
<p><strong>I cover the felt fabric with a layer of woven interfacing:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image010.jpg" alt=""></p>
<p><strong>Why have I chosen medium-weight woven interfacing? </strong>Light-weight woven interfacing won't hold its shape well enough whereas the appliqu� is supposed to be stiff.&nbsp; Heavy-weight woven interfacing, on the other hand, is hard to pierce through with a needle which will make it hard to embroider the piece.</p>
<p><strong>I fuse the layers together with the help of the press iron:</strong> first I only let them catch and then fuse them together properly. I check if the layers hold together well enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image012.jpg" alt=""></p>
<p>And then I put fusible web over the area where I want to place an appliqu�. Paperless fusible web is the best choice for this purpose. I highly recommend using exactly this type of fusible web.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image014.jpg" alt=""></p>
<p><strong>I use white biflex as decorative fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image016.jpg" alt=""></p>
<p><strong>I iron the lay through a piece of thin paper</strong> to make sure the biflex fabric does not get damaged when being fused with the woven interfacing.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/tutorial%201.%20Thick%20Decoration%20Elements_files/image018.jpg" alt=""></p>
<p>All layers are fused together very neatly. There is felt fabric from one side and decorative biflex fabric from the other. <strong>I put the appliqu� in place and trace its contour with a disappearing ink marker.</strong></p>
<p>I have transferred its outline onto the. I will transfer the inner contours later, after cutting out the appliqu� and sewing zigzag stitches along its perimeter. I am not doing it now because lines drawn with a disappearing ink marker will be gone before I can embroider them.</p>
<p><strong>I cut out the appliqu�.</strong></p>
