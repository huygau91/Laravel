<h1>Tutorial 2. Finishing the Edges.</h1>
<p><strong>I serge the edges of the cut-out appliqu� with a zigzag stitch.</strong> I set the sewing machine for wide and closely spaced zigzag stitches. Sometimes fabric gets stuck in the sewing machine, so I push it forward with one hand and spread it out with the other.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image002.jpg" alt=""></p>
<p>The edge is supposed to be fully serged. I pass along the corners in the following manner:&nbsp; I reach the end and then turn the appliqu� around and stitch along the same corner again but at a different angle.&nbsp; Sometimes the needle walks over the fabric, other times it walks right along the edge, and still other times it goes off the edge (i.e. does not catch the fabric).</p>
<p>I move along the entire perimeter of the appliqu� in this manner.</p>
<p><strong>Next I need to finish the edges of the cut-outs inside the appliqu�.</strong></p>
<p><strong>I place the needle at a small distance from the beginning of the arc to stitch over the corner from one side. </strong>After going along the perimeter of the cut-out, I will arrive at the same point from the other side and stitch over the corner again to secure it. I will do the same thing with all corners.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>I finish the edge from one side of the cut-out. </strong>My zigzag stitch is currently set at 4mm: the needle catches about 3mm of fabric and goes 1mm beyond the edge of the motif.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>I pass through the corner and stitch a couple of millimeters more:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>I turn the appliqu� around.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image010.jpg" alt=""></strong></p>
<p><strong>I stitch over the corner again, this time moving in the other direction,</strong> and then stitch along the other side of the cut-out and over the third corner, and stitch a couple millimeters more.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image012.jpg" alt=""></p>
<p><strong>And I turn it around again:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image014.jpg" alt=""></strong></p>
<p><strong>I stitch over the corner again, moving in the other direction,</strong> and then stitch along the third side of the cut-out and over the first corner again, and return to the starting point.</p>
<p><strong>The cut-out looks very neat now:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image016.jpg" alt=""></strong></p>
<p><strong>I finish the edges of smaller cut-out areas the same way:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image018.jpg" alt=""></strong></p>
<p><strong>Now the edges of my appliqu� are all finished.</strong> I only need to transfer the contours of the part I want to embroider.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%202_files/image020.jpg" alt=""></p>

