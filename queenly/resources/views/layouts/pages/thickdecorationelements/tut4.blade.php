<h3>Tutorial 4. Embroidery Techniques.</h3>
<p><strong>I start embroidering the fragment. </strong>&nbsp;The needle comes out right at the edge and goes in 2mm below it to leave some room for the next stitch.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image002.jpg" alt=""></p>
<p><strong>I will use seven seed beads in every stitch</strong> because my seed beads are very small. The stitch is a little shorter than one centimeter.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>I bring the needle down 2mm below the edge. Here is what the stitch looks like: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image006.jpg" alt=""></p>
<p><strong>I bring the needle back up at about 1/3 of the previous stitch: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image008.jpg" alt=""></p>
<p><strong>I continue embroidering the braid</strong> always bringing the needle up from the outer side of the previous stitch and then down 2mm below the edge of the motif to leave some room for the next stitch. I bring it out at 1/3 of the previous stitch. I bring it out at 1/3 and not at 1/2 to make the row look thicker.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image010.jpg" alt=""></p>
<p><strong>I continue embroidering the contour of the element. It doesn't take long.</strong> My task is to conceal the edge entirely.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image012.jpg" alt=""></p>
<p><strong>I have already finished embroidering a small fragment</strong> and you can now see the concept. I will explain the step-by-step of working on all individual elements later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image014.jpg" alt=""></p>
<p><strong>The next stitch is made of a seed bead, a pearl bead, and another seed bead.</strong> I make these stitches one after another next to the seed bead contour of the piece. You can use the same stitch for embroidering ï¿½stairs". Pearl beads always add elegance to the garment and stand out really well among other kinds of embroidery materials.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image016.jpg" alt=""></p>
<p><strong>Next I fill in the remaining empty space with a row of stem stitches with short bugle beads: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image018.jpg" alt=""></p>
<p>As I have demonstrated before, you can control the width of the row which allows you to <strong>make the embroidery neat and smooth.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image020.jpg" alt=""></p>
<p><strong>The next stitch is made of a seed bead, a long bugle bead, and another seed bead.</strong> I make one stitch after another. You protect the thread from getting damaged by placing seed beads at the ends of twisted long bugle beads.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image022.jpg" alt=""></p>
<p><strong>The next stitch is made of a faceted bead and a long bugle bead.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image024.jpg" alt=""></strong></p>
<p>I sew a row of stem stitches. It will add diversity to the overall composition. Faceted beads are iridescent and match the shine of the Japanese bugle beads. All stitches are made identically: the needle always comes out next to the middle of the previous stitch, apart from the very last one. What concerns the last stitch, it is made of a single faceted bead because there is not enough room for a bugle bead. It's the end of the row.</p>
<p><strong>The next row is made of stem stitches with AB seed beads.</strong> In this case your main task is to keep the same number of beads in each stitch and always bring the needle out at the same level. Only then will the row look even and neat.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image026.jpg" alt=""></p>
<p>When embroidering motifs right on the garment, you can either start from the outline and then fill in the inner areas or start from a particular part of the motif: for example, embroider the contour of a curl and then fill it in.</p>
<p><strong>A stitch made of two not very long Japanese bugle beads looks really good.</strong> It creates a particular kind of shine because these bugle beads are still a fair bit longer than regular short bugle beads and form a rather lovely ï¿½braid" with two beads in each stitch. It repeats the shape of the contour very well. Don't forget about this stitch when you want to spice up the overall composition of your embroidery.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image028.jpg" alt=""></p>
<p><strong>I embroider the next row by alternating between two kinds of stitches.</strong> The first one is made of a seed bead, a golden bugle bead, and a seed bead. And the second stitch is made of six seed beads. I use this combination to fill in all areas that are supposed to stand out the most.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/2/Tutorial%204_files/image030.jpg" alt=""></p>

