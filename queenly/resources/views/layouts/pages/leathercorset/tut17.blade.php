<h1>Tutorial 16. Finishing the Top Edge of the Corset with Bias Tape.</h1>
<h3>Marking the Placement of Grommets.</h3>
<p><strong>I sew the same kind of a bias tape to the first assembled half of the corset.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image004.jpg" alt=""></strong></p>
<p><strong>I trim the end of the bias tape down to about 1cm.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image008.jpg" alt=""></p>
<p><strong>And now I will do the familiar trick - glue the bias tape in place.</strong></p>
<p><strong>First I smear the bias tapes on the central panel and the first half of the corset with rubber-based adhesive in the seam allowance area and below it.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image012.jpg" alt=""></strong></p>
<p><strong>I start from the bias tape on the central front panel of the corset: I spread it out and form neat corners trimming the excess of seam allowances. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image024.jpg" alt=""></p>
<p><strong>The bias tape at the top of the central front panel looks really good.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image026.jpg" alt=""></p>
<p><strong>I perform the same procedure with the bias tape at the top of the first half of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image044.jpg" alt=""></p>
<p><strong>The result looks like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image046.jpg" alt=""></p>
<p><strong>I compare the central panel with the half of the corset to make sure the bias tapes are placed at the same level.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image048.jpg" alt=""></p>
<p><strong>Waiting for the adhesive to set, I mark the placement of grommets on the assembled half of the corset for lacing the central panel to it.</strong> I step 1.5cm from the top and the bottom edge and mark the placement of grommets above and below the ceinture. There is about 9.5cm between the outermost grommets on the corset and the ceinture. I divide this distance in even sections making sure the intervals between grommets do not exceed 2.5cm. <strong>My grommets will be placed with 2.4cm intervals.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image050.jpg" alt=""></p>
<p><strong>I mark the placement of grommets accordingly along the other side of the assembled piece and transfer the marks symmetrically onto the central front panel.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image052.jpg" alt=""></p>
<p><strong>I take revolving punch pliers and punch holes as marked.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image056.jpg" alt=""></p>
<p><strong>And I do the same along the other side of the central panel.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2016_files/image058.jpg" alt=""></p>

