<h1>Decorative Wide Elastic Waistband.</h1>
<p><strong>This bonus tutorial is dedicated to sewing a wide elastic waistband with leather decor. </strong></p>
<p>And I hope you will grow even more convinced that working with leather is nothing beyond your skills.</p>
<p>You will need a clasp of any kind and decoration accessories to make this waistband.</p>
<p>Most importantly, it is a great way to use those bits of leather left after cutting other garments.</p>
<p>Creating a waistband like this is fast and easy work: it will take you, at the very most, an hour and a half whereas the result will make you happy for years. It will be a pretty and robust waistband!</p>
<p>You can use the same technique to sew waistbands from fake leather, suede cloth, and other non-fray materials. You can come up with countless designs as long as you know the step-by-steps of sewing a wide elastic waistband.</p>
<p><strong>So the idea is to make an elastic waistband, decorate it with leather, add a lovely clasp and embellish it with metal accessories. In other words, make a fancy accessory from leftover bits of leather.</strong></p>
<p><strong>First of all, I need leather to use as the background for appliqu� elements.</strong> I have also cut two pieces of gold leather and duplicated them with iron-on fabric. <strong>You will find the appliqu� template in the appendix.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image006.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image008.jpg" alt=""></p>
<p><strong>I have also prepared four pieces of black leather: two pieces the size of the template and two pieces 0.5cm larger for the lining.</strong> These pieces are not duplicated.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image012.jpg" alt=""></p>
<p><strong>I cut a strap of black leather for the clasp and duplicate it with iron-on fabric</strong> to prevent it from stretching out. The iron-on fabric should be about 3mm narrower than the slit in the clasp.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image014.jpg" alt=""></p>
<p><strong>I fold the edges of leather over the edges of the iron-on fabric and check if it can go through the slit.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image018.jpg" alt=""></p>
<p><strong>I smear the strap with glue and press the folded edges down. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image022.jpg" alt=""></strong></p>
<p><strong>Then I cut it in half, put each end through the slit in the clasp, and glue it closed. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image025.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image029.jpg" alt=""></p>
<p><strong>I transfer the motif from the template on the black leather pieces using a silver leather marking pen and cut it out.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image031.jpg" alt=""></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image033.jpg" alt=""></strong></p>
<p><strong>And then I glue the motifs on the gold background using a rubber-based adhesive.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image035.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image037.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image041.jpg" alt=""></p>
<p><strong>Next I need to stitch around all contours of the motif.</strong> I use a regular presser foot because a Teflon foot would partly cover up the motif and I would not be able to make as even rows of stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image043.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image045.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image047.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image049.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image051.jpg" alt=""></p>
