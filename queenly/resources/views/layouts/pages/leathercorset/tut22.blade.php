<p><strong>All I need to do now is stitch both ends of the waistband and trim the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image097.jpg" alt=""></p>
