<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image055.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image057.jpg" alt=""></p>
<p><strong>The first appliqu� piece is finished. The second piece should be treated just the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image059.jpg" alt=""></p>
<p><strong>I mark the placement of metal accessories and install them. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image061.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image063.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image065.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image067.jpg" alt=""></p>
<p><strong>Now it's time to glue both ends of the clasp to the appliqu� pieces.</strong> I mark the centre of the leather strap on the clasp and the centre of the appliqu� piece. The clasp is supposed to be as close as possible to the appliqu�. I smear the pieces with glue, press them together, and wait for it to set.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image069.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image071.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image073.jpg" alt=""></p>
<p><strong>I cut a piece of elastic band 10cm shorter than the waist circumference and seal its ends with a lighter.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image075.jpg" alt=""></p>
<p><strong>I need to secure the decorated pieces with the clasp halves on the band.</strong> I smear them with glue, press them in place, and trim the edges of elastic band that stick from behind the appliqu�.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image077.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image079.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image081.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image083.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image085.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image087.jpg" alt=""></p>
<p><strong>I take the slightly larger pieces of black leather and glue them on from the back as a lining for the appliqu�.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Decorative%20Wide%20Elastic%20Waistband_files/image089.jpg" alt=""></p>
