<h1>Tutorial 1. Applying Alterations to the Pattern. Marking Defects on Leather.</h1>
<h3>Transferring the Pattern.&nbsp; Duplicating the Leather.</h3>
<p><strong>I am about to start sewing a transformer leather corset.</strong> It is an underbust corset like in the previous chapter. I will use the same patterns as for the first corset apart from the two front pieces which need a little remodeling. The central front piece will be whole and not divided into two laced together halves like it was on the first corset. I will make several versions of this central panel so I can create new looks without sewing a new corset each time. The central panel will be laced to the corset. There will be laces at the back as well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image002.jpg" alt=""></p>
<p><strong>It is enough to print out the two front patterns to adjust the model of the corset.</strong> I adjust their length after the patterns of the first corset, trim away the seam allowance of the central seam because it will be a whole piece, and leave the seam allowances at the sides.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image004.jpg" alt=""></p>
<p><strong>I cut out the central front piece (piece 1) and pin it to the intermediate front piece (piece 2) aligning their sewing lines. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image008.jpg" alt=""></p>
<p><strong>I need to mark the border of the lacing area.</strong> Usually, there is about 2.5cm between the edge of the lacing bar and the edge of the grommet. I add another 1cm to make the new intermediate front piece wider. I draw a lacing line at an angle to give the piece a tapered shape (this kind of a silhouette will make the waist appear smaller).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image010.jpg" alt=""></p>
<p><strong>I cut up the pinned together pieces following the lacing line.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image012.jpg" alt=""></p>
<p><strong>Now the front of the corset is made of two new pieces: the central piece with a fold line and the intermediate piece.</strong> All I need to do is add 1.5cm for the seam allowances and for finishing the lacing area. I will glue additional sheets of paper to the patterns to extend them because it is a lot better to cut a garment with seam allowances already accounted for.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image014.jpg" alt=""></p>
<p><strong>Remember to examine your leather for defects before cutting the corset.</strong> I will need two hides to sew a corset with several removable central panels.</p>
<p><strong>I carefully examine each hide in good lighting and circle all defects with a silver leather marking pen</strong> so I can avoid them when cutting the garment.</p>
<p><strong>Make sure to check the flesh side of the hide as well.</strong> You might often notice that some areas of the flesh side are not smooth enough. It is best not to use them. Duplication fabric will come off such areas, which may cause air bubbles to form on the face of the corset.</p>
<p>When examining your hides, note the direction of the backbone and the location of belly parts. It is very important to account for it in the cutting.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image016.jpg" alt=""></p>
<p><strong>I start cutting the garment. </strong>First I will cut one set of pieces and then cut the rest of removable central front panels. I have added seam allowances to the adjusted central and intermediate front pieces. All other pieces are the same as in the corset from the previous chapter.</p>
<p><strong>I lay the pieces down parallel to the backbone, trace them, and mark all notches.</strong> There are no ceinture notches on the central front piece because there will be no ceinture on it. It is a purely decorative piece and there is no need in additional reinforcement: I will simply duplicate it with iron-on fabric. Remember that you should try to cut leather as efficiently as possible without deviating from the direction of the backbone too much. And don't forget to cut the second set of pieces flipped horizontally for the other half of the corset.</p>
