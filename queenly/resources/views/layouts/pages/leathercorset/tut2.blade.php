<p><strong>I mark the side seam on the side pieces of the back (piece 4) to avoid confusion.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image044.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image046.jpg" alt=""></strong></p>
<p><strong>Next I duplicate the corset with iron-on batiste.</strong> It is done the same way as on the previous corset.</p>
<p><strong>I remove the excess of leather to spare the iron-on fabric.</strong> I always step about 0.5cm from the marked lines when cutting leather.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image048.jpg" alt=""></p>
<p><strong>I iron the hide.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image050.jpg" alt=""></strong></p>
<p>And then I fuse iron-on batiste over its flesh side. The batiste is lying with its lengthwise grain parallel to the backbone. I set the iron temperature to "wool."</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%201_files/image052.jpg" alt=""></p>

