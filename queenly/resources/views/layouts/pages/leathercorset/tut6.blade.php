<h2>Decorating the Central Front Panels of the Corset.</h2>
<h3>Appliqu�.</h3>
<h4>Tutorial 5. Appliqu�. Part 1.</h4>
<p><strong>One of the removable central front panels will be decorated with appliqu�. </strong></p>
<p><strong>I have designed a motif and drawn a template.</strong> You will find it in the appendix. The appliqu� will be made of a layer of black leather with a cut out motif and a layer of silver leather underneath it. I won't duplicate the top layer because I need to cut out the motif anyway.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image002.jpg" alt=""></p>
<p><strong>I put the paper template over the central piece aligning their marked centres.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image004.jpg" alt=""></p>
<p><strong>I will first secure the template on the piece using a glue stick.</strong> I will glue it to the flesh side because I don't want to damage the coating on the face. <strong>I carefully smear the template with glue and secure it on the flesh side of the central piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image006.jpg" alt=""></p>
<p><strong>I transfer the motif with a silver leather marking pen.</strong> Make sure not to skip a single line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image008.jpg" alt=""></p>
<p><strong>I have transferred the motif on the leather. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>Next I take a pair of sharp scissors and start cutting it out. </strong>First I notch the inside of the motif and then move to the contour. I cut away all silver lines.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image016.jpg" alt=""></p>
<p><strong>My black leather appliqu� is ready.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>Now I need to glue it on the silver background.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>I smear the black piece with some rubber-based adhesive.</strong> You should do it very carefully not to stain the face of the piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image022.jpg" alt=""></p>
<p><strong>I carefully put the piece down on the background.</strong> First I press it firmly at the top and down the bottom and then I spread out the entire piece and press it with a roller.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%205_files/image030.jpg" alt=""></p>
<p><strong>The pieces are glued together and now I can stitch them.</strong></p>
