<h2>Foreword.</h2>
<p><strong>This chapter explains the technique for sewing another type of leather corset: a transformer corset with removable central panels. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image002.jpg" alt=""> </strong></p>
<p><strong>The idea of making a corset like this did not come as a random thought:</strong> most corsets are based on the same construction and if you choose a base of a neutral colour - black, for example - you can transform the corset beyond recognition by using removable panels in the centre of the front. These panels can be made of fabric as well as leather.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image004.jpg" alt=""></p>
<p><strong>First of all, let me say a few words about the panels themselves and the decoration options demonstrated in this chapter.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image006.jpg" alt=""></p>
<p><strong>The first central panel is decorated with grommets and rivets of mixed sizes.</strong> It looks great on young girls.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image008.jpg" alt=""></p>
<p><strong>The second central panel is decorated with machine embroidery.</strong> I will explain how to embroider leather. Embroidery patterns included in the appendix can be used with nearly all brands of embroidery machines. All you need to do to use them is upload the file to your embroidery machine in the required resolution.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image010.jpg" alt=""></p>
<p><strong>The third central panel is decorated with leather appliqu�.</strong> I will provide you with detailed guidelines to making leather appliqu�. It's a rather tricky process which involves certain technical fine points. The same technique can be used with suede cloth, thin felt fabric, and other similar non-fray materials.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image012.jpg" alt=""></p>
<p><strong>Finally, the fourth central panel features a 3D leather appliqu� in the form of poppies.</strong> In this chapter, I will explain a rather simple yet very effective trick for making leather flowers. It allows for some truly amazing transformations. If you need more detailed information on making leather flowers, I am sure you will be able to find it online because it is a whole industry of its own.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Foreword_files/image014.jpg" alt=""></p>
<p><strong>In conclusion, I would like to highlight that the purpose of this course is to show you that working with leather is nothing hard!</strong></p>
<p>This course teaches you about different types of leather, explains how to choose leather in stores, what parts of a hide are best for what garments, how to determine the direction of lengthwise and crosswise �grain" on leather, how to cut leather garments correctly, etc.</p>
<p>After studying the book carefully, you will notice that at least a half of tools and materials used for sewing fabric corsets can be used for making leather corsets as well. And the rest are not hard to find and you are likely to already have them at home.</p>
<p>All corsets described in this book were sewn on a household sewing machine. Of course, an industrial sewing machine is more reliable and stitches better over bulky areas but you can still use a household machine if you don't have a choice.</p>
<p><strong>I am sure all your fears of working with leather will fade away after reading this book and you will see how easy it is to sew leather corsets! </strong></p>

