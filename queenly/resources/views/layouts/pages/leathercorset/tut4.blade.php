<h1>Tutorial 3. Opening the Seam Allowances of the Face.</h1>
<h3>Joining the Face with the Lining.</h3>
<p><strong>I need to open the seam allowances of the corset and secure them with glue.</strong> I notch each seam allowance along its length, smear it with rubber-based adhesive, open it, and press it with a roller.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image016.jpg" alt=""></p>
<p><strong>The first half of the corset is ready and I do the same with the second half.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image018.jpg" alt=""></strong></p>
<p><strong>Now I will assemble the two halves of the lining (each half is made of four pieces).</strong> I will use the same type of thread to make it stronger.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image030.jpg" alt=""></p>
<p><strong>Both halves of the lining are ready.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image032.jpg" alt=""></strong></p>
<p><strong>I press open the seam allowances of the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image034.jpg" alt=""></p>
<p><strong>Next I sew a ceinture on the lining with two rows of parallel stitches, aligning the top end of the ribbon with the notch.</strong> Before sewing the ceinture in place, place your iron on a high temperature and steam setting and iron it. Some ceintures tend to shrink a lot, which may make the garment way too tight in the waist.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image040.jpg" alt=""></p>
<p><strong>I do the same with the other half of the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image044.jpg" alt=""></p>
<p><strong>And I start joining the lining with the face of the corset. Let me remind you that I place the leather face of the corset 0.5cm from the edge of the lining.</strong></p>
<p><strong>I make a row of joining stitches 1.5cm from the edge of the lining.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image046.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image048.jpg" alt=""></strong></p>
<p><strong>I topstitch the seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image054.jpg" alt=""></p>
<p><strong>And I make a row of joining stitches along the opposite side of the same half of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image056.jpg" alt=""></p>
<p><strong>Again, I topstitch the seam.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image058.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image060.jpg" alt=""></strong></p>
<p><strong>And I repeat the same steps with the other half of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image068.jpg" alt=""></p>
<p><strong>The lining is now sewn to the face of the corset.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%203_files/image070.jpg" alt=""></p>

