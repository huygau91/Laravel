<h1>Tutorial 13. 3D Appliqué. Part 4.</h1>
<p><strong>The large poppy looks a lot better with five petals instead of four. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>I secure the appliqué on the central panel of the corset using polychlorpropene adhesive.</strong> Make sure to stay within the borders of the marked decoration area. And don't try to glue the entire petal to the background, or else it will look flat.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><strong>All five petals are in place now.</strong> Polychlorporpene adhesive works so well that you don't even need to sew them on additionally − it will only make them flatter anyway. But if you do decide to secure them by stitching, you should do it right now before gluing the middle in place.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/leather%20corset/gothic%20corset/transcripts/Tutorial%2013_files/image016.jpg" alt=""></p>
