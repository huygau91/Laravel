<h1>Tutorial 7. Making Scale Templates.</h1>
<p><strong>My scale templates will be made of soft-drink tin cans. I cut them as shown below. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image002.jpg" alt=""></p>
<p>I cut the largest template with an 8cm diameter from paper. And I mark all other templates right on it so I don�t have to cut seven more separately. Each new size differs from the previous by 5mm.</p>
<p><strong>I put the paper template on the metal and outline it by scratching with an awl or a seam ripper. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image008.jpg" alt=""></p>
<p><strong>I mark the diameter on the template (8cm) and carefully cut it out trying to keep the edges smooth. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image010.jpg" alt=""></strong></p>
<p><strong>The largest metal template is ready! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image011.jpg" alt=""></p>
<p><strong>I adjust the paper template down to the next size (7.5cm diameter) and cut it from metal just the same way. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image014.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image016.jpg" alt=""></strong></p>
<p><strong>Then I adjust the paper pattern down to a diameter of 7cm and do the same. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image017.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image019.jpg" alt=""></p>
<p><strong>At the end, I have eight metal templates with 8cm, 7.5cm, 7cm, 6.5cm, 6cm, 5.5cm, 5cm, and 4cm diameters. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image021.jpg" alt=""></p>
<p>I carefully press the shiny curtain fabric and put it on a glass plate.</p>
<p>I will cut the scales on the lengthwise grain because they will sparkle more nicely this way.</p>
<p><strong>I trim the edge of the fabric using a ruler and a soldering iron. This makes the fabric stick to the glass. I will align the straight edges of the scales with this even fabric edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image023.jpg" alt=""></p>
<p><strong>I put the 8cm metal template to the even horizontal edge and cut out a scale with a soldering iron. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image025.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image026.jpg" alt=""></p>
<p><strong>I continue cutting more scales until I reach the opposite end of the fabric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image032.jpg" alt=""></p>
<p><strong>I continue cutting after evening out the edge anew. </strong>I sharpen the soldering tip and wipe it with a wet tissue every now and then. I use scissors to remove small scorch marks off the scales.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image040.jpg" alt=""></p>
<p>I cannot say in advance how many scales of each size I need to cut. I will certainly count them later so I can tell the bride that her dress is decorated with, say, 4,000 hand-cut scales. Everybody knows the high value of handiwork. You can imagine how long it takes to cut so many scales and put them on the dress. This naturally boosts the price of the garment and your reputation. If I accept an order for a similar dress in the future, I will know the approximate required amount of scales or petals in advance. But, frankly speaking, cutting scales with a soldering iron or, even better, with a special pyrography tool is not overly time-consuming. I will show you how to put them on the dress in a fast and easy way.</p>
<p><strong>I have cut a fair amount of scales and sorted them out per size. Attention, there was a small change: </strong>I replaced 6.5cm scales with 4.5cm scales.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%207.files/image042.jpg" alt=""></p>

