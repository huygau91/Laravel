<p><strong>I start the fourth row (triangles). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image043.jpg" alt=""></strong></p>
<p><strong>I position the triangles in the fourth row right opposite the triangles from the second row. </strong>The fourth row is made 1cm above the previous.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image054.jpg" alt=""></p>
<p><strong>Scales in the fifth row are arranged chessboard fashion to the scales from the third row. </strong>Whenever you start or finish a row, you should remember that the peplum gradually narrows down towards the top and position the outermost scales along the central back edges at the right angle. And when determining the interval between one row and the next one, make sure the scales overlay the seam allowances of the triangles from the previous row.</p>
