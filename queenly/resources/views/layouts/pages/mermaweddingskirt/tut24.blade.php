<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image016.jpg" alt=""></strong></p>
<p><strong>This pretty edge of the peplum will settle upon the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image018.jpg" alt=""></p>
<p><strong>The second row is positioned 1cm above the first one and is made of triangles. Their down-pointing corners should be positioned in line with the tops of the scales. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image020.jpg" alt=""></p>
