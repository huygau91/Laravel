<h1>Tutorial 23. Putting Decorative Elements on the Peplum.</h1>
<p><strong>As I said before, the first row of scales will go along the bottom horizontal row of stay-stitches. </strong></p>
<p><strong>I start by putting a diagonally positioned scale in the corner to overlay the edges which are supposed to come together in the middle of the back. </strong>The ends of the straight side of the scale should fall upon the rows of stay-stitches. In this case the vertical edges of the peplum will be perfectly symmetric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image004.jpg" alt=""></p>
<p><strong>I start putting the first row of scales along the bottom row of stay-stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2023.files/image006.jpg" alt=""></p>
