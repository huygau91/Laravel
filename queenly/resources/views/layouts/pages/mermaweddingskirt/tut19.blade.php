<h1>Tutorial 18. Sewing the Side Seams.</h1>
<p><strong>I have sewn the first side seam, folded the seam allowance flat to the side, and topstitched it.</strong></p>
<p><strong>Now, moving from the bottom row upwards, I start filling all hollows with scales of corresponding sizes so as to overlay and conceal the side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image003.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image005.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image007.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image009.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image011.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image013.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image015.jpg" alt=""></p>
<p><strong>I move to the second row of 8cm scales.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image017.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image019.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image021.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image023.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image025.jpg" alt=""></p>
<p><strong>And then I add lacking 8cm scales to the third row. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image029.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image031.jpg" alt=""></p>
<p><strong>Remember that the goal is to overlay the side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image033.jpg" alt=""></p>
<p><strong>As you can see, there is nothing difficult about it. I simply fill in all hollows with the required amount of scales. </strong>I gradually advance up to the very top of the skirt changing the size of the scales depending on the row.</p>
<p><strong>Next, I sew the second side seam. </strong>I fill in the hollows and overlay the side seam just the same way, only this time it does take a little more effort.</p>
<p><strong>When all work is done, you should carefully press the skirt. You can use steam, too. </strong></p>
<p><strong>We have accomplished the key task: the skirt is finished! </strong>It looks very beautiful with this exotic texture, wonderful iridescence, and smooth transition from larger to smaller scales.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image035.jpg" alt=""></p>
<p><strong>It is really hard to notice the zipper, too. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image037.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image039.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image041.jpg" alt=""></p>
<p><strong>I am absolutely sure that this skirt is one-of-a-kind! You will never find one like that in a store! </strong></p>
<p><strong>The lining was cut after the same pattern as the face of the skirt, only without the train prolongation. </strong>It is 5cm shorter than the face at the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image043.jpg" alt=""></p>
<p><strong>Now I need to insert the lining in the skirt, face side out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image045.jpg" alt=""></p>
<p><strong>The skirt will be sewn right over the bottom edge of the bodice together with the lining. </strong></p>
<p><strong>Detailed guidelines to creating a custom dress-form can be found in �Wedding Dresses. Gown with Bare-Skin Effect" by the Corset Academy. </strong></p>
<p>First I will secure the skirt and the top part of the dress on the dress-form and then I will join them on the sewing machine and decorate the joint so as to conceal it.</p>
<p><strong>It is very important not to let any top scales get onto the seam allowance of the joining seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2018.files/image047.jpg" alt=""></p>

