<h1>Tutorial 2. Preparing for the Cutting.</h1>
<p><strong>I apply the calculated adjustments to the gored skirt pattern. </strong>It is the back half of the skirt.</p>
<p><strong>I measure 4cm down from the waistline and trim it away because the skirt is supposed to sit 4cm below the waist. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image054.jpg" alt="knub.rusal-010"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image055.jpg" alt="knub.rusal-011"></p>
<p><strong>I need to remove 1cm along the centre back line. I draw a line but I don�t cut along it yet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image056.jpg" alt="knub.rusal-012"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image007.jpg" alt="knub.rusal-013"></p>
<p><strong>I adjust the side seam line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image057.jpg" alt="knub.rusal-014"></p>
<p><strong>I will use this half-gore pattern for cutting different parts of the back of our skirt. </strong>And hence I don�t trim anything, but simply mark all relevant adjustments on the pattern so I can apply them directly on fabric. There is no need in making several nearly identical patterns.</p>
<p><strong>Let us see what adjustments I need to account for. </strong></p>
<p><strong>There will be a zipper sewn in the central back seam. </strong>There will be decorative buttons going along the centre of the back of the bodice and the zipper will be their prolongation. This means I need to provide for a 2cm seam allowance for installing and finishing the zipper.</p>
<p>And, therefore, I need to <strong>add and not remove one centimeter </strong>along the centre back line on the part of the gored skirt stretching from the central back seam to the back princess seam so as to achieve a seam allowance of 2cm. <strong>I write it right on the pattern, next to its vertical edge. </strong></p>
<p><strong>I also leave a note at the top of the pattern to keep in mind that adjustments to the shaped edge are only supposed to affect the side seam line and not the princess seam line. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image058.jpg" alt="knub.rusal-015"></strong></p>
<p><strong>Now I still need to get things clear with all length adjustments and account for seam allowances at the top and down the bottom. Seam allowances along the circumference lines have already been accounted for. </strong></p>
<p>The shaped edge of the pattern will serve both as a princess seam line, as well as a side seam line, in the cutting. For this reason, I make a note next to it saying that I need to prolong the side seam line by 15cm plus seam allowance and the princess seam line by 28cm plus seam allowance. I make another note next to the vertical edge to remember that the central back line needs to be prolonged by 40cm plus seam allowance.</p>
<p><strong>I start applying adjustments to the bell-shaped front piece. </strong></p>
<p><strong>I measure 4cm down from the waistline and trim it away because the skirt will have a bit of a low rise. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image059.jpg" alt="knub.rusal-016"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image060.jpg" alt="knub.rusal-017"></p>
<p><strong>I adjust the pattern by measuring 1cm down the centre front line and trimming it away. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image061.jpg" alt="knub.rusal-018"></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image062.jpg" alt="knub.rusal-019"></strong></p>
<p><strong>I make a note on the pattern to remember that I must account for seam allowances at the top and down the bottom, and prolong the centre front line by 10cm and the side seam line by 15cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image020.jpg" alt="knub.rusal-020"></p>
<p><strong>All key adjustments have been made now. </strong></p>
<p><strong>But, as you can see from the sketch, the top edge of the skirt is not going to be straight. </strong>There is a 7cm downward pointing V at the front of the skirt. And the top edge of the back half is an arc-shaped line with 3cm descent in the middle. This will make the dress more elegant.</p>
<p><strong>I cut a 7cm V in the top edge of the front.</strong> This basically removes the front dart off the pattern: only some 5mm is left. It makes no sense to account for it then, I will simply ease in this area when sewing the skirt to the bodice.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image063.jpg" alt="knub.rusal-021"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image064.jpg" alt="knub.rusal-022"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%202.files/image065.jpg" alt="knub.rusal-023"></p>
<p><strong>I will give the top edge of the back half an arc-shape after joining the gores. </strong></p>
