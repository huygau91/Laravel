<h1>Tutorial 11. Marking Guidelines for the First Rows of Scales on the Train.</h1>
<p><strong>It is time to transfer the scales layout from the writing board directly on the face of the skirt. </strong></p>
<p><strong>The only difference from the original layout </strong>is that the first row will go right along the bottom edge and not 2cm above it.</p>
<p><strong>I will transfer the layout separately on the back and then on the front of the skirt. </strong></p>
<p><strong>I start from the back. You can see it in the picture below, fully unfolded. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image002.jpg" alt=""></p>
<p><strong>Guideline (1) for the first row of scales goes across the entire back half of the skirt following the stitches which hold the soft Rigilene boning tape, 5mm from the edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image004.jpg" alt=""></p>
<p><strong>The half-arc of the second row (2) goes only across the central gore and crosses the centre front line 2cm from the bottom edge. </strong>The second row reaches the first row going along the very edge of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image006.jpg" alt=""></p>
<p><strong>The third row (3) </strong>starts from row (1) on the side gore, crosses the back princess seam 2cm from the bottom edge, and passes onto the central gore. Guideline (3) first goes 2cm above the bottom edge and moves 2cm higher up after reaching guideline (2). As the result, guideline (3) crosses the central back seam of the central gore 4cm from the bottom edge. After going past the central back seam, guideline (3) fully repeats its initial curve on the other side gore of the back.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image012.jpg" alt=""></p>
<p><strong>You can use French curves to achieve smoother lines. </strong>I don�t think you can make them absolutely perfect, but this will not distort the overall pattern in any way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image014.jpg" alt=""></p>
<p><strong>Guideline (4) is basically parallel to guideline (2). </strong>It also goes across the central gore at the back of the skirt and stops at row (3). The interval between row (4) and row (3) along the central back seam is 2cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image016.jpg" alt=""></p>
<p><strong>For the time being, I will make guideline (5) go across the entire back of the skirt: from side seam to side seam. </strong></p>
<p>The interval between row (5) and the previous rows is 2cm along the full length.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image019.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image021.jpg" alt=""></p>
<p><strong>Before I start putting scales along these guidelines, let us talk about the way they must be positioned at the back princess seams and at the side seams where the pieces themselves and, consequently, the guidelines form an angle.</strong></p>
<p><strong>How to position scales next to the side seam? </strong></p>
<p><strong>It is clear that you must leave a seam allowance for sewing the side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image023.jpg" alt=""></p>
<p>But you should also remember that scales from either side of the side seam are supposed to meet.</p>
<p><strong>Moreover, I will arrange the scales in rows chessboard fashion to make it even more beautiful. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image025.jpg" alt=""></p>
<p><strong>And this means that even though I leave a seam allowance for the side seam, the first scale from the next row will definitely pass onto the front half of the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image026.jpg" alt=""></strong></p>
<p><strong>For this reason, I recommend you step from the side seam edge by the width of the scale (8cm in this particular case). </strong></p>
<p><strong>I mark this distance from either side seam edge of the back half of the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image030.jpg" alt=""></strong></p>
<p><strong>The scales overlap one another in the area where their guidelines meet forming an angle. This does not look harmonious enough. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image032.jpg" alt=""></p>
<p><strong>I suggest disguising such overlaps with an additional scale positioned horizontally. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image034.jpg" alt=""></strong></p>
<p><strong>The back princess seam area will then look like this: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image036.jpg" alt=""></p>
<p><strong>I recommend you draw additional horizontal guidelines to remember about these horizontal scales. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image038.jpg" alt=""></p>
<p><strong>Summing up the above-said, I will step 8cm from the side seam edge and arrange scales in rows with about 1cm overlaps starting from guideline (1). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image040.jpg" alt=""></strong></p>
<p><strong>I will keep checking the intervals between the rows in the process. They are 2cm for now. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2011.files/image042.jpg" alt=""></strong></p>
<p><strong>So, let us put the first five rows of decorative scales on the back half of the skirt. </strong></p>

