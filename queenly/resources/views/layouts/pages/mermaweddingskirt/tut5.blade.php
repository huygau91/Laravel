<h1>Tutorial 4. Designing a Layout for Putting Decorative Elements on the Skirt.</h1>
<p><strong>We have made things clear with the cutting and now it is time to talk about decoration. </strong></p>
<p><strong>I want to cover the skirt with small scales. </strong>The mermaid skirt has its name for a reason in the end!</p>
<p><strong>The scales will be made of shiny curtain fabric. </strong></p>
<p>I want to arrange them in arc-shaped rows repeating the shape of the hem. And I also want to use scales of mixed sizes: larger at the bottom and gradually getting smaller toward the top of the skirt. The top scales will overlay the bottom ones.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%204.files/image002.jpg" alt=""></p>
<p><strong>Since the back of the skirt is made of three gores, the scales will also be arranged in three columns. </strong>I will make three vertical rows of stitches over the front of the skirt to make it symmetric with the back. And I will keep the rows of scales on the front aligned with those on the back.</p>
<p>In this case, the overall pattern will be beautiful and harmonious!</p>
<p><strong>I will cut the scales using templates. I will first cut them from paper and them from thin sheet metal. </strong></p>
<p><strong>A scale template is a plain half-circle.</strong> The largest one will have a diameter of 8cm and the smallest one − a diameter of 4cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%204.files/image004.jpg" alt=""></p>
<p><strong>The scales will gradually decrease in size as I move up from the bottom of the skirt. Each size will be different from the previous one by 5mm. </strong></p>
<p><strong>Therefore, I need eight templates with the following diameters: 8cm, 7.5cm, 7cm, 6.5cm, 6cm, 5.5cm, 5cm, and 4cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%204.files/image006.jpg" alt=""></p>
<p>You can use a pyrography tool or a regular soldering iron for cutting scale templates from sheet metal.</p>
<p>As you can imagine, this is rather tedious work.</p>
<p>But it is truly worth it! A skirt covered in these small scales will have a wonderful texture!</p>
<p><strong>It will look exclusive, luxurious, and expensive.</strong></p>
<p>At the same time, the base of the skirt will remain soft: there will be no reinforcements apart from soft Rigilene boning along the hem.</p>
<p>Before working with the scales, I will install a zipper in the central back seam and sew the rest of it to join the two central half-gores.</p>

