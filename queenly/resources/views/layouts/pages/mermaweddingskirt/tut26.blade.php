<h1>Tutorial 25. Costs of Production. Pictures of the Finished Garment.</h1>
<p></p>
<p><strong>In conclusion, I would like to present a comprehensive list of all materials which were used for my bodice with bare-skin effect and mermaid skirt with a train. </strong></p>
<p><strong>I have used: </strong></p>
<p>- 2.5m main fabric;</p>
<p>- 2m lining fabric; </p>
<p>- 2m curtain fabric (scales and triangles); </p>
<p>- 0.5m see-through mesh fabric (bodice);</p>
<p>- 0.5m lace fabric (bodice);</p>
<p>- 3m Rigilene boning; </p>
<p>- 3.5m soft Rigilene boning; </p>
<p>- 3m bias tape; </p>
<p>-300 pcs. rhinestones (size 20);</p>
<p>- 1 tube of glue; </p>
<p>- 1 zipper; </p>
<p>- 15 buttons and snaps.&nbsp; </p>
<p><strong>The total cost of all these materials was approximately 3,500 RUR (wholesale prices). </strong></p>
<p>You can calculate your expected profit bearing in mind that the cost of production must not exceed 20% from the final price of the garment.</p>
<p>A wedding dress like this can be made within 5-6 days of work at moderate pace. </p>
<p><strong>I would like to add a couple of words on how I finished the dress.</strong></p>
<p>I simply put the top edge of the skirt over the bottom of the bodice, sewed it right on, and finished the seam allowance with bias tape decorated with rhinestones. </p>
<p><strong>You can see the finished dress with the peplum in the picture below.</strong> </p>
<p>I believe it looks somewhat more formal and strict with the peplum. The bride will decide herself what part of the celebration will be appropriate for a more formal look and at what moment she can take the peplum off. </p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2025.files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>To sum it up, I want to say </strong>that I really like how this dress has come out (especially, if you take into account I had never sewn a dress like this before!). </p>
<p>This dress model has opened a wide range of new opportunities for me and, hopefully, for you, too. I am already getting lots of ideas for new wedding dresses based on this one. You could play around with the following things:</p>
<p>- train length; </p>
<p>- decorative pattern on the skirt; </p>
<p>- different shades of fabric for the decorative elements; </p>
<p>- different shapes, sizes, layouts for the decorative elements, etc. </p>
<p><strong>There are countless opportunities before you now that you know how to cut, sew, and decorate a dress like this!</strong></p>
<p><strong>It will be the greatest reward for me if you start creating your own dresses based on the know-how from my books. I wish you to achieve lots of success, always stay in a great mood, and... sew happy! </strong></p>

