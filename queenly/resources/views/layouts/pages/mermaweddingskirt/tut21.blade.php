<h1>Tutorial 20. Examining the Finished Garment in the Atelier.</h1>
<p><strong>Please take a look at the results of our work. </strong>I have put the dress on the dress-form and pinned the bodice to the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2020.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2020.files/image004.jpg" alt=""></p>
<p>The skirt shimmers beautifully and does resemble a mermaid�s tail.</p>
<p>Please note the graceful silhouette of the skirt! It is perfect because I used a tried and true pattern.</p>
<p>I will add a string of rhinestones along the top edge of the skirt.</p>
<p>A dress like this is worth being displayed in the window of any bridal salon or sewing atelier. Add proper lighting and it will definitely become an eye-catcher and attract new clients.</p>
<p>Needless to say that it is far from a budget garment. The skirt features abundant hand-made decoration and parts of the bodice imitate bare skin.</p>
<p><strong>I would like to add just a couple of words about what kind of brides such dresses are made for. </strong></p>
<p><strong>Firstly, </strong>such dresses are only recommended for women with a well-proportioned figure because the bodice has no waist restriction.</p>
<p><strong>Secondly, </strong>this is not a dress for an average bride who wants to look like the majority of brides in ready-made dresses or dresses from budget bridal salons.</p>
<p><strong>Brides who order dresses like this</strong> are demanding self-assured women who look for a one-of-a-kind garment. Most of them are girls of 24 years or older, established in their profession, who can make their living and know how to spend money wisely taking into account all pros and cons.</p>
<p>A bride like this is willing to pay good money for her wedding dress, but not before you convince her that it truly is the dress of her dream!</p>
<p>One of the arguments in favor of this dress (apart from a reasonable price) is its multifunctionality. Why? Because it can actually be two dresses in one!</p>
<p><strong>It is time to share my idea with you: why not make a removable peplum fastened by snap buttons? </strong></p>
<p>I want to emphasize it again because it is really important: this dress is only suitable for a well-proportioned woman. <strong>And a peplum is capable of enhancing the figure by highlighting the waist and making the hips appear fuller. </strong></p>
<p><strong>To cut a long story short, I suggest we make a removable peplum with snap buttons at the back.</strong> A peplum like this can be worn with any skirt and not just a mermaid skirt.</p>
<p>I will decorate the peplum with scales to match the main skirt but arrange them in a different pattern to make it stand out.</p>
<p><strong>In other words, I will show you how to turn our mermaid dress into an even more sought-after transformer dress without spending too much time and materials! </strong></p>

