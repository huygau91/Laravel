<h1>Tutorial 5. Preparing the Front and the Back of the Skirt for Decoration.</h1>
<p><strong>I have installed a zipper in the central back seam of the skirt and serged it, as well as the side seams. </strong></p>
<p><strong>The back princess seams have also been sewn, but I have not serged them yet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image002.jpg" alt=""></p>
<p><strong>As I said before, I want the skirt to have a scalloped hem. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image004.jpg" alt=""></p>
<p><strong>I start from the back half of the skirt. </strong></p>
<p><strong>I move about 15cm up the back princess seam, re-draw the bottom edge in a large wave, and cut along the new line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image010.jpg" alt=""></p>
<p><strong>Then I unfold the back half of the skirt and examine the shape of the bottom edge and the train. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image012.jpg" alt=""></p>
<p><strong>I am not very happy with the shape of the train so I make it a little rounder. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image014.jpg" alt=""></strong></p>
<p><strong>It looks good now, I believe. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image016.jpg" alt=""></strong></p>
<p><strong>I trim the top edge of the back piece so as to make it 3cm deeper in the middle. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image021.jpg" alt=""></p>
<p><strong>I need to align the front and the back along the side seam before I can adjust the bottom edge of the front. </strong>I put the two pieces together, align their side seam edges, and adjust the top edge of the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image023.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image025.jpg" alt=""></p>
<p><strong>Next, I make the bottom edge of the front round. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image027.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image029.jpg" alt=""></p>
<p><strong>This is what the rounded bottom edge of the front looks like: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image030.jpg" alt=""></p>
<p><strong>Now I need to finish the hem of the skirt with soft Rigilene boning tape.</strong></p>
<p><strong>I rip up the back princess seams by about 30cm from the bottom upwards so I can treat the bottom edge of each gore separately. </strong>I use a press-iron to give the boning tape the desired arc-shape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image032.jpg" alt=""></p>
<p><strong>The hem is now finished with Rigilene boning on both halves of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image034.jpg" alt=""></p>
<p><strong>I carefully press it </strong>starting from the back half of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image040.jpg" alt=""></p>
<p><strong>Then I press the hem of the front half of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image041.jpg" alt=""></p>
<p><strong>The boning helps the hem hold its shape very well. </strong></p>
<p>I restore the princess seams, serge them, and topstitch them toward the centre to prevent them from getting caught in the stitches and wrinkled when I start putting decorative scales on the skirt. You may also want to press open and topstitch the central back seam for the same reason.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image043.jpg" alt=""></p>
<p><strong>Now I need to mark guidelines on the front piece to arrange the scales in three columns just like at the back of the skirt. </strong></p>
<p><strong>I start by drawing a line with three scallops along the bottom edge of the front piece. </strong></p>
<p>The bottom edge is 85cm long.</p>
<p>I mark 30cm from either side seam. This leaves 25cm for the central scallop.</p>
<p>The start and end points of the central scallop are positioned 5cm above the bottom edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image045.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image047.jpg" alt=""></p>
<p><strong>I draw inclined vertical lines going through the start and end points of the central scallop. </strong>By doing so, I divide the front of the skirt in three gores. The central gore narrows down towards the top. The skirt will look very beautiful when covered with scales.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%205.files/image049.jpg" alt=""></p>
