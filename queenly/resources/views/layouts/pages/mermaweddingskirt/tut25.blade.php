<h1>Tutorial 24. Joining the Face with the Lining. Applying Rhinestones.</h1>
<p><strong>This is the face of the peplum decorated with scales and triangles: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image002.jpg" alt=""></strong></p>
<p><strong>Now I need to join the face with the lining.</strong></p>
<p><strong>You can do it the traditional way: put the pieces together face to face, join them along three sides, and turn it out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image004.jpg" alt=""></p>
<p>In this case, however, some decorative elements might get caught in the seams.</p>
<p><strong>For this very reason, I prefer another easy way of joining the face with the lining. You simply join the pieces along the edges and finish them with bias tape. </strong></p>
<p><strong>Of course, you should join and turn out the vertical edges because the decorative elements are supposed to meet neatly when the edges of the peplum come together. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image006.jpg" alt=""></p>
<p><strong>In any case, the first thing I must do is secure those fastening pieces between the face and the lining. </strong></p>
<p><strong>As I said before, the fastening pieces will be attached to the vertical bones and go past the vertical edge of the peplum by about 3cm. </strong>There will be fastening pieces with studs on them along one edge and there will be sockets along the opposite edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image007.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image008.jpg" alt=""></p>
<p><strong>I start attaching the fastening pieces to the horizontal Rigilene bones on the lining of the peplum. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image013.jpg" alt=""></p>
<p><strong>I finish the vertical edges of the peplum, topstitch the seam allowances, and turn it out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image015.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image026.jpg" alt=""></p>
<p><strong>One edge is finished. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image028.jpg" alt=""></strong></p>
<p><strong>The opposite edge needs to be finished just the same way.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image029.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image031.jpg" alt=""></strong></p>
<p><strong>Here is what the middle of the back looks like now: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image033.jpg" alt=""></p>
<p><strong>I will finish the top and bottom edges of the peplum after putting the snaps on the fastening pieces. This way all sewing threads will stay inside. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image034.jpg" alt=""></p>
<p><strong>Here is my finished peplum: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image035.jpg" alt=""></p>
<p><strong>The top and the bottom are neatly finished with bias tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image038.jpg" alt=""></p>
<p><strong>The closure at the back is also very neat, and you can hardly notice the joint. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image044.jpg" alt=""></strong></p>
<p><strong>I feel like decorating the top edge of the peplum which will hug the bride�s waist. </strong>Of course, bias tape itself looks rather pretty but you can always add a little more charm to it: for example, apply bits of the same lace used for the bodice.</p>
<p><strong>As for me, I will decorate it with two rows of rhinestones (size 20). </strong></p>
<p><strong>The technique for applying rhinestones on fabric is described in all detail in various courses by the Corset Academy </strong>including free tutorials. You can also find a plenty of information on the Internet. I recommend using special-purpose E 600 glue but you can also use a more budget-friendly version like Moment Crystal, etc. It is handy to use a syringe if your glue is liquid.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2024.files/image046.jpg" alt=""></p>
