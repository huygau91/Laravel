<h1>Tutorial 16. Adjusting the Scales Layout.</h1>
<p><strong>I mark new guidelines to deepen four new rows of scales on the front half of the skirt. </strong></p>
<p>I mark 2cm up the side seam edges and the princess seams and 1.5cm up the centre lines of the gores. And I draw new arc-shaped guidelines (1), (2), (3), and (4) going through those points.</p>
<p><strong>Looking at the layout, I feel like making these rows even deeper, so I will also add row (5). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image002.jpg" alt=""></p>
<p><strong>Five corresponding guidelines must be made on the back of the skirt as well. </strong></p>
<p><strong>I start putting 5cm scales along the new guidelines marked on the front half of the skirt. </strong></p>
<p><strong>I step from the side seam edge and put the first scale along guideline (1) chessboard fashion to the scales from the previous row. </strong>After securing the scale, I press the reverse stitch button of the sewing machine, put the next scale over the first one, and move ahead along the guideline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image010.jpg" alt=""></p>
<p><strong>I make the scales form very neat corners at the princess seams. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image014.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image016.jpg" alt=""></strong></p>
<p><strong>I continue putting rows of decorative scales between the princess seam edges. I try to position the scales in such a way so as to repeat the curve of the guideline. For convenience�s sake, every subsequent scale is put at a light angle to the previous one.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image026.jpg" alt=""></strong></p>
<p><strong>I form a neat corner at the other princess seam. The scales are supposed to meet edge to edge. Sometimes the previous scale in the row has to be almost fully overlaid in order to achieve this. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image028.jpg" alt=""></strong></p>
<p><strong>I finish row (1) at a seam allowance width from the other side seam edge. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image032.jpg" alt=""></strong></p>
<p><strong>Row (2) is basically made the same way. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image033.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image035.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image037.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image039.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image041.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image043.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image045.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image047.jpg" alt=""></strong></p>
<p><strong>After putting scales along all adjusted guidelines, I continue making regular rows at 1.5cm intervals. </strong></p>
<p><strong>However, after going literally just 10cm up the skirt, I notice that the sheen of the decorated skirt has changed. </strong>The horizontally positioned scales at the princess seams used to produce soft pearly iridescence, but this lovely effect has disappeared now that I no longer put them there, and all I can see is plain sheen. Moreover, this sheen is uneven because the scales at the princess seams are all positioned at different angles.</p>
<p><strong>It is a serious reason to change my mind. I will go back and put those horizontally positioned scale in all rows. </strong>Maybe I would not have had to do this if the scales fabric had been matte.</p>
<p><strong>What concerns the depth of the arc-shaped rows, everything looks perfect. It was a good decision! </strong>I have accentuated the princess seams, and it looks really pretty!</p>
<p><strong>I have made seven rows of 5cm scales on either half of the skirt. It has taken 180 scales and I will need another 20 for the side seams. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image049.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image051.jpg" alt=""></p>
<p><strong>Now I am going to work with 4.5cm scales.</strong></p>
<p><strong>When creating the scales layout, the dilemma was to choose the right interval for the rows of 4.5cm scales: 1.5cm or 1cm. </strong>I will use 1.5cm because 1cm means very closely spaced rows which will visually widen the wearer�s hips. You can use 1cm intervals if your scales are made of thinner fabric.</p>
<p><strong>There is, however, another challenge about putting these rows of scales on the skirt: </strong>it is he zipper in the central back seam, which starts after literally just 5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2016.files/image053.jpg" alt=""></p>
<p>If the zipper were installed in the side seam, the layout would require no adjustments. It would get overlaid by the scales very neatly.</p>
<p>But I put the zipper in the central back seam to maintain symmetry with the line of buttons at the back of the bodice.</p>
