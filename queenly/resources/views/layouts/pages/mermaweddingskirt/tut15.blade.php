<h1>Tutorial 14. Marking the First Guidelines and Putting Scales on the Front of the Skirt.</h1>
<p><strong>Let us return to the scales layout and work on the front half of the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image002.jpg" alt=""></strong></p>
<p>As you can see on the layout, rows from (7) and upwards will be aligned properly, i.e. they will be full rows going from one side seam edge to the opposite one.</p>
<p>The first thing I need to do is transfer the guidelines on the front half of the skirt.</p>
<p>The only difference from the layout on the writing board is that the bottom row will go along the very edge of the skirt.</p>
<p>You can probably remember that I made vertical rows of stitches on the front of the skirt to imitate front princess seams and fancy-shaped hem.</p>
<p>I have also drawn dashed lines for the centre front line and the centre lines of the imitated side gores.</p>
<p><strong>Row (1) goes right along the marked fancy-shaped hem. </strong></p>
<p><strong>The two half-arcs of row (2) start from the side seams, 2cm from row (1), cross the centre lines of the side gores at the same height, and smoothly blend in with row (1). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image004.jpg" alt=""></strong></p>
<p><strong>Row (3) goes across the whole front of the skirt. </strong>It starts from the side seam edge, 2cm above row (2), and continues 2cm above rows (1) and (2).</p>
<p><strong>The two half-arcs of row (4) start at the side seam edges, 2cm above row (3), cross the centre lines of the side gores 1cm from row (3), and gradually blend in with row (3). </strong></p>
<p><strong>Row (5) goes across the whole front of the skirt. </strong>It starts from the side seam edge, 2cm above row (4), and continues 2cm above rows (3) and (4).</p>
<p><strong>The two half-arcs of row (6) start at the side seam edges, 2cm above row (5), and gradually blend in with row (5) in the middle of the side gores. </strong></p>
<p><strong>Finally, row (7) is fully even and goes across the whole front of the skirt. </strong>It starts from the side seam edge, 2cm above row (6), and continues 2cm above rows (5) and (6).</p>
<p><strong>From row (7) upwards, all three imitated front gores are even and the arc-shaped rows of scales have the same depth of 5cm.</strong></p>
<p><strong>I start putting scales along the guidelines. </strong></p>
<p><strong>I put 8cm scales along guideline (1). I start 8cm from the side seam edge and go across the whole piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image008.jpg" alt=""></p>
<p><strong>I make the scales form neat corners at the imitated princess seam lines. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image011.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image013.jpg" alt=""></strong></p>
<p><strong>I finish the row a couple of centimeters before the opposite side seam edge. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image014.jpg" alt=""></strong></p>
<p><strong>I put scales along the first half of row (2).</strong> I leave a side seam allowance and arrange the scales chessboard fashion to the scales from row (1).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image018.jpg" alt=""></p>
<p><strong>I put the last scale in the first half-arc of row (2). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image020.jpg" alt=""></strong></p>
<p>I start putting <strong>scales along the second half-arc of row (2)</strong> from row (1) on the imitated side gore moving toward the opposite side seam edge. I make sure to arrange the scales chessboard fashion.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image022.jpg" alt=""></p>
<p><strong>I finish the row leaving a seam allowance for the side seam. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image024.jpg" alt=""></strong></p>
<p><strong>I overlay the corners formed by the scales overlapped at the imitated princess seams with horizontally positioned scales. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image026.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image028.jpg" alt=""></strong></p>
<p><strong>I put scales along guideline (3) which goes across the whole front similarly to guideline (1). </strong>The scales are arranged chessboard fashion. They meet at an angle at the imitated princess seams.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image032.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image036.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image038.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image040.jpg" alt=""></strong></p>
<p><strong>I finish the row leaving a seam allowance for the other side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image042.jpg" alt=""></p>
<p><strong>I start putting scales along the first half of row (4).</strong> I leave a side seam allowance and arrange the scales chessboard fashion to the scales from row (3).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2014.files/image043.jpg" alt=""></p>
