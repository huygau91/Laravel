<h1>Tutorial 13. Marking Guidelines and Putting the Rest of Scales on the Train.</h1>
<p><strong>Let us return to the scales layout on the writing board. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image002.jpg" alt=""></p>
<p><strong>I have already put scales along the first five guidelines (1 - 5).</strong></p>
<p><strong>Now I will transfer guidelines (6), (7), (8), and (9) on the fabric and put decorative scales along them. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image004.jpg" alt=""></strong></p>
<p><strong>Row (6) is nearly parallel to row (4).</strong> It also goes across the central gore and blends in with row (5) from both ends. There is 2cm between row (5) and row (6) along the central back seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image006.jpg" alt=""></p>
<p><strong>Row (7) </strong>starts from row (5) on the side gore. It crosses the centre line of the side gore and the back princess seam at 2cm from row (5). Then row (7) passes onto the central gore and goes 2cm above row (5) and then, upon reaching row (6), 2cm above row (6). After the end of row (6), row (7) continues going 2cm above row (5). It goes past the other back princess seam at the same level, passes onto the other side gore, and crosses its centre line at 2cm from row (5).</p>
<p>And then row (7) gradually blends in with row (5) fully repeating its configuration on the first side gore.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image007.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image009.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image011.jpg" alt=""></p>
<p><strong>Row (8) is basically parallel to row (6).</strong> It also goes across the central gore, blending in with row (7) from both ends.</p>
<p>There is 2cm between row (8) and row (7) along the central back seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image013.jpg" alt=""></p>
<p><strong>Row (9) is the last row for now. It goes across the entire back, from side seam to side seam. </strong></p>
<p>Row (9) stays 2cm above the previous rows along its full length.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image018.jpg" alt=""></p>
<p><strong>I move to my sewing machine table and start putting scales along the new guidelines just like I did before. </strong>I arrange the scales chessboard fashion and overlay the corners formed by the guidelines.</p>
<p><strong>I start from guideline (6) going across the central gore. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image022.jpg" alt=""></p>
<p><strong>I put the last scale in row (6). The end of the row smoothly blends in with row (5). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image026.jpg" alt=""></p>
<p><strong>I move to guideline (7). It starts from row (5) on the side gore. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image032.jpg" alt=""></strong></p>
<p><strong>I go past the back princess seam, form a neat corner, and continue sewing over the central gore. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image036.jpg" alt=""></strong></p>
<p><strong>I go past the other back princess seam and finish row (7) on the other side gore. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image038.jpg" alt=""></strong></p>
<p><strong>I only need to put down the last scale and row (7) will blend in with row (5). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image042.jpg" alt=""></strong></p>
<p><strong>I put a horizontally positioned scale over the overlapped scales at the back princess seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image048.jpg" alt=""></p>
<p><strong>I start putting scales along guideline (8) on the central gore. It starts from row (7) and stops at it, too. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image052.jpg" alt=""></p>
<p><strong>I put the last scale in row (8). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2013.files/image054.jpg" alt=""></strong></p>
<p><strong>I have approached row (9) going across the entire back of the skirt, from one side seam to the other. </strong></p>
<p><strong>I leave a side seam allowance and put the first scale chessboard fashion to the scales from row (5). </strong></p>
