<h1>Tutorial 12. Putting the First Rows of Decorative Scales on the Train.</h1>
<p><strong>I start putting scales along guideline (1) which goes across the entire back of the skirt 5mm from the bottom edge. </strong></p>
<p><strong>I step 8cm from the side seam edge and start sewing. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image004.jpg" alt=""></strong></p>
<p><strong>Each next scale overlaps with the previous one by about 1cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image010.jpg" alt=""></p>
<p><strong>Arriving at the back princess seam, I make sure the edge of the scale reaches it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image012.jpg" alt=""></p>
<p><strong>And I continue sewing. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image014.jpg" alt=""></strong></p>
<p><strong>Scales near the other back princess seam are put the same way. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image016.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image018.jpg" alt=""></strong></p>
<p><strong>I secure the last scale of row (1). </strong>There is still 1cm left before the line marking 8cm from the side seam edge, but I won�t add another scale there.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image020.jpg" alt=""></p>
<p><strong>I overlay the scales positioned at an angle next to the back princess seam with an additional horizontal scale. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image024.jpg" alt=""></p>
<p><strong>And I do the same with the scales at the other back princess seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image028.jpg" alt=""></p>
<p><strong>I move to guideline (2) which only goes across the central gore of the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image030.jpg" alt=""></p>
<p><strong>It is very important to arrange the scales in the second row chessboard fashion to the scales in the first row. </strong>I achieve the desired effect by overlapping the scales.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image032.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image036.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image038.jpg" alt=""></strong></p>
<p><strong>I successfully finish row (2).</strong> It does take some time because it is only the beginning and I need to carefully align and even out everything. The top rows will be made a lot faster.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image040.jpg" alt=""></p>
<p><strong>I start the third row.</strong> It starts from about the middle of the side gore, goes across the central gore, and stops at about the middle of the other side gore of the back.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image042.jpg" alt=""></p>
<p><strong>I stick to the same chessboard fashion and form neat corners at the princess seam edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image046.jpg" alt=""></p>
<p><strong>I have approached the back princess seam. </strong></p>
<p><strong>The next scale stops at a distance from the corner formed by the guidelines and not right at it. It is more important for me to maintain the chessboard pattern. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image048.jpg" alt=""></strong></p>
<p><strong>The next scale in row (3) starts right from the corner formed by the guidelines. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image050.jpg" alt=""></p>
<p><strong>I continue putting scales over the central gore of the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image052.jpg" alt=""></p>
<p><strong>I reach the beginning of row (2) and continue putting scales along row (3). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image056.jpg" alt=""></p>
<p><strong>I have approached the other back princess seam. </strong></p>
<p><strong>Here I can bring the scale right to the very corner without disturbing the pattern. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image058.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2012.files/image060.jpg" alt=""></strong></p>
