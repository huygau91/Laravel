<h1>Tutorial 9. Designing the Scales Layout.</h1>
<p>There are no particular challenges about <strong>drawing the scales layout for the central front gore</strong> because the sides of the gore have the same length.</p>
<p><strong>The scales will be arranged in parallel arc-shaped rows with a depth of 5cm, repeating the shape of the scalloped hem. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%209.files/image002.jpg" alt=""></strong></p>
<p><strong>Let us look at the side gores at the front of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%209.files/image004.jpg" alt=""></p>
<p>They are 104cm along the front princess seam, 112cm long along the centre, and 110cm long along the side seam. I will use the shortest side in the calculations (104cm).</p>
<p><strong>The difference from the central length is 8cm: </strong></p>
<p><strong>112cm - 104cm = 8cm</strong></p>
<p><strong>This central length, however, includes the depth of the arc </strong>which was determined based on the central front gore. It was 5cm and it must stay so on all other gores of the skirt.</p>
<p><strong>Therefore, the side front gore is 3cm too deep in the middle. </strong></p>
<p><strong>8cm - 5cm = 3cm</strong></p>
<p><strong>I find the excess along the side seam the same way: </strong></p>
<p><strong>110cm - 104cm = 6cm</strong></p>
<p>The row stops at the side seam so I don�t need to account for the depth of the arc here.</p>
<p><strong>Thus, the layout must be designed in such a way that</strong>, after you form a couple of bottom rows, the subsequent rows of scales get aligned with the rows on the central gore at the front of the skirt.</p>
<p>At the same time, I need to account for the fact that the front and the back will be joined by the side seams, which means I cannot change the chosen intervals between the scales positioned next to the side seams!</p>
<p><strong>I will put the scales on the skirt starting from the bottom and moving upwards. The size of the scales will gradually decrease along with the intervals between them. </strong></p>
<p>The first row of 8cm scales will go right parallel to the hem, 2cm from it.</p>
<p>I mark 2cm up the side seam line and the centre line and draw the second half-row.</p>
<p>By doing so, I compensate for 2cm along the central line as compared to the princess seam. There is only one extra centimeter left now. I also compensate for 2cm along the side seam line, but there is still 4cm left.</p>
<p>The third row will be a full row across the whole gore made at a 2cm interval.</p>
<p>Next, I mark 1cm along the centre line and 2cm (interval) along the side seam line and draw the fourth half-row.</p>
<p>I have thereby compensated for the excess length along the centre line. And there is only 2cm excess left along the side seam line, too.</p>
<p>I draw the fifth full row at a 2cm interval from the previous one.</p>
<p>I mark 2cm up the side seam line and draw the sixth half-row which stops in the centre of the gore.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%209.files/image006.jpg" alt=""></p>
<p><strong>These six rows (three full rows and three intermediate half-rows) allow me to even out the intervals along the side seam line starting from the seventh row. Subsequent scales will be arranged in parallel arc-shaped rows with a depth of 5cm repeating the shape of the rows on the central front gore. </strong></p>
<p><strong>Let us move to the back of the skirt. </strong></p>
<p><strong>Look at the side gores of the back. </strong>The principle is just the same.</p>
<p>The back princess seam is 115cm long, the side seam is 110cm long, and the gore is 121cm long in the centre. Obviously, the side seam line must align well with that of the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%209.files/image008.jpg" alt=""></p>
<p><strong>Let me calculate all excess lengths which need to be compensated for. </strong></p>
<p>The shorter side is the side seam: I will make no changes to it. Scales going along it will be positioned at 2cm intervals just like on the side front gore.</p>
<p><strong>There is an excess of 11cm along the centre vertical line of the gore: </strong></p>
<p><strong>121cm - 110cm = 11cm</strong></p>
<p>This length, however, includes the depth of the arc which was determined based on the central gore. It was 5cm and it should stay so on all other gores of the skirt.</p>
<p><strong>Therefore, the excess along the centre of the side back gore is only 6cm. </strong></p>
<p><strong>11cm - 5cm = 6cm</strong></p>
<p><strong>Now I need to even out the back princess seam with regard to the side seam:</strong></p>
<p><strong>115cm - 110cm = 5cm</strong></p>
<p>The first row of 8cm scales goes parallel to the hem, 2cm above it.</p>
<p>I mark 2cm up the princess seam line and up the central line and draw the second half-row.</p>
<p>By doing so, I have compensated for an excess of 2cm in the length of the princess seam line as compared to the side seam line. There is an excess of three more centimeters left now. I have also compensated for an excess of 2cm in the length of the central line, and now there is only four more left.</p>
<p>Additional compensation half-rows can only be inserted between full rows though. So the third row will be a full one.</p>
<p>Again, I mark 2cm up the princess seam line and the central line and draw the fourth half-row.</p>
<p><strong>I still need to compensate for an excess of 1cm in the back princess seam and 2cm in the central line. </strong></p>
<p>I draw a full row (the fifth one) at a 2cm interval from the previous one.</p>
<p><strong>So I know I need to move 1cm up the back princess seam line and 2cm up the central line of the side gore. </strong>It all works well in the centre: it coincides with the chosen interval. But I cannot mark 1cm up the back princess seam line because it is supposed to be aligned with the princess seam line of the central back gore and I still don�t know how the rows will be arranged on it.</p>
<p><strong>Considering this, I suggest we should make the sixth full row parallel to the fifth one, positioned 2cm from it. </strong>In this case, the back princess seam line will be 1cm shorter than the side seam line starting for the sixth row. In other words, when the last row reaches the waistline at the side seam side, the same row will go 1cm past the waistline at the back princess seam side.</p>
<p>As you remember, all measurements were taken from the supposed natural waistline. And the top edge of any classic skirt is always shaped as an arc with a depth of about 1.5cm. Thus the last row of scales will arrive right at the actual top edge of the skirt at the princess seam side, and the lacking 1cm will be compensated for.</p>
<p><strong>Things are all clear with the side back gore for now. </strong></p>
<p><strong>And now let us look at how the sides of the front are aligned with the sides of the back. </strong>It is great that all rows are made at 2cm intervals. But I used four rows on the side back gore and six on the side front gore.</p>
<p>And this means I need to put two more rows on the side back gore without changing the size of the scales (8cm).</p>
<p><strong>Let us now look at the train, or the central back gore. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%209.files/image010.jpg" alt=""></strong></p>
<p><strong>The central back gore has two absolutely identical princess seam sides, each with a length of 115cm, and is 135cm long along the centre. </strong></p>
<p>There is, therefore, no need in evening out the princess seam lines. I only need to adjust the depth of the bottom arc and bring it to 5cm.</p>
<p><strong>There is an excess of 20cm along the centre back line: </strong></p>
<p><strong>135cm - 115cm = 20cm</strong></p>
<p>This additional length, however, includes the depth of the arc-shaped sewing line. It is 5cm and it must be the same on all gores of the skirt.</p>
<p><strong>This means the actual excess in the length of the centre back line makes only 15cm. </strong></p>
<p><strong>20cm - 5cm = 15cm</strong></p>
<p><strong>Just like before, I draw the first full row 2cm from the bottom edge of the gore.</strong></p>
<p>I move 2cm up the centre back line and draw the second half-row shaped like a half-arc.</p>
<p>By doing so, I have compensated for 2cm along the centre line of the train. There is still an excess of 13cm left.</p>
<p>I draw the third full row at a 2cm interval from the previous one.</p>
<p>I move 2cm up the centre back line again and draw the fourth half-arc half-row.</p>
<p>I only need to compensate for the remaining 11cm now.</p>
<p>I continue alternating between full rows and half-rows positioned at 2cm intervals. After the fourteenth half-row is finished, there is an excess of only 1cm left along the centre back line. And I don�t have to worry about it because of the arc-shaped top edge of the skirt.</p>
<p>One should remember that the back princess seam lines of the central gore must align well with the corresponding lines of the side gores. In other words, the number of rows of scales of each size must be identical on them.</p>
<p><strong>Let us sum it up. </strong></p>
<p>I have prepared a separate scales layout for each gore.</p>
<p>Now I need to align these separate layouts.</p>
<p>For this purpose, I will draw the layout for the front and the back of the skirt taking into account the alignment of gores along the back princess seam lines and the side seam lines.</p>

