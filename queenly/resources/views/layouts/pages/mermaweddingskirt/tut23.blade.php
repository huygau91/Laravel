<h1>Tutorial 22. Working on the Peplum (Continued).</h1>
<p><strong>I have put Rigilene bones on the lining in accordance with the guidelines and pressed it. When the ends of the peplum come together, its shape will resemble a bell or a dome. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image002.jpg" alt=""></strong></p>
<p><strong>I need to prepare 3 pieces for the fastener. </strong>It is very easy: I sew a casing from bias tape and insert a piece of a wide Rigilene bone in it. The length of the bone is about 5cm.</p>
<p>And then I stitch closed both ends of the casing.</p>
<p>The finished piece is about 6-7cm long.</p>
<p><strong>These three pieces will serve as prolongations of the horizontal bones sewn in between the lining and the face of the peplum. </strong>And there will be snaps on them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image003.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image005.jpg" alt=""></p>
<p><strong>It is time to talk about decorating the peplum. </strong></p>
<p>I have already mentioned that I want it to stand out but at the same time not to look out of place on the dress. <strong>The peplum will be decorated</strong> with the same scales as on the skirt (4cm in diameter) and with equilateral triangles with a base of 4cm and a height of 2cm.</p>
<p><strong>I have prepared a steel template and cut triangles from the same fabric as scales. You can cut out two rows of triangles in one go if you arrange them smartly. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image007.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image009.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image011.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image013.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image015.jpg" alt=""></strong></p>
<p><strong>I have tried several ways of arranging the decorative elements on the peplum and alternating between scales and triangles. </strong></p>
<p><strong>I believe the following way is the best: </strong></p>
<p>I will put these decorative elements in rows, just like I did on the skirt, alternating between rows of scales and rows of triangles and arranging them chessboard fashion.</p>
<p><strong>The rows will be made at 1cm intervals. </strong></p>
<p><strong>I will decorate the face of the peplum </strong>and then join it with the lining and turn it right side out.</p>
<p>This will leave the inside of the peplum neat and clean.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image017.jpg" alt=""></p>
<p><strong>I make a row of stay-stitches along the perimeter of the piece. I stitch over the face side at a presser-foot width from the woven interfacing. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image019.jpg" alt=""></p>
<p><strong>The bottom horizontal row of stay-stitches will serve as a guideline for the first row of decorative scales. </strong>It will give the peplum a nice framing. Then I will alternate between rows of triangles and rows of scales positioned at 1cm intervals. I will make sure to position the first and the last element in each row neatly to achieve the same effect at the back of the peplum as in the zipper area on the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2022.files/image021.jpg" alt=""></p>

