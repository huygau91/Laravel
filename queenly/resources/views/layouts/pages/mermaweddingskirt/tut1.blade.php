<h1>Why a&nbsp;Mermaid Skirt?</h1>
<p><strong>I relied on the following requirements when choosing the skirt style: </strong></p>
<p>- It must be a popular style;</p>
<p>- It must be a luxury style;</p>
<p>- It must be an eye-catcher;</p>
<p>- It must be appropriate for different age groups.</p>
<p>After analyzing a wide range of wedding dresses, relying on my own and my students� experience, <strong>I decided that the mermaid wedding dress is in high demand and meets all the above-mentioned criteria. </strong></p>
<p><strong>The mermaid dress is truly a luxury type of a wedding dress. </strong></p>
<p>Fashion trends come and go, but exclusive wedding dresses like this are simply timeless.</p>
<p>The mermaid dress is particularly popular among brides who keep an eye on current trends.</p>
<p><strong>This type of a dress is typically ordered by women between 24 and 30 years. They stand on their own two feet with confidence, there is enough life experience behind them, and they have a certain status in the society. </strong></p>
<p>These women are ready to pay a high price for a good garment and they know the value of handiwork. And there is no way they will wear a budget dress on their special day.</p>
<p><strong>They are one-of-a-kind brides </strong>who look for one-of-a-kind dresses!</p>
<p><strong>The know-how given in this book will allow you to sew an elegant mermaid skirt with perfect fit and fabulous texture. </strong></p>
<p><strong>Expect to hear lots of compliments from the bride and from her guests! </strong></p>
<p><strong>I can guarantee that your bride will be the centre of attention in any wedding hall or reception venue! </strong></p>
<p><strong>No one else will have a fabulous skirt like hers! </strong></p>
<p>&nbsp;</p>
<h2>Foreword.</h2>
<p><strong>This&nbsp;course -&nbsp;detailed guidelines to sewing a wedding dress with a mermaid skirt. </strong></p>
<p><strong>Tight-fitting wedding dresses are part of today�s reality.</strong> As I said before, dresses of this type are frequently chosen by mature women. They know what they are worth, they are well-acquainted with wedding fashion trends, and they don�t fall for excessive d�cor and pomposity.</p>
<p><strong>And this is the main challenge about making a tight-fitting dress: you must make it gorgeous with as little d�cor as possible! </strong></p>
<p><strong>Unlike in most other courses by the Corset Academy, I will not use any ready patterns this time. Instead, I will create an absolutely new model right before your eyes relying solely on my experience and on the design concept I have in mind.</strong> I believe this should be all the more useful to you. You will be able to participate in the whole process and see how I analyze the situation, solve arising contradictions, and make conclusions. This approach will inspire lots of new interesting design ideas both in me and in you. It is your chance to go creative!</p>
<p><strong>All I know for now is that I want to sew a straight tight-fitting dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/forword.files/image002.jpg" alt=""></p>
<p><strong>Certain parts of the bodice will imitate bare skin. </strong>Wedding dresses with the bare-skin effect are popular among relatively mature brides of a good build who also tend to like tight-fitting dresses.</p>
<p><strong>Actually, the bodice is already ready. It was sewn based on the guidelines from another highly recommended book by the Corset Academy: �Wedding Dresses with Bare-Skin Effect." </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/forword.files/image004.jpg" alt=""> </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/forword.files/image006.jpg" alt=""> </strong></p>
<p><strong>Since I am making a tight-fitting dress, the skirt can be straight, gored, or trapeze-shaped. </strong></p>
<p>But I want this dress to be truly exclusive and beautiful beyond the bride�s expectations.</p>
<p><strong>There are several ways to achieve this goal. </strong></p>
<p><strong>The easiest way</strong> is to cut the skirt the traditional way but use expensive fabrics (lace, for instance). This, however, naturally increases the cost of production which is not something we normally want.</p>
<p><strong>Considering all this, I have decided: </strong></p>
<p><strong>- Firstly, </strong>to make the pattern more complicated. The front part of the skirt will be bell-shaped while the back will be cut as a gored skirt with train. The train will have a fancy shaped hem.</p>
<p><strong>- Secondly,</strong> I will give the fabric the desired texture myself.</p>
<p>This will allow me to receive maximum profit at minimum cost.</p>
<p>Let me highlight that I have intentionally decided not to include ready patterns in this book. We will go through the whole cutting process together and create a custom pattern.</p>
<p>I want you to develop a proper understanding of how I calculate, shape, and adjust my skirt patterns.</p>
<p><strong>In conclusion, let me briefly mention what parts the skirt is made of. </strong>Needless to say there is no petticoat inside, but only the wearer�s legs and the lining. And the top layer is the face of the skirt.</p>
<p><strong>And this is exactly what I want us to start from: making a pattern and cutting the face. </strong></p>

