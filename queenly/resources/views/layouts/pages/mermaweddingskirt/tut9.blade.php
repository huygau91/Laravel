<h1>Tutorial 8. Doing Calculations for the Scales Layout.</h1>
<p><strong>There is a schematic drawing of the front and the back of the skirt in the picture below. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%208.files/image002.jpg" alt=""></strong></p>
<p><strong>We have approached what I believe to be the key part of the whole book: designing the layout for putting scales on the skirt. </strong></p>
<p>This may seem like nothing difficult at first sight. You simply need to put decorative elements in arc-shaped rows which repeat the shape of the hem of the skirt.</p>
<p>And it truly would have been that simple if there were no train, i.e. if the hem were even all around.</p>
<p>But the train makes the layout on the central back gore totally different from the layout on the side gores.</p>
<p>And thus the overall pattern will not click together when I join the front with the back. All scales will be positioned at different angles, the rows will stop at different levels on different gores, and it simply won�t look like one whole anymore.</p>
<p>Of course, I could add more scales to fill the hollows at the top of each gore, but it will only make things worse.</p>
<p><strong>As the result, the skirt will look like the drawing on the right side of the board below. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%208.files/image004.jpg" alt=""></p>
<p><strong>I could have simply supplied you with a ready layout for putting decorative scales on this particular skirt. </strong>And it would be easy to adjust it if you changed the length or the width of your skirt proportionately. <strong>But it would be absolutely impossible to use the same layout if you decided to change the shape of the hem or the length of the train! </strong></p>
<p><strong>My key goal is to teach you how to create a harmonious layout for putting decorative elements on similar skirts regardless of the number of gores, the shape of the hem, or the length of the train. </strong></p>
<p><strong>Our skirt is made of six gores: </strong>three gores at the back and three imitated gores at the front. It won�t be a problem for you to make a similar layout for an 8-gore or 12-gore skirt after you get the point.</p>
<p><strong>It is also important to bear in mind that we are working with scales in eight different sizes. </strong></p>
<p>This means you need to create a smooth transition from larger to smaller scales and, at the same time, remember to keep their rows symmetric so you can align all gores neatly.</p>
<p><strong>The key task in the making of a layout like this is to determine the right interval between two vertical rows of decorative elements.</strong> In fact, the whole layout is based on this interval.</p>
<p><strong>Our scales are plain half-circles. The largest one has a diameter of 8cm and a radius of 4cm. </strong></p>
<p><strong>I will use � of the radius as an interval between the scales. </strong>I will round the value up and not down.</p>
<p>As the result, rows of 8cm, 7.5cm, 7cm, and 6.5cm scales will be positioned at 2cm intervals. Rows of 6cm and 5cm scales will be positioned at 1.5cm intervals. I have not yet decided whether I want to use 1.5cm or 1cm for rows of 4.5cm scales, but 4cm scales will definitely be positioned at 1cm intervals.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%208.files/image006.jpg" alt=""></p>
<p><strong>The second most important task is to determine the exact lengths of all gores.</strong> If you look at the drawing, you will notice that they are all different in length.</p>
<p><strong>Looking ahead, </strong>I can tell you that the key goal is to keep all scales at the same levels on all gores above the flare line and make all rows on each gore repeat the shape of the hem.</p>
<p>All measurements are to be taken from the supposed natural waistline (the top horizontal line on the drawing).</p>
<p><strong>I need to know the length of the side line and the central vertical line of each gore because I will use the difference between the central line and the side line in further calculations. </strong>Let me remind you that the front half of the skirt is split in three imitated gores by two vertical rows of stitches (as compared to the back half made of three actual gores with arc-shaped hems). The shape of the hem of each front gore is also marked by a row of stitches. The central front gore, for instance, is 104cm long along the sides and 109cm along the centre. In other words, the difference between the side length and the central length is 5cm.</p>
<p><strong>I measure all required lengths and write them down on the drawing. </strong></p>
<p>And now I can start working on the layout.</p>

