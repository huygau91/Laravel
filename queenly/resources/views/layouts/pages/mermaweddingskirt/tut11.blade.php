<h1>Tutorial 10. Drawing the Scales Layout.</h1>
<p><strong>What you see in the picture below is a layout for putting scales on one half of the skirt: from the central back seam line to the centre front line. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/petite%20wedding%20skirt/Tutorial%2010.files/image002.jpg" alt=""></strong></p>
<p>I will sew the central back seam before <strong>putting the scales on the front and on the back of the skirt, </strong>but I won�t join the two halves along the side seams.</p>
<p>And that is why we shall discuss the layout and the order of putting scales on the skirt separately for the front and for the back.</p>
<p><strong>All rows are numbered in the same order in which they are supposed to be made. The numbers are black on the back and blue on the front. </strong></p>
<p><strong>Please note </strong>that all rows meet at the side seam, as well as at the princess seams. The rows are made of 8cm scales and there are 2cm intervals between them.</p>
<p><strong>I will start from the more complicated back half. </strong></p>
<p><strong>I step 2cm from the bottom edge and start putting scales along full row (1) moving from the side seam edge </strong>across the side back gore and the central back gore. As you understand, the same arc passes onto the other half of the back, as well.</p>
<p><strong>I return to line (1) on the central piece and start putting scales along half-arc (2) </strong>which crosses the central back seam and stops on the second part of the central back piece.</p>
<p><strong>I return to the side piece and go along half-arc (3)</strong> which transforms into full arc (3) after crossing the princess seam. The full arc goes across both halves of the central piece and finishes as a half-arc on the second part of the side back piece.</p>
<p><strong>I put scales along half-arc (4) the same way as I did along line (2).</strong></p>
<p><strong>I return to the side seam and go along full arc (5) </strong>across the whole half of the back, the same way I went along arc (1).</p>
<p><strong>Half-arc (6) is made just the same way as half-arc (4). </strong></p>
<p>I continue putting scales on the skirt in correspondence with the layout.</p>
<p><strong>Staring from full arc (15) and upwards all guidelines on the half of the back are even and the train prolongation is fully compensated for. </strong></p>
<p><strong>The layout for putting scales on the front half of the skirt is a lot simpler. </strong></p>
<p><strong>I step 2cm from the bottom edge and draw full arc (1) which starts from the side seam,</strong> goes across the whole front half of the skirt, and stops at the other side seam.</p>
<p><strong>I move 2cm up the side seam and up the centre line of the side front gore and start putting scales along half-arc (2) on both imitated side front gores. </strong></p>
<p><strong>Full arc (3) goes across the whole front half of the skirt, similarly to arc (1). </strong></p>
<p><strong>I move 2cm up the side seam and 1cm up the centre line of the side front fore. I start putting scales along half-arc (4) on both the imitated side front gores. </strong></p>
<p><strong>I return to the side seam, step 2cm up, and start putting scales along full arc (5) </strong>which goes across the whole front half of the skirt.</p>
<p><strong>I move another 2cm up the side seam and put scales along the short half-arc (6) stopping at the centre line of the imitated side front gore. And I do the same for the other side front gore. </strong></p>
<p><strong>Starting from full arc (7) and upwards all guidelines are even and the difference in the sides of the front (caused by the shape of the hem) is fully compensated for. </strong></p>
<p>Therefore, if I put two more full arcs at 2cm intervals between the side seam edges of the back half of the skirt, the front and the back of the skirt will be perfectly even.</p>
<p><strong>While, because of the train, I had to go about 30cm up from the bottom edge to even out the rows on the back half of the skirt, here, on the front half, it took only 8cm. </strong></p>
<p>I believe you can see why I did not give you a ready layout but decided to go through the whole process of creating it together. <strong>Now you won�t be scared off by any hem shape or train length! </strong></p>
<p><strong>Let us talk about alternating between scales of different sizes. </strong></p>
<p>I will use the largest (8cm) scales below the level where the rows on the front and on the back become even.</p>
<p>It looks beautiful when the train is made of large scales!</p>
<p>But the most responsible part of the skirt is its front, indeed.</p>
<p>I only need to go 8cm up from the bottom edge of the front to achieve symmetry with the back with 8cm scales.</p>
<p>And the full length of the skirt along the centre front line is 109cm.</p>
<p><strong>7.5cm, 6.5cm, and 6cm scales in the area above the alignment level will be most likely put so as to stop 40-50cm from the bottom edge. The whole top part of the skirt will be covered in smaller scales. </strong></p>
<p>Those small scales at the front of the skirt will make it look especially elegant!</p>
<p>As I mentioned in the beginning of the book, I am sewing this dress for the first time.</p>
<p>And this is why we will literally make those rows of scales together, one after another, and see when it is best to replace larger ones with smaller ones, etc.</p>
<p>If you and your client will be absolutely happy with the decorated skirt, I recommend you should keep a neat drawing of the final layout so you can sew similar skirts without spending much time on it in the future!</p>
<p><strong>In conclusion, I would like to give you a piece of advice. </strong></p>
<p><strong>If you had a chance to carry out a fitting before decorating the skirt and you are sure about its length (especially, at the front), then I recommend you do the following: </strong></p>
<p>- Make the first row of scales go across the whole skirt right along the hem. Instead of stepping 2cm from the bottom edge, step literally just some 5mm (presser-foot width).</p>
<p>In this case, the bottom scales will basically lie on the floor, fully and not halfway, adding more elegance to the skirt.</p>
<p><strong>If you are not that sure about the length of the skirt though, it naturally makes sense to put the first row of scales 2cm above the bottom edge. </strong></p>
<p><strong>Actually,</strong> you can always add another row of scales at the bottom and even increase their diameter if your skirt happens to be shorter than needed for whatever reason.</p>
<p>I will mark a couple of guidelines at a time on the fabric, put scales along them, mark more guidelines, put scales along them, and so forth.</p>

