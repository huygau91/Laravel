<h3>Tutorial 15. Forming the Base of the Dress Top. The Front.</h3>
<p></p>
<p><strong>It's time to work on the dress itself.</strong> As you have already noticed this garment doesn't require a ready pattern. The function of a pattern is basically performed by our dress-form.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>First of all I need to shape the part of the dress that covers the cup area and passes onto the side piece. </strong>The most challenging task is to give the cups their dome-shape. </p>
<p><strong>I need to make the edge of the neoprene very even</strong> so I trim it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I put the neoprene over the dress-form with its even edge along the neckline and secure it with pins along the neckline, around the cup and along the edge of the dress-form.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the edge of the dress-form on the neoprene with a disappearing ink pen.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The incline of the front towards the side will go below the hip line so I mark 24cm along the bottom of the piece (that equals the hip circumference) and draw a vertical line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the fabric after the contour leaving a couple extra centimeters.</strong> Later I will even it out and leave 1.2cm (0.7cm for the thickness of the dress-form and 0.5cm for the seam allowance). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Let's work on the cups.</strong> </p>
<p><strong>First of all I need to decide where I should bring the dart?</strong> I believe it should be brought to the very side of the cup so it's easier to disguise it with lace later. I mark the approximate location of the dart. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the fabric back and smear the cup with Gutermann glue without touching the area of the dart.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I stick the fabric onto the cup trying to be as accurate as possible. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I carefully cut the cup through to the dart level.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I smear the rest of the cup </strong>and cover up the dart by placing one piece of fabric over the other. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut off the inner part of the dart.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Ideally, the neoprene should lie edge-to-edge.</strong> And mind you, you shouldn't sew the edges together. Later we will glue a lace motif over this area to disguise them. I will also trim the top edge of the neoprene following the shape of the cup but for now I leave it as is to make sure the lace will settle well when I start decorating it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I leave it out so the glue can set. What I get as a result is the inner shape of the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I repeat the same steps with the other half of the front. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image046.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image048.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image050.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image052.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image056.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image060.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image062.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image064.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image066.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image068.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image070.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image072.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image074.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I notch out the bottom triangular part of the front leaving 0.5cm for the seam allowance.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>As you can see I've created the base of our dress without a pattern using ready foam cups.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>But this is not all yet.</strong> I will continue 'sculpturing' the dress. The next stage is adding see-through mesh fabric in the centre and over the shoulders. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2015.files/image082.jpg" class="img-responsive center-block"></p>

