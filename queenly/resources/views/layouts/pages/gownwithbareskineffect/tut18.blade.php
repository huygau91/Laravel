<h3>Tutorial 17. Forming the Base of the Dress Top. The Mesh Part of the Dress Top.</h3>
<p></p>
<p><strong>I overlay the top of the dress with �illusion�. </strong>This mesh fabric is very delicate.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>You can adjust the neckline at this stage.</strong> I've decided to make it rounder and wider so I can overlay its edges with lace motifs in a prettier way. </p>
<p><strong>I re-draw the shape of the neckline and cut it.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I overlay the top with the mesh fabric and secure it with pins.</strong> I pull the mesh up to make sure it doesn�t slack. It would be impossible to do this without our dress-form! Thanks to the dress-form we don't have to account for any extensibility coefficients or design complicated patterns.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>We need to know the distance from the waist to the neckline in order to cut the neck hole in the mesh.</strong> When working on such dresses I usually take all thinkable measurements off my client and then decide whether I need all of them or not right at work. I will cut the neck hole later.</p>
<p><strong>The mesh is pinned down.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Before removing the pins and cutting the mesh I take my Gutermann glue and start gluing the mesh to the neoprene along its edges.</strong> You can only use this trick if you plan to have dense decorations along the edge of the nude fabric because sadly the glue does tend to leave dark stains. If there are no closely spaced decorations, then you can glue the mesh down together with your lace motifs. Be very careful when working with glue - avoid unwanted drops.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I spread the mesh and cut it.</strong> I've decided to cover the darts on the cups with the mesh. It has settled nicely over the cups and I don't have to trim much. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I leave a seam allowance and cut the mesh around the armscyes and the shoulder seams.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image037.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the mesh at the top.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image041.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I remove the pins that secure the neck hole and cut the required shape of the opening referring to the waist-to-neckline measurement.</strong> This requires particular accuracy because the raw edges of the neckline will be left 'as is.' The mesh fabric is so delicate that you won't be able to see them: they will merge with the skin colour. The bare-skin effect will disappear if you decide to welt the neckline with a bias tape since it'll be obvious that the top is partially made of mesh fabric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2017.files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>I wait for the glue to set before I continue.</strong></p>

