<h2>Creating a Custom Dress-Form.</h2>
<p></p>
<h3>Tutorial 1. Materials Required for Making a Dress-Form.</h3>
<p></p>
<p>Working with high-stretch fabric is often associated with a particular challenge: a regular foam dress-form is no longer good enough in this case. Your client's body is noticeably different from the shape and the size of your regular dress-form, which will inevitably result in inaccuracies in the fit of a garment sewn with high-stretch fabrics. </p>
<p>After trying out, combining and comparing various ways of working with resilient materials I developed my own method that I’m glad to share with you. <strong>The key idea is to make a custom dress-form with your client's body dimensions.</strong> </p>
<p><strong>For a start you need to take all measurements off your client.</strong> They are all regular corset tailoring measurements plus the measurements you take for sewing garments with shoulder seams. The more measurements you take, the more precise your dress-form will be. Another important measurement to include is crotch length - the distance from the front waistline to the back waistline measured between the legs. You need this measurement for the built-in panties.&nbsp; </p>
<p><strong>Materials Required for Making a Dress-Form: </strong></p>
<p><strong>You need a large piece of paper to draw a template for what will later become your dress-form. </strong>You can use any kind of it from square paper to regular A3 paper. </p>
<p><strong>Cardboard.</strong> You can use regular cardboard boxes. As for me I will use two flattened boxes - one put over the other to make four layers of corrugated cardboard. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image002.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-015.jpg"></p>
<p><strong>White craft glue:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image004.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-016.jpg"></p>
<p><strong>Paintbrushes of varied width: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image006.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-017.jpg"></strong></p>
<p><strong>Wrapping film.</strong> It is usually sold in rolls. Food wrap won't do because it's too thin.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image008.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-018.jpg"></p>
<p><strong>Packaging tape:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image010.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-019.jpg"></p>
<p><strong>Spray foam insulation.</strong> Builders use this spray foam to fill the cracks after installing window and door frames, and I use it to fill in the cups of the dress-form. You could also replace spray foam with bits of fabric or padding polyester but spray foam makes the cups a lot more solid and easier to work with.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image012.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-020.jpg"></p>
<p><strong>Let me explain the basics schematically.</strong> </p>
<p><strong>First I draw a vertical line</strong> that marks the middle of the dress-form. </p>
<p><strong>Then I draw horizontal lines</strong> that we normally refer to when building or altering a corset pattern: shoulder line, bust line, under-bust line, waistline, stomach line and hip line. </p>
<p><strong>I mark the crotch length down from the waistline </strong>and find the bottom point of the panties where the closure is supposed to be located. </p>
<p>I mark the shoulder length <strong>on the shoulder line </strong>at the back and at the front. I mark the notches.</p>
<p>Next I mark the bust front measurement <strong>on the bust line</strong> and mark the notches, too. Then I subtract the bust front from the bust circumference, find the part of the bust circumference that falls upon the back, mark the determined value on the bust line and mark the notches. </p>
<p>I mark a quarter of the under-bust circumference left and right of the middle<strong> of the under-bust line</strong>, and again I mark the determined value and the notches on the under-bust line. </p>
<p><strong>What concerns the waistline</strong> I only mark notches on the semi- waist circumference, i.e. without dividing it into the front and the back half. I assume it's obvious that such models are only suitable for women of very good proportions. Stomach bulge is totally out of the question! It is a soft curve-hugging dress designed for a nearly perfect figure. That's why we don't distribute the waist circumference between the front and the back.</p>
<p>I mark the semi-circumferences of corresponding measurements with notches <strong>on the stomach line and on the hip line</strong> as well. </p>
<p><strong>Make sure to mark the bust middle on the bust line.</strong> The bust middle measurement is taken depending on the way you want to locate the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%201.files/image014.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-021.jpg"></p>
<p><strong>You can also mark your client’s shoulder length </strong>for further reference, to know where her neck will be. You will need to account for a number of additional factors when you start constructing your dress-form on paper: i.e. compare the bust middle measurement with the shoulder length and the shoulder slope, etc. But it's already clear that we can transfer the front and the back of the body onto cardboard separately. They differ from each other in the area from the shoulders to the waistline.</p>
<p><strong>This is how we prepare a flat template for the dress-form</strong> that we will later use for both dresses described in this course book. You can also use this dress-form for other clients with similar proportions. </p>

