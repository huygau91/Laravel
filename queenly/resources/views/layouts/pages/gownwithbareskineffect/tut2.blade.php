<h2>List of Measurements Required for Making a Dress-Form.</h2>
<p></p>
<p>Tie a cord or an elastic band around the waistline. Stand the way you normally do. You should neither let the measuring tape hang loose nor pull it too tight. </p>
<p><strong>Bust front. </strong>It is measured nearly the same way as bust circumference but between the side seams and from the front. </p>
<p><strong>Under-bust front.</strong> It is measured nearly the same way as under-bust circumference but between the side seams from the front. </p>
<p><strong>Bust circumference</strong> (determines the body size). Your measuring tape should pass through the prominent points of the shoulder-blades and the most prominent points of the breasts at the underarms level. Even if your client has low-set breasts, you should still place the measuring tape strictly horizontally to take this measurement. Later you can add a few extra centimeters for the fullness of the bust. </p>
<p><strong>Under-bust circumference.</strong> The measuring tape passes right under the bust and under the shoulder-blades. </p>
<p><strong>Waist circumference.</strong> It is taken around the thinnest part of the waist. </p>
<p><strong>Stomach circumference.</strong> It is taken 10-14cm below the waistline. The measuring tape should be parallel to the waistline and the hips. </p>
<p><strong>Hip circumference. </strong>It is taken around the most prominent part of the hips with some allowance for the stomach. </p>
<p><strong>Bust middle</strong> (distance between the apex points of the breasts). It is taken strictly horizontally. </p>
<p><strong>Front shoulder to waist. </strong>It is taken from the end of the shoulder seam that's closer to the neck and through the apex of the bust down to the cord tied around the waist. </p>
<p><strong>Front neck to waist. </strong>It is taken from the sternal notch down to the waist. </p>
<p><strong>Neck base to bust middle.</strong> It is taken simultaneously with the front shoulder to waist measurement. The measuring tape goes from the end of the shoulder seam that's closer to the neck down to the apex of the bust. </p>
<p><strong>Shoulder tip to bust middle. </strong>It is taken from the apex of the bust to the shoulder tip. </p>
<p><strong>Nape to waist.</strong> It is taken from the sevenths cervical vertebra to the waist. </p>
<p><strong>Back width.</strong> You measure it by placing the measuring tape horizontally between the bottom corners of the underarms at the level of the prominent points of the shoulder-blades. </p>
<p><strong>Front width. </strong>Imagine vertical lines going up from the front corners of the underarms and place the measuring tape between them horizontally right above the breasts. </p>
<p><strong>Front shoulder width. </strong>This measurement is taken between the shoulder joints at the front. </p>
<p><strong>Back shoulder width. </strong>This measurement is taken between the shoulder joints at the back. </p>
<p><strong>Side length.</strong> It is taken along the side from the waistline up to the lowest point of the armscye. </p>
<p><strong>Back shoulder to waist: </strong>This measurement is taken parallel to the spine from the most prominent point of the shoulder to the waistline. </p>
<p><strong>Back shoulder slope. </strong>It is taken from the point where the waistline intersects with the spine to the shoulder tip. Make sure your measuring tape is tight. </p>
<p><strong>Waist to hip length.</strong> Measure the distance between the waistline and the hip line at a straight angle. This measurement is normally about 16-20cm. </p>
<p><strong>Armscye circumference.</strong> Measure around the arm in the underarm area. </p>
<p><strong>Elbow circumference. </strong>Measure around the elbow. </p>
<p><strong>Wrist circumference.</strong> Measure around the wrist joint. </p>
<p><strong>Shoulder length. </strong>It is taken from the end of the shoulder seam closer to the neck to the opposite end of the shoulder seam, or sometimes from the middle of the shoulder slope to the shoulder tip. I recommend you should measure the <strong>sleeve length</strong> at the same time. </p>
<p><strong>Shoulder to elbow. </strong>This measurement is taken from the shoulder joint down to the elbow. </p>
<p><strong>Shoulder to wrist. </strong>This measurement is taken from the shoulder joint down to the wrist. </p>
<p><strong>Shoulder circumference (upper arm circumference). </strong>It is measured horizontally around the fullest part of the shoulder with the arm hanging down freely. The ends of the measuring tape should meet on the outer part of the arm. </p>
<p><strong>Crotch length.</strong> Measure between the legs from the front waistline to the back waistline. </p>
<p><strong>Garment length. </strong>Measure along the middle of the back from the seventh cervical vertebra down to the desired length.</p>
