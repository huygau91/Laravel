<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I'm not satisfied with the quality of the side seam on the base of the dress so I re-stitch it.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>And I move on to the skirt.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image048.jpg" class="img-responsive center-block"></p>
<p>
