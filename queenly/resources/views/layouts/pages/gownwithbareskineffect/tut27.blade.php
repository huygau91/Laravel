<h3>Tutorial 26. Sewing Down the Side Seams of the Dress.</h3>
<p></p>
<p><strong>I start sewing down the first side seam following the marking.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I pass onto the skirt making a single side seam down to the notch at the beginning of the flare.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image006.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trim the seam allowance down to 7-8mm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>There's no need in pressing the seam since it unfolds without effort.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>You should do the overlock finish on the closed area of the side seam on the skirt.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I'm going to sew the rest of the side seam on the skirt (from the notch at the beginning of the flare down to the bottom) the same way I do on dresses with open side seams.</strong> I pull the lining up, find the beginning of the seam, place the needle of the machine right there and join the face pieces of the skirt down to the very bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I step 7-8cm from it and sew down the side seam of the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew down the other side seam exactly the same way.</strong> I move upwards from the notch at the beginning of the flare.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image036.jpg" class="img-responsive center-block"></p>
<p>
