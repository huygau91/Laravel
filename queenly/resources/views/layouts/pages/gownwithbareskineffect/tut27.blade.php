<p><strong>You won't be able to see them after I restore the appliqu�. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image065.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I put the dress back on the dress-form.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image067.jpg" class="img-responsive center-block"></p>
<p>Seeing the dress on the dress-form that fully imitates my client's body allows me to be sure it's neither too tight nor too slack. Everything is alright.</p>
<p><strong>However I have some doubts regarding the shoulders. I think I'll need to pull the dress up in the shoulders on the client. </strong>I can tell there's not enough tension there.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2026.files/image069.jpg" class="img-responsive center-block"></p>
