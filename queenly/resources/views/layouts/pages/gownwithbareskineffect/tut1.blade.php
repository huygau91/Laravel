<h2>Review of the Dress Model and Materials Used.</h2>
<p></p>
<p><strong>This series of tutorials is dedicated to sewing a new type of wedding dress sketched on the whiteboard below.</strong> It is a dress sewn with materials that are absolutely new to my readers since I had never used them before writing this book. At the same time I will also use certain classic tailoring tricks and techniques that you're familiar with.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image001.jpg" class="img-responsive center-block"></p>
<p><strong>When you put this dress on, it looks as if there were nothing between the guipure and the skin.</strong> Although our dress features long sleeves, only the upper arms and the area around the wrists will be covered by lace appliqu�. </p>
<p><strong>The shape of the lace inset at the front of the dress </strong>allows us to imitate bare skin in the middle of the d�collet� area with the help of very resilient see-through mesh fabric that'll reach from the collarbone down to the very waist. The dress also features a mermaid skirt with six gores.&nbsp; </p>
<p><strong>The back and the front of the dress are nearly identical in silhouettes.</strong> The only difference is the absence of mesh at the back of the dress: i.e. the back will be open and there will be lace motifs at the sides. There will be a zipper at the back of the skirt and a rather voluminous train adorned with decorative elements. </p>
<p><strong>The techniques I will demonstrate during the sewing process are totally new to you.</strong> We shall put corsets aside for a change. This particular course is not related to corsetry. Instead we will work with resilient mesh fabric and lace, and I will share some very important tricks and know-how's of working with these materials. <strong>Moreover, it will involve making a custom dress-form for your client</strong>. </p>
<p>I will prove that you shouldn't experience any troubles sewing dresses like this if you follow my sewing technique and methods of working with the specified materials. </p>
<p><strong>So, let us take a closer look at the materials used for the dress. </strong>I am quite certain you have never used some of them (at least they were never mentioned in any of my previous books and tutorials).</p>
<p><strong>Normally you should use a nude-coloured fabric </strong>because the whole point is to imitate bare skin and conceal minor skin imperfections.&nbsp; </p>
<p><strong>The base of the dress will be made of nude resilient fabric that is known as neoprene in Israel.</strong> It resembles lycra knit fabric but not the kind used in leotards, dancewear or bodysuits. This material is made of layers of very thin knit fabric with a layer of latex (a rubber underlay) in-between. Neoprene is about 1.5mm thick. It is very resilient and stretches in all directions and at the same time it's soft and nice to the touch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Now let us talk about mesh fabric used in this kind of garments.</strong> There are numerous types of mesh fabric. </p>
<p><strong>I usually use nude mesh fabric that is referred to as 'illusion'.</strong> The name speaks for itself: this mesh is absolutely see-through and basically invisible. It seems to disappear as soon as you put the garment on.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image003.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image005.jpg" class="img-responsive center-block"></p>
<p><strong>It is all-way stretch.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It is synthetic, which means you don�t have to finish its raw edges.</strong> The mesh fabric secures the garment on the body in the open areas and around the wrists: it holds the dress in place while staying invisible.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image007.jpg" class="img-responsive center-block"></p>
<p>When you saw pictures of similar garments in fashion magazines, you must have often wondered how the lace manages to stay on the body. You probably didn�t notice the seams that attached it to the mesh because such joining seams are nearly invisible if you trim their seam allowances down to a couple millimeters. And of course it will be totally impossible to see the edges of the mesh if you edit the pictures in Photoshop. </p>
<p>As I have already mentioned, illusion is an all-way stretch fabric, which can sometimes make it hard to work with. <strong>That's why there is another similar mesh fabric that is as see-through but stretches only crosswise.</strong> I often use this mesh fabric folded in half in my see-through corsets.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>This time I will use it for the back of our dress.</strong> I need the back piece to stretch towards the top and fit tightly against the client's back and at the same time I don't want it to stretch horizontally. Our dress has an open back and there should be no tension from the bust-line level to the waistline. Otherwise, too much of the back will be revealed. </p>
<p><strong>I use the same kind of two-way stretch mesh fabric for the built-in panties of the dress, too.</strong> A dress like this has to feature panties to ensure the back is always pulled on tightly. On the one hand the back should hold around the shoulders and on the other hand it should be pulled on down the bottom. The panties help it stay in the right place. They are a must since the skirt is not heavy enough to pull on the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>You will also need ready foam bra cups to shape the bust area of the garment. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image010.jpg" class="img-responsive center-block"></p>
<p>They are sold in all shapes and sizes, both with and without push-up pads, so you can choose from different options. The cups should correspond with your client's size and the dress model. </p>
<p><strong>You can also adjust them if necessary: alter the top edge, trim the corners for attaching straps, etc.</strong> I will show you how to insert and secure them in the garment.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>Another thing you�ll need is round steel underwires for the foam cups.</strong> The length of the underwire should be chosen based on the size of the cups. It should be 1.5-2cm shorter than the lower arch of the cup. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Raw edges are sometimes finished with a special-purpose elastic band.</strong> You can use it to secure the edge of the back of the dress and ensure better fitting against the body for example. You can then overlay the band with lace appliqu� to conceal it.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>And finally, you will need lace to decorate the dress.</strong> There's an unlimited choice of lace fabrics you can use for this purpose. It doesn't have to be expensive stretch lace. I have chosen rather heavy non-stretch lace fabric with embossed embroidery. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Review%20of%20the%20Dress%20Model%20and%20Materials%20Used.files/image014.jpg" class="img-responsive center-block"></p>
<p>That is all I wanted to tell you before we get to work. This time we won�t have to adjust any standard patterns after client's measurements. We will do something totally different instead... Create a custom dress-form for the client!</p>

