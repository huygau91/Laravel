<h3>Tutorial 27. Sleeves: Drafting, Cutting, Decorating with Lace.</h3>
<p></p>
<p><strong>So let's continue working on our dress.</strong> As I've already mentioned models like this are best for women of perfect shapes. These are usually women who wear petite sizes, with not so full arms. </p>
<p><strong>Let me demonstrate a great trick</strong> that I often use for drafting sleeves for Italian clothing sizes 40-46. It works particularly well for tight sleeves made with knitted fabric. It helps sew them in really neatly. </p>
<p><strong>Here is the method: </strong></p>
<p>- Put your hand down on a piece of paper;&nbsp; </p>
<p>- Your thumb and your pinkie should be at the same level (i.e. form a 180 degree); </p>
<p>- The rest of your fingers should lie naturally; </p>
<p>- Draw a line passing through the top points of all fingertips.&nbsp; </p>
<p><strong>What you get is a sleeve top.</strong> The part from the middle finger to the pinkie falls upon the back and the part from the middle finger to the thumb upon the front.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the upper arm circumference along the horizontal line between the thumb and the pinkie. It should be symmetric from both ends.</strong> This measurement normally equals 27-30cm for petite sizes. My client's measurement is 27cm. And since we want to make slim fit sleeves, we don�t add anything either to the armscye circumference or to the upper arm circumference. They remain 'as is.' </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I move down literally just by 1cm and prolong the sleeve top to meet the ends of the 27cm section. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I measure the length of the sleeve cap.</strong> It should fully coincide with the armscye circumference of the dress (38cm). It's exactly the same as my client's measurement. You don't need to ease in when sewing sleeves into the armscyes of garments made with resilient fabric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>The marked sleeve cap arch is 38cm long.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image010.jpg" class="img-responsive center-block"></p>
<p>If the length of the sleeve cap built after this method turns out bigger or smaller than the armscye circumference, then you can simply adjust the length of the arch, i.e. make it longer or shorter while preserving the shape. </p>
<p><strong>I continue building the sleeve following the traditional method:</strong> </p>
<p>- I draw a line in the middle;&nbsp; </p>
<p>- I mark the distance from the shoulder to the wrist down from the top of the cap (57cm);&nbsp; </p>
<p>- I mark the distance from the shoulder to the elbow down from the top of the cap (34cm); </p>
<p>- I mark a half of the elbow circumference (12cm) left and right of the vertical line at the elbow level;</p>
<p>- I mark a half of the wrist circumference (9cm) left and right of the vertical line at the bottom of the sleeve;</p>
<p>- I draw the sleeve; </p>
<p>- And I account for a 0.5cm seam allowance along the sleeve cap only. I won't add any seam allowances lengthwise since the mesh fabric stretches very well and the arms will be able to move freely anyway. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I cut the sleeve from extra-fine all-way stretch illusion mesh.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I cut the second sleeve the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I recommend decorating the sleeves before sewing them up.</strong> For that purpose I put the sleeve on the dress-form that serves as a mat, overlay it with prepared lace motifs at the top and down the bottom, and glue them on. And then I glue identical mirrored motifs onto the second sleeve. I'll insert the sleeves in the armscyes after the glue sets.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2027.files/image022.jpg" class="img-responsive center-block"></p>

