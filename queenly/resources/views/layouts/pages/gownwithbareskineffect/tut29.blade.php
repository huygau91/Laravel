<h3>Tutorial 28. Shaping the Back to Get a Paper Pattern. </h3>
<p></p>
<p><strong>Our dress is fully prepared for a fit test.</strong> I have glued lace motifs onto the base and the sleeves and sewn down the sleeves. You'll need to apply some changes to the sleeves during a fit test because every woman's arms have a unique shape and the sleeves should repeat it perfectly for a good fit.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image002.jpg" class="img-responsive center-block"></p>
<p>Our dress would've fit my client flawlessly if not for the back neckline that reveals the prominent part of the back. It would've been a lot easier if the back were covered up by mesh fabric. </p>
<p>But since the back is open, its edges won't be snug against the skin because of the slope of the back and the curved area of the spine. We couldn't account for these factors with a flat dress-form. That's why these alterations are going to be quite noticeable.</p>
<p><strong>This raises a question: why haven�t I prepared the pattern beforehand?</strong> The thing is that all women without exception have non-symmetric bodies. It's really hard to calculate and build individual patterns for the right and left halves of the back. Besides, there is no guarantee you'll get the precise size. I prefer working with everything in its actual size so I can see it clearly how the back piece sits on the body left and right, that's why I perform all alterations during the fit test. </p>
<p><strong>What to do if you're sewing a dress of a certain size but not for a certain client? (For example: for selling it ready-made or showcasing it as a part of your designer collection).</strong></p>
<p>I'm going to show you how to shape the back of a dress in similar cases to make it fit a body of average I will explain how to model the back of the dress in cases like this to fit average body proportions. Of course you can't be 100% certain the back will fit a particular client perfectly but at least if you do need to apply some alterations they will be minor ones. </p>
<p>I would like to emphasize that dress models with see-through mesh insets like this should have a flawless fit, repeat your client's body curves perfectly. You won't make it better by easing in or inserting elastic bands! In other words it should be very precise from the very start.</p>
<p><strong>We have made a custom dress-form for the client. And now let's discuss how to design a pattern if you're making a showcase dress or working for a distant client. </strong></p>
<p><strong>I draw a straight vertical line on a piece of paper.</strong> It marks the centre of the back, the pattern, and the dress-form.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put the dress-form down, match the marked line with the central line of the dress-form, and trace a half of the back of the dress-form. </strong>I mark all major lines of reference: shoulder line, under-bust line, waistline, stomach line and hip line. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the major lines of reference and add extra space for the thickness of the dress-form that I removed when I was constructing the custom dress-form.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I start drawing the back.</strong> </p>
<p>First I divide the back width in half at the waistline (point O).</p>
<p>Then I step 1cm towards the side from point O and mark point E.</p>
<p>I draw a line perpendicular to the waistline through point E and determine the points of its intersection with the edges of the piece (A and B). These points will be the peaks of the central dart that I'm going to build and re-model.</p>
<p>Next I draw horizontal lines through points A and B and mark their intersections with the side seam (points C and D). </p>
<p>And I join points C and D. </p>
<p>Points C and D are the peaks of the side dart the depth of which equals 3.5cm (section K-L).</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The dart at the waistline looks too deep and I think it'll make the fabric too tight at the back and cause the edges of the back neckline to stick up.</strong> This dart obviously has to be smaller. But at the same time the quarter of the waist circumference (section L-F) has to stay the same.</p>
<p><strong>Let's assume the side seam passes through section C-D. </strong></p>
<p><strong>I reduce the side dart.</strong> I mark 1.5cm outwards from the side seam for a start and draw a new approximate side seam (red line). I remove the remaining 2cm of the side dart from the centre: mark 1cm left and right of the middle of the central dart (point E) and join these points (O and N) with the peaks of the dart (points A and B).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to work on the central dart a little.</strong></p>
<p><strong>I cut the back piece following the new contour of the side seam and cut it along the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I cover up the central dart. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put the cut-up back piece together along the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>But in this case the pieces overlap and form a reverse dart near the middle of the neckline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>If I decide to straighten this area, there will be extra fabric that'll settle in a natural dart. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image027.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I notch the fabric in that area. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image029.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The dart is about 1cm deep. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I put the new pattern over the dress-form matching their waistlines.</strong> See how the pattern has shifted. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>The edges of the back neckline will be snug against the skin after we sew down the shoulder seams since the fabric will be pulled up more.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>I return to the initial side seam of the back to make its configuration fully identical to the side seam of the front.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>But since the waist circumference is not supposed to change, I add the piece I've notched out near the central seam at the waistline level.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image039.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the final contour of the back piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>The modeling process is now finished. Of course there might be some slight inaccuracies.</strong> Open-back dresses always require that some adjustments be made during the fit tests. By the way the fit test doesn't feel too tiring for the client.</p>
<p><strong>Usually the dress fits perfectly and only a certain spot at the back might raise your doubts.</strong> I'm talking about this marked area of the neckline onto which I've transferred the side dart. Various factors tend to make it challenging: way too pronounced back slope, uneven shoulders, spinal curvature, etc.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2028.files/image045.jpg" class="img-responsive center-block"></p>

