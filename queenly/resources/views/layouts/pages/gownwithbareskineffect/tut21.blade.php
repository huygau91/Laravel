<h3>Tutorial 20. Taking the Dress Front off the Dress-Form. Attaching the Skirt.</h3>
<p></p>
<p><strong>I put the skirt to the front part of the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2020.files/image002.jpg" class="img-responsive center-block"></p>
