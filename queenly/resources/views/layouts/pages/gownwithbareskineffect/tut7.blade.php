<h3>Tutorial 6. Preparing the Cups.</h3>
<p></p>
<p><strong>Once the dress-form is wrapped you need to complement it with cups.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image002.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-128.jpg"></p>
<p>However semi-finished foam cups are too flat and we can't leave them that way. <strong>We need to insert steel underwires into the cups before putting them in place </strong>so they acquire their dome-shape just like in a bra. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image004.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-129.jpg"></p>
<p><strong>You will need bone casing for that purpose - i.e. special bias tape with a tunnel for inserting the underwires. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image006.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-130.jpg"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image007.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-131.jpg"></p>
<p><strong>You sew this bias tape to the small ease on the foam cups with two parallel stitches. Then you insert the underwire and make sound bar tacks at the ends.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image009.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-132.jpg"></p>
<p><strong>I’d like to warn you about something.</strong> Say your cups are white and you want the garment to be all nude-coloured inside. In this case you need to make a nude lining for the cups before attaching the bias tape. Or if the top of your dress is see-through and the cups are going to show, you should sew cases of matching fabric for the cups and cover them up from the face side before attaching the bias tape.</p>
<p>By the way the structure of the cups material is very similar to the neoprene fabric that we are going to use. Only our neoprene is much finer. </p>
<p><strong>I sew the bias tape to the cup leaving small tails that I can later make even with the edges of the cup. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image011.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-133.jpg"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image013.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-134.jpg"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image015.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-135.jpg"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image017.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-136.jpg"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image019.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-137.jpg"></strong></p>
<p><strong>Now I can insert the underwire.</strong> Please note that you insert it with the lower end towards the centre of the cup and the higher end towards the underarm. It's why the tips of underwires are usually marked with different colours. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image021.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-138.jpg"></p>
<p><strong>I insert the underwire into the tunnel of the casing. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image023.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-139.jpg"></p>
<p><strong>I push it in leaving some space for bar tacks. </strong>These bar tacks are very tight zigzag stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image025.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-140.jpg"></p>
<p><strong>The cup is ready - you could sew it into the dress like that.</strong> It has volume, it is hard and resilient and it holds its shape very well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image027.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-141.jpg"></p>
<p><strong>I sew another bias tape to the other cup and insert the underwire. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image029.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-142.jpg"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image031.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-143.jpg"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image033.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-144.jpg"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%206.files/image035.jpg" class="img-responsive center-block" alt="Описание: C:\Users\Admin\Desktop\kn4 4.0\kn4.4image\kn4.41-145.jpg"></p>
