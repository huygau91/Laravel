<h3>Tutorial 14. Finishing the Seam Allowances of the Train with the Lining. </h3>
<p></p>
<p><strong>I handle the seam allowances of the train with the lining the same way I did with the zipper.</strong> I put together the seam allowances of the train and the lining and match their notches. I make a joining seam over the face side along the seam I made to sew in the train, 1mm higher towards the seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>One side of the train is finished.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next I finish the other side.</strong> I find the seam allowances of the train and the lining, lay one over the other and match their notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>You have to sew the seam allowances from the bottom up on this side of the train and the lining.</strong> That's why I start by making a small seam down to the matched notches to avoid distortion and then stitch up from the very bottom of the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The lining of our skirt looks like this now. </strong>If you don't like the look of that gap on the lining, you can spread the area through the side seam and stitch it up. As for me, it doesn�t bother me.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>So we have sewn in the train and all underside seams are hidden underneath the lining. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image032.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I have a very important comment.</strong> Our train starts rather high and our skirt has a somewhat slim fit around the knees. How can this slim fit hold if the train flares quite a fair bit at the knee level? </p>
<p><strong>There is a very simple solution: </strong>we will sew in a 3-4cm long elastic band at the knee level of the triangular train. &nbsp;</p>
<p><strong>I mark a spot on the triangular part of the train a couple centimeters above the notch at the beginning of the flare and rip up the seams that join the train by the width of the elastic band.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Then I insert the elastic band into the seam, turn the garment out, and stitch the elastic band down from the other side.</strong> I make a couple of bar tacks.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I push the other end of the elastic band into the prepared spot on the opposite side of the train and sew it down the same way as previously.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>That's it! The skirt won't 'fall apart' at the knee level and the silhouette will stay the same. </strong>This doesn't affect the appearance of the train: it's still triangular and hanging freely but the elastic band prevents it from sinking into the skirt. Of course you could insert this elastic band when finishing the train with the lining but I wouldn't recommend that. It's really hard to determine the right spot for attaching the elastic band during the cutting or the sewing process. It's a lot easier to rip up the seams and insert it in place later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2014.files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I will press the skirt thoroughly to finish it.</strong></p>

