<h3>Tutorial 21. Shaping the Back of the Dress.</h3>
<p></p>
<p><strong>The front half of the dress is ready and we can move to the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image002.jpg" class="img-responsive center-block"></p>
<p>I suggest we should trace the contour of the existing front side seam in order to maintain the same configuration at the back. This way we won't have to worry about how long it should be and where it should start. Everything will match perfectly.</p>
<p><strong>But first let's compare the sides of the dress front.</strong> I fold the piece in half to match the sides. There is some divergence in their height and width. I mark that difference and trim them correspondingly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put the front piece over a sheet of neoprene and trace the contour of its side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut along the marked line, put the neoprene over the dress-form, leave some space for the seam allowance and pin it down along the entire length of the side seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the central vertical line on the neoprene.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I pin it to the dress-form along the central line and mark the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to draw a beautiful contour of the back from the top to the waistline and then a graceful transition from the waistline to the side. </strong>NB: there's a zipper at the back of the skirt and you need to account for it when modeling the shape of the sides. </p>
<p><strong>I put down the back half of the skirt.</strong> It makes sense to leave a small area of the skirt near the zipper before starting the transition to the side. It's totally fine if a small area of neoprene will be sewn to that area of the skirt and followed by a separately cut side piece since the spot will be covered by lace.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a preliminary contour of the back with regard to the specified area near the zipper.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I take the piece off the dress-form and draw a graceful silhouette.</strong> I make the transition to the side somewhat convex: it will look really beautiful. I add about 1cm for the seam allowance.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And I cut out the first half of the back.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The second half of the back is made exactly the same way.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image032.jpg" class="img-responsive center-block"></strong></p>
<p>In this course I started the entire working process from the front half of the dress because I wanted to show you how to work on the cups and explain the essence of my method in the first tutorials so you�d have more time to see into it. But when you work on new similar garments in the future, you will see that it's easier to start from the back of the dress. After drawing the side seam on one half of the back you can transfer it onto the other half, then cut the front of the dress referring to the same side seam and work on the cups afterwards. It doesn't really matter where to start when you're experienced enough though.</p>
<p><strong>I pin both back halves to the dress-form.</strong> I bend the sides of the neoprene so that they touch the table. This way I mark the same seam allowance for the side seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I put mesh fabric for the panties over the dress-form and pin it down.</strong> It's the same mesh I've used for the front half of the panties and I lay it down the same way: with the stretchy side widthwise.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the outline of the panties.</strong> You can use a pattern or do it by eye - the key requirement is keeping the same width down the bottom at the front and at the back.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out a half of the panties, bend it over the central line, and cut out the other half.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>The panties are ready. I hide the seam allowances of the mesh fabric under the neoprene.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I smear the seam allowances with Gutermann glue and glue them to the base.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Now the panties are secured in place on the dress-form. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image052.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I cover the back with mesh at the top and pin it to the dress-form.</strong> It�s the same mesh I used for the panties and I place it stretchy side widthwise. The mesh is supposed to hold the dress! I pin it down leaving seam allowances for sewing in the sleeves and sewing the shoulder seams. I pull the mesh fabric up while pinning it down to prevent it from slacking. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>And again I take the glue and smear the edges of the mesh fabric along the neoprene edges the same way I did at the front.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully trim the seam allowances. </strong>Now the back of the dress is fully covered by mesh in the middle. I wait for the glue to set completely and start working with the lace.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2021.files/image058.jpg" class="img-responsive center-block"></p>

