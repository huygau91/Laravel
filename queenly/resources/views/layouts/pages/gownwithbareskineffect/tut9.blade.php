<h2>Working on the Skirt.</h2>
<h3>Tutorial 8. Making a Gore Pattern for the Trumpet Skirt.</h3>
<p></p>
<p>Let's work on the skirt while waiting for the spray foam to set.</p>
<p><strong>Our dress features a trumpet skirt (aka mermaid skirt).</strong> We need to cut out six sew-in panels or wedges. </p>
<p><strong>How do you make a pattern for the gore of a classic trumpet skirt?</strong> You take the waist circumference, stomach circumference and hip circumference and divide each by the number of the gores. And then you build the gore depending on the difference in the waistline, stomach line and hip line sections. </p>
<p>But our dress is quite exotic: <strong>the skirt mainly starts at the hip level</strong> and only partially at the waistline since the sides of the dress are see-through and there are no darts around the waist or the stomach. All darts above or below the waistline of our dress are in the side seams.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image002.jpg" class="img-responsive center-block"></p>
<p>The front of our skirt features three gores and so does its back. Since there are no darts, the task of building the gore of the skirt will be easier than ever. </p>
<p><strong>I divide the hip circumference (98cm) by the number of gores (6) and get 16.3cm. </strong>I'm only going to draw a half of the gore to make it easier. So I divide 16.3cm in half and get about 8.2cm. </p>
<p><strong>Why am I placing so much importance upon the precision of our calculations?</strong> Suppose we make a half of each gore 8cm wide instead of 8.2cm. The lacking 0.2cm multiplied by 12 seams will narrow the circumference of the skirt by as much as 2.4cm. That's why this 0.2cm difference in the width of a single gore half is truly crucial when you're cutting a trumpet skirt! </p>
<p>I've decided to leave some extra room just in case and make a half of each gore 8.3cm wide. I can always use the side seams to narrow the skirt or opt for a slightly looser fit later. </p>
<p><strong>Let's start building the gore of the skirt.</strong></p>
<p><strong>I draw a straight line and mark the skirt length that is 120cm.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I draw the waistline, step 19cm below it and draw the hip line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the width of a half of the gore (8.3cm) plus a 1.2cm seam allowance along the hip line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to decide where we want the skirt to start narrowing down.</strong> It usually narrows down around the knees, about 55-57cm below the waistline. You could also start narrowing it just above the knees: for instance 50cm below the waistline. In this case the skirt would appear more graceful but it'd be harder to walk in it.&nbsp; </p>
<p><strong>I mark the beginning of the narrowing area with a line 50cm below the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image010.jpg" class="img-responsive center-block"></p>
<p>I wouldn't recommend making it too narrow. Please keep in mind that narrowing the gore by 0.5cm from one side will narrow the entire skirt by 6cm (since there are 12 seams), which equals shifting each side seam inwards by 3cm. </p>
<p><strong>I've decided to narrow the gores by 0.7cm from each side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The narrowing starts 1.5cm below the hip line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s see how large the gore should be.</strong> I suggest expanding it by about 20cm for a beautiful flare. </p>
<p><strong>I fold the pattern along the centre of the gore. Then I mark 21cm from the narrow-down point on the bottom edge of the skirt</strong> and draw a half of the gore.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Next I draw the bottom edge of the gore. </strong>I transfer the length of the central fold line (70cm) onto the flare line. <strong>And I round off the line of the beginning of the flare.</strong> </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>As you can see, you just draw the gore and the flare however you please.</strong> My gore flares by 20cm. You can expand it more if you think it's not enough. And if it looks way too full, just remove a couple of centimeters to make the flare more modest.</p>
<p><strong>That's how simple it is to build the gore of a trumpet skirt. I cut out the gore and I'll use it for cutting the skirt. </strong>I also account for a seam allowance of 1.2cm.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make notches at the beginning of the flare and on the hip line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image026.jpg" class="img-responsive center-block"></p>
<p>I will cut the back of the skirt as two separate halves with a seam in-between to insert the train. The train starts rather high up in skirts like this. </p>
<p>That's why <strong>I make a notch in the middle of the gore where the zipper is supposed to end, 20cm below the waistline, and then I step 8cm down from that point and make a notch to mark the beginning of the train.</strong> The train looks particularly impressive when it�s placed high enough. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The skirt pattern is ready! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%208.files/image030.jpg" class="img-responsive center-block"></strong></p>

