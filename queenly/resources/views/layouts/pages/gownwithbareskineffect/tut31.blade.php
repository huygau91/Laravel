<h3>Tutorial 30. Alterations After the Second Fit Test. Finished Garment.</h3>
<p></p>
<p><strong>We have nearly completed all work.</strong> I believe it would've been unfair if I hadn't shared the results of the second fit test with you. I was hoping the back of the dress would fit perfectly after I pulled the dress up in the shoulders. But it turned out <strong>another important adjustment had to be made</strong>: I'm talking about that very dart that goes from the side seam to the edge of the neckline. It was impossible to do without it! Although the dart is not very deep, the back fit like a second skin as soon as I made it!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Don't stress if you notice an error like this on a basically finished garment with a finished lace overlay.</strong> It's not the end of the world if you notice that the dress is not going to fit well without a particular adjustment! It's easy to unglue the lace from the base or the mesh fabric if you press the required area with a warm (not hot) iron for a few seconds. </p>
<p><strong>What did I do? </strong>I unglued that area of the lace, sewed up the dart, then notched and restored the lace. The dart seam is entirely disguised.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image004.jpg" class="img-responsive center-block"></p>
<p>You do the same if you notice a trouble with the side seam of a finished dress. </p>
<p><strong>Now I start working on the inside of the dress. I want it to look glamorous too.</strong> There are some raw and even glued edges left. I suggest overlaying them with delicate thin lace. Of course you'll have to sew it by hand very carefully (remember to secure it with a few drops of glue first).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I serge the hemline of the lining and the face and decorate it with lace using the sewing machine.</strong> Some lace will show from under the hemline. I will decorate the train with lace motifs as well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%205/transcript/Tutorial%2030.files/image012.jpg" class="img-responsive center-block"></p>
