<h1>Tutorial 3. Cutting the Mock-Up.</h1>
<p></p>
<p><strong>I have cut the pieces from paper, arranged them on the mock-up fabric, and pinned them down.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p>I have placed the pieces on the cross (perpendicular to the selvage) to spare some fabric. In fact, it doesn't matter how you place your patterns on cotton fabric.</p>
<p>In theory, the fabric is supposed to stretch in the direction of the crosswise grain and you are thus supposed to put patterns down along the lengthwise grain. But modern fabrics are different. Up-to-date materials, which we use for sewing actual garments and not mock-ups, are synthetic. Different manufacturers produce all-way-stretch fabric. The gleam of the fabric and the quality of vertical seams depends on the way you place your patterns. For example, if you cut certain satin-weave fabrics crosswise, they acquire a more noble kind of a gleam and the quality of vertical seams also becomes a lot better. That is to say, there are always exceptions to the rule which are determined by test and trial. </p>
<p><strong>Students often ask me, "Are your courses for professionals or for beginners? Can I start sewing corset-based wedding and evening dresses if Iâ€™m a newbie to sewing?"</strong> It is a very tricky question. It all depends on the quickness of your mind and your self-study background. </p>
<p>You are, for example, far from a complete newbie if you can easily tell the front half of the dress from its back half by looking at the pattern, if you can tell the top from the bottom, if you know how to sew seams and how to use the sewing machine! In this case you should be able to sew this kind of a garment! Besides, I try to supply all work stages with generous comments! </p>
<p>Quite many of my students had never sewn before getting acquainted with my sewing techniques. They started from my tutorials and from rather sophisticated garments, too. </p>
<p>On the other hand, certain experienced professionals stick to their own methods and theories and deny my approaches to the subject! They usually don't like it that I never build a separate pattern for each individual client. They thick it is wrong. Everyone chooses their own manner and methods of work. There are just as many professionals who share my point of view: they comprise the majority of my subscribers interested in the sewing of wedding and evening dresses. They agree with me relying on their own practical experience. They agree that my methods are fast and easy: i.e. aimed at producing sophisticated garments within a relatively short period of time. Therefore, these methods allow you to complete custom orders faster and make up for the money invested into the learning materials sooner. </p>
<p>I always encourage my subscribers to have no fear of the unknown but to give things a try! Instead of spending hours or even days on the theory, just take some plain fabric and start sewing. Only after sewing your first garment, you will know if sewing is your thing and if you understand the basics well enough or not yet!</p>
<p><strong>Back to work. I cut out all parts of the mock-up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to make all notches.</strong> </p>
<p><strong>Notches</strong> are your guides to control the location of major reference lines, the size of the garment, and many other things. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>In corset patterns, however, notches are not used as control points for aligning the pieces. This particularly concerns curvilinear pieces and cup pieces at the level of the bust line.</strong> Those notches only indicate the direction of the bust line, they are not to be used for aligning the pieces. </p>
<p><strong>The point for aligning the pieces (point A) is the intersection of the bust line and the seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the central front piece to the side front piece in point A.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And I turn the pattern about the needle to align the joining lines.</strong> As you can see in the picture below, the pieces are perfectly aligned while the notches are shifted against each other. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>After I join the front pieces in the curvilinear area, the notches below the under-bust line will get aligned.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the paper: all parts of the mock-up are ready now. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>

