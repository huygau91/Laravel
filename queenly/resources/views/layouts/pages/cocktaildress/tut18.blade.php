<h1>Tutorial 18. Sewing a Zipper into the Back of the Dress.</h1>
<h2>Sewing Down the Princess Seams.</h2>
<p><strong>The front half of the dress is ready.</strong> There is no need in whipping the edges because the side seams will be fully covered up by the lining of the back.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image002.jpg" alt=""></p>
<p><strong>Let us work on the back of the dress.</strong></p>
<p><strong>I remove the paper patterns and separate the back pieces into the face and the lining</strong>. Each part of the back consists of two face pieces and two lining pieces. They are absolutely identical. I put the lining aside to work on the face now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image006.jpg" alt=""></p>
<p><strong>What are the tasks now?</strong> I need to sew down the princess seams and install a zipper into the face of the back. And then I will sew on a couple of decorative stripes. I will show you how to get them aligned in a beautiful and neat way!</p>
<p><strong>In addition, I will sew a shoulder strap</strong> and attach it to the dress. I will show you how to determine the right spot for sewing it on. There is a special formula.</p>
<p><strong>I have cut a shoulder strap that is 6.5cm wide and 50cm long.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image008.jpg" alt=""></p>
<p><strong>I fold it in half lengthwise and stitch it up with a 6mm seam allowance (the width of the presser foot).</strong> I don't want the shoulder strap to be too narrow because I plan to decorate it as well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image010.jpg" alt=""></p>
<p><strong>I use the traditional method of turning out shoulder straps:</strong> I leave a long enough thread tail at the end of the stitch, take a thick needle, thread it with the tail, and tie a regular knot. I insert the needle into the strap eye forward and run it through. And then I carefully pull on the thread, turn the strap out, and spread it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image016.jpg" alt=""></p>
<p>My shoulder strap does not stretch lengthwise. I purposely cut it that way to make sure it will perform the function of a support. <strong>I tear off the thread, roll the seam slightly to the side, and press it like that. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image018.jpg" alt=""></p>
<p><strong>And now let me show you how to determine the spot for attaching the shoulder strap.</strong></p>
<p><strong>If you want to place it at the same level where bra straps are normally placed,</strong> then you should measure the distance between your client's bra straps at the back, at the shoulder blade level or below (i.e. at the level of the top edge of the dress)<strong>. On my client, this distance is 26cm.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image020.jpg" alt=""></p>
<p><strong>Next, you take the central back piece and measure its length at the top. I got 12.5cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image022.jpg" alt=""></p>
<p><strong>It is important to account for seam allowances though: 2cm from one edge and 1.2cm from the other.</strong></p>
<p><strong>Therefore, </strong></p>
<p><strong>12.5cm - 2cm - 1.2cm = 9.3cm</strong></p>
<p><strong>9.3cm x 2 = 18.6cm</strong></p>
<p><strong>The distance between the princess seams of the zipped-up back is approximately 19cm. </strong></p>
<p><strong>Let me calculate how far I need to step inwards from the princess seam on the side back piece to reach the spot for attaching the shoulder strap.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image024.jpg" alt=""></p>
<p><strong>It is very simple: </strong></p>
<p><strong>26cm - 19cm = 7cm </strong></p>
<p><strong>7cm&nbsp; � 2 = 3.5cm</strong></p>
<p><strong>Therefore, if I exclude the 1.2cm seam allowance and mark 3.5cm inwards from the princess seams, I will reach the very middle of the shoulder strap.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image026.jpg" alt=""></p>
<p><strong>Now I know where I need to place it. </strong>But since the dress is asymmetric, with a shoulder strap only on the left side, I will not mark anything yet. First I will assemble the back of the dress, install the zipper, and only then mark the spot for attaching the shoulder strap from the required side.</p>
<p><strong>I suggest we should start by&nbsp;installing&nbsp;a blind zipper between the central pieces of the back.</strong></p>
<p><strong>First I sew the zipper tape onto the piece at a 2cm distance from the edge using the regular presser foot</strong>. I make a bar tack when I reach the slider.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image030.jpg" alt=""></p>
<p><strong>Since the fabric stretches vertically, I recommend you should mark control points at the level of the notches on the right and left halves of the zipper tape.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image032.jpg" alt=""></p>
<p><strong>I take the other central back piece and sew the other half of the zipper tape onto it.</strong> The control points allow me to put the zipper tape down correctly with regard to the notches. If you have any problems sewing a zipper into elastic fabric, I recommend you simply duplicate the sewing area with some dense iron-on material.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image036.jpg" alt=""></p>
<p><strong>The zipper is now secured in place. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image038.jpg" alt=""></p>
<p><strong>I double check if the notches on the central back pieces coincide when it's zipped up.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image040.jpg" alt=""></strong></p>
<p><strong>Next, I switch to a special invisible zipper presser foot.</strong></p>
<p><strong>I position the groove of the presser foot over the line of zipper teeth and start stitching.</strong> I stop at the slider (since the sewing machine won't stitch further) and make a bar tack.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image044.jpg" alt=""></p>
<p><strong>And then I do the same thing on the other half of the zipper tape.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image046.jpg" alt=""></p>
<p><strong>Then I close the zipper. I double check it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image050.jpg" alt=""></p>
<p><strong>And I switch to a left-toe presser foot.</strong></p>
<p><strong>I put together the seam allowances of the zipper seam somewhat folding the zipper tape in half inwards, align the notches, and pin it down.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image052.jpg" alt=""></p>
<p><strong>I find the spot where I stopped sewing on the zipper tape itself.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image054.jpg" alt=""></p>
<p><strong>I place the needle as close as possible to this spot, lower the presser foot, and start sewing.</strong> Instead of making a bar tack by stitching to and fro, I make a couple of stitches forward and then lift the presser foot, lift the needle, return to the beginning of the stitching, and continue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image056.jpg" alt=""></p>
<p><strong>Everyone knows that a one-sided presser foot does not make very neat seams.</strong> Which is why I do the following: <strong>I make a small row of stitches with the one-sided presser foot</strong> just to pass the area where the zipper interferes with the stitching and where it's compulsory to stitch close to its teeth.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image060.jpg" alt=""></p>
<p><strong>Then I switch back to the regular presser foot and sew the rest of the seam.</strong> In this case, the seam will have good quality. <strong>NB:</strong> try to get right into the stitches, into the needle-pierced holes of the previous seam, to continue it perfectly evenly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image062.jpg" alt=""></p>
<p><strong>I reach the notch that marks the beginning of the slit and make a bar tack by stitching backwards. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image064.jpg" alt=""></p>
<p><strong>I remove the pins, unfold the piece, and study the area where the zipper blends into the seam.</strong> This transition will be basically unnoticeable after I press the garment.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image066.jpg" alt=""></p>
<p><strong>Now that the zipper is installed, I can sew down the vertical princess seams of the back.</strong> I sew with a 1.2cm seam allowance and make bar tacks at the beginning and the end of sewing. Aligning the notches is compulsory because the fabric stretches vertically!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image068.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2018_files/image070.jpg" alt=""></p>
