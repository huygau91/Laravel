<h1>Tutorial 14. Adjusting the Top Edge of the Dress.</h1>
<h2>Cutting the Decorative Fabric Stripes.</h2>
<p><strong>I put the pressed front half of the dress on the dress-form with the seam allowances on the outside. </strong>I need to adjust the neckline in accordance with the design we have discussed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image002.jpg" alt=""></p>
<p><strong>In the mean time, I want to reveal a small secret and show you how to use the dress-form to carry out a fitting of a corset garment.</strong> Although we are working on a non-corset dress, I have promised to share some corsetry-related tricks in the process. I believe you have already realized that you need to master the skills of sewing bust princess seams and pressing cups before you can start working on an actual corset garment. And if we had sewn a mock-up after the <strong><em>adjusted pattern</em></strong> and then put it on the dress-form, we would have basically got a copy of the client's figure. <strong>In other words, it would be a custom dress-form for your client!</strong> Moreover, that dress-form would be soft (at least, in the waist, which is exactly where you want it to be soft).</p>
<p><strong>This dress base is the lining.</strong>&nbsp; On the inside, all seams in contact with the skin will be smooth and neat.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image004.jpg" alt=""></p>
<p><strong>The part of the dress which we have called an interfacing will be the face of the cups</strong>. I put the interfacing face side up, spread it carefully, and pin it onto the dress-form together with the dress aligning all notches and corners. Since the pieces were cut out simultaneously, all princess seams, notches, lengths and widths coincide perfectly. I carefully align the top edges and secure them by inserting pins perpendicular to the edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image006.jpg" alt=""></p>
<p><strong>Now the dress-form shows exactly how the dress will look on the client:</strong> you can see how the corners are placed or whether or not you are happy with the shape of the pattern.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image008.jpg" alt=""></p>
<p><strong>I have drawn and adjusted the neckline according to my liking. You can see the result below.</strong> I have turned out the dress and put it on the dress-form inside out.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image010.jpg" alt=""></p>
<p><strong>Now I can pin down and style the decorative black bar at the top.</strong> I will pin it down from the lining side, sew it on along the top edge and the armscyes, and turn the garment out.</p>
<p><strong>But first I need to decide on the width of the bar.</strong> I hadn't cut it in advance because it was impossible to predict everything. This task can only be solved now, on the dress-form. The decorative bar is not supposed to go far beyond the apex of the bust - otherwise it will not settle down neatly but will stick out.&nbsp; <strong>I believe it should be 6.5cm wide.</strong> Since it is double-layer, I need a strap with the width of 13cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image012.jpg" alt=""></p>
<p><strong>I cut the strap in the direction in which the fabric stretches because it will settle better upon the bust in this case.</strong> I put it down on the garment and check how it looks. I can tell it will look better if I make it asymmetric: narrow it down in the corner and leave it wide at the opposite side. I pin it in place.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image016.jpg" alt=""></p>
<p><strong>And then I cut it out roughly making sure not to catch the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image018.jpg" alt=""></p>
<p><strong>I take the dress off the dress-form. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image020.jpg" alt=""></strong></p>
<p><strong>And I trim the excess of black fabric above the top edge.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image022.jpg" alt=""></p>
<p><strong>Here is what I got: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image026.jpg" alt=""></p>
<p><strong>I need to add another really small strap along the armscye.</strong> It will lie on top of all layers and then, after I stitch along the neckline of the dress, I will turn it out and it will be at the bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image030.jpg" alt=""></p>
<p><strong>I adjust the shape of the strap in correspondence with the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image032.jpg" alt=""></p>
<p><strong>Here is what it looks like from the inside. </strong>I have pinned it on in the actual size. Such things are hard to predict during the cutting. Besides, we would have spent a lot of time on calculating, adjusting, and aligning everything.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2014_files/image034.jpg" alt=""></p>
<p><strong>All I need to do now is finish the armscyes and the top edge and turn it out.</strong></p>

