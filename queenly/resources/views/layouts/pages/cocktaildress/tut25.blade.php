<h1>Tutorial 25. Finishing the Zipper and the Slit with the Lining.</h1>
<h2>Pictures of the Finished Garment.</h2>
<p><strong>I have pressed the side seams. You should keep spreading out the lining to make sure the seam is even when you do it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image002.jpg" alt=""></strong></p>
<p><strong>Part of the seam allowance of the central seam on the lining (with a bone sewn onto it) has a zipper next to it. I will sew it to the zipper tape by hand.</strong> There is no way of getting there with the sewing machine now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image004.jpg" alt=""></p>
<p><strong>And now let us work on the slit at the bottom of the dress. </strong></p>
<p>To prevent the lining from pulling on the slit of the dress, <strong>I put it onto the face with a 0.5cm shift upwards. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image006.jpg" alt=""></p>
<p><strong>I shift the lining upwards and secure it with a pin to fix the seam in place.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image008.jpg" alt=""></p>
<p><strong>Then I remove the paper backing from the fusible web at the bottom of the face part of the back of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image010.jpg" alt=""></p>
<p><strong>I need to make a neat triangle. </strong></p>
<p><strong>I take the bottom point of the slit and fold it diagonally so as to form a corner aligning the raw edge of the slit with the bottom edge of the dress.</strong> The bottom edge of the dress is serged and has a fusible web on it. The seam allowances coincide: 2cm for turning the hem and 2cm for finishing the slit.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image012.jpg" alt=""></p>
<p><strong>I will need to stitch from the intersection of the bottom fold line and the seam allowance to the bottom edge at a 45 degree angle.</strong> I place a pin to mark the direction of the stitch.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image013.jpg" alt=""></p>
<p><strong>And I make a row of strengthening stitches. The result is a neat corner which you can see below: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image015.jpg" alt=""></p>
<p><strong>I trim the corner. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image016.jpg" alt=""></p>
<p><strong>And I turn it out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image018.jpg" alt=""></p>
<p><strong>You can see the turned hem of the bottom edge left of the corner and the turned edge of the slit right of it. </strong>I could serge the edges of the slit but I won't do it because they will be finished with the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image020.jpg" alt=""></p>
<p><strong>I take the serged bottom edge of the lining. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image022.jpg" alt=""></p>
<p><strong>I fold it and place it at the point where the corner stops. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image024.jpg" alt=""></p>
<p><strong>And I secure it with a pin.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image026.jpg" alt=""></p>
<p><strong>Then I turn out the slit and carefully pin together the seam allowances of the face and the lining.</strong> The seam allowances settle together neatly, with no creases or tucks, because I shifted the lining 0.5cm upwards in the area of the slit and trimmed the bottom edge correspondingly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image028.jpg" alt=""></p>
<p><strong>Now I need to find the end point of the central seam of the back on the face of the dress. I will secure the slit with a row of strengthening stitches made along this side and until this very point. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image030.jpg" alt=""></p>
<p><strong>I make a row of strengthening stitches. I start from the triangle at the bottom. </strong>I stitch without bar tacks: I will study it from the other side and see if I'm happy with the result.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image034.jpg" alt=""></p>
<p><strong>I need to get right into the end point of the central seam of the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image038.jpg" alt=""></p>
<p><strong>Now I can finish the stitch from whichever side is more convenient because I made no bar tack at the beginning.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image042.jpg" alt=""></p>
<p><strong>The lining and the corner look very neat!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image044.jpg" alt=""></p>
<p><strong>It is best to sew a clean-finished seam so it's easier to press the slit.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image046.jpg" alt=""></p>
<p><strong>The clean-finished seam is made over the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image050.jpg" alt=""></p>
<p><strong>The hem looks like this when the corner is finished:</strong> I need to press and fuse the hem in place now. If you don't like the idea of leaving the lining like this, you can secure it with fusible web the same way as the hem of the face.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image052.jpg" alt=""></p>
<p><strong>The same procedures need to be done from the other side of the slit. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image054.jpg" alt=""></p>
<p>First of all, I pin the hem and the slit together at a 45 degree angle. Then I stitch a triangle, trim it, and turn it out. And I make a seam with a clean finish along the other side of the slit. <strong>This is how you finish the slit and the hem of the dress.</strong> All I need to do now is press the garment.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image058.jpg" alt=""></p>
<p><strong>The slit of the dress is fully finished.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image060.jpg" alt=""></p>
<p><strong>I have sewn the lining to the zipper tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2025_files/image062.jpg" alt=""></p>
<p><strong>And I have sewn the shoulder strap in its place at the front.</strong></p>
