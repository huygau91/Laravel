<h1>Tutorial 9. Review of the Dress Model and Materials Used.</h1>
<p><strong>It's time to talk about the dress we are going to sew.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%209_files/image002.jpg" alt=""></p>
<p>Some of you may get disappointed but others will probably be glad to hear that this dress is not yet a corset dress. It is a dress sewn after a corset-based pattern.</p>
<p><strong>This manual pursues the following goals:</strong></p>
<p>- I want to show you how to sew dresses of this kind;</p>
<p>- I want you to learn to work with a corset-based pattern to get a grasp of a range of techniques which you will later employ in the making of corset garments.</p>
<p>Corset-related patterns and techniques are also used for sewing regular (non-corset) evening dresses. As a rule, they are light cocktail dresses for small parties that don�t feature a corset. They can be sewn after corset patterns though.</p>
<p>The style I have chosen is equally suitable for very slender and for plump girls and women. It is basically universal. The horizontal stripes which you can see on the sketch will visually add some "volume" to a lean body and disguise any imperfections in the proportions of a full body.</p>
<p><strong>What are those horizontal stripes at the front of the dress? </strong>They are straps of fabric folded in half, i.e. a kind of drapes made as very wide straps. Once you master the process of making such basic drapes, you will be able to learn how to work with finer, narrower drapes. I will drape the dress on the dress-form.</p>
<p><strong>The triangular insets</strong> are pieces of decorative lace placed underneath the horizontal straps.</p>
<p>The front neckline of the dress will be finished with a <strong>decorative bar</strong>.</p>
<p><strong>There will also be one shoulder strap.</strong> The decorative element on the shoulder strap is supposed to disguise the spot where the strap is attached to the dress.</p>
<p><strong>There will be two horizontal stripes and vertical princess seams at the back.</strong></p>
<p><strong>You could also replace these vertical princess seams by darts </strong>instead of cutting the back through to the very bottom.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%209_files/image004.jpg" alt=""></p>
<p>The fabric I am going to use is synthetic and very "dry." It is difficult to iron this kind of fabric and there is a big chance the ends of the darts will show through the face of the dress. I have therefore decided to cut the dress through to the very bottom and use princess seams instead.</p>
<p><strong>I will install a blind zipper into the dress. </strong></p>
<p>We will go through the entire cutting and sewing process together. What is most important, I will share a lot of clever tricks and interesting nuances by sewing a non-corset dress after a corset-based pattern and using elements of the techniques normally used for corset garments. After mastering those techniques, you will be prepared for sewing your first very basic corset.</p>
<p>And then a broad scope of opportunities will unfold before you! At the moment, there are as many as 20 manuals and DVDs in the Corset Academy's collection, and we never stop working on it. Imagine how many more new and exciting things I am ready to share with you. Mind you, all these DVDs and manuals are different! Each of them contains new techniques, new ideas, new patterns and samples. In short, every new course does actually teach you something you didn�t know.</p>
<p><strong>The main fabric I will use for the dress is taffeta.</strong> This fabric is peculiar in that it stretches in the direction parallel to the selvage.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%209_files/image006.jpg" alt=""></p>
<p><strong>I will use 0.5m of the same kind but differently coloured taffeta for the bar at the top of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%209_files/image008.jpg" alt=""></p>
<p><strong>The triangular insets will be made of a narrow strap of lace fabric.</strong> I will cut it in half lengthwise. This lace fabric has a festoon edge which I will use as a decorative element.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%209_files/image010.jpg" alt=""></p>
<p><strong>These are all materials required for the dress.</strong> We have already adjusted the pattern after client's measurements: it's time to start cutting.</p>

