<p><strong>In conclusion, I would like to return to the question about the size of this particular pattern.</strong> It can only be determined by measuring the mock-up you have sewn yourself. </p>
<p><strong>Here is a concrete example:</strong> suppose you have been sewing for many years and you prefer to use a 1cm seam allowance. You don't feel comfortable working with a 1.2cm seam allowance. Trust me it will not affect the shape of the dress. But the dimensions of your mock-up will differ from the dimensions of a mock-up sewn with a 1.2cm seam allowance after the very same pattern. Your mock-up, sewn with a 1cm seam allowance, will have its own dimensions. And it is why I recommend you always sew mock-ups. </p>
<p><strong>It is very hard to measure a ready mock-up while it's lying flat on the table.</strong> It's best to put it on the dress-form. It is the next stage that I will explain to you. Sometimes you can get the person to try the mock-up on: for example, if you are sewing for yourself. But I wouldn't advise you to ask your client to try it on because the mock-up still requires adjustments and may look somewhat “strange.” </p>

