<h1>Tutorial 4. Sewing the Mock-Up.</h1>
<p></p>
<p><strong>Our next task is to join the pieces of the mock-up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I would like to repeat why it is a good idea to make a mock-up before working with the pattern. First of all,</strong> it allows you to apply adjustments. <strong>And then,</strong> it helps you acquaint yourself with the pieces. After sewing the mock-up, you will understand how to align the pieces, how to join them correctly, where to ease in the fabric and where to avoid it, and other similar nuances. We are sewing a rather simple garment now and you probably think there should be nothing hard in joining the four pieces. Indeed, it is easy! But there are garments (for example, see-through corsets) where the front alone consists of five separate pieces, the back – of another three pieces, and each cup is also made of three parts. You need to sew a mock-up for such garments at least to make sure you won't confuse the pieces and to double-check the size.</p>
<p><strong>Let us start by sewing down the bust princess seams. </strong>&nbsp;A mock-up is a great opportunity for some training. It's best not to make any bar tacks on the mock-up so you can easily rip the seams up and re-stitch them if necessary. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>NB: I hardly ever use pins in this case!</strong> You may pin the pieces together in certain rare cases when it’s impossible to avoid it. But even if such is the case, I definitely do not recommend basting them together. </p>
<p><strong>The only thing that might require adjustments and proper alignment from the very beginning is the bust line.</strong> Remember the control point I told you about. In order to align the central front piece with the side front piece, you need to align their bust lines and not the notches on them. You can use a pin to secure the pieces along the bust line. </p>
<p><strong>I spread out the seam and stitch with a 1.2cm seam allowance.</strong> I remove the pin when I reach it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It is best to align the edge of the bottom piece with the guide line on the needle plate of the sewing machine</strong> which determines the size of the seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I place the top piece over the bottom piece with my left hand.</strong> Sewing the bust princess seams is the most challenging task. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I continue sewing in a straight line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Seam allowances should be pressed open or flat. You can also topstitch them − open or folded to the side. It all depends on the technique. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest we should fold the seam allowances to the side and topstitch them on the mock-up.</strong> I do not recommend trimming them at this stage. Otherwise, you won’t be able to adjust the shape of the pieces later if necessary. You should not press the mock-up either: steam-setting and pressing always results in deformation and we don't want to deform anything. We want to preserve the initial dimensions of the pattern. </p>
<p><strong>I fold the seam allowance toward the centre of the piece, step 1-2mm from the seam, and make a strengthening stitch.</strong> I carefully spread the pieces apart. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Let us attach the other side front piece.&nbsp; </strong>I stitch from the top downwards. </p>
<p><strong>I pin the side front piece to the central front piece at the intersection of the bust line and the princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>I align their princess seam edges</strong>... </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>...and secure them with a pin.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>I use a 1.2cm seam allowance here.</strong> I remove the pin when I reach it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same thing I did just before: align the edge of the bottom piece with the guide line on the needle plate and adjust the other piece on top of it with my left hand. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>It's how you sew down the bust princess seam of a corset pattern.</strong> There is no need in basting or anything like that. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I fold the seam allowance toward the centre and topstitch it.</strong> It doesn't matter now whether you stitch from the top down or from the bottom up. The most important thing is to spread the seam allowance well to prevent it from curling up. Do not stretch it, simply spread it out carefully. It is very easy when you’re working with cotton fabric. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I have assembled the first half of the mock-up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image033.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to assemble the other half of the mock up (the back) just the same way.</strong> There are no particular tricks about sewing down princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image035.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>It is important not to confuse the right half of the back with the left one though.</strong> And it's easy to get confused because the fabric looks the same from both sides: it has no right and wrong side. </p>
<p><strong>Let me share a trick which will make it impossible to get confused here.</strong> After reaching the end of the first half of the back, I put the pieces of the other half of the back next to it in a vertically mirrored fashion (see below). This way I can be sure I won't confuse the pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image039.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowance.</strong> It doesn't matter to which side you fold it in this case. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image043.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image045.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image047.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The back of the mock-up is ready and now we can sew the side seams.</strong> Remember that the side seams are sewn with a 2cm seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image049.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%204_files/image051.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
