<h1>Tutorial 20. Sewing Decorative Stripes onto the Back.</h1>
<p><strong>I secure the stripes on the dress by sewing them on.</strong> I have undone the zipper to make it easier. You should fold the corner of the seam allowance near the zipper so it doesn't show on the face of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image004.jpg" alt=""></p>
<p><strong>This is what it looks like from the face:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image006.jpg" alt=""></p>
<p><strong>I secure the stripes on the other half of the back the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image008.jpg" alt=""></p>
<p><strong>I fold the corner and make a bar tack.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image010.jpg" alt=""></p>
<p><strong>And I zip it up. It looks great!&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image012.jpg" alt=""></p>
<p><strong>I turn the back wrong side up and trim the messy edges.</strong> I secure the stripes with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image020.jpg" alt=""></p>
<p><strong>Compare the two halves of the back and make sure they are symmetric at the top. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2020_files/image022.jpg" alt=""></p>
<p><strong>The back of the dress looks very even and pretty.</strong></p>
