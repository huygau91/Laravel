<h1>Tutorial 22. Finishing the Bottom Edge of the Back.</h1>
<p><strong>Let us press the top of the back of the dress.</strong> It is really easy thanks to the clean-finished seam: everything settles down the way it should naturally.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image002.jpg" alt=""></p>
<p><strong>It's time to finish the bottom of the back. </strong></p>
<p><strong>I have already serged the bottom edge of the face part of the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image004.jpg" alt=""></p>
<p>Now I need to compare the side seam configuration of the front and the back and adjust the bottom edge of the back after the bottom edge of the front. How do I do it?</p>
<p><strong>I put the face and the lining together face to face. </strong>I recommend you should do it from each side of the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image006.jpg" alt=""></p>
<p><strong>I align the hip-line notches of the front and the back, spread the back, and make a notch to mark the level of the bottom edge of the front.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image008.jpg" alt=""></p>
<p><strong>And then I do the same with the other half of the dress.</strong> Compare the lengths of both halves just to double check it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image010.jpg" alt=""></p>
<p>The seam allowance at the bottom of the back near the side seam is 3cm just as was planned. I narrow it down to 2cm near the slit. <strong>I fold the seam allowance along the bottom edge of the back and secure it by pressing.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image012.jpg" alt=""></p>
<p><strong>I overlay the back with the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image014.jpg" alt=""></p>
<p><strong>We want the lining to reach down right to the level of the hem fold line.</strong> Since the lining is rather thick, I am not going to hem it properly. I will simply trim the bottom edge and serge it without turning it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image016.jpg" alt=""></p>
<p>Our current task is to trim the bottom edge of the lining of the back with account for the hem seam allowance of the main part of the back.</p>
<p>I align the hip line notches <strong>on the side seams</strong> of the main piece and the lining of the back, find the bottom edge, and make a notch at the level of the hem fold line on the main piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image018.jpg" alt=""></p>
<p><strong>What concerns the slit, I suggest a slightly different solution.</strong> I purposely shift the notch that marks the slit on the lining 0.5cm upwards as compared to the notch of the slit on the main piece of the back in order to prevent the lining from pulling the main piece upwards. This makes the lining slightly shorter. I will now take this 0.5cm in account and adjust the length of the lining correspondingly. I make a notch at the bottom of the slit.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image020.jpg" alt=""></p>
<p><strong>This is how much of the lining needs to be trimmed.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image022.jpg" alt=""></p>
<p><strong>I do the same with the other half of the back.</strong> I align the hip-line notches, fold the hem, and make a notch. Next, I find the notch that marks the slit, shift the lining 0.5cm upwards against it, and determine the level at which it needs to be trimmed. I make a notch.</p>
<p><strong>I put the two halves of the lining together.</strong> The notches at the bottom are aligned perfectly! I connect the notches with a line and trim both halves of the lining simultaneously. Don't forget to restore the bar tacks at the bottom of the princess seams.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image024.jpg" alt=""></p>
<p><strong>I add a strap of fusible web at the bottom of the main back piece. I leave the paper backing on for now.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image028.jpg" alt=""></p>
<p><strong>I press the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image030.jpg" alt=""></p>
<p><strong>It looks like this with the bottom edge finished. </strong>Now I only need to serge the edges of the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2022_files/image032.jpg" alt=""></p>

