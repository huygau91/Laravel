<h1>Tutorial 2. Getting Acquainted with the Pattern.</h1>
<p></p>
<p><strong>Suppose you have already printed out all eight pages of the pattern.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image002.jpg" class="img-responsive center-block"> </p>
<p>I tried my best to make them easy to join. Take the first and the second page. <strong>They compose the central front piece of the dress.</strong> The pattern files are set precisely at the dimensions of A4 pages: 210mm wide and 297mm tall. Your printer might leave white borders along the perimeter of each page. Make sure to print everything in the actual size and don't be disturbed by the white borders â€“ it's just how your printer works. <strong>Do not trim these borders either! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image004.jpg" class="img-responsive center-block"> </p>
<p><strong>There is a 2cm seam allowance for gluing the pieces together.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image006.jpg" class="img-responsive center-block"> </p>
<p>Smear it with glue along the edge, <strong>align the pages, and glue them together.</strong> The centimeter grid will indicate the direction and the thumbnails of the patterns will show you what piece each page corresponds with.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image008.jpg" class="img-responsive center-block"> </p>
<p>I have purposely placed each piece on two pages to divide it into parts. It will then be easier to apply adjustments to the pattern, which will be done right on paper.</p>
<p><strong>The side front piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image010.jpg" class="img-responsive center-block"> </strong></p>
<p><strong>The side back piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image012.jpg" class="img-responsive center-block"> </strong></p>
<p><strong>The central back piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image014.jpg" class="img-responsive center-block"> </strong></p>
<p>Aligning and gluing the pages together is really as easy as pie. As the result, you get four pieces: two for the front and two for the back of the dress. <strong>Please note that seam allowances are included into the patterns.</strong> They are 1.2cm along the princess seams and the top edge and 2cm along the side seams and the central seam at the back. All of them are marked by dashed lines. I believe it is all clear!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image016.jpg" class="img-responsive center-block"> </p>
<p><strong>All necessary notches are also marked: along the bust-line, the under-bust line, the waistline, and the stomach line 12cm down from the waistline.</strong> The bottom edge of the pattern is at the hip-line level.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%202_files/image018.jpg" class="img-responsive center-block"> </p>
<p><strong>Let me highlight that this pattern is suitable for any kind of a single-piece skirt</strong> you may choose for your dress. It can be a straight skirt or any kind of a flared skirt: mermaid skirt, bell-shaped skirt, skirt with a slit at the front and a train at the back, etc. You can quite easily apply relevant adjustments to the pattern! Necessary guidelines are available in my manuals and on my DVDs.</p>
<p><strong>Let us return to the key questions:</strong> "What are the dimensions of the pattern? What size is it? What size will be the dress I get after cutting and joining the pieces?" Judging by your e-mails, these things tend to puzzle you the most. </p>
<p><strong>There are three ways you can go.</strong> </p>
<p><strong>First of all, you can measure the pattern,</strong> exclude the seam allowances along the major lines, and then add together the lengths of the required sections and multiply them by two. In this case, you will find out the bust circumference, the under-bust circumference, the waist circumference, the hip circumference, and the stomach circumference of the pattern, and you can also measure the clip. Therefore, you can determine all key measurements using the printed-out pattern. <strong>It is the easiest but the least accurate method.</strong></p>
<p><strong>The second method involves sewing a mock-up after the pattern</strong> to take all measurements directly off it. It is a more accurate way to determine the dimensions of the pattern because it accounts for the peculiarities of your sewing and measuring habits.</p>
<p><strong>The third option is to sew a proper garment after the given pattern</strong> and take the measurements off it. It is the most accurate but also the most time-consuming and difficult method.</p>
<p><strong>I suggest we go with the second method and sew a mock-up after the pattern. </strong>What does it mean? First, I will cut the pieces from paper and use them to cut the same pieces from mock-up fabric and then I will join the fabric pieces providing you with detailed explanations. </p>
<p><strong>What is mock-up fabric then?</strong> It is muslin, calico, or any dense but not too thick non-stretch fabric. You can even use an old bed sheet or a duvet cover as your mock-up fabric. It doesn't have to be anything special. Although to tell you the truth, I believe every seamstress who deals with custom orders must have some mock-up fabric in her arsenal. <strong>Mock-ups are used for modeling, re-shaping, and adjusting garments but their main function is to let you double-check the size of the pattern.</strong></p>

