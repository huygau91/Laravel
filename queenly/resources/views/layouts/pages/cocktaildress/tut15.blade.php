<h1>Tutorial 15.&nbsp; Finishing the Top Edge of the Front of the Dress.</h1>
<p><strong>I make a row of stitches along the top edge of the dress.</strong> I start with a seam allowance of 1.2cm and gradually bring it down to 0.5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image004.jpg" alt=""></p>
<p><strong>I make a bar tack as I reach the corner.</strong> Forget about turning it about on the needle! It is one of the key rules which my students know by heart!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image006.jpg" alt=""></p>
<p><strong>I remove the pins and notch out any excess of fabric that makes the seam allowances too thick.</strong> When you leave such thick areas on the seam allowances, they form bumps after you turn out the garment.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image008.jpg" alt=""></p>
<p><strong>I make a seam with a clean finish by folding the seam allowances to the side of the dress and stitching 1mm below the edge.</strong> The seam allowance should be spread out very evenly. Remember that you must never allow for the neckline to get stretched out widthwise or else it will never cling to the body.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image016.jpg" alt=""></p>
<p><strong>This is how the bar will look when I turn it out.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image018.jpg" alt=""></p>
<p><strong>I trim the seam allowance in the area of the corner. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image022.jpg" alt=""></p>
<p><strong>And I sew the bar on along the armscye with a 1.2cm seam allowance.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image024.jpg" alt=""></p>
<p><strong>Again, it's a seam with a clean finish.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image026.jpg" alt=""></p>
<p><strong>I trim the seam allowance in the area of the corner.</strong> Of course you cannot achieve a perfectly sharp corner with so many layers of fabric but it does make it look better.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image028.jpg" alt=""></p>
<p><strong>I make a row of strengthening stitches along the raw sides of the cups joining the face, the lining, and the bar at the top. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image032.jpg" alt=""></p>
<p><strong>The bar is sewn in place.</strong> It needs to be pressed now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image036.jpg" alt=""></p>
<p><strong>It looks like this from the inside:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2015_files/image038.jpg" alt=""></p>
<p><strong>In conclusion, I would like to answer a question you are likely to ask: "Why haven't we inserted the shoulder strap in-between the layers?"</strong> I should say, I don't like installing shoulder straps this way because a shoulder strap is an individual element. And I always pin it in place with the garment on the client. Not even on the dress-form but on the client herself. And particularly, when I deal with asymmetric garments like ours. The way the shoulder strap will settle upon the body depends on the angle and the direction. And it is basically impossible to foretell before you assemble the dress. That's why there is nothing wrong in sewing the shoulder strap onto the dress by hand later. I will simply use a decorative element to conceal the joining area.</p>

