<h1>Tutorial 21. Finishing the Top Edge of the Back of the Dress.</h1>
<p><strong>I join the decorative piece with the main piece. </strong>I stitch at a presser-foot distance from the edge at the sides. This seam will be disguised by the seam allowance of the side seam. Since I adjusted the shape of the back, I need to make a row of strengthening stitches along the top edge as close as possible to the edge of the decorative piece to make sure the seam does not show on the face of the garment. When I finish sewing, I trim all thread tails and remove the pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image008.jpg" alt=""></p>
<p><strong>Let us finish the top edge of the dress with the help of the lining.</strong></p>
<p><strong>I put the face and the lining together face to face.</strong> Please note that the central seam of the lining is still open.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image010.jpg" alt=""></p>
<p><strong>I pin the right half of the lining to the face along the top edge and the side seam edge.</strong> I have noticed that my fabric gets deformed when I press it. That's something new. See why it is difficult to answer when you ask what fabric you should use? Each time there are different fabrics, different manufacturers, and different dress models. Sewing a new garment is always a new task, a new riddle that needs to be solved.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image012.jpg" alt=""></p>
<p><strong>The shoulder strap needs to be sewn into the left half of the back. </strong></p>
<p><strong>First of all, let us determine where exactly the shoulder strap needs to be placed.</strong> I mark 3.5cm inwards from the side seam in correspondence with our calculations. It is supposed to be the level of the middle of the strap. I make a notch.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image014.jpg" alt=""></p>
<p><strong>I put the shoulder strap onto the face of the dress face down and parallel to the princess seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image016.jpg" alt=""></p>
<p><strong>I secure the strap in place with two pins to prevent it from shifting.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image018.jpg" alt=""></p>
<p><strong>I cover the strap up with the left half of the lining and pin it all together along the top edge and the side seam edge</strong>.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image020.jpg" alt=""></p>
<p><strong>I have pinned together the face and the lining.</strong> Make sure to align the top edge of the lining with the level of the zipper (so you sew it on at the same level).</p>
<p><strong>I make a row of joining stitches with a 1.2cm seam allowance from the face side.</strong> Why from the face side? Because there you can see the seam which joins the face of the dress with the decorative piece and you can use it as a guiding line for your joining stitches. Besides, it's where the zipper is. That's why you will be able to see the beginning of the zipper and make sure both halves of the lining are sewn on at the same level. Remember to make bar tacks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image022.jpg" alt=""></p>
<p><strong>You need to pass onto the other half of the lining as neatly as possible.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image024.jpg" alt=""></p>
<p><strong>I sew on the other half of the lining and the shoulder strap. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image026.jpg" alt=""></p>
<p>And then I remove the pins and check the quality of the seam. It's important to check how well the lining is sewn on near the zipper. It should be even.</p>
<p><strong>I zip it up and check it. The two halves are aligned perfectly!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image028.jpg" alt=""></p>
<p><strong>I unzip it, fold all seam allowances to the side of the lining, step 1-1.5mm from the seam, and make a seam with a clean finish on the lining.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image034.jpg" alt=""></p>
<p><strong>You should only trim the seam allowances</strong> after doing the clean-finished seam. I leave them at 3-6mm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image038.jpg" alt=""></p>
<p><strong>Now we can press the top of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2021_files/image040.jpg" alt=""></p>

