						<h1>Tutorial 23. Sewing Down the Side Seams.</h1>
						<p>
							<strong>I put the back onto the front face to face. </strong>I
							leave the lining of the back detached for now.
						</p>
						<p>
							<strong>You should carefully check how well all control points at
								the top are aligned. I align the notches along the entire side
								seam. </strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image002.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image004.jpg"
								alt="">
						</p>
						<p>
							<strong>And I check if the decorative stripes at the front are
								aligned with those at the back well enough.</strong> It looks
							great!
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image006.jpg"
								alt="">
						</p>
						<p>
							<strong>I carefully align the notch at the bottom of the back
								(the side seam edge) with the bottom of the front and secure it
								with a pin.</strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image008.jpg"
								alt="">
						</p>
						<p>
							<strong>And I wrap the bottom edge of the front in the hem of the
								back. </strong>I secure it with a pin.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image010.jpg"
								alt="">
						</p>
						<p>
							<strong>I do the same with the other half of the back.</strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image012.jpg"
								alt="">
						</p>
						<p>
							<strong>I sew down the side seams with a 2cm seam allowance.</strong>
							I pull the lining up because it is not supposed to be involved in
							the sewing of the side seams. I place the needle right at the
							intersection of the lining-finished top edge of the back and the
							top edge of the front at a presser-foot distance from the edge.
							Here is how I make the bar tack: first I sew a couple stitches
							forward and then I return to the initial point. pulling the
							thread, and continue stitching.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image014.jpg"
								alt="">
						</p>
						<p>
							<strong>I reach the hip line.</strong> I don't like the shape of
							the side seam at the level of the hip line: it is way too arched
							and won't look good on the body. I have decided to straighten
							this highly pronounced arc of the side seam. I draw a new side
							line and a new side seam 2cm from it.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image016.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image018.jpg"
								alt="">
						</p>
						<p>
							<strong>I reach the bottom of the dress. </strong>There is a
							turned hem at the bottom. I make a bar tack and remove the pins.
							Those decorative stripes at the front make the dress more
							resilient and durable.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image020.jpg"
								alt="">
						</p>
						<p>
							<strong>I check the quality of the side seam at the top.</strong>
							It's all good!
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image022.jpg"
								alt="">
						</p>
						<p>
							<strong>Let's move on to the other side seam.</strong>
						</p>
						<p>
							<strong>First, I sew a small area of the side seam from the face</strong>
							so as to get into the very intersection of the lining-finished
							top edge of the back and the top edge of the front at a seam
							allowance distance from the edge. Trust me, you will never get
							right into that intersection point if you sew from the other side
							of the dress! I make a bar tack.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image024.jpg"
								alt="">
						</p>
						<p>
							<strong>And then I turn the garment around and continue sewing
								the side seam trying to get into the very same needle-pierced
								holes. </strong>
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image026.jpg"
								alt="">
						</p>
						<p>
							<strong>I reach the hip line.</strong> Again, I will somewhat
							straighten the side seam at the hip level like I did from the
							opposite side of the dress. I re-draw the side line, step 2cm
							from it, and draw a new side seam.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image028.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image030.jpg"
								alt="">
						</p>
						<p>
							<strong>I reach the bottom of the dress. </strong>I make a bar
							tack and remove the pins.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image032.jpg"
								alt="">
						</p>
						<p>
							<strong>Right now you can check how well the decorative stripes
								merge together in the side seam.</strong> As you can see, they
							are perfectly aligned and the lace insets look neat.
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image034.jpg"
								alt="">
						</p>
						<p>
							<img class="img-responsive center-block"
								src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2023_files/image036.jpg"
								alt="">
						</p>
						<p>
