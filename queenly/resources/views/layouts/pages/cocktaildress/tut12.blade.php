<h1>Tutorial 12. Duplicating the Interfacing.</h1>
<p><strong>Let me make a small digression from the main subject.</strong> I have already told you how I set up my cutting table. Now I would also like to share my easy-and-quick method of turning the cutting table into a pressing table.</p>
<p><strong>I have sewn the following construction for my table.</strong> I took four layers of thick cloth the size of the table surface and joined them by quilting them with large stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image002.jpg" alt=""></p>
<p><strong>I put a fabric case on top of this "mat" (similar to a stretch mattress cover).</strong> It is really easy to put the case on the mat: the elastic band wraps around the table and spreads the fabric evenly in a matter of seconds.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image004.jpg" alt=""></p>
<p><strong>I cover it up with a piece of light-coloured calico when I need to take pictures of the process.</strong> You don't need to do it: I only do it to make things look clearer on your screen.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image006.jpg" alt=""></p>
<p><strong>Thanks to all those tricks, I have a large pressing table</strong> which is easily transformed into a cutting table and vice versa. And I have access to the table from all four sides of it.</p>
<p><strong>I use a regular sleeve-pressing board for pressing seams, cups, or any narrow areas of the garment.</strong> I simply place the required area of the garment on the board and press it while the rest of it is hanging down at the sides.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image008.jpg" alt=""></p>
<p><strong>You can keep your press-iron on the table or on a separate stand.</strong> Mine stands on a separate mobile drawer unit. I can always roll it closer to the table or further from it.</p>
<p><strong>Let's get back to work though. I put the front pieces of the dress on the table.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image010.jpg" alt=""></p>
<p><strong>I remove the pins to separate the interfacing. </strong>It needs to be duplicated with iron-on batiste.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image012.jpg" alt=""></p>
<p><strong>I put the pieces together in a "sandwich":</strong> first I put a batiste piece fusible side up, then I put two interfacing pieces on top of it, and then I top it all up with another piece of batiste placed fusible side down.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image018.jpg" alt=""></p>
<p><strong>I carefully press the pieces from both sides.</strong> It's how you duplicate separately cut pieces to make them identical.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image022.jpg" alt=""></p>
<p><strong>I duplicate the interfacing of the central front piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image026.jpg" alt=""></p>
<p><strong>Central pieces are always somewhat asymmetric because of the fold line</strong> which causes mild distortion. It's why you need to adjust their shape right after duplicating them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image028.jpg" alt=""></p>
<p><strong>I have finished duplicating the interfacing. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image030.jpg" alt=""></p>
<p><strong>Now, before we start joining the front pieces, I recommend we press the central fold line flat.</strong> It's always there and not always easy to flatten out but you should try your best.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image032.jpg" alt=""></p>
<p><strong>If the side front pieces also require some pressing, it's best to do it now. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2012_files/image034.jpg" alt=""></p>
<p><strong>The pieces are ready for joining.</strong></p>

