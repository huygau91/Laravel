<h1>Tutorial 10. Cutting the Front of the Dress.</h1>
<p><strong>Finally, here comes the moment when we can start cutting the dress.</strong> I have folded the fabric in half. This fabric will serve as the base of the dress. The horizontal stripes will be sewn onto it.</p>
<p><strong>I mark 56cm from the bottom edge of the fabric: it is the waistline level.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image002.jpg" alt=""></strong></p>
<p><strong>The skirt is supposed to be 60cm long when finished </strong>so the bottom stripe should surpass the edge of the base by at least 4cm.</p>
<p>Looking at the sketch, you may wonder why I have arranged the patterns the way you do for a symmetric dress when it is clearly asymmetric.</p>
<p>The answer is simple: I will trim away the unnecessary corner and adjust the shape of the neckline in the process.</p>
<p><strong>I pin the central and the side pieces of the front onto the fabric. </strong>The dress will be perfectly straight so I simply prolong the pieces down to the very bottom. I try to align them perfectly so I can separate them by simply cutting through the princess seam line. I pin down the bottom parts as well to prevent them from shifting against each other. As you have probably noticed, there is no additional lining. I will cut a lining for the back of the dress though.</p>
<p><strong>What concerns the front, I will duplicated the cups with the same fabric and with iron-on batiste.</strong> The duplicating fabric will stop 4cm below the under-bust line. I will use 23cm of the fabric left above the top edges of the pieces for this purpose.</p>
<p><strong>I cut out each piece following the side seam line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image004.jpg" alt=""></strong></p>
<p><strong>I mark another 23cm upwards so I can fold the fabric and simultaneously cut out the same-fabric interfacing for the cups.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image006.jpg" alt=""></p>
<p><strong>I fold the piece at the top. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image008.jpg" alt=""></p>
<p><strong>I align the edge of the folded part with the border between the glued together pieces</strong> about 4cm below the under-bust line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image010.jpg" alt=""></p>
<p>I pin the layers together.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image012.jpg" alt=""></p>
<p><strong>And I need to add a layer of iron-on batiste, folded the same way, to duplicate the piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image014.jpg" alt=""></p>
<p><strong>I align the fold lines and the edges of the batiste and the main fabric and secure the layers with pins.</strong> I prefer cutting all pieces simultaneously: it makes them perfectly identical and easier to join.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image016.jpg" alt=""></p>
<p><strong>Now I can safely finish cutting the dress. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image024.jpg" alt=""></strong></p>
<p>If you are still in the process of equipping your workroom, I highly recommend that you place your cutting table in a way which will allow you to move around it freely. There is seldom enough space in workrooms. Here is how I have solved the issue: I use a plastic "picnic table" as my cutting table. It is 80cm wide and 180cm long when unfolded. It's the best possible size for cutting any wedding or evening dresses. There is absolutely no need in buying and setting up a huge fixed cutting table.</p>
<p><strong>Make sure to make all notches marked on the pattern</strong>: along the bust line, the under-bust line, the waistline, the stomach and the hip line. The hip line is the bottom edge of the paper pattern.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image026.jpg" alt=""></p>
<p><strong>I have simultaneously cut out all pieces including the iron-on batiste pieces for duplicating the cups.</strong> You can use any kind of a duplicating material: fusible webbing, soft charmeuse, etc. I use it to prevent the princess seams from showing through the face of the garment after I press them. Besides, it reinforces the cups and makes them smoother and more even. Cups always look prettier and neater when duplicated. I try to always do it if possible.</p>
<p><strong>I have cut out the front half of the dress. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2010_files/image028.jpg" alt=""></strong></p>

