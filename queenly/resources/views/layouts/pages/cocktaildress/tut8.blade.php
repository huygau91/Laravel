<h1>Tutorial 8. Constructing the Pattern Based on the Calculations.</h1>
<p><strong>I need to adjust the standard pattern after my client's measurement.</strong> To make things easier, I have printed out another set of patterns and glued together the four pieces of the corset.</p>
<p><strong>The central front piece:</strong></p>
<p><strong>The only alteration concerns the depth of the neckline. </strong>I draw the bust line, mark my client's "distance from the bust line to the neckline of the corset" measurement up from it (6cm), and add a seam allowance (1.2cm). In short, I mark 7.2cm above the bust line. I will re-draw the neckline later in correspondence with the adjusted clip on the side front piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image002.jpg" alt=""></p>
<p><strong>The side front piece:</strong></p>
<p><strong>The side seam of the front remains unchanged, the only alteration is applied to the clip measurement.</strong> According to the calculations, I need to remove 0.5cm from the clip toward the corner. There are no changes in the clip toward the armscye. But, looking at the mock-up, I feel like removing 1cm from the clip toward the corner. It will look nicer this way. I re-draw the corner. I will polish it up later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>The side back piece:</strong></p>
<p><strong>The entire side seam of the back needs to be shifted 1cm inwards.</strong> By the way, you can apply all alterations directly on fabric. There is no need in doing it on paper and then transferring the adjusted pieces on the fabric. It all comes with practice though.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image006.jpg" alt=""></p>
<p><strong>Next, I take the second copy of the piece and use it as a template to re-draw the side seam.</strong> I transfer all notches onto the new line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image008.jpg" alt=""></p>
<p><strong>The most important thing is the side length.</strong> You must preserve the initial value. Beginners make their biggest mistake when they make the side seam 1cm shorter and then draw the top edge of the piece in an arbitrary fashion. You must preserve the initial shape of the back! <strong>It's why I use the initial piece as a template to draw the top edge of the altered piece. And then I use the standard pattern to draw a smooth transition to the centre. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image010.jpg" alt=""></p>
<p><strong>It's a new pattern with new notches and the initial side length. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image012.jpg" alt=""></strong></p>
<p><strong>I need to apply changes to the princess seams at the back. </strong></p>
<p><strong>The changes are made at the bust level and at the stomach level.</strong> I need to shift the princess seam 0.7cm inwards at the level of the bust and bring it to the under-bust level at a nearly straight angle.&nbsp; There is no alteration at the waistline level. What concerns the level of the stomach, I need to shift the princess seam 0.5cm inwards and bring it to the hip level at a nearly straight angle. We have somewhat flattened the shape of the buttocks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image014.jpg" alt=""></p>
<p><strong>The central back piece:</strong></p>
<p><strong>The same kind of alterations should be applied from the other side of the back.</strong> I shift it 0.7cm inwards at the bust level and 0.5cm inwards at the stomach level. <strong>And I re-draw the princess seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image016.jpg" alt=""></p>
<p><strong>The top edges of the central and the side pieces of the back can also be adjusted with the help of the standard pattern.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image018.jpg" alt=""></p>
<p><strong>I have applied all alterations to the pieces of the back of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>Let us return to the neckline of the central front piece. </strong>I lower the neckline based on the mark I made and try to give it a beautiful shape by using the standard pattern as a template again.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image022.jpg" alt=""></p>
<p><strong>This is what the new central front piece looks like.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image024.jpg" alt=""></strong></p>
<p><strong>You can see the finalized side front piece in the picture.</strong> I have lowered the corner and shortened the front princess seam to align it with the central piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image026.jpg" alt=""></p>
<p><strong>And now I would like to show you a clever trick that I often use to check the shape of the neckline.</strong></p>
<p><strong>I fold the seam allowance of the central front piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image028.jpg" alt=""></p>
<p><strong>And I put the central front piece onto the side front piece so as to overlay its seam allowance. </strong>And I check the shape of the neckline. The shape of the neckline will look just like this when I join the pieces. <strong>I think it looks very neat!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%208_files/image030.jpg" alt=""></p>
<p><strong>I have constructed a pattern for my client.</strong> I have applied necessary alterations to the standard pattern of a corset dress to adjust it after my client's measurements. At the same time, I have fully preserved the shape of the side seam. The side seam is responsible for the beautiful corset silhouette. I have also preserved the front princess seam responsible for the beautiful shape of the cups. And, finally, I have used the princess seam of the back to adjust particular areas of the standard pattern which always require customization.</p>
<p><strong>Now I can start cutting the fabric.</strong></p>

