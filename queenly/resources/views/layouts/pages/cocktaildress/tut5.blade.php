<h1>Tutorial 5. Measuring the Mock-Up.</h1>
<h2>Determining the Dimensions of the Pattern.</h2>
<p><strong>I have put my mock-up on the dress-form. </strong>I always use pre-made foam shoulder pads for this purpose. You should always have some at hand, really.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image002.jpg" alt=""></p>
<p>Please note that my dress-form is a lot smaller than the mock-up. You need to balance out the proportions in order to put the mock-up on the dress-form correctly. The side seams should be vertical and the raw edges of the back should be strictly parallel to each other. <strong>It should look as if the mock-up were put on a real person with proportions similar to your client's. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image006.jpg" alt=""></p>
<p>I have put many shoulder pads underneath the mock-up to shape the bust and other parts of the "body." As the result, the mock-up sits just right. It is even slightly stretched. There are no obvious hollows or wrinkles.</p>
<p><strong>You can see everything on the mock-up now.</strong> You can see the shape of the neckline and the shape of the armscye. And you can adjust anything you are not happy with. Suppose you don't like the direction of the corner at the front.&nbsp; You can bend it and check how it will look then.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image008.jpg" alt=""></p>
<p><strong>Why am I offering you this particular pattern with this particular neckline shape?</strong> It is a tried and true pattern and it’s easy to work with it during further stages. The neckline is universal, too. If you need to adjust the pattern for a bigger bust size, you will spread the pieces apart and the front corners will get spread apart and get right in place automatically. It will work the same when you change the clip measurement and many other things. All these nuances are explained in detail in my manuals and on my DVDs. So whenever you want to alter a pattern, account for the peculiarities of each particular case (i.e. the model of the dress you are sewing, etc). What concerns the initial pattern, I recommend you leave it as is. You will agree with me after trying it yourself.</p>
<p><strong>We have a "figure" with the mock-up on it. And we need to measure this figure as if it were a person standing before us.</strong> You can tie cords around the main circumferences or simply mark all relevant lines on the mock-up. The notches are your control points for drawing those lines. <strong>I draw the bust line, the under-bust line, the waistline, and the stomach line 12cm below the waistline.</strong></p>
<p><strong>Let us also draw the clip. </strong>Always remember about the 1.2cm seam allowance at the top. I mark the seam allowance and the apex of the bust (point A).</p>
<p><strong>I draw the clip as if I were measuring it on the actual client. </strong>&nbsp;When you look at the mock-up on the dress-form, imagine the client whom you are supposed to take the measurement off.&nbsp; The clip toward the armscye should be measured as indicated by the arrow (1). Next, you can either measure another clip to the corner (2) or to the princess seam (3) depending on the dress model and available adjustment opportunities.</p>
<p>The clip toward the neckline is less important because we are dealing with a boat neck. It would be crucial if you were working on a V-neck though.</p>
<p>A much more important measurement in our case is the distance from the bust line to the neckline (4).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>I take a measuring tape and measure the mock-up the same way I would measure my client's body.</strong> And here comes another reason why my measurements differ from yours. The thing is that you might be used to taking measurements – say, the bust circumference – at a slightly different level and not along the lines I have marked on the mock-up.&nbsp; It is only natural that you will take each measurement in the same place where you usually take it off a client. And therefore, our measurements are going to be different!</p>
<p><strong>Now let me say a few words on the bust middle measurement.</strong> Many people ask me why my bust middle measurement tends to somewhat larger than usual on small-size garments and somewhat smaller than usual on large-size garments. You will find the answer in the definition of the bust middle measurement (the distance between the bust princess seams of a corset garment). This measurement helps me determine the location of the bust princess seams. In other words,<strong> the bust middle measurement indicates where the bust princess seams are supposed to be placed.</strong></p>
<p>When I look at a client standing before me, I immediately try to imagine how the garment should look on her, how to make her bust appear larger or smaller (if relevant), and re-shape her body in a flattering way. And so I need to decide whether I should place the bust princess seams further from the centre or bring them closer to it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image012.jpg" alt=""></p>
<p><strong>Such small things are responsible for the overall look of the corset!</strong> It is impossible to account for them by means of pure calculation. Calculation methods are based on statistically average data: they measured a couple thousands of people, took the average parameters, and created formulas based on them. No wonder these formulas don't necessarily lead to correct results when you are dealing with corset garments. No formula can show you the best way to take the bust middle measurement off a particular woman.</p>
<p><strong>That is all I wanted to say. Now let’s measure all key circumferences, heights, and lengths.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image014.jpg" alt=""></p>
<p><strong>When measuring the circumferences, remember that the central seam at the back is sewn with a 2cm seam allowance and the edges of the back are supposed to close without a gap when the garment is finished.</strong> It’s not always easy to achieve on the dress-form but you can always imagine that the edges of the back meet properly. And for this reason, you should include the seam allowances in the circumference taken off the mock-up. Remember about the seam allowance at the top when you measure the height of the back, too.</p>
<p><strong>And last but not least: my pattern provides for no gap between the edges of the back.</strong> But it is up to you whether you actually want those edges to meet or whether you want to make a gap between them. If you want a triangular opening at the back of your dress, simply mark it on the pattern and account for a seam allowance.</p>
<p><strong>You can see how to measure the under-bust circumference with regard to a triangular opening and a 2cm seam allowance in the picture below.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%205_files/image016.jpg" alt=""></p>

