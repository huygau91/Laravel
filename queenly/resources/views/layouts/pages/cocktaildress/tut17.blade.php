<h1>Tutorial 17. Decorating the Dress with Drapes (Continued).</h1>
<p><strong>I have used three wide stripes of fabric to decorate the straight part of the skirt.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image002.jpg" alt=""></strong></p>
<p><strong>Narrow stripes</strong> are supposed to be long enough to reach from one edge of the dress to the opposite edge. I have stitched them up with the machine, serged the edge, and fused them in place. Since all stripes have different lengths, I have labeled them with stickers with numbers: 1, 2, 3, 4, and 5.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image006.jpg" alt=""></p>
<p><strong>The fifth stripe is the last one so you need to fuse a strap of web onto its face.</strong> I then fold its edge the way you would fold a hem and press it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image010.jpg" alt=""></p>
<p><strong>Now I need to sew the stripes on one by one.</strong> You will have to account for the fact that prominent parts of the dress-form (or the figure) will always form darts which won't let the straps lie perfectly parallel to one another. They will meet at the ends but not in the middle. That's the main challenge of arranging these stripes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image012.jpg" alt=""></p>
<p><strong>I remove the paper from the web on the first stripe and put it in place.</strong> I can already tell that it's somewhat tilted down towards the hip. Try to arrange it in a straight line, with no creases or tucks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image018.jpg" alt=""></p>
<p>It is important to overlay the wide stripe with a lace inset properly despite the fact that the stripe tends to settle down in an arc. With this in mind, <strong>you should constantly keep checking the level at which the stripe is placed with the help of a measuring tape.</strong> If you don't keep this one at the right level, all subsequent stripes will go askew.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image022.jpg" alt=""></p>
<p><strong>I secure the first stripe in place with the press-iron. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image028.jpg" alt=""></p>
<p><strong>And I put down the second stripe.</strong> Make sure to maintain the same distance from the bottom edge of the stripe to the under-bust line along the side seams and the princess seams. I also check the distance from the bottom edge of the second stripe and the bottom edge of the first stripe in the area between the princess seams.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image032.jpg" alt=""></p>
<p><strong>I have decided to first pin all stripes onto the dress</strong> and then secure them one by one to make sure they're placed evenly and neatly. I remove the paper backing from the web before doing it though.</p>
<p><strong>I pin the third stripe in place. </strong>And I check if it's symmetric enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image034.jpg" alt=""></p>
<p><strong>There goes the fourth stripe.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image036.jpg" alt=""></p>
<p><strong>And the fifth stripe. Looks like I have successfully reached the top of the dress!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image038.jpg" alt=""></p>
<p><strong>Let me emphasize that accuracy and precision are very important in this kind of work!</strong> When you start sewing your first corset, always remember that you should be extremely precise and accurate as you sew. No corset will tolerate your saying "whatever, it will do!" More often than not, it will prove you wrong by showing "no, it will not do!" A corset requires absolutely perfect symmetry in everything that concerns both cutting and aligning the pieces. I have purposely chosen a dress model which allows you to practice these skills. I always tell newbies who join the Corset Academy that corset-making actually never starts from making a corset. You need to learn to sew simple cocktail-type dresses before you can move on to corset-based garments. Once you have mastered a range of necessary techniques, you will see that sewing a corset is not any more complicated than sewing a dress of this kind. Of course, there are certain nuances but, on the whole, sewing a corset-based dress basically involves all the same techniques we are using now.</p>
<p><strong>While working on this dress, you will learn how to use mock-ups and dress-forms to your advantage.</strong> By the way, I forgot to mention that I aligned all notches of the dress (waistline, stomach, and hip line notches) with the notches on the mock-up when I was putting the dress on the dress-form. By doing so, I made sure the dress sits on the dress-form correctly: balanced out and without distortion. See how handy it is with a mock-up on the dress-form!</p>
<p><strong>Very many people neglect my advice about sewing a mock-up.</strong> And I often receive those commonly asked questions in e-mails: "How can I measure the pattern? How can I adjust the pattern? This or that does not work, I don't get what the pattern is supposed to look like after I adjust it? I've adjusted my corset and put it on my daughter, why is it too big (or too small) now?" I write back saying "Sew a mock-up garment, put it on the dress-form or on your daughter, and study it. You will immediately notice any flaws and you will be able to figure out the dimensions of the pattern." Sometimes, I have a feeling that people get offended by it. They don't take my words serious and think I just wrote something quickly to get it over with whereas they were waiting for a long, detailed answer. But my answer is very simple:&nbsp; "Just take some fabric, any old bed sheets you don't need anymore, sew a mock-up, and put it on your dress-form! Trust me, the result will answer all your questions."</p>
<p>I hope many of you will now appreciate the idea of sewing a mock-up. Every time you start working on a new garment, you discover more and more previously unknown advantages of using the mock-up method. Sometimes you have no idea of what great help it will turn out to be!</p>
<p><strong>I remove the last, fifth stripe for now and start working with the stripes I have already pinned down.</strong> A little later, after I secure them in place by pressing, I will also be able to pin down the lace.</p>
<p><strong>I secure the stripes in place with the press-iron.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image052.jpg" alt=""></p>
<p><strong>There is the last stripe left at the top which still needs to be pinned down and secured by pressing.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image054.jpg" alt=""></p>
<p><strong>Many questions tend to arise when you're working with the stripe at the very top.</strong> It stays perfectly in place and does not shift about precisely because I fuse it on with the help of fusible web. Whoever tries to sew it on without fusing it on in advance is pretty much bound to fail. You see, the shape of the garment is rather uneven near the cups: it is curvy and this makes it hard to determine where you need to stitch. The stripe will hold in its place firmly if you fuse it on though.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image056.jpg" alt=""></p>
<p><strong>I need to place triangular lace insets underneath all fused-on decorative stripes before securing the last one in place.</strong> The last stripe should only be secured for good after you place a lace inset underneath it.</p>
<p><strong>I turn up the second stripe, put down the lace, and arrange it so as to make an inset symmetric to the previously made ones. I pin it down and trim the excess.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2017_files/image058.jpg" alt=""></p>
