<h1>Tutorial 6. Calculating Pattern Alterations.</h1>
<p><strong>Let’s continue working on the pattern.</strong> Before we start altering the pattern after client's measurements, I would like to say a few words to those who are only about to jump on the bandwagon of corset-makers and are not yet familiar with my pattern adjustment methods.</p>
<p><strong>My approach to corset garments, or my corset philosophy, is very simple!</strong> It revolves around the fact that any corset is a stiff construction aimed at re-shaping the human body and enhancing non-standard proportions. Our perception of beautiful body proportions does not change. If the corset has a graceful shape, then the wearer's body will also look graceful. In traditional clothing, we construct a pattern after a particular body with regard to its proportions and peculiarities to make sure the garment will have no creases and won't go askew − because traditional clothes are soft. It’s why we need to construct a new pattern each time. Traditional garments do not hold a fixed shape.</p>
<p><strong>But it’s an entirely different story with the corset!</strong> It is a stiff construction of a beautiful shape which requires a correct fit. The side seams should be placed correctly and the edges of the back should meet properly or, optionally, form a beautiful triangular opening. The shape of the breasts should look appealing and the length of the garment should be perfect. And client's measurements simply indicate the size of the body which this stiff construction (the corset) is supposed to accommodate.</p>
<p>Everyone agrees that the dress-form has beautiful, harmonious proportions. And it will stay beautiful if you make it wider or narrower without disturbing the proportions. <strong>My method of altering standard patterns, which I call the Adjustable Dress-Form Method, is based on this peculiarity.</strong></p>
<p>I suppose everyone knows what's an adjustable dress-form. It is usually divided into four sections, i.e. the body is divided in half horizontally and vertically. There is an adjustment dial in the middle of the dress-form which you turn to expand it or narrow it down. But what happens with the shape of the dress-form in this case? <strong>Nothing!</strong> The shape of the breasts, the side, the back, etc. stays the same. Only the size of the dress-form changes. The system I want to introduce you to is based on this very principle.</p>
<p>It's easy! We <strong><em>proportionally</em></strong> expand or narrow down the standard pattern to adjust it after the client's body size. As you can see, there is no need in having 1,000 separate patterns! You don't have to construct a separate pattern for each new client! Moreover, it doesn't even matter what size is the initial pattern that you have downloaded, printed out, glued together, and used to sew the mock-up to put on the dress-form. All that matters is the beautiful corset shape of the pattern!</p>
<p>Before adjusting the pattern, you need to study the distribution of the bust, under-bust, waistline, stomach, and hip circumferences: i.e. figure out how much falls upon the front and how much upon the back of the garment. <strong>The bust front measurement and the under-bust front measurement will help you determine it.</strong></p>
<p><strong>I suggest we should use a table to calculate adjustments for the pattern.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image002.jpg" alt=""></p>
<p><strong>The first column contains the key measurements which you need to take off your client and off the mock-up to adjust the pattern. They are:</strong></p>
<p>- Bust front,</p>
<p>- Under-bust front,</p>
<p>- Bust circumference,</p>
<p>- Under-bust circumference,</p>
<p>- Waist circumference,</p>
<p>- Stomach circumference. Make sure to mark the distance from the waistline in brackets. For example, I measured the stomach circumference 12cm below the waistline, both on the client and on the mock-up.</p>
<p>- Hip circumference. Again, remember to mark the distance from the waistline in brackets. This distance depends on whether your client has low-set or high-set buttocks. This measurement is particularly important for dresses. It is less relevant when you're sewing a corset as an individual garment. I took this measurement 19cm below the waistline, both on the client and on the mock-up.</p>
<p>- Bust middle. As I have already said, this measurement is the distance between the imagined lines <strong>along which you want to place the bust princess seams.</strong></p>
<p>- Clip,</p>
<p>- Distance from the bust line to the neckline of the corset.</p>
<p><strong>These are all required measurements. </strong></p>
<p><strong>The second column of the table contains client's measurements. </strong></p>
<p><strong>And the third column contains the dimensions of the initial pattern.</strong> In other words, they are measurements taken off your mock-up.</p>
<p>To make things easier for beginners, I have chosen a client whose body proportions are close to the proportions of the pattern. If you want to learn how to apply more serious adjustments to the standard pattern, keep in mind that I explain the process of pattern adjustment in detail in every manual and on every DVD. Besides, I have prepared special pattern adjustment tables for you, which account for ten different body types. You can download these tables together with my free tutorials by subscribing to the Corset Academy’s newsletter. And right now my task is to explain <strong>the very principle of constructing a pattern</strong> for a particular client by adjusting the standard pattern.</p>
<p><strong>Let's get straight to work.</strong></p>
<p>All adjustments will be applied to the contour of the side seam of the front half of the dress (column 4)...</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image004.jpg" alt=""></p>
<p>...and the contour of the side seam of the back half of it (column 5).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image006.jpg" alt=""></p>
<p>After performing the calculations, I will adjust the configuration of the side seam of the back of the dress (column 6).&nbsp; And, finally, I will use the princess seam at the back (column 7) to balance out all adjustments in the side seams of the front and back and adjust the fit after the client's size.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image008.jpg" alt=""></p>
<p><strong>The first thing you need to do is determine the configuration of the side seam on the front half of the dress.</strong> All calculated values should be recorded in column 4.</p>
<p>Two measurements are responsible for the configuration of the side seam or the balance of the garment: bust front and under-bust front.</p>
<p><strong>Bust front measurement:</strong> 49cm on the client and 49cm on the pattern. The measurements coincide.</p>
<p><strong>49cm - 49cm = 0</strong></p>
<p><strong>Under-bust front measurement:</strong> 40cm on the client and 40cm on the pattern. The measurements coincide.</p>
<p><strong>40cm - 40cm = 0</strong></p>
<p>These values show that the configuration of the side seam on the pattern coincides with the desired configuration on the garment. In other words, I don’t need to change the shape of the side seam at the front. It follows that <strong>all adjustment values for the side seam of the front are zero in my client's case. I write zeroes in lines 1-7 of column 4.</strong></p>
<p><strong>The bust middle measurements fully coincide on the pattern and on the client and equal 20cm.</strong> Since there are no alterations in the middle of the piece, the edge of the pattern will be aligned with the fold line of the fabric. In other words, I don't need to shift it inwards or outwards. <strong>I write 0 in line 8 of column 4. </strong></p>
<p><strong>Let’s look at the clip measurement. </strong></p>
<p><strong>- Towards the armscye:</strong> 11.5cm on the client and 11.5cm on the pattern. The measurements coincide.</p>
<p><strong>- Towards the corner:</strong> 11.5cm on the client and 12cm on the pattern.</p>
<p><strong>11.5cm - 12cm = -0.5cm</strong></p>
<p>I need to remove 0.5cm in the corner. But I don't think it makes sense to actually do it. I will decide on it when working with the actual garment. <strong>I write 0 in line 9 of column 4.</strong></p>
<p><strong>- Distance from the bust line to the neckline of the corset:</strong> 6cm on the client and 7cm on the pattern.</p>
<p><strong>6cm - 7cm = -1cm</strong></p>
<p>It seems like I need to lower the neckline by 1cm. But I think it is better to decide on it when working with the garment. <strong>I write -1 in line 10 of column 4.</strong></p>
<p><strong>These are all calculations related to the side seam of the front.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image009.jpg" alt=""></p>
<p><strong>Let’s move to the side seam of the back.</strong> First I pre-calculate the configuration of the side seam and write the values under “Side Seam Back" (column 5).</p>
<p><strong>Let’s begin calculating.</strong> You’ll see what happens after I take into account all measurements and calculations from the table.</p>
<p><strong>The bust front and the under-bust front measurements are not involved in these calculations.</strong> I write dashes in the table.</p>
<p><strong>Bust circumference: </strong>90cm on the client and 92cm on the pattern.</p>
<p><strong>90cm - 92cm = -2cm</strong></p>
<p>It looks like the pattern is 2cm larger than it should be. In this case, I need to shift the side seam of the back 2cm inwards. Since the back of the dress is made of two halves, I need to narrow each one down by 1cm.</p>
<p>I divide -2cm in half because of the two halves. As the result, <strong>I need to shift it 1cm inwards at the bust-line level. &nbsp;I write -1cm in the table (column 5, line 3).</strong></p>
<p><strong>Under-bust circumference:</strong> 77m on the client and 82cm on the pattern.</p>
<p><strong>77cm - 82cm = -5cm</strong></p>
<p>If I altered the side seam of the front at this level, I would have to account for the adjustment value in the rest of the calculations. In other words, I would have to add or remove that value (multiplied by two since we record the half-size in the table). I would subtract the pattern’s measurement from the client's measurement and divide the resulting value in half. But right now we are working with a very simple case with no alterations in the side seam of the front. I explain more complicated calculation cases in my other, advanced courses.</p>
<p>I divide -5cm in half to account for the two halves of the back. As the result, <strong>I need to shift it 2.5cm inwards at the under-bust line level. &nbsp;I write -2.5cm in the table (column 5, line 4).</strong></p>
<p><strong>Waist circumference:</strong> 75cm on my client and 77cm on the pattern.</p>
<p><strong>75cm- 77cm= -2cm.</strong></p>
<p>I divide -2cm in half to account for the two halves. As the result, <strong>I need to shift it 1cm inwards at the waistline level. &nbsp;I write -1cm in the table (column 5, line 5).</strong></p>
<p><strong>Stomach circumference: </strong>93cm on the client and 97cm on the pattern.</p>
<p><strong>93cm- 97cm = -4cm</strong></p>
<p>I divide -4cm in half. As the result, <strong>I need to shift it 2cm inwards at the stomach level. &nbsp;I write -2cm in the table (column 5, line 6).</strong></p>
<p><strong>Hip circumference: </strong>100cm on the client and 102cm on the pattern.</p>
<p><strong>100cm- 102cm= -2cm.</strong></p>
<p>I divide -2cm in half. As the result, <strong>I need to shift it 1cm inwards at the hip line level. &nbsp;I write -1cm in the table (column 5, line 7).</strong></p>
<p><strong>Bust middle, clip, and the distance from the bust line to the neckline of the corset are not involved in the calculations of the side seam of the back.</strong> I write dashes in the table.</p>
<p><strong>You might need additional measurements, such as 'side length' or 'back length,' to work on the back of the dress. </strong></p>
<p>You can measure the length of the back on the mock-up or on paper and adjust it according to your client's liking. Detailed guidelines on constructing the corners for attaching shoulder straps are available in my manuals and on my DVDs.</p>
<p>The side length measurement can also be taken off the mock-up or on paper.</p>
<p><strong>These are all preliminary calculations related to the side seam of the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image011.jpg" alt=""></p>
<p><strong>Let us see what we have got</strong>.</p>
<p><strong>There are no alterations in the side seam of the front.</strong> If I needed to shift it inwards or outwards, I would have to choose a particular fixed value to preserve the shape of the seam. Always remember the Adjustable Dress-Form Principle! The shape of the side seam of the front, as well as the shape of the front princess seam and the side seam of the back, never changes on an adjustable dress-form! You should only ever alter the shape of the side seam very slightly and only if there is no other choice. Usually, the shape of the side seam should be preserved both at the front and at the back.</p>
<p><strong>Look what happens with the side seam of the back.</strong> I need to shift it 1cm inwards at the bust-line level, 2.5cm inwards at the under-bust line level, 1cm inwards at the waistline level, 2cm inwards at the stomach level, and 1cm inwards at the hip line level. <strong>Instead of a beautiful and graceful contour, I got a messy irregular line!</strong> The column with preliminary adjustment values of the side seam of the back usually looks quite frightening to beginners! Do not despair though! You can cope with it! Simply study all calculation parameters and choose <strong><em>a fixed value</em></strong> by which you will shift the side seam of the back inwards or outwards.</p>
<p><strong>I need to shift the side seam of the back inwards preserving the beautiful corset silhouette and at the same time bringing the pattern closer to the client's size</strong>. <strong>I will use -1cm as a fixed adjustment value.</strong></p>
<p><strong>It's why column 6 is titled “Side Seam Back Adjusted."</strong></p>
<p><strong>The bust front and the under-bust front measurements are not involved in these calculations.</strong> I write dashes in the table.</p>
<p><strong>I narrow down the pattern in the area between the bust line and the hip line by shifting the entire side seam of the back 1cm inwards.</strong> By doing so, I reduce all circumferences by 2cm. I write -1cm in lines 3-7 of column 6.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image013.jpg" alt=""></p>
<p><strong>Let us see what I get then. </strong>This time I will use the data from column 6 (“Side Seam Back Adjusted") in the calculations and not the data from column 5 (“Side Seam Back") which I simply ignore for now.</p>
<p><strong>Bust circumference: </strong>90cm on the client and 92cm on the pattern.</p>
<p>I have already narrowed the pattern down by 2cm along the bust line though.</p>
<p><strong>92cm - 2cm = 90cm</strong></p>
<p>As the result, the pattern corresponds with the client's size along the bust line.</p>
<p><strong>Under-bust circumference: </strong>77m on the client and 82cm on the pattern.</p>
<p>I have already narrowed the pattern down by 2cm along the under-bust line though.</p>
<p><strong>82cm- 2cm= 80cm.</strong></p>
<p>At the moment, the under-bust circumference of the pattern is 80cm while it is 77cm on the client. In other words, there is an excess of 3cm. I will need to take this 3cm into some or other seam. <strong>It is best to remove any extra or add any lacking centimeters using the princess seams at the back.</strong></p>
<p><strong>It's why there is column 7 in the table, called "Princess Seam Back."</strong> I need to remove extra 3cm from the under-bust circumference of the pattern. There are two princess seams at the back, which means each of them should be used to remove 1.5cm. But every princess seam is made of two edges that you sew together.</p>
<p><strong>1.5cm ÷ 2 = 0.7cm</strong></p>
<p><strong>Therefore, I need to shift each edge of each princess seam 0.7cm inwards at the under-bust level. </strong>In this case, the total circumference of the pattern will be 77cm, which fully complies with the client's measurement. <strong>I write -0.7cm in line 4 of column 7.</strong></p>
<p><strong>Please note that I have preserved the correct corset shape of the side seam and the shape of the bust princess seam by using the princess seams at the back in the calculations.</strong> Adjustments in the princess seams of the back allow you to enhance its shape with regard to your client's silhouette. A correctly shaped and cut back ensures a good fit of the corset and prevents its top edge from biting into the skin. It looks very bad when people make the back too narrow and then try to lace it up tightly after distributing calculated adjustment values in a wrong way. It simply turns out too narrow and you can see skin bulging over the top edge of the corset. It is then obvious that the corset was constructed incorrectly and the pattern designer was not familiar with the basics of pattern-making!</p>
<p><strong>Waist circumference:</strong> 75cm on my client and 77cm on the pattern.</p>
<p>I have already narrowed the pattern down by 2cm along the waistline though.</p>
<p><strong>77cm - 2cm = 75cm</strong></p>
<p>As the result, the pattern corresponds with the client's size along the waistline. <strong>The princess seam of the back remains unchanged in the waist.</strong></p>
<p><strong>Stomach circumference:</strong> 93cm on the client and 97cm on the pattern.</p>
<p>I have already narrowed the pattern down by 2cm in the stomach though.</p>
<p><strong>97cm - 2cm = 95cm</strong></p>
<p>At the moment, the stomach circumference of the pattern is 95cm while it is 93cm on the client. In other words, there is extra 2cm. I will remove it using the same princess seams of the back. There are two princess seams at the back, and each princess seam is made of two raw edges. Therefore:</p>
<p><strong>1cm&nbsp; ÷ 2 = 0.5cm</strong></p>
<p><strong>&nbsp;I need to shift each edge of each princess seam 0.5cm inwards at the stomach level. </strong>In this case, the total circumference of the pattern will be 93cm, which fully complies with the client's measurement. <strong>I write -0.5cm in line 6 of column 7.</strong> Make sure to mark each value in the table with a minus or a plus so you don't forget whether you need to add or remove it.</p>
<p><strong>Hip circumference: </strong>100cm on the client and 102cm on the pattern.</p>
<p>But I have already narrowed the pattern down by 2cm along the hip line.</p>
<p><strong>102cm - 2cm = 100cm</strong></p>
<p>As the result, the pattern corresponds with the client's size along the hip line. <strong>The princess seam of the back remains unchanged at the hip level.</strong></p>
<p><strong>I have finished calculating pattern adjustments for my client.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%206_files/image015.jpg" alt=""></p>
<p><strong>As you can see, it was not difficult. There are only some minor alterations in the princess seams at the back.</strong> I will demonstrate the actual process of applying the adjustments to the pattern in the next tutorials.</p>
<p><strong>Let me add a couple more words in conclusion. </strong>I had purposely chosen a very basic case to explain the very principle of calculating pattern adjustments for a particular client. If I had started from complicated calculations, many of you would have probably been discouraged and would have refused to continue.&nbsp; I, on the contrary, want you to realize that it's easy as long as you grasp the basics.</p>

