<h1>Tutorial 11. Cutting the Back Half of the Dress and the Decorative Stripes.</h1>
<p><strong>I have folded the fabric for the back half of the dress twice </strong>to make four layers and simultaneously cut out the face and the lining. I use the fabric left after cutting the front half of the dress.</p>
<p><strong>I mark 63cm from the edge and draw the waistline.</strong> The length of the finished dress is supposed to be 60cm. I add another 2cm for turning the hem. By the way, you should always add another centimeter for the prominence of the buttocks. In other words, the length of the finished dress should be 61cm and not 60cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image002.jpg" alt=""></p>
<p><strong>I arrange the pieces of the back using the waistline as a guide.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image004.jpg" alt=""></p>
<p>I pin the pieces down. I never use the selvage of the fabric, not even for the seam allowance. Remember that the back will be cut through to the very bottom. I draw vertical cutting lines down to the bottom. I don't try to narrow the dress. If you want to do it, you will need to make a vent or a slit in the seam at the back. As for me, I prefer to do without it.</p>
<p><strong>NB: you should make a notch to mark the length of the zipper at this stage.</strong> Remember about the seam allowance at the top.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image006.jpg" alt=""></p>
<p><strong>I cut out the pieces. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image008.jpg" alt=""></strong></p>
<p><strong>And I make all notches.</strong> These notches will help you align the pieces. Besides, they will also come in handy when you starting draping the dress (sewing those stripes onto the base).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image010.jpg" alt=""></p>
<p><strong>I have cut out all pieces of the dress.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image012.jpg" alt=""></p>
<p><strong>Let me repeat:</strong> I cut the back pieces simultaneously with a full-length lining. The pieces are 63cm long down from the waistline - such is the length of the skirt with extra centimeters added for the hem and the buttocks. I have cut the front half of the dress simultaneously with an iron-on piece and a same-fabric interfacing for the cups. The interfacing stops 4cm below the under-bust line. The base of the dress stops 56cm below the waistline. It will be supplemented with a fabric stripe to finish the bottom of the dress.</p>
<p><strong>Now I can measure and cut out several draping stripes.</strong> There are 5 narrow and 3 wide stripes on the sketch.</p>
<p><strong>I will make the narrow stripes 6cm wide when ready.</strong> And I will add another 2cm for the overlap. In short, the stripe is supposed to be cut with the following width:</p>
<p><strong>(6cm + 2cm) x 2 = 16cm</strong></p>
<p>I will add another 0.5cm and leave it at 16.5cm to account for the thickness of the fold line.</p>
<p><strong>The cutting process is very simple: I mark 16.5cm and tear it off.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image014.jpg" alt=""></p>
<p><strong>I need five such stripes but I will only prepare three for now</strong> because the width of the dress varies (for example, it is narrower in the waist) and three stripes may actually be enough. I can always prepare more if the need comes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image016.jpg" alt=""></p>
<p><strong>How did I determine the width of the stripes?</strong> You can see it clearly on the central front piece.</p>
<p><strong>The narrow stripes start at the under-bust line</strong> and you are supposed to fit five of them into the area that stops at the hip line. There is 30cm between the under-bust line and the hip line: therefore, the width of the ready narrow stripe should be 6cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image018.jpg" alt=""></p>
<p><strong>The wide stripes start at the hip line and go down to the very bottom edge</strong> 60cm below the waistline. There is 41cm between the hip line and the bottom edge: therefore, each stripe is supposed to be 14cm wide when ready.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image020.jpg" alt=""></p>
<p><strong>Thus, the width of a ready wide stripe should be 14cm.</strong> Again, I add another 2cm for the overlap. Therefore, I need to cut it out with the following width:</p>
<p><strong>(14cm + 2cm) x 2 = 32cm</strong></p>
<p>I will add another 0.5cm and leave it at 32.5cm to account for the thickness of the fold line.</p>
<p><strong>I tear off one stripe that is 32.5cm wide and 150cm long</strong> (the full width of the fabric). The draping process will show if it's enough for all three stripes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2011_files/image022.jpg" alt=""></p>
<p>I bought a total of 2.5m of fabric and I still have another 0.5m left. It will be used for cutting the shoulder strap.</p>

