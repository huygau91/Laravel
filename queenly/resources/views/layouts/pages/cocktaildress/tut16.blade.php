<h1>Tutorial 16. Decorating the Dress with Drapes.</h1>
<p><strong>I have decided to narrow the dress before draping it. </strong>I will narrow it down towards the bottom by just 2cm from each side and make a slit in the central seam at the back. It will not affect the outer look of the dress so much but it will definitely make it more graceful and elegant.</p>
<p><strong>I take the side front pieces of the dress, step 2cm inwards from the edge at the bottom, draw a line going from the hip line to the bottom edge, and cut through the line.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image004.jpg" alt=""></strong></p>
<p><strong>The same adjustment needs to be applied to the pieces of the back: both the lining and the face.</strong> It is best to do it right now, while the pieces are still pinned together with the paper patterns.</p>
<p><strong>I step 2cm inwards at the bottom of the side seam on the side back pieces.</strong> I draw a line that starts at the hip line and stops 6cm above the bottom edge and I cut through the line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image008.jpg" alt=""></p>
<p><strong>I take the central back pieces and make a notch to mark the level of the slit at the back, 23cm above the bottom edge (including the seam allowance and the turn up).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image010.jpg" alt=""></p>
<p><strong>It's time to start draping the dress, i.e. sewing decorative stripes onto its front half.</strong></p>
<p><strong>But first I need to prepare the stripes themselves.</strong> I fold each wide stripe in half lengthwise, stitch it down along the longer side, and whip the raw edges. Each ready strap is 16cm long.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image012.jpg" alt=""></p>
<p><strong>I have also serged the bottom edge of the front half of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image014.jpg" alt=""></p>
<p><strong>I have some fusible web at hand.</strong> Fusible web and other similar materials are perfect for securing such stripes in place. Later, when all stripes are placed where they should be, you can secure them additionally by stitching.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image016.jpg" alt=""></p>
<p><strong>Let us return to the front half of the dress.</strong> Remember that the side with the pressed open seam allowances is the face.</p>
<p><strong>I measure about twice the length of one stripe.</strong> My piece of fabric is not enough for three stripes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image018.jpg" alt=""></p>
<p><strong>I iron the stripe.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image020.jpg" alt=""></p>
<p><strong>And I fuse some web onto its reverse side.</strong> I use a 1.5cm wide strap of paper-back fusible web. It will help secure the drapes on the dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image024.jpg" alt=""></p>
<p><strong>Next, I need to mark the location of the first stripe.</strong> As you probably remember, the dress is supposed to be 60cm long. I cut it at 56cm but then it got shorted because of the serged edges. Moreover, the fabric may have "shrunk."</p>
<p><strong>I measure the length of the skirt. It is 55cm.</strong> I obviously need to add another 5cm, i.e. the strap should go beyond the edge of the dress by 5cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image026.jpg" alt=""></p>
<p>I wait for the fusible web to cool down <strong>and remove the paper backing </strong>from the area about as long as the decorative stripe. <strong>I draw a line 5cm from the other edge of the stripe</strong> (in other words, I mark the border of the visible part of the stripe).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image028.jpg" alt=""></p>
<p><strong>I align the bottom edge of the dress with the line marked</strong> and fuse it on in place.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image030.jpg" alt=""></p>
<p>I trim the sides of the stripe.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image034.jpg" alt=""></p>
<p><strong>And I turn the garment right side up. The stripe is right where it should be!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image036.jpg" alt=""></p>
<p><strong>The next stripe should overlay the first one by 2cm. I mark it from both ends.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image038.jpg" alt=""></p>
<p><strong>I remove the paper backing from the fusible web and put the second stripe down as marked.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image040.jpg" alt=""></p>
<p><strong>I fuse it on with the press-iron.</strong> The stripe is secured in place now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image042.jpg" alt=""></p>
<p><strong>Now I can turn the second stripe back and sew the first stripe on.</strong> Next, after fusing on the third stripe, you will be able to sew on the second one, etc. All these stripes will be secured on the dress following this algorithm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image044.jpg" alt=""></p>
<p><strong>Now I need to trim the second stripe at the sides.</strong> It is best to do with after turning the garment onto the other side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image046.jpg" alt=""></p>
<p><strong>You can also fuse the bottom edge of the dress to the stripe the same way.</strong></p>
<p><strong>I turn the bottom edge back and fuse a piece of web onto the stripe. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image050.jpg" alt=""></p>
<p><strong>And then I remove the paper backing from the web. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image052.jpg" alt=""></p>
<p><strong>I put the bottom edge of the dress back in place and fuse it on.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image054.jpg" alt=""></p>
<p><strong>But I also need to add some decorative lace elements.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image056.jpg" alt=""></p>
<p><strong>Let's mark the placement of these lace insets.</strong> I will pin them on and trim the excess.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image060.jpg" alt=""></p>
<p>When you use lace for such decorative purposes, make sure it is not only very delicate and ornate but also double-faced. In this case, your fabric consumption will be very efficient.</p>
<p><strong>If you want these lace triangles to look identical from both sides, you should make sure to place them symmetrically.</strong> Keep checking it with the help of a measuring tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2016_files/image062.jpg" alt=""></p>
<p><strong>I trim both triangles at the sides and remove the excess of fabric at the top of the second one. </strong></p>
