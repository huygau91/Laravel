<h1>Tutorial 19. Preparing to Finish the Top of the Back.</h1>
<p><strong>Let us continue working on the back of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image002.jpg" alt=""></p>
<p><strong>I have installed the zipper and sewn down the princess seams and the central seam down to the level of the slit.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image004.jpg" alt=""></p>
<p><strong>I have also pressed all seams open. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image006.jpg" alt=""></p>
<p><strong>I tried to press the seam allowances of the slit</strong> but it didn't look even enough so I decided to postpone it until later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image008.jpg" alt=""></p>
<p><strong>I have sewn down the vertical princess seams of the lining of the back and pressed them open just the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image010.jpg" alt=""></p>
<p><strong>I leave the central seam of the lining open for now and put it aside. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image012.jpg" alt=""></p>
<p><strong>There will be decorative stripes at the back of the dress similar to those at the front. They will stop at about the waistline level. </strong></p>
<p><strong>It's important to remember </strong>that the stripes at the front of the dress are supposed to gradually blend into the stripes at the back after we join the two halves of the dress!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image014.jpg" alt=""></p>
<p>I put together the front and the back of the dress, align the notches of their seam allowances, and <strong>make notches on the back to mark the level where each decorative stripe ends at the front of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image016.jpg" alt=""></p>
<p><strong>I do the same from the opposite edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image018.jpg" alt=""></p>
<p><strong>And then I remove the front half of the dress and return to the back.</strong></p>
<p><strong>I place a stripe onto the back.</strong> It was left from the stripes sewn onto the front of the dress.</p>
<p><strong>I align the edge of the stripe with the notch that marks the edge of the corresponding stripe at the front.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image020.jpg" alt=""></p>
<p><strong>And I make a notch to mark the level where the next stripe is supposed to begin.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image022.jpg" alt=""></p>
<p><strong>The next stripe is rather wide.</strong> I have cut it out the same way I cut out the narrow stripes. It will cover the back up to the very top. Generally speaking, these are purely decorative elements. They perform no other function. I simply felt like decorating the back of the dress, at least in the area above the waistline.</p>
<p><strong>I put the stripe in its supposed place.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image024.jpg" alt=""></p>
<p><strong>You can pin the stripes together but using a fusible web will make it a lot more precise. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image026.jpg" alt=""></p>
<p><strong>I fuse a strap of web onto the narrow stripe. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image028.jpg" alt=""></p>
<p><strong>I remove the paper backing from the web.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image030.jpg" alt=""></p>
<p><strong>And I put the wide stripe over it and fuse it on.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image034.jpg" alt=""></p>
<p><strong>The same thing needs to be done for the other half of the back. </strong></p>
<p><strong>I put a narrow stripe in place according to the notches and make a notch to mark the edge of the wide stripe.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image036.jpg" alt=""></p>
<p><strong>The stripes on the right and left halves of the back should be perfectly symmetric.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image044.jpg" alt=""></p>
<p><strong>I fuse vertical straps of web onto the face of the decorative stripes.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image050.jpg" alt=""></p>
<p><strong>Then I fold the edge with the fusible web and secure it by pressing.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image056.jpg" alt=""></p>
<p><strong>The result is two semi-finished pieces:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image058.jpg" alt=""></p>
<p><strong>I spread the back of the dress and even out any irregularities.</strong> You should push the air out of any hollows formed by the dart. Simply push it toward the bottom. I secure it with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image060.jpg" alt=""></p>
<p><strong>I put the semi-finished pieces onto the back from either side of the zipper, as marked by the notches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image062.jpg" alt=""></p>
<p><strong>And then I remove the paper backing from the web. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image066.jpg" alt=""></p>
<p><strong>And I fuse the stripes onto the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image068.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/cocktail_dress/transcript/Tutorial%2019_files/image070.jpg" alt=""></p>
<p><strong>I stamp it with a ruler and wait for it to cool down.</strong></p>
<p><strong><br>
