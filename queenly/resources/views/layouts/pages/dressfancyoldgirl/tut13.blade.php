<h1>Tutorial 13. Finishing the Lacing Area. Installing Grommets.</h1>
<p><strong>I have evened out the drapes to make them as symmetric as possible:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>And I fuse them on the webbing by pressing.</strong> I do it without removing the pins. I just need to secure the main areas and then I can take them out. After removing the pins, I press it again to iron away the holes.&nbsp; The webbing holds the drapes very well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image004.jpg" alt=""></p>
<p><strong>Here is our waistband! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image006.jpg" alt=""></p>
<p><strong>I need to secure the drapes along the sides: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><strong>I trim away all excess chiffon:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image012.jpg" alt=""></strong></p>
<p><strong>It is important to serge the edges of chiffon which are parallel to the lacing edges: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><strong>I lock the serging threads with machine stitches.&nbsp; </strong></p>
<p>Then <strong>I cut a matching piece of narrow Rigilene boning</strong>. I tape its ends with bits of masking tape, <strong>place it at the fold line, and secure it with two rows of parallel stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image016.jpg" alt=""></p>
<p><strong>Then I fold the seam allowance with the bone onto the lining and sew at about 1cm from the edge: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image018.jpg" alt=""></p>
<p><strong>I make another row of parallel stitches to prevent grommets from popping out from the chiffon.</strong> Grommet holes will be punched between these two rows.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><strong>I do the same thing with the opposite half of the back. </strong></p>
<p><strong>Here is the prepared lacing area: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><strong>I mark points for punching holes. </strong>In my case, they will be made with 2cm intervals.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image024.jpg" alt=""></p>
<p><strong>I mount a hole-punching die on my hand press tool and punch the holes: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image026.jpg" alt=""></p>
<p><strong>Then I change the die and set grommets in the holes. </strong>Make sure to use two-piece grommets with matching washers: they are very robust and hold in place very well.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image028.jpg" alt=""></p>
<p><strong>Here is the lacing area: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2013_files/image030.jpg" alt=""></strong></p>

