<h1>Tutorial 3. Cutting the Dress.</h1>
<p><strong>I start cutting the dress. </strong></p>
<p>I have chosen stretch fabric for the base of the dress and the lining and chiffon for the wide skirt and the drapes in the cups area. Multi chiffon is preferable.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image002.jpg" alt=""></p>
<p>Sewing a dress for a little girl <strong>requires duplicating the face and the lining</strong> the same way you would for a woman's dress. I will duplicate the dress down to the waistline. The duplicated area of the face of the bodice may even stop above the waistline because it will feature a decorative waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image004.jpg" alt=""></p>
<p><strong>Seam allowances are not included in the patterns! </strong></p>
<p><strong>I start from the front piece. </strong>All seam allowances, including that of the side seam, are 1.2cm.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image006.jpg" alt=""></strong></p>
<p><strong>I take the back piece.</strong> The seam allowances are 2cm for the central back seam and 1.2cm for the side seams and the top edge. I also transfer the dart.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image008.jpg" alt=""></p>
<p><strong>I have transferred the pieces on the fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image010.jpg" alt=""></p>
<p><strong>I pin the layers together and cut the lay in parts. </strong>I will cut the pieces out properly together with the main fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image012.jpg" alt=""></p>
<p><strong>I cut strictly along the waistline</strong> and get two pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image014.jpg" alt=""></p>
<p>If the skirt is flared, then <strong>you can position the pieces in a head-to-toe manner </strong>as shown in the picture below to spare some fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image016.jpg" alt=""></p>
<p>Simply position the front piece as shown and position the back pieces the other way round. <strong>In other words, the back pieces should be flipped vertically. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image018.jpg" alt=""></strong></p>
<p>I fold part of the fabric in half to cut out a short lining which will stop at the waist level. I pin the iron-on fabric with the marked bodice pieces onto the main fabric, put down the paper patterns, and continue working. I mark 1.2cm seam allowances, the stomach line, and the hip line. <strong>I mark the length of the skirt down from the waist (80cm). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image020.jpg" alt=""></p>
<p>I mark the bottom line perpendicular to the fold line. Then I measure the hip width, mark it along the bottom line, <strong>and mark a flare of 10cm from it. </strong>I mark the same 80cm down from the waist along the side seam line and make the bottom line a little rounder. Next, I draw a straight side seam line (you can give it any configuration to your liking) and I pin the pieces together.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image022.jpg" alt=""></p>
<p><strong>I fold the other end of the fabric and pin down the iron-on fabric with the back pieces marked on it: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image024.jpg" alt=""></p>
<p><strong>I put down the paper patterns and continue:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image026.jpg" alt=""></p>
<p><strong>I mark 2cm seam allowances for the central seam and 1.2cm seam allowances along the sides, mark the hip line, transfer the dart, </strong>and remove the paper patterns.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image028.jpg" alt=""></p>
<p><strong>I mark the end and the sides of the dart with pins:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image030.jpg" alt=""></p>
<p><strong>Then I cut off the folded part of the fabric so I can unfold the rest of it and continue cutting the back of the skirt.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image034.jpg" alt=""></p>
<p>I prolong the central seam down as a straight line which stops 80cm from the waist and mark the bottom line. Then I measure the hip width, mark it along the bottom line, <strong>and mark a flare of 10cm from it. </strong>I mark the same 80cm down from the waist along the side seam line and make the bottom line a little rounder. You can lower the bottom line by another 1cm to account for the prominence of buttocks.&nbsp; I connect the hip line and the bottom line with a straight line and secure the pieces with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image036.jpg" alt=""></p>
<p>I cut out all parts of the dress. The iron-on batiste pieces and the main fabric pieces are cut out simultaneously. <strong>As the result, I have two pieces: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image038.jpg" alt=""></p>
<p><strong>I compare the lengths of their side seam lines. They are the same:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%203_files/image040.jpg" alt=""></p>
<p><strong>Now I only need to cut a chiffon full sweep skirt. </strong></p>

