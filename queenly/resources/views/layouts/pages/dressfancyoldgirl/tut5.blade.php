<h1>Tutorial 5. Joining the Face and the Lining of the Front and the Back.</h1>
<h2>Marking the Position of the Waistband and the Cups with Respect to the Child's Body Proportions.</h2>
<p><strong>I recommend you press the pieces with the bones</strong> to straighten them properly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image002.jpg" alt=""></p>
<p><strong>I also press the darts at the back:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image004.jpg" alt=""></p>
<p><strong>I lay a narrow Rigilene bone next to the dart: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image006.jpg" alt=""></p>
<p><strong>I would not recommend overlaying the dart with the bone </strong>because it will make it too thick and uneven. The bone settles perfectly right next to the dart seam. I sew it on with two rows of parallel stitches.</p>
<p><strong>Then, I stitch up the dart on the non-duplicated piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>And I add hanging loops </strong>by folding 20cm long ribbons in half and sewing them to the garment in the underarm areas. I secure the loops with two rows of stitches with an interval of about 2cm in-between. The first one is made on the seam allowance and the second one - on the inside of the cup. This will help the dress hang more evenly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image012.jpg" alt=""></p>
<p><strong>I put together the lining and the face </strong>and secure them with pins. <strong>I sew a joining seam along the row of stay-stitches.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image014.jpg" alt=""></p>
<p><strong>Then, I remove the pins and ease in the top edge of the bodice with the help of an organza ribbon. </strong>&nbsp;The ribbon is a strip torn off a whole piece of organza; it has a width of 2-2.5cm. I can see the stay-stitch and the joining seam through as I sew easing in the fabric underneath very slightly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image016.jpg" alt=""></p>
<p><strong>I fold the organza up to the seam allowance and secure it: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>And then I turn the piece around and topstitch the neckline seam. </strong>The seam allowance is folded to the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>I trim the seam allowance:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image022.jpg" alt=""></p>
<p><strong>And I turn the garment out.</strong> The eased-in neckline now looks rounder thanks to the organza ribbon.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image024.jpg" alt=""></p>
<p><strong>I press the darts at the back: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image026.jpg" alt=""></p>
<p><strong>And I press the top edge.</strong> Please do use cup pressing moulds. Even though the dress is straight, I try to give the neckline a somewhat curved shape to ensure good fitting in the chest.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image028.jpg" alt=""></p>
<p><strong>I join the face of the back with its lining. </strong></p>
<p>I leave the central seam open for now because I will later install a blind zipper there.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image030.jpg" alt=""></p>
<p><strong>Again, I finish the top edge with an organza ribbon.</strong> Only this time I simply reinforce it without easing in.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image032.jpg" alt=""></p>
<p><strong>I fold the organza up to the seam allowance and secure it:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image034.jpg" alt=""></p>
<p><strong>I topstitch the seam:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image036.jpg" alt=""></p>
<p><strong>I trim the seam allowance:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image038.jpg" alt=""></p>
<p><strong>And I press the top edge of the back:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image040.jpg" alt=""></p>
<p><strong>Now I need to mark the position of the decorative waistband.</strong> I start by finding the waistline. Then I mark a line 4cm above the waist: this is where the chiffon skirt will be sewn to the bodice and where the waistband will begin. I divide the area above the line in half to separate the cups area from the draped waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image042.jpg" alt=""></p>
<p><strong>I transfer the same lines onto the back of the bodice:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image044.jpg" alt=""></p>
<p><strong>And now I need to decide how I am going to drape the back.</strong> There will be vertical drapes on the cups but there is too little room at the back of the bodice to extend them onto it. <strong>I believe it will look best with the waistband smoothly passing onto the back, i.e. with only horizontal drapes at the back of the bodice.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image046.jpg" alt=""></p>
<p><strong>I make a notch at the beginning of the waistband and mark the length of the zipper down from it. I make another notch at the supposed end of the zipper. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%205_files/image048.jpg" alt=""></strong></p>

