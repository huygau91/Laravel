<h1>Tutorial 9. Calculating Fabric Consumption and Cutting the Chiffon Skirt.</h1>
<p><strong>Let us figure out how much fabric I need for cutting the chiffon skirt. </strong></p>
<p>Similar dress models for women usually require sewing two full sweep skirts. Two full sweep skirts are too much for a child though, <strong>so I will use three half-circle skirts instead.</strong></p>
<p><strong>Vicky's waist circumference is 55cm.</strong></p>
<p>If I divide it by three, I will get <strong>18.5cm </strong>−<strong> half-circumference of each half-circle skirt.</strong></p>
<p>According to the formula <strong>πR=18.5cm, </strong>the waist opening of each half-circle skirt has a radius of <strong>6cm</strong>.</p>
<p><strong>The length of the skirt is 80cm according to the measurement. </strong>But I have raised the waistline 4cm and I need to add a 2cm seam allowance, which makes it <strong>86cm.</strong> And if you multiply add the 6cm radius and multiply it all by two, <strong>you will get 184cm.</strong></p>
<p>3 half-circles;</p>
<p>WC = 55;</p>
<p>55<br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image002.png" alt="">3=18.5</p>
<p>πR=18.5</p>
<p>R=6</p>
<p>SL = 80+4+2 (seam allowances) = 86</p>
<p>(86=6)*2=184 cm</p>
<p><strong>Let us see how much fabric I need to cut just two half-circle skirts for now. </strong>I draw a rectangle and imagine that it is a piece of fabric with a width of 150cm. The piece is folded in half and the fold line is on the left. <strong>X denotes fabric consumption. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image004.jpg" alt=""></p>
<p>(fold line)</p>
<p><strong>Here is a schematic layout for cutting two half-circle skirts:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image006.jpg" alt=""></p>
<p>(fold line; selvage)</p>
<p>In order to find <strong>Х,</strong> I need to use the right triangle formula (the sum of the squares of the lengths of the legs equals the square of the length of the hypotenuse)<strong>: </strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image008.png" alt=""><strong>. </strong>Therefore, <strong>Х = 107cm.</strong> Since my piece of fabric is folded in half, I need to multiply 107cm by two. <strong>It will take 214cm of fabric to cut two half-circle skirts. </strong></p>
<p><strong>The third half-circle skirt will be cut from a separate piece of fabric with a width of 150cm and a length of 184cm: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image010.jpg" alt=""></p>
<p>(fold line ; selvage)</p>
<p>86cm (skirt length) plus 6cm (waist radius) makes 92cm. I multiply it by two <strong>and get 184cm.</strong> This is how much fabric I need for the third half-circle skirt.</p>
<p><strong>As the result, the total fabric consumption for a skirt made of three half-circle skirts is as follows: </strong></p>
<p><strong>184cm + 214cm = 398cm or about 4m. </strong></p>
<p><strong>The red numbers mark separate pieces: </strong></p>
<p>Number 1 is a quarter of a circle,</p>
<p>Number 2 is a full half-circle,</p>
<p>Number 3 is another full half-circle.</p>
<p><strong>The schematic drawing below shows the order in which you are supposed to join the pieces to avoid confusion:&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image012.jpg" alt=""></strong></p>
<p><strong>The two pieces marked by number 1 are two quarter-circles </strong>which will be located at the front. <strong>And the pieces marked with 2 and 3 are two full half-circles </strong>located closer to the back. One of the vertical seams will be the central back seam. The front vertical edges of the quarter-circles will be cut along the crosswise grain and form a slit at the front of the skirt. All other vertical seams will be sewn along the lengthwise grain, i.e. run parallel to the selvage. <strong>It is very important to take such things into account when joining half-circles and quarter-circles. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image014.jpg" alt=""></p>
<p>(fold line; selvage; crosswise grain (уток); lengthwise grain (долевая))</p>
<p><strong>And now - on to the cutting!</strong></p>
<p><strong>I start from piece 3, a full half-circle. </strong></p>
<p>I tear a piece of fabric with a length of 184cm <strong>and fold it in half (the fold line is on the left). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image016.jpg" alt=""></p>
<p><strong>Now I need to fold it into a triangle: </strong>I take the selvage edge and bring it to the fold line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image018.jpg" alt=""></p>
<p><strong>I fold the resulting triangle in half, </strong>spread out the fabric as well as I can, and secure it with pins. I trim the sharp point by 6cm <strong>and mark the required length of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image020.jpg" alt=""></p>
<p><strong>I cut out a larger piece than required just in case: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image022.jpg" alt=""></p>
<p>Now I need a piece of fabric with a length of 214cm. I take 220cm and fold it in half. <strong>The fold line is on the left and the selvages are at the top. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image024.jpg" alt=""></p>
<p><strong>I fold the selvages to the raw edges on the right of the piece so as to form a triangle: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image026.jpg" alt=""></p>
<p><strong>And then I fold the triangle in half to form a cone. </strong>I carefully put the lay on the ironing board and secure the layers with pins. And then <strong>I mark the radius and the length of the skirt </strong>on the triangle.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image028.jpg" alt=""></p>
<p><strong>I cut along the marked line and get two quarter-circle pieces. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image030.jpg" alt=""></p>
<p>Then I take the remaining fabric which is still folded in half. The fold line is in my hand.&nbsp; <strong>I fold the selvages to the fold line and then fold it in half again. </strong>I secure the layers with pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image032.jpg" alt=""></p>
<p><strong>I will cut out a full half-circle piece now. </strong>I put the half-circle piece which I cut out before on top of the folded fabric and <strong>cut out another half-circle following its contour.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image034.jpg" alt=""></p>
<p><strong>I cut the radius at the top on both pieces simultaneously: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image036.jpg" alt=""></p>
<p><strong>As the result, I have two half-circles and two quarter-circles, which makes a total of three half-circles. </strong></p>
<p><strong>Remember to trim away the selvages </strong>because they tend to draw the fabric together when you sew, making the seams look messy. It is better to get rid of them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%209_files/image038.jpg" alt=""></p>

