<h1>Tutorial 2. Taking Measurements.</h1>
<p><strong>I tie cords around the girl's body to take all measurements correctly:</strong> one around the waist, another around the stomach (do not tie it too tightly, keep it natural), and still another along the under-bust line. Needless to say that taking measurements off children is not the easiest thing in the world because they keep moving around. They can't just stand there waiting for you to get focused on the task. So try to do it quickly and yet accurately!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image002.jpg" alt=""></p>
<p><strong>I start taking measurements.</strong> They are the same as for a grown woman.</p>
<p><strong>Bust Circumference:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>Under-Bust Circumference:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>Waist Circumference:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>Stomach Circumference: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image010.jpg" alt=""></p>
<p><strong>Hip Circumference </strong>is a must because I am sewing a long dress. The measuring tape goes horizontally through the most prominent points of the buttocks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image012.jpg" alt=""></p>
<p>Next, I measure the distance from the level where I just placed the measuring tape up to the waistline along the side. <strong>This is Hip Depth.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>I always mark the distance from the waistline to the stomach line: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image016.jpg" alt=""></strong></p>
<p>And I also <strong>measure the distance from the waistline to the under-bust line.</strong> This measurement is called <strong>Waist to Under-Bust</strong>.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image018.jpg" alt=""></p>
<p><strong>There are some more compulsory measurements on the list. </strong></p>
<p><strong>Bust Width Front </strong>is measured from side seam to side seam along the bust line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image020.jpg" alt="">v</p>
<p><strong>Under-Bust Width Front </strong>is also measured from side seam to side seam:</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image022.jpg" alt=""></p>
<p>Vicky has a bit of a belly. She can suck it in, of course, but we can't expect her to do it whenever she is wearing her favorite dress. And this is why I measure her <strong>Waist Width Front </strong>from side seam to side seam along the cord tied around the waist.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image024.jpg" alt=""></p>
<p>And I take a similar measurement, <strong>Stomach Width Front, </strong>from side seam to side seam along the stomach line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image026.jpg" alt=""></p>
<p>After deciding on the position of the front princess seams, I measure from <strong>Bust Apex to Bust Apex</strong>. <strong>&nbsp;&nbsp;</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image028.jpg" alt=""></strong></p>
<p>And then I place my finger at the <strong>Bust Apex point</strong> and measure <strong>Clips: </strong></p>
<p><strong>- Straight up to the neckline.</strong> I decide how far from the bust apex point the neckline will be.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image030.jpg" alt=""></p>
<p><strong>&nbsp;- To the armpit point:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image032.jpg" alt=""></p>
<p><strong>&nbsp;- And to the supposed centre of the neckline </strong>if you plan to give it some fancy shape.</p>
<p><strong>Waist to Floor:</strong> measure the distance from the waist down to the floor.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image034.jpg" alt=""></p>
<p><strong>If you are sewing a garment with shoulder seams, you can take additional measurements the same way you would for an adult. </strong></p>
<p><strong>Side Neck Point to Bust Apex:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image036.jpg" alt=""></strong></p>
<p><strong>Shoulder Tip to Bust Apex:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image038.jpg" alt=""></p>
<p><strong>Sternal Notch to Waist (Centre Length Front): </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image040.jpg" alt=""></p>
<p><strong>Shoulder Length:</strong> measure from the side neck point to the shoulder tip.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image042.jpg" alt=""></p>
<p><strong>Shoulder to Shoulder Across Front: </strong>measure the distance from shoulder tip to shoulder tip across front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image044.jpg" alt=""></p>
<p>Then I ask the girl to turn around and measure the <strong>Shoulder to Shoulder Across Back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image046.jpg" alt=""></p>
<p>I find the seventh cervical vertebra and measure the distance to the waist <strong>(Centre Length Back).</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image048.jpg" alt=""></p>
<p><strong>Full Length Back: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image050.jpg" alt=""></strong></p>
<p><strong>Shoulder Slope Back: </strong>this measurement is taken diagonally from the centre of the waistline to the shoulder tip across the back. It will help you draw the right incline of the shoulder at the back of the bodice.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image052.jpg" alt=""></p>
<p><strong>Armpit to Armpit Back: </strong>with the girl's arms hanging down freely, measure the distance between the supposed armscyes at the armpit level across the back. This measurement will help you determine the position of the armscye. Imagine the armscyes or those corners at the back of the bodice and measure the distance between them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image054.jpg" alt=""></p>
<p><strong>Armpit to Armpit Front: </strong>with the girl's arms hanging down freely, measure the distance between the supposed armscyes at the armpit level across the front.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image056.jpg" alt=""></p>
<p><strong>Side Length:</strong> measure the distance from the waistline to the underarm. This measurement must be taken in the actual size. Ask the girl to lift her arm so you can see where the armscye should stop. It shouldn't rub against the skin and yet it shouldn't be too low either.</p>
<p><strong>Armscye Circumference:</strong> wrap the measuring tape around the girl's arm, think over how loose the sleeve should be, and measure it in the actual size. This measurement indicates the actual length of the armscye on the finished dress. It is compulsory for dresses with sleeves or shoulder straps.</p>
<p><strong>Upper Arm Circumference </strong>(taken if you plan on sewing sleeves or armlets): wrap the measuring tape around the girl�s upper arm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%202_files/image058.jpg" alt=""></p>
<p>These are basically all measurements I need to draft a pattern of a dress for Vicky. The more measurements you take off the child, the easier it will be to construct the pattern. You won't have to make guesses in the process.</p>
