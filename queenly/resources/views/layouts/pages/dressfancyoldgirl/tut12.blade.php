<h1>Tutorial 12. Draping the Waistband.</h1>
<p><strong>Let us decorate the waistband part of the dress.&nbsp; </strong></p>
<p>The drapes will be secured with fusible webbing as always. <strong>I apply strips of fusible webbing on the dress in the direction in which I want to arrange the drapes. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image002.jpg" alt=""></p>
<p><strong>I carefully overlay the seam allowance of the skirt. </strong>The entire seam allowance should be hidden underneath the drapes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image004.jpg" alt=""></p>
<p><strong>I measure the distance along the left side piece to give the waistband the same incline on the right one</strong> and double-check the position of the webbing.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image006.jpg" alt=""></p>
<p><strong>I continue covering the bodice with strips of fusible webbing: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image008.jpg" alt=""></p>
<p><strong>And then I remove the paper backing: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image010.jpg" alt=""></p>
<p>I take my chiffon and check whether it is large enough for the full circumference: from lacing edge to lacing edge. Then I fold it in half and find the middle. I press the fold line in the middle so I can keep referring to it in further work. <strong>And I align the middle of the fold line with the middle of the dress. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image012.jpg" alt=""></p>
<p>When I start forming drapes, I will stick strictly to this vertical fold line. It will be very hard to form symmetric drapes if the fabric starts going askew. They will all have different inclines in this case. If you don't want this to happen, always remember to mark the middle straight away so you can use it as a point of reference in further work.</p>
<p><strong>I have pinned the chiffon to the dress: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image014.jpg" alt=""></p>
<p><strong>And I start forming drapes.</strong> The folds must form a strictly vertical line in the middle. The drapes will be spaced more closely in the center than at the sides because I want to achieve a fan-like look.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image016.jpg" alt=""></p>
<p><strong>I form less closely-spaced drapes on the side seam.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image018.jpg" alt=""></p>
<p><strong>And I try to form symmetric drapes on the other side seam: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image020.jpg" alt=""></p>
<p><strong>Then, I form drapes in the middle of each half of the back. </strong>I make sure to leave 1.5-2cm before the joint with the skirt. The drapes don't have to run along the bias grain. They settle perfectly along the crosswise grain, too!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image024.jpg" alt=""></p>
<p><strong>I trim all excess chiffon leaving about 2-3cm for finishing the bottom edge:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image026.jpg" alt=""></p>
<p><strong>I fold the bottom edge inwards: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image030.jpg" alt=""></p>
<p><strong>And I secure the drapes by pressing: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image032.jpg" alt=""></p>
<p><strong>I check whether the drapes meet well and the two ends of the waistband have the same width:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2012_files/image034.jpg" alt=""></p>

