<h1>Tutorial 11. Sewing the Chiffon Skirt to the Bodice.</h1>
<p><strong>Now I need to press all seams on the chiffon skirt, as well as the finished slit. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image002.jpg" alt=""></p>
<p><strong>You can notch the seam allowances to help the front seam open properly:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image003.jpg" alt=""></p>
<p><strong>I trim the seam allowance to match the width of the finished edge and make it less noticeable: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image005.jpg" alt=""></p>
<p><strong>I pin the front slit to the sleeve ironing board so I can press it thoroughly.&nbsp;&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image007.jpg" alt=""></p>
<p><strong>I mark a guideline for sewing the skirt to the bodice: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image009.jpg" alt=""></p>
<p>I find the middle of the bodice, pin the middle of the chiffon skirt right to it, <strong>and continue gradually adding pins along the perimeter.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image011.jpg" alt=""></p>
<p><strong>I insert the bodice in the skirt and secure it with pins from the other side.</strong> Use a generous amount of pins to distribute the fabric properly along the joining seam and form more even folds.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image013.jpg" alt=""></p>
<p><strong>Here is the skirt pinned to the bodice:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image015.jpg" alt=""></p>
<p><strong>I join them for good by stitching right over the face of the dress: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2011_files/image017.jpg" alt=""></p>
<p><strong>The dress is thin enough − even the bones don't make a big difference. </strong>By the way, I use the same technique for sewing chiffon skirts to dresses for women. I remove the stay-stitches because they might show from behind the draped waistband.</p>
<p><strong>The chiffon skirt is in place! </strong></p>
