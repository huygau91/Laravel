<h1>Tutorial 6. Installing a Zipper.&nbsp; Preparing the Lacing Area.</h1>
<p><strong>It is time to install a blind zipper in the dress. </strong></p>
<p><strong>I start sewing the zipper in </strong>from the notch made at the end of the waistband and down to the notch which marks the end of the zipper.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image002.jpg" alt=""></p>
<p><strong>Remember to make notches and mark the end of the zipper so you can align the pieces properly later. </strong>&nbsp;The longer the zipper is, the harder it is to get it right. <strong>I marked the end of the zipper with chalk </strong>when it was closed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image004.jpg" alt=""></p>
<p><strong>I carefully align the chalk mark with the end notch and sew the zipper in place. </strong>The fabric is resilient and tends to stretch a little but the zipper does not stretch at all so I need to prevent the fabric from getting stretched out. I arrive right at the notch at the bottom of the waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image006.jpg" alt=""></p>
<p>Done, the zipper is in place. <strong>I switch to a specialty blind zipper presser foot and sew it on properly.</strong> The zipper has already been prepared for getting sewn in for good and I don't have to align anything again.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image008.jpg" alt=""></p>
<p><strong>I make a bar tack as I reach the notch:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image010.jpg" alt=""></p>
<p>And I sew it on along the other side from the top notch to the bottom notch. <strong>I zip it up and check it:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image012.jpg" alt=""></p>
<p><strong>I put together and pin the seam allowances of the detached part of the zipper.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image014.jpg" alt=""></strong></p>
<p><strong>I switch to a one-sided presser foot, find the point where I stopped sewing in the zipper, make a bar tack, and continue sewing downwards:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image016.jpg" alt=""></p>
<p><strong>The result looks like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image018.jpg" alt=""></p>
<p><strong>I switch back to the regular presser foot and finish sewing the central seam:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image020.jpg" alt=""></p>
<p><strong>And then I press open the entire seam with the zipper: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image022.jpg" alt=""></p>
<p><strong>Now I need to hide the zipper underneath the lining and finish the transition from the zipper to the lacing area.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image024.jpg" alt=""></p>
<p><strong>I fold the lining, turn the piece out through the open side seam, and align the notches.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image026.jpg" alt=""></p>
<p><strong>And I sew from the notch down to the end of the lining. </strong>The left side of the presser foot moves along the zipper teeth.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image028.jpg" alt=""></p>
<p><strong>I do the same thing from the other side of the zipper tape. </strong></p>
<p><strong>Without turning out the piece, I deepen the notch almost to the very zipper tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image030.jpg" alt=""></p>
<p><strong>I turn the piece out and spread out the seam allowance:&nbsp;&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image032.jpg" alt=""></p>
<p><strong>I secure the turned-out lacing edge with a row of stitches joining the lining with the face. </strong>There is a smooth transition from the zipper to the lacing now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image034.jpg" alt=""></p>
<p><strong>I sew the side seam </strong>joining the lining with the face. The back is now a single piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image036.jpg" alt=""></p>
<p>I zip it up and fold the seam allowances to the inside. <strong>Here is the lacing area. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%206_files/image038.jpg" alt=""></strong></p>

