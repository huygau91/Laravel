<h1>Tutorial 8. Sewing Down the Side Seams of the Dress.</h1>
<p><strong>Before I can continue, I need to secure the drapes properly. </strong></p>
<p><strong>First of all, I trim away all excess fabric in the cups area: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image002.jpg" alt=""></p>
<p><strong>I mark the position of the draped waistband which is supposed to pass onto the back: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>I secure the drapes</strong> with a row of machine stitches made at a presser foot width from the top edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image006.jpg" alt=""></p>
<p>I remove the pins <strong>and trim the chiffon lengthwise leaving 0.8-1cm from the stitching.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image008.jpg" alt=""></p>
<p><strong>Then I put a bias tape next to the stitching made at a presser foot width from the top edge and sew it on. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image010.jpg" alt=""></p>
<p>I fold the bias tape to the inside, press it, <strong>and secure it with hand stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image012.jpg" alt=""></p>
<p><strong>I try my best not to catch the face fabric so I can easily turn out the lining: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image014.jpg" alt=""></p>
<p><strong>The top edge has a very clean finish now: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image018.jpg" alt=""></p>
<p><strong>Next, I need to secure the drapes at the back of the bodice. </strong>I sew right over the side seam line and along the seam allowance of the lacing area at about a presser foot width from the edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image020.jpg" alt=""></p>
<p><strong>I trim away all excess fabric: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image022.jpg" alt=""></p>
<p><strong>Then I move the lining out of the way and align the top edges of the front and the back of the bodice. </strong>&nbsp;They should be perfectly even and levelled.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image024.jpg" alt=""></p>
<p><strong>I pin the lining and the face of the back to the face of the front keeping its lining detached: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image026.jpg" alt=""></p>
<p><strong>And I start sewing the side seam: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image028.jpg" alt=""></strong></p>
<p><strong>I align the hip line notches and sew the side seam down to the very bottom: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image030.jpg" alt=""></strong></p>
<p><strong>Then I fold the lining of the front and sew its side seam with the same seam allowance. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image032.jpg" alt=""></p>
<p><strong>The other side seam is sewn just the same way. </strong></p>
<p><strong>When I turn out the joined pieces, the side seams look perfectly clean both on the face and on the lining because the seam allowances are hidden between the layers of fabric:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image036.jpg" alt=""></p>
<p><strong>I press the side seams </strong>flat toward the front. I also press them from the face to avoid tucks and creases.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image040.jpg" alt=""></p>
<p><strong>So I have draped the cups area, sewn and finished the side seams, and draped the back of the bodice.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image042.jpg" alt=""></p>
<p><strong>I have serged all edges, including the edges of the side seams, the lacing edges, and the edges in the zip-up area.&nbsp; </strong></p>
<p>I continue working on the dress. The lining is still detached and it does not look very good. <strong>I will overlay the edge with a bias tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image044.jpg" alt=""></p>
<p><strong>I sew the bias tape in place with two rows of parallel stitches. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image046.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image048.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image050.jpg" alt=""></strong></p>
<p><strong>The bias tape also forms an additional tunnel </strong>for inserting a ribbon or an elastic band. If the girl does not feel comfortable in the dress (a strapless dress, for instance, might start sliding off), you will insert a ribbon or an elastic band in the tunnel, tie it inside the dress, and make it hold better around the waist. It is not the case with Vicky but I have still made the tunnel just to be on the safe side. Besides, I had to overlay the open edges of the lining.</p>
<p><strong>The bias tape looks very nice and makes the inside of the dress a lot neater: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image052.jpg" alt=""></p>
<p>Do not worry about <strong>stitching through all layers of the dress: </strong>although the stitches are visible on the face, they will be covered up by the chiffon skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image054.jpg" alt=""></p>
<p><strong>I lock the ends of the serger chain in the lacing area</strong> and trim away the thread tails.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%208_files/image056.jpg" alt=""></p>

