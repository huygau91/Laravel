<h1>Tutorial 1. Choosing a Suitable Style with Respect to the Child's Body Proportions.</h1>
<p><strong>I start working on a dress for Vicky. Vicky is 9 and her height is 130cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image002.jpg" alt=""></p>
<p><strong>Let us figure out her body proportions. </strong></p>
<p>Please remember that <strong>the key ratio </strong>is the ratio of the head to the total height.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image004.jpg" alt=""></p>
<p><strong>In Vicky's case, it is 1:6.&nbsp; </strong></p>
<p><strong>Now I need to find the waistline level on her body. </strong>The blue guide shows the girl's waistline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image006.jpg" alt=""></p>
<p>I have found several dress models on the Internet and now I will try to "put" them on Vicky.</p>
<p><strong>Take this dress, for example: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image008.jpg" alt=""></p>
<p>It is a great choice for primary school-aged girls. The waistline is basically placed in its natural position on the original dress. It needs to be made a little higher with the help of decorative elements, which is exactly what we see - those flowers and waistband.</p>
<p><strong>One should never forget about proportions though! </strong></p>
<p><strong>First of all, let us examine it from the golden ratio point of view.</strong> It works perfectly with the length of the skirt, the position of the waistline, and the shoulder line position.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image010.jpg" alt=""></p>
<p><strong>Let us experiment with the width of the skirt, too.</strong> If you compare the distance from shoulder to shoulder with the width of the skirt, you will see that it is too wide for Vicky.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image012.jpg" alt=""></p>
<p>I adjust it down to the required width on computer. <strong>Here is what the dress should look like to suit Vicky's body proportions. </strong>It looks gorgeous on her!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image014.jpg" alt=""></p>
<p><strong>Here is another dress, a long one: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image016.jpg" alt=""></p>
<p><strong>Let us examine it carefully. </strong>We know that the girl is six heads tall, i.e. her head-to-height ratio is 1:6. If the dress has a long skirt and a short bodice, then the bodice should take up one sixth of the dress and the long slightly flared skirt - the remaining five sixths. <strong>I mark a dress length which suits Vicky's proportions.</strong> You will know that I am right when you look at the pictures below.</p>
<p><strong>Here is a well-proportioned dress for Vicky:</strong> one sixth for the bodice and five sixths for the free flowing skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image018.jpg" alt=""></p>
<p>The waist is purposely placed a little higher than natural to balance out the proportions.</p>
<p><strong>I have also improved the ratio of the waistband to the cups </strong>by dividing the area from the neckline to the skirt in about a half. In this case, the cups are to the waistband as is 1 to 1. The skirt looks proportionate to the bodice and corresponds with Vicky's head-to-height ratio.</p>
<p><strong>This work stage involves doing all necessary calculations: </strong>if you are good at using computer, mark all coordinates using Photoshop rulers and calculate the exact values in centimeters. Alternatively, you can measure the distance from the supposed waistline to the floor, as well as the length of the bodice, right on the child. Hang a measuring tape or a height chart on the wall so you can roughly estimate all values.&nbsp; Correct measurements will help you draft a correct pattern and sew a harmonious dress for your child.</p>
<p><strong>The picture below on the right shows Vicky's dress during the first fitting: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%201_files/image020.jpg" alt=""></strong></p>
<p><strong>All proportions are perfect! </strong>&nbsp;<strong>The dress looks exactly the way I wanted it to! </strong></p>
