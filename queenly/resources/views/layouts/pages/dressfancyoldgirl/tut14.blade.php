<h1>Tutorial 14. Carrying Out a Fitting. Finishing the Hem of the Dress.</h1>
<h2>Decorating the Dress.&nbsp; Pictures of the Finished Garment.</h2>
<p><strong>Vicky has come to the first fitting. </strong>Actually, it is the first and the last one.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image002.jpg" alt=""></p>
<p>You can tell that Vicky is happy about her dress. She feels comfortable in it. And she really loves this wide skirt!</p>
<p><strong>I only need to adjust the length.</strong> I set it at 1cm above the floor using a hem marker. Vicky will be able to walk freely this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image004.jpg" alt=""></p>
<p><strong>I mark the desired level along the perimeter of the skirt: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image006.jpg" alt=""></p>
<p><strong>And I trim the hem accordingly </strong>leaving one centimeter for folding it. The skirt pieces have naturally stretched out along the bias edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image008.jpg" alt=""></p>
<p><strong>I need to serge the hem edge. </strong></p>
<p>I form a corner with my fingers, fold the very tip, and then fold it another 0.5cm. <strong>I basically fold it by the width of the serging stitch.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image010.jpg" alt=""></p>
<p><strong>I fold the lining 1cm and stitch at a 0.5cm width from the edge: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image012.jpg" alt=""></p>
<p><strong>Then I carefully press the hem seam: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image014.jpg" alt=""></p>
<p><strong>What I do next is prepare some flower motifs for decorating the dress. </strong></p>
<p><strong>I cut three 12cm long ribbons in yellow and three 7cm long ribbons in pale lilac.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image016.jpg" alt=""></p>
<p><strong>I form loops with the help of Gutermann glue: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image017.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image022.jpg" alt=""></p>
<p>Then I glue on some more lilac and yellow ribbons of random lengths <strong>to give the motif a finished look:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image024.jpg" alt=""></p>
<p><strong>Next, I take three fabric roses of a matching colour and glue them on in the middle of the motif. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image026.jpg" alt=""></p>
<p>As soon as the glue sets properly, I secure the decoration in the middle of the dress. I highly recommend you heat seal the ends of the ribbons. <strong>And here is the final result: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%2014_files/image028.jpg" alt=""></p>
<p><strong>The dress is ready! </strong></p>

