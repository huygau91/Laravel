<h1>Tutorial 4. Duplicating the Pieces.&nbsp; Marking the Position of Bones.</h1>
<h2>Sewing on Bones.</h2>
<p><strong>I need to mark the position of the dart on all pieces.</strong> The end and the sides of the dart were marked by pins in the cutting process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image002.jpg" alt=""></p>
<p><strong>The dart must be transferred onto the iron-on batiste pieces</strong> for duplicating the face and the lining. It is easy because the line shows through the fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>It is time to duplicate the pieces. </strong></p>
<p><strong>I start from the back of the dress. </strong></p>
<p><strong>I fuse the short batiste pieces onto the lining and the long ones onto the dress itself. </strong>I only remove the pins at the top of the dress to keep the long pieces together. I will remove the rest of the pins after duplicating the pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image008.jpg" alt=""></p>
<p><strong>I take the front piece</strong> and remove all pins because I need to unfold it. <strong>I carefully put the iron-on batiste over the lining piece</strong> and <strong>fuse it on</strong> trying my best not to deform it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image010.jpg" alt=""></p>
<p>Then <strong>I put the non-duplicated piece on top</strong> of the duplicated piece to adjust its shape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image012.jpg" alt=""></p>
<p><strong>I put down another piece of batiste and fuse it on. </strong>This helps me achieve perfect symmetry.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image016.jpg" alt=""></p>
<p><strong>I fold the lining piece in half and mark the center. Now I am going to mark the positions of bones. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image018.jpg" alt=""></p>
<p><strong>Please note that there are no strict rules here - you are supposed to do it all by eye and rely on your experience.</strong> I divide the top and the bottom in about equal sections and draw two vertical bones. The bones at the sides will be placed at an angle.</p>
<p><strong>Let me share a small tip with you.</strong> Whenever you think of sewing a bone somewhere, always ask yourself why you want to place it there. What function will it perform? What will it support?</p>
<p>For example, right now I am asking myself whether I am going to sew a bone in the middle of the piece.&nbsp; And the answer is no, I am not! Sometimes, such central bones behave quite unexpectedly: they might, for example, start pushing the fabric upwards and deform the neckline.</p>
<p><strong>I take the back piece.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image020.jpg" alt=""></p>
<p><strong>I will probably add a bone along the dart later. The rest depends on the situation. I will also definitely lay a bone along either lacing edge 2cm from the central seam.&nbsp; </strong></p>
<p>But before sewing on the bones, <strong>I need to sew down the dart on the lining of the back.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image022.jpg" alt=""></p>
<p><strong>I make a row of stay-stitches along the top of the lining to define the edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image024.jpg" alt=""></p>
<p><strong>Then, I take a narrow Rigilene bone, </strong>tape its end with a piece of masking tape, step about half a centimeter from the top row of stay-stitches, and <strong>sew it on with two rows of parallel stitches </strong>stopping half a centimeter above the bottom edge of the iron-on fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/9%20yo/Tutorial%204_files/image028.jpg" alt=""></p>
<p><strong>This half a centimeter at the top will help me turn out the garment properly. Otherwise the bone might deform the shape of the neckline!</strong></p>
<p>In this dress, the bones don't have to surpass the waistline. The lining also doesn't have to be longer than that. There is absolutely no need in restricting a child's stomach or hips! Besides, there will be a skirt flowing freely down from the waist.</p>

