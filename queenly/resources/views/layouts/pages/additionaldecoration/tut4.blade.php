						<h1>Tutorial 4. Hot Fix Rhinestones. Working with an All-Purpose
							Tip.</h1>
						<p></p>
						<p>
							When I am working with rhinestones of a wide range of sizes, I
							simply use an all-purpose hot stop tip. Right now I need to apply
							a relatively small rhinestone so I take a rather small hot stop
							tip. <strong>You can see it in the picture below.</strong> I
							screw it on the applicator and wait for it to heat up.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%204_files/image002.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							I will use one and the same tip for working with different
							rhinestones. <strong>I have purposely decided to use rhinestones
								of mixed sizes. </strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%204_files/image004.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							I press the rhinestone lightly <strong>with the heated tip and
								wait a little for it to heat up too</strong>. I wait for about
							ten seconds and check if it holds well enough.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%204_files/image006.jpg"
								class="img-responsive center-block">
						</p>
						<p>This tip is very handy because it doesn't damage the
							surrounding fabric. I hold it down longer when working with
							larger rhinestones. It is also a good idea to go through the
							already applied rhinestones using the same tip and secure them in
							place a little better.</p>
						<p>
							<strong>I keep applying the rest of the rhinestones one after
								another.</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%204_files/image008.jpg"
								class="img-responsive center-block">
						</p>
						<p>That's how easy it is to apply a small number of rhinestones
							when you have no glue at hand but do have a hot fix applicator. I
							have demonstrated it just to make you aware of this method.</p>

