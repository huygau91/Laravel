						<h1>Tutorial 1. Glue-on Half Pearls.</h1>
						<p></p>
						<p>
							<strong>You can see two packs of faux pearls in the picture
								below: </strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%201_files/image002.jpg"
								class="img-responsive center-block">
						</p>
						<p>They are made of high quality plastic (acryl) and are very easy
							to work with. I glue them on with the same E6000 adhesive I used
							for applying rhinestones to the black corset in the previous
							tutorial. Each drop of glue should be a little smaller than the
							half pearl bead. Make sure to press the bead down very firmly. I
							also use the same rhinestone picker pencil and it works just fine
							even with large half pearls. I press the bead down with the other
							end of the pencil to stick it to the fabric properly.</p>
						<p>It looks particularly impressive when you use beads of
							different sizes: from the smallest to the largest! You can make
							rows of half pearls that gradually increase in size, mix them
							randomly, or choose a particular size to your liking.</p>
						<p>Of course, it makes a difference whether you use faux pearls or
							sew-on Japanese pearls: especially if you're sewing a dress for
							rent (which implies frequent wear). The coating of faux pearls
							eventually starts flaking while Japanese pearls stay as they are.
							These faux half pearls are a good choice for a dress which won't
							be worn often though. They do look just as beautiful.</p>
						<p>These pearl beads come in different colors. You don't need to
							sew them on and it's their greatest advantage. Gluing beads on is
							a lot less time-consuming.</p>
						<p>
							<strong>Let me take a sample piece of lace and show you how I
								glue these half pearls on.</strong> I put them on the table
							decorative side up. I just pour a generous amount of beads on the
							table and then turn over as many as needed. You can either work
							on a ready dress decorated with lace or on individual lace
							fragments which you are going to sew or glue on it later.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%201_files/image004.jpg"
								class="img-responsive center-block">
						</p>
						<p>The size of each glue drop should correspond with the size of
							the pearl. It's the tricky thing about this kind of work. Make a
							larger drop for large pearls: when you press the pearl down, a
							very thin border of glue should appear along the perimeter.</p>
						<p>
							<strong>I am going to glue on a row of half pearls gradually
								decreasing in size.</strong>
						</p>
						<p>
							<strong>I make a row of glue drops - from the biggest to the
								smallest.</strong>&nbsp; Make sure to account for the size of
							each pearl bead and leave enough space between the glue drops.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%201_files/image006.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							I pick up the largest half pearl with the pencil, put it on the
							first drop of glue, and press it down firmly until there is a
							thin border of glue around it. Then I take a smaller pearl bead,
							press it down, etc. The smallest pearl bead is placed at the very
							end of the decorative element. <strong>Here is what I got:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%201_files/image008.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I decorate two more fragments using the same technique:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/decoration/4/Tutorial%201_files/image010.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>A garment fully decorated with such pearl beads will look very
							rich!</p>
						<p>
							<strong>But you should always think about the way the garment is
								supposed to look from a distance when arranging pearl beads into
								patterns.</strong> Never forget about it when applying
							rhinestones, gems, pearls, or other decoration materials to your
							garment.
						</p>
						<p>
							<strong>The layout is supposed to look good from a certain
								distance:</strong> a relatively big distance if we are talking
							about a wedding dress (think a wedding hall). If it's an evening
							dress, which implies face-to-face conversations etc, then the
							control distance should be smaller - literally just a couple of
							meters.
						</p>
						<p>
							<strong>Besides, you should account for the goal you pursue:</strong>
							i.e. do you want the pattern to be clearly discernible from the
							control distance? Or do you simply want to highlight the color of
							the lace or the main fabric? In the second case, you need to
							carefully choose decoration materials of a subtle matching shade.
						</p>
						<p>
							In other words, you should always think about the way the
							embellished garment will look from the distance the wearer will
							be mainly looked at. It therefore makes sense to prepare a test
							sample, put it on the dress, take a few steps back, and study it
							from a distance. <strong>If you are happy with the results, you
								can safely use this type of embellishment!</strong>
						</p>

