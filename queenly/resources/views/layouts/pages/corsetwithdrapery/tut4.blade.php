<h2>Tutorial 4. Cutting the Corset.</h2>
<p></p>
<p><strong>I have drafted and cut out cardboard patterns referring to the mock-up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I start transferring them onto the mesh fabric. </strong></p>
<p><strong>I start with the central front piece. </strong>The first thing I should do is remove half a centimeter across the width of the whole piece from top to bottom. I place the pattern underneath the fabric to trace it. I have drawn a new bottom line according to the altered pattern. I mark necessary notches. Then I look into the table and see that I need to add 1mm at the waistline level of the front princess seams. I finish it off.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The next piece is the intermediate front piece. </strong>I follow the same procedure: outline the contour and add 1mm at the waistline level. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The next piece is the side piece. </strong>It requires more alterations as I need to shift the side seam 1.5cm outwards. I add 1mm to the princess seam as well.&nbsp; And mark a new notch. </p>
<p><strong>Here is an interesting detail: </strong>if the bottom line were less complicated, I could re-distribute this 1.5cm between this side front piece and the intermediate piece. I would add 7mm to each piece and expand them proportionally. But since we have calculated the bottom line, I can add 1.5cm only to the side piece. As you can see, it is important to think over the sewing process in advance and model all pieces before cutting them out. If you find modelling the bottom line difficult, I suggest you should sew a couple of mock-ups. Then model the bottom line directly on the mock-up sewn after your adjusted pattern to achieve the shape you want. And only afterwards you can apply relevant alterations to the general pattern. </p>
<p><strong>In order to speed up the process </strong>you can cut patterns out of your main fabric (the mesh fabric in our case) at its full length and then draw model lines right on the finished garment and cut the bottom the way you like. <strong>By the way I use this method a lot more often </strong>because it allows me to combine the modelling process with the first fitting. You just put the corset on your client as is and draw a new line the way you see it or maybe your client will have her own ideas to share. Following your client�s advice is quite acceptable in this case. And that�s also exactly the case when you could alter patterns proportionally, i.e. expand both (intermediate and side) pieces by 7mm. </p>
<p><strong>So I outline the piece, shift it by 1.5cm and draw a new line for the side seam. Please note the way it should be done:</strong> I shift the piece and re-draw its original line using the pattern. And then I connect the ends of the old and new lines of the side seam with a smooth line using the original pattern as a stencil. I don�t just use horizontal lines to connect the old and new lines of the side seam at the top and down the bottom. Had I done so, we would�ve already lost 2mm of the side seam length. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>This often raises questions when done wrong: </strong>"I have adjusted the pattern but the pieces don�t match. Neither the side seams nor the length of the princess seams match. There must be a fault in your pattern". </p>
<p>No, this is not the case! <strong>There are no faults in the original pattern - you must have made a mistake during the cutting process! Now let me draw the side piece of the back. </strong>I start by outlining the piece as is and apply necessary alterations afterwards. </p>
<p><strong>The princess seam: </strong>I add 1mm at the bust level, remove 2mm at the under-bust level, add 1mm at the waistline level and proceed to the stomach level with no alterations. The seam looks rather wavy but - trust me - it will straighten when you join the pieces together.&nbsp; </p>
<p><strong>The side seam: </strong>there are no alterations at the bust-line level but I need to remove 1cm starting at the under-bust level and going down to the bottom along the whole length. I use the original pattern as a template for drawing a new side seam in the area from under the bust down to the bottom. I join the ends of the old and new side seam lines using the pattern as a stencil, the same way I did with the side piece of the front.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>The intermediate back piece: </strong>and again I place the piece underneath the fabric, outline it and draw a new bottom line. </p>
<p><strong>Only princess seams require adjustments here. </strong>I use the original pattern to re-draw the bottom line. I add 1mm at the bust level, remove 2mm at the under-bust level, add 1mm at the waistline level and proceed to the stomach level with no alterations. The result is a slightly deformed dart. I apply the same alterations to the second princess seam: add 1mm at the bust level, remove 2mm at the under-bust level, add 1mm at the waistline level and proceed to the stomach level with no alterations.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And finally the last central piece of the back. </strong>I place it underneath the fabric, outline it, draw a new bottom line and apply the same alterations to the princess seam. </p>
<p><strong>Here are our patterns transferred onto the mesh fabric.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The mesh fabric will be duplicated by chiffon. </strong>How to cut it correctly? Basically, it makes no difference whether you cut it lengthwise or crosswise. You are free to arrange the patterns any way you like. I fold the chiffon in half, overlay it with the mesh fabric with transferred patterns, match their fold lines carefully and secure them with pins. </p>
<p><strong>Let me give you a small tip:</strong> I recommend that those who don�t quite comprehend the pattern should <strong>number its pieces</strong> using a disappearing ink pen, a piece of chalk or paper stickers. Numbers are marked at the spots where you should start joining the pieces. They also point at the top of each piece and show what points precisely should be joined. It will help you avoid confusion. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p>It is better to use some �throwaway" scissors for paper or cardboard because mesh fabric might dull your working scissors. </p>
<p><strong>Here it is - I have carefully cut out all our corset pieces.</strong> They are numbered. Don�t worry if the chiffon doesn�t look that neat from the other side. It is very thin and prone to deformation. But we will cope with this trouble and sew a beautiful corset!</p>
