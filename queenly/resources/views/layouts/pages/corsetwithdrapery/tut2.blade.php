<p><strong>Next I join the cup pieces. </strong>Working with a mock-up you can check if all notches match and if all pieces coincide properly and determine areas that require some easing-in (or those where fullness should be removed). You can also slightly customize the pieces. In other words it is a kind of a final rehearsal before sewing the actual garment. Sometimes new ideas concerning assembly sequence or junction treatment strike you as you�re working on a mock-up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image026.jpg" class="img-responsive center-block"></p>
