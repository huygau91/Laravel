<p><strong>Now I take the second pattern, match it with its corresponding cut-out piece and mark the top edge.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image064.jpg" class="img-responsive center-block"></p>
