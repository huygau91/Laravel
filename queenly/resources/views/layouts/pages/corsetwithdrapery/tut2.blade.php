<p><strong>It is a good idea to topstitch all seams now </strong>turning their seam allowances to whichever side you want them to stay on when the garment is finished. I would like you to pay special attention to the direction of each seam allowance because it has a direct influence upon the location of vertical seams and outer cased bones that will later be stitched along all vertical curves of the corset. As a rule you lay a cased bone over a seam allowance. So the seam allowances that show through fabric now basically indicate the location of cased bones. </p>
