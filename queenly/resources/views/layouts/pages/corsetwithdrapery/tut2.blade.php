<p><strong>I join the side seams </strong>after matching their waistline and bust-line notches keeping in mind there is an additional seam allowance along the top edge of the cup piece for joining the face with the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the side seams directing their seam allowances towards the back piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Then I write the number of each corset piece at its bottom </strong>because I want to model the bottom line later. Since we are going to alter the configuration of the bottom, I have decided to number the pieces before putting the mock-up on the dress-form to avoid confusion and to know where each part belongs. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Now I put the mock-up on the dress-form </strong>and give it a proper shape with the help of foam shoulder pads and padding polyester.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>And then I take a measuring tape and measure the results.</strong> == </p>
<p>&nbsp;<strong>- Along the waistline:</strong></p>
<p><strong><br>
