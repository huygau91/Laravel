<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Do it any way you find convenient: </strong>you can unstitch the pieces, iron them and arrange them together to apply necessary alterations, or you can do it without unstitching them. It depends on your skills. </p>
<p><strong>I take one pattern and lay the corresponding cut-out piece over it. </strong>Then I outline the top edge of the cut-out piece (keep in mind that there is a seam allowance here). </p>
