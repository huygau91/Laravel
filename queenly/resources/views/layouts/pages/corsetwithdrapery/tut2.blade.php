<p><strong>I match the cup piece with the corset body and join them.</strong> </p>
<p>I make notches in the middle of the cup and the body pieces and match them along these notches. I make a joining seam starting in the middle and going left and then I make another one starting in the middle but going right.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image030.jpg" class="img-responsive center-block"></p>
