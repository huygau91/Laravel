<p><strong>I topstitch the joining seam </strong>directing the seam allowances up towards the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to join the side seams.</strong> </p>
<p><strong>I match their bust-line notches. </strong>Please note that there is some additional allowance for finishing the lining on the cup piece whereas the back piece will have a pencil-edge finish along its top edge and therefore needs no extra allowance.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%202_files/image036.jpg" class="img-responsive center-block"></p>
<p>
