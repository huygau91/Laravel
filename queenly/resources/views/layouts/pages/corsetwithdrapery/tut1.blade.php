<h2>Tutorial 1. Getting Acquainted with the Model. Cutting a Mock-up.</h2>
<p></p>
<p><strong>I am about to begin working on the first corset sewn in a simplified technique. </strong>I will describe the whole work process in detail, step by step. Moreover, in the framework of this course I will address important issues that many of you are concerned with: what is a corset mock-up, how to measure patterns correctly to be able to adjust them after your client�s measurements, how to model a corset (change its bottom line, shape, length, certain details or pieces and curves). In other words I will explain you all about how it should be done. </p>
<p><strong>So, what is this simplified technique for sewing a corset?</strong> </p>
<p>Let me make a <strong>technical drawing</strong> and you�ll see!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>We will sew a corset shaped like this.</strong> </p>
<p>It has a usual corset back; the only difference is that I will add two extra curved seams (princess seams) that will provide more opportunities for alteration. Using two extra curves at the back makes it much easier to alter and adjust the pattern after individual measurements. The drawing represents the structure of the corset we will sew. </p>
<p><strong>This type of a corset is peculiar in that its cup piece has no face part. </strong>All the corset will have at the front is a beautiful drapery! This drapery will pass onto the back and end there. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>What kind of fabric will be used for sewing this corset?</strong> </p>
<p><strong>The base is mesh fabric typically used for corsets.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p>There have been broad discussions among members of my online forum on where to find this kind of fabric. As far as I know, such mesh fabric is used for silk-screen printing and for producing beach bags, beach sandals or summer sandals. In short it is used in small wares and footwear industry. It is see-through, very light and very strong. </p>
