<h2>Tutorial 9. Finishing the Lacing Bars. Sewing the Side Seams.</h2>
<p></p>
<p><strong>In this tutorial I will show you how to finish the raw edge of the back and the lacing bars. I begin by placing a wide Rigilene bone at the middle cut of the back from the face side of the corset (the mesh fabric side).</strong> I make one vertical stitch along the inner side of the bone.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Then I sew on another wide Rigilene bone parallel to the first one, almost edge-to-edge. </strong>The bones I use are 1.2cm wide.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the second bone with two parallel stitches.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I repeat the same procedure with the other half of the back.</strong> I sew on two parallel bones nearly edge-to-edge. </p>
<p><strong>Any extra mesh fabric and chiffon sticking out from behind the outmost bone should be trimmed.</strong></p>
<p><strong>Now I take one of the lacing bars that we have prepared previously. </strong>Its wider part should be on the lining side and the narrower part will be folded over the face. I place the parallel bones as close to the fold line of the bar as possible.<strong> And I make a joining stitch along the outer edge of the first bone.</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make another vertical stitch at a presser foot distance from the second bone in order to secure the bar. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I carefully fold the bar over and make a stitch at a 1.2cm distance from the edge. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>This is the first tunnel </strong>for inserting a plastic or a steel bone. </p>
<p></p>
<p><strong>Now I pull the loose end of the bar slightly to me to provide tension and make a strengthening stitch at a presser foot distance from the edge of the bar.</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I want to finish the visible raw edge of the bar with a beautiful lace ribbon. </strong>I sew it on so as to cover the raw edge of the bar. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I make a parallel stitch to secure the lace. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>That�s one of possible ways to finish the edge of a lacing bar. </strong></p>
<p><strong>And now I repeat the same procedure with the other half of the back. </strong></p>
<p>I put the halves of the back together and trim superfluous ends of the lacing bars. </p>
<p><strong>That�s what my lacing bar looks like from the lining side of the corset:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I can sew the side seams of the garment. </strong>I match the waistline notches of the front and the back of the corset and <strong>sew a side seam with a 1.2cm allowance</strong> starting from the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Please note:</strong> the seam allowance of the back pieces is used for attaching the cup piece. </p>
<p><strong>I make an additional notch at the seam allowance of the back piece, at the level of the top edge of the front body piece that will be sewn to it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
<p>I match it with the other half of the back piece and transfer the notch onto its seam allowance. This is done in order to ensure that the front piece will be attached to the back piece symmetrically from both sides. This will help attach the cup piece later. </p>
<p><strong>Now I place the front piece at the other half of the back piece very accurately and - making the first and only bar-tack - sew the second side seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>You probably have a question: where to direct the side seam? </strong></p>
<p><strong>It should be directed strictly towards the front </strong>because directing or turning it towards the back will cause the seam allowance for inserting the cup piece to turn as well.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I direct the seam allowance towards the front and topstitch it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance of the side seam only along the sewn area. </strong>You shouldn�t touch the area of the seam allowance that will be inside the cup piece. </p>
<p><strong>Now I do the same with the other side seam. </strong>I direct the seam allowance towards the front, topstitch it and trim it along the sewn area. Be careful not to cut through the chiffon when you trim. </p>
<p><strong>This way we preserve the seam allowances of side seams that will go inside the cup piece. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I stitch a cased bone onto the side seam. </strong>Watch the location of the bone. Its edge (in our case the right one) should overlay the seam from the very beginning. <strong>Try not to let the edge of the bone go over the seam allowance of the cup piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the bone with another parallel stitch.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And then I cover the side seam on the other side the same way. </strong>Make sure to spread fabric underneath the bone to avoid bubbles. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Our corset body is ready!</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%209_files/image042.jpg" class="img-responsive center-block"></strong></p>

