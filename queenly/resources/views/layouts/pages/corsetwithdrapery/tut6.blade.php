<p>Don�t be disturbed by minor mismatches of pieces but do try to arrange the chiffon evenly underneath the mesh fabric. Use your finger to spread it when needed. And remember to make sure there are no folds or tucks and <strong>the mesh fabric is covered with the chiffon perfectly evenly.</strong> Horizontal edges should be left as is for now, don�t stitch them up.</p>
