<p><strong>Next I take an intermediate front piece (piece 2). </strong>I unpin it and prepare it for sewing. I recommend unpinning the pieces one by one as the process advances instead of unpinning them all in one go. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p>What I do now might be the most toilsome work in the whole process of making a corset with a plastic mesh base. I keep arranging the chiffon with my finger because there is bias grain along nearly all edges of all narrow pieces. </p>
<p><strong>I take the second intermediate front piece. </strong>I will arrange the pieces in a vertically mirrored fashion and sew them the same way. I look to the pen-written marks not to misrecognize the sides and to make sure the pieces mirror each other. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
