<h2>Tutorial 6. Joining the Hard Mesh with the Chiffon Lining.</h2>
<p></p>
<p><strong>Now is the time to start sewing the body of the corset. </strong>Right now we�re using double-faced fabrics that look the same from both sides. I am talking about the hard mesh and the chiffon. When you work on a see-through corset, it is particularly important to avoid confusion between left and right pattern pieces because the face and the lining look the same. As we were cutting out patterns, we numbered them for easy matching. I had turned the pieces upside down and numbered them from the other side as well. This helps you know which side is a conventional face side (in other words which side should face up). This will prevent us from joining a pair of identical back pieces for example. We are about to join the mesh and the chiffon: all patterns should be carefully arranged so that we can tell the left side from the right one. </p>
<p><strong>Let�s start from the central front piece. </strong>I unpin it and spread it. I�m going to join the chiffon and the mesh fabric now. You should by no means join them all around the perimeter but <strong>only along their vertical edges</strong> or else you�ll end up with a bubble distorting the corset. <strong>You should look to the mesh fabric as a reference since the chiffon is only a covering fabric. </strong></p>
<p><strong>I start joining them.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p>Whenever you start performing this operation, it is hard to tell whether you should place drapery fabric over mesh fabric or underneath it. I work with the first piece to test the process. This time after making the first seam I could tell that sewing is uncomfortable with the chiffon on top. </p>
<p><strong>I turn the piece upside down and stitch along the other vertical edge. </strong>Transparent mesh fabric allows me to see the way the chiffon lies and I can spread it, pull it and even it out to match the edges well. </p>
<p><strong>The stitch is made at a presser foot distance. </strong>Don�t worry about the chiffon going beyond the edges of the mesh fabric at the top or down the bottom - this is not relevant at the moment. <strong>The main task is to join the vertical edges of the parts as neatly as possible.</strong> </p>
<p>
