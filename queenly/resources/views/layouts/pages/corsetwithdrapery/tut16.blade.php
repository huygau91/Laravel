<h2>Tutorial 16. Marking the Drapery.</h2>
<p></p>
<p><strong>I put the corset on the dress-form. </strong></p>
<p>Don�t worry if the size of the corset doesn�t correspond to that of your dress-form. Our task is to spread the corset as well as possible and fix it properly in place. </p>
<p><strong>This is one of the main preconditions for making drapes. </strong></p>
<p>I am going to drape the corset as shown on the technical drawing I made at the very beginning of this course. </p>
<p><strong>You should mark the borders of drapery areas with the help of cords</strong> in order to know for sure which way the drapery should be arranged. Remember, the fabric will rest the same way the cord does. </p>
<p></p>
<p><strong>I pull the cord across the border between planned drapery areas in the middle of the corset and trace its line with a disappearing ink pen (line 1).-</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to determine the location and direction of folds at the back. </strong>I pull the cord again and draw the border of the second drapery area (line 2) that goes from the vertical edge of the back to the middle of the side front piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The drapery should fall at the same height on both halves of the back. </strong>That�s why I measure the distance between the top point of the cord on the first half of the back and the top corner of the back. I mark the same distance on the second half of the back and pull the cord on from that spot to draw another borderline for the drapery. I bring the cord to the intersection with line 1 and draw line 3 with the disappearing ink pen.</p>
