<h2>Tutorial 19. Securing the Drapery. Finishing the Lacing Bars.</h2>
<p></p>
<p><strong>I have prepared two wide Rigilene bones with a length that equals the length of the back minus 1cm. </strong>I have taped their ends with pieces of white duct tape to make sure no fishing lines will stick out. </p>
<p><strong>Now I�m going to encase these bones in fabric straps in such a way that some fabric is left from each end of the bone and can serve as an allowance that will be bent over the edge. </strong>We have already performed this operation before but I forgot to leave this extra fabric back then. I make the first stitch and then the second one pulling the fabric to me. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I take the corset and secure the drapes with a stitch. </strong>There will be a row of grommets along the first tunnel (1) and a fabric-cased bone along the lace ribbon. You need to secure the drapery in such a way that you can trim any extra fabric and overlay the edge with a cased bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I basically stitch along the edge of the bar that I sewed on to finish the lacing.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>You can see the stitch from the face side of the corset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I trim all extra fabric that goes beyond the lacing bar leaving about 0.5cm.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I overlay the edge of the drapery with a cased bone. </strong>I bend its ends inwards from both sides. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And then I start stitching it on. </strong>I try to place the new bone edge-to-edge to the parallel one next to it so as to have three parallel bones in a row. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Then I make a strengthening stitch along the other side of the bone. </strong>Make sure the folds are spread properly and try to avoid any tucks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>The same procedure should be performed on the other side of the corset. </strong></p>
<p><strong>That�s how you can finish lacing bars and achieve a clean drapery edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I should fix all our draping straps together. </strong>I do it by making a joining seam at a presser-foot distance from the edge of the corset to capture all straps beyond the border. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I make sure there are no tucks and check if it�s easy enough to bend the fabric over the edge.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>And then I trim and even out all draping straps leaving about 1cm from the stitch I have just made. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>This raw edge needs some treatment. </strong>I am going to finish it with lace but you could also use a bias tape or fabric welting. <strong>Put the edge of a lace ribbon over the strengthening stitch (that serves as a guideline) and sew it on. </strong>Stop sewing at the corner of the corset bottom. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I bend the corner from one side.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put the lace ribbon over it and keep stitching it onto the seam allowance. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>And then I just bend the ribbon to the inner side of the corset, pin it and secure it with hand stitches. </strong>You can iron it as well. This is a neat way of finishing the edge of a corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>Next I should repeat the whole procedure with the top edge of the corset. </strong></p>
<p><strong>I make an outer joining seam at a presser-foot distance from the top edge of the corset.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image033.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And I trim and even out the draping straps.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Then I place another lace ribbon at the edge and sew it on up to the corner of the neckline.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image037.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I bend the laced section to the inner side of the corset and secure it with pins.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image039.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Next I sew on another lace ribbon that goes from the other neckline corner to the opposite edge of the top edge:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image041.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I bend it to the inner side of the corset and secure it with pins.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I sew on a lace ribbon along the neckline of the corset.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I bend the last laced section of the top edge to the inner side of the corset and fix it with pins. </strong>Now you can secure the whole top edge with hand stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>This is what the inside of our corset looks like after its top and bottom have been given a neat finish: </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2019_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The corset is almost ready. All we need to do now is add a modesty flap for the lacing area, punch holes and insert grommets.</strong></p>

