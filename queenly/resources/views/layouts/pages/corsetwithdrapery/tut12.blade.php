<h2>Tutorial 12. Pressing the Cups and Covering Them with Padding Polyester.</h2>
<p></p>
<p><strong>Now I start pressing our finished cup piece. </strong>I use a cups pressing mould for this purpose. I carefully press the sides too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now the cups have a fixed shape.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I need to cover the cup piece with straps of padding polyester. </strong>This is done in order to prevent stiff bones from protruding through the face fabric and preserve the smooth look of the surface. If you find your layer of padding polyester too thick, you can flatten it with your press-iron. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the polyester to the cups.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>You can see the pins from the other side. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I need to attach the polyester to the piece in a secure way. </strong></p>
<p><strong>I start from the side edge of the piece. </strong>You can stitch either along the vertical bone or along the edge. <strong>I stitch along the edge. </strong>The strengthening stitch goes along the inner stitch attaching the vertical perimeter bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>You may turn the piece around on the needle when sewing.</strong> </p>
<p></p>
<p><strong>I turn the piece and start securing the padding polyester along the bottom edge following the inner stitch that attaches the bottom horizontal perimeter bone.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I move along the other side edge of the piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>As I reach the underarm area, I place a ribbon loop there for hanging the garment when it�s finished. </strong>The ribbon is 20-25cm long when unfolded.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I continue stitching through the padding polyester along the inner stitch attaching the top horizontal perimeter bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the opposite underarm area and place the second ribbon loop there.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Then I return to the starting point </strong>and remove all extra threads. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I recommend securing the ends of the ribbons so they don�t fly off. </strong>I make the second strengthening stitch.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the pins. </strong>Look at the other side of the piece to check if any more pins are left. <strong>Make sure no pins stay in the cups! </strong></p>
<p><strong>Now I trim the edges of padding polyester. </strong>You can remove excessive thickness using the scissors. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And now you should fold inwards all remaining seam allowances in the underarm areas and at the top of the piece the way it was done at the sides and at the bottom. </strong></p>
<p><strong>I notch the seam allowances to make them easier to fold. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the seam allowance at the top of the piece inwards (the seam allowances in the underarm areas will be folded and secured when you insert the cup piece into the corset body). </strong>The stay-stitch should remain on the rib of the crease! </p>
<p><strong>I make a strengthening stitch. </strong>Make sure this stitch is very even because you are basically shaping the top edge of the cups now! </p>
<p><strong>The look of the whole garment depends on the quality of this stitch!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2012_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>The cup piece is ready! We can insert it into the corset body!</strong></p>
