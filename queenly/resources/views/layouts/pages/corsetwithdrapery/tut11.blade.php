<h2>Tutorial 11. Final Steps of Quilting the Cups with Bones.</h2>
<p></p>
<p><strong>Now I need to quilt the cups with inner bones. </strong></p>
<p><strong>First of all I sew on a straight ungathered bone along the bust-line. </strong>There are different ways of putting this bone. You can lay it below or above the marked line or right over it. I am going to lay this bone to the left of the marked line. I push the end of the bone underneath the vertical bone. I pull the fabric slightly to me while pushing the bone away from me. This is done to prevent fabric from getting eased in underneath the bone. But don�t try too hard: it�s important to find the golden mean because soft cups easily get stretched and deformed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone, push its end underneath the vertical perimeter bone and make a bar-tack.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I would like to point out that for now all perimeter bones have been attached with just one outer stitch.</strong> </p>
<p><strong>I go back and secure the bone with another parallel stitch.</strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now you can lay a bone over the princess seam. </strong>I push its end underneath the horizontal bone at the top and start sewing it on pulling the fabric to me and pushing the bone away from me the same way I did before. Then I trim it, push its end underneath the bottom horizontal bone and go back with a parallel stitch. You can start sewing on these bones either at the top or down the bottom, it doesn�t really matter. An important thing is to prevent fabric from getting eased in underneath the bone and avoid stretching the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same procedure with the other seam allowance.</strong></p>
<p><strong>And I add a bone in the centre following exactly the same procedure.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to sew on more bones that would form a crisscross. </strong>The bones will lie better if you gather them slightly. You could also use straight bones but make sure they don�t distort the fabric. Push the end of each bone underneath the relevant perimeter bone. Let it lie naturally following the marked line we have drawn previously. Instead of trimming the bone at the very edge of the bone perpendicular to it, trim it at its midpoint. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take more gathered bones and quilt the cups diagonally forming a kind of a snowflake.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Now you can lay additional vertical bones to enclose the cups. </strong>I place an ungathered vertical bone from each side of the cup piece 5-6cm from the edge. The larger the size of the cups is the bigger the distance from the edge should be.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>This is a simplified technique for quilting the cups.</strong> The under-bust line of the cups is straight, the neckline hasn�t been rounded yet and there is no distinct arch. </p>
<p><strong>I finish forming the cups. </strong>I make the second strengthening stitch along the perimeter bones making bar-tacks at the beginning and the end of sewing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I need to perform one more operation that will allow me to insert the cups into the body of the corset neatly. I make notches at the bottom edge of the cup piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the seam allowances inwards, first at both side edges and then at the notched bottom edge, and stitch through them. </strong></p>
<p>The strengthening stitch is made 1-2mm from the edge. I fold each seam allowance over the bone and the stay-stitch stays on the rib of the crease. </p>
<p>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Now the cup piece is ready for getting inserted it into the corset body. </strong></p>
<p><strong>I get rid of excessive thickness along the seam allowances.</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2011_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And then I turn out the piece. </strong></p>
