<h2>Tutorial 14. Joining the Body with the Cups. Inserting Stiff Bones.</h2>
<p></p>
<p><strong>Now we should attach the cups to the body of our corset. </strong>I will use an overseam for this purpose. Everything has already been prepared and I only need to pin the cups in place. </p>
<p><strong>I start at the top. </strong></p>
<p><strong>I place the seam allowance of the side seam of the body over the seam allowance of the side seam of the cup piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s look at the way the notched seam allowance at the top edge will fold.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I start pinning the cup piece to the body. </strong>Make sure the seam allowance stays even.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And now I repeat the same procedure with the other side of the corset. </strong></p>
<p><strong><br>
