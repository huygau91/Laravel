<p><strong>After pinning it at the sides I move on to the bottom of the cup piece. </strong>You shouldn�t try too hard to match the princess seams of the cups and the body. Just make sure they are symmetrical to the quilted bones from both sides and that the seam allowance stays the same. You should pin the cups very evenly, strictly referring to the seam allowances. I know that some people prefer doing it on a dress-form. They put the body of the corset onto the dress-form, place the cups at it and secure them with pins. As for me I prefer using my desk or pressboard for this purpose, or sometimes even my own knee. It�s important that you feel comfortable working with pins. Make sure the corset is spread well and there is no unintended easing in. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And now I should stitch through it with the sewing machine. </strong>The stitch goes over the cups from the lining side and all pins stay inside. Keep moving along the edge of the bone, you could even try to get right into the seam that attaches it. Be careful as you go: it is quite thick and then there are still pins there. You should pass over all princess seams both on the cups and the body. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove all pins and check the quality of the seam. </strong>It�s important to make sure the thread didn�t break at the areas where the pins were located. </p>
<p><strong>And now I should sew the bones that poke out onto the inside of the cups. </strong>I sew them on from both sides with a simple stitch. This procedure is a must because you need to secure the bones so that they stay inside the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the seam allowance of the cup piece in the underarm area, notch out excessive thickness, and make a stitch about 2-3mm from the edge. </strong>The same procedure should be done from the opposite side.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>This is what the cup piece looks like when inserted into the corset.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now we need to insert either plastic or steel spiral bones. </strong></p>
<p>As I have already mentioned a few times, I work only with plastic bones. This corset is meant for a young girl and I don�t think it�s necessary to insert a plastic bone into each tunnel, but you could do it if you wanted. <strong>I will alternate between empty tunnels and bones.</strong></p>
<p><strong>How do I measure the length of a bone? I lay it over a princess seam starting at the top edge of the tunnel and cut it off about 1.5cm above the bottom edge. </strong>Then I trim the corners at the end of the bone. I always cut off two symmetrical bones and insert them one after another into symmetrical tunnels from both sides of the corset. I push the bones inside with their manufactured ends first. </p>
<p>Unfortunately all bones that are sold by the meter at our local stores are too soft and could only suit for making children�s corsets. </p>
<p><strong>Why don�t I let the bones reach the very bottom of the tunnels? </strong>If I did, they would hamper further treatment of the corset.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I insert the first bone that goes along the edge of the back. I insert it from the face side.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And then I insert a bone of the same length at the opposite edge of the back, from the face side as well. </strong>I use a screwdriver to push bones inside properly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>All subsequent bones are inserted from the lining side in symmetrical pairs.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2014_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>So, I have inserted all stiff bones from the lining side (apart from those at the edges). </strong></p>
<p><strong>The surface remains smooth from the face side so I can easily drape it. </strong></p>
<p><strong>Using draperies is always easy when you work with see-through corsets </strong>because they are single-layered and thin but at the same time very strong. </p>
<p>It can be quite a shame <strong>when bones inserted from the face side</strong> protrude through a beautiful drapery of a corset. Some people are afraid of working with bones sewn over princess seams and prefer making an opaque corset instead of a see-through one - but a corset like this looks too thick when draped. </p>
<p><strong>If you want to, you can insert stiff bones inside the intermediate tunnels </strong>but I don�t think it is crucial. I can tell that the corset is already stiff enough and there�s no need of overloading it with additional bones. </p>

