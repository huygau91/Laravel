<h2>Tutorial 5. Cutting the Cup Pieces. Marking the Location of Bones.</h2>
<p></p>
<p><strong>Now I start cutting the cup pieces. </strong></p>
<p>I would call the technique used for sewing this corset a <em>relatively</em> simplified technique rather than plainly simplified. The matter is that the simplified technique involves a smaller number of pieces, a faster sewing process and less usage of fusible materials (used for the cups in our case). But in that case you need to use rather dense and resilient fabric for the lining. If you use a lighter material for the lining, you will have to duplicate the cups with some iron-on stiffening fabric in addition to iron-on batiste.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p>I am using fabric of medium density and medium thickness for sewing this corset. I know how to work with this kind of fabric from my own experience so <strong>I just fuse it with a single layer of iron-on batiste.</strong> However I would recommend using stiffer materials for better bonding for a start. If buying such materials is problematic for you, you can try to do without them. </p>
<p><strong>I am going to cut the cup pieces crosswise. </strong>&nbsp;In this case the pieces will stretch vertically and not along the bust-line. </p>
<p>I have torn off a piece of iron-on batiste that�s big enough for cutting our cup pieces. I lay the fabric on my pressboard and fuse it with iron-on batiste. This piece of fused fabric will be used for cutting the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>Now I need another piece of iron-on batiste that has the same length as the back of the corset. I will cut out two straps for finishing the lacing bars: each 7.5cm wide. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p>The piece of fabric fused over these straps should stretch vertically. These will be our two lacing bars. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p>I cut out the fused lacing bars and the piece of fused fabric meant for cutting the cups. </p>
<p><strong>Please note that I cut out only one piece of lining for the cups. </strong></p>
<p><strong>I fold the bars leaving 4cm from one side and 3.5cm from the other, and press them lengthwise. </strong>The wider side of the bar can later be finished in any desirable way: with an overlock, a strap of thin lace or a pretty ribbon. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And now we move on to the cups. </strong></p>
<p>I fold the fused piece of fabric in half and press it. Then I take the pattern and my notebook with measurement alterations in order not to miss anything. <strong>Let�s draw our adjusted pattern. </strong></p>
<p><strong>The central cup piece: </strong>as you can remember, I have to narrow the cups by removing 0.5cm from the bust middle. I outline the standard pattern unchanged for now. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to lower the neckline so that the distance between it and the bust-line is 4cm. </strong>I add 1.2cm for a seam allowance and make a mark on the central fold line 5.2cm above the bust-line. <strong>This is a point of reference (D) for drawing our new line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p>Before applying other alterations to the clip measurement and the side seam, I need to outline the <strong>standard pattern of the side cup piece</strong>.<strong> &nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the side seam by 1.5cm right away and transfer the bust-line notch.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the length of the side of the cup piece using the pattern! </strong>There would be a great mismatch if I simply used horizontal lines to connect old and new lines of the side seam at the top and down the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Here is our new side seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to shift it down by 2cm. </strong>(It has just occurred to me that I forgot to account for this adjustment when preparing the corresponding piece of the corset body. I am going to correct this mistake a little later). I mark a point 1.2cm above the new side seam line to account for a seam allowance and this very point (A) is now the top point of the side. So I need to shift the side down by 2cm starting from this point.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>What I got is a new top point of the side length of the piece (B).</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>According to the clip measurement alterations </strong>I need to reduce the distance between the middle of the bust and the underarm by 3cm (line 1) and shift the corner down by 2cm (line 2). It is clear that I need to subsequently <strong>reduce the length of the princess seam </strong>by about 2cm (lines 3 and 4). </p>
<p><strong>Next I should find the bust middle point (M) </strong>that is used for taking the clip measurement. I determine it by marking 1.2cm from the cutting line along the bust-line to account for a seam allowance. </p>
<p>My client�s clip measurement from the bust middle to the middle of the underarm is 9cm. I add 1.2cm for a seam allowance and end up with 10.2cm. I mark this length as a radius going from point M to the intersection with line 1 (point B1). </p>
<p>The corner of the corset should be located at the height of 10cm. I add 1.2cm for a seam allowance and get a total of 11.2cm. I mark this length as a radius going from point M to the intersection with line 2 (point B2). </p>
<p><strong>Now we have a clear view of what the armscye should look like.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the armscye of our corset by connecting points B, B1 and B2 with a smooth line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And then I connect it with the top line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image034.jpg" class="img-responsive center-block"></p>
<p>After measuring the distance from point D to the top line I have found out that it got shifted by 3.2cm instead of 2cm. </p>
<p><strong>I start transferring the line at the same angle as on the original pattern.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>And try to make a smooth line connecting it with the new mark.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image040.jpg" class="img-responsive center-block"></p>
<p>Even if the top line is located somewhat higher than needed in the middle, it might be worth leaving as is. You should always ask yourself what�s more important: a precisely taken measurement or beautiful blending of lines. Remember that taking a 100% precise measurement is impossible; it remains an approximation. And it�s important to have all lines blending smoothly. <strong>You can always arrange pieces together to check where the top line of the corset will go when the garment is finished.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image042.jpg" class="img-responsive center-block"></p>
<p>If you�re satisfied with the way the top line passes from the side cup piece onto the central one - just leave it as is. <strong>You can always correct your top line, should you for whatever reason need to make it end at the point that strictly corresponds to the measurement.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>In our case the princess seam got shifted down by a couple millimeters and the corner of the corset went upwards. </strong>But the top line is precise and the length of the princess seam has been double-checked.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image046.jpg" class="img-responsive center-block"></p>
<p>I have applied all necessary alterations to the cup pieces.<strong> Now I pin them together and cut them out following their new lines. </strong>Let me remind you that the cup pieces are cut crosswise and stretch vertically. I make notches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>You can mark the location of bones right now on these patterns. </strong></p>
<p><strong>First of all I need to match the bust notches. </strong></p>
<p><strong>I am going to quilt these cups with bones using a �snowflake" technique. </strong>This technique is widely used in simple corsets. Bones are stitched-on in the following sequence: <strong>first along the perimeter and then in a crisscross fashion from the middle of the bust. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image050.jpg" class="img-responsive center-block"></p>
<p>The markings I make are approximate because the bones themselves will dictate you how to arrange them better when you start sewing them on. However marking guidelines for the location of bones is highly recommended; they help you see where to direct the bones. <strong>It is compulsory to place a vertical bone along the central fold line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image052.jpg" class="img-responsive center-block"></p>
<p>I mark symmetrical guidelines for bones from the other side of the side piece as well. <strong>That�s the simplest way of marking their location.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Working on the cups reminded me that more adjustments are yet to be made - I need to shorten all back pieces by 2cm </strong>parallel from both sides of the corset because the side seam and the length of the back coincide. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%205_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>That�s all about cutting the corset! </strong></p>
<p>Now while the press-iron is still on, you can <strong>cut out fabric straps for finishing the bones of the corset</strong>. �Cut out" is rather overstated! The whole �cutting" process consists of tearing off straps of fabric. I normally use fabric at its whole width - 1.5m. I start by cutting off the selvage because it pulls on the fabric and hinders the process. If your fabric is ravel-prone, you should make straps that are 2.7-2.8cm wide (if it�s not that ravel-prone, 2.5cm should be enough). My fabric is elastic which means the width of a strap will shrink when it�s pulled on, that�s why I make my straps 2.7cm wide. I prepare two such straps in advance and I believe it should be enough. </p>
<p>I iron the straps carefully through.</p>

