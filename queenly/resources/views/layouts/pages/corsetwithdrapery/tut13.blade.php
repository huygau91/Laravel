<h2>Tutorial 13. Finishing the Top Edge of the Corset.</h2>
<p></p>
<p>The cups are ready but before working with them further we need to perform one more operation with the top edge of the corset. </p>
<p><strong>We need to lay a narrow Rigilene bone along the top edge from the lining side, i.e. overlay the ends of vertical bones and princess seams. </strong></p>
<p><strong>I take a narrow bone, tape its end with masking tape, place it 2-3mm from the edge and sew it on along its inner side. </strong></p>
<p>Don�t bring the bone to the very edge on the opposite side! But do try to place it as close as possible to the top cut edge. This bone is responsible for the look of the corset. Remember to spread the fabric underneath it as you sew. This corset made of hard mesh fabric isn�t really prone to deformation however if you use organza or soft mesh fabric, it will be necessary to spread it a little. I reach the spot where the cups should be inserted, leave a fly-off end of the bone (2-2.5 cm long) and tape it with some masking tape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And now I perform the same procedure from the other side. </strong>I take a narrow bone, place it at the edge and look to the mesh fabric for reference. You should trim the chiffon if it gets in the way. I leave a 2-2.5cm long end and make a stitch along the inner side of the bone. It is necessary to make bar-tacks here and spread the piece carefully. Don�t stretch it; just spread the corset according to the pattern! Don�t bring the bone to the very corner because it�s already quite thick there. </p>
<p><strong>You can trim all vertical bones and mesh fabric that goes beyond the edge. </strong>The top edge should be perfectly smooth so make sure to remove all extra bits. </p>
<p><strong>I take some white duct tape and cut pieces of it for taping vertical perimeter bones along the top edge. </strong>Why do I use duct tape instead of masking tape? Duct tape is stronger and thicker than masking tape and therefore better at protecting fishing lines from sticking out of Rigilene bones along the top edge. You could also slightly melt the tips of the fishing lines with a soldering iron. &nbsp;</p>
<p><strong>This duct tape doesn�t stick that well so I make another stitch to secure each piece of it. </strong>This stitch will fix the bone, secure the vertical princess seams and attach the duct tape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same thing with the other half of the top.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>You should remove all duct tape ends that can be seen from the face side. </p>
<p><strong>And now we will give the top edge a pencil-edge finish. </strong></p>
<p>I will finish the top edge using leftover straps of fabric for casing the bones. There are various ways of treating the end of such a strap. <strong>I have folded my strap in half and stitched it up at the end; it forms a neat corner when I turn it out: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I place the corner of the strap at the edge of the corset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And I start sewing. </strong>I try to make the stitch go along the inner side of the bone. You can easily feel the bone underneath and see where you should direct the sewing machine then. I sew very accurately pulling the strap slightly to me. <strong>I sew the strap onto the bone at the top edge of the corset at its full length.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And now I turn out the strap so as to form a neat corner, push all threads inside, fold it and stitch it on with the sewing machine. </strong>You should do it carefully because the fabric is thick at the lacing side and I basically have to turn the machine wheel by hand to stitch through it. But you don�t need to worry about the rest of the pencil-edge too much as it will be disguised by drapes. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2013_files/image014.jpg" class="img-responsive center-block"></p>
