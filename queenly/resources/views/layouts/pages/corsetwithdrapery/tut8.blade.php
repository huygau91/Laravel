<h2>Tutorial 8. Preparing the Bones and Sewing Them on.</h2>
<p></p>
<p><strong>Now I need to count how many bones are required for the princess seams at the front and at the back. </strong></p>
<p>There is a simple way of doing it: I take one of the wide bones that are going to be sewn on and use it as a ruler to measure all princess seams. Remember to measure the length of the side bone according to the front side seam and not the back one because the bone should start at the under-bust level and go down to the bottom. <strong>I measure the length of a half of all princess seams</strong> (from the side seam to the central piece) and get a total of about 70cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>This means I need a 140cm long bone for all princess seams at the front. </strong>The length of our fabric strap for finishing the bones is 150cm which is obviously enough. This is it, our bone. I write �F" on it in order to remember that it�s meant for the princess seams at the front. </p>
<p><strong>I determine the length of the bone for the princess seams at the back in the same manner. </strong></p>
<p><strong>I need the length of the middle seam of the back (where the lacing will be located) and the remaining two princess seams. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p>I measure the length of the bone with a ruler and the result is slightly over 75cm. This means I need about 152cm boning for the back. Right, this exceeds the length of our prepared 150cm fabric strap but remember that this fabric is elastic and we can easily stretch it by a couple centimeters. I mark this bone with a �B" for �back". </p>
<p><strong>Now let�s start working on our bones. </strong></p>
<p><strong>I shall start with the bone for the front. </strong></p>
<p><strong>I take one fabric strap, put it wrong side up and place the bone over it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I wrap the strap around the edge of the bone and secure it with a stitch 1mm from the edge. </strong></p>
<p>Sewing is much easier when you keep pulling the strap to yourself slightly stretching it. This way the strap hugs the edge of the bone much more easily. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I trim extra fabric and turn the bone around. </strong>I pull up the strap lightly to provide for necessary tension straight away and make wrapping the bone easier. And I <strong>stitch along the other edge of the bone</strong> the same way I did before.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p>Since tension was there from the very beginning, it is really easy to encase the bone in fabric. <strong>Try to stitch as close to the edge as possible </strong>but don�t go right over the edge or else the fabric will jump off. </p>
<p><strong>The matter is that we are now preparing tunnels for inserting plastic or steel bones later. </strong>These tunnels should be wide enough to hold a plastic bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>And now I stitch the bone onto the spot of its permanent location. </strong></p>
<p><strong>Please note the way I overlay the seam allowance. Notice the joining seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I place our fabric-encased bone so that it slightly overlaps this seam </strong>and check if it matches the bottom edge of the corset well. If the bone is a bit too long, you can trim it later. I overlap the joining seam very slightly for the sake of disguising it and at the same time the whole bone overlaps the seam allowance. I trim it at the end. I would like to point out that you shouldn�t trim bones in accordance with the length of a princess seam in advance. It is much easier to trim them after they�ve been sewn on.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I return to the start and stitch along the other side of the bone.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The face of our corset looks like this:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p>It is basically impossible to make perfectly even stitches. At least I have never managed to achieve it because we�re using plastic mesh fabric and the sewing machine doesn�t make perfect stitches over plastic-based fabric. </p>
<p><strong>I continue overlapping the remaining princess seams and their seam allowances with the bone.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p>You will gradually learn to sew on these bones either on the left or on the right from the joining seam. <strong>In this particular case the seam allowance is directed to the left and the joining seam stays along the right side of the bone.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>You need to overlay this seam with the bone just like before</strong>, i.e. only slightly. The bone should cover the seam by literally as little as 1mm. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And again, I trim the bone down to the required length. </strong>It is very important to maintain symmetry by placing the bone at the right spot at the start and the end of stitching.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And now the next princess seam. I repeat exactly the same procedure. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%208_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I encase the bone for the back princess seams in the other fabric strap in exactly the same manner. </strong>Remember to pull the strap to yourself because wrapping the fabric around the edge of the bone will be a lot easier then. </p>
<p><strong>And I overlap the seam allowances of all princess seams from both sides of the back.</strong> </p>
