<h2>Tutorial 7. Sewing Front and Back Princess Seams.</h2>
<p></p>
<p><strong>Let me start sewing the princess seams. </strong></p>
<p><strong>I take the central front piece. </strong>It consists of chiffon that is in contact with the body and mesh fabric that serves as face fabric and will be decorated. <strong>Let�s say the chiffon is our lining and the mesh is the face. </strong>And now I would like to share my novel trick with you: see-through corsets are normally sewn along their princess seams with seam allowances turned outwards. <strong>But we will turn our seam allowances inwards. </strong>This is done to keep the face of the corset smooth; all boning tunnels will stay on the lining side while the face will have a beautiful drapery. <strong>That�s why we need to lay out all pieces mesh to mesh.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>Find corresponding pieces using the numbers we wrote on them and match them mesh to mesh along their princess seams. Seam allowances should stay on the lining side. <strong>There is another small trick:</strong> no bar-tacks are made either at the beginning or at the end of sewing. <strong>Sew the princess seam between the central piece and the intermediate pieces of the front using a 1.2cm allowance</strong> and focus on the mesh fabric pieces as a reference standard. Don�t make any bar-tacks.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Next you should turn these joint pieces upside down and topstitch the seam allowance. </strong>But first of all you need to decide whether you want to direct it to the side or towards the centre. This will influence the location of bones. If you direct the seam allowance towards the centre, the bones will be located closer to the centre and thereby visually narrow the central piece. If you direct the seam allowance to the side, the bones that overlay it will visually expand the central piece. Sometimes your choice is determined by the technique you use (or the very possibility of turning the seam allowance to either side), other times it is determined by the corset model. <strong>In our case I bend the seam allowance towards the centre. </strong></p>
<p><strong>I topstitch the seam allowance at a 2mm distance from the seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Now I join the central front piece and the second intermediate piece along the other princess seam. </strong>I match the pieces as accurately as possible. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I turn them upside down, direct the seam allowance to the centre and topstitch it like before.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p>So, I have topstitched both princess seams. Now I take the same industrial scissors I used for cutting the hard mesh and <strong>trim the seam allowances leaving about 5-6mm.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I take the next pair of pieces and sew the princess seams between the intermediate and the side pieces of the front. </strong>The procedure is the same as before. </p>
<p><strong>This time I direct the seam allowances to the side </strong>in order to leave the intermediate front pieces free of bones. These pieces are already very narrow and I will sew bones onto adjoining pieces from their both sides. I trim the seam allowances leaving 5-6mm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p>If the pieces tend to slip as you begin stitching, you can secure them with a single pin. You could also pin at the end of the stitching line to make sure the edges match properly. </p>
<p><strong>And now I begin joining the back pieces. </strong>I look to the numbers written on them. </p>
<p><strong>I sew the princess seams between the central back piece and the intermediate pieces. </strong>I help myself by pinning at the beginning of the stitching, look to the mesh fabric pieces and make no bar-tacks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowances directing them to the side </strong>and then trim them.</p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I sew the princess seams between the intermediate and the side pieces of the back.</strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And again, I topstitch the seam allowances directing them to the side and then trim them. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Our princess seams are ready now!</strong></p>

