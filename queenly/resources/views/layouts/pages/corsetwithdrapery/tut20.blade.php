<h2>Tutorial 20. Inserting Grommets. Sewing a Modesty Flap for the Lacing Area.</h2>
<p></p>
<p><strong>Let�s prepare a modesty flap for the lacing area. </strong></p>
<p>I have cut out a rectangle that corresponds to the length of the corset back. </p>
<p><strong>And now I fold it in half and stitch it up from both ends.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I make a clean-finish-edge seam that will help turn the piece out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>And I turn it out and make a joining seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Now you could finish this edge with an overlock or a bias tape, but I will use a lace ribbon since I�ve already used it for finishing all edges around the perimeter of the corset inside.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I make a strengthening stitch. </strong>It will prevent the fabric from fraying. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I pin the modesty flap at the same spot where a lace ribbon was sewn onto the lacing bar.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And I stitch it on with the sewing machine going over the seam that attaches the bone.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove all pins and make another parallel stitch going over the bone like before. Now our modesty flap is properly secured. </strong>The only function of this decorative flap is to cover the skin that might show through the lacing.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And now I should insert grommets along the medium bone. </strong>I bend the flap back and pin it to prevent it from getting punched through when I make holes for the grommets. </p>
<p><strong>I need to make relevant markings. </strong>The length of the corset back is 24cm and I will insert grommets at 4cm intervals. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put a hole-cutter die onto the hand press grommet machine and punch holes according to the marks. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And then I swap to a grommet setting die and insert the grommets.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
