<h2>Tutorial 17. Making Preparations for Draping the Corset.</h2>
<p></p>
<p><strong>According to our plan the drapery should be arranged in rather large fan-like folds and making such folds out of a whole fabric sheet would be irrational. </strong>Firstly, you would spend more fabric. Secondly, it would be quite difficult to arrange the folds over the garment. The direction of the grain would change not once nor twice and the folds would start tilting as soon as you reach the bias grain. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I have an idea: let�s imitate a drapery with the help of separate fabric straps. </strong></p>
<p>All we need to do is guess the maximal visible width of a fold. I doubt it could exceed 5cm. Therefore it makes sense to prepare straps of fabric that can be folded so as to have one side 6cm wide and the other 3cm wide. There is no need of cutting a 12cm wide strap so as to fold it in half and get a width of 6cm. We can use 10cm wide straps for the sake of sparing our fabric and reducing the thickness at overlapping areas. </p>
<p><strong>Let�s try making one 10cm wide strap for a start. </strong>This 10cm is comprised of the main (visible) width of approximately 6.5-7cm and a 3-3.5cm bend. </p>
<p><strong>Let�s give it a try. </strong>I shall start arranging straps in the first drapery area and if the result is satisfying, we�ll continue working with straps of the same size. </p>
<p><strong>I tear off a 10cm wide strap and prepare it. </strong></p>
<p><strong>What do I mean by preparing the strap? </strong>I mean you should iron it, fold it and fuse it with iron-on web. I will use iron-on web (that is sold in broad sheets or rolls) not only for fusing it onto the straps but <strong>for fixing their fold lines in place</strong>. </p>
<p>I�ve decided to use a steel ruler as<strong> a template for the bend </strong>because it has a suitable width - 2.8cm. If I don�t like the result, I will make a cardboard template. </p>
<p><strong>I have torn off a strap across the entire width of the fabric. Now I iron it and bend it over my steel ruler.</strong> </p>
<p>
