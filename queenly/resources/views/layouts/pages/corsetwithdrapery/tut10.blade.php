<h2>Tutorial 10. Joining the Cup Pieces and Quilting Them with Bones.</h2>
<p></p>
<p><strong>And now I start working on the cups. </strong></p>
<p><strong>I unpin the pieces to start joining them along their princess seams. </strong></p>
<p>I carefully match the edges of princess seams. <strong>You could secure the edges with a pin to prevent them from shifting.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I make a bar-tack, remove the pin and join the pieces along the princess seam. </strong>Make sure the pieces are matched accurately. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest you should topstitch the seam allowances instead of pressing them open. </strong>Pressing these cup pieces won�t affect their shape since they are fused only with iron-on batiste and the fabric is too soft to hold the shape. That�s why I replace pressing by topstitching. <strong>I make a stitch from each side of the seam stepping about 1mm from it.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I trim the seam allowances. Please note: </strong>I trim their corners at the top and down the bottom. This way I remove extra fabric from the joining seams and make the start and end of the princess seam thinner. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same procedure with the other princess seam. </strong></p>
<p><strong>Then I make a stay-stitch along the whole perimeter of the cup piece. I start at the edge of the piece. </strong>The stitch should be made at a distance of a seam allowance and finish at the opposite edge.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I start at the side edge and move to the other edge.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And again, I start at the edge. Do not turn the garment around on the needle!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>You may flip the garment when making stay-stitches in order to achieve perfect corners. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I continue making stay-stitches.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I need to prepare a narrow (8mm) Rigilene bone </strong>that will be sewn-on along the perimeter of the piece. I gather the bone slightly by pulling inner fishing lines out of it until it acquires a rounded shape. I trim the fishing lines at the end.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I tape the end of the bone with some masking tape. </strong>I usually prepare a couple pieces of masking tape in advance, stick them to the sewing machine table and use them whenever needed. </p>
<p><strong>I place the bone at the upper edge - not at the very corner but stepping 2-3mm from the perpendicular stay-stitch and 1mm from the stay-stitch that will guide our bone. </strong>I ease in underneath the bone at the d�collet� area. This is done by pulling the bone to yourself while slightly pushing the fabric underneath it away from you. I trim the bone at a 2-3mm distance from the intersection of stay-stitches and make a bar-tack. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I gather another Rigilene bone for the underarm area. </strong>I give it a rounded shape following the above described method and trim the fishing lines. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I tape the end of the bone with some masking tape and place it underneath the first bone. </strong>I step 1mm from the stay-stitch and start sewing it on slightly easing in the fabric. I trim the bone at a 2-3mm distance from the intersection of stay-stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I prepare and sew another bone in the second underarm area exactly the same way. </strong></p>
<p><strong>Then I sew on a bone along the bottom of the piece. </strong></p>
<p>I gather the bone like before, tape its end, step 2-3mm from the vertical stay-stitch, and sew it on along the bottom stay-stitch at a distance of 1mm from it. I hardly ease in any fabric along the bottom edge but I do keep it in place with my fingers to avoid stretching.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to attach bones along the vertical side edges. </strong></p>
<p>There�s no need of gathering these bones. I take a bone, tape its end with masking tape, push it underneath the horizontal bone, and sew it on along the vertical stay-stitch at a 1mm distance from it. At the end I trim the bone at a distance of 2-3mm from the opposite stay-stitch, tape its end with masking tape, and push it underneath the horizontal bone. </p>
<p><strong>I always push vertical bones underneath horizontal bones because it�s a good way of securing them and preventing them from going beyond the borders determined by the stay-stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_1/transcripts/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
