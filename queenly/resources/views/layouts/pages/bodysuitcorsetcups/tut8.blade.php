						<h3>Tutorial 8. Covering the Cups with the Lace Front.</h3>
						<p></p>
						<p>
							<strong>I finish working on the front half of the bodysuit by
								covering the cups.</strong>
						</p>
						<p>I join the face part of the cups with the lining along their
							upper cut edges. I secure them with pins, match the sides and the
							corners of the neckline cut, and carefully re-shape the fit. Then
							I do the same with the other side: match corners and side seams.
							The fullness that was made for ensuring better fitting of the
							cups along the scye should get re-distributed from the face side
							as well. I gradually go on to the central part. In order to make
							the face part easy to turn out and embrace the lining with, I let
							the lining off by 3-4mm in the central part to make it smaller
							and expand the face a little.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%208_files/image002.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I switch to a one-sided presser foot and get the needle
								right into the stay-stitch gliding along the bone:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%208_files/image004.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>
							<strong>I make bar tacks at the intersections of the
								stay-stitches.</strong> Bar tacks should never reach the edge of
							your stitchwork. I also never turn the garment around on the
							needle. I start and finish making joining seams at the start and
							end points of the sewing correspondingly.
						</p>
						<p>I remove pins and check the quality of the seam. Machine teeth
							could have torn the thread of the stitch because the pins were
							located at the bottom.</p>
						<p>
							<strong>I trim seam allowances only when I am sure that the seam
								is of good quality:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%208_files/image006.jpg"
								class="img-responsive center-block">
						</p>
						<p>I trim as much as possible in the corners; in this case they
							will look sharp and neat when the garment is turned out. I also
							pay attention to the spots where curves meet. I trim as much as
							possible off these allowances to avoid extra thickness and
							density or unwanted lumps in this area.</p>
						<p>Then I turn out the garment carefully. I spread all seams,
							straighten the corners and make sure there are no lumps at the
							spots where curves meet.</p>
						<p>&nbsp;</p>
						<p>
							<strong>Then I pin together the sides, pull down the face of the
								cups and secure it with pins:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%208_files/image008.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>
							<strong>I make a joining stitch literally 1mm from the edge:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%208_files/image010.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>
							<strong>I would like to highlight once more that you should have
								no fear of overseams!</strong> There are certain difficulties
							when you join fine fabric and stiff constructions at the same
							time. And using an overseam is the best option here as shown by
							longstanding practice.
						</p>
						<p>
							<strong>Please look at the overseam I have made as a proof:</strong>
						</p>
