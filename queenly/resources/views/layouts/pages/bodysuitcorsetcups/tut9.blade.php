							possible.</p>
						<p></p>
						<p>
							<strong>I turn the crotch towards the seam allowance and stitch
								it up by 1mm just to secure the seam:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image004.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>I make this stitch go along the top and bottom and leave other
							cut edges of the crotch unfinished.</p>
						<p>As I have said, I don�t like making any fasteners here so I
							just need to join the crotch.</p>
						<p>
							<strong>I sew together the front and back pieces along the crotch
								seam using a 0.5 seam allowance:</strong>
						</p>
						<p></p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image006.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>And afterwards I press the seam allowances of the crotch.</p>
						<p>
							<strong>I press flat both sides of the crotch seam: </strong>
						</p>
						<p></p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image008.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>
							<strong>I put the crotch in place in accordance with the notches,
								and secure it with pins:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image010.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p></p>
						<p>I take a regular narrow elastic band about 0.2 inches wide and
							stitch it onto the scyes of the crotch.&nbsp; I stitch-on the
							elastic band preserving the original 0.5 seam allowance. I pull
							the band slightly towards myself creating some fullness.</p>
						<p>
							<strong>I stitch bands onto both halves of the bodysuit:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image012.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I have overlocked the cuts with the band on:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image014.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I bend the resulting seam allowance with the sewn-on band
								and stitch it again at a presser foot distance from the edge:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image016.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>
							<strong>I stitch-on the crotch:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image018.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>
							<strong>And now it�s time for the final step - joining the side
								seams:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image020.jpg"
								class="img-responsive center-block">
						</p>
						<p>I match the beginnings and the ends of the pieces as well as
							their waistline notches.</p>
						<p>You can overlock the side cuts before stitching them together,
							but I have decided to sew them first and then treat them with a
							single overlock seam.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image022.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>Now I need to sew thin "spaghetti" straps</strong>.
						</p>
						<p>I have prepared a 1.4 wide strap of fabric for making the
							straps. I fold the fabric in half face side inwards. It is
							desirable to make two parallel stitches with a couple of
							millimeters between them to ensure that the strap is strong
							enough.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%209_files/image024.jpg"
								class="img-responsive center-block">
						</p>
						<p>In order to finish the sewing I usually leave a long thread,
							run it through the eyelet of a sack needle, tie it in a knot and
							then push it inside the pencil-edge with the stub end of the
							needle and turn it out on this thread.</p>
						<p>I arrange the resulted strap in the corners at the front, and
							close to the scye at the back, to make a loop. And then I adjust
							its length directly on the client during a fit test. You can
							hand- or machine-stitch the strap onto the garment.</p>
						<p>
							<img
