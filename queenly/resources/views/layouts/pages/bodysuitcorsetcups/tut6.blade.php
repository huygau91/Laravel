							sequence. This is not a coincidence! Did you notice that I don�t
							jump off my place after each step made with the help of the
							machine and don�t go pressing a newly-sewn piece? I try to
							arrange the sewing technique in such a manner that a few pieces
							at once are ready for pressing. Otherwise you are going to spend
							a minute here, a couple of seconds there and some five minutes
							there. And this time adds up to an hour or a couple of hours, or
							even longer than that.</p>
