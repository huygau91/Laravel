<h3>Tutorial 7.&nbsp; Sewing the Back of the Bodysuit. Concealed zipper.</h3>
<h4>Working on the Back Piece.</h4>
<p></p>
<p><strong>I start working on the back of the garment. </strong></p>
<p>I need to treat the upper cut edge of the back. For this purpose I have prepared a small strap of the same elastic lace and cut it along the edge of the lace fabric. The length of this strap corresponds to the length of a half of the back and it is 1 inch wide. </p>
<p><strong>I treat the upper cut edge of the back by edge piping:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>You can use various kinds of treatment: you can stitch an elastic band below the overlock, you can wrap the edge in a bias tape or you can welt it. The only condition: you should take the seam allowance into account. </p>
<p><strong>In order to make the back even, I try stitching the pieces one after another using a single thread:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I turn up the welt strap and stitch it to the seam allowance: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the welt backwards and stitch it onto the back:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>The handled top of the back looks like this:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<h4>Sewing-in a Concealed Zipper.</h4>
<p></p>
<p><strong>I begin by sewing the band of the zipper onto the seam allowance, i.e. securing it in place:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p>I make a bar tack when I reach the notch at the end of the zipper. </p>
<p><strong>I sew the other half of the zipper onto the other half of the back: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p>When you sew a zipper onto elastic fabric, please try not to let the fabric stretch underneath the zipper. If you�re not careful - there will be drafting waves at the back. When sewing-on the second half of the zipper, please make sure that the right and left halves of the back match fully, as well as the waistline notches and the notches at the ends of the zipper.</p>
<p><strong>Having switched to a presser foot for sewing-in concealed zippers I finish stitching close to the teeth of the zipper, first from one side and then from the other: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I zip the fastener and check the length of the back:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I switch to a one-sided presser foot and stitch up the central seam of the back, going from the point where I finished sewing the zipper down to the very bottom:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>If you sew-in a zipper in this very order, the transition from the seam to the beginning of the zipper line won�t be visible:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And now I am going to show you a small trick that helps make a perfectly straight zipper at the back.</strong> </p>
<p>This concerns those cases when a zipper is sewn into some elastic knitted fabric that is easily stretched. You probably know that a zipper at the back often doesn�t look so pretty because knitted fabric stretches no matter what you do and there is not enough stiffness to keep the back perfectly straight. </p>
<p><strong>So what do I do in this case?</strong></p>
<p>I cut 2 Rigilene bones that go from the top of the zipper until the level 2.8-3.1 inches below the waistline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p>Then I tear off a strap of the lining fabric about 1.4-1.5 inches wide. </p>
<p>I treat the bones the same way I did it when sewing hanging loops for the lacing on the dress. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>After hiding the end of the strap under the seam allowance I stitch-on one of the bones close to the teeth of the zipper.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p>I place the bone in such a way that it overlays the seam allowance at the same time leaving enough space for the stitch, i.e. basically along the very edge of the allowance. In the meantime, the other side of the bone should stay a couple millimeters away from the teeth of the zipper to make stitching along this side of it easier.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>This is what it is going to look like when finished:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p>And next I stitch-on the second bone to the other half of the back.</p>
<p>&nbsp;</p>
<p>Please take a look at how neatly the zipper is finished; and note that the back has acquired some firmness. </p>
<p><strong>Now the zipper is going to fit perfectly!</strong></p>
