<h3>Tutorial 3. Fusing the Cups. Marking the Location of Bones.</h3>
<p></p>
<p>Now I need to fuse the hard iron-on fabric onto the corresponding lining pieces.</p>
<p><strong>I place the iron-on pieces adhesive side down and press them with the iron, preferably from both sides (be sure to use steaming as well):</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I mark the location of the bones.</strong> </p>
<p><strong>Left side cup piece:</strong></p>
<p>- I draw a bust line connecting notches at the bust (line 1);</p>
<p>- I step back by 1.2-1.4 inches depending on the height of the cups and draw the location of the upper bone at a slight angle dividing the armhole about in half (line 2);</p>
<p>&nbsp; - as I have mentioned before when working with rounded quilted cups, a cup should be restricted by a bone placed about 2.4 inches from the side seam. So I mark this distance with the seam allowance included - that makes about 2.8-3 inches - and draw a vertical line which sets a border for the bones and serves as a boundary line for the cups (boundary line 3);</p>
<p>- I step 0.8 inches from the edge of the curve and draw a line going upward at a slight angle. This line can be either drawn to the very corner or shifted towards the centre (line 4);</p>
<p>&nbsp;- I step 1.6 inches down from the bust-line and draw a line for the bottom bone of the cup (line 5).</p>
<p>The lines of bones location starting on one cup piece and going onto the other piece have the same numbering. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Right side cup piece:</strong></p>
<p>- I draw a bust line connecting notches on the bust (line 1);</p>
<p>- I step back by 1.2-1.4 inches depending on the height of the cups and draw the location of the upper bone at a slight angle dividing the armhole about in half (line 2);</p>
<p>&nbsp; - I mark this distance with the seam allowance included - that makes about 2.8-3 inches - and draw a vertical line which sets a border for the bones and serves as a boundary line for the cups (boundary line 6);</p>
<p>- I step 0.8 inches from the edge of the curve and draw a line going upward at a slight angle. This line can be either drawn to the very corner or shifted towards the centre (line 7);</p>
<p>&nbsp;- I step 1.6 inches down from the bust-line and draw a line for the bottom bone of the cup (line 5).</p>
<p>The lines of bones location starting on one cup piece and going onto the other piece have the same numbering. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%203_files/image007.jpg" class="img-responsive center-block"></p>
<p>Sure enough the bones on the right and the left side pieces should be marked identically.</p>
<p><strong>Central cup piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%203_files/image009.jpg" class="img-responsive center-block"></strong></p>
<p>&nbsp;- I draw a bust line connecting notches on the bust (line 1);</p>
<p>&nbsp;- I draw a central vertical line along the centre of the cup (line 8);</p>
