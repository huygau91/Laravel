						<h3>Tutorial 5. Quilting the Cups with Bones.</h3>
						<p></p>
						<p>
							<strong>I start stitching bones onto the lining of the cups.</strong>
						</p>
						<p>I gather a Rigilene bone for the semi-round cut of the cups in
							the previously described manner and tape its end with masking
							tape.</p>
						<p>I place the gathered bone 2-3mm from the stay-stitch going to
							the armscye, and 1mm from the stay-stitch passing along the
							neckline cut.</p>
						<p>I make a machine stitch along the outer side of the bone and
							let it fly-off for now, i.e. attach it only with one stitch.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image002.jpg"
								class="img-responsive center-block"
								alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image1336.jpg">
						</p>
						<p>I try to ease-in the cut underneath the bone along the whole
							central piece of the cups. Let me repeat that the level of
							fullness depends on the size of the cups being quilted. The
							smaller the size - the more fullness is needed and vice versa,
							the larger the size - the less fullness required. Thereby I
							regulate the fitting of the cut to the body.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image004.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>
							I gather another Rigilene bone a little<strong> in order to
								stitch it to the bottom of the cups </strong>and thereby make it
							more elastic so that it could pass even through the slightest
							bends along the bottom of the cups.
						</p>
						<p>I don�t start stitching-on the lower bone from the very corner
							but from the spot where the hard iron-on fabric begins.&nbsp;</p>
						<p>I leave a little space at the side seam to make it possible to
							adjust the under-bust line in case some alterations are needed
							after the test fit. A bone stitched-on this way won�t become an
							obstruction for these alterations. Otherwise the bone would be in
							the way and I would have to rip it off an almost finished garment
							to trim it and stitch it on all over again.</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image006.jpg"
								class="img-responsive center-block">
						</p>
						<p></p>
						<p>
							<strong>And now I am going to prepare a semi-round bone for the
								armpit area.</strong>
						</p>
						<p>There are 2 opposed opinions on whether one should sew-on a
							Rigilene bone here or not.</p>
						<p>
							<strong>If this area is small</strong> then I try to sew no bones
							here because additional stiffness in the armpit area makes a
							garment uncomfortable to wear.
						</p>
						<p>
							<strong>But if the area is vast enough, </strong>I do place a
							bone there. The matter is that if this area is large it means the
							corner of the cut lies closer to the centre and the armscye is
							deep enough and won�t trouble the client. Cups like this are also
							going to look much better.
						</p>
						<p>A Rigilene bone stitched onto an edge will even this edge out
							and make it perfect. Besides, fabric tension is then balanced
							out, which looks very beautiful! And you could also ease-in some
							fabric in the armpit area creating tighter fitting to the bust.</p>
						<p>
							<strong>I stitch-on a bone along the armpit area from both sides
								of the cups:</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image008.jpg"
								class="img-responsive center-block">
						</p>
						<p>Having taped over the end of the bone I push it underneath the
							upper perimeter bone and finish sewing-on the bone without
							reaching the side seam with it. Again, itt is done this way in
							case I will need to adjust the size of this garment after the fit
							test.</p>
						<p>
							<strong>And then I stitch-on straight (not gathered) Rigilene
								bones along the curve lines.</strong> These bones are attached
							by two stitches and their ends are hidden underneath the
							perimeter bones.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image010.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>Now I stitch-on straight Rigilene bones going along the
								outer edges of the cups (bones 3 and 6 after the marking).</strong>
							I stitch-on these bones with one stitch along the outer side
							because the ends of all horizontal bones will be later pushed
							underneath them.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image012.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I stitch-on the central horizontal bone going along the
								bust-line (bone 1 after the marking).</strong>
						</p>
						<p>The lining fabric underneath this bone should by no means be
							gathered, there should be no fullness here! You should try to
							pull the fabric slightly towards yourself and push the bone away.
							But sometimes less is more! Do be careful and don�t pull too much
							fabric, it should be pulled up well underneath the bone but not
							stretched.</p>
						<p>I stitch the bone on with 2 stitches and push its ends
							underneath the bone on the border of the cups.</p>
						<p></p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image014.jpg"
								class="img-responsive center-block"
								alt="Description: E:\Corset Academy\FINISHED\English\AK 1\mp4\Corset 1\Transcript\tut (book)_files\image1348.jpg">
						</p>
						<p>
							<strong>I stitch-on the inner vertical bone 4 with 2 stitches:</strong>
						</p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image016.jpg"
								class="img-responsive center-block"></strong>
						</p>
						<p>
							<strong>And then I stitch-on all inner vertical bones of the cups
								the same way and in the following order (bone numbering keeps
								with the marking): bones 10, 7, 9, 8. </strong>
						</p>
						<p>
							<strong>I stitch-on a slightly bent lower Rigilene bone under the
								bust-line (bone 5):</strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image018.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>And I stitch-on another slightly bent Rigilene bone along
								the line above the bust (bone 2).</strong>
						</p>
						<p></p>
						<p>
							<strong><br> <img
								src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%205_files/image020.jpg"
								class="img-responsive center-block"></strong>
						</p>
