<h3>Tutorial 1.&nbsp; Introductory Tutorial.</h3>
<p></p>
<p><strong>I start working on the next garment - a bodysuit. </strong></p>
<p>Before I start talking directly about the pattern for this garment and the working process, I would like to talk about the pros and cons of a bodysuit.</p>
<p><strong>Here are the pros of a bodysuit</strong>: </p>
<p>&nbsp;- it is a light and elastic garment that doesn�t hinder your movements;</p>
<p>&nbsp;- it fits your body tightly and there are no folds on the stomach or at the back if the garment is sewn correctly; </p>
<p>&nbsp;- it is a one-layered garment that can be made either of see-through lace or an opaque elastic material. </p>
<p><strong>Here are the cons of a bodysuit</strong>:</p>
<p>&nbsp;- first of all, its fastener is not conveniently located. Lately I have completely given up using button, snap and hook fasteners here. All of these have been tried and showed no good results when worn.&nbsp; That is why I either make a one-piece bodysuit or a sewn-together bodysuit. I always warn my clients about this. Usually we decide to sew a one-piece garment similar to a jumpsuit or a swimsuit. </p>
<p>&nbsp;- in most cases functional straps are needed regardless of the size due to unavoidable vertical tension. Straps prevent one from having this unpleasant feeling that the garment is going to slide down, and they are absolutely necessary for women with full breasts because without them a body won�t be able to hold a heavy bust even if the cups have been quilted and glued.</p>
<p>&nbsp;- there are quite high requirements to one�s forms. This garment cannot change body forms dramatically and cannot disguise fat rolls.</p>
<p>&nbsp;- it is rather difficult to decorate it on the stomach and at the back. It is hard to drape it and fix the drapery because a bodysuit is made of elastic materials. That�s why either your mannequin should have the precise size of your client or all alterations after the fit test, as well as all decorative elements, should be applied directly on the client.</p>
<p><strong>However: </strong>despite the cons, bodysuit remains popular and is often used in the tailoring of evening gowns and wedding dresses. If the skirt of a future garment is going to be rather heavy and voluminous, you can sew a bodysuit without the panties part. In this case you just sew it like a singlet and the skirt provides vertical tension with its weight. This eliminates all the main disadvantages of a bodysuit. But straps remain a necessity.</p>
<p></p>
<p><strong>In this picture you can see a technical drawing of the bodysuit I am going to sew and the scheme of its patterns:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%204/transcript/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>The bodysuit itself will be made of an elastic material and have one layer; a non-elastic dense material with lining will be used for the cups. </p>
<p><strong>The pattern I use myself and offer you consists of the following pieces:</strong></p>
<p><strong>Cup:</strong></p>
<p>1. Central piece of the front with a crease.</p>
<p>2. Two side pieces of the front.</p>
<p><strong>Body:</strong></p>
<p>3. Front piece with a crease. </p>
<p>4. Two back pieces.</p>
<p>5. One-piece crotch.</p>
<p><strong>Required materials.</strong></p>
<p><strong>Main fabric</strong> - elastic lace.</p>
<p><strong>Fabric for the lining and base of the face</strong> - satin.</p>
<p><strong>Iron-on batiste</strong> for fusing face and lining.</p>
<p><strong>Hard iron-on fabric</strong> for fusing cups.</p>
<p><strong>Elastic band </strong>for handling at the leg area.</p>
<p><strong>Rigilene bones. </strong>&nbsp;For this very model you can just use narrow bones (0.3 inches).</p>
<p><strong>Thread, needles, pins.</strong></p>
<p><strong>Scissors, pruner</strong> for cutting the bones.</p>
<p><strong>Measuring tape.</strong></p>
<p><strong>Narrow ribbon </strong>for the loops used for hanging the garment.</p>
<p><strong>In this picture you can see all tools and materials required for making a bodysuit:</strong></p>
<p>
