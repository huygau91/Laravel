<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And then I switch to an invisible zipper presser foot. </strong>I open the zipper all the way down and position the groove of its tape right underneath the groove in the presser foot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I close the zipper and check if it looks even enough.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Then I switch to a left-toe presser foot.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the seam allowances of the zipper seams, align the notches and pin them together.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>We will only sew the central seam down to the notch that marks the beginning of the slit.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I find the point where I stopped sewing in the zipper and position the needle right there</strong> and make strengthening stitches down to the notch at the beginning of the slit. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I spread the seam open to check if the end of the zipper merges well enough with the central seam at the back.</strong> It looks great! The transition from the seam to the zipper will be absolutely invisible after I press the seam that attaches the latter. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I switch back to the standard presser foot and sew down the princess seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the corner of the seam allowance of the zipper seam and trim it lengthwise down to 4-5mm starting from the hips and to the bottom.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the other princess seam exactly the same way.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image050.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image052.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The face of the back is finished. All we need to do is press the princess seams and the central seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%209_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
