<h2>Tutorial 22. Pressing the Side Seams Open.</h2>
<p></p>
<p><strong>First I need to press open the seams on the lining and the face of the skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>You can also press the slit at this point.</strong> You must have already noticed that the slit didn't cause any trouble when I was sewing down the side seams. We had made things a lot easier by handling it before sewing down the seams.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the dress out to press the side seam.</strong> I use a pear-shaped pressing mould to achieve the right shape of the side seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to apply some final touches to the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>We have basically finished it!&nbsp; </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2022_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>What is still left to do then?</strong> </p>
<p>We need to<strong> make shoulder straps</strong> of required length and width and hand-sew them to the corners at the back and at the front of the dress. I will decide on the length of the straps during a fitting. </p>
<p>And of course we need to<strong> hem the bottom edge of the dress</strong>. You can double-check the length during a fitting. But you can also finish the bottom of the dress right now provided you are absolutely sure you had measured the length correctly and cut everything correspondingly.</p>

