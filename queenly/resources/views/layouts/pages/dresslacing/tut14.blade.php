<h1>Tutorial 14. Attaching Vertical Bones. Closing the Princess Seams.</h1>
<p></p>
<p><strong>I start sewing wide vertical Rigilene bones onto the pieces.</strong> I have already pressed them straight. I place them in correspondence with the markings and tape their ends with masking tape where necessary.</p>
<p><strong>What is good to remember when youï¿½re attaching bones?</strong> </p>
<p>We have made stay-stitches at the top of each piece. There should be about 0.5cm between the end of the bone and the edge of the piece. Later the ends of these vertical bones will be overlaid by horizontal bones going along the armscye and along the neckline. </p>
<p><strong>The side front piece:</strong></p>
<p><strong>Please note </strong>that there should be at least 2cm between the edge of the bone that lies closest to the side seam and the side seam itself. It's why I place this bone left of the line that marks its location.</p>
