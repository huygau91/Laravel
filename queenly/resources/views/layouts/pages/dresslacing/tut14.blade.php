<p><strong>I pin the pieces together along the princess seams.</strong> It's best to do this by moving from down to up. </p>
<p><strong>You can mark the bust middle points to align the princess seams more precisely.</strong> I find the bust line, step inwards by the width of a seam allowance and place a pin at that point. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2014_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Then I find a corresponding point on the bust line of the side front piece (at the same distance from the princess seam) and join both points with a pin.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2014_files/image059.jpg" class="img-responsive center-block"></p>
