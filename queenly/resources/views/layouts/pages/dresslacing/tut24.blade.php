<h1>Supplementary Patterns. </h1>
<p></p>
<p><strong>I would like to talk about the supplementary patterns that you�ll find on this DVD. </strong></p>
<p>It makes sense to assume that a woman whose wardrobe staples include a pencil dress will need some additional matching accessories or clothing items. My clients often order shrugs and boleros of various styles.&nbsp; What I offer you this time are patterns of two types of garments that have proven to be particularly sought-after in my practice. These patterns are simple and easily adjustable and garments sewn after them will never go out of fashion. </p>
<p><strong>I have here a sketch of such garments.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The first one is a collar capelet that just covers up the shoulders.</strong> It can be made of resilient and non-resilient fabric alike. You can use lace, guipure, cr�pe, chiffon, georgette cr�pe, or sometimes (although rarely) even organza. By the way it can also be a fur neckpiece if you're working on a winter wedding outfit for example. </p>
<p><strong>The second garment is a bolero jacket.</strong> My version features short sleeves and a stand-up collar similar to a mandarin collar. When you study the pattern, you will see that it also allows you to sew a sleeveless jacket, adjust the shape of the collar or make a classic collarless bolero. </p>
<p>Now let me explain how to print and prepare the patterns and adjust the size. </p>
<p><strong>As usual all patterns are made of A4 sheets of paper. Below is the capelet pattern.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now let us study the process of working with a pattern based on a concrete example. </strong>I will take pictures of my computer's screen.</p>
<p><strong>Suppose we have chosen the bolero pattern.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I open the first file.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>First of all, what can you see on this sheet?</strong> You can see a thumbnail where grey marks the part of the pattern that you are currently looking at. The thumbnail also shows how to put four sheets together to compose the pattern of the front of the bolero. Right now you are going to print the first sheet. It is marked grey on the thumbnail. </p>
<p>Click the corresponding print icon (red arrow). Page size and orientation are usually set at 'Actual' and 'Portrait' by default. Do not change any settings! </p>
<p>Remember that all printers have their own peculiarities: some will leave a thin blank border along the perimeter of the sheet. Do not trim this border when you start aligning the pieces! Simply prolong the existing lines over those blank areas. </p>
<p>Once you have printed out all pages, put them together following the instructions included into every separate set of files. They explain how to align parts of the front, how to align parts of the sleeve, how to compose the back, etc. </p>
<p>You will have a ready pattern made of separate paged glued together.</p>
<p><strong>Here is the pattern of the front half of the bolero jacket. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>This is the pattern of the back of the jacket. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>This is the sleeve of the jacket.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The patterns come in three different bust circumferences: 102cm, 108cm and 112cm.&nbsp; </strong></p>
<p><strong>How to alter the pattern if you want to make it bigger or smaller?</strong> I�ll demonstrate it on the capelet. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Attention: </strong>&nbsp;transversal lines on the pattern are the lines you use to make the size smaller or bigger. I have marked the intersection points of the transversal lines and the cutting lines. If you measure the distance between these points, it will be the same along the entire perimeter and for all sizes. For example the distance between the points on the pattern with 102cm BC and the points on the pattern with 108cm BC is always 9mm, and the distance between the points on the 108cm BC pattern and on the 112cm BC pattern is always 6mm. <strong>As you can guess, each size differs from the next one by 3mm.</strong> </p>
<p><strong>Suppose you want to build a pattern with a 100cm bust circumference.</strong> In this case you need to mark 3mm inwards from each point of intersection between the transversal lines and the cutting lines of the pattern with the bust circumference of 102cm. These transversal lines are not marked randomly. Their incline was calculated specially to enable you to make the pattern smaller or bigger. Next you connect the points by hand or with the help of the pattern. I guess you got it! </p>
<p><strong>Here is how you get a pattern with a 100cm bust circumference.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>You can do the same thing to build a pattern with, say, a 116cm bust circumference.</strong> In this case you need to mark 6mm outwards from each point of intersection between the transversal lines and the cutting lines of the pattern with the bust circumference of 112cm. You can prolong the transversal lines as much as you need in either direction. Just make sure to preserve the original incline! You mark the intersection points, connect them with a line and get a pattern of a much larger size - the size you need.&nbsp; </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>That's all you need to know to use the patterns I'm sharing with you. </strong>By the way this method is really precise. I use it all the time and I'm very happy with it! </p>
<p>I can't guarantee that the garments you'll sew after these patterns will fit your client like a glove. But you can always compare the patterns with your client's measurements: shoulder width, shoulder length, the length of the garment you want to make, etc. </p>
<p><strong>Besides you can always alter the patterns the way you find necessary</strong>: for example, remove the stand-up collar and turn it into a collarless bolero jacket (red line). You can also make it sleeveless if you want. You can vary the length of the jacket in any way you like (blue line). </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>In short there is a sufficient amount of possible variations to create matching accessories for your client's wardrobe staple - the pencil dress. </strong></p>
<p><strong>Please note that the patterns are given without seam allowances.</strong> I have left a note on the first sheet to make sure you remember about it. You will need to add seam allowances to the ready pattern.&nbsp; My seam allowances are always 1.2cm but you can use any width that's convenient to you.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Supplementary%20Patterns_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>It wasn't my task to show you how to sew after these patterns. </strong>&nbsp;I hope you are experienced enough to cut and sew such pieces on your own. They are basic garments sewn in the traditional technique. If some challenges do arise, remember that there are numerous website with a great number of free tutorials dedicated to sewing basic accessories like this. </p>
<p>Something that could have stopped you from sewing them would be a lack of a suitable pattern or a pattern of a certain size, or simply a pattern that is easy to alter and adjust after client's individual measurements and preferences. <strong>And that is the issue I�ve tried to solve for you! I have provided you with a multipurpose pattern that will help you create your own masterpieces and expand the range of your clients' wardrobe essentials! </strong></p>

