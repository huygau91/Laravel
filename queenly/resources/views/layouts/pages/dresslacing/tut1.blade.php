<h1>Sewing plus size dress with built-in corset and hidden lacing. Review of Materials.</h1>
<p></p>
<p><strong>We are about to start working on a new dress from the series 'For Women with Bust Size 100cm and Above.'</strong> This dress will fit a woman with ample curves, US size 14 and up. </p>
<p><strong>How to sew an open d�collet� dress of size 14 or above?</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p>It won't be a long dress. It will only just cover the knees - a classic Chanel length. You can make a slit in the princess seam at the front or, alternatively, a vent or a slit in the princess seam at the back for better comfort of movement.&nbsp; </p>
<p>I will explain the following things during the work process: </p>
<p>- How to design cups for heavy full breasts;</p>
<p>- How to prevent the corset from 'breaking' in the waist, i.e. achieve a smooth even surface in that area; </p>
<p>- How to reinforce the dress and prevent Rigilene bones from breaking.</p>
<p><strong>Our dress is a pencil dress.</strong> This garment is really versatile: you can pair it up with a jacket, bolero, blouse, kerchief or shawl of any kind. It will work equally well for the office and for a promo party, a theatre night-out or a formal dinner.</p>
<p>I will share important know-how of constructing special corners for attaching shoulder straps <strong>at the back</strong> of the dress and building the armscye so as to prevent full upper arms from bulging over the edge of the garment. Moreover, we will make a rather low back neckline without affecting the body control effect. I will demonstrate how to make a concealed lace-up area underneath the zipper of the dress: there will be a blind zipper at the top and a lacing underneath it that will allow for better waist and stomach restriction. </p>
<p>These are the main tasks we need to solve together!</p>
<p><strong>Now let me show you the fabric that I will use.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I have chosen cr�pe fabric.</strong> This particular cr�pe is lightweight and very nice to the touch. At the same time it forms beautiful drapes. Besides, it has great wrinkle-resistant properties! </p>
<p>But since my cr�pe is a little too thin, there will be a visible border between the duplicated and non-duplicated parts of the dress (it will be duplicated with batiste down to the hips just like any classic corset-based garment). </p>
<p><strong>There are two ways we can go:</strong> either add a third layer (make a double-layer face) or duplicate the face with iron-on batiste from top to bottom considering that it�s a below-the-knee dress. </p>
<p>We will decide on it in the process. </p>

