<h2>Tutorial 8. Assembling the Lining of the Back.</h2>
<p></p>
<p><strong>First of all let's press our inner lacing bars. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I also press the side pieces of the back and overlay the bones with straps of iron-on batiste.</strong> This will prevent them from pushing through the face fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p>I do the same with the other side piece of the back. The width and length of batiste pieces is not important as long as they overlay the bones properly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to sew down the princess seams and sew the lacing bars into them.</strong> </p>
<p><strong>Let's take the corresponding pieces: the side and the central piece of the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>How do I arrange the lacing bars?</strong> </p>
<p>There are stitches attaching the bones and stitches defining the lacing area at the back of the lacing bar. <strong>The face side is cleaner and only shows the stitches that define the lacing area.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I put the lacing bar in place wrong side up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>When my client puts the dress on, the back will spread out and reveal the face side of the lacing bar. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Thatï¿½s the only right way to join these pieces.</strong> I put the lacing bar onto the central back piece, align their waistline notches and pin them together. The raw edges of the lacing bar and the central piece are identical because they were cut after the same pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the lacing bar to the central back piece with a row of stitches made at a presser-foot distance from the edge.</strong> It will be easier to sew down the princess seam this way.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the side piece, align the notches at the waistline...</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>...and sew down the princess seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowance towards the side seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Next I trim the corner of the seam allowance of the princess seam and make a stay-stitch.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I sew a wide Rigilene bone onto the princess seam</strong> with two rows of parallel stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Make sure there are no tucks in the princess seam after you overlay it with a bone. The princess seam should look flawless.</strong> As you can see, the back is stiff and robust enough because we have reinforced it with a fair amount of stiff bones. This is very important for large-size garments. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>The inside looks clean and neat enough, too. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>Now I'll do the same with the other half of the back.</strong> </p>
<p><strong>I put the lacing bar onto the central back piece, align their waistline notches and pin them together.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a stay-stitch before sewing them together. It's easier this way.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I attach the lacing bar to the central back piece with a row of stitches made at a presser-foot distance from the edge.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image052.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew down the princess seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the corner of the seam allowance...</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong>...and topstitch it towards the side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>I lay a wide Rigilene bone over the princess seam </strong>and attach it with two rows of parallel stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image062.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image064.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>The second half of the back is ready.</strong> The bone holds the princess seam safely. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>There is enough room left for tightening the garment.</strong> After we sew in the zipper, there will still be a couple of centimeters for pulling the dress tighter to the body. It's best not to make the gap between the lacing bar and the zipper too large or else it will be hard to get it right with tightening. If you tighten the dress too much, the zipper will be all distorted. Sometimes it makes the version with inner lacing bars unsuitable for that reason.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%208_files/image070.jpg" class="img-responsive center-block"></p>

