<h2>Tutorial 5. Marking the Bones. </h2>
<p></p>
<p><strong>Marking the placement of bones is one of the most important stages of our entire work process.</strong> Why is it so important? Because at this very stage you must make up your mind about the sewing technique you're going to use and about the way you're going to sew bones onto the garment. What your garment will look like and how comfortable it will be to wear depends on this decision. </p>
<p>Since we are working on a garment for a woman with a bust size above 100cm, we need to account for that peculiarity in order to choose the correct technique. <strong>It is not the best idea to offer round boned cups to women whose bust circumference exceeds 100cm. </strong>&nbsp;As you know, round boned cups serve to push the bust up and make it appear larger! Full-breasted women have no need in this. By pushing full breasts up you would force them into the d�collet� zone, which would look rather unattractive and even vulgar. <strong>I recommend you should only use vertical bones on cups for women with a bust size of 100cm and above.</strong> Do not make the garment too tight under the bust - it won�t let the body settle naturally inside the garment. </p>
<p><strong>The central front piece:</strong></p>
<p>I connect the notches on the bust line and draw a <strong>line (1)</strong> to mark the central horizontal bone. This bone is a must. It�s responsible for the shape of the cups.</p>
<p>I divide in half the distance between the bust line and the under-bust line. In this particular case I step 3.5cm below the bust line and mark another <strong>horizontal bone (2)</strong>. This bone provides support to the bust and defines the shape of the cups. </p>
<p>You can either place bones horizontally above the bust line or place them at a slight incline and bring them to the neckline. I have chosen the second option <strong>(3 and 4).</strong> The bone starts 4cm above the bust line and stops 6cm above the bust line. All bones should stop at the same level or else they might start pushing the fabric towards the neckline when you sew them on. This would affect the shape of the neckline. If we sew these bones on at different levels, they will deform the fabric in different areas and most likely disturb the symmetry of the neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p>I draw a vertical line through the middle of the piece to mark a vertical<strong> bone (5).</strong> How far below the waistline this bone will reach always depends on the dress model and the anatomical build of the stomach. In any case, this bone is not supposed to reach below the level of the hip joint. The maximum length of the vertical bone that I can afford for my client is 18cm. But I don't think I should make it go that low. The best choice is 15cm. The central vertical bone will most likely be a prolongation of two narrow bones framing the neckline and not a single whole bone.&nbsp; </p>
<p>We need to lay two more vertical <strong>bones (6 and 7).</strong> I divide in half the distance from the centre of the piece to the princess seam (without the seam allowance). The bones will be placed about 4cm left and right of the centre. You can make them 1cm shorter than the central bone and stop 14cm below the waistline. The vertical bones will form a small triangle at the bottom. </p>
<p>The vertical bone that lies closest to the princess seams can be even shorter - 13cm. </p>
<p>There are no other bones on the central front piece. </p>
<p><strong>The side front piece:</strong> </p>
<p><strong>I draw the bust line (1)</strong>. Bone 1 will pass onto it from the central front piece. </p>
<p><strong>Bone 4</strong> passes from the central piece. It starts at the princess seam 4cm above the bust line and stops roughly in the middle of the armscye 6cm above the bust line.</p>
<p><strong>Bone 2</strong> also passes from the central piece. It is placed 3.5cm below the bust line. </p>
<p><strong>We need to mark the borders of the cup area.</strong> </p>
<p>There is about 5-6cm between the side seam and the edge of the cup (including 2cm seam allowance). I wouldn�t recommend limiting the cups too much for a plus bust size. It's best to choose the smaller value - 5cm <strong>(point A).</strong> The first vertical <strong>bone (8)</strong> will pass through point A. </p>
<p>I suggest the second vertical <strong>bone (9)</strong> should pass through the middle of the neckline (point B). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>I draw the waistline to use it as a guide line. All vertical bones will be placed perpendicular to it. <strong>I draw bone 8 through point A and bone 9 through point B. </strong></p>
<p><strong>Please note that the distance between the side seam and the bone number eight should be at least 2cm.</strong> Curvy figures are much more susceptible to the body control effect than angular figures. You can usually make the garment a little tighter during a fitting depending on how your client feels in it. More often than not, you will find that you need to narrow it down in the waist. It will be a shame then if you�ve already sewn that bone onto the lining - it�ll stand in the way and won't let you adjust the garment with the help of the side seams. You will have to rip it off and then sew it back on. </p>
<p>Don�t lose courage if that distance does happen to be less than 2cm on your garment. <strong>Simply place the bone (8) at a slight incline (dashed line). </strong>This way you will shift the row of stitches that�ll attach the bone at the waistline level further from the side seam.</p>
<p>In my case there is 3cm between the edge of the seam allowance and the bone (8).</p>
<p><strong>Now let's talk about the length of the vertical bones.</strong> Bone 9 will be 14cm long and bone 8 will be 15cm long. We have already decided that the length of the vertical bone that�s closest to the princess seam will be 13cm. The bones will form a semi-circle when the garment is assembled. </p>
<p><strong>The horizontal bones of the cup (1 and 2) will only reach the vertical bone (8) that marks the border of the cup.</strong> This will allow you to apply alterations to the side seam at the level of the cup after a fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I repeat the same steps to mark symmetric bones on the other side front piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Well, we have all bones marked on the cups.</strong> If you feel like yours need additional reinforcement, you can add more bones in the sewing process. But trust me, this amount is usually enough.</p>
<p><strong>Now we need to mark the location of bones at the back. </strong></p>
<p><strong>The central pieces of the back:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>We won't mark anything on these pieces. First of all</strong> they involve seam allowances of 2cm and 1.2cm and what's left in-between is a rather narrow area. <strong>And besides</strong> there will be additional pieces sewn onto it to make the lace-up area. We haven't cut them out yet, it�s the next stage. There will be a stiff lacing bar and a bone following the princess seam. There�s no need in any additional strengthening. </p>
<p><strong>The side pieces of the back:</strong> </p>
<p>The side pieces need a little polishing. They are usually very prone to changes: tension results in creases that need to be straightened out with the help of bones. </p>
<p>You should step at least 2.5cm from the 2cm seam allowance of the side seam and <strong>lay a vertical bone there (12).</strong>&nbsp; </p>
<p>I divide in half the distance from this bone to the princess seam (remember that there�s a 1.2cm seam allowance) and lay another parallel <strong>bone (13)</strong>. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I often hear the following question: 'Where do I mark the top border of the vertical bones at the back if it features corners for attaching shoulder straps?' </strong></p>
<p><strong>If you are sewing with thick fabric</strong> that won't result in a crease along the top border of the bones, then you can bring them to the level of the original top edge of the back (where it would be if we hadn't added those corners).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>It's the level of your client's shoulder blade line, which shouldn't be surpassed by the bones. <strong>It's best to make the bones stop 1cm below that line.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image016.jpg" class="img-responsive center-block"></p>
<p>The above said is only applicable when you're working with thick fabric and are sure that the bones won't push through. <strong>My current fabric is rather thin though, which is why I will bring the bones all the way up to the stay-stitch along the top edge of the back.</strong> </p>
<p><strong>These bones will support both the back and the corners. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>These vertical bones will stop 14cm below the waistline just like the bones along the princess seams.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same steps to mark symmetric bones on the other side piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%205_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>All bones are marked now.&nbsp; </strong></p>

