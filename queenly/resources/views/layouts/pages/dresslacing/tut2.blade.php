<h1>Tutorial 2. Calculating Pattern Alterations.</h1>
<p></p>
<p><strong>We need to determine necessary alterations to adjust the pattern after client's measurements.</strong> </p>
<p><strong>You can see all relevant measurements written on the board in the picture below.</strong> We will start from the measurements required for the base of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Here are the measurements I took off my client to adjust the standard pencil dress pattern: </strong></p>
<p>- Bust front, </p>
<p>- Under-bust front, </p>
<p>- Bust circumference, </p>
<p>- Under-bust circumference, </p>
<p>- Waist circumference, </p>
<p>- Stomach circumference. Make sure to record the distance from the waist in brackets. I took this measurement 12cm below the waist.</p>
<p>- Hip circumference. And again, I mark the distance from the waist in brackets. What�s important, the broadest part where I measured my client�s hip circumference was 22cm below the waistline. The hip line of the standard pattern is marked 18-19cm below the waistline. In order to adjust the standard pattern you should simply shift the hip line down to the level of 22cm below the waistline.</p>
<p>- Bust middle, </p>
<p>- Clip: towards the armscye, towards the corner and towards the neckline.&nbsp; </p>
<p>- Back width, </p>
<p>- Corner to corner (the distance between the corners for attaching the shoulder straps at the back of the dress). </p>
<p>- Corner height (the distance from the waistline up to the top of the corner for attaching shoulder straps at the back of the dress). </p>
<p>- Back length.</p>
<p><strong>These are the measurements required for constructing or altering the pattern of the dress base.</strong> </p>
<p><strong>I draw a table with eight columns.</strong>&nbsp; </p>
<p><strong>The first column contains the dimensions of the standard pattern while the second column is meant for recording individual client's measurements.</strong> </p>
<p><strong>Let me remind you how to measure the pattern correctly. </strong></p>
<p>The most sensible way is to print out the pattern enclosed with this course and sew a mock-up with some plain thick non-stretch fabric (cotton, for example). </p>
<p><strong>What do I mean by a mock-up?</strong> A mock-up is a garment with sewn down princess seams. Don't try to say that it's too time-consuming. Sew a mock-up just once and you will be confident about how to proceed. Suppose you have assembled your mock-up by sewing down all princess seams, put it on the dress-form, stuffed all hollows with padding polyester, wadding or bits of fabric, and achieved the shape the garment is supposed to acquire after you sew bones onto it. Do not sew any bones onto it at this stage. </p>
<p><strong>Once you have fully recreated the required shape and size, take a measuring tape and measure the mock-up <em>the same way you would measure a person</em>. </strong>For example, I take the bust middle measurement a little higher than they do traditionally. I have been sewing corset garments for 25 years and I've developed the habit of taking it wherever the actual bust middle is meant to be on the corset. The corset always produces a push-up effect lifting the breasts a little higher up. </p>
<p>Take the clip measurement the way you would take it off a client. Measure the waistline circumference, stomach circumference and all other measurements the way you normally do. <strong>The levels where you take measurements off your client will fully correspond with the same levels on the mock-up.</strong></p>
<p>Many people write to me saying they don't feel like spending their time on sewing mock-ups. Well, let me tell you something. You end up spending more time on exchanging e-mails than you would on sewing a mock-up. Whenever somebody asks me to measure my pattern and send its precise dimensions so they can compare them with what they have printed out, I consider it a waste of both their time and mine. <strong>Do make your own pattern!</strong> It doesn't matter if your printer makes it slightly different. The difference can't possibly be that great. You shouldn�t experience any troubles just because the dimensions of your pattern are not fully identical to mine. You have at hand a pattern with good proportions that provides a great foundation for further work. Don't be disturbed if its dimensions differ from the ones written in my table. It�s no big deal! </p>
<p><strong>Right now I�ll explain how to alter the standard pattern and adjust it after individual measurements! </strong></p>
<p><strong>Let's look at my client's measurements.</strong> </p>
<p>My client's measurements happen to be pretty close to those of the original pattern. However there are certain peculiarities. My client's body possesses certain distinctive features: full breasts, relatively small waist, beautiful and rather full hips. It reminds me of the classic hourglass body type. So now I need to find alteration values to adjust the pattern for this particular woman.</p>
<p><strong>The first thing you need to do is determine the location of the side seam.</strong> </p>
<p>The location of the side seam is determined by two measurements: bust front and under-bust front. And here comes the first challenge you�re likely to face. Look here:</p>
<p><strong>The bust front measurement</strong> is 56cm on the client and 49cm on the pattern. </p>
<p><strong>56cm - 49cm = -7cm</strong></p>
<p>I divide it in half because there are two side seams. It's obvious then that <strong>I need to expand the pattern by 3.5cm from each side</strong>. </p>
<p><strong>Under-bust front measurement:</strong> 41cm on the client and 40cm on the pattern. </p>
<p><strong>41cm - 40cm = -1cm</strong></p>
<p>I divide it in half to account for two side seams. It turns out<strong> I need to expand the pattern by only 0.5cm from each side</strong>. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I hear you asking 'What are we supposed to do in this case?'</strong> Just shift the sides outwards by 3.5cm along the bust-line and by 0.5cm along the under-bust line. Don't worry, there is nothing wrong with it!</p>
<p><strong>Let�s look at the clip measurement. </strong></p>
<p><strong>- Towards the armscye: </strong>14cm on the client and 12cm on the pattern. </p>
<p><strong>14cm - 12cm = 2cm</strong></p>
<p>I need to add another 2cm to the pattern.&nbsp; </p>
<p><strong>- Towards the corner: </strong>14cm on the client and 12.5cm on the pattern. </p>
<p><strong>14cm - 12.5cm = 1.5cm</strong></p>
<p>I need to add another 1.5cm to the pattern. </p>
<p><strong>- Towards the neckline: </strong>10cm on the client and 7cm on the pattern.</p>
<p><strong>10cm - 7cm = -3cm</strong></p>
<p>I need to add 3cm to the clip towards the neckline.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image006.jpg" class="img-responsive center-block"></p>
<p>Now let's look at the alterations of the clip and of the first two measurements. <strong>The resulting dispersion of values might make you puzzled.</strong> But you should always imagine the alteration process in your head. It is obvious that I need to make the pattern bigger. Judging from the alterations of the bust front and under-bust front measurements the pattern needs to be expanded - in other words, its sides need to be shifted outwards as compared to the standard pattern. However the clip measurement also needs to be made bigger if you expand the standard pattern widthwise. Our calculations show that the clip is supposed to be made 2cm longer. The clip-towards-armscye tends to grow 2-2.5cm bigger whenever you expand a standard pattern by 3cm from each side. In other words, if we shift each side of the piece 3cm outwards, the clip will shift proportionately. Everything will basically fall into place then. </p>
<p><strong>Considering all this I have accepted the alteration value of 3cm for shifting the <em>entire</em> side seam of the standard pattern. </strong>We will double-check this value in further calculations. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>The bust middle measurement is 20cm on the pattern as well as on the client.</strong> Since there are no alterations in the middle of the piece, you should just align the edge of the pattern with the fold line of the fabric without shifting it. </p>
<p>Looking at the bust middle measurement you might say, �Wait, how this is possible? The standard pattern has a bust circumference of 93cm and the client's bust circumference is 102cm. Can their bust middle measurements be the same then?' </p>
<p>The answer is 'Yes!' Now let me explain why. </p>
<p>We are working on a corset-based dress not a regular dress. The bust middle measurement helps us determine the location of princess seams, place them in a graceful way. If you place princess seams too far from one another on a garment made for a woman with full breasts, you will make her body look bigger. Her already large bust will appear even larger. </p>
<p>But we, on the contrary, want to make it appear smaller, bring it down to the common standard. If we shift the princess seams closer to the centre, they will make the bust look a little smaller. </p>
<p>That's why I determined the distance between the princess seams straight when I was taking the bust middle measurement to ensure my client will look graceful in her dress. And that's why the measurements coincide.</p>
<p>Measurements that refer to the back of the dress are not relevant here. I write dashes in the table. </p>
<p><strong>Let�s move to the sides of the back.</strong> First I pre-calculate the configuration of the side seam and write the values into column 5 (Side Seam Back). </p>
<p><strong>Let�s begin calculating.</strong> You�ll see what happens after I take all measurements and calculations from the table into account. </p>
<p><strong>The bust front and the under-bust front measurements are not involved in these calculations.</strong> I write dashes in the table.</p>
<p><strong>Bust circumference: </strong>102cm on the client and 93cm on the pattern.</p>
<p>However I have already expanded the pattern by 3cm from each side - a total of 6cm. </p>
<p><strong>93cm + 6cm= 99cm</strong></p>
<p><strong>102cm - 99cm = 3cm</strong></p>
<p>I divide the value in half. As you can see, <strong>I need to expand the pattern by 1.5cm from each side along the bust line. </strong>I write 1.5cm into the table.</p>
<p><strong>The under-bust circumference </strong>is 82cm on the client and 80cm on the pattern.</p>
<p>But I have already expanded the pattern by 3cm from each side - a total of 6cm. </p>
<p><strong>80cm + 6cm= 86cm</strong></p>
<p><strong>82cm - 86cm = -4cm</strong></p>
<p>I divide it in half. As a result, <strong>I need to narrow the back down by 2cm from each side along the under-bust line.</strong> I write -2cm in the table. </p>
<p><strong>Waist circumference: </strong>75cm on the client and 75cm on the pattern. The measurements coincide. </p>
<p>However I have already expanded the pattern by 3cm from each side - again, a total of 6cm. </p>
<p><strong>75cm + 6cm= 81cm</strong></p>
<p><strong>75cm - 81cm = -6cm</strong></p>
<p>I divide it in half. As a result, <strong>I need to narrow the back down by 3cm from each side along the waistline.</strong> I write -3cm in the table.</p>
<p><strong>Stomach circumference:</strong> 103cm on the client and 95cm on the pattern.</p>
<p>However I have already expanded the pattern by 3cm from each side - a total of 6cm. </p>
<p><strong>95cm + 6cm= 101cm</strong></p>
<p><strong>103cm - 101cm = 2cm</strong></p>
<p>I divide it in half. As a result, <strong>I need to expand the back by 1cm from each side along the stomach line.</strong> I write 1cm in the table.</p>
<p><strong>Hip circumference: </strong>110cm on the client and 102cm on the pattern.</p>
<p>But I have already expanded the pattern by 3cm from each side - a total of 6cm. </p>
<p><strong>102cm + 6cm= 108cm</strong></p>
<p><strong>110cm - 108cm = 2cm</strong></p>
<p>I divide it in half. <strong>I need to expand the back by 1cm from each side along the hip line.</strong> I write 1cm in the table.</p>
<p><strong>Bust middle and clip measurements are not involved in these calculations.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Next we need to construct raised corners at the back of the dress to attach shoulder straps.</strong> Those corners serve to cover up the excess of skin in the area where the arms join the back. It's a problem area for everyone. Some skin always sticks out over the top of the corset when you start lacing it up. This has to be disguised! And that's why we need those corners at the back of the dress.</p>
<p><strong>You only need three measurements to construct those corners:</strong> </p>
<p>- <strong>Back width.</strong> That is the distance from arm to arm measured across the back with arms hanging naturally.</p>
<p><strong>- Corner to corner.</strong> Usually you just feel for the straps of your client's bra and measure the distance between them. What makes it the best way to take this measurement? Bra straps will naturally draw out their placement on the shoulders of a woman with bust size 100cm or above. They even tend to leave red marks on the skin. And even if you try to move the straps closer to the shoulders for the sake of design, they will still inevitably jump back into those natural grooves. It's their rightful placement and you shouldn't invent any other.</p>
<p>- <strong>Corner height</strong> (measured in accordance with the model). Find a nice way to place the corner at the back of the dress and measure the distance up from the cord tied around the waistline.</p>
<p>Both the back width and the corner to corner measurement are 19cm on the pattern. Let me tell you what this means. It's the width of the central back piece along the top edge without seam allowances. If you measure that distance and multiply it by two, you will get 19cm. </p>
<p><strong>Let's calculate it.</strong></p>
<p><strong>Back width:</strong> 38cm on the client and 19cm on the pattern.</p>
<p><strong>38cm - 19cm = 19cm</strong></p>
<p>I divide it in half because the piece has two sides. I get <strong>9.5cm - it's the value I�ll need to mark from the princess seam toward the side edge on the side back piece. </strong>&nbsp;As the result I will find the point from which I took the back width measurement. It's that problem area that needs to be covered up. I write 9.5cm in the table.</p>
<p><strong>Corner to corner: </strong>26cm on the client and 19cm on the pattern.</p>
<p>It's the same story here. We know that the width of the centre of the back is 19cm on the pattern. In other words the distance between the princess seams (without seam allowances) will be 19cm after we join the central pieces of the back. We also know the distance between the corners - 26cm. The task is to calculate the value by which I need to shift the princess seam to achieve the desired configuration of the corner. </p>
<p><strong>26cm - 19cm = 7cm</strong></p>
<p>I divide it in half and get <strong>3.5cm - it's the value I�ll need to mark from the princess seam toward the side edge of the side back piece.&nbsp; </strong>It defines the location of the vertical line on which the top point of the corner is supposed to lie. I write 3.5cm in the table.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>When I was measuring the height of the corner on my client, I thought it would be good to place it 26cm above the waistline.</strong> I write that value in the table. When I start drawing the corner on the pattern, I will step 3.5cm inwards from the princess seam and 26cm up from the waistline to find its top point. </p>
<p><strong>The back width measurement is 17cm.</strong> I write it in the table.</p>
<p><strong>Let us study the alterations of the side seam contour at the back.</strong> As you can see there is a great dispersion of adjustment values. The standard pattern needs to be made smaller under the bust and in the waistline and bigger in the bust, stomach, and hips. You will never get a beautiful contour of the side seam if you leave it as is. </p>
<p><strong>I have taken the following decision:</strong> we will recalculate the side seam configuration at the front and at the back and record newly adjusted values in the corresponding columns. And of course we will use the princess seams at the back and at the front to alter the pattern.</p>
<p>Analyzing the current configuration of the side seam at the back I can tell we need to make the pattern smaller under the bust and in the waist and bigger in the bust, stomach, and hips (by about the same value). </p>
<p><strong>It makes sense to do the following to adjust the side seam configuration at the front (column 4):</strong> keep the adjustment value at 3cm along the bust line and reduce it down to 2cm under the bust and in the waist, i.e. give the pattern more of an hourglass shape. A difference of 1cm is not crucial. You can easily make the pattern 1cm bigger or smaller. However a difference of 1cm in the side seam configuration will result in a rather noticeable incline! It's not worth the risk! I will also use a value of 1cm to adjust the side seam in the area from the waistline to the stomach at the front. Only this time I will add and not subtract it. The value of adjustment in the stomach and in the hips will be 3cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The adjusted side seam configuration at the back (column 6) </strong>has to repeat the configuration at the front. In other words the difference between the adjustment values has to be 1cm too. I suggest using a common invariable of 1.5cm along the bust line. In order for us to preserve the difference of 1cm the adjustment value under the bust and in the waistline should be 0.5cm. The value of adjustment in the stomach and in the hips will be 1.5cm. This way the side seam will have the same configuration at the back and at the front as compared to the standard pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>We still haven't achieved the desired result so let�s make use of the princess seams at the front and at the back of the dress.</strong></p>
<p><strong>I would like to say a couple of words about possible ways of using front and back princess seams to alter patterns.</strong> </p>
<p><strong>You can change the depth of the back princess seams in the area from the bust down to the hips. </strong>&nbsp;Not only are such alterations safe to make but they will also bring the configuration of the dress closer to your client's natural body shape. If her body has a well-pronounced hourglass silhouette, you will have to make the darts in the waist deeper anyway and expand the pattern in the stomach and in the hips.&nbsp; This is the only way to recreate very pronounced curves on the pattern.&nbsp; Bit by bit you will see it from your own experience how client's measurements point at the peculiar features of her figure in calculations. You will be able to imagine her body and the configuration of the pattern just by looking at those measurements. </p>
<p><strong>What concerns the princess seams at the front of the garment, <em>they can only be involved in the calculations starting from the waistline and below.</em> </strong>Never change the depth or the shape of the princess seams at the front of the garment along the bust line or under the bust! <strong>They should stay untouched from the top down to the waistline! </strong>&nbsp;You can safely change the depth of the darts below the waistline: at the level of the waist, the stomach, and the hips. </p>
<p><strong>Let us work on the princess seams at the back and the front of the garment (columns 7 and 8). </strong>We will use the values of the adjusted side seam configuration at the front and at the back (columns 4 and 6). </p>
<p><strong>Along the bust line:</strong> </p>
<p>Let's see what's happening with the princess seams at the back. I have added 6cm to the measurement of 93cm on the pattern to adjust the side seam contour at the front and 3cm to adjust it at the back. </p>
<p><strong>93cm + 6cm + 3cm = 102cm.</strong></p>
<p>The resulting 102cm corresponds with my client's measurement. <strong>This means I should leave the front and the back princess seams unchanged along the bust line. </strong>I write a zero in the table.</p>
<p><strong>Along the under-bust line:</strong></p>
<p>I have added 4cm to the measurement of 80cm on the pattern to adjust the side seam contour at the front and 1cm to adjust the contour at the back.</p>
<p><strong>80cm + 4cm + 1cm= 85cm.</strong></p>
<p>My client's measurement is 82cm. </p>
<p><strong>82cm- 85cm= -3cm.</strong></p>
<p>There are three extra centimeters. I can distribute this 3cm between the four raw edges of the princess seams at the back. In other words<strong> I need to shift each edge of each princess seam 0.75cm inwards.</strong> I write -0.75cm in the table. </p>
<p><strong>Along the waistline: </strong></p>
<p>I have added 4cm to the measurement of 75cm on the pattern to adjust the side seam contour at the front and 1cm to adjust the contour at the back.</p>
<p><strong>75cm + 4cm + 1cm= 80cm.</strong></p>
<p>My client's measurement is 75cm.</p>
<p><strong>75cm - 80cm= -5cm.</strong></p>
<p>There are five extra centimeters. </p>
<p>We can use the front princess seams starting from the waistline, which means this 5cm can be distributed between the four raw edges of the back princess seams and the four raw edges of the front princess seams. I divide the value by eight.&nbsp; Now I know that <strong>I need to shift each side of each front and back princess seam 0.6cm inwards.</strong> I write -0.6cm in the table. </p>
<p><strong>Along the stomach line: </strong></p>
<p>I have added 6cm to the measurement of 95cm on the pattern to adjust the side seam contour at the front and 3cm to adjust it at the back.</p>
<p><strong>95cm + 6cm + 3cm= 104cm.</strong></p>
<p>My client's measurement is 103cm.</p>
<p><strong>103cm - 104cm = -1cm</strong></p>
<p>There is one extra centimeter. </p>
<p>This 1cm can be distributed between the four raw edges of the back princess seams and the four raw edges of the front princess seams, i.e. it can be divided by eight.&nbsp; Thus <strong>I need to shift each side of each front and back princess seam 0.1cm inwards.</strong> I write -0.1cm in the table. </p>
<p><strong>Along the hip line: </strong></p>
<p>I have added 6cm to the measurement of 102cm on the pattern to adjust the side seam contour at the front and 3cm to adjust the contour at the back.</p>
<p><strong>102cm + 6cm + 3cm= 111cm.</strong></p>
<p>My client's measurement is 110cm.</p>
<p><strong>110cm - 111cm= -1cm.</strong></p>
<p>There is one extra centimeter, just like at the level of the stomach.&nbsp; </p>
<p>This 1cm can be distributed between the four raw edges of the back princess seams and the four raw edges of the front princess seams, i.e. it can be divided by eight. Thus<strong> I need to shift each side of each front and back princess seam 0.1cm inwards.</strong> I write -0.1cm in the table. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%202_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Last but not least:</strong> we don't have to account for that 1cm adjustment in the stomach and in the hips since we are making a dress and not just a short corset. We can just leave it for better comfort of movement. It's enough to deepen the dart in the waist. </p>
<p><strong>This is all that concerns calculating the pattern of our pencil dress! </strong></p>

