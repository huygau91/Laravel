<h1>Tutorial 3. Constructing the Pattern.&nbsp; Calculating Fabric Consumption. </h1>
<p></p>
<p><strong>We have printed, glued together and cut out the patterns. Now we need to start altering them in accordance with the calculations.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have decided to apply all alterations on paper and not on fabric for better clarity.</strong> &nbsp;For that purpose I have printed out the same patterns but without the centimeter grid and glued them together. Imagine that I've placed the standard pattern on fabric and traced the contours.&nbsp; In other words I am preparing an adjusted paper pattern. You can do the same if you don't feel confident enough to work with fabric straight away. In that case you can first prepare a paper pattern and apply all adjustments and then sew a mock-up after the adjusted pattern and double-check everything. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The side part of the front:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I apply the calculated alterations to the side seam contour at the front.</strong> Let's look at column 4 (Side Seam Front Adjusted). </p>
<p><strong>The side seam contour need to be shifted 3cm outwards in the bust...</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>...and 2cm outwards under the bust.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I also need to shift it 2cm outwards in the waist... </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>...and 3cm outwards along the stomach line 12cm below the waistline. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p>The hip line is 19cm below the waistline on the standard pattern and 22cm below the waistline on my client. <strong>I re-draw the hip line and prolong the side seam and the princess seam perpendicular to it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I need to shift the side seam contour 3cm outwards along the new hip line 22cm below the waistline.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I also need to alter the clip.</strong> If you don't count the seam allowance (point A), the distance to the underarm should be 14cm plus 1.2cm of the seam allowance, which makes a total of 15.2cm (point B).&nbsp; The corner of the neckline should be located at the distance of 15.2cm from point A (point C). Therefore the armscye should pass through points B and C. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I extend the princess seam by about 0.5cm and re-draw the top line using the pattern as a template. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Next I connect points B and C with a smooth line following the contour of the pattern.</strong> This also involves prolonging the side seam upwards by about 1.6-1.7cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>As a result there�s a dart at the under-bust level that we will remove later.</strong> I mark the depth of the dart. You can safely make it 2cm. The dart will turn the top of the piece towards the bust and ensure good fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Then I connect the reference points and re-draw the side seam with the help of the standard pattern. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>You can see the adjusted piece below. </strong>I will adjust the princess seam later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And now let's work on the back side piece. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>The side seam contour of the back piece:</strong> </p>
<p>All alterations are applied exactly the same way as on the front half of the dress. Let's look at column 6 (Side Seam Back Adjusted). The side seam needs to be shifted outwards by 1.5cm in the bust, 0.5cm under the bust, 0.5cm in the waist, and 1.5cm in the stomach (12cm below the waistline). I also bring the hip line down to the level of 22cm below the waistline to match my client's measurement and shift it 1.5cm outwards as well.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take the standard pattern that serves as a base and re-draw the side seam using it as a template. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>The top edge of the side back piece is not so important because we still need to add the corners. </strong>&nbsp;&nbsp;But if you don�t plan to do that, you should re-draw the top edge with the help of the standard pattern and cut the piece accordingly.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I take the central back piece and align its waistline with the waistline of the side piece. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the hip line of the central back piece down to the level of 22cm below the waistline. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to apply changes to the princess seam now. </strong>&nbsp;</p>
<p><strong>The back princess seam requires the following alterations: &nbsp;</strong></p>
<p><strong>I need to shift it 0.75cm inwards under the bust. </strong>I use the pattern as a template to re-draw the area of the princess seam from the top down to the under-bust notch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I need to shift each raw edge of the princess seam 0.6cm inwards in the waist. </strong>Again, I align the waistline notches and re-draw the princess seam from the under-bust notch down to the waistline with the help of the standard pattern. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>You can keep it �as is� in the stomach and in the hips. </strong>&nbsp;I bring the princess seam down following the contour of the pattern.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>The height of the back should be 17cm plus a seam allowance of 1.2cm - 18.2cm total.</strong> Perfect. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I start constructing the corner. </strong>&nbsp;</p>
<p>I prolong the princess seams upwards and mark the seam allowances. It follows from our calculations that the ascent will begin at a 9.5cm distance from the princess seam when the latter is sewn down.&nbsp; We can add another 1.2cm to account for the seam allowance.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Where is the top point of the corner? </strong>It is located at a 3.5cm distance from the closed princess seam and 26cm above the waistline (point A).&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a graceful line. </strong>&nbsp;We want to cover up the problem area at the base of the arm from the side seam to point A. I think the corner is placed too low now so I move it higher up. I leave a horizontal seam allowance of 2.4cm at the top of the corner.&nbsp; </p>
<p>The best way to design the back of our dress is to cut its pieces tall following the red dashed line, sew them together, and then cut out a beautiful back neckline. It's the surest way to achieve a graceful shape! </p>
<p>But we can also do it right now. Let�s stick to the basic rules of geometry - rules of dealing with secants. Give the same incline to lines 1 and 2 to make sure they will merge well in the sewing process. The rest is a question of taste. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>The pattern of the back is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Let us return to the side and the central piece of the front.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I need to apply changes to the front princess seam.</strong> </p>
<p><strong>The front princess seam requires the following alterations:</strong></p>
<p><strong>I need to shift each raw edge of the front princess seam 0.6cm inwards in the waist.</strong> I align the waistline notches and re-draw the area of the princess seam from the under-bust line to the waistline with the help of the standard pattern. </p>
<p><strong>You can leave it �as is� in the stomach and in the hips.</strong> I bring the princess seam down following the contour of the pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I prolong the central front piece down</strong> so as to move the hip line 22cm below the waistline and<strong> extend the princess seam up by 0.7cm</strong> to make its length identical to the princess seam on the side front piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I also need to apply alterations to the clip.</strong> If you don't count the seam allowance (point A), the distance to the middle of the neckline should be 10cm plus a 1.2cm seam allowance, which makes a total of 11.2cm (point B). Therefore the top line should pass through point B. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>I prolong the fold line upwards and I'm going to try and draw a neckline of a suitable shape.</strong> Sometimes you have to slightly adjust the clip because it�s responsible for the overall look of the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image070.jpg" class="img-responsive center-block"></p>
<p>If your client demands that the d�collet� zone be covered up to point B, you will have to prolong the front princess seams upwards and lift the top corner of the side front piece. If you took that measurement solely for reference and know that your client won't be all black and white about it, then you can draw a beautiful neckline shape to your liking. </p>
<p><strong>I highly recommend that you put the pieces together the way you are going to join them and draw a graceful merged line to achieve a beautiful neckline.</strong> I cut out the central front piece following the new contour and align it with the side front piece with the seam allowance in account.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>Right now it's a messy broken line that needs to be re-drawn.</strong> I have made two versions of a neckline: without a pronounced raised part (blue line) and with a rise (red line). <strong>I prefer the version with a rise </strong>but you can feel free to go creative. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>My new pattern of the front looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>I have taken all details into account and we can now embark on the cutting process.</strong></p>
<p><strong>You can determine fabric consumption precisely by laying all pieces down in the exact same order you are going to cut them.</strong> </p>
<p><strong>First measure the distance from the fold line of the fabric to the middle of the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%203_files/image078.jpg" class="img-responsive center-block"></p>
<p>I got about 69cm. I�ll make it 75cm to be on the safe side - 1.5m if you unfold it. </p>
<p>Fabric consumption depends on the way you're going to cut. </p>
<p><strong>If you are going to cut cross-grain, you will need 1.5m of fabric.</strong> </p>
<p><strong>If you are going to cut lengthwise</strong>, then 1.45-1.5m will be enough for the width and the length will depend on the length of the skirt. The skirt of my dress goes 70cm below the waistline and I always add some extra fabric just in case.&nbsp; </p>
<p>I need 110cm of fabric with the width of 150cm for the lining and as much for the face. Therefore 2.2m is enough to sew this dress.&nbsp; </p>
<p>Frankly speaking I always take a little more fabric just in case. For example, you need additional fabric to make a concealed lace-up area.&nbsp; </p>
<p><strong>As for me I would feel safe with 3m of fabric for this pencil dress. </strong></p>

