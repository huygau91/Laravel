<h2>Tutorial 7. Sewing Bones onto the Back. Finishing the Lacing Bars.</h2>
<p></p>
<p><strong>I start sewing straight wide Rigilene bones onto the side pieces of the back.</strong> </p>
<p><strong>I make a stay-stitch along the top edge of each piece for a start.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>I tape the ends of all bones with masking tape. <strong>The bone thatï¿½s closer to the side seam should be placed left of the marked line</strong> to preserve maximum distance between it and the side seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>All bones are sewn on with two rows of parallel stitches.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I sew on the second vertical bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And then I sew bones onto the other side piece exactly the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now both pieces have bones sewn onto them! </strong></p>
<p><strong>It's time to work on the lacing bar inset that will be hidden 'inside' the dress.</strong> </p>
<p>I put the pieces down and divide them into the face and the lining as per usual. In this particular case it doesn't matter which piece to choose for the face and which for the lining - just make sure they make symmetric left and right pairs. </p>
<p><strong>Here is the plan for working on the inset.</strong> We will make stay-stitches along all sides of each lining piece <strong>save for the side of the princess seam</strong>. Then we will sew three parallel wide Rigilene bones onto each inset to turn it into a lacing bar and make space for grommets. Sometimes they add horizontal bones at the bottom and at the top of the lacing bar. But that is only applicable if the lacing bar is really wide and you are afraid it might stretch out under tension. I wonï¿½t do that since we are not looking for a strong tightening effect. </p>
<p><strong>I make stay-stitches on the lining pieces.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew on the bones. </strong>Each bone is attached with two rows of parallel stitches.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image040.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image050.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I sew symmetric bones onto the other lacing bar.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image062.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image064.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now we need to overlay each lining piece with a corresponding face piece, align them, and join them.</strong> You can pin the pieces together at the top, down the bottom and along the waistline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I join the face with the lining following the stay-stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image070.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image072.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image074.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the corners and the seam allowances. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image078.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the piece out, spread it and secure it with pins. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>You need to make strengthening stitches basically following the rows of stitches on the bones.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image084.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image086.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>Now the lacing area and the area for setting grommets look more pronounced. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image090.jpg" class="img-responsive center-block"></p>
<p><strong>We can make a strengthening stitch along the edge of the piece. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image092.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The same should be done with the second piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%207_files/image094.jpg" class="img-responsive center-block"></p>
<p><strong>Now you can press the pieces.&nbsp; </strong></p>

