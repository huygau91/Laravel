<h2>Tutorial 10. Preparing to Join the Face with the Lining. </h2>
<p></p>
<p><strong>Now we need to press all seams on the lining and on the face of the back.</strong> I like using a sleeve-ironing board for this purpose. It is perfect for pressing seams or individual small areas of dresses and corsets. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I press open the princess seams of the face.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold and press the seam allowances of the slit. It will help me finish it later.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I press the zip-up area and let it dry and cool down in this position to fix the fabric in place.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>You can see the back with the zipper below:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>There is a perfectly smooth transition from the zipper to the seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can start joining the face with the lining.</strong> Please note that the lining is still made of two separate halves. I have purposely left the central seam open.</p>
<p><strong>You can take a piece of chalk and mark notches from each side of the zipper </strong>just to be sure. I do it even though the top edges of the halves are aligned perfectly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can open the zipper and start pinning the pieces together.</strong> First I will join the face with the lining with a clean-edge finish seam along the neckline and then I will join them along the armscye, too. I pin them together only from the inner side for now. We don't need to lay a bone along the top edge at the back of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the pieces down looking to the chalk marks and the stay-stitches.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>

