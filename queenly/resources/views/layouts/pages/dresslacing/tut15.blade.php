<h2>Tutorial 15. Pressing the Princess Seams and the Cups of the Lining. </h2>
<p></p>
<p><strong>So we have sewn down the princess seams and topstitched and trimmed the seam allowances on the cups.</strong> Now we need to press the cups. I will use cup pressing moulds for this purpose.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Why do I place so much importance upon pressing, or steam-setting, in this particular case?</strong> The thing is our cups are not round boned cups. In other words we cannot use bones to give them that proper nice and round cup shape. We are going to achieve it by pressing instead. Besides we also need to press the vertical princess seams open and give them a neat look. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The picture below clearly shows the difference between the cup that has been pressed and the cup that hasn't: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It is a lot easier to press seams on a raised platform rather than on the flat tabletop.</strong> You can press individual areas while the rest of the piece is hanging over the edges of the sleeve-ironing board without getting in the way. This is incredibly handy when working on large garments. Particularly, on the transition from the stomach to the hips and from the waist to the stomach - the areas of noticeable change in circumferences that are rather hard to press on a flat surface. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2015_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
