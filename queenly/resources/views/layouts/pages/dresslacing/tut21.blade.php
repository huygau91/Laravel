<h2>Tutorial 21. Sewing Down the Side Seams of the Dress.</h2>
<p></p>
<p><strong>So we have serged the side seams. The front and the back of the dress are ready. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can sew down the side seams. </strong></p>
<p><strong>I align the waistline notches and the hip notches and pin the pieces together.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I sew the side seam from the top down to the hip line.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let me show you one trick that I want to use for this dress. </strong></p>
<p><strong>I move the lining away, place the needle at the hip level and stitch down to the very bottom. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I join the lining layers that I moved away. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch from down to up and stop 5-6cm below the hip line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I turn it out.</strong> </p>
<p><strong>Look what happens as the result. The joining seam on the lining is hidden inside and the raw sides of the dress remain open down to the hip level. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The point of using this method is to prevent the lining from pulling on the main fabric of the skirt and at the same time leave the side seam of the dress open. </strong>This was the first trick. </p>
<p><strong>Now I'll share another trick that can help reduce the amount of wrinkles in the transition from the corset to the skirt on a not so long straight-cut dress.</strong> You surely know those treacherous wrinkles that tend to form around the stomach or around the thigh area whenever you sit down or stand up. It's one thing when you sit down and stand up - this makes wrinkles inevitable. But it's a whole different thing if they form while you're just standing. </p>
<p><strong>The trick I want to share with you might not eliminate such wrinkles entirely but it definitely helps reduce their amount considerably.</strong> </p>
<p><strong>The first thing I do is sew a wide Rigilene bone onto the seam allowance of the side seam. It should reach down to the hip level.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>When you open the side seam, this bone is hidden inside. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image032.jpg" class="img-responsive center-block"></p>
<p>My student Julia Trofimova who moderates the online forum on my website has improved this method and uses it rather often now. I find her idea great. This is what Julia does: <strong>she encases a wide Rigilene bone in fabric the way we normally do for see-through corsets and then sews it onto the side seam. </strong>This way the bone reinforces the side seam as well as the entire construction of the dress. And it makes the dress a lot more wrinkle-proof. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Let us use Julia's trick. </strong>&nbsp;</p>
<p><strong>I topstitch the side seam so it's easier to sew the bone on.</strong> It's best to do this from the side of the face. </p>
<p>Please note that you must have no doubts about the size of the dress before you take this step. Think of it as sewing your dress together for good. I highly recommend you should do a fitting before topstitching the seam.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I need to measure the exact length of the bone.</strong> Before sewing down the side seam I had marked the hip line with chalk at the back. It�s the level the bone is supposed to reach.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>This time I use white electrical tape to tape both ends of the bone. It is more durable than masking tape.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Let's encase the bone in the main fabric of our dress.</strong> I fold the fabric over one edge of the bone and leave a seam allowance along the other edge without folding it. This is done so I can fold it over the top edge of the dress later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I lay the fabric-encased bone over the side seam starting from the top. </strong>Later I will fold the tail back and sew it down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the bone on.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>It looks great! </strong>It does require some pressing though. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>There will be a smooth transition from the front to the back of the dress after I fold the tail of the strap over the top edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>Here is what it looks like from the inside: you can see the stitches that attach the bone and a transition from the open seam to the closed seam of the lining.</strong> The side seam is now very strong. Now trim any thread tails and carefully secure the tail of the strap on the inside.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>Next I do the same with the other side seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image070.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image072.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image074.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image078.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I topstitch it down to the hip line. </strong>The first side seam was easier to work with because the other side seam was open. This time I have to unzip the dress and work through that opening.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image080.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image082.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>I measure the bone, encase it in a strap of main fabric, and sew it in place.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image086.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image088.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image090.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image092.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image094.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2021_files/image096.jpg" class="img-responsive center-block"></p>
<p><strong>I believe we have managed the task very well.</strong> As you can see, it's a valuable trick that often comes in handy. Thank you, Julia! All we need to do now is carefully secure the tails of those straps by hand and press the side seams. </p>

