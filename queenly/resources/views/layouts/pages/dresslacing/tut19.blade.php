<h2>Tutorial 19. Sewing Down the Princess Seams of the Face of the Front. </h2>
<h2>Pinning the Face to the Lining.</h2>
<p></p>
<p><strong>We need to sew down the princess seams of the face of the dress.</strong> This involves no particular tricks or challenges: you simply join the three pieces that make the front of the dress by sewing down the vertical princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance moving upwards from the hip line.</strong> I leave it at about 0.5cm and trim it at an incline at the top.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the second princess seam of the front.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let me remind you that the bust-line notches don't have to be aligned perfectly.</strong> Simply align the lines between these notches in the point where they meet. That is the point through which the seam passes. The notches on the patterns indicate the direction of the bust line, which explains why they are somewhat shifted against each other in the arched area of the edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>In contrast, the notches under the bust, in the waist and in the hips must be aligned perfectly before you start sewing!</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trim the seam allowances.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to thoroughly press the joining seams open.</strong> I place the garment on the ever so handy cup pressing mould and spread the seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Be particularly careful with the corner in the beginning of the seam.</strong> If you press it open with due care, there will be no tucks inside the seam allowance and the seam will look much neater. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I continue pressing on a flat surface.</strong> I find a sleeve-ironing board very convenient for this purpose. The piece hangs down at the sides while the seam stays on the small pressing surface. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Let me highlight it again:</strong> the front of a slim-fit corset-based garment has to feature uninterrupted vertical princess seams going from the top down to the very bottom. There is so much fullness in the bust that you can't possibly align the front pieces with no gaps in-between. So you have to either break it up with vertical princess seams or cut a separate skirt piece starting at the waistline or below the bust or at the hip line, depending on the style. </p>
<p><strong>I turn the garment face up and press the seam from the face side.</strong> I use a pressing cushion to cool the fabric and dab it dry. Try to feel the seam allowance with your fingers to make sure it is spread well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>The princess seams are now pressed open.</strong> It's a pleasure to sew corset garments with cr�pe fabric. It�s very pliable and easy to shape under steam pressure. Besides it is matte and not sheen. I simply love this type of fabric! </p>
<p>You should let the garment cool down before joining the face with the lining. </p>
<p><strong>I have fused the missing piece of batiste onto that corner on the lining</strong> to make it smooth when I turn the garment out.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Let's join the lining with the face.</strong> </p>
<p><strong>I put the pieces together face to face...</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>...and secure them with pins from the side of the face.</strong> Why from this side? Because there are too many bones sewn on from the side of the lining and it�s hard to pierce it. </p>
<p>It's a lot easier to join the face with the lining on large-size garments than on small garments because there is little fullness in the neckline and in the armscyes and therefore there is basically no difference between the face and the lining. </p>
<p>Still. In order to distribute it all evenly I pin the layers together in two outermost points and then divide the distance in half, re-distribute the fabric and stick pins in horizontally. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Don't try to align the princess seams of the face and the lining.</strong> It is even recommended that you shift them against one another by 1mm to reduce the thickness of the seam allowances. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Feel free to use as many pins as you need.</strong> The more pins you use, the neater the stitches will be.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2019_files/image056.jpg" class="img-responsive center-block"></p>

