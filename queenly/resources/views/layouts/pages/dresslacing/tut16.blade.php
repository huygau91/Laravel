						<p><strong>Features:</strong></p>
<ul>
<li>Classic pencil-dress. Modern and trendy design fits any body type perfectly!</li>
<li>Working with patterns for plus-sizes: US 12, EU-44 and larger (bust circumference 100+ cm and larger).</li>
<li>Hidden lacing.</li>
<li>The pattern included can be used for making &nbsp;a light cocktail dress as well.</li>
<li>Patterns of bolero jacket and shoulder cape included in the course.</li>
</ul>
