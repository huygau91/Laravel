<h2>Tutorial 16. Sewing Bones onto Large-Size Cups.</h2>
<p></p>
<p>I didn't elaborate on the process of attaching the outermost vertical bones when we were sewing vertical bones onto separate pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p>Since we plan to push the ends of horizontal bones underneath those vertical bones, they should only be attached along the outer side. The inner side is supposed to stay detached. <strong>I rip up the stitches along the inner sides of the outermost vertical bones.</strong> You can do this with regular scissors. I try to preserve the stitches at the very beginning of the bones so they don't just come undone entirely.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s attach the central horizontal bone.</strong> I turn the cup in. Then I take a narrow Rigilene bone, push its end underneath the outermost vertical bone and sew it on. You don't have to tape the ends. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the bone and push its other end underneath the opposite vertical bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I secure it with another row of parallel stitches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I lay a bone along the neck hole.</strong> It's a narrow Rigilene bone that you can easily gather into an arched shape by pulling out the fishing lines. I trim their tails and tape the end of the bone with masking tape. </p>
<p>I lay the bone down <strong>about 0.5cm below the corner</strong> and 1mm below the stay-stitch that goes along the neckline. I start sewing it on. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I slightly ease in the fabric in the area between the princess seam and the middle of the neckline.</strong> Avoid easing it in too much when working on large-size garments for women with full breasts. Otherwise the cups will be too tight and dig into the body. It's the opposite task when you're sewing for a client with small breasts - you need to make the cups round artificially to make the bust appear fuller. A full-figured woman has no need in this illusion and her body really won't look aesthetically pleasing when squeezed too tight in the d�collet� zone.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I make another row of parallel stitches through the bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>And I lay the second bone along the neck hole. </strong></p>
<p><strong>I start sewing it on from the bottom up.</strong> I have taped its end with masking tape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I have reached the arched area.</strong> In order to bend the bone in the right direction I roughly measure the necessary length of the remaining section of the bone, add some extra millimeters just to be safe and trim it. Next I slightly gather the bone from its trimmed end and bend it in the required direction by pulling on the outermost fishing line. The bone acquires the desired shape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can continue sewing it on.</strong> I ease in the fabric in the same area as for the previous bone.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I make another row of parallel stitches through the bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I start attaching bones along the armscyes. </strong>I have gathered them in advance.</p>
<p><strong>Please remember </strong>that the seam allowance of the side seam is 2cm and you should leave at least 1.5cm for possible further adjustments. Try to avoid easing in the fabric when adding bones along the armscyes. Too tight armscyes will force the upper arms out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image050.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image051.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image053.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image055.jpg" class="img-responsive center-block"></p>
<p><strong>Now it's time to add the top horizontal bones.</strong> I gather them in advance and push their ends underneath the vertical bones. Each bone is attached with two rows of parallel stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image057.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image059.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image061.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image063.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image065.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image067.jpg" class="img-responsive center-block"></p>
<p><strong>The next bone is the bottom horizontal bone.</strong> I gather it in advance and push the ends underneath the vertical bones. This bone is also attached with two rows of parallel stitches.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image069.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image071.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image073.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image075.jpg" class="img-responsive center-block"></p>
<p><strong>We need to overlay the front princess seams with wide Rigilene bones.</strong> I push the top end of the bone underneath the horizontal bone going along the neckline.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2016_files/image077.jpg" class="img-responsive center-block"></p>
