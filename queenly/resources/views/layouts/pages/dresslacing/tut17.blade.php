<h2>Tutorial 17. Reinforcing the Waist with Additional Bones. </h2>
<p></p>
<p><strong>Another FAQ is �How to protect Rigilene bones from bending and breaking?�</strong> It concerns the waistline in particular and of course large-size dresses as such. </p>
<p><strong>The answer is obvious. You need to reinforce the Rigilene bones in the area of the waist.</strong> </p>
<p><strong>And that can be done with the help of plastic or metal spiral bones.</strong> I am not used to working with metal bones but I do use plastic boning very often. We need to somehow secure these plastic bones in the waistline area. </p>
<p><strong>First let�s make markings on the face side of the lining. </strong></p>
<p>I look to the notches to draw the waistline. </p>
<p><strong>Then I mark the area that needs to be reinforced.</strong> I draw a line 10-11cm below the waistline and another line just below the under-bust line (if you place these stiff bones right under the bust, they will dig into the body). In my case I've marked the latter 6cm above the waistline. </p>
<p><strong>Next I take bias tape of a matching colour and cut it into straps the length of which equals the length of the marked area plus the seam allowances.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I pin the straps right onto the vertical Rigilene bones on the face side of the lining. </strong>I also add a strap between the two central narrow Rigilene bones.</p>
<p><strong>As the result I get fabric tunnels:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to sew them on.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I attach each strap of fabric so as to make a 'pocket.�</strong> I stitch along both vertical edges and along the bottom edge and leave the top edge unstitched so I can insert a plastic bone there. </p>
<p><strong>Plastic bones inserted into these pockets will reinforce the Rigilene bones in the waist area and protect them from breaking.</strong> Of course this construction is rather stiff, which means the wearer will need to carry herself well without stooping or humping her back. Beauty comes at a price! </p>
<p><strong>I secure the pockets by machine-stitching. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Try to stitch along the very edge of the strap</strong> to leave enough room for inserting a bone. Check if you can easily do it after securing the first pocket. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>If it works, you can safely continue securing the remaining pockets. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2017_files/image018.jpg" class="img-responsive center-block"></p>

