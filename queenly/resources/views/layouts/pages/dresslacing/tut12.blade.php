<h2>Tutorial 12. Applying Final Touches to the Back of the Dress.</h2>
<h2>Setting Grommets.</h2>
<p></p>
<p><strong>I start joining the edges that I've pinned together. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I leave a small unstitched area at the top. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the corner of the seam allowance... </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>...and I turn it out.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The face looks nice and even when I zip it up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>It is not uncommon to add a small hook-and-eye closure above the zipper for additional safety.</strong> It reinforces that area of the back and protects the zipper from coming undone under tension. Remember to press all top edges afterwards.&nbsp; </p>
<p><strong>Let us join the lining with the face along the side down to the hip level.</strong> Make sure to carefully match their waistline, bust, and hip line notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch at a presser-foot distance from the edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p>You can finish the raw edges with the overlock machine. You can also do the same with the raw edges of the princess seams on the lining, at the level of the skirt. </p>
<p><strong>It is not typical to serge raw edges on the face of a garment</strong> because they might start pushing through. But our fabric is duplicated down to the very bottom so it's safe to do this. I don't think these serged seams are going to push through and show on the face side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>The central seam of the lining of the back is still open. </strong>&nbsp;</p>
<p><strong>I take the lining layers in my hands and align their bottom raw edges from the side of the skirt hem. </strong>&nbsp;&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the back of the dress around.</strong> The zipper tape extension is at the bottom.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I align the notches that mark the beginning of the slit...&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>...and I start sewing down the central seam moving up from the notch. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Please note </strong>that I leave 4-5cm unstitched at the end to make a corner on the lining for neater transition to the face. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>You should carefully press this unstitched corner later. </strong>You don�t want to put additional pressure on the central seam so the lining is supposed to stay detached in that area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I have serged the raw sides of our corset-based dress and the hem of its skirt.</strong> I leave the raw sides of the skirt open for now. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Next we need to hide any serger thread tails.</strong> You can either stitch them down (which I often do) or thread them into a thick needle with a large eye, pull it through the serging stitches and trim the tails. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image044.jpg" class="img-responsive center-block"></p>
<p>Let�s finish the back so we can move to the front half of the dress. We need to punch holes in the lacing bars. <strong>I measure the length of the lacing bar and divide it into equal sections so as to leave identical intervals between the holes.</strong> It's best if those intervals don�t exceed 4cm. I step 1cm inwards from the top and from the bottom of the lacing bar. The remaining length is 27cm. Thus in order to ensure proper tightening the intervals between the holes should be 3cm. <strong>I make corresponding markings.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I make holes with a hole-punch tool.</strong> If your hole cutting die has gone blunt, try placing a piece of cardboard underneath the lacing bar. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I have punched all holes in the first lacing bar. Now I take the other lacing bar, place it under the first one and mark the location of holes through the ones that I�ve already made.</strong> Now I know that the holes will be symmetric on both lacing bars. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I punch holes in the second lacing bar. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to install metal eyelets into the holes.</strong> You don't have to worry about the colour of the eyelets since the lacing area will be hidden inside. Mine are silver. Just make sure not to confuse the face of the lacing bar with its reverse side. Remember that the lacing will only be seen when the dress is unzipped. <strong>I insert the grommets and install them. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Here we go! All grommets are in place! When you unzip the dress, you see the face of the lacing bars.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2012_files/image056.jpg" class="img-responsive center-block"></p>

