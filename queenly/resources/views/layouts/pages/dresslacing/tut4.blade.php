<h2>Tutorial 4. Cutting the Dress. Duplicating the Lining. </h2>
<p></p>
<p><strong>Let us start cutting our pencil dress.</strong> As I have already mentioned, I have chosen rather thin fabric. Because of its properties the border between its batiste-duplicated and non-duplicated areas is going to be visible from the face. </p>
<p><strong>I have touched upon two ways of solving this problem. </strong></p>
<p>You can add another layer for the face of the dress - in other words, make it a double-layer face where the bottom layer is main fabric duplicated with batiste and the top layer is non-duplicated main fabric. </p>
<p>Or you can simply duplicate the entire sheet of fabric with iron-on batiste. </p>
<p><strong>I have gone the second way and prepared a piece of fabric that is 150cm wide and duplicated at the length of 93cm (the width of my iron-on batiste).</strong> The dress we are going to sew is not very long, it stops below the knee, so there are many reasons why it's easier to use fully duplicated face fabric. The dress will be more wrinkle-proof, it will be easier to handle and it will simply look better. Besides, it is also more cost-effective than adding a second layer. </p>
<p>I will use the same fabric for the lining which I�ll duplicate with iron-on batiste down to the hip level. </p>
<p><strong>I have made a fabric 'sandwich.'</strong> First iron-on batiste, then batiste-duplicated main fabric, then plain cr�pe - all this makes the lining.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have placed the iron-on batiste at the top to copy the contours of all pieces down to the hip line.</strong> I pin the pieces together, trim the excess of batiste along the hip line, and continue cutting. </p>
<p><strong>You might be wondering why I hadn�t duplicated the entire lining in advance just like the face fabric.</strong> </p>
<p>Of course I could have but it seemed unreasonable to spend so much batiste. I believe it will be more than enough if we duplicate it down to the hip line! Why didn�t I duplicate the narrow part down to the hip line?&nbsp; The answer is simple: I didn't want to make a mistake. I find it easier to first cut a piece out and then duplicate it. But if you feel confident about the exact location of the hip line, then you can surely duplicate both the lining and the face in advance and then cut them the same way you would cut a corset. </p>
<p><strong>I lay all pieces down and align them along the waistline so we can cut everything in one go without worrying about the skirt. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I trace the contours.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>You can pin your paper patterns onto fabric and cut along the perimeter. As for me I prefer marking the contours on fabric and then cutting after them. </p>
<p><strong>What concerns the princess seam at the back,</strong> you can either overlap the seam allowances of the side and the central piece or trim those seam allowances and put the pieces together with no gap between them. The princess seam won�t reach the very bottom but look like a plain dart then. It is possible to do this at the back of a straight-cut dress! This method wouldn�t work for a forefront though because this kind of overlapping takes up too much of the cup area.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I have decided not to risk it with a dart at the back but make the princess seam a smooth line.</strong> I have outlined all pieces, made the notches and pinned all layers together. I try to stick pins in a gliding motion to avoid deforming or shifting the layers of fabric. You can trim the top layer of batiste along the hip line afterwards. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I extend all lines downwards to make the skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The skirt is 63cm at the side, which is perfectly enough for a pencil dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I pin all layers together and cut the pieces out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make a notch to mark the end of the slit in the middle of the back.</strong> I have made it 26cm above the bottom edge but it is individual for every client. You could actually make a vent there but I'll confine myself to a regular slit. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>What we need to do now is cut the remaining cup pieces from stiff iron-on fabric.</strong> </p>
<p><strong>Please note the way this stiff iron-on fabric tends to fray.</strong> There are rather strong fibers that should be parallel to the slit. With that in mind I lay the piece down so as to have those strong fibers going horizontally. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the stiff iron-on fabric in half and put the central front piece onto it.</strong> You can trace its contour for the sake of convenience or pin it down and cut along the perimeter. It doesn't have to be 100% precise since we will polish the shape later anyway. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I outline the piece down to the under-bust level and draw a slightly convex cutting line at the bottom gradually moving 1cm upwards from the fold line.</strong> I cut the piece out. Later I will transfer the notches from the piece onto which it will be fused. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I cut iron-on duplicates for the side pieces of the front.</strong> I cut across the grain so that the stiffer fibers of the iron-on fabric go vertically from the top to the bottom of the piece. I make a notch under the bust from the side of the princess seam and another notch from the side of the side seam to mark the top of the dart. You can make this piece 1mm smaller straight away because my personal experience shows that iron-on pieces like this always come out larger than needed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I put the stiff iron-on pieces aside for now and start duplicating the lining.</strong></p>
<p>The face parts of the dress are fully ready and duplicated from top to bottom. The lining pieces haven't been duplicated yet. There are separate pieces cut from the main fabric and from the iron-on batiste. </p>
<p><strong>The side front piece:</strong></p>
<p><strong>I unpin the iron-on batiste without touching the pins on the skirt of the dress. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I carefully take the batiste off and flip the layers of the dress so that only the lining layer stays on the table. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I put a layer of batiste adhesive side down over that bottom lining layer of the side front piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>And I put the second batiste side piece on top of it, adhesive side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>And I top it up with the second lining piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I fuse the iron-on batiste onto the lining </strong>by pressing from each side and then between the layers. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></p>
<p>Once you have finished pressing, you can safely remove all pins and separate the side front pieces into the face and the lining. </p>
<p><strong>I finish pressing the face pieces cut from duplicated fabric.</strong> This will secure any poorly fused areas.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Now you can fuse stiff iron-on parts onto the lining.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image052.jpg" class="img-responsive center-block"></p>
<p>You can trim the seam allowances along the armscye and the top edge to make them less stiff. Don't be upset if your iron-on material does not hold well enough. Trust me it�s not the end of the world! Later you can simply secure the pieces by stitching along the perimeter. </p>
<p><strong>The central front piece: </strong></p>
<p><strong>I take the iron-on batiste off and remove all pins, too. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I take off the face layer and carefully unfold the lining.</strong> It's best to press the fold line flat again. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse iron-on batiste onto the lining. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I highly recommend that you remove any excess of main fabric at this stage.</strong> It is there because of the fold line. And although it makes no difference on the skirt, it is crucial in the upper part of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance at the top of the stiff iron-on piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>And I fuse it on using the under-bust notches as guides.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I finish pressing the face pieces cut from duplicated fabric.</strong> This will secure any poorly fused areas.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>The central back piece:</strong></p>
<p><strong>I unpin the iron-on batiste without touching the pins on the skirt of the dress. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn all layers back and place the batiste piece on the table adhesive side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I overlay the batiste with two layers of lining and then the second batiste piece adhesive side down.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>And I fuse the iron-on batiste onto the lining </strong>by pressing from each side and then between the layers.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image074.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image076.jpg" class="img-responsive center-block"></p>
<p>Next I remove all pins and separate the central back pieces into the face and the lining. </p>
<p><strong>I finish pressing the face pieces cut from duplicated fabric.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image078.jpg" class="img-responsive center-block"></strong></p>
<p>Next you perform the same steps with <strong>the side back piece</strong>:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%204_files/image080.jpg" class="img-responsive center-block"></p>
