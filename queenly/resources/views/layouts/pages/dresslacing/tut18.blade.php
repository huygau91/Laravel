<h2>Tutorial 18. Padding the Cups with Polyester.</h2>
<p></p>
<p><strong>I would like to add a small comment to the previous tutorial.</strong> If you urgently need some bias tape and happen to have none at hand or if you fail to find the right colour at stores, you can make it yourself with the help of a bias tape maker. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p>This tool is really handy and very easy to use. You cut a strap of fabric on the bias. It should be two times wider than you want your bias tape to be. Insert the end of the strap into the wide end of the tool. As you start pulling the tool down the rest of the strap, folded bias tape will start coming out from its narrow end. All you need to do is press it. I have two bias tape makers: for 18mm and 12mm wide bias tape. It's 12mm wide bias tape that I've just used for the tunnels. It was from a store but I would've simply made my own with the main fabric of the dress if I hadn�t found it in that beautiful red colour. </p>
<p><strong>Let�s continue working on the dress.</strong> </p>
<p><strong>We need to overlay the central bones and the princess seam bones with iron-on batiste.</strong> I place a pressing cushion underneath to protect the dress against deformation. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I start padding the cups with polyester.</strong> I take strips of it and pin them down. You can easily distribute padding polyester over the cup. It will overlay and disguise the bones so they don't push through the face of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the excess of polyester and add it in the areas that require some more. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Now I�ll secure the polyester by machine-stitching and attach loops for hanging the garment.</strong> </p>
<p><strong>I will stitch in the round along the entire perimeter of the cup.</strong> I stitch along the inner sides of the horizontal bones along the armscyes and along the neckline and along the outer sides of the vertical bones. I stitch the polyester down along the bottom edge following the stitches that we made to secure the stiff iron-on fabric in place. Those stitches mark the border of the cup very well.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size%202/transcript/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
