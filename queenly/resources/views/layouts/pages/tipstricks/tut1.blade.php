<p>In this video I would like to demonstrate you how to make push-up pads.</p>
<p>"How to create the push-up effect?" is probably the most frequently asked question that is addressed to me. This can be done very simply. I have some leftover padding polyester that I had used for covering the cups. I tear it, fluff it up so that it resembles cotton-wool, and shape it into two wads of the size I need depending on the measurements of my client and my own vision.</p>
<p>These two wads are of about the same size. I am also going to need some regular hook-and-loop tape that has two sides: the rougher hook side and the softer loop side. This sticky tape is 2-2.8 inch long.</p>
<p>I take some extra fabric left from the lining material and put a polyester wad on, then fold it into a triangle and secure it with pins. I am securing it with pins to prevent the polyester from moving inside there. Now let me see if I can make the second triangle out of this little corner.</p>
<p>Yes, it is quite enough. Again I secure it with a couple pins to fix the polyester. I trim the extra fabric and separate the two triangles. Now I should take a marker, a pencil or a chalk and mark it to ensure that the triangles are about the same. They are normally maximum 4 inch long and 2.8-3 inch wide.</p>
<p>Here are our two triangles. Now we need to stitch them out with a sewing machine.</p>
<p>You can mark your line precisely but I have been doing it by feel for a long time. I am pushing the polyester a little to even it out, and remove the pins. Now I�m repeating this procedure. And here we go: we have two semi-sphere soft pads. I trim them, sew overlock seams and start attaching the pads to their spot. I take the hook-and-loop tape - only one strap for now - and undo it. I place its rougher side at the pad and the softer side at about this spot of the cup.</p>
<p>But I am doing it a certain way in order not to miss the right spot. At first I glue the rougher side to the pad. I�m gluing it with Guterman fabric glue but you could also hand-stitch it on. Using fabric glue is a faster and easier way for me. So I glue it on and hold it for some time to have it set. Now I am covering it with the corresponding side of the tape and put some fabric glue on it as well. Then I simply insert it into the spot where the push-up is needed. I am pressing it firmly to help the glue set.</p>
<p>Now I do the same with the second pad. This time I won�t separate the two sides of the tape, I am gluing its rougher side straight away instead and sticking it to the pad. Then I�m going to glue the second pad to the second cup.</p>
<p>This hook-and-loop allows you to change the location of the push-up pads as well as their size. You can attach push-up pads of various size and volume using the same hook-and-loop inside the cup. For example you could make a pad of this size for the first fit test and during the second fit test you will make the final decision whether you should leave this push-up pad or make it bigger or smaller. The push-up is usually placed the way I�m showing you now, because the bust needs to be pushed up to the centre.</p>
<p>Here is what I�ve got. You don�t need to add anything, trim anything or put anything under the lining. It is neat and clean the way it is!</p>
<p>I�m going to say a couple more words about how to change the push-up if the paid turns out to be too large in volume. Let me draw our pad. Here is the overlock, and here is the sewn or glued on hook-and-loop strap. This here is the push-up part that lifts the bust.</p>
<p>If your pad is too big, you could machine-stitch it, quilt across the pad in this area. This way the pad will be flattened and you will have less of its volume just the way you need it. If it comes to the pinch and you feel that the pad is also generally too big in size, you can just cut a part of it off. Then you re-stitch it with an overlock seam.</p>
<p>You most likely won�t have to alter the attached hook-and-loop strap.</p>
<p>This shape of a push-up is all-purpose. It fits both full-breasted women who simply wish to adorn their d�collet� line and women with a small bust.</p>
<p>I would like to give you a piece of advice in conclusion: do not make your life more complicated! Some push-up pads of this shape sewn in this technique and attached the way I�ve shown you are really enough. It has been proved by my experience.</p>
