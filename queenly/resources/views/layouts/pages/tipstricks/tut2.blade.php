<p>In this video I would like to demonstrate you how to make detachable bars used for fit tests.</p>
<p>Everyone knows that a corset is hard to try on especially when the garment hasn�t been finished yet. The main reason here is the lack of lacing which is still missing that makes it hard to perform a very precise fit test. You need to check how tight the garment sits, how correct the tailoring and the pattern size are. It is nearly impossible to hold together the back of the corset with pins because they won�t be able to resist the tightness and will end up breaking. That is why I came to the idea of making temporary detachable bars.</p>
<p>The color of the bars makes no difference. When I�m making a fit test of a black dress using white, orange or red bars my clients often joke asking �Is it going to stay this way?" But you simply cannot do without such a simple unsophisticated tool!</p>
<p>How do I make these bars? I normally chose the most basic fabric; in this case it is crepe-satin. Then I tear off two straps of iron-on batiste and glue them onto the fabric - each strap is 4 inch wide and its length equals to the length of a ready bar.&nbsp; I prepare four wide Rigilene bones and tape their ends with some sticky tape.</p>
<p>And then I tear or cut off straps of glued fabric, bend them precisely in half and press their crease line from the face side. Afterwards I unfold each strap and sew on the first Rigilene bone with two parallel seams from the batiste side right along the central crease. Next I sew on the second parallel Rigilene bone.</p>
<p>This is what the piece looks like from the front.</p>
<p>Now I�m turning down the seam allowance from both sides and wrap the bar.</p>
<p>I make the first seam on the face side of the bar along the seam attaching the first Rigilene bone; and the second seam along the seam attaching the second Rigilene bone. The third seam is made at a presser-foot distance from the second Rigilene bone.</p>
<p>This is what the piece looks like from the inside, and that�s how - from the outside.</p>
<p>I am inserting a supporting plastic bone into the tunnel formed at the edge of the bar. The second edge of the bar is finished by an overlock seam.</p>
<p>I mark the spots where grommets are going to be punched through. I use this punching machine for inserting grommets. At first I punch the holes with a special attachment piece and then install the grommets themselves.</p>
<p>You don�t need more than 10 min for making this bar and it can serve you for years. The end supporting plastic bone is very important because it prevents the bar from getting deformed during the tightening process.</p>
<p>Now I want to share some secret with you: such detachable bars are sometimes used in finished corsets as well. Salons providing such garments for hire use this trick because they don�t want to have to re-tailor them each time.</p>
<p>I am going to make a schematic drawing of what they do. Here, I�m drawing the back of a garment, its curves and the second back. At these salons the edge of a corset is not finished with a lacing, it is left undone instead and sometimes even made soft. The size adjusted after the back of the corset can vary from 2 up to 4 different sizes at once. It depends only on the spot where the detachable lacing bars are going to be sewn. If you sew the bars close to the very edge you will need an additional plate. And if you sew them taking into account that the edges will overlap, the effect will resemble this very additional plate. Some salons use the following tricks: they sew detachable bars very accurately trying to avoid creases and achieve a nicely finished edge. They stitch the bars on at some distance from the edge. In this case when the corset is laced up and the edges overlap - there is no need for an extra plate. When you need to change the size of the corset you simply re-stitch the bars and add an extra plate. A very simple and cost-effective technique!</p>
<p>And now I would like to add a few words on how to make a fit test and where to sew the bars on. Please note that during the fit test any quilted lining is always a little larger than the finished garment with a covered front part. That�s why I sew on detachable bars taking into account the expected slight shrinkage of the finished garment. In this case I sew on each bar so that its edge should stay 1-1.2 inch from the edge of the middle of the back - slightly exceeding the seam allowance distance. I check how well the bars match during the fit test. If they match well by such sewing-on then the lacing bars are also going to match properly on the finished garment.</p>
<p>If the design requires that the lacing expands into a triangle, then I mark the location of the bar edges during the fit test in such a way that the back is open in a triangular shape. Then I make the necessary calculations and trim all extra fabric.</p>
<p>I recommend you taking pictures of your client from all angles during the fit test: front on, from the side and from the back. This will allow you to see all the failures that you might not have noticed during the fit test. Using these pictures you can also always imagine the proportions especially at the back and calculate precisely where the lacing should be.</p>
<p>If you are going to use hanging loops instead of grommets in your corset, then you could sew on a pencil-edge of a braid at the spots where the grommets would be located on the finished garment. Or it could also be hanging loops made of a round silk cord. This won�t change the function of the bar, it will simply change the way it looks. Such a bar with hanging loops will demonstrate it well how big the loops should be and at what distance they should be sewn.</p>

