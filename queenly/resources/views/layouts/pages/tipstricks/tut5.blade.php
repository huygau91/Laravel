<p>Today�s tutorial is somewhat unusual. You are used to getting thoroughly prepared materials from me, illustrated with pictures, but this time I have taken the courage to write a tutorial �in one go.�</p>
<p>And here is why: I received an E-mail with a request from my client and fellow seamstress in the morning. She says she has accepted her first order for a corset but there�s a challenge - it should have sleeves and a collar. She doesn�t know how to fulfill this.</p>
<p>I took it as an urgent call for help! But many nuances are hard to explain in writing - you need to demonstrate them and provide necessary comments. That�s how today�s tutorial came about.</p>
<p>Those of you who have never sewn a sleeve corset have no idea how many things you need to account for and how many hidden pitfalls are to overcome.</p>
<p>So, what do you start with? Surely, you start by taking measurements off your client.</p>
<p><em>Clip measurement</em> is compulsory and you should pay special attention to the distance between the most prominent point of the bust and the supposed scye.</p>
<p>Be very careful when taking the <em>bust middle measurement</em> in order to determine the location of the scye at the front.</p>
<p>The second clip measurement is required when you intend to make a fancy d�collet�.</p>
<p>The next essential measurements are the <em>width of shoulders, front and back</em>. I take the measuring tape and measure it starting from the top point where the shoulder connects to the arm. Then I do the same thing from the back.</p>
<p>The measuring tape passes in a semi-circle if your client has a stooping posture.</p>
<p>These two measurements indicate the degree of stoop.</p>
<p>Once I got simultaneous orders from mother and daughter. The width of the mother�s shoulders was 40cm at the front and 42cm at the back. The daughter, on the contrary, had 42cm at the front and 40cm at the back. That was a perfect demonstration of the degree of stoop and the direction of shoulders. By the way this measurement comes in handy when you work on regular classic clothing pieces, too. I really like it.</p>
<p><em>Bust height</em> is measured the same way as for conventional clothing.</p>
<p><em>Shoulder height at the back</em> is measured in a straight line from the waistline.</p>
<p>You will also need to measure <em>shoulder length</em> to help determine how wide the straps should be.</p>
<p><em>Scye circumference</em>: the client lifts her arm a little, bends it and puts her hand on her waist. And you measure the supposed scye with your tape (her underarms are basically its bottom border). We will talk about this measurement in detail later. For now you should just know that the measuring tape should not be loose when you�re taking this measurement. Simply follow the joint.</p>
<p><em>Upper arm circumference</em> is another necessary measurement.</p>
<p>Afterwards you should measure <em>elbow and wrist circumferences</em>.</p>
<p>And of course you need to know the <em>sleeve length</em>, too. I usually measure it together with <em>shoulder length</em> so as to preserve the point where the shoulder connects to the arm. This allows you to keep these two measurements as precise as possible.</p>
<p><em>Arm fullness at the top</em> is another essential measurement for a sleeve corset. I start at this point, move around the arm and bring the measuring tape to the back. This measurement helps you provide for additional space to let the arms move freely. A corset is a very stiff construction that fits tightly to the body. And your client will feel really uneasy if she can�t move her arms properly. You should always keep it in mind and leave some extra space for unobstructed movement. This measurement helps you determine the width of the sleeve top correctly.</p>
<p>It is very important to measure <em>back width</em> at the mid-scye level. You should imagine the location of both armholes and measure the distance in-between them.</p>
<p>You should also take the same measurement with your client�s arms down, i.e. where the scye begins going up. Please note that you shouldn�t measure it from side to side but precisely in the spot where the scye begins rising up.</p>
<p><em>Side seam length</em> and <em>scye depth</em> are also essential.</p>
<p>But first I shall make a short digression. Mind you, sleeves are a highly awkward and delicate detail of a corset. Most clients don�t understand that certain corset models or fabrics are simply incompatible with sleeves. And you need to know how to explain it well. There are times when you ought to talk the client out of it and convince her to replace the sleeves with a shrug or a bolero. I personally never accept an order if I question the final result. I simply turn it down when I know that the client�s wish is technically impossible to fulfill. I tell her I can�t sew something like that and she should address a more experienced seamstress who might be capable of bringing such wonderwork to life.</p>
<p>But let us return to the side seam. Imagine a very tight-fitting shirt with sleeves. And it is not a jersey shirt. What happens when you raise your arm? The entire shirt goes up. The sleeve pulls it up. And now imagine a corset - it�s all the same! The deeper the scye is, the more the sleeve will pull the corset upwards. But since corsets are stiff and tightly laced, the sleeve might simply tear. It�s even worse when the corset is not laced tightly enough! That makes the whole construction move around on the client�s body and she�ll find it the most uncomfortable clothing item she�s ever worn. That�s why the scye of a corset should be as small as possible! You need to find the golden mean so that it neither rubs against the skin nor feels too tight in the underarm area.</p>
<p>Now let�s discuss the sleeves themselves. What kind of material can be used for them? The best choice is super-stretch knit fabric used in swimwear. However it�s not always possible to match this kind of knit fabric to the colour and texture of the main corset fabric. You could use stretchy mesh fabric that is frequently used in dancewear, sports swimwear or figure-skating outfits. But it can also be hard to match it with the main fabric of an evening-fashion garment. It�s easy if your dress is white or black but much more challenging when you�re using fabric of other colours. Stretch lace is a good material for sleeves, especially if you use it for decoration as well. One-way stretch Lycra is another suitable option. This kind of fabric can be used both for sewing the corset and skirt and for the sleeves.</p>
<p>What to do if the fabric doesn�t stretch at all? There is only one way out - you need to cut the sleeves bias! I often use chiffon, satin or crepe for bias-cut sleeves. And sometimes I use taffeta although it is very thick and hardly stretches even along the bias. When working with taffeta I have to make balloon-sleeves or short peasant sleeves, or make a pleat at the sleeve cap. That�s just the only way to do it! Otherwise the woman won�t be able to move her arm by further than 10cm!</p>
<p>We shall return to the topic in the second part of the tutorial.</p>

