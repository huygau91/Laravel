<p>In this tutorial I would like to teach you how to create fan-folded drapes.</p>
<p>For this design it is enough to cut and dress only the lining material to make your corset light and comfortable to wear and avoid it being thick like a fur-coat. Afterwards the lining needs to be sewn as per usual, fully quilted with the boning, and the bones need to be covered with some soft iron-on batiste to prevent them from breaking through the drapery onto the front of the garment.</p>
<p>I am going to get ahead of myself and make some explanations. The seam allowances on the cups and along the top of the corset should be turned down the underside of the lining and stitched-on at a 1-2 mm distance from the edge. This way the seam allowances are secured and prevented from sticking out when the piece is getting put on the mannequin or dress-form. The seam allowance in the middle of the back piece where the lacing is to be located needs to be turned down the underside exactly the same way. I leave the seam allowances along the bottom of the corset unclosed and free.</p>
<p>Now let me return to the previous frame. Next I need to cover the whole corset with some paper-backed fusible web. For convenience�s sake I am cutting this paper in no particular form starting with wide straps and then trimming into some squares or triangles in order to get the whole corset papered. I wait for the fusible web to cool down and dry up, and then remove the paper. The whole corset is now covered with fusible web.</p>
<p>Then I take some fabric, preferably of the kind that can be stretched in one direction. I place it crosswise and pin the first fold to the cup. I always begin by draping the cup. I place the first pin in the middle, then stretch and arrange the fabric a little, and afterwards I add the second pin on the side and the third pin closer to the middle of the cup. Then I keep adding pins align the centre of the cup softly arranging the folds. You can first make folds on the left, then on the right or the other way round - as you wish. It is important to remember the sequence.</p>
<p>I drape the whole cup in such a motion, trim the extra fabric and move on to the draping of the second cup. The whole procedure is now repeated. At first I tuck the edge of the fabric, make a small fold, turn down by around 0.8 inches, place at the edge of the cup and secure it with pins. Then I make the second fold. And afterwards I start making many folds align the centre of the cup. The fabric seems to lie randomly and improperly but in fact I have already specified the direction of each fold. Arranging them is now pure delight! I pull the folds slightly controlling them with my fingers and the fabric gets so beautifully draped. I trim all extra fabric again and now the cup drapery is finished.</p>
<p>We need to attach a new piece of fabric to the existing one in order to continue draping. For this purpose I add a small strap of fusible web, let it cool down and remove the paper. Then I place the fabric to this spot, and create a fold exactly the same way as when draping the cup. If it is handier to make folds align centre first then follow this technique. You can start by making folds going from the centre because they are minor here and drift apart resembling a fan. You may do as you wish. But please remember that the fabric of which folds are made (namely the crease of the fold) coincides with the bias when you�re making irregular fan-folds. If the next fold gets deformed during its arrangement - you have to stop and trim a piece of fabric. I repeat the procedure, add a strap of fusible web, remove the paper and continue working.</p>
<p>Regardless of the spot I settle upon the side seam first, then turn the garment so that its back is facing me and drape the back. Pieces of fabric extend from the centre of the front to the centre of the back. I keep moving to the very end repeating this procedure.</p>
<p>It is no big deal if the fabric used for the drapery doesn�t stretch! You should simply trim the fabric more often and continue draping.</p>
<p>Here I have rushed a bit believing that this piece of fabric would be enough for the whole draping. I have stopped a little too late. Look how deformed the last fold is. I should have stopped earlier, somewhere here, trimmed the fabric and finished the draping with a new piece of fabric without being greedy. It is going to be very noticeable when the garment is ready.</p>
<p>I should also mention that the sample should be given real shape when put on the dress-form; this means I push some leftover padding polyester underneath the corset or use foam shoulder pads. Do the same in the bust area, in the cups. I create the volume for the corset as if it were papier-m�ch�. If the corset sits loose on the dress-form the draping will start stretching it, changing its size and form.</p>
<p>So I have completed draping the second side by arranging a neat fold in the middle. When the drapery is fully finished, all the folds should be fixed by stream-pressing, fused and given the final shape. After the sample has dried up and cooled down - take it off the dress-form.</p>
<p>Now we need to secure the folds along the edges as well. I am attaching the folds with the sewing machine in the middle of the diamond-shaped cut-out. For now I�m not paying attention to the drapery passing beyond the top of the corset. I am making a machine-stitch in the middle of the back and along the bottom line of the corset at a presser-foot distance from the stitched-on bone. This way I secure all folds in the middle of the back at the drapery level as well as along the bottom line of the corset. Now I trim all extra fabric. The back and bottom of the corset have already been secured and trimmed.</p>
<p>Now I am securing the folds along the top of the corset. I place the fabric under the presser-foot and stitch across the folds at a presser-foot distance securing them in this manner.&nbsp; Then I trim all extra fabric at a 0.4 inch distance from this tacking stitch. I have made a tacking stitch all along the corset line for better securing. Once all the tacking stitches have been made I place a bias tape on the first seam that was made at a presser-foot distance from the edge of the corset, and stitch-on. Afterwards I bend the drapery and the tape inwards and sew them on with an invisible stitch.</p>
<p>The same is done along the bottom line of the corset. I stitch some bias tape on to the tacking stitch; then bend inwards the whole lining together with the drapery and the tape, press, and fix with invisible stitches.</p>
<p>I repeat the same procedure with the drapery on the back. I cover all cut edges with the bias tape, bend inwards, pin it, and fix with invisible stitches. The edge of the garment is now very neat along the top, the lacing area and the bottom.</p>
<p>What shall we do with the centre of the corset? I take my scissors and cut off all extra fabric. I put the garment on the dress-form and can now decorate it however I desire. As an example I have pinned on a strand of pearls.</p>
<p>This is what the corset looks like from the back. The grommets will be located between the tacking stitches.</p>
<p>This is what the corset looks like front-on. It can be decorated with Swarovski crystals, pearls - whatever may come to your imagination.</p>
<p>Our tutorial is now over, I thank you for your attention!</p>

