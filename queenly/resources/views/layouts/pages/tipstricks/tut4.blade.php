<p>In this video I would like to explain you how to sew a shrug.</p>
<p>I am taking a ready pattern that has already been cut out and glued together. Let me remind you that there are 0.5 inch seam allowances along all the main cutting lines of the pattern. I am using capron mesh as fabric; this mesh is normally used for stitching embroidery because it stretches only in one direction - widthwise - and not lengthwise. This is not the veiling that is used for petticoat or bridal veil fabrication.&nbsp; This mesh doesn�t tear because it is much stronger than the veiling. So, I am folding the fabric four times as I�m going to cut four samples of each piece.</p>
<p>I try to spread the pattern on the fabric efficiently and then I secure it with a couple of pins. I trim the extra fabric and the cutting process begins. You can trace the pattern with a disappearing ink marking pen or you can cut it like I do simply by using the ready pattern. None of the three pieces - neither the front nor the back or the sleeve - have a crease. I pin the paper patterns off and put them aside. The cutting is now finished.</p>
<p>Now I need to join the shrug pieces together into pairs. I am joining along the top at the back, along the middle seam in the middle of the back and along the bottom of the piece. The seam allowance is the standard 0.5 inch one. I am trying not to turn the fabric while sewing but to go beyond the garment edge where possible. I am making bar-tacks at the intersection of the seams. If the fabric gets far too stretched under the stitch I put some paper underneath and pull the sewing slightly with the right hand.</p>
<p>I am stitching the foreparts together. I am seaming the foreparts together along the shoulder-cut and align the centre of the front leaving armholes and the side seam unclosed.</p>
<p>Now I need to seam together the sleeve along the bottom line. This is the rare case when I must turn the fabric around the needle.</p>
<p>I make a notch in the corner of the cut-out to make sure the piece can be easily turned out. And then I trim the seam allowance leaving around 0.2 inches.</p>
<p>I also trim the allowance of the forepart. I have decided to use a clean-finish seam at this rounded spot because it will make it easier to turn it out and press open. You could make a clean-finish seam along all the cut edges of the stitched segments. It would then be much easier to turn the piece out and press open all details. The further handling or the kind of lace or ribbons you are going to use for decoration is the deciding factor. If a very accurate even edge is needed then I sew this seam for a piped-like edge along all the stitched cut edges. I think it best to make this clean-finish seam only align the centre of the forepart, where it is rounded.</p>
<p>I am trimming the seam allowances on the back. Please note that since all the bar-tacks have been made at the intersection of the seams they stay put during the trimming of the allowances.</p>
<p>And then I am turning out all the seamed pieces - most carefully in the corners - and press open.</p>
<p>A shrug like this could be sewn from any fabric: organza, chiffon, or you could also use various nettings. You could also use opaque fabric. It all depends on the design and the desired result. Silk ribbons, Richelieu ribbons or welting can also be used for decoration.</p>
<p>I am taking some decorative lace. It is recommended to use the kind of lace with recurring motives and a band-like repeat, so that these motives could be cut out and arranged into a straight band. As I have already mentioned apart from lace you could also use various ribbons as well as cotton or silk Richelieu - anything that comes to your mind.</p>
<p>So I have cut out a strap of lace and now I start placing it on the shrug to see what location is the best and how to use it most efficiently. I spread the back pieces on the pressboard. The pattern has been designed in such a way that the distance between the pieces is 2 inches. The cut-out lace strap is going to be used for connecting purposes and for a finished look at the same time. I am placing the strap where needed and securing it with pins. Now I am trimming the extra. And here is our back piece.</p>
<p>I have cut out an identical lace strap and am now placing it simultaneously to the shoulder seam and the cut-out. &nbsp;I am looking for the best way to place the forepart so that the lace motives would later cover it as well. I am securing it with pins. Here is our semi-finished back piece now.</p>
<p>Now I�m checking the way the forepart should be placed and the way I will arrange the middle of the front piece.</p>
<p>I have decided to attach only some lace leaves on the sleeves because I find the flowers a little too large.</p>
<p>But I do want to place some lace flowers align the centre of the front piece.</p>
<p>Now you can attach all the lace motives in any convenient way. By tradition I have used some Guterman glue but you could also hand-stitch them on. I stitch with the machine at a 1mm distance from the edge of every piece in order to secure the lace motives. Now the lace is very tightly attached and you cannot see the stitch from the front at all. I do the same thing with the foreparts and the bottom part of the sleeve.</p>
<p>Now it�s time to place together the pieces of the back and front and join them along the side cuts. I am placing the pieces face-to-face superimposing the side cuts. I am bending the lace so that there is a 2 inch distance between the pieces. Let�s secure it with pins.</p>
<p>At this stage you can already make a fit test and check how smoothly the shoulder seams fit on a mannequin or on your client.</p>
<p>I am sewing together the side seams with a 0.2 inch allowance, and attaching the forepart pieces along the shoulder seam.</p>
<p>I am gluing on the floating lace with Guterman fabric glue. You could also secure it by hand-or machine-stitching.</p>
<p>Now I am sewing the sleeves.</p>
<p>If ravel-prone fabric has been used then you can finish all the unclosed cut-outs with an overlock seam at this stage. As for me I have trimmed all allowances up to 0.2-0.3 inches and pressed open the side seams and the seams of the sleeve. I haven�t used any overlock seam because my fabric is see-through and it�s enough to just have some neat see-through lines of the seam allowances.</p>
<p>And now I am fitting the sleeve into the armhole. I won�t describe this process in detail because there are plenty of special manuals on this topic. Besides the choice of a technique also depends on the fabric you are using. I am pinning the sleeve on carefully re-distributing the shrinking of the top and the sleeve cap. I am stitching with a 0.5 inch allowance trying to smooth out any creases and folds that could form on the cap because of the shrinking. Now the pins should be removed. I am making the second machine-stitch along the whole armhole for additional securing. The seam allowance is trimmed with around 0.2 inch left there.</p>
<p>What a marvelous shrug it is! Really quick and simple to make!</p>
<p>Thanks to the lace insets you can easily adjust the size, and there is also a very wide range of materials that could be used to create such a small pretty bolero.</p>
<p>This tutorial is now over, I thank you for your attention!</p>

