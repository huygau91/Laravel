<h2>Tutorial 1. Getting Acquainted with the Model. </h2>
<p></p>
<p><strong>I am about to begin working on a corset with separately cut 3-part cups that�ll be sewn in the see-through corset technique. This time I have a surprise for you: </strong>we are going to use fully opaque fabric for this corset. </p>
<p><strong>I have prepared strong cotton fabric </strong>to use as the base of our corset. The whole front and cups will be made of this fabric and it will also be used for finishing the lacing bars at the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>And I�m going to use this lovely chequered fabric for the sides of the corset front and the entire back</strong>. I will cut this fabric on the bias since I want the squares to be arranged diagonally on the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I will also use some very thin red cotton as lining fabric for certain pieces</strong>. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/tutorial%201_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s make a technical drawing of our corset model and set to work. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/tutorial%201_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>This is what our corset will look like. </strong>That�s a rough sketch. </p>
<p>I will stitch out embossed tunnels for plastic bones <strong>along the princess seams at the front of the corset. Its central piece will be made of one layer of red cotton fabric </strong>that is a little thicker than the one I�ve chosen for the lining. <strong>The chequered fabric will be arranged diagonally on the sides. </strong>I have prepared some thin red lining to prevent it from getting stretched. <strong>The top part of the cups will also be chequered. </strong></p>
<p>There will be a lacing area <strong>at the back </strong>and only one princess seam on each half with no visibly showing tunnels. The tunnels will stay on the inside. <strong>The whole back will be chequered apart from the lacing bars. </strong>They will be smooth and red, made of the same fabric as the corset front. The corset will be laced up with a ribbon. </p>
<p><strong>According to tradition I start working on the corset by sewing a mock-up. </strong>First of all, we want to test the pattern and see how it behaves in the sewing process: do all notches match? Do all cut edges coincide? It is also important to measure the mock-up so you can adjust the pattern after your client�s measurements later. </p>

