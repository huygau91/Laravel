<h2>Tutorial 13. Finishing the Top Edge of the Corset. Joining the Chequered Pieces with the Central Front Part.</h2>
<p></p>
<p><strong>I suggest we should finish the top edge of the corset </strong>before joining the chequered pieces with the central front part. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The top edge of a corset sewn in the see-through corset technique is always finished with the help of a narrow Rigilene bone. </strong>If the edge is somewhat curved, you need to gather and arch the bone. In our case the top edge is straight enough and it�s safe to use an ungathered bone. </p>
<p><strong>I take a narrow Rigilene bone, tape its ends with some masking tape, place it right at the edge and stitch it on along its inner side. </strong>I stretch the fabric underneath the bone to spread it out. Please note: just to spread it out, not deform it. I trim the bone leaving a small end (2cm). This end will later go inside the cup and ensure a smooth transition from the cup to the corset body. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I make another parallel stitch 1-2 fishing lines below the edge of the bone. </strong>Then I remove any bits that stick from behind the top border of the corset, i.e. the horizontal bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I tape the ends of all vertical bones with duct tape and secure them with another stitch. </strong>Experience shows that this stitch is compulsory. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can give the top edge of the corset a pencil-edge finish using our main fabric. </strong></p>
<p><strong>I recommend that you start sewing from the underside, the side of the lining. </strong>I place a fabric strap at the edge and make a stitch at a distance of the bone width. I trim the strap leaving a 2cm long tip. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to finish the edge.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>You can make a pencil-edge in a simple way: flip the fabric over the edge, carefully trim its end and secure it with a stitch from the underside. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>But you can make it look much cleaner. </strong></p>
<p><strong>First flip the strap backwards. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Reduce your stitch spacing and stitch the strap up at the end.</strong> Trim the seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Carefully turn it out. </strong>You can use a needle to turn out the corner. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the edge of the strap inwards and secure it all with a stitch. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The edge of our corset looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The same thing should be done with the top edge of the second chequered half of the corset. </strong></p>
<p><strong>Now that the top edges have been finished, we can start joining the central front part with the chequered pieces. </strong></p>
<p><strong>They are joined by princess seams. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Remember that the seam allowances should stay on the face side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowance directing it towards the centre of the front. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And I trim the seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I overlay the seam allowance with a fabric-encased wide Rigilene bone that I�ve prepared in advance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I return along the other side of the bone carefully spreading the fabric underneath it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>The first half of the corset body is ready: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2013_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I assemble the second half in the same manner</strong> directing the seam allowances towards the centre of the front. Make sure the bones are stitched on symmetrically - keep comparing the distance between them. </p>
