<h2>Tutorial 14. Sewing a Lining for the Cups. Quilting the Cups with Bones. </h2>
<p></p>
<p><strong>I suggest that we should start working on the cups by finishing the top edges of their chequered pieces. </strong>Looking ahead I can tell you that there�ll be some fullness along the top and the cups will be sewn a little differently than usual.&nbsp;&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I trim any seam allowances that might interfere and start finishing the edge using a straight fabric strap (not a bias tape). </strong>I have chosen the fabric used for the lining. I start sewing in a straight line and begin gathering the fabric a little after the first 3-4cm. We did the same kind of gathering on the cups when we were sewing a corset with integral halter-neck straps. Leave an ungathered area at the end of the piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The shape of the piece resembles a mushroom cap: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I make an additional stitch 1cm below the edge.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I�m going to bend the fabric over this stitch and secure it by pressing with the iron. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the red strap over and stitch through it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The top edge of the cup has a neat finish now: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>You should perform the same procedure with its twin piece. </strong></p>
<p><strong>And now let�s work on the lining pieces. </strong></p>
<p><strong>I join the cup pieces along their corresponding curve lines. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And I topstitch the seam allowance from both sides. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Remember to trim the seam allowances </strong>because our pieces are duplicated with hard iron-on fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I join the assembled pieces with the top part of the cup. </strong>Notch the seam allowance to help spread the seam smoothly. At first I secure the pieces with pins in the middle and at the corners and then redistribute the pins across the whole length. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>I join the pieces using a 1.2cm seam allowance and topstitch the seam allowances from both sides. </strong>This procedure is a good replacement of a pressing process. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image027.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the excess of fabric off all seam allowances. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p><strong>This is the lining of our cup: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stay-stitch along the perimeter of the cup. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image035.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>Then I gather a narrow Rigilene bone rather tightly to give it a rounded shape. </strong></p>
<p><strong>At first I�m going to lay a bone along the top edge of the cup. </strong>I trim unnecessary fishing lines, tape the end of the bone with some masking tape, and place it at about 0.5cm distance from the intersection of stay-stitches and 1mm below the upper stay-stitch. <strong>I gather the fabric slightly underneath the bone to ensure good fitting to the body. </strong>I stop at a 0.5cm distance from the opposite intersection of stay-stitches, trim the bone and tape its end. <strong>I make sure the bone stays parallel to the stay-stitch and 1mm below it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image039.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>It should look like this. </strong>Don�t over-gather it along the top edge! The cup should curl inwards only slightly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>I lay another gathered bone along the bottom rounded edge of the cup 1mm above the stay-stitch. </strong>I push the taped end of the bone underneath the top-edge bone that�s been stitched along one side. There should be no gathering on the top bar but you do need some fullness in the inward-looking area (area 1) to make the cup round, not flat. And I gather yet a little more along the outward-looking side of the cup (area 2) forming the required dome-shape. Then I trim the bone, tape its end and push it underneath the previous bone. I finish it off with a bar tack. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2014_files/image045.jpg" class="img-responsive center-block"></p>
