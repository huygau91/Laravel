<p><strong>I lay the first bone across the bottom part of the cup following the upper marked arched line i.e. without curving it too much. </strong>Please note where I trim the end to push it underneath the perpendicular bone. I never reach the outmost edge of the overlapping bone. I trim it at about its mid-level. This way the end of the bone I�m attaching meets no obstruction underneath the other bone and settles freely. </p>
