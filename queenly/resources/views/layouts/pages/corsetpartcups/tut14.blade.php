<p><strong>I start from the vertical princess seam. </strong>I�m not gathering this bone. Although the cup has been duplicated with hard iron-on fabric, you should be careful not to gather it underneath the bone. Push the bone away from yourself and pull the fabric on to you. Keep checking the shape with your fingers, it should be rounded. I use two stitch lines to attach the bone.</p>
