<h2>Tutorial 17. Pressing the Lining of the Cups. Pinning the Cups into the Body. </h2>
<p></p>
<p><strong>Now we need to press the cups we have just sewn. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>After the cups have been pressed, they should be covered with a layer of padding polyester. </strong>Padding polyester will give the cups the necessary volume and prevent the bones from protruding through the face fabric, especially if it�s thin. It�s easier to lay padding polyester in two goes: first you take one strap of it and pin it to the cup, then you do the same with another strap. Both cups should be covered with padding polyester. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I take two ribbons, each 25cm long, and pin one at the side of each cup to make hanging loops. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Next I stitch through the padding polyester catching the ribbon as well to secure it all. </strong>I follow the stitch along the inner side of the perimeter bone trying to get right into it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and secure the ribbons inside the cup to prevent them from showing when the garment is being worn. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the excess of padding polyester along the perimeter. Our cups are ready for getting sewn into the corset body. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I take one cup and start pinning it into the corresponding cut-out on the body. </strong> I have already trimmed all bone ends or anything that shows from behind the edge. </p>
<p><strong>I start by pinning the cup at the sides. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>I keep pinning the cup across its perimeter preserving the 1.2cm seam allowance in the cut-out area. </strong>I do it by touch: feel for the edge of the cup and see where it should be located to preserve the seam allowance. You should lay out the pieces in a way that will ease the task. I prefer putting the cup over my knee - it helps spread the fabric and I can see and feel it properly. Be ready to use quite a few pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>So I�ve inserted the first cup and I�m going to move on to the second one. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let me emphasize the importance of this moment! </strong>The cut-out area is located on a piece that was sewn on the bias and it might get stretched in the sewing process. You might notice it once you�ve started pinning in the cup and in that case you�ll have to somehow restore its original size without excessive gathering. <strong>Don�t forget that the look of your entire corset depends on this procedure! </strong>We have sewn the cups and the body separately and they look pretty that way but now we need to join them as nicely.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image037.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>I have pinned the cups into the body. Next I need to secure them with a machine stitch. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2017_files/image040.jpg" class="img-responsive center-block"></strong></p>

