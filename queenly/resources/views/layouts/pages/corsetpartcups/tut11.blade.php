<h2>Tutorial 11. Assembling Chequered Pieces in the Throw-Over Technique. </h2>
<p></p>
<p>I don�t grow tired of saying it: <strong>there are a number of various techniques that you can use and combine for sewing a corset. </strong>In the previous series of tutorials I showed you the throw-over technique and explained briefly how and for what purposes you can apply it. I find it practical for this corset as well and I�m going to demonstrate you why exactly. </p>
<p>If you can still remember, our corset pieces were cut on the bias. I want to use decorative fabric cut on the bias for the sides and back of the corset. This fabric is very similar to knitted fabric, i.e. it stretches in all directions. </p>
<p><strong>How can you use such fabric for a corset piece? </strong>That�s why we have prepared a lining that doesn�t stretch and is going to hold the shape of the piece. </p>
<p><strong>But how can you make the print pass beautifully from one piece onto the other when you join them? </strong>According to the see-through corset technique there is a bone sewn along each princess seam but these bones will break the print and the corset into separate pieces which contradicts the original idea. We tried so hard cutting the patterns, matching the squares to achieve a nice blending effect. Was it all done in vain then? Is the print going to be broken by straight lines when the corset is finished? What a shame! Nevertheless we are going to follow the see-through corset technique: we don�t want a different one. In other words, we don�t want to make this corset too stiff by fusing all its pieces with an additional iron-on material or have the bones on the inside. </p>
<p>Let us use the throw-over technique for joining the side front piece, the back and the pieces in-between them and then join these with the front of the corset following the traditional technique for see-through corsets. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p>As I have already mentioned, <strong>you need to pick a reference point </strong>if you want to use the throw-over technique, i.e. decide whether you want to move from the centre to the back or from the back to the centre. Your choice will determine the direction of all bones, or all seam allowances. It probably makes more sense to direct them all towards the back. </p>
<p><strong>We will start at the front piece </strong>and gradually move onto the back. After finishing the back we will finish the lacing area and then join the assembled pieces with the front. </p>
<p><strong>Let�s begin. I unpin the pieces and join them in pairs. </strong></p>
<p>See-through corsets are normally sewn from either double-faced or see-through fabric so you�re always running the same danger of confusing the pieces of the right half with those of the left half! That�s why you should carefully lay them out in pairs. </p>
<p><strong>I will join the lining and the chequered face of the side front piece along their vertical cuts or princess seams. </strong>I even out their edges accurately, match the notches, and make a joining seam from both sides at about a presser-foot distance from the edge. I have arranged the pieces in a vertically mirrored fashion to avoid confusion between right with left. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p>I spread the joined pieces making sure there are no tucks or bubbles. You can also iron them. Of course there are minor irregularities since each piece was cut out individually. <strong>So now I need to shape certain pieces using the lining as a reference. </strong>This procedure is a must, do not neglect it - accuracy is particularly important for the see-through corset technique. The pieces should be identical! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take the next set of pieces - the side pieces of the back that will be attached to the first set of prepared pieces. </strong>Carefully match the lining with the decorative face. Make sure not to confuse right and left. <strong>We are sewing the side seam. </strong>Lay the pieces correctly so that the lining matches the face perfectly. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>The first prepared piece in the sequence should be sewn in-between the two layers of the next piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>You can start from joining the chequered fabric </strong>to be able to flip it and check how well the squares match. <strong>I pin the pieces together and make a stitch line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I am going to join the pieces with one whole stitch line. </strong>First I arrange their layers in the right sequence. I even out the top and bottom lining pieces as properly as possible to avoid skewing. Then I make a joining stitch stepping 1.2cm from the edge (the width of a seam allowance). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove the pins, unfold the pieces and topstitch the seam allowance onto the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The stitch looks like this from the underside: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And that�s what it looks like on the face. </strong>I spread the seam and check if the squares match well. You can also check it earlier if you start by joining alone the chequered pieces. I trusted they would match well just like that. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance leaving 6-7mm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>You need to lay a wide Rigilene bone with a tunnel casing at this princess seam. </strong>Let�s measure its length using the traditional method. I place a bone over the princess seam, account for some extra millimeters, then double the length and trim it. I encase the bone into a prepared strap of fabric. </p>
<p><strong>I lay the encased bone over the seam allowance making sure the chequered piece can bend over it freely</strong> (the bone should neither lie too deep inwards nor too distant from the princess seam). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2011_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew it on following the joining seam. </strong>You should keep pulling the princess seam to yourself to spread it and prevent the fabric from gathering underneath the bone. </p>
