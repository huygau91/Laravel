<p><strong>And now it�s time to work on the lacing area in the centre of the back. </strong></p>
<p><strong>I stitch a wide Rigilene bone along the very edge of the back. </strong>The stitch goes along the inner side of the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>And I stitch another wide Rigilene bone parallel to the first one. </strong>It�s impossible to stitch them edge-to-edge with no gap in-between but try to put them as close to each other as you can. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I make another strengthening stitch through the second bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image066.jpg" class="img-responsive center-block"></p>
<p>You can also step a couple millimeters from the edge and secure the first bone with another strengthening stitch to catch the bottom layer of fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image068.jpg" class="img-responsive center-block"></p>
<p>I trim the edge of the fabric to prevent it from sticking out from behind the bone. </p>
<p><strong>The same procedure should be performed on the other half of the back. </strong></p>
<p><strong>Next I take the fabric straps I�ve prepared for our lacing bars. </strong></p>
<p>You can overlock <strong>the open edge of the wider side of the bar </strong>or finish it with lace, or you can use a strap of fabric to give it a pencil-edge finish which is what I�m going to do now. <strong>I wrap the edge in fabric: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image070.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image072.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I need to finish the edge of the back with this bar</strong>. Its wider part stays on the lining side and the narrow part is bent over the edge onto the face side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>I place the edge of the bone straight at the fold line and make a strengthening stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>You can make another stitch along the edge of the parallel bone - it�s important to secure the wider part of the lacing bar. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stitch at a distance of a seam allowance (1.2cm) from the edge so as to create a tunnel.</strong> We�ll insert a plastic bone there. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the bar with another parallel stitch from the face side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image082.jpg" class="img-responsive center-block"></p>
<p>I even out the bar using the bones as a reference and trim the excess of fabric. </p>
<p><strong>Now we need to lay a wide Rigilene bone at the edge of this bar. </strong>I mark the required length and <strong>encase the bone in fabric</strong>. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>I sew it on with two stitch lines overlaying the edge of the bar. </strong>With my finger I feel for the edge of the second parallel bone and try to lay this fabric-encased bone edge-to-edge to it. Remember to stitch at about 1mm distance from the edge of the bone - or else the tunnel might be too narrow. You will need to insert a plastic or a spiral bone in there later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image088.jpg" class="img-responsive center-block"></strong></p>
<p><strong>That�s what our garment looks like from the underside: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2012_files/image090.jpg" class="img-responsive center-block"></p>
<p></p>
