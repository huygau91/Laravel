<h2>Tutorial 5. Cutting One-Coloured Pieces and a Lining for Chequered Fabric.</h2>
<p></p>
<p><strong>I have prepared the fabric that I�m going to use for our corset. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you what we have here: </strong></p>
<p>- Fabric (1) is thin cotton fabric. It will be used as a lining for the cups and for chequered pieces; </p>
<p>- Fabric (2) is chequered fabric that we�ll cut on the bias; </p>
<p>- Fabric (3) is the main fabric. It is cotton fabric that doesn�t stretch in any direction. It is rather thick and has a repp weave. I will use it for the front of the corset and for finishing its edges and bones. </p>
<p><strong>I take my patterns. </strong>I put the cup pieces aside as well as those back pieces and front side pieces that will be made of chequered fabric with a thin lining. <strong>The central and intermediate front pieces will be made of the main fabric. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>I have already trimmed the bottom line of my corset the way I find appropriate (as sketched on the mock-up). You are provided both with the original pattern and the adjusted one so you can either re-model it yourself or use my newer version. </p>
<p><strong>The cutting process is the same as always</strong>, there are no special tricks here. I fold the fabric widthwise. My experience shows that it works better with today�s fabrics and is more practical. There are no alterations on these pieces so I trace them as they are. I even out the edges of fabric and start tracing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Don�t forget that the waistline should be parallel either to the crosswise or to the lengthwise grain depending on the layout. In my case it is parallel to the selvage. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the fabric with pins and cut out the pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>This is all that concerns this fabric for now. </strong>This kind of fabric is actually very good for sewing mock-ups (especially for beginners) since it is dense enough and doesn�t stretch. Putting the mock-up on the dress-form and spreading it will be easier. </p>
<p><strong>And now I�m going to work with just the lining fabric. </strong>I fold it widthwise and even out the edges. I need to cut out the side pieces of the front and back and the central back piece. None of these pieces have a fold line. </p>
<p><strong>Let�s refer to our calculations. </strong></p>
<p>As you can see, only the side seam needs to be altered. I need to reduce the side pieces of the front and back by 0.5cm and add 2mm in the princess seams at the back. </p>
<p><strong>I lay out my pattern pieces and trace the front side piece �as is�. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I need to shift the side seam 0.5cm inwards at the waistline. </strong>I make a mark there.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Now I match the notch on the pattern with the mark and draw a line going down. It�s important to mark a new bottom line, i.e. trace the bottom corner of the piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>Then I do the same with the upper half of the piece. </strong>Don�t forget to mark the top corner of the side edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image017.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>There are some alterations in the length of the side seam, too. </strong>I shift the piece left to remove 1cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image021.jpg" class="img-responsive center-block"></p>
<p><strong>Don�t touch the area where the cup is sewn in! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Next I perform the same procedure with the back side piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>I need to shift the side seam 0.5cm inwards at the waistline level. </strong>Please note the way the bottom line of the piece has changed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>The location of the side seam has changed noticeably both at the top and down the bottom. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>And now I�m going to remove 1cm downwards from his newly drawn line.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>I join the new top line with the top point of the back princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to shift the top of the back princess seam by 2mm starting at the under-bust level. </strong>I place the pattern at the point of intersection between the princess seam and the under-bust line and turn it around this point shifting the top of the seam 2mm upwards. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image035.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%205_files/image037.jpg" class="img-responsive center-block"></p>
<p>Then I trace the central back piece. And again, I place the pattern at the intersection of the princess seam and the under-bust line and turn it around shifting the top of the princess seam by 2mm. I draw a new line for it. </p>
