<h2>Tutorial 18. Securing the Cups by Machine-Stitching. </h2>
<h2>Pulling the Face over the Lining. </h2>
<p></p>
<p><strong>I start securing the first cup with a machine stitch. </strong></p>
<p><strong>I lay the stitch 1mm above the bottom edge of the cup </strong>basically repeating the stitch attaching its perimeter bone. <strong>Bar tacks at the beginning and the end of sewing are a must. </strong>The layers are thick so don�t rush - take your time and sew carefully. Breaking a needle while sewing at a fast pace is dangerous. It�s better if you help it pass through thicker areas by turning the flywheel manually. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and secure the ends of the bones that pass into the cup from the top edge of the side and middle front parts. </strong>I secure them with literally one stitch. These bones will support the cup and hold its shape in place. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I make another stitch that follows the second stitch attaching the perimeter bone of the cup. </strong>It will secure both the vertical bones that pass onto the inside of the cup and the seam allowances. It�s recommended that you make bar tacks at the beginning and the end, however they are arbitrary. This time it is not as crucial as it was for the first stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p>I check the quality of the stitch and trim any seam allowances or bone ends that stick out trying to leave the inside of the cups clean and free of excessive thickness. </p>
<p><strong>These are our cups sewn into the corset: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>We are going to cover these lining-fabric cups with their corresponding face pieces.</strong> </p>
<p>I am going to use a different covering method. It is the most convenient way to preserve all dimensions and particularly all decorative elements, and put the cups in place with their chequered patterns perfectly symmetrical. As usual, I offer the least time-consuming way possible. </p>
<p><strong>I place the face piece over the lining and secure it with pins. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p>The extra centimeter that we�ve accounted for in the cups has really come in handy because the fabric is pulled on rather rightly. <strong>This 1cm is just enough to allow me to bend the face fabric over the edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I pull up the top of the face piece and secure it with pins. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>You should perform the same procedure with the second cup. </strong>Try to keep the squares symmetrical. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Why haven�t I just sewn the lining and the face together and then turned it out as usual? </strong>Let me explain. First of all, because of the decorative chequered bar. Had I started doing it now, the width of the bars and their location would hardly coincide. I could probably achieve symmetry by pulling the fabric. But if there�s a safer way why not use it? Besides I have demonstrated a new technique to you. It is much better-suited when the cups have complex decorative elements like in our case and when symmetry is crucial to the look. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to secure the face part by machine-stitching. </strong></p>
<p><strong>I stitch 1mm above the bottom edge of the cup. </strong>This process is quite important. As you know, the look of your garment depends on the quality of this stitch. Don�t rush: the layers of fabric are rather thick. Help the machine by spinning the flywheel manually when needed. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></p>
