<h2>Tutorial 8. Cutting the Cups. </h2>
<p></p>
<p><strong>We have prepared new cup patterns in a matter of minutes. </strong>Now we can safely cut all pieces after these new cardboards knowing that they will match well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Remember to adjust the length of the central front piece after reducing the cup length by one centimeter. </strong>I remove this extra centimeter. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image003.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to prepare some iron-on batiste. </strong>Since two of our cup pieces have both a face and a lining, I am going to need four layers of batiste. </p>
<p><strong>What kind of fabric are we going to work with?</strong> Of course it�s the main fabric for the bottom cup pieces and the lining for all pieces. The chequered fabric will be used for the top bar of the cups so we�ll save it for later. </p>
<p><strong>I fuse the lining with the iron-on batiste, fold it in half, trace the patterns and secure them with pins. </strong>Then I cut out the top bar piece. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image005.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I fuse the main fabric with the batiste</strong>, then fold it in half, place the pinned bottom cup pieces over it, re-pin them to catch all layers of fabric, and cut them out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image007.jpg" class="img-responsive center-block"></p>
<p><strong>The top bar of the cup will be chequered. </strong>This piece is not large so I place it straight to fit more squares. I place the bar piece with the lining over two layers of chequered fabric with their squares matched, secure it all with pins, and cut it out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>I cut two bar pieces out of the batiste individually and fuse them with the chequered pieces. </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image011.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Our cup pieces are ready! </strong>&nbsp;I write matching numbers on the bottom cup pieces to mark the joining sequence because they are absolutely identical and it�s easy to get confused. All I need to do now is fuse them with hard iron-on fabric and mark the location of bones. And then the sewing process can finally begin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
