<h2>Tutorial 6. Cutting Chequered Pieces on the Bias. </h2>
<p></p>
<p><strong>And now it�s time to work with our pretty chequered fabric. </strong></p>
<p><strong>Luckily for me, my fabric is double-faced and both sides are identical. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I need to lay these two layers together very carefully and evenly. </strong>There are a number of methods used for cutting chequered fabric but the key point is always the same - you need to match the squares very accurately. </p>
<p><strong>In my case I can tell that the fabric is quite loosely woven and tends to shift from square to square. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image003.jpg" class="img-responsive center-block"></p>
<p><strong>So each time I�m just going to cut one layer first and then match the second layer to it</strong> because I�d have to spend much more time trying to match squares perfectly on a large sheet of fabric. </p>
<p><strong>I put the central back piece on the fabric, match it accurately with the diagonal line of the squares, and trace it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image005.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I cut it out very carefully. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image007.jpg" class="img-responsive center-block"></p>
<p>This cut out central back piece will be joined with the side back piece. <strong>Their edges are nearly identical. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>I place the cut out central back piece over the fabric so as to match its edges with the print. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>I place the original side back piece over it matching their edges. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image013.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trace the cut edge following the initial pattern piece. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image015.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I put the initial piece over the fabric again, match its edge with the line and trace the whole piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image017.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image019.jpg" class="img-responsive center-block"></p>
<p>However we have applied some alterations to the side seam and the princess seam of the actual side piece of the corset back. The same needs to be done on the corresponding chequered piece. <strong>I place the lining on top of it and trace it. </strong>It is pretty time-consuming so be patient and don�t miss anything crucial. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image021.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s see what comes out of it? </strong>We can still double-check everything and match the lines better since we haven�t cut it out yet. <strong>I pin the central piece to the outline of the side back piece matching their edges. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>It does look quite neat! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let�s cut out the side piece of the back. </strong>Don�t forget about all alterations we�ve had. </p>
<p><strong>And then I need to cut out the next piece - the side front piece. </strong>I check if the side seam that joins the front and back pieces is well-matched. It looks good. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put the cut out side piece of the back on the fabric and locate its side seam edge so as to match the print. </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I put the side front piece over it, match their side seam edges, and trace it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image033.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I remove the back piece, place the front piece over the fabric again matching its side seam edge with the drawn line and make a full outline of the side front piece. </strong>All alterations have been accounted for. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Before I start cutting this piece, let�s look at the way the squares are going to lie. </strong>It looks rather pretty. Let�s hope we will manage to join the pieces as successfully on the actual garment. <strong>I cut out the piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>As you must have guessed, I am going to use these pieces for cutting out their �twins� by placing each piece over the fabric and matching the squares as well as I can. </strong>I secure the pieces with pins and cut them out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>What I have as a result are the following patterns: </strong>the back and the side of the corset front made of chequered fabric, and their cotton lining base to prevent the chequered fabric from stretching; two front pieces made of the main fabric - the intermediate one and the central piece with a fold line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%206_files/image041.jpg" class="img-responsive center-block"></p>

