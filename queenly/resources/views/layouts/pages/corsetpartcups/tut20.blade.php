    <h2>Tutorial 20. Finishing the Bottom Edge of the Corset. </h2>
    <p></p>
    <p><strong>It�s time to finish the bottom edge of the corset. </strong></p>
    <p><strong>I take a narrow gathered Rigilene bone, tape its end and start sewing it on along the bottom edge of the corset. </strong>Before doing this you should make sure both halves of the back are identical in length. I place the bone stepping 0.5cm from the corner (see below). And again, similarly to the way we finished the top edge of the corset, you need to spread the fabric between the princess seams. Do not stretch it, simply spread it. I arrange the bone trying to repeat the bottom line. This stage is very important because you�re basically shaping the bottom edge and if you skew it - the whole corset will look skewed. So don�t rush: spread the fabric carefully with your fingers and when you finish attaching the bone, put the corset on the dress-form to see if it�s been done well enough. </p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
    <p></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
    <p><strong>You should neither stretch the bottom nor allow any gathering underneath the bone. </strong>If there is gathering, the bottom of the corset will inevitably go warped. That wouldn�t look good at all. <strong>I trim the bone </strong>and check if the bottom looks symmetrical. </p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
    <p><strong>Then I make another strengthening stitch across the bone </strong>between its first and second fishing lines. </p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
    <p><strong>And now another essential procedure: </strong>we need to even out the entire bottom line and trim any extra bone ends, threads or fabric - anything that sticks out from behind the bone. </p>
    <p><strong>Next I take some duct tape, tape the ends of the bones of all vertical princess seams (including those on the lacing bars) and secure them by machine-stitching. </strong>Then I flip the corset over and trim any duct tape that sticks out from the stitch to make sure the lining is neat. </p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
    <p><strong>I take a strap of the main red fabric </strong>that�s been used for the central part of the corset, place it at the edge from the underside and stitch across its whole length at a bone width distance from the edge. You should pull the strap slightly to help it settle evenly. This strap forms a nice even pencil-edge despite the fact that the bottom edge is curved. You don�t have to use a bias tape or elastic fabric. </p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
    <p><strong>We need to give the ends of our pencil-edge a neat finish. </strong>You can choose any method you like. There are simpler ways of finishing it off but I do the following: reduce my machine�s stitch spacing, stitch the strap up at the end, trim it, turn it out and secure it with a stitch.</p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
    <p><strong>I do the same thing with the other end of the strap so I don�t have to break the stitch line later. </strong></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>
    <p><strong>And I make a stitch line to secure our pencil-edge along the whole length. </strong></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></p>
    <p></p>
    <p><strong>Our corset is as good as finished. </strong>All we need now is add various decorative elements: grommets on the lacing bars, a modesty flap and a bow. </p>
    <p></p>
    <p>
    <img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>

