<h2>Tutorial 16. Assembling the Face of the Cups. </h2>
<p></p>
<p><strong>Let us join the face pieces of the cups.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>There is an important detail. </strong>If you can recall, our turquoise corset also had individually cut cups and we expanded its face by 1cm in the area where you insert them so as to be able to pull the face over the lining. Our current model doesn�t have a distinct arched insert area. <strong>So I suggest that we account for this additional space when sewing the joining seam between the top bar and the bottom of the cup. </strong>That�s to say I will use a 7mm seam allowance instead of the regular 1.2cm one. This way I will end up with extra 4-5mm from each side. </p>
<p><strong>I notch the seam allowance and pin together the top and the bottom of the cup. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>I join the parts using a 7mm seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and topstitch the seam allowance with decorative stitch lines the same way I did it in the bottom part of the cup. </strong>These stitches are not compulsory. You can simply press the seam allowance flat. But I prefer topstitching every seam this way. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image015.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>Here is our cup: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>The seam allowance will be bent over the top edge: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image021.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I need to make a stay-stitch to prepare the cup for getting sewn into the body.</strong> I make a stay stitch with a regular seam allowance (1.2cm). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>I�m going to gather the cup on this stay-stitch to give it a rounded shape. This will also make joining it with the lining easier. </strong>You don�t need to gather tightly. We will work on the shape with the press-iron later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew the second cup exactly the same way: </strong>I topstitch the seam allowance, trim it and join the piece with the upper chequered part. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stay-stitch and gather the cup to make it rounder: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_3/Tutorial%2016_files/image031.jpg" class="img-responsive center-block"></p>

