<h2>Tutorial 7. Applying Alterations to the Pattern. Part 2.</h2>
<p></p>
<p><strong>I re-draw the line and the corner. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And I shift the side seam outwards by 1.5cm.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I draw a dart that is about 0.8cm deep. </strong>It stops at the seam allowance or a couple millimeters from it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the side seam section from the bust line to the under-bust line using the pattern as a template.</strong>&nbsp; I must also account for the additional 2cm along the under-bust line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I continue re-drawing the side seam with the help of the pattern.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Here is the new side seam configuration: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I re-draw the corner at the top.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now let's work on the top edge of the central front piece.</strong> </p>
<p><strong>We know the desired height (point A).</strong> I connect that point with the princess seam and pass onto the other half of the front without interrupting the line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p>That top edge is placed too low for a strapless dress, in my opinion. On the other hand, you can leave it like that if your client is a very young woman, who is not worried about exposing her d�collet� zone. Although my client is young, I know that she doesn�t want to reveal too much. So I need to bring the top edge up to the initial point B. <strong>In other words, I shift it 1.4cm upwards. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I move the top edge of the side front piece upwards by the same 1.4cm.</strong> I shift the princess seam literally just 1-2mm outwards at the top to make sure the cups won�t be too tight. </p>
<p><strong>Here is the shape of the front of the corset:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>What alterations have we applied to the front part of the corset? </strong></p>
<p>We have made a slanted cut across the chest and lifted up the corner, to which the shoulder strap will be attached.&nbsp; We have re-drawn the armscye in accordance with the clip measurement. It is crucial to achieve perfect correspondence with the measurement there. The corner of the corset is more or less relative so we could afford some variations. We have transferred the bust-line notch and marked the dart. When the dart takes in the fabric, the length of the side seam will remain the same - identical to that of the side seam of the back. And, of course, we have prolonged the pieces down by 3.5cm. </p>
<p><strong>Let's move to the back of the garment.</strong> </p>
<p><strong>I trace the side piece of the back.</strong> </p>
<p><strong>I add 3.5cm down the bottom and bring the vertical lines down at a straight angle. </strong></p>
<p><strong>Let's work on the side seam of the back.</strong> It needs to be shifted outwards 1.5cm in the hips, 1.5cm in the stomach (13cm below the waistline), 0.5cm in the waist, 0.5cm under the bust, and 1.5cm in the bust. </p>
<p><strong>It's the same incline and the same configuration as for the side seam of the front.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the side seam and the top edge of the back with the help of the initial pattern - exactly the same way I did for the side seam of the front.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I alter the princess seam of the piece. </strong>&nbsp;</p>
<p>There are no alterations along the bust line. It needs to be shifted inwards 7.5mm under the bust, 3mm in the waist, 1mm in the stomach, and another 2.5mm in the hips. I re-draw the princess seam using the initial pattern as a template.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>It is compulsory to determine the location of your client's shoulder blades.</strong> You need to make sure the top edge of the side back piece is below her shoulder blades. According to my client's measurement, there is 21cm between her waistline and her shoulder blade line. I mark 21cm up from the corresponding point on the waistline.&nbsp; As you can see, the top edge of the back will definitely pass below the shoulder blades. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>There is another crucial moment I would like to draw your attention to.</strong> </p>
<p><strong>If your client has a noticeably bulky back</strong> and you're worried about the bulge effect, then you might have to use two shoulder straps. One of them can be a wide shoulder strap (the one we were going to make from the start) and the other can be a thin spaghetti strap that will simply support the corner at the back (on the side with the straight neckline). In this case, there will be corners for attaching shoulder straps from both sides of the dress. As I have already mentioned, it makes no sense to add a corner for the wide strap because it will compensate for it anyway. My client is a young woman and there's no need in using two shoulder straps in her case. </p>
<p><strong>Let me demonstrate how to construct the corner at the back.</strong> </p>
<p>I mark a 1.2cm seam allowance from the princess seam side. And I mark another 9.5cm inwards from the seam allowance. It�s the upper arm area that needs covering up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p>I mark a 1.2cm seam allowance from the other end of the 9.5cm section. <strong>In order to determine the location of the shoulder strap, I mark 3.5cm from the seam allowance line and then another 1.2cm seam allowance.</strong> The shoulder strap should be placed 27.5cm above the waistline (26cm plus a seam allowance). That's the location of the corner. I add another 1.2cm seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>And I draw the corner.</strong> You can place it a little higher, there's nothing wrong with that. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Later, after you construct the central part of the back,</strong> you will prolong it upwards the same way and bring the line to the centre at the required incline to achieve the right height of the piece. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Here is the new pattern of the side back piece with all alterations applied.</strong> We won't add the shoulder strap corner but keep its top edge straight.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The central back piece: </strong>I will only draw its edge because it doesn't fit onto my piece of paper. And then I will draw the entire piece on fabric. Let me show all alterations schematically. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>This princess seam needs to be changed the same way as the princess seam on the previous piece.</strong> </p>
<p>There are no alterations along the bust line. The princess seam needs to be shifted inwards 7.5mm under the bust, 3mm in the waist, 1mm in the stomach, and another 2.5mm in the hips. I re-draw it using the initial pattern as a template. <strong>The resulting pattern can be used for cutting.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>What concerns the side piece of the front,</strong> its top part will differ from this piece but the configuration of the side should be absolutely identical to that of the newly adjusted piece: the length, the dart - everything should be the same. I will then apply all alterations to the patterns and cut them out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%207_files/image048.jpg" class="img-responsive center-block"></p>

