<h2>Tutorial 6. Applying Alterations to the Pattern. Part 1.</h2>
<p></p>
<p><strong>Let me take a sheet of Whatman paper and draw a mock-up of what we are supposed to achieve. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>What is the main challenge of cutting a dress or any kind of asymmetric garment?</strong> The right and left halves of the garment are not identical, which is something you need to account for during the cutting. But I don't like cutting on the open and I don't recommend you do that either. This particularly concerns garments that go beyond the hip line, i.e. dresses. You could do it for a corset but cutting the central part of a dress on the open is really difficult. For that very reason I still cut it with a fold line but with the largest pieces in account. It's their contours that I follow. And then, after duplicating the piece, I trim the pattern and adjust it on the open. </p>
<p>It is a little easier in our case because we won't really cut the face part. We will only cut the lining and then arrange drapes right on the garment. The face part of our dress will be made of drapes and there will be separately cut skirts starting below the waistline and sewn to the base.</p>
<p>I step inwards from the edge by the width of the straight top edge of the central front piece and draw a fold line. <strong>Then I place the piece at the fold line and trace its contour.</strong> I make sure to leave some space at the bottom of the sheet because my client's hip height is 22cm whereas on the pattern is it 18.5cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I take the other half of the central front piece that features a shoulder strap corner, place it from the other side of the fold line, and trace it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Here is what the pattern looks like:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image008.jpg" class="img-responsive center-block"><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>Let me mark 22cm down from the waistline before I forget about it. </strong>It's 18.5cm on the pattern, which means I add another 3.5cm.&nbsp; I re-draw the hip line and prolong the vertical lines straight down. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>What needs to be altered in the central front piece? </strong></p>
<p><strong>Firstly, you need to move up to the level desired according to the dress model.</strong> </p>
<p><strong>Our clip measurement is 10cm plus the seam allowance of 1.2cm.</strong> I measure 11.2cm up from the bust line and mark a new top point. It's hard to say in what way other parameters of the clip measurement are going to change before we construct the pattern of the side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>Secondly, there are alterations in the princess seam of the front.</strong> I need to shift it inwards by 5mm in the waist, just about 1mm in the stomach (13cm below the waistline), and about 3mm in the hips (along the new hip line). I mark new points. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>And I re-draw the princess seam using the pattern as a template.</strong> First I re-draw the section that goes from the under-bust line to the new point marked on the waistline. Then I turn the pattern about that new waistline point and arrive at the point marked on the stomach line. And then I turn the pattern about the stomach point and draw the section down to the hip line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same procedures from the other side.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image019.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now let's work on the next piece - the side front piece. </strong></p>
<p><strong>I put down and trace the standard pattern.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image021.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I shift the bottom line 3.5cm down and bring the vertical lines to it at a straight angle.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>And I re-draw the princess seam using the pattern as a template. </strong>We need to shift it inwards by 5mm in the waist, 1mm in the stomach (13cm below the waistline), and 3mm in the hips. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>Let us mark the alterations of the side seam of the front.</strong> </p>
<p>The adjustment values recorded in the table are 3cm along the bust line, 2cm along the under-bust line, 2cm along the waistline, 3cm along the stomach line, and 3cm along the hip line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the standard pattern, align the bust lines, and shift it to the left by 3cm. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the top of the side front piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to alter the clip.</strong> I mark a seam allowance of 1.2cm along the bust line and find the point from which we applied alterations to this measurement. </p>
<p><strong>Clip toward the armscye:</strong> </p>
<p>I mark a 1.2cm seam allowance from the top edge of the piece and measure the distance to the apex of the bust. <strong>It is 15cm but my client's measurement is supposed to be 14cm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>This means I need to step 1cm inwards from that line.</strong> I add a 1.2cm seam allowance and mark it down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>Clip toward the corner:</strong> </p>
<p><strong>According to my client's measurement it is also supposed to be 14cm, i.e. 15.2cm including the seam allowance. </strong>I mark it down. The corner should be located somewhere here. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>Let's take the standard pattern and put it to the newly marked points. </strong>We�ll see what we get. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%206_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>We need to prolong the side seam upwards to preserve the shape of the armscye.</strong> It's why I highly recommend you should make an under-bust dart on large-size garments, equal to the value by which you prolong the side seam. This dart will ensure better fitting in the bust and help eliminate wrinkles on the finished garment. We don't need to worry about those wrinkles in this particular case since the top part of the dress will be draped. If you were sewing a clean smooth-surface dress though, it would naturally ask for this dart. This issue raises many debates. Some people don't like seeing a horizontal seam on the face of the dress. As for me, I see nothing wrong with it. A seam is definitely better than wrinkles. Experienced seamstresses, who have been dealing with corset-based garments for years, often re-distribute this dart from up to down. They manage to distribute the fabric smoothly but - mind you - only very skilled professionals, who are well acquainted with fabric properties, are capable of doing this. They just have a good feel of the fabric: they can tell how to re-arrange the face without making a dart in that place. I recommend all beginner seamstresses should simply use it. You might be able to do without it one day but first you need to become absolutely confident in working with fabric. </p>

