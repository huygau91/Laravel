<h2>Tutorial 9. Preparing for the Cutting Process. </h2>
<h2>Transferring the Pattern onto Batiste.</h2>
<p></p>
<p><strong>I have printed out the skirt patterns and arranged them in the same sequence in which they should be joined.</strong> You will find a separate file with very detailed instructions titled "How to Align the Patterns." Don�t be disturbed if your sheets differ by some 1mm. It wasn't easy to scan and put together such a large pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>As you can see below</strong>, I tried my best to align the squares on the paper. Don't worry about blank borders left by your printer. Don't trim them - simply get the squares aligned and everything will work out well.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>After gluing the pieces together you will get a large skirt pattern. </strong>It�s good enough for further work. </p>
<p><strong>I fold the pattern along the hip line marked by notches.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>We need this very part of the pattern, from the hip line and down.</strong> To put it more precisely, we only need the shape of the raw side edge. We basically won't use the rest of it. Now, when we start cutting the lining of our dress, you will see how to use this pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The patterns are ready and now we need to lay them out on our iron-on batiste.</strong> Let me remind you: since we're sewing a dress, it's impossible to first duplicate the fabric with batiste and then cut the pieces out. We'll have to transfer the patterns onto the iron-on batiste first and then cut them out, and arrange them on the main fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the batiste in half, adhesive side to adhesive side, and put it down.</strong> I arranged the pieces on the table in advance to check how much fabric I would need. We will also use stiff iron-on fabric for duplicating the cups but that is a story for later. As you know, we are not going to sew the face of the dress. The face will be made of drapes at the top and a semi-circle skirt at the bottom. The batiste will only be used for the lining. The face of the batiste is its non-adhesive side. I will keep all this in mind when laying out the pieces. I won't make any more changes; I simply want you to see how I'm going to outline them. </p>
<p><strong>I place the central front piece at the fold line. It's an asymmetric piece and I trace its wider contour.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next come the side front pieces.</strong> </p>
<p><strong>I trace the first side piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>And then I put the other side piece over it and trace the top edge.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I will cut the side pieces following the widest (merged) contour </strong>and then even each piece out correspondingly after separating the layers. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>The back pieces are symmetric. I trace the side piece and the central piece of the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let me remind you:</strong> there are 2cm seam allowances in the middle of the back, where the zipper and the lacing will be installed, as well as along the side edges and all side seams. The seam allowances at the top, along the entire neck hole, and along all princess seams are 1.2cm (0.5 inches) each. </p>
<p><strong>I will cut the stiff iron-on fabric later</strong>, after finalizing the shape of the cut-out pieces. </p>
<p><strong>Look what we need to do now.</strong> I will pin together the marked pieces - simply to prevent the layers of fabric from shifting. </p>
<p>Moreover, it is likely that <strong>we will have to re-draw the pattern of the back from the opposite side.</strong> And here is why! Since the dress will feature a bell-shaped skirt, it simply makes more sense to spare fabric by placing the pieces next to each other in a head-to-tail fashion. </p>
<p><strong>I have cut through the bottom line of the pieces and now I will cut the batiste up into two large pieces: the front and the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The flared part at the side of the dress is a prolongation of the side seam.</strong> It is best to place the pieces of the back next to each other, head to tail. They are flared at the bottom so it will make the lay less fabric-consuming. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>It's best to re-draw the back from the opposite side.</strong> I trace the pieces again. You can do it by holding the fabric against the light. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>The pattern of the back looks like this.</strong> I have transferred the pins onto this side to avoid confusion. It would be hard to get confused here though, to be honest. That's the most logical way to arrange the pieces on fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></p>

