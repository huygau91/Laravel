<h2>Tutorial 11. Duplicating the Lining.</h2>
<p></p>
<p><strong>It's time to duplicate the pieces of the dress.</strong> </p>
<p><strong>I take the pinned together part of the dress that is composed of two side front pieces.</strong> I remove the pins that secure the batiste. Then I take off both layers of batiste and put one of them adhesive side down on the fabric. Try to get the notches aligned well. First I carefully fuse the batiste onto the fabric with the press iron and then I start using a little steam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the lay around, bend back the top layer of fabric, and iron the duplicated piece from the other side.</strong> I put the top layer of fabric back in place.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I put the other batiste piece adhesive side down</strong> carefully aligning all raw edges and notches. Of course, it's impossible to align them perfectly. We have unpinned the pieces and shifted them against each other so it won't be perfect no matter how hard you try now. That's the disadvantage of this method but there is still no alternative to it. We could duplicate the entire fabric and cut out the pieces if we were working on a corset, but it doesn't work that way with a dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I duplicate the other side piece of the front just the same way.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>And I iron it from the other side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can adjust the shape of the duplicated side pieces of the front with the help of the patterns.</strong> Make sure not to mistake the underside for the face. </p>
<p><strong>I put down the corresponding pattern, mark all relevant adjustments, and even the piece out.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the lay around, put down the other side piece pattern, and even it out the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can duplicate the side pieces with stiff iron-on fabric.</strong> </p>
<p><strong>I place the straight edge of the stiff iron-on fabric between the notches of the dart and fuse it on with the press iron. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn it around, trim the excess of the stiff iron-on fabric, and iron it again.</strong> It is not that bad if your stiff iron-on fabric does not adhere very well. We will simply stitch it down along the perimeter. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same with the other side piece.</strong> Now we can remove the remaining pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And we can start working on the central front piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I fuse stiff iron-on fabric onto the central front piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And I duplicate the side back pieces with batiste.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>The other side back piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I iron it from the side of the fabric to secure the batiste.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2011_files/image056.jpg" class="img-responsive center-block"></p>
