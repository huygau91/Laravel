<h2>Tutorial 24. Cutting the Skirts. Preparing to Insert the Zipper. </h2>
<p></p>
<p><strong>I press open the side seams.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same with the central seam at the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The semi-circle skirts will be attached to the dress at the level of the notches. That area will also be overlaid by a draped waistband. There will be a zipper in the frame of the top semi-circle skirt. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then we will bend the edges back, punch holes, set in grommets, and finish the lacing area. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Let me explain you why we have cut the base of the dress in this particular fashion.</strong> </p>
<p><strong>Although the base of the dress will be hidden behind the semi-circle skirts, the client will still feel more confident in it.</strong> She will feel the dress cling to her body making her silhouette more slender, and the flowing skirts will fall down beautifully. I have made the skirt flare starting from the hip to make the wearer's silhouette more graceful. It improves the fit of the dress! </p>
<p><strong>A slim-fit base like this inside any kind of a dress makes the wearer feel confident that her dress won't "slip off."</strong> The dress is very well secured around the waist and the hips. It's a truly all-around beneficial cutting method! The slim fit will not obstruct the wearer's movements because it is hidden inside and even if it does wrinkle - the skirts at the top will disguise it. And the woman will feel a lot more graceful. This fact has been proven by years of test and trial. </p>
<p><strong>You could even carry out an experiment.</strong> Just let your client try the dress on without the slim-fit lining base, i.e. only the skirts. She will be instinctively trying to narrow it down herself while standing before the mirror. But when she feels the base of the dress cling to her body, she will feel confident and won't try to change anything. </p>
<p><strong>Now we need to cut two semi-circle skirts: one will be sewn with our main fabric and the other with knitted fabric with a large floral print. </strong></p>
<p><strong>I hope you can still remember how to calculate fabric consumption for a semi-circle skirt.</strong> </p>
<p>I have measured the semi-circumference of the dress in the area where the skirt will be attached. It was about 4cm below the waistline. The semi-circumference is 80cm, wherefore: </p>
<p><strong>πR = 80cm</strong></p>
<p>Let's find the radius: </p>
<p><strong>R = 25cm</strong></p>
<p><strong>π is 3.14; 3.14xR=80; R=25; Skirt length: 112cm; 112+25=137; Fabric consumption: 137x2=274cm or 2.74m</strong></p>
<p></p>
<p><strong>And now let's calculate the length of the skirt.</strong> </p>
<p>I have a measurement that equals 115cm. But since I go 4-5cm below the waistline, it should actually be 110-111cm. And I need to add some more fabric for the seam allowances and for the hem. </p>
<p><strong>Let us take the length of 112cm for the skirt. </strong></p>
<p>The amount of fabric required in this case is as follows:</p>
<p><strong>112cm+ 25cm= 137cm</strong></p>
<p><strong>137cm х 2 = 274cm or 2.74m</strong></p>
<p><strong>Of course, I'll provide for a couple of extra centimeters so let it be 2.8m. </strong>We will need just as much fabric for cutting the floral skirt.<strong>&nbsp;&nbsp; </strong></p>
<p><strong>Now let's prepare the fabric for cutting.</strong> I fold it in half, align the selvages, and find the fold line.&nbsp; Then I put the selvages together with the fold line so as to form a kind of a cone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I fold it in half one more time </strong>so as to make a triangle. I secure it with pins. <strong>With my method, it is usually enough to have a pressing board at hand to cut a semi-circle skirt or a full sweep skirt.</strong> The most important thing is to lay it out neatly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I mark a radius of 25cm from the top point of the triangle.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>And I cut through the line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to mark the length of the skirt on the triangle.</strong> We agreed upon the length of 112cm. I mark it down, draw an arched line, and cut it. That's all about cutting the semi-circle skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And then you cut an identical skirt from the knitted fabric. </strong></p>
<p><strong>In order to install a blind zipper into knitted fabric, it's best to duplicate the zip-up area with iron-on fabric.</strong> I use iron-on batiste for all similar occasions. You could also use fusible web or interfacing fabric. It will be much easier to install the zipper into duplicated fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2024_files/image036.jpg" class="img-responsive center-block"></p>
