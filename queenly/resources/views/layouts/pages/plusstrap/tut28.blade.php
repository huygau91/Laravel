<h2>Tutorial 28. Finishing the Lacing Area. </h2>
<p></p>
<p><strong>You should stitch down the edges of the waistband, trim them, and serge them from both sides.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And then you will fold the serged edges inwards at the level of the lacing. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>We need to add the bones.</strong> I take a narrow Rigilene bone and tape its end. I sew it down with a row of parallel stitches along each side, from the zipper stop and up to the top. I leave a 2cm seam allowance so I can fold the edge in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image020.jpg" class="img-responsive center-block"></p>
<p>The lacing area will be thick enough at the level of the waistband after you fold the edges in. It'll be perfect for installing grommets. There are fewer layers of fabric above the waistband though, which means the lacing area won't be as thick there. </p>
<p><strong>With that in mind, I place another narrow Rigilene bone along the very edge above the waistband.</strong> It will help secure the eyelets in place. I sew it on with two rows of parallel stitches just like the previous one.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the edge in and make a row of strengthening stitches.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I make another row of strengthening stitches along the edge of the seam allowance to make sure the drapes won't plunge out from under the eyelets.</strong>&nbsp; I fix the seam allowance and the drapes in place simultaneously to secure the eyelets. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>We can insert a plastic bone into the tunnel that has formed there.</strong> This bone will come in handy because our corset is rather large, my client's bust is heavy, and the lacing bar won't be robust enough with just one Rigilene bone there. We can insert it at the very last moment with eyelets already set in.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same with the other half of the lacing.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>The back of the dress is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to install eyelets into the lacing bars.</strong> </p>
<p>I have marked their placement with 3cm intervals. </p>
<p><strong>I punch holes in correspondence with the marks. </strong>Here is a good tip: if your hole-cutter has gone blunt, place a piece of hard cardboard underneath the lacing bar. It will work great.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I punch holes in the other lacing bar.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image052.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I take off the hole-cutter and put on the setting die. I insert eyelets into the holes, take matching washers, and start setting them in.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we can insert those stiff bones into the tunnels.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>The edges are hard and robust. The corset will hold very well when laced up.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2028_files/image060.jpg" class="img-responsive center-block"></p>

