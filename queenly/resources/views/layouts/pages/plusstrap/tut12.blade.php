<h2>Tutorial 12. Marking the Bones.</h2>
<p></p>
<p><strong>Let us think over the necessity of placing certain bones before marking their location.</strong> </p>
<p><strong>Whenever you're sewing a garment for a full-breasted client, you need to think</strong> whether you have to lift the breasts, give them more support, or make them appear smaller and more natural. The placement of bones depends on what you want to achieve. It is generally recommended that you use as few bones as possible on a garment for a full-breasted woman since her bust will naturally settle in the corset in a beautiful way and won't call for any particular re-shaping. When the breasts are small, then you want to invent something to make them appear fuller and rounder. But women, whom Mother Nature gave such a great gift, don't need any special tricks. It's enough to use a beautiful quality pattern that forms the shape of the garment. It may seem like there is no need in using any bones at all, but our dress model feature small drapes going all over the cups and passing onto the back. </p>
<p><strong>And that's why we need the bones: not to support the bust, but to secure the drapes in place.</strong> Trust me: Rigilene bones will not be able to hold heavy breasts in place! Don't even bother trying to use them for that purpose. Rigilene bones are easily deformed even by the warmth of a human body. They can tell you whatever they want about spiral bones, yet they are just as fragile. They can only provide support if you squeeze the chest tight, which will make the d�collet� zone look really vulgar. We prefer natural looks and natural methods so we will only sew bones onto the cups for the purpose of securing the drapery. There is no need in lifting the breasts artificially. </p>
<p><strong>We most likely won't need to make round cups. We'll take this into account when marking the bones.</strong></p>
<p><strong>Let us begin with the central front piece.</strong></p>
<p>The first horizontal <strong>line (1) </strong>connects the bust-line notches. </p>
<p>There should be a vertical <strong>line (2)</strong> in the centre. I make a notch there. How low should this bone reach? According to my client's measurement, the maximum length of the corset can be 18cm (that is, from the waistline to about the hip joint. I don't think it needs to be that long. 14-15cm will be enough for a good fit in the hips. That's why I mark 15cm down from the waistline. That's where the vertical bone is supposed to end. </p>
<p>I mark 3cm left and right of the central line<strong> (3 and 4)</strong>. These will be the intermediate bones. I will make them 1cm shorter than the central one. </p>
<p>The princess seam bones <strong>(5 and 6)</strong> will reach down to the stomach line 13cm below the waistline. </p>
<p><strong>Now let's move on to the cups.</strong> </p>
<p><strong>I suggest we should place bones the same way we would do it on a one-piece corset</strong>, i.e. without making round embossed cups. The cups will be round and pretty anyway thanks to the shape of the pattern. And my client's bust does not need to be "made bigger." On the contrary, she would even like to make it appear a little smaller. </p>
<p>That's why I suggest <strong>we reinforce the cups with vertical bones</strong> placed with a 3cm gap between <strong>(7 and 8)</strong>. </p>
<p>I place horizontal bones 5cm above the bust line <strong>(9)</strong> and 4cm below it <strong>(10)</strong> in order to reinforce the top and the bottom of the cup and secure the drapes there. </p>
<p>It is compulsory to place a bone along the top edge of the cup, 1.2cm below it <strong>(11)</strong>.</p>
<p><strong>We won't place a bone under the bust.</strong> There is no need in breaking the corset and limiting it with a bone under the bust.</p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The first side front piece:</strong></p>
<p><strong>I place a vertical bone in the middle of the piece (12) and two parallel bones (13 and 14)</strong> 3cm left and right of it. These bones go 13cm below the waistline to allow sufficient freedom of movement. </p>
<p><strong>Bones 1, 9, 10, and 11</strong> will pass from the central front piece. I will bring them to<strong> bone 14</strong>. </p>
<p><strong>There will be a bone going along the princess seam (6). </strong></p>
<p><strong>And there will be no bone under the bust. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The second side front piece:</strong></p>
<p><strong>I place a vertical bone in the middle of the piece (15) and two parallel bones (16 and 17)</strong> 3cm left and right of it. These bones go 13cm below the waistline to allow sufficient freedom of movement. </p>
<p><strong>Bones 1, 9, 10, and 11</strong> will pass from the central front piece. I will bring them to<strong> bone 16</strong>. </p>
<p><strong>There will be a bone going along the princess seam (5).</strong></p>
<p><strong>And there will be no bone under the bust. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>That's the placement of bones at the front of the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The central back pieces:</strong></p>
<p>There will be bones of the lacing bar from one side of the piece and a princess seam bone from the other side of it.</p>
<p><strong>The piece is rather narrow and I don't see any need in using any more additional bones. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The side back pieces:</strong></p>
<p>I draw the waistline. I divide the piece in three equal parts along the waistline and draw two lines <strong>(18 and 19)</strong> for placing vertical bones. It's important to reinforce the side piece of the back very well. These bones go 15cm below the waistline. </p>
<p>A bone along the princess seam is compulsory <strong>(20)</strong>.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
