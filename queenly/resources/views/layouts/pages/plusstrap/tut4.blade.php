<h2>Tutorial 4. Calculating Pattern Alterations. </h2>
<h2>Part 2. Adjusting the Shape of Front and Back Princess Seams.</h2>
<p></p>
<p><strong>How do we use front and back princess seams in our calculations? </strong></p>
<p>We can alter the <strong>front princess seam</strong> starting from the waistline and down. We can reduce or increase the depth of the princess seam dart in the waist, in the stomach and in the hips. </p>
<p>We can use the <strong>back princess seam</strong> at its entire length, i.e. alter the entire dart from top to bottom: in the bust, under the bust, in the waist, stomach and hips. It won't affect the configuration of the pattern - on the contrary, the back princess seams will be of great help. </p>
<p><strong>Make sure to take pictures of your client from the front, side and back.</strong> At this stage you can look at a picture of your client and study the shape of her back and her stomach. It will be particularly obvious in the side view. Many people ask me how to prevent the back from bulging over the top edge of the corset.&nbsp; The answer is simple: alter the princess seams correctly. If your client's back is bulky, then you need to make sure the back of the corset is not going to dig into the body. Give it a slightly looser fit. The back bulge problem won't arise as long as the garment doesn't dig into the body. This regards the question of altering the princess seams at the back. If your client has clearly protruding shoulder blades and the bulging effect happens in that area, solve it in the same manner. Do not place the top edge of the corset right in the middle of the shoulder blades or make it overlay the shoulder blades by 2-3cm. There are two possible ways: either you place it right under the shoulder blades or even 1cm below them (i.e. your client's shoulder blades will be exposed). Or you place it well above the shoulder blades and use a slightly looser fit to provide for better freedom of movement to avoid the back bulge. Pictures of your client will help you!</p>
<p><strong>Let us move on to our calculations.</strong>&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>I erase all calculations of the front and back side seam configurations (columns 4 and 6) to make things less confusing. Right now we need to take into account only the values that refer to client's measurements, pattern measurements and <strong>adjusted side seams</strong> of the front and the back. </p>
<p><strong>Bust circumference: </strong>It is 102cm on the client and 93cm on the pattern.</p>
<p>We have expanded the pattern by 3cm from each side (a total of 6cm) and by 1.5cm from each side (a total of 3cm), which makes 9cm together. </p>
<p><strong>93cm+ 9cm= 102cm</strong></p>
<p><strong>102cm - 102cm = 0cm</strong></p>
<p>We have already reached the client's measurements.&nbsp; <strong>Nothing needs to be added in the princess seam of the back so I write a zero in column 8. And we can't add anything in the princess seam of the front so I write a dash in column 9.</strong>&nbsp; </p>
<p><strong>Under-bust circumference, </strong>It is 82cm on the client and 80cm on the pattern.</p>
<p>We have expanded the pattern by 2cm from each side (a total of 4cm) and by 0.5cm from each side (a total of 1cm), which makes 5cm together. </p>
<p><strong>80cm+ 5cm= 85cm</strong></p>
<p><strong>82cm - 85cm = -3cm.</strong></p>
<p>There are 3 extra centimeters that need to be removed from the pattern. <strong>We are only working with the back princess seams for now.</strong> There are two princess seams at the back, left and right, and each princess seam is made of two raw edges sewn together. <strong>Therefore, there are four raw edges at our disposal.</strong> We need to divide 3cm by four. In other words, <strong>we shift each raw edge of each back princess seam 0.75cm inwards at the under-bust level</strong>. </p>
<p><strong>Imagine the way it will look on the pattern of the back.</strong> We are not removing anything in the bust, only at the under-bust level. What happens with the back of the corset then? It doesn't cling as tightly to the body. <strong>And it's exactly what we want! </strong>It's what we have been trying to achieve! We don't want the back of the corset to bite into the client's back. When we deepen the dart at the back, its top edge gets automatically pulled away from the body. This very trick is going to help us achieve proper fitting of the garment from the back. It comes naturally! We didn't take any additional measures. The values provided us with all necessary information.</p>
<p><strong>Waist circumference: </strong>It is 75cm on the client and 75cm on the pattern.</p>
<p>We have expanded the pattern by 2cm from each side (4cm) and by 0.5cm from each side (1cm), which makes 5cm total. </p>
<p><strong>75cm+ 5cm= 80cm</strong></p>
<p><strong>75cm - 80cm = -5cm.</strong></p>
<p>There are 5 extra centimeters that need to be removed from the pattern.&nbsp; Let us do the following: <strong>we will use the back princess seams to remove 3cm in the waist (0.75cm from each side) and use the front princess seams to remove the remaining 2cm (0.5cm from each side).&nbsp;&nbsp; </strong></p>
<p><strong>Stomach circumference: </strong>It is 103cm on the client and 95cm on the pattern.</p>
<p>We have expanded the pattern by 3cm from each side (6cm) and by 1.5cm from each side (3cm), which makes 9cm total. </p>
<p><strong>95cm+ 9cm= 104cm</strong></p>
<p><strong>103cm - 104cm = -1cm</strong></p>
<p>There is 1 extra centimeter that needs to be removed from the pattern.&nbsp; Let us do the following: distribute it between all princess seams, both at the front and at the back. In other word, divide it by eight. <strong>It means we need to deepen each princess seam in the stomach by about 0.1cm.&nbsp; </strong></p>
<p><strong>Hip circumference: </strong>It is 110cm on the client and 103cm on the pattern.</p>
<p>We have expanded the pattern by 3cm from each side (6cm) and by 1.5cm from each side (3cm), which makes 9cm total. </p>
<p><strong>103cm+ 9cm= 112cm</strong></p>
<p><strong>110cm - 112cm = -2cm.</strong></p>
<p>There are two extra centimeters that need to be removed from the pattern. This 2cm will also be distributed between eight raw edges of all princess seams. <strong>We need to deepen each princess seam by 0.25cm in the hips. </strong></p>
<p><strong>We have calculated the entire table. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Let me highlight the most important points once more: how do we find the value for altering the side seam of the front?</strong> </p>
<p><strong>We look at three measurements: bust front, under-bust front, and clip.</strong> These measurements show how much you need to shift the pattern against the standard.&nbsp; As far to the side you shift it, as much you expand it along the side seam - as much, or slightly less, the clip will change.&nbsp; And then you know your clip measurements, you know that you need to expand the front by 2.5cm from one side and 1.5cm from the other. Simply think it over. If there's a noticeable adjustment along the bust line (in my case it is 3.5cm), guide yourself after it. Make sure to maintain a beautiful armscye line. That's all there is. </p>
<p>You are done with the calculations and you start cutting it. Everything may look lovely on paper but suppose you forgot to take something into account and started laying the pieces out on fabric. Always test it on paper if you don't feel confident enough! Just to be sure! You trace the side piece, shift it by the value you found appropriate and look at the clip. <strong>If the armscye line looks graceful - you have done everything right!</strong> If it doesn't look graceful, re-calculate it. </p>
<p><strong>Another essential measurement is bust middle.</strong> Luckily for me, it coincides with the standard pattern. <strong>NB: take the bust middle measurement off your client based on where you <em>want</em> to place the chest princess seams and not the way you would normally take it!</strong> The bust middle measurements taken for a corset garment usually differs from the regular measurement you take for jackets or blouses. It is different in that <strong>you choose the value yourself!</strong> If your client has full breasts, you take a slightly smaller value than the actual bust middle measurement to make them appear smaller. This is done to make the central part a little narrower so that it doesn't look too ponderous. You narrow it down by at least 1cm or sometimes even 2cm. Look: I have chosen a bust middle measurement of 20cm for a bust circumference of 102cm, while the actual BM measurement would have probably been 22-23cm. If your client's bust circumference is a lot larger than 102cm - say, 130cm - then your BM measurement will be 23-24cm. Should your client's BM measurement differ from that of the pattern, simply add another column to the table ("Bust Middle") and mark that difference: for example, 2cm. </p>
<p><strong>You will need to account for this 2cm in all circumference alterations from top to bottom.</strong> During the cutting process you will simply shift the pattern against the fold line of the fabric instead of aligning them. The fold line will stay in place and the pattern will be shifted 2cm beyond it. This way you will simply broaden the central part of the standard pattern. </p>
