<h2>Tutorial 30. Embellishing the Dress with Lace Applique.</h2>
<p></p>
<p><strong>I have chosen this lace (see below) to decorate our dress.</strong> It used to be white.&nbsp; I have tinted it pink using special textile dye. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I will cut out all flowers, leaves, and twigs and use them as appliqu� motifs.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I have also picked out four shades of Swarowski rhinestones: </strong>pink, purple, greige, and green AB. I've selected each type of rhinestones in different sizes: from 8ss to 16ss.&nbsp; </p>
<p><strong>I will use Gutermann fabric glue for the appliqu� and E6000 glue for applying the rhinestones. </strong>I will apply E6000 with a 10ml syringe. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image006.jpg" class="img-responsive center-block"></p>
<p>I will draw the glue into the syringe, apply drops wherever needed, and pull the plunger up to stop the glue from dripping. Then I will put the syringe aside. </p>
<p><strong>I will use the following device to apply the rhinestones (see below).</strong> I have simply put a plasticine cone onto a toothpick. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Let me explain how it works.</strong> You press the cone to a rhinestone. The rhinestone sticks to the cone and holds rather well. When you put the rhinestone to a drop of glue, the glue catches it and it comes off the cone without any effort. And that's how you put the rhinestones in place one by one. </p>
<p><strong>You can also use a special rhinestone picker,</strong> which is basically a white pencil with wax. You use it and sharpen it just like any regular pencil. </p>
<p><strong>As for me, I always apply rhinestones by hand.</strong> I never use thermo-adhesive rhinestones that are applied with the help of an iron or special accessories.</p>
<p><strong>I put the dress on the dress-form and pin the lace motifs on.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I secure them in place with Gutermann glue.</strong>&nbsp; When dealing with large motifs, I glue them on from one side and then remove the pin and glue them on from the other side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image018.jpg" class="img-responsive center-block"></p>
<p>I don't remove the pins before the entire motif is secured in place. The smallest twigs will be held in place by rhinestones. I purposely leave some petals detached to add some volume to the appliqu�. The remaining pins indicate areas that haven't been glued down yet. The fabric does not shrink underneath the lace motifs when you work with Gutermann glue.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2030_files/image020.jpg" class="img-responsive center-block"></p>

