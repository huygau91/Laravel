<p><strong>And I start sewing on bones.</strong> </p>
<p><strong>I use wide Rigilene bones for this purpose.</strong> If you feel like there is too little space between the bones, you can swap them for narrow Rigilene bones. You could also simultaneously use bones of both types. </p>
<p><strong>There is a very important rule.</strong> </p>
<p><strong>There should be at least 2cm between the side seam and the closest bone.</strong> The waist is obviously the narrowest area and you should be very careful about the way the bone passes there. Remember that the seam allowance of the side seam is 2cm. Stick strictly to the mark you've made, i.e. don't place the bone closer to the side seam, because otherwise you won't be able to adjust the size of the dress after a fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2013_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a wide Rigilene bone along the princess seam of the back. &nbsp;</strong>The ends of the bones are taped over with pieces of masking tape. The bone is placed 2mm below the stay-stitch at the top. It will stop 15cm below the waistline at the bottom, just the way we have marked it. </p>
