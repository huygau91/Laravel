<h2>Tutorial 10. Cutting the Lining of the Dress.</h2>
<p></p>
<p><strong>I have taken 1.8m of fabric and folded it along the lengthwise grain so as to cut it crosswise.</strong> The fabric stretches lengthwise and not crosswise. And it's what we want. <strong>I start by laying out the pieces of the back.</strong> I put down the skirt pattern and check how its straight edge lies. Then I place the central back piece at the hip line. I use a ruler to check if I have aligned the pieces correctly. <strong>And then I check the resulting length of the skirt.</strong> I take a measuring tape and measure it from the waistline notch on the back piece to the bottom of the skirt following the central seam at the back. I get roughly 114.5cm. According to my client's height, the dress should be 112cm long, considering that she will wear high-heels with it. It's all good then since I need a little extra fabric for the hem. I believe it is fine to cut it that way. If you happen to lack some fabric, make the lining 2-3cm shorter than the dress. In my case it's perfect and, hopefully, I can cut the lining at its full length. It is better to just trim it later.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the bottom of the skirt with chalk - not all of it, just a small part of the hem that equals the width of the central back piece - and I draw the central seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>You can secure the central back piece with pins. </p>
<p><strong>Now I need to mark the width of the bottom of the back piece from the central line of the skirt. It is approximately 14.8cm.</strong> By the way, you can cut the back without any vertical princess seams: simply shift the patterns against one another in such a way that you can use a single dart. If you're sewing a whole dress, then of course there is no need in cutting up the vertical seam. But since we're working on the lining now, frankly speaking, I prefer using this princess seam because it ensures a better fit. This dress model allows me to make a vertical seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Next I pin down the side back piece right next to the first piece.</strong> I have cut the pattern of the back in another two pieces to make sure the vertical line between the pieces is precise. I take the pattern of the skirt again, align its hip line with the hip line of the top of the dress, and trace the edge of the skirt. Now I can draw the entire hemline at the bottom. I have determined the location of the side seam on the skirt and recreated the bell shape. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>It's best to secure it all with pins.</strong> The fabric is rather thin and shifty, which is why you need quite a few pins to ensure perfect accuracy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out all pieces of the lining of the back and make all notches:</strong> on the hip line, at the end of the zipper, at the beginning of the zipper, on the waistline. These notches will help you adjust the pieces correctly when you start joining them. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I place the pieces of the front at the fold line and pin them down.</strong> I put the skirt pattern to the hip line of the dress, even it out against the fold line, and draw the side seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I draw two horizontal lines parallel to the fold line, one from the end point of the central front piece and another from the starting point of the side front piece.</strong> Unfortunately, it is impossible to make the front of a corset-based dress without vertical princess seams. No matter how many times I tried, it was always impossible to align the pieces well enough to achieve a beautiful shape of the bust princess seam. I have slightly miscalculated the fabric consumption. There is a little more than needed, just some 10cm extra. I suppose, now you understand why it was better to put those pieces next to each other head to tail. It is also why I transferred the patterns of the back onto the opposite side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully cut the pieces following the outermost contours.</strong> And, of course, you should never forget the notches. You can also provide for a slit in the skirt of the dress to allow for better freedom of movement. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>We have finished cutting the dress.</strong> Now we need to duplicate it and cut additional stiff iron-on pieces for duplicating the cups. </p>
<p>
