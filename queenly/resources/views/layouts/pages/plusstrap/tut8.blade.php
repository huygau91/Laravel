<h2>Tutorial 8. Applying Alterations to the Pattern. Part 3.</h2>
<p></p>
<p><strong>I take the side front piece with a straight neckline and trace it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I adjust it the same way I adjusted the previous side piece, except I take into account relevant clip alterations for this particular piece.</strong> </p>
<p><strong>I mark 3.5cm down from the hip line.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I re-draw the side seam configuration of the front.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I mark 1.5cm down from the top point. </strong>We added 1.5cm to the length of the clip. Since we have previously raised that area, we will step 1-2mm away from that line to prevent the cup from digging into the body. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I prolong the princess seam of the front upwards at a slight incline to the left and mark 1.5cm down.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I mark a seam allowance of 2cm and connect the top points.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Don't forget to draw the under-bust dart.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Let's adjust the princess seam of the front.</strong> I take the standard pattern and use it as a template. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I mark all altered areas with a bright marker.</strong> It shows the shape of the new pattern clearly and helps you notice any flaws. If some lines are messy and uneven, you can tell that the pattern has been constructed poorly and is not suitable for cutting and sewing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p>I can basically cut the pieces out right now. I could have done it all directly on fabric. On the other hand, it is probably good that I've done it on paper first. <strong>Now I will cut out the central piece, fold it along the middle, and we will see what lines should be our cutting lines. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>If it matters to you, where to place the shoulder strap (on the left or on the right side of the garment), you must mark the right side and the wrong side of fabric on your pattern to avoid confusion.</strong> If this side of the pattern corresponds with the right side of the fabric, then the shoulder strap will go over the right shoulder. If the other side is the right side, then the shoulder strap will go over the left shoulder. <strong>Let's make the shoulder strap go over the left shoulder.</strong> Mark the face side on all other patterns too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the central front piece in half vertically, face side up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>We will put the piece to the fold line of the fabric and cut it out following the wider contour.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And then we will open it and trim the excess. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the side front pieces.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I put the side pieces together, face to face, and compare them.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>As you can tell, their princess seams match perfectly but there is some divergence in the side seam and the length of the pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I even them out. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>During the cutting process, we will put these two pieces together, put them over the fabric, and cut them out simultaneously, following the wider contour.</strong> And then, with two pieces at hand, we will even each of them out after the corresponding pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I highly recommend that you make notches.</strong> Notches should be made at the same level on all patterns so you don't have to wonder where to place them when working with fabric. </p>
<p><strong>If you want to make a high-back dress</strong>, then bring the central back piece to the same height as the side piece and then shape the neckline in the desired way (line 1). &nbsp;<strong>Sometimes you need to make a low-back dress</strong>: in this case the back neckline should not stop lower than 7cm above the waistline (line 2). If it's hard for you to align the pieces of the back, then cut them out following line 3, sew down the princess seam, and then mark it and cut it out the way you like directly on the client. It's a perfect way to do it! You can also adjust line 4 during a fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>We have prepared all patterns necessary for cutting our dress.</strong> I used the same print-out patterns.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%208_files/image044.jpg" class="img-responsive center-block"></p>

