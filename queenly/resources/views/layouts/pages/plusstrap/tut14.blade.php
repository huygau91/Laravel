<h2>Tutorial 14. Sewing Bones onto the Front Half of the Dress.</h2>
<p></p>
<p><strong>I have put the garment on the dress-form and slightly adjusted the shape of the neckline.</strong> The convex top edge of the corset didn't look right and I wanted to make it more graceful. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I adjust the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Before we start sewing on the bones, let us sew down the darts </strong>we marked during the construction process. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stay-stitch along the top of the cups and the armscye. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>First of all, I will sew on a wide vertical Rigilene bone that will set a boundary for the cup. </strong>It should be placed a couple millimeters below the stay-stitch at the top. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I sew it on. </strong>The ends are taped with pieces of masking tape.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I sew it on from one side for now </strong>so I can push the bones that will pass from the cup underneath it. <strong>Please note:</strong> there should be at least 2-3cm between this bone and the side seam at the waist level. This will allow you to restrict the waistline if necessary. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a narrow Rigilene bone along the top edge.</strong> I have gathered it in advance to help it settle nicely along the curved edge. I trim the tails of all fishing lines, tape the end of the bone, place it 1mm below the stay-stitch, and start sewing. I place the bone 4cm from the edge of the piece. <strong>You should slightly ease in the fabric underneath it when sewing. </strong>&nbsp;Pull the bone on to you while pushing the fabric away from you. This trick will help you achieve a nice shape of the neck hole and ensure good fitting in the chest. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I stop 4cm before the opposite edge and trim the bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a narrow gathered Rigilene bone in the armscye area.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew wide vertical Rigilene bones onto the side pieces of the front.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image040.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I sew a narrow Rigilene bone along the bust line with two rows of parallel stitches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Now the bone at the bottom of the cups: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image052.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I gather the bone that goes along the top of the cups.</strong> Make sure not to ease in the fabric when you're securing horizontal bones inside the cups. Spread the fabric carefully, pull it tight enough, and make the cups round.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I make another row of strengthening stitches along the bone in the armscye area. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I continue sewing wide vertical Rigilene bones along the princess seams of the front and the central front piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2014_files/image066.jpg" class="img-responsive center-block"></p>
