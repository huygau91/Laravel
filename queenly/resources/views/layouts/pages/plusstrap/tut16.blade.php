<h2>Tutorial 16. Overlaying the Bones.</h2>
<p></p>
<p><strong>After pressing the pieces with bones sewn onto them, you need to overlay the bones with straps of iron-on batiste.</strong> It's important to make sure they don't push through the drapes since we are not using a separate layer of fabric for the face of the dress. We will use the same thin fabric for the drapery. If we start forming drapes without covering up the bones, they will most likely start pushing through. That's not what we want! And that's why I take straps of iron-on batiste left after the cutting and overlay the bones. (Of course, you can also cut these straps from a new piece of batiste). </p>
<p><strong>I overlay the bones on the back pieces.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It's really convenient to use a sleeve-pressing board for this purpose by placing it edgeways.</strong> The fabric between the bones does not wrinkle in this case. I do the same the other piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Next we need to duplicate with iron-on batiste the stomach area of the front half of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It's more than enough to disguise the bones.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Just make sure to spread the piece well to avoid wrinkles and make sure the size of the lining stays the same</strong> and does not shrink by a couple centimeters. You should keep turning the piece around to check if everything looks smooth or if there are any creases. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>You can also press it from the other side to remove any occurring wrinkles.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2016_files/image012.jpg" class="img-responsive center-block"></p>

