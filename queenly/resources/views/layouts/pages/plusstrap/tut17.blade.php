<p><strong>I put thin straps of polyester wadding onto the cups area.</strong> Since my client has a full bust, we don't really want to make it look larger than it is. Our goal is to simply cover up all bones and prevent them from sticking through the face fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the polyester with pins...</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>...and stitch along the perimeter of the cups. </strong>&nbsp;</p>
<p><strong>I stitch in the following order:</strong> first along the outer side of the vertical bone closest to the side seam (1), then along the outer side of the bone at the top (2), along the outer side of the vertical bone closest to the opposite side seam (3), and along the row of stitches attaching the iron-on fabric (4). Besides, I'm also going to add ribbons for hanging loops. Each ribbon is about 28cm long. You simply fold it in half and place it under the stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch with the machine.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I have made another row of stitches along the middle of the vertical bone to form the cleavage.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>That's how it looks from the reverse side.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove the pins and trim the polyester close to the stitch.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the top edge of the dress over the bone and stitch it down.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It's best to notch the seam allowance in the armscye area to help it settle nicely.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>The seam allowances at the top of the back pieces of the dress should be bent back and stitched down just the same way.</strong></p>
<p><strong>It makes sense to think over one particular detail at this stage.</strong> Our dress features drapes. The top edge will only look perfect if there's a bone going along it (like it does at the front). This bone will prevent the garment from wrinkling and allow us to bend the drapes back onto the lining side in a neat and accurate way. On the other side though, a bone like that might make the back of the dress so stiff that it will dig into the body, which will neither feel nor look nice. </p>
<p><strong>Two things are always on the scale:</strong> on the one hand, we don't want the back to dig into the wearer's body, and on the other hand, we want to arrange the drapes in a beautiful way. </p>
<p><strong>There is another problem that could be caused by this bone:</strong> it might interfere when you want to apply some adjustments after a fitting (for example, make the back piece narrower by using the side seams). </p>
<p><strong>Taking all this into account, I'd like to offer a compromise:</strong> we can make this bone pass along the areas that are particularly prone to deformation without letting it onto the areas that might require adjustments. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I will place a thin bone in this area and then bend the seam allowance over it, and secure it by stitching. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2017_files/image032.jpg" class="img-responsive center-block"></p>
