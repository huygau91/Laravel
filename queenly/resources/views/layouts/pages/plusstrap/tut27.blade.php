<h2>Tutorial 27. Draping the Waistband. Securing the Drapes. </h2>
<p></p>
<p><strong>Now we need to make a draped waistband for the dress.</strong> </p>
<p><strong>There are two ways you can drape it: either in a fixed order or randomly.</strong></p>
<p><strong>If you drape it in a fixed order</strong> by arranging one neat drape after another, it will be quite easy but the effect won't be as impressive!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I want to drape it randomly</strong> and make it look as though the dress were simply wrapped in a piece of fabric around the waist. It will be a little more time-consuming since it's harder to create an organized chaos than an organized orderliness. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image004.jpg" class="img-responsive center-block"></p>
<p>Let us begin. I have prepared a suitable piece of fabric: its width and length correspond to the size of the dress. <strong>I fuse a large piece of fusible web onto the underside.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the fabric around and fuse a thin strap of fusible web along the top and another along the bottom edge of the waistband.</strong> These straps will help you turn the edges down. I wait for it to cool down before removing the paper layer. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Next I take a strap of fusible web of the same width as the waistband.</strong> I remove the paper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I put the dress on the dress-form and gradually fuse the strap in place by steaming. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse it on in such a way so as to place the waistband slightly above the waist.</strong> I fuse the strap on gradually, bit by bit, because it helps me place it more accurately and neatly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I take my piece of fabric with the web fused onto it and remove the paper layer from both sides of it.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the edge in and put it to the top edge of the waistband on the dress aligning the centre with the central bone of the corset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>It's important to make sure the waistband is placed at an even height.</strong> In order to achieve that, I need to measure length of the side where I'm supposed to arrive and mark the spot. It's 16cm in my case. I mark the point and bring the waistband to it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Then I mark the same 16cm from the other side of the dress and bring the waistband there.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I mark corresponding points from both sides of the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold in the bottom edge of my piece of fabric and align it with the bottom edge of the waistband on the dress aligning the centre with the central bone of the corset.</strong> I pin it down.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I start forming random chaotic drapes.</strong> They will always try to settle in a fixed order - it's not what we want! We want to try and make these drapes totally random by pulling on the fabric and forming creases. By the way, it's really hard to drape an entire corset that way. It's a lot easier to make evenly distributed drapes. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image044.jpg" class="img-responsive center-block"></p>
<p>We covered the entire piece with fusible web to secure the drapes in place. It wouldn't work otherwise: these random drapes would not stay in their place later. I keep checking the width of the waistband. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Let's secure it all in place before the drapes fall apart. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Now the back of the dress: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2027_files/image056.jpg" class="img-responsive center-block"></p>
