<h2>Tutorial 19. Draping the Back of the Dress.</h2>
<p></p>
<p><strong>It's time to drape the back of the dress.</strong> Let's equip ourselves with pins, scissors, and a piece of fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p>I had 6m of fabric and <strong>I have spent 1.8m on cutting the dress and 2.9m on the skirt</strong>. Here is all that's left. I will use it to drape the back and the front of the dress and to make a draped waistband. Afterwards, I will use whatever is left for the shoulder strap. I hope there will be enough fabric in the end. </p>
<p><strong>I have arranged the back pieces on the pressing board and carefully pinned them in place down to the waistline.</strong> I have carefully spread it and evened it out to make sure it lies smoothly over the surface of the pressing board. Don't be surprised, the seam at the back has turned into a dart because of the deep darts of the princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>There is an important nuance</strong> if you want your lacing bars to be more robust and if you feel like they won't be thick enough because of the relatively thin fabric used for the drapes. In that case, you can duplicate the raw edges in the centre of the back (in the lacing area) with stiff iron-on fabric or with several layers of iron-on batiste. I haven't done it because I hope my lacing area will be robust enough. </p>
<p><strong>Now let me say a couple of words about the direction of the drapes.</strong> I will pin down cords that will indicate the direction. Hopefully, they will help me maintain symmetry. The cords will be pulled between the princess seam and the waistline notch. It will be great if these cords don't hinder the draping process. If they do, I will simply remove them. </p>
<p><strong>So I take my fabric.</strong> It stretches crosswise. </p>
