<h2>Tutorial 5. Constructing the Corner for Attaching Shoulder Straps. Preparing the Print-Out Patterns.</h2>
<p></p>
<p><strong>I want to show you schematically how to adjust the back and how to construct the corner correctly.</strong> </p>
<p><strong>I have here the central back piece and the side back piece.</strong> I have sketched them very roughly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p>We have calculated the distance from the middle of the back to the princess seam. It is 9.5cm on our pattern. If I draw a mirrored piece from the other side and remove the seam allowance (2cm), this distance will be 9.5cm total when I sew the pieces together. </p>
<p><strong>I know my client's back width measurement.</strong> What does it mean? It means there is 38cm from one edge to the opposite in about the same area I have marked on the drawing. </p>
<p><strong>We don't know the location of the starting point of the measurement (point A) on the pattern. </strong>Let's determine it.</p>
<p><strong>(38cm- 19cm) � 2 = 9.5cm</strong></p>
<p>It is precisely that distance from the princess seam to point A (without the seam allowance), from which we measured the back width.&nbsp; <strong>Point A marks the beginning of the rounding. </strong>&nbsp;As a rule, you take the back width measurement in the area you want to cover up due to certain imperfections (in our case, the back bulge). </p>
<p><strong>In order to determine the location of point A</strong> I mark 9.5cm horizontally from the raw edge of the side piece (seam allowance excluded) and lift that point up by 1cm. Now I can see where I need to start rounding the top edge of the corset. </p>
<p><strong>On to the next issue: we need to find the correct way to place the corner for the shoulder strap.</strong> </p>
<p><strong>We know the distance between the shoulder straps: 26cm. </strong>Let's find out where its starting point lies (point B):</p>
<p><strong>(26cm- 19cm) � 2 = 3.5cm</strong></p>
<p>It is that distance from the princess seam to point B (without the seam allowance), from which we measured the distance between the shoulder straps. </p>
<p><strong>There is an additional measurement that helps determine the location of point B - the corner height, which is 26cm.&nbsp; </strong></p>
<p>I mark 3.5cm horizontally from the raw edge of the side piece (without the seam allowance) and mark 26cm upwards from the waistline. Point B lies at the intersection of those two lines. </p>
<p><strong>I connect points A and B</strong> and finish shaping the top edge. </p>
<p><strong>I would like to say a couple of words about working with patterns.</strong></p>
<p><strong>Suppose you have printed them out.</strong> You should have 12 sheets at hand.&nbsp; I set my printing preferences to "Actual Size." </p>
<p><strong>Now you need to put the sheets together.</strong> There is a blank border at the top and down the bottom of every sheet. Do not trim those borders, simply overlay them, glue the sheets together and cut the patterns out. I will simply demonstrate it on one of the sheets. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Please remember that all printers are different.</strong> I purposely wrote all dimensions on each pattern piece. But these numbers are there purely for your information! Yours may be different! What does it depend on? It depends on whether your printed pattern got wider or narrower. What I'm trying to say is, don't try to get them aligned perfectly. There's no need in it, and here is why. </p>
<p><strong>You have the patterns at hand, you have the form.</strong> And it won't make a great difference if your pattern is 2mm wider, longer, or shorter! <strong>Sew a mock-up!</strong> Cut all pieces from any fabric that you don't really need anymore. Sew down all vertical seams, put the mock-up on the dress-form, and measure it. Measure it strictly in the same areas you measure your client. I, for example, take the bust circumference measurement a little higher than people who are accustomed to sewing traditional garments. As for you, you will measure it wherever you find appropriate. And then you will have your own bust line marked, i.e. the area where you normally take this measurement. Because if I measure it at one level for my pattern and you measure it at a different level, our measurements won't coincide. Do it your own way and don't expect to find a "cure-all recipe"! Leave some space for your own research and tweaking. </p>
<p>After you glue together all other sheets and cut the pieces out, you will have a ready pattern for cutting. </p>

