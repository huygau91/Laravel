<h2>Tutorial 20. Draping the Front of the Dress. Part 1.</h2>
<p></p>
<p><strong>Let's start draping the front of the dress.</strong> I put it on the dress-form and secure it with pins. You should fill any hollows with padding polyester or foam shoulder pads.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The biggest challenge</strong> is the fact that there are quite many drapes either arriving at the corner of the corset or "radiating" from it, which makes it rather thick. That's the difficult part. </p>
<p><strong>I place the fabric crosswise, as usual, and start draping.</strong> </p>
<p><strong>I bend the edge of the fabric, place it to the armscye, and start forming fan-like folds.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image007.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image009.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image011.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image013.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image015.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>It's very important to know when to stop when you're arranging fan-like drapes.</strong> When you notice that creases begin to form on the rib of the fold, i.e. it doesn't settle down evenly - stop, trim the fabric, and start anew from that spot. Just bend the edge of the fabric again and continue draping. </p>
<p><strong>Why is it necessary to use fan-like folds?</strong> This is done to ensure it won't be too way thick at the corner of the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>I stop here because it's getting harder and harder to make even drapes. I remove the excess of fabric. </strong>I have draped it down to the waistline and left some fabric to bend it back over the edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image021.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>And again, I bend back the edge of the fabric and form a new fold.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>You should slightly pull the fabric on</strong>. But don't try too hard or else you might deform it. You can make very narrow drapes or drapes of medium width, like mine. It's a matter of taste. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>First I pin it down at the top, then down the bottom, and afterwards I carefully arrange and pin it down in the middle.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image031.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>I stop here and cut the fabric again.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image037.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And then I bend the raw edge of the fabric and form another fold.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>It's hard to estimate fabric consumption here.</strong> I have used about 1.5m of fabric for a relatively small area, the top of the front and the back of the dress down to the waistline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/plus-size-1/transcripts/Tutorial%2020_files/image043.jpg" class="img-responsive center-block"></p>

