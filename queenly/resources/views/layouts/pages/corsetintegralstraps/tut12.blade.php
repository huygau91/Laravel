<h2>Tutorial 12. Joining Organza and Lace Pieces. </h2>
<p></p>
<p><strong>It�s time to start sewing the garment using the throw-over technique. </strong>The process will become very simple as soon as you grasp the point and the methods of this technique. But at first I would like to warn you about some important things to consider before you start sewing. </p>
<p><strong>First of all, you need to choose a starting point, i.e. decide whether you will assemble the garment moving from the centre of the front to the back or the other way round. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p>Your decision determines to which side you should direct the seam allowances with sewn-on bones. If you start from the centre of the front and proceed to the back, all seam allowances should be directed towards the back. If you do it the other way round, they should be directed towards the front. <strong>This is very important because the front piece is usually quite narrow. </strong>So if you plan to direct the seam allowances towards the front, it might turn out that the piece is not wide enough. In that case you would either need to re-model the central piece to make it wider or change the tactics. Another bottleneck is the arched cut on the intermediate front piece where the cup gets sewn-in. Due to the small width of this cut you might have to sew the bone right against the cup if you direct the seam allowance towards the front. <strong>That�s why you should start assembling your corset from the centre of the front and proceed to the back if there are narrow areas at the front. </strong></p>
<p><strong>This however doesn�t mean you should always move from the centre to the back! </strong></p>
<p><strong>There can be narrow areas at the back as well! </strong>Or maybe you will choose to have beautiful decorative sides with visible bones on them when you start joining the front with the back. </p>
<p>There are very many possible aspects and only practice makes perfect! But the key thing is to remember how important it is to choose the right starting point before sewing in the throw-over technique! </p>
<p><strong>But let�s return to our work.</strong> </p>
<p><strong>You can consider your pieces ready for joining if you have fused the organza layers together.</strong> But if they haven�t been fused, you should merge two organza layers of each piece by stitching them up along their vertical edges. </p>
<p><strong>And now let�s look at the lace pieces. </strong></p>
<p><strong>You need to join the lace with the organza on the first central piece of the front (where we start assembling the corset). </strong>A similar procedure was performed on the first corset when we joined mesh and chiffon pieces. But the task we are dealing with right now is somewhat harder since both materials are very soft and thin. You should make sure the organza doesn�t get eased in underneath the stitch and place the lace correctly. You should also watch out carefully not to sew together a pair of identical central pieces or else you will end up either with duplicated right pieces or with left ones. That�s why I sew all pieces in a vertically mirrored fashion, i.e. I place one piece with its top down and the other piece with its top up so as to make symmetry. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make a strengthening joining stitch from the other side at a presser-foot distance from the edge. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The central front pieces are ready. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now I take the bone I�ve marked with �F" and encase it in fabric. </strong>Don�t forget to pull the fabric slightly to you to wrap the bone properly. I make a stitch about 1mm from the edge. At the end I trim all extra fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the bone around, fold the fabric over the other edge and secure it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And now I would like you to pay attention to the way we will join the central front piece with the adjoining intermediate piece. </strong>I take the intermediate front piece and check if the face side of the lace fabric rests over the organza in a nice way. <strong>You can see the pieces and the way they should be joined in the picture below.</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to sew the central front piece in-between the layers of lace and organza on the intermediate front piece. </strong></p>
<p><strong>I put the lace layer of the intermediate piece over the sewn central piece. </strong>The lace layers lie face-to-face. I match their notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I flip the overlay with the organza side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I place the intermediate organza piece over it and stitch it on at a distance of a seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>This method of joining pieces is quite difficult. </strong>We will do it step-by-step for a start in case you haven�t had such experience yet. </p>
<p><strong>First I sew the lace layer of the intermediate piece onto the central piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I overlay it with the intermediate piece made of organza and make another stitch with a 1.2cm seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>It looks like this: the central piece is sewn in-between the layers of the intermediate piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The seam allowance remains inside. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to secure this seam allowance.</strong> I flip the intermediate organza piece back and topstitch the seam by 1-2mm, i.e. make a strengthening stitch. I make sure there are no tucks on the lace and the seam allowance is even. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I take scissors and trim the seam allowance leaving about 6-7mm. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we should sew a bone onto the inner seam allowance the way we usually do it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p>Try not to let the bone go beyond the joining seam - you should be able to turn the lace piece over it later. You should also watch where you�re sewing the bone and make it go right along the seam so that its edge is aligned with it. Trim the bone at the end. </p>
<p><strong>And then you should secure the bone with a parallel stitch along its other side </strong>pulling the pieces so as to smooth out any wrinkles underneath the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I flip the lace layer of the intermediate piece over the bone and join it with the organza along its outer edge. </strong>Spread the lace as well as you can and match the edges carefully. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Adjust the stitch to avoid unnecessary gathering and tension. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>You can leave the bone as is. </strong>Making additional stitches isn�t necessary. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>However if you wanted, you could make a stitch along the right side of the bone to highlight it or to imitate a bone that�s been individually cased in lace. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>So we have joined two pieces and sewn a bone onto their princess seam. </strong>Owing to the special technique our corset will look as if it were covered with a whole sheet of lace fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image046.jpg" class="img-responsive center-block"></p>
<p>Princess seams will stay nearly invisible when we start joining the pieces. <strong>This will create an illusion that the lace covering all bones and the corset itself has not been cut at all. </strong></p>
<p><strong>Now I take the side front piece and join it with the intermediate piece that is already sewn to the central piece. It is done the same way as previously. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I would like to remind you that each preceding piece should be sewn in-between the layers of lace and organza of the subsequent piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image052.jpg" class="img-responsive center-block"></p>
<p>I believe the remark on choosing a starting point that I made before we began sewing is now fully clear to you. Now you can see that any seam allowances - whether you like it or not - are directed towards the back if you start sewing from the central front piece. </p>
<p><strong>That�s exactly what you should keep in mind to make the right decision about the sewing sequence. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2012_files/image054.jpg" class="img-responsive center-block"></p>
