<h2>Tutorial 3. Sewing the Mock-up.</h2>
<p>&nbsp;</p>
<p><strong>I start joining all cut out pieces one by one. </strong>I refer to the numbers to tell the right side of the corset from the left one. At this stage you don�t just sew the pieces together but check if their notches match well, get acquainted with the pattern that�s still new to you, and reinforce your skills of joining curvilinear patterns. I recommend that you arrange at least the first pair of pieces in a vertically mirrored fashion to avoid confusion. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest you should topstitch the seam allowances instead of pressing them open. </strong>Now is also the time to think about the direction of these seam allowances. In other words you should determine whether vertical bones will be to the left or to the right of the joining seam. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
