<h2>Tutorial 15. Assembling the Lining of the Cups and Quilting It with Bones. </h2>
<p></p>
<p><strong>First of all I�d like to make a small remark. </strong>If you plan to decorate the bottom of your corset later, you could finish its lacing bars at this stage. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
<p>You should plan a few stages ahead: if you finish the lacing bars now and finish the bottom line with a bone and a pencil-edge afterwards, then this pencil-edge will overlap the lacing bars and you will need to find a way of treating it properly. There is no great difficulty about it; you should always judge by the budget you can spend on the garment. If you need to make it simple and quick, you can easily finish your lacing bars now and finish the bottom of the corset in one go later. However I will use a more difficult yet neater way and <strong>I leave the lacing bars unfinished for now. </strong></p>
<p><strong>And now let�s start working on the cups. </strong></p>
<p><strong>I take our cup pieces and join them along their princess seams. </strong>I match their edges carefully, make a bar-tack at the beginning of each seam and stitch with a 1.2cm seam allowance. Please note the way I join the pieces in their curvaceous areas. I leave the bottom piece in place and lay the upper piece over it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam instead of pressing it open. </strong>I spread the seam allowance carefully with my fingers and stitch at about 1mm from the middle. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Next I make the same kind of a stitch from the other side of the seam. </strong>You should be particularly careful in the curved areas. Spread the fabric well and avoid tucks or creases. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the seam allowances leaving 0.5cm and notch out their corners. </strong>Notching out the corners of seam allowances is necessary to reduce the thickness of future joining seams. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Make sure no tucks or overlaps appear on the face side of the piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Don�t worry if the lines we drew for the cups don�t match properly - this is no big deal!</strong> The most important thing is that the middle of the bust is well-matched. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Looking at the inside of the cup I hold by my opinion in that I won�t place a bone over the princess seam. </strong>The thing is that this princess seam is located aside from the middle of the bust and, since it�s purely decorative, there is no point in highlighting it or making it stiffer. I prefer placing bones along the lines I drew as well as other inner bones. And I will simply press this spot over my cup pressing mould, round it and flatten it a little to achieve a beautiful shape. </p>
<p><strong>I should make a stay-stitch across the whole perimeter of the cup (including the strap) before I start stitching bones onto it. </strong>I make this stitch 1.2cm from the edge (the width of a seam allowance). <strong>And again, there is no turning the garment around on the needle! </strong>I sew from the start to the end; I sew along one side first. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And then I go on to the next side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image018.jpg" class="img-responsive center-block"></p>
<p>Be careful with sharp turns - try to stitch a line that�s as accurate as possible. And then stitch along the other side of the strap moving on to the neckline. <strong>The stay-stitch is a kind of a guideline that shows where to sew on the bones. </strong></p>
<p><strong>I gather a narrow Rigilene bone to prepare it for getting sewn-on across the perimeter. </strong>I tape its end with a piece of masking tape. </p>
<p><strong>I start at the corner as usual. </strong>I step a couple millimeters from the vertical stay-stitch and place the bone 1mm below the horizontal stay-stitch. <strong>I keep sewing it on without gathering until I reach the princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>But once I reach the princess seam and the rounded section of the piece, I need to ease in a little. </strong>I push the fabric away from me with my fingers while holding the bone and pulling it slightly to me with my right hand. I make sure the bone stays 1mm below the stay-stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone </strong>about 0.5cm from the corner and make a bar-tack. <strong>The result should look like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I gather the next bone, tape its end with some masking tape and push it underneath the previous bone. </strong>I make a bar-tack at the start and ease in the fabric underneath the bone in the underarm area to make sure it will be snug against the skin. I trim the bone 0.5cm from the edge of the iron-on fabric and tape its end with masking tape. I make another bar-tack at the end. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I push the next bone under the previous one like before and I sew it on along the edge of the iron-on fabric. </strong>You should either not gather it at all in the area where the cup transforms into the strap or gather it literally very, very slightly - otherwise the bone will stretch the fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I do gather the bone a little as I reach the stay-stitch going along the vertical cut. </strong>The iron-on fabric makes it not so easy but you should still try your best to gather the bone properly. At the end you should trim the bone, tape its end with a piece of masking tape and push it underneath the bone that was sewn-on first. Make a bar-tack at the end. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I have finishing sewing on the perimeter bones of the cup. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I start sewing on its inner bones. </strong></p>
<p><strong>I start with the central bone that goes over the bust-line. </strong>Don�t gather this bone: it should stay straight. Push its end underneath the perimeter bone. <strong>The main task is to prevent the fabric from gathering underneath the bone. </strong>You should do the following simultaneously: push the bone away from you with your left hand and pull the fabric slightly to you with your right hand. Trim the bone as soon as you reach the opposite perimeter bone and push its end underneath it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I make the second strengthening stitch along the other side of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>And now I move on to attaching the bone that will form a vertical curve. </strong>I make the same motion I�ve just described: push the bone away from me while pulling the fabric to me. Try to let the bone rest naturally. In my case its location coincides with the drawn line perfectly. If your drawn line doesn�t really correspond to the natural way the bone tends to lie, then don�t try to force it but just let it lie naturally. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I sew it on with two parallel stitches like before. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Now I will sew on two vertical bones inside the cup. </strong>They should be straight as well. I leave each bone ungathered and push its ends underneath the perimeter bones. You should try to trim its end at about the level of the middle of the perpendicular perimeter bone under which you�re going to push it. The bone should lie freely enough so as not to deform the edge of the cup. Don�t forget to remove any excessive threads to prevent them from getting into the way when you stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>You should gather the bone slightly to lay it along the oval lines in the top and bottom halves of the cup. </strong>I start by placing the bone at the pre-marked line and keep checking if it follows this line properly as I sew. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I have looked at the face side of the cup that�s been quilted with bones and I am not satisfied with one particular spot at the princess seam: it seems to be somewhat sunk-in. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>If you notice something like this during the working process, don�t expect it to go away when the cup is ready! You should strengthen this area of the cup to prevent the fabric from sinking in!</strong></p>
<p><strong>That�s why I am going to sew a bone onto this spot now. </strong>I slightly rip up the thread of the stitch from one side of the bones perpendicular to this additional bone so as to be able to push its ends underneath them. I push the new additional bone underneath one of them, trim it and then push its opposite end underneath the other perpendicular bone. Now the ends of this small bone are hidden. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I secure it with a double stitch. </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Please see what this area of the curved seam looks like now. It won�t sink in anymore because it is robust!</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And finally, you should make the second inner stitch along all perimeter bones to fix them in place properly. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>This is what the face side of the cup looks like after it�s been quilted with bones: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image064.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I turn the cup out. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2015_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>And then I quilt the second cup with bones in exactly the same manner. When it�s finished, you will need to press both cups thoroughly to give them their final shape. </strong></p>

