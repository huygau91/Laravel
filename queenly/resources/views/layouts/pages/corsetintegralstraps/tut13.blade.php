<p>As you can see now, the throw-over technique is not that difficult but very helpful. It is rather widely used and comes in handy in many cases when traditional ways of decorating a corset are impossible to apply. It also creates an illusion that your corset is covered with a smooth whole piece of lace. </p>
<p><strong>Now after all pieces have been sewn together, we can start finishing the bottom of the corset. </strong></p>
