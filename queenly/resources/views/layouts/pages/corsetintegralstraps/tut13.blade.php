<h2>Tutorial 13. Sewing the Corset in the Throw-Over Technique. </h2>
<p></p>
<p><strong>I continue joining the pieces of the corset. </strong></p>
<p><strong>I take the side back piece and sew it to the ready half of the corset front, i.e. to the side front piece. </strong></p>
<p><strong>I start it by attaching the lace layer of the side back piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And then I turn the piece over and join the organza layers. </strong>Please note that I am not making any bar-tacks either at the beginning or at the end of the seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowance. </strong></p>
<p><strong><br>
