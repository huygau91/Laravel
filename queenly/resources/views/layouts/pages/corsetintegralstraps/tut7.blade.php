<h2>Tutorial 7. Tracing the Patterns with Alterations Accounted for.</h2>
<p></p>
<p><strong>Now I would like to show you how to transfer our patterns onto fabric and cut them out after newly adjusted lines. </strong></p>
<p>I will demonstrate it on a sheet of paper for better clarity. Imagine that instead of paper you are looking at our organza that�s been fused in two layers and folded in half. Now I draw our patterns on this paper as if it were organza. </p>
<p>Imagine that there is a selvage from one side of the sheet. Since our corset has cuts both in the middle of the front and in the middle of the back, we don�t need to account for any fold lines. </p>
<p>You should keep the following things before your eyes: your notebook with the calculation table, the pattern pieces and a triangle ruler that is frequently used for marking points of reference. </p>
<p><strong>I outline the first pattern - the central front piece of the corset. </strong>Making all notches is a must. <strong>The central piece remains unchanged.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s go on. </strong>It is desirable to make the waistline go along the crosswise grain perpendicular to the selvage or along the lengthwise grain if you�re cutting in the crosswise direction. You can make relevant markings on paper if you wish. </p>
<p><strong>I take the pattern of the front intermediate piece and outline it. </strong>And now let�s look at our table of alterations. We can see that the princess seam between this piece and the side front piece calls for an important adjustment. <strong>We need to mark 0.5cm at the waistline level (i.e. narrow the waistline) and 0.75cm at the level of the stomach inwards from the princess seam. We shall start altering this princess seam from point A that lies approximately at the under-bust level.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take our pattern piece, mark the point I�m going to rotate it about, and match it with point A. </strong>I rotate the pattern in such a way that its bottom line passes the mark I made at the level of the stomach. As I can see, the pattern nearly passes the mark at the waistline level, too. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I mark a new notch at the waistline level and draw a new line for the princess seam. Please note that I also draft a new bottom line for the piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p>You have probably noticed that the corner of the new bottom line is located a couple millimeters higher than on the original. The new bottom line has a different height. <strong>This happens because I preserve the original length of the princess seam by using the standard pattern and rotating it! </strong></p>
<p><strong>Now I take a triangle ruler and draw a line that extends the new location of the bottom onto the adjoining piece. Our pattern is re-shaped now; there is a new bottom line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image009.jpg" class="img-responsive center-block"></p>
<p><strong>If you look at the picture closely, you might notice that there is about 1mm space between the new princess seam and the mark at the waistline level. </strong>I shall leave this up to you. If you want to have a really tight fit (i.e. if you measured the waist circumference with your measuring tape pulled on very tightly), you will probably have to adjust this line. <strong>You will need to draw a new line for the princess seam to account for the full value for tightening the waistline (see the dashed line below).</strong> But if you believe there will be enough tightening without this millimeter, you can just leave the line as is. You should make your own decision depending on the situation.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s take the next piece - the side front piece. </strong>I outline it. </p>
<p><strong>First of all we need to apply alterations to the princess seam between this piece and the intermediate front piece. </strong>This is done the same way like before: we remove 0.5cm at the waistline and 0.75cm at the level of the stomach. I mark point B at about the same spot as on the previous pattern (point A). I will rotate the piece about this point. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p>I hold the pattern firmly at point B and rotate it so as to reach the mark at the bottom. Then I draw a new princess seam line using the pattern as a stencil. I mark a new waistline notch too. <strong>Please pay special attention to the bottom line - it has changed by literally 1mm. </strong>I re-draw the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And here we go again: </strong>I have noticed that there is 1mm space between the princess seam and the mark at the waistline level. You can hand-draw a dashed line that will strictly correspond to the alteration if you need a very tight fit at the waist. </p>
<p><strong>And now we need to alter the side seam of the front. </strong>Let�s refer to our table to see what needs to be changed. There are no alterations at the bust-line level. <strong>But we do need to add 0.5cm at the under-bust level. </strong>And we shall add this 0.5cm across the whole length, at the waistline and at the level of the stomach. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And now I do the following: </strong>I put the pattern down and draw a single line that starts at the under-bust level and goes through all new points down to the bottom. I always draw a new bottom line just in case. What�s important, the length of the princess seam stays the same. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>And then I mark a point at the under-bust level (point C). </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put the pattern down, fix it at point C and rotate it so as to make a smooth line from this point up to the top of the piece. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>If we look at the table, we will see that there the side length should also be altered: we need to reduce it by 0.5cm. </strong>I mark 0.5cm down from the top corner at the side seam of the front (point D). </p>
<p><strong>And now there are two options available: </strong></p>
<p>- We can either join point D with the original point (point E); </p>
<p>- Or we can reduce the whole side length up to the next princess seam thereby preserving the angle of the top line. <strong>I have chosen this option and shortened the princess seam by 0.5cm (point F). </strong>I have also shortened the princess seam of the intermediate front piece by the same 0.5cm lowering the top line proportionally. This is where the cup will be sewn-in, so the smooth line that goes from the side to the cup stays untouched. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>The next piece I am going to work on is the side back piece. </strong>I draw the waistline for further reference. And again I match the notches at the waistline of the piece according to the line I�ve drawn on paper. <strong>I highly recommend that you cut it like this in spite of the fact that the pattern may seem misdrawn. </strong>This way you will have straight grain parallel to the waistline, i.e. there will be no bias grain in this area of the pattern. This will minimize the risk of distorting your fabric and getting unwanted creases and tucks when you tighten the corset (and you do tighten a lot at the waistline). So the patterns should be arranged like this: parallel either to the crosswise or to the lengthwise grain. </p>
<p><strong>I outline the pattern and refer to the table to see what kind of alterations should be made. </strong></p>
<p><strong>We need to remove 0.5cm along the whole side seam.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image025.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I put the piece down and match it with the marks I have made at the top, down the bottom and at the waistline level.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>And I draw a new line for the side seam of the back. </strong>I would like you to pay attention to the top and bottom lines of the piece. The top line of our re-modelled piece got shifted. <strong>Besides, we need to shorten the length of the side of the back by 0.5cm.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I put the pattern back. </strong>You could either remove this 0.5cm along the whole length or bring the line to the original starting point. In our case I will bring it to the starting point because I don�t want to reduce the back length. I want to leave it as is or maybe even increase it. <strong>So here is our new top line:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to change the princess seam at the back: I mark 0.5cm at the waistline, 0.75cm at the bottom line and leave it as is at the under-bust line level.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>And now I hold the pattern at a fixed point at the under-bust level (point G) and rotate it so as to reach the bottom mark I made. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image035.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I draw a new princess seam line, make a new waistline notch and check the bottom line. </strong>Please have a look at the picture: the bottom line basically coincides with the original one so we can leave it untouched. <strong>If you want a tighter fit at the waistline, you can hand-draw a smooth rounded line at this section of the princess seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>And now I take the last piece - the central back piece. </strong>This piece should be arranged in such a way that the middle of the back is parallel to the selvage, parallel to the fold line or perpendicular to the crosswise grain. </p>
<p><strong>I outline the piece and start working on the princess seam. </strong>I mark a point at the under-bust level (point H) and then mark 0.5cm at the waistline level and 0.75cm down the bottom.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>I put the pattern down and rotate it about point H. Then I draw a new line for the princess seam, mark a new notch and check the bottom line. </strong>The new bottom line coincides with the original so we can leave it as is. <strong>If you want a tighter fit at the waistline, you can hand-draw a smooth rounded line at this section of the princess seam.</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image041.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I would like to draw your attention to the top line. </strong>If you look at the side piece of the back, you will notice that we have somewhat reduced the incline of the top line. I believe it is therefore sensible to leave this incline as is and lift the top line of the central piece a little bit. This corresponds to our calculations anyway since it was desirable to lift it by 0.5cm. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%207_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest we should leave it all as is for now and even out the top line after joining the princess seams. </strong></p>
<p><strong>Finally, I recommend checking with the table to make sure that all alterations have been accounted for. Once you are certain that everything is fine, you can move on to the cutting process. </strong></p>
