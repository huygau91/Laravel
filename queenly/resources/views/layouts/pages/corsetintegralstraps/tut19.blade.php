<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I start joining the cup pieces from the top of the strap moving along the neckline side. </strong>The joining seam repeats the stay-stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The seam should be very close to the bone with basically 1mm in-between. </strong>If you can achieve this using the regular presser-foot, then leave it on. If it doesn�t work that way, swap to a one-sided presser-foot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p>I remove the pins and trim all threads. <strong>And now I take an organza strap that is 2cm wide and slightly gather the cup over this strap in the area that fits most tightly against the body.</strong> You should by no means stretch the fabric in this area. I place the strap at it and keep pulling it to me while pushing the fabric away from me just a little. Since organza is transparent, you can see the seam that you need to recreate. This way you preserve the seam allowance and strengthen the area with robust organza. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The result should look like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I bend the organza strap towards the seam allowance and make a stitch a couple millimeters above the seam. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I join the seam allowance with the lining by making a clean-finish-edge seam.</strong> I spread the layers of fabric carefully and sew at about 1mm from the edge. I make sure the seam allowance lies over the lining side. I make a bar-tack at the end. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now I trim the seam allowance: </strong>I keep it as is at the very corners and leave about 3-4mm across the remaining length. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s spread the piece and stretch the face fabric to see how it�s coming out. </strong>Everything looks good! As you can remember, I made the face part of the cup a whole centimeter bigger than the lining at the arch level. And now, when I stretch the cup, there doesn�t seem to be any extra fabric at all. The cup would have been too tight and flat if I hadn�t added this extra centimeter. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to join the pieces in the underarm area of the cup and along a small section of the scye. </strong>I mark 3-4cm from the curved seam to determine the required scye area and make a notch there. </p>
<p><strong>I pin it together at the section that should be joined. I add pins in a gliding motion so that they pass underneath the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I make a joining seam. </strong>Let me remind you that you should stitch over the stay-stitch that is about 1mm above the bone. You can use the one-sided presser-foot again if you find it hard without it. I make a bar-tack at the start of the seam and at the notch level. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image024.jpg" class="img-responsive center-block"></p>
<p>Then I remove the pins and make a slant cut at the notch level. </p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I trim the seam allowance. </strong>I start with its whole width and gradually narrow it down to 0.5cm. Then I make notches all along its length. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I turn the cup out. </strong></p>
<p><strong>I pin the face side onto the lining to check the way it�s going to look on the corset. </strong>The cup sits well and I should definitely be able to shape it properly! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image030.jpg" class="img-responsive center-block"></p>
<p>Don�t be surprised that I haven�t stitched up the second side of the strap. <strong>I would like to show you my method of finishing such small arched straps and I strongly recommend that you should use it for making other garments, too. </strong>I believe this method allows you to make a perfectly shaped strap that corresponds to the pattern precisely. Besides it�s easier to iron a strap like that! </p>
<p><strong>I pin the face to the lining along the other side of the strap. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Then I make a joining seam stepping a couple millimeters towards the edge from the stay-stitch. </strong>This is a preparatory seam that joins the face with the lining. But you could make it go right over the stay-stitch straight away. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Afterwards you should bend the seam allowance like this and cover it with a bias tape or a pretty lace ribbon. </strong>This strap will look perfect after you iron it! It will preserve its original shape and you won�t have any trouble finishing it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I use a lace ribbon: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2019_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>And I will repeat the same procedure with the second cup as well. </strong></p>

