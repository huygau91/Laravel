<h2>Tutorial 19. Joining the Face and the Lining of the Cups. </h2>
<p></p>
<p><strong>I continue working on the cups. </strong></p>
<p><strong>And now is the time to join their lining with their face. </strong>I lay out the pieces and I will join them along the neckline and partially in the underarm area. You should pin the pieces together to join them correctly. I match their edges first and then shift the face side by just about 0.5mm to make it aligned with the lining or slightly beyond it. Then I pin them together. </p>
<p><strong>You should be particularly careful when joining the lining and the face of the cups in the fullness area. </strong>In order to do it correctly I start by matching their princess seams and then match the straps without easing in. The whole fullness on the face side should be gathered in the area that fits most tightly against the body. I distribute the fullness across this area by pinning the layers together: first in the middle and again, and so forth.&nbsp; </p>
<p></p>
