<h2>Tutorial 11. Cutting the Lacing Bars and Fabric Straps for Encasing the Bones. </h2>
<p></p>
<p><strong>Now we need to cut out straps of fabric for finishing the lacing. </strong>This corset will have a different finish at the back compared to the previous one. </p>
<p><strong>It is very easy to measure the width of the strap. </strong>We are going to sew on two parallel Rigilene bones and each of them is 1.2cm wide. It is impossible to have no gap between these bones when you sew them on. So let�s say the distance between the opposite edges of two sewn-on bones is 2.7cm. Now add 1.2cm for a seam allowance, multiply it by 2 and add a couple millimeters for the fold line. <strong>So the width of the strap is 8.5cm (with some extra fabric accounted for, just in case). </strong></p>
<p><strong>Now let�s determine the length of the strap. </strong>Its length should equal the length of our cut out corset back (31cm) plus 2.5cm for seam allowances. <strong>Thus each strap should be 34cm long. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I begin by cutting two straps of the corresponding size out of iron-on batiste. </strong>I fuse them one by one onto the shiny side of the fabric since I have chosen the matte side for the face. Then I cut them out, fold them in half and press them flat. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to make a couple of fabric straps for finishing the bones. </strong>I cut out (or tear off) <strong>three 3cm wide straps </strong>at the whole width of the fabric sheet (150cm). I have cut off the selvage in advance because it may stretch and deform the fabric. I recommend pressing these straps flat. </p>
<p><strong>According to the throw-over technique for sewing corsets I should first of all cut bones of required length for sewing them onto corresponding princess seams and finish them. </strong>This means we should find out how long our whole wide bone should be and finish it before we start joining the princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p>I measure a total length of two front princess seams referring to the cut out pieces. The result is 64cm. After multiplying it by two I can tell that <strong>I need a bone that is 135cm long for finishing the front of the corset. </strong>I mark this bone with �F" for �front". </p>
<p>Next I measure the side seam and the princess seam of the back to determine the length of the second bone. <strong>The result shows that this bone should be about as long as the first one, so 135cm. </strong>I mark this bone with �B" for �back". </p>
<p>These two bones should be encased in the fabric straps that I�ve cut. It turns out that two straps are enough in my case. </p>
<p><strong>I recommend that you should iron Rigilene bones to straighten them. </strong>Otherwise they will be hard to deal with later. </p>

