<h2>Tutorial 26. Finishing the Lacing Area with Narrow Bars. </h2>
<p></p>
<p><strong>We have already prepared our lacing bars.</strong> But we need to make some preparations before sewing them on. <strong>I make a stay-stitch 1.2cm from the edge from one side of each bar and another stay-stitch 1cm from the edge from the other side of each bar. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I fold the bars according to the stay-stitches (by 1.2cm and 1cm) and press them flat. </strong>The side where I folded down 1.2cm is the backside and the side that�s 2mm larger is the face. You should always keep a couple millimeters extra considering the thickness of bones and the edge itself so as to have enough to hem the edge of the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I take a bar and place its narrow side (where the seam allowance was 1.2cm) at the edge of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And I stitch it on along the inside of the corset leaving seam allowances at the top and down the bottom. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>There are various ways of sewing a bar like this on: you could place its narrow side underneath the corset and its fold line at the vertical edge of the corset. </strong>You could stitch the bar on over the edge of the bone or near the bone. I will do the latter with the second bar on the other side of the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And here comes a very important moment. </strong>You should fold the bar back onto the other side in such a way that its top part overlays the narrow part that�s been sewn-on by literally 1mm. Then you should pull up the upper end of the top part by 2-3mm and secure it with a pin. You should also pull down its lower end by 2-3mm and secure it with a pin as well. This way the top part of the bar is pulled on tightly and won�t bulge later when you bend it. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next I stitch up both ends of the bars as close as possible to the horizontal bones of the corset.</strong> You could use the one-sided presser-foot if you found it difficult. Remember to make bar-tacks at the start and end of the stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same thing from the other side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the pin from the other side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>And trim the seam allowance. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I also notch out the corners.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And now I turn the bar out. </strong>You can tell how tightly it�s pulled on even from looking at the picture. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the bar on along its edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2026_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The same procedure should then be repeated with the second bar. </strong></p>
<p><strong>The result is two pretty lacing bars. </strong>Their length is also perfect. </p>
