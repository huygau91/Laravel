<p><strong>This presser-foot is really handy as it has two levels. </strong>The edge of your fabric stays aligned against the thicker ski-shaped blade and helps you achieve a very even edge stitch. You can use this presser-foot for stitching pockets onto a garment, finishing sleeve cuffs, and doing a lot more things. It also suits well for finishing bones for see-through corsets. You can always use this presser-foot if you have trouble making an even edge stitch. Frankly speaking, I bought it not so long ago upon my students� recommendations. But I hardly ever use it other than to demonstrate someone else how it works. I didn�t have a presser-foot like this when I started sewing and I learnt to do without it. However I want to keep you informed and point out that this presser-foot probably often comes in handy. <strong>So let�s try to make a stitch at the edge of our cup with the help of this presser-foot. </strong></p>
<p><strong>I start stitching very carefully. </strong>I stitch through all thickenings nearly manually, i.e. by spinning the flywheel with my hand. Making bar-tacks is necessary. </p>
