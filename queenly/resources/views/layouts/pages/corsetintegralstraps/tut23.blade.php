<p><strong>Then I move on to the second cup. </strong>I make a couple of stitches and check if the cups are getting sewn at the same height and with the same incline. I make sure all is good and continue stitching. I gradually remove the pins since I can tell that they hinder the motion of the presser-foot. I make necessary bar-tacks at the start and end of the stitch. </p>
