<h2>Tutorial 18. Finishing the Lining of the Cups. </h2>
<p></p>
<p><strong>Let us continue working on the lining of the cups. </strong></p>
<p><strong>I bend the seam allowance of the inner part of the cup over the bone. </strong>The stay-stitch should be seen on the rib. I make a stitch 1-2mm from the crease. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>You can see the stay-stitch on the rib: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>You should trim this stiff curved seam allowance at its full width. </strong>Don�t trim at the very beginning but leave corners both at its start and end. </p>
