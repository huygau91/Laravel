<h2>Tutorial 10. Duplicating the Cups. Marking the Bones. </h2>
<p></p>
<p><strong>I have cut out matching pieces for duplicating the lining of the cups and now I fix them in place. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p>Hard iron-on fabric tends to go beyond the borders of the base fabric after you fuse it on. You should always trim it or else the original configuration of the pattern will change. <strong>Now I take those pieces where I should mark the location of bones. </strong>It is important to recreate the notches first. </p>
<p><strong>The first bone goes along the bust-line. </strong></p>
