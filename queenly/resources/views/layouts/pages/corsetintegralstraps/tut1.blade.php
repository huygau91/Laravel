<h2>Tutorial 1. Getting Acquainted with the Model and the Throw-Over Sewing Technique.</h2>
<p></p>
<p><strong>I am about to begin working on a corset sewn in a technique that is totally new to you. It is called the throw-over technique. </strong></p>
<p><strong>When should one use this technique? </strong></p>
<p>It happens that you want to make a see-through corset or a corset sewn in the same technique but the fabric you have chosen for decoration has the following properties: </p>
<p>- <strong>A very closely packed pattern </strong>with no distinct geometry, basically whole; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></p>
<p>- Or the other way round: <strong>the elements of the pattern are rare and scattered.</strong> </p>
