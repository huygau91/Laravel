<p>Some types of lace are based on synthetic nylon mesh or sometimes even organza. An example can be Richelieu lace or organza with embossed flowers or a bright sprayed pattern. </p>
<p>The throw-over technique will come to your aid if your client has brought you fabric like this and you have to use it for decoration while sewing in the see-through corset technique. </p>
<p><strong>The main point of this technique </strong>is that you don�t just cover the corset with decorative fabric during the sewing process (i.e. place lace onto joined pieces, arrange it and give it a desired volume). <strong>What you do is cut each piece individually out of your lace after the corset pattern. </strong>This way you can lay out the pattern in any way you like. You can make every single piece have a flower on it. </p>
