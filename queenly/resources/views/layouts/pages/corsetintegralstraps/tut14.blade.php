<h2>Tutorial 14. Finishing the Top Edge of the Corset. Joining Corset Pieces in the Middle. </h2>
<p></p>
<p><strong>Let�s continue working on our corset. </strong></p>
<p><strong>I need to put a bone along its top edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image002.jpg" class="img-responsive center-block"></p>
<p>Since our top edge isn�t straight, I need to use a gathered bone. I tape the end of the bone with some duct tape instead of masking tape and place it at the top edge stepping 0.5cm from the corner. I stitch the bone along its inner side and leave a 2-2.5cm long end. You could use tape this end with masking tape. </p>
<p><strong>I make the second parallel stitch </strong>somewhere between the second and third fishing line of the bone to secure the fabric properly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image004.jpg" class="img-responsive center-block"></p>
<p>Anything that goes beyond the border of this upper bone should be trimmed. </p>
<p>You can slightly melt the ends of the vertical bones with a soldering iron to prevent fishing lines from sticking out of them. Or you can also tape them with pieces of duct tape folding those over the top edge. <strong>I make an additional stitch to secure the duct tape. </strong>And then I trim these duct tape pieces from the other side to make sure they don�t show from behind the upper bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I take a 3cm wide strap of fabric and sew it on at a distance from the edge that�s roughly the same as the width of a bone. I pull the fabric slightly as I sew. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I finish the top with a pencil-edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Now the top edge of the corset is finished. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The same procedure should then be repeated with the second half of the corset. </strong></p>
<p><strong>And now I need to join our two separate halves in the middle. </strong></p>
<p><strong>I should join these front corset pieces edge-to-edge in the middle. </strong>That�s why the outmost bones attached to the central front pieces should be finished with fabric straps. I turn the garment with its lining up and <strong>stitch on a fabric strap pulling it on slightly. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the garment over and stitch the strap along the edge of the bone pulling the fabric on slightly, </strong>i.e. I stitch the strap to the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>This is what it looks like from the other side:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The same procedure should then be repeated with the second half. </strong></p>
<p><strong>And now we can join the halves with the help of these fabric straps. </strong>Place one piece over the other very carefully trying to feel the edges of the bones and match them. And then pin them together along the very edge of the bones. You should add pins up to the point where you want to stop sewing (depending on how deep you want your V-cut to be). I mount a one-sided presser foot onto the right side of the sewing machine needle so I can stitch along the bone accurately. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Don�t forget to make bar-tacks at the start and end of the stitch! </strong>And try to feel the edge of the bone with your fingers so that the sewing machine moves to the end point without shifting. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And now I swap back to the regular presser-foot and spread the fabric straps apart. We have joined the right half of the corset with the left one. </strong></p>
<p><strong>And finally, I fold one of the straps so as to make it as wide as a bone and give it a pencil-edge finish. </strong>I start from the joined section and continue up along the cut. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And then I fold the second strap and repeat the same procedure to give the whole centre of the piece a double pencil-edge finish. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>So we have joined two halves of the corset and finished the cut! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_2/Tutorial%2014_files/image030.jpg" class="img-responsive center-block"></p>

