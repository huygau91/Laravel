<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2036_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The second fragment is the black one </strong>because most part of it also falls upon the soft mesh fabric. Again, I have already marked a color alternation line. I will start from a dark ruffle, keep alternating the colors, and finish the last row with a light ruffle. You don't have to put down another light ruffle if the last one ends below the level of the green fragment on the right. Again, after finishing this fragment, I will work on the symmetrical fragment on the opposite side of the skirt.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2036_files/image008.jpg" class="img-responsive center-block"></p>
<p>After finishing the fragments on the soft mesh fabric, I will start sewing ruffles onto all other fragments moving onwards from the center of the skirt. </p>
<p><strong>Next, I will sew ruffles onto the small red fragment. </strong>It resembles the already finished green fragment. I will start from a dark ruffle and stick to the main requirements. And then I will sew ruffles onto the symmetrical fragment on the opposite side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2036_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The next, blue, fragment requires a lot of attention because the line makes frequent turns.</strong> You can only alternate the colors of the ruffles at the bottom on a fragment like this. The color alternation line should cross all sewing lines. The top of the fragment is round so the last two rows should be sewn with light ruffles.&nbsp; </p>
