<h1>Tutorial 36. Analyzing the Ruffles Layout. The Sides of the Front Half of the Skirt. </h1>
<p></p>
<p><strong>What you see in the picture below is the skirt pattern folded in half along the middle of the front.</strong> The two halves of the folded pattern are symmetrical, which means the same things need to be done on both of them.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2036_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you about the key rules of sewing ruffles on our skirt:</strong> </p>
<p>- The outermost ruffle needs to be light on every fragment;&nbsp; </p>
<p>- Two last rows on the large projecting fragments need to be sewn with light ruffles; </p>
<p>- Make sure to keep the last row light on all other fragments; </p>
<p>- Never alternate the two colors at the top of the fragment but do it either at the side or at the bottom;&nbsp; </p>
<p>- Remember to gather the last ruffle in the semi-circle areas at the top of the fragment to help it stick up. You don't need to gather it at the sharp turns of the sewing line.&nbsp; </p>
<p>Here are some basic rules of finishing the fragment: if the last ruffle goes from down to up and doesn't reach the end of the fragment, you need to sew on another ruffle after it. If the last ruffle goes from up to down though, then it doesn't matter where exactly it ends.&nbsp; </p>
<p><strong>The sewing technique stays the same, just stick to the layout.</strong> Sewing ruffles onto soft mesh fabric is the most challenging part of the task because the skirt grows heavier and heavier with each new finished fragment. <strong>I will leave the process of sewing the ruffles onto the skirt behind the scenes. </strong></p>
<p><strong>The first fragment I am going to work with is the large red fragment.</strong> I start from it because it is situated on the soft mesh fabric. You must have already noticed that it is pretty hard to work with soft mesh fabric if something has already been sewn on around the fragment. This fragment is an uninterrupted line that forms an irregular spiral. I have already marked a color alternation line as you can see. I will start from a dark ruffle, alternate the colors occasionally, and finish the last two rows of the fragment with a light ruffle. You don't have to put down another light ruffle if the last one ends below the level of the black fragment on the right. After finishing this fragment, I will work on the symmetrical fragment on the opposite side of the skirt. </p>
<p>
