<p><strong>I will calculate the final adjustments to the front side seam line after calculating all adjustments to the back side seam line. </strong></p>
<p><strong>I move on to the back side seam line.</strong> First I will calculate pre-estimated adjustment values and write them in column 5.</p>
<p><strong>The bust front and under-bust front measurements are not involved in these calculations.</strong> I write dashes in the table.</p>
<p><strong>Bust circumference </strong>is 86cm on the client and 92cm on the pattern.</p>
<p>But I have already adjusted the pattern down by 1cm from either side, i.e. a total of 2cm.</p>
