<h1>Tutorial 1. Calculating Pattern Alterations.</h1>
<p><strong>Let me start calculating adjustments to the standard pattern. I draw a table with eight columns.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%201_files/image002.jpg" alt=""></p>
<p><strong>I will use the same measurements I always use for sewing corsets.</strong> You don't need any additional measurements in this case.</p>
<p><strong>What you need to know are:</strong></p>
<p>- Bust front,</p>
<p>- Under-bust front,</p>
<p>- Bust circumference,</p>
<p>- Under-bust circumference,</p>
<p>- Waist circumference,</p>
<p>- Stomach circumference. Make sure to record the distance from the waist in brackets. In this case, I measured the stomach circumference 9cm and not 12cm below the waistline. I did so because with this new pattern it is desirable to take this measurement somewhere between the waistline and the hip line. It will help me adjust the configuration of the side seam, the front, and the back of the dress. Since the distance from the waistline to the hip line is about 19cm, I took the stomach circumference 9cm below the waistline.</p>
<p>- Hip circumference. And again, I mark the distance from the waist in brackets. I took this measurement 19cm below the waist.</p>
<p>- Bust middle,</p>
<p>- Clip 1 (toward the armscye) and clip 2 (toward the corner),</p>
<p>- Front neck depth (measured up from the bust line),</p>
<p>- Side length,</p>
<p>- Blade height back. Let me remind you that the blade height back measurement determines how tall the back of the finished garment should be. If the back of the dress surpasses your client's bottom shoulder blade line, the dress will likely be too tight and, as the result, there will be skin bulging over the back neckline.</p>
<p><strong>These are all measurements required for customizing the standard dress pattern.</strong></p>
<p><strong>I have taken measurements off my client and written them in the table (column 1). </strong></p>
<p><strong>The pattern measurements from column 2</strong> were taken off the mock-up sewn after the pattern.</p>
