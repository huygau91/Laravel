<p>I have already arrived at my client's measurement so I don't need to remove anything in the front or the back princess seams here. <strong>I put a zero in line 5 of columns 7 and 8.</strong></p>
<p><strong>Let's move to the stomach line.</strong> My client�s measurement is 80cm and it is 88cm on the pattern.</p>
<p>Again, the standard pattern has been adjusted down by a total of 4cm: the front side seam line has been shifted 1cm inwards from either side (2cm total), and the same thing has been done to the back side seam line.</p>
<p><strong>88cm - 4cm = 84cm</strong></p>
<p><strong>80cm - 84cm = -4cm</strong></p>
<p>As you can see, I need to remove another 4cm by applying changes to the princess seam lines at the front and at the back of the dress. If I want to do it using the intermediate front and back princess seams, then I am supposed to divide 4cm between the eight raw edges and shift each princess seam line 0.5cm inwards.</p>
<p><strong>-4cm / 8 = -0.5cm</strong></p>
<p>In this case though, the back princess seams will bend way too abruptly in the area between the waistline and the stomach line, which may result in a poor fit of the dress. I will use both the main and the intermediate princess seams on the front half of the dress alongside the back princess seams to solve this problem. This way I can distribute the total value of 4cm between 12 raw edges.</p>
<p><strong>4cm / 12 = 0.3cm</strong></p>
<p>This will reduce the bend of the back princess seam in the area between the waistline and the stomach line considerably.</p>
<p><strong>I write -0.3cm in the table (column 7 and 8, line 6) and make a note to remember that I am supposed to shift both princess seams at the front.</strong></p>
<p><strong>I am not allowed to change the princess seams at the hip line level. I put a zero in line 7 of columns 7 and 8.</strong></p>
<p><strong>That's it! We are done with the calculations! </strong></p>
<p><strong>I highly recommend you double-check all calculated values before altering your paper pattern.</strong> Simply sum up all adjustment values along each circumference line and add the total to the pattern measurement. The result is supposed to equal your client's measurement.</p>
<p>Moreover, I recommend you print out two sets of patterns and apply adjustments to one of them. First sew a mock-up based on the standard pattern and then sew another mock-up based on the adjusted pattern, put it on the dress-form, and measure it. It will help you avoid many mistakes.</p>
<p>Every new pattern that lands in your hands requires that you try it out, true it, and polish it up. Do not start cutting fabric (especially, an expensive kind) unless you are absolutely confident in your pattern.</p>
<p>It is why I insist on sewing a mock-up after customizing the pattern.</p>

