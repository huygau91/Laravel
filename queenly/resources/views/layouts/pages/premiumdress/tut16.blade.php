<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2015_files/image072.jpg" alt=""></p>
<p><strong>If you serge the edges from the inside, </strong>the serging thread will show through the face regardless of how well its color matches the fabric. This is not something you want to see on a premium-class garment. It just won�t look elegant enough, will it?</p>
<p><strong>Look what I do in such cases. </strong>I put a heavy steel ruler along the edge and trim the seam allowance with a hot soldering iron. I am using a 30 degrees bevel tip. The slanted part of the tip glides along the ruler.&nbsp; I try to move it along very quickly.</p>
