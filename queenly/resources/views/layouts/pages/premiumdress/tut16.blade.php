<p><strong>And I press the sequin pieces the same way. </strong>My fabric is very easy to press and there is no need in trimming the seam allowances on the cups because they open very neatly. Always choose high quality sequin fabric because low quality sequins will melt and lose their shine during pressing. It is a good idea to test a small piece of fabric before purchasing it.</p>
<p>And, to be honest, I simply don't trust any fabric brought by the client herself. I either insist we buy it together or she commits this task to me so I can go and buy what I find suitable as a professional.</p>
