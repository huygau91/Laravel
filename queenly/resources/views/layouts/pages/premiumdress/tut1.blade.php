<h1>Premium Dress.</h1>
<h2>Introduction.</h2>
<p><strong>I am glad to welcome you to this new sewing course dedicated to premium-class dresses. </strong>It is the result of collaboration between two authors: me, Tatiana Kozorovitsky, and my former student and now fellow seamstress Julia Trofimova.</p>
<p><strong>And now, please take a look at the dress we are going to sew:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image002.jpg" alt=""></p>
<p><strong>It is a dress by Krikor Jabotian, a top fashion designer from Lebanon</strong>, distinguished for his outstanding gift of combining �uncombinable" in an absolutely beautiful way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image004.jpg" alt=""></p>
<p>Sometimes, sewing a garment from materials of such different properties seems truly impossible. And our dress is just the case. Can you see it? It looks as though the girl were walking out from a cloud. The cloud effect is imitated by the abundant heavy ruffles sewn onto a skirt from very delicate mesh fabric.</p>
<p>We decided to prepare this course to prove that you can create your own versions of dresses by the greatest fashion designers no matter how complicated they may seem! We want to show you that everything is in your hands! There is nothing impossible! You will see that the technique is not that difficult after we explain the whole sewing process step after step. We will even stick to the original palette to fully convince you that you can sew a dress based on any picture from any magazine.</p>
<p><strong>Now let us talk about the required materials.&nbsp; </strong></p>
<p><strong>I will use nude mesh fabric for the see-through skirt, as well as for draping the bodice and securing the ruffle shoulder piece. </strong>This fabric stretches along the crosswise grain. Its selvage edge is quite resilient. We will discuss other materials used for the skirt a little later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image006.jpg" alt=""></p>
<p><strong>The main fabric is made of hundreds of tiny sequins and resembles fish scales. I chose it based on the pictures of the original dress. </strong>The sequins themselves are barely noticeable from an arm's length away, and what remains is the beautiful iridescence effect.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image010.jpg" alt=""></p>
<p><strong>The lining will be sewn from contrasting terracotta fabric. </strong>It is plain stretch-satin, which only stretches in one direction. Despite being quite cost-efficient, this fabric looks gorgeous. I will use the sheen side as the face of the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image012.jpg" alt=""></p>
<p><strong>The seams on the lining will be overlaid by pretty lace ribbons of a matching color. </strong>The side seams of the dress will be concealed. I won�t use the regular technique of sewing side seams because of the sequin fabric. When face fabric features beads or sequins, you must always conceal the side seams because they will simply irritate your client's skin. And that is why I will demonstrate you the technique of sewing concealed side seams.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image014.jpg" alt=""></p>
<p><strong>The dress will be complemented with a metallic belt of a perfectly matching color. </strong>The sequin fabric will become a little less shiny when I overlay it with the mesh fabric, and the belt will look just great!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image018.jpg" alt=""></p>
<p><strong>You must have also noticed that fancy belt buckle and hair clip in the form of 3D flowers.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image022.jpg" alt=""></p>
<p><strong>Our flowers will be made of a material called plastic suede.</strong> It is also known as foamiran or eva foam. I have prepared plastic suede of three colors: white, yellow, and brown.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image024.jpg" alt=""></p>
<p><strong>I will add more tones to the main colors with the help of pastels.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image026.jpg" alt=""></p>
<p><strong>Flower petals and leaves will be made using special moulds.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image030.jpg" alt=""></p>
<p>Since silicone moulds are way too soft for plastic suede, <strong>I will make duplicate moulds from polymorph plastic granules </strong>and use them in all further work.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image032.jpg" alt=""></p>
<p><strong>Other important things are clip bases and wooden knitting needles with ball-shaped knobs for shaping the flowers. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image036.jpg" alt=""></p>
<p><strong>All these materials (save for fabrics) were bought online.</strong></p>
<p><strong>I have intentionally decided to slightly change the construction of the dress. </strong>A premium dress is supposed to have a perfect fit!</p>
<p>And a corset garment can only have a perfect fit if it features many princess seams. Wedding and evening dresses normally have two bust princess seams but all who deal with custom tailoring are bound to run into one and the same problem: it takes an additional princess seam to narrow the garment in the waist.</p>
<p><strong>With that in mind, I have designed an absolutely new pattern for this course!</strong> I have sewn a mock-up using this new pattern and put it on the dress-form to show you the difference.</p>
<p><strong>Please note how this pattern differs from the classic corset dress pattern</strong> in that its front princess seams pretty much form semi-circles at the hip line level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image040.jpg" alt=""></p>
<p><strong>Let me explain the advantages of the new pattern.</strong></p>
<p><strong>I don't have to cut up the skirt anymore.</strong> This is just great when you work with lace or fabrics with embroidery, beads, or sequins and you really don't want to have to cut it up and sew princess seams. When you use a standard corset pattern with straight princess seams at the front, the skirt has to be made from several pieces because otherwise the prominence of the breasts won�t let you join the side pieces with the central piece. My new pattern solves this problem.</p>
<p><strong>I have added another princess seam.</strong> And it has allowed me to enhance the fit in the waist.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image042.jpg" alt=""></p>
<p><strong>You can also change the position of the front princess seams at the top.</strong> For example, in this particular dress I feel like shifting the bust princess seams 1cm to the side to achieve a more graceful look.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image044.jpg" alt=""></p>
<p><strong>The back of the skirt also features no princess seams.</strong> Since the back princess seams stop at the hip line level, I can leave the back of the skirt whole instead of cutting it through all the way down. Nevertheless, I will add a little flare in the central seam to form a train and sew the side seams the way you do on a bell-shaped skirt. The result will be an elegant, slightly flared skirt, which will highlight the beauty and the texture of the fabric!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Introduction_files/image046.jpg" alt=""></p>
<p><strong>A PDF file with the standard (non-customized) new pattern is enclosed with the course. </strong>I will explain how to customize it. It involves adjustments similar to those we normally apply to a standard corset pattern, only this time there are certain limitations because it is a pattern of a corset-based dress and not just a corset.</p>
<p><strong>In conclusion, I would like to let you know that I bought 3m of main fabric and as much lining fabric for this dress.</strong> Julia Trofimova will talk about fabric consumption in detail in the second part of the book.</p>
<p><strong>Enjoy your learning! </strong></p>

