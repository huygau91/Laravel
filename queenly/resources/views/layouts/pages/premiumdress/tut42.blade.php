<h1>Tutorial 41. Cutting the Shoulder Ruffles. </h1>
<p></p>
<p><strong>There were eight triangular pieces of light half-organza and eight triangular pieces of dark half-organza left after cutting the ruffles for the skirt.</strong> I have carefully pressed them. </p>
<p>Now <strong>I also need a piece of hard mesh fabric</strong> used for the hem of the skirt <strong>and the swirl template</strong> enclosed with the book. The piece of mesh fabric should be at least as big as this template. Print out the template, align and join the sheets, cut the swirl out, and transfer it onto plain cardboard. You can use separate bits of cardboard joined by masking tape if you don't have a large enough piece at hand. It is exactly what I have done. </p>
