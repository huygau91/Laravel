<h1>Tutorial 4. Cutting Duplication Pieces from Iron-On Fabric.</h1>
<p><strong>The cutting process starts from iron-on batiste.</strong> I arrange the patterns on the table and measure the lay to find out how much batiste I need. The result is 65cm with a small reserve. I multiply it by two and get 1.3m. And, since I need to cut iron-on batiste pieces both for the lining and for the face, I prepare two pieces with a width of 1.3m each. I fold each piece in half, even out the edges and the fold lines, and put them on the table.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>I lay the patterns out on the batiste and align the fold lines of both pieces with the fold line of the central piece.&nbsp; When putting down the pieces, try to make the waistline on all of them perpendicular to the fold line on the batiste</strong> regardless of whether you want to cut it along the cross grain or the warp.&nbsp; When the waistline is perpendicular to the fold line, the iron-on material will prevent the main fabric from getting stretched out in the waist, which is exactly what we want. <strong>I secure the pieces with pins and trace them. I transfer the notches and - just in case - control points for aligning the pieces.</strong> Please note that you can only transfer these control notches on fabric in the areas which did not have to be altered. You need to either mark new control points in the altered areas of the pattern or not mark them at all.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image006.jpg" alt=""></p>
<p><strong>I remove the paper patterns and secure the fabric layers with pins to prevent them from shifting against each other. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image008.jpg" alt=""></strong></p>
<p><strong>To make things easier, I cut the batiste in two halves: the front and the back.</strong> I cut along the marked bottom line without cutting the pieces out entirely yet.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image012.jpg" alt=""></p>
<p><strong>I will also cut stiff duplication pieces for the cups.</strong> I use medium stiff iron-on fabric by Hansel.</p>
<p><strong>First of all, I need to prepare patterns of the iron-on cup pieces based on the central front piece and the intermediate front piece of the dress. </strong></p>
<p><strong>I mark the bottom border of the cup on the central front piece </strong>by marking a point 1-1.5cm above the under-bust notch and connecting it with the under-bust notch on the bust princess seam line. <strong>I draw a rounded line and cut along. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image014.jpg" alt=""></p>
<p><strong>Then I connect the under-bust notches on the intermediate front piece and cut along the line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image016.jpg" alt=""></p>
<p><strong>I give the cup a rounder shape.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image018.jpg" alt=""></p>
<p><strong>I trim down the seam allowance at the top of the cup</strong> because it doesn't look good when stiff iron-on fabric gets into the seam allowance of the neckline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image020.jpg" alt=""></p>
<p><strong>The patterns of iron-on cup pieces are ready! </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%204_files/image022.jpg" alt=""></strong></p>
<p><strong>I arrange them on the stiff iron-on fabric, trace them, secure them with pins, and cut them out.</strong> I cut the stiff iron-on fabric on the cross to make the stiff grain run vertically (otherwise it will be too hard to ease in the fabric when sewing the cups). I cut the pieces out 1mm smaller along the perimeter because iron-on fabric tends to expand when you fuse it on by pressing.</p>
