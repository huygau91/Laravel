<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2037_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Afterwards, I will sew ruffles on the two small fragments: a green one and a black one. </strong>Although I have marked color alternation lines, I will simply keep alternating the ruffles, i.e. put down a light ruffle whenever the dark ruffle ends and so forth, because these two fragments are small and situated at the bottom of the train. </p>
