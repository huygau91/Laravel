<h1>Tutorial 37. Analyzing the Ruffles Layout. The Sides of the Back Half of the Skirt. </h1>
<p></p>
<p><strong>Marked below is the area of the skirt which I have already finished. </strong>As you can see, it is rather large.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2037_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>It is about 2/3 of the surface</strong> and the skirt is gradually acquiring the desired shape and volume. Of course, sewing on these ruffles took me two times longer than sewing ruffles onto the central part of the skirt. And working on the back half of the skirt takes even longer than that because of the heavy weight and the soft mesh fabric. It was about 12 hours of pure work. Nevertheless, I am approaching the final stages bit by bit. I would like to point out that the skirt looks so neat because the edges of all ruffles were heat-sealed with a soldering iron.</p>
