<h1>Tutorial 51. Shaping the Petals and Leaves. </h1>
<p></p>
<p><strong>I start from the leaves. </strong></p>
<p><strong>I put the first leaf face side out to the iron sole and heat it up in the middle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2051_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Then, I put the still hot leaf face side down over the large mould and rub it in to help transfer all veins. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2051_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2051_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>After forming the middle of the leaf, I work on its edges.</strong> I put the edge of the leaf to the iron sole, section after section, and then put it face side down on the sponge and help curl the edges down using the ball-shaped knob of the larger knitting needle.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2051_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2051_files/image010.jpg" class="img-responsive center-block"></p>
<p>
