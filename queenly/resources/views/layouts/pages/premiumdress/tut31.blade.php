<h1>Tutorial 30. Reinforcing the Hem of the Skirt.</h1>
<p></p>
<p><strong>I have decided to improve the base of the skirt before sewing on the ruffles.</strong> The ruffles are rather heavy and I want to reinforce the hem and protect it from deformation by sewing a wide Rigilene boning tape above the narrow Rigilene boning tape. I will sew it on with two rows of parallel stitches made right over the face side of the base. This additional boning tape will be fully disguised by the ruffles. I want the hem of the skirt to hold its wave-like shape very well.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2030_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have sewn wide Rigilene boning along the bottom edge and pressed it.&nbsp; </strong></p>
<p><strong>In addition, let me share a useful trick which will make sewing on the ruffles a lot easier.</strong> The base of the skirt is large and it is very awkward to turn it around under the sewing machine needle. I leave the area I want to work on and simply roll up the rest of the base and secure it with pins.&nbsp; I roll it up at the top as well. This makes the actual work surface a lot smaller. I will gradually unroll the fabric as I sew.&nbsp; I have also transferred the color alternation lines from the pattern onto the base of the skirt. </p>
