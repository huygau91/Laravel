<h1>Tutorial 45. Making the Hair Clip. </h1>
<p></p>
<p><strong>The picture below shows nearly all tools and materials required to create matching hair clip and brooch buckle for our dress. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2045_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let's talk about them all in detail. </strong></p>
<p><strong>The above-mentioned accessories will be made of plastic suede.</strong> Plastic suede is a material also known as foamiran or eva foam. It is really nice to work with and widely used in wedding and evening fashion. Designers use it to create various accessories, decorative cards, dolls, flowers, wreaths, and even festive table decorations. I will explain the basic rules of working with plastic suede by demonstrating the process of creating plastic suede flowers.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2045_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Before we continue, you need to print out the templates</strong> enclosed with the book.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2045_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Below are the main tools and materials used. </strong></p>
<p><strong>Glue gun. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Premium-Dress/transcripts/Tutorial%2045_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Iron.</strong> I will use a small travel iron because it does not get as hot as a regular household iron and I don't have to pay as much attention to the degree of heat. It doesn't mean you can't use a household iron of course.</p>
