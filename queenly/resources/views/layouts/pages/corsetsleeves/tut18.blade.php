<h2>Tutorial 18. Joining the Face of the Back of the Dress with the Lining.</h2>
<p></p>
<p><strong>I need to sew and turn out the shoulder straps before I can continue working on the dress.</strong> </p>
<p><strong>I sew down the prepared straps of fabric along the longer side.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Here is a useful trick:</strong> it will be easier to turn out a strap of any width if you slightly flare it out at the end. I leave a long tail, tie it around the eye of a thick needle, and turn the piece out on the thread. </p>
<p><strong>Here is a finished shoulder strap:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I insert the shoulder straps between the lining and the face of the back of the dress. </strong></p>
<p>To tell you the truth, I don't like this method of installing shoulder straps. In this case, you sew them in for good and it's very hard to make any changes if you notice minor flaws. </p>
<p>I prefer sewing shoulder straps to the dress by hand during a fitting. But since people often ask me how to insert shoulder straps between the lining and the face, I simply ought to demonstrate it. I will tell you frankly: after my client tried on this dress, I could tell I would have placed them differently if I hadn't sewn them in in advance! But what's done is done now! </p>
<p>Besides, shoulder straps hold a lot better when attached by hand stitches than when attached by the sewing machine. I know it from my personal experience.</p>
<p><strong>Anyway, let us put the shoulder straps where we think they are supposed to be.</strong> I stitch about 1cm below the top edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Once the shoulder straps are secured in place, I pin the face to the lining.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I prefer stitching from the side of the lining because you can see the stay-stitch.</strong> I simply follow the stay-stitch to join the pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I make a clean-finish seam.</strong> I turn the seam allowance toward the center of the lining, step 1mm from the seam, and make another row of stitches over the lining.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the seam allowance.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we need to do something very important: join the face with the lining along the central seam, into which we have installed the zipper.</strong> </p>
<p>I put the lining and the face of the dress face to face. <strong>I align the seam allowances of the face and the lining using the notches as guide lines.</strong> One seam allowance has the zipper tape sewn onto it and the other is the seam allowance of the seam on the dress. I secure them with pins.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I step by a presser-foot distance from the zipper teeth (you can easily feel them with your fingers) and make a row of joining stitches parallel to the first seam that attaches the zipper tape. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the corner at the top. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And then I turn it out and spread it out.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same with the other half of the back: overlay the face with the lining looking to the notches and the end of the zipper seam.</strong> I even out the layers of fabric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And I make a row of machine stitches parallel to the first seam that attaches the zipper tape.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the end of the zipper... </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>...and I carefully trim the seam allowance and notch out the corners.</strong> I try not to pass onto the zipper tape itself. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>This is how neat it looks from the side of the lining.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I zip it up and check how it looks from the face and from the side of the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Then I find the notches that mark the beginning of the triangular lace inset in the central seam at the back of the dress.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down a small area of the central seam starting from those notches and up to the end of the zipper. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I spread out the top edge of the dress and join the face with the lining along the side seam, down to the hip line. I stitch at about a presser-foot distance from the edge. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image050.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image052.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The back of the dress is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2018_files/image056.jpg" class="img-responsive center-block"></p>

