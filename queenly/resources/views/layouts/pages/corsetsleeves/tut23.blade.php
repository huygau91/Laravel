<h2>Tutorial 23. Sewing Down the Side Seams (Final Steps). Pressing the Garment. </h2>
<p></p>
<p>During the fitting I determined the precise length of the garment and evened out the hem. Now I can start sewing the lace overlay onto the face of the skirt. </p>
<p><strong>I sew it on along the raw side of the front.</strong> I will later trim any excess of lace. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Then I sew it on along the other raw side of the front. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew it on along the hem of the front exactly the same way.</strong> Make sure there will be no wrinkles when the client puts the garment on. The lace overlay should not draw up the fabric underneath it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the lace overlay onto the skirt along the raw side of the back half.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew it on along the other raw side of the back half.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I sew it on along the hem of the back.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>After sewing the overlay onto the main fabric, I can sew down the side seams.</strong> </p>
<p><strong>I turn the lining back and find the point where I stopped simultaneously sewing down the side seams of the face and the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Starting at this point, I sew down the side seam of the face of the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I even out the bottom edge and remove the excess of lace.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I turn out the lining that I turned back, the face side inside, and start sewing from the top downwards.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I stop 6-7cm before the place where the seams are sewn down together.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>As the result, there is a small hole with a separate lining and a separate face, which are then joined by a single seam from the top down to the hip line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to do the same for the other side seam.</strong> </p>
<p><strong>I turn the lining back. </strong>&nbsp;I find the place where I stopped simultaneously sewing down the side seam of the face and the lining. It's the area from the armscye to the hip line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I make a bar tack and join the face with the lace overlay from the hip line down to the very bottom.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the excess of lace and the corner of the seam allowance to avoid unnecessary thickness on the hem. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I put the lining face side inwards (so as to have the seam allowance on the inside) and sew it down from top to bottom stopping 6-7cm before the hip line.</strong> I trim the corner of the seam allowance, too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to press the side seams of the face and of the lining.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully spread apart and press the seam allowance of the formed hole.</strong> There is no unwanted thickness here. It's a single side seam from the face and, thanks to the hole, the lining does not draw up the main fabric or distort the shape of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the dress the right side out and press the side seam from the face side as well.</strong> I let it cool down and stamp it with a pressing cushion.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>What we get is a single side seam with no bumps and no visible transition areas.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2023_files/image046.jpg" class="img-responsive center-block"></p>

