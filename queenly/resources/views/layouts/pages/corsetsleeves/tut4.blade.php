<h2>Tutorial 4. Drawing the Adjusted Pattern on Iron-On Batiste.</h2>
<p></p>
<p>I put the patterns down in the order in which I am going to cut them and <strong>determine the consumption of the iron-on batiste. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>I got 70cm. In other words, I need 140cm of iron-on batiste. </p>
<p>Next, I think it over whether I should place the pieces in a head-to-toe fashion or in the same sequence in which they were lying in on the table. The thing is you need to place them head to toe if your skirt is going to be flared at the sides and along the princess seams. My dress has a fully straight cut. I might even slightly narrow it down closer to the bottom. Therefore, I can just put the pieces down one after another. </p>
<p><strong>I cut a piece of iron-on batiste that is 140cm long. </strong>I fold it in half and I fold it in half once more time. In other words, I fold it twice so I can simultaneously cut it for the face and for the lining. </p>
<p><strong>I check if all pieces fit into the length of the piece.</strong> I measured the hip circumference 16cm below the waistline. I re-draw the hip line of the standard pattern correspondingly. This way I have somewhat shortened the pieces and now they fit into the length of the batiste. </p>
<p><strong>There is another important aspect:</strong> this iron-on material has a selvage and you always feel tempted to place the bottom edge of the pattern right at the selvage. But I must warn you! The selvage is always a little thicker than the rest of the fabric, which might result in a visible border on the face of the garment at the level of the hip line. It means you need to cut up the fold line of the batiste and place the hip line at the raw edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p>There is a small trick that I often use. The point is to make the edge of the iron-on fabric uneven. So instead of cutting it, I simply tear it off, which makes an irregular "ragged" edge. And this kind of an irregular, uneven edge visibly smoothes out the transition from the duplicated to the non-duplicated part of the garment. </p>
<p>Right, this is exactly what I do: I tear off the required amount of batiste and fold it in half. That "messy" edge will adhere to the main fabric in a very neat way. Don't worry if the edge curls up a little. It will only make the transition border less noticeable.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I put the central front piece onto the folded batiste, align it with the fold line, and start tracing it.</strong> I transfer the contours onto the fabric with a regular pen. It's important to leave some extra space between the top edge of the piece and the selvage because you might need to raise it when altering the cups to achieve a graceful transition to the next piece.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I start applying the alterations.</strong> </p>
<p><strong>I need to add 3cm to the clip measurement on the central piece.</strong> Let's double-check the measurement on the standard pattern to make sure it really is 7cm. I mark a 1.2cm seam allowance from each side of the line and measure the length in-between. The result is 7cm just like it was supposed to be. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And my client's measurement is 10cm.</strong> I mark 11.2cm (a seam allowance included) and find the new top point of the clip measurement - point A.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I place the pattern at point A and look for a pretty way to shape the neckline.</strong> Something is telling me I will need to raise that top line by a centimeter to make the neckline look good. I leave it unchanged for now though. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I need to add literally 1mm in the waistline and remove 1mm in the hips on the princess seam of the front.</strong> I do add it in the waistline but I leave it as is in the hips. I prefer to double-check it during a fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I take the next piece - the side front piece</strong>, and I trace its contours.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I work on the clip.</strong> I prolong it by one centimeter towards the armscye.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p>I could have left <strong>the top point of the corner</strong> unchanged but, frankly speaking, I feel like I should add another centimeter here as well. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the side seam 3.5cm outwards in the area from the bottom of the armscye to the under-bust line. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I transfer the bust-line notch and align the side seam according to it.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I shorten the side seam by 1.5cm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>And I draw a graceful armscye line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I have raised the top edge by one centimeter on the side front piece and now I need to do the same with the neckline of the central front piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>You can align the pieces along the joining line to get a better understanding of how their top edges meet and how the neckline blends into the corner.</strong> Remember about the seam allowances! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>The top edge looks really bad and uneven.</strong> I wanted it to be nice and straight.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Let me fix it.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Thanks to this small estimation, I have determined the new height of the princess seam and the shape of the neckline.</strong> Now you understand why I tried to leave at least 2cm between the top edge of the pattern and the selvage, right? I knew that I'd need to adjust the neckline with pattern alterations like that. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Attention! </strong>I have prolonged the princess seams of the front pieces 2cm upwards. In this case, <strong>I need to add 2-3mm of fabric in the cups to make the bust dart smaller</strong> and prevent the cups from going flat. I urge you to always use your imagination and think about the final result during the cutting process. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Let us return to the side seam of the front. </strong></p>
<p><strong>I have already shifted it 3.5cm outwards at the under-bust level.</strong>&nbsp; </p>
<p><strong>According to the calculations, I need to shift it 4cm outwards in the waist, 3.5cm in the stomach (10cm below the waistline), and 3cm in the hips.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the side seam by connecting the reference points with the help of the pattern.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>I can't say the waist is very pronounced but it is still accentuated.</strong> If my mum and I realize that the garment can be made tighter in the waist during a fitting, I will simply remove 0.5cm and the side seam will basically return to its initial look. I won't risk doing it before a fitting though. </p>
<p><strong>I need to shift the princess seam of the front 4mm outwards in the waist.</strong> Don't be disturbed if the dart looks somewhat arched. There is nothing wrong with that. The seam will be straight after you sew it down and press it. It won't affect the look of the dress in any way! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And last but not least: </strong>by adding one centimeter to the clip towards the armscye on the side front piece I simultaneously increased the length of the side seam in relation to point A (its top point). There is some extra space between point A and the top point of the armscye.&nbsp; </p>
<p>The shape of the armscye (line 1) on my pattern is tried and true and it ensures a great fit.</p>
<p><strong>This means I need to account for an under-bust dart instead!&nbsp; </strong></p>
<p>The length of the dart equals the distance from point A to the top point of the armscye. I will simply take in this amount of fabric with the help of the dart.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>Why do you need this dart?</strong></p>
<p>When you start shaping the cups, there will be some extra fabric gathered and you will get rid of it with the help of the dart. When you sew down the dart, the entire dress, as well as the neckline, will get pulled towards the center of the body. And it will fit better in the chest. In other words, the neckline will cling to the body properly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>Let us move to the back. </strong>&nbsp;</p>
<p><strong>I trace the side back piece. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>Let us apply the alterations we have calculated.</strong></p>
<p><strong>I need to shift it 0.5cm inwards in the bust, bring it down to zero under the bust, and shift it 0.5cm outwards in the waist. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>The result is a plain straight line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the old side length. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>And I shorten the side seam by 1.5cm.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>I put the side back piece down and shift its top edge down by 1.5cm.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image076.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>Let's continue applying alterations to the side seam in the area that goes down from the waistline.</strong> </p>
<p>There are no alterations in the stomach but I do need to remove 0.5cm in the hips. I align the waistline notch with that of the standard pattern and re-draw the side seam.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I cross out the old lines and transfer the notches.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image082.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It's the turn of the princess seam now.</strong> </p>
<p><strong>I need to shift it 4mm outwards in the waist, 1mm in the stomach, and basically bring it to zero in the hips.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the princess seam with the help of the standard pattern.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>The last piece is the central back piece. </strong></p>
<p><strong>I trace its contours.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image088.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And then I step 1.5cm downwards and re-draw the top edge.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image090.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image092.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the princess seam 4mm outwards int he waist, 1mm in the stomach, and bring it to zero in the hips. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image094.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the princess seam using the initial pattern as a template.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%204_files/image096.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I would like to add a couple of words to sum things up.</strong></p>
<p>You shouldn't try to create a strong body-control effect in garments for middle-aged women. Women of this age group value comfortable clothes but they also want to look glamorous and make their body contours appear smoother. And we will achieve this effect in our dress. We will shape its corset part with the help of bones. </p>
<p><strong>Well then, all alterations have been applied.</strong> We have adjusted the clip measurement. We have added another centimeter, shifted the pattern, and prolonged the graceful princess seam to match the adjusted side seam. We have also noticed that the side seam of the front is a little too long. We will remove the excess with the help of a bust dart. The bust dart won't stand out because it will be covered up by lace. After we sew down the dart, the entire chest area of the dress will be pulled towards the center of the body, which will enhance the fit.&nbsp; </p>
<p><strong>All we need to do now is pin it all together and cut it out. </strong></p>

