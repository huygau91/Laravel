<h2>Tutorial 20. Sewing Down the Side Seams.</h2>
<p></p>
<p><strong>Let us press the lace parts of the dress.</strong> </p>
<p><strong>I press the front pieces on a cup pressing mould because I want to make the princess seam rounder. The princess seams of the back are pressed on a flat surface. </strong>Needless to say that it makes sense to choose fabric that is suitable for pressing for this particular dress. For example, if you choose sequin fabric, make sure to ask the seller whether the sequins will lose their varnish and turn matte under the temperature of the press iron or steam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>After pressing the pieces you should carry out a preliminary try-out</strong> by putting the lace pieces over the back and front of the dress and checking whether you need to adjust, even out, or trim anything. </p>
<p><strong>Let's look at the back of the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I need to trim the lace at the sides and down the bottom.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>You can only trim the seam allowances of the princess seams on the lace back pieces after making all necessary adjustments. </strong>How come you cannot do it before making those adjustments? Well, what if you realized there was not enough lace fabric in some or other area? If you had trimmed the seam allowances by that time, you would not be able to undo the princess seams and try to re-stitch them. But now I can safely trim the seam allowances because I am sure that the lace pieces correspond with the main pieces of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Let's check the front pieces. </strong>&nbsp;</p>
<p><strong>I even out the edges.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p>I am aligning pieces cut after two absolutely different patterns. I must say there are very minor alterations: I only had to trim the edges very slightly. </p>
<p><strong>I trim the seam allowances on the princess seams after applying the adjustments.</strong> Now they will look a lot neater.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the side seams of the lace parts of the front and the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I have serged the raw edges of the side seam in advance.</strong> Now I simply stitch over the serger thread tails to secure them. You can also thread them onto a large-eyed needle and draw them underneath the threads of the seam. Just do it the way you usually do. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I have serged the back and the lining together from the top down to the hip line and serged each layer separately in the area below the hip line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to sew down the side seams of the dress.</strong>&nbsp; As you must have already guessed, this dress has open (not topstitched) side seams. Many years of practice have taught me that corset-based dresses with open side seams are the most comfortable garments ever. You can always apply adjustments and I have never heard a single word of reproach from my clients: they all like it and find it comfortable. You can hand-stitch the seam allowances down at the top after pressing. It will look neat. But the greatest advantage is the opportunity of adjusting the garment after the client's body in a quick and easy way. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Now let us press open the side seams. </strong>You can use a pear-shaped pressing mould to achieve a more graceful transition from the waistline to the hip line.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I turn out the dress and press the side seam from the face side.</strong> I always let the seam cool down before taking the garment off the pressing mould or board to prevent it from deformation.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2020_files/image046.jpg" class="img-responsive center-block"></p>
<p>If <strong>you can invite your client for a fitting at this point</strong>, then you should do so and pin all lace parts onto the garment while it is right on her. If you cannot meet with your client, do the same on the dress-form. You can double-check and adjust the location of the shoulder seam during the fitting. </p>

