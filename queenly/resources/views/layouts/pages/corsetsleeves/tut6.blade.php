<h2>Tutorial 6. Cutting the Lace Parts of the Dress.</h2>
<p></p>
<p><strong>I put the lace down on the table to cut the top part of the dress.</strong> I use the pattern with a shoulder seam. All pieces are supposed to stop at the waistline.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I place the central front piece at the fold line of the lace fabric </strong>even though I will cut it up and separate it into two halves. It's simply easier this way. Make sure to make all necessary notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I apply the calculated alterations to the side seam of the central front piece. </strong>&nbsp;</p>
<p>I need to add one centimeter to the neck base to bust middle line (point A). </p>
<p>And I need to remove one centimeter from the shoulder tip to bust middle measurement (point B). </p>
<p>There is no need in altering&nbsp; the shoulder length because the measurements coincide. </p>
<p>I re-draw the shoulder line and mark the shoulder length (13cm) plus the seam allowances:</p>
<p>13cm + 2.5cm= 15.5cm</p>
<p>I connect the shoulder line with the center of the waistline (line 1).</p>
<p>There will be no seam allowance along the length of line 1 because I plan to simply trim the main fabric and finish the edge with scallops. This will be part of the decoration process. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>A couple of words on the clip measurement: </strong>we paid close attention to the measurement when we were cutting the base of the dress. Let us double-check it now, too. The clip from the bust apex to the armscye is supposed to be 13cm. I check its length on the central piece and get 14.5cm (a seam allowance included). I simply leave it as is since it corresponds with the measurement. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I trace the side front piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And I shift the side seam 3cm outwards.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Let's check it.</strong> The length of the side seam from the waistline is supposed to be 18cm plus a seam allowance. I have one centimeter extra. I could shift it down by one centimeter but I prefer to wait until the fitting. I can always lower it later. It will be a real pity if I do it right now and the armscye turns out to be too deep, so it's best to leave it as for now. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I need to shift the princess seams 6mm outwards at the waistline level. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trace the side back piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>And I apply the calculated alterations to the side seam: </strong></p>
<p>- I remove 0.5cm in the bust,&nbsp; </p>
<p>- I bring it to zero under the bust,&nbsp; </p>
<p>- and I add 0.5cm in the waist.&nbsp; </p>
<p>The result is a straight vertical line. I won't shorten it just yet. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>The last piece is the central back piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Let's apply the adjustments.</strong></p>
<p><strong>I need to add 2.5cm to the shoulder length. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And I need to add 0.5cm to the width of the back.</strong> But if I add 0.5cm in the princess seam of the central back piece, then I need to add the same 0.5cm in the princess seam of the side back piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the line so as to make the back a little broader. Both the nape to waist and the back shoulder slope measurements coincide (points A and D). </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I need to add 3cm to the back neck base to waist (point B). </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I connect the resulting points A and B.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the shoulder length (15.5cm inclusive of the seam allowances) from point A and connect the resulting point C with the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I try to make the neckline more graceful. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p><strong>There is the last adjustment: the princess seams need to be shifted 0.5cm outwards in the waist.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Let me share a small tip before you pin the pieces together and start cutting them out.</strong> If you are not entirely sure about the accuracy of your neck base to bust middle, back neck base to waist, and shoulder tip to bust middle measurements (as well as any other measurements that affect the location of the shoulder seam), I recommend you should use a 2-3cm seam allowance instead of the regular 1.2cm - just in case. </p>
<p><strong>How does it help?</strong> If you do notice an error, you will have some extra fabric for a minor adjustment of the location of the side seam. It is particularly handy for garments with narrow fitted sleeves. You will simply improve the fit and pin the shoulder seam in place during the fitting. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I have applied all adjustments and now I can cut the pieces out and make the notches. </strong></p>
<p><strong>I leave a 1cm seam allowance at the bottom to provide for more discretion in case I do need to fix something later.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I transfer the notches. </strong>The bust-line and under-bust line notches are the most important. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>Here are the pieces of the top part of the dress: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>I move on to the sleeve.</strong></p>
<p><strong>Let's think of the best way to cut it.</strong> The fabric stretches lengthwise, along the selvage, more than it does crosswise. I believe, it's best to cut the sleeve crosswise because it's not supposed to stretch vertically. </p>
<p><strong>The sleeve template is made of two pieces.</strong> And it allows us to adjust it the way we want by increasing or reducing the gap between them. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>The finished sleeve is supposed to be 60cm long from the top of the cap to the very bottom.</strong> I mark that length. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>It's important to lay the pieces correctly.</strong> </p>
<p>There are several crucial measurements. <strong>The first one is the upper arm circumference.</strong> It is 30cm on the pattern and 34cm on the client. Therefore, the gap between the pieces should be 4cm. I mark 2cm left and right of the vertical line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image064.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>Let's check the fullness of the arm.</strong> My client's arm fullness is 19cm and it is 21.5cm on the pattern. I know it from personal experience that it is only good to make it a little bigger than the actual measurement, despite the high enough resilience of the fabric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>I know that the distance from the top of the arm to the elbow is 34cm with the seam allowance included.</strong> And I should try not to make the sleeve too narrow in that area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>My client's wrist circumference is 23cm.</strong> I mark 13cm left and right of the middle of the sleeve at the wrist level (the seam allowances are accounted for).</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a straight line from the sleeve cap to the wrist.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>The sleeve pattern is ready.</strong> It is wide enough in the elbow. Notches are made in the middle and at the sides of the cap. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>Let's check if it agrees with the armscye.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image078.jpg" class="img-responsive center-block"></p>
<p>I think I could <strong>remove a little at the front and add a little at the back (see below).&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I check the circumference of the armscye.</strong> It is 51cm. You are not supposed to ease it in because the sleeve is made of resilient lace fabric. Don't even think of gathering it at the top!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the pieces together and cut them out.</strong> I leave a 1cm seam allowance at the bottom of the sleeve, just in case. <strong>I fold the sleeve in half, even it out, and trim the edges.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>Here we go. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%206_files/image086.jpg" class="img-responsive center-block"></strong></p>

