<h2>Tutorial 19. Joining the Face of the Front with the Lining. </h2>
<h2>Assembling the Lace Parts of the Top of the Dress. </h2>
<p></p>
<p><strong>Let us join the face and the lining of the front of the dress.</strong> I secure them with pins from the face side.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I stick pins in in a gliding motion</strong> because there are bones on the lining and there is no need in piercing the garment all the way through. It's enough to just catch the fabric of the lining. I carefully re-distribute the fullness along the top edge with a generous amount of pins. </p>
<p>I recommend you should slightly shift the face against the lining to make the central corner of the front neckline neater. <strong>I raise the face 5-6mm higher than the lining at the level of the corner.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>You don't need to align the princess seams of the face and the lining!</strong> It's only good if they are shifted against each other. Otherwise the princess seams areas would look too thick, which would be particularly noticeable on the neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the pieces together along the armscyes. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>And I make a row of joining stitches along the top edge of the dress.</strong> A right-toe presser foot makes moving along the bone a lot easier. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Attention, please! I do not turn the garment on the needle!</strong> I make bar tacks at the intersections of stay-stitches and try to get right into the stay-stitch as I sew. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The only spot where you can and should turn it on the needle is the corner of the neckline.</strong> You can do it because we made those stay-stitches by letting the sewing machine stitch forth freely. And the corner came out perfectly symmetric from both sides. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></p>
<p>After you make the row of joining stitches, remove the pins and <strong>make sure to test the quality of the seam! </strong>Feel the seam with your fingers and make sure the thread did not get torn anywhere. If you notice a small gap, re-stitch that area at once. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the neckline corner with Gutermann fabric glue to help it turn out in a neater way and prevent it from fraying.</strong> I drop literally just a couple of drops and smear them with the nose of the glue bottle. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowances. Please note</strong> that I leave the seam allowance at 1.2cm at the sides and gradually bring it down to 0.5cm in the middle. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I notch out the corners as far as possible. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>You should basically notch out the central corner of the neckline down to the very row of stitches.</strong> We have reinforced this area with the help of fabric glue and you don't need to worry about notching out the seam allowance right to the stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the seam allowances on the princess seams.</strong> Notch them out as far as you can. You will make the edge smoother and avoid bumps on the princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the garment out.</strong> </p>
<p>It is usually easy to turn out the corners in the areas with bones but, since those areas are also really thick, the corners will be somewhat rounded rather than sharp. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I carefully spread out the corner of the neckline.</strong> You can place a piece of padding polyester below the corner: it will help you spread the fabric and press it in a neater way. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I align the open side seams of the face and the lining and align the notches.</strong> And I pin them together from the very top down to the hip-line notch where the iron-on fabric stops. I spread out the top edge and make sure the face fabric is evenly pulled onto the lining.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Then I carefully press the top edge and particularly the central corner.</strong> It's best to do this on a cup pressing mould to give the neckline a rounded shape. We eased in the fabric underneath the bones in sewing to make the edges rounder and enhance the fit of the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>The corner looks very neat. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now that I have assembled the front and the back half of the dress, I can work on its lace top part.</strong> I unpin the corresponding pieces to join them along the princess seams. </p>
<p><strong>I sew down the princess seams at the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>Here is the armscye and the princess seam: </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I sew down the other princess seam. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image064.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew down the princess seams of the front.</strong> </p>
<p><strong>I notch the princess seam on the central part of the front because it is then easier to join the pieces in the curved area.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>I sew from the bottom upwards.</strong> I don't use any pins or basting stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image070.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>The result looks like this:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>The princess seam on the other front piece is sewn the same way. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2019_files/image076.jpg" class="img-responsive center-block"></strong></p>

