<h2>Tutorial 7. Duplicating the Pieces with Iron-On Fabric.</h2>
<p></p>
<p><strong>I take the side front piece.&nbsp; </strong></p>
<p><strong>And I make sure I have made all notches. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I unpin the iron-on batiste.</strong> Please note that I leave the pins in the skirt part of the dress to prevent the pieces from falling apart. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the iron-on batiste. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And I put it on the table adhesive side up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I spread it out and put down the first layer of the main fabric.</strong> Try to align the edges as well as you can. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the second layer of the main fabric. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And I top it all up with the second batiste piece placed adhesive side down.</strong> Try to be as precise as possible. The main fabric is usually a lot thinner and more pliable, which means it is prone to deformation along edges cut on the bias. The batiste pieces were cut after the initial pattern so I use them as the standard for aligning the pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I steam-press the batiste.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And then I put down another layer of batiste adhesive side up.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I take two layers of main fabric simultaneously and carefully put them down.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>And I put the other batiste piece on top of it, adhesive side down.</strong> You should try your best to stick to the shape of the batiste pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully "stamp" the fabric with the press-iron moving from one edge to another </strong>and then fuse it on by steaming, hardly touching the surface. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the lay around and carefully fuse the layers from the other side.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I steam-press it from the face as well.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The part of the lay with the pattern sketched right over the batiste will serve as the lining</strong> and the clean one - as the face. I remove the remaining pins from the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And I fuse the prepared stiff iron-on fabric onto the lay of the lining. </strong>I have trimmed away the seam allowances of the stiff iron-on pieces.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse the corresponding stiff iron-on piece onto the other side of the lay.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>The central front piece:</strong> </p>
<p><strong>Since the piece has a fold line, you need to remove all pins, including those on the skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I unfold and spread out the piece made of the main fabric.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image042.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I put the batiste piece over it adhesive side down. </strong>I spread out the main fabric trying to adjust it after the shape of the iron-on material. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully "stamp" the batiste with the press-iron moving from one edge to another.</strong> And when I'm sure the iron-on fabric has settled evenly, I secure it for good with the steam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I have noticed that the main pieces are a little bigger than the batiste pieces so I adjust them accordingly.</strong> Although the difference is literally just a couple of millimeters, it may affect the size and the shape of the corset significantly. All corset-based garments require great precision and accuracy.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse on the stiff iron-on piece that I have prepared in advance.</strong> This is the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I spread out the main layer of fabric and put the batiste piece on it adhesive side down.</strong> The main piece is actually smaller than the batiste one. It's a side effect of cutting pieces with a fold line. That's why I will look to the edges of the batiste piece when I start sewing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse the batiste on.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image054.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I duplicate the side pieces of the back.</strong> It is done the same way as with the side pieces of the front:&nbsp; I remove the pins from the iron-on material, take it off, put the first layer of batiste adhesive side up, and put the other layers on top of it to make a "sandwich." </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%207_files/image058.jpg" class="img-responsive center-block"></p>
