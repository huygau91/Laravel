<h2>Tutorial 1. Getting Acquainted with the Model.</h2>
<p></p>
<p>I thought of writing this book because every professional who makes wedding or evening garments has to deal with orders from middle-aged women and women in their later years, who often accompany the bride. Often, these clients are mothers of the bride and the groom, their grandmothers, or just guests. It made me think that a book on a dress from this category will be useful to a wide circle of seamstresses. </p>
<p><strong>I have made a schematic drawing of the dress we are going to sew.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>First of all, I would like to highlight that this dress has shoulder seams.</strong> There must have been many cases when you were asked to sew an evening dress that would cover up the client's shoulders. I have chosen a dress model where the entire top part of the dress is made of lace fabric. You can either leave the bottom part of the dress smooth or overlay it with decorative lace (which is what I am going to do). The dress will feature a scalloped neckline.&nbsp; </p>
<p><strong>The base will be a one-color corset dress with a not so low neckline and thin shoulder straps.</strong></p>
<p><strong>At the back, the top edge of the base will pass at the level of shoulder blades.</strong> There will be a zipper and a seam in the center. There will be a teardrop-shaped opening at the top of the lace overlay at the back. The entire dress will be covered by lace. </p>
<p>In order to allow for better freedom of movement in walking, I will make a small triangular gore at the back to slightly expand the skirt. </p>
<p><strong>Let me tell you about the materials I have chosen for the dress.</strong></p>
<p><strong>The main fabric</strong>, which will be used both for the base and for the lining, is deep blue Lycra fabric. It stretches widthwise (along the shoot) and does not stretch lengthwise. Taking it all into account, <strong>I will cut the garment in the traditional way - along the shoot, or perpendicular to the lengthwise thread</strong>. This will prevent it from stretching out in the waist and in the hips. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image004.jpg" class="img-responsive center-block"></p>
<p>The fabric has a sheen side and a matte side. Both of them have lovely texture. If you decide to use no lace overlay on the skirt, then it will make sense to use the matte side as the face. </p>
<p><strong>I will use the sheen side as the face because my dress will be covered by delicate lace and the two textures will produce a beautiful play of colors when combined. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I have also purchased a different kind of lace, which is similar to the main lace. I will use it to frame the neckline.</strong> This lace fabric has lovely scalloped edges and beading. I will use bits of it to decorate the dress itself, too. &nbsp;&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Our dress will certainly have a tummy-control effect, although a very mild one.</strong> Middle-aged or senior women don't need a strong tummy-control effect. It�s not like they want to feel constrained. Make sure to discuss the degree of tightness with your client during a fitting. There is always some or other way to enhance the body shape and at the same time ensure a good and comfortable fit.</p>
<p>I have already taken all measurements off my client. But before I start re-calculating the dimensions of the pattern according to those individual measurements, let me tell you about <strong>the patterns we are going to use. </strong>&nbsp;We will need to use<strong> two types of patterns</strong> since our dress features a lace top part with sleeves and covered up shoulders. </p>
<p><strong>The first pattern is a pattern for the bare-necked corset base of the dress.</strong> You can see the front and the back in the picture below. These patterns are included in the book and you can print them out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The second pattern is a pattern of a garment with shoulder seams. </strong>The front and the back are shown in the picture below. The pattern enclosed with the book stops at the waistline because we will only use it to make the lace overlay for the top of the dress. The lace overlay for the bottom part of the dress will be cut after the first pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I have also supplied you with a sleeve pattern.</strong> I will explain how to use it to construct the sleeve of our dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Since we have two types of patterns, we will also have to carry out two sets of calculations. </strong></p>
<p><strong>We will use the traditional corset measurements to determine necessary adjustments for the base of the dress.</strong> </p>
<p><strong>They are namely: </strong></p>
<p>- Bust front, </p>
<p>- Under-bust front, </p>
<p>- Bust circumference, </p>
<p>- Under-bust circumference, </p>
<p>- Waist circumference, </p>
<p>- Stomach circumference. (Make sure to record the distance from the waist in brackets).</p>
<p>- Hip circumference. And again, I mark the distance from the waist in brackets. My client (who happens to be my mum, by the way) is not tall and her side length measurement is somewhat shorter than usual. With that in mind, I decided to write down how far down from the waistline I measured her hip circumference. </p>
<p>- Bust middle, </p>
<p>- Clips: towards the armscye, towards the corner, and towards the neckline. </p>
<p>- Side length. </p>
<p><strong>These are the measurements required for constructing or altering the pattern of the base of the dress.</strong> </p>
<p><strong>I draw a table with eight columns.</strong>&nbsp; </p>
<p><strong>The first column contains my mum's measurements and the second column - the dimensions of the standard pattern.</strong> Don't forget that right now we are taking measurements off the pattern of the corset base of the dress. You are supposed to sew a mock-up, put it on the dress-form, and take all measurements off it.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%201_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Let us take a look at the four measurements that are primary for working with patterns.</strong></p>
<p><strong>Bust front and under-bust front:</strong></p>
<p>These measurements will indicate the configuration of the side seam on the altered pattern. </p>
<p><strong>Bust middle:</strong> </p>
<p>This measurement will show if we need to adjust the central front piece. In other words, it will show if we need to make that piece smaller or bigger. </p>
<p>My client's bust middle measurement coincides with that of the standard pattern, which means there is no need in adding another column for calculations related to the central front piece.</p>
<p><strong>Side length: </strong></p>
<p>This measurement is crucial for determining the location of the top point of the side seam. </p>
<p>If the side length of the pattern differs from the client's by more than 2cm, then it won�t be enough to just shift the line of the armscye (1). You will need to cut the central piece along the waistline (2) and then shift it correspondingly. </p>
<p>In my case, the difference is 1.5cm and I can simply lower the line of the armscye (1). </p>
