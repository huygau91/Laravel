<p><strong>Features:</strong></p>
<ul>
<li>The model suits for making dresses for elder women (mother or grandmother of the bride or the groom, relatives or friends).</li>
<li>The model is also perfect for younger women.</li>
<li>Corset base, hidden lacing and hidden zipper.</li>
<li>Dress with custom sleeves.</li>
<li>Sleeve patterns included.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>How to use thin hidden zipper and how to keep it from breaking under pressure.</li>
<li>How to calculate the pattern considering minimal&nbsp;tightening.</li>
<li>How to cut the sleeve and to attach them to the decollete top.</li>
<li>How to work with thin lace and how to attach it to corset base.</li>
<li>How to decorate the dress with separate parts of the lace.</li>
<li>How to alter the pattern, so that it would correct the body and yet stay comfortable.</li>
<li>How to choose correct dress length and where to place decoration.</li>
</ul>
