<h2>Tutorial 27. Decorating the Garment with Lace Pieces. </h2>
<p></p>
<p><strong>I have put the dress on the dress-form and placed the prepared lace on it trying to maintain perfect symmetry.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image002.jpg" class="img-responsive center-block"></p>
<p>The scallops should definitely be symmetric in all respects. It's not always easy to get it right from the first try. Sometimes you have to unpin things and fix something. Such lace pieces are seldom perfectly identical and symmetric: be prepared to pull it up or ease it in or simply trim it here and there. In short, the process requires a creative approach. </p>
<p><strong>I arrange the motifs from the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can smear all individual flowers, motifs, and whole lace ribbons with Gutermann glue, one by one, and secure them on the dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image014.jpg" class="img-responsive center-block"></p>
<p>This glue is water-proof and very reliable. You can safely wash the garment. If you notice a mistake or you urgently need to take off an appliqu� in the side-seam area for some final adjustments, you can easily melt down the Gutermann glue by steaming it. I put the dress on the pressing board, warm up the motifs, and take them off without effort. If there is enough glue left on the motif, you can simply fuse it back on after applying necessary adjustments. If it doesn't seem to hold well enough, just add a little more glue. </p>
<p><strong>It is a great and effortless way of decorating a garment with lace motifs.</strong> All you need is some patience and thoroughness. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2027_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>After embellishing the dress, I move to the decorative waistband</strong>: that is, the two straps I have glued together. It will be used to disguise the border between the top and the skirt of the dress, as well as the border between the lace fabrics. </p>
<p>I decided where to place the waistband during the very first fitting. I took a full-length picture of my mum before hemming the bottom edge and used it to determine the required length of the dress based on the golden ratio principle. (If you are not familiar with the golden ratio principle, you can easily find detailed explanations online. Besides, there are detailed guidelines to working with body proportions on my DVD "Fancy Dresses for Little Girls." In those guidelines, I share a lot of know-how on designing well-proportioned children's clothes, which are also applicable to adults' clothes). </p>
<p><strong>I marked golden ratio guidelines on the picture to find the best place for the waistband on this particular dress.</strong> I divided the distance from the shoulder line to the bottom of the dress into sections of&nbsp; 3/8 and 5/8. The division line is exactly where the waistband should be placed. The waistband will look natural and harmonious placed at this level. In the next tutorial I will pin the waistband in place and secure it on the dress. </p>
