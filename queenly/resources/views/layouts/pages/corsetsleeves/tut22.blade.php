<h2>Tutorial 22. Sewing Sleeves into the Lace Top Part of the Dress.</h2>
<p></p>
<p><strong>I sew down the shoulder seams of the lace pieces. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I assemble the first sleeve: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>And then the second sleeve:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And I press open the seam allowance of the sleeve.</strong> It's best to use a sleeve-ironing board for this purpose. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Once the sleeves are prepared, you can pin them into the armscyes and then sew them in with the sewing machine.</strong> </p>
<p><strong>I fold the armscye in half and find the point where the middle of the sleeve top should be placed. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I pin the sleeves into the armscyes.</strong> These is nearly no easing-in around the sleeve cap. The armscye for the sleeve of a corset-based dress is typically rather small. With small armscyes, the side seams of the dress will not "crawl" toward the shoulder when the wearer lifts her arms. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the other sleeve into the other armscye.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I carefully sew the sleeves in with the sewing machine. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The first one, and then the second one: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The sleeves are sewn in place!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>I press open the seam allowances of the joining seams </strong>with the help of a pressing cushion.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew the straps to the shoulder seams.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2022_files/image034.jpg" class="img-responsive center-block"></strong></p>

