<h2>Tutorial 17. Joining the Lace with the Base of the Dress. </h2>
<h2>Installing a Blind Zipper.</h2>
<p></p>
<p><strong>Let us attach the lace overlay with the sewing machine.</strong> </p>
<p><strong>The front of the dress: </strong>I join the lace overlay with the face along the waistline and along the side seams up to the hip-line notches (i.e. the edge of the iron-on fabric). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And then I stitch up the darts.</strong> I have checked the depth of the darts on the lining to make it identical on the face. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image011.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>You can make the area with the darts neater by cutting them up and pressing the seam allowances open. </strong>In my case, the darts are going to be covered up by lace so I simply press them flat without cutting the fabric up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>I join the lace overlay with the back of the dress just the same way.</strong> I stitch along the waistline, along the side seam - down to the hip-line notch, and along the middle of the back - down to the very bottom of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image017.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image019.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image021.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Let us install a blind zipper into the middle of the back.</strong> </p>
<p><strong>In order to make it easier and more accurate, I start by sewing the zipper tape in place with the regular presser foot.</strong> Don't forget about the 2cm seam allowance in the middle of the back. I step inwards by that width, put the zipper tape down, and sew it on with the regular presser foot, first along one side and then along the other. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image025.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image027.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image029.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>After sewing the zipper tape onto the garment, you can check if the pieces are symmetric when you zip up the dress and if the top edge of the corset and the edge of the lace are even and smooth. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image033.jpg" class="img-responsive center-block"></p>
<p>Now that I have checked everything, <strong>I switch to an invisible zipper presser foot</strong> and sew the zipper in place for good. I make a bar tack at the level of the zipper notch.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image035.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image037.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>We need to sew down the central seam of the back below the zipper.</strong>&nbsp; </p>
<p>I switch to a left-toe presser foot, <strong>align and secure the seam allowances</strong>, check the notches, and find the spot where I stopped sewing on the zipper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>I place the needle right at the point where I stopped sewing, make a neat bar tack, and continue sewing downwards.</strong> I make another bar tack after sewing down 7-8cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>I check if the zipper blends into the seam well enough. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image047.jpg" class="img-responsive center-block"></p>
<p>I prefer using a regular presser foot to get a quality seam along the whole length. So I switch back to it. </p>
<p><strong>But there is supposed to be&nbsp; a special triangular lace inset in the central seam.</strong> The triangular inset will be sewn into the slit at the back of the skirt and allow the wearer to move, dance, and sit down freely. It's why I stopped at the notch that marks the beginning of the triangle. The rest of the seam remains open for now. </p>
<p><strong>I press flat the seam allowance of the&nbsp; central seam and the zipper tape. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image049.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image051.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image053.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Next, I press the under-bust darts and the seams that hold the lace overlay. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image055.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image057.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I even out the bottom edge of the overlay.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image059.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image061.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I also prepare two shoulder straps.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image063.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>When the zipper is in place and the central seam is sewn down to the level of the triangular lace inset, you should measure the length of the slit and the width of the inset.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image065.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image067.jpg" class="img-responsive center-block"></p>
<p><strong>With these measurements at hand, you can safely cut the triangle out of the lace fabric.</strong></p>
<p><strong>I spread the lace on the table</strong> and fold it in half lengthwise. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image069.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a triangle of the required size.</strong> According to my measurements, it should be about 40cm long and about 30cm wide when finished. Since the lace is folded in half, I mark 15cm instead of 30cm. I round out the bottom edge, too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image071.jpg" class="img-responsive center-block"></p>
<p><strong>And I cut it out. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image073.jpg" class="img-responsive center-block"></p>
<p><strong>I have decided to use a double layer of lace to make the triangle less see-through without using a dense lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image075.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image077.jpg" class="img-responsive center-block"></p>
<p><strong>The double-layer lace triangle is ready!</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2017_files/image079.jpg" class="img-responsive center-block"></p>

