<h2>Tutorial 21. Pinning the Lace Pieces onto the Base of the Dress </h2>
<h2>And Securing Them in Place.</h2>
<p></p>
<p><strong>I have carried out the first fitting.</strong> After my client put the garment on, I pinned the lace pieces right onto it, decided on the placement of shoulder straps and shoulder seams, determined the spot for the closure at the back, the location of the armscye and the exact place for sewing in the sleeve. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I determined the length of the shoulder straps and pinned them in place.</strong> Later I will secure them by hand stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>After studying the armscye during the fitting, I realized I need to make it somewhat deeper.</strong> I adjust both armscyes correspondingly. My mum has sloping shoulders and therefore I will place the sleeves somewhat lower than usual. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I pinned down the shoulder seams during the fitting and now I can safely even them out.</strong> It's important to pin down the shoulder seams when the garment is on the client. My mum, for example, has uneven shoulders. In other words, her body is clearly asymmetric.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I have double-checked and even out everything.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I have pinned the lace parts onto the front and back of the dress for good now. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to join them.</strong> You could do it with the sewing machine but I have decided to do it by hand because I plan to use a plenty of decorative elements. Hand stitches are basically unnoticeable on such colorful lace. </p>
<p><strong>I sew the lace onto the dress around the waistline.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Then I attach the straps.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I secure the edge of the lace piece on the front of the dress by hand stitches.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold the seam allowance of the central open edge of the lace back piece and stitch it down by the sewing machine.</strong> I slightly pull the fabric to keep the stitches from drawing it up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Make sure the edge of the lace piece corresponds with the edge of the main dress.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same with the other half of the back.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2021_files/image040.jpg" class="img-responsive center-block"></p>

