<h2>Tutorial 8. Marking the Placement of Bones on the Dress.</h2>
<p></p>
<p><strong>Let us mark the placement of bones on the lining.</strong> </p>
<p><strong>The central front piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p>I connect the bust notches <strong>(line 1). </strong>&nbsp;</p>
<p>And I draw a vertical line in the middle <strong>(line 2). </strong></p>
<p><strong>Now I need to decide how far below the waistline the bones should reach.</strong> We can make the central bone (2) somewhat longer than all others. You know how the bottom edge of a classic corset is longer in the middle and shorter at the sides. But since we are not aiming at a strong body-control effect, I would suggest the following: the central bone should stop 10-11cm below the waistline and all other vertical bones - 9cm below the waistline. </p>
<p>I step 4cm left and right of the central line (2) and mark <strong>bones 3 and 4. </strong></p>
<p>I draw another pair of lines that start at the waistline, 2cm left and right of the central line <strong>(5 and 6)</strong>. These are the areas that require particular attention to restrict the stomach. It's a common trouble zone of most women. It is soft and easy to shape but it does stand out quite a bit. Taking more fabric in the princess seams rarely helps you restrict the stomach (which is exactly what we need to do). Later we will add small darts here and use a couple of clever tricks to create a good tummy control effect and enhance the side view of the silhouette. </p>
<p><strong>Let me mark the bones on the cup. </strong>&nbsp;</p>
<p>I step 2cm inwards along the bottom edge of the cup area and draw two inclined lines <strong>(7 and 8)</strong>, each of which divides the top arch of the cup in half. Then I step 4.5cm up and 3.5cm down the bust line and draw horizontal lines <strong>9 and 10</strong>. The bottom perimeter bone <strong>(11)</strong> will pass onto the side pieces and serve as the bottom border of the cup.</p>
<p><strong>I take the side front piece:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p>First of all, I find the point on the bust line where the cup is supposed to end <strong>(point A).</strong> It is usually placed at about 6cm from the side seam or 8cm from the edge of the piece. I would like to draw your attention to an important nuance before drawing the border of the cup. You certainly know that women's breasts differ not only in size but in configuration. Some women's breasts are set wide apart with the main mass placed closer to the armscyes. These women will often want to push their breasts together towards the center. If that is the case with your client, you need to increase the distance between the border of the cup and point A. Make the cups somewhat shallower along the side seams and it will re-shape the breasts and push them towards the center. I have decided to use this trick for my mum as well so I mark 9cm instead of 8cm from the border of the cup to point A. Remember to take pictures of your client during the first fitting and mark all peculiarities of her figure. It will help you a lot.</p>
<p>I draw the arched bottom border of the cup <strong>(11).</strong> </p>
<p>And I prolong lines <strong>1, 9, and 10</strong> that pass from the central cup piece. I bring line <strong>10</strong> to about the middle of the arched line<strong> 11.</strong></p>
<p>Then I mark 2cm inwards along the bottom edge of the iron-on fabric and 1.5cm inwards from its top right corner and draw line <strong>12.</strong> I don't bring the bone to the very corner because it's where the perimeter bones will intersect. </p>
<p>Since we didn't reinforce the area between point A and the edge, I highly recommend we place a vertical bone there to spread out the cup. You should, however, remember to leave enough space between the bone and the side seam if you want to be able to adjust the latter after the fitting. I step 1cm from point A and draw a slightly inclined line <strong>(13) </strong>to mark the bone. </p>
<p>Then I step 5cm inwards along the bottom edge of the cup and draw another vertical line <strong>(14)</strong>. This bone will stop 9cm below the waistline. </p>
<p><strong>Next, I mark symmetric bones on the other side piece of the front.</strong> </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p>I draw the arched bottom border of the cup <strong>(11).</strong> </p>
<p>And I prolong lines <strong>1, 9, and 10</strong> from the central cup piece. I bring line <strong>10</strong> to about the middle of the arched line<strong> 11.</strong></p>
<p>Then I mark 2cm inwards along the bottom edge of the iron-on fabric and 1.5cm inwards from its top right corner and draw line <strong>16.</strong> I don't bring the bone to the very corner because it's where the perimeter bones will intersect. </p>
<p>I step 1cm from point B and draw a slightly inclined line <strong>(17) </strong>to mark the bone. </p>
<p>Then I step 5cm inwards along the bottom edge of the cup and draw another vertical line <strong>(18)</strong>. This bone will stop 9cm below the waistline. </p>
<p><strong>The central back piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p>I mark one vertical bone <strong>(19) </strong>right in the middle of the piece, 6.5cm from the straight vertical edge. It starts at the very top and stops 10cm below the waistline. This bone serves to reinforce the central part of the back of the dress where the zipper will be installed. </p>
<p><strong>And then I mark symmetric bones on the other half of the central back piece.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p>I mark the same vertical bone <strong>(20)</strong> in the middle of the piece.</p>
<p><strong>The side back piece: </strong></p>
<p>This part of the corset is usually the most troublesome. Creases, tucks, and uneven areas pop up out of the blue. It is a good idea to use two vertical bones if the piece is large enough. </p>
<p>I don't think it is necessary in my client's case though, so I place one vertical bone <strong>(21)</strong> in the middle of the piece. The bone stops 10cm below the waistline just like on the central back pieces.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I take the other side back piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p>And I mark the same vertical bone in the middle <strong>(22)</strong>.</p>
<p><strong>There will be additional bones placed along the perimeter and along the princess seams.</strong></p>

