<h2>Tutorial 11. Pressing the Cups (Intermediate Stage). </h2>
<h2>Adding the Rest of the Bones.</h2>
<p></p>
<p><strong>I work on the shape of the cups with the help of a cup pressing mould although I still need to sew more bones onto them, as you must have noticed.</strong> I still need to lay bones over the arched lines at the top and bottom of the cups. But I have still decided to shape them by pressing. If you are experienced enough to spread the eased-in fabric in the corner of the cup (in the bottom arched area) with your fingers, you won't have any troubles sewing on those bones. But if you are not very experienced, it's best to press that eased-in fabric first. The bones will then settle down evenly and neatly in a natural way. It is also a good idea to press flat the eased-in fabric along the neckline now. But I won't press the eased-in fabric below the cups yet. I will do it later. All this fullness will be taken in the under-bust dart.</p>
