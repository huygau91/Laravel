<p><strong>It's time to place vertical bones along the princess seams and the marked lines.</strong> </p>
<p><strong>Try not to let the bones pass onto the cups!</strong> People often ask me, "Maybe I should overlay the bone underneath the cup with a vertical bone to prevent creases along the under-bust line?" No way! A vertical Rigilene bone won't solve the problem but the intersection of the bones will create unnecessary thickness. <strong>You must never forget that Rigilene bones are only used for shaping the garment!</strong> They will never be able to support heavy breasts. Shoulder straps are a must in this case. Only plastic or spiral bones can give support to heavy breasts but making some kind of a metal construction inside the dress just so you don't have to use shoulder straps doesn't seem like a reasonable decision to me. I just don't see a big need in that! In my opinion, it doesn't look that aesthetically pleasing when a woman with a large bust has uncovered shoulders. It makes the silhouette look awkward and the body somewhat hulky.&nbsp; Spaghetti straps or a combination of thin straps of different widths can look quite pretty. Broad but graceful shoulders and full breasts can be made a lot more attractive with shoulder straps than without them. </p>
<p><strong>The central front piece.</strong></p>
<p><strong>I sew vertical bones onto the princess seams and according to the marked lines (3, 2, and 4).</strong> I secure each wide Rigilene bone with two rows of parallel stitches and tape their ends over with bits of masking tape. I make bar tacks, too.</p>
