<p><strong>I finish sewing on the bones.</strong> </p>
<p><strong>I gather them by pulling on the fishing lines, give each one an arched shape, and place one at the bottom of the cup and another at the top (10 and 9).</strong> I attach each bone with two rows of parallel stitches. And I push their ends underneath the perimeter bones as usual. I make bar tacks, too.</p>
