<p><strong>When you start pressing or even sewing, you will notice some extra fabric in the side seam which really asks for a dart.</strong> The depth of this dart depends on the size of your garment, your client's bust size, and the degree of fullness in the arched bottom part of the cup. The more you ease in, the deeper the dart should be. It often depends on your manner of sewing as well. <strong>This dart is usually 1.5-2cm deep.</strong></p>
