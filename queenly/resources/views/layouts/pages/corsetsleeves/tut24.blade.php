<h2>Tutorial 24. Finishing the Hemline. </h2>
<p></p>
<p><strong>It's time to hem the dress. </strong></p>
<p><strong>First I do it on the lining.</strong> I turn up the serged and pressed bottom edge of the lining by about 1cm and sew it down with the machine. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I join the pieces of the triangular lace inset.</strong> </p>
<p><strong>I stitch along the bottom edge</strong> trying not to let the stitches draw up the fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the seam allowance to the side and do a clean finish. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And I trim the seam allowance.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the piece the right side out and stitch along the sides of the triangle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The lace inset is finished!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I press the hem of the lining.</strong> The hemline follows the lengthwise grain because we cut the garment along the shoot. Please, take a look here: the row of stitches at the bottom still does make it look somewhat wavy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I press the serged bottom edge of the face of the dress and stick a strap of fusible web onto it.</strong> I use the fusible web instead of sewing it down with the sewing machine. I do it because, no matter how much you may try, it is impossible to prevent the hem cut along the lengthwise grain from getting eased-in. If you stitch along, it will definitely go wavy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I check the length of the slit into which the triangular lace inset needs to be sewn. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I turn up and press the hem of the face of the dress. </strong>The paper backing, which is still on the fusible web, helps make the hem perfectly even: it basically serves as a ruler. The whole strap of fusible web serves as a hem template. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the paper backing from the strap.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And I turn the hem up along the pressed fold line and fuse it on. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I carefully press the bottom edge of the dress.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I pin the triangular lace inset in its place on the dress.</strong> I make the seam allowance of the inset about 1cm and leave the seam allowance of the central seam at 2cm.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the inset to the face of the dress. </strong>Try to get right into the pressed fold line of the central seam as you stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I stop at the point where the central seam ends, make a bar tack, and remove the pins. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I find a symmetric point on the other side of the slit and move downwards from it</strong> trying to get right into the fold line. I remove the pins and unnecessary thread tails. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>The triangular lace inset is now sewn into the slit in the middle of the back of the skirt.</strong> </p>
<p><strong>Now we need to hide the seam allowances of the joining seam between the face and the lining.</strong> I put the edges of the slit together in such a way that the fabric of the triangle is placed between the face and the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>The inset is attached only to the face from one side and from the other side its seam allowance is hidden between the face and the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take the other edge of the lining and align it with the other seam allowance of the inset.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Don't forget that the lining is shorter than the main dress. It's why I place the bottom edge of the lining 1cm above the bottom edge of the dress.</strong> You can use a 1.5-1.8cm seam allowance here. I stitch right to the top of the triangular inset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2024_files/image056.jpg" class="img-responsive center-block"></p>

