<h1>Tutorial 28. Applying Final Touches to the Dress.</h1>
<p><strong>I put the waistband in place and secure it with pins. </strong></p>
<p><img class="img-responsive center-block alignnone" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image002.jpg" alt="decorating dress" width="623" height="208"></p>
<p><strong>It's important to maintain symmetry here.</strong> I check if it's placed at the right distance along the side seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image004.jpg" alt=""></p>
<p><strong>You can make the waistband follow the edge of the lace pieces at the back. You can also notch it at the sides and lay it down in a straight line if you want.</strong> Simply do it the way you like. I prefer mine to have a slight incline at the back. It settles in a more natural way and it also looks more graceful in my opinion. Besides, it makes a better composition with the triangular inset.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image006.jpg" alt=""></p>
<p><strong>I secure the waistband with pins from the back of the dress and trim the excess.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image008.jpg" alt=""></p>
<p><strong>Everything looks neat now. I take my bottle of glue and start gluing the waistband to the dress in the round. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image010.jpg" alt=""></strong></p>
<p><strong>I must warn you that the Gutermann glue I'm using is acetone-based.</strong> Be very careful not to stain the dress if you have nail polish on your nails! It could be a great disaster, especially if you're sewing a white wedding dress! I highly recommend you should remove the nail polish in this case. It must look misleading that I'm working with the glue with nail polish on, but I only do it because my nail polish cannot be dissolved by acetone.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image012.jpg" alt=""></p>
<p><strong>One bottle of glue was enough for the entire dress but it's not like there were heaps of decorative motifs either.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image014.jpg" alt=""></p>
<p><strong>That's all: I just leave the dress to dry.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image018.jpg" alt=""></p>
<p><strong>And I add a couple more flowers at the top of the back to finish it up.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2028_files/image020.jpg" alt=""></strong></p>

