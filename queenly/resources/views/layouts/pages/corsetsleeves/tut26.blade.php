<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I continue cutting out straps of lace after smearing their beads and threads with fabric glue.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take the dress and see how long the strap of lace should be.</strong> As I can see, I need one more strap with an identical pattern, which could prolong the one I have already prepared. I will carefully align the two straps and the border between them will be basically unnoticeable. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out another strap with the corresponding pattern, align it with the first one, and glue them together. </strong></p>
