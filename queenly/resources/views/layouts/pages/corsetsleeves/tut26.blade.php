<h2>Tutorial 26. Thorough Pressing.&nbsp; Preparing to Decorate the Garment.</h2>
<p></p>
<p><strong>I press the seam joining the triangular inset.</strong> I tried to form an inverted pleat but, to be honest, I couldn't secure it properly so I simply let the inset hang freely.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to decorate the garment.</strong> </p>
<p><strong>I take the lace which I am going to use for this purpose.</strong> It is lace fabric embellished with beads. I need to design a pattern and cut the lace in straps in correspondence with it. These straps will be used to frame the raw edges at the front of the dress.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I start cutting out straps of lace.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image010.jpg" class="img-responsive center-block"></p>
<p>I have noticed that I cut through the thread that holds the beads. <strong>It's best to secure these beads and threads with fabric glue along the entire length of the chosen strap of lace before cutting it out.</strong> This way you won't have to waste time on gluing beads back in place later. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Here are the straps I have prepared:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2026_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
