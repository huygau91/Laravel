<h2>Tutorial 10. Shaping the Cups on the Lining.</h2>
<p></p>
<p><strong>You must make stay-stitches before sewing bones onto the lining.</strong> </p>
<p>Here is how I do it to achieve a perfectly symmetric V-neckline. I start by stitching along one side of the piece strictly with a 1.2cm seam allowance, and when I approach the corner, I let the machine "run free."&nbsp; It stitches a couple more centimeters in a straight line. Then I turn the piece over and do the same from the other side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The intersection of these stay-stitches (point C) marks the center of the dress.</strong> This center usually turns out to be placed 1-2mm off the vertical line you drew by a ruler. Some minor deformation must take place when you're sewing down the princess seams and pressing the garment. <strong>And now we have determined the correct center of the garment and the corner looks just like it should.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I make another stay-stitch along the armscye.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>By doing all this, you determine the top border of the bones and the configuration of the top of the dress. </strong></p>
<p><strong>Let me add bones along the perimeter of the cup.</strong></p>
<p><strong>I gather a narrow Rigilene bone</strong> by pulling on one of its outermost fishing lines until the bone acquires an arched shape. Then I trim the tails of the fishing lines and tape the end of the bone with a piece of masking tape. </p>
<p><strong>I step 2mm from the vertical stay-stitch and lay the bone down.</strong> It should be sewn on 1mm below the top stay-stitch. I keep slightly easing in the fabric until I reach the princess seam. After reaching the princess seam, I increase the degree of easing in. Just make sure not to ease in way too much or else the top edge of the dress will bite into the body. When I reach the corner, I make a turn downwards and trim the bone at the bottom border of the iron-on fabric. There will be a perimeter bone (11) going along the bottom border of the iron-on fabric and it will overlay the end of this vertical bone <strong>(the bones are numbered according to Tutorial 8)</strong>.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I prepare the next bone by gathering it the same way and put it in place on the other half of the piece.</strong> I try to make it perfectly symmetric to the first bone. It is very important to maintain the same degree of easing in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Now it's the turn of the arched bone (11) that passes below the breasts and limits the cups at the sides.</strong> Again, I gather it in advance. I stitch from the bust line to the top without easing in. The easing-in is necessary in the arched areas though. It will enhance the shape of the cups. I let the bone lie straight after passing the princess seam. I overlay the vertical bones, pass onto the other cup, and repeat the easing-in in the arched area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I lay a plain, ungathered bone (1) along the bust line.</strong> </p>
<p><strong>I keep pulling the fabric on to me and pushing the bone away from me.</strong> By doing so, I improve the shape of the cup and prevent the fabric from gathering underneath the bone. Here comes a crucial moment! People often complain about their cups being totally flat. It's a common mistake. This happens when you let the fabric ease in underneath the bone that goes along the bust line. It makes the cup smaller in size and it goes all flat. I push the ends of the bone underneath the perimeter bones.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And I make another row of parallel stitches to secure the central bone.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I lay straight, ungathered bones along the princess seams of the cups and sew each one on with two rows of stitches.</strong> I push the ends of these bones underneath the perimeter bones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Next, I add the straight inclined bones inside the cups (16, 7, 12, and 8). All of them are secured with two rows of parallel stitches, too.</strong> I hide the ends of the bones. Remember to make bar tacks at the beginning and at the end of the stitch. And make sure the fabric settles evenly and does not get gathered underneath the bones.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Plus-size/corset%20dress%20with%20sleeves/transcript/Tutorial%2010_files/image038.jpg" class="img-responsive center-block"></p>
