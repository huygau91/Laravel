<p><strong>I lay an arched, gathered bone in the armscye area.</strong> In this case, I do ease in the fabric to ensure better fitting to the body. I trim the bone 1.5-2cm from the side seam. That space will allow you to adjust the fit of the garment if necessary.</p>
