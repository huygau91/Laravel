<p><h9>Tutorial 9. Summing Up the Costs of Petticoat Production. </h9> </p>
<h2>Taking Measurements for the Face of the Skirt.</h2>
<p></p>
<p><strong>Now that the petticoat is ready, let me present a list of used materials with all costs and quantities. </strong></p>
<p><strong>I have used the following materials for the petticoat: </strong></p>
<p>- Hard mesh fabric - 6m. I actually cut thirty 160cm-long strips for the ruffles, i.e. the initial strip which I gathered with a 4:1 ratio was 48 meters long. </p>
<p>- Bias tape - 52m;</p>
<p>- Rigilene boning - 10m;</p>
<p>- Knit fabric for the mini-skirt - 0.5m;</p>
<p>- Elastic band - 0.6m;</p>
<p>- Factory-made petticoat - 1 piece;</p>
<p><strong>All these materials cost me about 1,140 RUR (wholesale). </strong></p>
<p>I don�t know how much the same materials cost in your region or country but <strong>the final price of the dress is supposed to include the cost of the petticoat </strong>in any way. </p>
<p><strong>A factory-made petticoat for a skirt with a train is not much more expensive but its quality leaves much to be desired and so will the shape of your skirt! </strong>It will look like that skirt from the bridal salon. </p>
<p><strong>I have finished working on the petticoat and now I can move directly to the face of the skirt. Let me take some additional measurements before we start discussing the cutting process. </strong></p>
<p><strong>I will use the following measurements already used in the making of the petticoat:</strong></p>
<p>- Waist to Floor (109cm);</p>
<p>- Mini-Skirt Bottom Circumference (100cm). </p>
<p><strong>There are two ways you can take necessary measurements for sewing the face part of the skirt. </strong></p>
<p><strong>Method 1.</strong> If the client can come to my atelier, I ask her to put on the top part of the dress and the petticoat. The top part does not have to have been decorated by this stage. But the petticoat plays a great role in the process of taking measurements because they largely depend on its size and volume. </p>
<p><strong>Method 2.</strong> I put the top part of the dress and the petticoat on the dress-form and take all necessary measurements. For the sake of accuracy, I make a row of stay-stitches 2cm from the bottom edge of the top part of the dress. It will be my guideline in all calculations for cutting the face of the skirt. </p>
<p><strong>I have put the top part and the petticoat on the dress-form.</strong> They fit together perfectly. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%209.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And I start taking all necessary measurements. </strong></p>
<p><strong>Front Skirt Length. </strong></p>
<p>I measure the distance from the above-said row of stay-stitches to the floor level along the centre of the garment. The measuring tape must cling tight to the petticoat fully repeating its shape including the ruffles. <strong>I got 83cm. </strong></p>
<p><strong>Side Skirt Length. </strong></p>
<p>I turn the dress-form and take the same measurement only this time along the side seam of the petticoat. <strong>It is 87cm in my case.</strong></p>
<p><strong>Back Skirt (Train) Length. </strong></p>
<p>I measure the distance from the same row of stay-stitches to the floor level along the centre of the train. The measuring tape clings tight to the garment. <strong>I got 92cm. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%209.files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%209.files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%209.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I need some more additional measurements to cut the face of the skirt more precisely. </strong></p>
<p><strong>I measure the distance between the back princess seams on the top part of the dress. It is 25cm. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%209.files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I also want to know what this distance will be if I visually prolong these princess seams down to the bottom of the skirt. </strong>In order to find out, I put the end of the measuring tape to the back princess seam and let it go down the petticoat with a slight shift to the side. I place a pin at the intersection with the bottom edge of the skirt. </p>
<p><strong>And I do the same for the other princess seam.</strong></p>
<p><strong>I measure the distance between the princess seams at the bottom of the skirt. It is 110cm. I also write down how far from the top edge of the petticoat this measurement was taken (70cm). </strong></p>
