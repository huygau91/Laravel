<h1>Tutorial 4. Cutting the Petticoat Cover.</h1>
<p><strong>First of all, let me show you schematically how I will cut the petticoat cover. </strong></p>
<p>I fold the mesh fabric in half along the lengthwise grain. The width of the lay is 83cm (which is enough in this particular case). The fold line is on the left.</p>
<p><strong>I start by drafting the front half of the cover. </strong></p>
<p>I mark 2cm down the fold line (point A).</p>
<p>Then I measure 26.2cm from point A along the top edge of the fabric and mark point B. This distance is the top quarter-circumference with a seam allowance included. I connect points A and B in an arc.</p>
<p>Next, I measure 57.5cm from the same point A down the fold line and mark point C. This distance is the calculated front cover length. I draw a line perpendicular to the fold line through point C, measure the bottom quarter-circumference along it (81.2cm), and mark point D.</p>
<p>I move point D down so as to make section B-D 65.5cm (side cover length).</p>
<p>I connect points C and D in an arc.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image002.jpg" alt=""></p>
<p><strong>Now I can draft the back half of the cover. </strong></p>
<p>I measure 2cm down the fold line and mark point E.</p>
<p>Then I measure 26.2cm from point E along the top edge of the fabric and mark point F. The distance is the top quarter-circumference with a seam allowance. I connect points E and F in an arc.</p>
<p>I measure 73.5cm from the same point E down the fold line and mark point G. The distance is the calculated cover train length. I draw a line perpendicular to the fold line through point G, measure the bottom quarter-circumference along it (81.2cm), and mark point K.</p>
<p>I connect points F and K in a straight line, measure the side cover length along it (67.5cm), and mark point H.</p>
<p>I connect points G and H in an arc.</p>
<p>Please note that cutting the petticoat cover does not require as much accuracy as cutting the face of the skirt.</p>
<p><strong>Now I can transfer the draft directly on the hard mesh fabric. </strong>The fabric is folded in half along the lengthwise grain.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image004.jpg" alt=""></p>
<p><strong>I draw the front half of the cover. </strong></p>
<p><strong>I came across some very curious divergences from the planned layout when I was transferring it on the fabric. </strong>Since you might get faced with the same issues, I am glad to talk about them in advance.</p>
<p><strong>My client is not tall. Taking that into account, I provided for a relatively short train of just 130cm. </strong>After I marked the actual front length of the cover on the fabric (57.5cm) and started drawing the B-D section corresponding with its side length (65.5cm), the end of this section (point D) turned out to be positioned above the perpendicular line going through point C. I was supposed to connect points C and D in an arc. But in this case, the actual bottom edge of the front half of the cover would be positioned higher than indicated by the perpendicular line. The shape of the cover would therefore be distorted. The end of the side seam (point D) must either fall upon the perpendicular line itself or below this line. Only in this case will the shape of the cover and the entire skirt be correct!</p>
<p><strong>Let me show you what solution I found. </strong></p>
<p><strong>I prolonged section B-D corresponding with the side length of the cover down to the end of the perpendicular line going through point C. I had to add 12cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image006.jpg" alt=""></strong></p>
<p><strong>By doing so, I increased the side cover length by 12cm: </strong></p>
<p><strong>65.5cm + 12cm = 77.5cm</strong></p>
<p><strong>When I start drawing the back half of the cover on the fabric, I will need to account for this change. </strong></p>
<p>After studying the resulting shape of the front half of the cover, <strong>I decided it would make sense to increase the top quarter-circumference by 8cm:</strong></p>
<p><strong>26.2cm + 8cm = 34.2cm</strong></p>
<p>When joining it with the top part of the petticoat, I will bring it to 26.2cm along the side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image008.jpg" alt=""></p>
<p><strong>I transfer the drafted back half of the petticoat cover on the mesh fabric. </strong>I draw it just the same way I did on the writing board, only this time with account for the changes in the measurements.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image010.jpg" alt=""></p>
<p>I re-draw the adjusted top edge (section E-F1) with a length of 34.2cm.</p>
<p>I mark the cover train length down the fold line (section E-G, 73.5cm).</p>
<p>Then, I draw a line perpendicular to the fold line through point G, measure the bottom quarter-circumference along it (81.2cm), and mark point K.</p>
<p>I connect points F1 and K in a straight line, measure the adjusted side cover length (77.5cm), and mark point H.</p>
<p>I connect points G and H in an arc drawing the train line.</p>
<p><strong>Examining the results, I can see that </strong>the train is going to be really short because of the increased side cover length. It won�t look beautiful this way.</p>
<p><strong>I re-draw the train (arc H-G1) and find that its adjusted shape requires adding 11cm to the cover train length. </strong></p>
<p><strong>The new cover train length is: </strong></p>
<p><strong>73.5cm + 11cm = 84.5cm</strong></p>
<p><strong>I cut the adjusted back half of the petticoat cover. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image012.jpg" alt=""></strong></p>
<p><strong>I apply all relevant changes to the draft on the writing board. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%204.files/image014.jpg" alt=""></p>
<p><strong>Since I have made the train longer on the cover, I must also adjust its length on the skirt itself. </strong></p>
<p><strong>I will make it 150cm. </strong>I have decided on this length because the train on the petticoat cover must be at least 20-30cm shorter than on the skirt.</p>
<p><strong>The length of the train is measured from the waistline on the skirt. I subtract Mini-Skirt Length from it (36cm) and get the following train length: </strong></p>
<p><strong>150cm - 36cm = 114cm,</strong></p>
<p><strong>i.e. 30cm longer than the train of the petticoat cover. </strong></p>

