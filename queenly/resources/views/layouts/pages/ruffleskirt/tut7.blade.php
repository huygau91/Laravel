<p><strong>I secure the second horizontal bone with two parallel rows of stitches.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image032.jpg" alt=""></p>
