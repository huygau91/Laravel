<h1>Tutorial 6. Reinforcing the Train.</h1>
<p><strong>I start putting perimeter bones on the piece securing them with a single row of stitches made along the outer edge. </strong>I seal the ends of the bones with bits of masking tape. If a bone is supposed to settle in an arc, I gather it slightly by pulling the fishing lines until it acquires the desired shape.</p>
