<p><strong>I continue adding the remaining vertical bones left and right of the central bone securing them all with two parallel rows of stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image052.jpg" alt=""></p>
<p><strong>It is hard to turn the piece around with all those bones so I simply go back to the start after making the first row of stitches and make a parallel row along the other edge of the bone. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image054.jpg" alt=""></strong></p>
<p><strong>I alternate between wide and narrow Rigilene bones.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image058.jpg" alt=""></p>
