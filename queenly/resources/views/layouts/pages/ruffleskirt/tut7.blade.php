<p><strong>I secure the next vertical bone. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image066.jpg" alt=""></p>
<p><strong>I have put all Rigilene bones on the piece following the guidelines. </strong></p>
<p><strong>Now I sew the second parallel row of stitches along the inner edge of all perimeter bones. </strong></p>
