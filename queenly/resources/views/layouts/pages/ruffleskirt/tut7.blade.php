<p><strong>And I do the same with the third inner horizontal bone. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image038.jpg" alt=""></p>
<p><strong>Now I can add the remaining vertical bones. </strong>I put them right over the attached horizontal bones and push their ends underneath the perimeter bones.</p>
<p><strong>I start by attaching the central vertical bone with two parallel rows of stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%206.files/image040.jpg" alt=""></p>
