<h1>Tutorial 11. Cutting and Hemming the Face of the Skirt.</h1>
<p><strong>I have cut the face and the lining of the skirt. Each consists of a half-circle skirt and a trapeze piece divided in half by a central back seam. </strong></p>
<p><strong>Three meters of fabric was enough in my case. Both parts of the skirt fit in the lay. </strong></p>
<p>The back princess seam edges on the half-circle piece run parallel to the lengthwise grain, which helps maintain the desired shape of the skirt in the sewing process. Moreover, the half-circle piece will form beautiful folds at the front which will settle perfectly upon the bottom hoop of the petticoat.</p>
<p>The central part of the gore is also cut along the lengthwise thread to preserve the graceful shape of the train.</p>
<p><strong>It is another reason why I don�t cut fishtail skirts as a single half-circle piece with a train. </strong></p>
<p><strong>I have assembled the lining of the skirt, serged the edges of the seam allowances, and finished the hem with a rolled seam. I have also made a row of stitches along the top edge so I can gather it down to the required size to match the face of the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image002.jpg" alt=""></strong></p>
<p><strong>I have sewn the central back seam on the face fabric and serged the princess seam edges. </strong></p>
<p><strong>Now I need to finish the hem of the face parts of the skirt (both the half-circle piece and the trapeze). </strong></p>
<p>I will also reinforce the train.</p>
<p><strong>The hem of the main skirt will be finished with soft Rigilene boning. </strong></p>
<p><strong>Soft Rigilene boning needs some treatment before you can start sewing it on. </strong></p>
<p>Considering that the hem of the skirt is arc-shaped, the soft Rigilene boning must be given the same shape to make it look neat and pretty.</p>
<p><strong>I start carefully shaping the soft Rigilene boning tape by pressing it. </strong>It is very pliable and acquires the desired shape easily under high temperature. Now the boning tape will settle along the bottom edge of the skirt very smoothly, with no waves. There will be no need in pressing the hem thoroughly afterwards. <strong>Do keep that in mind! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image006.jpg" alt=""></p>
<p><strong>I put the prepared Rigilene boning tape along the bottom edge of the trapeze piece, over the sheen side (the face). Its outer edge is supposed to be positioned 1.2cm above the bottom edge of the piece. And I stitch right along the edge of the boning tape </strong>without pulling it tight or easing in.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image008.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image014.jpg" alt=""></p>
<p><strong>I fold in the bottom edge of the trapeze piece along the top edge of the boning tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image016.jpg" alt=""></p>
<p><strong>You will probably feel like securing the folded boning tape on the inside by stitching right along the previously made seam and then along the top edge of the tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image018.jpg" alt=""></p>
<p><strong>But this will leave an additional thin row of stitches on the face of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image020.jpg" alt=""></p>
<p><strong>I will not do this. Instead, I will show you a different way of securing your soft Rigilene boning tape without having to make an additional row of stitches along the hem of the skirt. </strong>Of course, a thin row of stitches along the hem would look quite acceptable on the satin I am using for the face of my skirt. It would be completely unacceptable on veiling or other similar fabric. Veiling is see-through, which makes it highly recommended to use as few seams as possible, especially if the garment features decorative ruffles.</p>
<p>And so the offered method of finishing the hem with soft Rigilene boning is particularly great for veiling skirts where you want to keep the hem perfectly clean!</p>
<p><strong>I fold the attached boning tape to the inside, carefully press the arc-shaped hem of the skirt, and secure it with pins. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image024.jpg" alt=""></strong></p>
<p><strong>I start stitching along the top outer edge of the boning tape. </strong>This means there will be only one seam on the face of the skirt at a width of the boning tape from the edge. And this seam will also be overlaid by the veiling ruffles placed at the same level. I would like to stress it out again that this method is a great help in the making of veiling skirts in which a clean hem is very important!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image032.jpg" alt=""></p>
<p><strong>Remember to thoroughly press the reinforced hem of the skirt from the face side to make it smooth, even, and neat. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2011.files/image034.jpg" alt=""></strong></p>
<p><strong>The hem of the front half-circle face part of the skirt will be finished the same way. </strong></p>

