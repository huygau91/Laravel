<h1>Tutorial 5. Marking Guidelines for Putting Bones on the Cover.</h1>
<p><strong>I have cut the front and the back of the petticoat cover. </strong></p>
<p><strong>I will reinforce the back half with Rigilene bones</strong> to enhance the shape of the skirt and eliminate that depression which you saw at the back of the skirt from the bridal salon.</p>
<p><strong>Boning guidelines are quite simple as you can see in the draft below. </strong></p>
<p>What you see here is the back half of the petticoat cover folded in half along the lengthwise grain.</p>
<p><strong>There will be a central vertical bone going along the fold line. </strong></p>
<p><strong>Then I will divide the top and the bottom edges of the cover in five equal sections and draw guidelines for inclined vertical bones. </strong>The intervals between the bones will be about 5cm at the top and about 10cm down the bottom.</p>
<p><strong>Next, I will draw guidelines for horizontal bones. </strong>I will step 10cm from the bottom edge of the petticoat and draw an arc-shaped guideline for the bottom horizontal bone.</p>
<p><strong>And then I will draw three more horizontal guidelines at 20cm intervals. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%205.files/image002.jpg" alt=""></p>
<p><strong>I will put the bones on the petticoat the same way I would put them on a corset. </strong>First, I will put bones along the perimeter of the piece securing them along the outer edge. Then, I will add all inner bones securing them with two parallel rows of stitches and pushing their ends underneath the perimeter bones. And, finally, I will secure the perimeter bones with a row of stitches along the other edge.</p>
<p><strong>Useful tip: </strong>do not place vertical perimeter bones close to the side seams otherwise it will be hard to sew them.</p>
<p><strong>To sum up, </strong>I am using the smallest possible amount of bones required to reinforce the back half of the petticoat cover. Feel free to use more bones if you wish.</p>
<p><strong>I guess now you can see why the petticoat cover is cut as two separate parts and not as a half-circle skirt with a train: </strong>just because I will need to put bones on the back half.</p>

