<h1>Tutorial 7. Putting Ruffles on the Back Half of the Petticoat Cover.</h1>
<p><strong>Here is the back half of the petticoat cover, reinforced with bones and thoroughly pressed. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image002.jpg" alt=""></p>
<p><strong>I carefully press the bottom edge of the front half. It was finished with a bias tape as you remember. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image004.jpg" alt=""></p>
<p><strong>I still need to work on the long strip of mesh fabric prepared for the ruffle: even out the joints, trim the seam allowances, and press them all flat to the same side. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image006.jpg" alt=""></p>
<p><strong>I finish one edge of the strip with a bias tape and press it. </strong></p>
<p><strong>Then I switch to a ruffle presser-foot and start gathering the strip. </strong>My presser-foot produces a 2:1 gathering ratio. I know this is not enough so I will gather the strip twice to achieve a gathering ratio of 4:1. For better results, I slightly slow down the feeding of the mesh fabric by keeping my finger behind the presser-foot.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image010.jpg" alt=""></p>
<p><strong>As I had expected, the ruffle is not gathered thickly at all. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image012.jpg" alt=""></p>
<p><strong>I take the second approach. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image016.jpg" alt=""></p>
<p><strong>Now the ruffle looks perfect. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image018.jpg" alt=""></p>
<p><strong>It is time to put it on the back half of the petticoat cover. </strong></p>
<p><strong>First of all, I need to draw a guideline.</strong> Please note that the ruffle will stay within the reinforced part of the piece between the perimeter bones. It will run parallel to the bottom edge.</p>
<p><strong>The first ruffle will be positioned 5cm above the bottom horizontal perimeter bone (bone 1). </strong></p>
<p>Ruffles between the first ruffle and the third horizontal bone (bone 3) will be put at 5cm intervals.</p>
<p>Ruffles between bones 3 and 4 will be put at 7cm intervals.</p>
<p>Finally, ruffles between bones 4 and 5 (the top horizontal perimeter bone) will be put at 10cm intervals. It is fine if the top ruffle goes beyond bone 5. Just make sure the top perimeter bone is overlaid.</p>
<p><strong>For your convenience, I have numbered the bones on the back half of the cover in the picture below. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image020.jpg" alt=""></p>
<p><strong>There is no need in drawing all guidelines on the piece. It is enough to mark a pencil guideline for the bottom ruffle 5cm above the bottom edge. </strong>The rest of the ruffles will be put parallel to the first one at the specified intervals.</p>
<p><strong>I start putting the bottom ruffle along the guideline 5cm above the bottom edge of the piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image024.jpg" alt=""></p>
<p><strong>I fold the vertical edge at the beginning of the ruffle. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image026.jpg" alt=""></strong></p>
<p><strong>I trim the ruffle and finish sewing as I reach the vertical perimeter bone. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image028.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image030.jpg" alt=""></strong></p>
<p><strong>Without turning the piece, I cut the thread and return to the sewing start point. </strong></p>
<p><strong>I measure 5cm up from the first ruffle and make a mark for putting the second parallel ruffle along it. </strong>I look to the seam holding the bottom ruffle and not to the horizontal bones. When necessary, I take a ruler and check the distance between the seam holding the first ruffle and the one I am currently sewing.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image040.jpg" alt=""></p>
<p><strong>The third ruffle is also put 5cm above the previous one. </strong>Please make sure to keep the seam allowances facing down. The ruffles themselves are supposed to be facing up.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image046.jpg" alt=""></p>
<p><strong>I can fit a fourth bone 5cm above the third one before reaching bone 3. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image050.jpg" alt=""></p>
<p><strong>I mark the required intervals between the bones I am going to work with next. </strong></p>
<p><strong>The fifth ruffle</strong> will be put above bone 3 so I mark 7cm from the seam holding the fourth one. <strong>I will put two more ruffles (6 and 7) at 7cm intervals between horizontal bones 3 and 4. </strong>The seventh ruffle will be positioned above bone 4 and it will overlay it. <strong>I mark 10cm from that ruffle and put the eighth one, then mark another 10cm, and put down the last ninth ruffle. It is positioned above bone 5 and overlays it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%207.files/image052.jpg" alt=""></p>

