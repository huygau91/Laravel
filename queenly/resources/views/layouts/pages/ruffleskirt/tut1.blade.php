<h1>Why a Ruffle Fish Tail Skirt?</h1>
<p><strong>I relied on the following requirements when choosing the skirt style: </strong></p>
<p>- It must be a popular style;</p>
<p>- It must be a luxury style;</p>
<p>- It must be an eye-catcher;</p>
<p>- It must be appropriate for different age groups.</p>
<p>After analyzing a wide range of wedding dresses, relying on my own and my students� experience, <strong>I decided that an elegant fishtail wedding dress is one of the most sought-after types and meets all the above-mentioned criteria. &nbsp;</strong></p>
<p><strong>The fishtail skirt is particularly popular among brides between 20 and 26 years. </strong>Most of them are self-confident girls, rather tall, slender, and well-built.</p>
<p><strong>They say a firm �no" to the princess wedding dress </strong>and don�t want their dress to have a full skirt.</p>
<p><strong>These girls want to demonstrate their beautiful curves on the one hand and preserve the classic image of a princess-bride on the other hand! </strong></p>
<p>For the same reason, <strong>most of them order their wedding dresses in white </strong>or, a little less often, in ivory.</p>
<p><strong>The wedding dress must have a train </strong>and the longer the better.</p>
<p><strong>And, finally, the dress must look luxurious from the top down to the very hem of the skirt. </strong></p>
<p><strong>Brides from this category usually have their wedding at an expensive venue </strong>with a beautifully decorated hall, a beautiful and solemn ceremony, etc. They are ready to pay for all these things and to patiently stand through any possible discomfort associated with this particular dress model for the beauty of their special day!</p>
<p><strong>The know-how given in this book will allow you to sew an elegant fishtail skirt with a perfect fit. </strong></p>
<p><strong>Expect to hear lots of compliments from the bride and from her guests! </strong></p>
<p><strong>I can guarantee that your bride will be the centre of attention in any wedding hall or reception venue! </strong></p>
<p><strong>No one else will have a fabulous skirt like hers! </strong></p>
<p><strong>This course was made to give you detailed guidelines to sewing a fishtail wedding skirt with a train. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image002.jpg" alt=""></strong></p>
<p><strong>What made me choose this particular type of a wedding skirt? </strong>My choice was largely influenced by all those questions from your E-mails related to the sewing process, as well as the shape of the skirt and the train.</p>
<p><strong>Many of you wondered why your fishtail skirt just failed to have a beautiful elegant shape. Many of you asked why one skirt looked flawless and another was far from perfect despite the same cutting process. All these questions inspired me to teach you how to sew a fishtail skirt of a perfect shape. </strong></p>
<p><strong>There is only one answer to the key question about the shape of the skirt: </strong>only a well-prepared petticoat can turn your skirt into a high-quality luxury garment and make it stand out among the rest. No matter how you decorate the skirt, no matter how you cut it and design it, it will never look perfect with a poorly made inner construction!</p>
<p><strong>Let us look at two pictures so I can prove my point. </strong>The picture on the left shows a ready-made fishtail dress from a bridal salon. It was sewn in the commonly used technique. The picture on the right shows the same kind of a dress sewn in the technique I want to share with you.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image004.jpg" alt=""></p>
<p><strong>One does not have to be a professional to notice the difference between the two skirts. </strong>The skirt from the bridal salon looks boring, it does not have enough volume, it has a poor shape, and the bottom diameter is too small. I have to say it looks funny! You may be wondering how it happens when both skirts are cut just the same way. <strong>It is all about the petticoat. </strong></p>
<p><strong>Look at the next two pictures with the side view of the same skirts. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image006.jpg" alt=""></p>
<p><strong>Can you see how much better the skirt on the right looks compared to the skirt from the bridal salon? </strong>There is a large depression at the back of the dress where the skirt joins the top part. This is far from pretty!</p>
<p><strong>And this negative effect is caused by the shape of the petticoat. </strong>The bride in the salon dress is wearing a standard factory-made petticoat with one hoop at the bottom.</p>
<p><strong>Let me show you schematically what happens to the shape of the skirt when you use a standard petticoat like this. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image008.jpg" alt=""></p>
<p><strong>There is hollow space between the petticoat (red outline) and the beautiful contour of the skirt. </strong>Since the fabric of the skirt is not supported by anything, it will simply settle upon the petticoat and acquire its shape. This is where that funny shape comes from!</p>
<p><strong>There is only one possible conclusion: </strong>you must never use a petticoat with a single hoop for a luxury skirt like this, especially when it features a train! A skirt like this requires a special petticoat that will support its beautiful shape. The fabric of the skirt is supposed to rest upon a fixed inner construction! The first half of this book is dedicated to making a custom petticoat for this purpose.</p>
<p><strong>I would like to emphasize that this book teaches you how to sew a beautiful fishtail skirt <em>with a train</em>. </strong></p>
<p><strong>I am sure the following problem has popped up at least once in your sewing career. </strong>You have achieved a great shape of the skirt and added a gorgeous train to it. The bride looks amazing in her dress when she is standing up posing for a picture with the train spread out neatly across the floor. But as soon as she moves to change the pose, let alone to spin around as if in a dance, the train starts to behave very strange: not like a prolongation of the skirt but like a separate piece that has nothing to do with the skirt itself. What is going on? Not only does the train refuse to move in sync with the skirt, on the contrary, it starts twisting and curling under the skirt. Nothing is left of the initial beauty and elegance! Most ready-made wedding dresses behave exactly this way. Girls buy them, get married in them, and think it is normal.</p>
<p><strong>But I will show you </strong>how a good petticoat allows the bride to move, spin around, and dance in her wedding dress as much as she wants with the skirt and the train moving in sync with her and looking like one whole all the time.</p>
<p><strong>The pictures below prove that I am right! </strong></p>
<p><strong>The bride is spinning around and the skirt with the train move together with her. </strong></p>
<p><strong>I will teach you how to achieve the desired effect! </strong></p>
<p><strong>WARNING: the offered technique for making a petticoat and a skirt does not suit for skirts with a smooth undecorated surface. </strong>Your skirt must be decorated with ruffles, falbala, swirls, petals, or anything else of the kind. The technique explained in this book is not applicable for a skirt with a layer of quality satin as the face. <strong>It is a topic for a whole separate book. </strong></p>
<p><strong>What other useful things will you learn? </strong></p>
<p>- I will teach you how to make ruffles and put them on the skirt without any seam allowances;</p>
<p>- I will teach you how to cut the base of the skirt together with a beautiful train;</p>
<p>- You will learn how to give the train a graceful shape and decorate it with cascading ruffles.</p>
<p><strong>All these things are the answers to some of the most frequently asked questions from your E-mails. </strong></p>
<p><strong>Let me briefly talk about the materials I will use for this skirt. </strong></p>
<p><strong>Knit lining fabric. </strong>It is white, elastic, and see-through.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image010.jpg" alt=""></p>
<p><strong>Lining fabric. </strong>It is wedding satin fabric or crepe-back satin.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image012.jpg" alt=""></p>
<p><strong>Thick satin for the base of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image014.jpg" alt=""></p>
<p><strong>Hard mesh fabric. </strong>I will use it for the petticoat.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image016.jpg" alt=""></p>
<p><strong>Veiling.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Why%20a%20Mermaid.files/image018.jpg" alt=""></p>
<p><strong>Rigilene boning.</strong> I will use 12mm and 7-8mm boning.</p>
<p><strong>Soft Rigilene boning. </strong>I will use 3-4mm boning.</p>
<p><strong>Bias tape. </strong></p>
<p><strong>Factory-made petticoat with seven hoops. </strong></p>
<p>You will find the exact quantities of all used materials and the approximate costs of production in a financial analysis presented at the end of the book.</p>

