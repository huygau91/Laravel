<h1>Tutorial 15. Putting Ruffles on the Front Half of the Skirt.</h1>
<p><strong>I start putting ruffle (1) on the skirt aligning the gathering stitches with the guideline. </strong>I start from the stay-stitching made along the back princess seam edge. Remember to fold the end of the ruffle.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image004.jpg" alt=""></p>
<p><strong>I trim the ruffle upon reaching the other back princess seam edge, fold the end, and finish sewing next to the row of stay-stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image010.jpg" alt=""></p>
<p><strong>The first half of the second (short) ruffle is secured along the guideline in the same manner. </strong>I start from the stay-stitching made along the back princess seam edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image012.jpg" alt=""></p>
<p><strong>I reach the intersection with ruffle (1), trim ruffle (2), and finish sewing its first half on the skirt. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image016.jpg" alt=""></p>
<p><strong>I shift the piece accordingly and start sewing the other half of ruffle (2) on the skirt moving past the intersection with ruffle (1). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image018.jpg" alt=""></p>
<p><strong>I reach the other back princess seam edge, trim the ruffle, fold the end, and finish sewing it on at the level of the stay-stitching. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image020.jpg" alt=""></p>
<p><strong>I have put two bottom ruffles on the front half-circle skirt piece: a long one (1) and a short one (2). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image022.jpg" alt=""></p>
<p><strong>We can analyze the first results at this stage. </strong></p>
<p><strong>Please note </strong>that the ruffles stick up at about 45 degrees from the surface of the skirt. I like this effect for making the skirt �fluffy." If you want your skirt to be smoother, then you should fold down the top half of the attached ruffle and secure it with a row of stitches made literally 2-3mm from the fold line. The seam will be very neat and, besides, it will be overlaid by the next ruffle.</p>
<p><strong>Despite the early stage, I have already noticed a quite serious disadvantage: you can see the background fabric through the ruffles. </strong>I find it absolutely unacceptable because the skirt will never look like a luxury garment this way.</p>
<p><strong>In order to fix this, I will make double-layer ruffles. This means I will use two strips of veiling instead of one. </strong>The gathering technique will stay the same. As the result, each ruffle will be made of four layers of veiling, i.e. it will be twice as thick as before. I won�t rip off the two ruffles already secured on the skirt. Instead, I will simply put a pair of identical ruffles right over them.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image024.jpg" alt=""></p>
<p><strong>I have put all double-layer ruffles on the front half of the skirt. </strong>I am happy with the results now because the background fabric no longer shows through. The short ruffles have settled on the skirt very well. It looks great!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image026.jpg" alt=""></p>
<p><strong>This is what the half-circle piece looks like from the inside. </strong>The skirt may have slightly shrunk lengthwise because of the seams holding the ruffles but I had accounted for this effect by positioning the bottom ruffle so as to keep its bottom edge 2cm above floor level. You can always shorten it if needed.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2015.files/image028.jpg" alt=""></p>
<p><strong>The front half of the face of the skirt is finished. </strong></p>

