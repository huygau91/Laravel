<h1>Tutorial 10. Making a Pattern of the Face Part of the Skirt.</h1>
<p><strong>Before we start drafting the face of the skirt, I would like to remind you what measurements I am going to rely on and make a schematic drawing of a half of the skirt. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2010.files/image002.jpg" alt=""></strong></p>
<p><strong>I need the following measurements:</strong></p>
<p><strong>- Waist to Floor (109cm). </strong>This measurement is used to adjust the height of the dress-form correctly and ensure perfect accuracy of all other necessary measurements.</p>
<p><strong>- Mini-Skirt Bottom Circumference - 100cm.</strong> In other words, it is the length of the top edge of the face part of the skirt.</p>
<p><strong>- Front Skirt Length - 83cm.</strong></p>
<p><strong>- Side Skirt Length - 87cm.</strong></p>
<p><strong>- Back Skirt (Train) Length - 92cm.</strong></p>
<p><strong>- Back Princess Seam to Back Princess Seam (Top Part) - 25cm.</strong></p>
<p><strong>- Back Princess Seam to Back Princess Seam (Skirt) - 110cm. This measurement was taken 70cm below the top edge of the skirt. </strong></p>
<p>I have marked the middle of the front, the middle of the back, as well as front and back princess seams on the schematic drawing of the top part of the dress. I have also prolonged the back princess seam line down to the bottom of the skirt.</p>
<p><strong>And now let me tell you how I am going to cut this skirt.</strong></p>
<p><strong>The face of the skirt will be made of two individually cut parts or halves: </strong>the first half (1) is the front of the skirt limited by the back princess seams, and the second half (2) is the back of the skirt limited by the same back princess seams.</p>
<p><strong>I am convinced it would be a great mistake to cut a fishtail skirt with a train based on a half-circle skirt pattern. </strong></p>
<p>Of course, you can cut a plain skirt with a train which starts at the waist as a whole half-circle piece with an integrated train. <strong>But a fishtail skirt with a train</strong> is supposed to start well below the waistline. The top radius of your half-circle piece will be too large because of the big circumference of the top edge of the skirt (100cm in our case). The pattern will never fit into the width of your fabric with a large radius like that plus a train. Besides, there are certain advantages to cutting a fishtail skirt with a train in two separate parts. I will tell you about them later, after the cutting.</p>
<p><strong>So my skirt will be made of two parts joined by two seams going along the back princess seam lines. </strong></p>
<p><strong>The first part (1) will be cut as a regular half-circle skirt. </strong></p>
<p><strong>And the second part (2) will be cut as a trapeze. </strong>It can be made of two parts joined by a central seam: in this case, you will spare up to about 1m of fabric. The central seam will be disguised by the décor put on the skirt. You can also cut a whole trapeze piece. Regardless of the way you do it, the central vertical axis will run parallel to the lengthwise grain.</p>
<p><strong>Let me do the calculations for cutting the half-circle piece (1). </strong></p>
<p><strong>I need to know the circumference of the top part of the skirt (i.e. waist circumference). I will name it C (1). </strong>In this particular case, it is the length of the arc going across the front of the skirt from one back princess seam to the other. You will find this value by subtracting Back Princess Seam to Back Princess Seam (Top Part) from Mini-Skirt Bottom Circumference:</p>
<p><strong>C (1) = 100cm - 25cm = 75cm</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2010.files/image004.jpg" alt=""></p>
<p><strong>There is a fixed formula for working with half-circle skirts: </strong></p>
<p><strong>πR = Waist Circumference</strong></p>
<p><strong>Let us find the radius of the skirt:</strong></p>
<p><strong>πR = 75cm</strong></p>
<p><strong>R = 25cm</strong></p>
<p><strong>It is easy to draw a half-circle skirt knowing the radius and the lengths. </strong></p>
<p><strong>And it is exactly the length measurements of the face part of the skirt that I would like to talk about now. </strong>After examining the draft one more time, I can tell for sure that its train will be very short. There is only 9cm difference between the front length of the skirt and the length of the train.</p>
<p><strong>92cm - 83cm = 9cm</strong></p>
<p><strong>I have decided to extend the train by adjusting the length of the skirt. </strong>The finished petticoat will stay unchanged. If I notice that it does not hold the shape of the skirt well enough, I will add another ruffle at the bottom.</p>
<p><strong>The skirt is 83cm long at the front.</strong> This length was measured down to the floor level. And it is a very important measurement because it defines the length of the dress. The rest of the dress, starting basically from the side seams, will drag along the floor. The front length of the skirt may be limited to the level of the bottom ruffle. I have measured the distance from the bottom edge of the ruffle to the floor along the front on the dress-form and got 4cm. This means I can shorten the front length by 4cm and thereby increase the difference between the front skirt length and the train length.</p>
<p><strong>The adjusted Front Skirt Length will be 79cm. </strong></p>
<p><strong>83cm - 4cm = 79cm</strong></p>
<p><strong>I will add 20cm to the initial Front Skirt Length and make it 107cm. </strong></p>
<p><strong>87cm + 20cm = 107cm</strong></p>
<p><strong>I will add 38cm to the initial Back Skirt (Train) Length and make it 140cm. </strong></p>
<p><strong>92cm + 38cm = 140cm</strong></p>
<p><strong>I need to find another measurement: Skirt Length along Princess Seam. </strong></p>
<p>This measurement can be calculated as the mean value between the side length and the back (train) length. <strong>It is about 120cm. </strong></p>
<p><strong>(107cm + 140cm) / 2 ≈ 120cm</strong></p>
<p><strong>I start drafting the lay. </strong></p>
<p><strong>I fold the fabric in half. Its width along the fold line is 150cm. </strong></p>
<p><strong>I measure the calculated radius of 25cm along either edge of the fabric starting from the top of the fold line (point A). I mark points B and D and connect them in an arc.</strong></p>
<p><strong>Then I measure 79cm (Front Skirt Length) along the fold line and mark point C. </strong></p>
<p><strong>I measure 120cm (Skirt Length along Princess Seam) from point F along the selvage of the fabric and mark point G.</strong></p>
<p><strong>Next, I need to draw the bottom arc of the skirt (section C-G). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2010.files/image006.jpg" alt=""></p>
<p><strong>In order to draw a beautiful smooth bottom line, I divide the B-A-F angle in half and mark 99.5cm along the bisector (section H-J). </strong>This length is the mean value between Front Skirt Length and Skirt Length along Princess Seam.</p>
<p><strong>(79cm + 120cm) / 2 = 99.5cm</strong></p>
<p><strong>I draw a beautiful smooth arc through points C, J, and G. </strong></p>
<p><strong>The pattern of the front half of the face is ready. </strong>I will need to account for a 2cm seam allowance in the cutting to join the two halves along the princess seam line, as well as for seam allowances along the top and the bottom edges.</p>
<p><strong>I start drawing the other half of the face on the same lay of fabric (2). </strong>It will be trapeze-shaped piece made of two parts joined by a central seam.</p>
<p><strong>I mark section E-D up from the bottom point of the fold line (point E). This section equals 1/2 Back Princess Seam to Back Princess Seam (Top Part) plus seam allowances for the central back seam and the back princess seam. </strong></p>
<p><strong>25cm / 2 + 1.2cm + 1.2cm = 15cm</strong></p>
<p><strong>Then I measure the back (train) length from the same point E along the bottom edge of the fabric, add another 2cm for finishing the bottom edge of the train, and mark point N. </strong>This is the centre of the back, cut strictly along the lengthwise thread.</p>
<p><strong>130cm + 2cm = 132cm</strong></p>
<p><strong>I draw the side of the trapeze with a length of 120cm (section D-L). </strong>It is the side of the second part of the skirt which will be joined with the side of the first part (section F-G).</p>
<p><strong>In order to draw this line correctly, I will use the additional Back Princess Seam to Back Princess Seam (Skirt) measurement taken 70cm below the top edge of the skirt. </strong></p>
<p><strong>I mark 70cm along the bottom selvage (section E-M). </strong></p>
<p><strong>Then I draw a line perpendicular to point M and mark along it section M-K equal to ½ Back Princess Seam to Back Princess Seam (Skirt), or 55cm. </strong></p>
<p><strong>I draw a straight line through points D and K and mark along it section D-L with a length of 120cm. </strong></p>
<p><strong>I connect points N and L in a smooth arc and draw the bottom arc of the second part of the skirt. </strong></p>
<p>With a compact layout like this, I will need about 150cm of fabric folded in half to cut the face of the skirt. In other words, I will need 3 meters of fabric with a width of 150cm.</p>
<p><strong>This compact layout, however, is only possible if both parts of the skirt fit in the width of fabric (150cm). </strong>They may overlap if you are sewing a skirt of a larger size. If such were the case, I would shift the E-D section along the bottom selvage so as to make the D-L section and the C-J-G arc intersect.</p>
<p><strong>This does increase fabric consumption but I can assure you that even a really large skirt will not require more than 4 meters (2 meters for each half). </strong></p>
<p><strong>I will simultaneously cut the face and the lining fabric. And then I will adjust the lining to make it 5cm shorter. </strong></p>
<p><strong>I will use satin as the main fabric. The sheen side will be the face. </strong></p>
<p><strong>I fold the main fabric in half, put it on the cutting table, and put the lining fabric on top of it. The lining fabric is folded in half just the same way. </strong></p>
<p><strong>I pin the fabrics together along the fold line, draw the pattern on the lining fabric, and cut the pieces out. </strong></p>
<p>Then I shorten the lining by 5cm.</p>
