<h1>Tutorial 17. Joining the Skirt, the Lining, and the Top of the Dress.</h1>
<p><strong>The face of the skirt is ready. </strong>It is really full, luxurious, and beautiful!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image002.jpg" alt=""></p>
<p><strong>I need to make certain preparations before joining it with the lining and the top part of the dress. </strong></p>
<p><strong>I turn the skirt wrong side out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image004.jpg" alt=""></p>
<p><strong>And I put the top part of the dress in it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image006.jpg" alt=""></p>
<p><strong>I align the princess seams of the pieces, gather the top edge of the skirt down to the right size, and pin it to the rest of the dress along the full circumference, carefully redistributing the fabric. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image012.jpg" alt=""></strong></p>
<p><strong>Then I put the lining of the skirt inside </strong>and pin it face side to the wrong side of the top.</p>
<p><strong>In this case, the seam allowances of the lining will face the inside of the main skirt after I turn it right side out. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image016.jpg" alt=""></p>
<p><strong>First I align the princess seams and then redistribute and re-pin the top edge of the lining.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image024.jpg" alt=""></strong></p>
<p><strong>I sew all three pieces together with a single seam made with a 2cm seam allowance. </strong>I trim the zipper and − the dress is nearly finished!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2017.files/image026.jpg" alt=""></p>

