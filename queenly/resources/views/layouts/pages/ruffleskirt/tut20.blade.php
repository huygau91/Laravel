<h1>Tutorial 19. Decorating the Train.</h1>
<p><strong>In this tutorial I will decorate the train of the skirt additionally. </strong></p>
<p><strong>The d�cor will be made of circles cut from veiling and put together in a cascade. </strong>Their diameters depend on the width of the veiling. It usually has a width of 3m and is folded two times, i.e. there are four layers, each with a width of 75cm. That means one lay of veiling is enough for cutting two circles with a radius of 75cm or a diameter of 150cm.</p>
<p><strong>I put the veiling folded two times on the table, mark guidelines on it, and cut a rectangle with a size 150cm x 75cm. And then I do the same thing one more time. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image002.jpg" alt=""></strong></p>
<p><strong>I fold both rectangles in a triangle, and mark and cut four circles with a radius of 75cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image012.jpg" alt=""></strong></p>
<p><strong>I usually make these circles from leftover scraps of veiling. </strong></p>
<p><strong>The edges of all circles need to be finished with bias tape. </strong>Actually, there are many ways you can finish them. You can use lace trimmings, create a simple pencil edge, finish the edge with soft Rigilene boning following the method described previously in this book and then overlay it with a wide strip of lace, etc.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image014.jpg" alt=""></p>
<p><strong>I press the circle after finishing its edges with bias tape, find the crosswise grain on it, and put it on the table with the diameter line positioned horizontally. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image018.jpg" alt=""></p>
<p><strong>Then I find the crosswise grain on the second circle of veiling and put it on top of the first circle: first aligning their diameter lines and then shifting it against it by 10-20cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image024.jpg" alt=""></strong></p>
<p><strong>Then, I put down the third and the fourth circle with the same shift in the crosswise grain direction. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image028.jpg" alt=""></p>
<p><strong>I even out the intervals between the circles put on top of each other in a cascade. </strong></p>
<p><strong>I mark a section of 110cm from the bottom edge of the lay along the diameter line. This is 20cm shorter than the train of the skirt. </strong>I put a pin there fixing all four circles on the table.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image030.jpg" alt=""></strong></p>
<p><strong>One by one, I fold the top parts of the circles over a supposed line going through the point marked by the pin. </strong></p>
<p><strong>I try to maintain equal intervals between the bias tape edges of the circles. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image036.jpg" alt=""></p>
<p><strong>I measure the length of the piece once again. It should be 110cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image038.jpg" alt=""></p>
<p><strong>I pin all layers together along the fold line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image040.jpg" alt=""></p>
<p><strong>I turn the piece shorter side down and find its middle.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image046.jpg" alt=""></p>
<p><strong>I mark 25cm along the fold line, left and right of the middle of the piece. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image048.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image050.jpg" alt=""></strong></p>
<p><strong>Then, I stitch along the marked section of 50cm at a presser-foot width from the top edge, gather it thickly on the thread, and secure the gathers with another row of stitches. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image052.jpg" alt=""></p>
<p><strong>The bustle piece is ready! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image054.jpg" alt=""></p>
<p><strong>I secure it at the back of the skirt. </strong></p>
<p>It makes the skirt even fuller and adds particular charm to it. Yet in my opinion it is a bit too much. But you can always use similar pieces in your dresses <strong>or sew bridal veils using the same technique. </strong>Two circles of veiling would be enough for a bridal veil.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2019.files/image056.jpg" alt=""></p>
