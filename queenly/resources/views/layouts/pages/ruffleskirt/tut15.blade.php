<h1>Tutorial 14. Cutting the Ruffles. Marking Guidelines on the Skirt.</h1>
<p><strong>I need to cut on the cross strips of veiling, each with a width of 25cm. </strong>I cover the cutting table with a piece of fabric and draw vertical guidelines on it at 25cm intervals.</p>
<p>I unroll the veiling (it is usually folded in half two times), even out the edges, and cut it according to the guidelines. The length of each strip equals the width of the veiling (3m).</p>
<p><strong>I can cut five 25cm-wide strips of veiling at a time. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image002.jpg" alt=""></p>
<p><strong>I remove the prepared strips, unroll the veiling further, and continue cutting. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image008.jpg" alt=""></p>
<p><strong>I press the lay if necessary so I don�t have to press each strip separately later. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image010.jpg" alt=""></strong></p>
<p><strong>I have prepared a certain amount of 25cm-wide strips of veiling and now I will gather them with the sewing machine. </strong></p>
<p><strong>I will use a special-purpose ruffler presser-foot. </strong>I have tried different types of ruffler presser-feet and found the one in the picture below to be the best. I will stitch at the longest stitch length.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image012.jpg" alt=""></p>
<p><strong>There is a good way to speed up the process. </strong>I fold the strip in half lengthwise, find the middle, and insert the sewing machine needle right there. Then I unfold the strip and check what number of the ruler on the extension table is found on either side of the strip. In my case, the right edge of the strip passes along the 600 mark and the left edge - along the 350 mark. Thus, when gathering the strip, I will always keep it between 350 and 600. This will ensure that the row of gathering stitches will go strictly along the middle of the strip. The task does not require 100% accuracy though: there will be no disaster if you happen to slightly deviate to the side at some point.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image016.jpg" alt=""></p>
<p><strong>I start gathering the first strip of veiling. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image018.jpg" alt=""></p>
<p><strong>It looks just great. The gathering ratio is about 3:1. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image020.jpg" alt=""></strong></p>
<p><strong>After I put it on the skirt, the top half will fall upon the bottom half. It will look lovely. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image022.jpg" alt=""></strong></p>
<p><strong>When there is about 10cm left before the end of the strip, I take the next one, overlap it with it, align their long edges, and continue gathering. </strong>The strips are therefore joined by the gathering stitches.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image026.jpg" alt=""></p>
<p><strong>And again, when there is about 10cm left before the end of the second strip of veiling, I take the next one, etc. </strong>This is how I simultaneously gather and join all prepared strips of veiling in a single ruffle.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image028.jpg" alt=""></p>
<p><strong>The ruffle is ready. Let us mark guidelines on the half-circle skirt piece. </strong></p>
<p><strong>I will show you how to mark the first three guidelines. </strong>The rest of them are marked just the same way. <strong>All ruffles are positioned at 5cm intervals along the full length of the skirt. </strong></p>
<p><strong>The skirt is folded in half. </strong></p>
<p><strong>I take a ruler and mark three sections of 5cm along the princess seam line, starting from the bottom. And I mark the same three sections along the additional transverse lines (radii). </strong></p>
<p>The more lines you draw, the more correctly you will position your rows of ruffles. <strong>I finish at the centre front line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image040.jpg" alt=""></p>
<p><strong>And I start drawing ruffle guidelines. I will draw guidelines for the first eight ruffles for a start. </strong></p>
<p><strong>I connect the marked points and draw a guideline for the bottom long ruffle (1) which goes parallel to the hem of the skirt, 5cm above it.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image042.jpg" alt=""></strong></p>
<p><strong>The guideline for the short ruffle (2) </strong>starts at the princess seam line, 5cm above ruffle (1). First ruffle (2) goes parallel to ruffle (1) and then it makes a curve and blends into it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image044.jpg" alt=""></p>
<p><strong>Next, I draw a guideline for another long ruffle (3). </strong></p>
<p>It starts at the princess seam line, 5cm above ruffle (2), and runs parallel to it first.</p>
<p>But, after going past ruffle (2), ruffle (3) starts going parallel to ruffle (1) and arrives at the centre front line 5cm higher than ruffle (1).</p>
<p><strong>Guidelines for ruffles (4), (5), (6), (7), and (8) are drawn just the same way. </strong></p>
<p>The points of intersection of short and long ruffles are not supposed to be all placed at the same level.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2014.files/image046.jpg" alt=""></p>
<p><strong>I transfer the guidelines on the other half of the skirt (they show through the fabric clearly enough). </strong></p>

