<h1>Tutorial 18. Examining the Finished Garment in the Atelier.</h1>
<p><strong>Look what a beauty we have made! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2018.files/image002.jpg" alt=""></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2018.files/image004.jpg" alt=""></strong></p>
<p><strong>Now this is a true luxury garment!</strong></p>
<p><strong>The skirt has a great long train, the lining does not show through, the ruffles look very neat and pretty, and there are no visible joining seams anywhere. </strong></p>
<p>&nbsp;</p>
<p><strong>Please pay your attention to the length of the skirt along the front. </strong></p>
<p>The height of my dress-form was adjusted in accordance with my client�s Waist to Floor measurement.</p>
<p>The ruffles at the front of the skirt touch the floor. It was a good decision to add that extra 2cm when I was putting the bottom ruffle on it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2018.files/image008.jpg" alt=""></p>
<p><strong>But I must warn you again:</strong> ask the bride to walk and spin around in the dress so you can make absolutely sure the front length is correct.</p>
<p>The hem of the skirt will inevitably curl under the bottom petticoat hoop, if it does turn out to be longer than needed.</p>
<p><strong>This is why the bottom ruffle was made 5cm longer. </strong></p>
<p>I can always trim the bottom edge of this ruffle if I need to shorten the skirt at the front. This won�t require any additional treatment either. Minor irregularities will be completely unnoticeable.</p>
<p><strong>I would like to draw your attention to the joint between the skirt and the top part of the dress. </strong>It is decorated for a smoother transition from the top to the bottom. The d�cor matches the top part of the dress.</p>
<p><strong>Thanks to this d�cor,</strong> I did not have to treat the top of the skirt in any particular way. As I said before, if you don�t plan to decorate it, you can put an additional ruffle between the two ruffles at the top to make it a little fuller.</p>
<p><strong>We are approaching the last pages of this book dedicated to sewing a fishtail skirt with a train. </strong></p>
<p><strong>I believe I have managed to show you that the shape of the skirt depends on the petticoat! </strong></p>
<p>You must have noticed that the making of the petticoat was more challenging than the making of the skirt itself!</p>
<p><strong>No d�cor, no embellishments will make your skirt beautiful if you use a poorly-made petticoat for it. </strong></p>
<p><strong>Only the petticoat is responsible for its final shape! </strong></p>
<p>Can you imagine the same skirt with a single-hoop petticoat? No way! The only exception could be made for a straight-silhouette dress.</p>
<p><strong>Before accepting a new order, I always think over the inner construction of the skirt first. The face and the d�cor come second. </strong></p>
<p>If your petticoat is made incorrectly, the skirt will never acquire the desired beautiful shape no matter how neatly you cut and sew it or how generously you decorate it.</p>
<p><strong>Please never forget that the petticoat is the key in any wedding dress! </strong></p>
<p><strong>I have also shown you how to gather and put ruffles on the skirt in a fast and easy way, and without seam allowances. </strong></p>
<p>Skirts decorated with such ruffles look a lot more expensive, beautiful, and luxurious. You can use this method for vertical ruffles, as well as for horizontal ruffles. I use it widely myself and it is my hope that you will too! Your clients will be happy and you will be able to get good wages!</p>
<p><strong>All techniques I have tried to demonstrate and explain in this book can be widely used in the making of other kinds of wedding garments. </strong></p>
<p><strong>At the very end of the book I will answer a range of frequently asked questions from your E-mails. </strong></p>
<p><strong>The most urgent of them all is probably the question of garment balance. </strong></p>
<p><strong>Moreover, </strong>I will fulfill your request and show you how to decorate the train of the skirt additionally although in my opinion the original minimalist design is enough! The train looks impressive as it is!</p>

