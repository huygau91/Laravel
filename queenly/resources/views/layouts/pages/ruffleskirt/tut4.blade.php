<h1>Tutorial 3. Doing Calculations for Cutting a Petticoat Cover.</h1>
<p><strong>As you could see, the petticoat is made of a top part (knit fabric mini-skirt) and a bottom part. </strong>The bottom part is made of a customized factory-made petticoat. I have removed four bottom hoops and trimmed it at the top.</p>
<p><strong>In this tutorial, I will do calculations required for cutting a hard mesh fabric cover for the petticoat. </strong>The cover will then be fully finished with hard mesh ruffles.</p>
<p><strong>The cover will consist of two halves: the front and the back. </strong>The back will be reinforced additionally.</p>
<p>I will use the existing and the newly calculated measurements to make my cover pattern.</p>
<p><strong>Required previously calculated measurements: </strong></p>
<p><strong>- ½ Mini-Skirt Bottom Circumference</strong> which is basically the top half-circumference of the cover (50cm);</p>
<p><strong>- ½ Bottom Hoop Circumference</strong> (150cm).</p>
<p><strong>The cover must be 5cm shorter than the petticoat. </strong>In other words, all length measurements used for cutting the hard mesh fabric cover must be 5cm shorter than the corresponding measurements used for the petticoat itself.</p>
<p><strong>Therefore, I will use the following measurements for cutting the cover (seam allowance included).</strong></p>
<p><strong>- Front Cover Length: </strong></p>
<p><strong>61.2cm - 5cm + 1.2cm ≈ 57.5cm</strong></p>
<p><strong>- Side Cover Length:</strong></p>
<p><strong>69.2cm - 5cm + 1.2cm ≈ 65.5cm</strong></p>
<p><strong>- Cover Train Length:</strong></p>
<p><strong>77.2cm - 5cm + 1.2cm ≈ 73.5cm</strong></p>
<p><strong>There will be a hard mesh ruffle going along the hem of the cover 5cm from the edge. </strong></p>
<p><strong>The ruffle needs to be positioned 10cm lower than the bottom hoop of the petticoat. </strong>In this case, it will spread the face fabric very well and improve the shape of the skirt.</p>
<p><strong>The bottom edge of the ruffle must stay 3-5cm above floor level. </strong></p>
<p><strong>If the ruffle has a width of 15cm, then its bottom edge will be positioned 3cm above the floor. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%203.files/image002.jpg" alt=""></strong></p>
<p><strong>Please note</strong> that the ruffle must be wide enough. It must not stop at the edge of the bottom hoop of the petticoat: if it does, it will simply curl under the hoop instead of giving the skirt its proper shape. <strong>It is for this reason that the cover should be made 5cm shorter than the petticoat. </strong></p>
<p><strong>As I mentioned before, the cover will be made of two halves: the front and the back. </strong></p>
<p><strong>Since it will be cut with a fold line, I need to know 1/4 top and bottom circumferences of the cover. </strong></p>
<p><strong>I must also account for</strong> 1.2cm seam allowances and allowance to let the cover settle over the bottom hoop of the petticoat snugly. It is enough to add just 5cm at the bottom of either half of the cover.</p>
<p><strong>As the result, I will cut the cover with the following measurements: </strong></p>
<p><strong>- ¼ Top Circumference:</strong></p>
<p><strong>50cm / 2 + 1.2cm = 26.2cm</strong></p>
<p><strong>- ¼ Bottom Circumference:</strong></p>
<p><strong>150cm / 2 + 1.2cm + 5cm = 81.2cm</strong></p>
<p><strong>This allows the lay to fit into the width of my hard mesh fabric. </strong></p>

