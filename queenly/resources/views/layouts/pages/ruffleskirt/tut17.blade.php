<h1>Tutorial 16. Marking Ruffle Guidelines on the Train.</h1>
<p>It is time to mark guidelines for putting ruffles on the train.</p>
<p><strong>They all run parallel to the bottom edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image002.jpg" alt=""></p>
<p><strong>The first ruffle will be placed 2.5cm above the bottom edge of the skirt. </strong>And the second ruffle will be placed at the same distance from it.</p>
<p><strong>The next two ruffles will be placed at 3cm intervals and the fifth one − at a 4cm interval. </strong></p>
<p><strong>All other ruffles will be placed at 5cm intervals from one another. </strong></p>
<p><strong>Finally, </strong>considering that the bottom edge of the train has a more pronounced arc-like shape than the top edge of the piece, I will use one more additional ruffle to fill in the space between the top ruffle and the top edge. It will go parallel to the top edge.</p>
<p><strong>All ruffles will start and stop 2cm from the side edges of the piece.</strong></p>
<p><strong>There is a reason why the ruffles are more closely-spaced at the bottom than at the top. </strong></p>
<p><strong>We want </strong>to make the train harder and help it hold its shape fixed. The closely-spaced bottom ruffles help secure the position of the train on the floor.</p>
<p><strong>I would also like you to notice </strong>that the ruffles layout on the train was designed to allow the ruffles on the train to get aligned properly with those on the half-circle skirt piece when you join them along the princess seams.</p>
<p><strong>Although the train will also be embellished with double-layer ruffles, there is a difference in the process of putting them on it. </strong>On the half-circle skirt, I stitched along the fold line of the ruffle and left it open, but here on the train I will secure it in the folded position by making an additional parallel row of stitches at a presser-foot distance from the main joining seam.</p>
<p>The thing is I want the train to be smoother than the front part of the skirt.</p>
<p><strong>Last but not least, </strong>I recommend you should put an additional ruffle between the last ruffle (i.e. the top one) and the last-but-one ruffle and thereby increase the fullness if you do not plan to decorate the joint between the skirt and the top part of the dress.</p>
<p><strong>Here is the back of the skirt with the train embellished with ruffles after taking in account all the above-said requirements. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image004.jpg" alt=""></p>
<p><strong>Note how well the hem of the train holds its shape!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image006.jpg" alt=""></p>
<p><strong>I start sewing together the two halves of the face of the skirt. </strong>I put the half-circle piece and the train face to face, align their princess seam edges, pin them together, and join them.</p>
<p><strong>I put a Rigilene bone along the seam allowance of the train. </strong>Then I fold the seam allowance with the bone over the train and <strong>secure it with a row of stitches</strong> which I make over the face of the skirt spreading the ruffles open with my fingers. This seam will thus be overlaid by the ruffles.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2016.files/image012.jpg" alt=""></p>
<p>I have calculated fabric consumption for decorating the front of the skirt and the train as I had promised you. It took exactly 100 strips of veiling with a width of 25cm and a length of 3m. In other words, <strong>I have used 25m of veiling for the ruffles. </strong></p>

