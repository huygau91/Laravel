<h1>Tutorial 20. Skirt Balance.</h1>
<p></p>
<p><strong>Let me first introduce you to some basic theory. </strong></p>
<p><strong>What is garment balance? </strong></p>
<p><strong>Balance </strong>occurs when all parts of a garment are in harmony with each another, as well as with the body of the person wearing the garment. </p>
<p><strong>Professionals distinguish between front-back balance and side balance.</strong></p>
<p><strong>Front-back balance</strong> means equilibrium between the front and the back of a garment with shoulder seams, a skirt, or a pair of trousers. </p>
<p><strong>When the front-back balance of a garment is disturbed, it tends to become skewed either to the back or to the front. </strong></p>
<p><strong>Of course, we will talk about the balance of our skirt. </strong>It mainly concerns the weight of its front and of its back. </p>
<p><strong>First of all, I would like to remind you about the main stages in the making of this skirt. </strong></p>
<p><strong>The key component is the petticoat. </strong></p>
<p>It resembles a lamp-shade in shape, except there is no wire to hang it on. </p>
<p>My petticoat is nearly fully symmetric which means it has great balance. </p>
<p><strong>Before putting the main skirt on the petticoat, I put a fabric cover on it. </strong>The ruffles make the back of the skirt much heavier than the front. </p>
<p>You would thus expect the bottom hoop of the petticoat to rise and lift the front of the skirt with it. </p>
<p><strong>Nonetheless, this does not happen, and the skirt preserves its original shape and good balance! </strong></p>
<p><strong>How is this possible?</strong> </p>
<p><strong>The good balance of the skirt is ensured solely by the additional reinforcement</strong>, i.e. those vertical Rigilene bones on the train of the main skirt and along the princess seams. They bear the weight of the back ruffles on them, push into the floor, and prevent the back half of the skirt from lowering down. </p>
<p>The stiff bottom ruffles on the petticoat cover also help maintain the right balance. </p>
<p><strong>If I had not taken those preventive measurements, </strong>the balance of the skirt would have been distorted, and it would look just as unpretty as most skirts of this type offered by many dressmakers today. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%2020.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Poor garment balance is a commonly found problem</strong> and yet many dressmakers forget (or simply don�t know) about it. They try to fix local defects failing to realize that these defects are caused by an issue that lies much deeper within: namely, by disturbed balance.</p>
<p>Most unwanted folds and creases will disappear automatically once different parts of the garment are put in equilibrium. </p>
<p><strong>You must think over garment balance as soon as you start sketching the garment you want to make.</strong></p>
<p><strong>Before sewing a garment with a sophisticated skirt like this, remember to take the following things into account:</strong></p>
<p>- Inner construction underneath the skirt;</p>
<p>- The cut of the skirt; </p>
<p>- Overall design;</p>
<p>- Garment balance. </p>
<p><strong>Although working on the top part of the dress implied following the tips and patterns from other books by the Corset Academy, its balance is nothing to worry about. It was double-checked very thoroughly and brought to perfection. </strong></p>
<p><strong>You will only need to find the right balance for the skirt! </strong></p>

