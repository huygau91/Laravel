<h1>Tutorial 1. Taking the Key Measurements.</h1>
<p><strong>The work starts from taking measurements. </strong>Measurements are the backbone in our business.</p>
<p><strong>You can see the top part of the dress in the picture below. </strong>It is a low-back dress sewn in the technique described in �Low-Back Wedding Dresses" by the Corset Academy. It is a great book, highly recommended.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image004.jpg" alt=""></p>
<p><strong>I already know all measurements required for sewing the top part of the dress but I still need to take some additional measurements for the skirt. </strong></p>
<p><strong>Apart from explaining how to sew a fishtail wedding dress correctly, I also want to show you how to sew it without having to invite your client to intermediate fittings to verify the length. </strong></p>
<p>There are types of dresses the length of which can be easily adjusted at any moment (one example is a full skirt with elastic band from our same-name book). Even if you make a mistake when taking measurements off your client, you can always invite her to come over with her wedding shoes, double-check it, and trim the bottom edge of the skirt accordingly.</p>
<p>And there are other dresses, like ours, when you must know the exact length of the skirt before you start cutting it. In theory, you could adjust the front length of the finished fishtail dress but it would be very hard and tedious work. It is much better to take all measurements correctly from the start and sew the dress without any additional fittings. You would only invite the bride to come over to witness that the sewing process is going as planned.</p>
<p><strong>The first key measurement I always take when sewing skirts is Waist to Floor.</strong> I take it together with all other required measurements.</p>
<p>In order to take this measurement, I ask the bride to stand before me in shoes with the same high heel height as her wedding shoes. I tie a cord or an elastic band around her waist. Then I take a measuring tape, put it to the waistline, and let it hang down freely to the floor. <strong>My client�s measurement is 109cm. </strong></p>
<p><strong>Then, I secure the measuring tape at the 109cm mark at the waistline of the dress-form </strong>and keep adjusting its height until the opposite end of the measuring tape touches the floor.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image006.jpg" alt=""></p>
<p>I have therefore adjusted the height of the dress-form to my client�s height. Now I will be able to sew the skirt with the right length from the start. It no longer matters what kind and size of a petticoat the bride will wear. <strong>Remember to use this handy trick! </strong>The Corset Academy is here to turn you into professionals who can sew amazingly beautiful wedding garments with minimum fittings and even accept long-distance orders from clients living in a different town or country.</p>
<p><strong>The next important measurement is Train Length. </strong>In order to take it, I ask the bride to stand in front of a mirror, secure the end of the measuring tape at the cord tied around her waist at the back, and keep walking away from the bride to determine the best length from the waist to the supposed end of the train dragging along the floor. You should not pull the measuring tape tight: let it hang freely visually repeating the shape of the skirt. <strong>In my client�s case, I got 130cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image008.jpg" alt=""></strong></p>
<p><strong>Next, I need to find the length of the mini-skirt which will be the top part of the petticoat. </strong>It is measured from the waistline to the supposed joint with the skirt.</p>
<p>You can take this measurement off the client straight away after roughly estimating where the flare of the skirt will begin. Skirts like ours are usually flared from just below the buttocks.</p>
<p>If you have already finished the top part of the dress and the bride has tried it on, it is enough to take that measurement off the dress-form by measuring the distance from the waistline to the bottom edge of the finished top part of the dress across the front (remember to add a seam allowance).</p>
<p><strong>My client�s Mini-Skirt Length is 36cm. </strong>Your client�s measurement may not be the same of course.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image010.jpg" alt=""></p>
<p><strong>Another compulsory measurement is Mini-Skirt Bottom Circumference. </strong>I simply measure the bottom edge of the top part of the dress in the round.</p>
<p><strong>In my client�s case, it is 100cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image012.jpg" alt=""></p>
<p><strong>Needless to say, I need to know her Waist Circumference, too. </strong></p>
<p>This measurement is one of the key compulsory measurements required for sewing the top part of the dress and I have already taken it. <strong>My client�s Waist Circumference is 65cm. </strong></p>
<p><strong>Next, I need to take measurements off the petticoat itself. </strong>I usually buy wholesale inexpensive petticoats with five or seven hoops. I prefer petticoats with seven hoops because of the bigger bottom diameter (usually, around 3m). If I need fewer hoops for a particular skirt, I can always trim away the excess and lengthen the top of the petticoat.</p>
<p>If you have read �Full Skirt with Elastic Waistband" by the Corset Academy, then you probably remember that I moved the hoops higher up when adjusting the factory-made petticoat to increase the fullness of the skirt in the waist.</p>
<p><strong>Petticoat hoops in a fishtail skirt, on the contrary, need to be lowered as much as possible. And you must always account for this when taking measurements. </strong>I would also like to share two methods of taking measurements off a petticoat put on the bride or on the dress-form.</p>
<p><strong>Method 1, with the petticoat on the client. </strong>The bride must be wearing high-heel shoes. I help her put on the factory-made petticoat and then pull it down so as to have its bottom edge positioned 10-15cm above floor level. <strong>I measure the distance from the bottom hoop to the floor. It is 13cm. </strong></p>
<p><strong>Method 2, with the petticoat on the dress-form.</strong> I have already adjusted my dress-form to my client�s height. After putting the petticoat on the dress-form, I pull it down just like in the previous case to have its bottom edge positioned 10-15cm above the floor. <strong>I measure the distance from the bottom hoop to the floor. It is 13cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%201.files/image014.jpg" alt=""></strong></p>
<p><strong>You can also measure the length of the bottom hoop (bottom circumference). Mine is 3m. </strong></p>

