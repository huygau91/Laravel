<h1>Tutorial 8. Joining the Cover with the Petticoat.</h1>
<p><strong>Below is the back half of the petticoat cover with all ruffles. </strong>I have made a row of stitches along the top edge so I can gather it down to the required half-circumference measurement (50cm).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image002.jpg" alt=""></p>
<p><strong>Now I need to join the front with the back. </strong>I will keep the seam allowances on the face side with the ruffles to prevent their raw edges from catching on the petticoat fabric. The cover is supposed to glide over the surface of the petticoat. In addition, I make a row of stitches along the top edge of the front half of the cover just like I did on the back half, to gather it down to the required length.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image004.jpg" alt=""></p>
<p><strong>I will start sewing the two halves together from the bottom edge finished with bias tape. </strong>I will even out the pieces along the top edge if the need comes.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image006.jpg" alt=""></p>
<p><strong>Next, I sew the side seam of the petticoat cover. </strong>Again, I keep the seam allowance on the face side, topstitch it, and trim it.</p>
<p><strong>After sewing the side seam, I put two rows of ruffles along the full circumference of the cover. </strong></p>
<p><strong>The first additional row of ruffles </strong>is put directly along the bias tape at the bottom of the cover.</p>
<p><strong>The second row </strong>is positioned parallel to the first row, 2cm above it. In other words, the second additional row of ruffles is positioned 3cm lower than the bottom ruffle previously put on the back half of the petticoat cover.</p>
<p><strong>After finishing the second row, I realized</strong> there was a free end of the ruffle left unused. I secured it on the front half of the cover at the level of the bottom ruffle previously put on the back half, i.e. 5cm above the bottom edge.</p>
<p><strong>I have coped with the task. You can see the front, the back, and the side of the petticoat cover in the pictures below. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image012.jpg" alt=""></strong></p>
<p><strong>And now I need to join the basically finished cover with the petticoat itself. </strong></p>
<p><strong>First, I make a row of stitches along the top edge of the petticoat </strong>so I can gather it down to the required half-circumference measurement (50cm) and align it with the top edge of the cover.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image014.jpg" alt=""></p>
<p><strong>Then, I insert the petticoat in the cover, align their side seams, align their top edges gathering them down to 50cm, pin the top edges together, and join them with a row of stitches. </strong>Now the bottom part of the petticoat is ready to be joined with the mini-skirt part which still needs to be made.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image018.jpg" alt=""></p>
<p><strong>Let us start cutting the mini-skirt part. </strong></p>
<p><strong>The knit lining fabric</strong> I have chosen for the mini-skirt is almost non-stretch along the lengthwise grain. It only stretches along the crosswise grain so I will cut it lengthwise.</p>
<p><strong>If you are going to sew your mini-skirt with stretch mesh fabric, </strong>please remember that it stretches in all directions. The bottom part of the petticoat is quite heavy so I recommend you should put the garment on the dress-form after joining it with the mini-skirt, measure the length of the mini-skirt part, and trim it at the top if it has stretched out and become longer.</p>
<p><strong>I measure the calculated half-circumference of the mini-skirt along the selvage of my knit fabric. It is 51.2cm (seam allowance included). </strong></p>
<p><strong>I fold the fabric at this level and measure the length of the mini-skirt along the fold line. It is a total of 41.5cm including seam allowances and allowance for a tunnel for inserting an elastic band. </strong></p>
<p><strong>I draw a rectangle and cut it out. </strong></p>
<p><strong>I sew the side seam and form a tunnel for the elastic band along the marked line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image026.jpg" alt=""></p>
<p><strong>We have approached a very responsible moment. </strong>I will join the pieces keeping the seam allowance on the face side. Then I will insert an elastic band in the tunnel - and the petticoat will be finished!</p>
<p><strong>I have already joined the cover with the petticoat. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image028.jpg" alt=""></strong></p>
<p><strong>I have also sewn and pressed the mini-skirt part. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image030.jpg" alt=""></strong></p>
<p><strong>I put the mini-skirt in the petticoat, align its side seam allowance with the seam allowance of one of the two side seams on the petticoat, and pin them together. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image032.jpg" alt=""></strong></p>
<p><strong>I align the top edge of the petticoat with the bottom edge of the mini-skirt and carefully pin them together along the full circumference. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image033.jpg" alt=""></strong></p>
<p><strong>I start sewing the pieces together.</strong> The seam allowance stays on the face side. I fold it to the side, topstitch it, and trim it. <strong>This is not an easy task because of the large amount of ruffles and the steel hoops. I have tried to show you how you can put the garment under the sewing machine needle in the picture below. </strong>I turn the garment slowly to sew in the round.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image035.jpg" alt=""></strong></p>
<p><strong>This is probably the most challenging and the least enjoyable work stage. When it is finally done, I insert a 58cm long elastic band in the tunnel and put the finished petticoat on the dress-form. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image037.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/mermaid%20wedding%20skirt/Tutorial%208.files/image039.jpg" alt=""></p>
