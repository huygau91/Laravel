<p><strong>My embroidery is ready. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image004.jpg" alt=""></strong></p>
<p><strong>I put the kiss-lock in place to check what the purse will look like when fully finished. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image006.jpg" alt=""></p>
<p><strong>I believe you can now see that there is nothing too hard in doing tambour embroidery. All you need to do is stick to certain basic rules. </strong></p>
<p><strong>You must simply practice! A few 20-30 minutes sessions a day will allow you to upgrade your skills rather fast! </strong>You will develop your own manner and technique.</p>
<p><strong>Only practice makes perfect - there are no other secrets here!</strong></p>
<p><strong>In conclusion, I would like to go through the key rules of tambour embroidery one more time. </strong></p>
<p><strong>You mount your fabric in the embroidery frame and put beads on a thread. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image008.jpg" alt=""></p>
<p><strong>You put the thread over your finger with its tail toward you and hold it in place with your thumb. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image010.jpg" alt=""></p>
<p><strong>The embroidering process begins. </strong></p>
<p><strong>You make a knot first. Insert the needle in the fabric, grab the thread off your finger, and pull it up to the top side of the mesh fabric. </strong>Keep holding the thread tail in place so that it stays tight.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image016.jpg" alt=""></p>
<p><strong>Make a small stitch in the supposed embroidering direction. The screw of the hook is pointing forward, i.e. in the direction in which you move ahead. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image018.jpg" alt=""></p>
<p><strong>Grab the thread holding its free end firmly, rotate the hook 180 degrees (the screw pointing backwards), and pull a loop up to the top side of the mesh fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image022.jpg" alt=""></p>
<p><strong>Now make another stitch like this forward or backward, turning the hook in the embroidering direction. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image030.jpg" alt=""></p>
<p><strong>The knot is finished. You can let go of the tail. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image032.jpg" alt=""></p>
<p><strong>Now let me say a couple of words about the thread with beads positioned under the mesh fabric. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image034.jpg" alt=""></strong></p>
<p><strong>The beads make this thread rather heavy, which can make the embroidering process difficult. Try to keep few beads hanging while letting the rest lie on the table. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image036.jpg" alt=""></strong></p>
<p><strong>What you do next is slide a bead up the thread, make a stitch of a length depending on the size of the bead, and continue stitching with beads following the guideline. </strong></p>
<p><strong>There are other important technical moments that require your attention. </strong></p>
<p><strong>Suppose you are embroidering in a certain direction and then you need to make a sharp turn. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image038.jpg" alt=""></strong></p>
<p><strong>In this case, you should make a stitch in the same direction in which you have been embroidering before actually changing the embroidering direction. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image044.jpg" alt=""></strong></p>
<p><strong>One more thing: </strong>according to the tambour embroidering technique, you are supposed to rotate the hook 180 degrees after grabbing the thread.</p>
<p><strong>But you can rotate the hook clockwise or anti-clockwise, i.e. away from you or toward you. </strong></p>
<p><strong>Of course, you will pull a loop up in either case, but the quality of the stitch does depend on what side of the needle the thread passes along! </strong></p>
<p><strong>And this is why I want to share another tip with you: let the thread pass along that side of the needle toward which you are going to rotate the hook!</strong></p>
<p><strong>Scenario 1. You find it more comfortable turning the hook toward you. </strong></p>
<p>I, personally, do it this way.</p>
<p><strong>The thread passes along the side of the needle which is opposite to the viewer�s side in the picture below. </strong></p>
<p><strong>And then I continue working in the described technique: </strong>grab the thread, rotate the hook 180 degrees toward me, and pull up a loop.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image046.jpg" alt=""></p>
<p><strong>The loop and the stitch look neat, untwisted, and pretty. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image048.jpg" alt=""></strong></p>
<p><strong>Scenario 2. You feel more comfortable turning the hook away from you. </strong></p>
<p><strong>The thread passes along the side of the needle that faces the viewer in the picture below.</strong></p>
<p><strong>And then I continue working in the described technique: </strong>grab the thread, rotate the hook 180 degrees away from me, and pull up a loop.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image050.jpg" alt=""></p>
<p><strong>Again, the loop and the stitch come out really neat and pretty. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image052.jpg" alt=""></strong></p>
<p><strong>If you neglect my advice, you will get a twisted poor-quality loop like the one in the picture below. </strong>Of course, it is easy to fix it by rotating the hook again. But why waste your time untwisting every loop when you can just make it right from the beginning!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image054.jpg" alt=""></p>
<p><strong>I hope I have given you maximum know-how concerning the tambour embroidering technique. This is all you need to know about it! </strong></p>
<p>Purchase convenient embroidery hoops and frames, find a comfortable work position, and go creative!</p>
<p><strong>And do make sure to get yourself a luneville hook! </strong>I highly recommend branded luneville hooks from this online store (I have several hooks purchased from there):</p>
<p><a href="http://www.broderieplaisir.com"><strong>http://www.broderieplaisir.com</strong></a></p>
<p>When you get experienced enough, it will not matter to you whether to use a branded hook or a replica hook made in China. But a branded luneville hook really does make a difference when you are still learning.</p>
<p><strong>As for me, I use a branded #70 luneville hook. </strong>It has one of the finest needles you can find.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image056.jpg" alt=""></p>
<p><strong>I have removed my embroidery from the frame. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image058.jpg" alt=""></strong></p>
<p><strong>Remember to secure your embroidery on the purse unless it was made with some special-purpose reinforced thread. </strong>You must understand that anything can happen with the purse in wear!</p>
<p><strong>I recommend you smear all threads with fabric glue to secure the embroidery. </strong>You know what the tambour stitch is! It is basically a braid - just yank on a torn thread and the whole embroidery will go undone! And clear fabric glue will not be visible from the face side of the piece.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image060.jpg" alt=""></p>
<p><strong>Needless to say that you must not use any glue if your embroidery will be in direct contact with bare skin! </strong>You must use very high-quality tear-proof thread in that case.</p>
<p><strong>Just to let you know: this small embroidered piece weighs 110 grams. </strong>And now imagine a whole dress fully embroidered with seed beads, rhinestones, sequins, let alone pearl beads! Not every bride can cope with such "pressure!" Please remember about your own responsibility when accepting such orders. The final garment must be wearable in the end!</p>
<p><strong>As for us, we have fulfilled our task. </strong>All we need to do is cut and sew the purse and then embellish it with this beautiful embroidery!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/5.files/image062.jpg" alt=""></p>

