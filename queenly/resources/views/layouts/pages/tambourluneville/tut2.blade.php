<p><strong>I make a strengthening stitch, make a turn, and continue stitching with pearl beads. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image174.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image176.jpg" alt=""></strong></p>
<p><strong>There is another handy tip. </strong></p>
<p><strong>When your thread with beads is pulled too tight,</strong> for example, under the weight of the beads, then it tends to start sliding off the needle whenever you try to pull a loop up to the top side of the fabric. Just keep part of the thread with beads lying on the table if that is the case.</p>
