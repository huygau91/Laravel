<div itemprop="articleBody">
<p><strong>And now, let me explain you how to mount fabric in the embroidery frame. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image002.jpg" alt=""></p>
<p><strong>The embroidery frame normally comes with clips for holding the fabric in place.</strong></p>
<p>I have completely given up on using these clips because they never hold the fabric well enough.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image004.jpg" alt=""></p>
<p><strong>I use my own method of mounting fabric in the embroidery frame. </strong></p>
<p><strong>I wrap the mesh fabric around one of the two round bars and secure it with pins. </strong>I place the pins criss-cross trying my best to keep the edge of the mesh fabric straight and even. The amount of pins depends on how well the fabric stretches: the more it does, the more pins you need to use.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image010.jpg" alt=""></p>
<p><strong>I turn the frame around for the sake of convenience and wrap the mesh around the opposite bar. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image012.jpg" alt=""></p>
<p><strong>The process will be a lot easier if you take the frame off the stand and put it on the table. </strong>I recommend you always mount fabric in embroidery frames or hoops on a flat surface.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image014.jpg" alt=""></p>
<p>Unlike with the first round bar, I need not only to secure the mesh around the opposite bar but also pull it tight enough.</p>
<p><strong>I pull the mesh fabric tight and secure it with pins driving them right into the soft surface of my table. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image016.jpg" alt=""></strong></p>
<p><strong>Next, I tighten the mesh fabric additionally by rotating the round bar and then secure it with criss-crossing pins. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image020.jpg" alt=""></p>
<p><strong>The mesh fabric is pulled tight in one direction. </strong>You might not get the desired result from the first try. Sometimes you have to remount it a couple of times, especially, if you are not very skilled at it yet. Of course, mounting fabric in an embroidery frame is nothing difficult. All it requires from you is accurateness and composure. Although I have to say it is always my least favourite part of the embroidering process. I suppose it is because it is preparatory work and I can never wait to start with the creative part.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image022.jpg" alt=""></p>
<p><strong>You can fold back the excess fabric and secure it with pins </strong>or simply trim it if the mounted piece of fabric is not supposed to have a prolongation.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image024.jpg" alt=""></p>
<p><strong>After tightening the mesh fabric in the lengthwise direction, you need to do the same in the crosswise direction. </strong></p>
<p><strong>This is done with the help of a ribbon. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image026.jpg" alt=""></p>
<p><strong>I secure the end of the ribbon at the edge of the mesh fabric with criss-crossing pins. </strong>The ribbon does not have to be pulled tight from this side. Our task is just to secure it safely along the edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image042.jpg" alt=""></p>
<p><strong>One side of the mesh fabric is now secured in place with a ribbon.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image044.jpg" alt=""></p>
<p><strong>I move to the other side. </strong></p>
<p><strong>This is more responsible work because I need not only to secure the fabric with the ribbon but also pull it tight. </strong>You can use a petersham ribbon or any plain satin ribbon: it does not really matter.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image046.jpg" alt=""></p>
<p><strong>I highly recommend you pin both ribbons to the mesh fabric at the same levels, i.e. keep it symmetric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image048.jpg" alt=""></p>
<p><strong>I wrap the ribbon over the bar, pull the mesh fabric tight, and secure the ribbon by placing a pin parallel to the edge of the fabric. </strong>I gradually move along the edge of the piece this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image054.jpg" alt=""></p>
<p><strong>In order to prevent the mesh fabric from getting torn or severely deformed under tension, I secure the ribbon with criss-crossing pins removing the previously placed pins.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image056.jpg" alt=""></p>
<p><strong>I continue tightening and securing the mesh fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image060.jpg" alt=""></p>
<p><strong>Every time I pin down a new part of the ribbon, I go back to the previous part, secure it additionally with criss-crossing pins, and remove the previously placed pin. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image068.jpg" alt=""></p>
<p><strong>Now the mesh fabric is mounted in the frame properly. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image070.jpg" alt=""></p>
<p><strong>It has been almost 30 years since I learnt embroidering. My teacher was from Germany and I still remember her words: �Well-tightened fabric must be bouncy like a trampoline but it must not be too tight, or else it will simply burst!" </strong></p>
<p><strong>Please remember these words, too! </strong>You should be particularly careful with elastic and high-stretch materials. It was for this reason that I chose hard mesh fabric for you to start from.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image072.jpg" alt=""></p>
<p><strong>Now that the fabric is mounted and the frame is back on the stand, I can mark the embroidery area and then transfer the pattern on it. </strong></p>
<p><strong>I am often asked what to use to transfer the pattern on the fabric. </strong>You can use anything washable or removable. You can use a regular disappearing ink pen for drawing small fragments which don�t take long to embroider. If you plan to work on a larger piece and need to transfer the whole pattern at once, then use a plain water-soluble marker.</p>
<p><strong>I will use a water-soluble marker because hard mesh fabric dries in an instant and does not get deformed when wet. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image074.jpg" alt=""></strong></p>
<p><strong>There are also special iron-off pens. Drawings made by these pens disappear under hot steam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image076.jpg" alt=""></p>
<p><strong>I put the kiss-lock on the mesh fabric and transfer its inner outline with the marker. </strong>It is the precise outline of my embroidery area. I try to transfer the outline very neatly, especially the rounded areas. I make marks at the level of the connecting screws: it is where the embroidery will stop.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image078.jpg" alt=""></p>
<p><strong>I put the kiss-lock away. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image080.jpg" alt=""></p>
<p><strong>I connect the bottom points of the drawing with a ruler. This line must be very even.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image082.jpg" alt=""></strong></p>
<p><strong>That�s all: I have marked the actual embroidery area. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image084.jpg" alt=""></strong></p>
<p><strong>Before I can start embroidering, I need to put pearl beads on the embroidering thread. </strong></p>
<p><strong>I pick pearl beads with a special beading needle which has a big eye in the middle. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image086.jpg" alt=""></p>
<p><strong>You put the embroidering thread in that eye or split. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image088.jpg" alt=""></p>
<p><strong>I really like the big eye beading needle because it allows you to use thread of any thickness and pick beads of any size: from the tiniest to really large ones.</strong></p>
<p><strong>I will use plain sewing thread for my embroidery. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image090.jpg" alt=""></p>
<p><strong>And now I want to talk about the beads themselves. </strong></p>
<p><strong>I will use Swarovski pearl beads, and I will start from those with a diameter or 3mm. </strong>Of course, you don�t have to always use Swarovski beads. There are other beads of good quality made in the Czech Republic, for example. You can also practice with lower-quality cheaper Chinese pearl beads too, just for a start. But let me tell you frankly: embroidering is rather tedious work and you naturally want to be happy with the results. When you work with high-quality materials, you can enjoy the process from the very beginning because you can already anticipate good results!</p>
<p><strong>For this reason, I highly recommend you invest in quality beads. </strong>It is fine, however, to practice with something simpler if you already have it in stock.</p>
<p><strong>Let us talk about the luneville hook, too. </strong></p>
<p><strong>I use a #70 luneville hook. </strong>I recommend you should start learning with a good branded hook. It will not cost that much. At the same time, I believe there is no point for a beginner in spending lots of money on embroidery frames or hoops. I really don�t think it is so important.</p>
<p><strong>But the hook must be very good! </strong>Your learning and working pace, as well as the final results, depend on its quality.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image092.jpg" alt=""></p>
<p><strong>Very important tip: when you secure the needle in the handle, make sure to keep the screw aligned with the opening of the needle! </strong>And then, if you hold the hook with the screw looking forward, you will know that the needle will grab the thread from this side as well. If you follow my advice and adjust your hook this way, you will experience no problems with the rest of the process.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image094.jpg" alt=""></p>
<p><strong>I have put the required amount of beads on the thread and I can now start embroidering. </strong></p>
<p>I put away the big eye needle used for picking the beads, adjust the thread (it should not be too twisted, or else it will keep getting tangled), and trim its end.</p>
<p><strong>The first thing I need to do is make a starting knot. </strong></p>
<p><strong>I put the needle through the opening in the start point of the embroidery area. </strong></p>
<p><strong>The key rule is to hold the hook with the screw pointing in the embroidering direction! The screw is always supposed to show in what direction you are moving! </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image096.jpg" alt=""></strong></p>
<p><strong>I grab the thread, rotate the hook 180 degrees (screw backward) to prevent it from catching the mesh, and pull a loop up to the top side of the fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image098.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image100.jpg" alt=""></p>
<p><strong>Keeping the loop on the hook, I rotate the hook back in the initial position (screw forward), step literally just a couple of millimeters from the start point in the direction pointed by the screw, and bring the hook in an opening in the mesh fabric. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image102.jpg" alt=""></strong></p>
<p><strong>I grab the thread with the hook the way you do when you crochet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image104.jpg" alt=""></p>
<p><strong>I rotate the hook 180 degrees again (screw backward), grab the thread, and pull out another loop. </strong>Please note that the position of the hook must allow you to grab the thread and, at the same time, pull it out properly the way you do in crocheting!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image106.jpg" alt=""></p>
<p><strong>At this stage, my task is to make a knot. </strong></p>
<p><strong>I rotate the hook again (screw forward) and insert the needle in the start point. </strong>The loop is on the hook.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image108.jpg" alt=""></p>
<p><strong>I grab the thread. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image110.jpg" alt=""></strong></p>
<p><strong>I rotate the hook (screw backward), grab the thread, pull out a loop, pull up the thread and that�s it - the knot is made! Now I can go ahead!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image112.jpg" alt=""></p>
<p><strong>After making the knot, I can make the first stitch. </strong></p>
<p><strong>The process resembles crocheting very much. </strong></p>
<p><strong>I pass the needle through the opening holding the hook with the screw looking forward, grab the thread, rotate the hook 180 degrees (screw backward), and pass the thread through the opening. </strong></p>
<p><strong>This is all there is about the tambour embroidering technique! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image114.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image116.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image118.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image120.jpg" alt=""></p>
<p><strong>And now I will start making stitches with pearl beads. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image122.jpg" alt=""></strong></p>
<p><strong>There is a loop on my hook. </strong></p>
<p><strong>I slide a pearl bead right up to it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image124.jpg" alt=""></strong></p>
<p><strong>I bring the needle in the mesh fabric at about a pearl bead width from the marked line. The screw is pointing forwards in the embroidering direction. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image126.jpg" alt=""></strong></p>
<p><strong>I grab the thread. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image128.jpg" alt=""></p>
<p><strong>I rotate the hook (screw backward). </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image130.jpg" alt=""></p>
<p><strong>And I pull a loop up to the top side of the mesh fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image132.jpg" alt=""></p>
<p><strong>The first stitch with a pearl bead is finished. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image134.jpg" alt=""></p>
<p><strong>I rotate the hook screw forward, insert the needle into the mesh fabric again (this time I already know the required spacing), slide a second pearl bead in place, and finish the stitch. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image136.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image138.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image140.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image142.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image144.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image146.jpg" alt=""></p>
<p><strong>I have already made two stitches with pearl beads. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image148.jpg" alt=""></p>
<p><strong>I will make stitches with beads along the entire vertical line marked on the mesh fabric. </strong>Don�t worry if a loop slips off your hook: you can always pick it again. I must also say that every embroiderer has their own manner of holding the hook and their own embroidering pace.</p>
<p><strong>Just make sure to stick with this simple algorithm: grab the thread, rotate the hook, and pull a loop up to the top side of the fabric! </strong>You can embroider on any kind of fabric this way!</p>
<p><strong>As for me, I continue embroidering on my mesh fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image150.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image152.jpg" alt=""></p>
<p><strong>I recommend you always embroider the outline first and then fill in the inner embroidery area. </strong></p>
<p><strong>I make stitches with beads along the marked perimeter or outline of the embroidery. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image154.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image156.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image158.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image160.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image162.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image164.jpg" alt=""></strong></p>
<p><strong>I have reached another point where I need to make a sharp turn of 90 degrees. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image166.jpg" alt=""></p>
<p><strong>It is compulsory to make a small strengthening stitch of just 1mm without a bead before making this turn. </strong>If you turn without making a strengthening stitch first, the thread may show in this part of the embroidery later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image168.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/2.files/image170.jpg" alt=""></p>
