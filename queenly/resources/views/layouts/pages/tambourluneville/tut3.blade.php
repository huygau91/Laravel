<p><strong>It is time to fill in the inner embroidery area. </strong></p>
<p><strong>But first let me briefly talk about the materials I am going to use for my embroidery. </strong></p>
<p><strong>You already know that I am using Swarovski pearl beads. </strong></p>
<p><strong>The largest pearl beads I will use have a diameter of 8mm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image004.jpg" alt=""></strong></p>
<p><strong>I will also use 6mm and 5mm Swarovski pearl beads. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image008.jpg" alt=""></strong></p>
<p><strong>Apart from excellent quality, Swarovski pearl beads are great in that they come threaded. </strong>It is easy to restring them on your embroidering thread with a big eye beading needle.</p>
<p><strong>Of course, I will also continue using 3mm pearl beads − the ones already used for the outline. </strong></p>
<p><strong>I also have some 4mm white pearl beads made in the Czech Republic. They have a slightly different shade and will look great together with the Swarovski beads. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image010.jpg" alt=""></strong></p>
<p><strong>I will draw inner embroidering guidelines right in the process. </strong></p>
<p><strong>For now, I have drawn one arc which I will embroider with the largest pearl beads (8mm). </strong>And then I will move in different directions embroidering more arcs with beads of different sizes.</p>
<p><strong>It was not my goal to show you how to embroider some complicated pattern. </strong>Such arcs or half-circles are more than enough for learning and practicing the tambour embroidering technique.</p>
<p>They look quite pretty and don’t take long to make!</p>
<p><strong>Let us get back to work.</strong></p>
<p><strong>It all starts from a knot of course. </strong></p>
<p><strong>It is recommended to tie these knots outside the embroidery area. </strong>In this particular case, I will tie a knot outside the perimeter of the embroidery area but within the seam allowance for joining the mesh fabric with the base of the purse. This is compulsory, or else I might accidentally trim away the knot when I am cutting out the embroidered piece.</p>
<p><strong>Making a knot is very easy:</strong> I insert the needle in the start point, pull a loop up and out, make a stitch forward, grab the thread, and pull it out, and then make a stitch backward, grab the thread, pull it out − that’s it, the knot is ready!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image016.jpg" alt=""></p>
<p><strong>Here it is!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image018.jpg" alt=""></p>
<p><strong>I make a stitch from the knot to the point where I want to put the first pearl bead. </strong></p>
<p><strong>The algorithm is the same as before: </strong>I insert the needle in the desired point (screw facing forward and pointing in the embroidering direction), grab the thread, rotate the hook 180 degrees (screw facing backwards), and pull a loop up to the top side of the mesh fabric.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image022.jpg" alt=""></p>
<p><strong>I start stitching with large pearl beads. </strong></p>
<p><strong>I slide the first bead up the thread. Then I determine the length of the stitch based on the size of the bead, insert the needle in the fabric, and finish the stitch catching the bead and securing it in place. Please remember to pay attention to the position of the screw. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image026.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image028.jpg" alt=""></p>
<p><strong>The first stitch with the first pearl bead is finished. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image030.jpg" alt=""></strong></p>
<p><strong>I slide a second pearl bead up the thread and continue embroidering the arc as marked. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image038.jpg" alt=""></p>
<p><strong>I have approached the last pearl bead. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image042.jpg" alt=""></p>
<p><strong>The first arc is finished. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image044.jpg" alt=""></p>
<p><strong>Now I need to secure this row of pearl beads. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p><strong>I make a stitch outside the perimeter of the embroidery area like I did at the beginning, tie a knot within the seam allowance of the supposed joining seam, and trim the thread. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image046.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image048.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image050.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image052.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image054.jpg" alt=""></strong></p>
<p><strong>I smear both knots with a little Gutermann glue just in case. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image056.jpg" alt=""></strong></p>
<p><strong>Let us see what it looks like from the face side. </strong>This row of large pearl beads will look neater after I embroider more rows next to it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image058.jpg" alt=""></p>
<p><strong>I continue filling in the inner embroidery area. </strong></p>
<p><strong>Right now I will add a row of 6mm pearl beads from either side of the first row. </strong>I mark two guidelines considering the size of pearls in the adjoining row.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image060.jpg" alt=""></p>
<p><strong>I start the first new row from tying a knot on the seam allowance just like before. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image064.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image066.jpg" alt=""></p>
<p><strong>The knot is ready and I can start embroidering!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image068.jpg" alt=""></p>
<p><strong>I start from making a stitch toward the start point of the guideline.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image070.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image072.jpg" alt=""></p>
<p><strong>And I start the first stitch with a pearl bead. </strong></p>
<p><strong>I slide the bead up the thread, determine the required length of the stitch, and make it accordingly. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image074.jpg" alt=""></strong></p>
<p><strong>The first bead is in place. I continue stitching until I reach the end of the arc. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image076.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image078.jpg" alt=""></strong></p>
<p><strong>After securing the last bead, I make a stitch outside the perimeter of the embroidery area and tie a knot. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image080.jpg" alt=""></strong></p>
<p><strong>The face side of the piece looks like this now: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image082.jpg" alt=""></strong></p>
<p><strong>Now I will make a symmetric row of 6mm pearl beads from the other side of the row of large beads. </strong>I tie a knot, make a stitch toward the start point, and start stitching with pearl beads following the guideline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image084.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image086.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image088.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image090.jpg" alt=""></p>
<p><strong>The third row also finishes with a knot. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image092.jpg" alt=""></p>
<p><strong>I smear all knots with fabric glue just in case. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image094.jpg" alt=""></p>
<p><strong>You can already see a clear pattern. </strong>The rows are still a little uneven but they will look neater when the embroidery is finished.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/3.files/image096.jpg" alt=""></p>

