<p><strong>I make a knot and start stitching with beads along the guideline. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/decoration/Tambour%20or%20Luneville%20Embroidery/4.files/image062.jpg" alt=""></p>
