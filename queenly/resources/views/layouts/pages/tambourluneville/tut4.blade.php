<p><strong>I reach the end of the arc. </strong>Instead of making a knot, I make a stitch outside the perimeter of the piece and then a second stitch along the outline and a third stitch back inside the embroidery area to get in the start point of the second guideline for 4mm beads. I will move in the opposite direction now.</p>
