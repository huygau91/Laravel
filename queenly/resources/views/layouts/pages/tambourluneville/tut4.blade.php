<p><strong>Now I will work on the corner of the embroidery. </strong></p>
<p><strong>I will make one row of 5mm pearl beads and two rows of 4mm pearl beads. The remaining blanks will be filled in with 3mm beads. </strong></p>
<p><strong>I mark a guideline and start stitching with 5mm pearl beads. </strong></p>
