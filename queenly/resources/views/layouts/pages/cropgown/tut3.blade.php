<h1>Tutorial 2. Getting Acquainted with the Lace Pattern.</h1>
<p><strong>After adjusting the size of the initial pattern, I need to work on the lace fabric pattern. </strong></p>
<p><strong>I start from the front of the top.</strong></p>
<p><strong>The first thing I need to do is draw the whole front using the adjusted half-piece pattern <em>without seam allowances.</em></strong> The initial pattern given in the book has no seam allowances either. If you decide to use it again for sewing some other garment, you will need to add them. Usually, I always include seam allowance in patterns of corset-based garments but this is not the case!</p>
<p><strong>So, I have prepared a full pattern of the front and I want to draw a beautiful lace ornament on it. </strong></p>
<p><strong>You can feel free to use my ornament </strong>but your own ideas are, of course, always welcome.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%202.files/image002.jpg" alt=""></p>
<p><strong>Before we continue, let me briefly tell you about pyrography on fabric. </strong>This technique will be used in the making of our wedding fashion masterpiece.</p>
<p><strong>This technique is <em>relatively</em> easy and quite fast.</strong> I bet many of you did something similar back in childhood, and you should have no problems with it today either.</p>
<p><strong>Pyrography can be used for lots of things. </strong></p>
<p><strong>It is hardly possible to name all situations when it comes in handy in wedding fashion, for example. </strong>Doing pyrography on fabric allows you to create 3D flowers, 3D appliqué compositions, various ruffles, and other decorative elements. You can burn patterns and ornaments on fabric − in different colours, too. You can imitate lace fabric. Nothing will limit your creativity if you master this technique!</p>
<p><strong>But it is my duty to warn you against non-professionalism! The look of amateurish pyrography works on the Internet leaves much to be desired. </strong>Of course, there are true masterpieces made by talented artisans but those are very rare! And the majority of works widely exposed on various websites are obviously amateurish. Take a closer look and you will notice uneven lines, lack of symmetry, and other nuances that make it look messy. It is, of course, tedious work but it is not done professionally!</p>
<p><strong>Such examples of pyrography on fabric are mainly represented by DIY interior objects which are very popular today. They are basic things like</strong> table napkins, tablecloths, cookies baskets, or (at the very most) separate motifs on kitchen curtains or decorative curtain borders. And you can notice impatience, non-professionalism, and plain untidiness even behind these simplest things. Doing pyrography on fabric does not take long at all: you can easily decorate a kilometer of curtain trim in one evening. And so whenever I see those works, I really want to ask the person who made it: “Why did you have to hurry so much? Why could you not invest just a little more time in it or make less but with much higher quality!"</p>
<p><strong>I am always for thoroughness! </strong>And that is why I don’t like the look of certain works I see on the Internet. They don’t inspire me to do the same! I don’t want to see a single trace of non-professionalism or amateurishness in my garments.</p>
<p>For this very reason, I did not feel like making something primitive when I was thinking over a lace pattern for the top. I wanted to find a golden mean, something not too hard and yet not too easy, and I decided to do simple operations but on multiple layers of fabric.</p>
<p><strong>I will show you how to make ajour lace fabric. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p><strong>I will do pyrography on a garment made of three layers of fabric.</strong> The top will be made of two layers of crepe-back satin and one layer of voile curtain fabric as lining fabric.</p>
<p><strong>Since I don’t want to go amateurish, I have also decided against using connecting brides. </strong>I am talking about those thin bridges that connect separate elements in Richelieu lace, etc. There is a similar thing in pyrography. If I used a Richelieu pattern as a base for my pyrography work, it would definitely involve some detached elements. There could be, for example, a cut-out with a certain element positioned inside it and connected with the main fabric with bridges. Those bridges or brides are just thin strips cut in the fabric. It is these bridges that make your work look non-professional when you lack some experience and don’t have a full picture of the finished garment in your head. And this is why there will be no bridges on my lace.</p>
<p><strong>Just to make it clear, my lace pattern can be compared with a fretwork pattern.</strong> There is a whole surface in which you cut holes of different shapes to form a certain ornament. Another thing you can think of is a stencil sheet. In order to transfer an image on fabric or on the wall, you need to draw it correctly on the sheet so it stays whole after you make the cut-outs.</p>
<p><strong>My lace pattern was made based on the same principle. </strong>I saw some gorgeous lace fabric online and I felt like making something similar. There was no way I could find ready lace fabric like this in stores. But I really loved the ornament! And I started drawing!</p>
<p><strong>I put the half-front pattern before me and started drawing.</strong> Those were real throes of creation. I drew with a pencil, erased it, drew it all over again, erased it again, and fixed things here and there. Then I outlined with a pen what seemed to be the final version but the result was not what I wanted. I glued bits of paper over certain parts and re-drew them all over again.</p>
<p><strong>At last, I achieved what I wanted and outlined the contours with a black marker.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%202.files/image004.jpg" alt=""></p>
<p><strong>I transferred the final version on a newly printed pattern. It was easy because the contours showed well enough through the paper. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%202.files/image006.jpg" alt=""></p>
<p><strong>Then I scanned the pattern with the ornament and polished it up in Photoshop</strong> to make all lines perfectly smooth. <strong>I also used Photoshop to make a mirrored copy of the image and created a full picture of the front half of the top. </strong></p>
<p><strong>You can see the result in the picture below. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%202.files/image008.jpg" alt=""></strong></p>
<p><strong>Please note that the lace will stay whole after you make all cut-outs. </strong>If I had drawn some kind of a flower inside the big cut-out in the middle and then cut it out too, then this flower would stay on the table after I picked up the piece. I would have to somehow put it back in the big cut-out. And, of course, I would need to use brides in that case. But I decided to do without brides because you are not experienced in pyrography yet and you don’t know the properties of fabrics you are going to use.</p>
<p><strong>If you want to make your own lace pattern or adjust mine in some way, </strong>I highly recommend you should start from stencil or fretwork patterns. Although there may not be too many of them online, you can always take a pencil and customize them to your liking. Any Richelieu embroidery pattern can be turned into a stencil pattern: simply shade all supposed cut-outs and you will see what elements are fully surrounded by the shaded areas. These elements will end up detached so you need to find a way to connect them with the main fabric.</p>
<p><strong>Last but not least: how to adjust the size of the ornament after adjusting the initial pattern? </strong></p>
<p><strong>If you need to make the ornament smaller, </strong>put it over your new adjusted piece and see what part of it does not fit in. It is not a big deal if some or other element does not fit in at the edges but other areas will most likely require minor adjustments.</p>
<p><strong>If you need to make the ornament larger,</strong> then add some simple elements at the sides and at the shoulders. Such changes are nearly unnoticeable!</p>
<p><strong>And, of course, it is very easy to adjust the size of the lace pattern in Photoshop.</strong> Scan the adjusted pattern, add an overlay with the lace pattern file, and then shrink or expand it. <strong>Photoshop is really worth learning!</strong> It is really great software which allows you to do lots of things: from editing pictures of finished garments to determining correct garment proportions before starting a new sewing project.</p>
<p><strong>I also want to say a couple of words about choosing fabric for your pyrography project.</strong> There is only one requirement − <strong>it must be 100% synthetic. </strong>This is good news for your budget because you can create a fabulous garment with even the cheapest lining fabric! I have chosen fabric of higher quality (crepe-back satin) and I will use both the matte side and the sheen side. The lining will be sewn with voile curtain fabric. You can also use all kinds of synthetic satin, chiffon, and organza. I recommend you first practice with non-stretch fabrics until you feel confident enough to work with stretch satin and other similar fabrics. <strong>And, of course, don’t forget that you will need a pyrography tool. </strong></p>

