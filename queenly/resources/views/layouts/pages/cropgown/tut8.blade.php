<h1>Tutorial 7. Joining the First Layer with the Second Layer.</h1>
<p><strong>Here is what I have achieved by now: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image002.jpg" alt=""></p>
<p><strong>Please note that only the bottom edge of the piece is cut out properly at this stage. The low back cut-out, the sides, the armscyes, the shoulders, and the neckline have not been finished yet. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image006.jpg" alt=""></p>
<p><strong>I need to turn around the first layer to continue. I have noticed an error which needs to be fixed. </strong></p>
<p><strong>Remember how I marked the overall dimensions with crosses on the paper pattern?</strong> I drew those crosses at random and did not account for the fact that they need to be aligned when I turn the layer face side to the pattern. <strong>The crosses must be positioned symmetrically against the centre of the ornament. </strong></p>
<p><strong>I unpin and take off the top layer of fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image008.jpg" alt=""></p>
<p><strong>In order to position the crosses symmetrically, I fold the paper pattern with the tracing paper precisely in half, leave the crosses where they are on the first half (simply outline them to make them brighter), and transfer them onto the other half. It is easy because they show through.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image016.jpg" alt=""></p>
<p><strong>Now the crosses that mark the dimensions of the pattern are symmetric. I put the first layer back in place carefully aligning the contours of the ornament, partially secure it with pins to prevent it from shifting, and transfer the new crosses on it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image022.jpg" alt=""></p>
<p><strong>Now everything is right both on the patter and on the first layer of fabric.</strong></p>
<p><strong>I remove the pins, turn the first layer paper backing up, move the whole lay to the regular table for pressing, and align the crosses on the pattern with those on the first layer of fabric.</strong> It is much easier to align these crosses than the ornaments themselves.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image026.jpg" alt=""></p>
<p><strong>I pin the fabric to the paper again.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image028.jpg" alt=""></p>
<p><strong>And then, very slowly and carefully, I start peeling the paper backing off the fabric.</strong> I try not to pull too hard to avoid deformation. The ornament was made to resemble a stencil pattern, which means no elements are supposed to stay on this paper backing after I remove it: the ornament will stay whole. The backing peels off without any effort. I purposely opted for an ornament made of rather large elements to make it easier. After mastering the technique, you will be able to create amazing one-of-a-kind masterpieces by playing with different colors and textures.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image032.jpg" alt=""></p>
<p><strong>The first layer looks like this:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image034.jpg" alt=""></p>
<p><strong>I remove the pins and double-check the alignment of ornament contours. </strong>Removing the paper backing always causes minor deformation: certain elements may have been shifted against each other. I urge you to try to minimize any irregularities at this early stage. Of course, some things may slip your attention but, on the whole, the ornament must stay perfect because the work is not over yet. And it is exactly why I compare the ornament on the first layer with the initial ornament on the paper pattern: I still need to trim the fabric along the armscyes and along the side seam lines, make a cut-out in the middle, etc. In short, the contours must be aligned very well!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image036.jpg" alt=""></p>
<p><strong>Once everything is checked, I prepare a piece of crepe-back satin for the second layer. </strong>Please remember that the face matte side of the first layer is now facing down. It is lying wrong side up. But the second layer will have its sheen side as the face. For this reason, I put it matte side up now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image038.jpg" alt=""></p>
<p><strong>I carefully align the selvages to keep to the direction of the lengthwise thread, secure it with pins, and start pressing it with the press iron. </strong></p>
<p><strong>I press it very carefully! No steaming, no moving hither-and-thither! And make sure to allow no air bubbles!</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image040.jpg" alt=""></strong></p>
<p><strong>I trim excess fabric, remove the template, and turn the lay around. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image044.jpg" alt=""></p>
<p><strong>The result is a matte ornament set against a sheen background! </strong>All cut-outs are backed by the sheen side of the second layer of crepe-back satin.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image046.jpg" alt=""></p>
<p><strong>Now you can safely press the lay from both sides. </strong>Press at moderate temperature and be careful with steam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image050.jpg" alt=""></p>
<p><strong>Next, I will return to the tracing table, secure the paper pattern with tracing paper on it, and put down the lay aligning the crosses.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image052.jpg" alt=""></p>
<p><strong>Then I will start doing pyrography on the second layer. </strong>I will need to burn the elements with the red outlines, as well as the bottom border of the ornament which is marked blue.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%207.files/image054.jpg" alt=""></p>
<p><strong>The piece already looks gorgeous and can easily be used as a decorative element!</strong></p>
