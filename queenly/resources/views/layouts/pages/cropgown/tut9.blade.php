<h1>Tutorial 8. Doing Pyrography on the Second Layer of the Back Piece.</h1>
<p><strong>I am about to start working on the second layer of fabric for the back half of the top.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image172.jpg" alt=""></strong></p>
<p><strong>I will first burn the elements with red outlines. </strong>This will make the garment more ajour because these elements are positioned inside the cut-outs already made in the first layer.</p>
<p><strong>I start by making small cut-outs at a distance from the main ornament.</strong> I need to burn two layers of crepe-back satin simultaneously this time, so I need to choose the right temperature. Besides, it allows me to test the tracing paper which is now the only backing between the fabric and the paper pattern.</p>
<p>Do not forget that I will also need to burn holes in a single layer of fabric like before. Keeping all this in mind, I have chosen medium temperature. I will simply move the tip slower when burning two layers simultaneously.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image173.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image174.jpg" alt=""></p>
<p><strong>I have set an appropriate temperature and now I can start doing pyrography. </strong></p>
<p><strong>I burn holes in two layers of fabric. The technique stays the same: </strong>I start from the edge of the element and move the tip without interruption to make a closed outline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image175.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image176.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image177.jpg" alt=""></p>
<p><strong>Then I move to the elements with red outlines which need to be burned in a single layer of fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image178.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image179.jpg" alt=""></p>
<p><strong>Then I burn more shapes in the two layers of crepe-back satin. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image180.jpg" alt=""></p>
<p><strong>Tip:</strong> when making heart-shaped cut-outs, make the tip go inside the element so as to draw a corner inside it. It will be easier to remove the cut-out fabric in this case.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image181.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image182.jpg" alt=""></p>
<p><strong>I look for more red outlines and burn them. </strong>Remember to clean the tip with a cloth from time to time.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image183.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image184.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image185.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image186.jpg" alt=""></p>
<p><strong>I compare it with the initial ornament to check whether I have burned all red elements. I have found a couple more. The work continues. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image187.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image188.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image189.jpg" alt=""></p>
<p><strong>Now all elements with red outlines have been burned. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image190.jpg" alt=""></p>
<p><strong>I take a pair of scissors and start taking out the detached pieces of fabric. </strong>I also remove occasional threads.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image191.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image192.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image193.jpg" alt=""></p>
<p><strong>The first stage is over. </strong>The ornament looks more ajour now.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image194.jpg" alt=""></p>
<p><strong>Now I can move to the next stage: make new cut-outs in the second layer of crepe-back satin based on the contours of the existing ones.&nbsp; </strong></p>
<p><strong>I remove the pins, take the lay off the paper pattern, and put it on the tracing table. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image195.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image196.jpg" alt=""></p>
<p><strong>What I will do now is add scallops along the edges of the larger cut-outs in the first layer of fabric. As the result, all cut-outs will be framed by scalloped-edge borders of sheen crepe-back satin. </strong></p>
<p><strong>As usual, I start by testing it on a small piece of fabric outside the actual ornament. </strong>It is hardly possible to make perfect scallops but they will be so small that minor variance will, hopefully, be unnoticeable.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image197.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image198.jpg" alt=""></p>
<p><strong>Let me start. </strong>This work does not require turning on the lamp light.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image199.jpg" alt=""></p>
