<p><strong>I burn the armscye line as a straight line following the existing edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image201.jpg" alt=""></p>
<p><strong>The first cut-out is finished. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image202.jpg" alt=""></p>
<p><strong>I remove the middle. What remains is a sheen scalloped border.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image203.jpg" alt=""></strong></p>
<p><strong>The result is an ajour cut-out which will later get backed by voile fabric. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image204.jpg" alt=""></p>
<p><strong>Every element will be decorated in this fashion. Apart from looking really pretty, these scallops make the edges look more even and add a little shine to them. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%208.files/image205.jpg" alt=""></strong></p>
<p><strong>I recommend you should work on pairs of symmetric elements to maintain the same size of scallops and the same width of borders. </strong></p>
