<h1>Tutorial 32. Putting Horizontal Ruffles on the Skirt. Part 3.</h1>
<p></p>
<p><strong>I have gradually approached the top of the side piece and reached the level of 7.5cm ruffles. </strong></p>
<p><strong>The length of the guideline </strong>does not allow me to put just one 7.5cm ruffle along it because there will be about four more centimeters left in this case. I need to use another 7.5cm ruffle or, more precisely, a half of it. But, since I have reached the top of the skirt, I will purposely add a full 7.5cm ruffle to make this area less see-through. </p>
<p><strong>As the result, most part of this row of ruffles will be double. </strong></p>
<p><strong>Before this area, </strong>I always turned the piece on the needle after finishing a row and moved along the next guideline in the opposite direction. For this reason, the overlap points were always placed in different parts of the guidelines: closer to one end on odd guidelines and closer to the opposite end on even ones. </p>
<p><strong>But there are so many ruffles at the bottom of the piece right now that I won�t be able to turn it on the needle like before. </strong>I will always sew in the same direction: from the front edge to the princess seam of the back. </p>
<p><strong>I suggest the following method of putting ruffles on the piece </strong>to maintain the overall symmetry with adjoining rows looking in the opposite directions when you cannot turn the piece on the needle.</p>
<p><strong>I will put the second ruffle over the first one on odd guidelines and under it on even guidelines. </strong>In this case, the rows will be arranged chequerwise even if I always sew in the same direction. </p>
<p><strong>To make things easier, </strong>I number the guidelines and highlight the odd ones blue and the even ones red. </p>
