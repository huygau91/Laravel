<p><strong>Make sure not to catch the ruffles when pinning the waistband to the skirt. I secure the hanging loops in the upward position to avoid catching them too. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image046.jpg" alt=""></strong></p>
<p><strong>I use a generous amount of pins at basically just 2cm intervals to prevent the lining from getting gathered when I start sewing. This may happen because the garment is rather thick in that area. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image048.jpg" alt=""></p>
<p><strong>Upon examining the joint, I have decided against decorating it with lace because it simply won�t match the overall style. Instead, I will put a double horizontal ruffle right along the joining seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image050.jpg" alt=""></p>
<p><strong>I have made the last seam and secured the waistband for good. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image052.jpg" alt=""></p>
<p><strong>I have answered another frequently asked question: how to sew a skirt to a corset? </strong>This is one of the possible ways. The seam looks clean and pretty and the inside of the gament is neatly finished with a lace trimming. It would be the same if I were working with a corset and not a waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image054.jpg" alt=""></p>
<p><strong>After measuring the length of the sewing line, I cut sixteen 7cm ruffles with wavy edges and pin them on the waistband along the joining seam in the opposite direction</strong> so as to make them hang down the same way as the two vertical ruffles already added at the top.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image060.jpg" alt=""></p>
<p><strong>I sew the last top ruffle on the garment.</strong> It is quite hard to make a neat row of stitches along the very edge of the waistband so I sew the ruffle on by hand first. Using the sewing machine is not compulsory because the seam will be disguised by the ruffles anyway.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image062.jpg" alt=""></p>
<p><strong>And now, as was promised before, I want to show you how to make a pretty flower from carbage left after cutting the ruffles.</strong></p>
<p><strong>I take 9 circles left after cutting the ruffles. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image064.jpg" alt=""></p>
<p><strong>I put the first circle on a cotton cloth. Then I take the next circle and fold it in half two times. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image066.jpg" alt=""></p>
<p><strong>I put the folded circle in the middle of the first circle, fuse it on with the pyrography tool, and press it down firmly with my finger. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image068.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image070.jpg" alt=""></p>
<p><strong>I fold another circle the same way as the previous one, turn the cloth around, put the new piece down with a little overlap, and fuse it on in the middle of the background circle. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image072.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image074.jpg" alt=""></p>
<p><strong>I continue moving in the round and fusing the rest of the petals on the background circle.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image076.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2041.files/image078.jpg" alt=""></p>
