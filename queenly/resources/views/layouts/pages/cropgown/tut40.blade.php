<h2>Tutorial 39. Putting Vertical Bones on the Face of the Waistband.</h2>
<p><strong>I continue working on the waistband. </strong></p>
<p><strong>I need to put wide vertical Rigilene bones on its face. </strong>I will mark their positions, decide on their lengths, and encase them in the same crepe-back satin fabric the waistband is made of.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image002.jpg" alt=""></p>
<p><strong>I have cut and pressed strips of crepe-back satin with a width of 3cm. The matte side will be used as the face of the casing. </strong>I will leave 1.5-2cm ends which will be folded together with the seam allowance of the waistband.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image006.jpg" alt=""></p>
<p><strong>Wide Rigilene bones will be put along all princess seams and along the side seams. </strong></p>
<p><strong>Since the side front piece is rather wide, I will reinforce it by sewing the side seam bone and the princess seam bone right on it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image010.jpg" alt=""></strong></p>
<p><strong>I will also put bones along the lacing edges from the face side of the waistband. </strong></p>
<p><strong>When I was working with the lining, I decided to put another bone along the lacing edge. </strong>As the result, there are two wide Rigilene bones on the central back part of the lining. This was done to increase the distance between the lacing loops and prevent them from coming edge to edge. Apart from its main function, the lacing is also a decorative element and the waistband will look prettier with slightly parted lacing edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image012.jpg" alt=""></p>
<p><strong>The wide vertical Rigilene bones along the central back edges </strong>will be placed at the same distance from the edge as on the lining.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image014.jpg" alt=""></p>
<p><strong>There will be a total of eight wide vertical Rigilene bones on the face of the waistband: four on either half. </strong></p>
<p><strong>I mark their positions and measure their lengths. </strong></p>
<p>I leave 1.5cm allowance from the top and the bottom edge. The bone going along the back princess seam (1) and the side seam bone (2) have the same length − 9.7cm. The bone going along the front princess seam (3) is 10.1cm long. I will mark the positions of bones along the lacing edges later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image016.jpg" alt=""></p>
<p><strong>I carefully press a Rigilene bone, mark the length of each bone two times in a row, cut it accordingly, and seal the ends of all bones with masking tape. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image020.jpg" alt=""></strong></p>
<p><strong>Now I can mark the positions of lacing edge bones (4). </strong></p>
<p><strong>I measure the distance between the lacing edge and the second vertical bone on the lining. </strong>It is 4cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image022.jpg" alt=""></p>
<p><strong>I mark the position of bone (4) on the face of the waistband</strong> 4cm from the lacing edge and measure its supposed lengths. It is 9.7cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image024.jpg" alt=""></p>
<p><strong>I mark 9.7cm two times and cut two bones accordingly. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image026.jpg" alt=""></p>
<p><strong>I seal the ends of the bones thoroughly</strong>, first with electrician’s tape and then with masking tape, to make sure no fishing lines will poke out.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2039.files/image028.jpg" alt=""></p>
<p><strong>I encase the bones in fabric using the matte side as the face </strong>and leaving about 2cm tails from either end.</p>
