<h1>Tutorial 4. Pyrography Tool and Equipment.</h1>
<p><strong>The first thing you need is a tracing table. </strong>It is easy to make one yourself. Take two small plastic baskets, put them upside down on the table, put a plain glass panel on them (slightly larger size than your pattern), and secure it with sticky tape. Make sure you can put a lamp underneath the glass panel.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image004.jpg" alt=""></p>
<p><strong>Needless to say, the main equipment is a pyrography tool with various useful accessories. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image008.jpg" alt=""></p>
<p><strong>My pyrography tool has variable temperature control.</strong> It is an essential feature because I plan to burn very delicate fabric, as well as several layers of fabric simultaneously. This means I will need to work with different temperatures.</p>
<p>There is a wide range of pyrography tools available today: from expensive to really cheap ones. They are basically the same wood burning tools but you need special interchangeable fine tips which are sold separately. Since pyrography is very popular today, you should not have any problems finding a suitable tool and accessories.</p>
<p><strong>Other things you will need are short metal ruler, sticky tape, pencils, and pens. </strong>I must warn you against using markers or gel ink pens because they may stain your fabric.</p>
<p><strong>Another essential material you absolutely cannot do without is fusible web with paper backing sold in rolls or in sheets.&nbsp; </strong>You can use fusible web without paper backing if you really have to, but it will be a lot less convenient.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image010.jpg" alt=""></p>
<p><strong>You will also need some dense tracing paper or baking paper. </strong>It is supposed to protect the paper with the ornament from getting burned. One sheet of tracing paper will be enough in our case.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image012.jpg" alt=""></p>
<p><strong>I will put the pattern on the tracing table and cover it with tracing paper for protection.</strong> The top layer will be fabric duplicated with fusible webbing.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image014.jpg" alt=""></p>
<p>I need to prepare the ornament before we continue. I print out the sheets and carefully glue them together.</p>
<p><strong>I have assembled the front of the top and now I need to assemble the back. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image016.jpg" alt=""></p>
<p><strong>I will use separate patterns for the left sleeve and the right sleeve for the sake of convenience. You don’t need to glue anything together here. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image018.jpg" alt=""></strong></p>
<p><strong>Two help sheets will help you avoid confusion when putting together separate pattern sheets. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image020.jpg" alt=""></p>
<p><strong>I will first do pyrography on the back half of the top. </strong></p>
<p><strong>I glue together the separate parts of the back. </strong>Make sure to align the sheets as neatly as you can, both vertically and horizontally.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image030.jpg" alt=""></p>
<p><strong>After assembling the back piece, I restore all lines that were not printed properly.</strong> You often end up with unprinted areas along the edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%204.files/image032.jpg" alt=""></p>
<p><strong>Have you already noticed red, blue, and green lines on the pattern apart from the main black contours? </strong>These lines in different colours indicate the order in which you are supposed to make the cut-outs. You must have a clear understanding of how you will combine separate layers of fabric from the very start. I use several layers of fabric not only to make the garment stronger. Of course, that is also important because a single-layer ajour garment will never be robust enough. But several layers of fabric will allow me to achieve the desired ajour effect.</p>
<p>In this particular case, I will use the matte side of the crepe-back satin as the face of the top layer. The second layer will be the sheen side of the same crepe-back satin. Its edges will go beyond the edges of the ornament cut in the first matte layer adding iridescence to the garment. The third layer will be voile curtain fabric.</p>
<p><strong>When you do pyrography, separate layers of fabric get fused together because of the fusible web and because of the heat itself. </strong>I will follow the red lines for cut-outs in the second layer of crepe-back satin to ensure proper adhesion to the first layer. And I will follow the green lines when working on the third layer thereby fusing the voile fabric with the rest of the garment.</p>
<p><strong>Always remember to test your fabric before you start doing pyrography on it. </strong>I made several samples with cut-outs of different shapes. I tried various ways of combining fabrics for pyrography work and could tell what I liked and what I didn’t. Of course, there are certain fine points and tricks about doing pyrography but they are all easy to figure out. It just takes some thinking over, the same way you think over the right choice of fabric and technique before you start sewing a corset or a dress. You need to know in advance what you will do, in what order, and why. That is why those coloured lines are not random − they show you how to make cut-outs on each separate layer.</p>
