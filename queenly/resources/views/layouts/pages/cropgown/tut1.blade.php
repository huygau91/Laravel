<h1>Foreword.</h1>
<p>Hello, my dear fellow dressmaker! Let us dive into the beautiful world of wedding fashion! You have already seen a picture of the finished garment, and now I invite you to travel back in time to the moment when I was just about to start working on it.</p>
<p><strong>All I had back then was this sketch below. There is a lot of work ahead so let�s go!</strong></p>
<p><strong>This time we will be sewing a two-piece wedding dress which consists of a top and a skirt. </strong></p>
<p><strong>The fact that this dress is actually made of two separate garments makes it incredibly popular and multi-functional! </strong>Such dresses are a good choice for basically any body type. You can visually improve any figure by setting correct proportions to the two components of the dress. You can make a plump girl look taller and thinner or, vice versa, add fuller curves to a skinny girl.</p>
<p>Dresses like this are trending this year. Very many brides opt for an outfit made of a top and a skirt. And you must have already guessed that it implies using interchangeable parts which allow you to create a countless number of combinations!</p>
<p><strong>There is one even more special thing about the dress we are going to make!</strong> I am sure you have noticed that the top is made of some fabulous lace fabric! And this unique lace fabric is fully hand-made! I will create it right before your eyes.</p>
<p><strong>I suppose I have convinced you that there are unlimited possibilities waiting ahead! </strong></p>
<p>Moreover, I will also explain certain laws and rules which will help you choose or create patterns for your lace fabric correctly to achieve the best results and complement the garment.</p>
<p><strong>Let me say a couple of words about the skirt now.</strong> You can find detailed guidelines to sewing similar skirts in my previous books and video tutorials. But <strong>this particular skirt will be made in a totally different technique. </strong></p>
<p><strong>Firstly,</strong> I will use a pattern which was never used for sewing skirts like this before. I will show you how to customize it and make it suitable for sewing a ruffle skirt.</p>
<p><strong>Secondly,</strong> there will be no complicated inner supporting construction in this skirt. It will be lightweight and �mobile" and at the same time it will hold its shape very well under any conditions. This is another advantage of this skirt!</p>
<p><strong>And, thirdly, </strong>I will show you an absolutely new technique for assembling ruffle skirts. I am sure you will use it widely in the future.</p>
<p><strong>What are the benefits of this new technique?</strong></p>
<p><strong>First of all, it really helps you save a lot of time on working with complicated skirts like ours. It makes things easier physically. </strong>My new technique eliminates all those physical challenges that await you in the process of assembling heavy wedding skirts with lots and lots of ruffles. It will be a lot less tiresome.</p>
<p><strong>In addition, this technique allows you to assemble such skirts on a regular household sewing machine. </strong></p>
<p><strong>I will also suggest a totally new shape for the ruffles </strong>themselves. They will be different from the ones used in the previous books by the Corset Academy.</p>
<p><strong>The new technique will also allow you to adjust the shape of the skirt right in the making. </strong>This is very important for emergency cases when your client suddenly asks to adjust the shape of a nearly finished skirt. Such things are very hard to do when you stick to the traditional technique! But my new technique will help you fulfill your client�s wish: you will be able to increase or decrease the fullness of the skirt, prolong the train, adjust the shape of the train, etc. That means a lot!</p>
<p><strong>I believe every dressmaker ought to know this particular technique of sewing complicated wedding skirts with ruffles! </strong></p>
<p>Let us get to work then! You are about to learn many useful things!</p>

