<h1>Tutorial 25. Cutting Smooth-Edge Ruffles for Vertical Rows.</h1>
<p><strong>I printed out, assembled, and cut out the ruffle patterns enclosed with the book. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image002.jpg" alt=""></p>
<p><strong>I also cut them from quality cardboard. These templates will be used for cutting ruffles from voile fabric. </strong></p>
<p><strong>There are five templates for the ruffles which will go along the vertical guidelines. They are 7cm, 8cm, 9cm, 10cm, and 11cm circles with plain smooth edges. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image004.jpg" alt=""></strong></p>
<p><strong>I will cut the ruffles with the pyrography tool.</strong></p>
<p>I hold the tip of the tool at 45 degrees to the edge of the template <strong>to preserve the cardboard and the initial size of the template despite continuous use. </strong></p>
<p><strong>I start from the 11cm wide ruffles.</strong></p>
<p><strong>I put the paper pattern on the table and cover it with a glass panel. </strong>I need the pattern for trimming the length of the ruffles.</p>
<p><strong>I put a piece of voile fabric on the glass panel, put down the cardboard template, and align the slit with the line on the pattern.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image008.jpg" alt=""></strong></p>
<p><strong>I cut out the ruffle.</strong> I move the tip of the pyrography tool along the edge of the template at 45 degrees.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image018.jpg" alt=""></p>
<p><strong>The ruffle is ready. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image020.jpg" alt=""></p>
<p><strong>I have prepared lots of ruffles of the same size without damaging the template. </strong>If you notice some soot along the edges of the template, you can easily remove it with a nail file.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image022.jpg" alt=""></p>
<p><strong>When I need to adjust the length of a ruffle - for example, down to 14cm - </strong>I put it over the pattern seen through the glass, find a mark that corresponds with 14cm, and trim it accordingly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image024.jpg" alt=""></p>
<p>It took me relatively little time to cut ruffles of various widths, sort them out, put them in different boxes, and sign the amount stored in each box on a piece of paper. <strong>I have made</strong> 42 ruffles with a width of 7cm; 39 ruffles with a width of 8cm; 36 ruffles with a width of 9cm; 44 ruffles with a width of 10cm; and 41 ruffles with a width of 11cm.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image026.jpg" alt=""></p>
<p><strong>When putting the ruffles on the skirt, I will start from the largest ones (11cm) and move from down to up gradually decreasing the width. </strong></p>
<p><strong>Let me demonstrate it on the pattern to make it clearer. </strong></p>
<p><strong>I will start from the side piece. </strong></p>
<p><strong>There are two purple guidelines along the princess seam edge of the back piece. They indicate rows of vertical ruffles. </strong></p>
<p><strong>I will start putting 11cm ruffles on the piece from point (1) and stop at the cross which marks the end of the first section. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image028.jpg" alt=""></strong></p>
<p><strong>The seam allowances of the ruffles must be directed toward the seam allowance of the piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2025.files/image030.jpg" alt=""></p>
<p><strong>The length of the ruffle is 18cm while the length of the first section is 29cm. I position the second ruffle in about the middle of the first ruffle to make it stop at the end of the section. </strong></p>
<p><strong><br>
