<h1>Tutorial 19. Installing the Zippers. Sewing the Side Seams.</h1>
<p><strong>When I was sewing the darts, I realized it was a good idea to make rows of strengthening stitches close to the side seam edges and the shoulder seam edges of the pieces to join the voile with the crepe-back satin. </strong>I sew these rows without bar tacks.</p>
<p><strong>The back piece: </strong></p>
<p><strong><br>
