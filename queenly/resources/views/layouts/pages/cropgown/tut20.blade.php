<p><strong>It holds very well. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image032.jpg" alt=""></p>
<p><strong>The remaining sides of the zipper tapes need to be sewn to the seam allowances of the front piece. </strong></p>
<p><strong>I put together the front and the back, face to face, position the zipper as marked, and glue it in place. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image034.jpg" alt=""></strong></p>
