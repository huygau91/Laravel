<p><strong>I switch to an invisible zipper presser foot and sew along one side of the zipper tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image024.jpg" alt=""></p>
