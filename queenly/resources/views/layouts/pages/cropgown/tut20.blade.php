<p><strong>Then I sew one side of the other zipper tape to the seam allowance of the other side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image028.jpg" alt=""></p>
