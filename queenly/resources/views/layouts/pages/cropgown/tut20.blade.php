<p><strong>Now I can sew the zippers in the side seams. </strong></p>
<p><strong>I secure the zipper tape on the seam allowance of the voile fabric with the help of G�termann fabric glue.</strong> It is absolutely impossible to sew it in place evenly without gluing it down first.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image016.jpg" alt=""></p>
<p><strong>I position the zipper tape in accordance with the marks. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image018.jpg" alt=""></p>
<p><strong>I glue the second zipper to the seam allowance of the opposite side seam the same way.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2019.files/image020.jpg" alt=""></strong></p>
