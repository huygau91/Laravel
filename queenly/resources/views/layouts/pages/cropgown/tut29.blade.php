<p><strong>I have something to say before we continue. </strong></p>
<p><strong>Firstly, </strong>I have marked the precise points of transition from one ruffle width to another along the central edge of the back piece to achieve perfect symmetry. I referred to the width and the amount of ruffles sewn on along the princess seam edge. </p>
<p><strong>Secondly, </strong>I felt like slightly changing the layout again. Don�t worry: the layout enclosed with the book is the final version with all adjustments applied. I will talk about it in detail just so you can rely on my experience when sewing ruffles on other garments. I believe you will find it useful. </p>
<p><strong>Please note </strong>that I plan to put quite many vertical ruffles at the bottom of the central back piece, i.e. on the train. Apart from the two rows which go down the full length of the piece, I will put four more short rows in the area of the flare. As the result, there will be six rows of ruffles arriving at the bottom of the piece at about 1cm intervals. Considering their expected width (11cm), the train will be framed by lots and lots of rich ruffles. </p>
