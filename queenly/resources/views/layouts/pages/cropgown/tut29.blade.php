<h1>Tutorial 28. Putting Ruffles on the Central Back Piece.</h1>
<p></p>
<p><strong>First, I make a long row of ruffles along the princess seam edge of the central back piece. </strong>I start from the top of the piece. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the first 7cm ruffles. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>And I overlap it with the second 7cm ruffle. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Look how neatly these ruffles settle down:</strong> it is very easy to control their position under the presser foot. The second ruffle settles upon the first one fully repeating its configuration, which makes the sewing process a lot more comfortable. </p>
<p><strong>And I hope you have noticed the benefits of reinforcing vertical seams with soft Rigilene boning. </strong>If I had not reinforced the vertical seams of the pieces, the main fabric (organza) would have likely started getting gathered when I were sewing ruffles on the piece, which could in its turn affect the length of the skirt (and differently in different areas, too). <strong>The Rigilene boning </strong>makes each piece dense enough and you can easily put ruffles on it. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2028.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>It is turn to sew four 8cm ruffles on the piece. </strong></p>
<p><strong>Let me give you a piece of advice which will help you avoid certain mistakes when working with ruffles of different widths.</strong>&nbsp; </p>
<p><strong>You can tell, of course, that putting ruffles on garment pieces is not hard but quite monotonous work and requires continuous attention.</strong> It is easy to get confused, especially if you get distracted by something. You may forget how many ruffles of a particular width you have already attached and how many are still to go; what type of ruffles you are putting on the piece right now and what type is supposed to come next. </p>
<p><strong>I recommend you should stick to a fixed order to make things easier and avoid major mistakes. </strong></p>
<p><strong>You probably remember that I sorted all ruffles by size, put them in different containers, and put a piece of paper with the size marked on it in each container. </strong></p>
<p><strong>Now I need to put four 8cm ruffles on the piece. </strong>I put the container with 8cm ruffles before me, take out four ruffles, and put them on the sewing machine. Now I won�t have to count how many ruffles of this size I have already sewn on the piece and how many I still need to add. And I leave the signed container before me as a hint to remember that I am now working with 8cm ruffles. </p>
<p><strong>After I secure the last 8cm ruffle, I look at the layout to see what size comes next. </strong></p>
<p><strong>In my case, I need to sew two 9cm ruffles on the piece now. </strong>I put away the container with 8cm ruffles and put the container with 9cm ruffles before me. I take out two 9cm ruffles, put them on the sewing machine, and keep the container near as a hint. I continue sewing. </p>
<p><strong>It is almost impossible to make a mistake when you stick to this algorithm!</strong></p>
