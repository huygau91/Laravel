<p><strong>And, therefore, I have decided to do the following. </strong></p>
<p><strong>Firstly, </strong>I will remove the short row of ruffles next to the seam allowance line. There will be enough ruffles on the train without it. </p>
<p><strong>Secondly, </strong>I will change the width of the ruffles at the bottom of the central back pieces, i.e. on the train. </p>
<p><strong>Here is how many rows of ruffles are to be put along the central edge of the central back piece in accordance with the new layout (moving up to down):</strong></p>
