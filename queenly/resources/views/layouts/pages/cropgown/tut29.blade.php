<p><strong>Row (1).</strong> This row runs down the full length of the piece. It is made of three 7cm ruffles, two 8cm ruffles, two 9cm ruffles, and three 10cm ruffles. I will use one 9cm and three 8cm ruffles instead of 11cm ruffles closer to the bottom of the piece to decrease the fullness. </p>
<p><strong>Row (2).</strong> It is a short row made of three 9cm ruffles and one 8cm ruffle. </p>
<p><strong>Row (3).</strong> It runs down the full length of the piece starting at the top and going right along the seam allowance line. It is made of three 7cm ruffles, two 8cm ruffles, two 9cm ruffles, four 10cm ruffles, and two 9cm ruffles. Only the top and the bottom sections stay on the other back piece. </p>
<p><strong>Row (4).</strong> It is a short row made of two 10cm ruffles.</p>
<p><strong>Row (5).</strong> It is another short row made along the seam allowance line. It is made of three 11cm ruffles. </p>
<p><strong>I have managed to put another three short rows along the central edge at the top of the central back piece: </strong>&nbsp;there will be three 7cm ruffles in the inner row (red line), one 7cm ruffle in the middle row (black line), and two 7cm ruffles in the row which runs parallel to the zipper (black dashed line). </p>
<p><strong>Moreover, I have added two rows of horizontal ruffles along the bottom edge of the central back piece to continue the frame which began on the side piece. </strong></p>
<p>There will be three 10cm ruffles in the inner horizontal framing row (green line) and three 11cm ruffles in the outer framing row (blue line). </p>
