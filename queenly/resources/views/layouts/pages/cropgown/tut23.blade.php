<h1>Tutorial 22. Cutting the Skirt.</h1>
<p><strong>I have printed out all pattern sheets. </strong>There are quite many of them because the pieces are rather large. You will find a help sheet which will tell you how to put them together. Please remember to print in the actual size. The pattern includes 1.2cm seam allowances along the top line, the bottom line, and along all princess seam lines and vertical seam lines.</p>
<p><strong>I glue the sheets together. </strong></p>
<p><strong>Attention:</strong> the patterns you have got were adjusted to fit my client�s measurements.</p>
<p><strong>If your client�s measurements are 1-2cm smaller or bigger than these, </strong>then you can use our traditional method of shifting the edges of the pattern inwards or outwards. <strong>If you need to change the size radically, </strong>please use the initial pattern and adjust it to your client�s size using my adjusted pattern for reference.</p>
<p><strong>I put the glued together skirt pieces on the table in the right order and cover them up with a piece of organza folded in half. </strong>I put the front piece to the fold line of the organza. All other pieces need to be positioned correctly with regard to the direction of the lengthwise grain.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image002.jpg" alt=""></p>
<p><strong>I secure the organza with vertically inserted pins.</strong> You have already seen the foam mat I put on my cutting table. It is made of separate pieces.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image004.jpg" alt=""></p>
<p><strong>The base could also be sewn with the same voile curtain fabric used for the ruffles. </strong></p>
<p><strong>Why am I using such thin fabric for the base of the skirt? </strong>My key goal is to create a lightweight delicate skirt. And I hope the bride�s legs will not show through because the skirt will be covered by a rather generous amount of ruffles.</p>
<p><strong>Looking ahead, I can tell you that this skirt will have a lining. </strong>The seam allowances will be on the inside of the skirt. The traditional sewing technique implies assembling the skirt first. The seam allowances are kept on the face and then you sew ruffles on the skirt and overlay those seam allowances. But this time, I will keep the seam allowances on the inside and then cover all seams by the lining. The inside of the skirt will be smooth. The lining will be sewn with the same voile curtain fabric used for the ruffles. It will be cut as a single piece and have no princess seams at the back. The only princess seams will be at the front of the lining.</p>
<p><strong>Unlike before, I haven�t cut out the printed out skirt pieces. I will simply cut the organza following their contours. </strong>I cut the pieces adding about 5mm along the perimeter lines.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image012.jpg" alt=""></p>
<p><strong>I have cut all main skirt pieces from organza. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image014.jpg" alt=""></p>
<p><strong>After cutting the pieces, I take a regular pencil and start transferring the ruffles layout on the top layer of organza.</strong> I don�t take the pieces off the paper patterns: they stay pinned down. Make sure to hold the fabric down with your hand to prevent it from getting shifted as you transfer the lines. I draw crosses where the colour of the line is supposed to change (and, therefore, the size of the ruffle).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image020.jpg" alt=""></p>
<p><strong>Then I carefully transfer the vertical ruffles guidelines and the perimeter lines on the organza. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image022.jpg" alt=""></strong></p>
<p><strong>I have transferred all lines on the first layer of organza and now I need to do the same with the second layer. </strong>I take the organza pieces off the paper patterns, turn them around, and transfer the lines on the bottom piece. I press the organza to the table with my hand to prevent the layers from shifting against each other and draw the lines between my fingers.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image024.jpg" alt=""></p>
<p><strong>It took me a fair bit of time to transfer all guidelines and perimeter lines on two layers of organza. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2022.files/image028.jpg" alt=""></p>
