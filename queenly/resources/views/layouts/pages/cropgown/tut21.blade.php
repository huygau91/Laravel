<p><strong>The first half of the necklace is ready. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image080.jpg" alt=""></p>
<p><strong>The second half is made the same way. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image082.jpg" alt=""></p>
<p><strong>I position the necklace clasp in the middle of the back and sew the rings to the two halves of it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image084.jpg" alt=""></p>
