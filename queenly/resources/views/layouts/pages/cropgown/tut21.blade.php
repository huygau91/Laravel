<p><strong>This is basically enough to secure the bottom fishing line but I will put it through another two beads because I have left a rather long tail. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image060.jpg" alt=""></p>
<p><strong>I continue stringing beads on the fishing line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image062.jpg" alt=""></p>
<p><strong>All 24 pearl beads are now on the fishing line. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image064.jpg" alt=""></p>
<p><strong>I add a stopper bead and a clasp. I put the end of the fishing line through the stopper bead one more time and pull the fishing line so as to leave no gaps between the beads. The string must be even and elastic. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image068.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image070.jpg" alt=""></p>
<p><strong>I firmly press the stopper bead with the pliers, put the end of the fishing line through several beads, and trim it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image072.jpg" alt=""></p>
