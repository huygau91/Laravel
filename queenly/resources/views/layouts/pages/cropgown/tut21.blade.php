<h1>Tutorial 20. Attaching the Sleeves. Adding a Decorative Necklace.</h1>
<p><strong>I will use this paper mock-up to show you how to assemble the sleeves and pin them to the garment. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image002.jpg" alt=""></p>
<p><strong>The two central elements are supposed to be put together and positioned over the shoulder seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image006.jpg" alt=""></p>
<p><strong>Then I will put together the other elements of the sleeve: first from one side and then from the other side. I will distribute the sleeve cap along the armscye and pin it down. The supposed armscye line is marked by pencil on the paper mock-up. These darts will add fullness to the sleeve cap and give it its proper shape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image012.jpg" alt=""></p>
<p><strong>I have put the top on the dress-form and pinned the sleeves to it. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image020.jpg" alt=""></p>
<p><strong>Then I take the garment off the dress-form, carefully re-position the pins on the sleeves, and sew them to the top by hand. </strong>When I was doing the pyrography work, I purposely left some cut-outs backed by voile fabric near the armscyes to make setting the sleeves in easier.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image026.jpg" alt=""></p>
<p><strong>Both sleeves are sewn in place. </strong>This job did not take long.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image032.jpg" alt=""></p>
<p><strong>And now there comes the last stage of working on the top. I need to create a decorative necklace for it. </strong>I will use 6mm �White Pearl" Swarovski beads. They have a very noble kind of shimmer.</p>
<p><strong>The first thing I will do </strong>is mark the exact positions of the beginning and the end of the necklace on the dress-form and figure out the exact amount of pearls required for one half of the top.</p>
<p><strong>I will need the following tools and materials: </strong>pearl beads, flat nose pliers, thick needle, clasp, stopper beads, pincers, and 0.4mm fishing line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image034.jpg" alt=""></p>
<p><strong>First of all, </strong>I take a disappearing ink pen and mark the beginning and the end of the necklace on the dress-form, near the base of the neck. According to my estimation, I will need 24 beads to decorate one half of the top along the neckline. I take the top off the dress-form.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image036.jpg" alt=""></p>
<p><strong>I put a thick needle in the start point of the necklace, thread it with fishing line, and pull it through. </strong>I will string pearl beads on the top half of the fishing line and use the bottom half to reinforce it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image040.jpg" alt=""></p>
<p><strong>I trim the ends of the fishing line leaving long enough tails, put a stopper bead on the top half of the fishing line, put the bottom end of the fishing line through the same stopper bead, and pull it to the very edge of the neckline. </strong>The opposite end of the fishing line is about 4-5cm long now. The key task is to secure the stopper bead at the very edge because this is where the necklace is supposed to start. I press the stopper bead with flat nose pliers and make sure the fishing line holds very firmly in it.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image042.jpg" alt=""></p>
