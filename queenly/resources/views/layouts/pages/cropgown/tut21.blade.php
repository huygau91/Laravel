<p><strong>I start stringing pearl beads on the top half of the fishing line.</strong></p>
<p><strong>I put three beads on the fishing line and then put the end of the bottom half of the fishing line through the same three beads. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image054.jpg" alt=""></p>
