<p><strong>One half of the necklace is secured.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image088.jpg" alt=""></p>
<p><strong>I do the same with the other half. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2020.files/image090.jpg" alt=""></p>
