<p><strong>And I cut the voile along the bottom edge of the ornament. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image028.jpg" alt=""></strong></p>
