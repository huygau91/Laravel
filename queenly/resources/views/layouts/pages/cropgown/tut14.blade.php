<h1>Tutorial 13. Making the Third Layer of the Front Piece.</h1>
<p><strong>Two layers of the front half of the top are ready. </strong>I have made all necessary cut-outs, made the darts, and trimmed it along the side seam lines and shoulder seam lines. There are no seam allowances.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image002.jpg" alt=""></p>
<p><strong>I used a needle-like tip for doing pyrography on a single layer of fabric. It is easier to use a cone-shaped tip for working with several layers of fabric (see below). You will also need to increase the temperature. </strong>There are lots of interchangeable tips for pyrography tools and with time you will figure out what are best for some or other tasks.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image004.jpg" alt=""></p>
<p><strong>I start working on the third layer made of voile curtain fabric.</strong></p>
<p><strong>I press a piece of voile, put it on the pattern covered by tracing paper, and secure it with pins. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image006.jpg" alt=""></p>
<p><strong>Then I put the two layers of crepe-back satin on top of it, carefully align the cut-outs with the contours seen through on the paper, and pin it to the pattern covered by voile fabric. </strong>I have to say it is a lot easier to align the contours after cutting the darts and trimming it along the side seam lines and shoulder seam lines.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image008.jpg" alt=""></p>
<p><strong>Now I can start doing pyrography. </strong></p>
<p><strong>I start from the cut-outs along the bottom edge of the piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image018.jpg" alt=""></p>
<p><strong>I remove excess fabric. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2013.files/image024.jpg" alt=""></strong></p>
