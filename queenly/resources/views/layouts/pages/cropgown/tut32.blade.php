<h1>Tutorial 31. Putting Horizontal Ruffles on the Skirt. Part 2.</h1>
<p></p>
<p><strong>Let us analyze the intermediate results. </strong>By this moment, I have put 11cm, 10.5cm, and 10cm horizontal ruffles on the two central back pieces and the two side pieces. </p>
<p><strong>I pin all pieces together and put the half-finished skirt on the dress-form. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>To be honest, I am happy with the result! </strong>Let me remind you that you will need to insert a triangular gore in the middle of the back if you want to make the train longer because my train is basically as long as it can get with the current skirt width. The longer the train is, the wider the bottom of the skirt must be! In my case, the skirt looks harmonious and the train settles upon the floor in a beautiful way. </p>
<p><strong>Of course, it is a soft skirt. </strong>Its shape will not stay fixed in wear as it does in skirts with stiff train constructions. The bride will need to adjust it from time to time. And this is why the skirt is purposely made not too full, not too wide, but rather open. </p>
<p><strong>At this stage, I am satisfied with the shape, the volume, and the look of the ruffles! </strong>The skirt is acquiring proper fullness without looking bulky and awkward. The sea foam effect is great! </p>
<p><strong>Let�s get back to work.</strong> Please remember that I have put 11cm, 10.5cm, and 10cm horizontal ruffles on all skirt pieces apart from the front. </p>
<p><strong>I have reached the level of 9.5cm ruffles </strong>and I need to figure out the required amount before cutting them. I return to the skirt pieces with marked guidelines. </p>
<p><strong>I take the central back piece and look at the level where 9.5cm ruffles start. </strong></p>
<p><strong>According to the layout, I need to make six horizontal rows of 9.5cm ruffles. </strong>The guidelines are naturally shorter here than at the bottom of the piece. There will be just about 3cm before the end of the guideline after I put one ruffle along it. The second ruffle will have to overlap the most part of it: both ruffles will be positioned along the same guideline with just a couple centimeters difference. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>There is only one possible conclusion. I need to trim the second ruffle or, simply put, use a half of it! </strong></p>
<p><strong>Here is what I arrived at after careful considerations: </strong>I need to put a ruffle and a half along the four bottom guidelines and a single full ruffle along the two top guidelines. </p>
<p><strong>And then I will work with 9cm, 8.5cm, and 8cm ruffles. </strong></p>
<p><strong>Since the central back piece will grow even narrower,</strong> it will be enough to put just one ruffle along each guideline and a half of an 8cm ruffle along the very last guideline. </p>
<p><strong>Starting from the level of 7.5cm ruffles, </strong>it will be enough to put a half of a ruffle along each guideline. I will need to trim these halves and use even smaller parts closer to the top of the piece. </p>
<p><strong>The required number of ruffles of each size is written next to each guideline on the layout.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now, let us look at the layout on the side piece. </strong></p>
<p><strong>The width of the side piece is nearly the same down its full length. </strong>The guidelines make a turn in the narrowed area but their length hardly changes. </p>
<p><strong>This means I will put two ruffles along each guideline regardless of their width. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I will show you how to avoid confusion and work with the ruffles layout correctly through the example of 9.5cm ruffles on the central back piece. </strong></p>
<p>Looking at the central back piece lying on the table before me, I can see six guidelines for 9.5cm ruffles on it (the area between two crosses). I highlight these guidelines with a disappearing ink pen. </p>
<p><strong>Next, I examine the previous row of ruffles to determine in which direction I was moving when sewing them on. </strong>It is highly recommended to arrange the ruffles of the next row in the opposite direction. Upon reaching the end of the guideline, I turn the piece on the needle and start moving along the next guideline. I draw arrows which indicate the right direction. This really helps. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I need to put one ruffle and a half along the first four guidelines as follows from the calculations. </strong>I need to mark where the first full ruffle overlaps with the second half-ruffle on each guideline. I measure 8.5cm from the end of the guideline (the length of the half-ruffle) and mark the overlap point. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>There will be only one 9.5cm ruffle along guidelines (5) and (6) so I don�t need to mark any overlap points. I only draw arrows to mark the direction. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2031.files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Even if you get distracted during work, it is almost impossible to make a mistake with all these marks on the fabric. </strong>You will always know what kind of a ruffle to put along each guideline. </p>
<p><strong>I work with one type of ruffles at a time. </strong>For example, I start by calculating how many 9.5cm ruffles I need for each skirt piece, and then I cut the required amount of full and half ruffles and put them on two central back pieces and two side pieces. </p>
<p><strong>Then, I move to the next size (in my case, 9cm ruffles) and work in the same order. </strong></p>
<p><strong>After putting 9.5cm ruffles on all pieces, I examine the rest of the layout on the side pieces. </strong></p>
<p>I have noticed that the intervals between the guidelines arriving at the front edge of the piece grows smaller and smaller as I move upwards. This means the ruffles will be placed closer and closer to each other. <strong>There seems to be an obvious conclusion: why not reduce the amount of ruffles then? </strong></p>
<p><strong>But, starting from 7.5cm ruffles, the guidelines pass through all skirt pieces uninterrupted: </strong>the guideline starts at the central back seam, passes over the central piece and onto the side piece, and then over it and onto the front piece. </p>
<p><strong>I would not recommend adjusting these guidelines for this reason. </strong></p>
<p><strong>You can reduce the amount of 9cm, 8.5cm, and 8cm ruffles though. </strong></p>
<p><strong>I have decided not to bring all rows of ruffles to the front edge of the side piece. Some will stop earlier and thereby the amount of ruffles at the edge of the piece will get reduced. </strong>This won�t affect the overall layout because these rows of ruffles do not pass on the front piece. At the same time, I will make the skirt lighter and reduce fabric consumption. </p>
<p><strong>Here is what I will do starting from the bottom row of 9cm ruffles. </strong>I had originally planned to put two ruffles along each guideline. But now I will put two ruffles along the very bottom guideline which starts at the front edge of the side piece and goes to the princess seam edge of the back (red arrow). The next row will start from the princess seam edge and have only one ruffle in it (blue arrow). There will be no ruffles between the end of this ruffle and the front edge of the piece. I will put two ruffles along the next guideline between the front edge and the princess seam edge (red arrow) and then one ruffle starting from the princess seam edge (blue line), and so forth. </p>
<p><strong>I will do the same to reduce the amount of 8.5cm and 8cm ruffles. </strong></p>
<p><strong>The adjusted layout has allowed me to remove a total of 16 ruffles. </strong></p>
