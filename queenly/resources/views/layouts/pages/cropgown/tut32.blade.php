<p><strong>Let me remind you that the ruffles layout enclosed with the book is the final version. The amount and the size of all ruffles, as well as the sewing direction, are all marked on it. </strong>But this layout was made for my skirt and yours may differ in size and in style. I give you such a detailed explanation of how and why I arrived at this particular layout so you can rely on my insights for drawing your own ruffles layout for your own skirt. </p>

