<h1>Tutorial 42. Conclusion. Pictures of the Finished Garment.</h1>
<p><strong>The skirt is ready for a fitting and nearly finished. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image002.jpg" alt=""></p>
<p><strong>I have decided to decorate the waistband with a triangular piece of the same lace I used for decorating the top. </strong>I find that a lace triangle like this complements the shape of the waistband very well, adds to the overall harmony of the outfit, and makes it even more elegant.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image004.jpg" alt=""></p>
<p><strong>You can see the back of the skirt in the picture below. </strong>My dress-form is a little bigger than the bride but the lacing still looks really beautiful.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image006.jpg" alt=""></p>
<p><strong>The silhouette of the skirt also makes me happy and the transition to the train is very graceful. </strong>I didn�t want the skirt to be too flared as you remember.</p>
<p><strong>The amount of ruffles was also determined correctly. </strong>The skirt is covered by them thickly and they are lying just the way I wanted them to. It looks gorgeous.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image008.jpg" alt=""></p>
<p><strong>And here are some pictures from the first and only fitting. </strong>I am very happy because I did not have to make any adjustments at all! The bride was overjoyed!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2042.files/image012.jpg" alt=""></p>
<p><strong>Here is all information regarding fabric consumption:</strong></p>
<p>-&nbsp; 150cm crepe-back satin (top and waistband);</p>
<p>- 150cm organza (skirt base);</p>
<p>- 14m curtain voile fabric with a width of 3m (ruffles);</p>
<p>- 6m soft Rigilene boning;</p>
<p>- 3 zippers.</p>
<p><strong>As you can see, you don�t need to invest that much in a dress like this. It is quite cost-efficient. </strong></p>
<p><strong>In conclusion, I would like to share some impressions and observations made during the wedding ceremony which took place in the Bahamas. </strong></p>
<p><strong>First of all, </strong>I must tell you this dress was a true winner! People greeted the bride with applause and everyone wanted to take a picture with her!</p>
<p><strong>What is more important, </strong>the bride felt really comfortable wearing this dress! She put it on after the official part of the ceremony to which she wore a classic wedding dress and then she stayed in it the rest of the evening and danced in it, too.</p>
<p><strong>Let me give you some good tips in case you decide to sew a dress like this (and I am sure you will):</strong></p>
<p>- <strong>When you do pyrography on the top, the melted edges of the cut-outs turn out rather coarse and may irritate the wearer�s skin. </strong>In order to avoid this, I highly recommend you smear the edges of all cut-outs not backed by voile fabric with a thin layer of fabric glue (it is best to use a syringe). When the glue sets, the edges will be covered by thin silicone film and will no longer cause any irritation.</p>
<p>- <strong>I put 3 vertical strips of soft Rigilene boning on the front of the skirt.</strong> As the result, I had to finish the bottom edge of the skirt several times, with glue and with bias tape, because some fishing lines would always start sticking out from those strips of Rigilene boning. For this reason, I highly recommend you replace soft Rigilene boning with hard mesh fabric or mesh fabric of medium stiffness, such as the kind I normally use for reinforcing petticoats.</p>
<p><strong>This dress style is gaining popularity! </strong>Of course, it can be a different combination of a top and a skirt but the overall silhouette is trending! Tops like this look good on any body type. You can combine various types of lace fabric including hand-made exclusive lace like mine. And my pattern will help you cut a top from hand-made or ready-made lace fabric in no time.</p>
<p>Future belongs to these dresses! And I am absolutely sure that girls who stay in-step with the times and the trends will be happy to order a similar dress from you.</p>
<p><strong>That is the whole story! I sincerely wish you amiable clients and enjoyable work! </strong></p>
