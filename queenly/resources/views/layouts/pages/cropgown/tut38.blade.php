<h1>Tutorial 37. Making a Waistband Pattern.</h1>
<p><strong>I continue working on the skirt and now I need to make a waistband for it. </strong></p>
<p><strong>The key requirement to the waistband</strong> is that it should hug the waist tightly and hold the skirt in the right position. I will use ribbon loops for lacing the waistband, which means we are not speaking of any waist restriction effect here.</p>
<p>The wedding ceremony will take place in hot weather and <strong>the waistband must be hard and similar to a waist cincher</strong> because any soft wide waistband will inevitably wrinkle and the bride won�t feel comfortable in it. She must be able to sit down, stand up, or bend down freely.</p>
<p><strong>My waistband pattern will be based on the pattern </strong>from �Top Wedding Dresses. Dress 1" adjusted to my client�s size. You can use corset patterns from other books by the Corset Academy, such as �Corset for a Young Girl" or �Teen Corset with Busk."</p>
<p><strong>Here is the pattern I am going to use: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image002.jpg" alt=""></strong></p>
<p><strong>All pieces have already been adjusted to my client�s size,</strong> I don�t need to make any additional calculations. If you use a different initial pattern, remember to apply all adjustments according to your client�s individual measurements.</p>
<p><strong>I draw a line about 4cm above the waistline and another about 6cm below it. This part of the pattern will be used for cutting the waistband. </strong>I will add a down-pointing corner in the middle of the waistband. It will reach 9cm below the waistline.</p>
<p><strong>The cutting process is the same as for non-see-through corsets: </strong>I will duplicate the fabric with iron-on batiste, transfer the pattern on it, and cut it. Then I will sew a lining which will also serve as a mock-up, try it on the dress-form, and figure out the precise size.</p>
<p><strong>Let me say something about the lacing. </strong>I don�t want to install grommets in the waistband in this particular case. I prefer to make a more graceful lacing by using ribbon loops.</p>
<p><strong>There is a certain fine point about using ribbon loops though.</strong> No matter how tight you try to lace it, there will always be a small gap between the edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image004.jpg" alt=""></p>
<p><strong>What will happen to the waistband then? </strong>The skirt is fastened by a zipper and, of course, there is no gap between the edges and there should be none at the bottom of the waistband either.</p>
<p><strong>Considering all this, here is how the waistband will be made: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image006.jpg" alt=""></p>
<p><strong>The lacing area will be shaped like a down-pointing triangle. </strong>There will be tunnels along the vertical edges and I will insert plastic bones in them. Then I will encase Rigilene bones in fabric and sew them on parallel to the tunnels. The ribbon loops will emerge from under these Rigilene bones. This way they will be positioned correctly to let the edges of the waistband meet when it is laced. The bones will prevent the back of the waistband from bending.</p>
<p><strong>I start working on the waistband pattern. </strong></p>
<p><strong>I arrange the corset pattern on the table. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image008.jpg" alt=""></strong></p>
<p><strong>I draw a line 5.2cm above the waistline (4cm plus 1.2cm seam allowance) and another line 7.2cm below the waistline (6cm plus 1.2cm seam allowance) on all pieces. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image010.jpg" alt=""></strong></p>
<p><strong>The central front piece has a down-pointing corner in the middle which reaches 9cm below the waistline. The bevel edge starts 8cm from the middle of the waistband and partially passes on the side front piece. </strong>I add 1.2cm seam allowances everywhere.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image012.jpg" alt=""></p>
<p><strong>I cut along the new lines. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image014.jpg" alt=""></strong></p>
<p><strong>The waistband pattern is ready. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2037.files/image016.jpg" alt=""></p>
<p><strong>You can shape your waistband any way you like: </strong>make a symmetric corner at the top of the front, raise or lower the back, etc.</p>
<p><strong>It is time to cut the pieces from fabric. </strong></p>
<p><strong>I have prepared a big enough piece of the same crepe-back satin which was used for the top. </strong>I have fused iron-on batiste over the sheen side to use the matte side as the face. I fold the duplicated fabric in half two times to cut the face and the lining simultaneously. I will cut on the cross, as always, which means the princess seams will be parallel to the crosswise grain while the waistline will be parallel to the selvage.</p>
<p><strong>I arrange the pieces on the fabric, put the central front piece to the fold line, and outline it with a pencil. </strong>You can pin the paper patterns to the fabric. I transfer all notches and mark 2cm seam allowances along the central back edges and the side seam edges lest I forget about them.</p>
