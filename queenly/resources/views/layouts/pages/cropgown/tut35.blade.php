<h1>Tutorial 34. Analyzing Garment Balance.</h1>
<p><strong>I suggest we should talk about the lining before analyzing the balance of the skirt. </strong></p>
<p><strong>The lining will be cut after the skirt pattern adjusted to my client�s size. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2034.files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It will be made of two pieces: front and back. </strong></p>
<p>Since I want to make these pieces seamless, what I need to do is align the princess seam lines of the separate back pieces, remove the seam allowance along the central edge, and <strong>cut the whole back half of the lining as a single piece with a fold line. </strong></p>
<p><strong>The front half of the lining will be cut after the front piece of the skirt. </strong></p>
<p><strong>I will sew the lining with the same voile curtain fabric as the ruffles. </strong></p>
<p><strong>Lining made of voile fabric </strong>will not only look very nice but make the skirt really lightweight, which is exactly what we want! </p>
<p><strong>Now that things are clear with the lining, let us talk about the balance of the skirt.</strong> </p>
<p><strong>What is garment balance anyway? </strong>Good balance is when the assembled garment is positioned correctly on the wearer�s body. </p>
<p><strong>Our skirt is short at the front and really long at the back because of the train. </strong>If you fail to secure a skirt like this properly and ensure good balance, it will most likely get distorted just because the back is a lot heavier than the front. </p>
<p><strong>In other words, </strong>it is very hard to keep a skirt positioned correctly when it is very open (i.e. very short) at the front and quite heavy at the back.</p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2034.files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It is different with our skirt though. </strong>Although it may look complicated at first glance, you will hardly face any fit-related problems. The skirt will be very comfortable in wear. </p>
<p><strong>The side pieces passing onto the front are the first thing that improves the balance of this skirt. </strong>They don�t let the heavy back with the train outweigh the front.</p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2034.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Moreover, the skirt is bell-shaped, i.e. relatively narrow in the hips. </strong>Of course, there is a small degree of flare but not nearly as noticeable as on half-circle skirts, let alone full sweep skirts. This means the skirt has a rather tight fit from the top down to the hip line, which helps it hold well on the body. </p>
<p><strong>As the result, the skirt is very well-balanced and requires no additional support! </strong></p>
<p><strong>But what to do if you decide to change the style or the silhouette of the skirt: for example, make it wider and form drapes?</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2034.files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>In this case, you will need to find a way to reinforce the top part of the skirt down to the hip line. </strong>As I said before, if you sew a skirt in the technique described in this book and want to increase its volume by widening the pattern, then the front and the back are supposed to be finished with a lining sewn in the princess seams. You cannot put an additional supporting construction with hoops or Rigilene boning between the layers - it will be very obvious and look messy! </p>
<p><strong>One of the possible ways to reinforce the skirt and improve its balance </strong>is to put hard Rigilene bones under the ruffles starting from the hip level. </p>
<p><strong>Another solution</strong> is to add another lining layer made of denser and yet still lightweight non-see-through synthetic fabric. Similar fabric, for example, is used in ready-made low-budget petticoats. You will need to cut the central front piece and a short combined piece for the back and the sides of the skirt. You will also need to put one or more Rigilene bones along the bottom of these lining pieces to make them stiffer. This third lining layer will be placed between the main fabric and the see-through lining and sewn in the princess seams together with it. As the result, the top of the skirt will be strong enough and non-see-through. </p>
<p><strong>In conclusion, I would like to warn you about one more thing: </strong>when you are making an inner supporting construction for a skirt, remember that it must not obstruct the wearer�s movements. The bride is supposed to be able to sit down and stand up freely. The construction must be carefully thought-out and well-balanced, and the bride must feel comfortable in the garment! </p>
<p><strong>As for my skirt, it is very well-balanced and requires no additional supporting constructions!</strong></p>

