<h1>Tutorial 27. Analyzing the Ruffles Layout. Examining the Results.</h1>
<p></p>
<p><strong>I have put all vertical ruffles on both side pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2027.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I consider this task to be solved and I am quite happy with the way these pieces look now. </strong>The most important thing is that there are not supposed to be any gaps between the ruffles. The background fabric should not show through. The least amount of ruffles is situated at the top of the pieces. I will polish it up later when the skirt is nearly finished. </p>
<p><strong>The amount and the size of ruffles along each vertical guideline are marked right on the layout. </strong>You could make the difference in widths 5mm and not 1cm for an even smoother transition from one size to another. It won�t make a big difference though. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2027.files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now that I have put all vertical ruffles on the side pieces, let me focus on the central back pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2027.files/image006.jpg" class="img-responsive center-block"></p>
<p>When I examined the side pieces, I noticed that the vertical ruffles covered them rather thickly. This made me want to <strong>slightly adjust the ruffles layout on the central back pieces.</strong> Let me run ahead and warn you that <strong>all these adjustments are accounted for in the pattern </strong>enclosed with the book. </p>
<p>The outermost row of ruffles on the side piece is sewn right to the seam allowance, which means it will get in the princess seam when I join the side piece with the central back piece. I was planning to put a symmetric ruffle along the princess seam edge of the central back piece but I prefer to give up that idea considering the situation. I don�t want to make the skirt too heavy. In this case, the distance between the parallel rows of ruffles will stay the same after I join the pieces (about 1.5cm), only this time there will be one row of ruffles in the princess seam (the one sewn on the side piece). </p>
<p><strong>I adjust the layout accordingly (blue lines). The inner row of ruffles will go down the full length of the central back piece and the two short rows are supposed to fit between it and the seam allowance line. </strong></p>
<p><strong>What concerns the central back seam, I was planning to put a row of ruffles along either edge of it. </strong></p>
<p>But now I can see that the skirt will be full enough so <strong>I prefer to put an outer row of ruffles along the central seam allowance line on one central back piece and change the layout on the other piece (red lines). </strong>I will keep the long inner row of ruffles on the other central back piece, as well as the three short rows positioned between it and the seam allowance line. </p>
<p><strong>I still think that the zipper in the central back seam should be covered with ruffles from either side. </strong>For this reason, I will put ruffles along the zipper area of the central seam allowance line on the other central back piece. </p>
<p><strong>The black dashed line in the pictures below shows the part of the row of ruffles along the central back seam edge which is not the same on both central pieces. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2027.files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The bottom parts of the central back pieces are also different in the area along the central seam allowance line (black dashed line).</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2027.files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I want to give you a piece of advice in case you want to make the train longer and the skirt fuller at the back. </strong></p>
<p><strong>Please note</strong> that you cannot just prolong the train. You must widen the skirt starting a little above floor level. The volume of the skirt itself is not supposed to increase but the train should settle beautifully upon the floor. </p>
<p><strong>Here is what I recommend:</strong> sew a triangular gore in the central seam at the back of the skirt 20cm from the floor. The gore can have any configuration you want. It can flare out up to 50-60cm. You can put horizontal and vertical ruffles along the perimeter if you want. It will also look very pretty when framed by vertical ruffles emerging from the central back seam with horizontal ruffles in-between. </p>
<p><strong>In other words, even if you and your client decide to make the skirt fuller or the train longer at the very last fitting, you will always be able to cut a gore and sew it in the central back seam. </strong></p>
<p><strong>This is what makes my sewing technique so handy</strong>. When a skirt is made of separate parts, you can apply adjustments at any work stage. </p>
<p><strong>In conclusion, I would like to say some words about the ruffles. </strong>You already have some experience in working with ruffles. I really hope it is clear why I made them have different widths but the same diameter of the inner opening. This allows you to align one ruffle with another perfectly regardless of the width. It is really easy and fast even when the ruffles overlap. </p>
<p><strong>I am about to start putting vertical ruffles on the central back pieces in correspondence with the adjusted layout. </strong></p>
<p><strong>I will start from the 7cm ruffles and gradually move down. </strong>The amount and configuration of ruffles along the princess seam edge of the central back piece must be just the same as the amount and configuration of ruffles along the princess seam edge of the side piece. About the same amount of ruffles will be put along the central edge of the back piece.</p>
<p>
