<h1>Tutorial 29. Cutting Ruffles with Wavy Edges.</h1>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </p>
<p><strong>I take the cardboard template and the pyrography tool and cut lots of ruffles: about 50 in each size. </strong>I use a tip that is sharpened from two sides. It is better-suited for making wavy edges than the needle-like tip I used for cutting smooth-edge ruffles. Thanks to this tip, I can cut very many ruffles using the same template. And the ruffles themselves don’t stick to the template either. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2029.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I sort the ruffles by size, put them in containers, and mark their size and amount on a piece of paper. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2029.files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let me demonstrate you the step-by-steps of cutting a 10.5cm ruffle with wavy edges.</strong></p>
