<p><strong>I will start from the 11cm ruffles. </strong>They are marked by brown guidelines on the layout: seven lines on the central piece and seven lines on the side piece. I will highlight these guidelines with a disappearing ink pen on the fabric. This will help me avoid confusion in the sewing process. </p>
<p><strong>After putting all 11cm ruffles on the pieces, I will highlight the next seven guidelines for the 10.5cm ruffles and continue.</strong></p>
