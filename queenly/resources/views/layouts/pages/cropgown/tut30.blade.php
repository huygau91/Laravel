<p><strong>I cut the required amount of ruffles using one template for each size. </strong>By the way, you can use the cardboard left after cutting the inner circles to make fabric flowers. I will demonstrate it a little later.</p>
<p><strong>I have cut 11cm, 10.5cm, and 10cm ruffles for a start − 50 of each type. </strong>I will sew them on the pieces, see how it works, define fabric consumption, and then cut ruffles of the remaining sizes and continue. </p>
<p><strong>I will work on each skirt piece separately. </strong>It is much easier, especially now that the delicate main fabric is reinforced with strips of Rigilene boning.</p>
<p>
