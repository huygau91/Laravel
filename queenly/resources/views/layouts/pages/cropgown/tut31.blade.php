<p><strong>I have measured the length of the second 11cm ruffle in the first two rows so I don’t have to put it to the end of the guideline and mark its length again. It is 16cm. </strong>The guideline is 18cm on the pattern but I put the ruffle closer to the edge and it comes out shorter. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I mark 16cm from the end of the third guideline (3). This is the start point of the second ruffle. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I mark 16cm (the length of the ruffle) along guidelines (4), (5), (6), and (7). It is important to remember that I need to turn the piece on the needle whenever I move to the next row. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image048.jpg" class="img-responsive center-block"></p>
