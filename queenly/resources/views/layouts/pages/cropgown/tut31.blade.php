<p><strong>I return to guideline (3). </strong>I put an 11cm ruffle at the beginning of the row, sew it on down to the start point of the second ruffle, put down the second 11cm ruffle, and continue sewing until the end of the row: first two ruffles simultaneously and then just the remaining part of the second ruffle. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image056.jpg" class="img-responsive center-block"></p>
