<h1>Tutorial 30. Putting Horizontal Ruffles on the Skirt. Part 1.</h1>
<p></p>
<p><strong>I start sewing 11cm ruffles on the central back piece following the highlighted horizontal guidelines. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I put the first ruffle to the very edge of the guideline and stitch over a small section. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I take the second 11cm ruffle, pin its bottom end to the opposite end of the guideline, spread it along the guideline, and mark a sewing start point. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image008.jpg" class="img-responsive center-block"></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I finish sewing on the first ruffle stopping at the sewing start point of the second ruffle. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the second ruffle and continue sewing them on simultaneously until the guideline ends. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I turn the piece on the needle to start the next row of ruffles without interrupting the thread. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The first thing I do is put the end of an 11cm ruffle to the end of the guideline and mark its length. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put the first 11cm ruffle along the guideline and sew it on down to the marked sewing start point of the second ruffle. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I put down the next 11cm ruffle as marked and finish the second horizontal row of ruffles. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I start the third horizontal row of 11cm ruffles. </strong></p>
<p><strong>Again, I turn the piece without taking out the needle or interrupting the thread. </strong></p>
<p><strong><br>
