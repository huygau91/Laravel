<p><strong>I have made seven horizontal rows of 11cm ruffles. </strong>They hang down creating the effect of sea foam. This effect is achieved thanks to the wavy edges of the ruffles. If the ruffles had smooth edges, they would be plain straight rows of ruffles, which was not what I wanted. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image100.jpg" class="img-responsive center-block"></p>
<p><strong>I remove all thread tails left on the inside of the piece. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image102.jpg" class="img-responsive center-block"></p>
<p><strong>There are two possible ways to continue: </strong>you can either put 11cm ruffles on all skirt pieces or you can add ruffles of the remaining sizes on the same piece. </p>
<p><strong>I will continue working on the central back piece and put 10.5cm and 10cm ruffles on it before moving to the side pieces. </strong></p>
<p><strong>I highlight the seven guidelines for 10.5cm ruffles with a blue marker. </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image104.jpg" class="img-responsive center-block"></p>
<p><strong>Ruffles with a width of 10.5cm are to be put along the guidelines just the same way as 11cm ruffles. </strong>Each row has two of them in it.</p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image106.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image108.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The higher I move up the piece, the greater is the volume of sewn-on ruffles. </strong>It gets harder and harder to control the guideline and spread the fabric as you sew. In addition, you must keep the ruffles aligned with the guideline. </p>
<p><strong>I want to share with you a simple trick which makes the process of aligning ruffles a lot easier. </strong></p>
<p><strong>Suppose you have attached part of the first ruffle, reached the overlap point, and put down the second ruffle. </strong>The same radius of the inner circle helps you align these ruffles perfectly. I align the edges of the ruffles and pin them together at the end of the first (bottom) row. As the result, it is a lot easier to hold the ruffles in place as you sew. You can see the guideline better and you don’t have to keep trying to catch the end of the second ruffle. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image110.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image112.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image114.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image116.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image118.jpg" class="img-responsive center-block"></p>
<p><strong>I have put all 10.5cm ruffles on the central back piece and now I move to 10cm ruffles. </strong>I will highlight all six guidelines with a marker, mark overlap points, and put two ruffles in each row. </p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image120.jpg" class="img-responsive center-block"></p>
<p><strong>I switch to the side pieces after putting all ruffles on the central pieces. </strong></p>
<p><strong>I highlight the seven guidelines for 11cm ruffles with a marker. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image122.jpg" class="img-responsive center-block"></strong></p>
<p><strong>After measuring the guidelines, I noticed that the row lacks 2-3cm even with the smallest possible overlaps. </strong>It would be a real shame having to use a third ruffle or part of it to add these lacking centimeters. It would increase fabric consumption and make the skirt heavier. Please note that I am talking about the lengths of my guidelines: yours may be different. The length of the guideline is 18.5cm on the pattern. The actual length of the row of ruffles is 16cm, which is explained by the fact that I leave 2mm from the edges of the piece and not 5mm as I had previously planned. </p>
<p><strong>There is a simple way to solve the problem: </strong>I stretch every ruffle before sewing it on so as to straighten its inner sewing edge. In this case, two ruffles make a long enough row. Please note that you must sew them on down to the very end of the guideline even if their edges will be covered up by vertical ruffles. I mark overlap points after stretching the ruffles. </p>
