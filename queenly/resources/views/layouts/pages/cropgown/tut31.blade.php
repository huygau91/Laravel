<p><strong>And then I turn the piece again and start putting 11cm ruffles along guideline (5). </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image074.jpg" class="img-responsive center-block"></p>
