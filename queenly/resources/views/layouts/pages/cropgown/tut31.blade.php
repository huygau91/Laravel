						<p>In this course we will learn how to sew&nbsp;a trendy wedding gown design - Crop Top or Two Pieces gown, and the new advanced (yet super-easy) decoration technique.</p>
<p>And we will learn how to make a perfect fitting dress to a client from a long distance, relying solely on the measurements.</p>
<p><strong>Features:</strong></p>
<ul>
<li>Creating a custom lace for your specific pattern.</li>
<li>How to draw openwork lace and how to create your own tracery.</li>
<li>How to adjust&nbsp;a zipper for any size, nice and clean.</li>
<li>Easy way to create light and beautiful ruffle skirt, which doesn’t require any additional reinforcing.</li>
<li>How to&nbsp;sew on the ruffles and to assemble the skirt using home sewing machine.</li>
<li>The skirt contains 750 ruffles of 8 sizes and 2 edge types. All sewing patterns are included.</li>
<li>Making and attaching a bodice belt for a skirt.</li>
<li>Strong lacing on hanging loops.</li>
<li>Advanced decoration techniques.</li>
<li>Peculiarities of long distance sewing.</li>
<li>How to make a “virtual" fit test.</li>
<li>How to use a mannequin for fit test.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>This technology is absolutely universal and has no limits in choosing and creating any style of wedding and evening dresses!</li>
</ul>
