<p><strong>I turn the piece on the needle and move to row (4). </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image064.jpg" class="img-responsive center-block"></strong></p>
