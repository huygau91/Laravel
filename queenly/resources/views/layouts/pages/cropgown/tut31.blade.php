<p><strong>And I do the same with guideline (6). </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image084.jpg" class="img-responsive center-block"></p>
