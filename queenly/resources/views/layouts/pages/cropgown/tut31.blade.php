<p><strong>Finally, I put 11cm ruffles along the last guideline − (7). </strong></p>
<p>
<img src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2030.files/image092.jpg" class="img-responsive center-block"></p>
