<h1>Tutorial 35. Joining the Back of the Skirt with the Side Pieces.</h1>
<h2>Adding the Lining.</h2>
<p><strong>I pin the side pieces to the back piece along the princess seam edges. </strong>I have already installed a zipper in the central back seam. The princess seam edge is cut on the bias and may have been stretched out when I was sewing ruffles on the piece. Keeping this in mind, I stick to the shape of the princess seam edge of the side piece which was cut on the lengthwise grain when pinning the pieces together. I push the ruffles inwards with my fingers to prevent them from getting caught by the pins.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image004.jpg" alt=""></p>
<p><strong>I sew the first princess seam. </strong>The seam is very soft because I am only joining two layers of organza.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image014.jpg" alt=""></p>
<p><strong>I check the quality of the seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image016.jpg" alt=""></p>
<p><strong>I fold the seam allowance of the princess seam toward the central back piece and topstitch it down the full length. </strong>It is not hard because the outer vertical ruffles are positioned 1cm from the princess seam allowances.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image024.jpg" alt=""></p>
<p><strong>The result is a strong flat seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image026.jpg" alt=""></p>
<p><strong>The other princess seam is sewn just the same way, with the seam allowances folded toward the central back piece. </strong>This is how I assemble the back of the skirt.</p>
<p><strong>Working with the lining starts from the front piece. </strong></p>
<p><strong>I cut a lining for the front piece using the same pattern. </strong>I put the lining piece face to face with the front piece, pin them together along the bottom edge, sew a row of strengthening stitches, turn it out, and topstitch it folding the seam allowances to the lining side. Then I press the seam allowance. Next, I make a row of machine stitches along all sides of the piece and serge the edges.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image028.jpg" alt=""></p>
<p><strong>At this stage, you can put two rows of ruffles along the bottom edge of the front piece as marked on the layout. </strong>I will put two 8cm round ruffles along each guideline.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image030.jpg" alt=""></p>
<p><strong>Here is the front piece joined with the lining: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image032.jpg" alt=""></p>
<p><strong>I move to the large back half of the skirt. </strong></p>
<p><strong>The lining of the back is cut as a single piece with a fold line. </strong>I carefully press the fold line, put the piece on the glass panel, and cut along the fold line with the pyrography tool. The length of the cut equals the length of the zipper sewn in the face piece. I make two small inclined notches at the bottom of the cut so I can fold the seam allowances. I will then press the seam allowances to finish the zipper area neatly.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image034.jpg" alt=""></p>
<p><strong>I put the back of the skirt face side up, carefully spread it, put the lining face side down on it, and pin them together along the full perimeter. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image036.jpg" alt=""></p>
<p><strong>I mark the end of the front piece along the front edge of the back of the skirt.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image038.jpg" alt=""></p>
<p><strong>Starting from this point, I will notch the seam allowances and sew along until I reach a symmetric point on the opposite side of the back piece. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image040.jpg" alt=""></p>
<p><strong>After finishing this row of stitches, I will turn out the piece, fold the seam allowances to the lining, and topstitch about 2mm from the joining seam. </strong>Then I will carefully spread out all fabric and press the seam allowances flat trying not to touch the ruffles.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image042.jpg" alt=""></p>
<p><strong>As the result, the whole back of the skirt including the train will be finished by the lining save for the areas left for attaching the front piece. </strong></p>
<p><strong>I will sew together the seam allowances of the lining and the face in those areas and serge the edges. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image044.jpg" alt=""></strong></p>
<p><strong>Then I will finish the zipper area by hand-stitching the lining to the zipper seam allowances.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image046.jpg" alt=""></p>
<p><strong>Next, I will join the back with the front already finished with the lining. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image048.jpg" alt=""></strong></p>
<p><strong>And, finally, I will put a row of ruffles along the full bottom edge. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image050.jpg" alt=""></p>
<p><strong>Let us look at the preliminary results. </strong></p>
<p><strong>Both the large back piece and the small front piece are covered by a layer of voile fabric. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image052.jpg" alt=""></strong></p>
<p><strong>I have serged the edges of the back piece to which the front piece will be sewn. </strong>I joined the face with the lining with a row of strengthening stitches before serging the edges.</p>
<p><strong>The zipper is finished neatly with the lining fabric. </strong>I made a frame in the zipper area on the lining, pressed the seam allowances, and sewn it by hand to the seam allowances of the zipper.</p>
<p><strong>I also made a row of strengthening stitches along the top edge of the back and then serged it. </strong>It will be easier to attach the waistband now.</p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image054.jpg" alt=""></strong></p>
<p><strong>The soft Rigilene boning has made the edge of the skirt strong and elastic: it will no longer curl in under the weight of the ruffles. </strong>I made a row of stitches at a presser foot width from the edge to make it even more robust.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2035.files/image056.jpg" alt=""></p>
<p><strong>Now I need to put the bottom row of ruffles on either half of the skirt in accordance with the layout. </strong></p>

