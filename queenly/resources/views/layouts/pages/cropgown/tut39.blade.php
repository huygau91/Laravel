<p><strong>I put the second pair of hanging loops near the other side seam. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image136.jpg" alt=""></p>
