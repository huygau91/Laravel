<p><strong>And I sew them on following the top row of stay-stitches on the lining. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image132.jpg" alt=""></p>
