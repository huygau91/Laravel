<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image028.jpg" alt=""></p>
<p><strong>I press open the seam allowances and trim their ends at an angle. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image030.jpg" alt=""></p>
<p><strong>When I compared the length of the lining with my client�s waist circumference, it turned out to be 1cm bigger than needed. </strong></p>
<p><strong>The first thing I do to fix this </strong>is trim the seam allowances along the central edges of the central back pieces down to a width of 1.2cm. In other words, I narrow them by 8mm.</p>
<p><strong>The second thing I do </strong>is remove 1.5cm along the waistline to pull in the slack and draw a new ascending line for the lacing edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image032.jpg" alt=""></p>
<p><strong>I join the face pieces with regard to all changes, trim the seam allowances down to 5mm, trim their ends at an angle, and press them open. </strong></p>
