<p><strong>I mark the positions of all four hanging loops. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2038.files/image128.jpg" alt=""></p>
