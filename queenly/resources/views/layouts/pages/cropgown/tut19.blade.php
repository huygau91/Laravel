<h1>Tutorial 18. Sewing the Darts. Shortening Zippers.</h1>
<p><strong>Let us see what I have made by this time. </strong></p>
<p><strong>- The back of the top:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image002.jpg" alt=""></p>
<p><strong>- The front:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image004.jpg" alt=""></strong></p>
<p><strong>- The sleeves:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image006.jpg" alt=""></p>
<p><strong>Before assembling the top, I need to sew the darts on the front piece. </strong>I fold the lace elements along the edges of the dart to keep them out of the way, align those edges, and carefully sew them together.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image010.jpg" alt=""></p>
<p><strong>I sew the dart </strong>on the sewing machine.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image012.jpg" alt=""></p>
<p><strong>I fuse in place the previously folded lace elements.</strong> I do it on a cup pressing mould. I spread out the elements and fuse them on without steam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image020.jpg" alt=""></p>
<p><strong>The first dart is finished.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image022.jpg" alt=""></p>
<p><strong>The same work is done with the other dart: I sew it by machine and restore the lace. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image026.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image028.jpg" alt=""></strong></p>
<p><strong>Both darts are ready: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image030.jpg" alt=""></strong></p>
<p><strong>The first thing to do after sewing the darts is to compare the back side seam lines with the front side seam lines.</strong></p>
<p><strong>They fully coincide from one side of the top: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image032.jpg" alt=""></strong></p>
<p><strong>But the back side seam line is about 2mm longer from the other side of the top. </strong>I trim it with the pyrography tool.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image036.jpg" alt=""></p>
<p><strong>Now I would like to pay attention to a problem that needs to be solved. </strong>Although I did not account for waistline darts, I did shift the side seam lines inwards to make the top narrower in the waist. As the result, the bottom circumference of the top is rather small, which means it will be hard for the bride to get it over her head in a pre-wedding rush (especially with a bridal hair-do).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image038.jpg" alt=""></p>
<p><strong>Needless to say, zippers provide a great solution in this case! </strong>But you don�t always have a short white blind zipper in stock. Of course, you could shorten a regular zipper the traditional way - no questions! But our garment is so delicate that the traditional way will simply not do.</p>
<p><strong>A zipper sewn in the side seam is supposed to be unnoticeable. </strong>The best way is to shorten it starting from the bottom. In this case, the zipper stop will be at the top of the side seam and you will choose an appropriate length: the other end of the closed zipper is not supposed to go beyond the edge of the garment.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image040.jpg" alt=""></p>
<p><strong>I will use the same old good pyrography tool for shortening the zipper. </strong></p>
<p><strong>I start by marking the required length. I put the zipper to the folded side seam edges of the top, trim the tail above the zipper stop, and find the supposed start point. </strong>I make marks with a disappearing ink pen from either end of the zipper tape.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image042.jpg" alt=""></p>
<p><strong>I put the garment aside to work on the zipper itself. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image044.jpg" alt=""></p>
<p><strong>I turn around and open the zipper. </strong>I have copied the marks at the beginning and at the end.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image046.jpg" alt=""></p>
<p><strong>I will shorten the zipper with the help of a cylindrical bevel tip. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image048.jpg" alt=""></strong></p>
<p><strong>I test the temperature on a piece of crepe-back satin and wipe the tip on it thoroughly. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image050.jpg" alt=""></strong></p>
<p><strong>I melt the plastic teeth on either side of the zipper tape in correspondence with the marked lines. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image056.jpg" alt=""></p>
<p><strong>The melted plastic teeth will act as bottom stops preventing the slider from going further down. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20dresses/wedding%2010/transcripts/Tutorial%2018.files/image058.jpg" alt=""></p>
<p><strong>This is how I shorten the zipper down to the required length. I will first sew it in and then trim the ends of the tape in accordance with the shape of the armscyes. </strong></p>
<p><strong>The second zipper is shortened just the same way. </strong></p>

