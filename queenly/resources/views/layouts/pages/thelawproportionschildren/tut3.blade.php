<h1>Some Examples of How the Law of Proportions Works.</h1>
<p>I have prepared several examples to illustrate the things explained in the previous tutorial.</p>
<p>These are regular pictures found on the Internet. I simply searched among free images without looking for anything specific. These dresses caught my attention and I felt like commenting on them.</p>
<p><strong>What we have here are four different dresses. </strong>They looked rather pretty at first sight and yet something did not seem right, something seemed to be disturbed in the overall look of these little girls.</p>
<p>We already know that the laws of beauty, harmony, and proportions agree with certain formulas and mathematical calculations. I don't want us to deal with some complicated maths though.</p>
<p>What I want to do is analyze each dress step-by-step from a designer's perspective.</p>
<p><strong>Dress One. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image002.jpg" alt=""></p>
<p>First of all, I determine <strong>the harmonious position of the waistline</strong> on the body of the girl in the picture. <strong>&nbsp;</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image004.jpg" alt=""></p>
<p>According to the golden ratio rule, if you divide the full height of a standing person in eight equal parts, then the torso (above the waist) will make up 3/8 and the part below the waist − 5/8. <strong>This is the golden ratio which we perceive as harmonious. It looks natural to the human eye. </strong></p>
<p>Now, as you can tell from the picture, the waistband is placed lower than the supposed waistline. And so your intuition tells you to fix this. <strong>I put the waistband a little higher. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image006.jpg" alt=""></p>
<p>Can you see it? The dress already looks better on the girl.</p>
<p><strong>There is one more flaw though: </strong>the skirt is too narrow, it lacks volume. Let us figure out the girl’s head-to-height ratio.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image008.jpg" alt=""></p>
<p>It is obvious that the girl is roughly four head tall. In other words, her head-to-height ratio is 1:4. <strong>This is the basic body proportion of this particular child. </strong></p>
<p><strong>Now let's determine the width of her shoulders and use it to calculate the desired width of the skirt. </strong>I have prepared template guides: 1/4 and 3/4. If the width of her shoulders equals 1/4, then the width of the skirt should be 3/4. The dress will look more proportionate this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image010.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image012.jpg" alt=""></p>
<p>I make the skirt wider. <strong>Now isn't that a totally different story! </strong>Now it does look aesthetically pleasing! The dress looks a lot more harmonious on the girl.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image014.jpg" alt=""></p>
<p><strong>What did I do?</strong> I placed the waistband higher, at the supposed waistline level, and widened the skirt in correspondence with the child's natural body proportion where 1/4 is the width of the shoulders and 3/4 is the width of the skirt.</p>
<p><strong>You could take it even further than that.</strong> The bodice takes up 3/8 of the dress according to the golden ratio rule. The dress will look totally different if I shorten it so as to make the skirt 5/8.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image016.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image018.jpg" alt=""></p>
<p><strong>Very pretty, don't you think?</strong> In my opinion, it suits the girl much better. Just compare it with the original version!</p>
<p><strong>Dress Two. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image020.jpg" alt=""></p>
<p><strong>I determine the right position of the waistline following the same algorithm as before: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image022.jpg" alt=""></p>
<p><strong>This dress has a very low waist as you can tell.</strong> The girl's torso looks way too long compared to her legs.</p>
<p><strong>Let us determine her natural head-to-height ratio</strong>. I will measure the length of the head together with the hair-do because a tall hair-do like this visually makes the head larger and affects the overall body proportions.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image024.jpg" alt=""></p>
<p>The ratio is the same as in the previous case − 1:4.</p>
<p>Suppose our girl wants her dress to have a full skirt. <strong>Let me figure out how wide it should be</strong> to correspond with the girl's head-to-height ratio (hair-do included). I have prepared template guides: 1/4 and 3/4. If the width of her shoulders is 1/4, then the width of the skirt should be 3/4. The dress will look more proportionate this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image026.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image028.jpg" alt=""></p>
<p>It is seen clearly in the picture that the width of the skirt corresponds with the girl's key ratio. And this means we can keep this amount of fullness for the skirt.</p>
<p><strong>Now let's decide on the length of the bodice.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image030.jpg" alt=""></p>
<p>According to the head-to-height ratio, the bodice is supposed to be 1/4 and the skirt - 3/4.</p>
<p><strong>I change the lengths of the skirt and the bodice and get a completely new look!</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image032.jpg" alt=""></p>
<p>Now the dress looks a lot more beautiful on this particular child.</p>
<p><strong>Dress Three.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image034.jpg" alt=""></strong></p>
<p>It is a classic dress for a girl, a very popular style. And yet something looks wrong again! First of all, I want to check whether the key ratio is not disturbed here. <strong>I determine the golden ratio position of the waistline: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image036.jpg" alt=""></p>
<p>Now you can see that the decorative flower and waistband are basically placed at the waist, where they logically should be.</p>
<p><strong>Let us look at the girl’s head-to-height ratio:</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image038.jpg" alt=""></p>
<p>She is five heads tall, which means the bodice should make up 1/5 and the skirt 4/5 of the dress.</p>
<p>I use a template of 1/5 and 4/5 parts. If the width of her shoulders equals 1/5, then the width of the skirt should be 4/5. The dress will look more proportionate this way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image040.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image042.jpg" alt=""></p>
<p>You can see in the picture that the skirt is a little narrow. It will look better if I make it just a little fuller. To be honest, those thin shoulder straps bother me as well. I just don't like them! Let us remove them completely. <strong>The dress looks a lot better this way to me. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image044.jpg" alt=""></p>
<p><strong>I want to look at a shorter version of the same dress, too. </strong>According to the golden ratio rule, the bodice should then make 3/8 and the skirt - 5/8 of the dress.</p>
<p><strong>Look what a lovely little dress it is! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image046.jpg" alt=""><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image048.jpg" alt=""></p>
<p>The choice depends on the client and whether she prefers a shorter or a longer dress. Regardless of the girl's decision, I can offer her two harmonious versions to choose from.</p>
<p><strong>Dress Four. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image050.jpg" alt=""></p>
<p>Four or five year old girls often want to look like fairies and it is very hard to convince them that a long bodice and a low waist are not the best choice. What to do if the girl insists on fulfilling her wish?</p>
<p><strong>First of all, let us determine the harmonious position of the waistline for a girl of this age group. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image052.jpg" alt=""></p>
<p><strong>The waistline should basically coincide with the under-bust line.</strong> In other words, the best dress for a girl of this age is a trapeze dress flared from the under-bust line. But our little client insists on her own opinion: she wants to be a fairy. My task is to make her look natural and not awkward in this garment.</p>
<p><strong>Let us see how long the skirt should be to correspond with the length of the bodice.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image054.jpg" alt=""></p>
<p>According to the Golden ratio rule, the skirt should be longer than in the original version.</p>
<p>You can play around with the length of the bodice if you want to make the skirt a little shorter to allow for better freedom of movement. In other words, you can move around the golden ratio grid lines. The bodice is supposed to end at the central line.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image056.jpg" alt=""></p>
<p>Suppose the little lady insists on keeping the bodice at exactly the length she wants. And this means I need to adjust the length of the skirt correspondingly to achieve harmonious proportions.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/Some%20Examples%20of%20How%20the%20Law%20of%20Proportions%20Works_files/image058.jpg" alt=""></p>
<p><strong>Can you see the difference? These proportions are a lot more pleasing to the eye!&nbsp; The girl looks well-proportioned in this dress now!</strong></p>

