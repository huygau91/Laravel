<h1>The Law of Proportions in Children's Wear.</h1>
<p><strong>Each new garment always starts from an idea. </strong></p>
<p>Some of us get inspired by <strong>a picture in a magazine:</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image002.jpg" alt="det1-216" width="366" height="392"></p>
<p>Others find their inspiration <strong>in a blooming summer field:</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image004.jpg" alt="det1-217" width="365" height="351"></p>
<p>And still others simply marvel at <strong>the beautiful palette of a sunny winter day:</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image006.jpg" alt="det1-218" width="622" height="382"></p>
<p>The first thoughts start running across your mind: <strong>colours, silhouettes, textures. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image008.jpg" alt="det1-219" width="276" height="459"></p>
<p>Apart from pure creativity, <strong>it takes certain knowledge of composition laws and tactics</strong> to design a comfortable and stylish garment for a child.</p>
<p>The key laws of composition are the result of mankind's centuries-long creative activities and research.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image010.jpg" alt="det1-220" width="451" height="292"></p>
<p>These laws are applicable to any art object, be it <strong>a sculpture...</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image012.jpg" alt="det1-224" width="454" height="456"></p>
<p><strong>...a piece of architecture...&nbsp; </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image014.jpg" alt="det1-221" width="558" height="384"></p>
<p><strong>...a painting...&nbsp; &nbsp;</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image016.jpg" alt="det1-222" width="556" height="441"></p>
<p><strong>...or a garment.&nbsp; </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image018.jpg" alt="det1-223" width="521" height="395"></p>
<p><strong>Laws of composition are a set of rules which you must follow to create a harmonious work of art. </strong>&nbsp;Composition as a discipline is focused upon the principles of constructing various shapes, as well as means of achieving harmony and cohesiveness between separate elements combined together.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image020.jpg" alt="det1-225" width="478" height="352"></p>
<p><strong>Composition is founded upon certain key laws:&nbsp; </strong></p>
<p>1. The law of unity.</p>
<p>2. The law of proportions.</p>
<p>3. The law of symmetry.</p>
<p>4. The law of rhythm.</p>
<p>5. The law of focal point.</p>
<p>These are some things all fashion design students learn about in high education institutions. As useless and boring as the daily routine of doing related practical tasks may seem, it is the true key to success.&nbsp; Such tasks and exercises help them master the laws of composition so well that it becomes automatic.&nbsp; And those who manage to perfect them become acclaimed and celebrated haute-couture designers.</p>
<p><strong>Even the most extravagant catwalk garments (think Vivienne Westwood) are in fact very whole, well-proportioned, rhythmic, and harmonious:&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image022.jpg" alt="det1-227" width="266" height="422"><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image024.jpg" alt="det1-228" width="291" height="422"></strong></p>
<p><strong>Remove just one element and you will disturb the overall look and destroy the composition:</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image026.jpg" alt="det1-229" width="293" height="422"><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image028.jpg" alt="det1-230" width="299" height="422"></p>
<p>I won't dwell on all laws of composition in this book: sure, they are all important in children's fashion but, as long as you don't aspire to work as a leading designer of children's wear for a large fashion house, we can safely focus upon one single law − <strong>the law of proportions.</strong></p>
<p><strong>Every seamstress or tailor who accepts orders on children's clothes must know the basic laws of good proportions.</strong></p>
<p>While an adult's body is more or less proportionate and hardly changes throughout time, the situation with a child's body is a lot more complicated. <strong>Due to constant growth, a child’s body keeps changing </strong>making it absolutely impossible to do without the law of proportions.</p>
<p><strong>Thus, good proportions are the most important means of creating a harmonious look.</strong> Proportions define part-to-part and part-to-whole ratios represented by various mathematical relations between rational and/or irrational numbers.</p>
<p><strong>Proportions are the only means of composition which can "measure" beauty or harmony.</strong> They are responsible for the overall harmony of the garment and the wearer’s overall look.</p>
<p><strong>Proportions can be simple (based on rational numbers) and compound (based on irrational numbers within random geometric constructions).&nbsp; </strong></p>
<p><strong>Simple proportions are classified into contrasting and equal proportions</strong> and expressed by fractions with whole numbers between 1 to 8 as numerator and denominator. For example, a blouse may have a sleeve length of 3/4 and trousers may have a length of 7/8.</p>
<p><strong>Any idea why it only works with numbers between 1 to 8? Because an adult’s head-to-height ratio is 1:8.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image030.jpg" alt="det1-231" width="459" height="375"></p>
<p><strong>Contrasting proportions represented by 1:4, 1:5, and 1:8 ratios</strong> provide some great examples of using simple proportions in composition. They are the most extravagant and eye-catching.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image032.jpg" alt="det1-232" width="269" height="406"><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image034.jpg" alt="det1-233" width="267" height="407"></p>
<p><strong>Children’s fashion designers often use the child's head-to-height ratio as the denominator. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image036.jpg" alt="" width="352" height="510"></p>
<p><strong>For example, if a girl is 6.5 heads tall, then the total length of her dress should equal the length of the bodice multiplied by 6.5. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image038.jpg" alt="det1-235" width="278" height="415"></p>
<p>I will illustrate this principle with certain examples in the next tutorials explaining how I choose suitable dress models for girls.</p>
<p><strong>Part of simple proportions known as <em>equal proportions</em> are based on the 1:1 ratio, i.e. the garment is divided in equal parts.</strong> Garments based on such proportions look calm and static and are a good choice for casual and home wear.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image040.jpg" alt="det1-236" width="300" height="453"><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image042.jpg" alt="det1-237" width="302" height="452"></p>
<p><strong>Compound Proportions:</strong></p>
<p>People have been looking for perfectly harmonious shapes since very old times. <strong>Compound proportions based on irrational numbers and calculated with the help of mathematical formulas and geometrical constructions</strong> take origin in the ancient history.</p>
<p><strong>One type of such compound proportions is known as the golden ratio. </strong></p>
<p><strong>The golden ratio was particularly admired by Renaissance artists who used to call it the Divine Proportion.</strong></p>
<p><strong>The golden ratio is the most harmonious of all ratios.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image044.jpg" alt="det1-238" width="353" height="498"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image046.jpg" alt="det1-239" width="397" height="409"></p>
<p><strong>Two quantities are in a golden ratio if their ratio is the same as the ratio of their sum to the larger of the two quantities.</strong></p>
<p><strong>You can see it well in one of the pictures below: section AB is to section BC the same as section BC is to the entire line AC. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image047.jpg" alt="GolenRatio_1" width="379" height="115"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image049.jpg" alt="Золотое сечение for Eng" width="575" height="143"></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image051.jpg" alt="det1-242" width="383" height="208"></p>
<p>If this is the first time you hear about the golden ratio, I highly recommend you should read more about it. There is a lot of information available online and in various books dedicated to the subject.</p>
<p><strong>Trust me you will learn a lot of interesting and exciting things, some of which seem almost mysterious. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image053.jpg" alt="det1-243" width="476" height="367"></p>
<p><strong>Elements of a pentagon or a star are in a golden ratio: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image055.jpg" alt="det1-244" width="470" height="441"></p>
<p><strong>This ratio is present in a great number of natural objects: trees, sea shells, position of seeds in a sunflower. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image057.jpg" alt="det1-245" width="574" height="403"></p>
<p>Imagine that you are walking down an alley and notice an unoccupied bench. You are tired and feel like sitting down. What part of the bench will you land on? Believe it or not, <strong>most people will sit down at a golden ratio point.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image059.jpg" alt="det1-246" width="567" height="376"></p>
<p><strong>Moreover, I did a small experiment with my own cat.</strong> I noticed that he always slept in the same spot on the sofa. After I measured the sofa, it turned out <strong>my cat slept right in a golden ratio point.</strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image060.jpg" alt="" width="576" height="383"></p>
<p>Of course, the cat did not choose that spot based on his knowledge of maths and irrational numbers - he did it purely instinctively! The golden ratio law is another proof that man is part of creation.</p>
<p><strong>I turns out that a human body is a wonderful example of golden ratio-based proportions: </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image062.jpg" alt="det1-248" width="234" height="369"></p>
<p>A grown-up man is normally eight heads tall: his torso is three heads long and the part below the waist is five heads long. <strong>And that is a golden ratio. </strong></p>
<p><strong>Female body proportions are a little less perfect.</strong> A woman's body differs from a man's body: her total shoulder width is less than two heads but equal to her hip width. Women started wearing high-heels to make up for this little imperfection.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image063.jpg" alt="Пропорции человеческого тела" width="377" height="404"></p>
<p><strong>It is very easy to check whether your waistline is positioned where it should be or whether your body proportions are disturbed: </strong>just put your palm flat under your bust and put the other palm next to it as shown below.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image065.jpg" alt="det1-250" width="623" height="314"></p>
<p>If your two palms fit into the area from the bust to the belly button, then your waistline is lowered and your legs are a little short. <strong>Your body proportions are perfect if this area has a length of 1.5 palms.</strong> If it is shorter than that, then your natural waist is positioned higher than usual and you should lower it on the garment.</p>
<p><strong>Such simple tricks can help you disguise certain imperfections of your client's figure and visually improve its proportions.</strong> Without knowing the laws of proportions, people often distort their overall look with a skirt or trousers looking too short compared to the shirt and the torso unnaturally long, etc.</p>
<p><strong>It is the same with children's clothes: </strong>if you fail to find a harmonious position of the waistline, then the child will inevitably look awkward in the garment.</p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image067.jpg" alt="0_8017d_b781bf2e_L" width="320" height="436"></p>
<p><strong>Golden ratios of 3:8, 5:8, and 8:13 look particularly pleasing to the eye. </strong></p>
<p><strong>In women's suits, proportions are always determined based on the length of the skirt. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image068.jpg" alt="золотое сечение в одежде" width="457" height="366"></p>
<p><strong>You choose the most appropriate skirt length and then determine a golden ratio length for the jacket. </strong></p>
<p><strong>In children's clothes, you take the child's height, determine the golden ratio position of the waistline, and then position all elements of the garment accordingly. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image070.jpg" alt="det1-253" width="441" height="397"></p>
<p><strong>This is why high-waist dresses look harmonious on girls of a particular age group. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image072.jpg" alt="det1-254" width="623" height="494"></strong></p>
<p><strong>How did people figure out the numeric expression of the golden ratio?</strong> Back in the 12th century, Italian mathematician Fibonacci discovered a peculiar mathematical sequence of numbers. It was later named after him − the Fibonacci sequence.</p>
<p><strong>The Fibonacci sequence is a sequence of numbers where each next number is a sum of the two numbers before it: </strong></p>
<p><strong>1&nbsp; 1&nbsp; 2&nbsp; 3&nbsp; 5&nbsp; 8&nbsp; 13&nbsp; 21&nbsp; 34 …</strong></p>
<p><strong>Successive Fibonacci numbers are always in the same ratio to one another: </strong></p>
<p><strong>2/3 = 3/5 = 8/13 = 13/21 … = 0.62</strong></p>
<p><strong>3/2 = 5/3 = 13/8 = 21/13 … = 1.62</strong></p>
<p><strong>It is one of the many mathematical riddles, riddles of nature.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image073.jpg" alt="Тысячелистник v:shapes=" width="403" height="368"></strong></p>
<p><strong>Leonardo da Vinci spent a lot of time studying the Golden ratio in the days of Renaissance. </strong></p>
<p><img src="https://corsetacademy.s3.amazonaws.com/Fancy%20Dresses%20for%20girls/Introduction/The%20Law%20of%20Proportions%20in%20Children_files/image075.jpg" alt="det1-256" width="398" height="492"></p>
<p>Whenever he divided in parts a stereometric body formed by right rectangles, he got rectangles the sides of which were in a golden ratio to each other.</p>
