<p><strong>Lapped Seam</strong></p>
<p>In this video I am going to tell you how to make a lapped seam. Lapped seams are very widely used in corset tailoring.</p>
<p>In order to make one you first of all need to make a stay stitching line, and then stitch on a bone (usually a thin narrow RIGILENE bone) 1mm from the stay-stitch.</p>
<p>Afterwards the seam allowance is folded over and as a result our stay-stitch is placed on the edge of the bone. Now we are fixing the seam allowance on this bone re-stitching it by 1 mm.</p>
<p>As you can see another stitch has gone over the stitch-line that attaches the boning.</p>
<p>And now, as a rule, you should take the second fabric, place it where necessary and pin it in order to fix the pieces at the right spot. And one more time the pieces are stitched together at the bone attaching stitch-line and the pins are then removed. This is a lapped seam that�s very often used in corsets.</p>
<p>You were learning with Tatiana Kozorovitsky.</p>

