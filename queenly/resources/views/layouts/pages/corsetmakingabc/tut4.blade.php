<p><strong>How to Sew in the Boning</strong></p>
<p>I am going to tell you how to attach the boning to the corset. This can be done, for example, the way I�m showing you in this video - with the help of a pressed-open seam.</p>
<p>First of all you will need to prepare strips of masking tape. They should always be at hand for taping the bones. I would recommend having a good amount of these prepared in advance, so that you won�t have to distract yourself later. I stick them to the edge of the sewing machine or the table - this way they are always within my reach.</p>
<p>Now I take a wide RIGILENE bone and cover its edge with the masking tape. When you�re attaching a bone to a pressed-open seam, the bone can be located on the left, in the middle or on the right. It depends on the type of corset you are sewing. Now I will show you one of the ways of attaching a bone in the middle.</p>
<p>It is usually rather difficult to attach the bone exactly in the middle but it�s possible to track how evenly and precisely it lies. I often go beyond the edges of the garment, because then you don�t have to cut off as many superfluous thread ends on the inside.</p>
<p>That�s what a bone attached to the middle of the seam looks like.</p>
<p>And now I will show you what a bone looks like, when you need to overlay the allowance of a pressed-open seam.</p>
<p>It is a very secondary matter whether one should tape over the bone edge or not. I will touch upon this issue when explaining various know-hows; in any case the way of attaching the boning remains the same. I place the bone to the visible outside seam and stitch on alongside the edge. When you stitch like this, the foot may begin to slip because of the greater thickening. When this happens I try to push the bone under the foot with my middle finger to help myself and keep the sewing process under control. As a rule if a top-stitch is sewn 1 mm from the seam-line, the bone attaching stitch gets right in the top-stitch line.</p>
<p>Now I am going to tell you what a stay-stitch is. Stay stitching usually means sewing a supporting stitch alongside the edge of the garment at the distance of seam allowance. &nbsp;The common seam allowance is 0.5 inches, which is why the seam is sewn at the distance of 0.5 inches. It goes along the edge of the garment. The needle turn method is not applicable for stay stitching; the stitch ends always overlap in corners.</p>
<p>Why do we need this stitch? I often use overlapping bones. I mean, you can stitch on a few bones along the perimeter. As a rule I place these bones at 1mm distance from the stay stitching. I�m going to use various types of stitching-on because we are now training. When sewing on a bone I stop a couple millimeters away from the stay stitching and cut the bone off.</p>
<p>Let�s suppose this is our vertical wide bone, and I�m attaching the second one. Now I need to stitch on a narrow bone over the top of the garment. I tape it over and try to overlay the raw edge of the bone. Please take a look, I�m placing the bone at the distance of approximately 1mm from the stay-stitch and stitching it on. I finish it and leave the bone detached. Now I�m taking another wide bone which needs to be stitched on to the curve and push it under the first bone in this manner.</p>
<p>I�m stitching this vertical bone on. Then I am going to make the second parallel stitch and finish the stitching-on of the narrow bone with the second parallel stitch. As a result - as you can see from this side - I have overlapped the bones. You have probably noticed that I have done it in two separate ways. During the first way I have stitched on the vertical bone with two parallel stitches, then I�ve tried to overlap its edge with a horizontal bone, afterwards I�ve pushed the vertical bone under the horizontal one in order to prevent the ends of the fishing line from showing up. There were some cases when these stiff ends began to come out, then I blocked their way with an additional seam and the bone itself and finished stitching-on. This is the main principle of stitching-on overlapping bones.</p>
<p>Sometimes you need to stitch on a bone to the very edge of the fabric. How do I do this? In this case a narrow bone is normally used. I place it right at the edge. However there may be some variations: you could stitch on from the very edge or leave some space, it depends. I�m making a stitch along the inner side of the bone. I�m stitching exactly between the first and the second fishing line. Please pay attention to this detail - exactly between the first and the second fishing lines. This is done in order to guarantee that the fabric is caught in. This is how we stitch on a bone to the very edge of the garment.</p>
<p>I also often ease in some fullness underneath the bone. What does it look like? I start with a straight spot and then, placing my fingers this way, I start to push the lower fabric away from myself while holding the bone with the right hand and pulling it on to myself. In this manner there is somewhat of a light gather forming underneath the bone.</p>
<p>I use this method when quilting rounded cups and stitching on the boning to the upper edge of the corset, in other words - to the neckline. It allows for a good fitting of the corset in the bust area and for the forming of a dome-shaped cup. Look, we have some fullness here. But please keep in mind that the extent of fullness varies each time.</p>
<p>I have just showed you how you should quilt the upper edge of the corset. Sometimes when quilting round cups a little more fullness need to be eased in.</p>
<p>And now attention please! There are cases when you absolutely shouldn�t ease in fullness underneath the bone, namely when you ease in the inner bones of a cup. In order to avoid fullness you should pull the fabric slightly on to you. I pull the fabric on to myself with my left hand while pushing the bone away from myself with the right hand. Or the other way round - whichever hand is comfortable for you. The point is that the fabric is pulled on and the bone is pushed away. This is done to prevent fullness from forming underneath the bone. Anything can happen when a bone is being quilted freely, you cannot predict how well the sewing machine will perform. Now it has sewn it perfectly but sometimes it might ease in.</p>
<p>We�re finished, now you can securely quilt from the other side. Look, there is no easing in. It is very important when you quilt the boning inside the cup. When there is some easing in in the cup its size gets smaller and it is therefore deformed.</p>
<p>You were learning with Tatiana Kozorovitsky.</p>

