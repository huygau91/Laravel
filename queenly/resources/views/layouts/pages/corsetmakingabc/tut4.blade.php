<p><strong>Features:</strong></p>
<ul>
<li>"ABC of Corset-making" is the basic course for beginners.</li>
<li>We learn basic sewing actions, required for corset making.</li>
<li>We&nbsp;do not make finished products, only practice the technique.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>Cutting fabric the right way;</li>
<li>Applying fusible interfacing;</li>
<li>Topstitching;</li>
<li>Sewing in the boning;</li>
<li>Lapped seam;</li>
<li>Curving the boning;</li>
<li>Ripping off the boning;</li>
<li>Types of seams and pressing.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>These actions are used on a daily basis, in every corset based garment you make.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Author:</strong> Tatiana Kozorovitsky</p>
<p><strong>Total length:</strong>&nbsp;35m 23s</p>
						
