<p><strong>Cutting Fabric the Right Way </strong></p>
<p>In this video I am going to touch upon the question of choosing the right direction for cutting the fabric in order to get seams of high quality when stitching pieces together.</p>
<p>And now I have prepared these strips. One pair of strips is cut in the width-wise direction, which means the edge will run atop and I am going to stitch the strips together alongside the cutting line.</p>
<p>The other pair of strips is cut precisely lengthwise, which means the edge will run alongside the seam. I am going to show you, so you know what this seam looks like. We�re stitching the fabric strips together using a 0.5 inch seam allowance. This seam can be pulled a little tighter, although pulling is difficult when sewing corset garments - sometimes shaped seams need to be used; that is why this time I am not going to pull. Look, you can already see that the machine has eased in a little fullness.</p>
<p>You would think the fabric could be tightened and pressed open. But even the exact spot where I've tried to pull looks rippled.</p>
<p>Now I�m going to stitch-out the pieces that I have cut in the lengthwise direction. See what happens now: the same thing as before, the machine eases in a little fullness; but it can be pulled and spread out. In any case the fullness is clearly noticeable with the naked eye.</p>
<p>I am going to press two samples open and you will see what turns out of it. Let me do it right in front of your eyes to maintain the experimental integrity by all scientific rules. At first I�m pressing them open from one side and then from the other. Now I lay out the fabric strips and press each open individually.</p>
<p>No matter how much I try, the fabric and seam allowance stay rippled. Besides, the seam is not perfect even on the spot where I've tried to pull in the first place.<br>
I will press open the other two strips straight away.<br>
Look how much better the seam is. Can you see? In our alternative version the seam appears much more even. It looks neat and smooth. There is only one conclusion - almost 90% of modern fabrics with blended and synthetic fiber should be cut width-wise.<br>
You were learning with Tatiana Kozorovitsky.</p>

