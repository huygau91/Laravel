<p><strong>How to Rip off the Bone.</strong></p>
<p>In this video I am going to teach you how to rip off a bone that has been sewn-in incorrectly.</p>
<p>So, for example you have decided that you�ve sewn this bone in incorrectly. Don�t stress yourself by undoing small threads, it�s enough to take the scissors, cut a little and - look here- the bone is coming out itself. It�s best to perform this on the surface but basically you can do as you wish.</p>
<p>I wouldn't recommend you to tear the bone off; it does need to be carefully ripped off. I can�t recommend tearing it off because there would be way too much pulling-up on the fabric and the bone would get ripped off completely this way. I�m removing the threads and steam-pressing a little more.</p>
<p>The rest is now a matter of technique, the bone is gone. Any sewn-in straight bone can be ripped off in this manner. You simply use the same method I have just shown. As a result you can always re-sew a bone onto a different spot if you have accidentally eased in or stretched out and would like to correct the mistake.</p>
