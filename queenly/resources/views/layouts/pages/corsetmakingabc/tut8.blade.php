<p><strong>Types of Seams and Pressing</strong></p>
<p>In this video I am going to tell you about different types of seams and pressing.</p>
<p>There are two main types of seams used in corset tailoring. The first seam is a plain seam - that means I am just stitching the pieces together; the second one is a lapped seam. Plain seams can be pressed open, and I am going to show you how: I am pressing open seam allowances to both sides, like this.</p>
<p>Or the same seam can be pressed flat, which means pressed to one of the sides. The seam allowances are pressed to one side.</p>
<p>Depending on the technique they can be pressed either to the right or to the left.</p>
<p>You were learning with Tatiana Kozorovitsky.</p>

