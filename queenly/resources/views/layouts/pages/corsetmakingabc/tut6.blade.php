<p><strong>How to Change the Shape of the Boning</strong></p>
<p>In this video I am going to show you how to change the shape of a bone. A thin RIGILENE bone is often curved or given a rounded shape.</p>
<p>You can hardly stitch such a bone onto a rounded cut edge because it bends and twists. We need to gather it slightly and draw out the fishing lines so that the bone could lie flat on the fabric and take the necessary shape. Take one fishing line and hold it tight with your hand, then curve it beyond pushing the bone away from you. And voila, our bone is now well rounded.</p>
<p>Now we need to cut off the ends of the fishing line and put some masking tape over the edges. You could even out the tape depending on where it�s located. And now you can sew on this stitch in a curved line. Let�s suppose you need to sew it on to an arc like this.</p>
<p>Just place it where needed - you might also need to ease in a little fullness underneath pulling the bone on to yourself and pushing the fabric away from yourself. Or you could just place it there without easing in, this is also possible. Different ways work for different cases. And now you can make the second stitch.</p>
<p>If you wish to make an embossed piece it will look �mushroom-like� because of the fullness. If you wish to have it edge-shaped, you should place a curved bone there and then it�s enough to steam-press it and the spot will be flat. Here we go!</p>
