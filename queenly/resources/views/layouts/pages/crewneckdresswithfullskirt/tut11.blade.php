<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image043.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>The side front piece: </strong>I join the fabric along the princess seams and the side seam edges. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image047.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image049.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image051.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image053.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the edges where necessary referring to the base pieces as stencils. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image055.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Next I sew up the front princess seams. </strong>I notch the princess seams on the central front piece and stitch over the side piece as per usual. Remember to trim the seam allowances. </p>
