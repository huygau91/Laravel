<h3>Tutorial 11. Preparing the Face Pieces. Sewing Up the Princess Seams.</h3>
<p></p>
<p><strong>So we have finished working on the lining and it's time to join the face pieces now.</strong> I am going to join the duplicated base pieces and the taffeta pieces along their vertical open edges so I can later work on them as a whole. I will stitch over the base at a presser-foot distance from the edge.&nbsp; </p>
<p><strong>The central back piece:</strong></p>
<p><strong>I stitch along the central open edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Stitching along the inclined open edges is compulsory or else they might get stretched. </strong>I'd like to draw your attention to the fact that I overlap the seams and never turn the garment about the needle, i.e. there's a separate stitch line for each side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I start joining the pieces by sewing up the princess seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>The other central back piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>The side back piece: </strong>I join the base with the taffeta by sewing up the princess seam and the side open edge. Please remember that you need to join the layers of fabric before sewing up the princess seams!&nbsp; It's very easy to make a mistake!&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The other side back piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I sew up the princess seam between the central and the side pieces of the back.&nbsp; </strong></p>
<p>Remember that sewing up a curved seam can be more than challenging:<strong> make sure to notch the princess seam on the central back piece</strong> beforehand.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you that it's easier to stitch over the side piece. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I trim the seam allowance down to 6-7mm,</strong> i.e. basically cutting along the seam that joins the taffeta with the base. I trim the corner of the seam allowance at an angle.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>Press the princess seam open very carefully. </strong>It should be neat and flat.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I sew up the other princess seam of the back.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image031.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image033.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image035.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>The central front piece: </strong>I join the base with the taffeta along the princess seam and the neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2011_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
