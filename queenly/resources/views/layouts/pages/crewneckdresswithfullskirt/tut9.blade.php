<h3>Tutorial 9. Quilting the Front and the Back of the Corset with Bones.</h3>
<p></p>
<p><strong>The front of the corset: </strong></p>
<p><strong>I make a stay-stitch along the bottom edge. </strong>It will mark the border for the bottom ends of the bones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I also make stay-stitches around the armscyes and the neckline. </strong>They will prevent those areas from stretching and help you give them a neat finish later.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I make another stay-stitch along the bust line.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The first half of the corset back:&nbsp; </strong></p>
<p><strong>I start by making a stay-stitch along the inclined open edge of the back to protect it from stretching.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make another stay-stitch around the armscye: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And another one along the bottom edge: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I repeat the same procedure with the other half of the back.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we can start sewing on the boning in accordance with the markings.</strong></p>
<p><strong>The first half of the corset back:</strong></p>
<p><strong>I sew a narrow Rigilene bone onto the side back piece. </strong>I tape the ends of all bones with pieces of masking tape. I start from the bust line and leave the marked line on the right, i.e. avoid coming closer to the side seam. Although the marked line got distorted after we sewed up the princess seam to join the pieces, I still try my best to lay the bone straight.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I stop about 0.5cm from the bottom stay-stitch and trim the bone.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I make the second parallel stitch along the other side of the bone.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew a wide Rigilene bone over the princess seam. </strong>I start from the bottom, step 0.5cm up from the stay-stitch and lay the bone so as to have the princess seam right in the middle of it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I keep stitching the bone in a straight line even after the princess seam turns towards the side. </strong>I trim the bone at the armscye level. The fitting test will show that I could've made this bone 3-6cm longer at the top but unfortunately I haven't. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I make the second parallel stitch along the other side of the bone.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>There will be two vertical bones in the lacing area on the central back piece and I wasn't planning to add any more.</strong> But the central back piece is rather broad so <strong>I've decided to place another narrow vertical Rigilene bone</strong> at the same distance from the princess seam as the vertical bone on the side back piece. This bone will be as long as the one sewn over the princess seam. I could've also prolonged it upwards by a couple centimeters. I sew the bone on with two parallel stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The second half of the corset back:</strong> you simply attach all bones the same way as described above.&nbsp; </p>
<p>By the way <strong>I make a couple of small horizontal stitches over the top ends of the bones</strong>, which I do really seldom when working on corset garments. This is done in order to prevent the bones from sticking out as they don't reach the very edge of the garment.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image050.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>You could make this bone and the next one longer. There were crease marks on the finished dresses, which means I could�ve easily trimmed the bones 3-6cm higher. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image058.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>The back of our dress is now quilted with bones! </strong></p>
<p><strong>It's time to sew the boning onto the front of the corset in accordance with the markings.</strong></p>
<p><strong>I start from the cups area.</strong> Round cups should be quilted with narrow Rigilene bones.&nbsp; </p>
<p><strong>I sew on the bottom perimeter bone. </strong></p>
<p><strong>I have gathered it slightly in advance</strong> to make it somewhat arched. Now you can arrange it in any desired shape. I pull the fishing lines out of the bone and give it the right shape by pulling on the outermost line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I place the bone with its taped end at the bust line.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image066.jpg" class="img-responsive center-block"></p>
<p>I ease in the fabric <strong>in the arched area from the bust line to the princess seam</strong> to give the cup more volume. I stitch along the outer side of the bone.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>You shouldn't ease in between the princess seams. Stitch very carefully: </strong>you might stretch the fabric with the bone because the piece is not duplicated.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I pass onto the other half of the cup area and ease in the fabric again. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone at the bust line, tape its end over, finish sewing and make a bar tack. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image076.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The bottom perimeter bone is in place. </strong>Our cups are three-dimensional now. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image078.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I place a bone along the middle of the bust. </strong></p>
<p><strong>I stitch it on from the face side</strong> to prevent it from protruding through the face fabric since we are not going to pad the cups. This bone is also slightly gathered and I have taped its ends with some masking tape as usual. I start and finish sewing 4-5cm from the side open edges so I can still apply alterations after the fitting test if necessary. I attach the bone with two stitches parallel to the stay-stitch I made along the middle of the bust. Please note that the stay-stitch remains above the bone. I stitch down the ends of the bone as well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image080.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image082.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image084.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>Next I lay the inclined bone inside the cup, secure it with two parallel stitches</strong> and push its end underneath the bottom perimeter bone. I trim the bone at the bust-line level and stitch down its top end.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image088.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image090.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image092.jpg" class="img-responsive center-block"></p>
<p>Then I step about 0.5cm from the bottom stay stitch and<strong> sew a narrow bone over the princess seam with two parallel stitches</strong>.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image094.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image096.jpg" class="img-responsive center-block"></p>
<p><strong>And I lay other inclined bones inside the cup the same way.</strong> You need to gather them slightly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image098.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image100.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image102.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image104.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image106.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch a narrow Rigilene bone over the other bust princess seam. </strong>I let it settle naturally inside the cup.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image108.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image110.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image112.jpg" class="img-responsive center-block"></p>
<p><strong>The last inclined bone inside the cup: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image114.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image116.jpg" class="img-responsive center-block"></p>
<p><strong>Then I stitch on the horizontal bone below the bust. </strong>This bone is also gathered and I push its ends underneath the perimeter bones of the cups. I stitch along its outer side. I spread out the cups as I pass along them trying to give them more volume.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image118.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image120.jpg" class="img-responsive center-block"></p>
<p><strong>Next I sew the vertical bones onto the central front piece.</strong> I use wide Rigilene bones to reinforce the garment and make it stiffer. I start sewing 0.5cm up from the bottom stay-stitch and push the opposite ends of the bones underneath the horizontal bone below the bust line stitched along one side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image122.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image124.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image126.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image128.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can secure this horizontal bone by stitching along its other side.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image130.jpg" class="img-responsive center-block"></p>
<p><strong>I make a parallel strengthening stitch over the perimeter bone of the cup, too. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%209_files/image132.jpg" class="img-responsive center-block"></p>
<p></p>
