<h3>Tutorial 3. Rules of Working with a Mock-Up.</h3>
<p></p>
<p><strong>I have put my mock-up on the dress-form.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>NB:</strong> you are not supposed to focus on the balance of the garment or the location of the side seams at this stage. Don't be disturbed if they have shifted towards the front or the back - it doesn't matter at the moment because this mock-up wasn�t sewn after the dimensions of your dress-form. What you should do is carefully stuff any hollows between the mock-up and the surface of the dress-form. I use regular foam shoulder pads for this purpose. Feel free to use padding polyester, wadding, leftover bits of fabric or even loop towels if you need to increase the size of the dress-form considerably. The main task is to distribute and spread it out very evenly! </p>
<p><strong>Next you should mark the lines of reference right on the mock-up</strong>: i.e. bust line, under-bust line, waistline, stomach and hip line. If you find it hard or if you want to change the location of notches, simply tie cords around the mock-up the way you put your measuring tape around the client and re-draw the lines following these cords. Mark the notches - later you can easily transfer them onto your own pattern. </p>
<p><strong>You can study many fine points and take important measurements with a mock-up at hand.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Take a look at the sleeve scye.</strong> You can tell that this pattern was designed for slim arms, which means you should immediately think of making the scye deeper if your client�s has full arms. Of course the scye will expand in accordance with her clip and bust height measurements, yet you are already aware that the original scyes of the pattern only fit small arms. Even if your client's bust size corresponds with the pattern but her arms happen to be much fuller, you will be able to tell how to alter the scye to ensure she can move her arms freely. </p>
<p>However I'd like to note that a corset-based dress should have a maximum possible side length, i.e. its side seams should come as close to the underarms as possible. Make sure the fabric doesn't rub against your client's underarms. She should feel comfortable and at the same time the armscyes should be as high as possible to prevent the entire garment from getting pulled up when she raises her arms. When you sew with stretch fabric, the armscyes stretch whenever the client raises her arms allowing her to move them freely but we are dealing with a stiff construction that holds around the waist. The sleeves might simply tear off when the woman moves her arms actively. </p>
<p><strong>Bust middle is a very important measurement that requires your closest attention.</strong> You can tell from the location of the princess seams that the middle of the bust is somewhat misplaced on my pattern. The actual midpoint of the bust is located at point A but the princess seams here are purposely shifted to the sides because they are really steep and it wouldn't look nice if they passed through point A. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></p>
<p>When you take the bust middle measurement off your client, measure it between the bust princess seams, i.e. make it wide enough. </p>
<p><strong>As soon as you've determined the bust middle, you should take the clip measurement starting from this point (point B).</strong> You can measure the clip up to the armscye on the mock-up. This distance will determine the location of the armscye. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Next you should measure the shoulder width at the front and the shoulder width at the back.</strong> Don't forget that your mock-up has seam allowances you should subtract from your measurement values. Remember about the 2cm seam allowance on the central back piece when you measure the shoulder width at the back or else you'll end up with the shoulder width of your dress-form and not your pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Very important measurements are back shoulder slope and nape to waist.</strong> Nape to waist is measured from the point where you want the cut to start down to the waist. When you start altering the pattern, you will determine the right location by marking the value of this measurement up from the waist. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next you measure the shoulder length.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Neck base to bust middle and shoulder tip to bust middle are taken from the bust middle point.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>You shouldn't have any troubles calculating the size of the neck hole with these measurements at hand.</strong> It will be determined by the shoulder width at the front minus the shoulder length of this pattern. You can also easily determine its location with the help of the neck base to bust middle and shoulder tip to bust middle measurements. </p>
<p><strong>You can measure the neckline depth up from the bust line.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%203_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>So equip yourself with a measuring tape and take the same measurements off the mock-up as you are going to take off the client. </strong>&nbsp;The difference between these values will be used for all alterations. </p>
<p>This is how you measure the pattern for the first time. The dimensions of my pattern can be seen in the calculations.</p>

