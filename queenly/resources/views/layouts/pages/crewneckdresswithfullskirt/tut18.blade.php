<h3>Tutorial 18. Sewing In the Collar.</h3>
<p></p>
<p>You should only <strong>press the collar</strong> from the side of the joining seam and not from the fold line side.&nbsp; </p>
<p><strong>I lay out the garment and start pinning in the collar. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I notch the neckline in the rounded area.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I match the centre of the neck hole with the centre of the collar </strong>and secure them with a pin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully secure the first half of the collar with pins</strong> matching those points on the back where I will start sewing and the notches of the shoulder seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>It is very important to determine the location of the collar correctly!</strong> Before pinning down the other half of the collar I locate its other corner symmetrically to the first pinned one. Both corners should be located at the same height. The holes in the lacing area should also be symmetric. I secure the other corner with a pin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Then I carefully pin down the other half of the collar </strong>matching the notches of the shoulder seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>If you have measured and cut your collar correctly, the length of its seam allowance should match the length of the neck hole perfectly. </strong>You won't need to pull it up or ease it in! </p>
<p><strong>The collar is ready to be sewn in.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>So let�s do it. </strong>&nbsp;</p>
<p><strong>It's best to sew in collars or any other similar long pieces starting from the middle. </strong>I stitch from the middle to the end of the piece, turn the garment, return to the middle and stitch to the other end. This way the joining seam settles well and looks more even.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>The ends of the collar are perfectly matched.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>It's highly recommended that you use the turn-and-stitch finish to help the seam allowance spread. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image033.jpg" class="img-responsive center-block"></p>
<p>However <strong>I suggest you should overlay the seam with a lace ribbon or a bias tape</strong> before making the finishing stitch. It will look much prettier this way. <strong>&nbsp;</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I overlay the joining seam with a bias tape.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image036.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I do the turn-and-stitch finish directing the seam allowance towards the dress. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>The collar is sewn-in and the seam has a neat finish!</strong> You can secure the bias tape by hand. Now you should check how the garment with the sewn-in collar looks on the client or on the dress-form.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>All that's left to do at this stage is sew up the open edges at the sides.</strong> I sew them up the traditional way with a 2cm seam allowance. It's all set!&nbsp;&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image056.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The dress top is basically finished. </strong>All we need to do is press it, insert plastic bones to reinforce the edges of the lacing bars and attach the modesty flap.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>I press open the seam allowances of the side seams </strong>with the help of my pear-shaped pressing mould.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2018_files/image064.jpg" class="img-responsive center-block"></p>
