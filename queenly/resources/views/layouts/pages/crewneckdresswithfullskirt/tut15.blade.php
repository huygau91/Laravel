<h3>Tutorial 15. Sewing Up the Side Seams. Finishing the Neck Hole and the Lacing Area.</h3>
<p></p>
<p><strong>I make a machine stitch along all pinned open edges at a presser-foot distance from the edge. </strong>You must never turn the garment around the needle! I finish the seam, cut the thread and start a new seam from the edge.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and trim the threads.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>It's important to reinforce the inclined open edges of the back. </strong>Even though we have secured them with stay-stitches, they are still prone to stretching because they were cut on the bias. Moreover, so is the collar that we are going to sew in. The inclined edges will stretch when they meet.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest we should reinforce them with organza ribbons. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch on the ribbon from the side of the lining.</strong> You can see the stay-stitch through the organza. I step 1mm from the stay-stitch towards the open edge and make a strengthening stitch. I ease in the fabric very slightly underneath the organza ribbon.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the ribbon over the seam allowance and secure it with a strengthening stitch.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>And I perform the same steps from the other side of the garment.</strong> I pull the ribbon to me and ease in the fabric underneath it.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I overlock all open edges including the inclined ones that I've just covered by organza. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to finish the lacing area. </strong>&nbsp;</p>
<p><strong>I sew a wide Rigilene bone along the central open edge of the back.</strong> I�ve taped its ends as usual. The bone starts about 2cm up from the bottom edge and stops 3cm from the top of the corset (that includes the seam allowances for sewing in the collar). &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the bone with the second parallel stitch.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I measure a wide Rigilene bone of the same length and sew it on along the central open edge of the opposite half with two parallel stitches. </strong>I place it at the same distance from the edges of the garment to make sure the lacing bars are identical from both sides.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>I lay a narrow Rigilene bone of the same length next to the attached bone and sew it on with two parallel stitches. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image062.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image064.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I sew a mirrored narrow Rigilene bone onto the other half of the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image066.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image068.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I bend the lacing area to the underside and make a strengthening stitch 1.2cm below the fold line.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image070.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>And I make another parallel strengthening stitch at a presser-foot distance from the first one. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image074.jpg" class="img-responsive center-block"></p>
<p>We will punch holes for the grommets between these two stitches and the fabric will be secured very well. <strong>You can insert a plastic bone into the formed tunnel to make the lacing bar more robust. </strong>But make sure not to do it before attaching the collar and setting in the grommets.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>I finish the lacing area on the other half of the back the same way.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image078.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image080.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I compare the lengths of the lacing bars: they should be the same.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2015_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>Now we should press the lacing area and set in the grommets because it's a lot harder to do it on a finished garment. </strong></p>

