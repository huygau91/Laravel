<h3>Tutorial 8. Sewing Up the Princess Seams of the Lining. Pressing. </h3>
<p></p>
<p><strong>Let's start working on the front pieces.</strong> </p>
<p><strong>First of all we need to secure the stiff iron-on material on the side front pieces by stitching to prevent it from coming off.</strong> I am using a budget version of iron-on material. The quality of its adhesive surface is not that great however it holds its shape rather well after you press it. It's pretty good value for the money. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>It's compulsory to notch the princess seams of the front pieces down to the under-bust level before joining them,</strong> or else they won�t be neat. I notch them rather deep, nearly to the very seam.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I match the bust lines of the side and the central piece </strong>and secure them with a pin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And I start joining the pieces by sewing up the princess seam of the side front piece.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p>I carefully open the seam allowance to help it settle evenly underneath the presser foot. </p>
<p>Although this seam is made on the lining, it doesn�t mean you shouldn�t try your best to keep it neat and even. Needless to say how important that is for all seams made on the face side!&nbsp; </p>
<p><strong>I remove the pin upon reaching the bust line and continue sewing.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I check the quality of the seam.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the seam allowance down to the under-bust level to remove the unnecessary thickness formed by the iron-on material. I leave about 6-7mm of its width. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Next I sew up the other bust princess seam exactly the same way.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>Please note that it's best to stitch over the side piece when you're sewing up a bust princess seam. </strong>&nbsp;That's why I'm going to sew from the bottom upwards this time. It's easier to control the seam allowance when you stitch over the side piece. When working on a one-piece dress, I start sewing from the waistline and up to the top of the dress and then I turn it over and sew up the other side down to the very bottom.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image031.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image033.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance and trim its upper corner at an angle.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>It looks quite neat - all I need to do is press it later. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>Let's sew up the princess seams at the back.</strong> </p>
<p><strong>I stitch over the side piece.</strong> And again, I notch the central back piece from the top down to the under-bust level and the princess seam settles very evenly.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image041.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image043.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image045.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I sew up the other princess seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image049.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image051.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image053.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image055.jpg" class="img-responsive center-block"></p>
<p><strong>It's time for some pressing. </strong></p>
<p><strong>Bust princess seams: </strong>I press the seam allowance flat towards the centre down to the under-bust level and press it open below it. <strong>I make a small notch at the under-bust level for this purpose.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image057.jpg" class="img-responsive center-block"></p>
<p><strong>I find it easier to press the seam allowances from the face side. </strong>Although I'm not using much steam, I still dampen the surface first and then iron it dry. Try to make the cups round. Right now the main task is to flatten the princess seams to help the cups settle over the pressing mould and make them smooth and round.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image059.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image061.jpg" class="img-responsive center-block"></p>
<p><strong>First I press the seam allowance flat from the lining side from the bottom up to the under-bust level. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image063.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image065.jpg" class="img-responsive center-block"></p>
<p><strong>Then I press it from the face side, too. It's a must.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image067.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image069.jpg" class="img-responsive center-block"></p>
<p><strong>The princess seams of the back settle really well after you press them open.</strong> I press them first from the lining side and then from the face side like before. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image071.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image073.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image075.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%208_files/image077.jpg" class="img-responsive center-block"></p>
<p>Be very careful when pressing the seams. Try not to stretch them out.&nbsp; </p>
<p>I finish pressing the back of our dress and get ready for sewing on the bones. </p>

