<h3>Tutorial 5. Altering the Pattern of Our Corset-Based Dress with Shoulder Seams. </h3>
<p></p>
<p><strong>I have glued together another set of standard pattern sheets to customize them after my client�s measurements. </strong>&nbsp;</p>
<p><strong>I start from the central front piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>First of all I need to narrow the middle of the piece by 1cm in correspondence with the bust middle measurement. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove 2cm from the clip measurement and mark a new point, point L. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image005.jpg" class="img-responsive center-block"></p>
<p><strong>Front shoulder width measurements coincide.</strong> </p>
<p><strong>Neck base to bust middle:</strong> my client's measurement is 23cm. </p>
<p><strong>I need to mark 23cm plus another 1.2cm for the seam allowance (24.2cm total) upwards from the middle of the bust (point A).</strong> I mark point B. I prefer making full-size markings to avoid mistakes.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image007.jpg" class="img-responsive center-block"></p>
<p><strong>Shoulder tip to bust middle:</strong> my client's measurement is 21cm.</p>
<p><strong>Shoulder width stays the same and I draw a vertical guiding line to keep it in mind. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image009.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now I need to draw an arch with a 22.2cm radius (21cm plus another 1.2cm for the seam allowance) going from the middle of the bust (point A) to the intersection with the vertical shoulder border (point C). </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image011.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I re-draw the shoulder between points B and C.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>Next I mark the shoulder length (5cm) plus another 2.5cm for the seam allowances (a total of 7.5cm) from point C.</strong> As a result I determine the apex of the shoulder (point D).&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>And I re-draw the neckline and the armscye. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>I add 0.8cm to the seam allowance of the shoulder seam to make it 2cm. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>Now I have the right shoulder configuration for my client, located exactly where it should be. </strong>&nbsp;</p>
<p><strong>Let's apply the necessary alterations to the princess seams. </strong>I need to shift the front princess seam inwards by 0.8cm at the waistline, by 0.7cm at the stomach level and by 0.3cm at the hip line. <strong>I re-draw the princess seam. </strong>You can do it by hand or use the standard pattern as a stencil. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image021.jpg" class="img-responsive center-block"></p>
<p>I will draw the transition to the armscye after altering the pattern of the side front piece. </p>
<p><strong>The side front piece:</strong></p>
<p><strong>I shift the princess seam 2cm inwards in accordance with the clip measurement.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>And I move on to the side open edge. </strong>&nbsp;</p>
<p><strong>I shift it 0.5cm inwards at the bust-line level and 0.5cm outwards starting from the under-bust level and down to the bottom of the piece. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a pretty side line and re-draw the top line of the piece with regard to the clip measurement alteration keeping in mind that the side length should stay the same. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let's continue applying alterations to the princess seams.</strong> I need to shift the front princess seam inwards by 0.8cm at the waistline, by 0.7cm at the stomach level and by 0.3cm at the hip line. <strong>I re-draw the princess seam. </strong>You can do it by hand or use the standard pattern as a stencil. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image029.jpg" class="img-responsive center-block"></p>
<p>Looking at the altered pattern of the side piece I believe we have shifted the entire pattern to the side:&nbsp; we have shifted the side open edge outwards by 0.5cm and shifted the princess seam inwards by 0.8-0.7cm. <strong>My decision is to leave the side seam as it is now and shift the princess seam inwards at the waistline by a value that�s 0.5cm smaller. This way the pattern configuration won�t be deformed.&nbsp; </strong></p>
<p><strong>I trace the new princess seam line with a red marker and leave the side seam unchanged from the under-bust level down to the bottom.&nbsp; </strong>I only shift it 0.5cm inwards along the bust line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image031.jpg" class="img-responsive center-block"></p>
<p><strong>Next I need to draw the armscye.&nbsp; </strong></p>
<p><strong>I bend the top of the side front piece along the seam allowance and place it at the seam allowance of the central front piece.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image033.jpg" class="img-responsive center-block"></p>
<p>The armscye looks too sharp in my opinion. I suggest we should make it rounder and make the princess seam a little longer - we can always trim the armscye later when working on the garment.&nbsp; Besides, our 22cm side length accounts for set-in sleeves. But our dress is sleeveless so we can safely cut the side length down to 21cm. </p>
<p></p>
<p><strong>The armscye with applied alterations looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>Customized patterns of the central and side front pieces are ready!</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image037.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image039.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let's move on to the back of the dress. </strong>&nbsp;</p>
<p><strong>The side back piece: </strong></p>
<p><strong>We need to adjust the side seam.</strong> I shift it 2.5cm inwards at the bust-line level and 1.5cm inwards starting from the under-bust level and down to the hip line. I transfer all notches onto the new side seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>Next we need to work on the princess seam.</strong></p>
<p>I need to shift it 0.5cm outwards at the bust-line level. I suggest transferring this 0.5cm onto the side seam, i.e. leaving the princess seam unchanged at the bust-line and shifting the side seam inwards by 2cm instead of 2.5cm at the same level. This way we will achieve the same kind of an incline of the side seam between the bust line and the under-bust line both at the back and at the front.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image043.jpg" class="img-responsive center-block"></p>
<p><strong>Let's continue altering the princess seam of the back. </strong>I shift it 0.5cm outwards at the under-bust level, 0.8cm inwards at the waistline, 0.7cm inwards at the stomach level, and 0.3cm inwards at the hip line. But as I start re-drawing the princess seam, I notice that the line would've been smoother if I had shifted the princess seam 0.3cm outwards at the bust-line level. I shift the side seam 2.3cm inwards at the bust-line level in order to preserve the balance of the garment. Besides I also adjust the length of the side seam at the back so it's equal to the length of the side seam at the front. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>The central back piece:</strong></p>
<p><strong>I need to alter the princess seam at the back. </strong>I mark 0.5cm outwards at the bust-line level and at the under-bust level, 0.8cm inwards at the waistline, 0.7cm inwards at the stomach level and 0.3cm inwards at the hip line <strong>and re-draw the princess seam.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I have also measured the back width. </strong>It is 31cm on the client and 37cm on the pattern. I need to remove 3cm. I draw a new line going up from the bust line. I shorten the back width (blue line) by 0.5cm and remove the remaining 2.5cm from the princess seam side (point K). <strong>Let's check it: </strong>my client's back width measurement is 31cm. I divide this value in half (the back is made of two halves) and get 15.5cm. There are also seam allowances: 1.2cm for the princess seam and 2cm for the side seam. This makes a total of about 19cm.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image049.jpg" class="img-responsive center-block"></p>
<p><strong>Back length:</strong> I mark 15cm plus another 1.2cm for the seam allowance upwards from the waistline (a total of about 16.5cm, point H). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>Back neck base to waist:</strong> I mark 44cm up from the waistline plus another 2cm for the shoulder seam (a total of 46cm, point E). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image053.jpg" class="img-responsive center-block"></p>
<p><strong>My client's shoulder width is supposed to be 38cm from the back.</strong> I divide this value in half and add 2cm and 1.2cm for the seam allowances, which makes a total of 22.2cm. I mark this value from the vertical line on the side.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image055.jpg" class="img-responsive center-block"></p>
<p>And then <strong>I mark the back shoulder slope measurement</strong> (43cm plus a 2cm seam allowance, i.e. 45cm) from the waistline to the intersection with the vertical back shoulder width line (point F).&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image057.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the top shoulder border through points F and E. Then I mark the shoulder length (5cm) plus 2.5cm for the seam allowances (7.5cm total) from point F. As a result I determine the end of the shoulder (point G).</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image059.jpg" class="img-responsive center-block"></p>
<p><strong>I draft the back neckline by connecting points G and H. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image061.jpg" class="img-responsive center-block"></p>
<p><strong>And then I make it smoother and more graceful. The back neckline of the finished garment will look like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image063.jpg" class="img-responsive center-block"></p>
<p><strong>The shoulder is in place and now I need to connect it with point K (back width with the seam allowance).&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image065.jpg" class="img-responsive center-block"></p>
<p><strong>I start building the armscye. </strong></p>
<p><strong>I need to bring the armscye from point K to the princess seam on the central back piece and match it with the princess seam and the armscye on the side back piece. </strong>The armscye should smoothly pass from the central piece onto the side piece.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image067.jpg" class="img-responsive center-block"></p>
<p><strong>But first of all I'd like to check if the side seams match well enough.</strong> I cut the side pattern of the front following the side seam line, turn it upside down and place it over the side pattern of the back matching their side seams. I compare their configurations and adjust the length of the side seam.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image069.jpg" class="img-responsive center-block"></p>
<p><strong>Next I adjust the princess seams at the back. </strong>&nbsp;&nbsp;</p>
<p><strong>I cut the side back piece following the princess seam line.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image071.jpg" class="img-responsive center-block"></p>
<p>Then I turn the pattern upside down and place it over the central back pattern matching their princess seams.&nbsp; I draw the section of the armscye.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image073.jpg" class="img-responsive center-block"></p>
<p><strong>Now the armscyes match perfectly.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%205_files/image075.jpg" class="img-responsive center-block"></p>
<p><strong>The standard pattern is now fully adjusted after the client�s measurements. </strong>&nbsp;All we need to do is cut it out and start cutting the fabric. If you are not very experienced in altering patterns of corset-based garments with shoulder seams, I highly recommend that you sew a pilot mock-up before cutting high-quality expensive fabric. It'll be the same kind of a mock-up you made to measure the initial pattern. It won't take long and you'll be able to adjust it at any time if you notice any flaws.&nbsp; When you put your new mock-up on the dress-form, you will immediately notice if the garment is misbalanced or distorted and you'll see if it corresponds with your client's measurements.&nbsp; I will also sew a mock-up out of solidarity with you but I'll leave the cutting and sewing process 'behind the scenes.' </p>
<p></p>
<p><strong>So, here are the patterns of the dress customized after our client's individual measurements. </strong></p>
