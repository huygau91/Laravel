<h3>Tutorial 20. Cutting the Skirt and All Parts of the Petticoat. </h3>
<p></p>
<p><strong>So I have cut out two semi-circle taffeta skirts: </strong>the one with the smaller radius is the lining, and the one with the bigger radius is the upper skirt.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>There will be soft nylon netting attached to the bottom edge of the corset</strong>. It is nice to the touch and perfect for skirts. Not to be confused with veiling fabric! I have prepared two straps: each 3m long and 42cm wide considering that the distance from the waistline to the bottom edge of the corset is 12cm. I will assemble these two layers and sew two skirts. Each skirt will be about 50cm long with the seam allowance included.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I feel like making the skirts yet fuller in the area where they join the corset. </strong>I have cut 15cm wide straps of regular tulle for this purpose. I will gather them thickly and make a three-tiered skirt. This skirt will be attached to the corset with an overseam just above the soft skirts made of nylon netting.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image006.jpg" class="img-responsive center-block"></p>
<p>&nbsp;I have prepared seven straps of stiff netting to shape the upper skirt. They are 1.5m and 55cm wide each. I'm going to gather these straps in a single line using a specialty presser foot, then cut sections of desired length and attach them to the bottom of the corset with an overseam tier after tier.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I have cut three 55cm long straps of nylon netting for the skirt that will be sewn on in three layers.&nbsp; </strong>This netting will show from under the shaped hem of the main skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The main skirt will be decorated with lace appliqu�. </strong>I will compensate for the unlike shades of taffeta and netting by using different kinds of lace whiter than the taffeta. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I need to gather all these straps now. </strong>&nbsp;</p>
<p><strong>I have put on a specialty ruffler foot. </strong>I know that many people face a certain challenge when working with this presser foot. The feed dogs tend to tear delicate fabric. You need to loosen the clamp-screw of the presser foot by a couple of turns in order to reduce the pressure. I loosen it by 10 turns. Don't forget to re-tighten the clamp-screw once you have finished gathering the fabric.&nbsp; </p>
<p><strong>I gather 15cm wide straps of tulle in a single line with overlaps. </strong>Then I will cut it up and make a multi-tiered skirt. You can decide on the number of tulle straps on the spot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Next I gather my 42cm wide straps of nylon netting. </strong>I gather each layer individually to make the skirt fuller. I bring each strap to the length of 82cm - the circumference of the hips or the corset bottom.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>55cm wide straps of stiff netting are also gathered in a single line with overlaps. </strong>Later I will cut it up into straps of desired length in correspondence with the circumference of the corset in the area they will be sewn. This netting is what makes the entire skirt so bouffant.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2020_files/image020.jpg" class="img-responsive center-block"></p>

