<h3>Tutorial 23. Joining the Petticoat with the Dress Step by Step. </h3>
<p></p>
<p><strong>I join the lining skirt and the skirt made of three layers of soft nylon netting.</strong> I spread the skirts carefully and secure them with pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The netting is sewn to the lining.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>We need to gather the front and the back of the upper skirt in the middle. </strong>&nbsp;</p>
<p><strong>I mark the gathering area on the front of the skirt. </strong>&nbsp;</p>
<p><strong>And I notch it in the middle. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image013.jpg" class="img-responsive center-block"></p>
<p><strong>I'd like the skirt to remain ungathered 8cm down from the side seam.</strong> I make a notch there.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>And I mark the gathering area on the back of the skirt.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>I follow the traditional approach to gather the front of the skirt: make two parallel stitches and pull on the threads. </strong>&nbsp;</p>
<p><strong>Why don't I just use the ruffler foot? </strong>I need to gather the skirt down to a particular size. Besides, these gathers should be really neat and beautiful. They are made on the very face of the garment so I don't want to rely on the machine or the ruffler foot.&nbsp; </p>
<p><strong>I make two parallel stitches through the gathering areas of the main upper skirt. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image019.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image021.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image023.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image025.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image027.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image029.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image031.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I pull on the thread and gather the fabric. </strong>There should be the same degree of gathering on the left and on the right so I'm pulling on the thread from both sides simultaneously. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image033.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>I put the upper skirt over the lining skirt joined with the netting. </strong>I spread the top edge of the main skirt to match it with the top edge of the lining skirt, i.e. to make their waistline circumferences identical. Then I join the skirts along the centre of the front, the centre of the back and the side seams.&nbsp; <strong>I arrange the top of the main skirt evenly and secure it with pins.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image037.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image039.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image041.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image043.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image045.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image047.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image049.jpg" class="img-responsive center-block"></p>
<p><strong>I have arranged the skirts and secured them with pins along the waistline.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>All we need to do now is secure them by machine-stitching and overlock the edges. Our multi-layer skirt will be ready for getting sewn to the corset afterwards.&nbsp; </strong></p>
<p><strong>The main zip-up skirt is finally ready! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image053.jpg" class="img-responsive center-block"></p>
<p><strong>There will be a petticoat and a lining underneath it.</strong> I will cut the front arch in the very last moment when the skirt has its final appearance since it's basically impossible to foresee the way the main skirt will settle over the full petticoat.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image055.jpg" class="img-responsive center-block"></p>
<p><strong>I mark a line for stitching the main skirt to the corset before attaching the petticoat.</strong> It's much easier to do it now. I mark the location of the seam allowance of the skirt I'm about to attach. </p>
<p><strong>I mark 10cm up from the bottom edge in the middle of the corset back and 11cm up the side seams.</strong> I draw a guiding line through the points.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image057.jpg" class="img-responsive center-block"></p>
<p><strong>The entire line is marked 11cm up from the bottom at the front of the corset. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image059.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we can start attaching the petticoat. </strong>&nbsp;</p>
<p>The first layer of the petticoat is the soft skirt made of nylon netting that will be in contact with the legs and the three-tiered tulle skirt. </p>
<p><strong>I fold the petticoat in half and find the centre of its front. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image061.jpg" class="img-responsive center-block"></p>
<p><strong>Then I find the centre of the corset front. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image063.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I match the centres of the pieces and pin the top of the petticoat to the bottom edge of the corset face netting layers up starting from the lacing bar.&nbsp; </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image065.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image067.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image069.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I join the pieces with a 1.2cm seam allowance.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image071.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image073.jpg" class="img-responsive center-block"></p>
<p><strong>The first layer of the petticoat is in place. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image075.jpg" class="img-responsive center-block"></p>
<p><strong>I attach the stiff netting layer with an overseam next to the joining seam on the first layer of the petticoat to make the entire skirt fuller.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image077.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2023_files/image079.jpg" class="img-responsive center-block"></p>
