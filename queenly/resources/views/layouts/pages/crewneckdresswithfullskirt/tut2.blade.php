<h3>Tutorial 2. Cutting and Sewing a Mock-Up Based on the Standard Pattern.</h3>
<p></p>
<p><strong>Let us start working with the pattern of our dress base.</strong> You must have already printed out the sheets from the <em>Patterns</em> appendix. Now you need to match the outlines on the sheets and glue them together. It's a matter of minutes. Please note that you don't need to trim anything but simply join the sheets 'as is'. There is extra 2cm provided specially for gluing: all you need to do is follow the guiding lines. </p>
<p><strong>Here is the pattern of the central back piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have glued together the pattern of the side back piece, too:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next, the side front piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And the central front piece:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now I have whole patterns of a yet unknown size.</strong> I'd like to point it out that every printer prints according to its own settings. Although I have tried my best to unify the printing process, there still might be some divergence between my pattern dimensions and yours. <strong>I highly recommend that you sew a mock-up after this very pattern, put it onto the dress-form, measure it and write down all values to double-check everything.</strong> </p>
<p><strong>I cut out my paper patterns.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I lay them over a sheet of mock-up fabric. Mock-up fabric</strong> is any natural or synthetic fabric that is dense enough and not prone to deformation.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I trace all patterns in the actual size. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I cut them out.</strong> Please remember that they include 1.2cm seam allowances for all princess seams and 2cm allowances for all side seams and the central seam of the back piece. You can secure the fabric with pins. Don't forget to transfer all notches. </p>
<p><strong>All pattern pieces are now ready </strong>and I'm going to join them, put the mock-up on the dress-form and measure it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>You should assemble the mock-up the same way you're going to assemble the actual dress later.</strong> Try to stick with the given seam allowance as accurately as possible. Of course you can feel free to sew the way you find more convenient - if you prefer using 1cm seam allowances, just do it. But in that case the dimensions of your patterns and your mock-up will differ from mine. However it is not crucial because the corset shape doesn�t change. You will carry out all calculations in accordance with your pattern size. </p>
<p><strong>Let�s sew up the bust princess seam.</strong></p>
<p><strong>First of all you need to notch it</strong> - it�s impossible to make a curved princess seam without doing this. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I match the main reference lines and the notches of the central and the side front pieces and secure them with pins.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>And I join the pieces by sewing up the princess seam.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I sew up the second bust princess seam following the same procedure. </strong>Please remember that the really curved line of the bust princess seam makes it particularly hard to sew.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I direct the seam allowances of the princess seams towards the centre and stitch them down from the outside: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The armscye area is quite challenging on this pattern.</strong> You will need to either trim it or prolong the line depending on your clip measurement. It is only reasonable to apply your personal adjustments to the pattern before sewing. My pattern is there to lay a foundation for creating your own garments. And you'll need to adjust it after your client's individual measurements. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I sew up the side seam with a 2cm seam allowance, direct it towards the centre of the back and stitch it down from the outside:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I attach the central back piece with a 1.2cm seam allowance and stitch down the seam allowance directing it towards the centre of the back just like before. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image040.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I sew up the other side seam with a 2cm seam allowance, direct it towards the centre of the back and stitch it down, too.</strong> Always make sure to fold the seam allowances in the same direction on both halves of the garment when you are reinforcing the seams. In other words if you direct the seam allowance of the right side seam towards the centre of the back, you should direct the seam allowance of the left side seam towards the centre of the back as well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>I attach the central back piece from the other side of the mock-up using a 2cm seam allowance, direct it towards the centre of the back and stitch it down as per usual. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%202_files/image048.jpg" class="img-responsive center-block"></p>
