<h3>Tutorial 7. Cutting the Dress Top.&nbsp; Marking the Location of Bones. </h3>
<p></p>
<p><strong>I have prepared a lay of fabric:</strong> the top layer is stretch satin that will serve as the lining. I have already duplicated it and the shiny side will be the face. The next layer is also made of duplicated stretch satin with a shiny face side. This layer will be the base. And finally the last layer is non-duplicated taffeta. I will cut the taffeta lengthwise and the duplicated stretch satin crosswise. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>I match the fold lines of all layers, trace the adjusted patterns onto the fabric, transfer all notches and apply some minor alterations I came up with during the fitting test. Then I secure all layers with pins a<strong>nd I cut the patterns out. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p>I suppose you don't have any questions regarding fabric consumption because you have the patterns of your garment at hand. In my particular case I have used 60cm of 150cm wide taffeta, 120cm of 150cm wide satin for the lining and the base, and 120cm of 90cm wide iron-on batiste for each side correspondingly. We'll touch upon fabric consumption for the skirt later. </p>
<p><strong>So, all our patterns are cut out. I remove the pins, take the lining pieces off and put the face pieces and the taffeta aside.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>It's time to mark the location of bones. </strong>&nbsp;</p>
<p><strong>I start from the front pieces.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Let's think it over properly. </strong>&nbsp;Before placing a bone, you should make sure you know why you are placing it there and not somewhere else and what load it's supposed to bear. Our garment has shoulder seams. That means the shoulders and the d�collet� zone are covered up. Shoulder-seam garments like this limit your opportunities of shaping the cups. It's impossible to follow the traditional way of sewing bones onto the lining of the cups area and then overlay it with face fabric and make the latter repeat the shape of the boned cups. In this situation you should try to sew bones onto the cups in such a way that they can perform their function to the maximum and yet are easy to join with the face fabric.&nbsp; </p>
<p><strong>I'd like to reinforce the cups area with dense iron-on fabric at the sides before marking the bones. </strong>&nbsp;</p>
<p><strong>I draw the borders of the cup on the paper pattern of the side front piece.</strong> According to the traditional technique the cup is supposed to start 5-6cm from the side seam of the corset. My client's breasts are not large so I mark 6cm plus 2cm for the seam allowance (a total of 8cm) inwards from the side edge of the piece.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Next I draw an arch-like border of the cup that will be followed by a bone.&nbsp; </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I prolong this bone upwards. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Then I cut matching side cup details out of stiff iron-on fabric and iron them onto the lining. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>Now let�s mark the location of bones on the central front piece. </strong>Let me remind you that this piece is only duplicated with batiste. </p>
<p><strong>I place a horizontal bone along the bust line (1). This bone passes onto the side front pieces, too. </strong></p>
<p><strong>I find my client's <em>actual</em> bust middle that is 16cm (points A and B), her bust circumference being 86cm.&nbsp; </strong></p>
<p><strong>Bones placed along the front princess seams (2 and 3) should be brought to the actual midpoints of the bust (A and B). </strong>We are not going to add any bones above the bust line since we only need some support below the bust to push it up and make a kind of semi-cups. The part above the bust line remains unaltered because the front involves a part that covers the body up to the very neck.&nbsp; </p>
<p>The bone along the bottom border of the cup (4) that passes onto the side front pieces is similar to a bra underwire. I lift it 2cm above the under-bust line in the middle section that�s 2cm long.&nbsp; </p>
<p><strong>And I mark another horizontal bone (9) 3cm below the bust line that passes onto the side front pieces. </strong></p>
<p><strong>Then I draw two vertical bones (5 and 6) from the horizontal bone (9) down to the stomach line at a 1.5cm distance from the centre of the piece.&nbsp;&nbsp; </strong></p>
<p><strong>I step 2cm from the princess seam at the under-bust level and 4cm from the centre at the bust-line level and place two inclined bones there as well (7 and 8).&nbsp; </strong></p>
<p>You can't pad the cups on such dresses because it will be too noticeable when not disguised by d�cor.&nbsp; </p>
<p>There is no d�cor in the bust area of our dress so I will sew the central bone (1) from the other side to prevent it from showing through the face.&nbsp; </p>
<p><strong>I have marked all bones on the central front piece. </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>And now I need to mark the location of bones on the side front piece.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Bone 4 passes from the central piece and follows the bottom border of the cup. </strong></p>
<p><strong>Bone 1 goes along the bust line and passes from the central piece as well. </strong></p>
<p><strong>Horizontal bone 9 also passes from the central piece. </strong></p>
<p><strong>I step 2cm inwards from the princess seam at the under-bust level and mark an inclined bone (10).&nbsp; </strong></p>
<p><strong>Then I step 4cm inwards from the side open edge at the waistline level and mark a vertical bone (12). </strong>&nbsp;There should be at least 2cm between the bone and the side seam - otherwise you won't be able to apply any changes if necessary. </p>
<p><strong>I step 4cm from bone 12 and mark another vertical bone (11).&nbsp; </strong></p>
<p><strong>And then I mark mirrored bones on the other side front piece.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The next step is to mark the location of bones at the back. </strong></p>
<p><strong>The side back pieces:</strong></p>
<p><strong>Apart from the bones that follow the princess seams on the side pieces of the back I will also place symmetric vertical bones perpendicular to the waistline going from the bottom edge up to the bust line (16 and 17). Each bone divides the piece in about half. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>There will be bones in the lacing area on the central back pieces and there's no need in adding more.&nbsp; </strong></p>
<p><strong>We have finished marking the bones and now it's time to proceed to the next stage and begin with the sewing process. </strong></p>

