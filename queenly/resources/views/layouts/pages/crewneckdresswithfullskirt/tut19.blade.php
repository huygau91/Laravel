<h3>Tutorial 19. Calculating Fabric Consumption for the Full Skirt. </h3>
<h3>Review of Materials Used for the Petticoat. </h3>
<p></p>
<p><strong>Let's calculate how much fabric we need for the skirts.&nbsp; </strong></p>
<p><strong>There are two skirt layers apart from the petticoat, both are semi-circle and made of taffeta. </strong></p>
<p><strong>Their waist circumference is 68cm each. </strong></p>
<p><strong>The first skirt is the main one: </strong>it's a 56cm long semi-circle skirt gathered at the front and back that features a shaped hem at the front. </p>
<p><strong>The second skirt serves as a lining:</strong> it is a 55cm long ungathered semi-circle skirt. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Both skirts are calculated based on the same formula: </strong></p>
<p><strong>πR = waist circumference.</strong></p>
<p><strong>Let's calculate the radii of the skirts. </strong>&nbsp;</p>
<p><strong>For the second skirt: </strong></p>
<p><strong>R = </strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image004.png" class="img-responsive center-block"></p>
<p><strong>&nbsp;= 22cm. </strong></p>
<p><strong>For the first skirt:</strong></p>
<p>Let's gather about two times the initial length - the waist circumference will then be about 140cm. </p>
<p><strong>R = </strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image006.png" class="img-responsive center-block"></p>
<p><strong>&nbsp;= 44cm.</strong></p>
<p><strong>So we need to cut two semi-circle skirts or one full sweep.</strong> It'll make the cutting process a lot easier.&nbsp; </p>
<p><strong>The fabric lay for a full sweep skirt is a rectangle. </strong>One side of this rectangle (b) equals the width of our fabric (150cm). We also know its hypotenuse (a), the length of which equals the sum of the radii and lengths of our skirts:&nbsp; </p>
<p><strong>a = 22 + 55 + 44 + 56 = 177cm. </strong></p>
<p>Thus the other side of the rectangle (x) makes a half of fabric consumption for both skirts.&nbsp; </p>
<p><strong>I find a half of fabric consumption (x) following the right triangle formula: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image008.png" class="img-responsive center-block"></p>
<p><strong>=</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image010.png" class="img-responsive center-block"></p>
<p><strong>Therefore we only need 2m of fabric to cut two semi-circle skirts. </strong></p>
<p><strong>And now I'd like to show you what fabric consumption would be like if we cut each semi-circle skirt separately instead of using my method. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p>You need a piece of fabric that's about 1.5m long <strong>to cut the lining skirt</strong>. After cutting it out you'll be left with a 150x70cm piece. &nbsp;</p>
<p>&nbsp;And you need a piece of fabric that's about 2m long <strong>for the main skirt</strong>. In this case you'll be left with a 200x50cm piece after cutting it out.</p>
<p><strong>Thus if you cut each skirt separately, you will need 3.5m of fabric. </strong></p>
<p><strong>Let me explain my skirt cutting method one more time, on a paper mock-up. </strong></p>
<p><strong>Imagine that this piece of paper folded in half is a piece of fabric with a fold line.</strong> The fold line is on the left and the selvages are at the top and down the bottom. Let's transfer our skirt patterns onto it.&nbsp; The fabric is 150cm wide. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out both skirts. </strong></p>
<p><strong>Here is the lining skirt.</strong> It's a semi-circle with a fold line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>We'll sew up the seam at the back and insert a zipper. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image018.jpg" class="img-responsive center-block"></strong></p>
<p><strong>This is the main skirt. </strong>It is made of two quarter-circles: the front and the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>We will sew up the side seams of the main skirt. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then we'll cut up its back, insert a zipper and sew up the central seam. There will also be a small pick-up at the front of the skirt.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Our construction made of two skirts will look like this:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now it's time to cut the skirts out of fabric. </strong></p>
<p><strong>I take a piece of fabric that's 2m long and 1.5m wide</strong> and fold it in half: the fold line is on the left of me and the selvages are at the top and down the bottom.&nbsp; </p>
<p><strong>I start from the main skirt. </strong>&nbsp;</p>
<p><strong>I take the top left corner of the lay and bring it to the side opposite to the fold line so as to make another fold line passing through the middle of the top right corner. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Then I fold it one more time </strong>into a kind of a cone and secure the corner with a pin.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image030.jpg" class="img-responsive center-block"></p>
<p>I mark the radius of the main skirt from the corner (44cm). You need to cut through this line to make an opening for the waist that will then be gathered into half the initial length.&nbsp; </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>Mark the length of the skirt (56cm) plus 1cm for the seam allowance from the radius: a total of 57cm.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the skirt leaving 1cm for the seam allowance along the top edge. </strong>I have cut out two quarter-circles. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to cut the lining skirt out of the remaining fabric. </strong></p>
<p><strong>I take the bottom right corner and bring it to the fold line so as to make another fold line passing through the middle of the bottom left corner.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>And I fold it one more time </strong>and secure the corner with a pin.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I mark the radius of the main skirt from the corner (44cm) and then mark the length of the skirt (55cm) plus 1cm for the seam allowance from it, which makes a total of 56cm.&nbsp; </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I cut out the skirt leaving 1cm for the seam allowance along the top edge.</strong> Please note how little fabric is left after the cutting process.&nbsp; </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p><strong>My cutting method is really fast and efficient!</strong> </p>
<p><strong>However I know it from my own experience that you might have troubles folding the fabric correctly before cutting out the skirts. That's why I'm going to demonstrate the entire skirt cutting process once more on a paper mock-up. </strong></p>
<p><strong>Imagine that this piece of paper folded in half is the fabric that you've folded in half. </strong>The fold line is on the left and the selvages are at the top and down the bottom.&nbsp; The main skirt is cut against corner A and the lining skirt - against corner B. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I take the top left corner of the fabric and bring it down to make another new fold line passing through the middle of corner A. Then I fold it once more to make a kind of a flat cone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Then I mark the radius of the main skirt (44cm) from point A and mark the length of the skirt (57cm) from the radius. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I cut it out.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Here is our main skirt: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I unfold the remaining fabric to cut the lining skirt. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I fold the fabric the same way as before making a new fold line through the middle of corner B. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>Then I mark the radius of the lining skirt (22cm) from point B and mark the length of the skirt (56cm) from the radius.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image065.jpg" class="img-responsive center-block"></p>
<p><strong>I cut it out. Here is the lining skirt:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image067.jpg" class="img-responsive center-block"></strong></p>
<p><strong>We have cut two skirts using just 2m of fabric - very efficient consumption!</strong></p>
<p><strong>The inner skirt sewn in-between the layers of taffeta will be made of special nylon netting for skirts. </strong>This netting is 3m wide and it only stretches crosswise. <strong>I'm going to cut three rectangles: each 3m long and 55cm wide sticking by the length of the lining skirt.&nbsp; </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image069.jpg" class="img-responsive center-block"></p>
<p><strong>I will also cut several straps of 1.5m wide stiff netting for petticoats.</strong> The width of these straps equals the length of the skirt (55cm) minus the length of the corset down from the waistline (12cm), i.e. a total of about 40cm. I will gather these straps slightly and sew them onto the bottom edge of the corset after attaching the skirts. It will make the skirt fuller. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%202/Tutorial%2019_files/image071.jpg" class="img-responsive center-block"></p>

