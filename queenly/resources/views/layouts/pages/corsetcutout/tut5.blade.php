<h2>Tutorial 5. Re-drawing the Patterns with Alterations Accounted for. </h2>
<p></p>
<p>Since we’re going to use thin white bobbinet, it won’t be easy to demonstrate all alterations right on fabric. That’s why I will draw it all on white paper the same way as done on fabric. I’m going to show you everything in detail and then repeat it on fabric and cut out the patterns. Prepare a triangle ruler and a pen/piece of chalk/disappearing ink pen or whatever you prefer to draw with, and of course your set of patterns. You should always keep the table with calculations before your eyes, too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let us begin.</strong></p>
<p>Imagine that this piece of paper is a sheet of fabric that’s been folded in half. The fold line is on the left. </p>
<p><strong>I take the central piece of the corset front (#1) and trace it. </strong>This pattern remains unchanged since there are no alterations in the central part (the bust middle measurement coincides with the client’s). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Make sure to transfer all notches. </strong>There is a notch at the bust-line level (1), a notch on the princess seam of the cup (2), and a marking (3) for matching it with the opaque piece - you should mark the latter on fabric without notching it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Next I take the intermediate front piece (#2).</strong> Try to arrange the patterns so as to make the waistline coincide with the no-stretch thread. If you have enough fabric, you can lay this piece at an angle and match its waistline notch with the lengthwise thread because my fabric stretches crosswise and not lengthwise. On the other hand it will be a very slant cut. You need to steer a middle course here. I have arranged the piece the way shown in the picture below: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I trace the pattern.</strong> There are no adjustments anywhere so I just transfer it and trace it as is. The only thing we have noticed on the mock-up is that we might straighten the top line of the piece. We are going to shorten the length of the cups to remove 1cm from the clip measurement. So we are most likely going to lower the whole top line of the piece by 1cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Let’s take the next piece - the side piece of the front (#3).</strong> You can arrange it in such a way that its waistline is perfectly horizontal. <strong>First I outline the whole piece: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>There are notches at the bottom and on the right side of the piece. </strong>Notches 1 and 2 demonstrate the start and end of a decorative bone that’ll be sewn right on top of the piece. This bone will lie at the same angle as the princess seams. Its function is purely decorative but it’s important to transfer its location and maybe even draw it to avoid mistakes. There are notches at the waistline (3 and 4), too. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I alter the top line of piece #3 in accordance with piece #2. </strong>I mark 1cm down the princess seam and join that point with the top point of the side seam for now. </p>
<p><strong>We have calculated the alterations of the front side seam line. </strong>There are no changes at the bust-line level, however we need to add 0.5cm at the under-bust level, at the waistline and at the bottom оf the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I take the pattern, match the waistline notches, and draw a new side line. </strong>Don’t forget to re-draw the bottom line in accordance with the pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I use the pattern to re-draw the section between the bust-line and the under-bust line. Remember to re-draw the top line in accordance with the pattern. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>As you can see, the side seam has changed its location at the top and jumped a step up: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I connect the new top point of the side seam with the top point of the opposite princess seam: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image023.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Here is our new side seam. </strong>It has the same initial length. And that means it’s certainly going to match the side seam of the back. Remember to extend the marking for the decorative bone. Its location stays the same just slightly prolonged upwards. I make a new notch there. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image025.jpg" class="img-responsive center-block"></p>
<p><strong>Now let’s move on to the side piece of the back. </strong>I locate its waistline horizontally. Remember that I need to expand the side of the back by 0.5cm. I step a little from the ready front side piece and outline the original pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>What alterations should be applied here? </strong></p>
<p><strong>First of all I need to shift the whole side seam outwards by 0.5cm. </strong>I mark 0.5cm at the top, down the bottom and at the waistline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image029.jpg" class="img-responsive center-block"></p>
<p><strong>I put the pattern down, match the waistline notches, and draw a new side seam line using the pattern as a stencil. </strong>Remember to copy the configuration of the top and bottom edges. I transfer the bottom line onto its new location. The corset is going to have a beautiful curved bottom line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image031.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image033.jpg" class="img-responsive center-block"></p>
<p><strong>Next you should remember to adjust the princess seam. </strong>I remove 1cm at the waistline level and 0.75cm at the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image035.jpg" class="img-responsive center-block"></p>
<p><strong>First I draw a new line going from the waistline down to the bottom. </strong>I bear in mind that the bottom line has a new location since the back is 2mm shorter now. Therefore the bottom line of the back has changed both due to the side seam alterations and to the alterations of the princess seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image037.jpg" class="img-responsive center-block"></p>
<p><strong>I extend the new princess seam from the waistline up to the under-bust level. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image039.jpg" class="img-responsive center-block"></p>
<p><strong>The side back piece is finished: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image041.jpg" class="img-responsive center-block"></p>
<p><strong>It’s time to work on the last piece - the central piece of the back. </strong>I trace the initial pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image043.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Now I need to determine the location of our new dart or princess seam. </strong></p>
<p><strong>First of all let’s apply alterations to the princess seam at the back: </strong>I mark 1cm inwards at the waistline level and 0.75cm at the bottom. </p>
<p><strong>Then I mark a 1.2cm seam allowance from these new points. </strong></p>
<p><strong>There is a lacing bar at the edge of each half of the back </strong>and it’s going to cover its outer vertical bones. Looking ahead I can tell you that there will be a wide (1.2cm) Rigilene bone sewn at the very edge. Next there will be a narrow (0.8cm) Rigilene bone and then another wide bone. <strong>So the width of a ready lacing bar will be 3.2cm. </strong>I mark it roughly. </p>
<p><strong>Next I divide in half the distance between the edge of the lacing bar (point B) and the seam allowance (point A) and mark the middle (point C). </strong>I draw a line going through point C that’s perpendicular to the waistline and parallel to the lacing bar. <strong>This is our new princess seam. </strong></p>
<p>You have to agree dividing this central back piece into two narrow pieces would be a bad idea. Working with thin bobbinet fabric would make it particularly difficult. Making a dart instead is a lot more convenient. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image045.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I re-draw the princess seam connecting the waistline notch with the bottom point and re-draw the bottom line. </strong>This is particularly important when the bottom line of your corset has a fancy shape and requires perfect matching of the seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image047.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the section of the princess seam that goes from the waistline to the under-bust level. </strong>By the way you can always refer to the mock-up to determine the location of the under-bust line. When in doubts, just take a measuring tape, measure it on the mock-up and transfer onto the pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image049.jpg" class="img-responsive center-block"></p>
<p><strong>Next I mark 1cm left and right of point C at the waistline and 0.75cm at the bottom of the corset (see below). </strong>The top point of the dart (point D) coincides with the intersection of the princess seam and the under-bust line. <strong>It is shown in the picture below. </strong>Of course, you could make a concave dart but trust me: it makes no difference with stretch fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image051.jpg" class="img-responsive center-block"></p>
<p><strong>This is all that concerns cutting the bobbinet. Now let’s look at the cups. </strong></p>
<p><strong>I’m going to trace the central cup piece now. </strong>Imagine that the right edge of the piece of paper is the fold line of our duplicated blue fabric. I place the central cup piece at the fold line and trace it. I transfer all notches, princess seams and open edges carefully. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image053.jpg" class="img-responsive center-block"></p>
<p>There are no alterations in the bottom part of the central cup piece. All adjustments concern the area of the princess seam that joins the central cup piece with the side one. <strong>I trace the side cup piece so I can apply and compare alterations to both princess seams later: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image055.jpg" class="img-responsive center-block"></p>
<p><strong>All these alterations are made at the levels of the décolleté cut-out and the scye. </strong>Looking at the mock-up on the dress-form we have decided to <strong>remove 2cm at the underarm level and shorten the side cup piece by 1cm </strong>with a 1.2cm seam allowance in mind. <strong>&nbsp;The corner needs to be shifted down by 1.5cm. </strong>It won’t be a big deal <strong>if you shift it by 2cm </strong>because this measurement is rather <strong>relevant</strong>. You took it looking at a person but now that you’re going to draw a pretty décolleté or scye line you can choose what looks better - a smoother or a steeper line. <strong>I prefer a smoother line so I’m going to shift the corner down by 2cm. </strong>I draw a new (red dashed) line parallel to the original one. <strong>I am removing 2cm as you can see in the picture: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image057.jpg" class="img-responsive center-block"></p>
<p>I drew a beautiful décolleté line on the mock-up. And now I can check how much the princess seam changed back then. <strong>It was in fact the same 2cm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image059.jpg" class="img-responsive center-block"></p>
<p>Since I’ve shortened the princess seam of the side cup piece by 2cm, <strong>I need to shorten the princess seam of the central cup piece just as much. And I need to remove 1.5cm at the level of the décolleté cut-out. </strong>I draw a new beautiful cut-out using the pattern as a stencil. I have decided to make it deeper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image061.jpg" class="img-responsive center-block"></p>
<p><strong>The patterns with applied alterations are patterns customized for your client! </strong>You can cut them out of fabric without worrying about any mismatches. You can fully trust these new patterns. </p>
<p><strong>I have folded the bobbinet in half two times since each piece has a double layer. </strong>I have laid all cardboard patterns with their waistline parallel to the selvage, traced them and cut them out. In this particular case the patterns stretch vertically and not horizontally. My bobbinet fabric is 150cm wide and 60cm was enough for a four-layer layout. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%205_files/image063.jpg" class="img-responsive center-block"></p>

