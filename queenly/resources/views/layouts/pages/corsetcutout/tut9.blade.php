<h2>Tutorial 9. Encasing the Bones and Sewing Them onto Princess Seams. </h2>
<p></p>
<p><strong>I�ve prepared a 3cm wide strap of our main fabric for encasing the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And now I�m going to wrap my prepared Rigilene bone into this fabric strap. </strong></p>
<p><strong>I�ve already experienced problems when encasing bones in this particular fabric </strong>so I�ve decided to use an edge-stitch presser foot instead of the regular one. You can use this special-purpose presser foot if the standard one jumps off the bone. It makes a nice even stitch and prevents the fabric from slipping off the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I attach the fabric-encased bone at the edge of the lacing bar with two stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>And I attach a symmetric bone at the edge of the other lacing bar. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The lacing area now looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And now we should sew a bone over the princess seam of the dart. </strong></p>
<p><strong>I measure the length of the bone referring to the pattern and make a marking at its end. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I lay the bone over the seam allowance of the dart and make a couple centimeters of stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I place the marked end of the bone at the edge of the corset and secure it with a pin. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Then I distribute the fabric underneath the bone to spread it evenly and avoid distortion of the length of the princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
