<h2>Tutorial 12. Assembling the Lining of the Central Piece. </h2>
<p></p>
<p><strong>Let�s make some preparations while the pieces of our cup lining are still lying flat on the table. We need to make a stay-stitch on them but in a slightly different way than usual. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The side cup pieces </strong>will have a stay-stitch with a 1.2cm seam allowance going along all their open edges (not including the princess seams). </p>
<p>There will be a regular stay-stitch with a 1.2cm seam allowance <strong>on the central piece </strong>going along the bottom edge and up the sides to the curved areas (see above). Another stay-stitch will go along the d�collet� down to the bust-line notch. You will need to use a 0.5cm seam allowance in the area from the bust-line notch to the notch that marks the beginning of the bobbinet insert. This stay-stitch with a 0.5cm seam allowance will then frame the diamond cut-out up to the opposite notch of the bobbinet insert. You will need to switch back to the 1.2cm seam allowance when stitching in the opposite area between the notches and go up the d�collet� line symmetrically. <strong>Why does it matter? We will have to do the same thing from the face side!</strong> The shape of the cut-out and the transition to the d�collet� should be identical on the lining and the face. &nbsp;And this means we�re going to need a reference point. Let it be this small gap between two notches. </p>
<p><strong>Now we can get to work. Let�s make our stay-stitches. </strong></p>
<p><strong>The side cup piece: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The central piece:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>You should do this with particular care and accuracy. </strong>You are basically �drawing� the edge of your future garment by stitching. </p>
<p>
