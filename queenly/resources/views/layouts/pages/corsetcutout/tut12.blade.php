<p><strong>Next I sew the princess seam of the cup. </strong></p>
<p><strong>I stop right at the stay-stitch and make a bar tack. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I make a notch at the point where the stay-stitch ends (point A). </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now it�s easy to divide our cup into the side part and the central part. </strong>You can finish their bottom edges separately. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>You should find the location of point A on the second cup and pin its pieces together right there before sewing the princess seam. </strong>This helps achieve perfect symmetry of the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Then you can join the princess seam of the second cup. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a notch at point A. Now the second cup is also divided into the side part and central part. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong>All preparations have been made and we can start quilting our dome-shaped cups. </strong></p>
