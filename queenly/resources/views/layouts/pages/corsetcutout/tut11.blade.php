<h2>Tutorial 11. Finishing the Top Edge of the Corset. </h2>
<p></p>
<p><strong>The top edge of the corset should be finished with a narrow Rigilene bone. </strong>I�m going to gather it in advance because the edge has an arched shape. The radius is rather large so you shouldn�t gather too tightly. It�s enough to make it slightly arched. </p>
<p><strong>I start stitching the bone on. </strong>I leave a small tip covered in masking tape - it�ll pass onto the inside of the cup. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>The top edge is finished the same way as on our previous corsets. </strong>The only challenge is the fact you can�t afford to either stretch or gather the bobbinet underneath the bone. This fabric is thin and stretchy so be careful when arranging the bone over it. You can use the mock-up as a reference. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I trim any bits that show from behind the bone at the top edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take my duct tape, tape the ends of all vertical bones (as was done before) and make a strengthening stitch to secure it. </strong>I trim any duct tape that might make the pencil-edge look messy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Next I give the top edge a pencil-edge finish using a fabric strap. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I demonstrate the easiest method of finishing the end of the pencil-edge on this corset. </strong>You leave a small end, continue sewing and then fold it over to the underside. Then you just trim it, tape it over and secure it with a stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I gather another narrow Rigilene bone of the same length as the first one. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch it along the top edge of the other half of the corset leaving a tip of the same length as on the first bone. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></strong></p>
