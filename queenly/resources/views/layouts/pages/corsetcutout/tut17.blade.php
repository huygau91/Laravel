<p><strong>Afterwards I cover the first cup with padding polyester </strong>following the traditional procedure used for all types of corsets with quilted cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the wavy seam allowance: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>The layer of padding polyester doesn�t have to be thick. </strong>It is there to prevent the bones from protruding through the face fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I cover the second cup, too.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Next you should secure your padding polyester by stitching. </strong>Prepare two ribbons to make hanging loops for the garment. My ribbons are 25cm long each. <strong>I secure the padding polyester by machine-stitching through the inner side of the perimeter bones. </strong>Remember to put the ribbons in place and stop at exactly the same spot you started at. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>And then I secure the padding polyester on the second cup in the same fashion - following the inner stitch that attaches the perimeter bones. </strong>If you follow the outer stitch at the very edge of the cup, there�ll be bits of padding polyester left after you trim it. It�ll lie too close to the edge of the cup and inhibit further work. That�s why I always stitch it on through the inner side of the bones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I secure the ribbons inside the cup to prevent them from poking out when the garment is worn. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and trim all extra padding polyester along the perimeter. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2017_files/image052.jpg" class="img-responsive center-block"></p>

