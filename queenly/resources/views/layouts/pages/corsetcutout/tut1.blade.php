						<h2>Tutorial 1. Getting Acquainted with the Model.</h2>
						<p></p>
						<p>
							<strong>The corset we are going to sew truly lives up to its
								name. </strong>It�s going to be very see-through, <strong>made
								of bobbinet fabric. </strong>
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%201_files/image002.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							It�s not quite the same kind of tulle used for sewing skirts. To
							put it more precisely, it is totally different. <strong>This type
								of bobbinet fabric is used for bodysuits worn by figure skaters,
								ballet dancers, and rhythmic gymnasts. </strong>It is used for
							transparent inserts - sleeves, back pieces or individual elements
							on the bodysuit. This fabric stretches in one direction and is
							basically non-stretch in the opposite. <strong>We are going to
								use a double layer of bobbinet fabric to ensure better
								durability. </strong>I have heard that one layer is enough since
							this fabric is tear-proof, yet I prefer to be absolutely
							confident. I always use a double layer of it and so far I�ve
							never had any troubles.
						</p>
						<p>
							<strong>This fabric is see-through. </strong>You can see my arm
							clearly in the picture:
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%201_files/image004.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>The body of the corset will be made entirely of this
								fabric. </strong>Of course we will cut it in such a way that it
							will stretch vertically and not horizontally, i.e. not across the
							waistline. <strong>The challenging part </strong>is the fact that
							the fabric does stretch quite a lot vertically and it is thin,
							which means it might gather during the sewing process. So we
							shall <strong>keep checking the length of all princess seams </strong>and
							comparing it to the mock-up that we�ll prepare in advance before
							sewing the garment.
						</p>
						<p>
							<strong>I still haven�t said a word about the model I�ve designed
								for you. </strong>It is rather sophisticated which is why you
							find it at the end of the book. I hope that by now you have
							learnt enough, mastered your skills in practice, and are
							well-prepared for working on this corset model.
						</p>
						<p>
							<img
								src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%201_files/image005.jpg"
								class="img-responsive center-block">
						</p>
						<p>
							<strong>I have filled the opaque parts of the corset with blue. </strong>As
							you can see in the picture, the central part is going to be
							opaque and have a bobbinet insert in the middle. The lacing bars
							are also opaque.
						</p>
						<p>
							<strong>The pattern is designed in such a way that bones will be
								located diagonally at the front. </strong>
						</p>
						<p>
							<strong>The corset has a standard back. </strong>It is comprised
							of two pieces: the centre and the side. I will place a vertical
							bone in the middle of the central piece to simulate a princess
							seam. This means there will be two princess seams at the back. I
							believe it�s going to look more harmonious since there are
							several bones at the front and the back should match it.
						</p>
						<p>
							<strong>In the picture below I�m demonstrating the fabric used
								for the opaque corset pieces. </strong>It is a dense elastic
							material that is also strong enough. I�m going to use its matte
							side for the face of the opaque pieces and the sheen side for
							their lining.
						</p>
