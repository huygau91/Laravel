<h2>Tutorial 10. Sewing Bones onto Princess Seams (Continued).</h2>
<p></p>
<p><strong>I encase another wide Rigilene bone into a newly prepared fabric strap. </strong></p>
<p><strong>I take the pattern, place the bone over the princess seam and mark the required length two times. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I place the bone on the back piece, overlay the seam allowance and pin the other edge of the piece to the end point marked on the bone - the same way I did before. </strong>I spread the fabric underneath the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>These bones help spread the fabric properly. </strong>The markings of their length give you full confidence in knowing you�re doing everything right preserving the size and the pattern configuration. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I take the other half of the back.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Please note: </strong>I�m not cutting any bones in advance. I mark the required length on the bone and cut it after stitching it on along one edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>The underside of the back looks like this: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And here is another tip:</strong> try to put the bone over the princess seam in such a way that it overlays the red dashed line by literally 1mm, not more. This way you will basically get right into the princess seam when you start stitching through the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Let us check if plastic bones can fit into these tunnels. </strong>Yes, it�s perfect. We can continue encasing the bones with the same degree of tension. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s continue overlaying all vertical princess seams of the front with bones. </strong></p>
<p><strong>I�ve sewn them all on in accordance with the pattern: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image022.jpg" class="img-responsive center-block"></p>
<p>Everything looks really smooth and even, without a single tuck or wrinkle. </p>
<p><strong>Please pay attention to the last short diagonal bone that ends at the side seam. </strong>I have brought it right to the edge, i.e. its end was trimmed at the very edge of the piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to join the side seams of the corset and overlay them with bones. </strong>The bones are prepared in advance. Don�t forget to make sure that the seam allowances stay on the face side when you�re joining the front with the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>It�s important to measure the length of the side seam and mark it on the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I overlay the seam allowance of the side seam with the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I place the marking at the edge of the garment and secure it with a pin. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully distribute the fabric underneath the bone to make sure the seam allowance and the joining seam stay at the edge of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>The symmetry and beauty of the entire garment depends on how well you sew this bone on or overlay the seam allowance with it. </strong>If you shift the bone left or right by just 1-2mm, it�ll be noticeable that the intervals between the bones are different on different halves. That won�t look nice! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2010_files/image042.jpg" class="img-responsive center-block"></p>

