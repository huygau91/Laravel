<h2>Tutorial 3. Measuring the Mock-Up to Get the Dimensions of the Pattern. </h2>
<p></p>
<p><strong>Our corset mock-up is ready. </strong>I have put it on the dress-form, filled the cups with some padding polyester and added foam shoulder pads to shape the �hips�. <strong>The corset sits on the dress-form the way it�s expected to sit on a real person. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%203_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%203_files/image004.jpg" class="img-responsive center-block"></p>
<p>Now you can take all those measurements off this corset that you normally take off a person so as to be able to compare them with your client�s measurements. </p>
<p>Besides, each time you sew a mock-up after a new pattern you can see the way all notches and pieces match and check if you need to apply any minor alterations: round an edge off, gather a little, give more fullness, etc. In other words you are getting acquainted with the pattern. You should pay attention to the silhouette of the model. You can always alter and adjust anything you�re not very happy with. You should also consider the direction of princess seams and seam allowances. In my case they are directed towards the centre but you might want to do it differently. </p>
<p><strong>You literally need some half an hour to sew a mock-up like this, yet it will help you study the pattern attentively.</strong> &nbsp;You will see the way the pieces should be sewn together, what shape they acquire after getting joined, what the bottom line looks like. You will understand the pattern much better by looking at a number of its parameters. </p>
<p>A mock-up is also a true must-have when you plan to use tricky materials like our thin bobbinet fabric that is meant to be joined with the hard fabric of the central piece and stiff bones! <strong>We are going to measure the length of each princess seam on the mock-up and refer to it on the actual garment. </strong>We are also going to measure the length of bones using the same princess seams and then sew them onto our thin bobbinet. <strong>This is the only way to recreate our mock-up accurately.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%203_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I�d like to add a couple more words on the importance of sewing mock-ups. </strong>As you saw your patterns, you probably wondered why there is a fold line on the central piece when the model is supposed to have such a deep cut-out at the front. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%203_files/image008.jpg" class="img-responsive center-block"></p>
<p>After sewing a mock-up you will see that it�s much easier to use one central piece made of bobbinet with a fold line and then make the cut-out rather than join two separate central pieces - one made of hard fabric and the other of bobbinet. You won�t have to match the pieces: it�ll be fast and very easy! </p>
<p><strong>So, let�s start taking the key measurements. </strong>I mark all measurements directly on the patterns. I prefer having all measurements written down on each pattern designed for a certain model. I�m going to mark them on the central piece and on the cups. </p>
<p><strong>Bust middle measurement - 19cm: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%203_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Clip measurement.</strong> &nbsp;Start at the middle of the bust and don�t account for the seam allowance.</p>
