<h2>Tutorial 19. Joining the Face and the Lining of the Central Part. </h2>
<p></p>
<p><strong>And now I need to put the face over the lining that we�ve attached. </strong></p>
<p><strong>I match the notches on the cups.</strong> We have left a certain section unfolded and unstitched (see below). I secure it with pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Then I pin the face and the lining together close to the corner. </strong>I stick the pin in a gliding motion to catch the lining and avoid the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>You don�t have to match the princess seams of the cup perfectly. </strong>It�s even better if they are somewhat misaligned because the fabric is thick. The seam allowance at the top is going to double this thickness and that might be visible. So let these princess seams shift against each other a little bit. </p>
<p><strong>We have a plenty of face fabric since we�ve eased in a lot in the d�collet� area. </strong>This fabric should be distributed along the whole length. First I add a pin in the middle of the arch that was eased in the most. And then I�m going to divide each section in half, pin it in the middle, divide it in half again, etc. That way it�ll be even. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can secure it in the underarm area the same way. </strong>I re-distribute that light fullness. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I move to the second cup after pinning the top edge of the first one. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And now I need to join it all by machine-stitching. I am using a one-sided presser foot with the ski-shaped part on the right </strong>to make stitching along the bone easier. I�m trying to get right into the stay-stitch. I move the presser foot along the bone and the needle gets right in. This is only going to happen if you stepped 1mm from the stay-stitch when laying the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Do not turn the garment around on the needle, stitch through every corner individually! </strong>Stop at the intersection of stay-stitches and make a bar tack. You are basically drawing the outline of the cups by stitching. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I remove the pins and check the quality of the stitch! </strong>It�s easy to break the thread with pins underneath the piece. It might result in a small hole when you turn it out. You should always check the quality of the stitch and make an additional one when needed! </p>
<p><strong>I sew the second cup following the same steps. </strong>Don�t forget to check the quality of the stitch again. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Then I trim the seam allowances. </strong>I leave some allowance at the edges of the cups instead of removing all of it. You can notch out the corners. Notch the corners of the seam allowances on princess seams or else the excess of fabric will be noticeable when you turn out the cups. Don�t remove the bar tacks when trimming the seam allowances. They should stay because they�re made at the intersection of stay-stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I turn it out and make sure if it�s not too thick near the princess seams. </strong>If you notice any bumps there, you�d better turn it out again and trim those seam allowances. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%20%2019_files/image024.jpg" class="img-responsive center-block"></p>

