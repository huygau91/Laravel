<h2>Tutorial 4. Adjusting the Pattern after Individual Measurements.</h2>
<p></p>
<p><strong>So we have reached the stage when we can calculate all alterations that should be applied to the original pattern. </strong></p>
<p><strong>I have prepared my traditional table with client�s measurements (column 2) and pattern measurements taken off the mock-up (column 3). </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p>There is some divergence between the pattern and my client�s measurements so I need to think it over and make some adjustments. </p>
<p>The first measurements you should pay close attention to are <strong>bust front and under-bust front </strong>that allow you to determine the location of the side seam, and then the <strong>bust middle measurement </strong>that shows if you need to alter the central front piece. </p>
<p><strong>All calculations are recorded in the fourth column of the table. </strong></p>
<p><strong>Both bust middle measurements coincide (19cm) which means there are no alterations on the central front piece. </strong></p>
<p><strong>Now let�s see what happens with the location of the side seam. </strong></p>
<p><strong>Bust front measurement:&nbsp; </strong>my client�s measurement is 47cm but we have 48 on the pattern - therefore we need to narrow the pattern by 0.5cm to get the right size. </p>
<p><strong>Under-bust measurement: </strong>it�s all the other way round here - the client�s 39cm correspond to 37cm on the pattern so we need to expand the pattern by 1cm. </p>
<p>And now with our calculations in mind we need to choose a value that will be either added or removed with the help of the side seam to preserve the beautiful shape of the side curve. </p>
<p>First of all let�s look at all other measurements and try to estimate what adjustments in general we really have to make. The client�s bust circumference almost coincides with the pattern, only some 0.5cm is lacking. Her waist and stomach circumferences are considerably smaller than on the pattern. The clip and other measurements are not as relevant. </p>
<p>All this tells me that I need to add a certain value to all measurements of the pattern - at least at the bust-line and the under-bust line level. It should be expanded, not narrowed. <strong>I have decided to expand the whole side by 0.5cm. </strong>This means I will slightly shift the side seam back at the bust-line level and at the same time expand the pattern across its entire length the way the measurements call for. </p>
<p><strong>Bust middle measurement </strong>requires no alterations - I write a zero in the table. </p>
<p><strong>Now the clip measurement:</strong> </p>
<p><strong>- Clip towards the scye</strong> - I need to remove 2cm; </p>
<p><strong>- Clip towards the corner</strong> - I need to remove 1.5cm; </p>
<p><strong>- Clip towards the d�collet�</strong> - according to the calculations I need to remove 2cm but I am going to remove just 1.5cm. I noticed that you could make the d�collet� indent either 5 or 6cm wide when I was taking the measurement. If the original measurement is 7cm and I remove 1.5cm, the client�s measurement will equal 5.5cm, i.e. the average value between 5 and 6. </p>
<p>The side length and the back length measurements coincide so I write zeros in the table. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can look at the side seam of the back and see what happens there. All calculations are filled into column 5. </strong></p>
<p><strong>Bust front and under-bust front measurements </strong>are not relevant in these calculations so I write dashes in the table. </p>
<p><strong>Bust circumference. </strong>Since we have already added 1cm at the side of the front, we have zero alterations at the back. </p>
<p><strong>Under-bust circumference. </strong>It was 68cm on the pattern but we have added another centimeter and got 69cm. It still lacks 1cm to match the client�s measurement (70cm). We need to divide this value by half and expand the pattern by 0.5cm. </p>
<p><strong>Waist circumference. </strong>It�s a whole different situation here. The pattern was already too large in the waistline (70cm) and we have added another centimeter. The client�s measurement is 64cm. </p>
<p>Therefore: </p>
<p>71 - 64 = 7cm.</p>
<p>I divide 7cm by two and get 3.5cm that should be removed from each side at the waistline level. </p>
<p><strong>Stomach circumference. </strong>It is 88cm on the pattern and we have added another centimeter and got 89cm while the client�s measurement is 84cm. </p>
<p>Therefore: </p>
<p>89&nbsp; -&nbsp; 84 = 5cm.</p>
<p>I divide 5cm by two and get 2.5cm that should be removed from each side at the stomach level. </p>
<p>The remaining measurements require no alterations. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Let us examine the back side piece before determining the exact location of its side seam. </strong>As you can see, there is great divergence of values in the area between the stomach level and the under-bust level: from -3.5cm to +0.5cm. I will obviously have to remove a little at the waistline and stomach levels using the princess seams at the back, i.e. I will have to adjust the side seam of the back. </p>
<p><strong>Let�s look at the front side piece before adjusting the side seam of the back. </strong>I don�t have a separate column in the table because only one measurement needs to be adjusted - the bust circumference. <strong>Here:</strong> we have removed 0.5cm from the bust front measurement and added 0.5cm to the bust circumference for the side seam of the front. I think it makes sense to add nothing at all at the bust level, i.e. write a zero there. </p>
<p></p>
<p>It is no big deal at all: if you take the side piece of the front and look at the shape of its side seam line, you will see that it�s perfectly fine if we leave it �as is� at the bust level, add 0.5cm under the bust and prolong the line downwards. The side seam will be straighter that�s all: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s determine the side seam of the back. All relevant calculations are recorded in column 6. </strong></p>
<p><strong>So we�ve decided to apply zero alterations to the bust circumference for the side seam of the front. </strong>Then let�s add 0.5cm to the bust circumference for the adjusted side seam of the back. We leave +0.5cm for its under-bust circumference and continue adding the same 0.5cm at the waistline and stomach levels. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p>I�m doing it despite the fact that I need to remove quite a lot from the standard pattern both at the waistline level and at the stomach level. </p>
<p><strong>There are two ways of re-distributing these extra centimeters: you can either use the side seams or the princess seams at the back. </strong></p>
<p><strong>Your choice is determined by your client�s body shapes. </strong>I have already said it multiple times - take pictures of your clients! You will know better what to add or remove if you have a picture as a reference at hand. </p>
<p><strong>If your client�s figure is far from an hourglass, </strong>it makes sense to narrow the pattern at the waistline and stomach levels using the side seams so as to emphasize the curve and create more of an hourglass-like silhouette. <strong>If your client�s body has a nicely curved side line </strong>but her buttocks are not full enough, then you should definitely use the princess seams at the back. </p>
<p><strong>In my particular case it makes more sense to narrow the pattern using the princess seams at the back. </strong>My client�s natural side line is fine and the curve on the pattern doesn�t need to be emphasized for a more distinct hourglass shape. <strong>That is why I�m going to use the princess seams at the back for any adjustments that concern narrowing the pattern. </strong></p>
<p><strong>Let us move on to our calculations related to the princess seams at the back. They should be recorded in column 7. </strong></p>
<p><strong>Everything�s just fine at the bust and under-bust levels, there are no changes in the princess seams and I write zeros there. </strong></p>
<p><strong>We are left with the waistline and stomach levels. </strong>Let me make a short digression. When I was working on the pattern of our future corset, I thought I�d most likely make two princess seams at the back. But looking at it I realized I didn�t want to cut the central back piece yet again and have it made of some kind of straps. Back then I didn�t know what adjustments I�d have to make later. But now I can tell that in order to apply those significant alterations at the waistline and stomach levels it is reasonable to distribute the values between two princess seams. However I am not going to cut the central back piece into two separate pieces. <strong>What can I do instead then? I�ll simply make a dart. </strong>I will mark its location wherever I want to make another princess seam when cutting. This dart will be located at the waistline and stomach levels and then covered with a bone. I briefly mentioned a decorative princess seam at the back when I was explaining the technical drawing of our corset. And there is no need of cutting a narrow piece into �straps� for that purpose! </p>
<p><strong>During our calculations I�m going to account for an extra princess seam at the back. </strong></p>
<p>So, I need to remove 3.5cm <strong>at the waistline level. </strong>However we have added 0.5cm using the side seam, therefore 4cm has to be removed now. Since there are 2 princess seams and each princess seam has 2 sides, I need to divide this 4cm by 4. <strong>As a result, I need to remove 1cm at the waistline level using the princess seams at the back. </strong></p>
<p><strong>I make similar calculations for the stomach level and the level of the bottom edge. </strong>I need to remove 2.5cm. However we have added another 0.5cm using the side seam, therefore 3cm has to be removed. I divide 3cm by 4. <strong>As a result, I need to remove 0.75cm at the stomach level using the princess seams at the back. </strong></p>
<p><strong>In other words there will be a dart on the central back piece. It will be 1cm deep at the waistline level and 0.75cm deep in its bottom part. </strong>The dart will start at the under-bust level. It�ll form an additional princess seam that�ll be covered by a bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p>Let�s use our mock-up <strong>to check if our clip measurement was calculated correctly. </strong>We need to remove 2cm in the scye area, 1.5cm from the corner and 1.5cm in the direction of the d�collet�. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Let us draw the new shape of the cup. </strong>Looking at the mock-up I can tell that I�d better reduce the side length by 1cm for a prettier passage from the cup to the body. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>The mock-up is going to make the cutting process much easier. </strong>When I start transferring the patterns onto fabric, I�ll be able to refer to the mock-up and see how much this or that curve has changed to form a beautiful d�collet� line, etc. In short, taking decisions about pattern adjustments becomes much easier when you have a real-size example of the desired result. </p>
<p><strong>I hope now you see how useful a mock-up really is. </strong>I have demonstrated how to work with it on all corsets described in this course. A mock-up is simply irreplaceable both for pattern adjustments and for designing new models. </p>
<p><strong>I would like to give a piece of advice to those beginners who have never worked with corset patterns before. </strong>These patterns are not familiar to you yet: you don�t quite grasp how the pieces are joined or what the whole process should be like. <strong>Sewing a mock-up is absolutely mandatory in your case! </strong></p>
<p>Everyone has their own habits related to taking measurements and sewing. After sewing and measuring a mock-up you should use those very measurements as your standard ones. They may differ from the measurements of my standard patterns. Mine are based on my manner of sewing and taking measurements off a client. Your measurements taken off your own mock-up are going to be correct as well because you are going to take measurements off your client referring to the mock-up and then sew your corset in the same fashion you have sewn the mock-up. </p>

