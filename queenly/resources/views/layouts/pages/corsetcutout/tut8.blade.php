<h2>Tutorial 8. Sewing the Princess Seams. Working on Lacing Bars. </h2>
<p></p>
<p><strong>We should finish the back of the corset with bones and add two lacing bars. </strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I should sew on the bones before cutting the lacing bars.</strong> You should double-check the length of all princess seams! And again, the mock-up serves as a reference for measuring all princess seams in order to prepare bones of appropriate length. You can also refer to the patterns customized after the measurements taken off the mock-up. </p>
<p>Remember, once I said you don�t need to prepare the bones in advance (first cut them all and then sew them on)? Now is the opposite case. <strong>This time you should carefully measure the length of the back on the pattern and cut bones of precisely the same length. </strong>Mine is 28.8cm long. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I�m going to sew the bones onto the face side of the back. </strong></p>
<p><strong>The face side is the side with seam allowances on. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>There are no princess seams on the underside, only stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch a wide Rigilene bone (that�s 28.8cm long) at the very edge of the back, on its face side. </strong>The length of the bone should perfectly coincide with the length of the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I make another strengthening stitch stepping a couple fishing lines inwards from the edge of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>Then I sew on another parallel Rigilene bone - a narrow one since I don�t want the lacing bar to be too wide. </strong>I didn�t prepare it in advance: I�m just sewing it on referring to the length of the first bone. I try to make the gap between the bones as small as possible. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I take another prepared Rigilene bone that�s 28.8cm long and stitch it onto the other half of the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same steps as on the first half. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I measure the width of the bar. </strong>Two bones make about 2.2cm. Let�s calculate the width of a fabric strap for finishing the bar. I need a strap with one side that�s 2.5cm wide and the other side that�s 3.5cm wide. <strong>So the total width of the bar should be 6cm.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image026.jpg" class="img-responsive center-block"></p>
<p>I have cut a strap of fabric that�s 6cm wide, duplicated it with iron-on batiste, and finished the edge of its wider side with an overlock. Then I folded and pressed it so as to have one side 2.5cm wide. <strong>I�ve prepared two straps, each 32cm long. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to finish the edge of the back using this strap. Its narrower side should be on the face of the back, i.e. where the bones are, and the wider side should stay on the underside. </strong>Be careful not to confuse the face with the underside. The face of the corset is the side with seam allowances on. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I place the outer bone right at the fold line of the fabric strap and make a stitch. </strong>You don�t have to stitch precisely at the edge of the bone - the important thing is keeping the bone right at the fold line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Next I make another stitch to hold the bar and the back piece together. </strong>You can stitch right through the bone or at its edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I fold the strap over the edge, pull its unstitched end up a little and make a stitch 1.2cm from the edge to form a tunnel.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>I make another stitch along the edge of the bar. </strong>And then I trim the excess of fabric at the top and down the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Our semi-finished lacing bar looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I prepare the second bar in the same fashion. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image046.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove all extra fabric and compare the lengths of both halves. </strong>They should be absolutely identical. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to sew fabric-encased Rigilene bones onto the princess seams at the back. But first let�s determine their length. </strong>I refer to the mock-up again in order to determine the length of bones that will be encased in fabric. I measure the length of the lacing bar (there�ll be a bone next to it) and the length of the dart, and multiply it all by two. This value isn�t supposed to exceed 150cm or else our fabric sheet won�t be wide enough. Next I�m going to cut a bone of the required length, encase it in fabric and cut it up in two - one for the dart and one for placing at the bar. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%208_files/image056.jpg" class="img-responsive center-block"></p>

