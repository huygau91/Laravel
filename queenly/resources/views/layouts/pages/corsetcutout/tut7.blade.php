<h2>Tutorial 7. Joining the Bobbinet Layers of the Pieces. </h2>
<p>&nbsp;</p>
<p><strong>We should prepare the pieces for getting joined. </strong>What does it mean? All corset body pieces are cut out of see-through bobbinet fabric and each piece consists of two layers. Right now our task is to assemble all paired pieces without confusing right and left. The bobbinet is very thin and it�s easy to deform it during the sewing process so you should be very careful. I help the sewing machine by pulling the piece back to avoid gathering. Besides I join the pairs in a vertically-mirrored fashion. I don�t cut them up but just continue sewing with the same thread to distinguish the right side from the left one. </p>
<p><strong>Now I�m assembling the back pieces: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I prefer to join the princess seams after assembling paired back pieces.</strong> The key task remains the same: prevent the joining seam from gathering. Since I�ve assembled these pieces in vertically-mirrored pairs using the same thread, I can join the princess seams on the right and left halves without separating them. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowance of each princess seam following the see-through corset technique. </strong>Let me remind you: all seam allowances should be directed towards the same side, i.e. wherever you want to locate bones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I�m working on the dart we�ve made. I make a vertical stitch in the middle of the dart </strong>in order to make it more even. It�s easy to join paired double-layer pieces using this stitch as a reference. The dart is neat and accurate. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the dart as if it were a seam allowance. </strong>Don�t forget about symmetry - if there is a dart on one piece, then you should make the same kind of a dart on the opposite piece. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Keep making sure the pieces don�t get gathered on the thread. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to trim the seam allowances. </strong>Don�t cut too much off, it�ll be perfect if you leave 6-7mm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>So we�ve joined the back pieces. </strong></p>
<p><strong>Now let�s arrange and sew the front pieces in pairs. </strong>Move from piece to piece gradually. Don�t unpin them or remove pins from all pieces at once. </p>
<p><strong>Then I take the central front piece, </strong>unpin it and prepare the layers for joining. Keep spreading the fabric to help the machine make a straight ungathered stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>And I unpin the next set of paired pieces - the intermediate front pieces - and assemble their layers the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I assemble the layers of paired side pieces of the front. </strong>Don�t rush or try to join their princess seams without joining the layers of each piece first. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Now the pieces are ready for getting joined and I sew their princess seams one by one. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the seam allowances and direct them towards the centre. </strong>Be careful when evening out the edges - even though the pieces are stretchable and transparent, you should match them perfectly.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same steps on the other half of the corset. </strong>I join the princess seam between the central and intermediate parts of the front and topstitch the seam allowance directing it towards the centre. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image054.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew the last princess seam of the corset front: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image064.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image066.jpg" class="img-responsive center-block"></p>
<p><strong>Then I mark the inclined bone that will imitate a princess seam. </strong>It�s better to make a stitch for this purpose and to secure the fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%207_files/image070.jpg" class="img-responsive center-block"></p>
