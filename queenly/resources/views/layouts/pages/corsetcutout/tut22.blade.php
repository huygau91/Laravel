<h2>Tutorial 22. Finishing the Bottom Edge of the Corset. Inserting Grommets. </h2>
<p></p>
<p><strong>The corset is as good as done. It�s time to lay a Rigilene bone along its bottom edge and give it a pencil-edge finish. </strong>Remember to check if the back length is identical on both halves of the corset. Since the bottom edge is curved, I have gathered the bone rather tightly. </p>
<p><strong>I tape the end of the bone, place it 0.5cm from the edge and start stitching it on along the inner side. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>It�s important not to deform the bottom edge! </strong>I carefully move from one bone to another. I spread the fabric with my hand without pulling on it, trying to help it lie freely. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>You should check the shape of the corset at this stage. </strong>It�s best if you put it on the dress-form and see if the bottom line is curved symmetrically on the left and on the right. Everything should match perfectly. </p>
<p><strong>Next you can make the second strengthening stitch through the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I trim all excessive bits or threads, </strong>i.e. anything that might interfere when I continue finishing the bottom edge. </p>
<p><strong>I tape the ends of all vertical bones with some duct tape and secure them by stitching. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take a fabric strap </strong>that�s about 3.5cm wide, leave a small tip at the beginning of sewing, and stitch through it at a presser-foot or a bone-width distance from the edge. You should keep slightly pulling the fabric towards you. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Then I flip it and form a pencil-edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the ends of the pencil-edge back and secure them with blanket stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>And then I insert grommets into the lacing bars. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>There is no modesty flap </strong>because it wouldn�t look pretty on a see-through corset like this. </p>
<p><strong>Our corset with a fancy cut-out is finished! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2022_files/image022.jpg" class="img-responsive center-block"></p>
