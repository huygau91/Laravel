<h2>Tutorial 16. Pressing the Face of the Central Piece. </h2>
<p></p>
<p><strong>I press the face of the cup: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I notch the seam allowance to help open it the same way as I did on the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the seam allowance leaving about 0.5cm and notch out its corners. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Now I can turn the cup out and press it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I perform the same steps on the other cup: notch the seam allowance, press it open and trim it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Once youï¿½ve removed the excess off seam allowances, you should turn out the cup and press its face if necessary. </strong>Be particularly careful at the beginning and the end of the seam. You should press it open and make it perfectly flat. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I take the cup off the pressing mould. I need to bend the outer arched edge of the cup inwards along the stay-stitch and secure it by pressing. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Try to make the stay-stitch stay right on the fold line, on the crease. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Bend the seam allowance inwards and fold the corner neatly. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>You must have already noticed how pretty the cup looks, especially the corner in the area passing onto its round part. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same with the other cup: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>And now we need to bend inwards and press all outer edges of the cup. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I step 1mm from the stay-stitch and make notches with 7mm intervals on the small arched section from both sides.</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I bend it all inwards onto the underside. </strong>This task requires great accuracy. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I move on to the long side. </strong></p>
<p><strong>You should notch the curved area as well. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>The stay-stitch is located right on the crease of the fold line. </strong>Some pinpoint accuracy is required here. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>At this point you should choose where you want to start attaching your bobbinet insert. </strong>The diamond cut in the middle is meant to be covered by it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>At what level exactly do you want to start attaching the bobbinet? </strong>This decision is very important because youï¿½ll need to fold inwards and press the seam allowances up to this level. My piece has under-bust notches and notches at the level Iï¿½ve chosen for the bobbinet. <strong>I cut these notches deeper so as to reach the stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I notch the corners of the diamond so I can spread the seam allowances open. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I know it from personal experience that keeping a small seam allowance open is pretty challenging (especially when working with fancy shapes). </strong>So I always have some Gutermann fabric glue at hand. <strong>I put some glue onto the seam allowance and stick it to the underside. </strong>This will protect the corners of the cut-out from fraying and secure the seam allowance in place. You can always adjust it and even it out before the glue dries. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image064.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>Help yourself with pins at sharp corners where opening a seam allowance is particularly difficult. </strong>Do not rush when doing this! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>Once youï¿½re certain that everything is fine, just press the seam allowances with a hot press-iron without steaming. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>Put the piece aside for some time to let it cool down and wait until the glue sets. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image076.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>Our cups are fully formed and we have finished the transition from their sides to the curved rest of the piece. </strong>The cups are ready to be covered with the face now. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2016_files/image080.jpg" class="img-responsive center-block"></p>

