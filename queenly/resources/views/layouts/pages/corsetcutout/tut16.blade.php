						<p><strong>Features:</strong></p>
<ul>
<li>Using thin synthetic net and armani satin;</li>
<li>Special pattern with sloped&nbsp;reliefs and whole piece center part.</li>
</ul>
<p><strong>Skills you gain:</strong></p>
<ul>
<li>Making a curvy front part with deep cut-out;</li>
<li>Inserting see-through fabric in the deep cut-out in the front;</li>
<li>Working with stretching fabrics and keeping the shape of the corset;</li>
<li>Adding an additional relief on the back with out altering the pattern.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>Widely used technology for creating wedding (and evening) dresses, and also for youth fashion corsets.</li>
</ul>
