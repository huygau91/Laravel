<h2>Tutorial 20. Finishing the Face of the Central Piece. </h2>
<p></p>
<p><strong>And now we need to cover the lining with the entire face piece including the cups.</strong> This task is rather challenging to tell the truth. Everything has to match perfectly. All dimensions should coincide. Don�t forget that the lining is stiffer and it has been deformed by bones. I have turned out the cups to make it more convenient. Note how much easier it is to cover the cups when they are bent-in like this. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2020_files/image002.jpg" class="img-responsive center-block"></p>
<p>
