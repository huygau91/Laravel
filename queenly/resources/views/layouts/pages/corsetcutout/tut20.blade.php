<p><strong>As you can see, this corset is not simple and there are lots of delicate points in the sewing process. </strong>We had to keep checking the length of the princess seams and the shape of the corset because the fabric is so thin. And the fancy insert in the middle is a real hit! You can use the same kind of bobbinet when you want to overlay a corset with thin lace. It is also suitable for the lining or base of your corset if you want it to look as though it were entirely made of lace. There are multiple ways of using bobbinet fabric even when you�re working on less sophisticated corset models. </p>

