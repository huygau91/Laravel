<p><strong>Step by step and pin after pin I have arranged the piece and secured it in place. </strong>I believe everything is in good symmetry. I might notice some faults when I put it on the dress-form. But they definitely won�t be serious. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2020_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And again, we are faced with an important task: we need to make a nice even stitch through the face and attach the cups. </strong>Let me highlight it again: do not rush, just take your time and be very careful! These are the most important stitches because they will stay on the front, visible to everyone. </p>
