<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2020_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I should say we�ve completed the major workload.</strong> The central element of the corset looks very even! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%2020_files/image050.jpg" class="img-responsive center-block"></p>
