<h2>Tutorial 6. Cutting the Central Piece with Cups. Marking the Location of Bones. </h2>
<p></p>
<p><strong>It�s time to cut the central piece of the corset </strong>out of blue opaque fabric. Its matte side will serve as the face and the sheen side as the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have duplicated the fabric with iron-on batiste and folded it in half. </strong>The face is underneath and the lining is at the top. I�m going to draw the cup on the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the fold line with pins. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I take the central cup piece and transfer it onto fabric. </strong>I am using the pattern that�s been customized. I transfer all notches carefully. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I lay the side cup piece in such a way that its bust line coincides with the direction of the selvage. Do not forget</strong> that you need to expand the face of the cups by about 1cm to be able to pull it over the edge of the lining. You can always remove the excess when needed. Usually there is no excess at all and this extra centimeter really comes in handy.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Next I duplicate the side cup piece and the top part of the central piece (up to the under-bust line) with the same iron-on batiste, </strong>i.e. just fuse another layer over it. It�ll make it durable enough.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>And then I fuse another layer of batiste over the other side:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now you can secure the layers with pins and cut out the patterns. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image016.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I lift the lining and adjust it after the initial pattern </strong>i.e. remove the extra centimeter and make notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>We have prepared our face and lining pieces. You can put the face pieces aside for now. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>Now we should mark the location of all bones. </strong></p>
<p><strong>The central cup piece: </strong></p>
<p>I connect the bust-line notches (line 1). Then I mark 3cm upwards and 4cm downwards from that line (lines 2 and 3). I step 2cm inwards from the princess seam at the under-bust level and draw a line (line 4) that goes up and divides the top arch of the cup in about half. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image021.jpg" class="img-responsive center-block"></p>
<p>Looking ahead I want to tell you that <strong>a narrow Rigilene bone </strong>will be placed across the perimeter of the central cut-out at about 0.5cm from the edge, as well as along the sides of the piece at a 1.2cm distance from the edge (the width of a seam allowance) <strong>to ensure additional strengthening of the piece. </strong>I might place more bones along the dashed lines. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>The side cup pieces: </strong></p>
<p>I connect the bust-line notches (line 1). I mark 3cm upwards and 4cm downwards from that line (lines 2 and 3). These bones will lie at an angle and divide the top and the bottom of the cup in about half. I step 2cm inwards from the princess seam and stop at about 1cm from the seam allowance (you should by no means bring the bone to the very corner). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Transparent_corsets/corset_4/transcripts/Tutorial%206_files/image025.jpg" class="img-responsive center-block"></p>

