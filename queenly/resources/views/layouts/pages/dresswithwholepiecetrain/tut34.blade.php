<h2>Pattern Constructor.</h2>
<p></p>
<p>In this tutorial I�d like to share my own method of designing dress patterns based on the construction kit principle. </p>
<p>My method is good because: </p>
<p>- It helps you spare room for storing your patterns; </p>
<p>- It provides endless opportunities for further modification; </p>
<p>- It removes any size limitations of the initial standard patterns. </p>
<p><strong>I have sketched schematic patterns of the top of a corset-based dress for better clarity: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image001.jpg" class="img-responsive center-block"> </p>
<p>There is only one difference from the regular corset pattern: it is prolonged to the hips. </p>
<p>You can always distinguish three horizontal lines on any pattern - the bust line, the waist line and the hip line. Vertical lines are the fold line at the front and the central open edge at the back. </p>
<p>99% of corset-based dresses are princess dresses with two princess seams in the chest area at the front and two princess seams at the back. </p>
<p>A dress like this can have any kind of a top and any neckline. It can be strapless (like shown in the picture); it can feature a boatneck or integral straps: singlet-type straps, halter-neck, off-shoulder straps. It can be a dress with sleeves that cover the shoulders. Or it can be a very open dress with a fancy d�collet� and a low cut back. It can be anything you like. </p>
<p>I have chosen a basic pattern as an example. But it doesn�t mean it you have to work with standard patterns. I will show you how to adjust patterns after individual measurements in the next tutorials. </p>
<p>And now I would like to show you how to build a pattern of a corset-based dress and its skirt combining standard pattern elements as if they were construction kit pieces. My aim is to prove that you don�t have to construct a pattern from scratch for every particular size or style. </p>
<p>I�ll start from the basics. </p>
<p></p>
<p><strong>First I�d like to talk about the classic straight cut. </strong>You can see two models. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image002.jpg" class="img-responsive center-block"> </p>
<p>This dress can be decorated with any kind of drapery or falbala, or it can be smooth and straight with a side slit or a back vent, a wiggle dress or a flare dress, etc. All these dress styles can be made after the pattern we are about to discuss. </p>
<p><strong>All these dresses are based on a corset top and a straight skirt. </strong></p>
<p><strong>Let�s return to the pattern. </strong>What do you do in order to turn it into a straight cut dress? You should prolong its vertical lines into a straight skirt of desired length. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image004.jpg" class="img-responsive center-block"> </p>
<p>In other words, there�s another horizontal line on the pattern - the skirt length. Your dress can be long, medium or really short. It makes no difference. I simply prolong the lines perpendicular to the hip line right on fabric whenever I want to add a straight skirt. <strong>I apply minor alterations later if necessary. </strong></p>
<p><strong>For example I can narrow the skirt using the side seam or princess seams at back...</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image005.jpg" class="img-responsive center-block"> </p>
<p><strong>Or I can add a vent there...</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image006.jpg" class="img-responsive center-block"> </strong></p>
<p><strong>...mark the length of the slit on the pattern, etc. </strong></p>
<p><strong>And again, I�d like to emphasize that all patterns of the dress top can be modified according to your client�s size. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image007.jpg" class="img-responsive center-block"> </strong></p>
<p><strong>A few more words about the pattern: </strong>whenever you�re making a straight corset-based dress, you feel tempted to make darts at the front of the skirt instead of full princess seams going down to the bottom. But any corset-based dress is known for its accentuated bust area, sloping hips, emphasized waist, and very flat stomach. These peculiarities are taken into account on the pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image008.jpg" class="img-responsive center-block"> </p>
<p>And if you try to cut a one-piece skirt for two pieces of the corset front, then the dart will be located rather low and you�ll still have to prolong the seam of the skirt nearly to the very bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image009.jpg" class="img-responsive center-block"> </p>
<p>What concerns the back half, you can cut a one-piece skirt pattern with no princess seam in the middle. <strong>It�s enough to combine the patterns and account for the original seam allowances. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image010.jpg" class="img-responsive center-block"> </p>
<p><strong>What you see below are corset-based dresses with a bell skirt. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image011.jpg" class="img-responsive center-block"> </p>
<p>The skirt is straight to the knee level or above and the rest of it is flared along the side seam. It�s a very popular skirt style. I sew such dresses with a slit along the middle of the leg and drape them with chiffon or lustrous silk satin, i.e. there are many possible variations. <strong>But this dress model is always based on a corset top and a one-piece bell skirt. </strong>I have a bell skirt pattern and I don�t worry about its original dimensions. It�s surely great to have a cardboard or plastic skirt pattern with a double-checked flare that satisfies your clients and yourself regardless of the size. The key is a beautiful side seam line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image013.jpg" class="img-responsive center-block"> </p>
<p>I prolong the vertical lines on the central front piece to build a pattern for this corset-based model. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image015.jpg" class="img-responsive center-block"> </p>
<p>And I prolong the back piece as well. Please note that I�m using a ready skirt pattern. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image017.jpg" class="img-responsive center-block"> </p>
<p>Then I match the hip lines of the side piece of the dress top and the skirt pattern. I trace the skirt pattern with a pencil or a piece of chalk following the side seam. Next I shift the pattern to match the hip line from the opposite side. And I trace the other edge of the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image019.jpg" class="img-responsive center-block"> </p>
<p>After removing the skirt pattern I have an accurate pattern of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image020.jpg" class="img-responsive center-block"> </p>
<p>I work on the back piece the same way. I match the hip lines from one side and then from the other side and trace the edges over fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image021.jpg" class="img-responsive center-block"> </p>
<p>I can apply any alterations with regard to the model: make the skirt narrower or flare it, mark a slit, etc. <strong>I have turned two separate patterns into a single pattern for a one-piece bell-skirt dress. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image023.jpg" class="img-responsive center-block"> </p>
<p><strong>Now let�s look at a more complicated task: a corset-based dress with a godet bell skirt. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image024.jpg" class="img-responsive center-block"> </p>
<p>The skirt has a bell-like flare along the side seam and a godet at the back which can be prolonged into a beautiful train. This dress looks stunning as you walk: it�s somewhat narrowed towards the knees with a slim fit at the front and there�s a very beautiful train or just a godet flare at the back. </p>
<p>Just like in the previous case I have a separate pattern of a godet skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image026.jpg" class="img-responsive center-block"> </p>
<p><strong>It turns into a dress pattern the same way as before. </strong>I simply prolong the vertical lines of the front piece to the required skirt length and then I carefully match the hip lines of the top and the skirt and trace the edges over fabric. I don�t care if the waist lines are mismatched. I am only interested in the bottom part of the skirt - from the hip and down. I can apply additional alterations later if needed: add a train, a gore, etc. </p>
<p><strong>And now I�d like to talk about making a pattern of a fishtail dress or a godet dress. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image027.jpg" class="img-responsive center-block"> </p>
<p>The construction process remains unchanged. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image029.jpg" class="img-responsive center-block"> </p>
<p>I have a pattern of a godet skirt. I place each skirt pattern over the corresponding corset pattern matching their hip lines and trace the edges onto fabric. First I trace it from one side and then I shift the pattern and trace it from the other side. </p>
<p><strong>Let�s go ahead. The dress has a skirt that flares at the hips. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image031.jpg" class="img-responsive center-block"> </p>
<p>I take the pattern of the skirt and turn it into a dress using the familiar scheme. I do it even when the skirt pattern is considerably smaller than the corset pattern customized after my client�s size. I believe you have already grasped the essence of the method. And if I need to change the flare, I re-draw it right on fabric. That concerns, for example, wedding dresses with fuller skirts. </p>
<p><strong>And, finally, the last model I�d like to touch upon is a dress that flares from the waist. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Pattern%20Constructor_files/image033.jpg" class="img-responsive center-block"> </p>
<p>I follow the same steps. There�s only one difference: I match the waist lines of the corset pattern and the skirt pattern, i.e. the flare starts right at the waist. </p>

