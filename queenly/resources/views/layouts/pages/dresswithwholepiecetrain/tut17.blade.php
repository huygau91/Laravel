<h3>Tutorial 17. Joining the Lace with the Face of the Dress. </h3>
<p></p>
<p><strong>The next stage is joining all front and back face pieces with their lace overlays. </strong></p>
<p>We have already cut separate lace pieces and now we need to put them over the corresponding face pieces. If you use plain lace like in the case at hand, you can arrange and assemble the pieces right on the table. </p>
<p><strong>But what to you do if your lace is embellished with beads, rhinestones or sequins? </strong>They add a certain bit of weight to the fabric so they will certainly pull it down. If you arranged such lace pieces and pinned them to the main pieces on a horizontal surface, your lace would not only stretch out vertically when the garment is finished - it would also pull on the seams. We want to join our lace overlays with the main pieces from the top down to the hip line. All princess seams and side seams that hold the lace would then stretch (and stretch differently) and distort the contour of the dress. It�s particularly bad on large-sized dresses made of rather broad pieces. It�s nearly impossible to even it out in that case! I have faced this problem more than once in my practice. </p>
<p><strong>Let me share a trick that will help you overcome this problem. </strong>My know-how is to join the lace overlays and the main dress pieces while they�re hung up in the air. </p>
<p><strong>You simply pin the main piece to the curtain with just a couple of pins along its top edge. </strong>It�s very important that it should hang freely. <strong>I have pinned the side part of the back: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2017_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And then you start pinning the corresponding lace piece over it: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2017_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>You will notice the way lace fabric behaves when hanging in the air. </strong>You can pin and spread the lace much more accurately as on the surface of the table. Please note that I still keep an eye on the way the lace settles when matching the notches: you might need to pull it up or loosen it in certain areas. This is particularly important when you work with heavy lace fabrics! </p>
<p>The back pieces should be joined with the lace in the area between the hip line and the beginning of the fishtail flare. This will ensure a good fit and a smooth transition to the fishtail part. I re-distribute the lace and pin the pieces together down to the specified level. I keep making sure the lace is not slack or doesn�t pull the main fabric down. There will be a single side seam down to the beginning of the flare and then the pieces will split into two separate layers. </p>
<p><strong>How come I recommend adding the lace overlay before joining the face pieces? </strong>If you assemble the lace and the face of the dress individually, all seam allowances will show on the face. When you�re joining pieces with sewn-on lace overlays, the seam allowances stay on the underside. The lace is secured in place and doesn�t shift.</p>
<p><strong>Now you know my special trick for joining face pieces with their lace overlays. We need to join all front and back face pieces with the corresponding lace pieces, one by one. </strong></p>

