<p><strong>I trim the lace that goes beyond the seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>And I sew up the other princess seam of the back the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image072.jpg" class="img-responsive center-block"></p>
<p>
