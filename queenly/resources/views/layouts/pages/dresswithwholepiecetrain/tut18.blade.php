<h3>Tutorial 18. Assembling the Face of the Dress. </h3>
<p></p>
<p><strong>We have pinned lace overlays to the face pieces and now we need to join them by machine-stitching. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>These pieces should only be joined along their vertical open edges! </strong>If you really have to stitch along the horizontal lines for whatever reason, remember - only short bar tacks are acceptable. </p>
<p>I am going to attach the lace to the pieces of the front down to the hip line following their side seam and princess seam lines. On the back pieces I�ll follow the princess seams down to the beginning of the flare and sew along the central open edges down to the level 3-4cm below the zipper. </p>
<p><strong>I stitch at a presser-foot distance from the edge without making any bar tacks. </strong>I recommend that you use the biggest stitch spacing possible - this way you�ll be able to correct the stitch line if necessary. </p>
<p><strong>The side front piece: </strong></p>
<p><strong>I join it with the lace down to the hip level following the princess seam line. </strong>When you sew up the princess seam between this piece and the central front piece, you�ll join the main pieces and their lace overlays down to the hip line and then join each layer separately below it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I join the piece with the lace down to the hip line along the side seam as well. </strong>The stitch goes from the hip line upwards. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>You can make a horizontal stitch along the top of the piece to secure it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now you can remove the pins. The piece is ready! </strong></p>
<p><strong>The central front piece: </strong></p>
<p><strong>I join the piece with its lace overlay down to the hip line following the princess seam line. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I stitch out a small corner in the neckline area by turning the garment about the needle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Then I join the piece with the lace down to the hip line following the other princess seam.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>The piece is finished! </strong></p>
<p><strong>Let�s continue sewing the lace onto the remaining dress parts. </strong></p>
<p><strong>The second side front piece: </strong></p>
<p><strong>I join the piece and its lace overlay down to the hip line following the side seam line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I make a horizontal stitch along the top of the piece to secure it properly. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The central back piece: </strong></p>
<p><strong>I stitch through the lace following the princess seam down to the beginning of the flare. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>And I stitch it down to the level 4cm below the zipper along the center open edge of the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I overlay the other central back piece with lace in the same manner. </strong></p>
<p><strong>The side back piece: </strong></p>
<p><strong>I stitch through the lace down to the beginning of the flare following the princess seam. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I stitch down to the hip line following the side seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Then I attach the lace overlay to the other side back piece the same way. </strong></p>
<p><strong>I have secured the lace overlays on all face parts of the dress. </strong></p>
<p><strong>Now we can join them. </strong></p>
<p><strong>I start from the princess seam at the back.</strong> </p>
<p><strong>I take the central and the side pieces of the back and sew up the princess seam down to the beginning of the flare. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>Then I bend back the main fabric pieces to separate the lace </strong>and make a notch on it. I bring the needle to the point where I�ve just stopped sewing, carefully align the lace overlays, make a neat bar tack and continue sewing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image048.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image050.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>Then I bend the lace towards the side and put the face fabric back in place. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I make a bar tack and join the main fabric (separately from the lace) along the princess seam.</strong> Make sure not to catch any lace. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>The first half of the back is ready! </strong>There�s a single princess seam that joins the main pieces and their lace overlays down to the flare level. The layers are fully independent in the flared area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>You should trim the seam allowance in the duplicated area of the back. </strong>I trim the corner at the top and trim the entire seam allowance down to 6-7mm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2018_files/image066.jpg" class="img-responsive center-block"></p>
