<h3>Tutorial 12. Pressing the Lining and the Bones. </h3>
<p></p>
<p><strong>I have prepared a sleeve board and a cup pressing mould for pressing our semi-finished sewn pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I start from the back pieces. </strong></p>
<p><strong>All topstitched parts of the princess seams should be pressed open. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The rest of the seam allowance (from the hip line down to the very bottom) should be directed towards the center and pressed flat. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Sometimes it�s easier to press it from the face side. </strong>I direct the seam allowance as required. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>And I press the seam allowance on the other half of the back the same way. </strong></p>
<p><strong>Here is why I don�t do the overlock finish on these pressed seam allowances: </strong></p>
<p>- Modern fabrics don�t tend to fray as much and besides it is not a casual-wear dress. </p>
<p>- The princess seams are hidden between two layers of fabric: the face and the lining. The client can�t see them. </p>
<p>- The overlock finish creates extra thickness that protrudes through the face after you press it. </p>
<p><strong>That�s why I wouldn�t recommend the overlock finish for any vertical cuts unless there is no choice! </strong></p>
<p><strong>I direct the seam allowances of the vertical princess seams on the front lining towards the center and press them flat as well, from the hip line downwards. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I use the cup pressing mould to make the princess seams of the cups rounder. </strong>They should be smooth. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Here, look at the difference between �before� and �after�: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I press the princess seam of the other cup. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I press open the upper sections of the front princess seams. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Pressing at this stage is a must if you don�t have a good press-iron with a powerful steam generator. </strong>At this point you can still use a semi-industrial or a household iron to make the cups rounder before quilting them with bones. </p>
<p><strong>I turn the cups out and press them from the other side as well since all bones will be inside. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>It�s a good idea to press Rigilene bones themselves. </strong>They are usually rolled up when you take them out of the pack, besides they tend to get somewhat eased-in when exposed to heat and steam. That�s why I recommend pressing the bones in advance, i.e. not just spreading them out but shaping them so they don�t deform the cups later. </p>
<p><strong>It�ll be a lot easier to work with the bone after you straighten it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2012_files/image034.jpg" class="img-responsive center-block"></strong></p>

