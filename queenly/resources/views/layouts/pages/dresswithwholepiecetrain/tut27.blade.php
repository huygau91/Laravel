<h3>Tutorial 27. Pressing the Side Seams of the Dress. </h3>
<p></p>
<p><strong>Let�s continue working on the dress and press open the side seams. </strong></p>
<p><strong>I use my special pear-shaped pressing mould for this purpose. </strong>Its shape should be as close as possible to the curve of the side seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The transition of the side seam from the waist to the hip should settle over the pear-shaped mould particularly smoothly. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>You can press the straight areas of the side seam on your regular pressboard or table. </strong>A sleeve board also comes in handy: the garment hangs freely at the sides, which prevents the press-iron from touching the areas you don�t want to press. Don�t use very strong steam - it might deform the adjoining areas. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I press the area that formed at the transition of the open side seam to the closed seam on the lining. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s press the other side seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I also use my cushion to dab it dry. </strong>You can secure the seam allowances of the side seam at the top with a couple of hand stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image014.jpg" class="img-responsive center-block"></p>
<p>Anyone who plans to make a living from sewing wedding or evening dresses will appreciate the benefits of this technique of open side seams. You could polish it up for working on single orders or custom VIP-orders. </p>
<p><strong>Let�s press the side seams of the lining. </strong>You can press the seam allowance flat towards the front - it�ll make the transition from the open side seam to the closed one look neater. Of course you could notch the seam allowances on the lining, fold them inwards and stitch it up but any additional stitches at the top might pull on the lining and result in a crease or a �bubble� on the side of the dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I press open the central seam on the lining of the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image022.jpg" class="img-responsive center-block"></p>
<p>You can do an overlock finish on the seams of the lining and the main garment in the area 50-70cm up from the bottom edge. Finishing them up to the very top would be pointless. </p>
<p><strong>I press the side seams of the dress base. </strong>And again, you could direct them towards the front or towards the back, or press them open which is exactly what I�m doing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>Next we should press the side seams of the lace skirt. </strong>Their seam allowances can be directed to either side - they will be hardly noticeable in any case. If your lace is rather stiff, you can even out the seam allowances and trim them a little to make them thinner. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I turn out the dress and check if the seam looks good enough from the face. </strong>If you feel like making some adjustments, press it again but don�t get obsessed with steaming. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Please don�t neglect the transition of the open side seam to the closed seam! </strong>This spot requires some accurate pressing and then it will be nearly unnoticeable. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image036.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2027_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>We have pressed the main seams and I suggest now is the time to carry out the first fitting test. </strong></p>

