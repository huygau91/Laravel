<h1>Tutorial 6. Working with the Mock-Up. </h1>
<p></p>
<p><strong>I have sewn the mock-up and put it on the dress-form. </strong>The only thing you should keep an eye on when putting it on the dress-form is the location of the side seams! The side seams should be strictly vertical: try to preserve the balance and put them exactly where they�re supposed to be. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p>There might be certain challenges if your dress-form is much bigger than the size of your mock-up. In that case all side seams will actually shift towards the front and the back won�t close. And this happens to be my case: the dress-form is larger than the client and, therefore, larger than the mock-up. That�s why the side seams have moved forward a little but I�ve still managed to locate them perfectly symmetrically and keep them vertical. </p>
<p><strong>I stuff all hollows formed underneath the mock-up with regular padding polyester shoulder pads. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p>These kind of semi-finished shoulder pads always come in handy for this purpose. They are curved and they thicken gradually towards one side. They are lightweight and very easy to work with. I imitate my client�s body shape by pushing them under the mock-up. </p>
<p><strong>If the back of the mock-up doesn�t close properly, make sure the open edges in the middle are parallel to one another. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Let me remind you why you need a mock-up to sum it all up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p>The mock-up helps you check the shape of the chest, the cups and the neckline and determine how low the neckline can go. When you join the parts of the mock-up, you get to know the pattern and develop a better understanding of the way its notches match. It�s a priceless experience! It doesn�t take long to sew a simple cotton mock-up - you basically make a few vertical stitches - but a couple of minutes invested into it will make a great difference later. You will be able to double-check all dimensions and see whether you�ve calculated it correctly and hit your client�s measurements or not. By the way, when you take measurements off the mock-up, remember that the seam allowances of the central seam at the back are 2cm each so each circumference is actually 4cm smaller when the garment is sewn. You can check the clip measurement and determine whether the corners are in place and whether the side length is correct. </p>
<p>But most importantly the mock-up provides you with an opportunity to see how the soon-to-be dress will look on your client. And I suggest you should keep the mock-up on the dress-form until the garment is finished. You can refer to it at any time and shoulder pads do provide a great way to imitate a human body since they are soft enough and can be easily tightened. </p>
<p>I�m sure you will discover lots of other benefits of working with a mock-up!</p>

