<h3>Tutorial 32. Assembling and Finishing the See-Through Skirt. </h3>
<p></p>
<p><strong>So I have cut a double-layer skirt in accordance with the mock-up. </strong>Two layers hold the shape much better while the skirt still stays see-through. </p>
<p><strong>I finish the hemline with a lace border before sewing up the central seam at the back of the skirt. </strong>Let me remind you that I�m sewing this garment for educational purposes. That�s why I�ve chosen a budget type of lace for the hemline. It could look fancier if you embroidered it with beads. Just make sure not to make the hemline too heavy or else it will pull down on the skirt. We don�t want to deform it! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image002.jpg" class="img-responsive center-block"></p>
<p>I have finished the hemline with lace. But I feel like decorating the central seam at the back of the skirt, too. I�ve decided to do it with the help of the same lace border. </p>
<p><strong>I�m going to stitch this lace border along one side of the seam from the face side. </strong>I start at the bottom to match the motifs and move upwards stitching along the entire length of the seam. Don�t be bothered by the opening left for the zipper. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image006.jpg" class="img-responsive center-block"></p>
<p>You could join the open edges of the skirt with an overseam straight after leaving about 30cm unstitched to insert the zipper later. In this case the zipper area will be covered up by the lace. But if you intend to decorate the skirt, I highly recommend that you sew up the central seam at the back after doing so. </p>
<p>The skirt can be decorated with lace appliqu�, motifs taken from a lace border or elements from the main fabric. You can attach the appliqu� with the help of Gutermann glue or fusible web. Skirt decoration requires individuality and you can let your creative spirits run free. </p>
<p><strong>Skirt decoration.</strong> I mark the location of all lace motifs at the front of the skirt, along the hemline, and on the train with the help of fusible web. Then I iron them on. If necessary, you can secure certain spots with fabric glue - or with hand stitches if there�s enough time. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image012.jpg" class="img-responsive center-block"></p>
<p>You can also embellish your decorative motifs with sequins or rhinestones or embroider them with beads or pearls. But please don�t overindulge in the process. The skirt shouldn�t be too heavy. </p>
<p><strong>Next I fold the skirt in half edge-to-edge and sew up the central seam. </strong>Thanks to the lace border the central seam has a neat finish. I overlay it with several additional lace motifs, too. I leave an opening for inserting the zipper at the top. It will be covered up by the lace. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I have also prepared lace motifs for decorating the entire dress. </strong>I�d like to point it out that you don�t have to use motifs from just one kind of lace. You can combine different kinds and even use some leftover bits from your previous works. Lace fabrics always look similar, particularly when embroidered with pearls or beads. Although the general structure and handle should match, of course. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I also have some non-embroidered lace. </strong>I take manicure scissors and cut out certain motifs that can be decorated with rhinestones or embroidered with beads. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I�d like to share another helpful trick used in garment decoration. </strong></p>
<p><strong>Suppose your garment has detachable parts </strong>- it could be, for example, a belt or a skirt attached with transparent snaps. When you take the detachable part off, those transparent sockets might become too noticeable and spoil the overall look of the garment! That�s why I make decorative caps. </p>
<p><strong>There is this type of finishing borders that feature flowers </strong>with or without rhinestones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2032_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I cut out the flower and sew the cap of the snap onto the back of it. </strong>Then I snap it onto the socket. </p>
<p>When bridesmaids help the bride take her belt / skirt / train off the dress, they can quickly disguise all visible sockets with these flower caps. It looks perfect - no one can tell that the flowers are snap-on. </p>
<p>Of course you can feel free to use any other suitable motifs and not just flowers. </p>
<p>I have simply shared a smart way to decorate the snaps with something pretty. It can be bows, flowers, whole ornaments - anything you like. </p>

