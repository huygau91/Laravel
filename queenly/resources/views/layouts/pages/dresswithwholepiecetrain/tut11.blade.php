<h3>Tutorial 11. Sewing Up the Princess Seams of the Lining. </h3>
<p></p>
<p><strong>We need to join the pieces of the lining along their vertical princess seams. </strong></p>
<p><strong>Let�s start from the princess seams at the back of the dress. </strong></p>
<p><strong>I join the central back piece with the side piece along the princess seam with a 1.2cm seam allowance.</strong> You should look to the notches and put them together if necessary. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image004.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the corner of the seam allowance at an angle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>You can either trim the entire seam allowance down to 0.5cm or simply topstitch it. To topstitch a seam allowance </strong>means to make parallel stitches 1-1.5mm left and right of the joining seam to secure the fabric in place. You should topstitch this seam allowance only down to the hip level, i.e. along the batiste. I usually do this first and then iron it or simply iron it later when the bones are in place. You can also trim the seam allowance if it still stands in the way after you topstitch and press it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make a stay-stitch along the top of the back. </strong></p>
<p><strong>A stay-stitch </strong>is a stitch made 1.2cm below the edge (the width of a seam allowance). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s sew up the second princess seam of the back. </strong>You need to sew from the top downwards because these pieces are long. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the corner of the seam allowance and topstitch it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I make a stay-stitch. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now let�s work on the front pieces of the dress. </strong></p>
<p><strong>If your stiff iron-on fabric doesn�t adhere properly, stitch it on along the perimeter. </strong>It is now clear that you could also use some fusible web to secure the interfacing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image030.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>It�s time to sew the princess seams in the bust area. </strong></p>
<p><strong>I�d like to draw your attention to a peculiarity that concerns matching bust-line notches. </strong>These notches indicate the location of the bust line so when you sew up the princess seam you should try to match the bust lines rather than the notches! </p>
<p>I match the princess seams of the side and the central front pieces <strong>and pin at the bust line stepping 1.2cm inwards from the princess seam (the width of a seam allowance). </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Pierce it all the way through the symmetrical point on the bust line of the central piece </strong>at the same 1.2cm distance from the princess seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Then you spin the pieces around the pin as if around an axis and match their princess seams. </strong>The bust-line notches will be mismatched and you shouldn�t try to put them together. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>You can leave the pin where it is as you start sewing. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I recommend using the following technique when you sew along the curved areas of the pattern: </strong>the bottom piece slides along the needle plate and sets the right seam allowance. And you simply put the upper piece on top of it and match their princess seams and notches starting from the under-bust line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image046.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the other symmetric princess seam. </strong></p>
<p><strong>I pin through the bust line the same way as previously. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>And I start sewing up the princess seam. </strong>I guide the bottom piece over the needle plate along the marked seam allowance, then match the princess seam of the upper part with it and match all notches one by one starting from the under-bust line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image052.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the corners of the seam allowances at an angle. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>You can topstitch these seam allowances instead of pressing them open. </strong>But make sure the top of the seam is very carefully spread! You can topstitch the seam allowance along the entire length of the batiste piece or only on the cup piece. I�m going to topstitch the entire seam allowance from the top down to the hip line. Please note that there should be no tucks or creases. A topstitched seam should look as through you have pressed it open. It�s best to actually press it open if you fail to achieve this effect by topstitching! But topstitching is a lot more time-efficient, which is very important to any professional seamstress who makes a living from sewing wedding or evening dresses. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image058.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I topstitch the other symmetric princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image066.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>Don�t forget to trim the seam allowances on the cups where you have stiff iron-on fabric! </strong>Or else they will interfere when you start quilting the cups with bones. The topstitching seams mark how far you can go in trimming the seam allowances. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image070.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I have trimmed the seam allowances on the cups. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>Next I make a stay-stitch along the top of the dress front. </strong></p>
<p><strong>First I make a stay-stitch in the armscye area. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>Then I start making a stay-stitch along the edge of the neckline. </strong>Please note that I have started a new stay-stitch since <strong>stay-stitches are supposed to intersect! </strong>You must by no means turn the garment around the needle! I sew along the seam allowance very accurately, make a smooth transition to the marked section of the neckline and sew along it. I let the machine run free as I reach the bottom point of the neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image078.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>Then I turn the garment around and make a perfectly symmetric stay-stitch from the other side. </strong></p>
<p><strong>I start from the armscye and then I begin a new stay-stitch that goes along the neckline. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image082.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I follow the seam allowance as accurately as before. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image084.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I make a smooth transition onto the marked section of the neckline. I let the machine run free as I reach the bottom point of the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image086.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>These stay-stitches will serve as guidelines for sewing the bones. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2011_files/image090.jpg" class="img-responsive center-block"></p>
<p><strong>We have finished all preparatory work and now it�s time to press the vertical seams of the lining and the cups. </strong></p>

