<h3>Tutorial 7. Skirt Patterns. </h3>
<p></p>
<p><strong>So we are done with the top of the dress and now we can move to the skirt. </strong>It�ll have a bell-like front and a fishtail back with a train. Let�s use the construction kit principle. </p>
<p><strong>The bell skirt: </strong></p>
<p><strong>I have printed out the skirt patterns and arranged them in the right order. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>But we are not going to sew a complete bell skirt: we only need its side line starting from the hip level. <strong>That�s why I only glue together the sheets with the side piece of the skirt. </strong>You would only need the entire pattern if you wanted to sew a separate bell skirt. Please note that its front and back would be absolutely identical in that case. It may seem strange but I�ve been sewing such skirts this way for years and they have a perfect fit despite having the same darts on the front and at the back. </p>
<p><strong>We are interested in the side line section below the hips and the part of the bottom that will reach the gore of the dress. These lines will be part of the side seams of the dress. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The fishtail skirt: </strong></p>
<p><strong>I have printed out and glued the entire skirt pattern. </strong>The gluing process is exactly the same as for the top of the dress. Miniature pictures on each sheet indicate the piece you are holding. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
