<p>I�d like to draw your attention to the red line in the hip area: you should refer to it when cutting the princess seam lines on the front of the fishtail skirt. The whole pattern has a fold line on it. You�ll need to add another 2cm for seam allowances of the middle seam at the back of the skirt. </p>
