<h3>Tutorial 33. Decorating the Dress and the See-Through Skirt with Fabric Applique.&nbsp;&nbsp;&nbsp; &nbsp;</h3>
<p></p>
<p><strong>It�s time to decorate the dress. </strong>I arrange the motifs and secure them with pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>And then I pin the upper skirt in place. </strong>Let me remind you that you�re free to make it detachable or non-detachable. I�m going to sew it on for good since I�m making this dress solely for demonstration and it will be easier to take pictures of the finished garment this way. But you can make a detachable version for your client. I have already mentioned that you can first decorate the belt area of the skirt with lace motifs and then sew snap caps around it. Their sockets are sewn onto the dress itself and whenever the skirt is removed you simply decorate them with flower caps or similar lace motifs. It all depends on your own creativity. </p>
<p><strong>If you decide to sew the skirt on, make a machine-stitch through all dress layers. </strong>The dress is not that thick. Just make sure all seams are open and spread out evenly before stitching over them. I�m going to sew the skirt on with hand stitches just to secure it before the photo shoot. </p>
<p><strong>I will decorate the joining seam with lace motifs I�ve prepared beforehand </strong>and embellish them with rhinestones and beads. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I attach the lace motifs with the help of Gutermann glue. </strong>You can also do it by hand-stitching but I haven�t practiced this method in a long time since this glue is really great and easy to use. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image006.jpg" class="img-responsive center-block"></p>
<p>I suggest you should first secure the motifs with the help of glue even if you do choose to sew them on by hand. You can then remove the pins and do it. The motifs will definitely shift if you don�t glue them in place and it�ll be really hard to restore the original symmetry. Proven by my own experience! Remember: first you secure the general motif layout and then glue smaller details onto it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>The key task in decoration is to arrange the motifs perfectly symmetrically. </strong></p>
<p>Even if your motifs are taken from a single piece of fabric and are non-mirrored, you can still refer to larger elements of the pattern or ornament. I lay the largest elements symmetrically and then arrange any adjoining details spontaneously. This way the main focus is placed upon the larger motifs while smaller motifs don�t stand out even if there�s not as much symmetry between them. The overall pattern still looks harmonious. </p>
<p><strong>You could also put the dress on the dress-form before decorating it </strong>- it�ll help evaluate the symmetry and the general layout of the motifs. A dress-form is a true must-have for sewing and decorating dresses! </p>
<p><strong>Thanks to fabric glue the entire decoration process literally lasts just a couple of minutes. </strong>I�d like to add that Gutermann fabric glue is water-resistant so you don�t need to worry about washing your garments. Besides lace secured with this glue stays snow-white for a very long time. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>After I finish decorating the top of the dress, I sew on the skirt and decorate the seam with lace like before. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2033_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The dress is now fully decorated! </strong>I have glued all lace motifs in place. Let me note that I�ve used motifs from five different kinds of lace fabric. Feel free to use leftover pieces of lace for decoration but make sure your motifs are coupled. In other words if you add a flower on the left side of the dress, you should add an identical flower on the right side, too. </p>
