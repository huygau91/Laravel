<h3>Tutorial 9. Cutting the Lace. Duplicating the Patterns. </h3>
<p></p>
<p><strong>Let�s continue cutting the pieces of our dress. </strong></p>
<p><strong>I have arranged the lace on the table in the lengthwise direction and placed all dress patterns on top of it. </strong>The width of the lace allows me to place only three patterns simultaneously. I�m going to take another piece of lace for cutting the side back pattern. Try to spare your lace fabric when laying out the pieces. I have put an ironing cover over the table - its soft surface prevents the lace from sliding so well that I don�t need to use any pins. </p>
<p><strong>I cut each piece along the perimeter. </strong>Don�t forget to transfer all notches onto the lace! They will serve as reference points and help assemble the garment accurately because lace fabric stretches well. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>You should separate the lace from the main fabric after cutting out the pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>And you do the following with the dress patterns: </strong>batiste pieces should be ironed onto the face and the lining. Let me show you how to duplicate the patterns correctly with individually cut iron-on pieces. </p>
<p><strong>The central back piece: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong>I unpin the part that includes batiste pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I bend the piece back (there are two face layers at the bottom and two layers of the lining at the top). I put down one layer of batiste with its fusible side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I put two face layers over the batiste. </strong>I try to match their edges as accurately as possible. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I take the next batiste piece, put it fusible side down and iron this �sandwich� carefully. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I put down another batiste piece fusible side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>And I repeat the same procedure. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image018.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>Then I turn the piece upside down and finish ironing it. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It�s the only way to secure separately cut iron-on pieces </strong>otherwise they will definitely shift against one another. If you unpin the sets and fuse each pair one by one, they will come out unlike and it�ll be nearly impossible to even them out. </p>
<p><strong>Unpin the piece if you�re ready to continue working. </strong>Put the lining aside and match the face pieces with the corresponding lace pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The side front piece: </strong></p>
<p><strong>Unpin the part that includes batiste pieces. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Keep in mind that we also have stiff iron-on pieces for the cups beside the batiste pieces. I bend back the main fabric. </strong>I always use the hip line notch as a guideline - the batiste should stop right there. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I put one layer of batiste fusible side up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>I put two face layers over the batiste. </strong>I try to match their edges and notches as accurately as possible. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>Then I take two batiste pieces </strong>and pin them to the main fabric. Please note: the first batiste piece is placed fusible side down and the second one - fusible side up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Then I place two lining layers over it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>And I put the last batiste piece fusible side down. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I press the whole �sandwich� from both sides:</strong> first by stomping it and then by ironing it through. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image042.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the piece upside down and finish ironing it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>There are stiff iron-on cup pieces, too. </strong>It�s much better to set my iron-on material with the help of an industrial press-iron with a steam generator. <strong>Don�t forget that stiff iron-on fabric is only used for duplicating the lining: you shouldn�t use it on the face pieces! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I turn it upside down and iron my stiff iron-on fabric onto the other half of the cup lining.</strong> Later I will tell you what to do if your iron-on fabric starts coming off. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>So, we have duplicated the pieces with batiste and reinforced the cups with stiff iron-on fabric. </strong>Now we can remove the rest of the pins and separate the face from the lining. I group the face pieces with the lace. </p>
<p><strong>The central front piece: </strong>it always requires more work because of the fold line. You have to unpin it all. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully put the face piece on the table with its shiny side up. </strong>You need to press the fold line flat. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>Then I lay the batiste piece over it, fusible side down, even it out using the notches as reference points, and secure the batiste by stomping it with the press-iron. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>It�s best to put the corresponding lining piece over the same piece </strong>and try to match all notches so that it coincides with the bottom piece perfectly. I wouldn�t recommend ironing it if your fabric is thin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I put the batiste piece over it, match the notches and secure it by stomping with the press-iron. </strong>I start moving the iron in a gliding motion once I�m sure the batiste doesn�t shift. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image066.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I put the stiff iron-on piece over the cups and iron it on. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>Please remember that the neckline will be lower, I haven�t finished cutting it yet. </strong></p>
<p><strong>I�d also like to point out that the bottom edge of the stiff iron-on piece should resemble a shoulder-yoke: </strong>the line rises and makes a semi-circle in the center. If it were straight, it would turn into a convex semi-circle when we�d start quilting the cups with bones. This would form an ugly crease in the middle of the cup on the finished garment looking as if you had put something underneath the bust. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I highly recommend that you re-adjust the shape of the stiff iron-on fabric: </strong>no matter how precisely you have cut it, it always flattens and expands after getting ironed. </p>
<p>I separate the face from the lining. </p>
<p></p>
<p><strong>The last piece is the side back piece. </strong></p>
<p><strong>I remove the lace and repeat the same procedure. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%209_files/image074.jpg" class="img-responsive center-block"></p>
