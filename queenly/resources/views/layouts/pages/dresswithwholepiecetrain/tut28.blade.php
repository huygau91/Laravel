<h3>Tutorial 28. Virtual Fitting Test. Drafting the Underskirt.</h3>
<p></p>
<p>Let me teach you how to perform a virtual fitting test which is necessary when you don�t have the opportunity to invite your client for a personal visit (for example when she lives in another city). </p>
<p><strong>We made a mock-up of our dress in the beginning of the course. </strong>If you sewed your mock-up after adjusting the pattern, then you can literally imitate your client�s figure by putting it on the dress-form and stuffing all hollows with padding polyester. You can perform a fitting test on this mock-up and dress-form at any time when in doubts. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I put the dress on the dress-form right over the mock-up to perform a virtual fitting test. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s check the back. </strong>Remember that our seam allowance is 2cm so the halves can�t possibly close the way they�re supposed to on the dress-form. </p>
<p><strong>Next I check the top open edge of the dress. </strong>You can tell that there will be about 4cm between the halves of the back if we lace it up and keep the 2cm seam allowance (which is what we have done). </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>There�ll a light tightening effect at the waistline - just like we wanted. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>We�ve hit the mark at the stomach and the hip level, too!</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s check the front of the dress. </strong>Check if you got the clip measurement right and if the garment sits well on the dress-form. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>This method allows you to review the garment at any stage of the sewing process. </strong>The dress is likely to fit your client well enough if it sits well on the dress-form with the mock-up. You could take a cord and lace it up for better accuracy. This makes the whole mock-up concept even more useful! </p>
<p><strong>If you look at the train of the dress, you�ll notice that it�s hanging rather than lying. </strong>But we want to achieve the same skirt shape as on the picture you saw before we started working. </p>
<p><strong>I put the lining of the dress backside up. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>It�s crystal clear we should do something about this lining to make the skirt acquire the shape of a train at the back and cascade down beautifully from below the buttocks. </strong></p>
<p><strong>I have folded the lining in half along the central seam </strong>and outlined the train part of the skirt that needs to be reinforced. </p>
<p><strong>This area needs some support and we will use bones for this purpose. </strong></p>
<p>We are going to sew bones along the central seam, the princess seam and the border of the train. It might be a good idea to add another vertical bone between the bones sewn along the central seam and the princess seam. </p>
<p>We will lay horizontal bones to prevent the train from folding and help it hold the shape. In other words we will create netting. The bottom horizontal bone will be placed 5cm above the bottom edge to leave some space for finishing it. </p>
<p><strong>In short we�re making a kind of a crinoline underskirt similar to those jet-setters used to wear in old good times. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I transfer the marked lines onto the other half of the train by holding it up to light to achieve perfect symmetry. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Our construction looks like this: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2028_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s start sewing on narrow Rigilene bones. </strong>First I will lay them along the perimeter the way we did on the cups and then I�ll push the ends of all vertical and horizontal bones underneath the perimeter bones sewn on along the outer side. I will slightly gather the horizontal bone to give it an arched shape. </p>
<p>Once the netting is ready, we�ll check how this underskirt looks on the dress-form and see if we need additional frills or if the construction is fine enough �as is�. </p>

