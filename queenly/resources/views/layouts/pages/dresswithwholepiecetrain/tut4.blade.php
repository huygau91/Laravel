<h3>Tutorial 4. Altering the Standard Pattern. </h3>
<p></p>
<p>Once you have printed out all pattern sheets, they should be arranged and glued together. </p>
<p><strong>Put the first and the second sheet together, carefully match all lines and squares, and secure them by gluing. </strong>If your printer has left white space at the edges just like mine (they are blank areas, not borders), <strong>do not trim them. </strong>The printer just can�t technically print at the very edges so it�s not a big deal. It should still be precise provided you�ve chosen to print in actual size. Ignore these blank areas at the edges, prolong the lines in your head and glue the sheets together. Here is the central front piece: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Repeat the same procedure with the second pattern - the side front piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p>For your convenience there is a small picture on each pattern sheet that shows how to arrange the sheets together. The sheet at hand is marked grey so you can easily tell what pattern you�re holding and what to match it with. Next you should assemble the sheets for the third and the fourth patterns. <strong>Our pattern is ready. </strong></p>
<p><strong>It�s time to apply alterations to the pattern in accordance with our calculations. </strong></p>
<p><strong>There are two ways you can go: </strong>you can cut the patterns out �as is� and sew a mock-up based on them. Then you�ll measure it, perform your calculations, transfer the paper patterns onto cardboard or plastic and keep them for future reference. It�ll be your standard pattern and you can mark all alterations right on it. Or you can use this pattern once and print it out anew next time. You�ll take your printed sheets, glue them together following me step-by-step, apply all determined alterations and use the adjusted pattern for cutting. </p>
<p><strong>I want to sew my mock-up using the pattern that�s already been adjusted in order to check whether we have done everything right. </strong>And I recommend that you do the same, especially if you�re a beginner! Alter the pattern, sew a mock-up and put it on the dress-form. Trust me, it�s very handy: you�ll be able to imitate your client�s figure on the dress-form and use it later to make a dress of the particular size. The only exception is the case when your dress-form is much larger than your client. </p>
<p><strong>Let�s start adjusting the standard pattern. </strong></p>
<p><strong>The central front piece:</strong></p>
<p><strong>The bust middle measurement has changed and we need to narrow the middle of the front by 1cm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>The clip towards the neckline also needs to be changed by 1.5cm, </strong>but I will apply this alteration later when changing the top line of the pattern. </p>
<p><strong>The side front piece: </strong></p>
<p><strong>We need to alter the side seam of the front. </strong>I shift it inwards by 0.5cm at the bust-line level, 0.5cm under the bust, 0.5cm at the waistline and at the stomach level, and leave it �as is� at the hip line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I draw a new smooth side line and straighten it a little at the top. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<p>Here�s another essential thing: I took the hip circumference measurement 19cm below the waistline. You should stick to the same level when applying alterations. If your pattern happens to be too short (for example, your client�s buttocks are located 22cm below the waistline), you should prolong it, re-draw the hip line and apply all alterations at the required level. On my pattern the hip line is 19cm below the waistline. <strong>I restore the notches on the new side line. </strong></p>
<p><strong>The side back piece: </strong></p>
<p><strong>I transfer the alterations onto the side seam of the back. </strong>The values are slightly different but the side line should have the same configuration as on the front piece. I shift it inwards by 1.5cm at the bust-line level, 0.5cm under the bust, 1.5cm at the waistline and at the stomach level, and 1cm at the hip line. Then I transfer all notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>There�s a small trick you can use: </strong>cut out the side front piece along its new side seam line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Place the side front piece over the side back piece, match their notches and copy the side seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>However there�s something you should keep in mind: </strong>you need to even out the length of the new side seam to make it identical to the initial side seam length. </p>
<p>Let me show you how to do it. I have glued another set of sheets 3 and 4 and cut out the side front piece to have its initial configuration at hand. The side seam of the side back piece has the same configuration so one pattern is enough. The length of the side seam changes whenever you alter it. In order to preserve the initial length, I place the initial pattern over the adjusted one, match their waistlines, and compare the length of the side seam. </p>
<p><strong>When you make such alterations, the length of the side seam changes inevitably at least by a couple of millimeters - either up or down. </strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the top of the seam. </strong>Later we will alter the clip and draw the final line. But for now it should look like this. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same thing with the back piece. </strong>I place the initial pattern over the adjusted side back piece and transfer the top of the side seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to alter the princess seam at the back. </strong>There are no alterations at the bust-line level but we need to shift it inwards by 0.5cm under the bust, 0.75cm at the waistline and 0.25cm at the stomach level. There are no alterations at the hip line. <strong>I re-draw the princess seam and transfer the notches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></p>
<p>It�s obvious that there will be some space between the top edge and our client�s back now and the edge won�t bite into the skin and force it to bulge out. Correct adjustments of the side seam and princess seams with regard to the client�s body type allow you to build a correct pattern. The dress will fit well and the client will feel comfortable. </p>
<p><strong>The central back piece: </strong></p>
<p><strong>Let�s use the same trick again: </strong>cut the side back piece along the adjusted princess seam and use it as a stencil for the second half of the back. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></p>
<p>Just to double-check it you could mark all alteration points on the princess seam of the central back piece starting from the under-bust line. Then you match it with the princess seam of the side back piece. In my case they match perfectly: the line passes through the reference points I�ve marked. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s look at the shoulder-blade area on the pattern. </strong>We don�t want the actual distance between the waistline and the shoulder blades to exceed 20cm. I have 19.5cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>Basically, we can cut out the back pieces now. </strong></p>
<p><strong>All that�s left to do is re-draw the location of the clip on the front piece. </strong></p>
<p>The clip towards the scye should be shifted by 2.5cm. The clip to the corner should be 10cm or 11.2cm together with the seam allowance. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I re-draw the top line with regard to the adjusted clip measurements. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p>In this particular case we are adjusting the standard pattern down. Those of you who are familiar with my courses know that you often have to make a dart underneath the bust when you make the pattern larger or quilt dome-shaped cups with bones. I delve into this issue in the previous edition of my Wedding Dresses DVD and book and in the books from the Dresses for Full-Figured Ladies series. You don�t have to deal with this issue when your client has small breasts. </p>
<p>The princess seam on the side front piece became 2.1cm shorter after I adjusted the clip measurements. <strong>I shorten the princess seam of the central front piece by the same 2.1cm and shift the clip towards the neckline down &nbsp;by 1.5cm. Then I draft the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image046.jpg" class="img-responsive center-block"></p>
<p>I bend back the �seam allowance� on the side front piece and place it parallel to the seam allowance of the central front piece in order to make sure their top lines match well and form a smooth transition. </p>
<p><strong>I match the front patterns and draw a pretty top line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>The top of the dress looks like this with its neckline enhanced: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%204_files/image050.jpg" class="img-responsive center-block"></p>
<p>And now we can cut out the patterns following their adjusted outlines and sew a mock-up of our dress. </p>

