<h3>Tutorial 23. Finishing the Lacing Area. </h3>
<p></p>
<p><strong>We need to perform certain procedures with the front and the back of the dress before sewing up the side seams. </strong></p>
<p><strong>The front of the dress: </strong></p>
<p><strong>I join the face with the lining along the side seam down to the hip notch, </strong>i.e. the end of the seam that joins the lace with the face. I stitch at a presser-foot distance from the open edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I make a symmetric seam from the opposite side of the dress front. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p>I serge the open sides in a single line down to the hip line and serge the edges of the face and the lining separately below the hip line. </p>
<p><strong>The back of the dress:</strong></p>
<p><strong>Let�s sew up the pinned-down seams to finish the zipper with the lining.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I do the same from the other side of the zipper. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now we need to sew the central seam of the lining. </strong>I fold the lining face-to-face and sew it with a 2cm seam allowance. You should step 4-5cm from the beginning of the zipper to prevent the central seam from pulling down on the garment. </p>
<p>You could serge all seams down to the level of 50cm above the bottom of the dress if you want to. The central seam must be pressed open. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I join the face with the lining in the lacing area. </strong></p>
<p>I stitch along the lacing bar at a presser-foot distance from the edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>And I do the same from the opposite side of the lacing area. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>Here�s a simple way to make a smooth transition from the lacing to the zipper. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I join the face and the lining along the side seam down to the hip level - like I did on the front part. </strong></p>
<p>You can secure it with pins at the sides if you don�t feel confident enough. I carefully spread the fabric with my hands and match the notches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I join the face and the lining of the back along the other side seam. </strong></p>
<p>I serge all layers down to the hip level and then serge the open edges of the face and the lining separately. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>It�s time to finish the lacing bars. </strong></p>
<p><strong>If you don�t want a gap between the central open edges of the back,</strong> you should bend the lacing area back by 2cm at the top and down the bottom. </p>
<p><strong>And if you prefer to have a small triangular opening at the back, </strong>then you can bend a little more at the top and bend the same 2cm down the bottom. <strong>This option has the following advantages: </strong></p>
<p><strong>- Firstly, </strong>a triangle like this performs an aesthetic function: a triangular opening at the back of a dress always looks really pretty. </p>
<p><strong>- Secondly,</strong> this triangle performs a practical function, too. It will allow you to adjust the degree of tightening in case you�ve got the size slightly wrong. It is certainly not the best way out but a kind of a last-resort method when nothing else works! </p>
<p><strong>I mark the triangle in the lacing area. </strong>I mark 4cm at the top and 2cm down the bottom, connect the points, and stitch the to-be fold line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I make the same kind of a stitch on the other lacing bar. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>Next you need to stitch Rigilene bones onto these seam allowances that you will then bend back. </strong></p>
<p><strong>I take two narrow Rigilene bones. </strong></p>
<p><strong>The first bone </strong>should be sewn along the stitched fold line, 1mm outwards from it to make sure you can bend the fabric over the bone so that the stitch line stays on its rib. This bone will reinforce the edge of the lacing bar. I tape the ends of the bone with pieces of masking tape. I sew it on with two parallel stitches leaving 0.5cm from the top and bottom open edges of the lacing area. </p>
<p><strong>The second bone is sewn on parallel to the first one. </strong>It will reinforce the grommets area to help them hold well and prevent them from jumping out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>So, I�ve made the lacing bar more robust. </strong></p>
<p><strong>Now I bend back the lacing bar and make a stitch about 1.2cm from the edge. </strong>&nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>We�ll be able to insert a stiff plastic bone into the fabric tunnel we�ve made </strong>to strengthen the edge of the lacing bar and avoid deformation when the dress is laced up. You can only insert this plastic bone after setting the grommets, otherwise it might interfere. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I finish the lacing bar on the other half of the back exactly the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Please note the way I turn the garment to even out the line of the seam that attaches the second parallel bone. </strong>The bones sewn along the princess seams make the central seam curve and acquire the shape of the dart. You should turn the garment so that the dart is on the princess seam and make the bones in the lacing area lie straight. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image056.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>I stitch 1.2cm from the edge. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>I suggest we should make an additional strengthening stitch </strong>5-6mm from the first one <strong>to prevent the lace from breaking out from underneath the grommets. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>It�s important to make sure both lacing bars are absolutely identical. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2023_files/image066.jpg" class="img-responsive center-block"></p>

