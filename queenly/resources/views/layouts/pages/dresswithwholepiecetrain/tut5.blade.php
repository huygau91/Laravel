<h3>Tutorial 5. Cutting the Mock-Up. </h3>
<p></p>
<p><strong>So I have taken my mock-up fabric and arranged the adjusted patterns on top of it. </strong>I place the hip line at the selvage. I always cut fabric crosswise - it�s easier and it helps reduce fabric consumption. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>I have traced the patterns, transferred all notches and marked the major lines </strong>so I can still see them after finishing the mock-up and putting it on the dress-form. I secure the layers with a few pins to prevent the fabric from sliding. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I start cutting out the patterns. It�s compulsory to transfer the notches. </strong>You could iron the fabric a little to make the surface perfectly smooth. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
<p>Besides I have marked the waistline level on the fold line to highlight its location. &nbsp;</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>There are certain fine points that concern the middle of the back. </strong>There will be both a zipper and a lacing area in the middle. The lacing area will stop 7cm below the waistline and the zipper will go down from there. <strong>It�s very important to make a notch at the end of the lacing area. </strong>Later I will also make a notch on the central seam of the skirt where the zipper stops. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>How come the lacing area should by no means stop at the waistline? </strong></p>
<p>The thing is the spine of a healthy human has a particularly distinct natural curve at this very spot. So if your lacing area stops at the waistline, there�ll be an opening between the lacing and the zipper when the dress is laced and zipped up. It doesn�t look pretty at all! I have seen it lots of times on low-quality corsets made in China. That�s why the lacing should always stop at least 4cm below the waistline - the best choice is 6-7cm because in this case the back of the dress will be snug against the skin and the woman will feel comfortable. </p>
<p>The lacing area can also stop 12cm below the waistline if you wish to increase the tightening effect and restrict the stomach. It will also help re-shape the upper part of the hips in a nice way. However I wouldn�t recommend prolonging the lacing area to the hip line. </p>
<p>I prefer tracing paper patterns rather than pinning them to fabric. I cut patterns following the outlines. </p>
<p><strong>I haven�t marked the under-bust line across all patterns since its location on the back changes after you join them.</strong> </p>
<p>Although you might feel like drawing a straight line, it is actually somewhat curved at the back. I will mark the under-bust line on the dress-form later. By the way I measure the pattern with regard to the adjusted location of the under-bust line; I take actual measurements off the mock-up. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I have cut all pieces of the mock-up. </strong>Now I�m going to join them along their vertical princess seams and put the mock-up on the dress-form. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%205_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>What are the advantages of this preparatory work? </strong></p>
<p>- You can check whether your calculations are correct; </p>
<p>- You can study the way the pieces are joined (particularly in the chest area); </p>
<p>- You can determine whether the top of the neckline looks good enough; </p>
<p>- You can check the appearance of the side seams and the way the back fits after all adjustments. </p>
<p><strong>I will show you how to imitate your client�s figure on the dress-form using shoulder pads or padding polyester. </strong>You�ll put the mock-up on the dress-form and simply leave it there. This way you�ll basically have a dress-form that repeats your client�s body shape. Imagine how handy it is! It will replace a custom-made dress-form manufactured after a particular client�s body! </p>
<p>In the next tutorials I will explain how to work with the dress-form and make it your true helper. </p>
<p>You could actually carry out the first fitting test on your adjusted dress-form without the client. </p>

