<h3>Tutorial 24. Setting Grommets into the Lacing Bars. </h3>
<p></p>
<p><strong>At this stage you can set grommets into the lacing bars provided you don�t have any doubts concerning the size of your garment. </strong></p>
<p><strong>However if you do have certain doubts, </strong>you should postpone this task for the last moment. It�s just that it�s easier to install grommets when the garment is spread flat. </p>
<p><strong>First of all we need to mark the location of holes for the grommets. </strong>The intervals between them should not exceed 4cm. </p>
<p>Moreover: the larger the garment is the smaller the intervals should be, i.e. <strong>there is an inverse proportion between the size of the garment and the intervals between the grommets. </strong></p>
<p>The more full-bodied the woman is the higher degree of tightening the garment requires. Therefore there should be smaller intervals between the grommets. This will help preserve the shape of the back and protect it from deformation. </p>
<p>And vice versa - the smaller the garment is the less tightening is required, so you can make larger intervals between the grommets. </p>
<p><strong>I step 1.2-1.5cm from the top of the bar and measure the length of the grommets section. </strong></p>
<p><strong>It is 24cm long. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Since the size of our dress isn�t big, I decide to use 4cm intervals. I make corresponding markings. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I set the hole-cutting die onto my hand press and punch the holes according to the markings. </strong>&nbsp;I highly recommend placing a piece of cardboard underneath the fabric - it will make the holes cleaner. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I punch perfectly symmetric holes on the other lacing bar. </strong>I simply put the first bar over the second one, match their edges precisely, and mark reference points through the holes punched in the first lacing bar. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>This helps you achieve perfect symmetry of the lacing bars. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I�m using 0.5cm grommets with washers. </strong></p>
<p><strong>One by one I insert each grommet into the hole, put the washer in place and set it with the hand press grommet machine. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>We have inserted the grommets! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Next I insert a plastic bone into the tunnel </strong>after measuring the required length. It shouldn�t reach the top border of the tunnel. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>The lacing bar is ready! </strong>You can stitch up the bottom of the tunnel before inserting the bone. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2024_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>The second lacing bar is finished exactly the same way. </strong></p>

