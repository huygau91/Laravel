<p><strong>I press the seams on the lace part flat towards one side. </strong>Their seam allowances settle down neatly because we notched the lace at the end of the merged side seam. </p>
<p>At this point you should double-check how well the lace is sewn on and make sure the seams are not pulled too tight. You will have to re-stitch it if the lace doesn�t make a smooth surface and if its princess seams are a lot shorter than on the base of the dress. </p>
<p><strong>What is a good way to remove beads or sequins off the lace near the seam? </strong>Some people simply rip them off and then sew them back on. But I prefer crushing them with a small hammer on all seam allowances. I do it on a firm surface. I carefully crush the beads without tearing the thread. It prevents other beads or sequins from falling off. </p>
