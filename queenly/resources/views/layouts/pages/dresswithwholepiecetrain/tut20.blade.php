<h3>Tutorial 20. Pressing the Face of the Dress. </h3>
<p></p>
<p>Now we should press all seams we have just sewn. <strong>I suggest that you press all seams open along the entire length on the face parts of the dress and press all seams flat on the lace. </strong>I would like to remind you that I�m not doing the overlock finish on any princess seams. You can do as you wish though. </p>
<p><strong>I start from the back piece: </strong></p>
