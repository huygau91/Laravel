<h3>Tutorial 22. Finishing the Front of the Dress. Joining the Face of the Back with the Lining. </h3>
<p></p>
<p><strong>Let�s join the face with the lining along the top of the back. </strong>I sew from the side of the lining following the stay-stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I put the other half of the back next to it and continue sewing in a single stitch line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins, check the seam quality, unfold the garment, and make a clean-finish seam directing the seam allowance towards the lining.</strong> I stitch literally 1mm from the joining seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image009.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image011.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image013.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image015.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image017.jpg" class="img-responsive center-block"></p>
<p><strong>Once the clean-finish seam is done I trim the seam allowance at the top of the back</strong> to about 0.5cm. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image019.jpg" class="img-responsive center-block"></p>
<p><strong>And again, I notch out the seam allowances of the princess seams to avoid unwanted thickenings - just like I did on the front part of the dress. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image021.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The same procedure is then performed on the other half of the back. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>That�s what the top edge of the back looks like before you press it: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image026.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let�s go back to working on the front half of the dress. </strong></p>
<p><strong>We have turned it out: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image028.jpg" class="img-responsive center-block"></p>
<p>I�m sure you know how hard it is to turn out and press the corner of the neckline. No matter how accurately you notch it out, there always tends to be a tuck or a crease of some sort. <strong>Let me share a trick that will solve this problem for good. </strong>I take a small piece of iron-on batiste, fold it in half and notch out a corner that matches the one on the neckline. I place the batiste fusible side down right below the neckline and iron it on. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image032.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully press the neckline area to make this spot as smooth as possible. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image036.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image038.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let�s work on the cups now. </strong>I put the first cup on the cup pressing mould and spread all fabric layers very carefully. Try to pull the fabric so as to help all seam allowances spread out. <strong>You should alternate between dry and wet pressing. </strong>First dampen the fabric by steaming, then dry it with the press-iron, wait for it to cool down and dab it dry with a cushion. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image040.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image044.jpg" class="img-responsive center-block"></p>
<p><strong>Look at the difference between the cups before and after pressing: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I press the second cup. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image048.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image052.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>Even after very thorough pressing there is often some hollow space below the cup in the area from the open edge to the princess seam. </strong>But we want the shape of the face to match the lining as well as possible. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image055.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I�ll show you a good method to solve this issue, eliminate the hollows and help the face acquire the precise shape of the lining. </strong></p>
<p><strong>I use fusible web to join the face with the lining in the problem area. </strong>The trick seems obvious yet it doesn�t occur to everyone. </p>
<p><strong>Please note that you can do this only after pressing the entire piece - after making sure it�s all good and you are ready to finish working on the dress! </strong></p>
<p><strong>I take a strap of fusible web of the required width </strong>and place it fusible side down over the lining so that a part of it is on the cup and a part of it below it. We want to duplicate this very spot and achieve the right shape of the face. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image057.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image059.jpg" class="img-responsive center-block"></p>
<p><strong>Then I pin together the face and the lining along the side seam pulling up the cup to give it the desired final shape. </strong>It is a crucial moment! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image061.jpg" class="img-responsive center-block"></p>
<p><strong>I fuse the layers together by pressing them with the hot press-iron (without steaming).</strong>&nbsp; The dress becomes monolithic in the right place. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image063.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image065.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image067.jpg" class="img-responsive center-block"></p>
<p><strong>I repeat the same procedure with the other cup.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image069.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image071.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image073.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image075.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I remove the pins and press the open edges near the cups. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image077.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image079.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I pin the open edges yet again to secure the pressed layers. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image081.jpg" class="img-responsive center-block"></p>
<p><strong>I even out the sides and remove any �extra� lining fabric that poked out after we quilted the cups. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image083.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image085.jpg" class="img-responsive center-block"></p>
<p><strong>The front half of the dress is ready! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image087.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Let�s work on the back of the dress. </strong></p>
<p><strong>First I press the top edges of the back pieces. </strong>I spread them out carefully and press them with the steam on and then dry them by pressing them without using the steam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image089.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image091.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to connect the zipper with the lacing area in the center of the back and ensure a smooth transition between them. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image093.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the central open edges of the back down to the zipper level. I am guided by the waistline notches and the beginning of the zipper. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image095.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the tips of the zipper tape and secure them with pins. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image097.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image099.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image101.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I deepen both notches at the beginning of the zipper basically through to its teeth. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image103.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image105.jpg" class="img-responsive center-block"></p>
<p><strong>This way I have separated the lacing from the zipper. </strong>Now we can join the seam allowances of the lining with those of the seam that attaches the zipper. I match the notched edges and then match the seam allowances of the face with the zipper on it and the seam allowances of the lining in the area between the notches at the beginning and the end of the zipper. For now I secure it with pins from both ends and later I�ll machine-stitch it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2022_files/image107.jpg" class="img-responsive center-block"></p>
