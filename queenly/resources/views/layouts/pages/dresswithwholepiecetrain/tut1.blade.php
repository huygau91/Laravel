<h1>Tutorial 1. Getting Acquainted with the Model.</h1>
<p><strong>The picture below demonstrates a schematic drawing of the dress we are going to sew. </strong>It�s a base for our future dress.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%201_files/image002.jpg" alt=""></p>
<p>The dress will consist of a lining quilted with bones and covered up with face fabric, and I will overlay the face with delicate lace.</p>
<p><strong>What is the model like? What pattern are we going to use? </strong></p>
<p>There are rounded cups <strong>at the front</strong> and the armscye areas are very pronounced, i.e. there are distinct corners. The neckline is low. It can plunge to the waistline or lower, or stop at the under-bust line or above it (a classic V-neckline stops 3-4cm above the bust line). The pattern I offer you suits all of these but it is not suitable for dresses with a straight neckline or a boat neck.</p>
<p>Since it is a corset-based dress with a tight fit, it�s important to add supporting bridges to prevent the edges of the neckline from coming apart. I recommend that you make bridges even if you plan to use a nude-colored insert made of bobbinet fabric to imitate bare skin. Bobbinet won�t be robust enough to handle the tension on its own. You could make bridges by folding that bobbinet multiple times. <strong>There has to be some kind of support for the bust! </strong></p>
<p><strong>The dress will be covered with a beautiful lace overlay </strong>that I�ve sketched schematically.</p>
<p><strong>The skirt is bell-shaped.</strong>&nbsp; What does it mean? The skirt fits rather tightly at the hips, slightly narrowed below the hip line to create a more elegant silhouette, and flares towards the bottom. Those of you who are familiar with corset-based dress patterns know that it�s impossible to sew a one-piece dress like this without vertical princess seams going from top to bottom! That�s why we will definitely make princess seams reach to the very bottom. And I suggest we make them flare starting from the hip line, by maximum 5cm from each side of the princess seam. This is done just to ensure that you can move freely in this dress, so it�s not too tight.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%201_files/image004.jpg" alt=""></p>
<p><strong>Now let�s move to the back of the dress. </strong></p>
<p>The back is absolutely straight at the top - you won�t need any additional measurements. It�s up to you where you want to add straps or not. And I�m going to make a train with the help of fishtail elements at the back of the dress. It won�t be longer than 20cm. I�d like to draw your attention to the fact that all garments sewn by me in my tutorials are not made for particular clients - they are made for purely educational purposes. That�s why the train will be rather schematic just to show you how to prolong the dress and determine the degree of flare. The entire back of the dress will also be covered with lace.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%201_files/image006.jpg" alt=""></p>
<p>But that�s not all! <strong>We will make an additional skirt for the dress once it�s finished. </strong>Transformer dresses are on trend today and I�d like to use a lightweight full skirt made of tulle. I have chosen tulle used for bridal veiling but you can also use stiffer mesh fabric. Our skirt will be semi-circle with a prolonged train, slightly gathered at the back in accordance with the initial dress model. The skirt can be detachable or sewn onto the dress. I�m going to sew it on to speed up the process as I believe making a separate belt for a detachable skirt is not a challenge for you. The skirt will be decorated with lace appliqu�.</p>
<p><strong>Below is another rough sketch. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%201_files/image008.jpg" alt=""></p>
<p>Lace applique will be located in the belt area. I�d like to say a couple of words about cutting a semi-circle skirt. You probably know that the degree of flare at the front and at the back will be the same if you fold the semi-circle in half and locate the joining seam at the side. But if you locate this seam at the back, the front will be less flared because of the straight lengthwise thread and most of the flare will be at the back. That�s exactly what we want! We want to have a light flare and gracefully hanging folds of translucent fabric at the front and leave the main volume of the see-through skirt at the back. I�m going to gather it slightly in the waistline area to emphasize the effect. The skirt will feature a long train and I�ll finish its hemline with contoured lace to make it stand out.</p>
<p>This dress style is a real hit! Please feel free to use your creativity and original ideas to alter my models for your own purposes. Trust your skills and experience!</p>
<p>Let me repeat: this dress base basically provides endless opportunities! You could, for instance, keep the top unchanged and alter only the cut of the skirt. <strong>I prepared my Pattern Constructor tutorial to explain you how to do such things.</strong> &nbsp;Pattern Constructor offers the basic know-how required for cutting a one-piece dress with different skirt styles. You can also use different fabric for your detachable skirt: chiffon, organza, crepe, or maybe the same fabric you�ve chosen for the dress, the same lace you have covered it with, etc. The skirt may or may not feature appliqu� or hem decoration, or a train; it can be bouffant or not.</p>
<p>I simply want you to remember that the basics I teach you actually enable you to sew a great diversity of wedding <strong>and evening </strong>dresses.</p>
<p><strong>Don�t think of this course as dedicated strictly to wedding dresses. </strong>The most elegant evening gowns are sewn using the same methods and techniques. And they are good enough to wear to the Oscar�s.</p>

