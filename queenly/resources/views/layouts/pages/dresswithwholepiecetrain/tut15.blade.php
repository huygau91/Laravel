<p><strong>I move to the other cup. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2015_files/image010.jpg" alt=""></p>
