<p><strong>I press the area around the cup and the vertical bones on the flat surface. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2015_files/image014.jpg" alt=""></p>
