<h3>Tutorial 15. Pressing the Lining Quilted with Bones.</h3>
<p><strong>I place one cup over the cup pressing mould and start working on it. </strong>I believe it makes sense to put the pressing mould onto a sleeve board to lift the entire garment above the surface of the table so it hangs down from the sides and you can easily press individual areas.</p>
<p><strong>I move the press-iron in a circular motion and use a lot of strong steam. </strong>I press the cup from all sides carefully distributing the fullness we�ve created to make the cup dome-shaped. I only use strong steam at the very beginning and afterwards I try not to use any at all and simply dry the cup and spread it out with the help of the press-iron.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2015_files/image002.jpg" alt=""></p>
<p><strong>Sometimes the inside of the cups or the face of the lining looks somewhat rippled.</strong> This depends entirely on the quality of fabrics and interfacing you are using. I try to make it as smooth as possible.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2015_files/image004.jpg" alt=""></p>
<p><strong>Make sure you carefully press the area underneath each cup where we eased in a lot. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2015_files/image006.jpg" alt=""></p>
