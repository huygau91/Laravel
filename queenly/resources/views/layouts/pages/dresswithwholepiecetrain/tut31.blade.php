<h3>Tutorial 31. Calculating and Cutting the See-Through Skirt. &nbsp;&nbsp;</h3>
<p></p>
<p><strong>We need to sew a decorative skirt with see-through tulle. </strong>I�m using special skirt tulle. It is robust synthetic fabric that�s basically impossible to tear unlike thin tulle used in children�s clothing. This skirt tulle is so transparent that you can hardly notice it on my hand: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s calculate the dimensions of our skirt. </strong></p>
<p><strong>The waist circumference or the circumference of the area where we want to attach the skirt is 66cm. </strong>I have measured our dress at the level where I want to attach the skirt. </p>
<p><strong>The front of the skirt is about 120cm long (turn up included). </strong></p>
<p><strong>The length of the skirt from the back is measured the following way: </strong></p>
<p><strong>First you draw the waistline, the bottom and the sides of the skirt. </strong>The back of the skirt will look really beautiful if the section of the hemline from the side seam to the central seam of the back has the same length as the length of the skirt front. That�s why our task is to measure the length of the central seam at the back of the skirt (X). You will find the length of the skirt back by adding X to the train section that is 30cm long. </p>
<p><strong>I calculate the length of the central seam of the back using the right triangle formula </strong>(as if it were a hypotenuse). What I get is 170cm. </p>
<p><strong>This means the length of the skirt back together with the train is about 2m. </strong></p>
<p><strong>Our skirt will be cut following the semi-circle skirt principle. </strong>If you use the method for cutting full-circle skirts, it will be very full at the top and down the bottom and won�t lie as beautifully as we want. Our particular model looks elegant exactly because the skirt is not so full at the top and yet very graceful at the back. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p>You probably know that the folds of a semi-circle skirt will be distributed evenly between its front and back if you join its open edges and locate the seam on the side. But if you locate it in the middle from the back of the skirt, most folds will pass onto the back and the front will look more modest and smooth. That�s exactly what we want. We want the skirt to have more folds on the train and as few as possible around the waistline - the front should look balanced. </p>
<p><strong>We�re going to calculate the pattern of our semi-circle skirt upon the assumption that the central seam at the back is 2m long and the front of the skirt is 120cm long. </strong></p>
<p><strong>Let�s calculate the radius of the waistline (R).</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image008.png" class="img-responsive center-block"><strong>r</strong></p>
<p><strong>According to the calculation formula r = 21cm.</strong></p>
<p><strong>Therefore we need a sheet of fabric that is 4.5m long and 1.5m wide. </strong>The width of our mesh fabric is 3m which means we can make a double-layer skirt. It will be stiffer and hold its shape better. </p>
<p>So we need to cut a semi-circle. The rounding should be very precise in order for the skirt to settle well. </p>
<p><strong>Let me demonstrate the cutting technique on a paper mock-up. </strong>I�ll use a 20cm radius for more convenience. </p>
<p><strong>I take a paper rectangle folded in half along the shorter side. The longer side is 220cm and the shorter one is 140cm. </strong>If you want to cut a double-layer skirt, your fabric should be folded in half along its longer side as well. </p>
<p><strong>So here is our fabric sheet:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image010.jpg" class="img-responsive center-block">&nbsp;</strong></p>
<p><strong>I fold the fabric twice to make a cone, mark the radius of 20cm, cut it and unfold it: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Now there is an opening for the waist. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image020.jpg" class="img-responsive center-block"></p>
<p>Next I need to round off the bottom edge of the pattern with a 140cm radius. This is done the same way as previously. <strong>I fold the fabric from the bottom upwards twice to make a cone, mark the radius (140cm), cut it and unfold it. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>The edge is very smooth and even. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image028.jpg" class="img-responsive center-block"></p>
<p><strong>The skirt pattern is ready! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The skirt will look like this after you sew up the central seam: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>You can even out the sides of the skirt if they seem too long. </strong>But I suggest you should put the skirt on your client before doing that. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2031_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>I will cut the skirt out of mesh fabric in correspondence with the mock-up and show you the final result. </strong></p>

