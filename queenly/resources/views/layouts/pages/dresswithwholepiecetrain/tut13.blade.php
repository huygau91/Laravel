<p><strong>I lay inclined vertical bones inside the cup: they start at the under-bust line and go upwards diagonally. </strong>I sew them on with two parallel stitches and push their ends underneath perpendicular bones that overlay the princess seams. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image116.jpg" class="img-responsive center-block"></p>
