<p><strong>And I do the same on the other cup: </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image124.jpg" class="img-responsive center-block"></strong></p>
