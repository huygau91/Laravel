<p><strong>There are two more bones that should be placed along the top and the bottom edge of each cup. </strong>Gathering these bones is compulsory since they should be arched. You mustn�t ease in any fabric underneath these bones though! I recommend pressing the cups before you start sewing them on if you fail to do it without easing in the fabric. That way the cups will be rounder and more even and it�ll be easier to lay down the rest of the bones. </p>
