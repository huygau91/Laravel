<h3>Tutorial 13. Forming Dome-Shaped Corset Cups. </h3>
<p></p>
<p><strong>First I�m going to sew bones onto the joined back pieces. </strong></p>
<p><strong>I stitch a wide Rigilene bone along the princess seam at the back. </strong>I place it 3-4mm below the stay-stitch and tape its end with a piece of masking tape. I overlay the princess seam with the bone by placing it right in the middle and attach it with two parallel stitches. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image002.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I don�t cut the bone in advance</strong> but trim it in the process. It should be 1cm longer than the bone on the side piece of the back. I tape the other end of the bone, too. Remember to make bar tacks at the beginning and at the end of the stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I make a parallel stitch along the other side of the bone. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image012.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I attach the vertical bone on the side piece of the back. </strong>I tape its end over with masking tape, place the bone centered over the marking, make a bar tack and sew it on. I spread the fabric underneath the bone to make sure it�s settled straight. This bone serves to protect the fabric from distortion. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone leaving 3-4mm to the stay-stitch, tape its end over and finish the stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I make a parallel stitch along the other side of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image020.jpg" class="img-responsive center-block"></p>
<p><strong>We have attached both bones on the first half of the back. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>Now we should do the same on the other half. </strong></p>
<p><strong>I start from the vertical bone on the side piece of the back. </strong></p>
<p><strong>And again, I step 3-4mm down from the stay-stitch. </strong>If you place a bone too close to the edge, it�ll protrude when you turn out the garment, and there will be noticeable thickening at the back. I prepare pieces of masking tape in advance and stick them to the edge of the sewing machine. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image026.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make a parallel stitch along the other side of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I secure the bone over the princess seam with two parallel stitches. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image032.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The back of our dress is now quilted with bones!</strong> &nbsp;</p>
<p><strong>It�s time to work on the front of the dress and particularly on the cups. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I take our narrow Rigilene bones. </strong></p>
<p><strong>You should gather the bones that pass along the curved areas to make them somewhat arched. </strong>First I gather the bone by pulling fishing lines out of it and then I finish shaping it by pulling the outermost fishing line. I trim the ends of fishing lines afterwards.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image038.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I lay a bone in the armscye area. </strong>As usual its end is taped with a piece of masking tape. Our seam allowance is 2cm so I place the end of the bone 5-6cm inwards from the open side edge. I follow the stay-stitch with the bone placed 1mm below it. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>For now I�m only stitching along the outer side of the bone. </strong>I stop 2-3mm from the corner, trim the bone, tape its end and make a bar tack. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image045.jpg" class="img-responsive center-block"></p>
<p><strong>I continue laying bones along the perimeter of the cup.</strong> I tape the end of the next gathered bone and push it underneath the bone I�ve just sewn in the armscye area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>The bone follows the stay-stitch and is placed 1mm below it. </strong>I stitch along its outer side without easing in the fabric until I reach the princess seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Upon reaching the arched part of the neckline I start easing in the fabric underneath the bone to ensure a good fit in the chest. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>You shouldn�t ease in the lower neckline area. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>The central bone passes along the marked cut of our soon-to-be low neckline, 1mm aside from the stay-stitch. I don�t recommend overlapping such central bones. I�m going to lay another central bone parallel to the first one. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image054.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone as specified by the marking. I tape its end with masking tape and finish the stitch. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image056.jpg" class="img-responsive center-block"></strong></p>
<p></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image058.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a parallel stitch along the other side of the bone going from the bottom upwards to the level 2-3cm below the under-bust line. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image060.jpg" class="img-responsive center-block"></p>
<p><strong>And I lay the second central bone </strong>going from the bottom upwards. It stays parallel to the first bone up to the neckline and then it continues along the neckline 1mm from the stay-stitch. The bone stays straight (ungathered) in this area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>You should gather this bone starting from the arched section of the neckline. </strong>I measure the required length of the bone, account for a little more for its ends and gather it from the opposite end. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I start easing in the fabric underneath the bone in the arched area of the cup and up to the princess seam. I try to achieve the same degree of fullness as on the other half. </strong>Remember: the neckline won�t be snug enough against the bust if you don�t add fullness in these areas! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image068.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone a couple millimeters from the intersection of stay-stitches; tape its end and finish sewing it on. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I lay a symmetric bone in the armscye area from the other side. </strong>I tape the end of the arched bone with a piece of masking tape and push it underneath the central bone stitched along one side. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone 5-6cm from the edge and tape its end. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>I finish attaching the bone in the armscye area. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the second central bone with a parallel stitch going from the bottom upwards to the level where you started sewing on the first central bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>Next we should lay a bone along the bottom of each cup to finish working with perimeter bones. </strong></p>
<p><strong>I push the non-taped end of the gathered bone underneath the perimeter bone in the armscye area and stitch along its outer side down to the bust line level, without easing in the fabric. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>I start easing the fabric underneath the bone from the bust line down to the princess seam giving the cup a dome shape. </strong>Be careful to avoid tucks and creases when easing in. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>I stop easing the fabric as I reach the princess seam and continue sewing as per usual. I use the edge of the stiff iron-on fabric as a guideline. I trim the bone in the middle of the perpendicular central perimeter bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image084.jpg" class="img-responsive center-block"></p>
<p><strong>I push the end of the bone underneath the central bone of the cup, finish sewing it on and make a bar tack at the end. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>And again, I push the end of the gathered bone underneath the other central perimeter bone. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image088.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a bar tack and sew without easing in right to the princess seam. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image090.jpg" class="img-responsive center-block"></p>
<p><strong>I start easing in upon reaching the princess seam. </strong>The bone should be centered over the marked arched line. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image092.jpg" class="img-responsive center-block"></p>
<p><strong>I reach the bust line and trim the bone at about the middle of the armscye bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image094.jpg" class="img-responsive center-block"></p>
<p><strong>I stop easing in the fabric, finish sewing on the bone and make a bar tack at the end. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image096.jpg" class="img-responsive center-block"></strong></p>
<p><strong>We are done with perimeter bones. The cups look nicely round thanks to the fullness we�ve created. </strong>If you notice that you�ve eased in one half more than the other, you can try to re-distribute excessive fullness. Gathered bones allow you to do this. Your cups will be even. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image098.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s start quilting the inside of the cups with bones. </strong></p>
<p>I suggest we should slightly gather each bone even if it�s supposed to lie in a straight line in order to make the cups smoother on the inside. </p>
<p><strong>I lay a bone along the bust line. </strong>I push its end underneath the perimeter bone and start sewing. </p>
<p><strong>It is highly important not to let the fabric ease in underneath the bone now. </strong>I pull the fabric to me with my left hand while pushing the bone away from me with the right hand. This creates tension that makes the cup rounder. Be careful not to pull too much or stretch it out! But most importantly - do not let the fabric ease in underneath the bone! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image100.jpg" class="img-responsive center-block"></p>
<p><strong>I trim the bone and push its end underneath the central bone placed over the princess seam. </strong></p>
<p><strong>I prefer attaching bones in pairs. </strong>As soon as I�ve sewn a bone along the bust line on one cup, I lay a symmetric bone on the other cup as well. </p>
<p><strong>I push its end underneath the central bone on the princess seam and sew it on along the bust line. </strong></p>
<p><strong>It�s desirable to lay all bones at the same level, i.e. maintain symmetry inside the cups. </strong>The neckline of the dress will go askew if you overlook asymmetry of bones inside the cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image102.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I trim the bone and push its end underneath the bone that overlays the princess seam. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image104.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I secure both symmetric bones with the second parallel stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image106.jpg" class="img-responsive center-block"></p>
<p><strong>Next I lay bones over the princess seams on the cups. </strong>I sew them on with two parallel stitches and push their ends underneath perpendicular bones overlaying the princess seams. </p>
<p>Feel free to use either gathered or perfectly straight bones. <strong>But remember: your cup will be flat if you let the fabric ease in along the bust line and along the princess seams! </strong>I recommend trimming the threads on both halves of the dress after attaching each pair of symmetric bones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2013_files/image108.jpg" class="img-responsive center-block"></p>
