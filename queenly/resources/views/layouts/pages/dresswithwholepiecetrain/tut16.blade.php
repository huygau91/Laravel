<h3>Tutorial 16. Covering the Cups with Padding Polyester.</h3>
<p></p>
<p><strong>I cover the cups with flocks of padding polyester, spread them out and secure them with pins. </strong>The task is to make the cups yet rounder and smoother, cover the bones and prevent them from protruding through the face. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>Here is how I stitch it on: I follow the <em>inner </em>stitch of the bone at the top of the cup and I follow the <em>outer </em>stitch of the bone at the bottom of the cup. </strong>This stops the padding from passing onto the seam allowance at the top of the cup after you trim it and forming unwanted thickenings when you join the face with the lining. It will also cover the bone at the bottom of the cup and prevent it from protruding. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Before you start securing the polyester, prepare two ribbons (each 20-25cm long) to make hanging loops for the garment. </strong></p>
<p><strong>I place one ribbon in the armscye area and start stitching through the padding polyester following the <em>inner </em>stitch on the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>I continue securing the padding by following the <em>outer </em>stitch on the arched bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Upon reaching the neckline I go back to sewing along the <em>inner </em>stitch of the bone. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the ribbon with two additional stitches, on the seam allowance and inside the cup, to prevent it from poking out when the garment is worn. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image015.jpg" class="img-responsive center-block"></p>
<p><strong>I add a symmetric ribbon and pad the other cup exactly the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image017.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image019.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image021.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image023.jpg" class="img-responsive center-block"></p>
<p><strong>Next I remove the pins and trim the polyester down to the seam that attaches it. </strong></p>
<p><strong>As you can see, both bones at the top and down the bottom of the cup are padded with polyester and the padding at the top doesn�t pass onto the seam allowance. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image025.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2016_files/image027.jpg" class="img-responsive center-block"></p>
<p><strong>Our cups are now padded with polyester: </strong></p>
