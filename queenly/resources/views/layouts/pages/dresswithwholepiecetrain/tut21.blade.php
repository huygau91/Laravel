<h3>Tutorial 21. Joining the Face of the Front with the Lining. </h3>
<p></p>
<p><strong>Let�s start joining the face of the front half of the dress with its lining. </strong>I�ll show you a simple traditional method of doing it. </p>
<p><strong>I put the lining onto the dress face-to-face and start pinning them together. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>First I pin at the corner. </strong>Bones on the lining make it nearly impossible to pierce the garment through that�s why I stick pins in a gliding motion to catch the fabric of the lining only slightly. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully match the corners of the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Let�s check if the face fabric settles over the cup neatly enough: don�t forget that we�ve eased in the area from the neckline corner to the princess seam and now we need to distribute the fullness in order to avoid creases. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>I secure the corner of the neckline with pins.</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image010.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Then I secure the top corner of the other cup. </strong>Princess seams in the bust area of the face and the lining don�t have to coincide perfectly. Moreover, I would recommend shifting them against one another - in this case their seam allowances will spread out and you�ll avoid excessive thickening that would otherwise form bumps when the garment is turned out. It�s best to shift the princess seam of the face a couple millimeters towards the open edge. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I secure it with pins at the princess seams. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>Now it�s time to distribute the fullness on the cups. </strong>You can turn out the lining for your convenience and distribute the face fabric on inverted cups. I pin the face to the lining in small sections along the top of each cup - first I pin at the reference points and then divide each section in half. I recommend using a generous amount of pins here. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image018.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image022.jpg" class="img-responsive center-block"></p>
<p><strong>We have finished pinning the top. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image024.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Now let�s see what we have in the neckline area. </strong></p>
<p><strong>I spread the fabric in the neckline area and add control pins to preserve the same degree of tension we�re supposed to have on the ready garment. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>I pin along both sides of the neckline. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image032.jpg" class="img-responsive center-block"></p>
<p>Don�t worry if you�ve gone below the neckline, it simply means you�ve secured it even safer. </p>
<p><strong>How to check if you�ve done the job well enough? </strong>I turn the garment over and make sure no pins show from the other side! Remember: you can use as many pins as you want, the more the better. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image034.jpg" class="img-responsive center-block"></p>
<p><strong>The front part of the dress is ready to be sewn: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>Now let�s pin together the back of the dress. </strong></p>
<p><strong>Remember that the lining of the back is made of two halves: the central seam is still open. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>You could lay a bone along the top of the back on a larger-size dress to spread the pieces more properly. </strong>There�s no need in doing that on a small dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the face to the lining face-to-face.</strong> The princess seams don�t have to coincide, you can slightly shift them against one another. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the face to the lining. </strong>I pierce the fabric all the way through since there�s no bone along the top. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image044.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>And I pin together the other half of the back too. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>Now we can assemble the back of our dress: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image050.jpg" class="img-responsive center-block"></p>
<p><strong>I switch to the right-ski presser-foot and begin joining the pieces along their pinned edges. </strong></p>
<p><strong>The front part of the dress: </strong></p>
<p><strong>I start at the very edge of the piece. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>The ski moves along the outer side of the Rigilene bone on the princess seam. </strong>The needle should get right into the stay-stitch provided you have attached the bone correctly 1mm aside from it. Spread the garment carefully as you sew to prevent unwanted fabric from getting caught in the seam. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully approach the spot where the neckline ends. </strong>You can set smaller stitch spacing shortly before reaching the corner of the neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the garment 90 degrees about the needle at the bottom of the neckline. </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image060.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And I make a small stitch. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image062.jpg" class="img-responsive center-block"></p>
<p><strong>And I turn it 90 degrees about the needle again. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image064.jpg" class="img-responsive center-block"></p>
<p><strong>I continue making the joining stitch along the neckline. </strong>You can return to the normal stitch spacing after stitching through a small section. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image068.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image070.jpg" class="img-responsive center-block"></p>
<p><strong>I make a bar tack at the intersection of stay-stitches and go beyond the border of the garment. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image072.jpg" class="img-responsive center-block"></p>
<p><strong>Then I remove the pins and thoroughly check the seam quality. </strong>The feed dogs of the sewing machine often tend to �rub down� the thread as the presser-foot passes over a pin. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image074.jpg" class="img-responsive center-block"></p>
<p><strong>If this has happened to you, i.e. the thread got rubbed down and is about to tear, you must restore the stitch! </strong>Simply remove all pins and make another joining stitch right over the first one - you can do that for additional safety even if the first stitch was fine! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image076.jpg" class="img-responsive center-block"></p>
<p><strong>I pin the face to the lining in the armscye area. </strong>Make sure to spread the fabric carefully. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image078.jpg" class="img-responsive center-block"></p>
<p><strong>I make a joining stitch in the armscye area and make bar tacks at the intersection of stay-stitches. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image080.jpg" class="img-responsive center-block"></p>
<p><strong>And again I check the seam quality to make sure the thread is not going to tear. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image082.jpg" class="img-responsive center-block"></p>
<p><strong>I join the pieces in the armscye area from the opposite side and check the seam quality. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image084.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image086.jpg" class="img-responsive center-block"></p>
<p><strong>It�s time to trim the seam allowances. </strong></p>
<p><strong>I try not to trim it too much near the side seam to ensure it can spread smoothly when I turn out the garment. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image088.jpg" class="img-responsive center-block"></p>
<p><strong>I gradually bring it down to 0.5cm. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image090.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image092.jpg" class="img-responsive center-block"></p>
<p><strong>And I trim the seam allowance on the opposite side exactly the same way. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image094.jpg" class="img-responsive center-block"></p>
<p><strong>Now we need to trim the seam allowance of the entire neckline and notch its bottom corner with particular care. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image096.jpg" class="img-responsive center-block"></p>
<p><strong>I highly recommend that you use some Gutermann glue before notching out the bottom corner of the neckline </strong>and apply it around the corner from both sides to secure the stitch and fabric fibers. Try not to go beyond the seam on the face side of the dress. Let the glue set afterwards. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image097.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image099.jpg" class="img-responsive center-block"></p>
<p><strong>You can start trimming the seam allowances on the cups while waiting for the glue to set. </strong></p>
<p><strong>Don�t forget that you won�t be able to make any adjustments here after the fitting test!</strong>&nbsp; You might somehow manage to alter the shape or make the neckline lower but you won�t be able to make it more modest! Please explain this to your client and discuss the design in advance! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image101.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully notch out the corner of the cup, i.e. trim the seam allowance shorter in the corner. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%20dress%201/Tutorial%2021_files/image103.jpg" class="img-responsive center-block"></p>
