<h4>Punching Grommets.</h4>
<p></p>
<p><strong>Now I need to punch grommets through. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%209_files/image022.jpg" class="img-responsive center-block"></p>
<p>First of all I need to make markings. </p>
<p>I measure the length of the back which is 9.8 inches. </p>
<p>The last grommets should be punched 0.4 inches from the edge. </p>
<p>The remaining length of the back is 9.4 inches. </p>
<p>The distance between the grommets should be ideally 1.2-1.6 inches. </p>
<p>I divide this distance into seven sections which means each section or the distance between the grommets is going to be: </p>
<p>9.4"&nbsp; � 7 = 1.3"</p>
<p><strong><br>
