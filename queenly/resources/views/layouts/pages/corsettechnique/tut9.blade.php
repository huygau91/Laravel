<h3>Tutorial 9.&nbsp; Sewing a Lacing Flap. Punching Grommets. </h3>
<h4>Lacing Flap.</h4>
<p></p>
<p><strong>I start sewing a flap used for the lacing: </strong></p>
<p>I have cut it when I was cutting the general pattern for the corset. </p>
<p><strong>The length of the flap</strong> equals the length of the corset back. </p>
<p>It is easy to calculate <strong>its width: </strong>if you or your client wishes to leave an open triangle on the back (the middle of the back won�t close) then you should <strong>measure the length of the gap and add 0.8-1.2 inches</strong> to account for the flap going inside the corset. </p>
<p>The flap is very easy to sew; you simply stitch around a rectangular piece of fabric.</p>
<p>You can see <strong>the sewing sequence </strong>in the pictures: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%209_files/image002.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%209_files/image004.jpg" class="img-responsive center-block"></p>
<p>I make <strong>a clean-finish-edge seam</strong> for a piped-like edge to make sure the flap turns inside out properly: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%209_files/image006.jpg" class="img-responsive center-block"></p>
