<h3>Tutorial 8.&nbsp; Finishing the Corset. Finishing the Bottom. </h3>
<p></p>
<h4>Fitting Results.</h4>
<p></p>
<p>So, now the fit test is over, what have I noticed? </p>
<p><strong>The upper cut edge of the front seems to slightly come off the body</strong>, i.e. there is no due fitting along the neckline. </p>
<p>That means I need to slightly pull this front part on top. </p>
<p>And that is why I <strong>make a 0.5 inch seam allowance on the front half of the corset instead of a 0.7 one</strong>. </p>
<p>Apart from that the corset fits Kate very well, the side seam is at its right place and there is good fitting along the waistline and the bottom of the corset. </p>
<p><strong>I start making up for my faults. </strong></p>
<p><strong>I rip the entire side seam </strong>to create a smooth flow from the re-adjusted area to the area that remains unchanged. Then I remove pins.</p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image002.jpg" class="img-responsive center-block"></p>
<p>And again I pin together the pieces of the front and the back. </p>
<p>I shift the back piece along the top by 0.2 inches fading away at the waistline. </p>
<p>If I make a side seam with a 0.5 inch allowance after the pieces have been shifted, then this allowance will be somewhat 0.7 inches at the top and then smoothly return to the standard 0.5 inches at the waistline level. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image004.jpg" class="img-responsive center-block"></p>
<p>I pin together the pieces of the front and the second half of the back in the same manner. </p>
<p>I make <strong>new side seams with a 0.5 inch allowance</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image006.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\ДЛЯ ЗАПАДА!!!\Текст на англ\kn1 2.0 ENG - 2_files\image263.jpg"></p>
<p></p>
<p>Afterwards I need to press open the seam allowances and catch them up by hand or by machine to prevent their ends from going beyond the edges of the corset. </p>
<h4>Finishing the Bottom.</h4>
<p></p>
<p><strong>And now I finish working on the bottom of the corset. </strong></p>
<p>I use a 0.5 inch wide bias tape for this purpose. </p>
<p><strong>I place the tape at the seam that joins the face and the lining along the bottom of the corset</strong> and sew it on with an overseam. I pull the garment onwards to avoid any creases. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image008.jpg" class="img-responsive center-block"></p>
<p>It is very important to sew the tape on directly along the existing seam that joins the bottom of the corset. This existing seam is made at a presser-foot gap from the horizontal bone attached down the bottom. There will be a light piped-like edge effect on the main fabric when I bend the tape inwards and thus the tape won’t be seen at the bottom of the corset. </p>
<p>It is usually difficult to choose a tape of a matching color for the main fabric if the latter is neither black nor white. There is always some slight color difference. And you don’t want a differently colored tape to be noticeable at the bottom of the corset. In our case this tape stays inside the corset and will not be seen. </p>
<p>I bend the tape back and trim <strong>the seam allowance:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>I bend the tape inwards</strong> and secure it with pins. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image012.jpg" class="img-responsive center-block"></p>
<p>The bend is really smooth thanks to the lower horizontal bone. </p>
<p>I make sure <strong>the back pieces of the corset have the same length:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the tape on by hand</strong> having previously secured it by machine at the back ends. </p>
<h4>Finishing the Lacing Area. </h4>
<p></p>
<p><strong>Now I start finishing the lacing area of the corset. </strong></p>
<p>I take a narrow Rigilene bone. </p>
<p>I tape its edge over with masking tape. </p>
<p>Now I place this bone at the stay-stitch made on the lining side along the side of the corset. Normally I would place this bone at the edge of the stay-stitch. But after the fitting I want to make the size of the back a little smaller, basically just by 0.2 inches. That’s why I place the bone in such a way that the stay-stitch goes along the middle of the bone. </p>
<p><strong>I stitch the bone on</strong> with two strengthening stitches: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image016.jpg" class="img-responsive center-block"></p>
<p>Then I <strong>bend the corset edge</strong> along this bone and<strong> sew it on</strong> along the stitch it’s been attached with: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>A finished side edge of the corset prepared for the lacing</strong> looks like this from the face side: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image020.jpg" class="img-responsive center-block"></p>
<p>I do exactly the same with <strong>the other side edge of the corset</strong>. </p>
<p>The seam allowance on the lining side that is now “detached" is very useful for me because it will create additional density when grommets are punched through: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Now is the time for some <strong>pressing. </strong></p>
<p>I thoroughly press the side seams as well as the top and bottom of the corset. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%208_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>So I have made all alterations that I found necessary during the fit test, finished the corset, finished and pressed the lacing area. </p>
<p>During the next tutorial I am going to sew a flap for the lacing and punch the grommets through onto the corset. </p>

