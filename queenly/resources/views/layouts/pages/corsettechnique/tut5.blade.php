<h3>Tutorial 5.&nbsp; Covering Cups with Padding Polyester. Joining the Face Pieces of the Corset. </h3>
<h4>Covering Cups with Padding Polyester. </h4>
<p></p>
<p>And now it’s time to cover the cups of my corset with some padding polyester.</p>
<p>I have prepared a strap of padding polyester about 7.9 inches wide and long enough to fully cover the cups at the front. </p>
<p>I take the lining with bones already stitched-on. I cover the cup area with padding polyester and attach it to the lining with a couple of pins: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%205_files/image002.jpg" class="img-responsive center-block"></p>
<p>And now I <strong>stitch-on this polyester</strong> leaving a presser-foot gap from the upper stay-stitch to prevent the polyester from getting into the seam allowance later: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%205_files/image003.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\ДЛЯ ЗАПАДА!!!\Текст на англ\kn1 2.0 ENG - 2_files\image173.jpg"></p>
<p>I continue stitching padding polyester in a circular motion. </p>
<p>I leave a 1.2-1.6 inch gap from the side seam. </p>
<p>I try to make the stitch attaching the polyester along the bottom of the cups parallel to the finished upper stitch. </p>
<p>The sewing-on of the polyester should be finished at the point where you started sewing. </p>
<p>I take a ribbon that can be up to 0.4 inches wide. </p>
<p>I cut off two pieces, each 9.8 inch long, and fold them into loops. </p>
<p>Now I place the first loop at the stitch that attaches the padding polyester at the level of the middle of the cup and sew it to the lining with two stitches: one going along the polyester attaching stitch and another a little lower: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%205_files/image004.jpg" class="img-responsive center-block"></p>
<p>I attach the second loop the same way at the level of the middle of the other cup. </p>
<p>These loops can be stitched-on at the very beginning together with the padding polyester. </p>
<p>The loops are used for hanging the corset.</p>
<p>Now the polyester is sewn-on. </p>
<p>All pins get removed. </p>
<p>The polyester should be trimmed close to the attaching seam or else it is going to add unnecessary thickness along the seams of the garment while its function is just to cover the bones on the embossed parts of the corset, i.e. cups. </p>
<p>Cups covered with padding polyester look like this: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%205_files/image006.jpg" class="img-responsive center-block"></p>
