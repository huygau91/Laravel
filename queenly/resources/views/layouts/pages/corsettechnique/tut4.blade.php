<h3>Tutorial 4.&nbsp; Stitching-on Bones. </h3>
<p></p>
<p><strong>During this tutorial I am going to stitch bones onto the lining pieces. </strong></p>
<h4>Stay-Stitch. </h4>
<p></p>
<p>I should do something very important before starting stitching-on bones. I need to <strong>make a stay-stitch</strong> in certain areas of the lining pieces. </p>
<p><strong>What is a stay-stitch? </strong></p>
<p>This stitch is a basic seam sewn at a distance of the seam allowance. </p>
<p>The stay-stitch carries out a few functions simultaneously: </p>
<p>- it marks precisely the location of the seam that is going to join the pieces; </p>
<p>- it supports cut edges therefore preventing cut bias areas from stretching; </p>
<p>- it is a guiding line for a very precise marking of the start and end points of sewing bones onto corset pieces. </p>
<p>One always feels tempted to mark this stitch with the help of a pencil and a ruler. </p>
<p>But trust me - nothing is more precise than a machine-made stitch. Stay-stitches can be made both along the face fabric and along the lining. </p>
<p>Every time you should make sure that the stay-stitch is identical from both sides. With this in mind, it is really convenient to make one stay-stitch on the face side along one half of the piece and another on the lining - along the other half. </p>
<p>I am going to make a stay-stitch on the front part of the corset along its top and bottom, and on the back of the corset along the three sides (excluding the side seam). </p>
<p>I make a stay-stitch along <strong>the bottom of the front:</strong> </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>Then I turn the piece over and make another stay-stitch along <strong>the top cut edge of the front</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>Now along the bottom of the back (on both halves): </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>And along the top of the back (also on both halves): </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Along the lacing area on the back (both halves): </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image010.jpg" class="img-responsive center-block"></p>
<h4>Stitching Bones onto the Back of the Lining. </h4>
<p></p>
<p><strong>I start stitching-on a wide Rigilene bone along the back curve. </strong></p>
<p>You should start stitching-on a bone like this at a 2-3 mm distance from the stay-stitch. And you should trim it at the end the same way - leaving 2-3 mm from the stay-stitch on the opposite edge of the piece. If you fail to follow this rule but start sewing-on the bone right from the stay-stitch, then this bone is going to be an obstruction when you need to turn the garment out. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image012.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\ДЛЯ ЗАПАДА!!!\Текст на англ\kn1 2.0 ENG - 2_files\image119.jpg"></p>
<p><strong>I place a wide Rigilene bone along the side part of the corset back: </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image014.jpg" class="img-responsive center-block"></p>
<p>I have already made the marking for sewing-on the bones (see tutorial 2). </p>
<p>For this bone there are also 2-3 mm left from the stay-stitch at the beginning and the end. </p>
<p>All bone edges need to be taped with masking tape. </p>
<p>I <strong>always make a bar tack</strong> at the start and end points of the sewing. </p>
<p>Upon making a stitch along one side of the bone I turn the piece round and <strong>make another stitch along the other side of the bone</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image016.jpg" class="img-responsive center-block"></p>
<p>This is what two sewn-on bones on the back look like <strong>from the lining side</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image018.jpg" class="img-responsive center-block"></p>
<p>And this is what they look like <strong>from the face side</strong>: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image020.jpg" class="img-responsive center-block"></strong></p>
<p>I repeat these procedures with the second half of the back. </p>
<h4>Stitching Bones onto the Front of the Lining. </h4>
<h5>Vertical Bones.</h5>
<p></p>
<p>All the rules of stitching that I have previously used for the bones on the back piece work here as well. I start with <strong>a wide vertical bone along the side part of the front</strong>: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image022.jpg" class="img-responsive center-block"></strong></p>
<p>I use previously prepared markings when sewing (see tutorial 2). </p>
<p>At the end I trim the bone at an angle parallel to the upper cut edge of the corset: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image024.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And then I make the second parallel stitch over this bone: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image026.jpg" class="img-responsive center-block"></p>
<p>I stitch-on <strong>a vertical bone along the other half of the front side part:</strong> </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image028.jpg" class="img-responsive center-block"></strong></p>
<p>And now I stitch-on <strong>the central bone of the front</strong>: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p>You shouldn’t prepare Rigilene bones in advance. </p>
<p>This would be a big mistake! </p>
<p>It is much better to cut bones on spot each time - like I do. </p>
<p>Now I only have to stitch the bones onto <strong>the front curves</strong>. </p>
<p>I have opted for using a narrow Rigilene bone because my corset is small-sized and the curve has quite a bend.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image032.jpg" class="img-responsive center-block"></p>
<p>Whenever I reach the cup level I <strong>pull the fabric onwards</strong> with my right hand while pushing <strong>the bone away</strong> from myself with the left hand. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p>I use this trick to prevent the fabric from easing-in underneath the bone at the cup level due to the sharp bend of the curve!&nbsp; </p>
<p>I make the cup look even rounder and more dome-shaped by pulling the fabric onwards like this. A flattened cup is a result of this bone being sewn-on incorrectly! </p>
<p>But you should not pull the fabric too hard! This may deform the shape of the corset! </p>
<p>I make <strong>another parallel stitch</strong> along this bone: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image036.jpg" class="img-responsive center-block"></p>
<p>I have noticed a crease or a tuck on the face side of the corset: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image038.jpg" class="img-responsive center-block"></p>
<p>You can’t neglect something like this! </p>
<p>It is necessary to slightly rip off this bone and even out the seam! </p>
<p>I take the scissors, push them under the bone and cut the thread: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image040.jpg" class="img-responsive center-block"></p>
<p>Then I spread the seam and sew the bone back into place: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image042.jpg" class="img-responsive center-block"></p>
<p>The fault has been fixed!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image044.jpg" class="img-responsive center-block"></p>
<p>Now I sew-on a bone <strong>along the second curve of the front</strong> using two stitches like before: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p>We’re done with the sewing-on of the vertical bones. </p>
<h5>Horizontal Bones.</h5>
<p></p>
<p><strong>I place a horizontal bone along the bust-line: </strong></p>
<p><strong>There is a rule: </strong>all horizontal bones of the corset should be narrow! </p>
<p>I leave about 1.2 inches from the side cut edge. </p>
<p>This is done in order to prevent the bone from getting into the seam allowance in case the garment needs to be adjusted with the help of a side seam. </p>
<p>When sewing-on this horizontal bone, I always try to pull the fabric onwards and push the bone away from myself. </p>
<p>Please pay special attention to the sewing-on of this very bone! </p>
<p>It goes along the central bust-line! </p>
<p><strong>And it is exactly this bone that’s responsible for the dome-shape of corset cups! </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image048.jpg" class="img-responsive center-block"></p>
<p>If you ignore my recommendations concerning the sewing-on of this bone, then the fabric is going to ease-in underneath it (which has been proven by practice) and the cups will be flat instead of having a beautiful rounded shape. </p>
<p>I make <strong>the second parallel stitch</strong> along the bone: </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image050.jpg" class="img-responsive center-block"></strong></p>
<p>&nbsp;</p>
<p>Take a look at the result! The cups are dome-shaped and the lining already somewhat holds its shape!</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image052.jpg" class="img-responsive center-block"></p>
<p>And now I need to place a bone along <strong>the bottom of the corset</strong>. </p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image054.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\ДЛЯ ЗАПАДА!!!\Текст на англ\kn1 2.0 ENG - 2_files\image161.jpg"></strong></p>
<p>There are also <strong>some peculiarities</strong> concerning the way of sewing-on this bone: </p>
<p>- there should be about 1.2 inches left from the side cut edge; </p>
<p>- the bone should be placed 1mm above the stay-stitch. </p>
<p>I pull the fabric onwards when sewing-on the bone. </p>
<p>I make the second parallel stitch. </p>
<p>I think it is now clear why I had left 2-3 mm from the stay-stitch when sewing-on vertical bones. <strong>It was done so that the horizontal bone could overlap the edges of the vertical bones. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image056.jpg" class="img-responsive center-block"></p>
<p>The horizontal bone thereby prevents fishing lines from sticking out of the edges of the vertical Rigilene bones. </p>
<h4>Pressing.</h4>
<p></p>
<p>And now I need to <strong>press these Rigilene bones</strong>. I took them out of a box where they were rolled into a spool. </p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image058.jpg" class="img-responsive center-block"></p>
<p>It is especially noticeable on the back pieces: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image060.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Rigilene bones are easy to press. </p>
<p>This is what the back piece with sewn-on bones looks like after it’s been pressed: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%204_files/image062.jpg" class="img-responsive center-block"></p>
<p></p>
<p>Rigilene bones have one <strong>significant disadvantage</strong>: they don’t hold their shape well when pressed heavily and can even break. </p>
<p>And that is why I use <strong>additional supporting plastic bones</strong> when making more sophisticated corset garments. </p>
<p>However stitched-on Rigilene bones are enough for keeping the shape of corsets for young women or corsets with no particular tummy control effect. </p>
<p>I would like to stress it that I am sewing this corset in a simplified technique without any additional means of support. </p>
<p>Let me summarize this tutorial. </p>
<p>I have stitched bones onto the pieces of the front and back of the lining. </p>

