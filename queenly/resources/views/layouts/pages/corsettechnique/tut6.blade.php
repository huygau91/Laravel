<h3>Tutorial 6.&nbsp; Joining the Face and the Lining. </h3>
<p></p>
<p><strong>I start joining together the ready sewn lining and the finished pieces of the face of the corset. </strong></p>
<h4>Back Pieces.</h4>
<p></p>
<p><strong>I have joined the back pieces of the face and the lining along their upper cut edge. </strong></p>
<p>I sew them together along the stay-stitch: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image002.jpg" class="img-responsive center-block"></p>
<p>I keep the previous piece under the machine to ensure that the length of the back is the same. I place the next piece on it and match their seam allowances until they are fully identical. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I sew a clean-finish-edge seam on both pieces. </strong></p>
<p>Now I turn the piece over directing the seam allowances towards the lining. </p>
<p>I spread the garment with my hands and make an additional supporting stitch 1mm from the joining seam: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I have stitched-on the vertical bones leaving 2-3 mm from the stay-stitch which is why their edges do not prevent me from making a clean-finish-edge seam. </p>
<p><strong>And finally now - <em>and not before sewing the clean-finish-edge seam</em> - I trim seam allowances:</strong></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image008.jpg" class="img-responsive center-block"></p>
<p></p>
<h4>Front Pieces.</h4>
<p></p>
<p><strong>I join the face with the lining of the front part of the corset. </strong></p>
<p>First of all I need to match the edges of the garment and secure them with pins. </p>
<p>I shift the curves of the face aside by 1-2 mm against the curves of the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image010.jpg" class="img-responsive center-block"></p>
<p>This is done to avoid unnecessary thickening at the areas where the curves overlay. </p>
<p>Thanks to this trick I achieve a smoother upper edge of the corset. </p>
<p>Afterwards the pieces are pinned together along the whole upper cut edge. </p>
<p>I put the pieces under the machine lining upwards and sew them together along the stay-stitch: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image012.jpg" class="img-responsive center-block"></p>
<p>I remove the pins. </p>
<p>I take <strong>a Capron ribbon</strong> around 0.7-1 inch wide (you could tear it off a piece of organza): </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I <strong>stitch-on this supporting ribbon</strong> that is going to help me ease-in at the upper cut edge to make it rounder, prevent it from stretching and ensure better fitting to the bust. </p>
<p>I pull the ribbon with the right hand when sewing and push the fabric away from myself with the left hand thereby achieving light fullness. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I stitch-on the ribbon along its middle following the seam that joins the pieces together. </p>
<p>I stop easing-in at the middle of the corset and start again when nearing the curve and going from the curve to the side. </p>
<p>Please take a look; this is what the corset top looks like with the ribbon attached: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image018.jpg" class="img-responsive center-block"></p>
<p>You can notice some light fullness.</p>
<p>Then I bend the ribbon upwards, towards the seam allowance, and attach it to the seam allowance 2-3 mm above the first stitch. I make sure there are no creases or tucks. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image020.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>I make a clean-finish-edge seam along the upper cut edge of the corset. </strong></p>
<p>The seam allowances should be directed towards the lining. </p>
<p>I make a supporting stitch at about a 1-1.5 mm gap from the seam that joins the pieces together. </p>
<p>This stitch should be made with particular care. </p>
<p>It’s hard to work because the bones have already been stitched onto the lining, the ribbon has been attached and the stitch itself is rather long. Remember to always spread out seam allowances. </p>
<p>I have managed to fulfill this task: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I check carefully one more time how well the seam allowance is sewn. </p>
<p>If you notice a crease, a tuck or a lump you will have to rip the supporting stitch and re-stitch it all over again. </p>
<p>The stitch should be very even. </p>
<p>And the corset should be easily turned out with no unnecessary thickening. </p>
<p><strong>There must be no slipshod work аt the level of the upper cut edge of the corset!</strong></p>
<p><strong>The upper cut edge is the most important edge of the corset! </strong></p>
<p>I trim seam allowances: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image024.jpg" class="img-responsive center-block"></p>
<h4>Pressing.</h4>
<p></p>
<p>As a next step <strong>all upper cut edges should be pressed</strong>. </p>
<p>At first I press <strong>the two back pieces</strong>: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image026.jpg" class="img-responsive center-block"></p>
<p>I press very carefully trying to make the upper cut edge flat and smooth: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image030.jpg" class="img-responsive center-block"></p>
<p>And now I go on to the pressing of <strong>the upper cut edge of the front half of the corset</strong>. </p>
<p>It is necessary to use a cup pressing mould. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image032.jpg" class="img-responsive center-block"></p>
<p>It is really handy when you have a few moulds of various sizes. A large-size mould is perfect for pressing upper cut edges of a corset and a smaller mould suits for cups. </p>
<p>First of all I spread the seam very carefully and only afterwards I start pressing. I keep turning the piece on the mould. Every inch of the upper cut edge is pressed the way it lies. The upper cut edge must keep its shape perfectly and be ideally smooth! </p>
<p>I also press <strong>the corset cups</strong> themselves using smaller-size moulds: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image034.jpg" class="img-responsive center-block"></p>
<p>The pressing process is finished:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%206_files/image036.jpg" class="img-responsive center-block"></p>
<p>I would like to remind you one more time that I am sewing a corset <strong>in the simplified technique</strong>. </p>
<p>I am not stitching-on a bone along the top and I am not making round quilted cups. </p>
<p>This leads to creases, folds and lumps. </p>
<p>And for this reason I cannot fulfill the strict requirements to the shape of the corset when I use the simplified technique. </p>
<p>However this can all be forgiven thanks to the technique itself. </p>
<p>I am going to show you later how to sew more sophisticated corset garments that hold their shape much better. </p>
<p>And so, during this tutorial I have joined together the lining and the face of the corset and pressed its upper cut edges. </p>

