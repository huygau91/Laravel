<h3>Tutorial 7.&nbsp; Joining the Side Seams. Fitting. </h3>
<p></p>
<p><strong>In this tutorial I am going to join together the side seams of the corset pieces and carry out a fit test. </strong></p>
<h4>Joining the Side Seams.</h4>
<p></p>
<p><strong>I start working with the pieces of the back. </strong></p>
<p>First of all I join the lining and the face of the corset along <strong>the middle of the back</strong>. </p>
<p>I make the stitch go along the stay-stitch. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image002.jpg" class="img-responsive center-block"></p>
<p>I join together the pieces of the back <strong>along the side seam</strong> leaving a presser-foot gap from the edge. </p>
<p>I join the notches and the cut edges of the pieces. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image004.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And next I need to make a connecting stitch <strong>along the bottom of the back pieces</strong>. </p>
<p>I make the stitch go along the stay-stitch again. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image006.jpg" class="img-responsive center-block"></p>
<p>You should have no problems joining the pieces of the back. The face and lining parts usually match very well. </p>
<p>I repeat the same steps with the other half of the back. </p>
<p><strong>A little more work needs to be done for the front piece. </strong></p>
<p>I secure the side cut edges with pins from both sides to avoid any skews. At first I pin at the waistline area matching the notches and then I arrange the fabric. I keep checking how much the face fabric is stretched to ensure the corset fits tight enough in the bust area. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image008.jpg" class="img-responsive center-block"></p>
<p>Having pinned the edges down I check if the fabric is stretched well enough along the main lines (I stretch the pieces as if the corset were put on the client): </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image010.jpg" class="img-responsive center-block"></p>
<p>The thing is that every fabric used for a corset behaves in its own way. </p>
<p>It often happens that the face pieces become slightly bigger than the lining. And you need to pull the face fabric if there is some slackness. It is usually enough to just let it off by 2-3mm from the sides. Less often you might have to let off the face fabric beyond the bottom of the corset. </p>
<p>I start pinning down the bottom of the front corset part always checking the degree of stretching. I have noticed that in my case the corset is going to fit beautifully if I make its face fabric 4-5mm longer than the lining: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I join <strong>the side seam</strong> with the help of the machine leaving a presser-foot gap from the edge: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image014.jpg" class="img-responsive center-block">&nbsp;</p>
<p>I have turned the garment around on the needle and started sewing <strong>a connecting stitch along the bottom</strong>. Note that the left edge of the presser foot follows the edge of the bone that’s been stitched-on along the corset bottom. </p>
<p>In other words this bone is a guiding line for the presser foot. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image016.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\ДЛЯ ЗАПАДА!!!\Текст на англ\kn1 2.0 ENG - 2_files\image235.jpg"></p>
<p></p>
<p>Now I <strong>join the other side seam of the front</strong> also leaving a presser-foot gap from the edge. </p>
<p>I remove pins.</p>
<p>The next step is to <strong>overlock the side seams of the front, the back and the middle of the back</strong>. </p>
<p>There are a few methods of fastening the ends of an overlock seam. </p>
<p>You can take a big needle and push thread ends inside the overlock seam. </p>
<p>Or you can simply machine-stitch these ends up. This is the method I usually use. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to join the side cut edges of the front and back.</strong></p>
<p>I begin by pinning them together at the waistline level, then atop and finally - down the bottom. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image020.jpg" class="img-responsive center-block"></p>
<p>Now I need to join the side seams. </p>
<p>I make a machine-stitch with a 0.5 inch seam allowance. </p>
<p>Right now I am preparing the corset for a fit test that’s why I can make a larger seam with the maximal stitch length in order to make ripping easier later. If everything turns out fine you can just sew another regular seam over it for better durability. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image022.jpg" class="img-responsive center-block"></p>
<p>I repeat the same thing with <strong>the other side seam</strong>. </p>
<p>Although I might have to rip these seams later, I still make bar tacks from both sides of the seam to measure it better without worrying that it will burst. </p>
<p>And then I take some <strong>detachable lacing bars</strong> for fitting and place them at the seam that joins the face and the lining. </p>
<p>I have made this seam follow the stay-stitch and it matches the seam allowance. Later I will have to bend the edge inwards and finish proper lacing, which means this seam allowance won’t be there when the garment is ready. For this reason I have sewn-on a detachable bar in such a way that it <strong>fully duplicates the actual lacing of the corset</strong>. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image024.jpg" class="img-responsive center-block"></p>
<p>I sew another bar like this onto the other side of the back. </p>
<p>If the bars are sewn-on precisely, a fit test will show me how much needs to be changed if at all. </p>
<p>I have demonstrated how to make such bars in one of my free tutorials that you can watch in the Corset Academy magazine, on my website or my You-Tube channel. </p>
<p>A corset with sewn-on fitting bars looks like this: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>It is recommended to press open these side seams</strong> even for a plain fit test. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image028.jpg" class="img-responsive center-block"></p>
<p></p>
<p>These seams are rather thick and if you don’t press them open the fit test might have incorrect results. </p>
<p>That is why I highly recommend you pressing these seams open (although not necessarily too thoroughly) from the face and the lining sides. </p>
<h4>Fitting.</h4>
<p></p>
<p><strong>Well, my corset is ready for a fit test! </strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/Tutorial%207_files/image030.jpg" class="img-responsive center-block"></strong></p>
<p>I would like to draw your attention to the fact that a corset sewn in the simplified technique has more in common with a bodice. </p>
<p>It is impossible to avoid small folds and light creases! </p>
<p>Do not place way too high demands on the garment sewn in the simplified technique. </p>
