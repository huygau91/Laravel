<h3>Tutorial 3.&nbsp; Joining Vertical Curves. </h3>
<h4>Joining the Front Pieces of the Lining. </h4>
<p></p>
<p>I start sewing together <strong>the front pieces of the lining</strong>. </p>
<p>I need to join the central part together with the side part. </p>
<p>I hear many people asking <strong>how to put these pieces together correctly</strong>. </p>
<p>The answer is very simple: <strong>the notches needn�t be matched!</strong> </p>
<p>Please take a look at the picture: I have put the pieces together very accurately (face to face), evened out their cut edges, the notches however don�t match! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image002.jpg" class="img-responsive center-block"></p>
<p>So what do I do in this case? I look for the intersection of the bust-line and the seam allowance line on the side part of the front and pin it at the spot: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image004.jpg" class="img-responsive center-block"></p>
<p>And this pin should be exactly at the intersection of the bust-line and the seam allowance line on the central piece of the front: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image006.jpg" class="img-responsive center-block"></p>
<p>Afterwards the pieces get joined together. </p>
<p>Please note that the notches don�t match:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>Thus the notches do not indicate where the pieces should be joined, but rather indicate the location of the bust-line, waistline and stomach line... </strong></p>
<p><strong>I start sewing together the pieces. </strong></p>
<p>Please note the way I use my hands when sewing pieces together: I am holding the bottom layer in one hand and the upper layer - in the other: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image010.jpg" class="img-responsive center-block"></p>
<p>While sewing, I even out cut edges right under the machine always correcting the seam allowance according to the line of the throat plate. </p>
<p>I am using neither pins nor tacks. I always hold the layers of fabric with both hands controlling thereby either the ease-in degree or the stretching. You should always feel the way the fabric lies under the machine. </p>
<p>The first curve is finished:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image012.jpg" class="img-responsive center-block"></p>
<p>Then, in just the same manner, I join and sew together the central part of the front lining piece and the second side piece. </p>
<p><strong>A small trick: </strong>it is very difficult to start sewing pieces together when fabric layers slide along each other like in my case. It is hard to push evened-out cut edges underneath the sewing machine.&nbsp; That is why I pin the layers together, then push them underneath the machine and start working. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image014.jpg" class="img-responsive center-block"></p>
<p></p>
<p>The pins are afterwards removed and I continue sewing, holding each layer with my hands - the upper layer with the left hand and the bottom layer - with the right one: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image016.jpg" class="img-responsive center-block"></p>
<p></p>
<p>I keep controlling how much the fabric is stretched under the machine.</p>
<p>This procedure is rather simple.</p>
<p>Having sewn a seam like this a couple times you will see this yourself and the situation will be fully under your control. </p>
<p>So I have finished sewing together the front of the lining and now I put it aside. </p>
<h4>Joining the Back Pieces of the Lining.</h4>
<p></p>
<p>Now I start sewing together <strong>the pieces of the back of the lining</strong>. </p>
<p>You can see the pieces I�m going to work with in the picture: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image017.jpg" class="img-responsive center-block"></p>
<p>I am sewing together the vertical curve: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image019.jpg" class="img-responsive center-block"></p>
<p>I hold the pieces separately with my hands, like before. I hold the bottom piece lightly but let go of the upper one. This way the fullness produced by the machine on the bottom layer is evened-out. I join even the most basic pieces this way. This should become your habit!</p>
<p>&nbsp;I trim corners on the seam allowances of the curve: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image021.jpg" class="img-responsive center-block"></p>
<p>This procedure is going to be repeated multiple times during the process of sewing corsets. </p>
<p>It is done in order to make the fabric less thick when you start joining the lining and the face along the curves. </p>
<p>I sew together the second vertical curve of the back of the lining also trimming the corners. </p>
<h4> </h4>
<h4>Pressing Open the Back Pieces of the Lining. </h4>
<p></p>
<p><strong>And then I start pressing open the seam allowances on the back of the lining. </strong></p>
<p>I press the seam open step by step from both sides - first from one side and then from the other- to preserve the volume of the piece: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image023.jpg" class="img-responsive center-block"></p>
<p>Then I trim about a half of the seam allowance leaving around 0.2-0.3 inches. </p>
<p>Afterwards you can press open one more time. </p>
<p>I repeat the procedure with the other back curve but <strong>in a different way. </strong></p>
<p>First I press the sewn curve along the iron-on fabric without unfolding the piece: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image025.jpg" class="img-responsive center-block"></p>
<p>Then I trim seam allowances: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image027.jpg" class="img-responsive center-block"></p>
<p>I unfold the piece and press open the seam like I did in the first case: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image029.jpg" class="img-responsive center-block"></p>
<p>You can choose whichever way suits you most. </p>
<p>It is slightly more difficult to press open in the second technique because the seam becomes small and hardly opens. However on the other hand the seam allowances are trimmed perfectly the same from both sides and the seam therefore looks neater. </p>
<p></p>
<h4>Pressing Open the Front Pieces of the Lining. </h4>
<p></p>
<p><strong>I start pressing open the front of the lining. </strong></p>
<p>I use the second press-open technique because I find it more convenient: </p>
<p>
<img cimage031.jpg"="" class="img-responsive center-block"></p>
<p>The seam allowances get trimmed:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image033.jpg" class="img-responsive center-block"></p>
<p>I press open the flat parts of the curves on an even, smooth surface: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image035.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And then I take my tools for pressing cups - a cup pressing mould and a cushion: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image036.jpg" class="img-responsive center-block"></p>
<p>I place the cushion on the pressboard then put the cup pressing mould on top of it and the cup itself - onto the mould. </p>
<p>I open the seam allowance: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image038.jpg" class="img-responsive center-block"></p>
<p>I press open the seam in a circular motion forming the required shape of the cup: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image040.jpg" class="img-responsive center-block"></p>
<p>And then I press the face side one more time very carefully: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image042.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image044.jpg" class="img-responsive center-block"></p>
<p>A corset should be pressed very thoroughly and you should by no means hurry! </p>
<p><strong>Note that the pressing process takes almost as long as the sewing one! </strong></p>
<p>You should let the seams cool down - then they will stay well pressed and won�t crease: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image046.jpg" class="img-responsive center-block"></p>
<p>As a result, in this tutorial I have shown you how to join together pieces without using pins and tacks. </p>
<p>I have sewn together the vertical curves of the lining and pressed them open. </p>
<p>Please take a look at the result of my work: </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%203_files/image048.jpg" class="img-responsive center-block"></p>
<p>I will continue working on the corset in the next tutorial. </p>

