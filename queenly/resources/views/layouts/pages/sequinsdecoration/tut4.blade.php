<h1>Tutorial 4. Different Ways to Sew Sequins. Part 3.</h1>
<p></p>
<p><strong>The next row is made of stitches with a sequin (positioned the way described above) and six seed beads.</strong> </p>
<p>I pick up the beads and bring the needle down wherever I want to direct the string of seed beads. <strong>I bring the needle out right at the end of the sequin. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%204_files/image002.jpg" class="img-responsive center-block"></p>
<p>And again, I pick up a sequin and six seed beads. I don't recommend changing the amount of beads. You can only do so at the end of the row for a neater finish. <strong>I will make a smooth turn downwards by bringing the needle down each time a little lower than before.</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%204_files/image003.jpg" class="img-responsive center-block"></p>
<p><strong>The result is a pretty braided row that features sequins. </strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%204_files/image005.jpg" class="img-responsive center-block"></p>
<p>It will work very well for concealing, as well as highlighting, the edge of an appliquï¿½. I keep adjusting the position of the previous sequins with the needle to make sure everything looks perfect. Accuracy and precision are very important in embroidery. You can reduce the number of seed beads in each next stitch if you need to narrow the row down towards the end.</p>

