<h1>Tutorial 2. Different Ways to Sew Sequins. Part 1.</h1>
<p><strong>Let us look at the classic method of sewing on sequins, with and without overlap. The first method is the most commonly used one: I slip a cupped sequin on the needle, with the cup facing down.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image002.jpg" alt=""></p>
<p><strong>Then I pick up a seed bead or a larger bead and bring the needle back through the hole in the sequin</strong> thereby securing the sequin with the bead.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>The sequin is in place.</strong> Cupped sequins are usually sewn on with the cup facing down. This way their planes give off more light</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image006.jpg" alt=""></p>
<p><strong>The next stitch will be made of two sequins with three seed beads in-between.</strong> I slip the first sequin on the needle cup down, pick up three seed beads, and slip the second sequin on the needle with the cup facing up.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image008.jpg" alt=""></p>
<p>And <strong>when I bring the needle down to finish the stitch,</strong> both sequins have their cup facing down and reflect the light the way I want them to. You can use it as a single stitch or make a series of stitches. I will show you both versions.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image010.jpg" alt=""></p>
<p><strong>Here is another way </strong>of sewing sequins. It is often used for sewing transparent sequins onto lace. They add a beautiful glow to the garment. Again, you can use individual single sequins or embroider a pattern. They will highlight the texture of the fabric in studio lights. Sequins are usually supposed to match the color of the main fabric and look neutral. The purpose of small transparent sequins is to make the garment glow. <strong>Here is how you sew them on:</strong> the sequin is placed cup down on the fabric, I bring the needle out through the hole, and make one, two, or three stitches to secure it. I won't need seed beads here. Choose transparent thread or thread of a matching color. It should be unnoticeable and yet robust enough to hold the sequin in place.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image012.jpg" alt=""></p>
<p><strong>You can also sew a sequin on with a seed bead stitch.</strong> I slip a sequin on the needle with the cup facing up and pick up six seed beads. This method is good for decorating the middle of a flower, for example. You can make such stitches in the round or arrange them in a line. All these versions will look beautiful and produce different effects. It all depends on the idea.</p>
<p><strong>Here is another version,</strong> and it works really well with rather large beads but you can also try it with smaller beads. I arrange a sequin in the traditional way, pick up a bead, and then bring the needle down at the side of the sequin and not through the hole in the middle. The bead, especially when large enough, thereby pulls on the sequin and tilts it down, which creates a different kind of shine. As you remember, sequins serve to reflect light. And if you make a row of sequins, you will notice how they are all tilted in the same direction (especially, if you draw the thread tight).</p>
<p><strong>Both versions are shown in the picture below:</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image014.jpg" alt=""></strong></p>
<p><strong>Now let's talk about rows of sequins. </strong></p>
<p><strong>Here is the classic method when you pick up a bead and bring the needle down through the middle of the sequin.</strong> There are certain variations here: you can either bring the needle back up right at the edge of the sequin or several millimeters from it. In the first case you will overlap the previous sequin by about a half and in the second case the sequins will form a plain row with small gaps in-between.</p>
<p>Let me show you how to sew sequins on with overlap. It resembles fish scales. <strong>I always bring the needle out right at the edge of the previous sequin.</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image018.jpg" alt=""></p>
<p><strong>The turn of the row depends on where you bring the needle out.</strong> If I keep bringing the needle out lower and lower, the row will gradually turn down.</p>
<p>And now I will show you a plain row of standard 6mm sequins with gaps in-between. Just like before, I pick up a bead and bring the needle down through the middle of the sequin. <strong>And then I bring the needle back up not at the edge of the previous sequin but at a distance which equals about a half of its width. </strong>In other words, when working with 6mm sequins, I bring the needle out 3mm from the edge of the previous sequin. And I sew on the next sequin just the same way. The sequins don't overlap and they will shine in a different way.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%202_files/image022.jpg" alt=""></p>
<p><strong>The first version is a better choice when you need to make a smooth transition.</strong> Some dresses are partially made of sequin fabric and partially of mesh fabric. And then you need to disguise the border between the two. Rows of overlapping sequins work great in this case. And rows made of non-overlapping sequins are better for highlighting certain parts of motifs.</p>
<p><strong>Let's move to the next stitch: sequin - seed beads - sequin.</strong> Again, there are two variations here: you can either make one stitch after another in a straight line or you can make stem stitches which I have already demonstrated with seed beads. You can do the same thing with sequins.</p>
<p><strong>Let me make a plain row of stitches first.</strong> I slip a sequin on the needle with the cup facing up, pick up three seed beads, and slip on another sequin with the cup facing down (both cups should face up when sewn on). I bring the needle out 3mm from the end of the first stitch (half the width of the sequin).</p>
