<h1>Tutorial 3. Different Ways to Sew Sequins. Part 2.</h1>
<p><strong>Let's look at the second way of making a row of stem stitches with a sequin, three seed beads, and another sequin.</strong> I will make a turn in the row. I have made the first stitch. I bring the needle up next to the middle of the previous stitch <strong>and always from the outer side of it.</strong> And again, I pick up a sequin, three seed beads, and another sequin (both sequins have their cups facing up when sewn on). I determine where I want to bring the needle down. This point will determine the direction of the row: whether it will be straight or have a turn upwards or downwards. I want my row to turn down so I insert the needle in the fabric with a slight shift downwards.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%203_files/image002.jpg" alt=""></p>
<p>I bring the needle back up next to the middle of the previous stitch with a slight shift to keep forming the turn. I pick up another sequin, three seed beads, and sequin. I bring the needle down with a turn downwards.</p>
<p><strong>Here is what the row looks like: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%203_files/image004.jpg" alt=""></strong></p>
<p>If I'm following a previously made sketch or a pattern printed on fabric, then I will simply regulate the direction of the row by bringing the needle down and back up in particular points. <strong>Just make sure the row looks even.</strong> If I choose the stitch made of two sequins with three seed beads in-between, then I will strictly stick to this combination. Only then will the embroidery look neat.</p>
<p>This braided row suits for outlining circles, ovals, and all kinds of flowers and petals used on lace fabrics. You could also design your own pattern on single-color fabric with no decorations or printed motifs. Or you could decorate an existing pattern. It also works for embroidering a whole dress and not just a separate motif.</p>
<p><strong>Simply make sure not to change the style of stitches within the same row.</strong> You can always make the next row with different stitches if you wish. They will all look great as long as you don't just interrupt a row out of nowhere to continue with different stitches.</p>
<p><strong>If you need to make the row narrower, you can reduce the amount of beads in the stitch:</strong> for example, a sequin - two seed beads - a sequin, and then a sequin - one seed bead - a sequin. This way you can gradually narrow the row, use a single sequin at the end, tie a knot, and move on. Just remember not to change the technique within one and the same row.</p>
<p><strong>The next option is a regular row of sequins. </strong>It's the easiest and most tried and true way of sewing sequins.</p>
<p><strong>I slip a sequin on the needle, press it down with my thumb, catch literally just 1mm of fabric at the edge of the sequin, and draw the thread tight. That's it! The sequin is secured in place! </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%203_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%203_files/image008.jpg" alt=""></p>
<p><strong>It's the fastest and easiest way ever!</strong> The direction of the row depends on where you bring the needle down.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/decoration/6/Tutorial%203_files/image009.jpg" alt=""></p>
<p>This stitch is very good for a smooth transition from sequin fabric to mesh fabric. Usually, there is a quite noticeable border between the two. In that case I simply cut the required amount of sequins right off the sequin fabric and sew them on along the border by hand.</p>

