<p>In this video I would like to make a short review of techniques used for the tailoring of opaque corsets.</p>
<p>An opaque corset is the most widely popular corset type, and I still haven�t made a tutorial explaining what this corset is like and reviewing its types. To make things clearer - let me say a couple words about its sewing technique. These corsets are always multi-layer. They consist of a lining and a front interconnected in various ways. Rigilene bones are quilted either along the lining or along the front. Sometimes no Rigilene bones are used at all, but instead there is plastic or steel spiral boning inserted into the tunnels of seam allowances. Either steel busk or lacing can be used as a closure. </p>
<p>For better clarity I have divided the sewing techniques into three types. I have given each their own name in order to differentiate between them easier. </p>
<p><strong>1. &nbsp;The tunnel technique; </strong></p>
<p><strong>2. &nbsp;The simplified technique; </strong></p>
<p><strong>3. &nbsp;The quilted cup technique. </strong></p>
<p>Now I am going to try explaining and illustrating corsets sewn in different techniques with some specific reference. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image001.jpg" class="img-responsive center-block"></p>
<p>Let me start with the best known corsets sewn in the tunnel technique. </p>
<p>These corsets are made of at least two layers of fabric. Their details may be extra duplicated with iron-on fabric or not duplicated at all. The usage of extra duplication depends on the purpose of the corset, the tear resistance of the front fabric and the desired tightening degree. </p>
<p>A typical feature of this technique is a total lack of horizontal expansion of the form by the boning. The layers are as a rule connected without any pencil-edge turning-over along the top and bottom of the garment. </p>
<p>The tunnels for inserting plastic or steel boning are made from the seam allowances of the vertical curves. Decorative elements of separately sewn-on contrasting ribbons can also serve as tunnels. Corsets that are sewn in this technique suit well-proportioned women with a small bust. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image002.jpg" class="img-responsive center-block"></p>
<p>They can be worn by the youth to the disco and to parties. </p>
<p>For some reason exactly these corsets are told to have special tummy control qualities. </p>
<p>In my opinion this is a common mistake. Most young girls don�t need any particular tummy control; a little highlighting for the waistline is more than enough. </p>
<p>More mature women in their turn look rather foolish wearing corsets like this, and full-breasted women even appear vulgar. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image003.jpg" class="img-responsive center-block"></p>
<p>Look, why did the model on the picture lift her arms like this? </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image004.jpg" class="img-responsive center-block"></p>
<p>Because, if she had put them down, she would have looked somewhat like this. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image005.jpg" class="img-responsive center-block"></p>
<p>This technique is used for sewing sexy corsets as well as historic period or antique stylized corset garments. The main limitation of such a corset construction technique is the fact that it flattens the bust due to the lack of horizontal expansion and pushes a full bust completely out. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image006.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image007.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image008.jpg" class="img-responsive center-block"></p>
<p>The low fit around the bust is a result of sewing peculiarities. Joining the lining and the front along the top without turning-over makes it impossible to ease in at the neckline for tighter fitting to the body. Because of this the corset stands stiff along the top instead of flowing smoothly around the rounded curves. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image009.jpg" class="img-responsive center-block"></p>
<p>Rigilene boning is usually not used for sewing such corsets - spiral steel bones are used instead, and they cannot be pressed or shaped. Steel bones straighten the curves causing the bust to lose its roundness. </p>
<p>I find that corsets sewn like this don�t suit for tailoring wedding dresses.&nbsp; </p>
<p></p>
<p>Few people know that there are many other techniques of sewing opaque corsets because the tunnel technique is so widely used. But using other techniques you can also make modern comfortable corsets with all the required corset qualities. They re-shape the body even better making the bust and the hips rounder and defining the waistline. Such corsets are suitable for the tailoring of wedding dresses and evening gowns. </p>
<p></p>
<p>And now let me speak in detail about the two main types of modern corset tailoring. </p>
<p>This corset is sewn with the simplified technique. You can see the picture. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image010.jpg" class="img-responsive center-block"></p>
<p>And this is a quilted cup corset. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image011.jpg" class="img-responsive center-block"></p>
<p>Basically these techniques have much in common: the fabric is duplicated the same way, and you can use the same patterns regardless of the chosen sewing technique. </p>
<p>There are the same rules for the cutting of the corset. </p>
<p>However there are also many significant differences.</p>
<p>Additional strengthening cup details need to be cut for a quilted cup corset like the one you can see now in the picture. There are also some peculiarities in the sequence of joining the pieces and some unlike pressing requirements. For example a press-iron with a powerful steam-generator is needed for sewing a quilted cup. The steam pressure should be no less than 3-bar; otherwise the desired effect and form will not be achieved.</p>
<p>The major differences deal with the quantity, location and sequence of sewing on the Rigilene bones. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image012.png" class="img-responsive center-block"></p>
<p>In this picture you can see the scheme of sewing on the boning according to the simplified technique. The vertical bones are also stitched along the curves; however there is always a horizontal bone going across the bust-line on the front. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image013.jpg" class="img-responsive center-block"></p>
<p>And this is a scheme for sewing on the boning according to the quilted cup technique. As you can see, the difference is obvious! </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image014.jpg" class="img-responsive center-block"></p>
<p>Corsets sewn with the simplified technique already have a beautiful fixed form. You can use simple drapery, appliqu� ornaments or a little rhinestone contouring. However complicated deep d�collet� cuts are not possible within this technique. A corset like this doesn�t hold the neckline shape very well which wouldn�t allow for a perfect fitting. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image015.jpg" class="img-responsive center-block"></p>
<p>However corsets sewn with the quilted cup technique allow their designers to use their imagination all the way.&nbsp; Here you can use any kind of drapery. You could even cover the whole corset with rhinestones if you wished. </p>
<p>The true perfect mastery comes with ornately shaped cups, valves, purls, waves, and deep cuts - they can even reach right down to the waist. All this can be done when working with the quilted cup technique. Most women of almost any body shape can wear corsets made with this technique. They help fragile women make their bust appear visually larger and give the more full figured women a defined waist. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image016.jpg" class="img-responsive center-block"></p>
<p>Corsets sewn with the simplified technique are good for prom-dresses or simple-design dresses. They are suitable for women whose figures don�t really need any particular tummy control or re-shaping. </p>
<p></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image017.jpg" class="img-responsive center-block"></p>
<p>These corsets also suit women with full breasts because their bust fills up the space of the corset in a way that looks very natural. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image018.jpg" class="img-responsive center-block"></p>
<p>Corsets sewn with either of these two techniques can serve either as an individual garment or as a part of a dress with a detachable skirt. </p>
<p>Besides the corset length can in this case vary: it can reach to the hips, to the middle of the stomach or to the waistline. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image019.jpg" class="img-responsive center-block"></p>
<p>Moreover this corset could be turned into a cup for any dress if you cut-out only the top part up to the under-bust level. The bottom of the cup at the point where it�s joint to the dress can be shaped in many ways, which allows for multiple available configurations of the cup cut-outs. Such dresses can be made with or without shoulder-straps. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image020.jpg" class="img-responsive center-block"></p>
<p>Women of any age and constitution love dresses like this. They have a relaxed fit, are comfortable and very elegant. Lace, silk, chiffon, satin or crepe is all suitable for such designs. You could also use print fabric and sew some summer tunics. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image021.jpg" class="img-responsive center-block"></p>
<p>And now here is a <strong>body-dress (bodysuit)</strong>. </p>
<p>Here you could also sew a cup for an elastic body similarly to what I have said about the corset-cup dress. Of course a body is not as convenient to wear as a dress and requires ideal shapes. But it also has some advantages which are not typical either of corsets or of corset-cup dresses. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image022.jpg" class="img-responsive center-block"></p>
<p>The body is more and more often present in wedding and evening fashion. Look what chic models can be made based on it. </p>
<p>As I have said, bodies have their disadvantages. The main one is the awkward closure down the bottom. Having tried all possible types of this closure - clasps, snaps, touch fasteners, and buttons - I settle it with my clients in advance that the body should be sewn into a whole garment, like a swimsuit. Girls agree and accommodate themselves to this. Beauty requires sacrifice! If a future dress is going to have a heavy skirt, it can do without the panties part. It is then enough to sew a singlet and then sew a skirt on to it. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image023.jpg" class="img-responsive center-block"></p>
<p>However the whole thing is much more difficult when it comes to the designs you can see now on the screen. Firstly a body in such a dress always requires panties. The skirt and the decorative finish details (such as drapery or lace) need to be attached atop a finished body and very accurately placed with due regard to the further extension of the elastic body-parts. </p>
<p>Secondly, it is hard to avoid dragging when putting a small sized body on a stock sized mannequin. The mannequin should match the measures of the client perfectly. Ideally the elastic part of the body should be stretched widthways and lengthways exactly the same way as on the client. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/Opaque%20corsets.%20Review%20of%20sewing%20techniques_files/image024.jpg" class="img-responsive center-block"></p>
<p>That is why the location of the skirt and all decorative elements is usually marked right on the client during the fit test. Afterwards everything gets sewn and another fit test is made. You often need to make corrections more than once because any skirt has its own weight that can unexpectedly distort the body. Lace and the elastic material of the body itself have unlike extension properties. </p>
<p>Another difficulty rises when finishing the closure of the skirt and of the body-back. You need to seek a solution each time. There can be two separate zippers on the body and on the skirt, or - when the skirt sits low - a zipper is attached on the skirt and goes over onto the body. In this case the client should be able to fit into the body without using the closure. Or if the back of the body is low you can make a zipper only on the skirt. </p>
<p>Nonetheless whether a body is used as an individual garment or connected to a skirt - it lets you create masterpieces of wedding and evening garments. </p>
<p>My review is now over. I hope I have helped you immerse yourself into the atmosphere of this endless universe of opaque corsets, corset-cup dresses and body-dresses. </p>
<p>You were learning with Tatyana Kozorovitsky. </p>

