<p><strong>Fancy-Back Dresses</strong></p>
<p>When choosing a dress style, we tend to focus on front decorations devoting undeservingly little attention to the back. However some dresses have backs that are much more interesting than their fronts. Sometimes it�s impossible to convince your client that an open-back dress doesn�t suit her body type or the original dress model and you have to gratify her wish regardless. And then you need to take special measures in order to reveal the back and at the same time conceal any problem areas. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image002.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_00_27_14.Still001.jpg"> </p>
<p>Right now you�re looking at an example that satisfies these requirements: the back is open and at the same time there are sleeves. The bridge in the middle provides a good solution and prevents the straps from sliding off the shoulders. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image003.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_00_59_10.Still002.jpg"> </p>
<p>And this model enables you to cover up skin imperfections on the back with the help of a removable lace motif secured by a snap fastener at the waistline level. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image004.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_01_09_18.Still003.jpg"> </p>
<p>This picture demonstrates the back of a halter-neck dress. This model is perfect if your client wants both an open back and a collar. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image005.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_01_20_29.Still004.jpg"> </p>
<p>For fans of asymmetry: here is a dress with a diagonal strap across the back. This kind of decoration enables you to hide a fastener on the shoulder and make the dress easier to put on. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image006.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_01_40_04.Still005.jpg"> </p>
<p>And this design is dedicated to all who are fond of buttons. Bobbinet fabric that stretches only vertically is embellished with corded motifs and rhinestones. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image007.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_01_43_06.Still006.jpg"> </p>
<p>This is a clever idea for a really low-cut dress back that can�t do without supporting straps - you imitate a short shrug and disguise the lacing area with a flower to restrict the waist without demonstrating the lacing. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image008.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_02_02_12.Still007.jpg"> </p>
<p>This dress has a gorgeous back rich in beads and rhinestones. But you should know that this model can�t be based on a corset. It can be a bodice-reinforced dress at the very best since it is zipped up from the side. Please warn your client in advance that she won�t be able to put this dress on with her hair done. She�ll need to get dressed first and then have her hair styled. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image010.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_02_29_29.Still008.jpg"> </p>
<p>Whole-piece straps decorated with rhinestones. It looks very glamorous but it�ll take some thoughtful considerations to choose the right type of closure and its location. Though, if you really wanted to, you could put this dress on even if the only closure was located on the skirt. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image012.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_02_43_23.Still009.jpg"> </p>
<p>Here is a lovely idea for an open-back dress: crossed straps provide additional bust support. You need to prepare separate patterns in order to make straps like this, i.e. you need to draw them and cut them out individually. The straps can be draped only when they�re finished. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image014.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_03_06_00.Still010.jpg"> </p>
<p>And another variation of a dress with halter-neck straps and a stand-up collar. Don�t try to choose lace elements with a ready border. It�s done the other way round - you cut patterns out of bobbinet fabric, sew them and then attach the border. Moreover, please notice that the lace motifs on the opaque part of the dress are appliqu�d over bobbinet fabric. That�s called final decoration of a ready dress. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image016.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_03_38_11.Still011.jpg"> </p>
<p>This model is only suitable for women with small breasts. Since the back is open and the straps are used for a purely decorative purpose, the dress is hardly secured in place.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image018.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_03_55_04.Still012.jpg"> </p>
<p> Whenever I sew dresses with a similar back, I add slim straps around the arms. Then the dress can be worn by a woman with any breast size at all. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image019.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_04_04_24.Still013.jpg"> </p>
<p>This design will satisfy a client who wants to have everything at once: buttons, collar, sleeves and a very low back neckline. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image020.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_04_12_18.Still014.jpg"> </p>
<p>This type of decoration allows you to hide wide straps that are necessary for full-breasted women. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image021.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_04_19_24.Still015.jpg"> </p>
<p>Here is another way you can use a whole sheet of lace fabric. But in this case the fastener is located on the side of the dress, which can be either bodice-reinforced or soft. </p>
<p>And this dress provides a nice creative way to cover upper arms: it is not a shawl, neither a throw-over nor sleeves. Please note that the drapery is imitated by separate straps of fabric - it makes the sewing process a lot easier and creates a particular effect. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image022.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_04_32_11.Still016.jpg"> </p>
<p>This is a perfect idea for those who think big instead of limiting their imagination to a vertical row of buttons. I personally love teardrop closures regardless of their size. They look delicate and feminine, they are neat and convenient and at the same time you can add slim straps for better fitting to the body. Lace appliqu� will cover up any problem areas. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/fancy-back%20text_files/image023.jpg" class="img-responsive center-block" alt="Description: E:\Corset Academy\English\texts for translate\fancy-back\Sequence 02.00_05_01_24.Still017.jpg"> </p>
<p>Speaking about this picture I�d like to draw your attention to the way the sleeves are attached. This dress is made of very delicate Solstiss lace from France. It is so fine that even a 0.5cm seam would look like a log of wood. That is why the scye is carefully cut after the lace motif. The sleeves are sewn-in by hand with very thin, basically invisible stitches creating a one-piece illusion. </p>

