<p>Greetings, my dear friends!</p>
<p>As you know, corsets can be see-through or opaque. This kind of classification implies same-name corset tailoring techniques and points at the type of fabric used. In other words, you can employ the see-through corset technique and related patterns for making opaque or semi-transparent garments.</p>
<p>&nbsp;<br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image001.jpg" class="img-responsive center-block"></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image003.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong>The See-Through Corset Technique Has the Following Key Features:</strong></p>
<p>The garment has individually cut quilted cups. </p>
<p>It is always a single-layer garment. Each piece is made of one or several layers of fabric joined with a single seam. There is no individual layer for the lining. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image005.jpg" class="img-responsive center-block"></p>
<p>Rigilene bones are stitched along the seam allowances on the face side of the garment and are visible when it�s finished. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image006.jpg" class="img-responsive center-block"></p>
<p>It is compulsory to use additional plastic or spiral boning apart from Rigilene bones in order to reinforce the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image007.jpg" class="img-responsive center-block"></p>
<p>The see-through corset technique allows you to sew a garment with see-through fabric based on an opaque corset pattern, i.e. without individually cut cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image008.jpg" class="img-responsive center-block"></p>
<p><strong>What Fabric Is Best for Working in the See-Through Corset Sewing Technique? </strong></p>
<p>You should use thin but strong fabric and it�s possible to use two-way stretch fabric. Mind you: all-way stretch mesh fabric used for sewing sleeves, swimsuits or ballet dancewear is absolutely inacceptable in this case! If you want to make a see-through corset, you should opt for very closely-woven synthetic mesh fabric. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image009.jpg" class="img-responsive center-block"></p>
<p>&nbsp;<br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p>According to the see-through corset technique each Rigilene bone serves as a part of a fabric tunnel in which you insert a plastic or a steel bone. Rigilene bones themselves are finished with a bias tape or encased in the main fabric of the garment. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image011.jpg" class="img-responsive center-block"></p>
<p><strong>The Shape of Individually Cut Cups. </strong></p>
<p>Cups can either be attached in a straight line at the under-bust level or ascending at the sides. They can also be sewn-in similarly to bra cups. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image013.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image014.jpg" class="img-responsive center-block"></p>
<p>You can easily combine the see-through technique with some elements of the opaque corset sewing technique and vice versa. For example you could use the opaque technique to make an opaque central part on a corset with a deep d�collet� and integral cups. And the rest of the corset would be sewn in the see-through technique. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image015.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image017.jpg" class="img-responsive center-block"></p>
<p>There are some highly important fine points that concern the cutting process and the technique for joining see-through and opaque pieces of such combined garments. I will teach you how to join them correctly to ensure that the thin fabric doesn�t get stretched out and avoid distortion. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>I Would Like to Give a Special Mention to Some Peculiarities of Sewing Bodysuit-Type Garments. </strong></p>
<p>Looking through online discussions dedicated to sewing bodysuit garments I have noticed several common misconceptions: people either want to reinforce a bodysuit-based garment with additional bones or - the other way round - they want to remove bones off a garment based on a see-through corset so as to turn it into a bodysuit.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image019.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image020.jpg" class="img-responsive center-block"></p>
<p>&nbsp;I can�t approve of such experiments! You can�t use any bones if you want to make a bodysuit from highly elastic fabric! You will end up wrecking your garment instead of reinforcing it! </p>
<p>But you can change the appearance of a garment like this radically by sewing it with non-elastic fabric following the see-through corset technique! </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image021.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image022.jpg" class="img-responsive center-block"></p>
<p>By the way, a deep d�collet� at the front of a bodysuit should be finished according to the technique for sewing see-through corsets. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image023.jpg" class="img-responsive center-block"></p>
<p>You can feel free to drape corset garments sewn in the see-through technique. They will stay lightweight and gracious. In this case bones are sewn-in from the underside and are nearly unnoticeable. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image024.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image025.jpg" class="img-responsive center-block"></p>
<p>The sewing process is plain and simple when you know how to finish and join the patterns correctly. </p>
<p><strong>A Few Words on Altering Patterns with Individually Cut Cups. </strong></p>
<p>This technique doesn�t require that you take any special measurements off your client. If you want to, you can measure the distance between the under-bust line and the waistline for further reference. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image026.jpg" class="img-responsive center-block"></p>
<p>But you shouldn�t forget that corset garments with individually cut cups are supposed to have the push-up effect and visually enlarge the breasts. That�s why I wouldn�t recommend offering a model like that to a client with a large bust. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image027.jpg" class="img-responsive center-block"></p>
<p>Corsets with individually cut cups are perfect for women with small to medium-sized breasts. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image029.jpg" class="img-responsive center-block"></p>
<p></p>
<p>And now I�d like to give you a valuable piece of advice! Don�t rush into altering the cups whenever you need to adjust your see-through corset pattern after an individual size! First sew a mock-up garment and see what can be done. If your client has a small bust, you can always use push-up pads and preserve the original cup size. No woman would be happy if her chest looked flat because of disproportionally small corset cups! If you�re new to corset tailoring, then I strongly recommend that you should try and sew a pilot sample after studying the course. </p>
<p>There have been multiple occasions when people bought my courses after accepting an order for a certain corset model. And they�d start working on the actual order right after getting their hands on the pattern and the guidelines. Experienced seamstresses get the hang of it in no time and get by without trouble! But people with less experience flood me with e-mails asking the following questions: </p>
<p>� What size is this pattern? </p>
<p>� Why didn�t my size match yours after I printed the pattern? </p>
<p>� My seams and notches don�t match. </p>
<p>� The princess seams on the cups and the straps are different in length. </p>
<p>� And many other similar issues. </p>
<p>I have to answer these E-mails and repeat what�s already described in the tutorials. So my advice is: if you want to engage yourself in sewing wedding or evening garments, please study the learning material carefully <em>before</em> accepting any orders! Sew a pilot sample, make a mock-up, measure its dimensions, check the way different pieces match - and then you won�t have any trouble adjusting the pattern after your client�s measurements. You will find a link to Pattern Adjustment Tables in the description below the video down the bottom of the page. </p>
<p>Even if you have already been working in the field of wedding fashion for some time, studying the material dedicated to the see-through corset technique will help you feel much more confident during negotiations. You will be able to rationalize sewing a beautiful dress with a see-through corset base thereby increasing your profit and leaving your clients pleasantly surprised at your skillfulness and experience. </p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image030.jpg" class="img-responsive center-block"></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/Reviews/transcripts/transparent2_files/image031.jpg" class="img-responsive center-block"></p>
<p>Another popular thing I get asked per E-mail is specific questions related to particular corset models. And when I inquire why you haven�t simply purchased the course dedicated to the technique you�re interested in, you say you didn�t like the models presented within the course. But you should feel free to follow your own creative flows! It�s hardly possible to come up with designs that will please everyone and yet I need to demonstrate the sewing technique! So please don�t think of my courses as tutorials for sewing particular corset models. They are focused on describing the essence of sewing techniques that are applicable to any model of your choice once you have mastered them. You will be able to sew corsets of various designs and complexity relying on the know-how provided in my courses and on your own experience. </p>
