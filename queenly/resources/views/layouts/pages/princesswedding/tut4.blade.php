<h1>Tutorial 3. Drawing Guidelines on the Lining.</h1>
<p><strong>My elastic waistband skirt will be assembled in tiers. In other words, I will put veiling ruffles on the lining one by one. It is the key trick. And this is why I need to mark corresponding guidelines on the lining in advance. </strong></p>
<p><strong>The lining was cut as a half-circle skirt.</strong> It is 105cm long along the selvage, which is 15cm shorter than the main skirt is supposed to be.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%203.files/image002.jpg" alt=""></p>
<p><strong>I will put four veiling ruffles on the lining. </strong>I mark the first guideline 20cm above the bottom edge of the lining (1). Then I draw the second guideline 20cm above the first one (2). And I draw the third guideline 20cm above the second one (3). The last guideline is marked 15cm above the third one (4).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%203.files/image004.jpg" alt=""></p>
<p><strong>Then I measure the lengths of all guidelines to figure out how long the gathered ruffles are supposed to be. </strong>Since the skirt is folded in half, it is easier to work with half-lengths. Here is what I got (your values may differ from mine):<strong> a half of ruffle (1) is 185cm; a half of ruffle (2) is 150cm; a half of ruffle (3) is 120cm; and a half of ruffle (4) is 85cm. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%203.files/image006.jpg" alt=""></p>
<p><strong>I would like to give you a piece of advice before we start calculating widths and lengths of veiling strips for the ruffles. I recommend you should cut the veiling on the cross so that the length of each strip equals the width of the veiling fabric (300cm). </strong>This will make the cutting process much easier and the gathers will also be neater. Each ruffle will be made of 300cm long strips of veiling, joined together and then gathered on a thread. High enough gathering ratio will allow you to gather the fabric very thickly and thereby hide the joints between the strips.</p>
<p><strong>I start calculating the widths and the number of veiling strips for the four ruffles which will be sewn directly on the lining of the skirt. </strong></p>
<p><strong>Ruffle (1).</strong> It is positioned 20cm above the bottom edge of the lining. But the main skirt is 15cm longer than the lining. <strong>This means the first ruffle needs to have the following width: </strong></p>
<p><strong>15cm + 20cm = 35cm</strong></p>
<p><strong>Judging from previous experience, I believe 6 will be the best gathering ratio for this skirt. GR ≈ 6. </strong></p>
<p><strong>This means I need to prepare seven strips of veiling for the first ruffle, and each strip should be 35cm x 300cm. I will join them in a single strip with a length of 21 meters and gather it down to 3.7 meters. </strong></p>
<p><strong>Veiling fabric consumption for ruffle (1): </strong></p>
<p><strong>0.35m × 7 strips = 2.45m</strong></p>
<p><strong>Ruffle (2).</strong> It is positioned 20cm above the first ruffle. <strong>The width of the second ruffle is as follows: </strong></p>
<p><strong>15cm + 20cm + 20cm = 55cm</strong></p>
<p><strong>Considering the gathering ratio, I will need six strips of veiling for the second ruffle, and each strip should be 55cm x 300cm. I will join them in a single 18-meters-long strip and gather it down to 3 meters. </strong></p>
<p><strong>Veiling fabric consumption for ruffle (2):</strong></p>
<p><strong>0.55m × 6 strips = 3.3m</strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%203.files/image008.jpg" alt=""></p>
<p><strong>Ruffle (3).</strong> This ruffle is positioned 20cm above the second one. <strong>It must then have the following width: </strong></p>
<p><strong>15cm + 20cm + 20cm + 20cm = 75cm</strong></p>
<p><strong>Considering the gathering ratio, I will need five strips of veiling for the third ruffle (75cm x 300cm each). I will join them in a single strip with a length of 15 meters and then gather it down to 2.4 meters. </strong></p>
<p><strong>Veiling fabric consumption for ruffle (3):</strong></p>
<p><strong>0.75m × 5 strips = 3.75m</strong></p>
<p><strong>Ruffle (4).</strong> It is positioned 15cm above the third ruffle. <strong>It must therefore have the following width: </strong></p>
<p><strong>15cm + 20cm + 20cm + 20cm + 15cm = 90cm</strong></p>
<p><strong>Considering the gathering ratio, I will need five strips of veiling for the fourth ruffle (90cm x 300cm each). I will join them in a single strip with a length of 15 meters and then gather it down to 1.7 meters (I will increase the gathering ratio at the top of the skirt). </strong></p>
<p><strong>Veiling fabric consumption for ruffle (4): </strong></p>
<p><strong>0.9m × 5 strips = 4.5m</strong></p>
<p>I will achieve the desired shape and fullness by putting these four ruffles on the lining and by making each ruffle reach down to the bottom of the skirt. I will need a total of 14 meters of veiling with a width of 300cm.</p>
<p><strong>The area between the fourth ruffle and the top edge of the skirt remains uncovered now. I will overlay it with three identical ruffles (5, 6, and 7), each with a length of 120cm (skirt length). </strong></p>
<p><strong>Each of these three ruffles will be made of three 300cm long strips of veiling. </strong>In other words, I will make three 900cm long ruffles and then gather each one down to the length of the top edge of the skirt with due account for the V-shaped part. Then I will put all three ruffles together and sew them to the top edge of the skirt with a single seam.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%203.files/image010.jpg" alt=""></p>
<p><strong>Total veiling consumption for the three ruffles (5, 6, and 7): </strong></p>
<p><strong>1.2m × 3 strips × 3 ruffles = 10.8m</strong></p>
<p><strong>There are considerable advantages to the offered method of assembling an elastic waistband skirt. </strong></p>
<p><strong>Firstly, </strong>joining three layers of fabric along the waistline is a lot easier than joining seven layers.</p>
<p><strong>Secondly,</strong> putting ruffles on the lining in tiers allows you to achieve a beautiful shape and desired fullness. Your skirt will no longer look like a lamp-shade!</p>
<p><strong>Thirdly, </strong>total veiling consumption is only 25 meters. And this means one roll of veiling with a width of 300cm will be enough for sewing two skirts like this.</p>
<p><strong>Now you can see that there is a simple and cost-efficient way to sew a luxury skirt! </strong></p>
<p><strong>The first time I introduced this method to my students was in 2014, during full-time courses at the Corset Academy in Israel. It has been widely used in the making of similar skirts ever since. </strong></p>

