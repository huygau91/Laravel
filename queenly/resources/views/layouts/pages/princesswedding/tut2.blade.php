<h1>Tutorial 1. Required Materials and Measurements. Pattern Calculation.</h1>
<p><strong>I will use the following materials for my full skirt with elastic waistband:</strong></p>
<p><strong>- Lining fabric.</strong> You can use plain lining fabric for a low-budget version or crepe-back satin for a more expensive version.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%201.files/image002.jpg" alt=""></p>
<p><strong>- Veiling.</strong> Since it is the main material, it must be chosen very carefully. I use medium-density veiling sold per roll in online stores. It helps me cut down the costs of production.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%201.files/image004.jpg" alt=""></p>
<p><strong>- Zipper. </strong></p>
<p><strong>- Petticoat. </strong>I will use a plain inexpensive petticoat with seven hoops. You can use a petticoat with five hoops: it won’t make a big difference. The distance between the top edge and the top hoop is usually the same regardless of the amount of hoops.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%201.files/image006.jpg" alt=""></p>
<p><strong>I will sew the skirt to a bodice which was made using the technique for sewing non-see-through (opaque) corsets and the patterns from the corresponding books by the Corset Academy. </strong>I have intentionally made the task harder by choosing a bodice with a V-shaped bottom edge.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%201.files/image008.jpg" alt=""></p>
<p><strong>You need to know the following measurements to calculate and cut the skirt (I took them off the bodice put on the dress-form): </strong></p>
<p><strong>- Waist Circumference. This measurement is taken at the supposed level of the top edge of the skirt. </strong>It is usually 4cm below the waistline. I got <strong>77cm.</strong></p>
<p><strong>- Skirt Length. I will use the standard length of 120cm. </strong></p>
<p><strong>- Centre Waist to Bottom Sharp Point. It is 7cm in my case. </strong></p>
<p><strong>The process of cutting an elastic waistband skirt is very simple and you are already familiar with it. </strong></p>
<p><strong>The base is a half-circle skirt cut from the lining fabric. </strong></p>
<p><strong>The main layers are cut from the veiling. </strong></p>
<p><strong>The half-circle skirt is calculated based on the following formula: </strong></p>
<p><strong>πR = Waist Circumference.</strong></p>
<p><strong>Let us find the radius of the skirt (R): </strong></p>
<p><strong>πR = 77cm</strong></p>
<p><strong>R = 25cm</strong></p>
<p><strong>You can cut a half-circle skirt knowing the radius and the length. </strong></p>
<p><strong>I will make the radius a little bigger just in case: </strong></p>
<p><strong>R = 26cm</strong></p>
<p><strong>The lining skirt is normally made 10-20cm shorter than the main veiling skirt. </strong></p>
<p>My veiling fabric is wide enough (150cm) to fit everything, even with a skirt length of 120cm and a radius of 26cm.</p>
<p><strong>When cutting the lining, I will make it 15cm shorter than the main skirt. </strong></p>
<p><strong>120cm - 15cm = 105cm</strong></p>
<p>I will cut the skirt right on the table using the method described in the previous books by the Corset Academy, i.e. by folding the fabric in a triangle.</p>
<p>I will also account for the fact that the skirt needs to be sewn to a bodice with a V-shaped bottom edge the bottom point of which reaches 7cm below the waistline.</p>

