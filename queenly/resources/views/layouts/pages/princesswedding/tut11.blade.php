<h1>Tutorial 10. Evening Out the Hem of the Skirt.</h1>
<p><strong>The goal of this tutorial</strong> is to show you that correctly taken measurements allow you to sew a veiling skirt without fittings and without having to adjust the length of the finished garment.</p>
<p>I was absolutely sure I would hardly have to even out the hem from the start. I could only expect to see some minor irregularities.</p>
<p><strong>I was so confident because I knew I had measured the skirt length correctly. </strong></p>
<p><strong>This is how I take this measurement: </strong>with my client standing in her shoes, I measure the distance from her waist to the floor letting the measuring tape hang down vertically. Suppose I got 116cm.</p>
<p>Then, I secure the measuring tape at the 116cm mark at the waistline of the dress-form and keep adjusting the height of the dress-form until the other end of the measuring tape touches the floor. This is how I adjusted my dress-form to the measurement taken off the client.</p>
<p>Next, I put the customized petticoat on the dress-form and measure the length of the skirt by bringing the measuring tape from the waistline of the dress-form to the bottom edge of the petticoat with account for its fullness. Suppose I got 120cm. <strong>This value is the precise length of the skirt! </strong></p>
<p><strong>And I relied on this value when I was calculating fabric consumption and cutting the veiling!</strong></p>
<p><strong>WARNING: this method works so great only with veiling skirts!</strong> The process of evening out the hem of a slim silhouette garment is more complicated.</p>
<p><strong>I have sewn a full veiling skirt knowing its correct length and now I need to even out the hem. </strong></p>
<p><strong>There are two ways you can do this on veiling skirts. </strong></p>
<p><strong>Method 1. All layers of veiling have the same length and are positioned at the same distance from the floor. </strong>When you cut a skirt this way, you should account for the fact that its volume will increase with each new layer of veiling. In other words, if you cut all layers of veiling with a length of 120cm, then each subsequent layer will end up shorter than the previous one because its bottom edge will be further from the floor.</p>
<p><strong>For this reason, I make each new layer of veiling 1cm longer than the previous. </strong></p>
<p>My skirt is made of seven layers of veiling, which means the last layer (ruffle (7)) must be 7cm longer than the first one (ruffle (1)).</p>
<p><strong>Method 2. All layers of veiling have different lengths, i.e. the bottom of the skirt looks tiered. </strong>In this case, I cut all ruffles with required lengths without accounting for the volume of the skirt, i.e. without adding 1cm to the length of each new layer.</p>
<p><strong>I use both methods depending on client�s preferences but, in my personal opinion, skirts sewn the second way look prettier. </strong>When all layers of veiling are slightly different in length, the irregular shape of the hem makes the skirt look even fuller. The skirt looks like a delicate soft cloud.</p>
<p><strong>Our skirt was also cut this way, with different lengths of veiling layers. </strong>I did not focus on this detail in the beginning of the book because you were not yet familiar with the overall sewing process.</p>
<p><strong>And now I am happy to show you that you will hardly have to even out the hem of your skirt if you cut your veiling layers following the guidelines like I did. </strong></p>
<p><strong>But first, let me give you some useful tips: </strong></p>
<p>- The hem of the skirt should stay 1-1.5cm above floor level. This will allow the bride to walk freely without getting tangled up in the inner layers of veiling. That distance to the floor will hardly be noticeable when she is standing, too.</p>
<p>- I highly recommend you ask the bride to move around actively and spin around in the skirt at the final fitting to make absolutely sure the length was chosen correctly.</p>
<p>- &nbsp;Always add another 15-20cm to the calculated skirt length if the bride comes to order her dress without the shoes she plans to wear with it or if she hasn�t decided on the high heel height yet.</p>
<p><strong>And now it is time to even out the hem of the skirt. </strong></p>
<p><strong>I put the dress-form with the finished garment on a platform </strong>so as to have the hem hanging down freely along the full perimeter.</p>
<p><strong>I shake up all layers of veiling inside the skirt and secure them with pins a couple of centimeters from the bottom edge. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2010.files/image002.jpg" alt=""></strong></p>
<p><strong>I find the third exterior layer of veiling (ruffle (5)) and trim the edges of all other layers to keep them even enough with it. </strong>This work does not require 100% accuracy. I keep turning the dress-form around gradually trimming the entire hem. The whole process takes no longer than 10 minutes. If you don�t feel confident trimming the hem of the skirt just like that, then you can use a ruler to keep checking the distance from the edge to the floor. With time, you will get experienced enough to do it by the eye.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2010.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2010.files/image006.jpg" alt=""></p>
<p><strong>Done! The dress is ready!</strong> I believe I have managed to show you that sewing a full skirt with veiling does not have to be difficult and that you can do it at minimum expense and within a rather short time, especially if you use the described technique of assembling a skirt in tiers. You don�t have to have years of practice behind you to sew such skirts without fittings.</p>
<p><strong>Needless to say, such skirts are timeless! A full skirt with elastic waistband is a classic garment that will always be trending in the wedding fashion world! </strong></p>

