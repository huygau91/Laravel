<h1>Tutorial 11. Teaching the Bride How to Correctly Manage Her Skirt in Wear to Walk Around, Stand Up, and Sit Down without Discomfort.</h1>
<p><strong>Here is what the finished dress looks like on my client: </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2011.files/image002.jpg" alt=""></p>
<p><strong>The bottom diameter of the skirt is 1.8 meters. </strong></p>
<p>The lining is basically lying on the floor while the face layers stay a couple of centimeters above it.</p>
<p>As I said before, this fullness and diameter have been achieved with the most efficient fabric consumption you could possibly think of. And it was enough to hide the hoops and the seams.</p>
<p>If you want to make a yet fuller skirt, then you should simply increase the amount of veiling layers.</p>
<p>Assembling the skirt in tiers by sewing layers of veiling to the lining at different levels prevents the ruffles from getting tangled up. They settle on the skirt properly and create the desired fullness.</p>
<p>The difference in the shape of the bottom edges of veiling layers adds to the charm of the skirt.</p>
<p><strong>I want to say a couple of words about spreading out the skirt after putting it on. </strong>After the bride puts the dress on, ask her to move her torso left and right several times. This will let some air in-between the layers of veiling causing them to spread out and enhance the look of the skirt.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2011.files/image004.jpg" alt=""></p>
<p><strong>You must also explain to the bride how to sit in a skirt like this. </strong>The top hoop of the petticoat is positioned right below her buttocks. This means the bride should lift the top hoop up to her waist before sitting down. She will be able to sit comfortably and the skirt will look great, too! She must never sit on the hoop because it will make the front of the skirt rise, which does not look pretty at all!</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%2011.files/image006.jpg" alt=""></p>
<p><strong>Now you know how to sew a full skirt with elastic waistband! I hope your new skills will bring lots of joy to you and your clients!</strong></p>
