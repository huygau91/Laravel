<h1>Tutorial 12. Costs of Production and Expected Profit.</h1>
<h2>Pictures of the Finished Garment.</h2>
<p><strong>In conclusion, I would like to present a list of all materials I used for the bodice and the skirt including their quantity and costs. </strong></p>
<p><strong>Skirt supplies: </strong></p>
<p>- 25 meters of veiling;</p>
<p>- 3 meters of crepe-back satin;</p>
<p>- 1 petticoat;</p>
<p>- 1 zipper.</p>
<p><strong>Bodice supplies: </strong></p>
<p>- 1 meter of main fabric;</p>
<p>- 10 meters of Rigilene boning;</p>
<p>- 0.2 meters of lace fabric;</p>
<p>- 100 rhinestones;</p>
<p>- 1 meter of woven interfacing.</p>
<p><strong>All these materials cost me a total of about 3,000 RUR (wholesale prices). </strong></p>
<p>I don�t know how much the production of a similar dress would cost in your region or country but I recommend you should find out how much local wedding salons ask for such garments and make your price 15-20% lower.</p>
<p>You can calculate your expected profit keeping in mind that the cost of production must not exceed 20% from the final price.</p>
<p>A bodice and a full skirt like these can be made within 4-5 days of work at quite moderate pace.</p>
<p>As you can see, you can have a decent income if you make 3-4 similar outfits a month.</p>
<p>Of course, the next thing you need to do is find your target audience! �The Basics of Working in Sewing Industry" by the Corset Academy will be very helpful in that regard.</p>
