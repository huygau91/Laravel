<h1>Why an Elastic Waistband Skirt?</h1>
<p><strong>I chose the skirt style based on the following requirements: </strong></p>
<p>- It must be a popular style;</p>
<p>- It must be a luxury style;</p>
<p>- It must be an eye-catcher;</p>
<p>- It must be appropriate for different age groups.</p>
<p>After analyzing a wide range of wedding dresses, relying on my own and my students’ experience, <strong>I decided that a classic wedding dress with a full elastic waistband skirt is one of the most sought-after types, and it meets all the above-mentioned requirements. </strong></p>
<p><strong>So, we are making a wedding dress with a full skirt with elastic waistband. </strong></p>
<p><strong>It is a classic wedding dress model. </strong>Fashion trends come and go but this style is timeless.</p>
<p><strong>Potential clients who are likely to order a dress like this are 18-24 year old romantic girls </strong>who want to look like princesses.</p>
<p><strong>These girls don’t care about trending wedding dress styles − they just want a gorgeous dress with a really full skirt! </strong></p>
<p><strong>Such dresses are also known as princess dresses. </strong></p>
<p><strong>Using the know-how’s given in this book, you will be able to sew a fabulous full skirt with elastic waistband, ensure a perfect fit, and cut down fabric consumption by up to 30%. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Why%20an%20Elastic%20Waistband%20Skirt.files/image002.jpg" alt=""></p>
<p><strong>Expect to hear lots of compliments from the bride and from her guests! </strong></p>
<p><strong>I can guarantee that your bride will always become the centre of attention in any wedding hall or reception venue! </strong></p>
<p><strong>No one else will have a fabulous full skirt like hers! </strong></p>
<p><strong>You will learn all this and much more from the tutorials presented in this new book by the Corset Academy − “Top Wedding Skirts. Full Skirt with Elastic Waistband." </strong></p>
<h1>Foreword.</h1>
<p><strong>I am about to start working on a full skirt with elastic waistband, a very popular style.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Why%20an%20Elastic%20Waistband%20Skirt.files/image004.jpg" alt=""></strong></p>
<p><strong>I will show you</strong> how to make a quality full skirt using a plain cheap petticoat with five or seven hoops. The final garment must deserve to be called a luxury wedding skirt. And we also want to spare up to 30% of veiling.</p>
<p>I believe every seamstress knows how to sew a plain elastic waistband skirt: you simply fold a rectangular piece of fabric in half, sew the central back seam, and gather the top edge of the skirt on an elastic band.</p>
<p>But I am also sure you have noticed that, in reality, skirts of this type can look very different.</p>
<p><strong>The main defect commonly found in elastic waistband skirts</strong> results from insufficient fullness when you can see the lining or, even worse, the petticoat hoops through the main fabric. When you see something like this, you can be sure that the dressmaker tried to spare the veiling and used too few layers of it. As the result, the skirt looks really awful!</p>
<p><strong>There is only one conclusion: you need to know how to sew such skirts correctly! </strong></p>
<p><strong>How to make a beautiful full skirt with elastic waistband? </strong></p>
<p><strong>When you see a poorly made skirt like that, you think of increasing the amount of veiling straight away. </strong>In other words, you decide to increase the length of the rectangular piece of veiling, as well as the number of layers. And this is what most people do, indeed. They take veiling with a width of 300cm and cut five 120cm long pieces for each layer. And they use a total of 5-7 layers like that.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Why%20an%20Elastic%20Waistband%20Skirt.files/image006.jpg" alt=""></p>
<p>Of course, the skirt will look a lot better with that amount of veiling. It may not be perfect but nothing will show through at least.</p>
<p><strong>This method, however, has serious disadvantages. </strong></p>
<p><strong>Firstly,</strong> the skirt comes out really thick in the waist. Imagine that you have to gather 15 meters of veiling down to, say, 90cm. This implies very thick gathers. And there are a total of seven layers like that! Sewing a skirt like this to a bodice will be a big problem. Your sewing machine won’t be able to handle it and you will have to use the hand wheel.</p>
<p><strong>Secondly, </strong>even if you manage to attach the skirt to the bodice, the amount of thickness in the waist will make it somewhat dome-shaped at the top and straight toward the bottom. The bottom diameter will be maximum 130-140cm.</p>
<p><strong>Frankly speaking, it is impossible to achieve a really beautiful shape using this method. </strong>The lining and the hoops will not show through but the skirt will resemble a lamp-shade.</p>
<p><strong>What to do to improve the shape of the skirt then? </strong>You could press the top of the skirt with steam. It won’t look perfect but it will make it flatter and more even. The shape of the skirt will look more balanced in this case.</p>
<p><strong>Finally, another disadvantage of this method deals with considerable fabric consumption. </strong>The skirt will require about 42 meters of veiling.</p>
<p><strong>Of course, you could increase the fullness of the skirt by using a special petticoat. It would be the easiest solution </strong>but such petticoats are expensive and hard to find. I gave up on this idea very soon.</p>
<p><strong>The sewing process was a real torture and the skirt turned out to be very expensive because of the high fabric consumption. Besides, it looked nothing like a “luxury skirt."</strong></p>
<p><strong>After careful thought, I decided to use a different sewing technique which I want to share with you in this book. </strong></p>
<p><strong>The main advantages of this method of sewing a full skirt with elastic waistband: </strong></p>
<p>- It is fast;</p>
<p>- It is budget-friendly;</p>
<p>- It does not imply using any special petticoats. It is enough to use a plain petticoat with 5-7 hoops;</p>
<p>- It cuts fabric consumption from 42-45 meters down to 25-29 meters (veiling);</p>
<p>- It allows you to increase the bottom diameter of the skirt from 130-140cm up to 180-200cm;</p>
<p>- It allows you to achieve a perfect shape. Your skirt will be true luxury garment!</p>
<p><strong>To sum up, this technique allows you to achieve the best results at minimum expense!</strong></p>
<p><strong>Classic wedding dresses which feature a full skirt with elastic waistband (also known as princess wedding dresses) are currently in high demand despite the trending silhouette dresses and skirts. </strong>According to the statistics, about a half of all brides prefer a classic wedding dress.</p>
<p><strong>Moreover, </strong>I have been successfully implementing the same technique in the making of other types of dresses and skirts.</p>
<p><strong>I will also explain how to even out the hem of a full veiling skirt in a fast and easy way and how to teach the bride to wear it correctly. </strong></p>
<p><strong>In addition, I will show you a fast and easy method of attaching the skirt to the bodice of the dress. </strong>I have intentionally chosen a bodice with a V-shaped bottom edge for this dress. The base of the skirt is made of a rectangular piece of fabric and many seamstresses tend to make the same mistake when sewing it to a bodice with a V-shaped bottom edge: they sew the skirt to the bodice first and then they even out the hem. You must never do it this way!</p>
<p><strong>I will need a plain five-hoop petticoat, lining fabric, and veiling for my skirt. </strong></p>

