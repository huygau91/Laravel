<h1>Tutorial 5. Joining the Strips of Veiling and Gathering Them to the Desired Length (Fast, Easy, and Precise Method).</h1>
<p><strong>I start working on the first ruffle (1). </strong></p>
<p><strong>I sew together all seven 35cm wide strips of veiling aligning their lengthwise grain edges. </strong>I sew with the smallest possible seam allowance (2-3mm). You should be very careful when gathering the veiling and make sure to keep all seam allowances on the same side.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image012.jpg" alt=""></p>
<p><strong>The resulting strip of veiling is 21 meters long. I carefully press it. (All seam allowances should be pressed flat to the same side). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image014.jpg" alt=""></strong></p>
<p><strong>I make a row of stitches along one long edge of the strip to gather it down to the required length. Its full length should be 370cm or 185cm when folded in half (the length of the horizontal guideline for the first ruffle).</strong> I select the biggest possible stitch length on my sewing machine. I am not using a ruffler presser-foot because I will work with different gathering ratios. <strong>Tip:</strong> leave 2-3cm from either end of the strip, you will need this seam allowance for assembling the skirt later.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image020.jpg" alt=""></p>
<p><strong>I start gathering the veiling on the thread.</strong> The strip is very long, 21 meters, and I need to gather it down to 370cm which is also quite long. Here is a handy trick to make this work easier: find the middle of the strip, pin it to the cutting table or to the dress-form, and start gathering both halves of the strip simultaneously. Each half needs to be gathered down to 185cm. You will achieve neater gathers and you won�t need as much space around you to do this work.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image022.jpg" alt=""></p>
<p><strong>The first ruffle is ready (1).</strong> Its full length is 370cm (185cm when folded in half).</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image023.jpg" alt=""></p>
<p><strong>I put the lining with horizontal guidelines on the cutting table, pin the middle of ruffle (1) to the middle of the corresponding guideline, and distribute the veiling neatly. I start from the middle, pin down first one half, and then the other half. </strong>I recommend you start from the bottom ruffle and gradually move upwards.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image028.jpg" alt=""></p>
<p><strong>That is all. The first ruffle (1) is pinned on along the sewing guideline. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image030.jpg" alt=""></p>
<p><strong>I start sewing it on. Remember to leave a 2-3cm seam allowance from either end. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image032.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image034.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image036.jpg" alt=""></strong></p>
<p><strong>I put the lining with the first ruffle on the dress-form with a factory-made petticoat on it. </strong>It looks great. The density of the veiling allows the ruffle to hold its shape well without slacking. The seam allowances of the joining seam can be overlaid with a bias tape or a lace trimming.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image038.jpg" alt=""></p>
<p><strong>The remaining ruffles (2, 3, and 4) are put on the lining just the same way.</strong> I gather the strips down to the required lengths to match the guidelines. <strong>I believe you got the hang of the process! </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%205.files/image040.jpg" alt=""></strong></p>
<p><strong>In conclusion, I would like to highlight that I am using the smallest possible amount of veiling strips and of ruffles themselves which allows you to create a luxurious skirt at minimum expense and with the least effort!</strong></p>

