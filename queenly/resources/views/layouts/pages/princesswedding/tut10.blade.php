<h1>Tutorial 9. Upgrading a Factory-Made Petticoat.</h1>
<p><strong>So, the dress is ready for a fitting during which I will make the final decision about its length. </strong>I will show you how to trim the hem of a veiling skirt correctly. Although the bottom edges of the veiling layers must be even, it does not require absolute accuracy.</p>
<p><strong>The skirt looks wonderfully full and the inside looks neat and clean, too. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%209.files/image001.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%209.files/image002.jpg" alt=""></strong></p>
<p><strong>And now I want to talk about the petticoat. </strong></p>
<p><strong>You can see a plain factory-made hoop petticoat in the picture below. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="https://corsetacademy.s3-us-west-2.amazonaws.com/wedding%20skirts/princess%20wedding%20skirt/Tutorial%209.files/image004.jpg" alt=""></strong></p>
<p><strong>It is my duty to show you that you must never use a petticoat like this without upgrading it first! </strong></p>
<p><strong>Firstly, </strong>after I measured the distance from the waist to the top hoop on this petticoat, I got 36.5cm along the back and 41.5cm along the front. The difference is as big as 5cm! This means your fabulous dress will inevitably get distorted!</p>
<p><strong>Secondly,</strong> very poor quality elastic bands commonly used in factory-made petticoats make them keep constantly sliding down. What does this mean? No matter how well your skirt is made, it will adjust to the shape of the petticoat and will, therefore, get distorted.</p>
<p><strong>Thirdly,</strong> many dressmakers (especially beginners) do not consider the petticoat an important component of the wedding dress and do not attend to the issue seriously.</p>
<p><strong>This is a very big mistake! </strong>I customize factory-made petticoats for all of my dresses and I give the petticoat as much attention as the dress itself!</p>
<p><strong>Please remember that it is the inner construction that makes a princess dress look so gorgeous! </strong>And every dress and every skirt type requires an individually made petticoat.</p>
<p><strong>In this tutorial, I will tell you how I customized the factory-made petticoat for this particular elastic waistband skirt. </strong></p>
<p>The first thing I do is trim away the elastic band at the top of the petticoat and trim the top edge so as to get the same distance to the top hoop along the full perimeter.</p>
<p><strong>There are two absolutely opposite ways you can go next depending on the type of your full skirt. </strong>If the fullness on your skirt needs to start right from the waistline, then you should shorten the petticoat at the top. And if you don�t want the skirt to be that full at the top, then you should shorten the petticoat at the bottom after removing one or two bottom hoops.</p>
<p><strong>After all adjustments, the bottom edge of the petticoat is supposed to be positioned 25-30cm above floor level </strong>(with the client standing barefooted).</p>
<p>Only in this case the bride will feel fully comfortable in her dress and the petticoat hoops will never show even if she lifts the fabric of the skirt to walk up a flight of stairs, for example.</p>
<p><strong>In our case, the skirt is supposed to be full from the very top. For this reason, I remove 10cm at the top of the petticoat (the taller the client, the smaller this value should be). </strong></p>
<p><strong>The following criterion can really help you determine the right distance from the top hoop to the waistline: </strong>the top hoop must be positioned just below the woman�s buttocks. Petticoats in full skirts are not supposed to sit too low! If your skirt has a pronounced silhouette (think mermaid skirt, for example), then you should not lift the top hoop of the petticoat.</p>
<p><strong>After shortening the petticoat by the desired length, I put a quality elastic band along its top edge. Its length corresponds with my client�s Waist Circumference. </strong></p>
<p><strong>In addition, I remove the bottom hoop and trim the bottom edge of the petticoat. </strong></p>
<p><strong>In conclusion, </strong>let me emphasize that the final look of the dress does depend on the petticoat!</p>
<p>A good and well-chosen petticoat can turn even a very plain unimpressive dress into a whole different garment!</p>
<p>I have already applied all the above-mentioned adjustments to my petticoat. Now I can put the dress on the customized petticoat, carry out a fitting, and decide on the final length.</p>

