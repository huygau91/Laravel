<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image069.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image071.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image073.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image075.jpg" alt=""></p>
<p><strong>Next, I need to join the layers along the remaining raw edges, sew a row of stay-stitches along them, fold the seam allowance over it onto the face, and sew it down.</strong> After doing this, I can decorate the finished edges.</p>
<p><strong>I pin the layers together aligning their princess seams and side seams and not their raw edges before doing the stay-stitching.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image077.jpg" alt=""></p>
<p><strong>Then I fold the front in half and draw a line along the neckline with a disappearing ink pen. And I sew a row of stay-stitches along this line: first from one side of the neckline and then from the other side. </strong>&nbsp;The seam allowance starts at 1.2cm and gradually narrows down stopping at nearly zero in the V-part of the neckline.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image079.jpg" alt=""></p>
<p><strong>The stay-stitching along the back neckline is made from up to down with a 1.2cm seam allowance. And the stay-stitching along the central back edge is made with a 2cm seam allowance. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image081.jpg" alt=""></strong></p>
<p><strong>I fold the neckline edge of the back to the face over the row of stay-stitches, press it, and sew it down. And the central back edges need to be folded to the inside.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image083.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image085.jpg" alt=""></strong></p>
<p><strong>The other half of the back is finished the same way. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image087.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image089.jpg" alt=""></p>
<p><strong>All these seam allowances will be decorated.&nbsp; </strong></p>
<p><strong>And here comes a very responsible moment: I need to fold the neckline edge of the front to the face, press it, and sew it down. </strong>The stay-stitching really helps you fold the edge neatly. It is almost as if you were using a ruler. I notch the corner of the neckline.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image091.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image093.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image095.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image097.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image099.jpg" alt=""></p>
<p><strong>Now I can join the top with the main bodice!&nbsp; </strong></p>
<p><strong>Of course, you could make an additional row of stitches along either side seam to reinforce it</strong> and prevent the layers from shifting against each other. But I decided not to do it now because I plan to embellish the top rather richly and I don't think there is a need in securing the layers additionally.</p>
