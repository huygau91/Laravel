<p><strong>And I sew the other side seam.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image034.jpg" alt=""></p>
<p><strong>I put the top over the dress seam allowances out. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image036.jpg" alt=""></p>
<p><strong>I align their princess seams and side seams and pin the top to the dress. The princess seams of the top repeat the shape of the bust princess seams, its side seams settle right over the side seams of the dress, and its back princess seams also arrive right at the princess seams at the back of the dress. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image042.jpg" alt=""></p>
<p><strong>The top was cut correctly and the fitting went great. </strong></p>
<p><strong>I assemble the second layer, trim and press the seam allowances, and press the cups on a cup pressing mould.</strong> I recommend trimming such seam allowances on a contrasting surface.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image044.jpg" alt=""></p>
<p><strong>Now I put the two layers face to face.</strong></p>
<p><strong>I will join them along the armscyes. Only the armscyes and not the necklines!</strong> I will simply fold the seam allowances of the back neckline and the central part of the front neckline to the face and sew them down just like that and then overlay them with lace appliqu�.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image048.jpg" alt=""></p>
<p><strong>I align and pin together the armscyes, the princess seams, and the side seams, and sew the layers together along the armscyes with a 1.2cm seam allowance. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image052.jpg" alt=""></p>
<p><strong>Then, I carefully trim the seam allowances close to the stitching and notch them additionally in the curved areas. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image057.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image059.jpg" alt=""></p>
<p><strong>I trim and notch the seam allowance of the other armscye.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image061.jpg" alt=""></p>
<p><strong>Then I turn out the top and carefully press it. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image063.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image065.jpg" alt=""></p>
