<h1>Tutorial 34. Sewing the Mesh Top.</h1>
<p><strong>It is time to sew the mesh top. </strong></p>
<p>I will first assemble the first layer and put it on the dress-form. If everything looks good, I will continue and assemble the top completely.</p>
<p><strong>I sew the bust princess seams. </strong></p>
<p><strong>Then I align the central front piece with the side front piece along the princess seam and notch them from the top down to the under-bust line. This will help me sew the curved area of the princess seam neatly.</strong></p>
<p><strong>I make a very neat bar tack at the bottom of the princess seam and start sewing from down to up. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image008.jpg" alt=""></p>
<p><strong>I sew the other bust princess seam. </strong>This time I start from the top.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image010.jpg" alt=""></p>
