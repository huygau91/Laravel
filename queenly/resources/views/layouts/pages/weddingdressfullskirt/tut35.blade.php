<p><strong>This was the most complicated part of sewing work because these princess seams must be perfectly even. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image016.jpg" alt=""></p>
<p><strong>I assemble the halves of the back. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image018.jpg" alt=""></p>
