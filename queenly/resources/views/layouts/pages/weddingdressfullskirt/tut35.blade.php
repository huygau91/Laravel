<p><strong>Next, I sew the first side seam.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2034_files/image026.jpg" alt=""></p>
