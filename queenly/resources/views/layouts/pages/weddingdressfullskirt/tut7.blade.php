<h2>Tutorial 6. Reinforcing the Construction of the Petticoat.</h2>
<p><strong>I have cut the front and the back of the petticoat from mesh fabric with all lines marked by pencil.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image002.jpg" alt=""></p>
<p><strong>I have sewn the side seams, folded them toward the back, topstitched them, and stitched along the marked lines. </strong>The central back seam remains undone until I sew all ruffles and bones on the petticoat.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image008.jpg" alt=""></p>
<p><strong>It is important to put the halves of the back together and see whether the tunnel lines are aligned properly along the central back seam. </strong>The ends of the tunnels are supposed to meet after you sew the central back seam of the petticoat.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image012.jpg" alt=""></p>
<p><strong>The next task is to finish the hem of the petticoat with soft Rigilene boning. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image014.jpg" alt=""></p>
<p><strong>Just to get all things clear with the sides of the petticoat: </strong>ruffles and bones will be sewn on the face side with the visible seam allowances to keep the inside smooth.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image016.jpg" alt=""></p>
<p><strong>I put soft 5cm Rigilene boning on the face of the petticoat, 1.2cm above the bottom edge, and secure it by stitching along one edge. Then, I fold it to the inside of the petticoat, press it to give it the required rounded shape, and secure it by stitching along the other edge. </strong>Soft Rigilene boning is a very flexible material which easily acquires the desired shape.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image024.jpg" alt=""></p>
<p><strong>The hem is now finished with Rigilene boning. I press it and trim away excess thread and fibers. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image026.jpg" alt=""></strong></p>
<p><strong>It is time to sew hoop tunnels.</strong> As I said before, these tunnels will be made from bias tape. I will start and stop from the middle of the back. I will step 2cm from either central edge of the back to make sure not to stitch down the tunnels by accident when sewing the central seam. There will be a small gap between the ends of the tunnel after I sew the seam.</p>
<p>It is up to you whether you want to have the tunnels on the face or on the inside of the petticoat.</p>
<p><strong>I will make a tunnel for hoop 1 on the face and the rest of the tunnels from both sides. </strong>I will do this just in case because the mesh fabric has not been reinforced and may get torn by the stiff hoops. Basically, I will be able to put the hoops from either side of the petticoat. The hem has already been reinforced by soft Rigilene boning so there is no need in sewing another tunnel from the other side of it.</p>
<p><strong>I sew a tunnel for hoop 1 along the bottom edge of the petticoat.</strong> The end of the bias tape is folded in.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image036.jpg" alt=""></p>
<p><strong>And I make another row of stitches along the top edge of the bias tape. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image044.jpg" alt=""></p>
<p><strong>Here is what the tunnel looks like from the inside. </strong>You could put a strip of lace underneath it for an even more sophisticated look.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image046.jpg" alt=""></p>
<p><strong>The first tunnel is ready and now I need to reinforce the petticoat with Rigilene bones as marked. </strong></p>
<p><strong>The bones are to be sewn on the face of the petticoat.</strong> Whether to use narrow or wide Rigilene bones is up to you.</p>
<p><strong>I will start by putting bones along the perimeter of the reinforcement area. I secure them with a row of stitches made along the outer side.</strong> The vertical bones will be secured with two parallel rows of stitches and their ends will be pushed under the perimeter bones. Then I will secure the perimeter bones with a row of stitches made along the inner side. I will use narrow Rigilene bones as perimeter bones and wide Rigilene bones as vertical bones.</p>
<p><strong>You can heat-seal the ends with a lighter instead of using masking tape. This is only acceptable in this particular case though</strong>. Do not do the same for corsets!</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image048.jpg" alt=""></p>
<p><strong>I step 2cm from the central back edge and put down the first perimeter bone parallel to the tunnel. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image054.jpg" alt=""></p>
<p><strong>I push the end of the vertical perimeter bone underneath the horizontal bone and sew along its inner side. </strong>The bone reaches down to the top border of the tunnel.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%206_files/image060.jpg" alt=""></p>
<p><strong>I put a bone above the tunnel. I gather it in advance to give it a rounded shape.</strong></p>
