<h1>Tutorial 27. Joining the Face with the Lining by Machine.</h1>
<p><strong>I need to finish the zipper with the lining fabric before joining the lining with the face.</strong></p>
<p><strong>I put together the seam allowances of the zipper and the central back seam, remove several outermost pins along the neckline, and turn the dress inside out through the bottom.&nbsp; </strong></p>
<p><strong>Please keep it in mind </strong>that the central back seam is still undone on the lining. I sewed the central back seam on the face of the dress because I had to install a zipper in it.</p>
<p><strong>I turn the dress out and align the seam allowances of the zipper and the central back seam putting them face to face. </strong>In other words, I align the face with the lining in the zipper area. I align the edges of the seam allowances and pin them together aligning the waist notches, as well as the sewing start points and the sewing end points of the zipper. The last pin is placed at the end of the zipper.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image004.jpg" alt=""></p>
<p><strong>I join the pinned together seam allowances by stitching from the top down to the very last pin at a presser foot width from the zipper teeth. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image008.jpg" alt=""></p>
<p><strong>I remove the pins, turn the dress right side out, and put back the pins I had previously removed from the top edge. The lining is joined with the face along one side of the zipper.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image010.jpg" alt=""></p>
<p><strong>Next, I join the lining with the face along the other side of the zipper. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image012.jpg" alt=""></p>
<p><strong>This time I sew over the face side of the dress along the pre-stitching that holds the zipper in place.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image016.jpg" alt=""></p>
<p><strong>I turn the dress inside out again, align the necklines, and put the pins back in place. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image018.jpg" alt=""></p>
<p><strong>Everything is ready for joining the face with the lining along the neckline for good. </strong></p>
<p><strong>I sew a row of strengthening stitches along the back neckline and the armscye at a presser foot width from the stay-stitching.</strong> When I reach the top perimeter bone, I move along its edge as if it were a guideline.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image030.jpg" alt=""></p>
<p><strong>I remove the pins, trim away thread tails, and move to the front part of the neckline. </strong>This is very responsible work. The whole look of the garment depends on how neatly I will sew this row of strengthening stitches now.</p>
<p><strong>First, I will sew a row of strengthening stitches along the stay-stitching in the V-part of the neckline. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image042.jpg" alt=""></p>
<p><strong>I remove the pins I no longer need.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image044.jpg" alt=""></p>
<p><strong>And I sew another row of strengthening stitches at a presser-foot width from the stay-stitches.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image056.jpg" alt=""></p>
<p><strong>I trim the seam allowance down to 6-7mm from the row of stitches made a presser foot width above the stay-stitching or the top perimeter bone. </strong>It will be easy to finish the seam allowance with a lace ribbon or a bias tape from the inside.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image060.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image064.jpg" alt=""></p>
<p><strong>It is best to notch out some lining fabric at the top of the princess seams to make the neckline thinner before folding it. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2027_files/image066.jpg" alt=""></p>
<p><strong>I have chosen rather dense cotton lace for finishing the neckline. I cut it in half lengthwise and serge the raw edge. </strong></p>
