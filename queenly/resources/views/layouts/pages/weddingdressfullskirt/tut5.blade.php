<h1>Tutorial 4. Constructing the Petticoat.</h1>
<p><strong>I have drawn the side view of our future petticoat on the board. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%204_files/image002.jpg" alt=""></p>
<p><strong>Now I will calculate everything and write down the values.</strong> Please keep in mind that these values don't include any seam allowances or turn ups.</p>
<p><strong>The flare of the skirt will start 15cm below the waistline.</strong> This needs to be taken into account.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%204_files/image004.jpg" alt=""></p>
<p><strong>There will be a tunnel for a 3cm-wide elastic band at the top of the petticoat.</strong> I will make the tunnel 3.5cm just to be on the safe side.</p>
<p><strong>The adjoining area is 11.5cm of light knit fabric that clings to the body. </strong></p>
<p><strong>15cm - 3.5cm = 11.5cm</strong></p>
<p><strong>The mesh petticoat itself starts just below that area. </strong>I will first sew hoop tunnels on it and then reinforce it with Rigilene bones and add ruffles.</p>
<p><strong>Further calculations are done from down to up. </strong>The dashed line marks the floor level.</p>
<p><strong>The bottom hoop (1) is sewn on along the hem of the petticoat. It is raised 20cm above floor level at the front. </strong>Later, the bottom hoop will be overlaid by a ruffle and the front of the petticoat will reach all the way down to the floor. <strong>The bottom hoop will gradually descend as it approaches the side seam and settle upon the floor at about 10cm past it. </strong>In other words, it will be positioned at an angle at the front and then gradually go horizontal toward the back. It is flexible enough for being bent that way.</p>
<p><strong>All other hoops will be sewn on horizontally. </strong>The second hoop (2) may also rise above floor level at the front and then gradually return to the horizontal position at the back. But this may only be done if you need to use many hoops, i.e. when you are sewing a very heavy skirt which needs to be spread out. In our case, four hoops are enough because the top skirt will be made of lightweight tulle and there will be no additional ruffles or other heavy elements on it.</p>
<p><strong>The hoops will be positioned the following way. </strong></p>
<p><strong>The first hoop (1) and the second hoop (2) will be positioned with a 5cm interval at the front. </strong></p>
<p><strong>In this case, the interval between the same hoops at the back should be 25cm for the second hoop (2) to settle horizontally: </strong></p>
<p><strong>20cm + 5cm = 25cm. </strong></p>
<p>It is too early to estimate the interval between these two hoops at the side seam level. I will figure it out later, with the lay before my eyes, and carefully mark a tunnel for the second hoop (2).</p>
<p><strong>The next hoop (3) will be placed 20cm above the second hoop (2). </strong></p>
<p><strong>And the last hoop (4) will be placed 20cm above the third one (3).&nbsp;&nbsp; </strong></p>
<p><strong>Let us calculate the distance from the last hoop (4) to the floor:&nbsp; </strong></p>
<p><strong>25cm + 20cm + 20cm = 65cm</strong></p>
<p><strong>This means that the top hoop of the petticoat (4) is positioned at the mid-thigh level. </strong>You can place hoops higher when you need to give the skirt a round shape. We don't need this effect. It is rather hard to sit down in a skirt with a hoop close to the waist, i.e. right at the buttocks. Never position a hoop there unless there is really no way to do without it.</p>
<p><strong>Now let us analyze the construction of the bottom of our petticoat. </strong>The bottom hoop, which is positioned at an angle, serves as an expander and, simultaneously, a kind of a support for the back of the petticoat.</p>
<p><strong>In other words, it is as if the petticoat were put on a mount. </strong>It is, however, quite possible that the bottom hoop will not be enough in the end: the fabric between hoops 1 and 2 may compress, the back of the hoop will then starting dragging along the floor, and the whole petticoat will shift forward distorting the shape of the skirt.</p>
<p><strong>The only solution is to spread out this part of the back by adding vertical supports.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%204_files/image006.jpg" alt=""></strong></p>
<p><strong>I mark vertical lines for putting Rigilene bones between the area where the skirt starts touching the floor and the middle of the back. </strong>I will decide on the intervals and the number of the bones when I have the lay before my eyes.</p>
<p><strong>How far up can those vertical supports go? It is a very good question. </strong>I believe they should not go above the knee level. Because if the bride wants to sit down or pick something up from the floor, long vertical supports will not let the fabric fold naturally: the bride will sit down but the skirt will stay fixed in the original position.</p>
<p><strong>For that reason, the supports will run from the top edge of the first tunnel (hoop 1) to the top edge of the third tunnel (hoop 3). </strong></p>
<p><strong>Each support will be about 45cm long and stop just below the knee level. </strong>The supports will be placed along the perimeter of the skirt at 10cm from the side seams. The ends of vertical Rigilene bones will be overlaid by hoop tunnels.</p>
<p><strong>This trick </strong>will help us reinforce the back of the skirt, ensure proper support for the train, protect the petticoat from deformation, preserve the proportions of the skirt, and allow the bride to move freely in the garment.</p>
<p><strong>Next, I mark all key lengths in correspondence with my client's measurements to determine fabric consumption. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%204_files/image008.jpg" alt=""></strong></p>
<p><strong>Let me calculate the length of the petticoat along the front, the back, and the side. </strong></p>
<p><strong>The full length of the petticoat including the flare and the yoke is 115cm. </strong></p>
<p><strong>Actual length along the front: </strong></p>
<p><strong>115cm - 15cm - 20cm = 80cm. </strong></p>
<p><strong>Actual length along the back: </strong></p>
<p><strong>115cm - 15cm = 100cm.</strong></p>
<p><strong>Actual length along the side (with the bottom hoop 5cm above floor level):</strong></p>
<p><strong>115cm - 15cm - 5cm = 95cm.</strong></p>
<p><strong>Now that I know the lengths, let us see how much fabric falls upon the back and upon the front of the petticoat at the top and down the bottom. </strong></p>
<p><strong>My client's stomach circumference is 88cm (measured 15cm below the waist), </strong>which means a half circumference of 44cm. Twenty-two centimeters falls upon the back and upon the front of the skirt.</p>
<p><strong>The bottom hoop has a circumference of 250cm,</strong> and a half of it makes 125cm. In this case, 65-67cm falls upon the back and upon the front of the skirt.</p>
<p><strong>I have all values for drawing a foundation for the petticoat (seam allowances are not included yet). </strong>&nbsp;I also know the intervals between all hoops and can now mark all tunnels for Rigilene bones.</p>
<p><strong>And I still need to mark the position of the train. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%204_files/image010.jpg" alt=""></p>
<p><strong>I do it by the eye. </strong>I put the ready-made petticoat on the dress-form and visually mark the distance between the supposed princess seams at the back of the dress. For my client�s dress size, this distance is 20-22cm or 10cm in half the size. I mark it on the drawing.</p>
<p><strong>And then, starting from the marked top point, I extend the measuring tape and roughly mark the desired position of the train. </strong>The train is not supposed to be too wide: you should not be able to see it when looking at the bride front on.</p>
<p><strong>After careful thought, I have decided to create an 80cm flare down the bottom of the train, i.e. about 40cm from either side of the back.</strong> I mark the train area on the drawing.</p>
<p>While preparing to the cutting, <strong>I will stitch out the marked tunnel lines and the train area </strong>to make further work easier.</p>
<p><strong>The tunnels will be made from bias tape.</strong> It is best to use plain cotton and not synthetic bias tape for this purpose.</p>
<p><strong>I will draft the pattern right on fabric adding all seam allowances.&nbsp; </strong></p>

