<h1>Tutorial 17. Calculating Fabric Consumption for the Base of the Dress.</h1>
<p><strong>Before we continue, let me determine how much face and lining fabric I need for my lay.&nbsp; </strong></p>
<p><strong>I will show you how I do this. </strong></p>
<p><strong>But first I would like to say a couple of words about my cutting table. </strong>As you know, cutting requires a rather large working surface. But you cannot keep a large fixed cutting table in a small atelier. What I do is use two plastic folding picnic tables, each of which is 75x180cm when unfolded. Putting them together gives me a cutting table with a total surface of 150x180cm, which is enough for cutting a skirt or a dress. What makes it even better, these picnic tables fit in very compact flat cases when folded.</p>
<p>I have to use two tables only when cutting large garments. In most cases, one table is enough (or, sometimes, even just an ironing board).</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image002.jpg" alt=""></p>
<p><strong>Other things I need to calculate fabric consumption are</strong> the adjusted patterns (paper or batiste), a ruler, some measuring tapes, and my calculations.</p>
<p>Luckily, the width of my fabric (150cm) coincides with the width of the cutting table allowing it to fully imitate the lay.</p>
<p><strong>I put the central front piece to the supposed fabric fold line. </strong></p>
<p>I want to check whether the skirt will fit in the width of the fabric if I cut it on the cross.</p>
<p><strong>The front length of the skirt measured from the waistline is 119cm without seam allowances. </strong>Another 2cm needs to be added for turning the hem.</p>
<p><strong>Therefore, there has to be at least 121cm between the waistline of the central front piece and the side selvage of the fabric. </strong>Now I can see that this width is enough.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image006.jpg" alt=""></p>
<p>The width of the central front piece across the stomach is 9cm including seam allowances, and the width of the flare along the front princess seam is 28cm. <strong>9cm + 28cm = 37cm.</strong></p>
<p><strong>I measure 37cm from the fold line, mark a point with a disappearing ink pen right on the table, and draw a line perpendicular to the fold line at 119cm from the waistline. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image008.jpg" alt=""></p>
<p>The length of the skirt from the waistline along the front princess seam is 120cm. <strong>I mark 120cm from the waistline to the intersection with the point marked 37cm from the fold line.</strong> I leave the measuring tape on the table.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image010.jpg" alt=""></p>
<p><strong>I take the side front piece and position it at the other end of the cutting table keeping its waistline perpendicular to the fold line of the fabric. </strong>The lines marked on the piece help me position it correctly.</p>
<p><strong>I mark equal sections from the central vertical line of the piece to the fold line of the fabric. There is 46cm between the two.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image014.jpg" alt=""></strong></p>
<p><strong>I put down another measuring tape. </strong>It passes through the intersection of the stomach line and the front princess seam line of the side front piece and runs strictly parallel to the fold line, i.e. 46cm from it along the full length.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image018.jpg" alt=""></p>
<p><strong>I mark 28cm from the front princess seam line at 120cm from the waistline. It is the flare. </strong>The gore is not supposed to overlap with the central front piece.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image020.jpg" alt=""></p>
<p><strong>I position the second measuring tape along the flare line and put down yet another one parallel to the fold line from the opposite side of the side front piece. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image024.jpg" alt=""></p>
<p><strong>I mark a flare of 29cm from the third measuring tape and put the second measuring tape along the supposed flare line. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image028.jpg" alt=""></p>
<p><strong>Then I put the side back piece right next to the selvage </strong>to keep enough length for the train. In this dress, the longest possible train length is determined by the width of the fabric. The main fabric will form a train with a length of just some 5-10cm. The rest of the train dragging along the floor will be mesh fabric sewn to it.</p>
<p><strong>I carefully position the side back piece keeping its waistline strictly perpendicular to the fold line. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image030.jpg" alt=""></p>
<p><strong>I mark the flare line the same way I did for the side front piece. </strong>The amount of flare is 29cm along the side seam line and 34cm along the back princess seam line.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image038.jpg" alt=""></p>
<p><strong>Next, I do the same with the last piece - the central back piece.</strong> I position it with the waistline perpendicular to the fold line. Only the princess seam line is flared and the width of the flare is 34cm. There is no flare along the central back seam.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image044.jpg" alt=""></p>
<p><strong>Now I need to measure the distance from the central vertical seam of the back to the edge of the table. This distance will define fabric consumption. It is 150cm. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image046.jpg" alt=""></p>
<p><strong>As you can see, it was rather easy and took me little time to find that I need 300cm face fabric and as much lining fabric for my dress. Both the face and the lining will be made of stretch satin. I will use the matte side as the face.&nbsp; </strong></p>
<p><strong>I add a little extra space just in case and cut two 310cm long pieces of fabric, then fold each piece in half, and prepare the lay. </strong></p>
<p><strong>WARNING: </strong>do not press the fold line of your fabric. It will be very hard to make it flat again!</p>
<p><strong>I repeat on fabric the same process I did on the table to determine fabric consumption.</strong> I arrange the pieces in the same order positioning them strictly parallel to the fold line, secure them with pins, and add the flare. I mark the length of the skirt down along the flare lines and draw the bottom line.</p>
<p><strong>The full process is illustrated in detail. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2017_files/image048.jpg" alt=""></strong></p>
<p><strong><br>
