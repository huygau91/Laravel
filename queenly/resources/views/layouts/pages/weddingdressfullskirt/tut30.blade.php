<h1>Tutorial 29. Steaming and Pressing the Dress on the Dress-Form.</h1>
<p><strong>I have put the dress on the dress-form to press and steam it.</strong></p>
<p><strong>Steaming is often used to give proper shape to garments.</strong> You can steam your dress right on the dress-form at any work stage or before the fitting if you have a good steam generator iron. It is fast and easy.</p>
<p>Professionals often steam an already finished garment and leave it for a few hours or overnight to dry and let the shape set properly.</p>
<p><strong>Of course, minor tucks and creases were formed on the dress when I was joining the face with the lining, but I can easily remove them now by steaming the entire dress from top to bottom right on the dress-form.&nbsp; </strong></p>
<p><strong>The effect is particularly noticeable on the bodice.</strong> All wrinkles, creases, and pin marks simply disappear.</p>
<p><strong>By the way, a pressing cushion is a very handy tool for removing minor folds and wrinkles.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image006.jpg" alt=""></strong></p>
<p><strong>There is a small air bubble in the middle of the under-bust line where I made a dart on the lining.</strong> If you don't plan to decorate this area and want to ensure good fitting, I recommend you should place a piece of fusible webbing between the face and the lining and fuse them together. As for me, I have nothing to worry about because I will certainly decorate this part of the dress.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image008.jpg" alt=""></p>
<p><strong>You can clearly see the result if you examine the bodice in the pictures below. </strong></p>
<p><strong>Before steaming...</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image010.jpg" alt=""></p>
<p><strong>After steaming...</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image014.jpg" alt=""></strong></p>
<p><strong>The preliminary pressing is over. </strong>It is best not to touch the dress for a couple of hours. Let it dry properly.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image016.jpg" alt=""></p>
<p><strong>And now it is time to work on the mesh top (overlay for the bodice). </strong></p>
<p><strong>There is a very important thing you must take into account before we start. </strong>The mesh top is supposed to be put over the dress. And this means I need to cut it based on the measurements of the sewn dress and not my client's measurements. Dome-shaped cups, bones, eased-in areas - all this has affected the initial measurements taken off my client and made the dimensions of the dress a little different.</p>
<p><strong>This is why I need to take certain measurements off the garment before cutting the mesh top.&nbsp; </strong></p>
<p><strong>I will take half measurements to make it easier. </strong></p>
<p><strong>Here is what I need to know: </strong></p>
<p>- Bust Width Front,</p>
<p>- Under-Bust Width Front,</p>
<p>- Waist Width Front,</p>
<p>- Waist Width Back,</p>
<p>- Stomach Width Front,</p>
<p>- Stomach Width Back.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2029_files/image018.jpg" alt=""></p>
<p><strong>Let us focus on the stomach width measurements for a moment. </strong></p>
<p><strong>They will be taken 7cm below the waistline. </strong>&nbsp;At first, I thought it would be beautiful if the tulle skirt started 10-12cm below the waistline. But now, after examining the finished base of the dress, I believe it will look a lot better <strong>with the tulle skirt positioned higher (6cm below the waistline) and the mesh top reaching 7cm below the waistline.</strong></p>
<p><strong>This explains why cutting the mesh top involves measuring Stomach Width Front and Stomach Width Back 7cm below the waistline. </strong>I tie a cord around the dress 7cm below the waistline.</p>
<p>What concerns the back half of the dress, <strong>I will only measure Waist Width Back and Stomach Width Back 7cm below the waistline</strong> because the low back neckline makes Bust Circumference and Under-Bust Circumference irrelevant.</p>
<p><strong>In addition, I need to account for the fact that the under-bust line runs 10cm above the waistline on the dress when I start adjusting the initial pattern of the mesh top. &nbsp;&nbsp;</strong></p>
<p><strong>I will also need to account for the adjusted length of the tulle skirt as compared to the main skirt </strong>because the tulle skirt will start 6cm below the waistline instead and not 10-12cm as was planned before.</p>
<p><strong>All measurements taken off the dress while on the dress-form will be put in the table. </strong></p>
<p>I will also print out the original pattern, measure it, and record all values.</p>
<p><strong>If you find it hard to obtain proper measurements of your printed out initial pattern,</strong> then I strongly recommend you sew a mock-up and measure it afterwards the same way you did with the initial pattern of the main dress.</p>
