<h1>Tutorial 8. Positioning Bottom Ruffles on the Petticoat.</h1>
<p>The gathered ruffle is 380cm long - just what I needed.</p>
<p><strong>First I will put that additional ruffle along the bottom edge of the petticoat </strong>and then I will put the main ruffle along the sewing line marked for hoop 1.</p>
<p><strong>I mark a line for the additional ruffle. </strong>I step 3cm down the side seam from the second hoop. And I step 7cm down (and not 5cm as was planned initially) from the second hoop in the area between the train line and the central back seam.</p>
<p><strong>As the result, part of the ruffle runs parallel to the hoop and then it gradually goes up. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%208_files/image002.jpg" alt=""></p>
<p>The additional bottom ruffle will thus basically lie on the floor at the side seam. It will drag along the floor at the back and go just beyond the train preventing the lining from getting caught on the surface of the floor.</p>
<p><strong>Firstly, </strong>when positioned like this, the additional ruffle will protect the lining of the train from getting dirty.</p>
<p><strong>Secondly,</strong> it will help you avoid that nasty effect when the bride turns in her dress and the train stays "fixed" where it was.</p>
<p><strong>We want the whole dress, including the train, to move in sync with the bride when she is dancing. </strong></p>
<p><strong>I put the additional ruffle along the marked line and sew it on starting from the central edge at the back of the skirt. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%208_files/image004.jpg" alt=""></p>
<p><strong>I leave about 5cm detached so I can join the ends of the ruffles neatly when sewing the central seam.&nbsp; </strong></p>
