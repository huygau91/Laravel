<p><strong>I cut out the final trued version of the adjusted top parts of the dress. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image044.jpg" alt=""></p>
<p><strong>I will first cut these patterns from iron-on batiste. And I will use the same paper patterns to cut the same pieces from the main fabric together with the skirt. </strong></p>
<p><strong>There is no point in duplicating the skirt with iron-on batiste below the beginning of the flare.</strong> There will be a mesh fabric overlay with appliqu� and rhinestones and I am sure it will disguise the transition from the iron-on batiste to the non-duplicated fabric.</p>
<p>If you want to keep your dress smooth, without any additional decor in the area of transition from duplicated to non-duplicated fabric, then you should conduct a small test. Fuse a small piece of batiste on your lining fabric, cover it with the face fabric, and check whether you can see the border between the duplicated and the non-duplicated part. The result depends on how thin and see-through the main white fabric is.</p>
<p><strong>I have finished cutting the iron-on batiste.</strong> I have simultaneously cut out all iron-on pieces for the lining and for the face of the dress, i.e. a total of four pairs of pieces.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image046.jpg" alt=""></p>
<p><strong>There are certain tricks you need to know before the cutting. </strong></p>
<p><strong>Since these iron-on batiste pieces will serve as patterns for cutting the main fabric,</strong> remember that you will need to arrange them in a head-to-toe manner to ensure a more efficient use of fabric considering the flare of the skirt. I arranged the paper patterns in a head-to-toe manner on the batiste as well.</p>
<p><strong>I recommend you take a disappearing ink pen, draw the waistline on the side front pieces and the side back pieces, and then draw a perpendicular line in the middle of each piece and mark notches. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image048.jpg" alt=""></p>
<p><strong>These lines will help you position the patterns correctly against the lengthwise grain of your fabric.</strong> When cutting very flared dresses, you are often tempted to put some or other piece diagonally or turn it by just a couple of centimeters when its flared part does not fit in the lay. Never do this! All these things will inevitably affect the finished garment.</p>
<p>The waistline and the perpendicular line must be positioned correctly against the lengthwise and the crosswise grain of your lay!</p>
<p><strong>I will cut my dress on the cross (as always) so the waistline will be parallel to the lengthwise grain of the main fabric and of the lining while the perpendicular line will be parallel to the crosswise grain. </strong>&nbsp;Marking lines on the batiste pieces really helps you position them correctly on the main fabric.</p>
<p><strong>I have also prepared hard iron-on fabric pieces for duplicating the cups.&nbsp;&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image050.jpg" alt=""></p>
<p><strong>I had marked the cup on the side front piece in advance and then cut the iron-on pieces as marked. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image052.jpg" alt=""></p>
<p><strong>When cutting an iron-on piece for the central part of the cup, remember to make its bottom edge ascend toward the centre. </strong>The larger the cup size, the smaller this ascension should be, and vice versa.&nbsp; The hard iron-on fabric will support the bust and push it up, and smaller breasts requirer a greater push-up effect.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image054.jpg" alt=""></p>
<p><strong>In conclusion, I would like to share some tips on cutting hard iron-on fabric. </strong></p>
<p><strong>Tip 1. </strong>The simplest way to cut the side cup piece without cutting the paper pattern is to put the hard iron-on fabric over the pattern and transfer the contour showing through it. Then you just fold the iron-on fabric in half and cut it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image056.jpg" alt=""></p>
<p><strong>Tip 2.</strong> If your iron-on fabric is made with hard weft yarn, make sure to position the central edge of the central front piece parallel to the crosswise grain. This will make it much easier for you to ease in the cups and make them round.</p>
