<h1>Tutorial 16. Sewing a Mock-Up Bodice of the Dress.</h1>
<p><strong>Now I need to sew a mock-up based on the adjusted pattern. </strong></p>
<p><strong>I arrange the adjusted top pieces of the dress on carefully pressed cotton mock-up fabric folded in half, transfer their contours and all notches, and cut them out.</strong> You can pin the layers together if you want.</p>
<p>I am working with the already adjusted pattern but I highly recommend you sew a mock-up based on the original pattern first. It will help you obtain correct measurements of the standard pattern.</p>
<p><strong>Why do you need to sew a mock-up based on the adjusted pattern? </strong></p>
<p><strong>First of all, it is a way to check whether you have done everything correctly.</strong> This is particularly important when you are working on a long distance order because there is no possibility of doing intermediate fittings. After sewing a mock-up, you can check whether all notches, edges, and princess seams are aligned properly and whether the side seams are positioned correctly.</p>
<p><strong>In addition, the mock-up method allows you to decide on the shape of the back neckline by drawing it the way you want right on the mock-up imitating your client�s body on the dress-form.</strong> It is basically the same as if you were looking at the client in the dress. You can see whether you need to shorten the side seam to achieve a nice smooth transition to the low back or apply any other adjustments.</p>
<p><strong>Trust me sewing a mock-up does not require that much effort whereas the benefits are simply invaluable! </strong></p>
<p>It takes little fabric to sew a mock-up. I believe every seamstress will have a suitable piece in stock.</p>
