<p><strong>My dress-form is a little wider than needed in the under-bust area.</strong> As the result, the seam allowance at the back is smaller than 2cm.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image018.jpg" alt=""></p>
<p><strong>Try your best to position the mock-up on the dress-form as symmetrically as possible</strong>: the side seams should be vertical and all princess seams symmetric.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image022.jpg" alt=""></p>
<p><strong>After double-checking all circumferences, I need to draw the desired shape of the back neckline. </strong></p>
<p><strong>Here is how I do it:</strong> I pin an elastic band or a cord at the top of the side seam and arrange it on the back of the mock-up to imitate the shape of a neckline. I try out several different ways and carefully examine the results from a distance.</p>
<p><strong>I choose the best configuration of the back neckline and draw a line following the elastic band to the centre of the back. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image026.jpg" alt=""></strong></p>
<p><strong>Please keep it in mind </strong>that the newly marked position of the back neckline includes a seam allowance.&nbsp; The actual back neckline will be 2cm lower on the finished garment.</p>
<p><strong>I take the mock-up off the dress-form and cut along the new line. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image028.jpg" alt=""></strong></p>
<p><strong>The back piece and the side front piece need to be adjusted accordingly. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image030.jpg" alt=""></p>
