<p><strong>All patterns are now ready. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image010.jpg" alt=""></p>
<p><strong>I sew them together along the vertical seams and put the mock-up on the dress-form.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2016_files/image012.jpg" alt=""></p>
