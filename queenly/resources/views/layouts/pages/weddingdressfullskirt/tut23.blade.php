<h1>Tutorial 22. Fitting the Lining of the Dress.</h1>
<p><strong>I have put the lining on the dress-form with the petticoat and I want you to take a look at it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image002.jpg" alt=""></strong></p>
<p><strong>I hope that this picture gives enough proof that all my plans turned out well! </strong></p>
<p><strong>The skirt required the most attention, of course.</strong> I did not worry about the bodice as much because I had already tested it with the mock-up.</p>
<p><strong>The length and the overall look of the skirt are more important.&nbsp; </strong></p>
<p><strong>What I wanted was to achieve the desired shape of the skirt without pulling it tight on the petticoat </strong>to avoid the impression that it was stuffed with blankets and pillows. That was not something I wanted!&nbsp; And I have managed to avoid this effect! The skirt lies freely upon the petticoat and forms lovely folds when I move it around.</p>
<p><strong>At the same time, I did not want any slack areas on the skirt!</strong> I was worried about the transition to the train at the back (the area with the top ruffles made of hard mesh). I needed a smooth transition, and it is exactly what I got!</p>
<p><strong>There is a little caving in in the stomach area at the front of the skirt. </strong>But it is expectable because the fabric between the stomach line and the beginning of the top ruffle was not reinforced in any way.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image010.jpg" alt=""></p>
<p><strong>Why am I not concerned by this caving in at the front of the skirt? </strong></p>
<p>It is right there that the tulle skirt will be sewn to the main dress (8-10cm from the waistline). Actually, it will be a full-sweep gathered four-layer skirt. The problem area on the main skirt will thus be hidden underneath thick tulle gathers.</p>
<p><strong>Let us talk about the length of the skirt now. </strong></p>
<p>I have already adjusted the height of the dress-form to my client's height. Her height measurement, however, does not include the shoes she will be wearing for her special day. I believe it is too to adjust the length of the skirt risky in this case. What if I make it too short? And I don't want to have to do the same work twice if it happens to be too long either. Instead, I will simply leave the hem as is for now and finish it when my client arrives for the final fitting.</p>
<p><strong>I am fully satisfied with the way the train lies on the floor by the way.</strong> It will be prolonged by a mesh train and a long veil.</p>
<p><strong>There is even more good news!</strong> The skirt does not get deformed when I turn the dress-form around. It moves along without getting distorted or creased. This makes me really happy! It means the petticoat has the right shape and construction! I have achieved the desired effect!</p>
<p><strong>I am done with the skirt and can now focus on the bodice.</strong></p>
<p><strong>The main flaw I can see concerns the side seams. They are slightly shifted toward the front of the dress at the armscyes. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image014.jpg" alt=""></strong></p>
<p><strong>This probably happened because the bias edges of the back got stretched out.</strong> If you want to fix this and adjust the position of the side seams, then you can play around with their seam allowances, i.e. make them smaller or bigger. This will, of course, disturb the smooth transition to the back a little bit. You will have to adjust it as well.</p>
<p><strong>I have decided to keep the position of the side seam unchanged.</strong> Only the part of the side seam between the armscye and the under-bust line has been shifted, and this won't be very noticeable. And besides, the dress will be decorated with rather closely spaced lace appliqu� motifs. Moreover, the side seam might have been shifted just because the under-bust area of my dress-form is a little wider than on my client's body. The side seam may click into place when she puts the dress on.</p>
<p><strong>The balance of the garment is more important right now. </strong>How well the dress fits in the chest and at the back is the key thing.</p>
<p><strong>Although I am happy with the shape of the back, the bias neckline edges have stretched out a little despite the stay-stitching. </strong>There are several ways you can fix this. The easiest one is to pull up the neckline edges on the stay-stitch thread, press them, and do the same with the face layer. If the face still happens to be too large, I will experiment with the side seam: slightly increase the seam allowance of the front piece and take the excess millimeters of fabric in the princess seam of the back piece.</p>
<p><strong>And here is what I will do to prevent these bias edges from stretching out: </strong>before taking the dress off the dress-form, I will measure their actual length along the row of stay-stitches (seam allowances excluded). Then I will take a piece of Rigilene boning with the same length and sew it on along the edge eased in on an organza ribbon. The bias edges will be thereby secured at the required length.</p>
<p><strong>There is something else I want to draw your attention to: </strong>when I made a dart of about 1cm in the middle of the under-bust line, the waistline of the dress naturally "jumped" 1cm upwards at the same level. For this reason, I tie an elastic band around my client's supposed waist and highlight the actual waistline of the dress with a marker.</p>
<p><strong>It is very important!</strong> When working on the petticoat, I referred to the waistline of the dress-form and did not pay attention to the distance from the under-bust line to this band tied around my client's waist. I was only interested in the part below the waistline. Of course, I mark the height and other measurements with regard to the actual position of my client's waist when I start working on a dress. As I said, I have tied an elastic band and highlighted the new waistline just in case so I can refer to it in further work if I need to make some changes.</p>
<p><strong>Next I want to say a couple of words about grosgrain ribbon or ceinture. </strong>I get quite many E-mails from professionals who are fond of classic body control corsets. They use grosgrain in all their corsets and they believe it is the only way to ensure proper waist restriction.</p>
<p><strong>Grosgrain ribbon </strong>is used to create additional support in the waist and prevent it from stretching out.</p>
<p><strong>It is a very tricky question indeed. Personally, I believe </strong>it is enough to simply narrow the natural waist circumference by 1cm to prevent the dress from slipping down! Even with a full and heavy skirt, the dress will fit tight around the waist and hold in place securely.</p>
<p><strong>But I will still show you how to add grosgrain ribbon to a dress for some diversity. </strong></p>
<p><strong>What is grosgrain ribbon anyway?</strong> Usually, it is a non-stretch corded ribbon with a width of 2-2.5cm. This ribbon is supposed to be put right along the waistline of the lining to completely prevent it from stretching out.</p>
<p>My client is a slender well-built woman and, of course, there is no need in adding a grosgrain ribbon to her dress. The natural body control effect is enough. I am showing how to add a grosgrain ribbon to a dress solely to teach you how to do this if the need comes.</p>
<p><strong>Let me explain you how to work with grosgrain ribbon. </strong></p>
<p>Start by measuring a length of grosgrain ribbon equal to your client's waist circumference.</p>
<p><strong>Please note that you are only supposed to cut a piece of required length and not sew it on! </strong></p>
<p>And remember to include seam allowances for folding the ends of the ribbon.</p>
<p><strong>The length of your ribbon with folded ends must be strictly the same as your client's waist circumference.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2022_files/image016.jpg" alt=""></strong></p>
<p><strong>Next, you need to carefully distribute the ribbon along the waistline of the dress.</strong> Simply fold it in half, mark the middle, and put it right to the middle of the waistline. Then, carefully distribute the ribbon between the princess seams and the bones and sew it on the lining with two parallel rows of stitches. <strong>Grosgrain ribbon is usually sewn on just above the waistline</strong> because you don�t want a slight depression formed right at the waistline.</p>
<p>There is actually nothing difficult in sewing a dress with grosgrain ribbon in the waist.</p>
<p><strong>Just remember that the resulting effect is rather questionable and may cause considerable challenges in further work. </strong></p>
<p><strong>For example,</strong> you will have to rip the ribbon off to adjust the size of the finished dress if the bride loses or puts on some weight before the wedding.</p>
<p><strong>To stay on the safe side, I will leave my ribbon detached at the side seams</strong>. This will not affect its function but I will have an opportunity of adjusting the side seams at the waist level if I need to.</p>

