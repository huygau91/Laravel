<h1>Tutorial 3. Materials Required for Making a Petticoat.</h1>
<p><strong>Let us look at the fabrics I will sew with. </strong></p>
<p><strong>The main petticoat will be made of hard mesh fabric</strong> and I will sew tunnels for steel hoops and ruffles on it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image002.jpg" alt=""></p>
<p><strong>The ruffles will be made of plain tulle. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image004.jpg" alt=""></p>
<p><strong>And the hem of the petticoat will be finished with soft Rigilene boning.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image006.jpg" alt=""></p>
<p><strong>The bottom hoop has a circumference of 300cm.</strong> I will use 250cm in the calculations but the actual circumference has to be bigger because the hoop will be positioned at an angle. The overlapped ends will eliminate the excess.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image008.jpg" alt=""></p>
<p><strong>Good quality hoops are coated with white enamel or rubber and their ends can be locked together with special tips.</strong> Don�t worry if your hoops don't have those special tips. You can always seal the ends with sticky tape or masking tape and use a plastic tube as a lock mechanism.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image010.jpg" alt=""></p>
<p><strong>I will choose the most suitable hoops from the wide range I have in stock (and so should you). </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%203_files/image012.jpg" alt=""></p>
<p><strong>The yoke will be made of either knit fabric or stretch satin or a wide elastic band.&nbsp; </strong></p>
