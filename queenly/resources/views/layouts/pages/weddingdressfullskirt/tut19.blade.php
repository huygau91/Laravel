<h2>Tutorial 18. Duplicating the Pieces. Marking Bone Positions. </h2>
<p></p>
<p><strong>I start duplicating the pieces.</strong> </p>
<p>I secured them all with pins after cutting them out.&nbsp; </p>
<p><strong>The side front pieces:</strong></p>
<p><strong>I only remove the pins from the iron-on fabric leaving the ones at the bottom to prevent the layers from shifting against each other. Then I separate the lay into main pieces and batiste pieces. </strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image002.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I make a �sandwich": </strong>first I put a batiste piece fusible side up, then I cover it with a pair of main fabric pieces put together face to face, then I put down another batiste piece fusible side down, cover it with the remaining pair of main fabric pieces, and put down the last batiste piece fusible side down. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image010.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image012.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image014.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image016.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image018.jpg" class="img-responsive center-block"></p>
<p><strong>The sandwich lay allows me to duplicate all pieces in one go.</strong> And they will be as close as possible to one another in this case. If you unpin the pieces and duplicate each one separately, then they will all come out slightly different making further work a lot harder. Besides, it will simply take much longer. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image022.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I press the fabric. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>And then I remove all pins and separate the lay grouping the lining pieces separately from the face pieces. I put the face pieces on a hanger, secure them with a pin, and put them aside.&nbsp; </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I duplicate the cups of the side front lining pieces with hard iron-on fabric.&nbsp; </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image032.jpg" class="img-responsive center-block"></p>
<p><strong>The central front piece: </strong></p>
<p>You should keep in mind that this piece needs to be unfolded before duplicating. To do this, <strong>I need to remove pins not only from the iron-on batiste but partially from the skirt too.</strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image034.jpg" class="img-responsive center-block"></strong></p>
<p><strong>First of all, I need to carefully press open the fold line to make it perfectly smooth. </strong>You should never press the fold line when preparing your fabric for cutting.&nbsp; </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>I put the batiste piece fusible side down on the main piece and fuse them together by pressing. </strong>I try to align the edges as neatly as possible. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image038.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image040.jpg" class="img-responsive center-block"></p>
<p><strong>I turn the piece around and press it from the other side. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>Then, I put the second piece on top of it, cover it with the second batiste piece, and fuse it on.&nbsp; </strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image044.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image046.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image048.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I press the skirt and separate the face from the lining. I put the face pieces on a hanger, secure them with a pin, and put them aside. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image050.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image052.jpg" class="img-responsive center-block"></p>
<p><strong>I duplicate the cups of the lining with hard iron-on fabric. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image054.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image056.jpg" class="img-responsive center-block"></p>
<p><strong>I press the skirt part of the lining and put it aside on a hanger too. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image058.jpg" class="img-responsive center-block"></p>
<p><strong>It is turn to duplicate the back pieces now. </strong></p>
<p><strong>I recommend </strong>you should fully concentrate on the process. Try not to get distracted by anything because you are forming the foundation of your dress at this point. </p>
<p><strong>The central back pieces: </strong></p>
<p><strong>I duplicate the pieces using the sandwich method just like I did with the side front pieces.&nbsp; </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image060.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image062.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image064.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image066.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image068.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image070.jpg" class="img-responsive center-block"></p>
