<p><strong>It is time to mark the positions of bones. </strong>&nbsp;</p>
<p><strong>You may be wondering</strong> why add any bones at all when the bride is so well-built and the dress has a low back anyway. Is there a point in reinforcing the cups? A slender woman like my client surely needs no body control effect!&nbsp; </p>
<p><strong>Let me explain it. </strong>These bones will spread out the dress and help it hold its shape and stay smooth. They will eliminate possible tucks and creases that may otherwise spoil the overall look of the dress.&nbsp; </p>
<p><strong>Trust me and be sure to explain it to your client </strong>that these bones will not obstruct her movements but, vice versa, she will feel a lot more comfortable in a dress reinforced with bones. Even when there is no need in a body control effect, the dress is supposed to be snug and not get wrinkled at the sides and along the princess seams.&nbsp; </p>
<p><strong>And this is why I use bones even when I am sewing for a slender woman.</strong></p>
<p><strong>I mark guidelines on the side front pieces. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image100.jpg" class="img-responsive center-block"></p>
<p>I draw the waistline and the bust line.&nbsp; </p>
<p><strong>There will be a horizontal bone running along the bust line (1). </strong>It will pass across the central piece and stop on the other side front piece. </p>
<p><strong>A half-circle bone (2) will go along the bottom border of the cup.</strong> Its position is already marked by the edge of the hard iron-on fabric. This bone also passes across all front pieces. </p>
<p>I step 2cm from the bottom end of the half-circle bone and <strong>draw an inclined bone (3)</strong> that stops in about the middle of the top edge of the piece.</p>
<p><strong>I draw another bone 3.5cm below the bust line for additional support at the bottom of the cups (4).</strong> </p>
<p><strong>And I draw a bone 3.5cm above the bust line to reinforce the top of the cups (5).</strong> </p>
<p><strong>Bones 4 and 5 also pass across all front pieces. </strong></p>
<p><strong>Next, I mark a vertical bone perpendicular to the waistline (6). </strong>This bone supports the horizontal bone (1) and, as a rule, starts from the intersection of the bust line and the bottom border of the cup. Make sure to step at least 2cm from the seam allowance of the side seam so you can adjust the dress down if needed. </p>
<p><strong>Another vertical bone (7) </strong>divides the area between bone 6 and the princess seam in about half.</p>
<p><strong>I mark symmetric bones on the second side front piece.</strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image102.jpg" class="img-responsive center-block"></p>
<p><strong>Bones 1, 2, 4, and 5 pass across the entire front of the cups. </strong></p>
<p><strong>There is an inclined vertical bone (8) and two straight vertical bones (9 and 10). </strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image104.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I mark bone positions on the side front pieces.</strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image106.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Bones 1, 2, 4, and 5 pass across the entire front part of the cups.</strong></p>
<p><strong>I step 2cm from the front princess seams and mark bones 11 and 12 at an angle. </strong></p>
<p><strong>Vertical bones 13 and 14 are placed 3cm from the central vertical line on the piece.</strong> They must be symmetric - otherwise they are likely to deform the shape of the neckline. </p>
<p><strong>Sometimes, easing in the cups to give them a round shape may result in a small fold along the under-bust line. </strong>I usually solve this problem by sewing a 5mm-1cm dart. This dart will get overlaid by the face fabric. You can even sew darts on the face if you plan to decorate these areas are with appliqu� or other embellishments. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image108.jpg" class="img-responsive center-block"></p>
<p><strong>Now I need to mark bones on the back of the dress. </strong></p>
<p><strong>Another thing people often ask me </strong>is why put bones on the back of the dress when the back pieces are so small?&nbsp; </p>
<p><strong>I believe it always depends on the situation. </strong>I usually put bones on the back of a garment to reinforce the back princess seams and to prevent the zipper from "sinking in" in the middle. </p>
<p><strong>The central back pieces:</strong></p>
<p><strong>I place a bone along the back princess seam (15) and another vertical bone (16) </strong>in about the middle of the piece (i.e. dividing in half the area between the seam allowance of the side seam and the princess seam.</p>
<p><strong>Then I mark symmetric bones on the other central back piece.</strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image110.jpg" class="img-responsive center-block"></p>
<p><strong>The side back pieces: </strong></p>
<p><strong>After marking a bone along the back princess seam, I mark a vertical bone (17) </strong>in the middle of the piece perpendicular to the waistline.</p>
<p><strong>Symmetric bones are then marked on the other side back piece.</strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2018_files/image112.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I recommend you press the bones before sewing them on </strong>because otherwise they will shrink later when you press the dress, which will result in deformation.</p>

