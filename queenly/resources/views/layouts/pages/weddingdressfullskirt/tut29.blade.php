<h1>Tutorial 28. Pressing the Neckline of the Dress.</h1>
<p><strong>Before pressing the bodice, I fold in the neckline edge and secure it with pins. </strong>It will be easier to press it this way.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image002.jpg" alt=""></p>
<p><strong>I carefully press the neckline on a cup pressing mould to secure the folded edge.</strong> I press it first with and then without steam. The V-part requires particular attention. And I recommend you press the back neckline and the armscyes on a sleeve ironing board.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image022.jpg" alt=""></p>
<p><strong>Now I need to sew the lace trimming to the lining by hand. </strong>After doing so, I will remove the pins and iron away the holes pierced in the face fabric.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image024.jpg" alt=""></p>
<p><strong>The intersection of the trimmings will be decorated with a bow.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image026.jpg" alt=""></p>
<p><strong>Do you know how to make a pretty bow for decorating the lining?</strong></p>
<p><strong>I take a satin ribbon with a width of about 1cm,</strong> cut a length of 15cm, and heat-seal the ends with a lighter. Then I fold the ribbon in half, and one more time in half, and press it to secure the fold lines.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image030.jpg" alt=""></p>
<p><strong>I fold the ribbon as shown in the picture below.</strong> Make sure to keep the pressed fold lines symmetric and parallel to the ends of the ribbon.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image032.jpg" alt=""></p>
<p><strong>Next, I thread a sewing needle</strong> and tie a very neat knot at the end of the thread. I secure the overlapped ends of the ribbon with a loop stitch.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image034.jpg" alt=""></p>
<p><strong>I make a row of small running stitches along the diagonal of the square without taking the ribbon off the needle. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image038.jpg" alt=""></p>
<p><strong>I put the central fold line of the ribbon to the needle and continue picking the fabric with running stitches right along the fold line. </strong>The whole ribbon is gathered on the needle along the fold line.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image040.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image044.jpg" alt=""></p>
<p><strong>I bring the needle out from the opposite end, gather the bow properly on the thread, wrap the thread around it several times, and secure it at the back.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image050.jpg" alt=""></p>
<p><strong>Here is the bow. </strong>You can sew it in place with the same threaded needle.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image052.jpg" alt=""></p>
<p><strong>I have secured the neckline with blind stitches and added a pretty bow in the middle.</strong> The inside has a neat clean finish and the bow makes it even prettier.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2028_files/image054.jpg" alt=""></p>

