<h1>Tutorial 32. Sewing a Mock-Up Top. Calculating the Mesh Skirts.</h1>
<p><strong>I have sewn a mock-up top and put it on the main dress.</strong> It fits very well! I have carefully examined the back and the front - both are perfect!</p>
<p><strong>Instead of sewing the side seams, I simply mark the exact positions of front and back side seam lines and pin the pieces together along them. The seam allowance is 2cm. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2032_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2032_files/image004.jpg" alt=""></p>
<p><strong>Before marking shoulder straps at the front and at the back of the top, I take all measurements again:</strong> both the ones I could not take off the initial pattern, as well as the ones I had taken before - just to double-check them. Now I can replace those question marks in the table with actual values.</p>
<p><strong>Next, I start working on the shoulder straps. </strong></p>
<p><strong>I have noticed that the shoulder straps are positioned in the middle of the shoulder on the mock-up</strong> as they would be on a summer singlet dress. <strong>But I want them to cover up the bride's shoulder tips. </strong></p>
<p><strong>I have measured Shoulder to Shoulder across the front of the top and here is what I think:</strong> I need to add 2cm to this measurement to position the shoulder straps the way I want. All other measurements are correct.</p>
<p><strong>I draw the front view of the finished garment </strong>with the desired shape of shoulder straps and add a seam allowance.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2032_files/image006.jpg" alt=""></p>
<p><strong>After choosing the desired strap width, I draw a graceful back neckline and mark a seam allowance.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2032_files/image008.jpg" alt=""></p>
<p><strong>After drawing the front and the back of the top, I take the mock-up off the dress-form and apply changes to the adjusted paper pattern. </strong>I have also realized I need to raise the top point of the side back piece by about 1cm.</p>
<p><strong>Now I can safely start cutting the top. </strong>By the way, mesh fabric can be optionally replaced with luxurious lace fabric.</p>
<p><strong>Please note that the shoulder straps will be cut in paired pieces. </strong>In other words, they will have a kind of face and lining.</p>
<p><strong>But I suggest we should first cut our mesh skirts and then use the remaining fabric for the top because it is made of relatively small parts. </strong></p>
<p><strong>Let us calculate mesh fabric consumption for the skirt. </strong></p>
<p><strong>As I said before, it will be a four-layer mesh full-sweep skirt with a train.&nbsp; </strong></p>
<p><strong>All measurements for drafting the skirt are to be taken off the finished base of the dress. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2032_files/image010.jpg" alt=""></strong></p>
<p><strong>Mine are all taken 6cm from the waistline. </strong></p>
<p><strong>Here are the measurements I take off the main assembled dress: </strong></p>
<p><strong>Waist Circumference</strong> - relative waist circumference taken 6cm from the actual waistline - <strong>86cm;</strong></p>
<p><strong>Skirt Length Front</strong> - <strong>118cm;</strong></p>
<p><strong>Skirt Length Side</strong> - <strong>120cm;</strong></p>
<p><strong>Skirt Length Back</strong> (small train included) - <strong>145cm.</strong></p>
<p><strong>I use a plain formula for calculating the circumference of a circle:</strong></p>
<p><strong>2πR = WC = 86cm, </strong>where</p>
<p><strong>π</strong> = 3.14 (invariable value) and <strong>R</strong> = the radius I will mark during the cutting.</p>
<p>My calculator tells me that:</p>
<p><strong>R ≈ 14cm</strong></p>
<p><strong>This is the radius I need to mark to achieve a waist circumference of 86cm.</strong></p>
<p><strong>And now I need to figure out the longest possible radius at which the layout will not exceed the width of my tulle fabric (300cm). </strong></p>
<p><strong>Knowing the longest possible radius length will allow me to calculate the biggest possible gathering ratio (GR): </strong></p>
<p><strong>GR = Rmax / R </strong></p>
<p><strong>I draw a straight angle. The horizontal side is the fold line of the mesh fabric (which is folded in half) and the vertical side equals half its width (150cm). </strong></p>
<p>I mark the <strong>Skirt Length Front </strong>measurement starting from the edge of the fold line. It is <strong>118cm</strong> plus double the value of <strong>Rmax</strong> (which needs to be calculated) and the value of <strong>Skirt Length Back (145cm). </strong></p>
<p>I draw a line perpendicular to the fold line between the two sections of <strong>Rmax.</strong></p>
<p>Then, starting from the bottom selvage, I mark the<strong> Skirt Length Side</strong> measurement along this perpendicular line <strong>(it is 120cm).&nbsp; </strong></p>
<p><strong>Rmax = 150cm - 120cm = 30cm. It is the longest possible radius I can use on a piece of mesh fabric with a width of 300cm.</strong></p>
<p><strong>GR = Rmax / R = 30cm / 14cm ≈ 2. </strong></p>
<p><strong>And this means I can gather the mesh skirt with a 2 to 1 ratio. </strong></p>
<p>The skirt will not be gathered thickly in this case. It will be neat, soft, and flowing.</p>
<p><strong>Let us calculate fabric consumption. </strong></p>
<p><strong>I sum up the lengths of all sections of the fold line to find the required length of mesh fabric for cutting a single layer skirt with a train: </strong></p>
<p><strong>118cm + 30cm + 30cm + 145cm = 323cm ≈ 330cm </strong></p>
<p><strong>A four-layer skirt will require about 14m of mesh fabric (with extra space for cutting the top). </strong></p>
<p><strong>WARNING: remember to mark a seam allowance along the top edge when cutting the skirt! </strong></p>
<p>Mine will be 1.5cm.</p>
<p><strong>In this case, I need to cut it with the following radius: </strong></p>
<p><strong>Rmax1 = 30cm - 1.5cm = 28.5cm.</strong></p>
<p><strong>There is no need in adding a seam allowance along the bottom edge because I will simply trim the hem! </strong></p>

