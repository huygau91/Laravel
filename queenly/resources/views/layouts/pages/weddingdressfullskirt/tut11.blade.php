<h1>Tutorial 10. Drawing the Ruffles Layout. Calculating All Ruffles.</h1>
<p><strong>I have made tunnels for all hoops and sewn on two bottom ruffles made of hard mesh fabric: the main one and the additional one. </strong></p>
<p><strong>Now I will calculate the required number of ruffles for the skirt itself and mark their positions on it. </strong>&nbsp;These ruffles will form the final shape of the skirt.</p>
<p><strong>I will use two types of tulle: soft and hard. </strong>The two bottom ruffles were made from hard tulle or mesh.</p>
<p><strong>Ruffles made from soft tulle:&nbsp; </strong></p>
<p><strong>Two ruffles will run along the tunnels for hoops 3 and 4 and another ruffle will be placed 20cm above the top tunnel. </strong>I carefully measure the distance from the bottom edge of the yoke to each tunnel. The bottom ruffle will start 56cm, the middle ruffle – 36cm, and the top ruffle – 16cm below it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2010_files/image002.jpg" alt=""></p>
<p><strong>There will be a soft tulle ruffle along each marked line.</strong> I will gather them rather quickly, with a 5-to-1 ratio. The ruffles will reach down to the floor along the full perimeter of the petticoat. They are lightweight and will not disturb the bride. Besides, nobody will be able to see that the petticoat is 3cm shorter at the front.</p>
<p><strong>In order to calculate the required amount of soft tulle strips for the ruffles, you need to know the full circumference or half the circumference of each line marked on the petticoat, as well as how far from the floor it is. </strong></p>
<p><strong>Here is what I got: </strong></p>
<p><strong>Top Ruffle (1): </strong>½ sewing line circumference – 60cm; distance to the floor – 84cm.</p>
<p><strong>Middle Ruffle (2): </strong>½ sewing line circumference – 80cm; distance to the floor – 64cm.</p>
<p><strong>Bottom Ruffle (3):</strong> ½ sewing line circumference – 100cm; distance to the floor – 44cm.</p>
<p>The distance to the floor is calculated based on the length of the petticoat from the bottom edge of the yoke to the floor (100cm).</p>
<p><strong>I will not account for the fact that the top layers of the ruffles come out shorter than the bottom layers</strong> on a full skirt like ours. The top layer is supposed to be cut larger than the bottom layer if you want them to be the same when finished. I will cut them identical though.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2010_files/image004.jpg" alt=""></p>
<p><strong>Let us calculate the required number of tulle strips. </strong></p>
<p><strong>My soft tulle has a width of 300cm. </strong></p>
<p><strong>The gathering ratio is 5 to 1.</strong></p>
<p><strong>G = 5.</strong></p>
<p>I need to define the length of each strip with regard to the gathering ratio and divide the resulting value by the width of my tulle fabric. This is how I determine the number of strips of the required width.</p>
<p><strong>Top Ruffle (1). </strong></p>
<p><strong>Length of tulle strip: </strong></p>
<p><strong>60cm × 5 × 2 = 600cm</strong></p>
<p><strong>600cm / 300cm ≈ 2 strips with a width of 84cm.</strong></p>
<p><strong>Middle Ruffle (2).</strong></p>
<p><strong>Length of tulle strip:</strong></p>
<p><strong>80cm x 5 x 2 = 800cm</strong></p>
<p><strong>800cm / 300cm ≈ 3 strips with a width of 64cm. </strong></p>
<p><strong>Bottom Ruffle (3).</strong></p>
<p><strong>Length of tulle strip:</strong></p>
<p><strong>100cm × 5 × 2 = 1000cm</strong></p>
<p><strong>1000cm / 300cm ≈ 4 strips with a width of 44cm.</strong></p>
<p>I will make the bottom ruffle thicker than the rest.</p>
<p><strong>Soft tulle consumption (fabric width = 300cm): </strong></p>
<p><strong>84cm × 2 + 64cm × 3 + 44cm × 4 = 168cm + 192cm + 176cm = 536cm ≈ 5.5m</strong></p>
<p><strong>Hard mesh (hard tulle) ruffles.&nbsp; </strong></p>
<p><strong>I need to increase the number of ruffles at the back of the skirt to account for the train</strong>. As I said before, we don't want the skirt look wider at the sides.</p>
<p><strong>The skirt should only widen in the train area at the back which I have outlined with a row of stitches (green lines in the picture).&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2010_files/image006.jpg" alt=""></p>
<p><strong>I will add three hard mesh ruffles in the train area: </strong>the top ruffle will start 10cm below the top soft tulle ruffle. The hard mesh ruffles will also be positioned with 20cm intervals. They will be pretty much sewn in-between the soft tulle ruffles.</p>
<p><strong>The top ruffle will start 26cm, the middle ruffle – 46cm, and the bottom ruffle – 66cm below the bottom edge of the yoke.&nbsp; </strong></p>
<p><strong>The bottom ruffle will extend 6cm below floor level and settle upon the floor in the train area. </strong></p>
<p><strong>I will also add 4cm to the length of the hard mesh part of the top ruffle and 2cm to the length of the hard mesh part of the middle ruffle to account for the expected difference in the final lengths. </strong></p>
<p><strong>To calculate the required number of hard mesh strips, you need to know the full circumference or half the circumference of each sewing line and its distance to the floor.</strong></p>
<p><strong>Here is what I got.</strong></p>
<p><strong>Top Ruffle (1): </strong></p>
<p><strong>–</strong> ½ sewing line circumference – 18cm;</p>
<p><strong>–</strong> Ruffle length: (100cm - 26cm) + 6cm + 4cm = 84cm</p>
<p><strong>Middle Ruffle (2): </strong></p>
<p><strong>–</strong> ½ sewing line circumference – 24cm;</p>
<p><strong>–</strong> Ruffle length: (100cm - 46cm) + 6cm + 2cm = 62cm</p>
<p><strong>Bottom Ruffle (3): </strong></p>
<p><strong>–</strong> ½ sewing line circumference – 28cm;</p>
<p><strong>–</strong> Ruffle length: (100cm - 66cm) + 6cm = 40cm</p>
<p><strong>Let us calculate the required number of hard mesh strips.</strong></p>
<p><strong>My hard mesh has a width of 180cm.</strong></p>
<p><strong>The gathering ratio is 3 to 1. </strong></p>
<p><strong>G = 3. </strong></p>
<p>I already know that this gathering ratio creates very nice ruffles so I will stick to it.</p>
<p>I need to define the length of each strip with regard to the gathering ratio and divide the resulting value by the width of my mesh fabric to determine the number of strips of the required width.</p>
<p>I round up the length of the half-arc in the calculations.</p>
<p><strong>Top Ruffle (1).</strong></p>
<p><strong>Length of hard mesh strip:</strong></p>
<p><strong>20cm × 3 × 2 = 120cm</strong></p>
<p><strong>120cm / 180cm ≈ 1 strips with a width of 84cm.</strong></p>
<p><strong>Middle Ruffle (2).</strong></p>
<p><strong>Length of hard mesh strip:</strong></p>
<p><strong>25cm × 3 × 2 = 150cm</strong></p>
<p><strong>150cm / 180cm ≈ 1 strips with a width of 62cm.</strong></p>
<p><strong>Bottom Ruffle (3).</strong></p>
<p><strong>Length of hard mesh strip:</strong></p>
<p><strong>30cm × 3 × 2 = 180cm</strong></p>
<p><strong>180cm / 180cm ≈ 1 strips with a width of 40cm.</strong></p>
<p><strong>Hard mesh consumption (fabric width = 180cm): </strong></p>
<p><strong>84cm × 1 + 62cm × 1 + 40cm × 1 = 186cm ≈ 200cm. </strong></p>
<p>I am sure it will be a very beautiful train.</p>
<p>I can always shorten the ruffles if they turn out to be longer than needed. It would be a shame to realize some 1-2cm were lacking.</p>

