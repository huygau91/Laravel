						<p>In this course we will learn how to sew the most popular wedding dress design – classic dress with a full skirt and lace straps.</p>
<p>And we will learn how to make a perfect fitting dress to a client from a long distance, relying solely on the measurements.</p>
<p><strong>Features:</strong></p>
<ul>
<li>Sewing a crinoline petticoat from A-Z.</li>
<li>How to make opaque a skirt made from thin tulle.</li>
<li>How to calculate skirt width so it will fit perfectly to the&nbsp;crinoline.</li>
<li>How to adjust the pattern for you client’s size.</li>
<li>How to draft a pattern for the shoulder straps (2 options)</li>
<li>Peculiarities of long distance sewing.</li>
<li>How to make a “virtual” fit test.</li>
<li>How to use a mannequin for fit test.</li>
<li>What to do, when a mannequin won’t fit your client’s size.</li>
</ul>
<p><strong>Where to use:</strong></p>
<ul>
<li>This technology is absolutely universal and has no limits in choosing and creating any style of wedding and evening dresses!</li>
</ul>
