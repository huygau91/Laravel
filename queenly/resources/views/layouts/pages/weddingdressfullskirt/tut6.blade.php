<h1>Tutorial 5. Cutting the Petticoat.</h1>
<p>I will first draw the lay on the board for clarity and then transfer it on mesh fabric in the full size.</p>
<p><strong>What you see on the board is mesh fabric folded in half. </strong>The fold line is on the right.</p>
<p><strong>The front of the petticoat: &nbsp;</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image002.jpg" alt=""></strong></p>
<p>I measure <strong>2cm (dip of waistline)</strong> down from <strong>point A</strong> at the top of the fold line and mark <strong>point B,</strong> and then mark <strong>point C</strong> left of point A at a distance equal to the width of the top half of the petticoat front:</p>
<p><strong>22cm + 1.2cm = 23.2cm</strong></p>
<p><strong>I connect points B and C with a smooth arc (it is the top edge of the front). </strong></p>
<p>Next, I mark <strong>point D</strong> down the fold line (which runs along the middle of the front) at a distance equal to the front length of the petticoat with top and bottom seam allowances included:</p>
<p><strong>80cm + 2.5cm = 82.5cm</strong></p>
<p>I draw a line perpendicular to the fold line which starts at <strong>point D </strong>and has a length equal to the width of the bottom half of the front of the petticoat <strong>(point E).</strong> I include a seam allowance and a little extra space just in case.</p>
<p><strong>67cm + 1.2cm = 68.2cm ≈ 70cm </strong></p>
<p><strong>I connect points C and E with a straight line. It is the side line. </strong></p>
<p>I measure the side length of the petticoat (seam allowances included) down the side line <strong>from point C </strong>and mark <strong>point F:</strong></p>
<p><strong>95cm + 2.5cm = 97.5cm</strong></p>
<p><strong>I connect points D and F with a smooth arc and get the bottom line of the front.</strong></p>
<p><strong>A rough draft of the front half of the petticoat is ready!&nbsp; </strong></p>
<p><strong>Now let us draw the back half: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image004.jpg" alt=""></strong></p>
<p>I mark <strong>point H</strong> down <strong>from point G</strong> at the top of the selvage (which is the central edge of the back) at a distance equal to the back length of the petticoat with seam allowances included:</p>
<p><strong>100cm + 2.5cm = 102.5cm</strong></p>
<p><strong>I mark point I 2cm down from point H.&nbsp; </strong></p>
<p>I draw a line perpendicular to the selvage <strong>from point I</strong> and mark the width of the top half of the back of the petticoat along it<strong> (point J).</strong> Seam allowances are included (remember about the seam at the back).</p>
<p><strong>22cm + 2.5cm = 24.5cm </strong></p>
<p><strong>I connect points H and J with a smooth arc to draw the top edge of the back.</strong></p>
<p>I mark <strong>point K </strong>along the top selvage at a distance from <strong>point G</strong> equal to the width of the bottom half of the back of the petticoat with seam allowances and some additional space included:</p>
<p><strong>67cm + 2.5cm = 69.5cm ≈ 72cm </strong></p>
<p><strong>I connect points K and J with a straight line and get the side line.</strong></p>
<p>I measure the side length of the petticoat <strong>from point J</strong> (seam allowances included) and mark <strong>point L:</strong></p>
<p><strong>95cm + 2.5cm = 97.5cm</strong></p>
<p><strong>I mark point M 10cm from point K.</strong> Point M is where the bottom hoop starts to touch the floor.</p>
<p><strong>I connect points M and L with a smooth arc.&nbsp; </strong></p>
<p><strong>Section G - M - L forms the bottom edge of the back. </strong></p>
<p><strong>The draft is ready! It can be transferred on the mesh fabric now.&nbsp; </strong></p>
<p><strong>The cutting begins. </strong>It is hard to show you the lay properly due to its large size. Lucky I drew it on the board first!</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image006.jpg" alt=""></p>
<p><strong>A handle with suction cups is a great tool for working with large surfaces. You can stick rulers of different lengths and widths onto it. Keep that in mind! </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image010.jpg" alt=""></p>
<p><strong>I will start from the back of the petticoat. </strong></p>
<p><strong>The mesh fabric is folded in half. I start drawing from the selvage. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image012.jpg" alt=""></strong></p>
<p><strong>I draw a line perpendicular to the fold line along the bottom selvage and mark the width of the bottom half of the back of the petticoat (72cm with seam allowances included). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image014.jpg" alt=""></strong></p>
<p><strong>Then, I mark the back length of the petticoat from the corner of the selvage which forms the central edge of the back (102.5cm with seam allowances included). And I mark another 2cm.&nbsp;&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image018.jpg" alt=""></p>
<p><strong>I draw a line perpendicular to the selvage with a length equal to the width of the top half of the back (24.5cm including seam allowances). </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image020.jpg" alt=""></p>
<p><strong>Then I draw the side line and mark the side length of the petticoat along it (97.5cm including seam allowances). </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image024.jpg" alt=""></p>
<p><strong>I draw a smooth arc for the top edge of the back. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image026.jpg" alt=""></strong></p>
<p><strong>And I draw the bottom edge of the back. </strong>As I warned you before, you need to correct the hypothetical calculated values when drafting patterns on fabric. In my case, the bottom hoop of the petticoat starts to touch the floor 35cm past the side line (and not 10cm as I had expected).</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image028.jpg" alt=""></p>
<p><strong>Next I mark all hoop positions. </strong></p>
<p><strong>I mark 25cm up from the bottom point of the central edge of the back (the interval between hoops 1 and 2) and then mark hoops 2, 3, and 4 with 20cm intervals in-between. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image030.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image032.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image034.jpg" alt=""></strong></p>
<p><strong>I measure the distance from the top hoop to the waistline. It is 50cm.</strong> This means the top hoop of the petticoat will go just above the knee level as was planned. There is no need in placing any hoops higher than that.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image036.jpg" alt=""></p>
<p><strong>I mark all hoop positions along the side line. I mark 15cm up from the bottom point of the side line (the distance from the bottom edge to hoop 2) and then mark hoops 2, 3, and 4 with 20cm intervals. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image038.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image040.jpg" alt=""></strong></p>
<p><strong>I pin the layers of mesh fabric together. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image044.jpg" alt=""></p>
<p><strong>I shift the lay and start drawing the front half of the petticoat. I start from the fold line of the mesh fabric which runs along the centre of the front. </strong></p>
<p><strong>I draw a line perpendicular to the fold line with a length equal to the width of the top half of the front (23.2cm including seam allowances). </strong>It fits in the available space perfectly.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image046.jpg" alt=""></p>
<p><strong>I step 2cm down the fold line to account for the dip of the waistline and draw the top edge of the front of the petticoat. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image048.jpg" alt=""></p>
<p><strong>I mark the centre front length of the petticoat up from the bottom point marked on the fold line. It is 82.5cm with seam allowances included. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image050.jpg" alt=""></strong></p>
<p><strong>I draw a perpendicular from the bottom point of the fold line with a length equal to the width of the bottom half of the front (70cm).</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image052.jpg" alt=""></strong></p>
<p><strong>But here comes a small challenge: </strong>there is not enough space to mark 70cm. The line overlaps with the back of the petticoat. It is impossible to take in account everything at once and you are just as likely to face a problem like this.</p>
<p><strong>Look what I do in such cases. </strong></p>
<p><strong>I keep moving down the fold line</strong> until the 70cm long perpendicular fits into the free space without getting overlapped with the drafted back of the petticoat.</p>
<p><strong>I re-draw the front of the petticoat, this time starting from the bottom edge. </strong></p>
<p><strong>I mark 82.5cm up from the bottom point of the fold line. It is the front length of the petticoat with seam allowances included. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image054.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image056.jpg" alt=""></strong></p>
<p><strong>I step another 2cm upwards and draw a perpendicular line with a length equal to the width of the top edge of the front (23.2cm).&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image058.jpg" alt=""></strong></p>
<p><strong>I draw the waist opening and the side line of the front. I mark the side length of the petticoat along the side line (97.5cm including seam allowances). </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image060.jpg" alt=""></strong></p>
<p><strong>And I mark hoop positions on the front of the petticoat. </strong></p>
<p><strong>I have to say</strong> that I forgot to include a 1.2cm seam allowance when I was marking hoop positions on the back of the petticoat. I will fix it later.</p>
<p><strong>I start by marking hoop positions along the central edge of the front. I mark 6.2cm up from its bottom point (the interval between hoops 1 and 2 plus a 1.2cm seam allowance) and then mark hoops 2, 3, and 4 with 20cm intervals. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image062.jpg" alt=""></strong></p>
<p><strong>I mark 16.2cm up from the bottom of the side line (the interval between hoops 1 and 2 plus a 1.2cm seam allowance) and then mark hoops 2, 3, and 4 with 20cm intervals.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image064.jpg" alt=""></strong></p>
<p><strong>I roughly draw the hoops themselves.</strong> They are supposed to be strictly horizontal at the front (i.e. perpendicular to the central edge) and somewhat rounded at the sides because of fabric distortion.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image066.jpg" alt=""></p>
<p><strong>Measuring the draft has shown that I need 125cm mesh fabric for the petticoat.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image068.jpg" alt=""></strong></p>
<p><strong>I cut out the front and the back of the petticoat. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image070.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image072.jpg" alt=""></p>
<p><strong>I put the pieces together and adjust the hoop positions on the back adding the missing 1.2cm seam allowance. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image074.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image076.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image078.jpg" alt=""></p>
<p><strong>Next, I need to mark the train. </strong></p>
<p><strong>I mark 10-11cm from the top point and 40cm from the bottom point of the central back edge, connect the resulting points, and draw a train line.</strong> I will stitch along the marked line because pencil gets rubbed off too soon.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image080.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image082.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image084.jpg" alt=""></p>
<p><strong>I mark the positions of vertical Rigilene bones for reinforcing the petticoat.</strong></p>
<p><strong>These bones will extend 3cm beyond the third hoop of the petticoat.</strong></p>
<p><strong>The first bone will be placed 15cm from the side edge. </strong></p>
<p><strong>Two more bones will divide the area between the first bone and the central edge of the back in three equal sections.</strong> The distance between the bones will be about 18cm at the bottom and 11cm at the top. I will also add a horizontal supporting bone 3cm above the third hoop of the petticoat.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image086.jpg" alt=""></p>
<p><strong>In my case, the border of the train area coincides with the line marked for second vertical bone,</strong> which means the train will be framed in a very nice way.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%205_files/image088.jpg" alt=""></p>

