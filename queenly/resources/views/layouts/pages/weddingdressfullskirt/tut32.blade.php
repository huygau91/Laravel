<h1>Tutorial 31. Adjusting the Top after the Measurements of the Main Dress.</h1>
<p><strong>I have arranged the initial patterns on the table and put the corresponding parts of the main dress on top of them. The latter are the patterns I used for cutting and sewing the base of the dress. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image002.jpg" alt=""></p>
<p><strong>For my own convenience, </strong>I will adjust the initial pattern of the top using the calculated values and the adjusted pattern of the main bodice. This will help me double-check the calculations and make the work easier.</p>
<p><strong>The first thing I do is remove 1cm along the central edge of the central front piece as specified by the Bust Apex to Bust Apex measurement and the calculations. </strong></p>
<p><strong>I re-draw the waistline 5mm lower than it was.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image008.jpg" alt=""></p>
<p><strong>And I start adjusting the back parts of the top. </strong></p>
<p><strong>According to the calculations, the back side seam line needs to be shifted 2cm inwards.</strong> But I really want to preserve certain parameters of the dress on the top as well: for example, I would like the back princess seams to stay where they are.</p>
<p><strong>For this reason, it is easier to adjust the central back piece of the top by this 2cm. </strong>In this case, it will be almost identical to the central back piece of the dress. The curve of the back is nothing to worry about because this area will be trimmed away. There will be a shoulder strap here.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image010.jpg" alt=""></p>
<p><strong>I compare the side back pieces of the top and the base of the dress.</strong></p>
<p><strong>The top is a little larger than the dress in the waist near the side seam. The difference is about 5mm.</strong> But when I was sewing the side seams of the dress, I brought the seam allowance to 1.5cm instead of 2cm in the waist, thereby making the piece 5mm larger. And this means there is no need in changing the side seam line of the side back piece of the top.</p>
<p><strong>I will only adjust the curve of the back princess seam.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image012.jpg" alt=""></p>
<p><strong>I move to the front of the top. </strong></p>
<p><strong>I put together the side front pieces of the top and the dress. </strong>There is no need in shifting the side seam line in the waist because I gradually brought the seam allowance to 1.5cm instead of 2cm in this area when I was sewing the side seams of the dress.</p>
<p><strong>As follows from the calculations, the front side seam line needs to be shifted 75mm inwards in the waist.</strong> You can say that this adjustment was made when I added 5mm to the side piece of the dress by narrowing the seam allowance of the side seam. There is no need in additional changes.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image014.jpg" alt=""></p>
<p><strong>I will adjust the princess seam lines of the side front piece and the central front piece in correspondence with the size of the dress pieces. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image016.jpg" alt=""></p>
<p>Judging from the calculations, only 25mm needs to be removed at the bust line level. <strong>I slightly straighten the bust princess seam on the side front piece. </strong>The central front piece remains unchanged.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image018.jpg" alt=""></p>
<p><strong>I am also supposed to shift the front side seam line 1.75cm inwards at the under-bust line level. </strong></p>
<p>Before applying all these alterations, I measure the distance between the front princess seam and the side seam along the under-bust line of the finished dress. It is 12cm. The same distance on the side front piece of the top is 11.8cm: <strong>therefore, I don't need to apply any changes to the front of the top at the under-bust level.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image020.jpg" alt=""></strong></p>
<p><strong>Let us decide on the length of the side seam.&nbsp; </strong></p>
<p><strong>The actual Side Length of the front is supposed to be 18cm. I add a seam allowance of 1.2cm and mark a total length of 19.2cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image022.jpg" alt=""></strong></p>
<p>I mark a clip of 11cm (or 12.2cm including the seam allowance) on <strong>the central front piece of the top </strong>from the bust apex point toward the armscye. There is an excess of 1.8cm left on the pattern. I will trim it away. The same 1.8cm needs to be removed at the top of the bust princess seam on the central front piece.</p>
<p><strong>I re-draw the armscye line of the side front piece by connecting the new top point of the bust princess seam with the new top point of the side seam.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image024.jpg" alt=""></strong></p>
<p><strong>I put the central front piece of the dress over the central front piece of the top and trace the neckline. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image026.jpg" alt=""></p>
<p><strong>The clip toward neckline is 6cm or 7.2cm with the seam allowance. It is the same on both pieces. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image028.jpg" alt=""></strong></p>
<p><strong>But I don't want the neckline of the mesh top to strictly follow the neckline of the main dress. </strong>I want the mesh fabric to go beyond the edge of the main fabric for a more delicate look, so I simply re-draw the neckline 2.5 higher on the top.</p>
<p><strong>I need to find the end of the shoulder strap. </strong></p>
<p>This involves marking the following measurements: Side Neck Point to Bust Apex (22cm or 24cm including the seam allowance) and Shoulder Tip to Bust Apex (21.5cm or 23.5cm including the seam allowance).</p>
<p><strong>I draw the end of the shoulder strap and connect it with the new top point of the front princess seam line re-drawing the armscye line. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image030.jpg" alt=""></strong></p>
<p><strong>I don't know the precise configuration of the neckline yet. </strong>I will only be able to see it and adjust the shape of the shoulder straps after sewing a mock-up top after the adjusted pattern and putting it on the assembled dress. At that stage I will be able to take all final measurements: Side Neck Point to Bust Apex, Mid-Armhole Width Front, Armscye Circumference, etc. <strong>And for now, I just mark the supposed position of the neckline and the end of the shoulder strap with red dashed lines. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image032.jpg" alt=""></p>
<p><strong>Now I only need to work on the top part of the back to finalize the shape of the shoulder strap. </strong></p>
<p><strong>I mark a half of Mid-Armhole Width Back on the central back piece. It is 15.5cm</strong> or 16.7cm including the seam allowance. Please note that I made the piece 2cm smaller by shifting inwards the central back edges. Remember that the central back seam has a seam allowance of 2cm.</p>
<p><strong>I mark the following measurements: Centre Length Back (38.2cm) and Shoulder Tip to Centre Waist Back (41.2cm). Seam allowances are included.</strong></p>
<p><strong>Considering that Shoulder to Shoulder is now 5mm smaller, I need to re-draw the shoulder line, the armscye line, and the top of the back princess seam. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image034.jpg" alt=""></p>
<p><strong>I mark the length of the princess seam on the side back piece (19.2cm with 1.2cm seam allowance included) and re-draw the armscye line.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2031_files/image038.jpg" alt=""></p>
