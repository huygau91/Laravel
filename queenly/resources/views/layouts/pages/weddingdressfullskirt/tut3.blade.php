<h1>Tutorial 2. Taking Measurements.</h1>
<p><strong>Below is a picture of a very cheap petticoat.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image002.jpg" alt=""></p>
<p><strong>I can imagine what you are thinking:</strong> some of you must be shocked, others are smiling, and still others feel pity for me. But, in fact, there is no drama. This really cheap price includes proper steel hoops and, frankly speaking, I have no idea what I could replace them with. There is no way you can use Rigilene boning as a foundation for a petticoat! Rigilene bones can be used to reinforce the petticoat but they cannot replace the steel hoops − they will not be able to hold the weight of the skirt. And I can't say anything about using plastic boning sold per meter as petticoat hoops.</p>
<p>I always buy a cheap petticoat and take out the hoops.</p>
<p><strong>Before making your own petticoat, you need to know </strong>the desired fullness of your future garment and your client's Waist to Floor With Shoes. This measurement is described in detail in Issues 1 and 2 of "Premium Skirts" available at the Corset Academy.</p>
<p><strong>Simply put, Waist to Floor With Shoes is measured from the cord tied around the bride's waist down to the floor while she is wearing the shoes she is going to wear for the wedding ceremony.</strong></p>
<p><strong>Please make sure not to confuse it with Waist to Floor.</strong> Waist to Floor is measured with the woman standing barefooted. But sometimes you need to account for the height of the shoes she will be wearing with the dress.</p>
<p><strong>My client's Waist to Floor With Shoes is 111cm.&nbsp; </strong></p>
<p><strong>I adjust the height of the dress-form in accordance with the measurement: 111cm from the waistline to the floor level.&nbsp; </strong></p>
<p><strong>The fullness of the petticoat is determined by the circumference of its bottom hoop. The bottom hoop of the original petticoat has a circumference of 250cm.</strong> This is basically as small as it gets. If it were 2m, it would be nearly impossible to walk in this petticoat.</p>
<p>I want to look ahead and tell you that the bottom hoop will have a length of about 3m to account for the train. This length will allow the bride to walk freely.</p>
<p>It is best to imitate the fullness of the petticoat on the dress-form <strong>before taking measurements for the future garment.</strong> In my case, there is no need in additional calculations. I will use the original petticoat for this purpose.</p>
<p><strong>I measure from the waist to the floor along the front. </strong>I place the end of the measuring tape at the waist and let it fall down loose so it doesn't deform the petticoat. <strong>The distance is 115cm.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image004.jpg" alt=""></p>
<p><strong>Then I take the same measurement along the side of the petticoat. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image006.jpg" alt=""></strong></p>
<p><strong>I need to account for a small train at the back.</strong> I will use my own measurement value in the calculations instead of measuring the same distance along the back of the petticoat.</p>
<p><strong>Before we continue, let us talk about the balance of the future skirt.</strong> As you can see in the picture, the original petticoat hangs loosely on the dress-form and the bottom hoop is rather far from the floor.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image008.jpg" alt=""></p>
<p><strong>The hoops themselves are sewn on askew.</strong> Although it is a great example of what you must never do, think of it as just a demo. Distorted fabric naturally makes the whole garment look deformed. And the fabric would be spread out properly if the hoops were positioned horizontally. But they were not. It is clear in the picture that one side of the skirt is steeper than the other.</p>
<p><strong>Conclusion: </strong>if the hoops of your petticoat are sewn on askew, the skirt will inevitably look deformed.</p>
<p><strong>Let us continue analyzing the balance of the skirt.</strong> The fullness will be increased with the help of additional ruffles. They are usually sewn on at random. Where exactly to add them depends on the style of the skirt (especially, if it features a train). Even the smallest train implies that you should make the back of the skirt fuller, somewhat hill-shaped.</p>
<p><strong>The skirt is supposed to have an increased slope at the back. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image010.jpg" alt=""></p>
<p>This is done with the help of various ruffles to achieve a smooth transition to the train.</p>
<p><strong>And this method is used regardless of where the flare begins. </strong></p>
<p>The skirt may be flared from below or just above the buttocks or right from the waist. It all depends on the style. In my case, the flare will start 12cm below the waist.</p>
<p><strong>It is important to know that additional ruffles will increase the weight of this part of the skirt though, and it will start sinking in. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image012.jpg" alt=""></strong></p>
<p>Needless to say this is not something we want!</p>
<p><strong>Ready-made petticoats with trains are notorious for this negative effect.</strong> Ruffles often make the train area heavier than the front of the petticoat, which causes the bottom hoop to rise at the front. As I said before, a petticoat should be comfortable enough for the bride to walk freely. The bottom hoop is not supposed to obstruct her movements.</p>
<p><strong>Therefore, the bottom hoop is normally positioned 10-15cm above floor level to ensure proper balance without obstructing the wearer's movements.&nbsp; </strong></p>
<p>Standard petticoats usually have their bottom hoops positioned horizontally 10-15cm above the floor.&nbsp; There is a stiff ruffle below the bottom hoop to prevent the hem of the main skirt from curling in.</p>
<p><strong>But since our skirt features a train, I will have to make it fuller at the back by adding some ruffles.&nbsp; </strong></p>
<p>Of course, there will be ruffles at the front of the skirt as well: not for the sake of making it fuller but to disguise the hoops. The additional ruffles at the back will be therefore a lot heavier than those at the front.</p>
<p><strong>The skirt will thus go askew if the bottom hoop of the petticoat runs parallel to the floor. </strong></p>
<p><strong>This is the most common defect of all ready-made petticoats.&nbsp; </strong></p>
<p><strong>I need to find a different solution and position the bottom hoop in some other way. </strong></p>
<p><strong>I suggest bringing it to about 20cm above floor level at the front while lowering it right down to the floor at the back.</strong> The hoop will be thereby anchored on the floor. The back half of the hoop will cling to the floor maintaining the desired slant and fullness of the skirt.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%202_files/image014.jpg" alt=""></p>
<p><strong>I will do this with my petticoat. </strong>The train will be rather small and cut from see-through mesh fabric together with the face of the skirt. A small train implies a small lining so I don't need to account for it on the petticoat.</p>
<p><strong>What I need to do </strong>is cut it correctly with regard to the bottom hoop and the stiff ruffle below it which will prevent the hem from curling in. I will take the bottom hoop circumference to be 250cm in the cutting.</p>
<p><strong>The stiff ruffle will be sewn on parallel to the floor, i.e. horizontally, and not parallel to the hoop. </strong>&nbsp;This will make the petticoat look straight despite the angled position of the bottom hoop.</p>

