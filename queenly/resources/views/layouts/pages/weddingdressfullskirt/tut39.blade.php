<h1>Tutorial 38. Finishing the Dress after the Fitting.</h1>
<p><strong>My client arrived and we did the first fitting.</strong> We were both a little nervous but everything turned out great. The dress fit her perfectly despite the fact she had put on a little weight.</p>
<p><strong>I wrote a to-do list.</strong> I still need to finish the hem of the dress, and then finish the shoulder straps, decorate them with the prepared lace motifs, decorate the neckline with rhinestones, and put push-up pads in the cups (something you always do for women with a small bust size). Actually, push-up pads were not compulsory in my client�s case because the cups did not look oversized. But she still requested this be done. And the last thing I need to do is add a small hook and eye closure at the top of the zipper for the sake of convenience.</p>
<p><strong>I planned to do all these things in any way so there is no surprise. </strong></p>
<p><strong>The bride put on the petticoat, the dress, and her shoes, and stood on a short plastic chair</strong> which I usually use as a platform. I trimmed the excess of mesh fabric along the hem.</p>
<p><strong>Then I put the petticoat and the dress on the dress-form, pulled it a little higher, and evened out the mesh fabric at the front. </strong>At the moment, I prefer not to reinforce the front of the hem with soft Rigilene boning because of these really beautiful folds naturally formed on the skirt.</p>
<p><strong>But I feel like drawing out the edge of the main skirt in the train area at the back of the dress.</strong> I did not like the way the train behaved when the bride moved around during the fitting. Since the mesh skirt is very soft, I have decided to reinforce the lining of the dress.</p>
<p><strong>I arrange the lining on the floor, take a disappearing ink pen, and draw the desired shape of the train and the hem between the side seams. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image002.jpg" alt=""></p>
<p><strong>Then I take the dress off the dress-form (I don't like cutting fabric when it is hanging).&nbsp; </strong></p>
<p><strong>First I will trim the hem of the dress along the marked line, then reinforce it with soft Rigilene boning, and then put the dress on the bride and make a final decision on the length of the dress in the area from the side to the middle of the front.&nbsp; </strong></p>
<p><strong>I have evened out the train, made the lining level with the floor,</strong> and heat-sealed the hem of the lining.</p>
<p><strong>Using my press iron, I give a piece of soft Rigilene boning the shape of a half circle so I can reinforce the train with it.</strong> It will be easy to sew it on this way.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image004.jpg" alt=""></p>
<p><strong>I start sewing the soft Rigilene boning to the back of the dress.</strong> I put the boning to the dress leaving a small gap toward the front from the side seam. I start sewing right along the bottom edge and then gradually bring the end of the boning upwards to the level of 1cm above it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image012.jpg" alt=""></p>
<p><strong>Then I fold the soft Rigilene boning to the inside, press the fold line from the face, secure the seam allowance with pins, and sew the Rigilene bone down along the other edge.&nbsp;&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image022.jpg" alt=""></p>
<p><strong>I need to finish the shoulder straps, too. </strong>I unpin the lace motif and trim away the angled part on the back half of the shoulder strap because the fitting proved it was not necessary. Then I put the shoulder strap layers together and pin along the edges. Now I just need to sew the shoulder straps with a 1.2cm seam allowance and glue the prepared lace motifs in place.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image026.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2038_files/image032.jpg" alt=""></p>
<p><strong>I add a small hook and eye closure above the zipper sewn in the central back seam.</strong> When my client starts putting her dress on, she will first fasten the hook and eye closure and then do up the zipper. Zipping up is a lot easier this way. And it creates additional support, too.</p>
