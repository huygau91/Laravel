<h1>Tutorial 41. Conclusion. Material Consumption. Pictures of the Finished Garment.</h1>
<p><strong>The dress is finished and I can summarize the results.&nbsp; </strong></p>
<p><strong>I will only speak about the consumption of fabric and other materials.</strong> There is no point in calculating the total cost of materials because we all live in different regions and countries with different currencies and different price policies.</p>
<p><strong>What concerns quantity, here is how much I spent:</strong></p>
<p>- 6.2m stretch satin for the face and the lining of the main dress;</p>
<p>- 5m hard mesh fabric for the base of the petticoat, the ruffles on the petticoat, and the ruffles on the train;</p>
<p>- 8.5m soft tulle for the ruffles on the petticoat;</p>
<p>- 16.5m euromesh for the mesh skirt (5 layers, 3.3m per layer);</p>
<p>- 14.2m narrow Rigilene boning (8.2m for the dress and 6m for the petticoat);</p>
<p>- 5.5m soft Rigilene boning for finishing the skirts and the petticoat;</p>
<p>- 0.2m stretch mesh fabric for the yoke of the petticoat;</p>
<p>- 0.6m elastic band for the waistband of the petticoat;</p>
<p>- One 30cm long zipper;</p>
<p>- Rhinestones and glue.</p>
<p><strong>These are all materials used in the dress.</strong></p>
<p><strong>These tutorials were also aimed at giving answers to a couple of important questions which many of you are concerned with as far as I could tell. </strong></p>
<p><strong>The first question is "How to create my own custom petticoat? What materials to use and where to buy those steel hoops?"</strong></p>
<p>I tried to answer it in great detail. It is up to you whether you want to create your own petticoat or use a ready-made one though!</p>
<p>In my opinion, there are cases when making your own petticoat from scratch is actually easier than customizing a cheap ready-made one.</p>
<p>I believe customizing a ready garment is always more difficult than sewing a new one.</p>
<p>What concerns those steel hoops, I take them out of cheap petticoats which I buy solely for this purpose.</p>
<p><strong>Another question</strong> you want to have an answer to deals with an issue I had never been faced with before. It took me a while to figure out how it could possibly arise in the first place. Some seamstresses complained that the bride's legs showed through the skirt of some or other dress they made which was similar to ours. After thinking it through, I realized that this happens because you put a rather full skirt on a petticoat without ruffles, i.e. with bare hoops.</p>
<p>I always put a considerable amount of ruffles on the petticoat when I sew dresses like this. In this case, you don't have to worry about the bride's legs showing through! There is no other way to solve this problem, even a thick lining won't help.</p>
<p>The only way to make a lightweight skirt from several layers of veiling non-see-through is to put it on a petticoat with full ruffles! Since I had never sewn such dresses with smooth petticoats, the problem was not familiar to me.</p>
<p><strong>There is yet another, third, question which was explained in detail in this course and I believe you will find it very useful: how to sew a mesh top for putting over the main dress.</strong></p>
<p>I have explained everything about calculating the pattern, cutting the top, joining it with the dress, adjusting it to the desired style, decorating it, etc. I am sure you have found answers to all related questions because I paid a lot of attention to the subject.</p>
<p>It is no accident that I focused on explaining how to sew a top like that: dresses with a matching mesh top overlay are super popular today! Girls on our forum often write about receiving orders on different variations of a dress with a tulle skirt of a varying degree of fullness and a tulle top with rhinestones, embroidery, or lace appliqu�. The top may have a boat neck or a turtle neck, it may feature short sleeves or shoulder straps (like in our case), and the design of the back may also vary: low back, closed, with a teardrop cut out, etc. The main point stays the same!</p>
<p>In addition, I have told you in what cases the top should be sewn with a double layer of fabric and in what cases a single layer is enough.</p>
<p>I guess that is all now. Thank you for staying with us through this rather challenging set of tutorials! I look forward to seeing pictures of your own works. You are always welcome at our online forum! Share your new achievements, ask your questions, and other users and I will be happy to answer them!</p>
<p>Wishing you friendly clients, high wages, good health, and lots of happiness!</p>
