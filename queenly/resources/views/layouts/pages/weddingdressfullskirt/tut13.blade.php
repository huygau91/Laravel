<h1>Tutorial 12. Sewing Ruffles on the Petticoat. Sewing the Yoke.</h1>
<p><strong>I would like to demonstrate how to sew ruffles on the petticoat. </strong></p>
<p><strong>The first bottom ruffle made of hard mesh fabric is already in place and overlaid with a bias tape. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image002.jpg" alt=""></p>
<p><strong>I put a soft tulle ruffle along the bottom row of stitches holding the tunnel for the third hoop.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image004.jpg" alt=""></strong></p>
<p><strong>I start from the central back seam and leave a seam allowance for joining the two ends of the ruffle.&nbsp; I start sewing from the vertical bone.</strong> I recommend you should trim all thread tails straight away.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image010.jpg" alt=""></p>
<p><strong>The edge of the ruffle needs to be finished with a bias tape.</strong> I fold in the end of the bias tape and sew it on getting right in the top row of stitches holding the tunnel on the inside (it shows through the fabric). Sew as neatly as you can to keep the tunnel on the inside wide enough for inserting a bone.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image022.jpg" alt=""></p>
<p><strong>I secure the bias tape with another row of stitches following the bottom row of stitches holding the tunnel. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image026.jpg" alt=""></p>
<p><strong>Here is what the bottom soft tulle ruffle looks like when finished with a bias tape: </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image028.jpg" alt=""></p>
<p><strong>Next I will sew the middle hard mesh ruffle on the petticoat following the marker stitches. </strong>And then I will put the long ruffle along the tunnel for the fourth hoop and overlay it with a bias tape.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image030.jpg" alt=""></p>
<p><strong>I have sewn on all ruffles. Here is what it looks like from the inside:&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image034.jpg" alt=""></p>
<p><strong>And here is the face of the petticoat: </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image036.jpg" alt=""></p>
<p><strong>I decided to overlay the top soft tulle ruffle with a plain white ribbon instead of a bias tape </strong>because I was afraid thick silver-coloured bias tape would show through the face fabric.</p>
<p><strong>I fold the top edge of the petticoat to the face side by 1.2cm and secure the resulting seam allowance with a row of strengthening stitches. </strong>These are necessary preparations before attaching the yoke.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image038.jpg" alt=""></p>
<p><strong>And now it is turn to work on the yoke itself. </strong></p>
<p><strong>It will be sewn with dense body control mesh fabric used for body-liners. </strong>A perfect choice!</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image040.jpg" alt=""></p>
<p><strong>I fold the mesh fabric in half and cut a strip with a length of 50cm and a width of 20cm.</strong> I draw a line perpendicular to the fold line thereby evening out the selvages, mark a 1.5cm seam allowance from this new line, and then mark 15cm (yoke length) and another 3.5cm (elastic band tunnel). I fold the edge to form the tunnel and press it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image046.jpg" alt=""></p>
<p><strong>I have already prepared a 3cm elastic band for the waist. Its length equals the waist circumference minus 10%. If my client's waist circumference is 66cm, then the elastic band should be 60cm long.</strong></p>
<p><strong>I will also need some narrow lingerie elastic for fastening loops.</strong> The yoke will be fastened by a pair of loops and buttons. One loop will be sewn to the elastic band and the other to the yoke, close to the joint with the petticoat. The petticoat will be secured safely around the wearer's waist.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image052.jpg" alt=""></p>
<p><strong>I sew the yoke using a flat lock sewing machine.</strong> If you don't have it, you can use the zigzag stitch on a regular household sewing machine or the plain straight stitch on an industrial sewing machine. The zigzag is more reliable, of course, because the thread may break under tension any time.</p>
<p><strong>First of all, I sew down the vertical edges of the yoke which were previously folded by 1.2cm and pressed, and then I do the same with the 3.5cm tunnel for the elastic band.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image056.jpg" alt=""></p>
<p><strong>There is a very neat seam on the other side.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image058.jpg" alt=""></p>
<p><strong>The opposite vertical edge of the yoke is sewn down in the same manner. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image060.jpg" alt=""></p>
<p><strong>I sew the tunnel. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2012_files/image064.jpg" alt=""></p>
