<p><strong>I put vertical bones on the back pieces. </strong></p>
<p><strong>This time I am using straight narrow Rigilene bones.</strong> I seal their ends with masking tape. I secure the bones with two parallel rows of stitches which start 5mm below the top row of stay-stitches and stop about 2mm above the bottom edge of the iron-on fabric.</p>
<p><strong>I put bone 16 on the central back piece, bone 15 along the princess seam, and bone 17 on the side back piece. </strong></p>
