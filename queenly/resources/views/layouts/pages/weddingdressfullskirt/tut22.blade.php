<p><strong>Next, I need to prepare the assembled lining for a fitting.</strong></p>
<p><strong>I set my sewing machine to the longest stitch length to temporarily sew the back central seam (from the notch which marks the end of the zipper down to the bottom) and the side seams. </strong></p>
<p><strong>The central back seam is sewn with a 2cm seam allowance. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image129.jpg" alt=""></p>
