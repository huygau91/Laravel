<p><strong>The same thing is done with the other back princess seam: I sew it and then topstitch it.&nbsp;&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image080.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image082.jpg" alt=""></strong></p>
