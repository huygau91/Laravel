<h1>Tutorial 21. Assembling the Back Half of the Lining. Reinforcing it with Bones.</h1>
<p><strong>I press the front of the dress and its vertical princess seams. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image002.jpg" alt=""></p>
<p><strong>All vertical bones need to be pressed flat and straight. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image008.jpg" alt=""></p>
<p><strong>Be very careful not to deform, stretch out, or distort the cups when you are pressing them. </strong>I press them on a cup pressing mould starting from the center. I flatten the bust princess seam and carefully smooth out the cup focusing in particular on the eased in areas.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image018.jpg" alt=""></p>
<p><strong>If you notice any tucks or creases, make sure to press them away. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image020.jpg" alt=""></p>
<p><strong>The cups hold their shape very well!</strong> The gathered bones become more flexible and protect them from possible deformation during pressing.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image022.jpg" alt=""></p>
<p><strong>I press open the princess seams of the dress. </strong>This type of work is best done on a sleeve ironing board.&nbsp; In this case, you have the seam before your eyes while the rest of the fabric is hanging down the sides of the board without getting in the way.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image024.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image026.jpg" alt=""></p>
<p><strong>I examine the face side. There should be no tucks or creases.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image028.jpg" alt=""></p>
<p><strong>The front half of the dress is ready.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image030.jpg" alt=""></p>
<p><strong>Now I need to cover the cups with padding polyester to prevent the bones from pushing through the face fabric.</strong> I will also add a pair of loops for putting the dress on a hanger.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image032.jpg" alt=""></p>
<p><strong>I take a thin layer of padding polyester and pin it on the first and then on the second cup. </strong>I stick all pins in with their points facing outwards and their heads facing the centre of the cup. Otherwise, the needle of the sewing machine may hit one of the heads and break.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image036.jpg" alt=""></p>
<p><strong>I trim away all excess padding polyester.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image040.jpg" alt=""></p>
<p><strong>And I stitch the padding on: </strong>I sew a row of stitches along the inner edge of the top perimeter bone of the cup and another row along the outer edge of the bottom perimeter bone of the cup. In this case, the seam allowance at the top of the cup will be neat and thin after I trim away all excess padding. At the same time, the bottom perimeter bone will stay covered up.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image042.jpg" alt=""></p>
<p><strong>I start sewing the padding on the cups. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image044.jpg" alt=""></p>
<p><strong>Do not forget to put hanging loops at the armscyes.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image048.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image054.jpg" alt=""></p>
<p><strong>I remove the pins and trim away all excess padding.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image060.jpg" alt=""></p>
<p><strong>I make additional bar tacks on the loops to prevent their ends from sticking up when the dress is put on a hanger. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image062.jpg" alt=""></strong></p>
<p><strong>The front half of the lining is fully finished! </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image064.jpg" alt=""></p>
<p><strong>It is time to work on the back half of the lining. </strong></p>
<p><strong>I sew the vertical princess seams with a 1.2cm seam allowance.</strong> I have serged the edges in advance.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image066.jpg" alt=""></p>
<p><strong>I carefully align the princess seam edges of the pieces whenever I pass from duplicated to non-duplicated fabric. </strong>I spread out the entire princess seam and keep aligning the edges section after section as I sew. This allows me to achieve a neat even princess seam.</p>
<p>I always do this with long princess seams regardless of whether on a skirt or a dress, face fabric or lining fabric. This compensates for the stretching out of fabric cut on the bias.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image068.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image070.jpg" alt=""></p>
