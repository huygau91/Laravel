<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image084.jpg" alt=""></strong></p>
<p><strong>You don't have to trim the back princess seams lengthwise but you should definitely trim their ends at an angle. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image086.jpg" alt=""></p>
<p>Before reinforcing the back pieces with bones, <strong>I usually sew a row of stay-stitches along the top edge </strong>to mark their supposed top border.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2021_files/image088.jpg" alt=""></p>
