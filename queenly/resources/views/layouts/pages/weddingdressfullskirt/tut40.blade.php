<h2>Tutorial 39. Embellishing the Dress with Rhinestones.</h2>
<p><strong>In this tutorial, I will decorate the front of the dress with rhinestones. </strong></p>
<p>Since the bodice is covered in lace, <strong>I will use large sew-on rhinestones in metal settings</strong> for the main highlights. They will stand out better against that background.&nbsp; <strong>&nbsp;</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image002.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image004.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image008.jpg" alt=""></strong></p>
<p>The rhinestone itself is held in the setting by prongs. There are two sew-on holes from either side of the mount.</p>
<p><strong>I will sew these rhinestones on with a long thin beading needle.</strong> It is a special purpose 8-9cm long flexible needle. It is very hard to sew such rhinestones on with a plain sewing needle.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image010.jpg" alt=""></p>
<p><strong>I start from the largest rhinestones: I arrange them on the dress and secure them with tiny drops of G�termann glue.</strong> I want to make sure the rhinestones will not shift when I start sewing them on. This is particularly important when you want to create a neat geometric pattern.</p>
<p><strong>I want to form a kind of a necklace with my rhinestones.</strong></p>
<p><strong>I will use SS 20 Swarovski crystals and SS 16 Preciosa crystals as bridges between the main large rhinestones in metal settings.</strong> They produce crystals of very good quality at Preciosa. I will form strings of crystals between the large metal setting rhinestones.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image014.jpg" alt=""></p>
<p><strong>You must choose rhinestones of very high quality when you plan to use relatively few of them</strong> because the overall geometry may easily get destroyed even by a single lost rhinestone. Quality requirements are not as strict when you want to create a scattered pattern.</p>
<p><strong>I have glued the large rhinestones in place and now I can start sewing them on.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image016.jpg" alt=""></p>
<p><strong>There is nothing difficult about it.&nbsp; </strong></p>
<p><strong>I bring the beading needle up near the hole in the mount of the rhinestone and pull it all the way through the mount taking it out from the opposite hole. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image026.jpg" alt=""></strong></p>
<p><strong>Then I bring the needle down in the fabric, bring it back up near the same first hole, and then through the same holes in the mount of the rhinestone. </strong>&nbsp;To make it easier, you can only catch the top layer of the fabric without piercing it through.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image038.jpg" alt=""></p>
<p><strong>After 3-4 stitches, I do the same with the other pair of holes in the mount.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image044.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image046.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image048.jpg" alt=""></strong></p>
<p><strong>The rhinestone is sewn on. I make a bar-tack and move to the next one without tearing the thread. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image054.jpg" alt=""></p>
<p><strong>I have sewn on all rhinestones. </strong>The dress is smaller than the dress-form so I simply pinned it to the surface. I need to have a proper view of the geometry of the pattern, and this would be hard without the dress-form.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image056.jpg" alt=""></p>
<p><strong>Now I take some SS 16 and SS 20 crystals and E6000 glue. </strong>You could also use Moment or G�termann glue.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image058.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image060.jpg" alt=""></p>
<p><strong>The best tool for applying glue on the dress is a 10ml medical syringe</strong> without a rubber stopper on the plunger. Moment and G�termann glues are more liquid. I recommend putting a thick medical needle with a bevelled end on the syringe for applying them.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2039_files/image062.jpg" alt=""></p>
<p><strong>I use a diamond mosaic tool for picking up rhinestones and gluing them in place. </strong>It comes with a block of glue and basically resembles a hollow pen refill only with no ball inside. You can buy it at any online DIY store.&nbsp; After applying a little glue on the tip of the pen, you can easily pick up a rhinestone and put it where desired.</p>
