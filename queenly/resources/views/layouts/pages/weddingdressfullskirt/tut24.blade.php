<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image074.jpg" alt=""></strong></p>
<p><strong>I make a row of stitches along the other edge of the bone.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image076.jpg" alt=""></p>
<p><strong>The entire neckline of the dress is now reinforced with Rigilene boning. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image078.jpg" alt=""></p>
<p><strong>And now it is time to add the grosgrain ribbon. </strong></p>
<p><strong>I cut a length of 66cm (client's Waist Circumference) and mark its middle.&nbsp; </strong></p>
<p><strong>I position the ribbon just above the waistline, align its middle with the middle of the dress, and pin it down right there.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image080.jpg" alt=""></p>
<p><strong>Next, I pin the ribbon to the halves of the back, re-distribute the fabric, and pin it on along the full length. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image082.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image084.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image086.jpg" alt=""></p>
