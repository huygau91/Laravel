<h1>Tutorial 23. Putting the Last Bones on the Lining of the Dress.</h1>
<p><strong>I can add some final touches to the lining after examining it on the dress-form. </strong></p>
<p><strong>I will, first of all, sew the side seams properly and then press them and put bones along them.</strong> I could have topstitched the side seams instead of pressing them open but I prefer to press them in case I need to apply changes and re-sew them later.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image004.jpg" alt=""></p>
<p><strong>I sew the first side seam.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image014.jpg" alt=""></p>
<p><strong>And I sew the other side seam.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image022.jpg" alt=""></p>
<p><strong>Both side seams have been pressed now.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image024.jpg" alt=""></p>
<p><strong>I will reinforce them with narrow straight Rigilene bones (wide bones would also do).</strong> The bone is sewn on with two parallel rows of stitches and its ends are sealed with masking tape. It starts 0.5cm below the top row of stay-stitches and stops about 2mm above the bottom edge of the iron-on fabric.</p>
<p><strong>I need to restore the row of stay-stitches on the face of the garment before sewing on the bone.</strong> First of all, I mark the position of the row with a pin. The pin should be visible from the inside.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image026.jpg" alt=""></p>
<p><strong>I sew the bone on.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image034.jpg" alt=""></p>
<p><strong>And I put a symmetric bone along the other side seam after marking the row of stay-stitches with a pin. </strong>This time I sew from down to up (from the edge of the iron-on fabric).</p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image036.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image038.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image040.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image042.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image044.jpg" alt=""></strong></p>
<p><strong>I move to the top edges of the lining. </strong></p>
<p><strong>During the fitting, I measured the expected length of the top edge on one half of the lining. It was 35cm. </strong></p>
<p><strong>I measure the actual length of the top edge </strong>from the zipper to the top corner of the neckline (the intersection of stay-stitches). <strong>It is 35.5cm. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image046.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image048.jpg" alt=""></p>
<p><strong>The lengths differ by just 0.5cm. </strong></p>
<p>What I will do is take a Rigilene bone with a length of exactly 35cm and put it along the neckline with a little easing in to fit it properly.</p>
<p><strong>I take a straight narrow Rigilene bone with a length of exactly 35cm and put it first along one half of the neckline and then along the other half of it. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image050.jpg" alt=""></strong></p>
<p><strong>I start sewing from the top corner of the cup.</strong> I move along the neckline in the direction of the armscye and distribute the fabric so as to make the opposite end of the bone arrive right at the zipper line (the central back edge without the seam allowance). I trim the bone after passing the vertical bone on the central back piece. The neckline is now reinforced along the full length (which is 35cm).</p>
<p><strong>The bone needs to be secured with two parallel rows of stitches and its ends sealed with masking tape.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image052.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image054.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image056.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image058.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image060.jpg" alt=""></strong></p>
<p><strong>I sew a row of stitches along the other edge of the bone.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image062.jpg" alt=""></p>
<p><strong>Sewing along the other half of the neckline is a little harder technically.</strong></p>
<p><strong>This time I have to start from the armscye.</strong> I try to keep the ends of the bone aligned with the desired sewing start and end points. I mark the sewing end point along the neckline and mark a line on the bone to specify where to trim it.</p>
<p><strong>Next, I align the opposite end of the bone (sealed with masking tape) with the point marked along the neckline and start sewing it on. I ease in the fabric as I sew so as to get the mark on the bone aligned with the top corner of the cup. </strong></p>
<p><strong><br>
