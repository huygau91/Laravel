<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2023_files/image090.jpg" alt=""></p>
<p><strong>I secure the ribbon with two rows of stitches: one along the top and another along the bottom edge. </strong></p>
<p>I make a bar tack as I reach the seam allowance of the side seam, continue sewing, and make another bar tack after passing the side seam and its seam allowance. I continue sewing until I reach the other side seam, make a bar tack before and after the seam allowance (just like before), and continue sewing until the ribbon ends. The bottom row of stitches is sewn just the same way, i.e. with bar tacks before and after the side seams. I also make bar tacks at the beginning and at the end of the sewing.</p>
<p><strong>With the grosgrain ribbon sewn on this way,</strong> I can always quickly rip up the stitches between the bar tacks near the side seams if I have to adjust the side seams in the waist area. The ribbon itself will stay secured in place. And I can always restore the stitches between the bar tacks made before and after the side seams after applying necessary changes.</p>
<p><strong>One row of stitches is made along the top edge of the ribbon: </strong></p>
