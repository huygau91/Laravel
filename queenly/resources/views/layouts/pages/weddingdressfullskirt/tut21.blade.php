<p><strong>Next, I need to put a perimeter bone along the bottom border of the cup.</strong> I step 5mm from the stay-stitching at the top of the cup, put a gathered narrow Rigilene bone along the edge of the hard iron-on fabric, and secure it with a single row of stitches along the outer edge. The curved areas are supposed to be eased-in, too. The ends of bones are sealed with masking tape as always.</p>
