<p><strong>The perimeter bones are all in place now. It is time to add the rest. </strong>I position narrow gathered Rigilene bones as marked and secure them with two parallel rows of stitches (the numbering is the same as before). The ends of these inner bones are to be pushed under the perimeter bones attached along their outer edge. Remember to make bar tacks!<strong> And never ease in when sewing bones on the inside of the cup! </strong></p>
<p><strong>I sew on the two halves of bone 1 which passes along the bust line. </strong></p>
