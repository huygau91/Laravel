<p><strong>The first bones will be put along the perimeter of the cups. </strong></p>
<p><strong>The bones that pass along the curved areas need to be gathered in a half-circle shape.</strong> First I gather the bone lightly by pulling on its fishing lines and then I give it the final shape by pulling on just the outermost fishing line. I trim the ends of fishing lines.</p>
<p><strong>I start by putting a narrow gathered Rigilene bone along the top border of the cup.</strong> The bone is positioned about 1mm from the stay-stitching.</p>
<p><strong>I slightly ease in the fabric in the curved areas of the cup. </strong>The degree of ease-in is always determined based on experience. You should be able to feel how much you need to ease in to ensure a good fit. There is one simple rule though: larger breasts imply less easing in, and vice versa. The ends of all bones are to be sealed with bits of masking tape.</p>
<p><strong>I put the bone about 1.5cm from the armscye edge and secure it with a single row of stitches along the outer edge.</strong> It passes along the neckline and stops under the cup, 2mm above the edge of the hard iron-on fabric.</p>
