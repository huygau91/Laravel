<h1>Tutorial 20. Putting Bones on the Front Half of the Dress.</h1>
<p><strong>I am often asked about the total length of boning required for the bodice of some or other dress. </strong>The front half of the bodice usually requires about 600cm. You can calculate it more precisely by measuring all marked guidelines. I already know that I will use 600cm.</p>
<p><strong>But, first, I need to make a row of stay-stitches along the edges of the side front pieces (1.2cm from the edge). </strong>I have already sewn a row of stay-stitches along the neckline of the central front piece.</p>
