<h1>Tutorial 11. Cutting the Ruffles and the Yoke.</h1>
<p><strong>I have made some adjustments in the cutting.&nbsp; </strong></p>
<p><strong>Soft tulle ruffles: </strong></p>
<p>When cutting the soft tulle, I realized it was way too soft and the calculated number of strips would not create the desired fullness. I had to cut some additional strips.</p>
<p><strong>Top Ruffle (1) with a width of 84cm: </strong></p>
<p>I cut two additional strips, i.e. a total of four.</p>
<p><strong>Middle Ruffle (2) with a width of 64cm:</strong></p>
<p>I cut two additional strips, i.e. a total of five.</p>
<p><strong>Bottom Ruffle (3) with a width of 44cm: </strong></p>
<p>I left the number of strips unchanged (four). The two hard mesh ruffles already sewn on along the hem of the petticoat will produce enough fullness.</p>
<p><strong>As you can see, the soft tulle consumption has changed: </strong></p>
<p><strong>84cm × 4 + 64cm × 5 + 44cm × 4 = 336cm + 320cm + 176cm = 832cm ≈ 850cm. </strong></p>
<p><strong>I adjust the calculated values on the writing board. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image002.jpg" alt=""></strong></p>
<p><strong>Hard mesh (hard tulle) ruffles:</strong></p>
<p>I have in stock some hard mesh fabric with a width of 250cm and not 180cm. It is also softer than the mesh fabric used for the bottom ruffles. As the result, <strong>the number of the strips has stayed the same but their length has changed.</strong> The increased length will allow me to gather the ruffles more thickly thereby compensating for their softer texture. The hard mesh fabric consumption stays the same.</p>
<p><strong>Now I only need to cut the yoke of the petticoat. </strong></p>
<p><strong>The finished yoke is supposed to have a half circumference of 50cm and a width of 15cm, 3.5cm of which falls upon a tunnel at the top for inserting an elastic band. </strong></p>
<p>Sewing the yoke involves cutting a piece of knit fabric. <strong>The piece is supposed to have a length of 50cm</strong> when folded in half (I included seam allowances along the vertical edges when applying adjustments).</p>
<p>I will add 3.5cm for the top tunnel and 1.5cm for the bottom seam allowance.</p>
<p><strong>Total yoke width:</strong></p>
<p><strong>15cm + 3.5cm + 1.5cm = 20cm. </strong></p>
<p><strong>Let us look at the ruffles.&nbsp; </strong></p>
<p><strong>I have cut my soft and hard tulle in accordance with the calculations.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image004.jpg" alt=""></p>
<p><strong>All strips of the same width need to be joined in a single very long strip. It is best to keep all seam allowance on the same side.&nbsp; </strong></p>
<p><strong>Forming soft tulle ruffles.</strong></p>
<p>After sewing all tulle strips together, I make a row of stitches along one edge and gather the fabric on the thread. Of course, it is not exactly easy to gather a several meters long strip down to a relatively short ruffle.</p>
<p><strong>But I will have a great tip for you.</strong> After making a row of stitches along one edge of the strip, I fold it in half, secure the fold line, and start carefully gathering it while alternating between the two halves.&nbsp; I gather each half down to half the initial length.</p>
<p><strong>For better clarity, let me illustrate with pictures the gathering of a ruffle with a width of 84cm. </strong></p>
<p>Initial length of the strip:</p>
<p><strong>300cm × 4 = 1200cm.</strong></p>
<p><strong>This strip should be gathered down to 120cm.</strong></p>
<p><strong>I fold it in half, secure the fold line on the dress-form, and walk six meters away from it pulling both ends of the strip to me.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image006.jpg" alt=""></p>
<p><strong>I alternate between the two ends gathering one after the other and gradually approaching the dress-form. Each half needs to be gathered down to 60cm.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image008.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image010.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image012.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image014.jpg" alt=""></strong></p>
<p><strong>It is a great method: it allows you to gather fabric quite fast and yet very evenly and neatly.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image016.jpg" alt=""></strong></p>
<p><strong>The hard mesh strips are gathered with the help of a ruffler presser foot. </strong></p>
<p><strong>I sew the ready ruffles to the petticoat. </strong>By this time, I have already sewn on the bottom ruffles and finished the hoop tunnels with bias tape from both sides.</p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image020.jpg" alt=""></strong></p>
<p><strong>The guidelines for sewing on the hard mesh ruffles are marked by stitches. I sew them on one by one starting from the bottom. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image022.jpg" alt=""></p>
<p><strong>I sew on the first bottom ruffle</strong> leaving 2cm seam allowance in the central back seam area. I will join the ends after sewing the central seam.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2011_files/image024.jpg" alt=""></p>

