<h1>Tutorial 37. Decorating the Dress with Lace Appliqu�.</h1>
<p><strong>All lace motifs are already pinned on the dress and I am ready to start gluing them on.</strong> You can sew them on by hand if this is something you are more used to, as long as you have enough time. But even in that case I highly recommend you secure the motif in place with a couple of glue drops before sewing it on. As for me, I always prefer using G�termann fabric glue over spending time on sewing.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image008.jpg" alt=""></p>
<p><strong>When I start gluing lace motifs on the skirt, I place a ruler wrapped in packaging film under the applied motif to prevent the glue from soaking through onto other layers of the skirt. </strong>The motif sticks to the packaging film, I take out the ruler, wrap it in a new piece of film, and glue on the next motif. The packaging film can be removed in 5-10 minutes after the glue dries.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image010.jpg" alt=""></p>
<p><strong>I start gluing the motifs on the dress.</strong> First I glue them on the areas that are easy to access. And then I lower or raise the dress-form to work on other areas.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image016.jpg" alt=""></p>
<p><strong>Be careful to glue the motifs on in the right order: first the bottom ones and then the overlaying ones. </strong>This work does not take as long as it may seem but it is rather tedious indeed. You can always do it to your favourite music or a good film though.</p>
<p><strong>This area is finished and I move to the next one. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image026.jpg" alt=""></strong></p>
<p><strong>Remember to carefully check whether all small elements hold well enough after gluing on all motifs.</strong> When I find a partly detached motif, I glue it on properly and press it down with a pin.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image028.jpg" alt=""></p>
<p><strong>Here is the result of my decoration work: </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image032.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image036.jpg" alt=""></p>
<p><strong>I will leave it at that for now in case I need to adjust the dress after the fitting.</strong> The shoulder seams are still undone and the shoulder straps remain undecorated for now. I have already prepared the motifs though.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2037_files/image038.jpg" alt=""></p>
<p><strong>I am quite happy with the result!</strong></p>
<p><strong>I have used five absolutely different types of lace fabrics which I bought at different times of my career.</strong> All of them are soutache but this is about the only thing they have in common. Some motifs have thinner and others denser mesh foundation. Some are embellished with seed and bugle beads, others - with seed beads, sequins, and pearls, and still others are simply smooth. The smooth motifs were used for the front neckline and the edges of the back because I will decorate them with rhinestones.</p>
<p>It looks as if it took a lot of complicated work to decorate the dress: lots of beautiful rich lace with smooth transitions from one type to another.</p>
<p><strong>It is a classic European style dress</strong> and I would be glad to leave it as is - very delicate - but my client really wants those rhinestones.</p>
<p><strong>I will leave the dress like this until my bride arrives for the final fitting. </strong></p>

