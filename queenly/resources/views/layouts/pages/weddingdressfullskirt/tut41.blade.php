<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image014.jpg" class="img-responsive center-block"></p>
<p><strong>I sew the plastic on from the other side. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image016.jpg" class="img-responsive center-block"></p>
