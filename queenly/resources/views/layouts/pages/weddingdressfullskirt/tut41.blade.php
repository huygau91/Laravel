<h2>Tutorial 40. Adding an �Expander" to the Petticoat. </h2>
<p></p>
<p><strong>I had already prepared my words of farewell to tell you that the dress was finished when the circumstances suddenly changed and I realized the work was not over yet!</strong></p>
<p><strong>Here is what happened. </strong>It popped up in a conversation with the bride and the groom that their wedding was to take place right on the seashore and not indoors. In other words, the bride will walk "down the aisle" barefooted on blazing sand and in really bright open sunlight. </p>
<p><strong>Imagine my surprise! While it sounds like an exotic adventure to the lovebirds, it is far from good news for me. </strong></p>
<p><strong>It took me so much work to make the skirt length perfect down to the millimeter!</strong> And, of course, I accounted for the length of her shoe heels in belief that my girl will walk down a runner carpet or some kind of solid flooring. I did everything to make sure the skirt would flow freely in wear without obstructing her movements. </p>
<p>Even though I accounted for relatively low heels, if the bride walks barefooted, the skirt as it is now will be as much as 5cm too long. And considering that her feet will be sinking in the sand, there must be an excess of at least 10cm.</p>
<p><strong>The most terrible thing is that I cannot make the skirt shorter now. And I ought not either!</strong></p>
<p><strong>I cannot make it shorter</strong> because the petticoat has already been finished and the entire construction was designed to match a certain length. The only thing I can do is trim away part of the hard mesh fabric at the front of the petticoat because its bottom hoop is raised 20cm at the front. At least, in this case the skirt will not get deformed when the bride's legs sink in the sand. </p>
<p><strong>As to why I ought not shorten the skirt:</strong> alongside the seashore and other exotic places, the ceremony includes a photo shoot and a festive celebration in a castle where the bride will naturally wear shoes. </p>
<p><strong>The only solution I can think of is to add a removable hard expander to the petticoat</strong> which will keep the skirt spread out even when it is longer than needed. Of course, this means no more talk of the bride's being light on her feet on the sand but at least she will not get tangled in those extremely long skirts. </p>
<p><strong>The expander will push the skirt forward</strong> giving the bride no time to step on it even if the hem curls in. </p>
<p><strong>And this expander must be made as soon as possible.</strong> I will have to sew it to the petticoat right on the spot because the newlyweds took the dress away with them. </p>
<p>I will bring it to the registration ceremony in my suitcase. </p>
<p><strong>Let us cut the expander now.&nbsp; </strong></p>
<p><strong>I cut a strip of mesh fabric typically used in see-through corsets. The strip is 30 by 130 cm.</strong> The length is supposed to equal the circumference of the bottom but one hoop of the petticoat to which the expander will be sewn. The circumference of the bottom but one hoop is about 127cm and I have added another 3cm for finishing the ends of the expander. </p>
<p><strong>I draw a line dividing the strip in half lengthwise. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The expander will be made of a curtain plastic strip with a width of 10cm.</strong> This plastic strip is usually sewn to the top edge of the curtain for the sake of forming folds. It holds its shape very well, it is stiffer than soft Rigilene and yet more flexible than regular Rigilene bones. You can sew with curtain plastic on a sewing machine.</p>
<p><strong>I take a 130cm long strip. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image004.jpg" class="img-responsive center-block"></p>
<p><strong>I have also prepared 3 strips of the same mesh fabric which was used for the ruffles on the petticoat.</strong> I will gather them very thickly, sew them to the expander, and finish the bottom edge with a white bias tape. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image006.jpg" class="img-responsive center-block"></p>
<p><strong>Such removable expanders are actually used quite broadly in wedding fashion and not only for the same purpose as mine.</strong> They allow you to slightly lengthen the skirt or experiment with the length of the petticoat when it has a certain standard length. A removable expander piece will provide a great solution to many out-of-line situations for someone who makes wedding dresses for rent. For example, you can always extend the petticoat or the train using such additional expanders if the bride happens to be taller than average.&nbsp; </p>
<p><strong>Back to work. I sew the strip of curtain plastic to the strip of mesh fabric. </strong>I put the mesh fabric over the plastic to avoid tucks and sew them together neatly. I secure the plastic in place with two rows of stitches along the edges. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image008.jpg" class="img-responsive center-block"></p>
