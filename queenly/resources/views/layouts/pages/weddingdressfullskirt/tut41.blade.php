<p><strong>I fold the seam allowances neatly to prevent them from getting all wrinkled and carefully turn out the first corner and then the opposite one.</strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2040_files/image026.jpg" class="img-responsive center-block"></p>
