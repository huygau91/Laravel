<h1>Tutorial 13. Assembling the Petticoat.</h1>
<p><strong>I am happy to tell you that the petticoat is ready! All I need to do is insert the steel hoops.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image002.jpg" alt=""></p>
<p><strong>I have sewn the central back seam and topstitched it from the face.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image004.jpg" alt=""></strong></p>
<p>When I turn the petticoat right side out, the ends of the ruffles at the back are still detached.</p>
<p><strong>This is how I solve this problem:</strong> I lift one ruffle after another starting from one at the top, open them, align the two ends of the ruffle along the central back edge, and join them by machine moving from down to up.</p>
<p><strong>I lift up the top soft tulle ruffle with a length of 84cm. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image006.jpg" alt=""></strong></p>
<p><strong>I align the ends of the ruffle and sew them together.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image008.jpg" alt=""></p>
<p><strong>The same thing is then done with the rest of the ruffles. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image012.jpg" alt=""></p>
<p><strong>Then I insert an elastic band in the tunnel and sew the buttons in place.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image016.jpg" alt=""></p>
<p><strong>I put all hoops in the tunnels and put the petticoat on the dress-form. </strong>The three top hoops were taken out of the ready-made petticoat and I chose a matching bottom hoop from the supplies I had in stock.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image018.jpg" alt=""></p>
<p><strong>It is clear in the picture that the front and the back of the petticoat are not of the same shape. </strong>You can see the additional ruffles at the back.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image020.jpg" alt=""></p>
<p><strong>It is still a question </strong>whether I need to add another layer of tulle at the back of the yoke to disguise the difference in shape. I believe there is no need in it but I will make the final decision later.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image022.jpg" alt=""></p>
<p><strong>Now that I have achieved the desired shape of the petticoat, I can start calculating and cutting the skirt of the main dress. </strong></p>
<p>I have put the petticoat on the table just for you to be able to see it properly. <strong>In reality, you should take all measurements with the petticoat standing on the floor. </strong></p>
<p><strong>I will draft it directly on the fabric.&nbsp; </strong></p>
<p><strong>All I need are precisely measured values of all lengths and circumferences. </strong></p>
<p><strong>All measurements will be taken from the waistline. </strong>I will decide where the flare should begin a little later.</p>
<p><strong>When taking measurements, let the measuring tape lie freely on the petticoat without flattening it.</strong> Of course, the soft tulle will be slightly flattened by the heavy face fabric of the skirt. It is fine as long as the skirt is made a little looser than the petticoat. I don't want the skirt to look as though it were stuffed with wadding.</p>
<p><strong>Let us take all necessary measurements for cutting the skirt. </strong></p>
<p><strong>I measure the length of the skirt along the side of the petticoat: from the waist to the floor. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image024.jpg" alt=""></strong></p>
<p><strong>Then I measure its length along the centre of the back (again, from the waist to the floor).</strong> It is too early to add the train yet.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image026.jpg" alt=""></p>
<p><strong>Do not forget to measure the length of the skirt along the back princess seam the same way. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image028.jpg" alt=""></p>
<p><strong>The same measurement is to be taken along the centre of the front and along the front princess seam. Same as always: from the waist to the floor. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image030.jpg" alt=""></strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image032.jpg" alt=""></p>
<p><strong>I also need to know all circumferences. </strong></p>
<p><strong>I find the first half circumference by measuring the distance between the side seams of the petticoat at the floor level.</strong> The dress-form is supposed to be standing on the floor. The measuring tape is also supposed to glide along the floor along the perimeter of the hem.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image034.jpg" alt=""></p>
<p><strong>I find the half circumference of the back of the skirt by measuring the distance between the side seams of the petticoat at the floor level.</strong> I don't account for the train at this point.</p>
<p><strong>The same half-circumference measurements need to be taken at half the length of the skirt from waist to floor. </strong>This will help me calculate the width of each gore of the skirt.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2013_files/image036.jpg" alt=""></p>
<p><strong>Finally, I need to decide where the flare should begin.</strong> In most cases, it looks beautiful when the flare starts 10-12cm below the waist and the area above it hugs the natural body curves.</p>
<p><strong>My skirt will start flaring 10cm below the waist.&nbsp; </strong></p>
<p><strong>In conclusion, I would like to highlight that you must always start from the petticoat when working on a wedding dress!</strong> Do not repeat mistakes made by others! Do not try to pick a matching petticoat for an already finished dress!</p>
<p><strong>You must first construct a petticoat, then bring its shape to perfection, and only then cut the dress itself.&nbsp; </strong></p>

