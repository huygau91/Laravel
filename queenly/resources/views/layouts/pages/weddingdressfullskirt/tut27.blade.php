<h1>Tutorial 26. Joining the Face with the Lining.</h1>
<p><strong>It is finally time to join the face with the lining. </strong></p>
<p><strong>This stage may bring about certain challenges </strong>because the lining is now a little different in size and shape compared to the face. This happened because I made a dart in the middle of the under-bust line, eased in the cups to make them rounder, and eased in the back neckline as well.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image004.jpg" alt=""></p>
<p><strong>These adjustments caused the size and the shape of the lining to change. </strong></p>
<p><strong>But I still need to join it with the face somehow. </strong></p>
<p><strong>You can find various methods of joining the face with the lining in my tutorials.</strong> You can always re-distribute excess face fabric and let it out at the top or down the bottom in short garments like corsets to compensate for changes to the lining. Things are more complicated when you are sewing a dress though.</p>
<p><strong>In any case, the first thing to do is insert the lining in the face the way it is supposed to be when the garment is finished.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image006.jpg" alt=""></p>
<p><strong>The best way to understand how to distribute the fabric correctly is to put the lining and the face on the dress-form. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image008.jpg" alt=""></strong></p>
<p>I pull the face fabric upwards and let it out at the top just a little to account for the under-bust dart on the lining, i.e. I don't try to align the edges of the face and the lining too precisely. Be very careful not to allow tucks or creases anywhere. <strong>The major joining work starts right now. </strong></p>
<p>Unlike in most cases, the princess seams of the face and the lining need to be a little misaligned now <strong>to avoid excess thickness along the neckline (and particularly at the princess seams). </strong>It is all the more important because the princess seams of the lining are duplicated with iron-on fabric. The cups were not eased in on the face as they were on the lining, which makes them larger and their princess seams naturally settle closer to the armscyes.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image012.jpg" alt=""></p>
<p><strong>After carefully distributing the face fabric over the lining fabric, I start pinning the pieces together with respect to all adjustments made to the lining.</strong></p>
<p>The result of this work largely depends on your qualification and experience, as well as your knowledge of fabric properties.</p>
<p><strong>Experienced seamstresses </strong>put the face on the lining, pin them together, take the garment off the dress-form, trim the edges of the face after the edges of the lining, and then put the face and the lining face to face, and sew them together. This results in a clean finish with seams hidden inside.</p>
<p><strong>But I want to show you an easier method. It is suitable for beginners, and experienced seamstresses can choose their own way of joining the face with the lining. </strong>My method is somewhat non-conventional but it will make things a lot easier for you and the finished garment will look totally professional made! I can promise that you will be happy with the result!</p>
<p><strong>I pull up and spread out the face fabric as neatly as I can, put it on the lining, and pin it down. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image014.jpg" alt=""></p>
<p><strong>Then I take the pinned together garment off the dress-form, turn it around, and examine the results. </strong>&nbsp;The face fabric is supposed to go beyond the edges of the lining fabric by the same width all around. The seam allowances came out different on the cups and I had to re-position a couple of pins to even them out.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image018.jpg" alt=""></p>
<p><strong>The side seams, which were shifted toward the front near the armscyes on the lining, have naturally taken the desired position on the face. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image020.jpg" alt=""></strong></p>
<p><strong>At this point, experienced seamstresses </strong>can polish up the shape of the dress, mark control aligning points along the pinned area with a disappearing ink pen, remove the pins, put the two parts of the dress together face to face, and clean finish it.</p>
<p><strong>But I will do something much simpler: </strong>I will sew a row of stitches along the pinned area at a presser foot width from the row of stay-stitches, fold the edge to the inside, and finish the seam allowance with a lace ribbon or a bias tape.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2026_files/image022.jpg" alt=""></p>

