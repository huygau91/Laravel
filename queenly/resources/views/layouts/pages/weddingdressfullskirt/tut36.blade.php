<p><strong>And now let me explain why I decided to sew this top with two layers of mesh fabric. </strong></p>
<p><strong>The first reason was the chosen style of shoulder straps and the low back.</strong> There are many bias edges on the top which tend to stretch out and need to be secured. In theory, you could secure the shape of the shoulder straps on a single layer of fabric by folding the seam allowance to the face and sewing it down (the way I am doing now). But the thing is that my top also features open armscyes. There are not supposed to be any appliqu� elements along the armscyes and they need to have a clean finish.</p>
<p><strong>Working long distance is another reason why I decided to sew the top with two layers of mesh fabric.</strong> I needed to stick to the right dimensions. I could not risk working with stretch fabric without securing the shoulder straps properly. I would have to sew silicone elastic bands to the shoulder straps to preserve their size on a single layer of lightweight mesh fabric.</p>
<p><strong>I would not hesitate to sew a single layer mesh top (even for a long distance client)</strong> if it featured sleeves, at least short ones, or if it were not as open.</p>
<p><strong>After securing the top on the dress, I move to the mesh skirt. </strong></p>
<p><strong>I will put all five layers together, one inside another, and then sew them together by machine along the top edge, and gather it down to 86cm.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2035_files/image032.jpg" alt=""></strong></p>
<p><strong>How not to get tangled in those large skirts but put them together in a quick and accurate way? I will tell you.</strong></p>
<p><strong>I have a stack of five pinned together skirt layers. </strong>I remove the pins which secure the layers along the bottom edge but keep the pins at the top. I made regular notches, as well as a V-notch, along the top edge when I was cutting the skirt.</p>
<p><strong>And here is what I do now.</strong> I put the stack on the table and remove the pins from the top layer. Then I stretch the top edge from notch to notch, gather the top layer along the full length holding the ends of the edge in my hands, and push the gathered layer inside the second layer of the skirt. I align the notches and spread out the first layer of mesh fabric inside the second layer.</p>
<p><strong>Then I do the same thing, only this time with two layers simultaneously (the first and the second).</strong> I gather them in a thin strip and put it inside the third layer. I align the notches as before and spread out the two layers inside the third layer.</p>
<p><strong>One by one, I have put all layers together in a five-layer skirt. </strong></p>
<p><strong>And now, with all five layers of mesh fabric in my hands, I align and pin them together at the V-notch. </strong>Then I find the next notch, align all five layers, and secure them with another pin. It is a lot easier to align the top edges in an area between two pins. I can align and pin the top edges together rather quickly moving along in this manner.</p>
<p><strong>This is not exactly easy work, of course, but it does not take that long! </strong></p>
<p><strong>I strongly warn you against taking all those layers apart and then trying to insert one in another!</strong> They will definitely get all tangled up and you will get annoyed and promise yourself to never sew mesh skirts again! I highly recommend sticking to my method instead!</p>
<p><strong>I have assembled all layers in a single skirt and secured the top edge with two rows of stitches because it is quite thick. </strong>I started sewing from the V-notch and went all along the perimeter. Next, I fold the top edge of the skirt exactly in half, pin the middle to the dress-form so you can see it better (although pinning it to the table would be easier), and start simultaneously gathering both ends of the top edge on the threads.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2035_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2035_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2035_files/image038.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2035_files/image040.jpg" alt=""></p>
<p><strong>I gather both halves down to a length of 43cm to achieve the desired circumference of 86cm.</strong></p>
<p><strong>In addition, I need to carefully cut all layers by the length of the zipper at the V-notch.</strong> But I will only do it after putting the skirt on the dress-form properly and securing it in place to prevent the notched layers from parting.</p>
<p><strong>You can do it before putting the skirt on the dress if you want to: mark the length by which the skirt needs to be cut, stitch along the edges of the supposed zip-up area, and cut it. </strong>This additional stitching will be disguised by decorative elements.</p>
<p><strong>The skirt is ready and gathered and I put it on the main dress.&nbsp; </strong></p>
<p><strong>I hope you will forgive me but I have decided to position the skirt 10cm below the waistline the way I had intended at the very start! </strong>I have no idea what made me take the wrong turning there. Oh well, what to do! What was done was done! The top is a little short but it is fine because the small gap between the top and the skirt will be disguised by generous embellishments.</p>
<p><strong>The distance from the waist was estimated correctly (10cm). The skirt sits on the dress perfectly. It flows down in beautiful large folds and looks just gorgeous!</strong></p>
