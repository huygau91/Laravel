<h1>Tutorial 35. Joining the Mesh Parts with the Dress.</h1>
<p><strong>I start putting the mesh top over the main dress.</strong></p>
<p><strong>This should be done very carefully. No rushing!</strong> I align the princess seams and the side seams of the top and the dress with respect to the required length and gradually pin the top down. It is particularly hard to position the bottom edge of the top correctly. To make it easier, I mark a circumference line 6cm above the waistline of the dress with a disappearing ink pen. The bottom edge of the top should stay 1cm below that line. I secure it with pins. I keep spreading out and distributing the fabric of the top.</p>
<p><strong>I don't pay attention to small creases because I plan to decorate the top richly. </strong>Such mesh fabric tops are seldom made without decor. The only exception is lace tops. But even in that case you usually need to fill some hollows with additional appliqu� elements to disguise minor imperfections.</p>
