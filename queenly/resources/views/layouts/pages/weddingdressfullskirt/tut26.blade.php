<h1>Tutorial 25. Assembling the Face of the Dress.</h1>
<p><strong>I have finished the vertical edges and now I can sew the princess seams of the face. </strong></p>
<p><strong>I start from the back princess seams.</strong> I sew with a 1.2cm seam allowance in the duplicated area and narrow it down to about 7mm on the skirt, starting from the area where I trimmed the edge and down to the bottom. I basically narrow it by the width of the trimmed away strip of fabric (about 5mm).</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2025_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2025_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2025_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2025_files/image008.jpg" alt=""></p>
<p><strong>I carefully align the princess seam edges of the pieces whenever I pass from duplicated to non-duplicated fabric. </strong>I spread out the entire princess seam and keep aligning the edges section after section as I sew. This allows me to achieve neat even princess seams.</p>
<p><strong>The second princess seam was sewn in two goes:</strong> first I stitched from a point a couple centimeters below the edge of the iron-on fabric up to the top of the back and then I turned the garment around and stitched from the same point down to the bottom. It was easier to maintain the right length of the princess seam this way.</p>
