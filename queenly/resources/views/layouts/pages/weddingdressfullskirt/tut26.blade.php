<p><strong>All princess seams need to be pressed open. </strong></p>
<p><strong>I start by pressing the bust princess seams</strong> on a cup pressing mould. I press the cups in a circular motion carefully flattening the princess seams and paying particular attention to the corners of the cups. After finishing the cups, I press the vertical princess seams on a flat surface removing all wrinkles or creases.</p>
