<p><strong>And I trim the seam allowances. </strong></p>
<p><strong>You can either</strong> trim them right now <strong>or</strong> press them open first. I will trim them right now. If you don't feel comfortable pressing open narrow seam allowances, then press them first and then trim them.</p>
<p><strong>Please remember:</strong> it will be harder to press the seam allowances if you trim them now but they will be more even. And if you press them open first and then trim them, the pressing process will be a lot easier but you will hardly be able to maintain the same width along the full length. In short, you need to set your own priorities here.</p>
<p><strong>I trim the seam allowances only in the duplicated area keeping them at about 0.6cm. The ends are trimmed at an angle.</strong> I gradually return to the initial width of 1.2cm as I reach the end of the iron-on fabric.</p>
