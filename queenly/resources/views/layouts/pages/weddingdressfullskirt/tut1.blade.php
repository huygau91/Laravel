<h1>Classic Corset Wedding Dress with Lace Straps and Full Skirt.</h1>
<h2>Foreword.</h2>
<p>My dear fellow dressmaker! Let us dive into the beautiful world of wedding fashion. You have already seen a picture of the finished garment and now I will take you back to the moment when I was about to start working on it.</p>
<p>Back then, all I had was a sketch of the dress I wanted to sew. There is a lot of work ahead, let's go!</p>
<p><strong>Instead of starting from the usual pattern adjustment calculations, I will first cut and assemble a petticoat for the dress. </strong></p>
<p><strong>And this is not just some random decision</strong>.</p>
<p><strong>I get quite many E-mails with questions like </strong>"Could you please explain how to sew a petticoat?" or "Ready petticoats are really expensive, is it possible to sew one yourself?", "Ready petticoats are expensive and I couldn't find one for a dress with a train anyway" or "Is it possible to sew a petticoat yourself? Won't it be cheaper then?"</p>
<p><strong>Many people also want to know</strong> what fabrics are suitable for sewing a petticoat, where to buy them, where to buy the steel hoops and how to insert them in the petticoat, or whether they can be replaced with Rigilene bones.</p>
<p>All this tells me that my fellow dressmakers have quite many questions related to making underskirts or petticoats. This is why I want to focus on these issues in the introduction and then make additional comments further in the process.</p>
<p><strong>The first thing I want to say is yes, of course, you can sew your own petticoat but it is not the best idea for beginners.</strong> I believe you should buy the cheapest possible petticoat for a start. Regardless of the price, most ready-made petticoats require some polishing up.</p>
<p><strong>What concerns suitable fabrics, </strong>they are regular lining fabrics: nylon, low-cost veiling, mesh fabric, etc.&nbsp; <strong>Finding proper steel hoops is a tougher task! </strong>But even the cheapest and most terrible "made in China" petticoat has perfectly even steel hoops in it. I recommend buying a cheap petticoat in any case − simply to use the hoops for your own high-quality petticoat made for a particular dress.</p>
<p><strong>Another thing you need to know </strong>is that standard petticoats all have the same shape (apart from, maybe, special petticoats for dresses with a train) and are supposed to fit any type of a dress. In reality though, all dresses are different. Different sizes, different materials, different styles, etc. Petticoat manufacturers cannot possibly account for all these things and so any ready-made petticoat has to be customized in the end. The same thing concerns petticoats for dresses with trains.</p>
<p><strong>There is nothing technically difficult in sewing a petticoat! </strong>What plays the key role is the construction. You should have a clear vision of what functions the petticoat needs to perform before you start sewing.</p>
<p><strong>Here are the main requirements to the petticoat: </strong></p>
<p>- It should be comfortable. And by "comfortable" I mean that the bride should be able to move freely in it. She is not supposed to step on the hoop of the crinoline or on the underskirts of her dress.</p>
<p>- It should be lightweight.</p>
<p>- It should have a well-balanced shape (otherwise the dress will be distorted).</p>
<p>- It should be robust enough to carry the full weight of the skirt of the future dress.</p>
<p>These are the most basic requirements to any petticoat!</p>
<p><strong>Further work involves perfecting the construction of the petticoat with regard to the weight of the skirt, the length of the train, and many other similar things. </strong>Please note that we can only see the outer surface when we look at pictures of a dress by a top fashion designer. We can guess the way it was cut, figure out the amount of flare in the skirt and the order in which the ruffles were sewn on, find a suitable decoration technique, etc. But it takes your own imagination and practical experience to understand what hides inside. We all know that practice makes perfect though, and every wedding dress you sew takes your knowledge, skills, and thinking to a whole new level. You gradually learn to see not only the outer look of the dress but the inner construction, too.</p>
<p><strong>I will show you all stages of making a custom crinoline petticoat from scratch with regard to a particular dress style. </strong></p>
<p>My petticoat will be based on a very cheap and very plain petticoat which I bought for just five dollars.&nbsp; I want you to see that even a poor quality petticoat like this can serve as a good foundation for creating a matching petticoat for your own dress.</p>
<p>I believe every professional seamstress should always have at least 4-5 budget crinoline petticoats in stock. In this case, she will be able to create a custom petticoat for any kind of a dress.</p>
<p><strong>Why is it such a good idea to have petticoats of different types in your atelier?</strong></p>
<p>When a client comes to order a wedding dress, it is very hard to understand from the first words how full she wants to the skirt to be. But if she tries on a crinoline, you will be able to decide on the size of the bottom hoop together. The bottom hoop of the petticoat is a base for the future skirt.</p>
<p><strong>Most importantly,</strong> this will protect you from misunderstandings and other unpleasant moments.&nbsp; The client won't be able to say she wanted a skirt of a different width and you didn't get her right. You will be set on the shape and size of the skirt after she tries on a crinoline and confirms it.</p>
<p><strong>Besides,</strong> you are supposed to make a crinoline petticoat for your dress anyway. And it will be a lot easier if you have a suitable sample at hand.</p>
<p><strong>What kinds of ready-made petticoats are worth keeping in stock? </strong></p>
<p>- Petticoat with the smallest bottom hoop (180-200cm circumference);</p>
<p>- Several petticoats with bottom hoops of most popular lengths (250-300cm circumference);</p>
<p>- Petticoats with bottom hoop circumference between 400 and 450cm;</p>
<p>- And the largest possible petticoat with 600-650cm bottom hoop circumference.</p>
<p><strong>My task is to create a supporting construction for the underskirt which will serve as a base of the dress. It should be very robust and yet comfortable to wear. </strong>I will use a petticoat with 250cm bottom circumference as a foundation.</p>

