<h1>Tutorial 1. Peculiarities of Long Distance Sewing.</h1>
<p><strong>This time I am sewing for a long distance client.</strong> I had a chance to take measurements off her when she was visiting but then she flew back home to another country. I have to sew the dress completely and the bride will only arrive for the last fitting when it is finished. The task is to sew this dress in such a way that only minor adjustments will have to be made at the end (and those within literally just one day).</p>
<p><strong>The making of this dress is a great example of working long distance, and I am very glad I can introduce you to this side of the business. </strong>Long distance orders are very important for dressmakers who live in small towns with few local potential clients. You can expand your target audience considerably by accepting orders via own website or social networks from clients from other cities and even countries.</p>
<p><strong>In long distance sewing, a lot depends on how you will present yourself to your potential clients. </strong>&nbsp;Needless to say that you cannot count on a high enough income if you present yourself as a pr�t-�-porter wedding fashion online shop because women look for ready-made wedding dresses when they cannot afford a more expensive custom-made dress. And they are convinced that the fit of a ready-made dress will inevitably have to be improved at a local dressmaker's, which involves additional costs.&nbsp; Online shops (and especially those based in China) have a bad reputation for selling cheap poor quality garments. That is why you cannot expect high wages from an online shop. Your visitors will be those women looking for cheap dresses regardless of the quality!</p>
<p>But you can make a decent income <strong>if you present yourself as a highly qualified professional</strong> by describing all your qualifications and special skills, adding high quality pictures of your garments, and posting reviews from your clients (with a focus on long distance orders) on your social network page!</p>
<p><strong>Do invest some time in studying similar websites.</strong> I, for example, really like <em>Julina</em>. I invite you to visit their page and study their prices. As for me, they are implementing a very well thought-out advertising policy which involves investing in quality pictures and videos. All garments are of high quality and good taste. And they have very many positive reviews. That being said, their prices are far from low! Let it be your motivation. You can do it too! Moreover, I am sure you can do even better than that! You are already experienced and skilled enough unless it is your first day at the Corset Academy and the first sewing course you have purchased from us.</p>
<p>I am sure that, regardless of the complicated economic situation in the world, you can make money by accepting online orders if you are serious about starting a wedding fashion business. It is something you can do even living in a small town.</p>
<p><strong>The first thing that may get you puzzled about working long distance is taking measurements. </strong>I prefer to do it via Skype. Just have a video conversation with your client and ask her to tie cords around her body and take all necessary measurements before your eyes so you can control the process. It is best if she asks her sister, mum, or friend to help her. The results will be a lot more precise this way. As you can see, taking measurements off a long distance client is not a big problem today!</p>
<p><strong>The main challenges begin</strong> when you stay all alone with a half-finished garment in a desperate need of an intermediate fitting to clear up all doubts. <strong>The only solution is a very good dress-form! </strong></p>
<p><strong>Let me explain what types of dress-forms are true must-haves and tell you about the ones I use.</strong> If you are familiar with my other sewing tutorials, then you know that I always recommend sewing a mock-up after the standard pattern included in the course, measuring it to obtain all standard pattern dimensions with account for your sewing habits, and analyzing the results. After doing so, you customize the pattern after your client's measurements and sew a new mock-up.</p>
<p>Even when I work on a garment for a new book in the regular (non distance) mode, I basically follow the same algorithm as if my client were somewhere far. I don't do any intermediate fittings but invite my models only to try on the finished garment! In other words, I always work �long distance" because I don't want to waste anyone�s time. Instead, I stick to a very convenient algorithm of working with mock-ups.</p>
<p><strong>The picture below shows a mock-up on a dress-form left after working on "Wedding Dresses with Sleeves." </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image002.jpg" alt=""></strong></p>
<p><strong>What you see there is a regular display foam dress-form for US sizes 8-14 (Russian sizes 44-46). </strong>The packaging film protects it against glue stains during various handiwork.</p>
<p>This dress-form is very light and easy to work with! But it is not perfect for fittings and I need to adjust it every time to imitate a particular client's body shape. I sew a mock-up based on my client's measurements, put it on the dress-form, stuff all hollows with padding polyester or any other suitable stuffing material, <strong>and get a proper imitation of my client's figure.</strong></p>
<p>The main disadvantage of my dress-form is its large size - most of my models are smaller than that. It is hardly possible to have dress-forms of all possible sizes in your atelier. There is no need in investing so much money in that, especially for a beginner! <strong>What I do instead is spread apart the two halves of the back of the garment by equal distance from the centre back line of the dress-form. </strong>You should always keep the central edges of the back strictly vertical.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image004.jpg" alt=""></p>
<p><strong>The side seams must also be strictly vertical.</strong> Of course, they will get shifted toward the front of the garment if your mock-up is smaller than the dress-form, and yet they must still stay vertical!</p>
<p><strong>This is how I imitate my client's figure.</strong> You can always double-check all dimensions. Practice makes perfect and with time you will develop a good understanding of peculiarities related to non-standard body types. After sewing several dresses, you will be experienced enough to notice peculiar features of your client's body via Skype.</p>
<p><strong>The key challenge of sewing for a long distance client lies solely in choosing the right sewing technique.</strong> But then again, it is just as important when you work with someone in person. There are no doubts that seeing your client face to face helps you adjust the garment sooner because you will notice all flaws during an intermediate fitting. For this reason, I always recommend beginners should first practice sewing for clients whom they can meet with personally until they feel confident enough to accept long distance orders. Needless to say, there is absolutely nothing to be worried about when you work with well-proportioned women!</p>
<p>Now you have seen my regular display foam dress-form, perfect for handiwork, for imitating different figures, and for carrying out virtual fittings with a mock-up.&nbsp; You need to have at least two such dress-forms. <strong>As you can see in the picture, I have another one of just the same size. </strong>The fabric casing needs to be changed from time to time because it tends to get dirty. It can be sewn with any plain knit fabric.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image006.jpg" alt=""></p>
<p><strong>And now let me show you my favourite beauty - a professional size-8 dress-form! Look what a wonderful shape! Nearly perfect!&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image014.jpg" alt=""></p>
<p><strong>Why do I love this dress-form? </strong>I love it for the vertical seams on the casing, for the central part with built-in cups which show where the seams should run, for the firmly fixed ribbon around the waist, and for its proper buttocks. A display dress-form always lacks fullness in the hips because it is hard to put a dress on a dress-form with proper shoulder and hip width.</p>
<p><strong>With this dress-form I can squeeze the shoulders, put a garment on, and bring the shoulders back to their natural width.&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image016.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image018.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image020.jpg" alt=""></strong></p>
<p><strong>A professional dress-form imitates the hips of a real woman, which makes it perfect for fitting skirts.</strong> Moreover, <strong>professional dress-forms have a cage bottom</strong> for draping the fabric and doing other similar handiwork. It is a serious advantage!</p>
<p><strong>Its height adjustment mechanism is very easy to use</strong> and comes with a foot pedal. The base of the dress-form has wheels with locks.</p>
<p><strong>Another great advantage of this particular dress-form is that it fully coincides with the measurements of the bride for whom I am sewing this time. </strong>It is a rare coincidence though, so I will still work with a mock-up.</p>
<p><strong>Two properties of this dress-form could be referred to disadvantages: </strong></p>
<p>- <strong>Its outer surface is too hard</strong> to stick pins in vertically, which makes it unsuitable for handiwork. The most you can do with this kind of a dress-form is form folds in certain areas of your garment. You can drape fabric by inserting pins horizontally. But, on the whole, <strong>this dress-form is only suitable for fittings.</strong></p>
<p><strong>I have to use a lightweight display dress-form which I can easily put on my lap for applying appliqu�, rhinestones or other embellishments on the garment. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%201_files/image022.jpg" alt=""></p>
<p><strong>- It is very heavy.</strong> But it is also an advantage because the dress-form stands firmly and it is hard to knock it down by accident. This makes it perfect for working on full, heavy wedding dresses!</p>
<p><strong>But the main disadvantage of a professional dress-form is its price!</strong> It will cost minimum 400 USD (I have compared prices from various online stores).</p>
<p><strong>Professional dress-forms are available in a wide range of sizes: from 6 to 20. </strong>&nbsp;Of course, famous fashion houses and rich sewing ateliers are equipped with dress-forms of all possible sizes because they are a great help, and especially when you work with custom orders. Small private ateliers obviously cannot afford a full range of dress-forms, both financially and in terms of available work space. As for me, I bought a single professional size-8 dress-form - it is the most popular size, very easy to work with!</p>
<p><strong>In conclusion, I would like to warn you against a big mistake made by basically all beginners (as follows from your E-mails and forum posts)!</strong></p>
<p><strong>They tend to start from the wrong thing</strong>: they buy expensive high quality equipment first and only then do they start searching for clients and buying various teaching materials.</p>
<p><strong>Please understand that this is totally wrong! </strong></p>
<p>Even the best and most up-to-date equipment will not help you if you cannot sew well enough and don't have your own client network.</p>
<p>I know lots of successful professionals who started their business with just a regular household sewing machine and a plain iron at hand! You don't have to buy the most expensive equipment and dress-forms straight away! My overall professional sewing experience in Russia and Israel is almost 30 years and it was not until recently that I got myself this super-expensive professional dress-form. And I was quite satisfied with regular display dress-forms and never experienced any particular problems before. Although sometimes I had to adapt to the situation, I always knew I could carry out intermediate fittings even with the help of those plain display dress-forms.</p>
<p>I won't deny that a professional dress-form is a real beauty worth a little envy!</p>
<p><strong>But I would like to emphasize</strong> that a couple of plain foam dress-forms and inexpensive sewing equipment is enough for a start!</p>

