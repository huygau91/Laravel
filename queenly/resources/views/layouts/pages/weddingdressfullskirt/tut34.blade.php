<h2>Tutorial 33. Cutting the Mesh Skirts. </h2>
<p></p>
<p><strong>I am about to start cutting the large skirt pieces. </strong>&nbsp;I fold my euromesh fabric in half and put it on two cutting tables put together. I have put a soft polyurethane mat on one of the tables, right under the fold line of the mesh fabric. It is easy to stick pins in it. I will cut the skirt layer after layer. In this particular case, it is impossible to fold the fabric in a cone and cut an even full circle because the length of the skirt varies along the perimeter. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image002.jpg" class="img-responsive center-block"></p>
<p><strong>The first thing I do is mark the front length of the skirt along the edge of the fabric (118cm) and then add a 1.5cm seam allowance and the calculated longest possible radius (28.5cm).</strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image004.jpg" class="img-responsive center-block"></strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image006.jpg" class="img-responsive center-block"></strong></p>
<p><strong>Since the cutting process often implies drawing circles, ellipses, and arcs, I invented a very simple tool which can replace a pair of compasses.</strong> It is a usual drawing pin which you can stick anywhere in the lay to mark the centre of a supposed circle and then tie a ribbon or a cord to it to mark any desired radius.</p>
<p><strong>I stick a drawing pin at the supposed end of the radius, mark 28.5cm along the cord, pull it on tight enough, and start turning it about the pin,</strong> as if about the centre of a circle, marking points along the perimeter. And then I connect the marked points in a half-circle cutting line.</p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image008.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image010.jpg" class="img-responsive center-block"></p>
<p><strong>Then I measure another 1.2cm from the end of the 28.5cm section on the cord and mark the front length of the skirt, which is 118cm.&nbsp; </strong></p>
<p><strong>The total length marked on the cord is 148cm: 28.5cm + 1.5cm + 118cm = 148cm. </strong></p>
<p><strong>Starting from the edge of the fold line, I mark points along the perimeter of the supposed half-circle with a radius of 148cm.&nbsp; </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image012.jpg" class="img-responsive center-block"></p>
<p><strong>I will add another 2cm to the radius after passing about a half of the half circle (the cord will be lying at 45 degrees to the fold line)</strong> because the skirt is that much longer at the sides than at the front.</p>
<p><strong>120cm - 118cm = 2cm</strong></p>
<p><strong>The length of the cord or the radius of the circumference is: </strong></p>
<p><strong>28.5cm + 1.5cm + 120cm = 150cm </strong></p>
<p><strong>I continue drawing with a radius of 150cm, which equals the width of the lay, and arrive right at the aligned edges of the fabric. </strong></p>
<p><strong><br>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>I draw the front part of the skirt. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image016.jpg" class="img-responsive center-block"></p>
<p><strong>I shift the lay. </strong>The drawing pin stays where it was on the fabric but moves to a different part of the table. <strong>I leave the same length of 150cm marked on the cord.</strong> It corresponds with the side length of the skirt with the radius in account. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image018.jpg" class="img-responsive center-block"></p>
<p>I am supposed to arrive at the full length of the train in about the middle of the half circle, i.e. with the cord lying at 45 degrees to the fold line. In other words, starting from that point I am supposed to arrive at the back length of the skirt - 145cm. <strong>The cord (or the radius) will then have the following length:</strong></p>
<p><strong>28.5cm + 1.5cm + 145cm = 175cm </strong></p>
<p><strong>I turn the cord and mark points along the perimeter of the ellipse. I prolong the cord by another 3-4cm after passing every 10-15cm of the arc. It is supposed to be 175cm long at the starting point of the train. I mark the point where the train is supposed to arrive on the fold line of the fabric. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image020.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image022.jpg" class="img-responsive center-block"></p>
<p></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image024.jpg" class="img-responsive center-block"></p>
<p><strong>I draw the back of the skirt with a train. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image026.jpg" class="img-responsive center-block"></p>
<p><strong>This is what the piece looks like.&nbsp; </strong></p>
<p><strong>I fold the mesh fabric in half and cut two layers simultaneously. </strong>Do not try to cut more than two layers in one go: they will shift against each other.&nbsp; </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image028.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image030.jpg" class="img-responsive center-block"></p>
<p><strong>I take the carbage left after cutting the first two layers of the skirt, keep all four layers of mesh fabric, and lay down the adjusted top pieces (save for the central front piece with a fold line). </strong>I pin the pieces down and cut them out. The side seams are to be sewn with 2cm seam allowances. I make notches correspondingly. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image032.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image034.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image036.jpg" class="img-responsive center-block"></p>
<p><strong>The central front piece will be made of carbage left from the skirt with a fold line after I prepare a new lay and cut out the remaining layers. </strong></p>
<p><strong>I have finished cutting the mesh fabric and ended up with 5 and not 4 layers with train extensions.&nbsp; Each layer is made of 330cm of fabric. The total fabric consumption is 1650cm or 16.5m. </strong></p>
<p><strong>The skirt has been cut out and all 5 layers pressed. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image038.jpg" class="img-responsive center-block"></p>
<p><strong>The top edge of the skirt will be gathered down to the desired waist circumference. I fold it in half, find the middle, and notch all layers through at the sides of the skirt. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image040.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image042.jpg" class="img-responsive center-block"></p>
<p><strong>I also make a straight notch at the front of each layer and notch it at an angle in the train area. </strong>Now I will be able to tell the back from the front. In addition, I cut the fold line of the back by about 7-8cm to install a zipper there. </p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image044.jpg" class="img-responsive center-block"></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image046.jpg" class="img-responsive center-block"></p>
<p><strong>I carefully put all layers together. </strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image048.jpg" class="img-responsive center-block"></p>
<p><strong>I take the carbage left from the waist opening in the skirt and cut the central front piece from it.</strong></p>
<p>
<img src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2033_files/image050.jpg" class="img-responsive center-block"></p>
