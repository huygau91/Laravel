<h2>Tutorial 7. Positioning, Calculating, and Cutting the First Ruffle.</h2>
<p><strong>I have pressed all Rigilene bones on the petticoat.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image002.jpg" alt=""></p>
<p><strong>The next step is to sew a hard mesh ruffle along the bottom edge. </strong></p>
<p><strong>This ruffle </strong>is only supposed to prevent the hem from curling in and not to increase the fullness.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image004.jpg" alt=""></p>
<p><strong>The bottom ruffle will be sewn on along the line which marks a tunnel for hoop 2 on the face on the petticoat. </strong>The edge of the ruffle will be therefore overlaid by a bias tape. The only function of this tunnel is to reinforce the mesh fabric. I will sew another one on the inside and insert a steel hoop in it.</p>
<p><strong>First of all, let us calculate the width of the ruffle. It is very important to get it right. </strong></p>
<p><strong>The second hoop is positioned 5cm above the bottom edge at the front and 25cm above the bottom edge at the back.</strong> The bottom edge of the ruffle is supposed to stay about 3cm above floor level at the front. The petticoat itself must not drag along the floor. If a stiff construction (a stiff cage in our case) touches the floor, the skirt will bump along in a very unpretty way when the bride walks around. It will be very obvious that there is a stiff construction hiding underneath the skirt. On the other hand, you cannot do without this stiff construction because otherwise the hem of the main skirt will keep curling up.</p>
<p><strong>If the second hoop is positioned 25cm above floor level and the bottom edge of the ruffle must be positioned horizontally and stay 3cm above the floor along the full perimeter, then the width of the main bottom ruffle should be 22cm.&nbsp; </strong></p>
<p><strong>But in this case, nearly the whole back of the petticoat will be left without ruffles and the train will not be reinforced. </strong></p>
<p><strong>To solve this problem, I will sew an additional 22cm wide ruffle between the side seams onto the back. It will be placed below the level of the second hoop. </strong>This additional ruffle will rest upon the floor and give support to the train and the lining of the dress.</p>
<p><strong>I will most likely add one more ruffle and sew it on at an angle:</strong> it will be positioned 5cm below the second hoop at the back and 3cm below it at the side. In this case, this additional ruffle will basically reach down to the floor at the back.</p>
<p><strong>Now that I know the widths of the main and the additional ruffle, I need to decide on their lengths and figure out fabric consumption with regard to the implied gathering ratio. </strong></p>
<p><strong>I know all necessary measurements: the second hoop (and the main ruffle) has a circumference of 240cm, and the additional ruffle should be 110cm long.&nbsp; </strong></p>
<p><strong>This makes a total length of 350cm.&nbsp; </strong></p>
<p><strong>With a 3 to 1 gathering ratio (which is more than enough), I will need a strip of mesh fabric with a length of 105cm and a width of 22cm.&nbsp;&nbsp; </strong></p>
<p><strong>Since my mesh fabric has a width of 180cm, I need to cut six 22cm strips.</strong> This will make a total length of 108cm.</p>
<p><strong>Here is how much mesh fabric I need considering its width: </strong></p>
<p><strong>22cm × 6 = 132cm ≈ 140cm</strong></p>
<p><strong>I cut six 22cm wide strips of mesh fabric. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image006.jpg" alt=""></p>
<p><strong>Next, I need to sew them together in a single strip. </strong>I sew the strips together with a presser foot width seam allowance.</p>
<p><strong>Remember to fold all seam allowances to the same side when doing this.&nbsp; </strong></p>
<p><strong>Let me explain it in more detail for those who are not sure how to do it right. </strong>Put two strips of fabric face to face and join them. The seam allowance stays on the wrong side of the fabric. Take the free end of the top strip and open the joined strips so as to have them lying face side up. Take the third strip and put it face down over the strip that was at the top. Sew them together. Again, the seam allowance stays on the wrong side of the fabric. Continue until you reach the last strip.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image016.jpg" alt=""></p>
<p><strong>All seam allowances are on the inside. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image018.jpg" alt=""></p>
<p><strong>The result is a ruffle with a total length of 110cm and with all seam allowances on the same side.&nbsp; </strong></p>
<p><strong>I switch to a ruffler presser foot.</strong> The gathering ratio is about 3 to 1.</p>
<p><strong>I test it on a small area and start gathering the ruffle.</strong> You will be able to gather more thickly if you slow down the feeding by placing your finger behind the presser foot and pushing the ruffle in with the other hand.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image022.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image024.jpg" alt=""></p>
<p><strong>The ruffle is ready!</strong> I am happy with the result. If I decide to make it thicker, I will simply gather it one more time.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%207_files/image026.jpg" alt=""></p>
<p><strong>And now, what concerns finishing the bottom edge of the ruffle. </strong>The mesh is rather itchy and may feel uncomfortable in wear. You can finish the bottom edge with a bias tape. But my dress has a tulle face and the bias tape edge of the ruffle will show if the hem curls up. I don't like it. I wouldn't want to make it too obvious that there is a petticoat under the skirt. I will leave the edge of the ruffle unfinished for now. It is so light and delicate. When my client comes to pick up the dress and tries it on, she will tell me whether the raw edge disturbs her. It won't take long to finish it with a bias tape if it causes her legs to itch.</p>
<p><strong>I will measure the length of the ruffle to see whether I need to make it longer and continue working.</strong></p>

