<p><strong>I am sure I have convinced you that a pyrography tool is a total must-have for us seamstresses! </strong></p>
<p><strong>I prepare two stacks of mirrored lace motifs </strong>to make further work easier. <strong>One stack contains the first type of soutache lace motifs </strong>and the other stack is motifs cut out from soutache lace of a slightly different type and style. They will look wonderful together. <strong>I have also cut some motifs from thicker lace fabric with beads and sequins. </strong></p>
