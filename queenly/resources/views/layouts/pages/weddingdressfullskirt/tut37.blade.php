<p><strong>It is fast and the result is clean and neat. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image022.jpg" alt=""></p>
<p><strong>I continue preparing various motifs for the dress.</strong> Imagine how much time it would take if I were using scissors!</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image024.jpg" alt=""></p>
