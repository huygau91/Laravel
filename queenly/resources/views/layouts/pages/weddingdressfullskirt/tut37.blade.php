<h2>Tutorial 36. Cutting Out Lace Motifs.</h2>
<p><strong>Decorating is the most enjoyable part of the process. </strong>My dress will be decorated with lace appliqu�.</p>
<p>Every seamstress engaged in wedding fashion has lots of lace fabric scraps in stock. Especially after sewing a whole dress with lace fabric: you end up with rather large scraps. And these are usually put aside till better days. Years of work have provided me with bags of expensive lace fabric scraps so I can't remember the last time I had to buy lace for decoration purposes.</p>
<p><strong>Let me share with you some tips on how to choose lace for decoration purposes.</strong> Lace fabric can be classified into several types based on its texture and I always sort out my scraps and store each type separately.</p>
<p><strong>I have here scraps of beaded lace, soutache lace, and another type of soutache lace with particularly soft and delicate mesh fabric foundation.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image002.jpg" alt=""></strong></p>
<p><strong>If you take a closer look at the motifs on different lace fabrics with the same texture, you will notice that they are often very similar (especially when embroidered with similar seed beads, bugle beads, or sequins).</strong> Even when the motifs themselves are different (for example, I have roses on one scrap and some fantasy flowers on the other), you can cut them out and combine them on the garment - no one will ever guess that they come from different lace fabrics. These pieces match each other perfectly because the texture is nearly the same in all of them: same thread, same mesh fabric foundation, same colour of beads.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image004.jpg" alt=""></p>
<p><strong>I have some scraps of beaded soutache lace and others with no beads or sequins on them. I will need both for my appliqu�. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image006.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image008.jpg" alt=""></strong></p>
<p><strong>I will combine different types of lace on my dress.</strong> And do you want to know why? Because a garment looks a lot more luxurious and unique when decorated with several types of lace. It looks like it must have taken a lot of time and effort, as well as very expensive lace fabric of various kinds. In reality though, these luxurious embellishments do not require serious investments: you simply use what you already have in stock without spending money on new materials. <strong>The only thing you need to be careful with is the colour of the motifs</strong> themselves and the beads on them. Although sometimes a good combination of different shades can create a very nice effect, too.</p>
<p><strong>Now you know everything about my decoration materials. Let us look at the tools.</strong> I will use my pyrography tool again. I will also use a small glass plate as a cutting surface, a glass jar for holding the hot burning tool, and a cotton tissue for cleaning the tip.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image010.jpg" alt=""></p>
<p><strong>Here are a couple more tips before we start. </strong></p>
<p><strong>Firstly,</strong> remember to thoroughly press your lace fabric before cutting out individual motifs! It will make the process a lot easier.</p>
<p><strong>Secondly,</strong> as I mentioned before, sometimes lace fabric has such a delicate foundation that a cut out motif turns out so soft that it is really hard to glue or sew it in place. But what if you really want to use that very motif! Look at this little rose for example!</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image012.jpg" alt=""></p>
<p><strong>Here is what I do in such cases.</strong> I take a piece of euromesh or a scrap of mesh fabric left from the petticoat, put it on the glass plate, put the desired motif on top of it, and cut it out with the pyrography tool together with the mesh fabric. The mesh fabric backing will get fused with the motif. The flower will become stiffer and denser - more suitable for appliqu�. If you simply cut it out with scissors, it will be too soft and you will have lots of trouble working with it.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image014.jpg" alt=""></p>
<p><strong>I take my pyrography tool and start cutting out selected lace motifs.</strong></p>
<p><strong>Here is a rather complicated motif of soutache lace with a mesh fabric foundation.</strong> For your consideration, it took me less than a minute to cut it out with this tool.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2036_files/image016.jpg" alt=""></p>
