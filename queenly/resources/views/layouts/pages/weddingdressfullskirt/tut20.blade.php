<h1>Tutorial 19. Assembling the Front Half of the Lining.</h1>
<p>Now that I have marked all bones, I can assemble the front and the back of the dress by sewing down the vertical princess seams.</p>
<p><strong>I will start from the front. </strong></p>
<p>First of all, I need to check whether the hard iron-on fabric holds well enough on the cups. <strong>I prefer to secure it additionally by stitching along the perimeter. </strong></p>
<p><strong>The side front piece:</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image002.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image004.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image008.jpg" alt=""></p>
<p><strong>And here is a picture of the stitched hard iron-on fabric on the face of the side front piece.</strong> The same kind of stitching is made on the other side front piece.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image010.jpg" alt=""></p>
<p><strong>By the way, I have serged the vertical edges of all pieces:</strong> the seam allowances of the side seams are serged along the full length and those of the princess seams – from the bottom up to the border of the iron-on batiste. I have also pressed the pieces after serging the edges.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image012.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image014.jpg" alt=""></p>
<p><strong>I stitch along the perimeter of the hard iron-on fabric on the cup of the central front piece as well. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image016.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image018.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image020.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image022.jpg" alt=""></p>
<p><strong>I fold the central front piece in half and even out the princess seam lines to make them symmetric.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image024.jpg" alt=""></p>
<p><strong>The face of the central front piece looks like:</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image026.jpg" alt=""></p>
<p><strong>Before joining the pieces, I sew a row of stay-stitches along the neckline of the central front piece (1.2cm from the edge). </strong>I start a row from either end of the neckline. The two rows meet right in the centre of the neckline.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image028.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image030.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image032.jpg" alt=""></p>
<p><strong>The stay-stitches help me highlight the very centre of the neckline on the face and on the inside of the central front lining piece.</strong> Please note that the center of the neckline is shifted 1-2mm against the previously made mark on the inside.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image034.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image036.jpg" alt=""></p>
<p><strong>I am about to sew the front princess seams of the dress. </strong></p>
<p><strong>Please remember to align the bust lines and not the bust-line notches when sewing the princess seams. The notches only indicate the position of the bust line on the piece!</strong></p>
<p>I align the princess seam edges of the side front piece and the central front piece and <strong>stick a pin in the bust line of the side front piece 1.2cm from the princess seam edge.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image038.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image040.jpg" alt=""></strong></p>
<p><strong>I bring the pin in a symmetric point on the bust line of the central front piece </strong>1.2cm from the princess seam edge.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image042.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image044.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image046.jpg" alt=""></p>
<p><strong>I align the edges of the pieces by turning the side piece on the pin.</strong> Their bust-line notches don't meet.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image048.jpg" alt=""></p>
<p><strong>I sew the bust princess seam with a 1.2cm seam allowance.&nbsp; </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image050.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image052.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image054.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image056.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image058.jpg" alt=""></p>
<p><strong>When I was sewing the bust princess seams of the mock-up, I noticed that the waistline notches were somewhat misaligned. </strong>I took note of this and I am not trying to get them aligned now, i.e. on the actual garment. There was a small error in the pattern but a 2-3mm mismatch of waistline notches will not affect the overall look and quality of the dress.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image060.jpg" alt=""></p>
<p><strong>A sewn bust princess seam can be either </strong>pressed open on a cup pressing mould <strong>or</strong> spread open and topstitched. And you can topstitch it either down to the end of the cup or down to the end of the iron-on batiste.</p>
<p><strong>I prefer topstitching the seam down to the end of the iron-on fabric.</strong> I topstitch it from the face side although some professionals prefer to do it from the inside. It is up to you.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image062.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image064.jpg" alt=""></p>
<p><strong>I topstitch the bust princess seam down to the end of the iron-on fabric. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2019_files/image066.jpg" alt=""></p>
