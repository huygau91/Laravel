<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image060.jpg" alt=""></strong></p>
<p><strong>I have thereby adjusted the pattern down to a length of 18cm.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image062.jpg" alt=""></p>
<p><strong>I finish cutting the side front piece. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image064.jpg" alt=""></p>
<p><strong>The flare of the skirt will begin 10cm below the waistline while the bottom edge of the piece (the stomach line) is situated 12cm below the waistline. Keeping this in mind, I trim the bottom edge by 2cm. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image066.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image068.jpg" alt=""></p>
<p><strong>I have adjusted the side front piece. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image070.jpg" alt=""></p>
<p><strong>Let us finish the central front piece now.&nbsp; </strong></p>
<p><strong>I mark 2cm up from the bottom edge of the piece and 1.2cm up from the waistline. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image072.jpg" alt=""></p>
<p><strong>I adjust the Waist to Under-Bust measurement. It is supposed to be 10cm. As you remember, I need to remove 1.2cm to achieve the desired length.</strong></p>
<p><strong>I cut the central front piece along the waistline and shift the bottom part of the piece 1.2cm up. I finish cutting the piece. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image074.jpg" alt=""></strong></p>
