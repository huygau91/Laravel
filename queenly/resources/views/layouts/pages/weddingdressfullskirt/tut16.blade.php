<p><strong>I cut along the waistline of the central back piece, shift the bottom part 1.2cm upwards, and glue the parts back together.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image108.jpg" alt=""></strong></p>
