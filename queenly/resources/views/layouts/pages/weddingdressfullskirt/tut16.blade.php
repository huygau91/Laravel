<h1>Tutorial 15. Customizing the Bodice on Paper.</h1>
<p><strong>All eight pattern sheets are printed out and now I need to glue them together. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image002.jpg" alt=""></strong></p>
<p><strong>Put the first and the second sheet together, carefully match all lines and squares, and glue them together.</strong> If your printer leaves white gaps along the perimeter of the sheet (not borders but unprinted areas), <strong>do not trim them away.</strong> The printer just cannot physically print at the very edges of the sheet. Remember to select "Actual Size" in printing preferences. Imagine that you have prolonged the line and glue the sheets together.</p>
<p><strong>For your convenience, there is a thumbnail on each sheet that shows how to join the pieces.</strong> The current sheet is marked grey. This way you can tell what part of the pattern you are holding in your hands and what sheet it needs to be joined with.</p>
<p><strong>Here is the central front piece: </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image004.jpg" alt=""></strong></p>
<p><strong>One by one, I glue together the separate parts of the standard pattern using a plain glue stick.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image008.jpg" alt=""></p>
<p><strong>I recommend you should print two sets of patterns. </strong></p>
<p>You will glue together the sheets of the first set, cut out the patterns, cut them from mock-up fabric, assemble a mock-up, and measure it.</p>
<p>And then, you will glue together the sheets of the second set and apply adjustments implied by client's measurements.</p>
<p><strong>I have prepared all four parts of the standard pattern. The skirt will be drawn directly on fabric. </strong></p>
<p><strong>Let�s start adjusting the standard pattern.</strong></p>
<p><strong>The central front piece:</strong></p>
<p><strong>Bust Apex to Bust Apex has changed and I need to narrow the front of the bodice by shifting the central edge 1cm inwards.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image010.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image012.jpg" alt=""></p>
<p><strong>The clip toward the neckline was adjusted as well. </strong>According to the calculations, it was made 1.5cm shorter and is now 6cm.</p>
<p><strong>By changing Bust Apex to Bust Apex by 1cm, you automatically shift the neckline by the same value. I draw a new neckline parallel to the initial one. </strong></p>
<p><strong>And by changing the position of the neckline I also change the length of the clip towards neckline. </strong></p>
<p><strong>I measure it from the bust apex point.&nbsp; </strong></p>
<p>The result is about 7.2cm including the seam allowance. The actual length of the clip toward the neckline is, therefore, 6cm, which fully coincides with the client's measurement.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image014.jpg" alt=""></p>
<p><strong>Here is the adjusted central front piece: </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image016.jpg" alt=""></p>
<p><strong>I move on to the side front piece.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image018.jpg" alt=""></p>
<p><strong>There are no changes to the side seam line of the side front piece. The only things that need to be adjusted are clip to the neckline and clip to the armscye. </strong></p>
<p><strong>Both these clips are supposed to have an actual length of 11cm (13.2cm with 1.2cm seam allowance included).</strong></p>
<p><strong>I draw the bust line and mark a clip from the bust apex point toward the middle of the neckline and another clip toward the armscye. Each clip includes a seam allowance and is 13.2cm long.</strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image020.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image022.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image024.jpg" alt=""></strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image026.jpg" alt=""></strong></p>
<p><strong>Before re-drawing the armscye line passing through the adjusted clips, I will shorten the side length by 3cm as suggested by the calculations. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image028.jpg" alt=""></strong></p>
<p><strong>I measure the distance from the waistline to the under-bust line on the side front piece. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image030.jpg" alt=""></p>
<p><strong>It is 10cm on the client and 11.2cm on the pattern. </strong></p>
<p><strong>11.2cm - 10cm = 1.2cm </strong></p>
<p>There is extra 1.2cm on the standard pattern.</p>
<p><strong>As follows from the calculations, I need to shorten the side seam by 3cm.&nbsp; </strong></p>
<p><strong>I remove 1.2cm by reducing the width of the area between the waistline and the under-bust line. </strong>Now the width of this area is 10cm, which corresponds with the client's measurement. I re-draw the waistline. Later, I will cut away the excess in this area and align the bottom part of the piece with the new waistline.</p>
<p><strong>I still need to shorten the side seam by another 1.8cm. </strong></p>
<p><strong>3cm - 1.2cm = 1.8cm</strong></p>
<p><strong>This is basically the largest possible value by which the side seam line can be shortened at the top (at the armscye).&nbsp; </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image032.jpg" alt=""></strong></p>
<p><strong>I re-draw the armscye line with respect to the adjusted clip toward to middle of the neckline, the adjusted clip toward the armscye, and the shortened side seam. </strong>The result is a graceful smooth line.</p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image034.jpg" alt=""></strong></p>
<p><strong>I re-draw the top line 1.8cm below the initial one to shorten the piece. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image036.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image038.jpg" alt=""></p>
<p><strong>The side seam line stays as it is but I need to work with the princess seam line.&nbsp; </strong></p>
<p><strong>I shift the princess seam line 0.3cm inwards at the waistline and 0.5cm inwards at the stomach line marked 12cm below the waistline.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image040.jpg" alt=""></p>
<p><strong>I re-draw the front princess seam line.</strong> The area from the stomach line to the hip line is not relevant.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image042.jpg" alt=""></p>
<p><strong>I return to the central front piece to finish the princess seam line and draw a transition to the side front piece at the top. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image044.jpg" alt=""></p>
<p><strong>I re-draw the princess seam line shifting it 0.3cm inwards at the waistline and 0.5cm inwards at the stomach line 12cm below the waist.</strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image046.jpg" alt=""></p>
<p><strong>In order to achieve a smooth transition from the central front piece to the side front piece, </strong>I trim the top of the side piece, fold back the seam allowance, align it with the central piece, and draw a graceful line - the way I want it to be after joining the two pieces along the princess seam. I forgot to tell you that this dress will have concealed side seams. It has a low back and I want the sides to look perfectly smooth.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image048.jpg" alt=""></p>
