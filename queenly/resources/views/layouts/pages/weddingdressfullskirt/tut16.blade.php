<p><strong>I adjust the Waist to Under-Bust measurement. It is supposed to be 10cm. Let me remind you that I need to remove 1.2cm to achieve the desired value. </strong></p>
<p><strong>I cut along the waistline of the side front piece and shift the bottom part 1.2cm upwards. </strong></p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%2015_files/image056.jpg" alt=""></strong></p>
