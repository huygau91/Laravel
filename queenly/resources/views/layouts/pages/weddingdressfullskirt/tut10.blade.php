<h1>Tutorial 9. Sewing Tunnels for Stiff Petticoat Hoops.</h1>
<p><strong>I carry out a virtual fitting on the dress-form.</strong> I adjust the dress-form to my client's height, waist to floor with shoes, and key circumference measurements. It imitates my client's body shape.</p>
<p><strong>I mark 15cm down from the waistline, </strong>secure an elastic band around the dress-form, and put the petticoat on it aligning its top edge with the elastic band which marks the beginning of the flare.</p>
<p><strong>The petticoat sits on the dress-form the way it is supposed to sit on the bride.</strong> I have pinned the central seam at the back of the petticoat and put hoops 1 and 2 in the tunnels without locking their ends yet.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image002.jpg" alt=""></p>
<p><strong>First of all, I check how well the hoops fit in the tunnels on the face of the petticoat. </strong></p>
<p>The first hoop fits in very well.</p>
<p>But the second hoop can hardly fit in its tunnel because it is stuffed with fabric from the main bottom ruffle. I will thus make a new tunnel on the inside of the petticoat right along the tunnel made on the face and insert the hoop there.</p>
<p><strong>Here is what I can say after analyzing the intermediate results: </strong>part of the bottom hoop is lying on the floor (as was planned) and the skirt itself looks well-proportioned, i.e. there is no distortion. The side seams are vertical and fully aligned with the side seams on the dress-form. The ruffles are positioned correctly.</p>
<p><strong><br>
<img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image004.jpg" alt=""></strong></p>
<p><strong>This stage has been completed successfully. I can take out the hoops and continue working on the petticoat. </strong></p>
<p><strong>I need to sew a tunnel for the fourth hoop on the inside of the petticoat.</strong> I put a bias tape along the marked line, 2cm from the central edge of the back, and sew it on with two parallel rows of stitches.</p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image006.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image008.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image010.jpg" alt=""></p>
<p><strong>Then I sew a tunnel for the third hoop. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image012.jpg" alt=""></p>
<p><strong>And, finally, I sew a tunnel on the inside of the petticoat along the tunnel for the second hoop already made on the face. </strong></p>
<p><strong>Here is what the petticoat looks like with tunnels on the face and on the inside. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image014.jpg" alt=""></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image016.jpg" alt=""></p>
<p><strong>The third and the fourth tunnel are made 2cm from the central edge of the back. Sewing a tunnel for the second hoop, I leave the ends of the bias tape detached. I will finish them after sewing the central back seam. </strong></p>
<p><img class="img-responsive center-block" src="http://corsetacademy.s3.amazonaws.com/wedding%20dresses/wedding%209/Tutorial%209_files/image018.jpg" alt=""></p>
