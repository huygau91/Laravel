<h3>Printing Out Patterns.</h3>
<p>So, you have extracted the enclosed archive into a separate folder. It contains the file with the book and a folder with patterns that you can open with a double click.</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image002.jpg" class="img-responsive center-block"></p>
<p>You need to open the files one-at-a-time in order to print out the patterns. All files open with a double click on the icon.</p>
<p>I double click the icon ‘1-2а’ and open the first pattern:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image004.jpg" class="img-responsive center-block"></p>
<p>To print out this pattern, you should click (just once) on the ‘Print’ icon (marked with the red arrow).</p>
<p><strong>When I click this icon, the printing menu comes up:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image006.jpg" class="img-responsive center-block"></p>
<p></p>
<p>You should select the printer that is currently connected to your computer<strong> in the Printer drop-down list marked with the red arrow</strong>:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image008.jpg" class="img-responsive center-block"></p>
<p>Now you need to select ‘Document’<strong> in the Comments &amp; Forms drop-down list marked with the purple arrow</strong>.</p>
<p><strong>And next you need to specify all necessary printing properties. </strong></p>
<p>The menu with your printer’s properties opens<strong> when you click the Properties button marked with the blue arrow (see above)</strong>:</p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image010.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>You should tick Borderless Printing, select the A4 printer paper size and tick the Portrait orientation (green arrows).</strong></p>
<p><strong>It is necessary to press ‘OK’ afterwards!</strong></p>
<p>This will return you to the printing menu. You should check all settings carefully one more time and then press the Print button. </p>
<p><strong>Now your printer should start printing.</strong></p>
<p></p>
<p>Please take a ruler and measure the size of the squares after printing out the first sheet. Each blue square should be 1 x 1cm and each red one - 0.5 x 0.5cm. &nbsp;Just measure 10 squares in a row, you should get 10cm. If the result is different, you must have made a mistake in the properties. You need to achieve perfect matching!</p>
<p>You should repeat the same procedure for printing out the second, third and further patterns. The corset consists of 5 pieces: 3 front pieces and 2 back ones. Only the first and the second pattern sheets need to be merged together. <strong>There is a special merging line for this purpose:</strong></p>
<p>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image012.jpg" class="img-responsive center-block"></p>
<p></p>
<p><strong>The other sheets should be simply printed out:</strong></p>
<p><strong><br>
<img src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/Printing%20Out%20Patterns_files/image014.jpg" class="img-responsive center-block"></strong></p>
<p><strong>So, you have printed and cut out all pattern pieces now!</strong></p>

