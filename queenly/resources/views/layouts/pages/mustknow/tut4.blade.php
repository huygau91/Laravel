<p>There are 8 columns and 10 lines in the table.</p>
<p><strong>Column 1 -</strong> necessary measurements that you need to take off the client.</p>
<p>I assume that before buying this book you had watched my free tutorials on taking clients� measurements where I explain in detail how it is done and how each measurement is used.</p>
<p><strong>Column 2 -</strong> your client�s individual measurements (Kate).</p>
<p>I have written them down into the table.</p>
<p><strong>Column 3 -</strong> measurements or sizes taken off the patterns.</p>
<p>Let me explain what it means.</p>
<p>I need to measure the pattern along the main circumference lines.</p>
<p>This column should contain the values of the <strong>same measurements</strong> I took off Kate but <strong>taken off my pattern</strong>.</p>
<p>Only then I can imagine what size the corset will be if I sew it according to the standard patterns and how much the size of the standard pattern should be altered (made smaller or bigger) so that the client could put her corset on.</p>
<p>There are necessary notches on the pattern corresponding to the main lines:</p>
<p>- bust;</p>
<p>- under-bust line;</p>
<p>- waistline;</p>
<p>- stomach.</p>
<p>The task is to lay out the patterns on the table in such a way that the main lines are parallel to each other and go through the notches of all pattern pieces.</p>
<p>Please take a look at how I spread the patterns on the table:</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%201_files/image006.jpg" alt=""></p>
<p>Then I measure each section length with a ruler or a measuring tape.</p>
<p>For example I measure sections 1, 2, 3 and 4 <strong>along the bust-line</strong>.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%201_files/image008.jpg" alt=""></p>
<p>I add up these values.</p>
<p>I take away the total value of seam allowances.</p>
<p>As a result I�ve got some value.</p>
<p>I multiply this value by two.</p>
<p>The result is the "bust circumference".</p>
<p>In this very case <strong>the bust circumference of the standard pattern is 36.6 inches</strong>.</p>
<p><strong>I write this value down into the third column of the table against the �bust circumference" measurement (line 5). </strong></p>
<p>Measurements of other section lengths are taken exactly the same way and fill up the third column of my table completely.</p>
