<p><strong>Now I would like to go into more detail concerning the side length. </strong></p>
<p>In my case I need to lower the side of the standard pattern by 0.4 inches.</p>
<p><strong>According to the rule of side adjustment you cannot cut off more than 0.6 inches atop. </strong></p>
<p>And this makes perfect sense.</p>
<p>The thing is that when the difference between the side seam lengths exceeds 0.8 inches it means the client has different proportions: the distance between her waist and her bust differs from the standard.</p>
<p>If you for instance reduce the sides by 2 inches for a petite client - the neckline cut of the corset is going to be right below her chin.</p>
<p>That is why the pattern should be altered <strong>in a different manner</strong> if the difference between the side seam lengths exceeds 0.8 inches.</p>
<p>You need to cut all patterns along the waistline and lay the top and bottom parts onto each other shifting them up by the value of this difference, i.e. reduce the pattern length.</p>
<p>The exact same thing needs to be done if you want to increase the side length: you simply prolong the side seam if the difference is up to 0.6 inches; if the difference is greater - you need to <strong>expand</strong> the pattern halves cut along the waistline by the required value.</p>
<p><strong>Now I start calculating alterations for the back side seam. </strong></p>
