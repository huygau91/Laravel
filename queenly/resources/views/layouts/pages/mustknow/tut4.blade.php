<h3>Measuring and Altering Patterns.</h3>
<p><strong>I start working on the first corset which I am going to sew in what is known as the simplified technique. </strong></p>
<p>I have chosen a simple basic design for the tailoring of this corset.</p>
<p>You can see a technical drawing of this design and its pattern schemes in this picture:</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%201_files/image002.jpg" alt=""></p>
<p>There are four parts in the pattern of my corset: 2 front pieces and 2 back pieces.</p>
<p>There is a crease line in the middle of the front and a cut in the middle of the back because the lacing will go across the middle of the back.</p>
<h4></h4>
<h4></h4>
<h4>Tools and Materials Required for Corset Tailoring:</h4>
<p>You are not going to need that many materials to sew this corset.</p>
<p>I have made a collage of all things necessary for making this corset and took a picture of it.</p>
<p>Please take a look at what you see on the picture.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/Opaque%20corsets/Corset%201/tut%201_files/image004.jpg" alt=""></p>
<p>There are:</p>
<p>Basic fabric that I�m going to use for the corset.</p>
<p>Some iron-on fabric. I am using iron-on batiste.</p>
<p>Thread and needles.</p>
<p>Rigilene bones: both wide (0.5") and narrow (0.3").</p>
<p>Measuring tape.</p>
<p>Scissors.</p>
<p>Pruner. I use it for cutting the bones.</p>
<p>Ribbons.</p>
<p>Plastic bones. I use them for adding support to the curves of the corset.</p>
<p>Small flock of padding polyester. I use it for covering corset cups.</p>
