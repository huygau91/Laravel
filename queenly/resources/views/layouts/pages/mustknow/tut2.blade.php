<p>I�m sure that you understand that exact and accurate measurement is the thing of high importance.</p>
<p>99% of mistakes and bad fitting are because of carelessness in measurement.</p>
<p>When you are starting to measure your client, you should have the exact plan:</p>
<ul>
<li>What pattern you will use in your work</li>
<li>What techniques of sewing you will use in your work</li>
<li>What additional measurements (according to the figure of the client) will help you to make corset of perfect size</li>
</ul>
<p>At the beginning you and the client are discussing the style, details, trimmings. And only then you are starting to measure.</p>
<p>So, this is the film.</p>
<p>Hi! Welcome to our first lesson of short-term course. This is about how to do measurement.</p>
<p>Let me say few words before we will start. At first we should measure the client, and then measure the same things of the mould. So we could change the mould that we already have. In other words we will compare the measures and change the mould. Believe me, if you will understand my method, you will forget about Muller�s method and other methods, which are calculating measures.</p>
<p>It takes 5-10 minutes to calculate a mould, and 5-10 minutes to draw a pattern on the material. Then it takes 6-7 hours to sew, and� your client could visit you for a fitting. Then it takes 2-3 hours to complete and to change different small things. I�m talking about sewing a corset with lace or draperies.</p>
<p>Believe me, it�s possible! It takes no more than 2 working days to sew an evening dress. Of course I�m just talking about time for sewing, without spending time on hand embroidery, decorating by beads, strasses and so on.</p>
<p>I�m so enthusiastic, because I�m working this way for many years and now I�m highly willing to share my knowledge with you.</p>
<p>Let�s measure. You should face the client, and it�s comfortable to place a mirror behind the back of the client. So you could see the way the tape measure goes, and so it�s easy to correct it.</p>
<p>Now, tie up two ropes: the one rope on the waist, the other on the belly. It�ll be much more comfortable for you, if you�ll place the rope at the borderline on the bottom of the corset. But there could be some differences. For example, if the corset is shorter on its sides and longer in the middle part. In that case you should place the rope at the corset�s sides. If you want, you could use elastic instead of the rope.</p>

