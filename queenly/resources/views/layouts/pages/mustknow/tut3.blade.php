<div itemprop="articleBody">
<p>&nbsp;</p>
<p><strong>The main means of production is a sewing machine.</strong> I have an industrial sewing machine but you could also use a household one.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image002.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\3.jpg"></p>
<p><strong>The next important equipment unit is a dress-form. </strong>It should have a foam core so that you can easily pierce it with pins. Adjustable or plastic dress-forms are not suitable.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image004.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\1.jpg"></p>
<p><strong>You will also need narrow (8mm) and wide (12mm) Rigilene bones. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image006.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\4.JPG"></p>
<p><strong>Regular paper-backed masking tape is perfect</strong> for taping over bone edges; I recommend using 1.5cm wide tape. You could also use some white duct tape instead.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image008.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\6.JPG"></p>
<p>In order to take precise measurements, you will need <strong>two regular silk cords or ribbons</strong>.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image010.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\13.jpg"></p>
<p><strong>Another essential tool is a measuring tape. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image012.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\14.jpg"></p>
<p>You will also need to use <strong>fabric glue</strong> in the process of making our corset. I use Gutermann fabric glue but you can use any other glue available in the nearest store - as long as it is suitable for working with textile.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image014.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\7.jpg"></p>
<p>Another tool is <strong>a tailor’s chalk</strong> that can be replaced with a piece of soap.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image016.jpg" alt="Description: E:\IMG_8632.JPG"></p>
<p><strong>Thread clippers. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image018.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\16.jpg"></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image020.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\18.jpg"><strong>Small pliers </strong>similar to those used by jewelers. You need them for working with Rigilene bones.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image022.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\17.jpg"></p>
<p><strong>A magnet with pins.</strong> It is really helpful when a couple of pins are always at hand on a magnet attached to the sewing machine.</p>
<p>&nbsp;</p>
<p><strong>A triangle ruler for working with patterns. </strong>Those with a wooden handle are very convenient. You can easily turn the triangle on fabric without shifting its layers.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image024.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\20.jpg"></p>
<p><strong>Pressing equipment and tools. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image026.jpg" alt="Description: E:\IMG_8631.JPG"></p>
<p>The second most important means of corset production after the sewing machine is a press-iron. You can see my semi-industrial press-iron in the picture above. An iron like this is good enough if the following requirements are satisfied (see below).</p>
<p>First of all your press iron should have a special valve plug for maintaining high pressure in the water reservoir. There should also be two individual controls: one for heating water in the reservoir and the other for heating the bottom of the iron itself. Steam pressure should be at least 2.5Bar. It is absolutely necessary to have a side-mounted steam button on the iron. I would also recommend buying a separate Teflon soleplate.</p>
<p>There are some <strong>pressing tools </strong>that you will need to make with your own hands. <strong>These are namely pressing moulds for corset cups. </strong>You should have several moulds like this in different sizes. The base of these moulds is foam spheres of varying diameters that are cut in halves. You could also use plastic spheres for this purpose.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image028.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\9.jpg"></p>
<p>Another tool that really helps this kind of work is <strong>a small cushion</strong> that you can easily sew and stuff with fabric clippings. You will see what I use it for in the upcoming tutorials. It’s nearly always in service.</p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image030.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\10.jpg"></p>
<p>And below is another essential tool. <strong>This is a pear-shaped mould that is used for pressing side seams and small rounded details of a corset. </strong></p>
<p><img class="img-responsive center-block" src="https://corsetacademy.s3.amazonaws.com/where_to_begin/must_know/transcript/equipment/image032.jpg" alt="Description: D:\vse gotovie diski\книги\book-ak1\sourse\img\11.jpg"></p>
<p>That’s all there is to it.&nbsp; See you in the tutorials!</p>

